              <p>The first stone tikis were said to have been carved around the year 1400 in the Marquesas Islands. The tiki can take many crafted forms, from giant sculptures of the war God Ku, to images of variant gods carved into drums, boats, or other utilitarian objects, to petroglyphs carved into rock or tattoo patterns on the body. The artistic likenesses created in tikis demonstrate a high level of craftsmanship and perfectionism. The most recognized tiki character has a strong, stocky body with a rectangular head, seemingly wearing a headdress. This mysterious figure is to a degree an intimidating 
                one, with large eyes and a stance that suggests he is ready for war.</p>
              <p>Beginning in the 1930s, an entire "tiki culture" representing the island life of the South Pacific began to form around these odd statues. Tiki-themed restaurants displayed kitschy memorabilia, such as tiki carvings, tiki torches, rattan furniture, tropical-print fabrics and wooden or bamboo items. Tiki bars served mai tais and other fruity island drinks. Before long, tiki culture had a large following in the 
                United States, using South Pacific themes in everything from clothing to interior design. After Hawaii became a state in 1959, tiki culture, the aloha shirt and other island representations were incredibly en vogue. </p>
              <p>Today, you can find giant wooden figures in several spots around the Hawaiian Islands, including the <a href="oahu/attractions/polynesianculturalcenter.php"> Polynesian Cultural Center</a> on Oahu's North Shore. At La Marianas on <a href="oahu/beaches/sandislandbeachpark.php">Sand Island</a> in Honolulu, possibly the most popular tiki bar in Hawaii, visitors can enjoy a tropical cocktail amidst an overwhelming amount of tiki memorabilia. Visitors to Hawaii can find a tiki image pretty much anywhere, but when you are browsing through the tourist-aimed mass-manufactured tiki items, remember how these images once represented 
                the beloved and revered Hawaiian gods. </p>
