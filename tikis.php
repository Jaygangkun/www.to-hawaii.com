              <h2>What are Tikis?</h2>
              <p>What are these mythical symbols and how do they fit into Hawaiian culture? If 
                you've ever seen the wooden, novelty representations of the ancient hand-carved figures, you will notice by their readied stances and stern facial expressions that a tiki was intended to represent very important, respected authorities, namely principal gods, guardians and spirit powers.</p>
              <p>Very few tiki lovers are aware of the unique history and spirituality of tiki images, but even a brief description of the ancient purpose of the tiki will give you a glimpse at the very essential role symbolism played in traditional Hawaiian 
                society. </p>
              <? include ("theme/text.html") ?>
              <p><strong>The Tikis' Origins</strong></p>
              <p>The first inhabitants of Hawaii voyaged from Polynesia about one thousand years ago, bringing with them religious and spiritual convictions. The many gods of Hawaii and Polynesia were represented by tikis. The name tiki can refer to many different types of images used throughout Polynesia, from images used ceremonially by Maori tribes in New Zealand, to the moa carvings on Easter Island, to modern day images displayed in Hawaii. </p>
              <p>In Polynesian mythology, tiki often represents the first human being on Earth. These images are still used today in some Polynesian cultures in the context of spiritual practice. It is not uncommon for small tiki figures to be worn for protection from infertility in New Zealand. </p>
              <p>In ancient Hawaiian culture, the gods, the aina, or land, and the kanaka, or people, shared a symbiotic existence. If the people took care of the land in a pono (right) manner, the gods were appeased. If the gods were happy, they would allow the land to provide sustenance for the people through verdant growth. Each god had many kinolau, or forms, including human and animal forms. </p>
              <p>Tiki statues were carved to represent the image of a certain god and as an embodiment of that specific god's mana, or power. With well-formed tikis, perhaps the people could attain protection from harm, strengthen their power in times of war and be blessed with successful crops. </p>
              <h3>The primary Hawaiian gods represented with tiki images include:</h3>
              <p><strong class="subtitle">Ku</strong> - the god of war<br />
                <strong class="subtitle">Lono</strong> - the god of agriculture and peace time<br />
                <strong class="subtitle">Kane</strong> - the god of creation, sunlight, forests, 
                fresh water<br />
                <strong class="subtitle">Kanaloa</strong> - the god of the sea realm</p>
              <p>The ancient Hawaiians kept their gods close using many creative forms of communication. Tikis were created as a medium of connection or interaction. Through continued communication with these all-powerful deities, the Hawaiian people were sure to follow the right path to appeasement. All Hawaiian people were said to have descended from the lineage of the gods. It was the job of the alii nui, or high chiefs, to make sure everything 
                was in societal balance and that the ultimate respect was paid to the gods through many avenues. </p> <?php
