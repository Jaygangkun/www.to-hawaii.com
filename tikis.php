?> <p>The alii nui were considered direct descendents of the gods and thus carried great responsibility. In addition to tiki images, the higher spirits and ruling deities were paid homage through every action undertaken in ancient Hawaiian society. From the whalestooth pendants, feathered helmets and feathered cloaks worn by kings, to animal sacrifice and the religious practices that separated men and women during meal times, divine guidance ruled society. </p>
              <p>The ancient system of religion called aikapu was abolished by King Kamehameha II in 1819, and the majority of temples and religious images, including tikis, were destroyed. However, some tiki artifacts remain to this day in remembrance of a time when strict religious beliefs guided the Hawaiian society. According to Hawaiian history, there was a time when gods walked the earth as men, and tiki images recognize not only their divinity, but also their human qualities as well. Tikis reminded the people just how close the realm of the gods was and reinforced their acknowledgement 
                of the awesome power the gods sustained. </p>
