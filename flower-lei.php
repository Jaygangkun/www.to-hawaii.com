              <h2>Hawaii's Lei Tradition</h2>
              <p>Hawaii and the flower lei are practically synonymous. The lei is the perfect symbol to represent the beauty of the Hawaiian Islands and the spirit of aloha shared throughout Oahu, Maui,
                Lanai, Kauai,
                Molokai, Niihau,
                Kahoolawe and the Big Island of Hawaii. Most people have seen a flower lei - either during a visit or on TV. If you haven't had the privilege of wearing one, you simply must make a point to celebrate your next visit to the islands with one of many beautiful flower lei types. Fragrant leis, like the pikake lei, present a treat for the senses as well as a genuine feeling of welcome.</p>
              <p>Orchid, pikake, plumeria, ohia lehua and maile leis are just a few of the many varieties fashioned in Hawaii. Although most recognizable leis are made from fresh flowers, a lei can be strung from feathers, foliage, shells, seeds, dried flowers, candy, or many other materials. A flower lei is usually crafted using a steel needle and string, but may also utilize strategic braiding, knotting and threading techniques.</p>
              <p>A lei is an extra special gift, as it carries not only a blessing and tangible beauty, but also represents the giving of time, as each one is hand-woven with care. Leis may be worn around the neck or the brim of a hat (lei papale). Some leis are very intricately designed with patterns of alternating flowers strung delicately to create a lei that will last for days with refrigeration. </p>
              <p>The lei of today are different from the lei of ancient Hawaii. With the number of indigenous flowers and plants dwindling, lei makers must use readily available materials. When one receives a lei made from a rare flower or a flower that grows in remote areas, it is considered a high honor. Some leis, as in the ones created for the towering statute of King Kamehameha at Aliiolani Hale on Oahu, are several feet in length and may take hours or even days to complete.</p>
