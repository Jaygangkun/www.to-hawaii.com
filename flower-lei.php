              <p><strong class="subtitle">Molokini</strong> - lei limu kala (blue)</p>
              <p>The beauty and vibrance of Hawaii's landscape, as well as the sincerity and kindness of its people, continue to be communicated with the sharing of the Hawaiian lei. If you are visiting the islands, you can find lei stands anywhere, from the airport to <a href="oahu/attractions/chinatown.php">Chinatown</a> to scenic country roads. If you've never given or received a beautiful fresh flower lei, take the time to experience this ancient and meaningful tradition.</p>
