?> <p>Leis were also made of bone, teeth and hair and could be woven with coconut husk, olona, banana or hau fibers. In ancient Hawaii and in many families still today, it is considered taboo for a pregnant woman to wear a lei as the cord hung around the neck may symbolize umbilical cord tangling. </p>
              <p><strong>Lei Etiquette</strong></p>
              <p>A lei is bestowed on another for many reasons - it can symbolize love, friendship, welcoming, affection, parting, a wish for safety, congratulations, graduation, union of marriage, health, birth, transition in death and many other messages of peace. There is no age restriction to be a lei recipient. Today, a kiss on the cheek may also accompany the giving of lei, although this practice was not customary in ancient 
                Hawaii. </p>
              <p>To place a lei correctly, it should fall equally on the chest and back, as opposed to being hung straight down from the neck. Whatever the occasion, birthday, wedding, anniversary, or other, a lei is a common part of any celebration in Hawaii. During high school or college graduation days in the islands, family and friends present the graduate with many lei, and often the proud student can barely see over the numerous neck garlands! Local tradition says that a lei recipient should not remove a lei while in the presence of the lei giver. </p>
                             <p>Lei are used in many facets of life in Hawaii, from tourism to religious and political events. The governor of Hawaii and other important figures may wear a lei during public appearances, state holidays, or on a daily basis. </p>
              <p><strong>Lei Day</strong></p>
              <p>Unique only to Hawaii, May 1st is Lei Day in the islands, and celebrations include making and exchanging a variety of lei types,
                music and entertainment, and, of course, plenty of local food. First celebrated on May 1, 1928, "May Day is Lei Day" festivities abound on several 
                islands. Lei Day is honored in most schools, and prizes are offered for unique or outstanding lei work. </p>
