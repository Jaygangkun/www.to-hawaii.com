              <h3>The Art of Lei Making</h3>
              <p>Lei making is an art form that some take quite seriously. To find the most prized native Hawaiian plants and flowers, much hunting and foraging may need to be done. There are two categories of lei today, traditional, also called Polynesian, and contemporary. The traditional leis are created for aesthetic beauty from native Hawaiian plants and hold very specific symbolism. Contemporary leis may include plants and flowers introduced after European contact, or permanent materials like silk, satin, yarn, cellophane, faux flowers and ribbon. </p>
              <p>Visitors can view priceless feather and shell lei dating back over a hundred years on display at the Bishop Museum on Oahu. Today, rare shell leis, like the Niihau shell lei, are highly sought by collectors and lei enthusiasts and can cost hundreds, even thousands of dollars. </p>
              <p>In ancient Hawaii, leis were worn as symbols of distinguishment or rank - a high ranking alii (chief) may have been recognized by his particular type, pattern or complexity of lei. Leis might also symbolize the occurrence of an important event, given during a birth, death, peace settlement or war victory. A lei might be worn during religious ceremonies, to ask the gods for safety in fishing or voyaging, or to pray for fertile crops. Different flowers and plants carried unique significance and were used accordingly. </p> <?php
