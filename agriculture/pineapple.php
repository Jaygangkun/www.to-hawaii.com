            <h1>Hawaii Pineapples</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p><img src="../images/pineapple_3.jpg" width="148" height="125" alt="Pineapple" align="right" />One of the things Hawaii is famous for is its pineapple production. Pineapple is a delicious fruit and is known worldwide for its sweet taste. The fruit was first called "anana," a Caribbean word for "excellent fruit." Hawaiians called it "halakahiki," which means "foreign fruit." </p>
<p><strong>The Fruit's Origins</strong></p>
<p>The pineapple, believed to have its origins in Paraguay or Brazil, was loaded on trade ships and taken to distant places like China, India, Australia, and Mexico in the 15th and 16th centuries. It is believed that the pineapple first arrived on the Big Island of Hawaii in 1527. </p>
<p><strong>Foreign Laborers in Hawaii</strong></p>
<p>As a result of a growing demand for labor on the various Hawaiian plantations, such as sugar cane and pineapple, the import of foreign workers started in the 1850s. These workers came from China, Japan, the Philippines, Korea, Germany, Portugal, Norway, Spain, Scotland, Puerto Rico and Russia. </p>
<p><strong>John Kidwell and James Dole</strong></p>
<p>The founder of Hawaii's pineapple industry was Captain John Kidwell, who tested a variety of pineapple and finally selected the Smooth Cayenne in the 1880s. James Dole, commonly known as the "Pineapple King," arrived in Hawaii in 1899. He started his first plantation in Wahiawa a year later and built a pineapple cannery the following year. </p>
<? include ("../theme/text.html") ?>
<p>In 1922, Dole bought the island of Lanai, where he established the largest pineapple plantation in the world, growing 75 percent, in its peak years, of the world supply. Dole passed away in 1958. Today, his Hawaiian Pineapple Company is still known worldwide as the Dole Food Company.</p>
<p><strong>Life on the Plantations</strong></p>
<p>For immigrant workers, life on the plantation was hard. The work day usually started at 6 in the morning and ended at 4:30 in the afternoon. While in the fields, the workers had to wear heavy protective clothing because of the sharp pineapple leaves. Even though the work was physically demanding, the wages were less than $20 a month. However, housing near the fields was free.</p>
<h2>How Pineapples are Grown</h2>
<p>Pineapple in Hawaii are grown year-round. Each field has to be prepared before one can start planting the fruits. Preparation includes fumigating the soil and placing black plastic mulch in the ground. The mulch helps to keep the moisture, confines the fumigant, controls weeds and pests, and stores heat for a better root growth. Even today, every single pineapple is planted by hand. This is done by digging a hole through the plastic mulch and placing the crown of the fruit, the green leaves, into it. A skilled planter can plant about 10,000 fruits a day. </p>
<p>A perforated tube placed between the pineapple rows irrigates the plants, and a mixture of liquid nitrogen and iron sprayed on the plant fertilizes them. It takes 18-20 months for a plant to produce the first pineapple and about 13-15 months for the second fruit. After the second pineapple is harvested the field is knocked down and a new cycle begins.</p>
 <? include ("../theme/text-2.html") ?>
<p><strong>When are Pineapples Ripe?</strong></p>
<p>A ripe pineapple is one that has green leaves and a firm body. A large pineapple doesn't mean that it is riper or better tasting. Also, the color of the outer shell doesn't indicate the state of ripeness, for even a pineapple that is green outside can be ripe inside. A pineapple won't become sweeter or riper after being picked. Therefore, it is best to eat a pineapple as soon as possible after its purchase. If you plan to store it for a few days, it is best to keep it in the refrigerator to preserve its freshness.</p>
<p><strong>Pineapple Production Today</strong></p>
<p>Hawaiian pineapple plantations produce almost a third of the world's crop and supply about 60 percent of canned pineapple products. Moreover, Hawaii's agricultural industry, including pineapple, sugar cane, other tropical fruits and flowers, plays an important part in the state's economy, for it provides over 40,000 jobs and generates almost $3 billion annually. </p>
<h3>Fun Facts about Pineapples</h3>
<p>Pineapples are fat and cholesterol free and low in sodium.</p>
<p>Pineapples are high in vitamin C.</p>
<p>The red color of the soil in which pineapple is grown is caused by decomposed volcanic ash resulting in oxidized iron in the soil.</p>
<p>Fresh pineapple contains bromelain. This protelytic enzyme breaks down protein in a manner similar to what happens in digestion. Because of this reaction, dairy products shouldn't be mixed with fresh pineapple until just before serving. However, fresh pineapple in meat marinade adds great flavor and tenderizes the meat.</p>
