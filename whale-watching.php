              <h2>Hawaii's Winter Visitors</h2>
              <p>Whale watching is a seasonal and very popular activity during the winter time in Hawaii. There are at about 10,000 to 15,000 humpback whales in the world, and over 1,000 of them visit the tropical waters of Hawaii every year at the end of November. They stay until the end of April; some are even seen until the middle of May before they make their long way back to Alaska. </p>
              <p>Whales can be seen in the waters of all the Hawaiian islands, but most of them like to hang out in the sheltered and shallow waters between the islands of Maui, Lanai and Kahoolawe. It is therefore best to do whale watching on Maui. Maui whale watching tours usually depart from  Lahaina Harbor or from Maalaea Harbor. There are many tour operators that organize day and sunset whale watching cruises on small and larger vessels. Most of them guarantee whale sightings, and if by any chance you do not get the chance to see them, you may get offered another tour for free. Usually, the tour price also includes a drink, snack and detailed narration. Some tour providers even provide hydrophones aboard that allow you to hear the whales' communication among each other.</p>
