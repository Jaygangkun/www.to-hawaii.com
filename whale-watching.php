              <p><strong>Amazing and Phenomenal Singers</strong></p>
              <p>The humpback whales are unique mammals because they have amazing talents and one of them is their singing ability. They know a huge variety of long, complex and beautiful songs. The humpback song is a mystery. No one knows why they sing and what their songs mean. The males sing long songs during the breeding period that are related with mating. However, it is not proven that the song is used as a tool to attract the female.</p>
              <p>Female whales can produce sounds as well, but only males can produce long and organized songs. The song length may vary and can last up to 20 minutes. It can be heard more than 20 miles away. It is phenomenal that all the males in the population sing their song in the same way, using the same behavior patterns and sounds. And when over time the song patterns change, all the males in the population make this change. Therefore, even when the song changes, they always sing the same song.</p>
