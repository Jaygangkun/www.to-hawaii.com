              <p><strong>Bubblenet Feeding Hunting</strong></p>
              <p>Another remarkable talent the humpback whales possess is their hunting ability, so called bubblenet feeding. Bubblenet feeding is a group hunting technique, where every participant has a specific role. One of the whales blows bubbles under a group of fish. Then the fish are trapped because they can not swim through the net of bubbles. After that, several other whales make sounds to scare and panic the fish. Then another group of whales swim in a circle and draw the fish up to the surface, where they are captured and eaten. The whales can consume up to one ton of food within a day. But overall, they don't feed while they are in Hawaii's waters.</p>
