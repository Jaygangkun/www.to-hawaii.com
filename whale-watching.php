              <p><strong>The Pacific Humpback Whales</strong></p>
              <p>The Pacific humpback whales migrate to Hawaii from Alaska and the coast of central California. Their 3,500-mile swim from their summer locations to Hawaii lasts about one to two months, depending on their swimming speed. The swimming speed is normally 3-9 mph (4.8-14 kmh), but case of danger, it can go up to 15-16.5 mph (24-26.5 kmh).</p>
              <p>The whales travel in large and loose groups, with almost no rest along the way. Their associations do not last longer than a few days. However, one exception is the strong and lasting connection between mother and calves. The warm, tropical Hawaiian waters provide good breeding conditions for the humpback whales. Many whales give birth while in Hawaii. The gestation period of pregnant females lasts between 10-12 months.</p>
