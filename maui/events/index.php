          <h1>Maui Events</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p><strong>January</strong></p>
              <p>PGA Kapalua Mercedes Championship: $1 million is at stake for elite golfers in this premier golf tournament happening early January at the Kapalua Resort on Maui.</p>
              <p>Hula Bowl Maui All-star Football Classic: The college football league&rsquo;s best players troop to Maui&rsquo;s War Memorial Stadium in mid-January to play an unforgettable all-star football game.</p>
              <p><strong>February</strong></p>
              <p>Chinese New Year: Celebrate the most auspicious day of the year (early February) with a traditional lion dance and dazzling fireworks at the heritage-filled Wo Hing Temple in Lahaina&rsquo;s Front Street.</p>
              <p>Great Maui Whale Festival: Come February or March when humpback whales come to the waters of Maui, the Pacific Whale Foundation holds various events, such as seminars, sailing, art exhibits, snorkeling and diving tours to make people aware and concerned about the ocean&rsquo;s gentle giants.</p>
              <p><strong>March</strong></p>
              <p>Run to the Sun: Maui hosts this adrenaline-pumping, 37-mile run stretching from Paia at sea level to the summit of the 10,000-foot Mt. Haleakala.</p>
              <p>Ocean Arts Festival: Whale watching, games and a touch-pool exhibit for children are lined up in Lahaina to honor the humpback whales that come here every year.</p>
              <p>East Maui Taro Festival: Taro, Hawaii&rsquo;s staple food, takes center stage in Hana, featuring music, crafts, hula and food celebrating the important crop. </p>
              <p>Prince Kuhio Celeberations: The birth of Jonah Kuhio Kalanianaole on March 26, 1871, is marked by a yearly state holiday on all islands.</p>
              <? include ("../../theme/text.html") ?> 
              <p><strong>April</strong></p>
              <p>Ritz-Carlton Kapalua Celebration of the Arts: On Easter weekend, the arts and culture of Hawaii get a boost, with lessons and workshops in chant, hula, tapa cloth-making, Niihau shell lei-making and primitive clay firing. A luau, live entertainment and activities for children are also part of the festivities.</p>
              <p>Buddha Day: Lahaina blooms with color in this splendid flower festival, a special pageant held by the Lahaina Jodo Mission to honor the birth of Buddha, marked on the first Saturday of April.</p>
              <p>Annnual Ritz-Carlton Kapalua Celebration of the Arts: Modern and traditional artists give free hands-on art lessons on a four-day festival beginning on the Thursday before Easter at the Ritz-Carlton in Kapalua.</p>
              <p>David Malo Day: Hawaii&rsquo;s eminent scholar is commemorated mid-April at the Lahainaluna High School in Lahaina with hula, festive activities and a luau.</p>
              <p>Ulupalakua Thing! Maui County Agricultural Trade Show and Sampling: Food boths, local products on exhibit and live musical entertainment are events not to be missed on the last Saturday of April at the Ulupalakua Ranch and Tedeschi Winery.</p>
              <p><strong>May</strong></p>
              <p>Annual Lei Day Celebrations: Hawaii&rsquo;s distinct garland of flowers, the lei, is celebrated each May Day, with lei-making contests and arts and crafts exhibits.</p>
              <p>In Celebration of Canoes: Recognizing the seafaring tradition of the Pacific Islands, the event on West Maui happening from mid to late May showcases canoe-building exhibits, canoe paddling and sailing regattas, art demonstrations, live music and a luau.</p>
              <p><strong>June</strong></p>
              <p>Hawaiian Slack-Key Guitar Festival: Guitar enthusiasts are treated to a five-hour long free concert at Kahului, featuring Hawaii&rsquo;s best performers.</p>
              <p>King Kamehameha Celebration: All the islands in Hawaii celebrate June&rsquo;s first weekend with a state holiday, the main highlight being a colorful flower parade.</p>
              <p>Maui Film Festival: For five days at the Wailea Resort (Maui) in mid-June, the spotlight is focused on Hawaiian cinema, with premieres and screenings of special films, as well as Hawaiian storytelling, hula, chants and music.</p>
               
              <p><strong>July</strong></p>
              <p>Makawao Parade and Rodeo: For generations, the annual cowboy event in Makawao in upcountry Maui has entertained countless locals and visitors alike.</p>
              <p>Kapalua Wine and Food Festival: Food and wine experts converge at the Ritz-Carlton and Kapalua Bay hotels in Maui to hold gastronomic panel discussions and sample new wines and food products.</p>
              <p><strong>September</strong></p>
              <p>Aloha Festivals: Hawaiian culture is on display with a stunning medley of parades, street festivals and other joyous events. A different island in Hawaii celebrates Aloha week, for each week from mid-September to October.</p>
              <p>A Taste of Lahaina: Maui&rsquo;s top-notch chefs create 40 signature entrees for a weekend festival that also includes cooking demos, wine tasting and live entertainment. Held at the Lahaina Civic Center on the second weekend of September, the sumptuous event begins Friday night with Maui Chefs Present, a dinner/cocktail party hosted by Maui&rsquo;s best chefs.</p>
              <p>Maui Marathon: On a Sunday in mid-September, runners begin before daybreak at the Maui Mall in Kahului, finishing in Kaanapali to thunderous cheers and applause.</p>
                            
              <p><strong>October</strong></p>
              <p>Maui Country Fair: Visitors to the oldest country fair in Hawaii can enjoy a grand parade, live entertainment, amusement rides and exhibits. This happens at the War Memorial Complex in Wailuku on the first weekend of October.</p>
              <p>Halloween in Lahaina: On Halloween Eve, the streets of Lahaina become a playground for more than 20,000 people wearing wild and spooky costumes.</p>
              <p>Aloha Classic World Windsurfing Championship: Hookipa Beach, considered the world&rsquo;s best beach for windsurfing, is home to the final event of the Pro Boardsailing World Tour, held late October to early November.</p>
              <p><strong>November</strong></p>
              <p>Hawaii International Film Festival: Movie lovers on the first two weeks of November can watch films from Asia, the Pacific Islands and the U.S. in this statewide movie festival, with most screenings taking place on Oahu.</p>
              <p><strong>December</strong></p>
              <p>Old-Fashioned Holiday Celebration: Santa Claus visits the Banyan Tree Park in Lahaina on the second Saturday of December to spread holiday cheer. Christmas baked goodies, live music and entertainment, a crafts fair and various activities for children are also featured.</p>
              <p>Festival of Lights: Come early December, spectators are treated to an annual parade, with marching bands and Sanda walking down Lahaina&rsquo;s Front Street.</p>
              <p>First Night: To welcome the New Year, Hawaii holds an extravaganza of arts and entertainment, with musicians, actors, dancers and magicians performing for 12 hours. A spectacular fireworks display marks the beginning of a new year.<br />
              </p>
