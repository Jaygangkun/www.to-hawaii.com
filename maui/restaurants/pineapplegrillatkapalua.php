                <p>Overlooking the Bay Golf Course and close to the Kapalua Resort tennis  club, Pineapple Grill provides its patrons with an elegant yet informal dining  experience. Set against the famous Cook Pines, diners get a clear view of the  golf action, most especially the Bay Course's 18th hole, from the  restaurant. Or, for golfers, this is a good place to relax over refreshments  after a challenging round. The cuisine is distinctly Pacific-style with  specific Maui Island influences. All menu items are offered with  an innovative and surprising twist, which ensures a new experience for the  taste buds.</p>
                
                <p>Visitors to the Pineapple Grill have the option of dining indoors or  outdoors. The outdoor seating is located on a lanai with tiki torches overhead.  Alternatively, patrons can choose to dine in the lively bar, which is made more  vibrant with live nightly entertainment and where the golf games at Bay Course  are relayed via flat-screen televisions. The restaurant places a lot of  emphasis on using fresh island ingredients in its cuisine. The Pineapple Grill  is open for breakfast with a selection of traditional morning fare such as  eggs, bacon, sausage, veggie dishes and waffles on offer. The lunch menu  includes starters, salads, sandwiches, burgers, and fish &amp; chips. As well  as a selection of pupus, cheese plates, soups and salads, the dinner offers  various seafood and a choice of meat dishes that includes roasted free-range  chicken, braised ribs, filet mignon, pork chops, and ribeye steak. Main dishes  are accompanied by a choice of seasonal vegetables, and a dessert menu is  available for afters. The comprehensive drinks list features wines, beers and  speciality cocktails. There is a happy hour every day between 2.30pm and 5.30pm as well as various special offers  on drinks at different times during the week.</p>
