                <h2 class="small">Sarento's on the Beach, Maui</h2>
                <p>With stunning views of three neighboring islands - Lanai, Molokini, and  Kaho'olawe - and the Pacific Ocean, Sarento's on the Beach brings a little  slice of Italy and the Mediterranean to Maui. A warm, hospitable environment  coupled with fine dining and magnificent scenery makes the perfect setting for  an intimate, romantic evening out. The multi-regional Mediterranean cuisine  offered by the award-winning restaurant is designed to impress, as is the  high-quality service that Sarento's prides itself on providing. The location  and the exciting inventions of the culinary experts create the sort of haven  where life-long memories are born. </p>
                <? include ("../../theme/text.html") ?>
                <p>The menu at Sarento's on the Beach is designed to complement the scenic  setting. The pasta is homemade, the Hawaiian fish and seafood is fresh, and the  steaks are prime quality. In addition to these offerings, a choice of signature  lamb, veal, pork and chicken dishes are available from the menu. A range of  salads and appetizers (seared Hawaiian ahi, beef carpaccio, hamakua mushroom  bruschetta and other delicacies) are available too as starters or for the  not-so-hungry. A sweet treat from the dessert menu, a savory cheese platter  and/or an after-dinner drink or two should add that final touch of satisfaction  to a meal at Sarento's. The after-drinks menu includes port, an assortment of  cocktails, soft drinks and coffee. The restaurant also has a private dining  area for parties, group get-togethers and other functions. This area can cater  for up to 18 people, and although a special events menu is available for such  occasions, anyone planning a party is not limited to the set menu and is  encouraged to discuss their specific requirements with the restaurant  staff.</p>
                <p>Sarento's on the Beach is open seven days a week. It is open from 7.00am until 11.00 am for breakfast and from 5.30pm until 10.00pm for dinner. The lounge is open  from 5.00pm until midnight. Reservations are advisable to avoid delays  in being seated.</p>
