              <div class="midpanel_row">
                <p>Situated about 1.5 miles from Piai on the way to Hana, and right on the  beach at Maui's North Shore, is Mama's Fish House. It is easy  to distinguish with a fishing boat bearing the restaurant's name sited at the  entrance to the parking lot. The name clearly states what type of eatery this  is, and Mama's Fish House is said to serve some of the best fish to be found on  Maui. The establishment is owned by  Floyd and Doris Christenson who lived on a boat for four years as they sailed  from California to Maui, exploring old Polynesia and the island kingdoms in the  Pacific. Traveling from Easter Island to New Zealand and on to Hawaii, they eventually settled in Maui where they opened their  now-famous fish restaurant. </p>
                
                <p>The d&eacute;cor within Mama's Fish House reflects the Christenson's travels and  their interest in all things Polynesian. With a secluded white-sand beach and  the majestic Pacific Ocean on its doorstep, the restaurant offers a unique dining experience. From  a small group of loyal customers in the early days, the fish restaurant now has  a large following. With a menu that is, of course, almost entirely focused on  fish there are many varieties to choose from. Each day, fishermen go out in  small boats to catch fish for the Christenson's. Ahi, Lehi, Mahi-Mahi (in  multiple colors), Moi, Onaga, tropical Ono, Opakapaka, Papio and Uku are all on  the menu. Fishermen take the fish directly the Mama's Fish House where it is  processed and served within 24 hours. The fishermens' names and where the fish  were caught are listed on the daily menu. Tahitian fish salad and Hinano beer  are also on the menu - in tribute to the time the Christenson's spent in Tahiti during their sea voyage. A  selection of desserts, wines and cocktails rounds off the dining experience at  Mama's Fish House.</p>
