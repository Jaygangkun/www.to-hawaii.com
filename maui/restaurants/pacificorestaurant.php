                <h2 class="small">Pacific'O Restaurant, Maui</h2>
                <p>The Pacific'O is located right on the beach at one of the sunniest spots  in Maui. The restaurant is situated next  to the Lahaina Shores Condominium, and it prides itself on offering its patrons  the finest in contemporary Pacific cuisine. The best food that Hawaii and the  Pacific has to offer is carefully infused with tradition and brought to the  table for the enjoyment of everyone who visits this oasis - whether they are  local residents of Maui or visitors to its beautiful shores. </p>
                
                <p>The menu at the Pacific'O is based on the finest and freshest  ingredients, sourced locally from the land and sea. All the fish on the  restaurant's menu is freshly caught in local waters, the fruit and veg is  organic and comes from the restaurant's own O'o farm in Kula. This fresh fare  is enhanced with exotic flavors and presented to diners in spectacular style.  The dinner menu typically offers a selection of appetizers (such as Pork Belly,  Scallops, Oyster, Lobster Ravioli and Soup of the Day). In addition to a number  of Salad options, the appetizers are followed up with a range of dishes from  the land (for example there is Chicken, Pork, Lamb Rack and Filet Mignon) as  well as a selection from the sea (Scallops, Snapper, Lobster and more). The  lunch menu also features a selection of appetizers and salads as well as a  choice of sandwiches, burgers and entr&eacute;es. The dessert menu has plenty options  and features such temptations as berries and cream and Chocolate Decadence. The  drinks menu includes fresh juices, wines, hand-crafted martinis, signature  cocktails and a selection of local and international beers. </p>
                <? include ("../../theme/text.html") ?>
                <p>Offering a beautiful beachfront dining environment, the Pacific'O  restaurant endeavors to set new standards for farm-to-table dining on the island of Maui. As well as being open daily,  Monday to Sunday, from 11.30am to 9.30pm for lunch and dinner, the  Pacific'O can cater for group and celebratory events. Patrons can choose  between private or general seating for their functions. Groups of less than 16  people have the option of choosing from the &agrave; la carte or set menu, but groups  over this size are required to select from the restaurant's set menu.</p>
