                <h2 class="small">Kimo's, Maui</h2>
                <p>Front Street is the main street in Lahaina and Kimo's restaurant has the  distinction of being the oldest restaurant on this famous thoroughfare. The  restaurant is set on the waterfront with stunning views of the Pacific Ocean and the nearby islands of Molokai and Lanai. The dramatic scenery, good food,  and the live Hawaiian entertainment offered nightly create the perfect  atmosphere for a romantic evening out in the heart of Lahaina. The menu at  Kimo's offers a wide choice of tempting dishes such as prime steak, ribs and  seafood entr&eacute;es.</p>
                
                <p>Having won many awards, including the 2014 and 2015 Certificates of  Excellence, Kimo's is open daily for lunch and dinner, and it also offers a  special keiki menu. The lunch menu includes a selection of pupus, salads, fresh  fish, home-made burgers and local favorites (also called Front Street favorites). While the dinner menu  also offers pupus, salads, fresh fish and other seafood, it additionally  features a selection of steaks, island favorites and sides. The keiki menu has  a number of entr&eacute;es to appeal to young palates including fresh fish, pork ribs,  cheeseburgers, chicken sticks, hot dogs, and more. The dessert menu, featuring  "The Original Hula Pie," is available to round off every meal. There is also a  selection of delicious drinks to accompany the keiki meals, while there is a  fairly comprehensive wine list to accommodate the adults.</p>
