                <h2 class="small">Lahaina Fish Co., Maui</h2>
                <p>Spread over two floors with stunning views of Lahaina Harbor, the neighboring islands of Molokai and Lanai and beautiful night-time sunsets, the Lahaina Fish Co is a popular dining spot that attracts locals and visitors alike. The restaurant boasts of being dedicated to the food it serves, the  service it provides, and to promoting healthy island living by encouraging all  to eat and live well. The Lahaina Fish Co is primarily a steak and seafood  restaurant. </p>
                
                <p> Located in the heart of Lahaina, the goal of the Lahaina Fish Co is to  source and serve fresh, natural local produce whenever feasible. The menu is  primarily made up of a selection of fresh fish and seafood and succulent angus  beef. Recent additions include an improved range of gluten-free options along  with a better selection of dishes to suit vegetarians and vegans. The aim is to  provide healthy and flavorsome food using locally-produced and sustainable  ingredients wherever and whenever these are available. As the restaurant's name  suggests, a lot of emphasis is placed on fish - fresh Hawaiian fish. As a result,  the fish section of the menu offers Opah, Monchong, Mahi-Mahi and Ahi, all  brought to the restaurant nightly by local fisherman and each one's particular  flavors accentuated by the restaurant's culinary experts. Another highlight on  the menu at the Lahaina Fish Co is the prime rib. The restaurant prides itself  on only offering prime "certified" angus beef and has indeed won awards for the  quality of its meat. The beef is slow-roasted in an "Electric Imu" overnight  and seasoned with Hawaiian Sea Salt. Other menu favorites include the Ahi Poki  Tacos, Seafood Lau Lau, Seared Pepper Scallops, Fire-Roasted Opah with garlic  sauce and roasted red pepper, Mac Nut Mahi Mahi with coconut ginger beurre  blanc and the Ahi Katsu Rice Bowl. </p>
