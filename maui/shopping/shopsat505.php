                <p>Situated, as the name implies, at 505 Front Street in Lahaina, this retail complex has an impressive oceanfront setting. Here you&rsquo;ll find a variety of shops, galleries, boutiques, award-winning restaurants and a remarkable luau show.</p>
                
                <p>At Betty&rsquo;s Beach Caf&eacute; you can sip a cool Raspberry Cosmopolitan or a refreshing Watermelon Lemonade. Or if you&rsquo;re feeling hungry, enjoy the beautiful sea view at I&rsquo;O while tucking into smoked sashimi or marinated calamari steak. Then, if you&rsquo;re not quite ready to leave, dance the night away at Timba, one of Maui&rsquo;s hottest nightclubs.</p>
