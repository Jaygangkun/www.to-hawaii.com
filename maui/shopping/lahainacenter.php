                <h2 class="small">Lahaina Center, Maui</h2>
                <p>Double your pleasure at two centers - just minutes from one another. A treasure trove of gifts, clothes and shoes can be found here. Between the New Lahaina Center and the Old Lahaina Center, there are over 70 stores and services. It is a pleasure to stroll around between the two as the fresh ocean breeze surrounds you.</p>
                
                <p>One of the best places for a Hawaiian shirt or lapa lapa is Hilo Hattie's at the New Lahaina Center. Other clothing shops you'll find are Banana Republic, Coral White, GAP &amp; GAP Kids, Maui Tees Outlet and Maui Clothing Co. You'll find unique gifts at East Meets South, Paradise Gifts and Hager Collection. For accessories, visit Shoe Fetish. </p>
				<? include ("../../theme/text.html") ?>
                                <p>There are a few extraordinary places for dining, The Bubba Gump Shrimp Co., Hard Rock Cafe and Ruth's Chris Steak House, which is more than your average restaurant. Other places for refreshing island snacks are Cold Stone Creamery, Maui Wave Riders Bar &amp; Grill, Jamba Juice and Maui's Own Ice Cream Parlor.</p>
                <p>Entertainment for all ages can be enjoyed at Front Street Theatres, Warren &amp; Annabelle's Magic! And Glow Putt Mini Golf.</p>
                <p>Special services include: Aloha Activities &amp; Adventures, Lahaina Fishing Supply, Lahaina Ticket Company, Lahaina Travel and Serenity Salon &amp; Spa. There are also convenience shops, supermarkets, banks and a post office.</p>
                
