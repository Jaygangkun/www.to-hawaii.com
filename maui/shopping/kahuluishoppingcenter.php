                <h2 class="small">Kahului Shopping Center, Maui</h2>
                <p>Feel like relaxing in the shade and shopping for fresh produce? If so, you'll be in good company at the Kahului Shopping Center, Maui's first shopping center. This is where many of the local older folks relax while playing cards and chatting. It is also one of four malls and dozens of stores that make up the commercial center of Maui.</p>
                <p>There are some great food options here, like the delicious ethnic food and produce at the Ah Fooks Market. You'll also find Ichiban Okazuya Hawaii, a popular Japanese restaurant. They serve lunch specials such as tempura udon specials with breaded mahi, black hiziki seaweed, large prawns and unagi bento at low prices and generous portions. Asian Cuisine is another restaurant in this center.</p>
                <p>You'll find fresh local produce at The Wednesday Farmer's Market, as well as hand-crafted gifts by local crafts workers.</p>
