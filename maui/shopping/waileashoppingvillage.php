                <h2 class="small">Wailea Shopping Village, Maui</h2>
                <p>The Wailea Shopping Village, located near Maui's famed Wailea Resort, has art galleries, jewelry, photography supplies, clothing shops and restaurants. You'll find treasures from internationally renowned designers like Tiffany &amp; Co. and Louis Vuitton to local artisans by leading artists from Maui. There is also a free Polynesian show every Tuesday afternoon. </p>
                <? include ("../../theme/text.html") ?>
                <p>Some of the luxury shops are Betsy Johnson, Bottega Veneta, Cache, Coach, Cos Bar, Vendi, Gucci, Guess, St. John Boutique and Tori Richard. When searching for fashion and casual clothing, some of the favorites are Banana Republic, Billabong, Chico's, Crazy Shirts, Enchantress Boutiques, Gap, Honolua Wahine, Maui Water Wear, Quicksilver, Sgt. Leisure, Sisters &amp; Co. Boutique Salon, The Walking Company and White House/ Black Market.</p>
                <p>For the perfect gift or jewelry, check out Elephant Walk- A Gallery of Life, Martin &amp; MacArthur, Sand People, Black Pearl Gallery, Dolphin Gallery, Folli Follie, Na Hoku, Tiffany &amp; Co and Wailea Jewelry.</p>
                <p>There are dining options to meet your mood and cravings ranging from typical American foods to Pacific Rim cuisine. Some options include Longhi's, Ruth's Chris Steak House, Tommy Bahama's Tropical Cafe and Cheeseburger Island.</p>
                <p>For entertainment options, sip a cocktail while you listen to Hawaiian music and dance at WOW! Wailea on Wednesdays. Other events include gallery receptions, artist appearances, retail and restaurant specials and free live entertainment every Wednesday.</p>
                
