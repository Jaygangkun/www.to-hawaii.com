                <h2 class="small">Queen Kaahumanu Center, Maui</h2>
                <p>The Queen Kaahumanu Center is the only place on Maui that has big department stores, such as Sears and Macy's. There are also more than 100 cafes and boutiques ranging from island-style clothing to traditional brands like American Eagle Outfitters and Forever 21. Other stores include Sharper Image, Borders Express, Tempo Music, Tropical Tantrum and Shapers. </p>
                
                <p>Hawaii's largest department store, Liberty House, and a Japanese department store, Shirokiya, are also located here. Plus, there is a children's soft-play area for ocean-themed fun. </p>
                <? include ("../../theme/text.html") ?>
                <p>This center is a great place for shoppers to grab a gourmet cappuccino, or dine at Ramen Ya, Sushi-Go, or Ruby's Diner. The Queen&rsquo;s Market Food Court offers great snacks at places like Edo Japan, Paradise Cafe, Alexander's Fish &amp; Chips, Maui Tacos and Pearl's BBQ. Koho, near Bank of Hawaii, is a popular breakfast spot. </p>
                <p>For entertainment, enjoy Hawaiian music and dance performances on the Queen&rsquo;s Stage. You can also grab an ice-cream and watch a movie at the Kaahumanu Theatres. Special events at the center include Kamaaina Nights, the Micronesian Cultural Festival, the Maui Marathon and the Stand for Children Rally.</p>
                
