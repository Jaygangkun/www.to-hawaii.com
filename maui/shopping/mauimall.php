                <p>At Maui's premier shopping center, you'll find a 12-screen Megaplex Theatre, more than 40 locally owned stores and a whole lot of games, food and conveniences. </p>
                <p>DasNutz and Red Eye Sojah is Maui's newest and hottest clothing store with local clothes for men, women, youth and infants. Another clothing shop is Ground Swell. For gift shopping, check out Cost Less Imports for tropical home d&eacute;cor, bamboo, hammocks, lamps and lanterns. </p>
                
                <p>Other fun gift shops are Maui Creations, Wild Creatures (for kids stuff) and Keiki Time. For jewelry, check out Royal Hawaiian Heritage Jewelry Crystal Dreams and Maui Creations </p>
