                <h2 class="small">Azeka Place, Maui</h2>
                <p>A favorite place among locals and visitors alike, the Azeka Place on Maui is built in traditional Hawaiian architecture with open gateways and tropical landscaping for beauty and shade. It is also the the largest shopping center in Kihei with more than 50 stores, located on both sides of South Kihei Road, mauka (mountain side) and makai (ocean side). </p>
                
                <p>Some of the fun stores for Hawaiian gifts and lifestyle for visitors are: B &amp; B Scuba, Blue Water Rafting, Destination Resorts Hawaii, Blue Water Rafting, Hiilani Ka-Leo-Aloha Apparel &amp; Gift Shop, Hula Hogs, Izakaya Matsu and Maui Quilt Shop.</p>
                <? include ("../../theme/text.html") ?>
                <p>For a unique food options check out Jawz Fish Tacos Island Style Grill for fresh Mexican with a twist of Hawaii, specializing in fish tacos. You'll also find Home Maid Cafe, Royal Thai Cuisine, Stella's Blues Cafe, Tips-Ups Tavern and Grill and Who Cut the Cheese, famed for aged gouda. The Coffee Store is a great place for iced mochas.</p>
                <p>If you are looking to rent snorkel gear, stop by Snorkel Bob's to snorkel at nearby Sugar Beach and other North Kihei resorts. Azeka Place also hosts special events, such as Maui's Keiki Art Contest, O-Bon Dance, Peggy Sue's Classic Car Show, Lokahi.</p>
                <p>Azeka Place also has a post office and gas station.</p>
                
