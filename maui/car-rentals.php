              <h2> Maui Transportation Tips</h2>
              <p>If you want to travel independently and see Maui on your own, a rental car is the best option. It is recommended to call ahead and reserve in advance, since rental cars can be hard to come by on holiday weekends and on national and state holidays.</p>
              <p>All the major car rental companies, such as Alamo, Avis, Budget, Dollar, Hertz, National and Thrifty, can be found on Maui. For those on a budget, older cars can be rented for lower prices from two discount companies, Word of Mouth Rent-a-Used Car in Kahului and Maui Cruisers in Wailuku. The main airport of Kahului and other locations all over the island are locations for car rental counters.</p>
              <p>Maui also has a good public bus system that provides service between various central, west, south, Haiku and upcountry Maui communities. The fare is $1 per boarding for adults. Monthly passes are also available. The routes operate seven days a week, including holidays. For more information about the individual routes and schedules, visit http://www.co.maui.hi.us/index.aspx?NID=605</p>
              <p>Operating every hour from 7 am to 6 pm each day, the Trans-Hawaiian airport shuttle goes from the Kahului airport to Kaanapali ($13 fee, no reservations required). Akina Tours offers another shuttle that goes to Kihei ($12 fee, reservations required).</p>
              <p>A convenient option for shoppers is the Maui Shopping Express, a shuttle that runs six times a day, stopping at Maui's major malls and shopping centers. There are also shuttles offered by various resorts that stop at different hotels.</p>
              <p>Going to places all over the island at any time, taxis are another option for travelers. The baggage claim area at the airport is a spot where taxis wait. From the airport, expect to pay about $60 to head for Kapalua, $50 for Kaanapali and $30 for Wailea.</p>
              <p>Those with a sense of adventure can rent motorcycles at Island Riders in Lahaina and Kihei, which also rents out Prowlers, Dodge Vipers, Corvettes and other muscle cars. Toy Cars in Lahaina rents out mopeds, motorcycles and dune buggies.</p>
              <p>The Holo Kaa Public Transit presents another option for people who want to go around Maui. The stops in West Maui are Kapalua, Napili, Kahana, Honokowai, Kaanapali, Lahaina Center, Lahaina Town and Maalea Harbor. The south Maui route starts at Maalaea Harbor, stops in several spots and ends in Wailea.<br>
              </p>
