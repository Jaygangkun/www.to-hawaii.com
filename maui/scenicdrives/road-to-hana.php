                <h2 class="small">Road to Hana, Maui</h2>
                <p>The Road to Hana (<a href="../../maui/scenicdrives/more_roadtohana_photos.html" onClick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>), also known as the Hana Highway, is one of Maui&rsquo;s most popular attractions. You can either drive this adventurous road yourself or book a <a href="https://www.roadtohanatours.com/" target="_blank">road to Hana tour</a> with one of the local activity companies. Both options have advantages. If you drive yourself, you can stop wherever you wish and as often as you want. If you&rsquo;re on a tour bus, you can relax and let your tour guide do the driving while you sit back and enjoy the views.</p>
<? include ("../../theme/text.html") ?>
                                <p>Even though this legendary road is just 52 miles (84 km) long (from Kahului to Hana), it takes around three hours and can be quite a challenge to navigate. That&rsquo;s because it is narrow and winding. There are about 620 curves and 59 bridges, 46 of which are one-lane bridges. It can take a toll on one&rsquo;s nerves, especially if one is used to driving on wide, multiple-lane highways. That&rsquo;s why the Road to Hana is also known as the &ldquo;Divorce Highway,&rdquo; because it has caused quite a few arguments among some couples. </p>
                <p>The drive is very much worth it though and offers many scenic views of Maui&rsquo;s northern coastline, rainforests, black-sand beaches, waterfalls and lush, tropical vegetation. The Road to Hana is listed on the National Register of Historic Places. Hana itself is a sleepy little town. There is a luxurious hotel, the Travaasa Hana, an art gallery and some small shops, eateries and road stands. </p>
                <p>So in case you plan to drive yourself, just take your time and make an entire day of this road trip. Stop at the scenic lookout points and take in all the views. Also, stop at some of the roadside stands that sell local foods, including really good banana breads and Hawaiian coffee.</p>
                <p>Just south of Hana is another popular attraction, the <a href="../../maui/attractions/oheogulch.php">Oheo Gulch</a>, also known as the Seven Sacred Pools. The road continues as a dirt road, the Piilani Highway (Highway 31). </p>
                
