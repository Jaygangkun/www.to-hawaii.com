                <h2 class="small">Wailea Alanui Drive, Maui</h2>
                <p>Wailea Alanui Drive is a scenic road that connects the oceanfront resorts and beach parks in Wailea. The road starts at the Wailea Ekahi Village and leads south, all the way to <a href="../../maui/beaches/poolenalenabeach.php">Poolenalena Beach</a>. It then continues south as Makena Alanui (Makena Road). The landscaping along the side of the road is meticulously maintained (the area would otherwise look parched because it doesn't get much rain). Many upscale resorts are located along the road, as are several of Maui's best and most popular beach parks, including <a href="../../maui/beaches/mokapubeach.php">Mokapu Beach</a>, <a href="../../maui/beaches/uluabeach.php">Ulua Beach</a>, <a href="../../maui/beaches/waileabeach.php">Wailea Beach</a> and <a href="../../maui/beaches/polobeach.php">Polo Beach</a>. </p>
