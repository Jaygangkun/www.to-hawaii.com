                <h2 class="small">Pi'ilani Highway (Hwy 31), Maui</h2>
                <p>Also known as the back road to <a href="../cities/hana.php">Hana</a>, the Pi'ilani Highway (Hwy 31) (<a href="../../maui/scenicdrives/more_piilani_highway_photos.html" onClick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) leads along Mt. Haleakala's southern flank, from upcountry Kula and Ulupalakua to the small communities of Kipahulu and Hana on Maui's east shore. The highway has a bad reputation and some rental car companies either caution against driving this road or prohibit it. </p>
                <p>The &quot;highway&quot; is not really a highway like you would imagine it, with at least one lane in either direction, but more like a remote country road. In some areas (such as at <a href="../beaches/lelekeabay.php">Lelekea Bay</a>) it's just a one-and-a-half lane bumpy road hugging the sea cliff around a blind curve. Fences have been installed on the mountain here to prevent rocks from falling onto cars. <br />
                  <br />
                  Luckily the traffic on Pi'ilani Highway is much lighter than on the <a href="road-to-hana.php">Hana Highway</a> between Kahului and Hana, the road most people take to get to Hana. Compared to the lush and green rainforest vegetation and many waterfalls along the Hana Highway, the vegetation along the Pi'ilani Highway is drier with red-brown meadows, pebble beaches and sea cliffs. </p>
