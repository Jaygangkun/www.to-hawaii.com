                  <li>Avoid driving Pi'ilani Highway during or after heavy rainfall</li>
                  <li>Four-wheel drive car not necessary</li>
                  <li>Drive slowly around narrow blind curves</li>
                  <li>Great views along the way - pebble beaches, sea cliffs, undeveloped coastline</li>
                  <li>Distance from Ulupalakua to Hana is about 37 miles (60 km)</li>
