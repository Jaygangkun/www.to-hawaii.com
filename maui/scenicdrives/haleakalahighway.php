                <h2 class="small">Haleakala Highway (Crater Road), Maui</h2>
                <p>Haleakala Highway, also known as Crater Road, is the two-lane road that leads to the summit of Haleakala. The road is well-maintained and well-traveled, but it's still a good idea to drive carefully and obey the speed limit signs. There are many switchbacks, blind turns and steep dropoffs without guardrails. Also, sometimes animals including cows and <a href="../../birds/hawaiiangoose.php">nene</a> geese cross the highway.</p>
                
                <p>At mile marker 10 there is a booth where all vehicles are required to pay $10 entry fee to <a href="../attractions/haleakala-national-park.php">Haleakala National Park</a>. The visitor center is coming up next on your right. It is located at 7,000 feet (2.134 m). The temperature up here will already be quite a bit colder than at sea level. In the winter months, the temperature here ranges between 41-59 &deg;F (5-15&deg;C). In summer it is between 47-66 &deg;F (8-19&deg;C). Six miles further up between mile markers 17 and 18 you'll reach the Leleiwi Lookout. From here you can also enjoy nice views of the crater. </p>
                <? include ("../../theme/text.html") ?>
                <p>Located after mile marker 20 is the upper visitor center. There is a large parking lot here as well as restrooms. The temperature up here is about 40&deg;F (4&deg;C) in the winter and 46&deg;F (8&deg;C) in the summer. Past the upper visitor center, the road continues to the summit at 10,023 feet (3.055 m). </p>
                <p>Along the way, keep an eye out for the <a href="../../flowers/silversword.php">silversword</a> plant (you'll probably already get the see this unique plant at the lower visitor center). It is endemic to Hawaii. This amazing plant can live up to 50 years but only blooms at the end of its life. </p>
                
