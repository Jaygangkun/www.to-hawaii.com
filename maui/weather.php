              <p>Just like on the other main Hawaiian islands, the weather on Maui's south and west shore is generally drier and sunnier than on the island's north and east shore. That's because the predominant tradewinds are blowing from the northeast about 90% of the time in the summer months (May to September) and about 50% of the time in the winter months (October to April), and these winds bring moisture with them. As these moisture-laden clouds approach the islands, they get stuck on the mountains, where the moisture comes down as rain. The weather on Maui's windward (meaning facing the tradewinds) shore is therefore usually a lot wetter than on the leeward (west) shore. But thanks to the rain, the windward facing shorelines are green and lush with waterfalls cascading down the mountains.</p>
          <? include ("../theme/text.html") ?>
              <p>The temperatures at sea level on each Maui coastline don't vary much and are about the same. The main difference is the amount of rain and wind that the coastal areas receive. The less windy areas are   between <a href="../maui/cities/kihei.php">Kihei</a> and Makena in South Maui and <a href="../maui/cities/lahaina.php">Lahaina</a> in West Maui. These same areas are also the driest. The summit of <a href="../maui/attractions/haleakala-national-park.php">Haleakala</a> is the chilliest spot on Maui since it rises 10,023 feet (3,055 m). In the winter, the average high temperature is 50&deg;F (10&deg;C) and the average low is 32&deg;F (0&deg;C). In the summer, the average high temperature is 58&deg;F (14&deg;C) and the average low is 38&deg;F (3&deg;C).</p>
              <p>Maui's <strong>average ocean temperature</strong> is around 75&deg;F (24&deg;C) in February and 80&deg;F (27&deg;C) in September.<br />
