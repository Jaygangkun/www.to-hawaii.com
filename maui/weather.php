              <p><strong><br />
                Volcanic smog (vog) on Maui</strong></p>
              <p>Vog (volcanic smog) is much more common on Hawaii's Big Island than on Maui since the Big Island is the only Hawaiian island that still has active volcanoes. About 200 tons of sulfur dioxide and other toxic fumes are spewing out of Kilauea's Pu'u O'o vent each day. Thanks to the tradewinds a lot of these fumes are quickly blown out to sea. Areas around Hawaii Volcanoes National Park and the Big Island's Kona Coast are the most affected by this vog.</p>
              <p>Occasionally when the winds are blowing from the south (known as Kona winds), the vog from the Big Island's volcanoes affects the air quality of the other Hawaiian islands, including Maui. On those days you will notice a haze in the air, especially when looking into the distance. While most people can tolerate vog and won't notice any difference in how they are feeling, for those with asthma or other breathing problems vog is much more of an issue and it is best be avoided by staying indoors.</p>
