                <h2 class="small">Olowalu Petroglyphs (Pu'u Kilea), Maui</h2>
                <p>Olowalu is one of the easily accessible petroglyph sites on Maui. Access is via a quarter mile (400 m) long dirt trail. Also known as Pu'u Kilea in the Hawaiian language, the Olowalu Petroglyphs are located along a basalt cliff in West Maui. About 100 ancient images have been chiseled into the rock here by ancient Hawaiians. You'll be able to see human and animal figures, as well as sails. </p>
                
                <p>The Lahaina Restoration Foundation got a lease to this site in 1964 and built a wooden stairway and viewing platform. Unfortunately the site has been vandalized in the past. Some of the rocks have graffiti on them.</p>
                <? include ("../../theme/text.html") ?>
                <p>Olowalu is also known as the site of a bloody massacre, which happened in 1790. The captain of the American ship <em>Eleanora</em> (Capt. Simon Metcalf) became upset because of a stolen boat and opened fire on Hawaiian canoes, killing more than 100 men and wounding more than 200 near the ancient Olowalu landing site. This massacre led Big Island Chief Kame'eiamoku to attack the next Western ship that arrived, which was the <em>Fair American</em>, captained by Metcalf's son. The entire crew was attacked and killed, except for one person, Isaac Davis, who in later years became an advisor to King Kamehameha the Great. <br />
                </p>
                
