                <h2 class="small">Kahakuloa Head (Pu'u Koa'e), Maui</h2>
                <p>Kahakuloa Head (Pu'u Koa'e) is located in a very remote area in West Maui (on the island's north shore) near the sleepy village Kahakuloa. The 636-foot (194 m) hill is a scenic landmark, but not many people make it here. The main reason is because the only road that leads here, Kahekili Highway (Route 340) isn't really a &ldquo;highway.&rdquo; Many people consider it hazardous and one of the spookiest roads they have ever driven. The road hugs the sea cliffs and is very narrow (often just one lane), with many blind curves and no guard rails. So not many people visit this part of Maui.</p>
                <? include ("../../theme/text.html") ?>
                <p>It is believed that Kahakuloa Head was one of King Kahekili's favorite cliff-diving spots. King Kahekili was king on Maui in the mid-1700s. He climbed up this hill and leaped into the ocean below. That's why the hill is also known as Kahekili's Leap (just like <a href="puukekaa.php">Black Rock</a> in Ka'anapali). Today, there's still a trail that leads up there, but it's too narrow and dangerous.</p>
                
