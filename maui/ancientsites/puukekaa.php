                <h2 class="small">Pu'u Keka'a (Black Rock), Maui</h2>
                <p>A prominent landmark on Maui's  Ka'anapali Beach is Pu'u Keka'a, also known as Black Rock. It was formed by an old lava flow. This large rock divides Ka'anapali Beach in half. It is backed by hotels, such as the Sheraton Maui Resort, which sits right behind it. The rock is used by young cliff jumpers who can be seen climbing up here and leaping into the waters below. The area surrounding the rock is known as an excellent snorkeling spot. </p>
                <? include ("../../theme/text.html") ?>
                <p>Besides being a popular visitor attraction today, Pu'u Keka'a was of great significance in the old Hawaii and was known as <em>leina a ka &lsquo;uhane</em> (meaning &ldquo;leap of the soul&rdquo;). Legend says that it is here where after death, souls  depart the earth and leap over into the spirit world. For this reason many battles were fought here because of the convenient location. If a warrior died in battle, his soul would be at the right place to find its way into the spirit world. </p>
                <p>King Kahekili (c. 1737&ndash;1794) was Maui's last ruling chief. He ruled the island for 27 years starting in 1766. He was known as a great athlete in the skill of <em>lele kawa</em>, meaning to jump from a high cliff or rock into the ocean. Both Oahu and Lanai have places known as Kahekili's Leap, cliff ledges where Kahekili was known to jump from. But Maui's Pu'u Keka'a was his favorite spot and it was where he made his most impressive jumps. The Hawaiians considered this an amazing feat because only a human with powerful <em>mana</em> (energy) could jump from such a rock and return unharmed, a place where the souls of the dead depart into the other world.</p>
                
