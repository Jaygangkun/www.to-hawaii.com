                <h2 class="small">Pi'ilanihale Heiau, Maui</h2>
                <p>Pi'ilanihale Heiau is quite an impressive sight. It is the largest heiau in all of Hawaii, encompassing almost 3 acres, which equals the size of two football fields. Standing next to it, it is impossible not to feel dwarfed by it. Its stone platform measures more than 415 feet (126 m) in length and 340 feet (103 m) in width and dates back to the late 13th century.</p>
                <? include ("../../theme/text.html") ?>                
                <p>The heiau was built out of precisely fitted lava rock and is unique in that it features a construction that can't be observed at any other heiau in Hawaii. Its northern wall is 50 feet (15 m) high with five steep, terraced steps. The lush surroundings, including tropical flowers, coconut palms and breadfruit trees, add to the mystic atmosphere of old Hawaii. </p>
                <p>The heiau is located on a bluff in East Maui, 4 miles (6.4 km) north of Hana, and has been incorporated into the Kahanu Garden, which is part of the National Tropical Botanical Gardens. </p>
                <p>The name Pi'ilanihale means &ldquo;house of Pi'ilani&rdquo; in the Hawaiian language. The exact history of the heiau is shrouded in mystery. It may have been built by one of the Hana chiefs during the time when Maui was divided, with two ruling chiefs, one in Hana and the other in the rest of Maui. The heiau was rededicated by chief Pi'ilani in the 16th century when he united all of Maui. </p>
                <p>To see the heiau, you'll have to enter Kahahu Garden. They have guided and self-guided tours. Self-guided tours are Monday to Friday between the hours of 9 am to 4 pm; Saturday hours: 9 am - 2 pm. Guided tours are on Monday, Tuesday, Thursday, Friday at 10 am and 12pm. <br />
                  <br />
                  Admission:<br />
                  Self-guided: Adults (13 yrs. and above) $10; children 12 yrs. and under free<br />
                  Guided: Adults (13 yrs. and above) $25; children 12 yrs. and under free </p>
                
