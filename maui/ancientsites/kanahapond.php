                <h2 class="small">Kanaha Pond State Wildlife Sanctuary, Maui</h2>
                <p>Located between downtown Kahului and Kahului Airport, the Kanaha Pond State Wildlife Sanctuary (<a href="../../maui/ancientsites/p1.html" onclick="NewWindow(this.href,'name','2550','620','yes');return false">view panorama</a>) was once a royal fishpond, believed to have been built either by chief Kihapi'ilani who lived around 1500, or by Kapi'ioho'okalani, a chief of Oahu and Molokai. </p>
                <p>Today, it is a waterfowl sanctuary home to over fifty species of birds, including three endangered Hawaiian bird species, the Hawaiian stilt (ae'o), the Hawaiian coot ('alae) and the Hawaiian duck (koloa). Other bird species that can be seen here are doves, geese, gallinules, herons, pheasants, owls, plovers, sandpipers and tattlers. </p>
                <p>There is a paved walkway at the southern end of the refuge from where the birds can best be observed. The pond was designated a National Natural Landmark in 1971. <br />
                </p>
