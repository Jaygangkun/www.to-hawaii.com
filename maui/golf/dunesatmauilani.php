                <p>The Dunes at  Maui Lani Golf Course is located in central Maui and is just a matter of  minutes from Kahului airport and the island&rsquo;s cruise terminal.  The course is built on million-year old sand dunes and the course architect,  Robin Nelson, incorporated the unspoiled terrain into the layout when designing  this golf course. The Dunes at Maui Lani is unique among other courses in the  US because of the dune terrain upon which it is built and because of the  techniques used in designing its layout. The aim of the  owners was to provide a true test for golf players, the way golf was meant to  be played, and to provide a unique golf experience in the Pacific that  incorporates the true spirit and best traditions of the game for all who use  this course. </p>
                
                <p>An 18-hole,  par-72 golf course, the Dunes at Maui Lani closely mimics a British-style links  course with its rugged, sloping terrain and cleaver hold designs. Because of the climatic zone and the particular conditions in  this Pacific region, it is not a precise replica of a proper links course but  it is as close to the authentic version as can be provided here in Hawaii.  Another unique aspect of the Dunes is that it offers the only lighted practice range with grass  tees in central Maui. With a slope rating of 141, the course plays 6,841 yards  from the back tees, and players are met with stunning dunes and scenic views at  every turn, challenging their game at each and every hole.</p>
