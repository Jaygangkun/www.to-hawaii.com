                <p>Pukalani Country Club is nestled into  the slopes of the world-renowned Mount Haleakala, an extinct volcano in  &quot;Up-Country&quot; Maui. Overlooking the Pacific Ocean, there are  spectacular views of the Valley Isle of Maui from the clubhouse and the golf  course. Mount Haleakala means &ldquo;House of the Rising Sun&rdquo; in Hawaiian and this  theme has been adopted by the club as its logo. Designed by architect Bob Baldock  and built in 1980, the club&rsquo;s greenery spans almost 160 acres at its 1,100 foot  elevation level in the mountainside.</p>
                
                <p> Measuring 6,962 yards from the championship tees, the  course is an 18-hole, par-72 challenge that features 19 greens in its layout. This  course is suitable for players of all skills levels, in part because its forgiving design gives players many  par opportunities and chances for a birdie. The fairways are wide, fast, and  undulating. The greens are sloped and the slightly rolling terrain can make for  some tricky play. The course features trees and a number of strategically  located bunkers to add to the challenge and fun. The 3rd hole on  this course, a par-3, is unique in that it gives players the choice of hitting  to one of two greens, one of which means hitting across a ravine and into the  wind, or another that requires a chip directly down to a well-trapped green  many hundred feet into the ravine below. This is the only course in Hawaii that  offers a challenge such as the one found at the third hole and in such a unique  setting. In Hawaiian, &ldquo;Pukalani&rdquo; means &ldquo;Entrance to Heaven&rdquo; and this may ring  true with visitors when they come to Pukalani Country Club.</p>
