                <h2 class="small">The Bay Course at Kapalua Resort, Maui</h2>
                <p>Kapalua Resort is located in Lahainia on Maui&rsquo;s northwest  shore and is home to two golf courses &ndash; the Bay Course and the Plantation  Course. The Bay Course is the older and most mature of the two. Since it opened  in 1975, the beautifully landscaped and perfectly manicured Bay Course has played host to more professional tournaments &ndash;  over twenty &ndash; than any other course in the Hawaiian Islands. It hosted the  first live prime-time televised golf event, the 1983 Kapalua International, as  well as the 2008 Kapalua LPGA Classic. Some of the best golf players in the  world have played, and many have won, at Bay Course. Greg Norman had his first  American win here, David Llwellyn and Ian Woosnam from Wales were victorious in  the World Cup of Golf at Bay Course, and Morgan Pressel had a thrilling victory  at the Kapalua LPGA classic. Fred Couples and Davis Love III, two of the most  popular PGA players of all times, were twice victorious at this course.</p><? include ("../../theme/text.html") ?>
                
                <p>With  its unique layout, this 18-hole, par-72 championship course snakes its way along amongst  tall palms and flower-laden hibiscus.  Designed by Arnold  Palmer and Francis Duane, this course features  some of the most stunning ocean holes in Hawaii. The fourth and fifth holes are  located close to the seaside. Of the two, the fifth is especially breathtaking  and the only one in Maui to play over the ocean with the tee on one side of an  inlet and the green on the other side. One of the best par-4s in Hawaii can be  found at the 16th hole, which requires players to hit over a pond to a split  fairway. Many of the remaining 15 holes provide pleasant ocean views as well as  a glimpse of Molokai across the Pailolo Channel. A course of great beauty, Bay  Course should be on every golf enthusiast&rsquo;s &quot;must play&quot; list.</p>
                <p>The Golf Academy and 23  acre practice facilities at Kapalua were designed by PGA professionals and are  the most state-of-the-art to be found in Hawaii. Beginners and experienced  players can get individual or group instruction in every aspect of the game &ndash;  chipping, putting, wedge play, bunker play, full swing, and specialty shots.  The golf shop, from which items can be purchased online, stocks a wide range of  merchandise including men&rsquo;s wear, women&rsquo;s wear, equipment, gifts, and more. A  number of dining options are also available at Kapalua, from local &quot;plate  lunches&quot; to gourmet Asian, French, Italian, Mediterranean, Pacific Rim,  and fresh Hawaiian fare. The resort also caters for golf outings and other  special events. </p>
                
