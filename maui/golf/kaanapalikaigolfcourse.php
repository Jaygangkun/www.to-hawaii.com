                <p>The Ka'anapali Kai Golf Course is located  in Maui, on land that was once  home to a sugar cane plantation. Originally built as an executive course and later redesigned in the 1970s  by architect Arthur Jack Snyder, Ka'anapali Kai is one of two championship courses at Ka'anapali Resort. It is shorter,  narrower, more forgiving, and has a lower slope rating that its sister course,  the Royal Ka'anapali. Following another renovation by course architect Robin  Nelson in 2008, Ka'anapali Kai played host to the ladies of Golf Channel&rsquo;s  Big Break Ka'anapali.</p>
                
                <p>With  superb views of the West Maui coast and of neighboring islands Molokai  and Lanai, this is an 18-hole, par-70  course. At 6,388 yards, it is made up of subtle, undulating greens that are  equally suitable for beginners and seasoned players. Some of the special  features that make this course an enjoyable experience are the lava rock  boundaries, the gulches, and natural canals. Native flora adorns the course &ndash;  bougainvillea, hibiscus, and plumeria &ndash; and many of the fairways are lined with  coconut trees and Norfolk Pine. The course has a forward set of  &quot;Hibiscus&quot; tees and many of the holes are tucked away into the  mountainside. Round strategy is  considered more important than distance on this scenic and player-friendly  course.</p>
