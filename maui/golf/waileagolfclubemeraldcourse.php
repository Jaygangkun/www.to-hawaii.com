            <div class="new-custom">
              <div class="midpanel_row">
                <p>Located on the south side of magical Maui, the  Emerald is one of three golf courses at the Wailea Golf Resort, the other two  being the Gold Course and the Old Blue Course. All three courses offer  spectacular views of the ocean. Like its sister courses, the Emerald has won  several prestigious awards, including being one of the first courses to be  granted a &ldquo;Top Fairways Award&rdquo; by <em>Golf  for Women</em> Magazine. The magazine identified the Emerald Course as being the  most women-friendly golfing venue in the country, an accolade it has gone on to  receive seven times.</p>
                
                <p>Designed by Robert Trent Jones Jr, the 18-hole,  par-72 championship Emerald course is suitable for virtually every level of  player. It is more modern than its neighbor the Old Blue, and is considered a  little easier than the Gold Course. Although it is no walkover, the generous,  carpet-smooth fairways and the four  to six tee boxes on every hole, which allows players to tailor their game to  their individual ability, makes this course more player-friendly than some  others. It is perfectly suited to those who prefer recreational golf and a  reasonable amount of challenge to a highly-competitive round that requires  near-professional skill and intense concentration. While the eleventh hole is  the longest, at 535 yards from the white tees, the par-4 sixth hole is one of  the Emerald&rsquo;s most memorable.  Measuring 415 yards from the white tees, this is a downhill dogleg that takes  the tee shot onto a generous green, but one that falls off dramatically in  different directions into grass bunkers. This can create a tricky situation if  the approach shot isn&rsquo;t accurate. One of the most unique features and the most  picturesque part of the Emerald Course can be found at the double green shared  by holes No 10 and 17 and where there is a 2.7 million gallon brackish water  lake. All in all, with its lovely tropical foliage and abundant ocean views,  this is one of Hawaii&rsquo;s most pleasant golf courses. </p>
