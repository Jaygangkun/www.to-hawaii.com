                <h2 class="small">Wailea Golf Club - Old Blue Course, Maui</h2>
                <p>Dubbed  the &ldquo;Grand Lady of Wailea,&rdquo; the Old Blue Golf Course at Wailea Resort is a true  classic Hawaiian course with picturesque views of nearby islands and the 10,023-foot  high Mt Haleakala, a dormant volcano. Located on the southern coast of Maui,  about 17 miles from Kahului Airport, the Old Blue is one of Wailea Resort&rsquo;s three golf courses, the other  two being the Emerald Course and the Gold Course. </p>
                <? include ("../../theme/text.html") ?>
                <p>The  18-hole, par-71 Old Blue Course, which was designed by Arthur Jack Snyder and  opened in 1972, has grace and beauty, deriving from an age when golf courses  were defined by character. Rolling along the foot of the mountain, this course  takes players along lush fairways on a leisurely and scenic golf journey. The  course, which measures 6,765 yards from the championship tees, is beautifully landscaped with plenty flowering  hibiscus, bougainvillea, plumeria, and wiliwili trees. There are several lakes  and fountains, lush fairways, classic  coral sand bunkers, and large greens that are not without a certain amount of  challenge. Although the fairways and greens are challenging, there are three  tee boxes at each hole, which makes the course suitable for players of all abilities.&nbsp; Possibly the most difficult hole at the Old  Blue Course is the fourth, a par-5 that requires a blind drive over a sharp  uphill slope. The sloping layout here means that most of the fairway and all of  the green is obscured. However, despite this difficulty, the superb views of  the West Maui Mountains and  Ma`alaea Bay from the hilltop are considered reward enough by many players.  There is no doubt that the Old Blue provides classic golf in a paradise  setting.</p>
                
                <p>The first ever golf course built in Wailea, the Old Blue has its own clubhouse offering a  wide range of amenities and friendly hospitality. These include cart rental,  club rental, bottled water, and locker rooms. The club has a practice facility  and its own pro shop with a selection of golf wear, equipment, gifts, and other  merchandise. There is also a restaurant at the clubhouse or visitors can avail  of any number of dining options offered by the resort as well as partaking of  its wider facilities.</p>
                
