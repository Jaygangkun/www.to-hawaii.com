                <h2 class="small">The Plantation Course, Maui</h2>
                <p>Part of the Kapalua Resort in Lahainia on Maui&rsquo;s  northwest shore, the Plantation Course is one of the resort&rsquo;s two courses, the  other one being Bay Course. The Plantation Course is set on the slopes of the West Maui Mountains and its design is in keeping with the  natural conditions of the locality. The course provides players with dramatic  views of the ocean along almost every part of its 7,411 yards. Every January,  the PGA TOUR Hyundai Tournament of Champions is held at the Plantation Course  and each year the tournament features an elite group of champions from the  previous year. Such golfing legends as Ernie Els, Justin Leonard, Davis Love  III, Vijay Singh, and Tiger Woods, to name but some game&rsquo;s greats, have graced  the course. </p>
                <? include ("../../theme/text.html") ?>
                <p>Designed  by Ben Crenshaw and Bill Coore, the 18-hole, par-73 Plantation Course has  consistently been ranked as Hawaii&rsquo;s Number One course. While its layout is  sufficiently tough to challenge professional golfers, the fairways are wide and  the greens are large enough to make it manageable for the less experienced  player. The course is notable for its dramatic changes in elevation, giving  rise to a fair share of downhill tee shots. The 18th fairway is  located on a relatively steep slope so players should be prepared to make one  of the longest drives they are ever likely to make. However, the experience  should leave them feeling like a pro and ending their game on a high. With its  great combination of beauty and challenge, the Plantation Course should be on every golfer&rsquo;s to do list.</p>
                <p>The Kapalua Resort offers  a full range of amenities befitting its status as a world-class golf resort.  The Golf Academy is state-of-the-art and provides professional tuition to  beginners and seasoned players. There is a 23-acre practice facility with a  driving range, putting areas, and chipping greens, which players can use to  improve their game or warm up before going onto the course. The golf shop, from  which merchandise can be purchased online, is well stocked with golf-related  products, including apparel, equipment, gifts, and much more. A number of  dining options are also available at Kapalua. Those in need of sustenance can  choose anything from local &quot;plate lunches&quot; and various Hawaiian fare  to all manner of international cuisine. Golf outings, social functions, and  other events are also catered for at this resort. </p>
                
