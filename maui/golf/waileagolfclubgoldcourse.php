                <h2 class="small">Wailea Golf Club - Gold Course, Maui</h2>
                <p>The Wailea  Golf Club&rsquo;s Gold Course can be found on the southern coast of Maui, about 17  miles from Kahului Airport, and it is  considered the toughest of the 1,500-acre resort&rsquo;s three golf courses. The other two courses are the Emerald Course  and the Old Blue Course. With spectacular views extending to Molokini and the  Pacific Ocean, the Gold Course has won many accolades, mainly from sources and  publications such as <em>Gayot, </em><em>Golf Digest, Golf Magazine, Golfweek, and Zagat</em>. Other honors include being voted one of  best designed courses in the world (readers of <em>Conde Nast Traveler</em>), and a top choice in the &ldquo;Best Golf  Course&rdquo; category (readers of <em>The Maui News</em>). The course was home to the  Champions Skins Game for six years from 2001 to 2007.</p>
                <? include ("../../theme/text.html") ?>
                <p>The  18-hole, par-72 Wailea Gold Golf Course was masterfully designed by architect  Robert Trent Jones II who strategically incorporated the rugged and natural  terrain of the locality into the layout. The prehistoric lava rock walls on the  site were carefully preserved during construction, a fact recognized by the Maui  Historical Society. The  resulting golf course is a real skills test requiring strategic planning and  finesse. Referred to as a &ldquo;thinking player&rsquo;s&rdquo; course, there are many  risk-reward options open to players on this course with possibilities to use  every club in the bag. However, with  four to six tee boxes at every hole, the course generally suits players of  almost all levels. One of the greatest player hazards or distractions on the  Gold course is, perhaps, the stunning views, of which the par-3 eighth hole is  a good example. Here, players face an unrivalled view of little Molokini and  the Pacific Ocean as they tee off. With a dark <em>papohaku</em> wall positioned  between the tee boxes and the fairway, and a ring of coconut trees and a white  bunker protecting the manicured green, this hole requires every ounce of a  player&rsquo;s concentration. However, with a 200 feet elevation change from start to  end, a more scenic Hawaiian view  than the one found here is difficult to envisage. </p>
                <p>A  highly-rated golf resort, Wailea offers a superb range of amenities. In addition to cart, clubs, and shoe  rental services, there is a comprehensive practice facility and a 12-acre  academy offering professional tuition. The award-winning pro shop offers a wide  selection of top-quality apparel, shoes, equipment, and accessories. The  resort&rsquo;s on-site restaurant and bar offers a wide range of local and  contemporary fare with locally-sourced fresh ingredients in a relaxed and  scenic atmosphere, with a choice of indoor or outdoor seating. The resort also  caters for all types of functions and events.</p>
                
