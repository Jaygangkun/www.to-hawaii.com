                <h2 class="small">Makena North Course, Maui</h2>
                <p>Makena  is located at the most southerly tip of Maui. The Makena North Golf Course is  part of the Makena Golf and Beach Resort and is set in an 1,800 plot of land  that is virtually entirely a nature preserve. The golf atmosphere here is quiet  and totally naturally. Once combined with the South Course, the North Course  was renovated and newly irrigated, and now operates as its own entity. There  are very few residential dwellings on this course, which is framed by thick  vegetation and black lava rock on each side. </p>
                
                <p>Designed  by Robert  Trent Jones Jr and opened in 1993, this 18-hole, par-72 course navigates its way around territory that is  dotted with natural gullies, streams and lava rock walls. There are great views  of the surrounding mountains and the Pacific Ocean from many parts of the  course, especially from the par-3 fourth hole and the 15th, a par-5  that plays downhill. Measuring 6,914 yards from the longest tees, the course is  laid with Bermuda grass and has a slope rating of 139. The signature 14th hole on the  back nine, at 620 yards from the back tees, has the honor of being the second  longest hole in Hawaii. However, play is entirely downhill and, provided the  shots are strong, a player can reach it in two. The layout at Makena provides a  memorable experience because this course is as much a scenic nature ramble as  it is a golf outing. </p>
                <? include ("../../theme/text.html") ?>
                <p>A club  and cart rental service operates at Makena Golf Course. There is a driving  range available for anyone who wants to get a little practice in or warm up  before their game. The pro shop offers a selection of golf wear, equipment, and  other related merchandise. There is a snack bar and restaurant where visitors  can relax with friends and/or fellow players while they partake of refreshments  and enjoy fantastic views.</p>
                
