                <h2 class="small">Maui Nui Golf Club , Maui</h2>
                <p>The  Maui Nui Golf Club was previously called the Elleair Maui Golf Club but  changed its name to reflect the legend and magic of Maui. This golf club can be  found in Kihei, tucked into a hillside in the heart of south Maui, and it is  indeed a hidden gem. You will  scarcely feel the island's winds at this club, at least much less than they can  be felt elsewhere, and there are breathtaking views of the ocean from every  part of the course, as well as a superb view of the world-renowned Maui Sunset.  There is also less rainfall here. </p>
                <? include ("../../theme/text.html") ?>
                <p>Designed by course architect Bill Newis, this  is an 18-hole, par-71 golfing challenge with enough doglegs and changes in  elevation to test every player&rsquo;s skill, as exemplified at the 423-yard fourth  hole. This par-4 hole plays to a 90-degree dogleg some 220 yards off the tee  with a bunker at the end of the fairway just 240 yards from the tee. Hence,  accuracy is essential if players are to get into a good position for the  approach shot. The greens are large at Maui Nui golf course and the fairways were recently made wider, but the  ball will need to be kept in play if golfers want to get a good score.&nbsp; The par-4, 392-yard 15th hole is  one of the most scenic holes on this course, and the 18th is  considered a great finishing hole, providing the chance for players to end  their game with a birdie or an eagle ... and, of course, a great view. The layout of the course meanders through  the foothills of Haleakula, providing superb ocean views and glimpses of the  West Maui Mountains. The volcanic atoll Molokini - a popular diving spot for  snorkelers &ndash; can also be seen from the course. Overall, Maui Nui Golf Course is considered challenging but  fair. </p>
                <p>The amenities at Maui Nui Golf Club are comprehensive. As well as rental carts and rental  clubs, the practice facilities include a lighted driving range, chipping and  putting areas, and PGA professionals to provide lessons. The pro shop is well  stocked with golfing apparel, golf clubs, and other related merchandise. Kono&rsquo;s  on the Green, a fully stocked bar, is the ideal place to relax with your  favorite beverage after a few hours on the course. Surrounded by beautiful  natural scenery, the clubhouse and restaurant at Maui Nui Golf Club are also  perfect for a corporate outing, a wedding, or other event.</p>
                
