                <p>Kahili Golf Course  is located in Waikapu at the foothill of the West Maui Mountains and offers  breathtaking views of Haleakala and  the Pacific Ocean. Just ten minute from Kahului airport, the course layout  makes the most of the local terrain and the Hawaiian trade winds to provide  players with a unique and challenging golf experience. The hillside location ensures that golfers are afforded  spectacular bi-coastal views &ndash; northwards to Kahului Harbor and southwards to  Maalaea Bay. </p>
                
                <p>The picturesque 18-hole, par-72 course,  designed by Robin Nelson and Rodney Wright, is suitable for players of all  abilities and is a favorite with both locals and visitors to the island. The  turf conditions are superb and the putting surfaces immaculate. Elevation  changes and doglegs are commonplace here. The hilly par-3s are a delight to  play, the reasonably short par-4s play both upwind and downwind, and the par-5s are designed to give long ball hitters the chance  of a birdie or even, perhaps, an eagle. The world-class course, at a length of 6,570 yards, plays host to several  local and state-wide tournaments each year. Unlike many other courses, the fact that there are no properties adjacent  to this course or lining the fairways is another feature that makes this a unique and  special golfing destination. All in all, Kahili Golf Course is considered a must-play for golf  enthusiasts and for anyone who happens to be in the area.</p>
