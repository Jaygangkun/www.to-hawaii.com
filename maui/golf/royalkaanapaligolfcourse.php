                <p>Located  on Maui, the Royal Ka&rsquo;anapali Golf Course is one of two championship  courses at the Ka'anapali Resort. The course is built on 1,200 acres of land  that was once home to Hawaiian royalty and is cleverly designed to make the  most of the views of the West Maui Mountains and the Pacific Ocean. The two  neighboring islands of Molokai and Lanai are visible from virtually every hole  on the course. </p>
                
                <p>Opened in 1961, the 18-hole, par-71 course measures  6,700 yards from the championship tees. In designing this course, the architect  Robert Trent Jones Senior made the most of the rolling landscape with the  result that the fairways slope gently and the large greens are contoured, all  of which ensures a degree of challenge as players approach each hole. The  course starts off at sea level and rises up into the foothills of the West Maui Mountains. This upward climb, the  contoured greens, and the ever-changing trade winds mean that players have to  be careful about their shot selections, which is all part of the unique  experience that is Royal Ka&rsquo;anapali. The 5th hole is the signature hole and  it takes players right down to the oceanfront to beautiful Ka&rsquo;anapali Beach and  Black Rock. The 18th hole, the 449 yard finishing hole, is also a  memorable one, playing longer that it looks and skirting the canal along the  full length of the fairway and green. Recently renovated, Royal Ka&rsquo;anapali plays host to the Wendy's  Champions Skins Game.</p>
