                <h2 class="small">Haleakala National Park, Maui</h2>
                <p>The Haleakala National Park (<a href="../../maui/attractions/more_haleakala_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is one of Maui&rsquo;s main visitor attractions. Located on the south side of Maui, the park&rsquo;s main feature is the famous Haleakala Crater. Mt. Haleakala is the largest dormant volcano in the world. It last erupted around 1790. The crater itself is huge, measuring 7 miles (11.25 km) across, 2 miles (3.2 km) in width and 2,600 feet (800 m) in depth. Visitors often say that they feel like in another world here, in these moon-like surroundings. There are large cinder cones inside the crater.</p>
                <? include ("../../theme/text.html") ?> 
                <p>Hikers can take one of two trails that lead from the summit down into the crater &ndash; the Halemauu trail and the Sliding Sands trail. There are three cabins in the crater where one can stay at, but they have to be reserved in advance. You can also bring your own tent and camp here with a permit.</p>
                <p>The drive up to the summit is very scenic and there are numerous overlooks. There are five distinct climate zones you&rsquo;ll pass through while on this adventure. The road to the summit holds the world record for climbing the highest elevation in the shortest distance, which is about 38 miles. </p>
                <p>At the summit, which is 10,023 feet (3,055 m) above sea level, you can see as far away as the Big Island of Hawaii if you come on a clear day. But make sure you dress warm because up here it can get up to about 30 degrees colder than in the rest of the island. Each morning many visitors come here to watch the spectacular sunrise. There are tours that do hotel pick-ups very early in the morning to take you here. Or you can drive yourself. Others come here to watch the equally amazing sunset.</p>
                <p>Many astronomers also come to Haleakala&rsquo;s summit because it offers unusually clear views of the night sky. In fact, Haleakala is one of the best places in the U.S. for amateur astronomy. You can rent telescopes and binoculars at many local merchants.</p>
                <p>When exploring Haleakala National Park, you also have the rare opportunity to see the Silversword plant. It&rsquo;s a threatened species of plant that only grows above 6,000 feet and can live for about 50 years. It only blooms once in its lifetime, right before it dies.</p>
                <p>Haleakala is a Hawaiian word, meaning &ldquo;house of the sun.&rdquo; According to a Hawaiian legend, the demigod Maui lassoed the sun from here to slow it down and lengthen the day.</p>
                <p>Before you drive up to Haleakala, make sure you have enough gas and take some food because there aren&rsquo;t any gas stations or convenience stores in this area. For pregnant women or people with respiratory problems this journey is not recommended due to the effects from the high elevation. Check with your doctor before you go.</p>
                <p><strong>Hours</strong></p>
                <p>The park is open 24 hours a day, 7 days a week, except for severe weather closures. <br />
                  Park Headquarters Visitor Center (7,000 ft/2,134 m): 8:00 am to 3:45 pm <br />
                  Haleakala Visitor Center (9,740 ft/2,969 m): 6:30 am to 3:00 pm <br />
                  Kipahulu Visitor Center (sea level): 9:00 am to 4:30 pm</p>
                <p><strong>Admission</strong></p>
                <p>All park visitors have to purchase a recreational use pass upon entering Haleakala National Park. It costs $12 per person and is valid for 3 days. This ticket is for one person with no car and includes admission to the summit and Kipahulu areas. It&rsquo;s typically used for bicyclists, hikers and pedestrians. Children 15 and under are free.</p>
                <p>A ticket to bring your private vehicle costs $25 and is valid for 3 days. It&rsquo;s for non-commercial vehicles with a passenger capacity of 14 or less. It admits all occupants of the vehicle.<br />
                </p>
                
