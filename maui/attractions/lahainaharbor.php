                <h2 class="small">Lahaina Harbor, Maui</h2>
                <p>Lahaina Harbor (<a href="../../maui/attractions/more_lahainaharbor_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is located right in the heart of Lahaina, in front of the legendary Pioneer Inn. Once notorious for harboring illegal whaling ships, it is a popular visitor destination today. Many activity companies that offer shoreline cruises, snorkeling and diving excursions and parasailing or glass-bottom-boat tours start from here. </p>
                <? include ("../../theme/text.html") ?> 
                <p>Also, the ferries to the neighboring islands of Lanai and Molokai depart from Lahaina Harbor. From here one can also enjoy nice views of Lahaina&rsquo;s shoreline, the neighboring island of Lanai and see a wide variety of vessels &ndash; sailboats, catamarans, motorboats and luxurious yachts.</p>
                
