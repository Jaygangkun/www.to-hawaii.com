                <h2 class="small">Makena State Park, Maui</h2>
                <p>Makena State Park, located on Maui's south shore south of Wailea, is a popular swimming, snorkeling and diving destination. There are three great beaches within this park, and all of them are unique. </p>
                
                <p><a href="../../maui/beaches/bigbeach.php">Big Beach</a>, also known as Makena Beach, is one of Maui's longest white-sand beaches at 1.5 miles (2.4 km) long. This is the perfect beach for the ultimate beach stroll.</p>
                <p>Neighboring <a href="../../maui/beaches/littlebeach.php">Little Beach</a>, also known as Pu'u Ola'i Beach, sits directly in front of the cinder cone after which the beach is named &ndash; Pu'u Ola'i (Red Hill). It is also known as Maui's unofficial nude beach (nudity is illegal in Hawaii). It is accessible via a short trail from Big Beach.</p>
                <? include ("../../theme/text.html") ?> 
                <p>The third beach is <a href="../../maui/beaches/oneulibeach.php">One'uli Beach</a> (also known as Naupaka Beach), which lies just to the north of Pu'u Ola'i. It also also called Black Sand Beach because of its dark sand. This beach is one of the least visited in the state park and is never crowded.</p>
                <p>All three of them offer great snorkeling and diving conditions when the ocean is calm. There is a spectacular coral reef, home to many colorful tropical fish. However, if the surf is up, the ocean can be treacherous here. Especially at Big Beach (the only beach with lifeguards in this area), the shorebreak is often times so powerful that swimmers have suffered spinal injuries and bone fractures.</p>
                <p>One of the most prominent features of this state park is Pu'u Ola'i, a dormant volcanic cinder cone measuring 360 feet (110 m) high. At its base are lava rocks and tidepools.<br />
                </p>
                
