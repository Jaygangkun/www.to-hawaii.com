                <h2 class="small">Dragon's Teeth, Maui</h2>
                <p>The result of one of the last lava flows on Maui, Dragon's Teeth is a unique rock formation located at Makaluapuna Point in Kapalua on the island&rsquo;s northwestern shore. Hundreds of years ago, the molten lava that poured from the West Maui volcano was lighter, denser and more fine-grained than other flows on the island, and as it crawled towards the ocean, fierce waves and sea winds sweeping in over the point caused the lava to harden in an upward fashion &ndash; causing jagged black points that look like dragon's teeth. </p>
                <? include ("../../theme/text.html") ?> 
                <p>                  Over the years, salty ocean spray has whitened the lava and even created holes in some areas. The large lawn in front of the Makaluapuna Point peninsula is an ancient Hawaiian burial site called Honokahua. The nearby Ritz-Carlton Kapalua Resort originally was to be built right here on this lawn, but after construction began and human remains were discovered, it led to a three-year battle (1986 to 1989) between the resort and Native Hawaiians. In the end, the resort was built further inland so that the sacred site would not be disturbed. It is estimated that over a thousand iwi (human remains) are buried here, some dating to A.D. 610. A sign was erected here to discourage visitors from walking over to Makaluapuna Point where Dragon's Teeth is located. </p>
                <p>                  The sign reads: &quot;Makaluapuna is a wahi pana (sacred site) to na kanaka maoli (Native Hawaiians). Entry is discouraged except for Hawaiian protocol or cultural practices. Your cooperation and respect are appreciated. Mahalo - Thank you.&quot;</p>
                <p>So if you decide to visit this area, be respectful and don't enter the burial ground.</p>

