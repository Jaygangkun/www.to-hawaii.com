                <h2 class="small">Ali'i Kula Lavender Farm, Maui</h2>
                <p>The Ali'i Kula Lavender farm, perched 4,000 feet (1,220 m) on the slopes of Haleakala, is home to over 55,000 lavender plants blooming across 13.5 acres. The farm grows more than 45 varieties of the lovely plant, of which seven are blooming year-round. In addition to lavender, the farm grows proteas, geraniums, hydrangeas, roses, olive trees and native Hawaiian plants.</p>
                
                <p>As lavender is not a native to Hawaii, the history behind the plants' arrival to the Islands is somewhat unclear. But local residents are saying that in the 19th century the herb traveled from England to the royal palace in Honolulu. Legend has it that Hawaii's last monarch, Queen Lili'uokalani fancied it. Living up to its noble history, Ali'i Kula Lavender means the Royal Lavender of Kula.</p>
                <? include ("../../theme/text.html") ?>
                <p>Lavender is renowned for its calming and stress relieving effects. It is used as a cure for insomnia and depression. The farm spreads across rolling hills is a great place to learn about the plant while enjoying the natural beauty of the place through the various walking tours and classes offered at the farm. </p>
                <p>A gift shop is located on the premises where visitors can find an impressive collection of products made from lavender. The range includes gourmet, bath and body, pet, aromatherapy and apparel as well as gift sets and specialty items. According to the owner, the wide range of products available is possible due to partnerships with more than 25 local businesses. </p>
                <p>Tasting one of the fabulous lavender scones with a cup of freshly brewed lavender tea is a great way to wind up a day at this pristine farm. <br />
                  <br />
                  <strong>Hours:</strong><br />
                  The farm is open daily from 9 am to 4 pm.<br />
                </p>
                <p><strong>Admission:</strong><br />
                  General Admission to visit the farm is $3.00 per person. Kama'aina, Military and Senior discounts are available. Children 12 and under are free admission.<br />
                Walking tours are $12 per person.</p>
