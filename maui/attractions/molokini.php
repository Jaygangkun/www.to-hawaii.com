                <h2 class="small">Molokini, Maui</h2>
                <p>Molokini (<a href="../../maui/attractions/more_molokini_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a tiny island located about 2.5 miles (4 km) off the southern coast of Maui. It is a scuba diver's and snorkeler's paradise. In fact, it was named one of the top ten dive spots in the world! Molokini Island, which is actually a sunken volcanic crater, is also a designated Hawaii State Marine Life and Bird Conservation District; that's why the water is as clear as glass here and the fish so abundant. Fishing is not allowed here.</p>
                <? include ("../../theme/text.html") ?> 
                <p>The crescent shape of Molokini Crater protects divers from high waves and currents. For snorkelers the inside area is a good spot because it is protected. Here you'll be able to see all kinds of marine life, such as colorful tropical fish, eels and turtles. More than 250 species of fish can be observed, some of which exist nowhere else in the world. Most commonly seen are the Yellow Tang, Black Triggerfish, Parrotfish, Moorish Idol, Bluefin Trevally and Raccoon Butterflyfish, as well as 38 hard coral species. So an excursion to this small rock island is an adventure for all Molokini snorkel fans.</p>
                <p>The visibility is also great. On a calm day, you'll be able to see up to 150 feet (45.7 m) down to the bottom. Molokini's back wall drops off to depths of 300 feet (91.4 m). So this area is perfect for advanced divers. Molokini snorkeling tours are a unique and popular activity on Maui. Boat tours depart from either Maalaea or Lahaina Harbor. Even if you don't want to get into the water and are just accompanying family or friends, you won't be bored. It's a fun boat ride and the views are great. Also, instead of fish you can observe a variety of sea birds. The small island itself is home to nesting seabirds, including Wedge-tailed Shearwaters, Bulwer's Petrels and Great Frigatebirds.</p>
                
