                <h2 class="small">Oheo Gulch, Maui</h2>
                <p>Looking to spend a few hours away from the lively beaches of Maui? Head to the southeast coast and discover the peaceful Oheo Gulch, part of the Haleakala National Park on the slopes of Maui's famed volcano. Also known as the Seven Sacred Pools, this sprawling park is home to streams, majestic waterfalls and an amazing display of Hawaiian flora.</p>
                <p>The name Seven Sacred Pools is actually a misnomer - there are dozens of pools in the gulch, each one fed by a series of waterfalls and draining into the ocean nearby. A large stream runs through the area and is crossed by an open bridge, through which visitors can pass to explore the other side. Once you cross (and pay the $10 parking fee), you can access the rest of the park, particularly the winding trail that leads to the peak of Haleakala. </p>
                <? include ("../../theme/text.html") ?> 
                <p>About 2 miles (3.2 km) inland, you will find the two streams that feed all the waterfalls in the area. The Pipiwai and Palikea Streams merge in the distance to create the pools that give the place its name. Over the years many people have enjoyed swimming in the pools, but currently the stream and pools are  closed for swimming until further notice due to the theft of stream monitoring equipment. Furthermore, swimming is not recommended in this area (even if the stream is eventually reopened) due to the danger of violent flash floods. In the past, very strong flash floods have occurred in the area and people have been swept into the ocean and drowned.</p>
                <p>The gulch is surrounded by a series of trails known as the Kipahulu Area trails. The most popular is the Pipiwai Trail, which lies 650 feet (198 m) above the gulch and spans 2 miles (3.2 km) each way. It's an exhausting 2.5-hour hike, but well worth it. Along the way, you'll see great waterfalls, lookout points and a wide expanse of greenery and blue skies. The trail ends at the 400-ft (122 m) <a href="../../maui/waterfalls/waimokufalls.php">Waimoku Falls</a>, which falls down a sheer wall of lava rock and makes a truly magnificent picture. </p>
                
