                <h2 class="small">Kihei Boat Ramp, Maui</h2>
                <p>The Kihei Boat Ramp (<a href="../../maui/attractions/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is where charter boats depart from to go out to Molokini or other South Maui destinations. It is conveniently located at the south end of Kihei, ideal for visitors who are staying in this area and don't want to drive up to <a href="../../maui/attractions/maalaeaharbor.php">Ma'alaea Harbor</a>. During the morning hours the boat ramp can get busy, but there is plenty of visitor parking at the two upper level parking lots. The lower lot is used by trucks and trailers.</p>
                <p>Even if you're not going out on a cruise, it's a nice place to visit and relax on the small beach. Just keep in mind that swimming is not allowed in the boat channel. There are some trees that provide shade on the beach. Picnic tables are available as well. Bathrooms are located between the two parking lots.</p>
