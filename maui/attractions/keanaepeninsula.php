                <h2 class="small">Keanae Peninsula, Maui</h2>
                <p>The Keanae Peninsula (<a href="../../maui/attractions/more_keanae_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is located off of Maui&rsquo;s famous Road to Hana. It&rsquo;s a nice spot to visit on your way to Hana (or on your way back). The scenery is gorgeous. There&rsquo;s an old Hawaiian village, taro patches and a church that was built in 1856. The shoreline on this peninsula is rocky. </p>
                <? include ("../../theme/text.html") ?> 
                <p>Even though there&rsquo;s no sandy beach, the waves slashing against the rocks and lava pinnacles provide some great picture-taking opportunities. In 1946, the Keanae Peninsula was almost completely destroyed by a tsunami. The only building that was left standing was the old stone church.</p>
                
                <h2 class="big">Keanae Peninsula Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Located right off of Hana Highway</li>
                  <li>Gorgeous shoreline, with waves crashing against lava rocks</li>
                  <li>Old Hawaiian village and church built in 1856</li>
                  <li>Swimming or other water activities are not possible here due to the rocky shoreline and dangerous high surf and currents</li>
