                <h2 class="small">Hana Farms, Maui</h2>
                <p>This little roadside stand is a &ldquo;must visit&rdquo; on your way to Hana (or on your way back). It&rsquo;s conveniently located on the famous Hana Highway on Maui&rsquo;s northern shore. Hana Farms is a popular stop not only for visitors, but locals alike. Their banana bread is loved by many and they sell six different varieties of it. They bake it fresh every day and when you buy it it&rsquo;s often times still warm. They also have fresh-squeezed lemonade, coffee, jams, candy, jewelry, art and ice cream.</p>
                <p>Hana Farms roadside stand is located right before the &ldquo;Welcome to Hana&rdquo; sign. Look for the small Polynesian huts and the banana bread sign.</p>
