                <h2 class="small">Maui Ocean Center, Maui</h2>
                <p>Even seasoned divers can only dream of reaching the depths of the Pacific and facing the creatures of the deep. But that&rsquo;s what the Maui Ocean Center is here for: it brings you some of the most fascinating marine creatures this side of the Pacific in an amazing, three-acre aquatic display. Located on the west coast of Maui at Maalaea Harbor, the park boasts more than 60 exhibits and hundreds of sea creatures, each one native to the Hawaiian Islands.</p>
                <? include ("../../theme/text.html") ?> 
                <p>The marine tour starts at the Surge Zone, an interactive display simulating the crashing waves and how the young fish scramble for cover. Next to it is the Living Reef, where you&rsquo;ll find colorful coral reefs and typical reef creatures, and watch them feed off the intricate architecture. Other areas worth exploring are the Tide Pool, the Marine Mammal Discovery Center, Hawaiians and the Sea and the Open Ocean.</p>
                <p>Perhaps the biggest draw, especially for kids, is the Maui Aquarium, which features a wide array of sea creatures in a magnificent larger-than-life aquarium. Kids especially love the Turtle Lagoon, home to the famous honu or green sea turtles, and the Hammerhead Harbor, where you&rsquo;ll come face to face with scalloped hammerhead sharks.</p>
                <p>The Maui Ocean Center is open throughout the year from 9 am to 5 pm. From July to August - peak season for tourists - the hours are usually extended up to 6 pm. You can buy tickets online or at the park entrance. No reservations are required, but they are recommended if you&rsquo;re coming in a large group.</p>
                
