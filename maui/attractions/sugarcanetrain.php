                <h2 class="small">Sugar Cane Train, Maui</h2>
                <p>From the old-school exterior to the historical discourse that goes on inside, the Sugar Cane Train on Maui gives you the ultimate walk through time. Located in near Lahaina and Kaanapali, the train is a larger-than-life reproduction of the actual sugar cane train that ran through the area in the early 1900s. Passengers are treated to a one-hour train ride featuring an authentic wooden trestle, a singing conductor and great panoramic views of the West Maui Mountains.</p>
                <p>Officially known as the Lahaina-Kaanapali and Pacific Railroad, the Sugar Cane Train makes use of authentic elements, such as steam whistles and a narrow-gauge track. Some of the parts actually come from the actual train date back as far as the 1880s. </p>
                <? include ("../../theme/text.html") ?> 
                                <p>The train runs from the historic Lahaina to Puukolii, a distance spanning roughly six miles (9.6 km). The open-air coach sits 200 passengers and passes through a scenic resort town, Kaanapali, where you&rsquo;ll get great views of the ocean and the nearby islands of Lanai and Molokai. As you pass through the 415-foot-high (126 m) wooden trestle, don&rsquo;t forget to look at the window as this is the best spot to view the ocean. From December to April, you may even spot a few humpback whales - a common sight in the warm Pacific waters.</p>
                <p>Over 5 million tourists have boarded the Sugar Cane Train since 1970. The train is also used for occasional parties and receptions, and many locals get on just to hang out or get to the Kaanapali resorts. Although the sugar mill it once served has long gone out of business, there are no signs that this train will go the same route!</p>
                
