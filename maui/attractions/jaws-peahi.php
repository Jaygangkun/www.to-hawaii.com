                <h2 class="small">Jaws (Peahi), Maui</h2>
                <p>Amongst rolling fields of sugarcane surfers migrate to the north shore of Maui for what is arguably the best surf in the world. It&rsquo;s true that there are a few other great surf spots on earth that boast similar wave heights, but Jaws is known for its excellent wave forming quality. The reef here is shaped in a way that magnifies the incoming waves and produces clean right and left-directional waves with huge barreling (hollow, air-tube interiors) sections. The waves at Jaws can reach heights of up to 70 ft (21 m)! </p>
                <p>The swells travel across the Pacific from an Alaskan Aleutian island chain in the northern part of the Bering Sea. Unblocked by landfall, the swells arrive at Jaws with an unmerciful bite. These enormous waves can reach speeds of up to 30 miles per hour (48 km/h). </p>
                <? include ("../../theme/text.html") ?> 
                <p>Peahi means &ldquo;beckon&rdquo; in the Hawaiian language and is the Hawaiian name for this surf break, but it is more commonly known as Jaws and got that name due to the size and ferocity of the swells. Surfers attempt to conquer Jaws just as the citizens of Amity Island attempted to defeat the threatening shark in Jaws, a 1975 film directed by Stephen Spielberg. </p>
                <p>The waves at Jaws are mostly surfed in the winter months (November to March) and break just a few times a year. Monumental breaks occur once every couple of years, but when the time comes, the beach is as flooded with people as the ocean itself. There are often times cameras pointing at surfers from both the beach and from helicopters above. </p>
                <p>Historically, Jaws is known for the introduction of &ldquo;tow-in&rdquo; surfing, a surfing technique in which the surfer is towed in by a rope connected to either a helicopter or a speedboat (or Jet Ski) in order to place the surfer in the right location of the waves. Due to the size and speed of the waves here, it is impossible for a surfer to traditionally enter the waves by paddling from shore. </p>
                <p>Laird Hamilton and Dave Kalama, two famous surfers of the area, are known for their innovations to this new technique of surfing. Some find tow-in surfing offensive as it takes away from the natural vibe that surfing is all about. However, if one is to conquer the rage of Jaws, tow-in surfing is the safest and most proficient way to go about the feat.</p>
                <p>With an increase of spectators and surfers, surfing here is becoming more and more dangerous. Too many people in the water can create a hazardous environment for the surfers and an increase in injuries. </p>
                <p>On an ordinary day when the water is calm, there isn&rsquo;t much to see here. But when the surf is way up, scores of people rush here to get a good vantage point on the cliff overlooking the bay. The space is tight here so some people in the past drove over the nearby pineapple fields to park their car, which angered the landowners. As of 2006, many of the paths to Peahi Bluff have been purposefully blocked with derelict cars set on fire by those angered by the increase in the overpopulated beach line. </p>
                <p>Post the winter of 2004, professional surfers also became angered by the masses of speculators and inexperienced surfers that were attempting to surf here. It is implied that surfers are only welcome at Jaws if they have an invitation from a veteran tow-in surfer who is locally experienced.</p>
                
