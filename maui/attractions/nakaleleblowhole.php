                <h2 class="small">Nakalele Blowhole, Maui</h2>
                <p>The Nakalele Blowhole is located near the northernmost tip on Maui. A blowhole is created when there are tubes in the rocks under water that were created by ancient lava flows. With each wave, water gets pressed into these tubes and shoots up into the air. The stronger the waves, the higher the water will shoot into the air. The tides also change how high the blowhole is blowing (during high tide it is more).</p>
                <p>This blowhole can be seen from the road or trailhead when there is enough wave action. If you decide to hike down to it, be sure to wear sturdy shoes and stay away from the blowhole area. Going too close to a blowhole can lead to serious injury or death. It may be a tempting idea to have a closer look at the blowhole and play in its spray, but this is very dangerous. Large waves can unexpectedly occur, sweep you off your feet and suck you into the blowhole. </p>
