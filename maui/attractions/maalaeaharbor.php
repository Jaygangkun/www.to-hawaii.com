                <h2 class="small">Ma'alaea Harbor, Maui</h2>
                <p>Ma'alaea Harbor (<a href="../../maui/attractions/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is conveniently located half way between West Maui and South Maui. This is where many tour boats and cruises depart from, such as snorkeling and scuba diving tours to <a href="../../maui/attractions/molokini.php">Molokini</a> or other nearby destinations and dinner cruises. It is the largest harbor on this side of the island. It' a place worth visiting even if you're not a boat passenger. There are many shops and restaurants located here as well, including the Ma'alaea Harbor Shops, the Pacific Whale Foundation and the <a href="../../maui/attractions/maui-ocean-center.php">Maui Ocean Center</a>.</p>
