                <h2 class="small">Lahaina Banyan Tree Park, Maui</h2>
                <p>Located right on Front Street in Lahaina on Maui is this amazing banyan tree. Imported from India, it was planted in April 1873 and measured just 8 feet (2.5 m) in height back then. Today, it is over 60 feet (18 m) high and looks like a mini forest with several tree trunks. </p>
                
                <p>The amazing thing about banyan trees is that they also grow sideways. Roots grow down from the tree trunk until they reach the ground and form a new trunk. The banyan tree in Lahaina now has 12 major trunks, in addition to its huge core. The tree covers an area of 200 feet (61 m) and shades two-third of an acre. </p>
                <p>So even though it looks like there are at least a dozen trees in this park, it&rsquo;s just one single tree. The Lahaina banyan tree is one of the largest in the world. Below it are several benches where one can relax. Often times arts and crafts demonstrations take place here.</p>
