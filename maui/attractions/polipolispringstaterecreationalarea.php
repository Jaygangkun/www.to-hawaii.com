            <h1><span itemprop="name">Polipoli Spring State Recreational Area (Polipoli State Park), Maui</span></h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <p>Located almost 10 miles (16 km) upland from the residential areas of the Kula district of Maui, you will need a four-wheel drive vehicle just to access this mountain hideaway. However, the drive itself, whilst not for the faint-hearted, features beautiful scenery.</p>
                <p>                  Polipoli Spring State Recreational Area has four hiking trails: Plum Trail, Haleakala Ridge Trail, Polipoli Trail and the Redwood Trail. Only the Plum Trail doesn't begin from the recreational area itself, and all four trails eventually link to form a loop of 3.5 miles (5.6 km). </p>
                
                <p>Stunning scenery accompanies hikers on whichever of the routes they choose. All four trails sit at around 6,000 feet (1,829 m) above sea level and lead through a variety of forests. Pines, eucalyptus, cypress, cedars, ash, redwood and sugi trees are all visible from the trails. Plum trees bearing delicious fruit can also be found along the Plum Trail.</p>
                <p>
                  Of the four, it is the Redwood Trail which has the greatest elevation gain; it begins from the high point of 6,200 feet (1,890 m) at the recreational area and drops 900 feet (274 m) through the course of the walk. The Redwood Trail is the only one of the four which permits access to mountain bikers. The others are strictly for hikers only, and all four are considered to be of moderate difficulty.</p>
                <p>                  Polipoli Spring State Recreational Area also provides camping and lodging facilities (one lodge is available). Picnic tables and restrooms are provided in various parts of the park. In season, pig and bird hunting is also possible.</p>
                <p>                  Due to the elevation, nights can get cold at Polipoli Spring State Recreational Area. Also, the lodging facilities do not have electricity. Flashlights are a must as candles are not permitted.</p>
