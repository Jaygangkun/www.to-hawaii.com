                <h2 class="small">Iao Valley State Park, Maui</h2>
                <p>The Iao Valley State Park (<a href="../../maui/attractions/more_iaovalley_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../maui/attractions/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located near <a href="../../maui/cities/wailuku.php">Wailuku</a> in Central Maui, is one of the most popular attractions on the island. A famous landmark inside the park is the Iao Needle, a 2,250-foot-tall (685 m) stone pillar, a remnant of Maui&rsquo;s volcanic past. It overlooks Iao Stream and is surrounded by the walls of Puu Kukui Crater. </p>
                
                <p>The scenery is very lush and the Iao Needle itself is covered in green. Iao Valley is the second wettest spot in Hawaii, after Mt. Waialeale on Kauai, receiving more than 400 inches (10,000 mm) or rain per year. </p>
                <? include ("../../theme/text.html") ?> 
                <p>Besides its natural beauty, the valley is historically significant because it was here in 1790 that King Kamehameha I destroyed the Maui army in the Battle of Kepaniwai to unite the Hawaiian Islands. </p>
                <p>There is a well-marked hiking path from the parking area to view Iao Needle. There&rsquo;s also a ridge-top lookout from where you can enjoy great views of the valley, its exotic plants and natural pools and see all the way to Kahului Harbor. </p>
                <p>Iao Needle is often times covered in clouds. The best time to go is early in the morning. While there, you can also visit the Hawaii Nature Center, which features interactive exhibits and is located within the valley. There are restroom facilities but no drinking water so bring some on your journey. Park hours are from 7 am to 6 pm. </p>
                
