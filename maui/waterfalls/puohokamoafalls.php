                <h2 class="small">Upper &amp; Lower Puohokamoa Falls, Maui</h2>
                <p>Upper Puohokamoa Falls is a small waterfall that is about 30 feet (9 m) high. It used to be a popular stop on the Road to Hana, drawing crowds. But since it is located on private property, it was closed to the public. However, you can get a bird's eye view of it from the  nearby Garden of Eden (which is a paid attraction that is located a half mile (0.8 km) past mile marker 10). </p>
               <? include ("../../theme/text.html") ?>
                 <p> Lower Puohokamoa Falls is further off and not many people know it's here because of its hidden location below the Hana Highway. It is a much larger waterfall, roughly 200 feet (60 m), but the trail is very steep and dangerous and closed to the public as well. However, there is a lookout point about 0.8 miles (1.3 km) after mile marker 10 (when driving toward Hana).<br />
                </p>
                <p>CAUTION: As with all freshwater streams and waterfalls in Hawaii, Leptospirosis bacteria may be present in the water. Do not drink the water and keep water out of open wounds (including small scratches) and eyes. <br />
                </p>
                
