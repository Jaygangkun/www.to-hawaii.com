                <h2 class="small">Haipua'ena Falls, Maui</h2>
                <p>Haipuaena Falls is a relatively small, but picturesque waterfall located on the Road to Hana (Hana Highway). Because it can't be seen from the road, many people don't know it's here and pass by it. </p>
                
                <p>The waterfall is nestled among rocky cliffs and greenery, along with a beautiful pool at its base.  Wild ginger grows along the trail and ferns grow on the rock wall behind the waterfall, creating an idyllic atmosphere. However, because of the cliffs, it is advised to not stand under the waterfall as there is a risk of falling rocks and other debris. </p>
                <p>The hike to the falls is only a short trail (about 100 yards &ndash; 90 m) on the Hana side of the bridge that leads to the lower falls and pool. There is also another larger waterfall upstream, but the hike can be very slippery, even on dry days.</p>
                <p>CAUTION: As with all freshwater streams and waterfalls in Hawaii, Leptospirosis bacteria may be present in the water. Do not drink the water and keep water out of open wounds (including small scratches) and eyes. </p>
