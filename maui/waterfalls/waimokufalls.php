<h2 class="small">Waimoku Falls, Maui</h2>
<p>Waimoku Falls (<a href="../../maui/waterfalls/more_waimokufalls_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is one of Maui's tallest waterfalls, dropping 400 feet (122 m) down a vertical cliff into a shallow pool. It is accessible via the 1.8-mile (2.9 km) Pipiwai Trail, which begins near the <a href="../../maui/attractions/oheogulch.php">Oheo Gulch</a> (also known as Seven Sacred Pools). Along the way about half a mile (800 m) into the trail, there is another waterfall, Makahiku Falls, which is also beautiful, even though it is &quot;only&quot; a 185-foot (56 m) cascade.</p>
<? include ("../../theme/text.html") ?>
<p>Swimming in the small pool below Waimoku Falls is not possible since it is too shallow. Also, it is dangerous to stand right under the waterfall because of the danger of falling rocks and debris. The amount of water gushing down the waterfall depends on recent weather conditions. If it has rained, the waterfall is much larger than during dry weather.</p>
<p>The Pipiwai Trail is one of the best hikes on the island. It leads along the Pipiwai Stream and passes through a beautiful bamboo forest and lush vegetation, including ferns, mango, guava and banyan trees.</p>
<p>CAUTION: As with all freshwater streams and waterfalls in Hawaii,   Leptospirosis bacteria may be present in the water. Do not drink the   water and keep water out of open wounds (including small scratches) and eyes. Also, beware of rockfalls in this area. In 2011 a rockfall killed an eight-year-old girl near this waterfall.</p>

<h2 class="big">Waimoku Falls Overview</h2>
<ul type="disc" class="mylist">
<li>Hike to Waimoku Falls is about a 4-mile (6.4 km) roundtrip (about 2.5 to 5 hours)</li>
<li>  Elevation gain of trail is 650 feet (198 m)</li>
<li>  Be careful when crossing streams on the way (flash floods can occur suddenly and have swept people away in the past)</li>
<li>  Don't hike this trail during rainy weather or if rain is in the forecast</li>
<li>  Upper section of trail can be muddy &ndash; boardwalks have been placed in the worst sections</li>
<li>Beware of rockfalls along the Pipiwai Trail and near the waterfall<br />
</li>
</ul><div style="clear:both"></div>
