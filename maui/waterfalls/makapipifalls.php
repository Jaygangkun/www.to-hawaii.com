                <h2 class="small">Makapipi Falls, Maui</h2>
                <p>Located along the popular Road to Hana, Makapipi Falls is an easily accessible waterfall. There are many waterfalls in this part of Maui, but what makes this one special is that you'll be able to stand right above it, which makes for a unique vantage point. </p>
                <p>Located just past mile marker 25 (when coming from Kahului), look for the bridge that crosses Makapipi Stream. You'll have to park behind the bridge on the side of the road and walk back to the bridge. On the ocean side of it, look down and you'll be directly above the waterfall. One disadvantage of Makapipi Falls is that it is seasonal and if there hasn't been sufficient rainfall, it is dry and you won't be able to see anything.</p>
                <p>CAUTION: As with all freshwater streams and waterfalls in Hawaii,   Leptospirosis bacteria may be present in the water. Do not drink the   water and keep water out of open wounds (including small scratches) and eyes.</p>
