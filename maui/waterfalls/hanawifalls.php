                <h2 class="small">Hanawi Falls, Maui</h2>
                <p>This Hanawi Falls is a popular stop on the Road to Hana on Maui&rsquo;s northern shoreline. But what most visitors see of this waterfall is actually known as Upper Hanawi Falls. Many people stop right here at the bridge that was built across Hanawi Stream. The waterfall is a 30-foot (9 m) cascade. It plunges down from the cliffs into a small pool. During or after heavy rainfall, the waterfall is much larger. </p>
                <p>The other part of Hanawi Falls is downhill from here. This part most people never get to see. It&rsquo;s a 200-foot (61 m) cascade. It can be accessed from the town of Nahiku via a trail. However, the trail passes through land that is leased by the East Maui Irrigation company, and they don&rsquo;t grant hiking permits to Hanawi Falls.</p>
                <? include ("../../theme/text.html") ?>
                <p>So the best and safest way to view Upper Hanawi Falls is from the bridge. Some people hike down to the pool area, but this is not advised. The hike down there can be slippery and rock slides or flash flooding can occur without warning.</p>
                
