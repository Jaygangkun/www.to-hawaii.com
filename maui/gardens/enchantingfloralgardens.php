                <h2 class="small">Enchanting Floral Gardens, Maui</h2>
                <strong>Permanently Closed</strong>
                <p>Located at an elevation of 2,300 feet (700 m) on the slopes of <a href="../attractions/haleakala-national-park.php">Haleakala</a>, this 8-acre botanical garden is a must-see for botany enthusiasts visiting Maui. The gardens feature an impressive 1,500 tropical and semi-tropical plant species, as well as many exotic birds, insects and other animals. Enchanting Floral Gardens make for a nice stop on your way to or from Haleakala.<br />
                  <br />
                  The gardens are home to flower and plant species from around the world, including many iconic Hawaiian flowers such as hibiscus, orchids, jade vines, plumerias, ferns, jacarandas, fruit trees, proteas and many more. Don't miss the impressive Hong Kong orchid tree, shell ginger stands and wood board figures for taking fun photos. </p><? include ("../../theme/text.html") ?> 
                <p>You can choose from many different paths that meander through the floral gardens, and you'll be treated to beautiful views of upcountry Kula.</p>
