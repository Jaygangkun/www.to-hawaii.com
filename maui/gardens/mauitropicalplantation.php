                <h2 class="small">Maui Tropical Plantation, Maui</h2>
                <p>Discover and Celebrate Maui's Agricultural Bounty.</p>
                <p>Maui Tropical Plantation is a place steeped in history. The intersection between Maui's past and future 
                  provide an alluring atmosphere for guests. The Plantation has something to offer the entire family as it is<br />
                home to a variety of experiences and there is no entrance fee.</p>
                <p>Nestled against the Waikapu Valley and greater West Maui Mountains, the views from the Plantation are
                  stunning. The inner property is host to a range of experiences while the larger acreage is part of active, 
                farming operations.</p>
                <p>The Plantation can be explored by zip line, tram, or on foot. The Maui Zipline Company operates a series 
                  of five, family-friendly ziplines. A Plantation Tour, complete with a coconut-husking demonstration,<br />
                  showcases tropical fruits, nuts, flowers and more. Visitors are welcome to walk the grounds at their own 
                pace, discovering colorful tropical flowers amidst a canopy of mature trees.</p>
                <p>Discover the perfect gift at the Plantation Store and other retail shops on the property. Indulge in Maui 
                  artisan works at Soley Aloha Boutique and Gallery.</p>
                <p>Pieces from Maui's past are uniquely displayed at the Plantation. The Claus Spreckels, a historic steam 
                  locomotive named after a Maui sugar baron, and the six-passenger Kalakaua Car formerly used by<br />
                Hawaiian Railroad Company are on display as part of the Mill House Restaurant &amp; Bar. The Plantation                grounds creatively display reclaimed pieces from Maui's now closed sugarcane mills.</p>
                <p>The Mill House sits at the heart of Maui Tropical Plantation, surrounded by farmland, against the 
                  breathtaking Waikapu Valley. The Mill House kitchen and bar offers an innovative approach to local<br />
                ingredients.</p>
                <p>Their many tropical venues provide flexibility in event planning and design. Their in-house Event 
                  Coordination team will work to ensure your event is a success.</p>
                <p><strong>Hours: </strong><br />
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 09:00-17:00">The Plantation grounds are open daily from 7 am until 9 pm</time>
                </p>
                <p><strong>Admission:</strong><br />
                  Tropical Express Tour: Adults $20, Children (ages 3-12) $10<br />
                  Tropical Express Tour departs daily at 10 am, 11 am, 12 pm, 1 pm, 2 pm, 3 pm and 4 pm.<br />
                </p>
                
