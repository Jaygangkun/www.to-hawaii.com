                <h2 class="small">Ke'anae Arboretum, Maui</h2>
                <p>Located alongside Pi'ina'au Stream on Maui's <a href="../scenicdrives/road-to-hana.php">Hana Highway</a>, the Ke'anae Aboretum boasts six acres of botanical gardens and forested land. This historic arboretum occupies leveled terraces built hundreds of years ago for the cultivation of taro.  Here visitors get an opportunity to check out native and introduced plants in a quiet, natural setting. </p>
                
                <p>                  The site features a number of different hiking trail options that take visitors on a tour of the lush vegetation found here. Ornamental, fruit and timber trees from the world bear labels with their scientific name, common name and country of origin. More than 150 varieties of tropical plants can be found here, including taro, ginger, papaya and hibiscus. The area's humid, warm climate creates the perfect growing conditions for these spectacular plants. </p>
                <? include ("../../theme/text.html") ?> 
                <p>                  When visiting the arboretum it's a good idea to bring insect repellent, as mosquitoes can be quite invasive at certain times of the year. Visitors are prohibited from picking plants and flowers. Accessing any unmarked trail or access road is also prohibited, as many of these are part of private property.</p>
                <p><strong>Hours:<br />
