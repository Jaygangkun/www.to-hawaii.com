                <h2 class="small">Maui Nui Botanical Gardens, Maui</h2>
                <p>This seven-acre botanical garden in <a href="../cities/kahului.php">Kahului</a> is home to an impressive collection of native and Polynesian-introduced plants, many of which were important crops throughout early Hawaiian history. The organization strives to educate the public about the importance of preserving native plant life and culture in order to better understand the relationship between the two. The Maui Nui Botanical Gardens are a must-see for plant lovers and conservationists who want to learn more about Hawaii's rich flora and native culture. </p>
                <? include ("../../theme/text.html") ?> 
                <p>                  Established in 1976 by native Hawaiian botanist Rene Sylva, the Maui Nui Botanical Gardens have become one of Maui's most cherished gardens. Maui Nui is much more than a tourist site, though it does welcome thousands of visitors each year. The organization works closely with others like the Hawaii Rare Plant Recovery Group and the Maui Invasive Species Committee to support research and conservation efforts of native Hawaiian plants. The garden hosts workshops in the historic use of  native plants to wilderness restoration projects and sells Hawaiian plants to local gardens. </p>
                <p>                  Here you'll find over 40 sugarcane varieties, native poppies, variegated (striped) bananas, wild hibiscus, 15 varieties of sweet potato and 70 different taro varieties. Maui Nui also grows anapanapa, a native plant that creates a natural shampoo when you rub it between your hands. The site also includes a large greenhouse, picnic tables, restrooms and a playground.</p>
                <p>                  Visitors can either do a self-guided tour or join a docent-led group tour to learn about  Hawaii's natural history and the ethnobotanical uses of different native plants. A variety of merchandise is available in the gift shop, including greeting cards, books and souvenir T-shirts.</p>
