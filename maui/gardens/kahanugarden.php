                <h2 class="small">Kahanu Garden, Maui</h2>
                <p>Located near <a href="../../maui/cities/hana.php">Hana</a> and directly on a gorgeous stretch of coastline, the Kahanu Garden is a 500 acre (2 km&sup2;) natural sanctuary that's well worth a visit. The garden is part of the National Tropical Botanical Garden system and features many tropical plants, including 130 varieties of breadfruit. </p>
                
                <p>In fact, Kahana Garden has the largest collection of this important Pacific fruit crop in the world. Another separate garden named the Canoe Garden features useful plants that the early Polynesian settlers brought with them to Hawaii.</p>
                <p>Kahanu Garden is also home to the centuries old Piilanihale heiau, which is one of the largest Hawaiian stone temples. It was built over 500 years ago and was a place of worship in ancient Hawaii.</p>
