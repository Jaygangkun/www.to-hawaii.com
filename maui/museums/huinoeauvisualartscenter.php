                <h2 class="small">Hui No'eau Visual Arts Center, Maui</h2>
                <p>The Hui  No'eau Visual Arts Center, located at the Kaluanui estate in <a href="../cities/makawao.php">Makawao</a>, is  dedicated to the education and learning in the arts. The visual arts center is  located on the historic estate of Harry and Ethel Baldwin, one of Maui's most  prominent families in the pineapple industry. Ethel Baldwin founded the Hui  No'eau Visual Arts Society in 1934. Since then, her dedication to the arts has  been a draw for artists from around the world.</p>
                <? include ("../../theme/text.html") ?>
                <p> The  main house and mansion of the estate, built in 1917, is the main gallery of the Hui No'eau  Center. It  also houses the studios for computers and drawing, as well as a dark room for  their photography programs. The rest of the studios, including the children's  studio, ceramics studio and reflecting pool are scattered across the estate.  Visitors are welcome to take a free self-guided tour of  this former pineapple plantation estate, botanical gardens and its galleries. </p>
                <p> The Hui  No'eau Center features art exhibitions and holds exhibits year-round. The  center's &quot;Art Affair,&quot; an annual fundraising event, takes place at the gallery  and features well-known artists, a live and a silent auction and a  black-and-white ball for attendees. These yearly fundraisers and auctions help  support the programs and classes held at Hui No'eau.</p>
                <p> Adult  programs and lectures are offered in a variety of disciplines. Studios and open  studios for photography, print making, painting and more are taught in a  variety of workshops for professional artists and novices alike. After  fulfilling the prerequisites for the basic studio classes, students and artists  are welcome to take part in the art center's open studio classes in  photography, ceramics, painting, drawing, print making and jewelry-making. </p>
                <p> The  center also offers programs for children from the ages of 3 to 18  years old who are interested in exploring the visual arts in painting,  photography, ceramics, sculptures, jewelry-making and more.</p>
