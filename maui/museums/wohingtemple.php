                <h2 class="small">Wo Hing Temple, Maui</h2>
                <p>Chinese immigrants were one of the ethnic groups who were brought to Hawaii to work in the sugarcane fields. In 1909, a few Chinese workers formed the Wo Hing society (which is a chapter of the Chee Kun Tong society), who in 1912 built this temple for the Chinese community. </p>
                <p>Today, the temple is on the National Register of Historic Places. It features artifacts and displays on the history of the Chinese in <a href="../cities/lahaina.php">Lahaina</a>. The old cookhouse next door has a theater, where movies of Hawaii taken by Thomas Edison in 1898 and 1903 can be seen. </p>
