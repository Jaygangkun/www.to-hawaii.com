                <h2 class="small">Alexander & Baldwin Sugar Museum, Maui</h2>
                <p>The Alexander and Baldwin Sugar Museum on Maui celebrates the historic sugar trade that kept up Hawaii's economy for most of the 19th century. A&amp;B, as it is known to locals, contains a museum and several artifacts detailing the history of the trade, the local geography and the evolving culture of Maui. At over 1,800 acres, A&amp;B is now the largest landowner on the island and brings in a large share of tourism and trade revenue.</p>
                <? include ("../../theme/text.html") ?>                
                <p>The museum itself is located in the manager's house, a small restored structure located next to the local post office. Here you'll find relics and pictures of the old plantation, including mills, bells and scale models of large machinery. Some of the models are still functional - you can even watch a narrative demo of how it works, complete with lighting and sound effects. </p>
                <p>Six rooms make up the main museum: the Geography Room, Water Room, Human Resources Room, Plantation Room, Fieldwork Room and Mill Room. Most tours start at the Plantation Room, which offers a large exhibit of the local livelihood. Some of the highlights are the old household equipment, religious artifacts and a scale model of the camp house used by the workers.</p>
                <p>The A&amp;B Sugar Museum is located in Puunene, a town known for its thriving sugar trade. It is about a 10-minute drive from the Kahului Airport and is right next to the largest Hawaiian sugar factory in existence. </p>
