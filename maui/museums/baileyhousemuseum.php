                <h2 class="small">Bailey House Museum, Maui</h2>
                <p>The Bailey House Museum was built in 1833 and showcases Hawaiian culture, paintings, artifacts and furnishings from 19th century Maui. It is located in <a href="../cities/wailuku.php">Wailuku</a> and was built on the site of the royal compound of Kahekili, who was the last ruling chief of Maui. </p>
                <p>The house was the mission station for the Wailuku Female Seminary for Girls until 1847. It later became the home of Edward Bailey and his family until 1888. The museum grounds feature gardens with native Hawaiian and missionary-era plants. Also, there are paintings by Edward Bailey. In the museum shop one can find many Hawaiian items and gifts.</p>
