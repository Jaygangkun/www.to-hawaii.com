                <h2 class="small">Hawaiian Islands Humpback Whale National Marine Sanctuary Visitor Center, Maui</h2>
                <p>Each year in the winter months (November to March), thousands of whales migrate from Alaska to the warm waters around Hawaii to mate, birth and raise their young. During this time, whales can often times be seen from the shoreline on all Hawaiian islands. </p>
                
                <p>To protect the whales and their natural habitat, Congress established the Humpback Whale National Marine Sanctuary in 1992. It encompasses the shallow (less than 600 feet) waters surrounding the main Hawaiian Islands and constitues one of the world's most important habitats for humpback whales. </p>
                <? include ("../../theme/text.html") ?>
                                <p>If you're interested in learning more about the Pacific humpback whales, Maui is home to the headquarters of the Hawaiian Islands Humpback Whale National Marine Sanctuary. At the Sanctuary Education and Learning Center's beachfront facility in Kihei, exhibits and artifacts offer information about the lives of the whales. They highlight many aspects of the humpback whales from both a scientific and a cultural standpoint. A marine science library is located here as well, offering material for adults as well as children.<br />
                  <br />
                  The center's outreach and education programs promote awareness among visitors and residents of the area. These projects include workshops, lectures, whale watches from the shoreline and volunteering. Each Tuesday and Thursday at 11 am, the center hosts the 45-ton Whale Talk, where visitors can learn about whale anatomy and behavior.</p>
