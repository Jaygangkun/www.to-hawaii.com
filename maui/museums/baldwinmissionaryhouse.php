                <h2 class="small">Baldwin Missionary House, Maui</h2>
                <p>The oldest building in Lahaina, the Baldwin Missionary House on Front Street has been beautifully restored and offers a vivid picture of the life of a missionary and his family on Maui in the early 1800s.</p>
                
                <p>Rev. Dwight Baldwin, a pastor and physician from New England, arrived in Hawaii with his wife in 1830 and moved into the missionary home eight years later. As pastor of the area's old Wainee Church, he was an important presence in the community for almost 40 years. His family founded many successful businesses in the Islands, including Alexander &amp; Baldwin, one of the five major companies in the 20th century and still one of Hawaii's largest landowners.</p>
                <? include ("../../theme/text.html") ?>
                <p>The historic two-story building is made of hand-hewn timber, stone and coral and served as both a center for missionary activities and also a medical clinic with an impressive garden of local and exotic plants and fruit trees. The family's grape vines still grow on trellises along the side of the house.&nbsp;</p>
                <p>For an atmospheric experience, candlelit tours are available on Friday evenings from 6 pm to 9 pm. Visitors can see personal and household items - including original antique furnishings - which belonged to the Baldwin family.<br />
