                <p>The Maui Arts &amp; Cultural Center (MACC) opened in 1994 and has since then brought about 1,800 events to the community in Maui. As a non-profit center, MACC strives to bring the community of Maui closer together with a place to gather and celebrate, a venue for them to enjoy music and theater and a place to serve as an educational resource for people of all ages in the community.</p>
                
                <p>From $32 million dollars in contributions, the MACC became a fixture in the community. The center serves over 26,000 people annually through its special events productions, arts education and cultural programs.</p>
                
                <p>Just a few of the ongoing events and cultural programs are very representative of the Hawaiian culture, like the Ku Mai Ka Hula: &quot;Hula Appears.&quot; This dance competition and event between dancers from Hawaii and Japan celebrates their historic friendship and lets viewers experience the different styles of hula.</p>
                <p>Another ongoing event at the MACC alongside dance events and art gallery expositions is another form of Hawaiian art &ndash; storytelling. The MACC provides workshops for people of all ages into the art and joys of storytelling &mdash; captivating its audiences with the folklore and legends of Hawaii.</p>
                <p>Special musical events are featured in the MACC's Castle Theater. Musical events, such as concerts from Ladysmith Black Mumbazo, Elton John and orchestra events by the Maui Pop Orchestra take place at the MACC. </p>
                <p>The gallery itself offers educational tours for elementary school children to college groups and even organized community groups. They provide the viewers with background information on the exhibit as well as engage the guests in observations and discussion over the works in the gallery.</p>
