                <h2 class="small">Hana Coast Gallery, Maui</h2>
                <p>The Hana Coast Gallery, located in <a href="../../maui/cities/hana.php">Hana</a> on the island of Maui right next to the Travaasa Hana Hotel, showcases original fine art, finely crafted handwork and decorative collectibles from the Hawaiian Islands, Polynesia and the Pacific Rim. The gallery has been named &quot;by far the best fine art and craft gallery on Maui&quot; in the <em>Travel + Leisure</em> magazine's Best in the World Ratings.</p>
                <? include ("../../theme/text.html") ?>
                <p>One wouldn't think that one of America's most respected cultural art galleries is located in the middle of &quot;nowhere.&quot; The Hawaiian Islands, including Maui, are about 2,500 miles away from the continental United States. And on Maui, the little town of Hana is also quite difficult to reach and definitely off the beaten path.</p>
                <p>But even though one may think that this is hardly a good place to showcase fine art, the quaint village of Hana is actually a great place for beauty and artistic craftsmanship. It is often called the &quot;Most Hawaiian Place&quot; for a reason.</p>
