                <h2 class="small">Whalers Village Museum, Maui</h2>
                <strong style="color:#F00;">Permanently closed</strong>
                <p>Some 200 years ago, <a href="../cities/lahaina.php">Lahaina</a> was a bustling port town and the whaling capital of the world. Today, whaling is long out of business and the town has found other industries, but the trade is immortalized in one of Lahaina&rsquo;s biggest attractions: the Whalers Village Museum. Part of an upscale shopping center of the same name, the museum celebrates Lahaina's seafaring life from the 19th century and the way it influenced Maui's culture.</p>
                                <p>Over 70 species of whales can be found at the museum, including authentic specimens of the Hawaiian state mammal, the humpback whale. Most visitors, however, are awed by the 40-foot (12 m) sperm whale, whose skeleton is dramatically displayed at the center. Other attractions include an authentic, fully-equipped whale boat and a recreation of the ship's forecastle, which showcases the life of whalers in the old days.</p>
                                <? include ("../../theme/text.html") ?>
                <p>The center offers a self-guided tour in English, Spanish, German and Japanese, as well as a gift shop offering various artifacts and souvenirs. Across the hall is the Hale Kohola (&quot;House of Whales&quot;), where you'll find more whale exhibits as well as educational artifacts about the history of the whaling trade. Families often visit the rest of the Whalers Village Mall after touring the museum, which is on the mezzanine floor.<br />
