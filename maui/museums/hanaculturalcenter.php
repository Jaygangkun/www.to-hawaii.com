            <div class="new-custom">
              <div class="midpanel_row">
<p>A trip to the Hana Cultural Center is like taking a trip back into Hawaii's past. This museum and information center located in Hana is home to a historic courthouse, the Kauhale Village, a replica of an ancient chief's compound, and ancient artifacts of Hawaiian history. The Hana Cultural Center was established by the wise elders of Hana, known in Hawaiian culture as the kupuna, as a way of preserving Hana's history and way of life and to commemorate the Hawaiian cultural renaissance. </p>
<p><a href="../cities/hana.php">Hana</a> is considered one of Hawaii's most isolated and traditional towns. The museum holds artifacts that tell many of the town's stories. The Hawaiian heirlooms inherited and collected by the cultural center include such items as stone axes called ko'i, poi boards, quilts, carved wooden koa bowls, fish nets and photographs of Hana. All these and more can be viewed at this historic location that is listed on the National Register of Historic Places. Even the Hana Court House on the grounds of the cultural center and replica village is still in use once a month by a local judge and can be visited.</p>
