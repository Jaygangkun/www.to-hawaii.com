                <p>Makena (<a href="../../maui/cities/p3.html" onclick="NewWindow(this.href,'name','2550','650','yes');return false">view panorama</a>) is a small community on Maui's south shore, located south of Kihei and Wailea. It is as far south as one can go by car before the road ends. Makena itself has a population of 99, according to the 2010 Census. It is mainly a resort area, with several hotels and vacation rentals. <a href="../../maui/attractions/makenastatepark.php">Makena State Park</a> and several nice beach parks make up a large portion of Makena, including <a href="../../maui/beaches/bigbeach.php">Big Beach</a> (also known as Makena Beach), <a href="../../maui/beaches/littlebeach.php">Little Beach</a> and <a href="../../maui/beaches/maluakabeach.php">Maluaka Beach</a>, among others.</p>
