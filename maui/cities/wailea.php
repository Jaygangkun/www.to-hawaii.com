                <h2 class="small">Wailea, Maui</h2>
                <p>Wailea is a 1,500-acre master-planned resort community located at the base of Mt. Haleakala on the south shore of Maui. It is an upscale area with high-end resorts, luxury condominiums, private estates and spa facilities. It was named one of the 99 Best Recreational &amp; Residential Private Communities in America. </p>
                <p>The town boasts five crescent-shaped beaches along its 1.5-mile shoreline, including <a href="../beaches/waileabeach.php">Wailea Beach</a>, which was named &quot;America's Best Beach&quot; by Dr. Stephen &quot;Dr. Beach&quot; Leatherman in 1999. The Wailea Blue, Wailea Gold and Wailea Emerald 18-hole golf courses have also been consistently ranked among the finest in the country.</p>
