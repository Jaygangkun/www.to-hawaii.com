                <h2 class="small">Wailuku, Maui</h2>
                <p>Located at the base of the West Maui Volcano at an elevation of 249 feet (76 m), Wailuku was once the center of power and the home of great Hawaiian chiefs. In 1790, it was the site of the victory of King Kamehameha's army from the Big Island and the defeat of Maui's King Kahekeli II's army in the battle of Kepaniwai at <a href="../attractions/iaovalleystatepark.php">Iao Valley</a>. As a result of the clash, many people died, their bodies clogging up Wailuku Stream.  Kepaniwai literally means &quot;the water dam.&quot; The place of the battle was then named Wailuku, which means &quot;water of destruction.&quot; </p>
                <? include ("../../theme/text.html") ?> 
                <p>In the mid-1800s, Christian missionaries started to arrive and settle in Wailuku, bringing with them their religious beliefs, culture and agricultural methods. Some local Hawaiian royalties helped propagate Christian beliefs, including King Kamehameha I's wife, Queen Ka'ahumanu. </p>
                <p>In 1876, the Ka'ahumanu Church was built in her honor. The church stands next to the home of Edward Bailey, the missionary and artist who designed the church. Today, the <a href="../museums/baileyhousemuseum.php">Bailey House</a> serves as a museum, showcasing artifacts from the old Hawaii and its missionary days and also artworks of Bailey. </p>
                <p>Truly a place of interesting historical events, Wailuku is also the birthplace of Hawaii's sugar industry. By the 1860s, the town was bustling with sugar production. In 1905, Wailuku was designated as the county seat of Maui County. </p>
                <p>Today, Wailuku is an interesting place to visit to learn about a piece of Maui's past. The town's houses and structures are architectural bearers of Wailuku's New England and Art Deco heritage.</p>
                
