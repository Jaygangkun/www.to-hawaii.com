                <h2 class="small">Napili, Maui</h2>
                <p>Napili (<a href="../../maui/cities/more_napili_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a beach town on the northwestern shore of Maui, situated between two other resort towns: <a href="../cities/kaanapali.php">Ka'anapali</a> and <a href="../cities/kapalua.php">Kapalua</a>. Napili is a prime tourist destination. Hotels and condos frame beautiful <a href="../beaches/napilibaybeach.php">Napili Bay</a>, which offers good swimming and snorkeling conditions when the ocean is calm. Located nearby are two world-class golf courses, the Bay Course and the Plantation Course, which were named the #1 golf course resort in Hawaii by <em>Travel + Leisure Golf</em> magazine.</p>
