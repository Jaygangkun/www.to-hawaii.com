                <h2 class="small">Hana, Maui</h2>
                <p>Hana, which means &quot;labor&quot; in the Hawaiian language, is a small, isolated, tropical paradise located on the eastern tip of Maui at the end of the popular <a href="../../maui/attractions/road-to-hana.php">Road to Hana</a> (Hana Highway). This sleepy village is characterized by lush greenery, botanical gardens and historical sites and churches. </p>
                
                <p>In 1768, Queen Ka'ahumanu (King Kamehameha's wife and, after his death, Hawaii's ruling monarch until 1832) was born here in a cave of Ka'uiki Hill. In 1849, the first sugar plantation in Hana was raised by George Wilfong, a sea captain. Wilfong hired Chinese laborers to work in his field when he was unable to hire native workers. By 1883, six other sugar plantations were already operating in Hana. </p>
                <? include ("../../theme/text.html") ?> 
                <p>In the 1930s, Paul Fagan helped revive the town when he built a 14,000-acre ranch hotel. Originally called Pu'u o Haka Hotel, it is now known as Travaasa Hana. The Fagan Memorial Cross, which provides the best vantage view of the town, was erected in his honor. The burial place of the aviator Charles Lindbergh, the Palapala Ho'omau Church, is located west of Hana.</p>
                
