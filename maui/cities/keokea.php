                <h2 class="small">Keokea, Maui</h2>
                <p>Keokea is a small community on the slopes of Mt. Haleakala in upcountry Maui. The town is located along Kula Highway about 21 miles (34 km) southeast of Kahului. Keokea is the last outpost along the Kula Highway, which then winds down to the Kipahulu and Hana coastline. Located 2,860 feet (872 m) above sea level, Keokoa offers panoramic views of <a href="../attractions/haleakala-national-park.php">Haleakala</a>. </p>
                
                <p>Keokea was originally settled by Chinese immigrants. The Hakka Chinese's influence on people and culture is evident in some town landmarks, such as the St. John's Episcopal Church (which is adorned with Chinese characters) and the Kwok Hing Society Temple. The Chinese hero and first president, Sun-Yat Sen, lived in Keokea for a while in 1911. A park was built in his honor. Sun-Yat Sen's brother, Sun Mei, was one of the many Chinese immigrants who made Keokea their home in the early 20th century. </p>
                <? include ("../../theme/text.html") ?> 
                <p>There are a few mom-and-pop stores to stock up on supplies. Grandma's Coffee House serves fresh baked goods and roasts their own coffee. Next to the coffee house is the Keokea Gallery, an art gallery featuring the works of local artists. Nearby Ulupalakua Ranch is home to Maui's only commercial winery, the Tedeschi Vineyards. </p>
                <p>Primary sources of income in Keokea come from farming, ranching and forestry. Keokea produces vegetables and flowers, which include cabbages, Kula onions, carnations and proteas. Ranches also abound in the area, with some offering horseback riding tours.</p>
                
