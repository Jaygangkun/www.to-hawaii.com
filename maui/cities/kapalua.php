                <h2 class="small">Kapalua, Maui</h2>
                <p>Kapalua is a resort community on the northwestern tip of Maui, about 6 miles (9.7 km) north of <a href="../cities/kaanapali.php">Ka'anapali</a>. A 23,000-acre resort complex developed by the Maui Land and Pineapple Company, Kapalua features upscale vacation condos and villas, shops, restaurants, golf courses and sandy beaches framed by hundred-year old Cook pines.</p>
                <p>Kapalua's beaches have been considered some of the best in Hawaii. <a href="../beaches/kapaluabay.php">Kapalua Bay</a> has earned several accolades. It has been named the &ldquo;Best Beach in the World&rdquo; by <em>Cond&eacute; Nast Traveler</em> magazine readers, the &ldquo;Best Beach in America&rdquo; by Travel Channel and &ldquo;America's #1 Beach&rdquo; on Dr. Beach's first annual list of America's best beaches. </p>
                <p>Kapalua also has world-class golf courses. Two of its golf courses, the Bay Course and the Plantation Course, were named the #1 golf course resort in Hawaii by <em>Travel + Leisure Golf </em>magazine.</p>
