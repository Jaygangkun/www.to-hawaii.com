                <h2 class="small">Ma'alaea, Maui</h2>
                <p>Ma'alaea is a small fishing village located in central Maui, in the southern part of the isthmus between the mountain ranges of Haleakala and West Maui. Ma'alaea Harbor accommodates inter-island shipping vessels, whale-watching cruises and boat charters. Snorkeling and diving excursions to the tiny offshore island of <a href="../attractions/molokini.php">Molokini</a> depart from here. </p>
                <p>Ma'alaea Bay is  a humpback whale sanctuary and it is also known to produce one of the world's fastest rideable waves during large southerly swells. The surf break is called Ma'alaea, or freight train, and breaks over a shallow coral reef.</p> 
                <p>A Ma'alaea visitor attraction is the <a href="../attractions/maui-ocean-center.php">Maui Ocean Center</a>, where a variety of marine species can be viewed up close. Along the harbor are condominiums, restaurants and shops. Nearby is the 691-acre Kealia Pond Bird Sanctuary, which is home to many native species of birds, including the Hawaiian stilt and the Hawaiian coot. </p>
