                <h2 class="small">Kula, Maui</h2>
                <p>Kula is a small community in upcountry Maui,  situated 3,000 feet (914 m) above sea level. As one of Maui's prime agricultural regions and source of farm produce, Kula grows vegetables using the dry-farming method. Some of the agricultural products of Kula include the famed sweet Maui onion, herbs, cabbages, lettuce, potatoes, strawberries and tomatoes. </p>
                
                <p>Kula, which means &ldquo;open meadows&rdquo; in the Hawaiian language, also boasts botanical gardens, including the <a href="../../maui/attractions/aliikulalavenderfarm.php">Ali'i Kula Lavender Farm</a> and the Enchanting Floral Gardens. Some of the commercially grown flowers in Kula are carnations, proteas, orchids and hibiscus. Ranching traditions, especially in slopes high above the residential areas, are also strong in Kula. </p>
                <p>A historical landmark in Kula is the Holy Ghost Catholic Church, located at 4300 Lower Kula Road. The historical octagon-shaped church was built by Portuguese plantation workers in the late 1800s and was placed on the National Register of Historic Places in 1983.</p>
