                <h2 class="small">Kipahulu, Maui</h2>
                <p>Kipahulu, which means &ldquo;fetch from exhausted gardens&rdquo; in the Hawaiian language, is a remote farming community in East Maui, about 10 miles (16 km) south of Hana. The town still lacks public electric and water utilities. Residents source water from streams. The community is accessible via <a href="../scenicdrives/piilanihighway.php">Pi'ilani Highway</a>, either from <a href="../scenicdrives/road-to-hana.php">Hana Highway</a> in the north (about 45 minutes past Hana) or from the west (Pi'ilani Highway). Both highways are narrow and curvy country roads.</p>
                <? include ("../../theme/text.html") ?> 
                <p>In the old Hawaii, Kipahulu was heavily populated. Ancient archaeological features, such as agricultural terraces and stone mounds, abound in the area. In the 1880s, the population dwindled as people moved to whaling ports like Lahaina. A former sugar town, Kipahulu was turned into a ranch-style village when the mill closed in 1923. During the 1900s, Kipahulu served as a port for an inter-island steamship company. </p>
                <p>Kipahulu is a paradise of rainforests, waterfalls, pools, mountain valleys and sweeping ocean views. Upper Kipahulu, between Hana and Kaupo, has been added to the <a href="../attractions/haleakala-national-park.php">Haleakala National Park</a> in order to preserve the natural and cultural heritage of the valley. </p>
                <p>A popular Kipahulu attraction is <a href="../attractions/oheogulch.php">Oheo Gulch</a>, also known as the Seven Sacred Pools. Several hiking trails, including the Pipiwai Trail to <a href="../waterfalls/waimokufalls.php">Waimoku Falls</a>, are located in this area. Other nearby attractions include the burial place of the American aviator Charles Lindbergh, who is buried on the grounds of Palapala Ho'omau Church, and the Laulima Farms fresh fruit stand, which uses a blender powered by a bicycle.</p>
                
