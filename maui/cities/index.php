                <p><a href="../../map-maui-towns.html" target="_blank"><img src="../../theme/images/mauitowns.jpg" alt="Maui Cities Map" align="right" border="0" style="margin-left:10px;" /></a>Maui has four major population centers: Kahului, Kihei, Lahaina and Upcountry Maui, which is the volcano-slope region of Mt. Haleakala. The former three are all located at sea level. Kahului is Maui's largest city with a population of about 26,300 people. This is where island residents go when they want to do some serious shopping as it is the only city on Maui that has department stores and several larger malls.</p>
                <p>Lahaina, on the other side of the island, is more laid-back and picturesque, with many restaurants and small stores right at the water's edge, a feature that most other towns in Hawaii surprisingly don't offer. To the north are Ka'anapali, Napili and Kapalua, which together with Lahaina are one of Maui's main tourism hubs. The other one is the Kihei and Wailea area to the south. Outside of these main residential areas are many sleepy little towns, including remote Hana and Kipahulu on Maui's east shore. Maui's total population is 144,444 (2010 Census).</p>
