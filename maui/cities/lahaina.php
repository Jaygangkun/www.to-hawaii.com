                <h2 class="small">Lahaina, Maui</h2>
                <p>Lahaina (<a href="../../maui/cities/more_lahaina_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>), the largest town in West Maui, was once the capital of the Hawaiian kingdom and seat of power of the Kamehameha dynasty. During the 1800s, Lahaina became a whaling port. </p>
                
                <p>Starting around 1819, whaling ships from around the world berthed in Lahaina for supplies and recreation. However, the sailor's unruly behavior placed them at odds with the missionaries who started to arrive in 1821. The missionaries tried to keep sailors out of the bawdy houses and Hawaiian women away from the sailors and their ships. After the whaling industry ended around 1860, Lahaina became a sugar town. </p>
                <? include ("../../theme/text.html") ?> 
                <p>Lahaina, which means &quot;merciless sun,&quot; refers to the area's hot weather, especially from May to October. Today, Lahaina's center of activity is along Front Street, which has colorful shops, art galleries and restaurants. </p>
                <p>One of the attractions in Lahaina is  the <a href="../attractions/lahainabanyantree.php">Banyan Tree Park</a>. In 1873, a banyan tree was planted here to commemorate the 50th anniversary of Christian missionary work in Lahaina. Today, this tree stretches over an area of 200 feet (61 m).</p>
                
