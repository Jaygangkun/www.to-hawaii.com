                <h2 class="small">Makawao, Maui</h2>
                <p>Makawao is an agricultural and ranching community located on the northwest slope of Mt. Haleakala at 1,578 feet (481 m) above sea level. Considered the hub of upcountry Maui, this rustic town has an atmosphere resembling the Wild West. Makawao has ranches, cowboy-themed stores, boutiques, caf&eacute;s, restaurants, steak houses and celebrates the 4th of July by hosting rodeos. Considered the art center of Hawaii, Makawao features galleries and art exhibitions, as well as art classes and workshops. </p>
                <p>Makawao's  cattle herding history runs as far back as the early 1800s. In 1793, the very first cattle were shipped from California to Hawaii as Captain George Vancouver's gift to King Kamehameha I. In 1830, King Kamehameha III brought Mexican vaqueros from Vera Cruz to Hawaii in order to rear the cattle and also to teach the locals how to herd them. The term Spaniards or Espaniolo, as the locals called the vaqueros, was shortened to &quot;paniolo&quot; and up until today, cowboys in Makawao and elsewhere in Hawaii are called paniolo.</p>
