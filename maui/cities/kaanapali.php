                <h2 class="small">Ka'anapali, Maui</h2>
                <p>Ka'anapali is a coastal town in West Maui, located about 5 miles (8 km) north of Lahaina. The town boasts beautiful <a href="../beaches/kaanapali-beach.php">Ka'anapali Beach</a>, volcanic rock formations, snorkeling spots and several upscale hotels, such as the Sheraton Maui Resort, the Hyatt Regency Maui Resort and The Westin Ka'anapali. It is one of the main tourist destinations on the island. </p>
                <p>Local  attractions include the <a href="../attractions/sugarcanetrain.php">Sugar Cane Train</a>, which goes from Ka'anapali to Lahaina, and the <a href="../shopping/kaanapaliwhalersvillage.php">Whalers Village</a> shopping center, which features many stores and restaurants, as well as the Whalers Village Museum.</p>
