                <h2 class="small">Kahului, Maui</h2>
                <p>Kahului (<a href="../../maui/cities/p1.html" onclick="NewWindow(this.href,'name','2550','750','yes');return false">view panorama</a>), Maui's largest city, is situated on the northern edge of the isthmus of West Maui and East Maui (Haleakala). In the 1850s, Henry Baldwin and Samuel Alexander purchased a large chunk of land between Makawao and Pa'ia from the Kingdom of Hawaii. The land where now Kahului is located was cultivated into a sugar plantation. </p>
                <? include ("../../theme/text.html") ?> 
                <p>In 1876, when water was scarce in the area, they constructed a 17-mile (27 km) long irrigation ditch that sourced water from Haleakaka. The flourishing of the sugar plantation led to the building of roads, houses and other industries in Kahului. </p>
                <p>Today, Kahului is Maui's commercial and travel hub. It has Maui's main airport, the Kahului Airport, and the island's only deep-draft harbor, which is always bustling with shipping activities. Kahului is also the retail center on Maui, having big retail giants like Home Depot and the only Walmart on the island. </p>
                <p>The largest shopping center in Kahului is the <a href="../shopping/kaahumanucenter.php">Queen Ka'ahumanu Center</a>, named after the famous Hawaiian queen. Cultural establishments, such as the <a href="../museums/alexanderandbaldwinsugarmuseum.php">Alexander &amp; Baldwin Sugar Museum</a> and the <a href="../museums/mauiartsandculturalcenter.php">Maui Arts &amp; Cultural Center</a>, are also located in Kahului. Natural sights include the <a href="../ancientsites/kanahapond.php">Kanaha Pond State Wildlife Sanctuary</a> and <a href="../beaches/kanahabeachpark.php">Kanaha Beach Park</a>.</p>
                
