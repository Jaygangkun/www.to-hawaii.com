                <h2 class="small">Pa'ia, Maui</h2>
                <p>Pa'ia (<a href="../../maui/cities/p2.html" onclick="NewWindow(this.href,'name','2550','650','yes');return false">view panorama</a>) is a small coastal community on the north shore of Maui. It often serves as a pit stop on the way to <a href="../cities/hana.php">Hana</a> (this is the last town you'll drive through before reaching Hana, 45 miles to the east). Pa'ia is worth visiting since it has retained a lot of its character from the the old sugar plantation days. Many of its wooden plantation-style buildings are still intact and have been converted into brightly colored surf shops, boutiques, caf&eacute;s and restaurants.</p>
                <p>Pa'ia is also known as the &quot;World Capital of Windsurfing&quot; because of nearby <a href="../beaches/hookipa-beach-park.php">Ho'okipa Beach</a>, where the windsurfing conditions are almost always excellent. There is a lookout point near mile marker #9 (Hwy 36) that's worth a stop, especially when the surf is up.</p>
