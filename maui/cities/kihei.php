                <h2 class="small">Kihei, Maui</h2>
                <p>Kihei is a beach town on the south shore of Maui. It  used to be called &quot;Kamaole,&quot; which means &quot;barren&quot; in the Hawaiian language. The original name refers to the town's hot and dry weather conditions all year round. Kihei gets less than 13 inches (330 mm) of rain per year.</p>
                
                <p>During the 1930s, efforts to establish a sugar plantation in this area did not succeed. Around that time, Kihei was home to just 350 residents. With no paved roads and only non-native kiawe growing in the area, it failed to attract visitors. Of the eleven beach lots put up for sale by the government, only six were sold. In the 1950s, farm lots could be purchased for only $225 per acre and residential lots for just 5 cents per square foot. </p>
                <? include ("../../theme/text.html") ?> 
                <p>It wasn't until around the 1960s, when water was piped from central and west Maui, that Kihei saw the start of a commercial boom. Developers took interest in Kihei and soon condominiums and shopping centers were built on almost every block. Today, with its miles of sandy beaches, Kihei is one of the top beach destinations in Hawaii. It is also one of the most densely populated places on Maui.</p>
                
