                <p>Huelo is a small community  located along the <a href="../../maui/attractions/road-to-hana.php">Road to Hana</a> (Hana Highway). It is considered part of Haiku. Located away from the population centers of Maui, such as Wailuku and Kahului, this little-known community has been described as &ldquo;the little Hawaiian village forgotten by time.&rdquo; Elevated at 480 feet (146 m) above sea level, Huelo offers a tranquil and laid-back island atmosphere and lush surroundings.</p>
                <p>Activities in Huelo include horseback riding and hiking along the Waikamoi Nature Trail. Located nearby are the black pebble beach of <a href="../beaches/honomanubayanddarksandbeach.php">Honomanu Bay</a> and <a href="../beaches/hookipa-beach-park.php">Ho'okipa Beach</a>, known as the windsurfing capital of the world. At the Door of Faith Road stands the Kaulanapueo Church, a historical Protestant Church built in 1853 using stone and coral. It features a New England architecture. </p>
                <p>Huelo used to be a sugar plantation community, but has shifted to cattle farming when the sugar industry in Hawaii ended. There are a few multi-million dollar oceanfront properties, vacation rentals and small farms growing tropical fruits, herbs and vegetables.</p>
