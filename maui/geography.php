          <h1>Maui Geography</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Maui is the second largest of the main Hawaiian Islands (after Hawaii's Big Island) encompassing 727.2 square miles (1883.5 km2). The island is made up of two shield volcanoes, West Maui Mountain and Haleakala. Both are joined by a low-lying isthmus. The Hawaiian Islands are all the result of volcanic activity. A few of these volcanoes were located close to one another, so close that their lava flows overlapped, merging into a single island. The island of Maui was formed by two shield volcanoes that were so close together that they created an isthmus between them.</p>
             <? include ("../theme/text-desktop-only.html") ?>
              <p>The volcano located in the island&rsquo;s northwest, named West Maui Mountain, is the older of the two. Since its creation it has eroded a lot. Its highest peak is Pu'u Kukui at 5,788 feet (1,764 m). The other volcano is Haleakala, rising to 10,023 feet (3,055 m). It is the largest dormant volcano in the world. At one point in time Haleakala rose to 15,000 feet (4,572 m) and was the largest mountain in the world when measured from its base at the ocean bottom. But like all the Hawaiian islands, Maui is eroding and its summits are shrinking. Eventually, in millions of years, the island will be just an atoll and then sink below the ocean's surface.<br />
