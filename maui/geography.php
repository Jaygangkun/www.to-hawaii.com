              <p>The eastern slops of Maui's mountains are green and lush with rainforests and waterfalls, while the western slopes are dry. That's because the predominant, moisture-laden tradewinds are blowing from the northeast. As they hit the island's mountains, the moisture comes down as rain. Maui's mountains shield the western shorelines from much of this moisture.<br />
                <br />
                The last volcanic eruption on Maui was around 1790 at Haleakala's Southwest Rift Zone. Even though Haleakala is considered dormant, it is not extinct yet and volcanologists believe that it can come back to life.<br />
                <br />
                About 20,000 years ago Maui was joined together with the neighbor islands of Molokai, Lanai and Kahoolawe. It was one big island referred to as Maui Nui, meaning Big Maui. There was a mini ice age at this time which caused the sea level to drop. As the islands eroded and the sea level rose, Maui Nui became what is now four separate islands. The ocean channels between these four islands are relatively shallow. Geologists believe that in about 15,000 years from now, Maui will be two separate islands. The low-lying isthmus will then be covered with water and form a new ocean channel.</p>
