                <h2 class="small">Wai'anapanapa Black Sand Beach, Maui</h2>
                <p>Wai'anapanapa State Park lives up to its Hawaiian name meaning &quot;glistening water.&quot; Right off <a href="../scenicdrives/road-to-hana.php">Hana Highway</a>, Wai'anapanapa Beach, also known as Pa'iloa Beach, is situated in the midst of the beautiful 120-acre Wai'anapanapa State Park. This beautiful black-sand beach is a Maui must-see and the perfect spot for an unparalleled photo opportunity. </p>
                <? include ("../../theme/text.html") ?> 
                                <p>Nestled between black lava cliffs and framed by tropical flora, Wai'anapanapa Beach provides dramatic contrasts and colorful surroundings. The black-sand beach was created by a lava flow several hundred years ago. </p>
                <p>Swimming is only possible here when the ocean is calm. When the surf is up, rip currents can form in the bay. Another thing to do while here is explore two spring-fed sea caves. A short loop trail in the park leads to them. </p>
                <p>Wai'anapanapa State Park is one of the places on the island where camping is allowed. However, a permit is required. More info about camping is <a href="../../maui/camping/">here</a>.</p>
                
                <h3 class="big">Wai'anapanapa Black Sand Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Prettiest black-sand beach on Maui</li>
                  <li>Located in the  122-acre Waianapanapa State Park</li>
                  <li>Beach is backed by black lava cliffs and lush vegetation</li>
                  <li>Ocean bottom drops off quickly</li>
                  <li>Rip currents are common here</li>
                  <li>Lava caves with freshwater pools located near the beach</li>
                </ul><div style="clear:both"></div>
