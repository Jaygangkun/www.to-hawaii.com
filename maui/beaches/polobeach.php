                <h2 class="small">Polo Beach, Maui</h2>
                <p>Polo Beach (<a href="../../maui/beaches/p10.html" onclick="NewWindow(this.href,'name','2550','770','yes');return false">view panorama</a>) is one of the beaches in the Wailea area, a large resort complex that encompasses two miles of shoreline. This beach is at the quieter south end of Wailea and is rarely crowded. Backed by low sand dunes, Polo Beach is a nice picnicking beach with BBQ grills, tables and a small park with a grassy area. </p>
                <p>When the ocean is calm, the snorkeling conditions can be good around the northern end of the beach. The ocean bottom drops of gently at Polo Beach, and when there is a small shorebreak, the conditions for bodyboarding  can be good as well. Keep an eye out for rocks hidden on the nearshore ocean bottom, especially at  the southern end of the beach.</p>
