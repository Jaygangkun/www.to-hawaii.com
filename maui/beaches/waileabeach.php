                <h2 class="small">Wailea Beach, Maui</h2>
                <p>Wailea Beach (<a href="../../maui/beaches/p16.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  on Maui's south shore is a pretty  white-sand beach with fine-grained sand. It is one of five beaches in the Wailea resort development (the other four are Polo, Ulua, Mokapu and Keawakapu). The beach is backed by sand dunes at its southern end. </p>
                <p>Wailea is a good beach for swimming. The nearshore ocean bottom drops off gently to the deeper waters offshore. The small shorebreak attracts beginning bodyboarders. The snorkeling conditions are good around the rocky points, but only when the waters are calm. </p>
