                <h2 class="small">Black Rock Beach (Pu'u Keka'a), Maui</h2>
                <p>Black Rock Beach is located on Maui's west shore and divides popular <a href="../../maui/beaches/kaanapali-beach.php">Ka'anapali Beach</a> in half. It is named after the large lava rock (<a href="../ancientsites/puukekaa.php">Pu'u Keka'a</a>) that sits in the nearshore waters on the beach. Legend has it that the souls of warriors left the earth here by leaping from this rock to meet their ancestors.</p>
                <p>The area around Black Rock is a popular snorkeling spot. The more crowded area is the protected and easily accessible southern side of the rock, which is also popular among cliff jumpers. There isn't that much to see in terms of corals in this area. </p>
                <? include ("../../theme/text.html") ?> 
                <p>The less-frequented horseshoe-shaped cove on the rock's open ocean-facing side has much better underwater sights. This harder to get to area is teeming with colorful fish, corals and turtles. It's best to swim here  when the ocean is calm as the currents and surges can be strong around the tip of Black Rock.</p>
                
                <h2 class="big">Black Rock Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Named after the big black rock, which divides Ka'anapali Beach in half</li>
                  <li>Good snorkeling spot around the rock, many corals and colorful fish</li>
                  <li>Be cautious of occasional currents and surges around the tip of Black Rock</li>
                  <li> Large hotel that sits on top of Black Rock is the Sheraton Maui Resort</li>
                  <li>South side of Black Rock is a popular cliff-jumping area</li>
                  <li>Torch-lighting ceremony takes place at Black Rock at sunset</li>
