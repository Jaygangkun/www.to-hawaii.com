                <h2 class="small">Waipu'ilani Beach, Maui</h2>
                <p>Waipu'ilani Park (<a href="../../maui/beaches/p15.html" onclick="NewWindow(this.href,'name','2550','550','yes');return false">view panorama</a>) is a nice, large beachfront park with lawns and shady areas, ideal for a picnic. The swimming conditions are rather poor on this beach because it has some rocks and usually murky water. But it's a good area to windsurf.</p>
                <p>Often times local community activities take place at Waipu'ilani Park, such as the Croquet Club. There are also some tennis courts. So come here to watch a community event or just relax and watch a beautiful sunset. The park is located in Kihei on Maui's south shore. In the Hawaiian language, waipu'ilani means waterspout. </p>
