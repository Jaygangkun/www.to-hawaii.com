                <h2 class="small">Cove Park, Maui</h2>
                <p>Cove Park (<a href="../../maui/beaches/more_kihei_beaches.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../maui/beaches/p11.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located in Kihei on Maui's south shore, has a small sandy beach and is a nice spot to just hang out, read a book and enjoy the surroundings. Seaweed can be a bit of a problem here, so the cove is not that popular among swimmers. The ocean is usually the clearest in the early mornings and  in the winter months (November to March) due to ocean currents. </p>
                <p>Cove Park is also a good surfing beach, and surf lessons are given here. There are  no lifeguards and no facilities, so bring supplies and be careful when swimming here. High surf and powerful rip currents may occur. The park is mainly used by local residents and can get crowded during weekends because there isn't much room. Also, it is located on a busy street (the main road that leads through Kihei). </p>
