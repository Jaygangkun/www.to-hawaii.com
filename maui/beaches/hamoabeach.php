                <h2 class="small">Hamoa Beach, Maui</h2>
                <p>Hamoa Beach, located south of the quaint little town of Hana, has already made an impression on James Michener, author of the popular novel <em>Hawaii</em>. Michener called Hamoa Beach the most beautiful in the Pacific. And quite true. Hamoa Beach is gorgeous and a photographer's dream. The salt-and-pepper sand beach is backed by 30-foot (9 m) high sea cliffs and lush vegetation, which contrasts sharply with the turquoise waters of the Pacific. </p>
                <p>Hamoa Beach is one of several pocket beaches in this area. It offers good swimming and bodysurfing conditions when the surf is not too high. Since the beach is not protected by an offshore reef, the surf can get pretty high and currents can be strong. This is when it is best to stay out of the water. The ocean is usually at its calmest during the summer and early fall.</p>
                <p>No lifeguards are stationed here, but you'll find restrooms and showers. The Travaasa Hana Hotel is located nearby.</p>
