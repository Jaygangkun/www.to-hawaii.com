                <h2 class="small">Kaihalulu (Red Sand) Beach, Maui</h2>
                <p>Kaihalulu Beach, or better known as Red Sand Beach, located just south of Maui's <a href="../../maui/beaches/hana-bay.php">Hana Bay</a>, has red-black sand, a product of the cinder cone hill (named Ka'uiki Head) on the northern end of the cove. </p>
                                <p>The beautiful ironwood trees continuously sprinkle numerous needles that blanket the ground. Kaihalulu Red Sand Beach takes some maneuvering to get down to the water's edge and can be  slippery due to crumbling cinder and pine needles from the trees, but the payoff is definitely worth the challenge. The blue ocean, the red sand, the green trees and the black lava sea wall make for a very unique sight. Bring your camera!</p>
                                <? include ("../../theme/text.html") ?> 
                <p>Although the beach is partially protected by a reef, it is not suitable for swimming or any other type of watersport due to strong currents and turbuent waters. If you decide to go into the water anyway, be cautious because often times there is a fast-moving rip current that flows out to the open ocean at the northern end of the beach. Also, do not swim behind the lava sea wall because the currents are so strong, you may not be able to swim back to shore. Kaihalulu means &quot;roaring sea&quot; in the Hawaiian language and this beach is named like that for a reason.</p>
                
