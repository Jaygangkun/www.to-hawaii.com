<h2 class="small">Sugar Beach, Maui</h2>
<p>Said to be the longest beach on Maui, Sugar Beach (<a href="../../maui/beaches/p7.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>) - also known as Kealia Beach - is perfect for a long beach stroll. This sandy shoreline stretches for about 2.5 miles (4 km), from Haycraft Park in Ma'alaea all the way to north Kihei. There aren't any buildings on the beach until you reach north Kihei and you won't find many other people here. Sugar Beach is mainly used by kite and windsurfers who  take advantage of the breezy offshore winds. Kayakers also launch from here.</p>
