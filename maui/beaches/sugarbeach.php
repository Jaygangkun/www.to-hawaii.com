<ul type="disc" class="mylist">
<li>Good beach for whale watching in the winter (November thru March)</li>
<li>Perfect for walking or jogging along the sandy shoreline</li>
<li>Good conditions for windsurfing, kitesurfing and kayaking</li>
<li>Beach is occasionally plagued by seaweed</li>
<li>Parts of beach backed by thorny kiawe trees</li>
