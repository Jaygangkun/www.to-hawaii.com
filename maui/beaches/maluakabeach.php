                <h2 class="small">Maluaka Beach, Maui</h2>
                <p>Maluaka Beach (<a href="../../maui/beaches/more_maluakabeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../maui/beaches/p9.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located right in front of the Makena Beach &amp; Golf Resort (formerly the Maui Prince Hotel) on the island's south shore. It is a pretty golden-sand beach that offers some of the best swimming conditions in Makena Bay. </p>
                <? include ("../../theme/text.html") ?> 
                                <p>The nearshore ocean bottom is sandy and slopes gently to the deeper and rockier offshore waters. There are some submerged boulders in the shorebreak at both ends of the beach, so be cautious when swimming in this area. When the surf is up, these boulders aren't always visible. </p>
                <p>Kiawe trees and sand dunes make up the baskshore. Because of these sand dunes, the beach is sometimes called Pu'u One, which means &ldquo;sand hills.&rdquo; There's a small landscaped park at the north end of the beach, which provides public access to the shoreline. </p>
                <p>Maluaka Beach has a nice grassy area, picnic tables, restrooms and showers. There's also a concession stand near the beach that is operated by the resort, where you can get drinks or rent beach equipment.</p>
                
