                <ul type="disc" class="mylist">
                  <li>Small but wide sandy beach, good for swimming</li>
                  <li>Sea turtles can often be seen here</li>
                  <li>Shady areas and picnic tables make for a good picnicking spot</li>
                  <li>Makena Beach &amp; Golf Resort located nearby</li>
