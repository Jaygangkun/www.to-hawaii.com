                <h2 class="small">Kanaha Beach Park, Maui</h2>
                <p>Kanaha Beach Park (<a href="../../maui/beaches/p20.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is one of the best locations on Maui for windsurfers or kiteboarders. The conditions are almost always perfect. Most wind- and kitesufers stick to the western end of the beach, the one that is closer to Kahului. The beach park is fairly large and a great place for a picnic. There are many picnic table and barbecue grills. The beach itself is long and sandy with a shallow nearshore ocean bottom with a mix of sand and rocks. Camping is allowed here as well, however, one needs a permit. More info about camping permits is <a href="../../maui/camping">here</a>.</p>
