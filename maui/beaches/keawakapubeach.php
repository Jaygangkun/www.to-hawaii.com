                <h2 class="small">Keawakapu Beach, Maui</h2>
                <p>Keawakapu Beach (<a href="../../maui/beaches/p17.html" onclick="NewWindow(this.href,'name','2550','530','yes');return false">view panorama</a>), located between Kihei and Wailea on Maui's south shore, is a beautiful white-sand beach. Some of the island's most expensive homes are located on this stretch of sand. The beach is a bit hard to spot because this large residential community lines almost the entire length of it; that's also probably why the beach is  less crowded than other beaches nearby.</p>
                <? include ("../../theme/text.html") ?> 
                                <p>The beach is wide and the offshore ocean bottom is sandy, which makes for great swimming conditions. However, Keawakapu Beach is not protected from the ocean's force because it lacks an offshore coral  reef, which would dissipate strong waves. So during times of high surf and big swells, it is best to stay out of the water.</p>
                <p>Divers frequent this beach for a reason that may sound strange at first. There is an underwater artificial reef consisting of about 150 junk cars, 2,250 tire modules and 35 concrete slabs. The cars were dumped here in 1962. The man-made reef is about a half mile (800 m) offshore and at a depth of 80 to 85 feet (24-26 m).</p>
                <p>This artificial reef was created for two reasons. First of all, it protects the shoreline from strong swells, which have repeatedly caused erosion and destroyed oceanfront property in the past. And second, it provides a shelter for fish. The fish populations have substantially increased since this creative reef was built here.</p>
                <p>The beach has some shady spots and is large enough that it never feels crowded. There are no lifeguards on Keawakapu Beach.</p>
                
                <h2 class="big">Keawakapu Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Beautiful white-sand beach with powdery soft sand</li>
                  <li>Backed by luxury homes </li>
                  <li>Only resorts are at north end of the beach</li>
                  <li>High surf and strong currents possible in the winter months </li>
                  <li>Can be windy in the afternoons</li>
                  <li>Perfect beach for a long beach stroll</li>
                  <li>Usually less crowded than other beaches in the area</li>
                  <li>Good whale watching beach in the winter months (November to March)</li>
                  <li>Artificial reef located offshore - attracts divers</li>
