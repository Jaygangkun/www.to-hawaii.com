                <h2 class="small">Big Beach, Maui</h2>
                <p>As its name implies, Big Beach (<a href="../../maui/beaches/p13.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located on Maui's south shore, has plenty of sand for everyone. At almost two-thirds of a mile (1 km) long and over 100 feet (30 m) wide, it is one of the most popular beaches on the island. Its official name is Oneloa Beach (or Makena Beach), but the name Big Beach describes this beach better.</p>
                <? include ("../../theme/text.html") ?> 
                                <p>Big Beach has everything you need for a fun day at the beach &ndash; powdery soft sand, picnic tables, portable toilets, paved parking and great views of the turquoise ocean. When the ocean is calm, the swimming and snorkeling is good here. When the surf is up (as is often the case in the spring and summer months and during southerly Kona storms), there is a powerful shorebreak and strong undertow at Big Beach. This is when it is best to stay out of the water.</p>
                <p>Just to the north of Big Beach lies a smaller beach, known as <a href="../../maui/beaches/littlebeach.php">Little Beach</a>, accessible via a short hike. Here you can try out nude sunbathing and swimming because it is one of the few beaches on Maui where some people go nude (even though public nudity is not allowed by law anywhere in Hawaii).&nbsp;Both Big Beach and Little Beach are part of <a href="../attractions/makenastatepark.php">Makena State Park</a>.</p>
                <p>The prominent hill on the north end of Big Beach is called Pu'u Ola'i, also known as Red Hill or Earthquake Hill. It is a volcanic cinder cone rising 360 feet (110 m). </p>
                
