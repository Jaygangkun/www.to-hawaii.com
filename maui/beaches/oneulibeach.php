                <h2 class="small">One'uli Beach, Maui</h2>
                <p>Also known as Black Sand Beach and Naupaka Beach, One'uli Beach (<a href="../../maui/beaches/p19.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a hidden little gem. It lies just to the north of Pu'u Ola'i, a 360-foot-tall volcanic cinder cone  with gorgeous red cliffs topped with green vegetation. If you look closely, One'uli's beach sand actually has three colors &ndash; black, white and red. </p>
                <p>One'uli Beach doesn't see as many visitors as the other beaches in this area, such as neighboring <a href="../beaches/bigbeach.php">Big Beach</a>. One reason is that One'uli can't be seen from the road. Also,  the swimming conditions aren't good here. There's a lava shelf on the nearshore ocean bottom. But the beach is a nice place for a picnic with scenic views.</p>
