                <h2 class="small">D.T. Fleming Beach Park, Maui</h2>
                <p> D.T. Fleming Beach Park, located in Honokahua Bay on Maui's northwestern shore, offers plenty of space. It's a nice beach park for a picnic. There are picnic tables, showers and restooms. Tall ironwood and palm tress provide plenty of shade.</p>
                <p>The beach park is situated on low land dunes. It has a long and wide white-sand beach. Lifeguards are stationed here, but have in mind that swimming and snorkeling are only possible when the ocean is calm as the shorebreak can be dangerous  and the nearshore ocean bottom has a steep slope, which can cause an undertow when the surf is up. This is when this beach is frequented by bodyboarders and surfers.</p>
