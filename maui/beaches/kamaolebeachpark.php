                <h2 class="small">Kama'ole Beach Park, Maui</h2>
                <p>Kama'ole Beach (<a href="../../maui/beaches/more_kamaolebeachpark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../maui/beaches/p22.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is divided into three sections, Kama'ole I, II and III. Kama'ole I Beach has the largest beach of the three. It is a  wide white-sand beach located between two rocky points. The ocean bottom is sandy, but drops off quickly to overhead depths. On some days there is a small shorebreak that provides good conditions for bodysurfers, but it's not big enough to be dangerous for swimmers. There's a grassy area at the south end that's great for picnics and ball games. The north end of Kama'ole I Beach is also known as <a href="../beaches/charleyyoungbeach.php">Charley Young Beach</a>. The snorkeling conditions are good here when the ocean is calm.</p>
                <? include ("../../theme/text.html") ?> 
                                <p>Kama'ole II, located a little further south, has a smaller white-sand beach and is also located between two rocky points. Swimming conditions are good here as well. The sandy ocean bottom also drops off quickly here and a small shorebreak is present on most days. Sometimes when the kona winds are strong, large swells are generated that erode the beach. When the sand washes out to the ocean, a large bed of shingles is exposed and as the waves wash over these beach rocks, also called ili ili, a rumbling sound can be heard. The Hawaiians called this beach Iliiliholo, or &quot;running pebbles&quot; because of this dramatic natural phenomenon.</p>
                <p>Kama'ole III has a short but wide sandy beach. It's the smallest of the three, but there is a large grassy park area that makes up for the smaller beach. The beach is bordered by rocky points and some large rocks are also exposed in a few places along the water's edge. The ocean bottom is sandy, but drops off quickly. The beach is good for swimming, except during times of high surf when rip currents and a dangerous shorebreak may occur. The waves here are more consistent, ideal for bodyboarders. Even though Kama'ole III has the smallest beach, it seems to be the most popular, probably because it has a playground for children, a wide grassy area at the south end and the most parking spaces. Here, you can play Frisbee or any other outdoor sports after enjoying the water. So pack your lunches and have a picnic on either or all three of these beaches. </p>
                
