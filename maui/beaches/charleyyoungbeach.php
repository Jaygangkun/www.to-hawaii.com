                <h2 class="small">Charley Young Beach, Maui</h2>
                <p>Located in Kihei on Maui's south shore, Charley Young Beach (<a href="../../maui/beaches/more_kihei_beaches.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../maui/beaches/p12.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a great spot for family picnics. It is right next to Kamaole I Beach (in fact it is difficult to tell where Charley Young Beach ends and Kamaole I begins because it's basically one long stretch of sand).</p>
                <p>The ocean bottom drops off gently here, and the snorkeling conditions can be good  when the ocean is calm. However, even though the beach is a nice place for swimming and other ocean activities on most days of the year, high surf and strong rip currents may occur, especially in the winter months (November to March).</p>
