                <h2 class="small">H.A. Baldwin Beach Park, Maui</h2>
                <p>H.A. Baldwin Beach (<a href="../../maui/beaches/p2.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>) is a picturesque community beach park with plenty of space for ball games and swimming. It is one of the most popular beach parks on the windward side of Maui. H.A. Baldwin Beach Park's main attraction is the surf that breaks along the entire length of the park's long, sandy beach.</p>
                <p>The shorebreak is usually small, but good enough for bodyboarders. On some days though, the waves get quite big here to be dangerous for novice swimmers and surfers. The ocean bottom drops quickly to overhead depths. </p>
                <? include ("../../theme/text.html") ?> 
                                <p>H.A. Baldwin Beach Park was a private recreation area for employees of the Hawaiian sugar industry in the past. In 1963, it became public. Today, it's a favorite gathering spot among locals and visitors alike.</p>
                <p>The beach park has a large grassy area with BBQ grills, picnic tables, a pavilion, restrooms and showers. There's even a soccer and baseball field nearby. Lifeguards are stationed at the Paia end of the beach. The beach park is located between Paia Bay and Spreckelsville Beach Park.</p>
                <p>The park is named after Harry A. Baldwin (born in 1871). He was the son of Henry P. Baldwin, who, together with Harry's uncle, developed their first sugar plantation on Maui. Harry also became a builder himself and also a delegate to Congress and a member of the state senate.</p>
                
