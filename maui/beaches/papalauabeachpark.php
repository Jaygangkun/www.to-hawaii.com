                <h2 class="small">Papalaua Beach Park, Maui</h2>
                <p>Papalaua Beach Park (also known as Papalaua State Wayside Park) has a long and narrow sandy beach. It is also known as Thousand Peaks, referring to the many surf breaks (or peaks) that are located offshore. During high surf, thousands of peaks form on the shallow reef, attracting bodyboarders and longboard surfers. When the ocean is calm, the snorkeling conditions are good here. The beach park is narrow and right next to a busy highway. The beach is lined by kiawe trees. Camping is allowed here, but you'll need a permit. Read more about camping permits <a href="../../maui/camping/">here</a>.</p>
