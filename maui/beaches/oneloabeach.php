                <h2 class="small">Oneloa Beach, Maui</h2>
                <p>A wide sandy beach that is never crowded, Oneloa (also known as Ironwoods Beach) is backed by resort villas and golf greens on the northwest side Maui. The best area for swimming is at the left (west) side of the beach. There is a reef at the right (east) end and the middle section of the beach, which makes entering the ocean difficult. On calm days, the snorkeling can be good here, especially at the west end of the beach. When the surf is up, rip currents can occur.</p>
