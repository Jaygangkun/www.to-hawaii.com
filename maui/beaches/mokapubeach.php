                <h2 class="small">Mokapu Beach, Maui</h2>
                <p>Mokapu Beach (<a href="../../maui/beaches/p5.html" onclick="NewWindow(this.href,'name','2550','740','yes');return false">view panorama</a>) is located at the Wailea resort complex. It is a sandy beach with some beach rock exposed in the center. The nearshore ocean bottom is sandy and slopes down gently, which makes for good swimming conditions when the ocean is calm. The snorkeling is best in the morning hours and around the lava outcropping between Mokapu Beach and neighboring <a href="../beaches/uluabeach.php">Ulua Beach</a>. </p>
                <p>The word mokapu is short for moku kapu, meaning &quot;sacred island&quot; in the Hawaiian language. It takes its name from a small rock island that used to be visible offshore before it was bombed out during World War II as a practice target.</p>
