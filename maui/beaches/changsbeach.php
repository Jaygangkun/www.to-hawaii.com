                <h2 class="small">Chang's Beach, Maui</h2>
                <p>Chang's Beach (<a href="../../maui/beaches/p8.html" onclick="NewWindow(this.href,'name','2550','750','yes');return false">view panorama</a>) is a popular bodyboarding beach, especially among locals who come here during times of high surf. When the ocean is calm, snorkeling is good here as well. The small cove has a marvelous offshore reef and you'll be amazed how many fish you can observe here.</p>
                <p>The name Chang's Beach doesn't exactly sound Hawaiian. The beach was named after the Chang family, who had farmed the land for a long time on a part of this beach. The house of Eddie Chang still stands. </p>
                <p>Chang's Beach is located in Makena on Maui's south shore, just south of <a href="../beaches/poolenalenabeach.php">Po'olenalena Beach</a>. There are no lifeguards or facilities.</p>
