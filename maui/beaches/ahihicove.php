                <h2 class="small">Ahihi Cove, Maui</h2>
                <p>Ahihi Cove (<a href="../../maui/beaches/more_ahihicove_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>) is one of the best snorkel spots on Maui. The cove is home to an abundance of marine life, which probably has to do with the area being protected within the Ahihi-Kinau Natural Area Reserve. It is illegal to catch any fish here.</p>
                <p>There is hardly any sand at Ahihi Cove because this is the region where the last lava flowed on Maui in 1790. As the lava flowed down Mt. Haleakala, it covered this entire area and created a surreal landscape. </p>
                <p>It may seem like a disadvantage that there is almost no sand here, but less sand in the water is actually a good thing when you want to snorkel because there is less sand to obscure your view. Also, if you plan to go snorkeling come early in the morning for the best visibility.</p>
                <p>Because the beach is quite remote, there aren't any lifeguards and facilities. Pets are not allowed and because this area is protected, any removal of marine life, coral or rocks is prohibited as well. The cove is a good spot for a family picnic or to just relax.</p>
