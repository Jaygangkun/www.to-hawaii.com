                <h2 class="small">Kalama Beach Park, Maui</h2>
                <p>Kalama Beach Park (<a href="../../maui/beaches/p21.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on Maui's south shore doesn't have a sandy beach, but the spacious 36-acre beach park has everything else to keep the whole family active and entertained. Sports events, outdoor concerts and festivals often times take place here. There's also a basketball and tennis court, an inline skating rink, a 20,000-foot skateboard park, a baseball and soccer field and a playground for children.</p>
                <p>The  beach park is a great spot for picnics. There are picnic tables, twelve pavilions, restrooms and showers. The swimming conditions are rather poor here because  the nearshore ocean bottom is shallow and rocky and the water is usually murky due to seaweed. In other words, this beach park is best for land-based activities. </p>
