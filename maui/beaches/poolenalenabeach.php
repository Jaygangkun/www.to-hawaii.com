                <h2 class="small">Po'olenalena Beach, Maui</h2>
                <p>Po'olenalena Beach (<a href="../../maui/beaches/p14.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located in South Maui just north of Makena, is a half-mile long sandy beach with great swimming conditions when the ocean is calm. The ocean bottom is mostly sandy and has a gentle slope. There are some large rocks exposed at a few places along the shoreline. Sand dunes and kiawe trees are located inland.</p>
                <p>Po'olenalena means &quot;yellow head&quot; in the Hawaiian language. It is the name of a large rock, located on the 5th hole of the Wailea Blue Golf Course. The rock has a dark color with yellow streaks on its sides. It is visible from the public road as well as from the golf course and has long been a landmark for Makena residents.</p>
