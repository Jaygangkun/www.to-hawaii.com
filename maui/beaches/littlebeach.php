                <h2 class="small">Little Beach, Maui</h2>
                <p>Little Beach is the only unofficial clothing optional beach on Maui, located right next to the more popular <a href="../beaches/bigbeach.php">Big Beach</a>. Both beaches are part of <a href="../attractions/makenastatepark.php">Makena State Park</a>. If  you have ever thought about trying nude sunbathing, this is the place to go. However, keep in mind that public nudity is illegal in Hawaii, but very little enforcement of this law is done at this beach, and many people who sunbathe on Little Beach do so without clothes. But if you want to keep your swimsuit on, it is ok too.</p>
                <p>Little Beach is situated in a secluded cove and is bordered by  two lava outcrops. It is also known as Pu'u Ola'i Beach, named after the cinder cone that backs the beach. It is a great swimming beach with a shallow sandy bottom. There's almost always a gentle shorebreak present that attracts bodyboarders and surfers. On calm days, the snorkeling conditions are excellent. </p>
                <p>The views are also very nice here. The neighbor island of <a href="../../kahoolawe.php">Kahoolawe</a> is visible on the horizon, as is the smaller island of <a href="../attractions/molokini.php">Molokini</a>, a world-class snorkel and diving destination. There is a trail right behind Little Beach where you can have a nice beach stroll.</p>
