                <h2 class="small">Ho'okipa Beach Park, Maui</h2>
                <p>Ho'okipa Beach Park (<a href="../../maui/beaches/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located on Maui's well-known Hana Highway (Road to Hana), is perhaps the best and most popular windsurfing destination in the world. Two world-class competitions take place here every year. Visit this beach during the Aloha Classic or Red Bull contests to catch some of the best surfers in the world. Several other watersport competitions are held here annually.</p>
                <? include ("../../theme/text.html") ?> 
                                <p>At this popular cliff-lined beach, surfers come from all corners of the world to ride the unique break. The waves can be sizeable and the currents challenging, so novice surfing is discouraged. Spectators can watch surfers and windsurfers from an elevated overlook point. To prevent an overlap of the two sports, surfers have the bay to themselves in the mornings and windsurfers in the afternoons.</p>
                <p>Ho'okipa Beach is complemented by the presence of a large coral reef area that tames incoming waves, so when the ocean is calm, swimming and snorkeling are possible, but there are better beaches on Maui to do these activities. In the winter months, the waves can reach heights of 15 feet (4.6 m) and more at Ho'okipa.</p>
                <p>The beach itself is little more than a strip of white sand behind a wide, rocky shelf. The nearshore ocean bottom is a mixture of reef and patches of sand. Because of this, the main water activities are windsurfing, surfing and fishing.</p>
                
