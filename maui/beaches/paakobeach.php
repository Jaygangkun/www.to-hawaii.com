                <h2 class="small">Pa'ako Beach, Maui</h2>
                <p>Pa'ako Beach (<a href="../../maui/beaches/p4.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located on Maui's south shore, just south of popular <a href="../beaches/bigbeach.php">Big Beach</a> (Makena Beach). Even though this beach is also known as Secret Cove, it is actually not so secret. It is a popular spot for weddings. This small cove probably got this name because it is hidden between homes in Makena. To get to it, you have to find the narrow opening in a lava-rock wall in an upscale neighborhood. This is the public beach access path. It's easy to miss if one doesn't know it's there. </p>
                <? include ("../../theme/text.html") ?> 
                <p>The sandy cove is quite small and because many weddings are performed here, you have to be lucky if you want to have it for yourself. The lava rocks on the beach create a nice scenery for picture-taking. Secret Cove isn't a good beach for swimming and you won't find any facilities. Most people come here for the lovely scenery and nice views. The two islands on the horizon are Kahoolawe and Molokini. </p>
                
