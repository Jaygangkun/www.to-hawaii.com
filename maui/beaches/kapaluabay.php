                <h2 class="small">Kapalua Bay, Maui</h2>
                <p>Kapalua Bay on Maui's northwestern shore is one of the most picturesque white-sand beaches on the island. Swimming is good here because a coral reef  and long rocky outcrops at both sides of the bay protect the beach from strong currents. Because of a lack of strong waves and currents, the inshore waters are usually clear, so the snorkeling and diving conditions are good, especially at the north end of the beach. There's a little bit less sand there, so the visibility is better. The crescent-shaped bay is backed by coconut palms and luxury condos.</p>
