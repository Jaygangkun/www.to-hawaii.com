                <h2 class="small">Ka'anapali Beach, Maui</h2>
                <p>Ka'anapali Beach is one of Maui's most popular beaches, fronting the Ka'anapali resort hotels. At the north end of the beach is Pu'u Keka'a, or <a href="../beaches/blackrock.php">Black Rock</a>, a large black lava rock that attracts adventurous cliff-jumpers. The snorkeling conditions around Black Rock are excellent when the ocean is calm. In ancient Hawaii, Black Rock was considered  a significant location where the souls of the recently departed leapt to the next realm. </p>
                <? include ("../../theme/text.html") ?> 
                                <p>The area south of Black Rock is where most of the Ka'anapali resorts as well as Whalers Village shopping center are located. A paved path runs along this entire stretch of beach, from the Hyatt  in the south to the Sheraton  at Black Rock. Many oceanfront restaurants are located here as well. A stroll along this beach promenade is nice especially around sunset. </p>
                <p>Ka'anapali Beach also offers good bodyboarding conditions when the waves are right. However, the ocean bottom drops off quickly to overhead depths, so when the surf is up, swimming is not recommended as this is when rip currents may occur. The further south you walk along the beach, the more rocks you'll find in the water especially around Hanaka'o'o Point.</p>
                
