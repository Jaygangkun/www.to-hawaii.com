                <h2 class="small">Ulua Beach, Maui</h2>
                <p>Ulua Beach (<a href="../../maui/beaches/p6.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is one of the beaches in the Wailea resort complex development on Maui's south shore. The sandy beach is located between two rocky points. </p>
                <p>Ulua Beach is a bit more popular than neighboring <a href="../beaches/mokapubeach.php">Mokapu Beach</a>, with which it shares a parking lot. When the surf is up, it is usually a little higher here than at other beaches in this area, attracting many bodyboarders who enjoy the waves.</p>
                <p>During calm days, the snorkeling and diving here is excellent. There are many colorful corals and fish. Ulua means &quot;adult crevalle fish&quot; in the Hawaiian language. </p>
