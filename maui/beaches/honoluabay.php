<h2 class="small">Honolua Bay, Maui</h2>
<p>The beach at Honolua Bay is quite small and primarily made up of boulders with a few scattered patches of sand, so surfing, snorkeling and scuba diving are the main activities here. The bay is a favorite spot among experienced surfers. During the winter months, the waves here are powerful. That's why snorkeling and scuba diving are only possible during the calmer summer months. </p>
<? include ("../../theme/text.html") ?> 
<p>Honolua Bay is part of the Mokuleia Marine Life Conservation District. This protected area encompasses Honolulu Bay and neighboring Mokuleia Bay (commonly known as Slaughterhouse Beach). When the waters are calm, it's a good spot to explore the bay's underwater world as there are many coral formations and an abundance of fish. The nearshore waters are usually murky especially if it has recently rained (because of Honolua Stream that empties into the bay), but the water is clearer a bit offshore.</p>

<h3 class="big">Honolua Bay Overview</h3>
<ul type="disc" class="mylist">
<li>Prime spot for surfing, offshore diving and snorkeling</li>
<li>Rocky beach, only a few sandy patches</li>
<li>Bay is part of a marine life preserve (no fishing is allowed)</li>
