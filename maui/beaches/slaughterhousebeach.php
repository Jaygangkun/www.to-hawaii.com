                <h2 class="small">Slaughterhouse Beach, Maui</h2>
                <p>This beach has a name that truly doesn't sound appealing, but don't judge it by this name. Slaughterhouse Beach got its name from a slaughterhouse that used to be above the cliffs that back the beach. The slaughterhouse was built by Honolua Ranch right at the edge of the sea cliffs, but was torn down in the mid-1960s. The beach has since then been known as Slaughterhouse Beach. Its Hawaiian name, Mokuleia, means &quot;district (of) abundance.&quot; This name describes this lovely beach much better.</p>
                <p>Located near Kapalua in West Maui, this beautiful beach features a large pocket of white sand backed by sea cliffs. During the winter months, the surf can be strong and often pounds the beach, washing sand offshore and exposing large rocks. During the summer, the waters are calmer and then the conditions are good for swimming, snorkeling, surfing and bodyboarding.</p>
                <p>Because the area is a protected marine preserve, fishing and spearing are not allowed on Slaughterhouse Beach. Harming or taking any marine life is prohibited. You can reach the beach by hiking down a trail leading down from the highway.</p>
