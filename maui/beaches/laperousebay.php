                <h2 class="small">La Perouse Bay, Maui</h2>
                <p>La Perouse Bay (<a href="../../maui/beaches/more_laperousebay_photos.html" onclick="NewWindow(this.href,'name','900','600','yes');return false">see more photos</a>), located on Maui's south shore in the Ahihi Kinau Natural Area Reserve, has a rocky shoreline. The bay features several small coves that lie between rocky points. These are often covered with tidal pools. Some of the coves have small sandy beaches, but most of them have coral-rubble beaches. The road that leads to La Perouse Bay is a narrow (one lane), and the last stretch to the bay is unpaved and riddled with lava rocks.</p>
                <? include ("../../theme/text.html") ?> 
                <p>                  The bay is protected from strong currents, but during periods of storms and heavy surf, the water inside the coves can become very turbulent. La Perouse Bay is known for being a great snorkeling location, but not the bay itself (which has poor visibility), but the small coves along the northern end of the bay in the nature reserve. <strong>However, the majority of the Ahihi Kinau Natural Area Reserve is currently closed to the public until July 31, 2018</strong> (see <a href="../../maui/beaches/more_laperousebay-closure_photos.html" onclick="NewWindow(this.href,'name','1070','800','yes');return false">map</a> of closed area). The Department of Land and Natural Resources (DLNR) decided to temporarily close the area to let the ecosystem recover from overuse. This includes the popular snorkeling coves to the north of La Perouse Bay, known as Kalaeloa (Aquarium) and Mokuha (Fishbowl). </p>
                <p>                  An alternative snorkeling location that remains open is Moanakala, locally known as Dumps, named after a rubbish dump that used to be located here. This place also offers somewhat easier access to the water (the entire area around La Perouse Bay is covered with sharp lava rocks). Dumps is located in the northern portion of the reserve, just south of <a href="../beaches/ahihicove.php">Ahihi Cove</a>. (After passing Ahihi Cove, go up the hill, where you'll find a rough parking lot on the right. From here it's a 5-minute walk to the ocean.) The snorkeling at Dumps is not suitable for beginners because the winds can be strong here and they make the ocean choppy. The area also gets occasional strong currents. Access to the northern portion of the reserve is allowed between 5:30 am to 7:30 pm daily. </p>
                <p>                  When visiting this area, it's best to wear sturdy shoes because of the sharp lava rocks. Also, bring your camera. The stark, barren lava rocks, the bright blue ocean, the coral beaches and the waves crashing against the cliffs provide the perfect scenery for some great shots. Just keep in mind that the bay is a protected nature preserve. It is therefore against the law to harass or approach any marine life or to remove or destroy any rocks or plants.</p>
                <p>La Perouse Bay is named after a French naval officer and explorer (Jean-Fran&ccedil;ois de La P&eacute;rouse) who was the first European to set foot on Maui in 1786. The Hawaiian name of La Perouse Bay is Keone'o'io.</p>

