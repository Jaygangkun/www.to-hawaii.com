                <h2 class="small">Napili Bay, Maui</h2>
                <p>Napili Bay, located on Maui's northwestern shore, is known for its fine golden sand. It lies between two rocky points and is an excellent beach for swimming and other water activities. The ocean bottom is sandy, but drops off quickly to overhead depths, which can cause a powerful shorebreak when the surf is up, which is often the case in winter.  This is when the beach attracts bodyboarders and surfers. The summer months are best for swimming and snorkeling. There are many hotels and vacation rentals on Napili Bay, so it's a rather busy beach with lots of sunbathers. </p>
