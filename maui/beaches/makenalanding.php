                <h2 class="small">Makena Landing Beach Park, Maui</h2>
                <p>Makena Landing Beach Park (<a href="../../maui/beaches/p1.html" onclick="NewWindow(this.href,'name','2550','660','yes');return false">view panorama</a>) is located in Makena, a small town of which many people say it is Maui untamed. There are some really nice beaches in this area. Makena Landing Beach is bordered by a small beach park, but this stretch of  shoreline is mostly rocky with little sand. South of the landing are two small pockets of sand, which are good picnicking spots. This is also a popular place to launch kayaks.</p>
                <p>In 1999, a storm hit this beach and the runoff caused unusually cloudy water conditions, which destroyed the coral reef. However, a popular scuba diving location is located just north of this beach, called 5 Graves (or 5 Caves).</p>
