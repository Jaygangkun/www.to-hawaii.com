                <h2 class="small">Lelekea Bay, Maui</h2>
                <p>Lelekea Bay is located in a remote area on Maui's east shore, just southwest of the sleepy little community of Kipahulu. It is a pebble beach and swimming is not recommended here due to strong currents. When you stand on the beach  and the surf is up the pebbles make an interesting sound, almost like a musical instrument. Look toward the southwest and you'll see Kalepa Point. </p>
                <p>The road that leads here is <a href="../scenicdrives/piilanihighway.php">Pi'ilani Highway</a>. Even though it's called a &quot;highway,&quot; parts of this road are very narrow (one lane), unpaved and curvy, hugging the sea cliffs. If you're driving a rental car, check with your rental car company first if they allow you to drive on Pi'ilani Highway (the section of Pi'ilani Highway between mile marker 33 and 38 is gravel/unpaved).</p>
