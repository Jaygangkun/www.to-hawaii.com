<h2 class="small">Honomanu Bay, Maui</h2>
<p>Honomanu Bay is located along the well-known Hana Highway (Road to Hana) on Maui's north shore, between mile markers 13 and 14. Many people slow down here or park along the road because the bay and surrounding lush vegetation looks beautiful. There are only a few small pullouts  from where the bay can best be seen. </p>
<p>The beach is not easily accessible by car because only a dirt road leads there, which is steep and sometimes muddy. The best way to get down to the beach is on foot via this access road, which begins at the pullout a half mile after mile marker 13. From here it's a 5-minute walk to the beach.</p>
<? include ("../../theme/text.html") ?> 
<p>The beach itself is rocky and the surf offshore dangerous, breaking over numerous submerged boulders. The swimming conditions are poor here due to the rocky ocean bottom. The bay is mainly frequented by experienced surfers and fishermen. When the surf is up, there are strong rip currents. </p>
<p>Honomanu Bay doesn't have any facilities, so the only thing one can do here is wade in the water or have a picnic (if one doesn't mind the sitting on small pebbles). There is also a small stream that flows into the ocean after crossing the narrow beach.</p>

