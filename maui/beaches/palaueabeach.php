                <h2 class="small">Palauea Beach (White Rock), Maui</h2>
                <p>Also known as White Rock, Palauea Beach (<a href="../../maui/beaches/p18.html" onclick="NewWindow(this.href,'name','2550','730','yes');return false">view panorama</a>) is a lesser-known South Maui beach that's a little hard to find if you don't know where it is. Unlike other more popular South Maui beaches, Palauea Beach doesn't have a designated parking lot (only roadside parking). Located just south of <a href="../../maui/beaches/polobeach.php">Polo Beach</a>, Palauea Beach is accessible via a short dirt path between the trees. </p>
