                <h2 class="small">Koki Beach, Maui</h2>
                <p>Located south of Hana on Maui's east shore, Koki Beach has a mixture of black, white and red sand. The dark reddish sand comes from the nearby red cinder hill, named Ka Iwi o Pele. </p>
                <p>Koki Beach is not suitable for swimming. The beach is unprotected from incoming high surf and currents can be very strong. Often times there are strong alongshore currents that run from right to left and then out past the rocks. These rip currents can easily suck swimmers out into the ocean. There are no lifeguards at Koki Beach, and many drownings have happened here. Local bodyboarders and surfers who are familiar with the beach come here to surf.</p>
                <? include ("../../theme/text.html") ?> 
                <p>Hikers may be tempted to follow the shoreline to the north of the red cinder hill, but this is not a good idea because the hill consists of very loose volcanic material that frequently breaks off and falls to the shoreline below. So better just stay on the safe side and take some pictures of the nice scenery. </p>
                <p>Koki Beach has a small grassy park, ideal for resting or having a picnic. Tall ironwood trees provide plenty of shade. There are no facilities here, except for picnic tables. Even though Koki Beach is not a typical white-sand beach and is different from other beaches on Maui, this is what makes it special and the reason why people come here. </p>
                
