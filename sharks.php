                     <p><strong>Sharks in Hawaiian culture</strong></p>
              <p>Sharks have always played a big role in Hawaiian culture. From one side, the animal provided the people with tools. Shark teeth, for example, were used as a knife and shark skins were used for hula drums. But sharks also played a role in Hawaiian spirituality. Some shark species were considered equal to Hawaiian ali'i (royalty). Sometimes when a family member died, it was believed that the deceased could reincarnate in the form of a shark. This shark then was the family's 'aumakua, a guardian spirit and protector. Also, at least nine Hawaiian gods are associated with sharks: Kamohoali'i, Kua, Kuhaimoana, Kawelomahamahai'a, Kane'apua, Kaholia-Kane, Ka'ahupahau, Keali'ikau o Ka'u and Kaehuikimano o Pu'uloa.</p>
