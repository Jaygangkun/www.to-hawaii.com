                          <strong>Why do sharks  occasionally attack humans?</strong>
              <p>Sharks play an essential part in the world's oceans. They  balance other fish populations. The most common shark attack on humans is the  so-called "hit and run" attack. In other words, the shark bites the human  (usually in the arm or leg), but quickly lets go because he realizes that the  human is not the seal or turtle he thought it is. From below, this is how  humans look like to a shark, especially surfers on a surfboard. Even though most sharks have a well-developed sense of vision, they can confuse humans with other marine animals that  are on their regular menu. Humans also often times splash around while  swimming, which further attracts sharks because for them it symbolizes sick or  injured marine life.</p>
              <p><strong>How many shark species can be found  in Hawaiian waters?</strong></p>
              <p>There are about forty species of shark in Hawaii,  of which eight can be observed near shore, including the reef whitetip,  sandbar, scalloped hammerhead and tiger shark, according to the Hawaiian  Lifeguard Association. Tiger sharks are the shark species that most often  attack humans in Hawaiian waters.</p>
              <p><strong>What safety precautions can one take to  reduce the risk of being attacked by a shark?</strong></p>
              <p>The State of Hawaii Shark Task Force  gives the following tips to reduce the risk of suffering a shark attack:</p>
              <ul>
                <li>Don't swim alone. </li>
                <li>Swim in guarded areas where lifeguards are present. </li>
                <li>Avoid swimming in the dark or twilight hours - this is when sharks are believed to be most active. </li>
                <li>Don't swim with bleeding wounds because sharks have a good sense of smell and are attracted to blood, as well as urine. </li>
                <li>Avoid murky water and waters with runoff or swimming near fishing boats. </li>
                <li>Don't wear shiny jewelry or high contrasting colors. </li>
                <li>Don't splash around unnecessarily - this can attract sharks because it signals injured marine life to them. </li>
                <li>Don't swim if sharks are known to be present. </li>
                <li>Be alert if turtles and fish are fleeing the area. </li>
              </ul>
              <p><strong>What to do after a shark attack has  happened?</strong></p>
              <p>Administer  first aid. Get the victim out of the water. Apply direct pressure to the wound.  Wrap a compressing towel (or anything else that's available, such as a t-shirt,  etc.) around the injured area to stop bleeding. Call 911 immediately. Stay with  the victim and keep the victim calm and warm.</p>
