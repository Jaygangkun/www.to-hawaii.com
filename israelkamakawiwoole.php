              <p> In 1997, IZ received several Na Hoku Hanohano Awards from the Hawaii Academy of Recording Arts. He was the male vocalist of the year, the favorite entertainer of the year, had the album of the year and also the island contemporary album of the year. He could not attend the award ceremony as he was in a hospital and had to watch it on television from his bed. </p>
