            <h1>Israel Kamakawiwo'ole</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <p>Braddah IZ, as this Hawaiian superstar was lovingly called by his fans and friends, was the most influential singer of Hawaiian music since the early 1990s until his death at the age of only 38 years. This large man had the most gentle and yet intense voice, which still touches the hearts of people who listen to his recordings. </p>
              <p>His songs are seasoned with a lot of soul, heartfelt longing and love, with humor and sadness at the same time. He sang about love. The love between people, the love for his people, the love for his aina, his land, the love for good food and the love for life itself. </p>
                           <p> He became popular outside Hawaii with his version of <em>Over the Rainbow/What a Wonderful World</em> that was used in the soundtrack of the movies <em>Meet Joe Black</em>, <em>Finding Forrester</em> and <em>50 First Dates</em>. You can find it on his album <em>Facing Future</em> that was released in 1993. </p>
              <p>Israel war born in 1959 on the island of Oahu to Henry Kakeialoha Naniwa und Evangeline Leinani Kamakawiwo'ole. He grew up right outside Waikiki in a part of Honolulu called Kaimuki. At the age of 11, he started playing music together with his brother Skippy. </p>
              <p> A few years later, his family moved to Makaha on the leeward side of Oahu. One day IZ was playing his ukulele by the beach and met Moon Kauakahi, who invited him over for a little jam session. This was the beginning of a successful band, the Makaha Sons of Ni'ihau that included IZ, his brother Skippy, Moon Kauakahi, Sam Gray and Jerome Koko. </p>
              <? include ("theme/text.html") ?>
              <p> They quickly became popular entertainers and toured Hawaii and the continental U.S. for several years from the late 1970s through the 1980s. They created 10 albums together. His brother Skippy died at a young age in 1982, the same year that IZ got married to his childhood love, Marlene. Together they had a daughter, Wehi.</p>
              <p> By 1990 IZ was ready to do his first solo album. It was called <em>Ka'ano'i</em>. This was the beginning of his amazing solo career. Besides the rainbow song, his popular album <em>Facing Future</em> contains hits like <em>Hawaii 78</em>, a moving song with a haunting melody and a voice expressing powerful emotion that cannot be escaped. </p>
              <p> This is one of the songs in which he passionately expresses his deep love for his people and his land. Especially in the later years of his career, IZ became more and more expressive about his wish for Hawaiian independence and about Hawaiian rights. His passion and involvement were and still are contagious. It was this what made him a hero on these islands. </p> <?php
