              <p> IZ did not only have a huge voice, passion and spirit. He had a huge body as well and weighed around 750 pounds at some point. He tried to battle his weight and save his health, but he also had an illness of the thyroid, which ran in the family. </p>
              <p> Israel Kamakawiwo'ole died on June 26th 1997 due to a respiratory problem that was related to his weight. He was only 38 years old. Before his ashes were scattered into the ocean at Makua Beach, more than 10,000 people paid their respects as his body lay in the Honolulu Capitol Building. All day long the Hawaiian flag was on half mast. Hawaii had lost one of her greatest sons. His spirit lives on through his music. It is more popular than ever and his recordings are still among the most sold albums of Hawaiian music today.</p>
