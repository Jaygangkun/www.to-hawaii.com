                           <h2>Private schools in Hawaii</h2>
              <p>Considered being the finest in Hawaii, <strong class="subtitle">Punahou School </strong>is a private, co-educational, non-sectarian school located in <a href="oahu/cities/honolulu.php">Honolulu</a> on Oahu. The school serves around 3,700 students from kindergarten through the twelfth grade, which makes it one of the largest independent schools in the United States.</p>
              <p>Punahou School was founded in 1841 and was originally a school for the children of Congregationalist missionaries. The school was known as Oahu College from 1859 to 1934. It's also listed on the National Register of Historic Places. The school's focus is on college preparation. Because of Punahou's rich tradition in academic and athletic excellence, the school is considered to be one of the best nationwide.</p> <?php
