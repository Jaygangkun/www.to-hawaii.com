              <p><strong class="subtitle">Iolani School</strong> is a private, co-educational school serving over 1,800 students from pre-school to grade 12. It was founded in 1863 by Father William R. Scott and was later patronized by King Kamehameha IV and Queen Emma, who gave the school its name in 1870. Today, Iolani School is affiliated with the Episcopal Church in the United States. It is administered by a Board of Governors and is one of the largest independent schools in the nation.</p>
              <p>Iolani school is located in Honolulu on Oahu. The school's focus is on college preparation and it's strong in athletics. More than 70 percent of all students belong to an Iolani School athletic team in over 32 competitive sports. </p>
              <p><strong class="subtitle">Kamehameha Schools</strong> is a private, co-educational school serving over 6,500 students from pre-school through the twelfth grade. The school was established in 1887 through the will and testament of Bernice Pauahi 
                Bishop, a royal family member of Kamehameha the Great. The Kamehameha Schools admission policy gives preference to students with Hawaiian blood. Attending this school is considered a privilege and there's great debate regarding its Hawaiians-only policy. Since 1965, the school has effectively excluded all but two non-Hawaiians from attending.</p>
              <p>The school operates one campus on Oahu, one on Maui and one on the Big Island. The main campus is in Honolulu on Oahu, located on Kapalama Heights. Tuition is much lower than other private schools in Hawaii. The school is funded by the Bishop Estate. Its religious orientation is Protestant/non-denominational. The school's focus is on college preparation and it's strong in performing arts.</p>
              <p><strong class="subtitle">Maryknoll School</strong> is a private, co-educational, Roman Catholic school serving 1,400 students from pre-kindergarten through the twelfth grade. Located in Honolulu on Oahu, the school is administered by the Diocese of Honolulu in association with the school's original founders, the Maryknoll Society of brothers and priests and the Maryknoll Congregation, also known as the Maryknoll Sisters. Maryknoll School is open to all Hawaii children, no matter what background or faith they have.</p>
              <p><strong class="subtitle">Sacred Hearts Academy</strong> is a private, Roman Catholic school for girls. Founded in 1909, the school is located in Honolulu on Oahu and serves students from kindergarten through grade 12. The school is affiliated with the Picpus Fathers of the Congregation of the Sacred Hearts of Jesus and Mary.</p>
              <p>The Academy offers a college preparatory education to girls with wide-ranging academic abilities, needs, religious traditions and socio-economic backgrounds. Sacred Hearts Academy focuses on character and values, cooperation, mutual respect, peaceful resolution of conflict and service.&nbsp; </p>
              <p><strong class="subtitle">Damien Memorial School</strong> is a private, Catholic school for boys located in Honolulu on Oahu. The school opened its doors in 1959 and is sponsored by the Congregation of Christian Brothers. The school serves student from grade seven through twelve.</p>
              <p>The academic environment at Damien is strong, traditional and college-oriented. Each student is placed into courses that fit his individual abilities. There is an Honors Program for the academically gifted as well as advanced placement courses for qualified students who want to earn college credit while still in high school.</p>
              <p><strong class="subtitle">Honolulu Waldorf School</strong> is a private, co-educational, non-sectarian school serving about 300 students from early childhood through grade 12. Founded in 1961, the school is located on two campuses. The lower school campus, which is home to grades one through eight, is situated in Niu Valley in East Honolulu. The high school campus (grades nine through twelve) is located in the Honolulu neighborhood 
                of Kahala.</p>
              <p>Honolulu Waldorf School is one of over 1,000 Waldorf schools throughout the world. The school's philosophy is based on an approach to education that focuses on child development and responds to the developmental phases of children. Creativity and free-thinking are encouraged and the school's academic program integrates academic, practical, experiential and artistic work. A mandatory foreign language beginning in grade one, fine arts, music, movement and drama are all part of a student's daily life at Waldorf.</p>
              <p><strong class="subtitle">Montessori Community School </strong>is a private, independent, non-sectarian and co-educational school providing a Montessori education for children ages two through twelve years (grade 6). Founded in 1972, the Montessori Community School in Honolulu is one of more than 4,800 Montessori schools in the nation. The school is a not-for-profit organization and is governed by a Board of 
                Directors.</p>
              <p>Montessori education is based on principles developed by Dr. Maria Montessori, who was born in Italy in 1870. She became the first woman physician to graduate in Italy. Throughout her life, she was working with children and developed a new and unique education technique, which supports the natural development of children. The teacher in a Montessori school serves less as an instructor and more as a guide. Children are encouraged to "learn how to learn." In other words, the philosophy of Montessori education is education-by-doing.</p>
              <p>Founded in 1949, <strong class="subtitle">Hawaii Preparatory Academy </strong>is a private, independent, non-sectarian and co-educational school serving students from kindergarten through grade 12. The school also offers a boarding program for students from grade six through twelve. The more than 600 students come from all over the world, including Hawaii, 15 U.S. states and territories and 15 foreign countries. </p>
              <p>Hawaii Preparatory Academy is located in Waimea (Kamuela) on the Big Island of Hawaii. It has two campuses. The Lower and Middle School are situated on six acres in the midst of Hawaii's ranching country, while the Upper School is located two miles from the foot of the Kohala Mountains.</p>
              <p>From grade nine through twelve, emphasis is put on college preparation and basic knowledge in all subject areas. The student-faculty ratio is 10:1. Students have access to resident teachers seven days a week, indoor tennis and recreation centers, a swimming pool, visual and performing arts facilities and a wireless computer campus.</p>
              <p><strong class="subtitle">Island School</strong> is a private, non-sectarian, co-educational school located in Lihue on the island of Kauai. The school is a college preparatory institution serving students from pre-kindergarten through the twelfth grade. </p>
              <p>The school's philosophy follows the concepts of the progressive school movement that education should help children develop a lively interest in the world, think critically and become an active member of the community. Community service is part of the curriculum and students participate in various charitable events and organizations.</p>
              <p>All students are also encouraged to develop artistic skills, such as dance, music, drama and in the visual arts. Students can also participate in various clubs and athletics. </p>
              <p><strong class="subtitle">Seabury Hall</strong> is a private, independent, co-educational and college preparatory school located on a 56-acre campus a mile above Makawao Town on the island of Maui. The school was established in 1964 and is affiliated with the Episcopal Church. Seabury Hall serves about 400 students from grade six through twelve. The school is financed mostly by tuition and governed by a volunteer board of trustees.</p>
              <p>Seabury Hall was awarded the Blue Ribbon, a designation by the U.S. Department of Education for outstanding educational achievement that is awarded to less than one percent of the nation's schools. In 1999, the school was also elected to the Cum Laude Society, the national secondary school honor society.</p>
