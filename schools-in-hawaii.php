?> <p>Punahou's athletics program is one of the best in the nation. In 2005, <em>Sports Illustrated</em> named it the number four U.S. high school athletics program. Facilities are first-class and include a heated Olympic-sized swimming pool and an 8-lane Mondo track surface. Students have the opportunity to compete in 22 sports, such as air riflery, baseball, basketball, bowling, cross country, cheerleading, football, golf, 
                gymnastics, sailing, soccer, swimming, diving, tennis, wrestling and many more.</p>
              <p>Besides its strong athletic program, Punahou has excellent visual and performing arts programs as well. The schools dance, drama and music programs are recognized nationally. Students also have access to a jewelry studio, photography darkroom and glass-blowing facilities. </p>
