              <p><strong>Education in Hawaii - from kindergarten to the doctorate degree</strong></p>
              <p>Since the arrival of the missionaries in 1820, education has developed quickly in Hawaii. In 2000, 84.6 percent of all state residents 25 years or older had completed high school and 26.2 percent had completed four or more years of college. </p>
              <p>Hawaii is the only state in the nation to have a single, unified, state-wide public school system, which was founded in 1840. So instead of each community having its own school board and financial responsibility for its school, there's one centralized school board, which is located on Oahu and which has jurisdiction for the entire state of Hawaii, over all the 284 public schools. The advantage is that funding, staffing and facilities are equally distributed. The disadvantage is the sometimes slow administration, which hinders flexibility and initiative.</p>
                            <p>About 17 percent of Hawaii's schoolchildren attend one of the state's 128 private schools, which have good reputations, but are costly. The percentage of Hawaii students attending private school is the highest in the nation. Private schools differ in size, tuition and curricula (emphasis). Some focus on academic preparation for college, others for example, stress creativity, personal development and balance. Here is a sample of some of Hawaii's private schools, a short overview of each one and the costs.</p>
