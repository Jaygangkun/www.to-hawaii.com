              <p><strong>Hawaii Pacific University (HPU)</strong> is a private, independent, co-educational, non-sectarian, not-for-profit and career-oriented university located in downtown Honolulu. Founded in 1965, it is the largest private university in the central Pacific today. </p>
              <p> The university has two main campuses, one in downtown Honolulu and the Hawaii Loa campus, located near Castle Junction in Kaneohe. HPU also operates satellite campuses on U.S. military bases, including Pearl Harbor, Hickam Air Force Base, Camp Smith, Schofield Barracks, Tripler Army Medical Center and Kaneohe Marine Corps Base Hawaii. Furthermore, the Oceanic Institute is part of HPU, so students have access to research programs in the marine and oceanic sciences. </p>
              <p> The university is accredited by the Accrediting Commission for Senior Colleges of the Western Association of Schools and Colleges and the National League for Nursing. HPU is also a member of nationally recognized groups, such as the National League of Nursing and the Executive MBA Council. HPU confers associate, bachelor and master degrees. </p>
                          <p> The university is most noted for its diverse student body. The more than 7,400 students come from all 50 states and over 80 countries. Students can choose from over 50 undergraduate degrees and 14 graduate degree programs. HPU's largest academic programs are nursing and business administration. </p>
              <p><strong>Chaminade University of Honolulu</strong> is a private, co-educational, not-for-profit university in Honolulu. It was founded in 1955 by the Society of Mary, a Roman Catholic religious order also known as the Marianists. The university is named after Father William Joseph Chaminade, a French priest and the group's founder. Located in Kaimuki, a Honolulu neighborhood, Chaminade confers associate, bachelor and master degrees in the arts and sciences. The university specializes in business, communication, education, interior design, law and religious studies degree programs. The university is accredited by the Western Association of Schools and Colleges. Total enrollment is over 2,700.</p>
              <p> <strong>Brigham Young University Hawaii (BYU Hawaii)</strong> is a private, co-educational university located in Laie on Oahu's windward coast, about 35 miles from Honolulu. The university is operated by the Church of Jesus Christ of Latter-day Saints and named after the American religious leader, colonist and first territorial governor of Utah and founder of Salt Lake City. The university is an undergraduate institution serving some 2,500 students from Asia, the Pacific Islands, the continental United States and other parts of the world. Students come from over 70 countries. Nearly 50 percent of all students come from foreign countries.</p>
              <p>One of the prime institutions of BYU Hawaii is the Polynesian Cultural Center, the largest living museum of Polynesian culture and traditions in the state of Hawaii. The Center is also the number one paid tourist attraction in Hawaii. More than 30 percent of BYU Hawaii students work there as performers, guides, food service personnel and in other positions to help pay for their educations. Brigham Young University Hawaii features programs in business, education, computers, technology, the sciences and fine arts. It is accredited by the Accrediting Commission for Senior Colleges and Universities of the Western Association of Schools and Colleges.</p>
              <p><strong>Argosy University</strong> is a private, co-educational university with locations in 18 U.S. states. Argosy University Hawaii was founded in 1994. It confers bachelor, master and doctoral degrees. The campus is located in downtown Honolulu. The institution also has satellite locations on Maui and in Hilo on the Big Island. Argosy University is accredited by the Higher Learning Commission and is a member of the North Central Association of Colleges and Schools.</p>
              <p><strong>Hawaii Tokai International College</strong> (HTIC) is a two-year liberal arts college located in Kapolei. It is accredited by the Accrediting Commission for Community and Junior Colleges of the Western Association of Schools and Colleges (WASC). HTIC is the only campus in the U.S. of the Tokai University Educational System of Japan (TES). Currently, the college offers three academic programs: the College Preparatory program, the Associate of Arts degree (Liberal Arts program) and the International Programs.</p>
