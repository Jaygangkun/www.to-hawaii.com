              <p><strong>Honolulu University</strong> is a private,  not-for-profit  institution of nontraditional higher education (distance learning). It is not accredited by an accrediting agency recognized by the United States Secretary of education. The university was founded by Warren Walker as Golden State University in California and has been 
                in Hawaii since the late 1980s. Honolulu University offers undergraduate, master and doctoral degrees and credit for career and life-learning experiences. The institution's programs are designed primarily for career professionals or self-learners.</p>
              <p><strong>LIGS University</strong> is a private university established in 2012. If offers online BBA, MBA, MSc and PhD degrees, as well as English language programs. LIGS University is accredited by the International Association for Distance Learning (IADL) and the American Association for Management Education and Accreditation (AAMEA). However, the university is not accredited by an accrediting agency recognized by the United States Secretary of education.</p>
              <p>The <strong>Hawaii Theological Seminary</strong>, formerly known as the International College and Graduate School, is a private, co-educational, Christian college and seminary located in Honolulu. Established by James R. Cook and J. William Cook in 1971, the school grew out of the International Baptist Church of Honolulu and Western Seminary, a theological institution located in Portland, Oregon. It is accredited by the Transnational Association of Christian Colleges and Schools. The seminary confers the Certificate in Biblical Studies.</p>
