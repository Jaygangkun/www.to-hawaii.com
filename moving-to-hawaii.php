              <p> According to the National Low Income Housing Coalition, there are 43% of renter households in Hawaii (2010-2014). Buying a house is beyond the reach for many people and often times  so is renting an apartment. That's why many local families share a house or apartment  with their extended family. Singles may choose to share an apartment with one  or several roommates. And then there are the many homeless. Statewide, between  12,000 and 15,000 people are homeless in Hawaii  at some point of the year and at least 6,000 are homeless on any given day,  according to the Hawaii H.O.M.E. Project. In Hawaii there are currently 487 homeless per 100,000 people, which is the highest rate per capita in the United States, above New York and Nevada according to federal statistics. </p>
