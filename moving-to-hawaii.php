              <p>If you plan to move to Hawaii,  you probably have many questions and want to have them answered before you   move to the islands. Since we live in Hawaii and we know the islands well, we will  provide the answers to some of your questions about moving to Hawaii right here.</p>
              <p><strong>Which island should I move to?</strong></p>
              <p> This is a question that only you can answer. However, we can help you with  some useful information and tips. First of all, if you can afford it, plan a  trip to Hawaii  before you actually move here. Once you have explored the islands and seen them  with your own eyes, you will have a better idea where you would feel most  comfortable and happy to live. Each one of the six islands is unique not just  in geography, but also in the lifestyle they offer, the cost of living,  salaries and job opportunities. But no matter which island you choose, keep in mind that salaries in Hawaii are generally lower than in the continental U.S., even though the cost of living is higher in the islands.</p>
              <? include ("theme/text.html") ?>
              <p> The island of <strong class="subtitle">Oahu</strong>, nicknamed &quot;The Gathering Place,&quot;  is the most populated of the Hawaiian islands (953,207 people as of the 2010 Census). It is where the capital of Honolulu  and world-famous Waikiki are located. Most  people who choose to move to Hawaii  choose Oahu because it has the most job and  entertainment opportunities and higher salaries compared to the other islands. Oahu is the busiest of all Hawaiian   islands and Honolulu  is quite similar to many major American cities with high-rises, busy highways  and traffic jams. Main shopping malls, such as Ala Moana Center, and certain beaches, such as  popular Waikiki Beach and Hanauma Bay,  can be quite crowded on most days of the year. Most jobs on Oahu  are in tourism, government/military, construction, agriculture, retail sales  and healthcare. </p>
              <p> The island of <strong class="subtitle">Maui</strong> is slightly larger in size than Oahu, but only 144,444 (as of 2010) people live  here.  Maui is therefore a lot less crowded. The  variety of jobs available on Maui is smaller  than on Oahu. The two main industries are  tourism and agriculture. Most people would agree that Maui  is a lot less hectic than bustling Oahu, but  not too sleepy to get bored, especially if you live in the Kahului, Lahaina and  Kihei areas.</p> <?php
