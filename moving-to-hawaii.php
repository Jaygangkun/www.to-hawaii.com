              <p><strong>Cost of Food</strong></p>
              <p> Although about 90% of all food in Hawaii  is imported and things cost more here than on the U.S. mainland, you can buy food at relatively  affordable prices at chains like Costco, WalMart and Sam's Club. Some regular  supermarkets also have special sales. A good idea for staying within your  budget is to buy what's on sale, not what you want to eat. If you like dining  outside, things can add up quickly. The average cost for a breakfast in Hawaii is from about $4  to $7, for lunch from about $5 to $10 and for dinner about $12 to $30 and more.</p>
              <h3>Living in Waikiki</h3>
              <p> Waikiki is well-known among visitors to Hawaii and many of the  buildings here are actually hotels. If you choose to live in Waikiki,  you will be close to Waikiki   Beach and Diamond Head. If you are into nightlife, Waikiki is a perfect place to live. Waikiki has nightclubs, restaurants, shows, bars and much more. A disadvantage of  living in Waikiki is that there is only one  grocery store (called Food Pantry), which is also very expensive. The only  other place you can buy food at are the ABC Stores, which are also expensive  and limited when it comes to groceries. However, ABC Stores are easy to find, since they  are located all over Waikiki.</p>
              <p><strong>Transportation</strong></p>
              <p><strong class="subtitle">Public bus:</strong> On Oahu, the public bus transportation system is  conveniently called TheBus. It is an affordable ($2.50 per ticket) and  convenient way to get around on the island. There are many bus routes that  cover most areas of Oahu. For more  information about the cost of a ticket, routes and schedules, visit www.thebus.org. On Maui, there is the Maui Bus. The fare is $1 per boarding.  For routes and schedules, visit www.co.maui.hi.us.  The Kauai Bus offers public transportation on Kauai.  Visit www.kauai.gov for more info. And on  the Big Island, there is Hele-On Bus, www.heleonbus.org. On Molokai, MEO (Maui Economic Opportunity) offers a Rural Shuttle Service at no cost. Visit meoinc.charityfinders.org/Transportation for more info. There are no public  transportation services on the island of Lanai.</p>
              <p><strong class="subtitle">Taxi:</strong> Taxi services are available  on all islands. </p>
              <p><strong class="subtitle">Traffic:</strong> Traffic is a real problem  on the island of Oahu. Rush hour is early in the morning  between 6 a.m. and 8 a.m. and in the afternoon between 4 p.m. and 6 p.m. </p>
              <p><strong class="subtitle">Parking:</strong> Parking is one of the  main transportation issues on the island   of Oahu. If you work in downtown  Honolulu or in Waikiki, it is highly recommended that you rent a parking  spot on a monthly basis in case your company does not provide one.</p>
