                <h2 class="small">Shark's Bay, Lanai</h2>
                <p>Shark's Bay is a secluded, yet easily accessible area on Lanai's south shore (a trail from <a href="../beaches/hulopoe-beach.php">Hulopoe Bay</a> leads here). Since Hulopoe Bay is the most popular beach on the island (the Four Seasons Resort at Manele Bay is located on Hulopoe Bay), some resort guests enjoy taking this short beach trail to neighboring Shark's Bay, one of the most scenic spots on Lanai. </p>
                <p>The bay has red lava cliffs and on the bottom of the cliffs is a small sandy cove. From above the cliffs you can enjoy great views of <a href="../attractions/puupehe.php">Pu'u Pehe</a>, also known as &ldquo;Sweetheart Rock,&rdquo; a picturesque 80 foot-tall (24 m) rock jutting out of the ocean and located 150 feet (46 m) offshore. Pu'u Pehe is the subject of a famous Hawaiian legend and is also said to be the site of an ancient bird heiau (temple).</p>
                <p>Swimming is discouraged at Shark's Bay due to the strong currents and a rocky ocean bottom. Facilities (showers and restrooms) are accessible next door at Hulopoe Beach.</p>
