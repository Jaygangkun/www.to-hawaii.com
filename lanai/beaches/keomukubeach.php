                <h2 class="small">Keomuku Beach, Lanai</h2>
                <p>Keomuku Beach encompasses six miles (9.6 km) of shoreline on Lanai&rsquo;s east coast. The shoreline consists of many narrow detrital sand and cobblestone beaches. Offshore is one of the longest stretches of fringing reef in Hawaii, which is more than a half mile (800 m) wide in several places.</p>
                <p>The ocean bottom is mainly shallow and rocky with only a few patches of sand inbetween. The water is usually murky and choppy because of the strong and steadily blowing tradewinds. For these reasons, it&rsquo;s not a favorite beach for swimmers. Keomoku Beach is primarily used by local fishermen and picnickers. There are also remains of two ancient fishponds. Kiawe trees make up the majority of the shoreline vegetation.</p>
                <p>Keomuku Beach is accessible with a four-wheel drive. There are many access roads from Keomuku Road to the beach. </p>
