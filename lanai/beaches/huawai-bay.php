                <h2 class="small">Huawai Bay, Lanai</h2>
                <p>Huawai Bay is a small beach that is one of Lanai&rsquo;s best-kept secrets. Few visitors ever get to see it. Graced with lovely salt-and-pepper sand and surrounded by small sea cliffs, this isolated treasure can be a lover&rsquo;s haven or an explorer&rsquo;s playground. A collection of tide pools extend along the lava shelf and add to the captivating scenery. </p>
                <p>Huawai Bay, located on Lanai's south coast, is a popular place among local fishermen and is accessible only after hiking down a steep trail, the Po&lsquo;opo&lsquo;o Fisherman Trail. Once you reach the beach, snorkeling and diving are favorite activities here when the ocean is calm. The offshore waters are deep. There are not lifeguard stations, no restrooms and no modern amenities, a touch that many think add to the secluded and true island feel of the bay. </p>
                <p>Above the bay, golf lovers can visit the par-72 championship Jack Nicklaus-designed Challenge at Manele course. Make sure to stop at Huawai Bay for a gorgeous seaside spot that captures the true unspoiled beauty of Lanai.</p>
