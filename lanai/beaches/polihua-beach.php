                <h2 class="small">Polihua Beach, Lanai</h2>
                <p>If you&rsquo;re in the mood of a little adventure, then the white sand Polihua Beach won&rsquo;t disappoint. Located on Lanai&rsquo;s northwestern end, Polihua requires the assistance of a four-wheel drive vehicle to maneuver the rugged, four-mile-long trail that leads here. </p>
                <p>Polihua Beach is over 1.5 miles long and is the longest white sand beach on Lanai. Characterized by rough waters and high winds, Polihua is more of a scenic beach rather than an interactive one. The winds are sometimes so strong they create sand storms, so if you go here on a windy day you&rsquo;ll most probably get sand blown all over yourself. On windless days, this beach is a good spot for sunbathing, picnicking and picture-taking. On a clear day, you can see across the Kalohi Channel all the way to Molokai or even to Oahu&rsquo;s Diamond Head.</p>
                    <? include ("../../theme/text.html") ?>
                                <p>Swimming and ocean sports are not recommended here due to the unpredictable surf and strong currents. The nearshore ocean bottom is steep and drops off sharply to overhead depths. Furthermore, Polihua Beach is completely exposed to the open ocean. It has no protective reef or rocks. The powerful alongshore current is always strong and will easily sweep you out to sea, even on a calm day. So no matter how calm the ocean looks, swimming is not safe here at any time of the year.</p>
                <p>Polihua, which means &ldquo;egg nest&rdquo; in the Hawaiian language, was once one of the most famous green sea turtle nesting beaches in Hawaii. Although the endangered green sea turtles are slowly making a comeback, they are hardly seen here now. You&rsquo;re much more likely to see whales if you come in the winter months. They often pass by very close to shore.</p>
                <p>To get to Polihua Beach, start at the Garden of the Gods and head &ldquo;makai&rdquo; (toward the ocean). At the trail&rsquo;s end, just a short hike from your parking spot you&rsquo;ll be rewarded with vast sand dunes shaped expertly by the relentless ocean-driven winds. Despite the lack of ocean activities at Polihua Beach, many visitors love the remoteness of it and appreciate the unique and beautiful landscape. </p>
                
