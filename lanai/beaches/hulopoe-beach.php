                <h2 class="small">Hulopo'e Bay Beach, Lanai</h2>
                <p> Hulopo'e Bay (<a href="../../lanai/beaches/more_hulopoebaybeach_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>) is Lanai&rsquo;s most popular swimming and picnicking beach, located on the island's south shore. Crystal clear waters invite snorkelers to observe the brilliant reef marine life below, a testament to the success of the marine life conservation district here. Hulopo'e Bay is a protected marine preserve, so visitors are asked to take only pictures and leave only bubbles. Also, no ship is allowed to anchor in this bay. Turtles and spinner dolphins are frequently seen here. </p>
                    <? include ("../../theme/text.html") ?>
                <p>The  long, crescent-shaped bay is bordered on each side by boulders. The ocean bottom slopes off quickly, so novice swimmers should be cautious. There's usually some surf, ideal for bodyboarding or surfing. Small rip currents sometimes form as well, and the swimming conditions are best in the summer months. On the bay's east side is a lava terrace that has several large tide pools, all of which are home to an abundance of marine life.</p>
                <p>The Four Seasons Resort at Manele is located on this bay, and Hulopo'e Bay is the main beach on Lanai that resort guests use (the other beaches on the island are not suitable for swimming). If you arrive on Lanai with the ferry from Maui, Hulopo'e Bay is within walking distance from Manele Bay where the ferry arrives.</p>
                
