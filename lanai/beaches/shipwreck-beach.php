                <h2 class="small">Kaiolohia (Shipwreck) Beach, Lanai</h2>
                <p>Shipwreck Beach on Lanai, also known as Kaiolohia  (<a href="../../lanai/beaches/more_shipwreckbeach_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>), is known for its shallow reef and strong currents. It has wrecked many ships in the past, hence its name. The <em>Alderman Wood</em>, a British vessel got into trouble and sank here in 1824 &ndash; which is the first documented shipwreck. Two years later, the <em>London</em>, an American ship, met its fate. The <em>London</em> was believed to have gold and silver on board, and it is uncertain how much of it was ever recovered. </p>                     
                           <? include ("../../theme/text.html") ?>  
                                <p>The shipwreck after which this beach is named that can still be seen today is popularly referred to as a World War II Liberty Ship. However, it is a ferrocement (reinforced concrete) gasoline barge built for the Navy in 1943. Instead of a name, these type of ships were just given a number designation. This particular ship was YOGN 42. It was purposely grounded. After the war ended, this was the most economical way to dispose of the vessel.</p>
                <p>Shipwreck Beach is a remote beach on Lanai's north shore, and besides a few green sea turtles sunning themselves on the beach and maybe a lonely fisherman, there's only peace and quiet here. A short walk past the Shipwreck sign, there is a trail that leads about 200 yards (180 m) inland to the Kukui Point petroglyphs.</p>
                <p>The white, sandy beach is a beachcomber's delight, revealing shells, rocks and odd debris thanks to the strong winds and slamming waves that pound the shore. Stroll the 8 miles (13 km) of Shipwreck Beach and hike up the overlooking hill for a one-of-a-kind view. </p>
                <p>A once-functional lighthouse nearby lies in ruins, adding to the ancient feeling of the scene. Due to the strong currents, only highly-experienced surfers and bodyboarders should brave these waters. </p>
                
