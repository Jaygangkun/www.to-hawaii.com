                <h2 class="small">Manele Bay, Lanai</h2>
                <p>Manele Bay has Lanai's only public harbor. It&rsquo;s where you arrive when you take the ferry from Maui to Lanai. The bay is located on Lanai&rsquo;s south coast, eight miles south of Lanai City. </p>
                <p>There is only a small white sand beach at the east end of the bay. It's a popular destination for most visitors because it&rsquo;s the easiest beach to access while on Lanai and because it offers good snorkeling and diving conditions. </p>
                <p>The ocean bottom is shallow and consists of a mixture of sand and gravel. Manele Bay is a marine preserve, established to protect and preserve the area&rsquo;s marine resources and geological features. </p>
                <p>The bay has restrooms and showers and since the reef is so shallow, even the young ones can have fun in the water. Manele Bay isn&rsquo;t big, even though it is the main port for all the boats to rest in. However, it is a great location to just relax and have a picnic. </p>
