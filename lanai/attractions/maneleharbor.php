                <h2 class="small">Manele Harbor, Lanai</h2>
                <p>Manele Harbor, located on Lanai&rsquo;s south shore, has the only public boat harbor on the island. Visitors who take the ferry from Lahaina on neighboring Maui arrive here (five times daily), as well as a few fishing and snorkeling tour boats. Manele Harbor&rsquo;s activity is limited to traffic from excursions and charters, while the other harbor on the island, Kaumalapau, handles commercial shipping.</p>
                <p>There is a small pebble beach at the east corner of Manele Bay, which is a popular diving and snorkeling spot. But the swimming and sunbathing, there are better beaches on Lanai, such as neighboring Hulopoe Bay.</p>
