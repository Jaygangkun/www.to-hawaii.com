                <h2 class="small">Pu'u Pehe (Sweetheart Rock), Lanai</h2>
                <p>Pu'u Pehe, also known as Sweetheart Rock, is located on Lanai's south shore, between <a href="../beaches/manelebay.php">Manele Bay</a> and <a href="../beaches/hulopoe-beach.php">Hulopoe Bay</a>. It is a beautiful location, with red rocks and cliffs that contrast sharply against the deep blue ocean. The little rock island was once part of the mainland, but the constant erosive force of the ocean separated it over time. Inside Pu'u Pehe Cove (also known as <a href="../beaches/sharks-bay.php">Shark's Bay</a>) lies a small white-sand beach.</p>
                <p>An ancient legend tells of two lovers, a young warrior from Lanai named Makakehau and a beautiful Hawaiian girl named Puupehe from Lahaina on the island of Maui. The young warrior loved the pretty girl so much that whenever he looked at her, his eyes would mist up with tears. Hence his name Maka (eyes) and Kehau (mist). </p>
                    <? include ("../../theme/text.html") ?>
                <p>Makakehau brought Puupehe with him to live on Lanai and hid her in a sea cave at the base of a cliff because he was afraid to lose her. One day as he was out gathering supplies, he noticed a storm brewing and headed back to the cave. There he found his Puupehe drowned by the surge of the storm waves. </p>
                <p>He was so stricken with grief that he took his girl in his arms and cried out to the gods and his ancestors to help him climb up the steep cliff of the little rock island, where he buried her. He then jumped to his death in the powerful surf and sharp rocks below. </p>
                
