                <p>Jagged high cliffs, wide bluffs, crystal clear waters and miles of pristine coastline &ndash; these are the features of Kaumalapau Harbor on Lanai. This harbor is Lanai&rsquo;s principal commercial seaport. It used to be a busy port during the island&rsquo;s pineapple-growing period, when pineapple was exported. Today, Kaumalapau is a rather quiet harbor. It is surrounded by dramatic sea cliffs, which rise to heights of 1000 feet (305 m), making it an impressive sunset-viewing location. </p>
                <p>The harbor is often wrongly labeled as a beach, but the commercial nature of the area makes most water activities unsafe. It is, however, an excellent place for recreational fishing. Here you can fish from a boat dock or sit on one of the rock walls. </p>
                               <p>Also worth checking out are the Kaumalapau tidepools to the right of the harbor. Low sea cliffs surround the area and create a nice natural shield. If you&rsquo;re the adventurous type, take a walk over and see some rare species of small fish and other marine creatures. Be sure to bring a pair of reef shoes, and wash off the sunblock if you have any because the chemicals can damage the ecosystem within the little pools.</p>
                <p>Kaumalapau Harbor is approximately 7.5 miles (12 km) west of Lanai City. Access to the harbor is limited on Thursdays due to freight activity and security matters. </p>
