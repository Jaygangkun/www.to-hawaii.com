              <h2 class="small">Kanepuu Preserve, Lanai</h2>
              <p>Kanepuu Preserve, located near the center of the island of Lanai, is a vast garden of rare flowers and native birds. This 590-acre (2.4 sq. km) lot is home to one of the world&rsquo;s most fragile ecosystems, where dozens of native plants and avian fauna can be seen. Some of these plants, such as the Hawaiian gardenia, are highly endangered and can be found nowhere else on Earth. Also spread throughout this area are patches of native dry land forest, one of the fastest disappearing ecosystems in the world. </p>
              <p>Many of the native plants have played a central role in local livelihood for centuries. The aiea, a particularly rare tree, was once used to build canoes and sustained many fishing villages. The lama (derived from the Hawaiian word for light) was used to build sacred fences, temples and offerings to Laka, the goddess of hula. Other plants worth looking at are the olopua, a native olive tree, and the mau hau hele, a rare local hibiscus.</p>
                  <? include ("../../theme/text.html") ?>
              <p>Among the park&rsquo;s main highlights is a short loop trail, which features native artwork by Hawaiian artists. The art is both a tribute to Kanepuu&rsquo;s beauty and a call to preserve its treasures. </p>
              <p>The Kanepuu Preserve is roughly a 20-minute drive from Lanai City. You can tour the park by yourself with a map, which is available at the entrance. There are also guided tours for large groups, offered by the Nature Conservancy of Hawaii. </p>
              
