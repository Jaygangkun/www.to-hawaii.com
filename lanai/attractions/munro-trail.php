                  <h2 class="small">Munro Trail, Lanai</h2>
                  <p>The most popular hike on Lanai is the 7-mile (11 km) long Munro Trail (<a href="../../lanai/attractions/more_munrotrail_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>), which leads up to Lanaihale, the highest peak on the island, at an altitude of 3,366 feet (1,026 m). This is the only spot in the state of Hawaii from where one can see five other Hawaiian islands, but only on a very clear day (the islands are Oahu, Maui, Molokai, Kahoolawe and the Big Island). The Munro Trail is a one-lane dirt road that is accessible by foot, bike or four-wheel drive. </p>
                     <? include ("../../theme/text.html") ?>
                  <p>The trail is named after George Munro, a naturalist from New Zealand who arrived on Lanai in 1890. In 1911, a lone Norfolk Island pine, planted at Koele in 1875, alerted ranch manager George Munro to the importance of making use of the fog that comes down from the island&rsquo;s highest peak. </p>
                  <p>Munro realized that the pine boughs collect water from the fog and clouds. So he started to plant pines across the island. The pines seen around Lanai today are Cook Island pines, which were initially planted under Munro&rsquo;s management. Thanks to him, Lanai now has an expanding forest. (Tests have shown that Cook pines can produce up to 20 inches (508 millimeters) of additional rainfall per year).</p>
                  <p>The Munro Trail starts at the Lanai Cemetery behind the Lodge at Koele. It takes you straight up through the valleys and the lower mountain peaks. Along the way, you can stop at various lookouts, such as the mystic Maunalei Gulch and the distant Hookio Ridge.</p>
                  <p>The Munro hike can take the better part of your day, depending on your pace and number of stops. Alternatively, you can take a bike up the trail, provided the weather is good and you&rsquo;re sure you can handle rough terrains. The same goes if you plan to explore the trail with a four-wheel drive. Also make sure to arm yourself with lots of water, comfortable clothing and a good camera &ndash; this is certainly an experience you&rsquo;ll want to document. </p>
                  
