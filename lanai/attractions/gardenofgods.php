                <h2 class="small">Garden of the Gods, Lanai</h2>
                <p>Legend shrouds the mystic rock formations that make up the Garden of the Gods on Lanai, located about seven miles (11 km) north of Lanai City. Known to locals as Keahikawelo, the Garden is a vast expanse featuring large boulders in a spectrum of red, orange, purple and earthen colors, scattered so perfectly that you&rsquo;d think they were placed here by hand.</p>
                               <p>According to scientists, the rocks were formed over thousands of years through natural erosion, leaving pinnacles and buttes over the soil. But even some geologists are baffled by the rocks&rsquo; artful arrangement and the way some of them are stacked in perfect balance. One legend claims that the rocks fell from the gods&rsquo; gardens in the sky (hence its name), while another says they hold the spirits of ancient Hawaiian warriors.</p>
                                   <? include ("../../theme/text.html") ?>
                <p>Theories aside, the Garden of the Gods is easily one of the most beautiful spots on Lanai. From the far end of the garden, you can look out at the Pacific Ocean and make out the nearby islands of Molokai and Maui. Depending on the time of day you visit the garden, the rocks&rsquo; colors will look differently. Sunrise and sunsets are great here, as is a nighttime visit during full moon. It&rsquo;s a truly otherworldly experience. </p>
                <p>Like the rest of the island, the road leading to the Garden is unpaved. It&rsquo;s a dirt-road and the easiest way to access the area is with a four-wheel drive vehicle, or you can hike here. If you&rsquo;re lucky, you&rsquo;ll see the occasional native deer grazing the meager grass that grows in small patches on the trail. It&rsquo;s a long hike, but it&rsquo;s more than worth it. </p>
                
