              <p> <a href="../../map-lanai-attractions.html" target="_blank"><img src="../../theme/images/lanaiattractions.jpg" alt="Lanai Attractions Map" align="right" border="0"/></a>The island of Lanai is the smallest of the main Hawaiian Islands (excluding Niihau and Kahoolawe) as well as the least populated one. There is just one small town in the center of the island, where not much is going on. But that&rsquo;s the whole point of coming here &ndash; to see a Hawaiian island that doesn&rsquo;t know crowds, traffic or stress. </p>
              <p>Some people who come to Lanai don&rsquo;t bother doing any sightseeing because they think there is nothing there. But it all depends on how you define &ldquo;nothing.&rdquo; Sure, you won&rsquo;t find shopping malls or entertainment complexes. </p>
              <p>However, what you will find is some of the most remote beaches in Hawaii, where sea turtles feel comfortable to sun themselves right on the beach, ancient Hawaiian petroglyphs (rock carvings), mysterious locations, such as the Garden of the Gods, and scenic hiking trails. Man-made attractions include two renowned championship golf courses. Below is a list of some of the best things to do on Lanai.</p>
