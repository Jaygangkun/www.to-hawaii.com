                <h2 class="small">Lanai Art Center, Lanai</h2>
                <p>Art buffs exploring Lanai won&rsquo;t have a lot of luck: there&rsquo;s only one multicultural community arts program on the island. But the Lanai Art Center more than makes up for it with its impressive collection of local and international art, year-round exhibits and frequent cultural events. The center often hosts shows and visits by famous artists, both from Hawaii and the U.S. mainland.</p>
                <p>What sets the Lanai Art Center apart is that you don&rsquo;t just get to view art &ndash; you also get to create your own. Workshops in various fields are offered throughout the year, from basic arts and crafts to specialized fields, such as calligraphy, woodworking and silk and textile painting. A frequent hit with visitors is the class on lei-making, a distinctly Hawaiian but surprisingly simple craft.</p>
                    <? include ("../../theme/text.html") ?>
                <p>The gallery also features works by Lanai residents, most of which are up for sale. Each piece is unique in its own right and very reasonably priced. Sometimes you can even meet the artists themselves &ndash; many of them show up regularly to give lessons or do volunteer work as part of their commission deal.</p>
                <p>The center is divided into mini-galleries featuring different art genres, with some rooms used for occasional art exhibits. Those worth checking out include the ceramics room, where you&rsquo;ll find ceramic products handmade by Lanai residents, and the stained glass and mosaics studio, where you can watch and learn the art with other visitors. One-day classes are offered for around $25, and longer courses are offered with facilities in the back of the gallery.</p>
                
