              <h2 class="small">The Challenge at Manele, Lanai</h2>
              <p>Built on lava fields and carved out of the rugged  coastline, the Manele Golf Course is located high above Hulopoe Bay. The course  offers stunning and colorful views from every part. Red cliffs rise from the  blue waters of the ocean, blossoms spring from cracks in the black lava rocks,  emerald-green fairways stretch along the 7,039&ndash;yard layout, and white sand  bunkers dot the landscape. </p>
                  <? include ("../../theme/text.html") ?>
                           <p>This Jack Nicklaus signature 18-hole, par-72  championship course was carefully crafted to preserve the ancient ruins within its  confines. The terrain at Manele Golf Course is  unspoiled, dramatic, and natural, with majestic ocean views from every hole. At  the starting tee from above the Pacific Ocean at Hulopoe Bay, players may face what is perhaps  the toughest water hazard in the world. With three holes situated on cliffs,  and the ocean as a water hazard, some holes require shots over gaping ravines,  and the 12th in particular is taxing where a shot must carry over  200 yards of surging water. However, the five-tee layout facilitates players of  all abilities. The Challenge at Manele features a number of short par-4s and tough  par-5s that weave deviously and join to create nine great doglegs. Still, this  course is more open and more forgiving than its upcountry counterpart, the  Nicklaus-inspired Experience at Koele, whose setting&nbsp; is of the mountainous and forested variety.  Nonetheless, players at Manele will need to remain focused to combat the many  severe challenges of this course.  Perhaps the 180-degree panoramic views of the ocean from every hole and the chance of  spotting the odd whale during the winter is compensation enough.</p>
              <p>The facilities at Manele Golf Course are fairly comprehensive. Golf carts are available, as  well as a club rental service, a driving range, a practice green, and a pro  shop. Lessons can also be provided as required. While there is no bar at the  clubhouse, there is a restaurant, which opens from 11.00am to 3.00pm. Other  dining options and conference facilities are available through the resort&rsquo;s  hotel.</p>
              
