              <p><img src="../../images/lanai_camping.jpg" alt="Lanai Camping" style="width:100%; max-width: 478px;"  /></p>
              <p>There is only one official campground on Lanai, located at <a href="../../lanai/beaches/hulopoe-beach.php">Hulopoe Beach Park</a>. The shaded picnic areas are perfect for leisurely meals al fresco, offering a nice view of the beach. Hulopoe Beach is as one of the most scenic spots on Lanai and the island's best swimming beach. It is located on the island's south shore. The beach is also used by local residents and hotel guests from the nearby Four Seasons Resort at Manele Bay. </p>
              <? include ("../../theme/text-desktop-only.html") ?>
              <p>There are currently eight campsites at Hulopoe, each one has room for up to 5 people. There are two bathrooms in the park, but no hot water showers (only cold water beach showers). Barbecue grills and picnic tables are available as well.</p>
              <p>The camping permit can be obtained from Castle &amp; Cooke. Camping fees are  $80 per night for up to 4 people (regardless of age) for a maximum of 3 nights.  Camping  permits are issued upon arrival at the beach park. For campsite reservations and information, contact Dony Haban, Park Manager, Castle and Cooke Resorts, PO Box 630310, Lanai City, HI 96763 or call (808) 565-2695.</p>
              <p>The beach park is roughly a 7-mile (11 km) drive from Lanai City. Before settling down, make sure you have all the supplies you need. There is no store or supermarket at the beach park, only a small store at Manele Harbor. The only supermarket on the island is in Lanai City. You'll have two transportation options to get to Lanai City: rent a car or call a taxi.</p>
