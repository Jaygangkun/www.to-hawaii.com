                Volcanic smog (vog) on Lanai</strong></p>
              <p>Vog (volcanic smog) is much more common on the Big Island of Hawaii than on Lanai since the Big Island is the only Hawaiian island that still has active volcanoes. Occasionally when the winds are blowing from the south (known as Kona winds), the vog from the Big Island's volcanoes affects the air quality of the other Hawaiian islands, including Lanai.</p>
              <p>                On those days you will notice a haze in the air, especially when looking into the distance. While most people can tolerate vog and won't notice any difference in how they are feeling, for those with asthma or other breathing problems vog is much more of an issue and it is best be avoided by staying indoors.</p>
