              <p>                Visitors to Lanai have accommodation options either in <a href="../lanai/cities/lanaicity.php">Lanai City</a> or on the island's south shore at <a href="../lanai/beaches/hulopoe-beach.php">Hulopo'e Bay</a>. Staying at the oceanfront resort has a more tropical feel (with the ocean breeze, warm air and palm trees), while staying in Lanai City offers more of a mountain environment, with the scent of Norfolk pines in the often times crisp air.</p>
              <p>                Lanai's average ocean temperature is around 75&deg;F (24&deg;C) in February and 82&deg;F (28&deg;C) in September.</p>
