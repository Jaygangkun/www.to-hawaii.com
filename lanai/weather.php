              <p>The climate in the Hawaiian Islands is influenced by the predominant trade winds, which are blowing from the northeast about 90% of the time in the summer months (May to September) and about 50% of the time in the winter months (October to April). As moisture-laden clouds approach the Hawaii Islands from the northeast, they get stuck on the islands&rsquo; mountains, where the moisture comes down as rain. <br />
                <br />
                Lanai island lies in the lee (or rain shadow) of the West Maui Mountains on neighboring Maui (which rise to an elevation of 5,788 feet / 1,764 m). Therefore, most of the rain clouds that these trade winds bring with them come down on Maui and don't reach Lanai. Lanai is an arid island. Its highest peak, Lana'ihale (rising 3,370 feet / 1,030 m) receives most of the island's rainfall, about 30-40 inches (760-1,015 mm) per year. The rest of the island receives only about 10-20 inches (250-500 mm) per year. </p>
                           <p> Lanai's only town, Lanai City (population 3,102 as of 2010), is located near the center of the island at an elevation of 1,644 feet (501 m). The temperature is therefore a bit cooler and the air a bit less humid in Lanai City than at sea level. In the winter months, it can get chilly in Lanai City at night. </p>
