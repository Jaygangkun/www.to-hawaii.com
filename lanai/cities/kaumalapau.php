                <p>Located on the west side of Lanai is the coastal village and harbor of Kaumalapau. The harbor was built by James Dole, the founder of the Hawaiian Pineapple Company. He purchased the entire island for $1,100,000 because he planned to turn Lanai into the world's largest pineapple plantation (which he later accomplished). </p>
                <p>He knew he had to build a harbor to ship the crops by barge to the cannery in Honolulu. In 1926, Kaumalapau Harbor was completed and included a 400-foot-long (122 m) concrete wharf and a large harbor basin with a minimum depth of 30 feet (9 m). </p>
                <p>At the peak of its operation, Kaumalapau Harbor was a model town. All the social necessities and machineries needed for efficient pineapple production were available. Lanai's pineapple industry ended in 1980 because the fruits could be produced cheaper in other parts of the world.</p>
                <p>Kaumalapau Harbor can be reached via Highway 440, 3.5 miles (5.6 km) west of the airport. At present, the harbor is used by cargo boats to bring supplies to Lanai and is closed to visitors on barge days, which are Wednesdays, Thursdays and Fridays.</p>
