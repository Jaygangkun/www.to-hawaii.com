                <h2 class="small">Koele, Lanai</h2>
                <p>Walter Murray Gibson, an adventurer from the southern United States, settled at the Palawai Basin on Lanai in 1862. He was a representative from the church of the Latter Day Saints and was tasked to develop a Mormon colony for Native Hawaiians in the area. But, after a few years, he lost interest and used the church's money to buy some land properties to start his interest in ranching. </p>
                <p>One of the properties he bought was an area called Koele. Koele got its name from the ancient Hawaiian word, <em>ko'ele</em>, which literally means a small land, cultivated and farmed for the sustenance of a village chief. Figuratively, Koele describes the dark rainy clouds, drawn down across Koele from Kaiholena Valley and the slopes of Lanaihale (Lanai's highest peak) that lie above. </p>
                    <? include ("../../theme/text.html") ?>
                <p>A great ranch complex sprang from Koele and scattered around Lanai, which has a perfect climate for raising livestock. Some 45,000 - 50,000 sheep and lambs, 600 horses, 500 horned cattle, goats and hogs and numerous wild turkeys inhabited the island in 1893. Koele evolved from a small village to a 250-people ranch complex, which had 30 residences, a local store, a one-room school house, offices and outlying buildings. Everything was focused on ranching.</p>
                <p>By 1950, the pineapple industry on Lanai flourished. The livelihood in Koele changed from ranching to farming. At present, Koele is a prime tourist destination. One of the most popular places to stay in Koele is the Four Seasons Lodge, which is a luxury hotel that offers guests activities like horseback riding, golf and other outdoor activities.</p>
                
