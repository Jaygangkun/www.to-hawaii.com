                <h2 class="small">Kaunolu, Lanai</h2>
                <p>Located on Lanai's southwestern tip, Kaunolu (<a href="../../lanai/cities/more_kaunolu_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is an ancient Hawaiian fishing village that was abandoned in the 1880s. The main attraction here is the largest concentration of stone ruins on the island, including <a href="../../lanai/attractions/halulu-heiau.php">Halulu Heiau</a> (ancient temple site). This Kaunolu Village archaeological site was added to the National Register of Historic Places in 1966. There is an interpretive trail that leads through the ancient village.</p>
                    <? include ("../../theme/text.html") ?>
                <p>The Kaunolu Gulch is a freshwater stream that runs into the ocean here, which provided water for the village people. The offshore waters are deep and provide good fishing conditions. King Kamehameha the Great often times visited here and enjoyed the fishing. His house used to stand across the gulch from Halulu Heiau on the edge of a cliff. It is said that he used to hold ceremonies at the heiau between 1778 and 1810.</p>
                <p>Kahekili, the high chief of Maui, also used to visit here during the late 1700s. Northwest of the heiau, look for a natural stone wall running along the sea cliff. Near the cliff's edge, there is a break in the wall and a steep 80-foot (24 m) drop, known as <strong>Kahekili's Jump</strong>. It was here where King Kamehameha the Great would test the courage of his warriors by making them jump into the surging waters below. </p>
                
