                <h2 class="small">Lanai City, Lanai</h2>
                <p>Lanai City (<a href="../../lanai/cities/more_lanaicity_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>) is the main residential development on the island of Lanai, located in the center of the island. It was recently (in April 2009) named one of the top most endangered historic sites in the United States. </p>
            
                <p>The town was developed by James Dole, Hawaii's pineapple baron, in the early 1920s and has a feature that none of the other Hawaiian islands have &ndash; an intact plantation town. Lanai City has vintage homes, an old courthouse, jail and police station. The historic district is threatened by a proposal for commercial development, which would demolish 15 to 20 old buildings.</p>
                    <? include ("../../theme/text.html") ?>
                <p>Throughout the 20th century, Lanai was home of the largest pineapple plantation in the world, producing over 75% of the world's pineapple. The plantation  spanned over 20,000 acres (81 km&sup2;). Back then Dole owned the entire island and brought thousands of plantation workers here (mainly Filipino immigrants). He built Lanai City to house the workers.</p>
                <p>Today, the majority of the island is owned by Oracle Corp. CEO Larry Ellison, who in May 2012 signed an agreement to buy most of the island (around 98%) from the previous land owner, Castle &amp; Cooke, Inc. Ellison reportedly paid between $500 million to $600 million.</p>
                <p>Since Lanai's population is very small, Lanai City is the island's main business area. Most of the island's shops and eateries are located in the small town square that surrounds Dole Park. Tourism plays an important role on the island today.  There are two large resorts, the Four Seasons at Manele Bay, located on the island's south shore, and the Four Seasons Lodge at Koele, located near Lanai City.</p>
                
