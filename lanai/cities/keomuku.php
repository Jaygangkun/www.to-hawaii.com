                <h2 class="small">Keomuku, Lanai</h2>
                <p>Keomuku (<a href="../../lanai/cities/more_keomoku_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>) is located on the east shore of Lanai. During ancient times, fishermen and farmers settled along the coastal portions of this area up to the valley of Maunalei. Keomuku was a small and sleepy fishing village up until 1899, when the Maunalei Sugar Company moved in, turning the village into a bustling sugar plantation. </p>
                <p>Over 500 workers, primarily Japanese, were brought here to work in the sugar cane fields. Additional housing structures, a pier and a railroad were built, and the harvested sugar cane was shipped to Maui, where it was milled. The sugar industry at Keomuku was thriving until 1900, when the plague that began in Honolulu on neighboring Oahu, hit the village. </p>
           <? include ("../../theme/text.html") ?>
                <p>Lanai's plantation workers were hit hard. But it got even worse for the Maunalei Sugar Company, when the freshwater sources that were used to irrigate the fields turned brackish. This forced the company to shut down its operations in 1901.</p>
                <p>There is another story that explains why the Maunalei Sugar Company was forced to shut down. According to a local legend, the sugar operation was successful until the company built the railroad along the coast, damaging the sacred stones of a nearby heiau (Hawaiian temple). The Hawaiians believed that this angered the gods and that this is the reason why the sugar mill&rsquo;s drinking water turned salty and the population was wiped out by an epidemic. </p>
                <p>Today, Keomuku lies abandoned and is called a ghost town by some. Little remains of the village and there is little evidence that once a thriving sugar industry was located here. The only structures that are left standing are a few old wooden houses and the original structure of the Ka Lanakila o Ka Malamalama Church, which was completed in 1903. It is the last structure that is still intact in the village. Also still there is a small locomotive and its tracks (look for the sign that leads to it).</p>
                <p>To reach Keomuku, take Keomuku Road or route 430. Expect six miles (9.7 km) of crystal clear waters along the narrow black detrital sand beach and only peace and quiet. Though there are no tourist facilities in Keomuku (most visitors to the island don't come out here), the area offers sheer natural beauty and teeming ocean life, which makes it great for nature lovers, fishermen and beachcombers. On the shore on either side of the village are the remains of two of Lanai's ancient Hawaiian fishponds. The pond walls can only be seen at low tide.</p>
                
