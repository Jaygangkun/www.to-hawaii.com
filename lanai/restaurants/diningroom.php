                <h2 class="small">Dining Room at the Lodge at Koele, Lanai</h2>
                <p style="color:#F00"><strong>Permanently closed</strong></p>
      <p>The exclusive Dining Room at the Lodge at Koele is the epitome of style and  elegance. This dining establishment gives a sense of being entertained in an  estate living room. The atmosphere is intimate and romantic, complete with  wood-burning fires. The inspired menu offers a combination of locally-grown  ingredients with a contemporary twist applied to classic cooking methods. The  cuisine is perfectly complemented with a selection of wines to provide a  pleasant and unrivalled dining experience.</p>
                     <p>Using an intuitive nature-to-table model, the innovative cuisine is  presented in a menu that is based on the clever use of locally-sourced  ingredients taken from various parts of the Hawaiian Islands. The produce and ingredients used  at this restaurant also take account of sustainability - the use of seasonal  produce and the careful harvesting of nature's bounty. Visitors to the Dining  Room are offered a magnificent tasting menu made up of four courses. The menu  varies according to the availability of fresh and seasonal ingredients. Each  week, the tasting menu offers the finest quality ingredients from around the  islands and, indeed, from all over the world. Examples of the culinary  offerings at various times include Fish and Seafood from Kona, Venison from  Molokai, Mushrooms from Hamakua, Vegetables from Kula Maui, Lamb and Beef from  Kahua Ranch, Artisanal Cheeses from Hawaii Island and Honey from Kiawe. The Sea  Salt and Olive Oils used by the Dining Room come from different locations  around the islands. All dishes are suitably paired with a complementary wine to  ensure a flawless culinary experience.</p>
                <p>An acclaimed restaurant and showcase for the sustainable use of local  produce, the Dining Room<strong> </strong>at the Lodge at Koele is open from Tuesday to  Friday, with dinner served from 6.00pm until 9.00pm. With an emphasis on upcountry  Hawaiian Cuisine, the Caf&eacute;s Tasting Menu is a spectacular affair that takes  diners on a memorable journey of new ingredients, new flavors and new  techniques. </p>
