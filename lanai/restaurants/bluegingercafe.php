                <h2 class="small">Blue Ginger Caf&eacute;, Lanai</h2>
                <p>A local favorite in the center of Lanai, the Blue Ginger Caf&eacute; is a small,  family-friendly Caf&eacute; that serves authentic island cuisine. The dining area is  comprised of just a few tables set on a front porch facing Dole Park, with a number of tall Norfolk pine trees to its front. Not only  is this a restaurant, it is also a bakery that makes its own tasty bread and  hamburger buns on a daily basis. Additionally, the sandwiches are tasty and the  hamburgers home-made with fresh ground beef. </p>
                    <p>Essentially, the Blue Ginger Caf&eacute; is a low-cost option compared to some  of the other upmarket restaurants on the island, especially those that are  located in hotels and luxurious resorts. The menu features a host of island  favorites, the type of food eaten by local residents every day. Popular dishes  include hamburgers (on the home-made buns produced by the on-site bakery), mahi  mahi served with mushroom and capers, and fried saimin. Typical breakfast fare  includes eggs (on their own or served with meat), vegetable or babyshrimp  omelets, French toast, and blueberry or banana pancakes. The lunch menu is  mostly comprised of a sandwich selection, plate lunches and fries. Those seeking  dinner at the Blue Ginger Caf&eacute; are offered fish, shrimp, saimin, stir-fry  dishes or any house specials that are featured on the day. A choice of drinks  is also available.</p>
