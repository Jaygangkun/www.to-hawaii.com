          <h1>Lanai Events</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <div style="clear:both"></div>
              <p>Like the rest of the Hawaiian islands, events take place on Lanai throughout the year. Here&rsquo;s a timeline of the most popular Lanai events throughout the year.</p>
              <p><strong>March<br />
                <br />
                </strong>The Annual Keiki Catch: Families will enjoy this youth-oriented event held at the island&rsquo;s school district. Keiki is the Hawaiian word for&ldquo;children.&rdquo; The Keiki Catch is aimed at fostering children&rsquo;s relationship with the ocean. If you&rsquo;re going alone, don&rsquo;t worry - the event is open to adults and you can &ldquo;adopt&rdquo; a child for the day.</p>
                <? include ("../../theme/text-desktop-only.html") ?>
              <p><strong>May<br />
                <br />
                </strong>Lei Day Celebration, Annual Surf Clinic: Hawaii&rsquo;s version of May Day, Lei Day celebrates the state&rsquo;s most enduring symbol and includes shows, workshops and festivals showcasing locally made leis. There are also lei-making contests and craft shows held on Lanai and the rest of the islands.</p>
              <p>If you&rsquo;re more into sports, you might enjoy the Annual Surf Clinic at Manele Bay. It&rsquo;s designed for beginners, but more experienced surfers can take part and help out. Finish the course and you qualify for the next year&rsquo;s Summer Competition - definitely a good excuse to come back!</p>
                           <p><strong>June<br />
                <br />
                </strong>King Kamehameha Celebration: Between the first and second week of the month, Hawaiians gather to honor King Kamehameha I, the leader who unified the Hawaiian islands over two centuries ago. While the event is centered in North Kohala on the Big Island of Hawaii, the King&rsquo;s birthplace, all the Hawaiian islands celebrate with flower festivals, dances and various cultural attractions.</p>
              <p><strong>July<br />
                <br />
                </strong>Pineapple Festival, Lanai Obon Dance: Culturally speaking, July is one of the busier months on Lanai, with at least two major events taking place. For food buffs, the annual Pineapple Festival is certainly a must-see. Sponsored by Destination Lanai, the festival celebrates Lanai&rsquo;s former plantation industry. Art exhibits, free tastings and cooking contests are some of the events.</p>
                                 <p>The Obon Dance, a tribute to the island&rsquo;s Japanese warriors, is held in the second week of July and features a unique dance performed amidst bamboo poles, paper lanterns and colorful paper flowers. People can watch from the sidelines or join in the dance. Afterwards, everyone gathers for a Japanese feast featuring delicacies such as sushi, chowfun and saimin.</p>
              
              <p><strong>September<br />
                <br />
                </strong>The Aloha Festival: September is the best month to visit if you&rsquo;re looking for cultural immersion. The main attraction at this time is the Aloha Festival, a statewide celebration of Hawaiian culture and arts. Dances, parades and street festivals are held from mid-September all the way to October, with each island playing host for a week.</p>
                
              <p><strong>November<br />
                <br />
                </strong>Hawaii International Film Festival: Throughout the first half of November, moviegoers are treated to a series of film screenings featuring directors fromthe U.S., Asia and the Pacific. Screenings traditionally take place on Oahu, but some of the events are held on the neighboring islands.</p>
