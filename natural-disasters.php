                        <p>Hawaii is truly a paradise. However, the islands are vulnerable to certain kinds of natural disasters, such as flooding, hurricanes, tsunamis, lava flows and earthquakes. </p>
                        <p>In March 2006, more than 30 days of torrential rain resulted in major damage from flooding, as well as serious public health issues. The following are some of the natural disasters that either have occurred or could occur in Hawaii. </p>
                        <p><strong><img border="0" src="images/flood.jpg" width="148" height="125" align="left" alt="manoa flood" />Flash Floods</strong></p>
                        <p>When there is heavy rain, some parts of each of the Hawaiian Islands are susceptible to flash floods. Heavy rain can turn a small, quiet river into a cascading one that sweeps everything away that comes into its way. Every few years, even lives are lost in flash floods, mostly people swept away in their cars or hikers. An approaching heavy rain can be difficult to forecast and may come suddenly. </p> <?php
show_widgets('','https://www.to-hawaii.com/vacation-rentals.php','rating_desc');
?> <p>In Hawaii, flash floods are much more common than other natural disasters, such as tsunamis or hurricanes. It's not too long ago that serious flash floods hit the islands. In October 2004, a flash flood occurred in Manoa Valley on Oahu and completely soaked the ground floor of the University of Hawaii at Manoa Hamilton library. Besides destroying parts of the library, the flood trashed at least 60 homes and caused 
                          more than $1 million dollars in damage.</p>
                        <p>In March 2006, a six-week rain period caused flooding in many places in Hawaii. On Kauai, it caused a dam break killing seven people. On Oahu, the rain caused Waikiki's sewer system to overflow, resulting in a sewage spill that polluted parts of the 
                          island's south shore for several days.</p>
                        <p><strong>Hurricanes</strong><img border="0" src="images/hurricane.jpg" width="148" height="125" align="right" alt="Hurricane" /></p>
                        <p>Two hurricanes (Iwa and Iniki) left devastation and death in their wake after passing through the state. Hurricane Iwa hit the islands of Niihau, Kauai and Oahu on November 23, 1982. It was a Category 1 hurricane on the Saffir-Simpson Hurricane Scale. Iwa was the first major hurricane to hit Hawaii since statehood in 1959. Hurricane Iniki was the most powerful hurricane to strike Hawaii in recorded history. The eye of the hurricane passed over the island of Kauai on September 11, 1992 as a Category 4 hurricane causing six deaths and around $1.8 billion in damage. Not only are the high winds of a hurricane or cyclone very destructive, but a phenomenon called storm surge that comes with it. Storm surge (wind-driven high waves) causes severe flooding in coastal areas </p>
                        <p><strong>Tsunamis</strong></p>
                        <p>Hawaii is the U.S. state at greatest risk for a tsunami. Hawaii records about one a year, with a damaging tsunami happening about every seven years. Tsunamis usually occur after an earthquake in a coastal or oceanic region. </p>
                        <p>Early in the morning on April 1, 1946, an earthquake with a reported magnitude of 7.8 occurred in the Aleutian Islands off Alaska. Almost five hours later, at 7 a.m., the largest and most destructive tsunami waves in reported history struck Hawaii, killing 159 people. Many were curious people, including school children, who ventured into the exposed reef area, not knowing the receding water to be a sign of an approaching tsunami. No warning was possible nor given for this tsunami.</p>
                        <p>Property damage totaled approximately $26 million. Maximum run-ups were reported to be 54 feet (16.5 m) on Molokai and 55 feet (16.8 m) in Pololu Valley on the Big Island. Waves in some areas penetrated more than half a mile inland.</p>
                        <p>Tsunamis are generated by earthquakes and travel at varying speeds. The fastest ones can reach 400-500 mph (645-805 kmh). In the open ocean, a tsunami is only about one foot high, so that it would pass a ship unnoticed. Once it hits shallow water, it slows down and builds up to a high, abrupt front. Tsunamis hit the land as a series of coastal waves and the largest wave is usually somewhere in the middle of the set.</p>
                        <p>There may be no damage at all in one coastal area, while in another one, the waves can be very large and powerful. The waves can reach inland as far as 1,000 feet (305 m) or more, covering the land with water and debris. As these powerful tsunami waves flow back into the ocean, they carry people and loose objects with them.</p>
                        <h2>Volcanoes</h2>
                        <p>There are several kinds of events caused by volcanic activity that can be harmful to life and property. These include lava flows, ash falls and debris avalanches.</p>
                        <p><img border="0" src="images/lava.jpg" width="148" height="125" align="right" alt="lava" /> Magma, which is molten rock, that flows from the interior of the Earth to the surface is called lava. The higher the silica content of the lava, the thicker and stickier it becomes. Low-silica basalt lava usually flows faster because it is more liquid. It can flow as fast as 10-30 miles per hour (16-48 kmh) or spread out and cover vast areas of land up to a few miles wide.</p>
                        <p>Such basalt lava flows erupted at Kilauea Volcano on Hawaii&rsquo;s Big Island between 1983 and 1993, destroying almost 200 nearby homes and covering the coast highway on the island&rsquo;s southeastern shore. As the lava flows down the volcano, it burns and buries everything in its path.</p>
                        <p>As they erupt, volcanoes also emit gases, of which the most common ones are water vapor (90 percent), sulfur dioxide, carbon dioxide, hydrogen and hydrogen sulfide. Sulfur dioxide can react with water in the atmosphere and come down as acid rain, which causes corrosion and has a damaging effect on vegetation. Carbon dioxide can also be dangerous because it tends to collect in valleys (because it is heavier than air) where it can accumulate in toxic concentrations and cause people and animals to suffocate.</p>
                        <p><strong>Earthquakes</strong></p>
                        <p>Earthquakes in Hawaii are closely linked to the islands' volcanoes. Even though hardly noticeable, thousands of earthquakes happen every year beneath the Big Island of Hawaii. The Big Island is the youngest of the Hawaiian Islands and is still growing today. Here, flowing erupting volcanoes and flowing lava can be witnessed. The island's active volcanoes are Kilauea, Mauna Loa and Loihi. Eruptions and magma movements within these volcanoes are usually accompanied by frequent small earthquakes. There earthquakes, also called volcanic earthquakes, originate in regions of magma storage 
                          or along paths that magma follows as it rises and moves before eruption.</p>
                        <p>Other earthquakes that can occur in Hawaii are called tectonic earthquakes, which can happen in areas of structural weakness at the base of the Hawaii's volcanoes or deep within the Earth's crust beneath the island. In the last 150 years, a few strong tectonic earthquakes (magnitude 6 to 8) caused major damage to buildings and roads and even triggered local tsunami. The most destructive earthquake in Hawaii occurred on April 2, 1868, which killed 81 people. The earthquake had a magnitude of 7.9, destroyed more than a hundred homes and generated a 15 m high tsunami along 
                          Kilauea's south coast. </p>
                        <p>Even though earthquakes happen often in Hawaii, the majority of them are too small to be felt. The last one that happened on the Big Island of Hawaii that could be felt even on the island on Oahu, 170 miles (274 km) to the north of the epicenter, occurred on October 15, 2006. It had a magnitude of 6.7. The earthquake caused property damage, injuries, landslides, widespread power outages and airport delays and closures. The most severe damage occurred on the north and western sides of the Big Island of Hawaii. Damage was also quite heavy on the eastern side of Maui and minor damage 
                          spread all the way out to western Oahu.</p>
