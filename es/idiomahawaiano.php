                He suena como &quot;Heh&quot;<br />
                Hi suena como &quot;Hii&quot;<br />
                Ho suena como &quot;Hoh&quot;<br />
                Hu suena como &quot;Huu&quot;</p>
              <p>El okina es un s&iacute;mbolo que se parece a un ap&oacute;strofe ('). Se  usa para separar las palabras. El okina o "glottal stop" se encuentra s&oacute;lo  entre vocales. Los nativos usan el okina cuando quieren hablar o escribir en  hawaiano. El okina no es usado siempre en impresiones ni en internet. Pero es  usado mayormente en todos los peri&oacute;dicos hawaianos y muchos libros en  "Hawaiiana".</p>
