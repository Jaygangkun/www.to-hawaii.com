              <p><strong>Una Mirada al  Lenguaje Hawaiano</strong></p>
              <p>El lenguaje hawaiano consta de doce letras: cinco vocales y  siete consonantes. Tambi&eacute;n est&aacute; el &quot;okina&quot; y el &quot;kahako&quot;;  estos son signos de puntuaci&oacute;n que pueden cambiar la pronunciaci&oacute;n de cada  palabra.</p>
              <p>El alfabeto hawaiano es el siguiente:<br />
                A, E, I, O, U, H, K, L, M, N, P, W</p>
              <p>Las vocales son pronunciadas de manera diferente que en el  idioma Ingl&eacute;s.</p>
              <p>A se pronuncia &quot;ah&quot;<br />
                E se pronuncia &quot;eh&quot;<br />
                I se pronuncia &quot;ii&quot;<br />
                O se pronuncia &quot;oh&quot;<br />
                U se pronuncia &quot;uu&quot;</p>
              <p>Tradicionalmente, la letra "W" en el lenguaje hawaiano suena  como la letra "V" en el Idioma Espa&ntilde;ol.</p>
              <p>Cuando las consonantes est&aacute;n acompa&ntilde;adas de una vocal,  suenan as&iacute;:</p>
