            <h1>Poblaci&oacute;n de Hawaii</h1>
            <div class="midpanel_row">
              <p>La belleza asombrosa de Hawaii concuerda con la belleza de  su gente, que contribuye a crear ese esp&iacute;ritu Aloha que hacen de las islas un  lugar incomparable alrededor del mundo. Hawaii se destaca por su diversidad, un  aut&eacute;ntico crisol de culturas cercanas y lejanas.</p>
              <p>Cuando miramos la diversidad de Hawaii de estos tiempos,  tenemos un indicio de sus comienzos, cuando las islas comenzaron a habitarse  por los polinesios, miles de a&ntilde;os atr&aacute;s. </p>
              <p>Misioneros cristianos desde Nueva Inglaterra llegaron a las  islas en 1820, impactaron dram&aacute;ticamente en la poblaci&oacute;n nativa de Hawaii ya  que trajeron con ellos enfermedades, y as&iacute; se fue formando la poblaci&oacute;n  Cauc&aacute;sica o "haole" (extranjera).</p>
              <p>La mezcla cultural presente se da, en parte, gracias al  peregrinaje de trabajadores de anan&aacute; (pi&ntilde;as) y de ca&ntilde;a de az&uacute;car provenientes  de Portugal, Jap&oacute;n, China y otras naciones del este, en la mitad y fin del  siglo XIX. Otros inmigrantes que siguieron incluyen puertorrique&ntilde;os, filipinos  y coreanos. Aproximadamente el 43.5 % de los residentes actuales de Hawaii  nacieron en otros lugares, y es f&aacute;cil ver la raz&oacute;n por la cual las personas  viajan desde las distintas partes del mundo para hacer de Hawaii su hogar  definitivo.</p>
              <p>Hoy, la isla de Oahu, que ostenta m&aacute;s residentes y  visitantes que cualquiera de las dem&aacute;s islas juntas, es cari&ntilde;osamente apodada  "El lugar de encuentro". Samoanos, tonganos, hawaianos, chinos, japoneses,  maor&iacute;es, canadienses, tahitianos, vietnamitas, espa&ntilde;oles, escoceses,  irlandeses, italianos y dem&aacute;s etnias se combinan para dar vida a estas  maravillosas islas. Cualquiera sea la isla donde usted est&eacute;, seguro encontrar&aacute;  personajes de distinta procedencia, locales o turistas, llenos de sabidur&iacute;a e  historias para contar.</p>
              <p>La fascinante mezcla de culturas de Hawaii hace que no sea  raro escuchar varias lenguas distintas en las calles, incluyendo samoano,  portugu&eacute;s, japon&eacute;s, hawaiano, chino y pidgin (un dialecto local que combina una  mezcla de idioma hawaiano e ingl&eacute;s).</p>
                         <p><strong>El Turismo y el  Ej&eacute;rcito en Hawaii</strong></p>
              <p>Los &uacute;ltimos a&ntilde;os del siglo XIX marcaron el comienzo del  turismo lucrativo en las islas, una industria que creci&oacute; fuertemente cuando  Hawaii se convirti&oacute; en estado en 1959. El a&ntilde;o 2000 super&oacute; todos los records anteriores  con 6.9 millones de turistas ese a&ntilde;o en sus 16.635 Km2 (6.423 millas  cuadradas). A pesar de las consecuencias devastadoras para el turismo que dej&oacute;  a su paso el ataque del 11 de septiembre, la econom&iacute;a pudo volver a su cause  cuando la industria se recuper&oacute; en el a&ntilde;o 2004.</p>
              <p>Como puerta de salida al pac&iacute;fico, Hawaii resulta el hogar  de distintas instalaciones militares, todas con base en Oahu, creando as&iacute; una  alta cantidad de personal militar en las islas: La Base del Cuerpo de Marina  (MCB) Hawaii, Las Fuerzas Armadas, Cuarteles de Schofield y el Fuerte Shafter,  MCBH bah&iacute;a Kaneohe, La Base A&eacute;rea y la Estaci&oacute;n Naval Pearl Harbor.</p>
