              <h2>The Hawaiian Flag</h2>
              <p>The Hawaiian culture is notorious for its incorporation of deeply rooted symbolism and multiple-fold meanings. <strong class="subtitle">Ka Hae Hawaii</strong>, or the<strong class="subtitle"> Flag of Hawaii</strong>, is no exception. Ka Hae Hawaii is a beautiful and symbolic representation of the many facets of Hawaii's rich and unique history. The Hawaiian flag symbolizes the Kingdom of Hawaii, the republic of Hawaii, its 
                previous status as a territory and its current status as a member of the United States. As one of the oldest flags still in use, the Hawaiian flag flies proudly in many locations throughout the islands, including numerous government 
                buildings, schools and private homes. </p>
              <? include ("theme/text.html") ?>
              <h3>The Hawaiian Flag History</h3>
              <p>The story of how the Hawaiian flag came about is an interesting one. Although historians may disagree on the exact details, it is settled that Kamehameha the Great (Kamehameha I) was responsible for its inception. </p>
              <p>We can better understand the meanings behind the Hawaiian Flag by learning a little about the King who oversaw its creation. Kamehameha I ruled successfully, creating a single sovereign nation and securing recognition by worldwide major powers. Before King Kamehameha I's rule, the individual islands were governed by separate chiefs. King Kamehameha I was a skilled warrior credited with conquering and uniting the 
                eight major Hawaiian Islands. </p> <?php
