?> <p>The King was a determined ruler, dedicated to protecting the welfare and sovereignty of his people. He did not allow non-Hawaiians to interfere in island politics, but he played the role of ambassador well and strived to form harmonious relationships and alliances. His ruling prowess and ability to be a symbol of goodwill helped the Hawaiian Islands to become an important center for industry - during his reign, fur and sandalwood trade and coffee and pineapple exporting were significantly kick-started. </p>
              <p>Relationships with Britain - and Britain's status as a protectorate - were very important to King Kamehameha I. In 1793, a portion of the islands were ceded to the UK in communication with representative Captain Vancouver, a deal that was never really acted upon by the British. Some say today's Hawaiian flag was created after controversy surrounding the waving of both the Union Flag and American Flag. Each side opposed the flying of the other's flag, so a compromise was reached. The Hawaiian flag is notoriously seen as a melding of the U.S. and British flags. When the new Kingdom of Hawaii flag was introduced, it was not long before the UK, France, the U.S. and Japan granted their official recognition of the icon. </p>
              <p><strong>The Hawaiian Flag Design</strong></p>
              <p>In 1816, Kamehameha the Great commissioned the design to an officer of the Royal Navy. The UK influence on the design is obvious - in the left upper corner is the duplicated Union Flag, a symbol of Hawaii's alliance with the British. A second-fold meaning to the inclusion of the Union Flag is the symbolic representation of the alia, a chiefly symbol created by crossing two spears rooted with an upright spear.</p>
              <p>The eight horizontal stripes that make up the body of the flag represent the major Hawaiian Islands of Oahu, Maui, Lanai, Kauai, Kahoolawe, Molokai, Niihau and the Big Island of Hawaii. The first version was said to have contained seven horizontal stripes as opposed to today's eight stripes. And some say that the alternating white, red and blue stripe colors were placed by mistake; that they were intended to in a sequence of red, white and blue. </p>
              <p>Hawaii's flag means many things to different observers. Hawaii has the distinction of being the only state to have been formerly ruled by a monarchy. The Hawaiian flag not only represents Hawaii's struggles and progress in modern times, it also continues to pay homage to a time when the Hawaiian Kingdom allowed the alii (chiefs) to govern their people with absolute sovereignty. Many Hawaiians recognized this flag as a symbol of independence and a representation of the monarchy's opposed overthrow, pledging eternal allegiance to Hawaii as a sovereign nation. </p>
