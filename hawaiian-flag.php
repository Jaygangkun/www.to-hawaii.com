              <p> The American overthrow of the independent nation of Hawaii was not the first time sovereignty had been challenged. In 1843, British Lord Paulette claimed stake in the islands, only to be rebutted by the country's Admiral Thomas, who reaffirmed Hawaiian independence and re-granted recognition of the Hawaiian flag on July 31 of the same year. After this scuffle, Kamehameha III spoke the words that were to become the Hawaii state motto: "Ua mau ke ea ka aina i ka pono," the life of the land is perpetuated in righteousness. On May 20, 1845, Ka Hae Hawaii was dedicated at the opening of the Legislature of the Hawaiian Kingdom.&nbsp; </p>
              <p><strong>Hawaiian Flag Day</strong></p>
              <p>July 31st was named Hawaiian Flag Day by the former Governor John D. Waihee III, the first of Hawaiian descent to govern under the realm of statehood. Annual celebrations allow Hawaiian residents to celebrate the complex meanings represented in this historic symbol. </p>
