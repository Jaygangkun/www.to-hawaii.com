              <p>Same-sex marriage is legal in Hawaii since December 2, 2013. Governor Neil Abercrombie signed the Hawaii Marriage Equality Act, also known as Hawaii Senate Bill 1 (SB1) on November 13, which allows same-sex couples to legally wed in the state. Both same-sex and opposite-sex couples also have the option to formalize their relationships legally in the form of civil unions and reciprocal beneficiary relationships. While Hawaii civil unions provide the same rights and obligations of marriage at the state level, reciprocal relationships offer only limited rights. In addition to the state of Hawaii now allowing and recognizing same-sex marriages, the U.S. federal government is doing so as well (the federal government started recognizing same-sex marriages after the Supreme Court overturned the federal Defense of Marriage Act (DOMA) on June 28, 2013).</p>
