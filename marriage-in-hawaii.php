              <strong>How to Apply for a Marriage License </strong><br />
              <em>(courtesy of the Hawaii State Department of Health)</em>
              <ul>
                <li>The prospective bride and groom must appear together in person before a marriage license agent to apply for a marriage license. Proxies are not allowed.</li>
                <li>The prospective bride and groom should be prepared to provide the necessary proof of age and present any required written consents and approvals. All of the necessary documents should be obtained prior to applying for a marriage 
                  license.</li>
                <li>The prospective bride and groom must prepare an official application and file the application in person with the marriage license agent. Applications are provided by the marriage license agent or may be downloaded from the Hawaii State Department of Health website. The application will not be accepted if sent by either postal mail or e-mail.</li>
                <li>Upon approval, a marriage license is issued at the time the application is made.</li>
                <li>The marriage license costs $60.00. The fee may be paid online or in-person at the time the application is submitted to a marriage license agent. </li>
                <li>The marriage license is good only in, but is valid throughout, the state of Hawaii.</li>
                <li>The marriage license expires 30 days from and including the date of issuance, after which it automatically becomes null and void.</li>
                <li>If you do not get married within the 30 days, return the unused license in the pre-addressed envelope provided to you for invalidation.</li>
              </ul>
