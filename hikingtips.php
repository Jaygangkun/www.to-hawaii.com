                <h2>Hiking in Hawaii - general tips before you go</h2>
                <p>Hiking in Hawaii can be fun, adventurous, healthy and dangerous at the same time. There are plenty of hiking trails in Hawaii, from trails ideal for beginners to trails that are only recommended for advanced and well-trained hikers. Before you go, familiarize yourself with the trail and also be sure you know about the hiking hazards explained below.</p>
                                <? include ("theme/text.html") ?>
                <p><strong>Nightfall</strong></p>
                <p>Since Hawaii is located close to the Equator, it gets dark quickly. As the sun approaches the horizon and finally dips into the ocean, you won't have a lot of time left until it's completely dark. Sunset in the summer is around 7:15 p.m. and in the winter around 6 p.m. Plan to finish your hike well before nightfall.</p>
                <p><strong>Extreme weather conditions</strong></p>
                <p>Check the weather forecast before you head out. Avoid hiking in torrential rain as many trails get slippery then. Also don't hike when there is a
                  high wind or flash flood warning.</p>
               <p><strong>Narrow trails</strong></p>
                <p>Some of Hawaii's trails are on top of mountain ridges and quite narrow with steep drop-offs on one or both sides. Strong winds or rain can make it even more dangerous. Watch your step and be careful.</p>
                <p><strong>Rockfalls</strong></p>
                <p>Rockfalls are most likely to happen during or after heavy rain, but can occur during other times as well. Watch out if you're hiking near waterfalls and under steep rock faces. </p> <?php
