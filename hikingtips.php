?> <p><strong>Leptospirosis</strong></p>
                <p>This is a bacterial disease that causes flu-like symptoms about 2 to 14 days after infection. It can lead to liver damage and even death. To avoid contracting leptospirosis bacteria, never drink stream or lake water unless it has been boiled or chemically treated. This type of bacteria can enter one's body through the mouth, nose, eyes or cuts in the skin. So it is also best to avoid swimming in streams and lakes in Hawaii. If you do, make sure you don't have any scratches on your skin and don't splash around, so the water doesn't enter your nose, mouth and eyes.</p>
                <p><strong>Hyperthermia (overheating)</strong></p>
                <p>Hiking can be strenuous and the sun can be hot in Hawaii. To avoid overheating, you should take precautions. It is recommended to start your hike early in the morning so that you avoid the afternoon heat. Also take enough water with you and drink some water at least every half hour so you don't get dehydrated. Wear a lightweight hat and sunglasses.</p>
                                 <? include ("theme/text-2.html") ?>
                <p><strong>Hypothermia (low body temperature)</strong></p>
                <p>Despite Hawaii's tropical climate, hypothermia is still possible. It can get chilly on top of mountain ridges due to the high elevation, wind and rain. Take a light rainproof coat or plastic covering and a light sweater with you. Some warm clothing or a light blanket also comes in handy in case you get injured on the way and can't make it back until nightfall (in case you have to wait for help to arrive the next day).</p>
                <p><strong>Hunters</strong></p>
                <p>Some of the trails are near areas where hunting for pigs, birds and goats take place. Always stay on the trail because hunters usually avoid getting too close to trails. It's also a good idea to wear colorful clothes when hiking, so you're more visible.</p>
                                 <p><strong>Marijuana fields</strong></p>
                <p>Most trails don't lead to areas where there may be marijuana growers, but it is recommended to always stay on the trail. In case you do come across an illegal marijuana patch, leave the area immediately because there have been conflicts between hikers and marijuana growers in the past.</p>
                   <p><strong>Car break-ins</strong></p>
                <p>At some of the trailheads, theft and vandalism are serious problems, so it is best to not leave any valuables in your car. </p>
                <p>One last word of advice, take a cell phone with you if possible, especially if you plan to hike alone. You never know when you may need it. An ankle is easily twisted and some trails are more remote and it may be a while until another hiker walks by. In case of emergency, call 911 for assistance. Also, since the condition of each trail changes over time, always use your own judgment regarding the safety of a trail. </p>
