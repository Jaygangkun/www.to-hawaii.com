              <p> <strong>Towns</strong><br />
                <br />
                The main town of <a href="../molokai/cities/kaunakakai.php">Kaunakakai</a> on Molokai's south shore has one major thoroughfare where residents do all their shopping. This is also where the Kaunakakai Wharf, the longest wharf in Hawaii, is located. <a href="../molokai/cities/maunaloa.php">Maunaloa</a>, a small plantation town located in the hills on Molokai's west side, is the perfect place for an enjoyable stroll past quaint shops and art galleries. </p>
              <p><strong>Beaches</strong><br />
                <br />
                Molokai is revered for its many uncrowded beaches, such as the 2-mile (3.2 km) long <a href="../molokai/beaches/papohaku-beach.php">Papohaku Beach</a>, one of the longest white-sand beaches in Hawaii. Good swimming beaches when the ocean is calm are <a href="../molokai/beaches/dixiemarucove.php">Dixie Maru Cove</a> on the island's west shore and <a href="../molokai/beaches/murphys-beach.php">Murphy's Beach</a> and <a href="../molokai/beaches/sandybeach.php">Sandy Beach</a> on the east shore.</p>
              <p><strong>Kalaupapa</strong></p>
              <p>                During your visit to Molokai, a must-see is the former Hansen's disease (leprosy) settlement <a href="../molokai/attractions/kalaupapanationalpark.php">Kalaupapa</a> on the Makanalua Peninsula on Molokai's north shore. After an uncontrollable outbreak of what is today known as Hansen's disease, those infected with the disease were forced into isolation at this isolated site. Surrounded on three sides by the sea and the fourth by insurmountable sea cliffs, Kalaupapa is about as secluded as you can get. Kalaupapa was run by Belgian priest <a href="../molokai/attractions/fatherdamienchurch.php">Saint Damien</a> and others who dedicated their lives with no thought to the risks of contracting the disease themselves. Today, Kalaupapa has a National Historic Site status and is home to 18 former Hansen's disease patients. Travel here on foot or by mule and experience the solemn remembrances of the selfless care and healing that transpired here. </p>
              <p><strong>What makes Molokai unique?</strong></p>
              <p> Molokai is for those travelers who seek a simplistic, unfettered and awe-inspiring island to explore. Come to Molokai for the deserted, gorgeous beaches and leave your watch behind. Come to Molokai for the absence of shopping malls, elevators, fast food drive-thrus and traffic. Molokai is said to be Hawaii as it was 50 years ago. On rural Molokai, you will find fishermen and farmers dedicated to preserving the land below their feet and keeping the coastal waters unpolluted. The pace is relaxed and country living is the only way to go. The wild, untamed island of Molokai is the perfect place to find your center, rediscover clarity and embrace the true essence of nature.</p>
