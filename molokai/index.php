              <p>Aloha and welcome to Molokai, also known as &ldquo;The Friendly Isle&rdquo; or &ldquo;The Most Hawaiian Isle.&rdquo; If you are looking for simplicity, quiet beauty and pristine beaches, Molokai is your island. You won't find traffic lights, tall buildings, chain stores or night clubs here. Molokai is a peaceful island destination with awe-inspiring landscapes.<br />
                <br />
                <strong>Geography and Climate</strong><br />
             

                Molokai is 38 miles (61 km) long and just 10 miles (16 km) wide, so no point on the island is more than 5 miles (8 km) away from the ocean. The island's highest mountain is Kamakou, rising 4,961 feet (1,512 m). Molokai's west end is dry and covered by kiawe forests, while the east end and north shore is lush and has rainforests, waterfalls and the highest sea cliffs in the world, some of which rise over 3,000 feet (914 m). Stretching for 28 miles (45 km) along Molokai's south shore is the longest barrier reef in Hawaii and the only barrier reef north of Australia. Also located along the south shore are over 60 ancient Hawaiian fishponds, built 700 to 800 years ago.<br />
                <br />
                Like the other Hawaiian islands, Molokai has a pleasant year-round temperature averaging 75&deg;F (24&deg;C), with night-time temperatures a bit cooler in the winter months. </p>
