                <h2 class="small">Phallic Rock (Kaule o Nanahoa), Molokai</h2>
                <p>This is an ancient Hawaiian site of love and fertility. The Phallic Rock (<a href="../../molokai/ancientsites/more_phallicrock_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>)  (its Hawaiian name is Kaule o Nanahoa, meaning &ldquo;the penis of Nanahoa&rdquo;) has been standing erect for generations at what is now known as the <a href="../attractions/palaaustatepark.php">Pala'au State Park</a>. It can easily be reached by walking on a short forest trail from the Kakaupapa Lookout parking lot (a 5-minute walk). </p>
                <? include ("../../theme/text.html") ?>
                <p>An ancient legend tells of the male fertility god Nanahoa, who lived in this area. His wife Kawahuna one day caught Nanahoa admiring a young girl. She got upset and pulled the girl by her hair. Nanahoa then got mad and struck his wife, who then fell down the cliff and turned into stone. Nanahoa then turned into the stone you see today, the Phallic Rock.</p>
                <p>In the ancient Hawaii infertile women came here to pray, bring offerings and spend the night in hopes of conceiving a child. Even today women still come here in hopes of getting pregnant soon.</p>
                
                <h2 class="big"><span class="small">Phallic Rock</span> Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Natural rock formation, but has been slightly &ldquo;enhanced&rdquo; by man over the years</li>
                  <li> Located on the summit of Nanahoa Hill (1,560 feet elevation), about 200 yards from the Kalaupapa Lookout</li>
                  <li> Surrounded by tall ironwood trees</li>
                  <li> Phallic Rock is about 5 feet tall from the tip of its head to the ground</li>
                  <li> Another boulder nearby, Kawahuna, has the form of female genitalia (located southwest of the Phallic Rock)</li>
                  <li> Women come here to pray and give offerings in hopes of getting pregnant</li></ul>  <div style="clear:both"></div>
