              <h2 class="small">Kapuaiwa Grove, Molokai</h2>
              <p>Kapuaiwa Grove (<a href="../../molokai/attractions/more_kapuaiwacoconutgrove_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../molokai/attractions/p5.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>) on Molokai's south shore is one of Hawaii's oldest coconut groves. The first coconuts were planted here in the 1860s by King Kamehameha V. Back then, coconut trees encompassed about 10 acres (40,468 sq. m) of land. Today, only a few hundred coconut trees remain at the grove. </p>
              <p>Because of the danger that coconuts could fall from the trees and cause harm, signs have been posted that warn visitors about the dangers of falling coconuts. A military bunker still rests at the edge of Kapuaiwa Grove, a sad memory of World War II.</p>
              <? include ("../../theme/text.html") ?>
              <p>Sometimes referred to as the &quot;Mysterious Taboo,&quot; Kapuaiwa Grove is a nice spot to visit during sunset. As the sun colors the sky, the dark shapes of the coconut trees with their long trunks and swaying leaves make for a beautiful view. </p>
              <p>Reaching Kapuaiwa Grove is easy if you're coming from Kaunakakai. Drive west on Highway 460 and the coconut grove will be on your left. Just across the street is Church Row.</p>
              
