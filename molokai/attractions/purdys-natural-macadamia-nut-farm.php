              <h2 class="small">Purdy's Natural Macadamia Nut Farm, Molokai</h2>
              <p>Crack the mac. This is a phrase that you have to know if you're planning on visiting the island of Molokai. Let the owners of Purdy's Natural Macadamia Nut Farm (<a href="../../molokai/attractions/p4.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), Tuddie and Kammy Purdy, teach you how to &quot;crack a mac&quot; or simply how to pry open a macadamia nut. </p>
              <p>The 5-acre (20,234 sq. m) farm is located in Ho'olehua, 2 miles (3.2 km) west of Kualapu'u. It has been in existence since 1980 and is a wonderful place to visit if you want to know about macadamia nut history and, of course, to try them and satisfy your taste buds. </p>
              <? include ("../../theme/text.html") ?>
              <p>There are 50 macadamia nut trees on this farm and all of them produce nuts year-round. The owners wait until the nuts are ready and fall to the ground, then pick them up and roast them in their oven, right on the farm. Nuts are never picked from the trees and the roasting involves no added oils or other ingredients. They also have unroasted nuts available. These nuts are all natural and are grown without the use of pesticides or fertilizers.</p>
              <p>Purdy&rsquo;s Macadamia Nut Farm is open on weekdays, from 9:30 am to 3:30 pm and on Saturdays from 10:00 am to 2:00 pm. The farm is accessible on Sundays and holidays only by appointment.</p>
              
