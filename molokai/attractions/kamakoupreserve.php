                <h2 class="small">Kamakou Preserve, Molokai</h2>
                <p>Located high in the mountains of East Molokai, the Kamakou Preserve is a 2,774-acre (11.2 sq. km) hidden sanctuary that shelters a rich rainforest of over 250 species of Hawaiian flowers, of which at least 219 can be found nowhere else in the world. If you want to take your time appreciating the beauty of the preserve, sign up for one of the monthly hikes that the staff of the National Conservancy in Hawaii offer*. Consider it your roundtrip ticket to an intimate date with nature. </p>
                <p>Once you get to the preserve, you can take a 3-mile (4.8 km) boardwalk through the conservation and the Pepeopae Bog. It's a relatively easy hike, but the preserve is not an easy place to reach. One needs a four-wheel drive vehicle to get here and it is recommended to come with an experienced guide. </p>
   <? include ("../../theme/text.html") ?>
                <p>Visitors who have been to the Kamakou Preserve have said that one of the highlights of the trail is hearing the Kawawahie and the Olomao sing. These are two Hawaiian birds that are considered endangered species, but can still be observed in the Kamakou rainforest. Other native animals that live here include the Pueo (a Hawaiian owl), the 'Apapane and the 'Amakihi. </p>
                <p>Before visiting this beautiful rainforest, you can also visit the Waikolu Valley and the Sandalwood Pit, as they are just along the way to the Kamakou Preserve. The ancient Hawaiians called this forest wao akua, which means &ldquo;the realm of the gods.&rdquo; Walking through this nature preserve, it is easy to see why.</p>
                <p>*For more information on visiting this preserve, contact the Molokai field office at (808) 553-5236 or e-mail hike_molokai@tnc.org.</p>
                
