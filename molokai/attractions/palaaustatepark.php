              <h2 class="small">Pala'au State Park, Molokai</h2>
              <p>About 3 miles (4.8 km) north of Kualapu'u at the end of Kalae Highway lies the 233.7-acre (0.9 sq. km) Pala'au State Park (<a href="../../molokai/attractions/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), featuring beautiful pasture lands and koa, eucalyptus and ironwood forests. The park is one of the most popular sites to visit on the island of Molokai. </p>
              <p>There is a scenic lookout point of the historic <a href="../attractions/kalaupapanationalpark.php">Kalaupapa Peninsula</a>, where Hansen's disease (leprosy) patients were once forced to live in exile. Another short trail leads to the legendary <a href="../ancientsites/phallicrock.php">Phallic Rock</a>, which is believed to enhance fertility. The Molokai Mule Ride also begins from the parking lot area of the park.</p>
              <? include ("../../theme/text.html") ?>
              <p>Campers can enjoy an overnight stay at the park's ironwood grove. However, you will need a permit if you want to camp out here. The rate is $5 per night per campsite*. </p>
              <p>Drinking water is not available in the park, but there are restrooms and picnic facilities. Because it lies at an elevation of about 1,600 feet (488 m), the temperature at the Pala'au State Park is cooler than on the beaches. Entrance to the park is free.</p>
              <p>*For more information on how to obtain a camping permit, visit hawaiistateparks.org </p>
              
