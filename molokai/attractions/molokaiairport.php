                <h2 class="small">Molokai Airport - Ho'olehua</h2>
                <p>The Molokai Airport at Ho'olehua (<a href="../../molokai/attractions/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is the island's main airport, where inter-island planes take off and land. It is located about 7 miles (11 km) northwest of <a href="../cities/kaunakakai.php">Kaunakakai</a>, Molokai's main town. </p>
                <p>The airport has one main building where both arrivals and departures take place. You won't have to worry about getting lost here. Even going through security is easy and fast  compared to many other airports. </p>
                <p>When you arrive on Molokai, you exit the plane right next to the runway and proceed to the building. Your luggage will be brought on a small cart and be dropped off right at the entrance. The car rental counter is also located  there. </p>
                <p>Four airlines service Molokai Airport: Island Air, Mokulele Airlines, Pacific Wings and Makani Kai Air Charters. The Molokai Airport code is MKK.</p>
