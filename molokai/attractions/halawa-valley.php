                <h2 class="small">Halawa Valley, Molokai</h2>
                <p>Located on the east shore of Molokai, Halawa Valley is one of the island's top recreational areas. It has everything you may ever want in a travel spot: lush mountains, waterfalls and a nice <a href="../beaches/halawa-beach-park.php">beach park</a> with two scenic coves.</p>
                <p>Halawa Valley also has a rich history. It was first settled by people from the Marquesas Islands in the 7th century. For many years, this area was extensively cultivated with taro due to its fertile soil. Also, many heiaus (places of worship) were built here. Young men who dreamed of becoming kahunas (priests) came from  the neighbor islands and went to the valley to learn and practice sacred spells. </p>
                <? include ("../../theme/text.html") ?>
                        <p>The taro-growing in Halawa Valley ended after the area was hit by two tsunamis, one in 1946 and one in 1957, which covered the valley with plant-destroying salt. Today, only a few families still live in the valley and the area is mainly used for recreational purposes, such as camping, hiking, fishing, swimming and surfing.</p>
                <p>Two of the main attractions in Halawa Valley are  the 250-foot (76 m) <a href="../waterfalls/moaulafalls.php">Moa'ula Falls</a> and the 500-foot (152 m) <a href="../waterfalls/hipuapuafalls.php">Hipuapua Falls</a>. Both waterfalls can be seen from the lookout above Halawa Valley. Moa'ula can be accessed more easily, so it's  more popular. However, both waterfalls are located on private property, so you will need to get permission from the land owners to reach the falls or come with a local guide who has a permit. Contact one of the tour operators on Molokai for hiking tour info.</p>
                <p>Legend has it that a big lizard, a mo&lsquo;o, lives in the pool at the base of the waterfall. Visitors who wish to swim in it must first drop a ti leaf on the surface of the water. If it floats, it is ok and safe to swim here. If it sinks, the mo&lsquo;o is not happy and doesn't want you to enter the water.</p>
                <p>Although Halawa Valley is as close to nature as you can get, life's most comfortable amenities are still available: from restrooms and barbecue grills to a picnic pavilion. </p>
                <p>To get to Halawa Valley, drive Highway 450 east all the way to its end. The valley is located about 30 miles (48 km) from Kaunakakai. </p>
                
