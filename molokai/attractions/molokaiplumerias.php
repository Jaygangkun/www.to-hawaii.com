                <h2 class="small">Molokai Plumerias, Molokai</h2>
                <p>Molokai Plumerias have been growing Hawaii's Aloha flower since 1982. They are the largest plumeria grower in Hawaii. Their farm is located just 2 miles (3.2 km) west of Kaunakakai. When you visit the farm, you can sign up for an afternoon tour and learn all about plumerias, how the farm works and about the importance of this flower in Hawaiian culture.</p>
                              <p>The so-called Aloha flower was named after Charles Plumier, a French botanist. Plumerias go by various names. Sometimes, Molokai plumerias are known as Celadine, Franginapani and Hawaiian Yellow. This Aloha flower is also a favorite among flower lei makers. </p>
                <p>Molokai plumerias are yellow in color when they are young, but their colors change gradually into white at the edge of the petals when they age. They are known not only for their beautiful colors and silky smooth texture, but also for their fragrance. </p>
                <p>You can buy the flowers in loose form, cuttings or flower leis. The flowers may also be ordered online.</p>
