              <h2 class="small">Saint Damien Church, Molokai</h2>
              <p>Saint Damien Church, formerly known as Saint Joseph's Church or Father Damien Church, is located in Kamalo on Molokai's south shore. St. Damien was a Belgian Catholic missionary priest who served the people of Molokai, especially people who were infected with Hansen's disease or leprosy and had to live in exile on Molokai's isolated <a href="../attractions/kalaupapanationalpark.php">Kalaupapa peninsula</a> between the years 1866 and 1969. This is where St. Damien served and nurtured his ministry. Later, he contracted the disease himself and passed away in 1889.</p>
              <p>Damien built four churches on Molokai, two of which are still standing today. In 1874, he built <a href="../attractions/sevensorrows.php">Our Lady of Seven Sorrows Church</a> in Kalua'aha. Two years later, in 1876, he built St. Joseph Church in Kamalo, the second oldest church on the island. A statue of St. Damien stands next to the church, which is often times adorned with flower leis in honor of him. The door to the church is usually closed but not locked, so people can visit the inside of it as well. The church is listed on the National Register of Historic Places. </p>
