              <h2 class="small">Kalaupapa National Historical Park, Molokai</h2>
              <p>The Kalaupapa National Historical Park (<a href="../../molokai/attractions/more_kalaupapanationalpark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../molokai/attractions/p7.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a portal to a number of historic sites on Molokai, such as the Kalawao Coast and the Kalaupapa Peninsula. Kalaupapa is the location of the former leprosy settlement on the island. Between 1866 and 1969, those who contracted leprosy, now known as Hansen's disease, were forced into exile on these five square miles (12.9 sq. km) of windswept land on Molokai's remote north shore. </p>
                   <? include ("../../theme/text.html") ?>
              <p>In the 19th century, infected people were so feared and avoided that when the boat that brought them here from the neighboring island of Oahu sailed past the peninsula, they were often forced overboard right into the turbulent waves. Many of the patients drowned before reaching the shore. And even those who made it to shore, they were given almost no medicine or supplies, not even materials to build a shelter. Kalaupapa became a miserable destiny for those brought here. Many died in those first years and were buried in unmarked cemeteries along the shore. Of about 8,000 patients buried at Kalaupapa, only about 1,300 have marked graves. </p>
         
              <p>In 1873, Saint Damien de Veuster, a Belgian missionary priest of the Congregation of the Sacred Hearts of Jesus and Mary, arrived at Kalaupapa. He became a gift from God for the Kalaupapa patients. He helped build churches and homes and served as a doctor, farmer, carpenter, legal advocate and much more. He selflessly cared for the patients and had close contact with them, even though he knew the disease is contagious. He gave his patients love and hope. He later contracted the disease himself and passed away in 1889.</p>
              <p>Kalaupapa is a rather solemn place to visit, to pray and to learn about an important part of Hawaiian history. As of today, the peninsula is home to 18 surviving Hansen's disease patients. All of them are over 70 years of age. Walking around Kalaupapa is like walking through a ghost town. All that can be seen are the park rangers and state employees who are making sure that everything works properly and that the residents are taken care of. </p>
              <p>The state operates a kitchen there and pays for all living expenses for the residents, such as food, housing and medical care. There is a small store for patients only. If a patient wants to buy something that's not available there, he or she has to order it by mail. A barge arrives once a year to bring large supplies like trucks, oil, gas and alcohol. Barge day is a major annual event. Otherwise, small supplies and mail are flown in regularly by charter planes or on Pacific Wings. The patients who live on this remote peninsula hardly ever leave. Of course, they are free to leave anytime, but patients choose to stay since it is the only home they know. </p>
              <p>The only visitors allowed in the park are those who are over the age of sixteen. Because Kalaupapa is a settlement of Hansen's disease patients, visitors are required by state law to have a permit before visiting the park. The National Park Service and the Hawaii State Department of Health share joint management of the park.</p>
              <p>The park's historical features, however, are not the only points of interest. The Kalaupapa National Historical Park has a rich composition of flora and fauna. The park likewise serves as a safe habitat for endangered species in Hawaii.</p>
              
