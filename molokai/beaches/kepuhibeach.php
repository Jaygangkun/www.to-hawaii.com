              <h2 class="small">Kepuhi Beach, Molokai</h2>
              <p>Kepuhi Beach  (<a href="../../molokai/beaches/more_kepuhibeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../molokai/beaches/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located on Molokai's west shore, is a long and wide sandy beach. On the southern end of the beach is Kaiaka Rock, which was once the location of a heiau (Hawaiian temple). </p>
              <p>Even though the beach looks inviting, swimming is only possible here on very calm days because of strong alongshore currents that can be treacherous. In the winter months or when the surf is up, the entire length of the beach gets pounded by high waves with dangerous shorebreaks. This is when powerful rip currents occur. </p>
              <? include ("../../theme/text.html") ?>
              <p>Sometimes the waves are good enough for bodyboarding or surfing, but only for experts. During extended periods of heavy surf, the waves often times erode the beach and flush the sand out to sea, exposing large beach rocks. </p>
              <p>Kepuhi Beach has two sections, which are devided by a few large lava rocks along the shoreline. The northern part is smaller and doesn't have exposed rocks on the shoreline.  The longer southern part has exposed beach rocks.</p>
              
