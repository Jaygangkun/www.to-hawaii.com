                <h2 class="small">Pohakumauliuli Beach, Molokai</h2>
                <p>Located on Molokai's west shore, Pohakumauliuli Beach (<a href="../../molokai/beaches/more_pohakumauliulibeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../molokai/beaches/p9.html" onclick="NewWindow(this.href,'name','2550','630','yes');return false">view panorama</a>) features two white-sand beach coves and a few large tide pools. The remote coves are good spots for sunbathing, but the swimming conditions are poor due to strong currents. Also, there are some large boulders right at the water's edge which can pose a hazard during times of high surf. </p>
                <? include ("../../theme/text.html") ?>
                <p>The nearby hill is a large cinder cone called Pohaku Mauliuli, which means &ldquo;black rock&rdquo; in the Hawaiian language. The cinder rock is quite loose in some parts and occasionally falls off into the sand below. The base of the hill is therefore not a good spot for walking or sunbathing and should be avoided. </p>
                <p>Pohakumauliuli Beach is also known as Make Horse Beach. In the past, when there was a ranch on Molokai's west shore, an old horse fell off the cliff here and died on the beach. In the Hawaiian language, &quot;make&quot; means dead.</p>
                <p>To get to Pohakumauliuli Beach, walk north along the shoreline from neighboring Kepuhi Beach. A waterfront golf course used to be located here and the golf cart trail is still visible. After a 5-10 minute walk you'll reach the first cove. The two coves are separated by a boulder hill. </p>
                
