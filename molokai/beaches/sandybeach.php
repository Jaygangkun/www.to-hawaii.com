                <h2 class="small">Sandy Beach, Molokai</h2>
                <p>Sandy Beach (<a href="../../molokai/beaches/p11.html" onclick="NewWindow(this.href,'name','2550','650','yes');return false">view panorama</a>) on Molokai's east shore is a small roadside pocket of sand and one of the best swimming beaches on the island. The nearshore ocean bottom is deeper here than on many other Molokai beaches, so it's easier to enter the water without having to walk on reef and rocks. The water is also clear here while many other beaches on Molokai's south and east shore have murky water.</p>
                <p>There is a reef a little further offshore, which protects the beach from strong currents. However, a rip current occasionally forms along the south point of the beach.</p>
                <p>Sandy Beach, located after <a href="../attractions/rockpoint.php">Rock Point</a> near mile marker 22, is the last sand beach before <a href="../beaches/halawa-beach-park.php">Halawa Beach Park</a>. The road to Sandy Beach and beyond is narrow and has many blind curves. It is described on this <a href="../scenicdrives/kamehamehahighway20-22.php">scenic drive</a> page.</p>
