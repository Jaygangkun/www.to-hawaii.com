              <h2 class="small">Po'olau Beach, Molokai</h2>
              <p>Po'olau Beach (<a href="../../molokai/beaches/p10.html" onclick="NewWindow(this.href,'name','2550','780','yes');return false">view panorama</a>) is a small sandy cove on Molokai's west shore, located south of the more popular <a href="../beaches/papohaku-beach.php">Papohaku Beach</a>. Parts of the beach as well as the nearshore ocean bottom are covered by lava rocks. It is therefore more suitable for a peaceful picnic rather than a swimming destination. When the ocean is calm (usually in the summer months) this beach sometimes attracts snorkelers. In the winter (November to March), the surf can be up.</p>
              <p>Access to the beach is via a paved beach access road, which can easily be missed. The other beaches along Pohakuloa Road (such as neighboring Paka'a Beach and Papohaku Beach) have signs that mark the public right-of-way to the shoreline, but the sign at Po'olau Beach is currently missing.</p>
