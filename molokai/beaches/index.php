                  <p><a href="../../map-molokai-beaches.html" target="_blank"><img src="../../theme/images/molokaibeaches.jpg" alt="Molokai Beach Map" align="right" border="0" style="margin-left:10px;" /></a>The island of Molokai has a coastline of about 88 miles (142 km). Molokai beaches are never crowded and most are rather deserted since the island is home to just a little over 7,400 residents. Also, Molokai is still off the beaten path (it hasn't been discovered by too many visitors yet). </p>
                  <p>There are only a few good  swimming beaches on Molokai. Most beaches on the island are rocky and have an offshore reef or a shallow nearshore ocean bottom with murky water. On Molokai's west shore, Dixie Maru may be the best for a refreshing swim, while on Molokai's east shore, Murphy's Beach is the most popular for snorkeling. Another nice sandy beach and the perfect place for a long beach stroll is Papohaku Beach, one of the longest white-sand beaches in the entire state of Hawaii. Beach sand in Hawaii comes in all kinds of shapes and colors. Read more about Hawaiian <a href="../../hawaiibeachsand.php">beach sand composition</a>.</p>
