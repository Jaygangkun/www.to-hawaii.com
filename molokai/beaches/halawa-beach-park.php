              <h2 class="small">Halawa Beach, Molokai</h2>
              <p>Comprised of two equally beautiful beaches, Kama'alaea Beach to the west and Kawili Beach to the east, Halawa Beach Park is located in gorgeous Halawa Bay on Molokai's east shore. Both beaches are detrital sand beaches and are backed by low sand dunes. The remote bay is a good spot for sunbathing, fishing and picnicking. </p>
              <p>The inshore waters of Kama'alaea Beach are shallow and mostly safe for swimming, except when there's a strong rip current that occurs during times of heavy surf. There's a small reef patch in the middle of the beach, however, the water is usually murky, which limits snorkeling. Mountain rainwater is carried by Halawa Stream to meet the ocean at the east end of the beach. </p>
<? include ("../../theme/text.html") ?>
              <p>Kawili Beach is not as protected from the open ocean and therefore, very strong currents occur when the surf is up. On calm days, swimming is usually safe. There are some rocks on the ocean bottom here.</p>
              <p>Legend has it that in the old Hawaii, the Molokai chiefs enjoyed surfing at Halawa Beach. Today, the beach remains a popular surf spot during the winter when the waves are big. </p>
              <p>If you're planning on taking a trip to Halawa Beach Park, take Highway 450 (Kamehameha Highway) all the way to its east end. After you cross the bridge, follow the rough road trail until you reach the gravel parking area. Up ahead is the beach park. </p>
              <p>Bring drinking water because there is no water available at the beach park. There are no concession areas either, but restrooms and picnic pavilions are available.</p>
              
