              <h2 class="small">Kakahai'a Beach Park, Molokai</h2>
              <p>Kakahai'a Beach Park (<a href="../../molokai/beaches/more_kakahaiacountypark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../molokai/beaches/p6.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a small roadside park located on Highway 450 on Molokai's south shore. It is also known as the Kakahai'a County Park and National Wildlife Refuge. The name Kakahai'a means &quot;fish slicing&quot; in Hawaiian. It's the name of an ancient inland fishpond. However, the pond is overgrown with bullrushes and is not in use today.</p>
              <p>Kakahai'a Beach is a narrow, detrital sand beach and is not suitable for swimming or snorkeling because the ocean bottom is rocky, shallow and the water generally murky. It's primarily visited by fishermen and picnickers. However, a good thing to do here is bird watching, since part of the beach is actually a bird sanctuary. The National Wildlife Refuge, where the beach is located, is home to the Hawaiian Stilt and the Hawaiian Coot. These birds are an endangered Hawaiian native species.</p>
              <? include ("../../theme/text.html") ?>
              <p>The Hawaiian Stilt - also known as the Ae'o - is a slim, wading bird that has a black and white forehead. There used to be a lot of these birds in existence, but today their number has dropped down to a tragic 1,500. The Hawaiian Coot, or the Fulica Alai, is  dark gray in color and the male and female species look alike. They are usually found in lowland valleys. Today, there are an estimated 2,000 to 4,000 Hawaiian Coots in existence all over the Hawaiian Islands. </p>
              
