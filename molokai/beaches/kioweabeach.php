              <h2 class="small">Kiowea Beach Park, Molokai</h2>
              <p>Kiowea Beach Park (<a href="../../molokai/beaches/p13.html" onclick="NewWindow(this.href,'name','2550','780','yes');return false">view panorama</a>) is located right next to a well-known  coconut grove, known as <a href="../attractions/kapuaiwa-grove.php">Kapuaiwa</a> or Kamehameha Coconut Grove, on Molokai's south shore. It is named in honor of the high chief Kapuaiwa, who in 1863 became King Kamehameha V. It is said that either he himself planted the coconuts in this grove, or that the trees were planted by his agent and ranch manager, Rudolph W. Meyer. </p>
              <p>Kiowea Beach is narrow and composed of detrital sand. The ocean bottom is rocky, shallow and the water murky, so the swimming conditions are poor. The shoreline here is quite eroded. In the past, there had been several freshwater springs in the park, but because of erosion, they are now located in the ocean offshore. They can be seen bubbling up at low tide.</p>
