              <h2 class="small">Awahua Beach, Molokai</h2>
              <p>This scenic beach is located on Molokai's <a href="../attractions/kalaupapanationalpark.php">Kalaupapa Peninsula</a>. The peninsula is backed by the world's tallest sea cliffs. Because of its remoteness and the fact that it is difficult to access, those inflicted with Hansen's disease (leprosy) were forced to live in exile here between 1866 and 1969. Today, the peninsula is home to a few surviving Hansen's disease patients.<br />
                <br />
                A narrow trail leads down from the <a href="../attractions/palaaustatepark.php">Pala'au State Park</a> on top of the cliffs to the peninsula below. However, visitors have to join a tour group that is led by a guide. Alternatively, you can see the beach from a public lookout that is located next to the trailhead, 1,664 feet (507 m) above the peninsula.</p>
                <? include ("../../theme/text.html") ?>
               <p> Because Kalaupapa is difficult to access and either involves a hike or mule ride, Awahua Beach doesn't see many visitors. The beach is composed of detrital sand, which appears black. It is therefore also known as Black Sand Beach. The swimming conditions are poor here. There is no protective reef and the nearshore ocean bottom drops off quickly. Strong rip currents make swimming hazardous.</p>
              
