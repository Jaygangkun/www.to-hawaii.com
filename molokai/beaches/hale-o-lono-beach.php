              <h2 class="small">Hale O Lono Beach, Molokai</h2>
              <p>Hale O Lono, or &quot;House of Lono,&quot; is a long and narrow white-sand beach on Molokai's south shore. It was once a heiau, or a place of worship, for Lono, who is the god of agriculture and fertility. Food and gifts were offered to Lono, as the people prayed that Lono will grant them abundant crop growth as well as prosperity both on land and sea.</p>
              <p>Although the land near the beach is owned by Molokai Ranch (they stopped operating, but still own the land nearby), Hale O Lono Beach is  open to the public. It is located at the Hale O Lono Harbor, where the annual Molokai Hoe (an outrigger canoe competition from Molokai to Oahu) starts. </p>
              <p>Due to the rocks and stones on the ocean bottom, swimming and snorkeling are less popular activities on the beach. Fishing is good though. Also, the beach is a prime spot for whale watching between December and April. </p>
              <? include ("../../theme/text.html") ?>
              <p>There are no lifeguards and restroom facilities at Hale O Lono Beach. To get to the beach, take Highway 460 towards Maunaloa. Once you reach the sharp curve at the pineapple exhibit, turn right towards the dirt road a little past Mokio Street. Go all the way down south until you come to a branched road. Take the left and there you'll find the beach park.</p>
              
