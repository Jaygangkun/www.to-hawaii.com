                <h2 class="small">Papohaku Beach, Molokai</h2>
                <p>Spanning a distance of  2 miles (3.2 km) long and 100 yards  (91 m) wide, Papohaku Beach (<a href="../../molokai/beaches/more_papohakubeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../molokai/beaches/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on the west shore of Molokai is one of the longest and widest white-sand beaches in the Hawaiian Islands. Low sand dunes and thorny kiawe trees make up the backshore. It is a great beach for a long shoreline stroll.</p>
                
                <p>Even though the beach looks inviting, swimming isn't safe here due to strong rip currents. Papohaku Beach doesn't have a protective reef, so it is fully exposed to the open ocean. Strong surf can occur at any time of the year. Since nothing blocks the waves, they break forcefully and can wash up high on the beach, which can pose a danger to anyone walking near the water's edge, especially children. Occasionally, experienced bodyboarders  go out and surf here, but this is not a beach for other water activities. </p>
                <? include ("../../theme/text.html") ?>
                <p>On windy days it can be unpleasant to picnic here or to lie down on a towel on the beach because the wind will blow the sand all over you. Even on moderately windy days, the wind will blow the sand all the way up to your chest.</p>
                <p>For many years in the past, Papohaku Beach was the location of the largest sand mining operation in Hawaii. In the early 1960s, sand was taken from here and shipped to neighboring Oahu for use by the construction industry. This operation ended in 1975. A concrete tunnel on the beach is all what remains of the former sand mining industry.</p>
                <p>Papohaku Beach was also the location of military exercises during World War II. Up until today, there are some wire rolls and other pieces of metal and even some abandoned vehicles partially buried in the middle of the beach at the water's edge, which are sometimes exposed during times of heavy surf. These objects can pose another hazard to beachgoers.</p>
                <p><strong>Editor's Note:</strong> Papohaku Beach is backed by a kiawe tree forest. Kiawe trees have thorns (<a href="../../molokai/beaches/n1.html" onclick="NewWindow(this.href,'name','650','650','yes');return false">see here</a>), so be careful when looking for a shady spot under a kiawe tree. Before sitting down, make sure there are no thorns in the sand. The thorns, which are up to one inch  (2.5 cm) long, are also not very pleasant to step into. Wear shoes when walking from the parking area to the beach. After our recent visit to the beach we had a couple of thorns  stuck in our running shoes and were glad we didn't wear slippers (flip flops).</p>
                
