                <h2 class="small">Mo'omomi Beach, Molokai</h2>
                <p>Located on Molokai's northwestern coast, Mo'omomi Beach and the surrounding area that extends inland feature one of the most impressive sand dunes in Hawaii. Since this side of the island is exposed to the tradewinds during most of the year, the wind carries the sand from the Mo'omomi shoreline and surrounding beaches inland, expanding the sand dunes even further. </p>
                <p>These massive sand dunes have been formed over centuries, extending inland for more than 4 miles (6.4 km) in some areas. That's why this deserted, sandy region is also called the Desert Strip, or in the Hawaiian language it is referred to as Keonelele, &ldquo;the wind-blown sands.&rdquo;</p>
                <p>This stretch of coastline was once a popular fishing area. The entire area is part of the Mo'omomi Preserve, which is managed by the Nature Conservancy of Hawaii. The nature preserve is a 921-acre (3.7 sq. km) sanctuary for 22 native species of Hawaiian plants. The conservancy offers regular hiking tours into the area. Call 808-553-5236 for more information. Reservations are required, but there is no fee. </p>
                <? include ("../../theme/text.html") ?>
                <p>If you want to go on your own, the trail begins at the Hawaiian Home Lands area, located in the east of the preserve. However, going to the beach can be quite a feat in itself, as the path leading to Mo'omomi is a dirt road that is almost impassable unless you drive a four-wheel vehicle. Parking is only allowed in designated areas and since this is a nature preserve, you'll have to stay on the trail. The sand dune area is fragile and protected by the preserve and no one should enter it. Also, all corals, shells, stones, plants and whatever else is there must be left in its place.</p>
                <p>For many longtime residents of Molokai, Mo'omomi Beach encompasses the entire 3-mile (4.8 km) stretch of coastline from the Hawaiian Home Lands center to &lsquo;Ilio Point. However, there are three different beaches in this area, Mo'omomi, Keonelele and Kawa'aloa. Mo'omomi Beach is a small pocket of white sand, located at the Hawaiian Home Lands Recreation Center.  The bay is shallow and rocky and not a good spot for swimming or other water activities besides fishing. Also, sharp rocks, strong currents and high surf, especially in the winter months, make swimming dangerous.</p>
                
