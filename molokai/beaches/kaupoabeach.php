              <h2 class="small">Kaupoa Beach, Molokai</h2>
              <p>Located on the west shore of Molokai, Kaupoa Beach is made up of two beaches shaped like crescent moons and beautified by white sands, crystal clear waters and dark lava rocks. The two beaches are divided by a rocky outcrop in the center. The inshore ocean bottom is mostly sandy, but drops quickly do overhead depths.</p>
              <p>The swimming and snorkeling conditions are excellent here, especially in the summer months when the waters are usually calm. You can meet a variety of marine life up close as you explore the many tide pools on the beach. During the winter months when the surf is up, swimming is not recommended because the water can get turbulent.</p>
              <? include ("../../theme/text.html") ?>
              <p>Numerous shipwrecks have occurred in this area in the past. One of them was the <em>Carrier Dove</em>, an American four-masted, 800-ton cargo ship, which was on its way from Tonga to Canada. On November 4, 1921, the ship hit the rocky coast near Kaupoa Beach at night and broke up within just a few hours. The <em>Carrier Dove</em> had an interesting history. Before her final day, she had survived violent storms, pirate attacks and two former groundings.</p>
              <p>There used to be 40 tentalows (small cabins) where one could camp, known as the Kaupoa Beach Village, but it closed in 2008 when the owner, Molokai Ranch, ceased operations.</p>
              
