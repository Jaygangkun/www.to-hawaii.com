                <h2 class="small">Dixie Maru Cove, Molokai</h2>
                <p>Located on Molokai's west shore, Dixie Maru Cove (<a href="../../molokai/beaches/more_dixiemaru_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../molokai/beaches/p5.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) has one of the best swimming beaches on the island. The protected sandy cove  features lava outcroppings on each side and is a nice place to enjoy the water, whether it's for a relaxing swim or snorkeling. Fringed by a reef, the beach usually has calm waters during the summer. This is when the swimming and snorkeling conditions are the best. Even though the bay is more protected than the other beaches in this area (such as nearby <a href="../beaches/papohaku-beach.php">Papohaku Beach</a>), stay out of the water if the surf is up. There are no lifeguards here.</p>
               <? include ("../../theme/text.html") ?>
                <p>Dixie Maru Cove got its  name &quot;Dixie Maru&quot; from a Japanese fishing boat name plate. In Japanese culture, &quot;maru&quot; is a suffix added by sailors to their ship names as this term connotes something that is beloved. The term &quot;maru&quot; in itself represents a sense of perfection. It was in the 1920s when the Japanese sailboat Dixie Maru was wrecked off the shore and its name plate was then hung near the beach's gate. From then on, the remote beach has been fondly called Dixi Maru Cove. Its Hawaiian name is Kapukahehu Beach.</p>
                <p>Dixie Maru Cove takes some time to get to as it is located on Molokai's remote west shore. However, the road that leads here is paved. On most days the beach is only sparsely visited. The beach is backed by thorny kiawe trees, so be careful when looking for a shady spot under one of the trees.</p>
                <p><strong>Editor's note:</strong> Last time we visited Dixie Maru Beach one of us stepped into a big fish metal fishhook (<a href="../../molokai/beaches/n2.html" onclick="NewWindow(this.href,'name','650','450','yes');return false">see here</a>), luckily wearing running shoes. It was really difficult to remove the hook from the shoe. So it is a good idea to wear shoes here to protect your feet from any discarded fishhooks, as well as the thorns from the kiawe trees that are plentiful on most beaches in this area.</p>
                
