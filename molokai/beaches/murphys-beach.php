                <h2 class="small">Murphy's Beach, Molokai</h2>
                <p>Murphy's Beach (<a href="../../molokai/beaches/p7.html" onclick="NewWindow(this.href,'name','2550','730','yes');return false">view panorama</a>) on Molokai's southeast shore is a narrow white-sand beach shaded by a few palm trees. The beaches along Molokai's south shore (when driving east from Kaunakakai) aren't great swimming beaches because the nearshore ocean bottom is shallow and the water murky. However, Murphy's Beach is an exception. When the ocean is calm, the water is clear here and the ocean looks inviting for a refreshing dip. </p>
                <p>The swimming conditions are fair since the nearshore ocean bottom is shallow and rocky, with a few sandy patches inbetween. But the snorkeling  can be good on calm days. Stay close to shore because outside of the reef area the currents can be treacherous. The beach is also a good fishing spot.</p>
                <? include ("../../theme/text.html") ?>
                <p>Beyond Murphy's Beach the road becomes even narrower (Kamehameha Highway turns into a one-land road about 2 miles before Murphy's Beach), so many visitors or those on a tight schedule (as well as larger tour vans) drive only up to Murphy's Beach and then turn around. There is a small parking area that overlooks the beach, which is a popular lookout point. Pictures taken from here grace quite a few Hawaiian wall calendars. Since Murphy's Beach is 20 miles east of Kaunakakai, it is also known as Twenty Mile Beach or Mile Marker 20 Beach.</p>
                
