                <h2 class="small">One Ali'i Beach Park, Molokai</h2>
                <p>Located on Molokai's south shore, One Ali'i Beach Park (<a href="../../molokai/beaches/p8.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) has two spacious sections (I and II), which are located next to each other on Kamehameha Highway (coming from Kaunakakai, Section II is first). </p>
                <p>The beach in both areas is narrow and the nearshore ocean bottom is shallow and rocky and the water murky, so the swimming conditions are poor. The beach park is mainly visited by fishermen and picnickers. There is enough space for  family gatherings and the large lawns are perfect for ball games.</p>
                <p>In the Hawaiian language, <em>oneali'i </em>means &quot;royal sands.&quot; The beach park is named after an ancient Hawaiin fishpond. Ali'i Fishpond is located at the west end of Section II, but it is not accessible from the beach park because of thick vegetation. </p>
                <p>Two stone monuments in the park mark the 75th and 100th anniversaries of Japanese immigration to Hawaii.</p>
