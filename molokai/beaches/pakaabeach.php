              <h2 class="small">Paka'a Beach, Molokai</h2>
              <p>Paka'a Beach (<a href="../../molokai/beaches/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on Molokai's west shore is a small rocky bay. There is no sandy beach. Some local residents come here to go diving and fishing. Other than that there isn't much to do here. Access is via a beach access road from Pohakuloa Road. </p>
              <p>There is no sign with the beach name on it, only a sign that says &ldquo;Beach Public Right-of-Way,&rdquo; so the beach is easy to miss if you don't have a map with you and therefore don't know which beach access road to take.  Paka'a Beach is located between <a href="../beaches/poolaubeach.php">Po'olau Beach</a> to the north and <a href="../beaches/dixiemarucove.php">Dixie Maru Cove</a> to the south.</p>
