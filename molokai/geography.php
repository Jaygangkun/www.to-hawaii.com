               <p align="center"><img src="../../images/molokai_from_air.jpg" alt="Molokai from air" border="0" style="width:100%; max-width:478px" /></p>
              <p>Molokai is the fifth largest of the six main Hawaiian islands (only the island of Lanai is smaller) encompassing 260.0 square miles (673.4 km&sup2;). At its longest points the island measures 10 miles (16 km) from north to south and 38 miles (61 km) from east to west, with a total shoreline of 88 miles (142 km). <br />
                <br />
                Molokai is made up of two shield volcanoes &ndash; East Molokai (also known as Wailau) and West Molokai (also known as Mauna Loa). Molokai's highest peak is Kamakou, which is part of the East Molokai Volcano, rising  4,961 feet (1,512 m). <br />
                <br />
                East Molokai Volcano used to be much larger in the past. About 1.5 million years ago, it experienced what is known as a catastrophic collapse, where its northern portion broke off and slid to the bottom of the ocean. Today, only the volcanoes southern half remains standing. What remains above the ocean's surface on the island's north shore are the tallest sea cliffs in the world, dropping 3,315 feet (1,010 m) to the Pacific Ocean. Molokai's south shore has the longest fringing reef in the United States, measuring almost 25 miles (40 km) in length.<br />
                <br />
                The largest town on Molokai is Kaunakakai, located on the island's south shore. It has a population of 3,425 (the total population on Molokai is 7,345). Molokai is part of Maui County, which also includes the islands of Maui, Lanai and Kahoolawe.</p>
