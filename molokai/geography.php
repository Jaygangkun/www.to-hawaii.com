                <p>About 20,000 years ago Molokai was joined together with the neighbor islands of Maui, Lanai and Kahoolawe. It was one big island referred to as Maui Nui, meaning Big Maui. There was a mini ice age at this time which caused the sea level to drop. As the islands eroded and the sea level rose, Maui Nui became what is now four separate islands. The ocean channels between these four islands are relatively shallow.</p>
