                <h2 class="small">Maunaloa, Molokai</h2>
                <p>Maunaloa (<a href="../../molokai/cities/p3.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>) is a small plantation village in upcountry Molokai, near the island's western end. The views from up here are nice. You can see a long stretch of Molokai's south shore. Maunaloa has a post office, a small supermarket, a gallery and a gift shop, the Big Wind Kite Factory where you can find a unique selection of hand-made kites. </p>
                <p>Many Maunaloa residents used to work at Molokai Ranch, which closed in March 2008. The ranch was the largest employer on the island. It operated a hotel (Molokai Lodge), the Kaupoa Beach Village, the Kaluakoi Golf Course, the Maunaloa gas station, movie theatre and cattle ranch. When the company ceased all operations on Molokai, it closed all of these businesses and 120 workers were laid off.</p>
                <? include ("../../theme/text.html") ?>
                <p>The reason was because it had wanted to build 200 upscale residential homes on 613 acres at La'au Point (Molokai's southwestern tip), but many Molokai residents opposed this plan. So Molokai Ranch decided to cease all operations. Maunaloa residents were forced to look for jobs elsewhere, many of whom still can't find work.</p>
                
