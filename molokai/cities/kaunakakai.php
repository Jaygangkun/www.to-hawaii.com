                <h2 class="small">Kaunakakai, Molokai</h2>
                <p>Kaunakakai, the largest and main town of tranquil Molokai &ndash; the Hawaiian island without traffic lights and big chain stores &ndash; is located on the island's south shore. With a few mom-and-pop stores selling groceries and other supplies, Kaunakakai is the main shopping hub for the 7,345 island residents.</p>
                <p>The town is just three blocks in size and most of the stores and eateries are located on Ala Malama Avenue. Many of the buildings are still the original construction, such as the Molokai public library, which was built in 1937. It is the only library on Molokai.</p>
                <p>Barges and tug boats deliver all essential items at the nearby <a href="../attractions/kaunakakaiharbor.php">Kaunakakai Wharf</a>. In the past, sugar and pineapple plantations dotted this port town where King Kamehameha V used to spend his summers.</p>
