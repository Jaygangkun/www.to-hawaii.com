                <h2 class="small">Kualapu'u, Molokai</h2>
                <p>Kualapu'u (<a href="../../molokai/cities/p2.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>) is located in the northern part of Molokai on the way to the Kalaupapa Peninsula Lookout. In the Hawaiian language, Kualapu'u literally means &quot;hill overturned.&quot; However, it is thought to be a corruption of Pu'u'uala, meaning &quot;sweet potato hill.&quot; The settlement is a former pineapple cannery village that was once associated with Del Monte Foods.</p>
                <p>Kualapu'u is also home to Coffees of Hawaii, a large coffee plantation that produces and distributes 100% Molokai coffee. Also located in Kualapu'u is a 1.4 billion gallon water freshwater reservoir at the foot of a cinder cone called Kualapu'u.</p>
