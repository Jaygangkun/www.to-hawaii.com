                <h2 class="small">Coffees of Hawaii, Molokai</h2>
                <p>Coffees of Hawaii is a 500-acre (2 sq. km) coffee plantation located in the heart of the island of Molokai. They produce and sell their coffee right here and are committed to producing the finest coffee. In addition to their Molokai Coffee, they also mill and roast 100% Kona and 100% Maui Coffee.</p>
                <p>Located right on the plantation is their caf&eacute; and gift shop, where besides their premium Molokai coffee varieties, they also sell island blends, unroasted beans, cups and saucers, apparel, coffee cards, vintage items and a variety of foods, such as bagels, muffins, croissants, sandwiches, soups, salads and their signature drink, called Mocha Mama&trade;. </p>
                <? include ("../../theme/text.html") ?>
                <p>They also offer two tours: the Morning Espresso Tour and the Mule-Drawn Wagon Tour. Or visitors can do a self-guided tour and learn more about the history of coffee in Hawaii.</p>
                <p>Located in Kualapu'u along Farrington Highway, &quot;Coffees of Hawaii&quot; is open every day of the week, from
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr 06:00-17:00">6 am to 5 pm </time>
                  on weekdays, from
                  <time itemprop="OpeningHoursSpecification" datetime="Sa 08:00-20:00">8 am to 8 pm on Saturdays </time>
                  and from
                  <time itemprop="OpeningHoursSpecification" datetime="Su 08:00-17:00">8 am to 5 pm on Sundays</time>
                  . </p>
                
