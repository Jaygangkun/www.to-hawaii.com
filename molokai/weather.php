              <p>Just like on the other main Hawaiian islands, the weather on Molokai's south shore is usually drier and sunnier than on the island's north shore. That's because the predominant trade winds are blowing from the northeast about 90% of the time in the summer months (May to September) and about 50% of the time in the winter months (October to April). As moisture-laden clouds approach Molokai from the northeast, they get stuck on the island's tall sea cliffs, where the moisture comes down as rain. </p>
              <p>The weather on Molokai's north shore is therefore usually a lot wetter than on the south shore. But since Molokai's north shore is largely undeveloped, you will probably stay somewhere on the island's south or west shore where most of the visitor accommodations are located. The island's main town, <a href="../molokai/cities/kaunakakai.php">Kaunakakai</a>, is located on the south shore.  Molokai's average ocean temperature is around 75&deg;F (24&deg;C) in February and 82&deg;F (28&deg;C) in September.</p>
