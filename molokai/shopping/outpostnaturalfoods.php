                <h2 class="small">Outpost Natural Foods, Molokai</h2>
                <p>Outpost Natural Foods is the only health food store on Molokai. It's a small market where you'll find fresh local fruits, frozen foods, dairy, eggs, vitamins, cosmetics, dry foods, snacks and beverages, as well as a lunch counter where you can order freshly made burgers,  sandwiches and salads. There is a picnic table behind the building where you can eat your lunch.</p>
                <p>The store is housed in a small wooden building just off the main street in Kaunakakai. It is not air-conditioned, so they leave the doors open for ventilation. Some of the items on the shelves are a bit dusty because of that.</p>
                <p><strong><em>Editor's note:</em></strong> Check for the expiration dates of the items you would like to buy. On our recent visit we found some items that were well beyond their expiration date.</p>
