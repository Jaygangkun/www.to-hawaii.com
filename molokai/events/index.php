          <h1>Molokai Events</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>There are a variety of annual events on Molokai. Below is an overview for each month.</p>
              <p><strong>January</strong></p>
              <p>One of the most highly anticipated events on Molokai is the Ka Molokai Makahiki. Makahiki celebrated the time when battles ceased and harvest time began. Today, the festival features music, ancient games, hula and food.</p>
              <p><strong>February</strong></p>
              <p>No major events scheduled.</p>
              <p><strong>March</strong></p>
              <p>No major events scheduled.</p>
              <p><strong>April</strong></p>
              <p>Sponsored by the Nature Conservancy, the Earth Day Celebration is an annual event since 1995. It aims to highlight the importance of preserving the natural resources of Hawaii. The Earth Day Celebration is an enjoyable event with lots of food, music and educational exhibits. </p>
                                       <p><strong>May</strong></p>
              <p>Lei Day is an annual event celebrating one of the symbols of Hawaiian culture. Contests and parades mark the occasion. </p>
              <p>Another event is the Molokai Ka Hula Piko, which is centered around the hula. Performers and musicians from all over Hawaii gather around the island where hula originated for an event of great entertainment. </p>
              <p><strong>June</strong></p>
              <p>June is when King Kamehameha I, the leader who unified the Hawaiian islands over two centuries ago, is being celebrated in Hawaii.   While the event is centered in North Kohala on the Big Island of Hawaii,   the King&rsquo;s birthplace, all the Hawaiian islands celebrate with flower   festivals, dances and various cultural attractions.</p>
                            <? include ("../../theme/text.html") ?>
              <p><strong>July</strong></p>
              <p>Hoolaulea O Ke Kai-A Molokai Sea Fest is a celebration of one of Hawaii's most beautiful features: the sea. For an entire day, Kaunakakai hosts a series of events, such as windsurfing competitions, canoe races and live entertainment.</p>
              <p><strong>August</strong></p>
              <p>No major events scheduled.</p>
              <p><strong>September</strong></p>
              <p>Come September, Hawaii holds the annual Aloha Festivals. From September until October, each island celebrates Aloha Week. Parades and food booths are only some of the activities designed to entertain locals and tourists alike. A highlight of Aloha Week is the Great Molokai Mule Drag and Hollaulea. Mules, which are an important part of agriculture in Hawaii, are honored with a mule race and a mule-dragging contest.</p>
                    <p><strong>October</strong></p>
              <p>The Molokai Hoe is one of the toughest and most competitive contests in the world. It is an outrigger canoe race covering the 41 miles (66 km) from Molokai to Oahu through the Kaiwi Channel. Considered as the world championship of men's long-distance outrigger canoe racing, it is one of the most exciting events of the year. </p>
              
              <p><strong>November</strong></p>
              <p>The Hawaii International Film Festival is held during the first two weeks of November. Film makers from Asia, the Pacific Islands and the United States are invited to showcase their works. The films are first shown on Oahu, but are screened all over the islands during the final weekend.</p>
                      
              <p><strong>December</strong></p>
              <p>Christmas is welcomed Molokai-style through the Festival of Lights. Kaunakakai is home to this much-anticipated event. Parades, food and live music are only some of the activities to be enjoyed. </p>
