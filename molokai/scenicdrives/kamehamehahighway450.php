                <h2 class="small">Kamehameha Highway (Mile 1-20), Molokai</h2>
                <p>Kamehameha Highway (Hwy 450) (<a href="../../molokai/scenicdrives/p5.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) leads along Molokai's south shore, all the way from <a href="../cities/kaunakakai.php">Kaunakakai</a> (Molokai's main town) to its dead end at <a href="../attractions/halawa-valley.php">Halawa Valley</a>. This scenic drive only describes the section of Kamehameha Highway from Kaunakakai to <a href="../beaches/murphys-beach.php">Murphy's Beach</a> (also known as Mile Marker 20 Beach or Twenty Mile Beach because it is 20 miles from Kaunakakai). </p>
                     <p>This stretch of the highway is for the most part two lanes wide (except for the last mile leading to Murphy's Beach). It is a nice drive with views of the ocean on one side of the highway and views of the mountains on the other.</p>
                                   <? include ("../../theme/text.html") ?>
                <p>The further east you drive on Kamehameha Highway, the fewer cars you'll see. The first stretch (10 miles) of the highway, from Kaunakakai to Kamalo, is comparatively wide and straight. Beyond that it is a bit narrower and curvier. </p>
                <p>Along this entire section of coastline are many fishponds. However, most of them are not easily accessible as they are backed by private residences. One fishpond can easily be seen from the road. It is Niaupaia Fishpond, located across from <a href="../attractions/sevensorrows.php">Our Lady of Seven Sorrows Church</a>. </p>
                <p>You'll also drive by <a href="../beaches/onealiibeachpark.php">One Alii Beach Park</a> (which has two sections) and <a href="../beaches/kakahaiabeach.php">Kakahaia Beach</a>. Both offer nice views of the island of Lanai, but offer poor swimming conditions due to shallow and murky nearshore waters.</p>
                
