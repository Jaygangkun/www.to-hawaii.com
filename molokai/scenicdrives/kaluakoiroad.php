                <h2 class="small">Kaluakoi Road, Molokai</h2>
                <p>Kaluakoi Road (<a href="../../molokai/scenicdrives/p4.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is the main access road to Molokai's west shore. It leads from Maunaloa Highway (Hwy 460) to the Kepuhi Beach Resort, Ke Nani Kai Condos and other vacation rentals along Molokai's west shore, a 7-mile (11 km) drive. </p>
                <p>From the beginning of Kaluakoi Road (where it intersects Maunaloa Highway), it is a downhill drive with scenic views of Molokai's west shore, the resort area and the arid landscape in this part of the island. It's an easy and relaxed drive with no traffic. </p>
                <p>From Maunaloa Highway, the turn-off to Kaluakoi Road is easy to miss because there is no sign that tells you in advance when to slow down to make a turn. Watch out for the large sign right at the beginning of Kaluakoi Road that reads: Papohaku Ranchlands - Paniolo Hale - Ke Nani Kai - Kepuhi Beach Resort (see second picture in photo gallery above). </p>
