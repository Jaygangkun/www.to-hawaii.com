                <h2 class="small">Pohakuloa Road, Molokai</h2>
                <p>This scenic drive on Pohakuloa Road (<a href="../../molokai/scenicdrives/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  begins at the end of Kaluakoi Road (near Papohaku Beach) and ends at the road's dead end at Dixie Maru Cove (Kapukahehu Beach) on Molokai's west shore. It's just a 2-mile (3.2 km) drive, but with many scenic views along the way. </p>
                <p>The road overlooks the coastline and along the way are several beach access roads (to <a href="../beaches/poolaubeach.php">Po'olau Beach</a>, Paka'a Beach and <a href="../beaches/dixiemarucove.php">Dixie Maru Cove</a>). Few people come all the way out here, so you will have the road almost to yourself. It's an easy, relaxed drive with  views of the ocean, kiawe forests and west Molokai's arid landscape. </p>
