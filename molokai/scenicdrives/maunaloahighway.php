                <h2 class="small">Maunaloa Highway, Molokai</h2>
                <p>Maunaloa Highway (Hwy 460) (<a href="../../molokai/scenicdrives/p6.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) leads from Kaunakakai to Maunaloa, a 16-mile (26 km) long scenic drive through the central part of the island, past <a href="../attractions/molokaiairport.php">Molokai Airport</a>. Along the way you'll enjoy views of Molokai's south shore with its azure blue nearshore waters, open grasslands and mountains. </p>
                              <? include ("../../theme/text.html") ?>
                <p>If you're coming from Maunaloa and drive towards <a href="../cities/kaunakakai.php">Kaunakakai</a>, you'll have a great view of the East Molokai volcano in the distance, as well as the island's central plateau. If you're driving towards <a href="../cities/maunaloa.php">Maunaloa</a>, the highway winds uphill through the reddish-brown and arid landscape surrounding the West Molokai volcano. </p>
                
