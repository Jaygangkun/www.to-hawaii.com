                <h2 class="small">Kalae Highway, Molokai</h2>
                <p>Kalae Highway (Hwy 470) (<a href="../../molokai/scenicdrives/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is the road leading to the popular <a href="../attractions/kalaupapanationalpark.php">Kalaupapa Peninsula</a> lookout and <a href="../attractions/palaaustatepark.php">Pala'au State Park</a> on Molokai's north shore. The highway begins about 4 miles (6.4 km) northwest of <a href="../cities/kaunakakai.php">Kaunakakai</a>, where it intersects with Maunaloa Highway (Hwy 460). Going towards the state park, it's an uphill 5.8 mile (9.3 km) drive with great views of open grasslands, green mountains and Molokai's south shore. </p>
