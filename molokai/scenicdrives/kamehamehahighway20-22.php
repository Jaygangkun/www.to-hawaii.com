                <h2 class="small">Kamehameha Highway (Mile 20-22), Molokai</h2>
                <p>Kamehameha Highway (Hwy 450) (<a href="../../molokai/scenicdrives/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) leads along Molokai's southeastern shore, all the way from Kaunakakai to Halawa Valley. It is therefore also known as Halawa Valley Drive. This scenic drive only describes the section of Kamehameha Highway from Mile Marker 20 to 22. Mile Marker 20 is where popular <a href="../beaches/murphys-beach.php">Murphy's Beach</a> (also known as Mile Marker 20 Beach) is located. This is one of the better-known Molokai beaches and a favorite snorkeling spot when the ocean is calm. Many people as well as small tour buses only drive up until here because after Murphy's Beach, the road gets narrower and curvier. </p>
                              <? include ("../../theme/text.html") ?>
                           <p>If you don't have time to drive all the way to Halawa Valley (another 7 miles) or simply don't have the nerves to drive on curvy one-lane road with many blind curves for too long, you can continue on for just another 2 miles (3.2 km) to Sandy Beach. This stretch of the road leads right along the ocean, so close that the spray of the waves sometimes splashes up onto the road. Along the way you'll also notice a rock formation on the ocean side right next to the road. It is known as <a href="../attractions/rockpoint.php">Rock Point</a> (Pohakuloa).</p>
                <p>There is no room to stop anywhere because the road is just one lane wide, but since you'll be driving slowly and it's just 2 miles until <a href="../beaches/sandybeach.php">Sandy Beach</a>, you can still enjoy the views. Every 150-300 feet or so the road is a little wider to allow two cars going in opposite directions to pass each other. At Sandy Beach there is a spacious parking area where you can then easily turn around, or if you enjoy the adventure you can continue on all the way to <a href="../attractions/halawa-valley.php">Halawa Valley</a>.</p>
                <p>The two small islands that you can see about a mile offshore are called Moku Ho'oniki (the larger one) and Kanaha Rock. During World War II Moku Ho'oniki Island was used for bombing practice. There are still unexploded warheads on the island.</p>
                
