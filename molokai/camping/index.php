          <h1>Molokai Camping at Campgrounds / Campsites</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p><img src="../../images/molokaicamping.jpg" alt="Molokai Camping" style="width:100%; max-width: 478px;" /></p>
              <p>With its pristine landscapes and uninhabited beaches, the &quot;Most Hawaiian Isle&rdquo; of Molokai is a treat for adventurers. Its natural environment is the perfect backdrop to pitching a tent and spending the night beneath a blanket of stars.<br />
                <br />
                If you plan to camp on Molokai, no matter if at a state or county site, you will need to obtain a camping permit in advance. <br />
                <br />
                <strong>Camping at a Hawaii state park on Molokai</strong><br />
                <br />
                There is just one state park campsite on Molokai, located at <a href="../attractions/palaaustatepark.php">Palaau State Park</a> overlooking the Kalaupapa Peninsula, where you can pitch a tent. There are no other shelters or cabins here. Restrooms are available.</p>
              <p>Camping is also possible at the Waikolu State Forest Reserve, located near the center of the island. This campsite is only accessible by 4WD vehicle. There is a sheltered pavilion with an outdoor picnic table and toilet facility.</p>
             <? include ("../../theme/text.html") ?>
              <p>All state camping areas in Hawaii are administered by the Hawaii DLNR, State Parks. They prefer that all reservations are made on their online portal, which is located at: www.hawaiistateparks.org/parks/molokai (see the button &ldquo;Online Reservations&rdquo; in the top left corner on their website). You can make your reservation up to one year in advance. <br />
                <br />
                <strong>Here is an overview of state campsite fees:</strong><br />
                <br />
                Hawaii residents: $12 per campsite per night for up to 6 persons; $2 per night for each additional person*. Maximum fee per site: $20/night.<br />
                Non-residents: $18 per campsite per night for up to 6 persons; $3 per night for each additional person*. Maximum fee per site: $30/night.<br />
                *Children 2 and under free.</p>
                        <p> <br />
                <strong>Camping at county campsites on Molokai</strong><br />
                <br />
                There are two county campsites on Molokai &ndash; <a href="../beaches/onealiibeachpark.php">One Alii Beach Park</a> and <a href="../beaches/papohaku-beach.php">Papohaku Beach Park</a>. To get a camping permit for one of the county campsites, contact the County of Maui Parks &amp; Recreation. On their website you can download and print a camping permit request form and mail it in. The form is located here: www.co.maui.hi.us/index.aspx?NID=409 </p>
