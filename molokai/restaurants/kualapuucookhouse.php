                <h2 class="small">Kualapu'u Cookhouse, Molokai</h2>
                <p>Located in the residential area of Kualapu'u, around 9.6km (or six miles)  from Kaunakakai, Kualapu'u Cookhouse is a popular eatery with residents of Molokai and visitors to the island. A  traditional plantation-style building is home to this diner and the furnishings  are relatively modest. Despite this, the menu is quite extensive and is  comprised of a variety of savory and sweet dishes. The Cookhouse offers a  choice of indoor or outdoor seating.</p>
                <p>With a limited number of restaurants on Molokai, both locals and visitors tend to  make the most of Kualapu'u Cookhouse, which serves breakfast, lunch and dinner.  The menu is roughly comprised of a selection of breakfast items, sandwiches,  burgers and main dishes. As well as some hefty breakfast options, diners can  choose Corned Beef Hash, Scrambled Egg or Pancakes, among other fare, to  kick-start their day. Some of the dinner menu offerings include Loco Moco,  Country Fried Chicken, Mushroom Swiss Burger served with fries and, usually,  one or more daily specials. Although the food presentation is good, many dishes  are quite calorific and possibly not for the figure-conscious or those on a  diet. The ambience in Kualapu'u Cookhouse is family-orientated and informal.  The interior furnishing is unfussy while the outdoor dining space is comprised  of a number of picnic tables where diners can enjoy their repast in the open  air.</p>
                              <? include ("../../theme/text.html") ?>
                <p>A family-focused restaurant offering hearty fare, Kualapu'u Cookhouse can seem like  something of an oasis for those travelling from Kaunakakai to Kalaupapa  Peninsula or vice versa. The Cookhouse serves breakfast and lunch every day.  Dinner is available five days a week, but not on Sundays or Mondays. A take-out  service is available for those who require it. It is worth noting that  Kualapu'u Cookhouse is a cash only establishment, but cash can be obtained from  the grocery store opposite.</p>
                
