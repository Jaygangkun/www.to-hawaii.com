            <h1>Hawaiian Tattoos </h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Tattooing has been an important part of  tribal life on the Polynesian islands for centuries, and Hawaii is no different. The tattoo artists of Hawaiian tribes were carefully trained and  held in high regard within the community. In fact, having a good tattoo artist  was a symbol of wealth and status at that time.</p>
<p>In  the beginning, native designs were tattooed on the skin to designate tribe and  hierarchy (even today Hawaiians view tattoos on a man&rsquo;s body as a sign of  status and importance) and was part of a warrior's rite  of passage to get the markings that he was a full member of the community. The most heavily  tattooed were the royal family, followed by other court  officials and those who had married into royalty. </p>
<p>Black ink, the most common color of a  Hawaiian tattoo and a feature still prevalent on the islands today, was most  commonly made up of kukui nut ashes and sugarcane juice. Other colors were  rare, but when they were employed, were made up of various brightly colored  island flowers, such as the Hawaiian Iris. </p>
 <? include ("../theme/text.html") ?>
<p>Before modern safety procedures arrived, it  was important to get tattooed by an artist who knew what he was doing because  some of the contents of the ink they used were poisonous, and ancient  instruments would have meant that the person getting tattooed was subject to  extreme pain. Of course even today it is important that a tattoo artist knows  what he is doing, but nowadays regulations are so strict that it is almost  impossible for amateurs to operate, whereas back then, the profession was open  to whoever wanted to do the job.</p>
<p>In  the old Hawaii,  skin was inked with tools from nature, like cactus  barbs,  bird beaks, fish bones, urchin spines and sharp animal  claws. The tattoo tool would be hit by a stick to make the punctures, while  assistants stretched the skin and wiped away blood.</p>
<p>Early  tattoos were mainly made up of an array of geometric and symmetrical designs  found in nature, such as stones, waves, sun and rain. But the art soon evolved -  much like today - as a means of celebration and self-expression, with more  pictorial forms, such as images of animals and flowers. Today, these motifs that  represent the island world remain, in the form of lizards, sea turtles, sharks and  dolphins. &nbsp;</p>
<? include ("../theme/text-desktop-only.html") ?>
<p>Placement  was important too - ancient lithographs show Hawaiian women wore designs  concentrating mainly on their hands, feet, fingers and calves, and both men and  women had facial tattooing, typically found on the brow ridge, cheek, cheek  bone and chin. </p>
<p>What  makes Hawaiian tattoo designs different from other parts of the world, and,  indeed their Pacific island neighbors, is that they are bolder and larger and &ndash;  given that they were originally used for individual identification rather than  ceremonial purposes - usually have a hidden, personal meaning. </p>
