?> <h3>The hula</h3>
                <p>Another Hawaiian icon is the hula. It&#39;s an image of swaying hips, graceful hands and colorful costumes. The hula has evolved over the years from an activity exclusively for men and for religious purposes to today&#39;s contemporary dances, where both the men and women dance for fun, expression and enjoyment. Hula today has two major forms, the ancient, or hula kahiko and the modern, 
                  or hula auana. </p>
                <p>Hula kahiko is accompanied in the Hawaiian language along with drums and other percussion implements. Hula kahiko is performed for storytelling or for religious and ceremonial purposes. Dancers are adorned with bark cloth, coconut fibers and native grasses, plants and ferns. Hula auana is colorful, fun, upbeat and musical. Auana dances are accompanied by song in either English or Hawaiian, as well as ukuleles and piano guitars. The dancers' dresses are colorful and are decorated with flower prints. </p>
