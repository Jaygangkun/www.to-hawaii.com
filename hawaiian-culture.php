                <p><strong>Expert canoe builders and non-instrument navigators</strong></p>
                <p>Early Hawaiians have a history of being expert canoe builders. The tradition requires that prayer and
                  food offerings are made to forest gods before, during and after the tree is taken from the forest. 
                  Cultural protocols determine exactly how the tree for a canoe would be felled.</p>
                <p>Early Hawaiians were also master non-instrument navigators. Using only the sun, moon, stars and waves, they were known to traverse the Pacific, which resulted in the occupation of many small islands in Polynesia. The Hawaiians had an intimate knowledge of the location and types of celestial bodies. Everything from the North Star to the South Cross have Hawaiian identities and lore.</p>
                <p><strong>The language</strong></p>
                <p>Then there&#39;s the Hawaiian language. Comprised of only five vowels and eight consonants, Hawaiian has the  shortest alphabet in the world. Tricky for most to master, the language is compromised of soft sounds with words having many and often hidden meanings. One of the most notable is the word aloha. It&#39;s probably the most widely known of all Hawaiian words, and it is one of the most important. </p>
                                 <p>Aloha not only means hello, goodbye and love, it also means sympathy, kindness, compassion, affection and fondness. This word is more than a greeting or expression of love - it is the basis of what Hawaiians consider to be one of the culture's core values. </p>
