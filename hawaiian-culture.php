                <h2>An overview of Hawaii&#39;s culture</h2>
                <p>Hawaii is a tropical paradise which has more than its share of pristine beaches, stunning sunsets, towering waterfalls and reefs teeming with colorful fish. Anyone who has been here knows Hawaii is different, unique, special and unforgettable. What sets these islands apart from the rest of the world? It&#39;s the native culture, the Hawaiian culture. It&#39;s a culture that is filled with fascinating customs, music,  legends, traditions and values. </p>
                <p><strong>The flower lei</strong></p>
                <p>Today, probably the most enjoyable and unforgettable Hawaiian custom is the flower lei. 
                  Custom dictates that a lei should be offered graciously with a kiss and removed only in private. It&#39;s considered rude to remove a lei once it&#39;s accepted in view of anyone, but especially in view of the person(s) who gave it to you. Lei designs are limited only to the imagination and range from the simple common one-strand orchid or tuberose presented as a special treat to those who arrive by plane or ship to the more elaborate or rare depending on how important the occasion may be.</p>
