                <p><strong>The music</strong></p>
                <p>Music has also grown to be a familiar and popular part of Hawaiian culture. Ever evolving from a beginning of simple drumbeats and chants, music today is filled with a multitude of artists and genres that include hapa-haole (Hawaiian melody with English lyrics), traditional,
                  luau, kolohe (naughty or teasing hula), chalangalang, jawaiian and more.</p>
                <p><strong>The legends</strong></p>
                <p>Another facet of the culture lies in legends. Ancient Hawaiians were known to be storytellers. Legends were a way of documenting history, knowledge, facts and beliefs from generation to generation. Some of the more popular legends include the story of the demi-God Maui, who pulled the island up from the bottom of the sea, the wicked, deceptive and jealous volcano goddess Pele and her snow god sister Poliahu. There are legends about the Kumulipo, which tell about the creation of the Hawaiian Islands and others that tell fishing stories about the shark gods.</p>
                <p>The Hawaiian culture also has many superstitions and omens, which are widely known and still observed today. Rain and rainbows are considered blessings from the gods. This is especially true if it rains during weddings. Taking pork over the Pali Highway, which connects the leeward to the windward side on the island of Oahu, is considered a deed that is said to anger the gods and to bring bad luck or at least car trouble. It&#39;s still considered bad luck to bring bananas on a boat, to step over a baby who is lying on the floor and to wear a lei if you are pregnant.</p>
                <p>Another dark omen that is more modern in nature is about the taking of lava rocks from a volcano, which will lead to being followed by bad luck. Many such rocks are known to be returned by visitors to Hawaii via mail. People often times send the rocks they collected and took home back to the Hawaii Volcanoes National Park with no return addresses. </p>
                <p>The Hawaiian culture is rich, living, mysterious and unique. It&#39;s what truly makes Hawaii special. So, when you&#39;re in Hawaii, be sure to look past the sunsets, sunshine, surf and sand and take the time to explore the culture that makes the Hawaiian Islands truly special and unforgettable.&nbsp; </p>
