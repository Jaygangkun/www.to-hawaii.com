                <p>Given the amount of showers and misty rain  that occurs when the sun is shining, rainbows are a common sight in Hawaii. In fact, one of Hawaii&rsquo;s nicknames is &ldquo;The  Rainbow State,&rdquo; and a rainbow is also depicted on vehicle licence plates.</p>
                <p>Rainbows can be seen when the sunlight  breaks up as it shines through the prism-like raindrops during a rain shower.  The curved spectrum of light includes all the colors that make up the sun&rsquo;s  white light.</p>
