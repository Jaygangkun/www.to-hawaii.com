                              <p>To clearly se a rainbow, the viewer must be  positioned between the sun and the rain, with his back to the sun. The eyes  must also be aligned with the sun, so rainbows are mostly seen either early or  late in the day, as this is when the sun is low in the sky and in the right  position for spotting a rainbow.</p>
                <p>Sometimes, double rainbows can be seen on  the Hawaiian Islands. These are caused by one  extra reflection of sunlight within the raindrops and are usually about 40 percent  dimmer than the original rainbow. An interesting fact about double rainbows is  that the colors are reversed in the second arc.</p>
