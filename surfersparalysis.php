                <p>Surfer's myelopathy is a nontraumatic spine injury. It usually occurs when surfers arch their back too often or too long while paddling on a surfboard. The arching of the back reduces blood flow to the spine, which can lead to paralysis of the legs. Even a one-hour surfing lesson is enough to cause this. People who became afflicted with this condition sometimes notice a slight back pain while surfing. A short while later, they are unable to move their legs or walk. Some recover completely over time, some never do, some only do partially. It depends on the severity of 
                  the injury and each individual person.</p>
