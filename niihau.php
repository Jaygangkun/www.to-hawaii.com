              <p>Can you imagine that the Hawaiian island of Niihau was once sold for as little as $10,000? A sum you can't even buy a house for today. Back then, in 1863, King Kamehameha 
                IV put the island of Niihau up for sale for that amoung and Elizabeth Sinclair, who was a resident of the neighboring island of Kauai decided to buy it. Nowadays her descendants, the Robinson family, are the owners of this small Hawaiian island and they have committed to preserve Niihau and keep the Hawaiian heritage alive. Today, Niihau is the most Hawaiian island and the only place where Hawaiian is the first and main language.</p>
