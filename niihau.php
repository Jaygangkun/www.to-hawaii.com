              <p>Niihau is also knows as "The Forbidden Island" because non-native Hawaiians are restricted to go there. Today, there are approximately 250 native Hawaiian residents living on Niihau and their lifestyle has remained the same over all these years. There are no shops or restaurants on the island, no electricity, no paved roads, no cars and no hotels. So in other words life is very simple. People get around by bike and on foot.</p>
              <p>The island of Niihau encompasses 70 square miles and it is located 18 miles northwest of Kauai. The only job available on Niihau is at the Robinson family ranch, where the majority of the population works raising cattle. They get food from the ranch as well. In addition, the people from Niihau also grow their own fruits and vegetables. They practice fishing and hunting with spears, nets, ropes and knives. The majority of the hunting tools are self-made and the rest are bought on the neighboring island of Kauai.</p>
              <p>Niihau is very dry, so the scenery and vegetation is not as lush and tropical as on the other Hawaiian Islands. Niihau doesn't even have the typical flower lei that is so common on the other islands. But instead, they make wonderful shell leis from unique shells that can be found on Niihau's beaches. Since these types of shells are so unique and rare, collectors are ready to pay thousands of dollars for Niihau shell lei. </p> <?php
