                               <p><strong>Formed by undersea volcanoes</strong></p>
                <p>All of the Hawaiian Islands were formed by volcanic activity, which means that the islands are actually the tips of huge mountains underneath the ocean that were born from gigantic eruptions. Starting from northwest and going down to the southeast, each volcano is progressively younger. The oldest one in the northwest of the island chain was formed about 80 million years ago. The volcanoes on the southernmost island, the Big Island of Hawaii, are still active today and the creation of new land can be witnessed there on many days of the year. </p> <?php
