                <h2>Hawaii &ndash; the longest and most isolated archipelago in the world</h2>
                <p>Did you know that Hawaii is the most remote island chain in the world? In fact, the Hawaiian archipelago is located in the Central Pacific Ocean, 2,390 miles (3,850 km) from California and 3,850 miles (6,195 km) from Japan. Hawaii is the furthest from any landmass in the world.</p>
                <p>Formerly known as the Sandwich Islands, the Hawaiian Islands span the distance of 1,523 miles (2,451 km) from the Big Island of Hawaii in the southeast to the Kure Atoll in the northwest. This makes Hawaii the world's longest island chain. </p>
                 <? include ("theme/text.html") ?>
                <p>The state of Hawaii is named after the largest of the islands, located in the south of the archipelago. The eight main Hawaiian Islands are Oahu, with the state capital of Honolulu, Hawaii (Big Island), Maui, Kauai, Molokai, Lanai, Niihau und Kahoolawe. They are located 
                  at the southeastern end of the island chain. Toward the northwest, there are 124 more uninhabited islands, reefs, shoals and atolls that are collectively known as the <a href="northwesternhawaiianislands.php">Northwestern  Hawaiian Islands</a>. </p>
                <h2>Hawaii's location in the world</h2>
