                <p>The highest mountains are located on the Big Island. Mauna Kea rises 13,796 feet (4,205 m) and Mauna Loa 13,679 feet (4,169 m) above sea level. The mean elevation of the state of Hawaii is 3,030 feet (923 m) above sea level.</p>
                <p>Hawaii is the 43rd largest of the 50 states in the USA ahead of Rhode Island, Delaware, Connecticut, New Jersey, New Hampshire, Vermont and Massachusetts.</p>
                <p>Hawaii's total land area is 6,422.63 square miles (16,634.53 km&sup2;). Hawaii's total coastline is 750 miles (1,207 km). Hawaii doesn't have many rivers, but the longest river is the Kaukonahua Stream on Oahu, measuring 33 miles (53 km) in length. Although Hawaii doesn't have many rivers, the Islands are home of many waterfalls. The highest 
                  waterfall is Kahiwa, a 1,750-foot (533 m) cascade on Molokai. </p>
                                           <p>Hawaii's location on planet Earth: 21&deg;18'41&deg;N 157&deg;47'47&deg;W</p>
