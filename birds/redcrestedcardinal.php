<h2 class="small">Red-Crested Cardinal Overview</h2>
<p>The red-crested cardinal is common in the lowlands of the main Hawaiian islands. Native to South America, it was introduced to Oahu around 1930. It is also known as Brazilian Cardinal. Red-crested cardinals can be seen in parks, lawns and open dry forests. They feed on seeds, insects, plant matter and fruits. <br />
  <br />
  Adult males and females look similar. Adult birds have a red head, crest and upper breast, a silver-grey bill and dark legs. Juvenile birds look similar to adults, but their head and crest is orange-brown and their bill is black.</p>
