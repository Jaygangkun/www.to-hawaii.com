<h2 class="small">Masked Booby Overview</h2>
<p>Known as 'a in the Hawaiian language, masked boobies nests on Moku Manu Island off Oahu and on Ka'ula off Ni'iihau. It is rare elsewhere in the main Hawaiian islands. It can also be found on some northwestern Hawaiian islands. <br />
  <br />
  With a wingspan of 60 inches (152 cm), the masked booby is the largest of all Hawaiian boobies. Adult birds are white with a black tail and black edge of the wings. Their bill and iris are yellow and their feet are dark. <br />
  <br />
  They typically fly low over the ocean's surface and can also be seen plunging down from great hights to catch fish and squid. Breeding season is from January to June. Two eggs are laid in a hole on the ground, but usually only one chick is raised.</p>
