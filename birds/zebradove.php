              <h2 class="small">Zebra Dove Overview</h2>
              <p>Zebra doves are some of the most commonly seen birds in urban areas in Hawaii. They frolic in beach parks, on sidewalks, in shopping centers and yards. They are quite tame and often times don't fly away when a human comes close to them. </p>
              <p>Their habitat reaches from sea level to hills up to 4,000 feet (1,220 m) in elevation. Zebra doves are native to Asia and were introduced to Hawaii in 1922. They are also known as Barred Doves. Adult males and females look similar. Their body is mainly brow-gray in color with a rose-colored belly and a bluish-gray face. Their legs and feet are reddish and the skin around their eyes and bill is a pale blue.</p>
              <p>Zebra doves mainly feed on small seeds. Breeding season is year-round and the nest is usually built in a tree or shrub, or in urban areas in building crevices or telephone lines. One or two white eggs are laid each time and they breed up to five times per year.</p>
