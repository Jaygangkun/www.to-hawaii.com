<h2 class="small">Northern Fulmar Overview</h2>
<p>The northern fulmar is related to albatrosses and shearwaters. It is most common in the northern oceans. The birds have a wingspan of 40-44 inches (102-112 cm). They are grey and white in color with a pale yellow bill and bluish legs. They are rather clumsy on land, but strong fliers. 
  Northern fulmars enjoy long lives and can live around 30 years.<br />
