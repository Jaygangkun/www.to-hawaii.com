              <h2 class="small">Semipalmated Sandpiper Overview</h2>
              <p>The semipalmated sandpiper is a small shorebird and a long-distance migrant. In the winter months, the birds migrate in large flocks to warmer regions. They forage on mudflats and mainly feed on aquatic insects and crustaceans. Their breeding habitat is Alaska and Canada's southern tundra. They nest on the ground. The male bird makes a few shallow scrapes in the ground and the female then chooses one and adds grass and other nest material. The female then lays four eggs and the male assists in incubation. When the young ones are just a few days old, the parents leave them and they feed themselves.</p>
