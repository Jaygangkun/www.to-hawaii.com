              <h2 class="small">Great Frigatebird Overview</h2>
              <p>Known as 'iwa in the Hawaiian language, the great frigatebird is common in the Northwestern Hawaiian Islands, where it nests (it is not believed to nest in the main Hawaiian islands, but some birds roost on Moku Manu off the island of Oahu). Great frigate birds are large with a wingspan of 90 inches (229 cm). Wings are pointed and angular and they have a deeply forked tail. <br />
                <br />
                Adult males are mostly black and have an inflatable red throat pouch that they use in courtship. Adult females are also mostly black, but with a white breast and throat. Breeding season is from March to May. One egg is typically laid in a nest composed of sticks and built in shrubs or low trees.</p>
