              <h2 class="small">Hawaiian Goose - Nene Overview</h2>
              <p>The nene is the state bird of Hawaii. It can be found on Maui (at Haleakala National Park), on the Big Island (at Hawaii Volcanoes National Park, Mauna Loa and Pu'u Wa'awa'a), on Kauai (Koke'e State Park, Kilauea Point) and on Molokai. <br />
                <br />
                The nene's habitat is grasslands, scrublands, golf courses and open lowlands. They feed on various native and introduced plants. Breeding season is from November to June. They build the nests hidden under bushes and lay between two and five eggs at a time.<br />
                <br />
                Nearly extinct in 1951, captive-bred birds were reintroduced into the wild in the 1950s on the Big Island, Maui and Kauai. Their numbers have increased a lot on Kauai in recent years since there are no mongooses on this island, which are predators of the nene.</p>
