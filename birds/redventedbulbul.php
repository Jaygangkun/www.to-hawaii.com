              <h2 class="small">Red-Vented Bulbul Overview</h2>
              <p>The red-vented bulbul is common on the island of Oahu, but it has also been observed on the Big Island, Kauai, Kahoolawe and Molokai. It came to Oahu in the 1950s as an unauthorized cage release.<br />
                <br />
                Adult males and females look similar. This species is known to be noisy. It can be seen in both urban areas and mountain forests. Breeding season is year-round. <br />
                <br />
                They feed on insects, fruits and the occasional lizard. It liked to feed on orchid buds and all kinds of fruits and has become an agricultural pest.</p>
