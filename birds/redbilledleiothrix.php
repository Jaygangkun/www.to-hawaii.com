              <h2 class="small">Red-Billed Leiothrix Overview</h2>
              <p>The red-billed leiothrix can be found in forests on all main Hawaiian islands, except Kauai and Lanai. Native to China, it was introduced to Hawaii in 1918. The red-billed leiothrix is also known as Pekin Nightingale or Japanese Hill.<br />
                <br />
                Adult males and females look similar. Their body is olive-green with a yellow throat. They have a red patch on their wings, a red bill, orange breast and a yellow belly. <br />
                <br />
                The red-billed leiothrix is difficult to spot as it likes to forage on the ground in bushes to find fruits and insects. Breeding season is from March to August. The nest is built in low shrubs. Between two and four eggs are laid. The eggs are blue with red spots.</p>
