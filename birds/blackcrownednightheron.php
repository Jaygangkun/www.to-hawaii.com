              <h2 class="small">Black-crowned Night Heron Overview</h2>
              <p>Black-crowned night herons, known as auku'u in the Hawaiian language, are wetland birds and can be found on all main Hawaiian Islands. A few of them have also made it to the Northwestern Hawaiian Islands. They can even be seen even at popular beach parks, such as at Ala Moana Beach Park on Oahu. </p>
              <p>They feed on fish, frogs, crustaceanas, insects and chicks of other waterbirds. Their habitat is around streams, marshes, exposed reefs, ponds and tide pools. Black-crowned night herons are most active at dawn or dusk. Breeding season for the black-crowned night-heron is between May and June. The nest is usually located up in trees and is composed of sticks.</p>
