<h2 class="small">House Finch Overview</h2>
<p>The house finch is common on all main Hawaiian Islands. It can be found in coastal regions as well as high up in the mountains. It is most common in urban areas. In Hawaii, the house finch is also known as papayabird or linnet. <br />
  <br />
  House finches  feed on seeds, insects and soft fruits. Breeding season is from February to August. Nests are built in trees in which between three and five greenish-blue eggs are laid. Both adult males and females have brown streaks. Adult males have a red or orange-yellow forehead.</p>
