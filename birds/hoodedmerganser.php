<h2 class="small">Hooded Merganser Overview</h2>
<p>The hooded merganser is a small fish-eating duck that can be seen on shallow ponds. Adult males in breeding season have a circular crest with a large white patch, golden eyes, black face, white chest with black and white stripes on the sides and a dark bill. In non-breeding season they have a grayish-brown back, breast and sides, a white belly, yellow eyes and a black bill.<br />
  <br />
  Adult females have a brown body, a small white patch on the chin and at the base of the wings. Their eyes are brown and their upper mandible is a blackish green with an orange edge. Their lower mandible is yellow or orange.</p>
