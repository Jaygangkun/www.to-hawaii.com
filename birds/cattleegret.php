              <h2 class="small">Cattle Egret Overview</h2>
              <p>The cattle egret is common in urban as well as remote areas on Oahu, Kauai, Maui and the Big Island (and is fewer on Molokai and Lanai). A few can also be found in the Northwestern Hawaiian Islands. <br />
                <br />
                Their habitat is around ponds, pastures, taro fields and along roadsides. On Oahu, they frequent the area around Pearl Harbor and Kaneohe Bay. On Maui, they can be seen around Kanaha and Kealia ponds. On the Big Island, colonies live around Loko Waka Pond. And on Kauai, many can be seen near Kilauea Point. <br />
                <br />
                Cattle egrets mainly feed on insects and crayfish. They travel in flocks to and from their feeding sites. Common nesting areas are in kiawe or mangrove trees.</p>
