              <h2 class="small">Ruddy Turnstone Overview</h2>
              <p>Known as &lsquo;akekeke in the Hawaiian language, the ruddy turnstone is a winter visitor to the Hawaiian Islands, typically arriving in August and leaving for arctic breeding grounds in April.</p>
              <p>They can be seen along shorelines, fields and mudflats and often times stick together in small flocks. In the late summer flocks of 250 or more birds can be observed. They usually fly together and the entire flock rises and turns in unison.</p>
              <p>They feed on crustaceans and insects and may even break open seabird eggs with their bill. Their call is a rattling a-ke-ke-ke.</p>
