<h2 class="small">Long-Tailed Duck Overview</h2>
<p>The long-tailed duck's plumage varies throughout the year. In the summer months, adult males have a black head, chest and wings with a gray face. During this time, their upper back feathers are long, buffy and black in the center. In the winter months, adult males have a white head and neck and a grey patch around the eyes. During this time, they have a large black spot that extends from their cheek down the sides of their neck.<br />
  <br />
  The summer plumage of adult females is a dark head and neck with white around the eyes. During this time their breast and back is brown or gray. In the winter months, adult females have a white head and neck with a round dark brown cheek patch, a white belly and a brownish-grey breast and back.<br />
  <br />
  Long-tailed ducks feed by diving for small fish, crustaceans and mollusks. They can dive to depths of 200 feet (61 m).</p>
