              <h2 class="small">Hawaiian Stilt Overview</h2>
              <p>Known as ae'o in the Hawaiian language, the Hawaiian stilt is endemic to the Islands. It can be found on all main Hawaiian islands. Adult males and females look similar. Their bodies are tall and slim, their bill is long, thin and black and their legs are long and pink. Adult males have a black back while females have a brown-tinged back. Both sexes have white bellies. Their live in marshes, ponds and mudflats. Stilts feed on crabs, fish, worms and insects.</p>
