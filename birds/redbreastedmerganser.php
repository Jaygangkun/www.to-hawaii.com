              <h2 class="small">Red-Breasted Merganser Overview</h2>
              <p>The red-breasted merganser is a diving duck that lives in rivers, lakes as well as oceans. However, it prefers saltwater. It has a long and thin bill. Adult males have a dark head with a green shimmer to it, a white neck with a rusty-colored breast, a black back and white underparts. Adult females have a rusty-colored head and a grey body. <br />
                <br />
                They dive to find food and feed on small fish, crustaceans, frogs and aquatic insects. It has been said that the red-breasted merganser is the fastest-flying bird in level flight, reaching speeds of 100 mph (161 km/h). However, this is disputed as the white-throated needletail is believed to be able to fly faster, at 105 mph (170 km/h).</p>
