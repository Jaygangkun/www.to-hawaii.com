<h2 class="small">Rock Dove Overview</h2>
<p>Rock doves are common on all main Hawaiian islands. They are also known as Domestic Pigeon. Native to Europe, Africa and Asia, rock doves were introduced to Hawaii in 1796. </p>
<p>Adult males and females look similar. Wild rock doves are grey with a shimmering neck, a gray tail and black wingbars. Some urban rock doves are white with a red bill and legs. </p>
<p>Rock doves live in the wild along cliffs and mountain gorges as well as in cities and towns. In cities they like to roost on buildings. </p>
<p>They feed on seeds and many types of food that humans leave behind, such as bread crumbs.</p>
