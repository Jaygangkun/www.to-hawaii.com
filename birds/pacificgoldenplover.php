              <h2 class="small">Pacific Golden Plover Overview</h2>
              <p>The Pacific golden plover is a migratory shorebird that breeds in Siberia and western Alaska in the summer months and winters on islands across the Pacific (including Hawaii), Southeast Asia and northeastern Africa. They nest on the ground in a dry, open area.</p>
              <p>Adult birds have a spotted gold and black crown, back and wings. Their face and neck are black with a white border. The breast and legs are black. In the winter months, they have a yellowish face and breast and white underparts. 
                Their diet includes insects, crustaceans and berries.</p>
