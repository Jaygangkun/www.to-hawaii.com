              <h2 class="small">Hawaiian Moorhen Overview</h2>
              <p>Known as &lsquo;Alae &lsquo;ula in the Hawaiian language, the Hawaiian moorhen generally occurs in wetland habitats below 410 feet (125 m) of elevation on the islands of Kauai and Oahu, as well as in small numbers on Maui's Keanae Peninsula and on the Big Island of Hawaii. On Kauai, the largest populations can be found in the Hanalei and Wailua river valleys. </p>
              <p>On Oahu, Hawaiian moorhen can most frequently be seen between Haleiwa and Waimanalo, as well as at Pearl Harbor and Lualualei Valley. They live in wetland agricultural areas (e.g. taro patches), freshwater marshes, reservoirs, wet pastures and sometimes in brackish water habitats. They feed on grass seeds, plant material, algae, insects and snails. <br />
                <br />
                Nests are built in areas with standing freshwater less than 24 inches (61 cm) deep with dense surrounding vegetation. Breeding season is year-round, but the main season is between March and August.</p>
