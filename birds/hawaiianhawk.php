              <h2 class="small">Hawaiian Hawk Overview</h2>
              <p>Also known as &lsquo;io in the Hawaiian language, Hawaiian hawks are common on Hawaii&rsquo;s Big Island, particularly at the Hawaii Volcanoes National Park and in valleys along the Hamakua Coast. They can be seen soaring in updrafts over grasslands and forests.</p>
              <p>Adult females are larger than adult males. Adult birds have a dark head and yellowish feet and legs. Young birds have a light head and greenish feet and legs.</p>
              <p>Their diet includes insects, rodents and small birds. In breeding season between one and three light blue eggs are laid in a next built of twigs and leaves in a tree. Usually only one chick is raised. Both parents feed the young and aggressively protect the nest. </p>
              <p>The &lsquo;io is a symbol of royalty in ancient Hawaiian legends.</p>
