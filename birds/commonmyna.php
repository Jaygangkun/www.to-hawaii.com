              <h2 class="small">Common Myna Overview</h2>
              <p>The common myna is common on all main Hawaiian islands. Mynas were introduced from India in 1865 to control insect pests. Males and females look similar. Male adults are brown and have a black head with yellow around their eyes.<br />
                <br />
                Mynas travel in pairs and are social birds gathering in noisy flocks. They feed on insects, fruits, small lizards and scraps.  Breeding season is from February to August. Nests are usually built in tree holes or in cavities in buildings.</p>
