            <h1>The Legend of the Menehune</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Hawaiian legend has it that many centuries ago, the Menehune were a mischievous group of small people, or dwarfs, who lived hidden in the forests and valleys of the islands before the first settlers arrived from Polynesia. These Menehune, who roamed the deep forests at night, were said to be about two feet (60 cm) tall, though some were as tiny as six inches (15 cm), small enough to fit in the palm of a hand. They enjoyed dancing, singing and archery, and their favorite foods were bananas and fish. </p>
              <p>The Menehune have been known to use magic arrows to pierce the heart of angry people, igniting feelings of love instead. They also enjoy cliff diving, and according to local lore, they were smart, extremely strong and excellent craftsmen. They were rarely seen by human eyes, and they are credited with mighty feats of engineering and overnight construction.</p>
       
              <p>These industrious master builders used their great strength to build temples (heiau), fishponds, roads, canoes and houses. One such structure they are believed to have built is  Kikiaola, also known as the Menehune Ditch,  a historic irrigation ditch  that funnels water from the Waimea River on Kauai. Another one of their amazing feats is  the  legendary overnight creation of the <a href="../kauai/ancientsites/menehunealekokofishpond.php">Alekoko Fishpond</a> on Kauai, which archaeologists estimate to be around 1,000 years old. </p>
              <p>It is said that they built the Alekoko Fishpond for a princess and her brother. The shy but strong group lined up in a double row, which stretched 25 miles (40 km) to distant Makaweli. The workers passed stones hand-to-hand to build the pond. They worked at night so as not to be seen by others, cutting, transporting and fitting stones for their projects in a long bucket brigade. If they were discovered, their work would have been abandoned. </p>
              <? include ("../theme/text.html") ?>
              <p>The Menehune were promised no one would watch them at work, which was carried out after dark. However, one night the royal siblings snuck up and watched the thousands of Menehune at work, only to fall asleep. At sunrise the Menehune discovered them and turned them into twin stone pillars that can be seen today in the mountains above the fishpond. Interrupted by the sun, the Menehune left two gaps in the fishpond wall. Many generations later, Chinese settlers filled the gaps to raise mullet, but the stonework that closed the gap was far inferior to that of the mystical Menehune.</p>
              <p>Another description that has been passed down in local folklore is of the three Menehune of Ainahou. Ainahou is a forest on the north side of Halekala Crater on Maui. The three  Menehune were called  Ha'alulu, Molawa and Eleu. All the other Menehune living in Hawaii knew them well because they possessed very unusual powers. Ha'alulu means &quot;to tremble&quot; and it seemed like this little man was always cold, but his magic gift was that whenever he would start shaking, he would become invisible and could travel anywhere without being detected. Eleu in Hawaiian means &quot;quick and nimble&quot; and whenever Eleu moved, he was so quick that he disappeared and no one could follow him. Molowa's name means &quot;lazy,&quot; but what most people didn't know was that whenever he appeared to be sleeping or lazy, his magical self became imperceptible and he would go around the island and do good deeds. </p>
              <? include ("../theme/text-desktop-only.html") ?>
              <p>Even though the Menehune were said to be displaced when the first settlers arrived in Hawaii, some people still believe that the Menehune are roaming the islands, carrying out tricks on people. Indeed, an 1820 Census of Kauai listed 65 people as 'Menehune.' Other Hawaiian mythology records refer to a few other forest dwelling races: the Nawao &ndash; who were large and wild hunters descended from Lua nu'u &ndash; the Mu people and the Wa people.</p>
