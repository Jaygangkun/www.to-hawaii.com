            <h1>The Legend of Ka'ena Point</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>On the northwestern coast of Oahu  is a point of land called Ka'ena Point. Nearby is a huge boulder known as Pohaku o Kauai, or Rock of Kauai. Legend  has it that both of these landmarks came to be because of the actions of one man  on a dark, stormy night many years ago.</p>
<p>  On this night, when the wind raged, lightning struck,  thunder rumbled and the rain beat down from the heavens upon the islands of Hawaii, a baby boy was  born. The storm was so vicious that the unrelenting rain that fell from the  black clouds washed the red soil down through the valleys and soon the flooded streams  and thundering waterfalls churned as red as blood.&nbsp; </p>
<p>  As the storm raged on, a rainbow appeared above the house  in which the baby was born. It was still there the next day when the storm died  down, the sky cleared and the sun came out. It was thought that the child was  special, and that rainbow was a sign of his power. </p>
<p>  The boy was named Haupu. He displayed incredible strength  at an early age and eventually grew to be a brave and powerful warrior. He was known  throughout the islands &ndash; respected for his amazing strength, but feared for his  quick temper.</p>
<? include ("../theme/text.html") ?>
<p>  One night, Ka'ena, an Oahu  chief, organised a night-fishing expedition. He gathered many people from the  village and they set out in canoes of all sizes, with torches and their largest  fishing nets. </p>
<p>  This same night, Haupa was sleeping in his royal home on Kauai, some 30 miles (48 km) from Oahu.  He awoke to strange noises out on the water, and when he looked out he saw  faraway lights dancing in the black distance. Half asleep, Huapu imagined a  group of warriors coming from Oahu to attack  his people, and so he rushed to the edge of a nearby cliff, heaved up a huge  boulder and flung it out across the channel between the two islands.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p>  The canoes were smashed and shattered into tiny pieces, and  chief Ka'ena, who stood proudly in the middle of his people, lost his life along  with many of the fishermen. The boulder hit the water with such force that the  resulting waves washed huge amounts of sand onto the shore, forming a point of  land.</p>
<p>  The survivors of the disastrous fishing expedition made  their way back to the shore of Oahu, and thereafter named the cape &quot;Ka'ena&quot;  after their fallen chief. The boulder, they named the Rock of Kauai.</p>

