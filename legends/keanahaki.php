            <h1>The Legend of Keanahaki and the Fish</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Many years ago in Namakalele (meaning &quot;the flying eyes&quot;) was a  married couple - Keawe and his wife Keanahaki. They lived happily on a small  plot of land in Moanalua Valley on Oahu. </p>
<p>  Keawe was a very busy man. Every day he went to the mountains  to cut wood, gather plants and do other chores. When this work was completed,  he hurried home to deposit the wood and food and then went straight out to sea  to fish. His days were always filled in this way and he had little time for his  family, which was steadily increasing. </p>
<p>  When their sixth child was born, Keanahaki decided that her  husband should not be away for so long every day and she said to him: &quot;You go  to the mountains, and I will fish.&quot; Keawe agreed and then went to the mountains  as usual, and Keanahaki went down to the sea. What Keawe did not know, however,  was that his wife was not like other women and had strange and unusual powers.</p>
<p>  When Keanahaki got to the shore, she began to chant,  telling her right eye to fly to the sea and bring certain fish, then to her  left eye to fly in another direction out on the water and catch other fish. She  stood on the beach for some time, and then she called her eyes to return. Her right  eye fluttered back, bringing many fish. Her left eye did the same. At first,  Keanahaki divided the fish into portions for her husband, children and herself.  But her greed overtook her, and after she had eaten her share, she continued  eating until only one fish remained. </p>
<p>  The powerful woman took the lone fish back home and when  her husband saw this small catch, he was very disappointed. He asked Keanahaki  if this was all she was able to catch, and she said yes, so he presumed that  this was because it was her first day of fishing and that things would improve  with practice. </p>
<p>  But day after day, Keanahaki went down to the beach and  returned with only one fish, and Keawe began to get suspicious. He was sure  that a person could not continually have such bad luck. Then one day, he  learned from a friend that his wife was not a normal person. He was told that  she had unusual powers and that she could send her eyes out to sea to fish and  that she always came back with plentiful supplies.</p>
<? include ("../theme/text.html") ?>
<p>  Keawe was angry and disappointed. He asked how he could  catch his wife and was told to gather leaves from the ipu 'awa'awa (a type of  gourd, with bitter pulp, grown on a vine). If he followed his wife to the  shore, he could catch her eyes and preserve them in the leaves. </p>
<p>  So one day, Keawe pretended to go to the mountains as  usual, then waited and watched for his wife to go fishing. When she did, he  followed her. As soon as Keanahaki got to the shore, she again chanted for her  right eye to fly over the sea and fish for certain fish and for her left eye to  fly in another direction and bring in other fish. Watching her, Keawe was  astounded. He walked silently behind his wife, and she did not see him because  without her eyes, she was blind. </p>
<p>  When enough time had passed, she called to her eyes to  return, but Keawe caught them as they flew back with the fish and carefully wrapped  each eye in the leaves of the ipu 'awa'awa. Then he gathered the fish and went  home, while Keanahaki stood on the shore calling for her eyes and wondering why  they did not return.</p>
<p>  When Keawe reached his grass hut, his hungry children  gathered and were proud of the large catch their father had brought in. He left  them admiring the fish and went to hide the eyes of his wife. Five of the  children were too preoccupied by the fish to notice, but the sixth saw him take  the small bundle and place it on a high ledge out of reach.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p>  In the meantime, Keanahaki had been waiting a long time on  the beach for her eyes to return and soon she became suspicious of what had  occurred. Stumbling and groping, she slowly found her way back up the path to  her home. </p>
<p>  She asked her children if their father had returned. They  said that he had come back with a big bag of fish. She asked if he had come  with anything else - something smaller. Five of them said that they had seen  nothing more, but the smallest told his mother that he had seen his father with  a small bundle wrapped in leaves and took her to the place where it was hidden.  After groping about Keanahaki found and restored her eyes to their sockets. </p>
<p>  After that day, Keanahaki still went to the beach to fish,  but she always brought home the plentiful supply her eyes had caught, for fear  that she would be left blind on the seashore again. </p>

