            <h1>The Legend of Greedy Chief Hala'ea</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Once upon a time, on the south point of the Big Island of  Hawaii, there was a greedy chief named Hala'ea. Every day, the local fishermen  would set off in the early morning off the coast of Ka'u.  They worked hard every day, casting the nets and luring the fish into them,  then hauling the fruits of their labor back to the island. </p>
<p>  But every evening when they returned to shore, the chief  paddled out in his canoe and demanded that the fishermen give all of their fish  to him, crying &quot;The fish is mine! Give me the fish!&quot;</p>
<p>  Then he would hold a feast, carousing and often wantonly  wasting the food with no regrets. As for the hardworking fishermen, they were  obliged to return to sea every day to catch the fish without ever having any to  take home to their families. </p>
<p>  Eventually, the common people decided that they could not  let the chief continue to help himself to their fish. They came up with a  plan to rid themselves of his oppression. It was the season of the 'ahi (the  yellowfin tuna), and one morning the fishermen gathered together early,  preparing their canoes, looking after the nets, the bait and whatever else was  required for the expedition. </p>
<? include ("../theme/text-desktop-only.html") ?>
<p>  Then they paddled out to sea, much earlier than usual, and  spent the entire day fishing. They returned that evening with a particularly  large catch, and as usual, the greedy chief paddled out to meet them and  shouted: &quot;The fish is mine! Give me the fish!&quot;</p>
<p>  The fishermen complied with his wishes. The separated so  that their canoes were on both sides of the chief, and they began to deposit  their catch into his canoe. The chief was overjoyed as they continued to dump  their loads into his boat. He was so overcome by the beautiful, glistening fish  piling up that he did not notice his canoe was being swamped and beginning to  sink.</p>
<p>  When the fishermen had unloaded all of their fish into the chief's  canoe, they quickly paddled away without looking back. By the time Hala'ea  realized that his canoe was sinking under the weight of all the fish, it was  too late. He looked about for help, but all the canoes had gone back to land. </p>
<p>  Some say that the chief was lost beneath the waves as he  struggled in his sinking boat; others claim he was swept away on the swift current  that carries his name, Hala'ea. Nonetheless, the greedy chief perished,  surrounded by the objects of his greed, and his people rejoiced and lived long  and happy lives.</p>

