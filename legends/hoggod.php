            <h1>The Legend of the Hog God</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Kama Pua'a was born at Kaluanui, part of the district of Hau'ula on the island   of Oahu. He was known as  a very powerful chief, but also as a destructive monster, a hog god. Sometimes he had a hog's body with a human head and limbs; sometimes  a hog's head rested on a human form, and other times when his brutal desires  overcame him, he assumed the shape of a hog completely.</p>
<p>Kama Pua'a was  endowed with superhuman powers, according to the legends, and could shape-shift  and command the rain and waters to obey him. He traveled  between the islands in a magical canoe, which became a small shell that he  could tuck into his loincloth when he was on land. One day he voyaged to the southeastern point of Hawaii to Cape Kumukahi. </p>
<p>He walked over lava, through forests and over hills and finally came  to Akani Kolea. From here, he could look down into Kalua Pele (the pit of Pele)  where the goddess of fire lived with her people. One of her sisters looked up  on this night and saw Kama Pua'a, playing a small hand drum and dancing gracefully on the hilltop.  She thought that he looked like a fine man who could be the husband of one of  them and begged Pele to call him down into the crater. </p>
<p>But Pele scorned him, for she knew of his brutal, hidden side. She  sent clouds of sulphur smoke and a stream of boiling lava up toward him, but to  her surprise he brushed the clouds away and stood before them unharmed.</p>
<p>Pele was impressed and sent for Kama Pua'a. After some time, the two  reconciled, married, and for a while they lived together as husband and wife.  But the marriage did not endure. Kama Pua'a had too many of the habits and  instincts of a hog to please Pele, and she was too quickly angered to suit the  overbearing Kama Pua'a and would often burst into a fiery rage.</p>
<? include ("../theme/text.html") ?>
<p>First, they disagreed only with taunts and bitter words, but soon,  Pele was determined to destroy her tormentor. She stamped on the ground, causing  cracks to open in the surface and clouds of smoke and steam to rise forth, but Kama  Pua'a matched her powers, and when Pele sent mighty streams of flowing lava, Kama  Pua'a called for the waters of the ocean to rise up. It was the goddess of fire  of Hawaii  against the hog god of Oahu. </p>
<p>Pele was driven inland with her former husband in pursuit until at  last she was forced back to her spirit home in the pit of Kilauea.  But Kama Pua'a was not satisfied. He gathered the waters together in great  masses and hurled them into the fire pit. Pele responded with violent  explosions, which tore open the sides of the crater and created great masses of  fire. The lava rose in lakes and fountains, while the hog god continued to  throw water into the pit, quenching the flames. </p>
<p>Just when it seemed that the volume of water would drown Pele, she  called on the gods of the underworld for help, and fierce eruptions shook the  pit once more. This attack was more than Kama Pua'a could endure. As streams of  lava poured out against him, he changed his body into a kind of grass now known  as Kukae'pua'a, which diverted the path of the lava.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p>But Pele, inspired by the beginning of victory, called anew upon the  gods of the underworld for strong reinforcements. With more lava piling up, the  grass of the hog god began to burn and he rushed down to the sea. Pele followed  close behind, throwing great masses of lava at Kama Pua'a, until he finally gave  up the battle, and, thoroughly defeated, changed himself into a fish.</p>
<p>This fish is called Humuhumunukunukuapua'a and has a thick skin to withstand  the boiling waves through which it had to swim out into the deep sea. The  Hawaiians say that this fish has always been able to make a noise like the  grunting of a small hog. </p>
