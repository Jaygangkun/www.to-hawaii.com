            <h1>The Legend of Nanaue the Shark Man</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Long ago on the Big Island of Hawaii, there lived a gorgeous young woman by the name of Kalei. Kalei lived in Waipio Valley. Each night Kalei would walk to the waters at the mouth of the valley to bathe in the sea. </p>
<p>One night, Kamohoalii, the king of all sharks in the region was swimming just below the surface of the waters where Kalei bathed. Under the shimmering moonlight, she disrobed and slipped into the warm waters as she did every night.</p>
<p>Kamohoalii saw Kalei and was entranced by her unique beauty. As shape-shifter he resolved that the next night he would take on his human form as a chief and search the land for the beautiful and mysterious woman.</p>
<p>He did as he planned. For the next few days he walked the land as a chief and ate and communed with the people of Waipio Valley in search of Kalei. In time, he found her. They fell in love and married.</p>
<p>Over time, Kalei became pregnant with Kamohoalii's child. Before the child was to be born, the shark king knew that his time had come to return to the sea. He never revealed his true identity to Kalei. Instead, he instructed Kalei to give birth alone and to watch over his child, who was to be a son, and to never allow the baby to eat the flesh of any animal. Reluctantly he left Kalei, whom he had come to love dearly and returned to the ocean. The lovers never saw each other again.</p>
<p>On a dark night, as the island winds whipped furiously through the valley, beautiful Kalei gave birth to the shark king's son. She was afraid and saddened when she saw that the baby was born with a deformity on his back - a large open hole that resembled a gaping fish mouth.</p>
<p>She wrapped the baby in a thin blanket of tapa cloth to hide the deformity from the others. That night she cried as she rocked her newborn son, for he was as beautiful as she, besides his disturbing deformity. She named him Nanaue.</p>
<p>As the child grew older, she kept him away from meat as the shark king had instructed her, but she could not protect him for long. Back in ancient times, men and women were not allowed to eat together.</p>
<? include ("../theme/text.html") ?>
<p>When the child came of age, his grandfather took him to eat with the men. He was fed meat for the first time and developed a voracious and insatiable appetite. The gaping mouth on his back grew rows of sharp teeth.</p>
<p>After that day when Kalei took her son to bathe in the stream she watched in horror and fascination as he took the form of a young shark - he would swim and play in the water in fish form, chasing and eating the smaller creatures in the stream.</p>
<p>As the child grew into a man, Nanaue also grew as a shark. After he would swim in the ocean, people would go missing. A great shark would come out of the water and tear his fellow swimmers limb to limb.</p>
<p>The people of Waipio Valley became suspicious - why was Nanaue never harmed in the ocean? They began to suspect that he had special powers, but they did not know that it was he who was actually killing his friends while in shark form.</p>
<p>One day, the villagers discovered the great mouth on Nanaue's back. It all came together that he was the one that had been causing all of the deaths in their otherwise peaceful waters. The villagers were very angry and decided to capture and kill him. Nanaue then changed into shark form and escaped into the sea.</p>
<p>From Waipio Valley, Nanaue swam to the Hana side of the island of Maui. There he took the form of a man. He married a chiefess while on Maui and tried to hold back his desire for human flesh, but did not succeed. One night his hunger took a hold of him and he kidnapped a young girl, ran to the ocean, shape-shifted and devoured her in plain sight of all. The people of Maui were outraged. They tried to spear Nanaue from canoes, but he swam quickly toward Molokai.</p>
<p>Once again, after he had reached the island of Molokai, Nanaue tried to keep his true nature a secret, but as he grew older his hunger only got stronger. By this time, tales of a dangerous shark man were circulating through the islands.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p>The Molokai people kept a look out for this strange man shark. Finally, they saw Nanaue shape-shifting in the sea when he thought that no one was watching. They snared him while in shark form and beat him with clubs until the ocean was red with his blood. The people chanted continuously until Nanaue became weak.</p>
<p>His shark body was drug to shore where he was chopped to pieces and incinerated in a large oven. Thus died Nanaue, son of Kamohoalii - King of Sharks. </p>

