            <h1>The Legend of the Ghost Sisters of Hilo Hills</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>This story begins with the goddess Hina, mother  of Maui, who was thought to have married the  moon. Hina had two daughters, Hina Keahi, the mistress of fire and Hina Kuluua,  the mistress of rain. As a gift, Hina gave two mountains to her daughters;  Halai for Hina Keahi and Puuhonu for Hina Kuluua. These hills were especially  rich and fertile, and the sisters and their people settled on these lands and  prospered for a long time.</p>
<p>  However, many years went by and soon the rains began to  fall less and less often until the ground became dry and shrivelled. The taro  planted on the hillsides died. The bananas, sugarcane and sweet potatoes  withered and the fruit on the trees died. Eventually, hunger beset the villages,  then famine, and before long the shadow of death was over the land and the  people feared the worst. </p>
<p>  Hina Keahi knew that something had to be done to save her  followers. She instructed the men to cross the river bed where no water flowed,  enter the dry forests of Koa and Ohia and gather firewood. The priests went on  the expedition as well, uttering incantations against the possibility of  failure. They offered sacrifices and prayers for the safe return of the men.  The weakened laborers gathered and carried back what they could and took them  back to Halai hill. </p>
<p>  Next, the famished men toiled, digging out  the hill under Hina Keahi's command and making a great imu, or cooking oven,  preparing it with stones and wood. They lit the fire, and when the stones were  hot, Hina Keahi directed the people to arrange the pit in its proper order as  if cooking the food for a great feast. Then Hina told them to make a place in  the imu for a human sacrifice.</p>
<p>  In quiet despair the workmen obeyed Hina Keahi and prepared  the place for a sacrifice with dread, wondering who would be chosen to appease  the gods in order to save the rest. But Hina Keahi was &quot;Hina the kind.&quot;  She surveyed their work and said it was good, and she was full of pity and love  for her people. </p>
<? include ("../theme/text.html") ?>
<p>  She said: &quot;This imu is my imu. I shall lie down on its  bed of burning stones. I shall sleep under its cover. But deeply cover me or I  may perish. Quickly throw the dirt over my body. Fear not the fire. Watch for  three days. A woman will stand by the imu. Do as she instructs you.&quot;</p>
<p>  Hina Keahi stepped into the great pit and lay down on the  burning stones. The men followed her instructions, placing the imu mats over  their chiefess and throwing the dirt back into the oven until it was all  thoroughly covered.</p>
<p>  Then they waited and watched over the oven, wondering what  had become of their beautiful leader. But Hina Keahi was the mistress of fire,  and so she could not be injured by the heat of the burning stones. She sank  down through the imu into the underground paths, which belonged to the spirit  world. </p>
<p>  One day later, a gushing stream of water flowed from the  land. On the second day, a pool full of water rose to the surface of the earth.  And on the third day, a great spring of pure water burst forth from the sea  shore in the very path of the ocean waves. Soon a woman appeared by the imu,  who commanded the laborers to dig away the dirt and open up the oven. When this  was done, the hungry people, to their amazement, found a great abundance of  food, so much that it would last until their plants ripened again and the days  of the famine were over. The people rejoiced because they knew that their  chiefess had escaped death and exalted Hina Keahi in stories and song about the  great mistress of fire.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p>  Meanwhile, the second sister, Hina Kuluua, who was always  very jealous of her beautiful sister Hina Keahi, heard of the miracles her  sister had performed and the singing and praise she had received from her people.</p>
<p>  Hina Kuluua's followers in Puuhonu were also  suffering from famine at this time, so she decided to provide for them in the  same way as her sister had done. She ordered a great imu to be dug, with space  for food and a human sacrifice. Yet in her jealousy, Hina Kuluua forgot that she  was a goddess of rain and that rain and fire could not work together. She entered  the pit, and her people quickly covered her with mats and earth as she had  commanded. They waited for the miraculous events to occur, but the hot stones  had destroyed Hina Kuluua, and she rose as a rain cloud above the imu. </p>
<p>  Her people waited three days, then four, then  five. But no one appeared and they were still starving. On the fifth day, the  villagers opened the imu and found nothing but the ashes of Hina Kuluua. Her  people perished.</p>
<p>  The ghosts of the sisters still appear near  the old hills from time to time. Hina Keahi, as flowing lava, and Hina Kuluua,  as clouds of rain.</p>
