            <h1>The Legend of Kamalo and the Shark God</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Long ago on the island of Molokai lived Kupa, the high chief, and Kamalo, one of his priests. Kamalo had two sons, who had great courage and accomplished great feats of daring. Kupa had a house constructed in the Mapulehu valley, which ended in the harbor and is now called Aikanaka. Kamalo's temple was located nearby, in the village of Kaluaaha, which faces the channel between Molokai and Maui. </p>
<p>Inside chief Kupa's temple, two very special drums were kept. Kupa used the beating of these drums to communicate his thoughts to his priests over great distances. One day, when Kupa was away on a fishing trip, Kamalo's sons decided to see the chief's great drums for themselves They entered the chief's home and marvelled at the magnificent drums, and although their father had taught them the ways of the temple and that things sacred to the gods must not be touched, temptation overcame them, and the boys began to beat hard on the drums. </p>
<p>When the chief returned and heard about what had happened, he became furious, killing the boys and presenting their bodies at the alter in sacrifice. When Kamalo learned of the death of his sons, a heavy bitterness filled his heart and he became obsessed with revenge. No match for Kupa on his own, he consulted with prophets and seers throughout Molokai, but each one was fearful of Kupa and would not help Kamalo. </p>
<p>Eventually, Kamalo came to the cliffs that overlook Kalawao and Kalaupapa. Down at the bottom there was a temple (heiau) devoted to the shark god, Kauhuhu. Kamalo climbed down the rock face and spoke to the priest of the temple, who told him to go to the cave of the shark god below. Exhausted, Kamalo made his way down the rockface and told his story to the guards at the mouth of the cave, winning their sympathy.</p>
<? include ("../theme/text.html") ?>
<p>The guards explained that in order not to be eaten, he had to hide in a nearby rubbish pile and wait for Kauhuhu, the shark god to arrive on the eighth wave. When he saw Kauhuhu, he should be completely silent and wait for the right moment to speak with him. Soon, the waves crashed against the cliffs, climbing higher and higher until the eighth one rose up and suddenly, the shark god was there in his human form.</p>
<p>Kamalo was silent and still in the rubbish pile, but Kauhuhu smelled a man and soon leapt upon him, jaws open to reveal sharp, shiny teeth. Kamalo quickly cried out for mercy and recounted his story in detail. He described how he was turned away by all the prophets of all the gods, and Kauhuhu, his final hope, was overcome with pity. </p>
<p>The shark god agreed to be his kahu, or guardian, and punish chief Kupa and his followers. He told Kamalo to go back to the temple of the shark god and carry the priest there over the cliffs to his own temple at Kaluaaha. There they had to construct a special fence and set up the sacred white tapa kapu staffs. Then, after they had gathered four hundred red fish, four hundred black pigs and four hundred white chickens, they were to wait for the arrival of the shark god, who would come and avenge Kamalo's sons. </p>
<? include ("../theme/text-desktop-only.html") ?>
<p>Kamalo did as he was told and waited. Weeks grew into months and still he waited with patience. Then one day, a white cloud appeared, different than any he had seen before. It grew rapidly and then began to move. It came across the channel and rested in the highest peaks above the valley where Kupa lived. A magnificent rainbow appeared spanning the entire valley, but soon the winds picked up and black clouds appeared in the sky, together with lightening and heavy rain. </p>
<p>It was the most devastating storm the land had ever seen. The rain flooded down the mountain, destroying everything in its path. Kupa's temple - where the boys had beat the drum - and his house were torn apart, and he was swept into the ocean along with his followers. There, the shark god's people waited in the bay, and they fed on Kupa and the villagers until the harbor turned red. Thereafter the harbor was called Aikanaka, or man-eater. Kamalo's sacred fence protected his land, his home and his people.</p>

