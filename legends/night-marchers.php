            <h1>The Legend of the Night Marchers</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>The retelling of spooky ghost tales has been a favorite form of entertainment and an important cultural link in Hawaii since ancient times. Ghostly images or haunting acts have been reported in old buildings, deep valleys, sacred burial sites, ancient temple sites (called heiau), forested areas, beaches and lava fields. With a rich history of mythology and folklore and numerous sacred sites, Hawaii's link to the past is ever present. A good majority of Hawaii residents have either had a spooky encounter at some point during their lives, or know of someone with an eerie story to tell. One of the most popular subjects of ghost stories in the Islands is the Night Marchers, or Hukai'po. </p>
<p><strong>Who are they?</strong></p>
<p>Night Marchers are ghostly apparitions of a band of beings who move with purpose to the beat of primitive pounding drums. Some say they are armed spirit warriors en route to or from battle, toting archaic weaponry and clothed in decorated helmets and cloaks. Other accounts tell of high-ranking ali'i (ruler) spirits being guided to places of high importance or to welcome new warriors to join in battle. Perhaps these restless souls are looking to reclaim rightful territory, replay a battle gone awry, or avenge their own deaths. Some say the Night Marchers are searching methodically for an entrance into the next world. </p>
<p>Night Marchers are said to roam through very specific locations and are often recognized by their raised torches and repeated olis, or chants. Although there have been a few scattered reports of daytime marches, these apparitions appear to be most active at night and are said to march on certain nights designated by the moon. And although the Night Marchers allegedly float a few inches off the ground, some local accounts tell of seeing mysterious footprints in their path after they have passed.</p>
<? include ("../theme/text.html") ?>
<p>Areas like the Nu'uanu Pali Lookout, Ka'a'awa Valley and Kalihi Valley on Oahu are rumored sites of Night Marcher trails, and nighttime visitors are encouraged to be wary. Other alleged Night Marcher sites include:</p>
<ul>
	<li>Oahu's Pali Highway runs along the famous Kamehameha battle site, and nighttime visits, especially alone, are not recommended. <br />
&nbsp;</li>
	<li>The Kamehameha Schools campus in Kapalama on Oahu, over one hundred years old, is said to have been visited by Night Marchers on many occasions. <br />
&nbsp;</li>
	<li>At Kualoa Ranch on Oahu's windward coast, Night Marchers have been spotted around an area that is said to house the remains of hundreds of Hawaiian chiefs and are said to be responsible for a good many nighttime car accidents. <br />
&nbsp;</li>
	<li>At La Perouse Bay, an area in the Ahihi-Kinau Natural Area Preserve in South Maui, the restless Night Marchers are said to roam along the hardened lava landscape in search of mischief.<br />
&nbsp;</li>
	<li>The town of Kaunakakai on Molokai is rumored to be a Night Marcher hotspot as the remnants of Ili'ili'opae Heiau, a sacred temple site, are located nearby.<br />
&nbsp;</li>
	<li>La'ie on Oahu was a city of refuge in ancient Hawaii, where criminals and offenders of the culture's strict aikapu religious practices were held. Even today, spirits of soldiers are said to roam the outskirts of La'ie in search of possible escapees.</li>
</ul>
<p>What to do when happening upon a night march in progress? The ghostly procession must never be interrupted. Legend has it that resting your eyes upon the Night Marchers could signal a grim fate for the perpetrator, a friend or relative, so witnesses are urged to crouch low to the ground, &quot;play dead&quot; and avert the eyes. Any sound or movement could invite a Night Marcher's deadly glance. These Night Marchers are set diligently upon their destination and are not considered spirits that will deviate from their path to haunt humans nearby. </p>
 <? include ("../theme/text-2.html") ?>
<p>
<strong>Favorite nights of the Night Marchers: Po Kane and Po Akua</strong></p>
<p><strong class="subtitle">Po Kane</strong> - during nights of the Hawaiian god Kane, chiefs, chiefesses, priests and close attendants march. </p>
<p><strong class="subtitle">Po Akua</strong> - on the 14th night of the new moon, spirits of chiefs, warriors and aumakua (guardian spirits) march between sunset and sunrise.</p>
<p><i><strong>Some characteristics of the mysterious night marches include:</strong></i></p>
<ul>
	<li>Heavy wind</li>
	<li>Rows of spirits carrying torches</li>
	<li>Alternating male and female rows</li>
	<li>Accompanying lightening and thunder</li>
	<li>Accompanying heavy rain or high surf</li>
	<li>Chanting and drumbeating</li>
	<li>Unusually bright torches</li>
	<li>Game playing and revelry</li>
	<li>Accompanying mist or fog</li>
</ul>
<p><i><strong>Other alleged Night Marcher sites:
</strong></i></p>
<p>Moanalua on Oahu <br />
  Kahakuloa on Maui<br />
  Kekaa on Maui <br />
  Hanapepe on Kauai<br />
  Hokunui on Lanai <br />
  Waipio Valley on the Big Island<br />
  Hilo on the Big Island<br />
  Waipio on the Big Island</p>
<p>Ghost tours in Honolulu are quite popular and several eerie tours center around the Night Marchers and other ethereal entities. The tale of the Night Marchers even inspired a 2001 film titled <em>The Night Marchers</em>. If you're interested in finding out more about the feared Night Marchers of Hawaii, pick up one of the many local books on the subject of the underworld, or sign up for a tour through haunted hotspots. </p>
<p>
Many locals who have never seen or heard the restless warriors will tell you they very much believe in their existence. If you happen to be in the path of the Night Marchers and the faint sound of drumming sends a chill up your neck, remember to show the ancient warriors respect through solitude. </p>

