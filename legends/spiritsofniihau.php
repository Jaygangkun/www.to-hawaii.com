            <h1>The Legend of the Man-Eating Spirits of Ni'ihau</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Early Hawaiians were always dependent on nature  and the seasons for survival. They had to preserve their resources, and so they  observed strict hunting and fishing seasons when the men were not allowed to  kill animals or catch fish. But when the kapu (prohibition) was lifted,  competition was fierce, especially in the case of fishermen, who then had to  travel far to find abundant fish to feed their villages.</p>
<p>  This is the story of five such men; Ekahi, Elua, Ekolu, Eha  and Elima. These fishermen were from Kauai,  and on one trip they were forced to set out in their canoes to fish off the  distant shores of Niihau, an abundant fishing  spot.</p>
<p>  But in ancient times, it was said that man-eating spirits  roamed the islands, and remote, uninhabited locations were dangerous, as anyone  who ventured there was easy prey for these evil spirits. </p>
<p>  These five men were just ordinary fishermen, but they made  the difficult decision to travel to Niihau in  order to find food to feed their community. They fished all day and ended with  a good catch. When the sky darkened into evening, they went ashore to Niihau to clean, salt and store their fish. They made a  fire and ate some of the fish, and finally they lay down on the warm sand to  sleep, for the next day would bring more hard work.</p>
<p>  When the sun rose, there were only four men. Elima was  nowhere to be found. At first they panicked, imagining that the spirits had  eaten their companion and that they too were in for the same fate. But Ekahi,  the leader of the group calmed them. He said, &quot;Elima has probably gotten  up with the sun for some early fishing. He will be waiting for us here when we  return with our catch.&quot; </p>
<? include ("../theme/text.html") ?>
<p>  The four men worked all day and returned late in the  evening, but the beach was still empty and there was no sign of Elima. They  searched the area to no avail and then waited and waited until the night grew  dark and starry. Eventually they fell asleep, lying close together on the  beach.</p>
<p>  The next morning, Eha had disappeared too and  the men knew that it was indeed the man-eating sprits that had passed by in the  night. Ekolu insisted that they leave this evil place.  Ekahi said: &quot;Yes, it is an evil place. But as fishermen, we  lead dangerous lives. We depend on this catch to survive. If we leave now, our village  will go hungry. There are only two days left in the season. Let us stay, but we  shall sleep in a canoe offshore.&quot;</p>
<p>  So that night, the three squeezed into one canoe, taking  turns to watch the skies as the others slept. Ekahi's watch passed quietly. Then  he woke Elua. Elua's watch passed quietly as well, and then he woke Ekolu.</p>
<p>  The next morning, just before sunrise, the  men woke to the sound of beating wings and saw poor Ekolu struggling to get out  of the hands of a flying, man-eating creature. It was bat-like, with huge  staring eyes and a horrible black hole for a mouth. With one gulp their  companion disappeared, and the beast flew off into the morning mist. </p>
<p>  Elua cried out for his friend and cursed the  place where they seemed doomed to die. But Ekahi reasoned with him, explaining  that if they left now, no fishermen from their village would ever dare to come  back, and their families would not have fish. Without fish, they would starve.  He came up with a plan to destroy the evil creatures.</p>
<p>  The two remaining men did not go out on the water that day.  Instead, they toiled on the beach, building a log house. Inside, they placed  two wooden images of men that they had carved with wood from the forest. Each  image had a pair of eyes made of the gleaming white insides of mussel shells. </p>
<p>  When they were done, they waited for the evil spirits to  come. Evening wore on and the two men, exhausted, fought against sleep. The  hours dragged by and eventually they dozed. Suddenly, they woke to strange  sounds close by. The spirits were at the entrance to the house, reluctant to  enter. They were sure that the two men were standing, awake, inside the house.</p>
 <? include ("../theme/text-desktop-only.html") ?>
<p>  They waited and waited hoping that the two  men would go to sleep, but their impatience overcame them, and eventually they  went inside and attacked the two wooden images. The sprits thought that the men  were tough and stringy, and before they had time to realize that they had been  tricked, Elua ran up behind and threw a fire torch at them. Then he ran to  where Ekahi had the canoe ready on the water and they paddled away quickly.</p>
<p>  The man-eating spirits were thus destroyed,  and the two brave fishermen of Kauai went back  to their village with plentiful supplies. The fishing grounds of Niihau remain safe to this day.</p>
