                <p>The Hawaiian alphabet is as follows:<br />
                  A, E, I, O, U, H, K, L, M, N, P, W,&lsquo;</p>
                <p>There are four basic rules in the Hawaiian language:</p>
                <ul>
                  <li>All words end in a vowel.</li>
                  <li>Every consonant is followed by at least one vowel.</li>
                  <li>Every syllable ends in a vowel.</li>
                  <li>Two consonants never appear next to each other.</li>
                </ul>
                <p>The Hawaiian alphabet consists of 13 letters &ndash; five vowels and eight consonants, one of them being a glottal stop (called &#699;okina). The reason why the &#699;okina is considered a consonant is because an &#699;okina that is missing can change the meaning of a word, as the following example illustrates: </p>
                <p>kai = sea<br />
                  ka&#699;i = to lead </p>
                <p>An &#699;okina indicates a break in the sound when a word is spoken. It is written with a backward apostrophe (&#699;). When saying a word that has an &#699;okina the symbol indicates a slight pause, similar to when one says &ldquo;uh-oh&rdquo; to show concern.<br />
                </p>
                                <p>Another example is lanai vs. lana&#699;i. The word &ldquo;lanai&rdquo; without the &#699;okina is pronounced &ldquo;lah-nigh&rdquo; and means balcony. The word &ldquo;lana&#699;i&rdquo; is pronounced &ldquo;lah-nah-ee.&rdquo; Lana&#699;i is one of the Hawaiian islands. <br />
                  <br />
                  Another pronunciation example is &ldquo;Molokai&rdquo; (Molokai is one of the Hawaiian islands). If we write the island name without the &#699;okina, it is pronounced &ldquo;Moh-loh-kai&rdquo; (kai like sky). If we write it with the &#699;okina, it reads Moloka&lsquo;i and is pronounced &ldquo;Moh-loh-kah-ee.&rdquo; Both pronunciations are acceptable and equally common (because most people today are not native Hawaiian speakers), but you should be cautious because sometimes an &#699;okina can change the meaning of a word. <br />
                  <br />
                  The &#699;okina is only found between two vowels or at the beginning of a word. Native and traditional speakers use the &#699;okina whenever they speak or write Hawaiian. The &#699;okina is not always used in print and on the Internet. It is, however, used in major Hawaiian newspapers and many books on Hawaiiana.</p>
                <p>Another grammatical mark in the Hawaiian language is the kahak&#333;, a symbol that looks like a line (-) that is placed over a vowel. When you see this, it means that it is a long vowel &ndash; simply drag the vowel out a bit longer than you normally would. It is a subtle difference that can be difficult to get the hang of. And even though it's not considered a letter, the absence or presence of it can also change the meaning of a word.</p>
