                <h2>Olelo Hawaii</h2>
                <p>The Hawaiian language (&#699;&#332;lelo Hawai&#699;i) is one of the oldest living languages in the world. The Hawaiian language has had a tumultuous history and was at one point even expected to be extinct by the end of the 20th century. However, a Hawaiian language &ldquo;renaissance&rdquo; has occurred over the past 40 years, and today more people are interested in learning the language again. Hawaiian is being taught in language immersion schools, and one can even obtain a Master's degree in the Hawaiian language from the University of Hawaii. Nevertheless, only about 0.1% of the people who live in Hawaii speak its native tongue today.</p>
                            <h3>History of the Hawaiian Language</h3>
                <p>Before Captain Cook's arrival in Hawaii in 1778, Hawaiian was strictly an oral language. Cook and his men recorded the Hawaiian language for the first time in 1778. They noticed that the language was similar to Tahitian and Maori.<br />
                  <br />
                  When the first missionaries arrived in Hawaii in 1820, they converted the oral Hawaiian language to a written language so that they were able to convey the messages of the Bible to the Hawaiian people. <br />
                  <br />
                  By 1826, the missionaries had created a Hawaiian alphabet. They also taught Hawaiians to read and write the language and translated the Bible into Hawaiian.</p>
                <p><strong>The Hawaiian Language Banned</strong></p>
                <p>After the annexation of Hawaii as a territory of the United States in 1898, the language was officially banned from schools and the government. Use of the Hawaiian language was even banned at Kamehameha Schools &ndash; a private school system reserved only for children of Hawaiian descent.<br />
                  <br />
                  It is important to note that this ban of the Hawaiian language was not made against the language in general. People were still allowed to speak Hawaiian on a daily basis. In fact, there were fourteen separate newspapers that were printed in Hawaiian. Notables are the Ka Lama Hawaii and Ke Kumu Hawaii, which began as early as 1834 and the Ka Nupepa Kuoka, which had a 66-year-run from 1861 to 1927.</p>
