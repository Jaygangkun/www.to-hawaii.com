?> <p><strong>The Food</strong></p>
                <p>The food menu on dinner cruises varies from provider to provider and from package to package. Standard packages usually offer a buffet and the menu includes pasta and green salads, mixed vegetables, Hawaiian fish, such as mahi mahi, roasted or fried chicken, butter rolls, steamed rice, mashed potatoes, fruits and dessert.</p>
                <p>The dinner menu on premium packages might include a fine choice of appetizers, entr&eacute;es and desserts. Premium cocktails and often times even champagne are included in those packages as well. </p>
                <p>Drinks include beer, wine, <a href="recipes/maitai.php">Mai Tai cocktails</a>, pina coladas and other tropical cocktails. Besides the cocktails that are already included in the cruise package, some vessels feature an open bar from where you can purchase more drinks. Coffee and tea are served for free.</p>
                <p><strong>Special Occasions</strong></p>
                <p>Some of the Hawaii dinner cruise providers offer special cruises on holidays, such as Valentine's Day, July 4th and New Year's Eve. </p>
                <p>Weddings and other occasions, such as birthday parties and anniversaries can sometimes also be arranged. For more information on how to organize a special occasion cruise, contact your Hawaii travel agency.</p>
