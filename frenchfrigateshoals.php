              <p>In addition to the marine life, the French Frigate Shoals are home to 18 species of seabirds, including the Laysan Albatross, Black-footed Albatross, Bonin Petrel, Bulwer&#39;s Petrel, Tristram&#39;s Storm-petrel, Wedge-tailed Shearwater, Christmas Shearwater, Red-tailed Tropicbird, Masked Booby, Red-footed Booby, Brown Booby, Great Frigatebird, Gray-backed Tern, Sooty Tern, Brown Noddy, Blue-gray Noddy, Black Noddy and White Tern. Several species of shorebirds also winter here.</p>
              <p>In a three-week research mission conducted by the National Oceanic &amp; Atmospheric Administration (NOAA), 100 new species never seen in the area before were discovered, including many that are completely new to science.</p>
