              <p>Encompassing 0.096 square miles (0.249 km&sup2;) of land, the French Frigate Shoals (Mokup&#257;papa) are comprised of about a dozen small islands, a 20-mile (32 km) long crescent-shaped reef, 12 sandbars and the 120 feet (36 m) high La Perouse Pinnacle, the only remains of the shoals' volcanic origins. They are about 
                560 miles (902 km) northwest of Honolulu.</p>
              <p>The French Frigate Shoals were named after French explorer Jean-Fran&ccedil;ois de La P&eacute;rouse, who almost lost two frigates when trying to navigate the shoals. There is no evidence of extensive human activity or presence on the shoals, but it is believed that the earliest human visitor to the French Frigate Shoals came from the larger Hawaiian islands in the south, which were settled by Polynesians between 1100 and 1300 AD. </p>
              <? include ("theme/text.html") ?>
              <p>In the late 1800s, American and European companies became interested in the possibility of mining guano in the Hawaiian Islands. In 1894, the Republic of Hawaii leased the French Frigate Shoals and also some of the neighbor islands (Kure Atoll, Midway Atoll and Pearl and Hermes Reef) for 25 years to the North Pacific Phosphate and Fertilizer Company. However, it was found that the guano and phosphate deposits 
                at the French Frigate Shoals were impractical to mine.</p>
              <p>On July 7, 1898, Hawaii became a U.S. Territory and the French Frigate Shoals were included among the islands acquired by the U.S. During World War II (in the early months of the U.S.-Japanese conflict), Japanese seaplanes sometimes used the French Frigate Shoals to refuel by submarine. But by mid-1942, U.S. naval activity increased in the area preventing further Japanese use. After the Battle of Midway, which took place from June 4, 1942, to June 7, 1942, the U.S. Navy built a Naval Air Station on Tern Island, which has a land area of 26.014 acres (10.527 ha). </p> <?php
