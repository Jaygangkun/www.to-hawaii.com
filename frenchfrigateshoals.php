?> <p>They enlarged Tern Island enough to support a 3,300-foot-long (1,005 m) landing strip. The main purpose of this runway was for emergency landings for planes flying between Hawaii and the Midway Atoll. Up until today, the original seawall, runway and some of the buildings remain. The island is now maintained as a field station in the 
                Hawaiian Islands National Wildlife Refuge by the United States Fish and Wildlife Service. In 2000, the French Frigate Shoals became part of the Northwestern Hawaiian Islands Coral Reef Ecosystem Reserve, which in 2006 was incorporated into the Northwestern Hawaiian Islands National Monument.</p>
              <p>There's a submerged island in the French Frigate Shoals - Whale-Skate Island &ndash; which suffered a lot of erosion beginning in the 1960s. By the late 1990s, it was completely washed over.</p>
              <p>The French Frigate Shoals are home to 41 species of stony corals, including some that are not even found in the Main Hawaiian Islands. Furthermore, the reef supports a large variety of fish. The masked angelfish (Genicanthus personatus), for example, is endemic to the Hawaiian Islands and is very common here. Also, more then 600 species of marine invertebrates, many of which are endemic (are not found anywhere 
                else in the world), live here, plus more than 150 species of algae. Many of the endangered Hawaiian green sea turtles come here to nest, and the shoals also provide refuge to the largest surviving population of Hawaiian monk seals. </p>
