            <h1>Lanai Sacred Places</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Lanai is the smallest of the six main Hawaiian islands, but even here, you can find historic sites that tell of Hawaii's interesting past. Below are three sites that are worth a visit.</p>
<p><strong>Halulu Heiau</strong></p>
<p><img src="../images/sacred/halulu_heiau.jpg" width="148" height="125" alt="Halulu Heiau" align="right"/>This heiau is one of the most impressive archaeological sites on Lanai. This area on Lanai's south shore was one of King Kamehameha's favorite fishing places. A few years after he built <a href="../lanai/attractions/halulu-heiau.php">Halulu Heiau</a>, missionaries arrived in Hawaii and things took a different wind of change. So this heiau is one of the last ones built in Hawaii before the missionaries' time. It was in use up to about 1819.</p>
<p><strong>Keahiakawelo (Garden of the Gods)</strong></p>
<p><img src="../images/sacred/keahiakawelo.jpg" width="148" height="125" alt="Keahiakawelo"  align="left" />This rock garden is a vast expanse featuring large boulders in a spectrum of red, orange, purple and earthen colors. It is popularly known as <a href="../lanai/attractions/gardenofgods.php">Garden of the Gods</a> because of this otherworldly landscape. This is the ancient site of the fire of Kawelo, who was a kahuna (priest) of Lanai. During the time he lived here he kept a constant fire burning on top of his rock altar.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p><strong>Pu'u Pehe (Sweetheart Rock)</strong></p>
<p><img src="../images/sacred/puu_pehe.jpg" width="148" height="125" alt="Puu Pehe"  align="right" />Pu'u Pehe is a beautiful spot on Lanai's south shore, with red rocks and cliffs that contrast sharply against the deep blue ocean. It is the site of an ancient Hawaiian legend that tells of two lovers and the fate they met here, hence the popular name <a href="../lanai/attractions/puupehe.php">Sweetheart Rock</a>. </p>
