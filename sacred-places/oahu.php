<p><strong>Waimea Valley</strong></p>
<p><strong><img src="../images/sacred/waimea_falls_park.jpg" width="148" height="125" alt="Waimea Valley" align="left" /></strong>Located within this lush valley are 78 reconstructed archaeological sites that show the life of earlier inhabitants of the area. These include religious sites and shrines and other structures. <a href="../oahu/attractions/waimeafallspark.php">Waimea Valley</a> is a popular visitor attraction on Oahu's North Shore. There is an entrance fee to visit the valley.</p>
