            <h1>Oahu Sacred Places</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Known as &quot;The Gathering Place,&quot; Oahu is the second oldest island of the six main Hawaiian islands. Hidden on this island is a host of sacred places worth visiting.</p>
<p><strong>Kane'aki Heiau</strong></p>
<p><strong><img src="../images/sacred/kaneaki_heiau.jpg" width="148" height="125" align="right" alt="Kaneaki Heiau" /></strong>Built in the 16th century, <a href="../oahu/ancientsites/kaneakiheiau.php">Kane'aki Heiau</a> is considered the best preserved heiau on Oahu. It is located on the island's leeward (west) side in Makaha Valley and features an altar, god images, two prayer towers, a drum house and a taboo house.</p>
<p><strong>Kaneana Cave</strong></p>
<p> <strong><img src="../images/sacred/kaneana_cave.jpg" width="148" height="125" alt="Kaneana Cave" align="left" /></strong>This remote cave on Oahu's leeward coast is shrouded in mystery. Estimated to be around 150,000 years old, there are many ancient legends that tell about the cave's history. According to one legend,  it was here where mankind was born, from the womb of the earth goddess. That's why the cave is called <a href="../oahu/ancientsites/kaneanacave.php">Kaneana</a> (cave of Kane), named after Kane, the god of creation.</p>
<p><strong>Kapaemahu Stones</strong></p>
<p><strong><img src="../images/sacred/kapaemahu_stones.jpg" width="148" height="125" alt="Kapaemahu Stones" align="right" /></strong>Near Kuhio Beach in Waikiki, four large stones known as Kapaemahu, Kahaloa, Kapuni and Kinohi can be found. Each one is said to contain the mana (power) of four wizards who came to Oahu from Tahiti a long time ago, possible around 400 A.D. Legend has it that these wizards stored their powers in those rocks, which are also popularly known as <a href="../oahu/ancientsites/wizardstones.php">Wizard Stones</a>.</p>
<? include ("../theme/text.html") ?>
<p><strong>Ku'ilioloa Heiau</strong></p>
<p><strong><img src="../images/sacred/kuilioloa_heiau.jpg" width="148" height="125" alt="Ku'ilioloa Heiau" align="left" /></strong>This heiau has three terraced platforms and is believed to have been a temple of learning and training, mainly in the arts of fishing, navigation and other ocean-related skills. <a href="../oahu/ancientsites/kuilioloaheiau.php">Ku'ilioloa Heiau</a> is located on a peninsula on Oahu's leeward coast.</p>
<p><strong>Kukaniloko Birthing Stones</strong></p>
<p><strong><img src="../images/sacred/kukaniloko_birthing_stones_2.jpg" width="148" height="125" alt="Kukaniloko Birthing Stones" align="right" /></strong>Located next to  Kamehameha Highway in Wahiawa is the <a href="../oahu/ancientsites/kukanilokobirthingstones.php">Kukaniloko Birthing Stones</a> State Monument. It is, quite literally, the birthplace of Hawaiian royalty. On these  irregularly shaped stones and hidden behind trees, royal women gave birth. </p>
<p><strong>Moli'i and 'Apua Fishponds</strong></p>
<p><strong><img src="../images/sacred/molii_fishpond.jpg" width="148" height="125" alt="Moli'i and 'Apua Fishponds"  align="left"/></strong>Moli'i Fishpond is one of the largest fishponds in all of Hawaii, encompassing 125 acres. The smaller 'Apua Fishpond is located right next to it. <a href="../oahu/ancientsites/moliifishpond.php">Moli'i Fishpond</a> is one of the few early royal fishponds in Hawaii that remain operational up until today. Threadfish, mullet and milkfish are raised in it.</p>
<p><strong>Pu'u o Mahuka Heiau</strong></p>
<p><strong><img src="../images/sacred/puu_o_mahuka_heiau.jpg" width="148" height="125" alt="Pu'u o Mahuka Heiau" align="right" /></strong>This is Oahu's largest heiau, covering almost two acres. It may have been constructed as early as the 1600s. The <a href="../oahu/ancientsites/puuomahukaheiau.php">Pu'u o Mahuka Heiau</a> was considered a sacred and powerful place for the kahuna (priests) and one of two places where the wives of the ancient chiefs gave birth. But the heiau was also a site of human sacrifice. </p>
<p><strong>Royal Mausoleum</strong></p>
<p><strong><img src="../images/sacred/royal_mausoleum.jpg" width="148" height="125" alt="Royal Mausoleum"  align="left"/></strong>Just 20 minutes away from Waikiki is the <a href="../oahu/attractions/royalmausoleum.php">Royal Mausoleum</a>, the final resting place of King Kamehameha II through King Kamehameha V, King Kalakaua and Queen Liliuokalani. Visitors are greeted by the sight of a magnificent kamani tree and a chapel built in Gothic-style architecture.<br />
</p>
<p><strong>Ulupo Heiau</strong></p>
<p><strong><img src="../images/sacred/ulupo_heiau.jpg" width="148" height="125" alt="Ulupo Heiau"  align="right"/></strong>This heiau, located in Kailua, is quite massive and required a large workforce, which is a hint that it must have been culturally important. Some Hawaiian heiaus were used as a place for sacrifice, others to celebrate a good harvest or the birth of ali'i (royals). It is assumed that the function of <a href="../oahu/ancientsites/ulupoheiau.php">Ulupo Heiau</a> changed over time. It might have been an agricultural heiau in the beginning and later might have become a heiau dedicated to success in war.</p>
