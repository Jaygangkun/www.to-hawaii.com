            <h1>Molokai Sacred Places</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Also known as the &quot;Friendly Isle&quot; or the &quot;Most Hawaiian Isle,&quot; Molokai has its share of interesting ancient sites. Below are three that are worth a visit.</p>
<p><strong>Halawa Valley</strong></p>
<p><img src="../images/sacred/halawa_valley.jpg" width="148" height="125" alt="Halawa Valley" align="right" />This valley on Molokai's east shore has a rich history. It was first settled by people from the Marquesas Islands in the 7th century. For many years, this area was extensively cultivated with taro due to its fertile soil. Also, many heiau (temple sites) were built here. Young men who dreamed of becoming kahunas (priests) came from the neighbor islands and went to <a href="../molokai/attractions/halawa-valley.php">Halawa Valley</a> to learn and practice sacred spells.</p>
<p><strong>Kaule o Nanahoa</strong></p>
<p><img src="../images/sacred/kaule_o_nanahoa.jpg" width="148" height="125" alt="Kaule o Nanahoa" align="left" />This is a big rock with a very interesting shape. The shape is natural with a little bit of human enhancement. Go and see it and you will understand why it has been a site of love and fertility for hundreds of years. <a href="../molokai/ancientsites/phallicrock.php">Kaule o Nanahoa</a> is easily accessible and located within Pala'au State Park.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p><strong>South Shore Fishponds</strong></p>
<p><img src="../images/sacred/molokai_fishponds.jpg" width="148" height="125" alt="Molokai Fishponds" align="right" />There are well over 20 ancient fishponds on Molokai's south shore between Kaunakakai and Murphy's Beach. However, many of them are located on or bordered by private property and are not accessible by the public. Two that can be seen are <a href="../molokai/ancientsites/niaupalafishpond.php">Ni'aupala Fishpond</a> and <a href="../molokai/ancientsites/kalokoelifishpond.php">Kaloko'eli Fishpond</a>.</p>
