            <h1>Kauai Sacred Places</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>Known as the &quot;Garden Isle,&quot; Kauai has more to offer than its endless expanse of white-sand beaches, sparkling blue waters and lush rainforests. It is also home to some of the most interesting sacred sites in the Hawaiian Islands.</p>
<p><strong>Hikinaakala Heiau</strong></p>
<p><img src="../images/sacred/hikinaakala_heiau_2.jpg" width="148" height="125" alt="Hikinaakala Heiau" align="right" />Built around 1300 AD, Hikinaakala means &quot;rising of the sun.&quot; This temple was used as a place to welcome the sunrise with prayers and chants. The <a href="../kauai/ancientsites/hikinaakalaheiau.php">Hikinaakala Heiau</a> is located near another ancient site named <a href="../kauai/ancientsites/hauolacityofrefuge.php">Hauola</a>, which was a pu'uhonua (place of refuge) in ancient Hawaii.</p>
<p><strong><img src="../images/sacred/maniniholo_dry_cave.jpg" width="148" height="125" alt="Maniniholo Dry Cave" align="left" />Maniniholo Dry Cave</strong></p>
<p>This cave on Kauai's north shore is named after <a href="../kauai/ancientsites/maniniholodrycave.php">Maniniholo</a>, the legendary head fisherman of the menehune, a mischievous race of small people, or dwarfs, who lived hidden in the forests and valleys of the islands before the first settlers arrived from Polynesia. The cave is the site of an ancient Hawaiian legend.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p><strong>Menehune Fishpond</strong><img src="../images/sacred/menehune_fishpond.jpg" width="148" height="125" alt="Menehune Fishpond" align="right" /></p>
<p>The <a href="../kauai/ancientsites/menehunealekokofishpond.php">Menehune Fishpond</a> was supposedly built by the legendary little people of Hawaii, the menehune, in just one night. It is estimated to be around 1,000 years old. The fishpond is even listed on the National Register of Historic Places.</p>
<p><strong><img src="../images/sacred/pohaku_hoohanau.jpg" width="148" height="125" alt="Pohaku Ho'ohanau" align="left" />Pohaku Ho'ohanau</strong></p>
<p>Large boulders mark this important historical site. <a href="../kauai/ancientsites/pohakuhoohanaubirthstone.php">Pohaku Ho'ohanau</a> is a birthing ground of the ali'i (Hawaiian kings or chiefs). Royal women would travel here to give birth, assuring their children of high rank.</p>
<p><strong>Poli'ahu Heiau<img src="../images/sacred/poliahu_heiau.jpg" width="148" height="125" alt="Poli'ahu Heiau" align="right" /></strong></p>
<p>Located with the Wailua River State Park, the <a href="../kauai/ancientsites/poliahuheiau.php">Poli'ahu Heiau</a> is believed to be a luakini heiau, a sacrificial temple. Its stone walls make for an impressive sight.</p>
