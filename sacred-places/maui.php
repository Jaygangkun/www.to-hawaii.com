            <h1>Maui Sacred Places</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>A popular local saying when describing Maui is &quot;Maui no ka oi,&quot; which means Maui is the best. Maui is the second largest of the main Hawaiian islands (after Hawaii's Big Island) and there are many sacred sites scattered all over the place. Below are a handful that are both easily accessible and interesting.</p>
<p><strong>Iao Valley State Park </strong></p>
<p> <img src="../images/sacred/iao_valley_state_park.jpg" width="148" height="125" alt="Iao Valley State Park"  align="right" />The first thing you will notice when you arrive at <a href="../maui/attractions/iaovalleystatepark.php">Iao Valley State Park</a> is Iao Needle, a prominent natural landmark. This peaceful valley is where in 1790 the bloody battle of Kepaniwai took place.  King Kamehameha's army from the Big Island defeated the army of Maui's King Kahekeli II. As a result of the clash, many people died, their bodies clogging up Wailuku Stream. Kepaniwai literally means &quot;the water dam.&quot;</p>
<p><strong>Kanaha Pond</strong></p>
<p><img src="../images/sacred/kanaha_pond.jpg" width="148" height="125" alt="Kanaha Pond" align="left" />This peaceful pond was once a royal fishpond, believed to have been built either by chief Kihapi'ilani who lived around 1500, or by Kapi'ioho'okalani, a chief of Oahu and Molokai. Today, <a href="../maui/ancientsites/kanahapond.php">Kanaha Pond</a> is a waterfowl sanctuary and home to three endangered Hawaiian bird species, the Hawaiian stilt (ae'o), the Hawaiian coot ('alae) and the Hawaiian duck (koloa).</p>
<p><strong><img src="../images/sacred/olowalu_petroglyphs.jpg" width="148" height="125" alt="Olowalu Petroglyphs" align="right" />Olowalu Petroglyphs</strong></p>
<p>Also known as Pu'u Kilea in the Hawaiian language, the <a href="../maui/ancientsites/olowalupetroglyphs.php">Olowalu Petroglyphs</a> consist of about 100 ancient rock carvings - figures or humans and animals, as well as sails.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p><strong>Pi'ilanihale Heiau</strong></p>
<p><img src="../images/sacred/piilanihale_heiau.jpg" width="148" height="125" alt="Pi'ilanihale Heiau"  align="left" />Dating back to the late 13th century, <a href="../maui/ancientsites/piilanihaleheiau.php">Pi'ilanihale Heiau</a> is the largest heiau in all of Hawaii, encompassing almost 3 acres, which equals the size of two football fields. The huge lava rock terraces are very impressive. Located in East Maui, the heiau is situated within Kahanu Garden.</p>
<p><strong>Pu'u Keka'a</strong></p>
<p><img src="../images/sacred/puu_kekaa.jpg" width="148" height="125" alt="Pu'u Keka'a" align="right" />This popular landmark is easily accessible as it is located right in the middle of popular Ka'anapali Beach. According to an ancient legend, it is at <a href="../maui/ancientsites/puukekaa.php">Pu'u Keka'a</a> where after death, souls depart the earth and leap over into the spirit world. </p>
