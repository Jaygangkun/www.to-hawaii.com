            <h1>Big Island Sacred Places</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p>More than just picture-perfect views and gorgeous beaches, the Big Island of Hawaii is filled with sacred places, a testament to the rich culture and heritage of the Hawaiian people. These places are a must-see for travelers who want to experience the fascinating history of the Big Island.</p>
<p><strong>Ahu'ena Heiau</strong></p>
<p><img src="../images/sacred/ahuena_heiau.jpg" width="148" height="125" alt="Ahu'ena Heiau" align="right" />This temple was built by King Kamehameha I to honor Lono, the god of fertility. It served as the king&rsquo;s personal refuge and was his home for the last years of his life. Many ritual prayers took place here. The heiau is within the premises of the Kona Beach Hotel. The <a href="../big-island/ancientsites/ahuena-heiau.php">Ahu'ena Heiau</a> is located in Kailua-Kona.</p>
<? include ("../theme/text-desktop-only.html") ?>
<p><strong>Hawaii Volcanoes National Park</strong></p>
<p><img src="../images/sacred/hawaii_volcanoes_national_park.jpg" width="148" height="125" alt="Hawaii Volcanoes National Park" align="left" />This national park, which includes a diverse range of ecosystems, is said to be the home of Pele, Hawaii's fire goddess, with the Halema'uma'u Crater as her residence. According to lore, her power controls the lava, fire, heat and anything related to volcanoes. Legend has it that if any person takes volcanic rock away from the park, then that person will be cursed by Pele until the rock is returned. The Pu'u Loa Petroglyphs are also located within the park. Meaning &quot;long hill,&quot; Pu'u Loa is considered a sacred ground where fathers in the ancient Hawaii went and left  their newborns&rsquo; umbilical cords as offerings to the gods in hope that they would grant long life to their children. The Pu'u Loa Petroglyphs are located at <a href="../big-island/attractions/hawaiivolcanoesnationalpark.php">Hawaii Volcanoes National Park</a>.</p>
<p><strong>Hikiau Heiau</strong></p>
<p><img src="../images/sacred/hikiau_heiau.jpg" width="148" height="125" alt="Hikiau Heiau" align="right" />This luakini (human sacrifice) heiau was built by King Kalani'opu'u. The heiau was later visited by Captain James Cook in 1778-1779. It was here where Cook performed the first Christian ceremony in Hawaii, a funeral service for one of his crew members. The <a href="../big-island/ancientsites/hikiauheiau.php">Hikiau Heiau</a> is located within Kealakekua Bay.</p>
<? include ("../theme/text.html") ?>
<p><strong>Ku'emanu Heaiu</strong></p>
<p><img src="../images/sacred/kuemanu_heiau.jpg" width="148" height="125" alt="Ku'emanu Heaiu" align="left" />This is the only temple that is believed to have been dedicated to surfing. It was used to pray for good surfing conditions. The <a href="../big-island/ancientsites/kuemanuheiau.php">Ku'emanu Heiau</a> is located just north of Kahalu'u Beach Park, south of Kailua-Kona.</p>
<p><strong>Mo'okini Heiau</strong></p>
<p><img src="../images/sacred/mookini_heiau.jpg" width="148" height="125" alt="Mo'okini Heiau" align="right" />This is one of the oldest and most significant heiau in Hawaii, built around AD 480. It is a luakini heiau, a heiau where human sacrifice took place. It was also a place where the ali'i nui (priests and chiefs)  prayed and communicated with the gods and their ancestors. The <a href="../big-island/ancientsites/mookiniheiau.php">Mo'okini Heiau</a> is located near Hawi.</p>

<p><strong>Naha Stone</strong></p>
<p><img src="../images/sacred/naha_stone.jpg" width="148" height="125" alt="Naha Stone" align="left" />This massive boulder weighs about 7,000 pounds or 2 tons. According to an ancient legend,  the person who could lift and turn the stone over would unite all of the Hawaiian islands and rule the kingdom, a feat said to be achieved by Kamehameha I. The <a href="../big-island/ancientsites/nahastone.php">Naha Stone</a> is located in front of the Hilo Public Library.</p>
<p><strong>Puako Petroglyphs</strong></p>
<p><img src="../images/sacred/puako_petroglyphs_2.jpg" width="148" height="125" alt="Puako Petroglyphs" align="right" />This is one of the largest petroglyph fields in Hawaii, encompassing 223 acres. More than 3,000 ancient Hawaiian rock carvings are located here. The carvings include figures of humans, animals and deity symbols. The <a href="../big-island/ancientsites/puakopetroglyphs.php">Puako Petroglyphs</a> are locatd near Holoholokai Beach Park on the Kohala Coast.</p>
<p><strong>Pu'uhonua o Honaunau</strong></p>
<p><img src="../images/sacred/puu_o_honaunau_national_historic_park_south_kona.jpg" width="148" height="125" alt="Pu'uhonua o Honaunau"  align="left" />Ancient warriors, war victims and those who broke the law used to seek refuge at this sacred place, which includes houses, ancient fishponds and a heiau. The  <a href="../big-island/ancientsites/puuhonua-o-honaunau-national-historical-park.php">Pu'uhonua o Honaunau</a> National Historical Park is located at Honaunau Bay.</p>
<p><strong>Pu'ukohola Heiau</strong></p>
<p><img src="../images/sacred/puukohola_heiau.jpg" width="148" height="125" alt="Pu'ukohola Heiau"  align="right"/>Dedicated to the war god Ku, this sacrificial temple was built by Kamehameha I, with his cousin and rival as the first human sacrifice. An underwater site was also built on nearby <a href="../big-island/beaches/pelekanebeach.php">Pelekane Beach</a> as a sacred monument to Hawaii's shark god. The <a href="../big-island/ancientsites/puukoholaheiau.php">Pu'ukohola Heiau</a> is located near Kawaihae on the Kohala Coast.</p>
