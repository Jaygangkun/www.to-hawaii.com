              <h2>Native Hawaiian Population</h2>
              <p>In 1778, when English explorer Captain James Cook arrived in Hawaii, there were, according to historic estimates, between 300,000 and 400,000 Native Hawaiians (kanaka maoli) living in the Islands. However, over the following century, the Native Hawaiian population declined by 80 to 90% due to introduced diseases, including small pox, measles, influenza and whooping cough.</p>
              <p>A century later, in 1878, the native population had dropped to an estimated 40,000 to 50,000 people. At that time, Native Hawaiians still comprised about 75% of Hawaii's total population. However, over the last 120 years, the Native Hawaiian population (those with pure Hawaiian blood) has continued to decline.  There are fewer than 8,000 pure Hawaiians living today, but the number of those who are part-Hawaiian, has increased steadily over the last century. Most Native Hawaiians today have less than 50% pure Hawaiian blood.</p>
