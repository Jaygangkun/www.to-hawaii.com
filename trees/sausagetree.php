              <h2 class="small">Sausage Tree Overview</h2>
              <p>The kigelia tree is more commonly known as sausage tree, referring to the sausage-like fruit it bears. The fruit is heavy and wood-like and difficult to open without a knife. In its raw state it is poisonous. In African folk medicine it is first dried, roasted or fermented before being consumed to cure a variety of ailments, such as rheumatism or snakebites.</p>
              <p>The tree grows up to 65 feet (20 m) tall and its bark is grey and peeling on older trees. Its wood is a pale brown or yellow. The tree is interesting to look at because of its unusual looking fruit, however, it is not a good idea to have a picnic in a sausage tree's shade as the falling fruit can cause serious injury.</p>
