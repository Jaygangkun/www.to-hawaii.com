              <h2 class="small">Breadfruit Tree Overview</h2>
              <p>The breatfruit tree has big green leaves up to 2 feet (61 cm) long. Polynesians on voyaging canoes brought this tree to Hawaii because of its fruit, which can be boiled or roasted. Its starchy pulp is rather tasteless, but provides valued nutrition and an easy to grow food source. The ancient Hawaiians didn&rsquo;t eat much breadfruit (ulu) and used it mainly to feed hogs. But ulu poi (breadfruit mixed with taro) was more popular.</p>
