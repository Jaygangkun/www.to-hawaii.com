              <h2 class="small">Banana Tree Overview</h2>
              <p>The banana tree is one of the most popular food plants of the tropics. The ancient Hawaiians developed 70 varieties of banana. Even though it is listed here in the tree section, banana is actually not a tree, even though some varieties can reach heights of up to 30 feet (9 m). </p>
              <p>The banana plant's trunks are leaf stems that grow very tightly together. It is so tight that new leaves that want to grow up from the center produce a talcum powder to ease their way up. </p>
              <p>After growing for about a year, the stalk grows a flower spear, which comes out in the center and then hangs down and develops bananas. After the bananas ripen, the tree has to be cut down. Otherwise it will wither. The core of the plant which is located under the soil will then send new shoots up to become a new tree.</p>
