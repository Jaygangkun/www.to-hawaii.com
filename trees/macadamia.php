              <h2 class="small">Macadamia Tree Overview</h2>
              <p>Native to Australia, the macadamia nut tree was first introduced to Hawaii in 1890. The tree has thin leaves that have sharp, tooth-like edges. Macadamia nuts are very hard and dense, and it is impossible to crack their brown shell with one's hand only. A hammer or special machine is necessary to open them.</p>
