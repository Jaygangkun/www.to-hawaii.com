              <h2 class="small">Almond Tree Overview</h2>
              <p>Almond is a small tree that grows to a height of up to 33 feet (10 m). Its trunk is about 12 inches (30 cm) in diameter. The tree's twigs are first green and then turn purple when they are exposed to sunlight. Later after about one year they turn grey. The flowers of the almond tree are white or light pink in color, coming out in early spring. </p>
              <p>Almond trees reach full maturity after 5-6 years. The fruit matures in the fall, about 7-8 months after flowering. Almonds are not a true nut, but rather a drupe, which consist of an outer hull and a shell with the seed (the nut) inside. The almond tree is native to South Asia and the Middle East.</p>
