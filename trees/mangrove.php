              <h2 class="small">Mangrove Tree Overview</h2>
              <p>Mangrove trees grow in places where other trees would never take root, such as in seaside marshes, in saltwater or brackish water. To avoid drowning, the mangrove grows stilt roots above the water line. These arching aerial roots form a dense thicket. </p>
              <p>Mangroves have the ability to stabilize a shoreline by collecting silt and soil and for this reason were brought to Hawaii in 1902. They were first planted to stabilize Molokai's mud flats in the southwestern part of the island. Now mangrove trees can also be found on Kauai (for example along the Wailua River), on Oahu (for example on the Kaneohe Bay shoreline), on Lanai and the Big Island.</p>
