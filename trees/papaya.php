              <h2 class="small">Papaya Tree Overview</h2>
              <p>Papaya trees can grow to a height of up to 25 feet (7.6 m). The tree grows leaves only on the top and the fruit dangles on the side of the trunk. The trunk doesn't consist of wood, but is rather hollow. It is held together by fibers in the bark. </p>
              <p>Papaya trees usually only live for about 10 years, in which time they are very productive. The trees bear fruit year-round. The papaya fruit has either yellow, orange or red meat (depending on the species) and the core of the fruit contains black round seeds. The seeds have a gelatinous coating and have a mustard flavor. They are edible and are believed to have medicinal properties. </p>
              <p>In Hawaii it is customary to serve one seed together with the fruit. Papayas contain many vitamins as well as the enzyme papain, which can be used as a meat tenderizer.</p>
