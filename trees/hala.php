              <h2 class="small">Hala Tree Overview</h2>
              <p>The hala tree is also referred to as screwpine and grows  from sea level up to an elevation of 2,000 feet (610 m). It is a common sight  in Hawaii's coastal regions (especially on the islands' windward coasts). Male  flowers are fragrant and small, arranged in drooping clusters about 1-2 feet (30-60  cm) long. Female flowers produce a large fruit that looks almost like a  pineapple. Male hala trunks are hard and solid while female trunks are soft  inside. </p>
              <p>The ancient Hawaiians used hala leaves (lau hala) to make baskets,  mats, hats, sandals and in construction, for example to make roofs. Lau hala  roofs can last up to 15 years while roofs made from coconut leaves usually last  just 3 years. The trees fruit can be eaten, but was consumed usually only  during times of famine.</p>
