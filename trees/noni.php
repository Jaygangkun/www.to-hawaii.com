              <h2 class="small">Noni Tree Overview</h2>
              <p>Commonly known as Indian mulberry, the noni tree was brought to Hawaii by the early Hawaiians. They treasured the fruit for its many uses, including the treatment of common ailments, such as wounds</span> and pain. </p>
              <p>They also extracted dyes for their kapa clothes from the tree, red from the bark and yellow from the tree's trunk and roots. Today, noni is sold as a nutritional supplement in many health food stores. </p>
              <p>The noni fruit emits a stinky odor and many people don't like the fruit's taste. It takes some getting used to. Alternatively, one can mix a small amount of noni juice with another sweeter fruit juice or eat noni fruit leather, which is also more palatable.</p>
