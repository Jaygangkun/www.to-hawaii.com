<h2 class="small">Eucalyptus Tree Overview</h2>
<p>More than 90 eucalyptus tree species have been introduced to Hawaii. Native to mainly Australia, eucalyptus trees grow fast and can reach heights of up to 300 feet (91 m), making them the tallest hardwood trees in the world. </p>
<p>A common sight on all the main Hawaiian islands is the rainbow eucalyptus (Eucalyptus deglupta), also known as Bagras eucalyptus or Mindanao gum. Its main distinguishing feature is its colorful trunk. As the outer bark falls off, the trunk reveals layers of green, red, orange, yellow, pink and purple. </p>
<p>This species if native to the Philippines and Indonesia. It is one of the fastest growing trees in the world, reaching 100 feet (30 m) in just seven years.</p>
