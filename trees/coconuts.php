<h2 class="small">Coconut Tree Overview</h2>
<p>There are dozens of coconut palm tree species of Hawaii. All but one, the loulu (genus Pritchardia), were brought here by humans. About 19 types of loulu exist in the Islands, but they are rare and many endangered. </p>
<p>The coconut tree is considered one of the most useful trees in the world. The coconut fruit has water and milk which can be drunk and flesh that can be eaten. From the pulp coconut oil can be extracted. The oil can be used in cooking, cosmetics and for lamp fuel. Coconut leaves can be used for weaving and thatching (for example to make roofs) and the fibers to make for example sandals. Coconut husks can be used for food bowls and the trunk to build all kinds of things.</p>
<p>Some coconut tree species can grow up to 100 feet (30 m) tall. The coconut is one of the largest seeds known, and even after floating in the ocean for months it can still sprout after making landfall.</p>
