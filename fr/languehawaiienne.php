              <p>Traditionnellement, le W se prononce &quot;v&quot;.</p>
              <p>Quand les consonnes sont jointes aux voyelles, elles  modifient le son de ces derni&egrave;res. Ainsi, le H plac&eacute; devant les voyelles  sonnent comme ceci:</p>
              <p>Ha devient &quot;hah&quot;<br />
