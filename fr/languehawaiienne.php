              <p>La langue hawaiienne, ou &quot;Oleho Hawaii&quot;, est l'une  des plus anciennes langues vivantes au monde. Depuis l'introduction de  l'anglais &agrave; Hawaii en 1778, le nombre de personnes parlant l'hawaiien  couramment a fortement diminu&eacute;. Aujourd'hui seul 1% des habitants de l'archipel  parle le langue native.</p>
