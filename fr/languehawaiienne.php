                Hu devient &quot;Hou&quot;</p>
              <p>L'okina est un symbole ressemblant &agrave; une apostrophe ('). Il  est utilis&eacute; pour casser un mot et est toujours plac&eacute; entre deux voyelles. Les  hawaiiens l'utilisent &agrave; l'oral comme &agrave; l'&eacute;crit. L'okina n'est pas toujours  employ&eacute; sur internet et sur les imprim&eacute;s, sauf sur la plupart des journaux et  des livres vendus sur l'archipel.</p>
              <p>Par exemple, le mot Lanai, sans okina, se prononcerait  &quot;Lah-na&iuml;&quot;. Avec son okina, il s'&eacute;crit Lana'i et se prononce  &quot;Lah-nah-i&quot;.</p>
