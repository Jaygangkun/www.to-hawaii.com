              <p><strong>Petit aper&ccedil;u de  l'hawaiien</strong></p>
              <p>Il y a 12 lettres, 5 voyelles et 7 consonnes, dans  l'alphabet hawaiien. Il y a aussi l'okina et la kahako, qui sont des  ponctuations changeant la prononciation d'un mot.</p>
              <p>L'alphabet est: A, E, I, O, U, H, K, L, M, N, P, W</p>
              <p>A se prononce &quot;ah&quot;<br />
                E se prononce &quot;eh&quot;<br />
                I se prononce &quot;i&quot; (i tra&icirc;nant)<br />
                O se prononce &quot;oh&quot;<br />
