                Histoire de l'hawaiien</p>
              <p>Avant l'arriv&eacute;e du capitaine Cook, l'hawaiien &eacute;tait une  langue exclusivement orale. Cook et ses hommes not&egrave;rent son existence pour la  premi&egrave;re fois en 1778. Ils se sont vite rendu compte qu'elle &eacute;tait similaire  aux langues parl&eacute;es par les Maori et les Tahitiens.</p>
              <p>Quand les missionnaires arriv&egrave;rent &agrave; Hawaii, ils avaient  besoin de convertir cette langue orale en une langue &eacute;crite. Ils enseign&egrave;rent  aux hawaiiens &agrave; lire leur langue &eacute;crite afin de pouvoir leur transmettre le  message de la Bible.</p>
              <p>Les missionnaires d&eacute;velopp&egrave;rent un alphabet hawaiien,  compos&eacute; de 12 lettres. Plus tard, deux symboles de ponctuation,  l'&quot;okina&quot; et le &quot;kahako&quot; furent ajout&eacute;s.</p>
              <h2>Le bannissement de  l'hawaiien</h2>
              <p>L'hawaiien fut la langue principale durant de nombreuses  ann&eacute;es. Apr&egrave;s l'annexion d'Hawaii aux Etats-Unis en 1898, elle fut  officiellement bannie des &eacute;coles et du gouvernement. Son utilisation fut m&ecirc;me  interdite dans les &eacute;coles Kamehameha, des &eacute;coles priv&eacute;es r&eacute;serv&eacute;s aux hawaiiens  de souche.</p>
