              <p>The reef breaks down into sand by two main factors &ndash; mechanical erosion and bioerosion. These two processes create some of the sand for Hawaii's beaches. Mechanical erosion is mainly waves and currents that erode the reef and break it down. Bioerosion is the breaking down of the reef structure into sediment by various marine animals. </p>
              <p>One such marine species that significantly contributes to bioerosion is the parrotfish. This fish species (there are about 90 species of parrotfish worldwide, known collectively as Scaridae) is named for their dentition. They have many teeth that form a parrot-like beak with which they scrape off algae and a variety of reef organisms and pieces of coral from the reef. They play an important role in making the coral reef grow because the algae they feed on would otherwise smother the coral. During the feeding process they grind up coral rock and ingest it. After the rock is digested, they excrete it as sand. One parrotfish can produce about 90 kg (198 lbs) of sand per year. </p>
              <p align="center"><img src="images/sand/palenose_parrotfish.jpg" alt="" width="82%" /></p>
              <p>So it is a curious thought how much of the sand on that pretty-looking Hawaiian white-sand beach actually came out of a parrotfish's digestive tract and how much of it is simply crushed shells and other material.</p>
              <p><strong>Hawaiian white sand beaches</strong></p>
              <p>For many people, powdery white-sand beaches are the ideal type of beach. White sand is mainly made up of calcareous, reef-derived material, such as crushed corals and shells. An example of a Hawaiian white-sand beach is <a href="oahu/beaches/waimanalobeachpark.php">Waimanalo Beach</a> on Oahu.</p>
              <p><strong>Hawaiian black sand beaches</strong><br />
                <br />
                Black sand beaches in Hawaii can mainly be found on Hawaii's Big Island (and another popular one, <a href="maui/beaches/waianapanapa-black-sand-beach.php">Waianapanapa</a>, is located on Maui). The black sand comes from volcanic activity and since the Big Island is the only island in Hawaii that currently has active volcanoes, most black sand beaches are located here (black sand beaches are located near the most volcanically active areas). Black sand beaches are created when lava flows into the ocean. Black sand beaches usually have a short life span (up to a few hundred years) because as soon as the volcanic activity stops, so does the source of the black sand. Over the years the ocean erodes the beach and the waves carry the black sand away. This is in contrast to white-sand beaches, which have a continuous source of coral and shell fragments and other reef-derived materials. An example of a Hawaiian black-sand beach is <a href="big-island/beaches/punaluublacksandbeach.php">Punaluu Black Sand Beach</a> on the Big Island.<br />
                <br />
                <strong>Hawaiian red sand beaches</strong><br />
              <p>Red sand beaches are a pretty, but rare sight. In Hawaii, they are a result of a nearby cindercone that is rich in iron and has eroded over many years. An example of a Hawaiian red sand beach is <a href="maui/beaches/red-sand-beach.php">Kaihalulu Beach</a> on Maui, located at the foot of Ka'uiki Head, a cindercone hill.              </p>
              <p>
