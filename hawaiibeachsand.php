              <strong>Hawaiian green sand beaches</strong></p>
              <p>                Green beach sand is composed of basaltic lava that has a high olivine content. Olivine is a semi-precious stone. One example of a Hawaiian green sand beach is <a href="big-island/beaches/papakoleagreensandbeach.php">Papakolea Green Sand Beach</a>, located on the Big Island. The green sand on this beach comes from the nearby littoral cone. As the ocean erodes these stones, they are crushed into fine sand.</p>
              <p>                Besides sandy beaches, there are also <strong>lava rock and pebble beaches</strong> in Hawaii, as well as one unique beach on the island of Kauai &ndash; <a href="kauai/beaches/glassbeach.php">Glass Beach</a>. Much of the beach here is made up of millions of colorful pieces of so-called sea glass.
