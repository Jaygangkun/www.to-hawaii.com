?> <p>The Hawaiian islands are surrounded by an extensive coral reef, which continuously grows as well as breaks down. The coral reef is built by marine animals, such as corals, and plants, such as algae, that live on the reefs. <br />
                <br />
                Corals are the main contributor to a reef's growth. A coral is a tiny animal referred to as a polyp. It has a hard shell that is composed of calcium carbonate, which it extracts from the substances of the ocean. A collection of matured hard coral is called a coral colony. Coralline plants, or calcareous algae, also contribute to a coral reef's growth. This algae grows on the coral heads and also uses calcium carbonate in the reef building process. Many other species, such as sponges and other algae, live on the coral reef that are part of this ecosystem.</p>
