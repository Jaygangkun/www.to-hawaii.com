                            <p>As with almost all other Northwestern Hawaiian Islands, all visitors to Nihoa need to obtain a USFWS-issued special-use permit to go on the island. This is to prevent the introduction of alien species to the island's fragile ecosystem. In 1988, Nihoa was added to the National Register of Historic Places and became part of the Papahanaumokuakea Marine National Monument in June of 2006.</p>
