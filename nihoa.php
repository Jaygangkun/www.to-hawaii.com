              <p>Nihoa, also known as Bird Island or Moku Manu, is the youngest of the Northwestern Hawaiian Islands; it is about 7.2 million years old. Situated about 120 miles (193 km) northwest 
                of the island of Niihau and about 280 miles (450 km) northwest of Honolulu, Nihoa Island encompasses 0.27 square miles (0.7 km&sup2;) of  land and is also the tallest of the Northwestern Hawaiian Islands, rising 903 feet (275 m). The island was once much larger, but erosion did its job so that these 900-foot cliffs are all that's left. </p>
