              <p>Nihoa was once settled by the early Hawaiians. Extensive prehistoric agricultural terraces and house sites have been found on the island. However, it is not known exactly how many people once lived on Nihoa. Even though large terraces were found that suggest a large number of people living there, there is only a small amount of freshwater on the island. That's why it is thought that Nihoa was only used for religious purposes. </p>
