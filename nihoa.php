              <p>Nihoa got its name based on its look - "Nihoa" means "tooth" in Hawaiian. The island's volcanic peak looks like a tooth that comes out of the ocean. In fact, Nihoa has two peaks, Miller's Peak in the west (903 feet/275 m) and Tanager Peak in the east (852 feet/260 m). There are also six valleys that slope down from north to south, meeting at the south coast of the island: West Valley, West Palm Valley, Miller Valley, Middle Valley, East Palm Valley and East Valley. Other interesting features on Nihoa include Dog's Head Peak (358 feet/109 m), which is named because it looks like that, and Pinnacle Peak (626 feet/191 m), which is a volcanic dike. Albatross Plateau is the only flat area on Nihoa. </p> <?php
