              <h2>The Spirit of Aloha</h2>
              <p>The literal meaning of aloha is "the presence of breath" or "the breath of life." It comes from "Alo," meaning presence, front and face, and "ha," meaning breath. Aloha is a way of living and treating each other with love and respect. Its deep meaning starts by teaching ourselves to love our own beings first and afterwards to spread the love to others. </p>
              <p>According to the old kahunas (priests), being able to live the Spirit of Aloha was a way of reaching self-perfection and realization for our own body and soul. Aloha is sending and receiving a positive energy. Aloha is living in harmony. When you live the Spirit of Aloha, you create positive feelings and thoughts, which are never gone. They exist in space, multiply and spread over to others.</p>
