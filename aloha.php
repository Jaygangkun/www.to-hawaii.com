              <h3>Aloha Friday</h3>
              <p>Many companies allow their employees to "dress down" on the last day of the workweek - on Aloha Friday. This is when aloha attire may be worn. But it hasn't always been like that. This custom has an interesting history, which is closely tied to Hawaii's clothing industry.</p>
              <p>Until the 1940s, much of the locally produced designs were sold to tourists. Employees at large Hawaiian companies were required to wear formal clothes - suits and ties for men and business dresses, skirts and blouses for women. In 1947, Aloha Week was established and people realized that Hawaii's economy would benefit if Hawaii's residents were to wear more Hawaii-made clothing. </p>
              <p>So each Friday, the City and County of Honolulu allowed men to wear sports shirts (but not all too colorful ones) while on the job. By the 1960s, the fashion barrier weakened even more. The Bank of Hawaii became the first corporation that allowed aloha attire on Fridays. Today, it is rare to find a business that does not follow the practice of Aloha Friday.</p>
              <p><strong>Aloha Shirt</strong></p>
              <p>The Spirit of Aloha has given its name to the famous and colorful Hawaiian shirt, known as the aloha shirt. It is adorned in an endless variety of designs and patterns and is tinted in bright colors.</p>
              <p>Hawaii produces two types of shirts - aloha shirts, manufactured for locals, and shirts manufactured for tourists and for export to the mainland and other parts of the world. The difference between both types of shirts is that the once manufactured for locals are designed in floral or generic Polynesian motifs, while the shirts manufactured for tourists and for export are much more colorful and can have some of the following motifs: sunny day in Hawaii, shirts adorned with palm trees, tropical cocktails, leis, beaches, blue ocean, flowers, etc.</p>
              <p>Another difference between both types of aloha shirts is that tourists wear the shirt as a casual and informal shirt, while the shirts that locals tend to wear are considered as official wear in business organizations and government.</p>
