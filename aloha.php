              <p><strong>What Does Aloha Mean?</strong></p>
              <p>Aloha is the most Hawaiian word. In the Hawaiian language, it can mean hello or goodbye. It also means love and affection. The word aloha is used in a combination with other words, such as aloha kakahiaka, which means good morning; aloha auinala used as a greeting that means good afternoon; and aloha ahiahi is how you can wish good evening in Hawaiian. Because of aloha's 
                unique meaning and popularity, Hawaii is called the Aloha State.</p>
              <p>Aloha is a Hawaiian symbol. Its meaning goes beyond any definition you can find about it in the dictionaries. In Hawaii, you hear aloha all the time and you are treated with aloha everywhere.</p>
