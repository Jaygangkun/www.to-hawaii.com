              <h2 class="small">Lizardfishes (Synodontidae) Overview</h2>
              <p>Lizardfishes usually lay calmly on rocks or sand and strike quickly when prey is spotted. They have a mouthful of sharp teeth, including on their tongue, and blend in easily in their surroundings and are therefore difficult to spot. Their main food source is other fish, which they usually swallow head first and in one piece. The Hawaiian name for lizardfish is 'ulae, and 16 species can be found in Hawaiian waters.</p>
