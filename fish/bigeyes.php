              <h2 class="small">Bigeyes (Priacanthidae) Overview</h2>
              <p>There are about 18 species of bigeyes. Two of them, the  Hawaiian Bigeye and the Common Bigeye, live in Hawaii's nearshore waters. Two  other species live in deeper waters further offshore. The most obvious  characteristic of this family of fish is, as their name implies, their  relatively large eyes that are suited to their nocturnal and carnivorous  lifestyles. Their body is narrow with fine scales and is typically bright red  in color. </p>
              <p>During the day, they usually hide in caves and under ledges.  They come out during the night to feed on planktonic animals. Their eyes are  very reflective. Nightdivers who shine a flashlight into the eyes of a Bigeye  will notice the reflected laser-like beam. </p>
              <p>The Hawaiian name of this fish species is 'aweoweo, meaning  "glowing red." It is rather rare that large schools of Bigeye can be seen near  the shore. But this happened in January 1891 at around the time of King  Kalakaua's death in San Francisco. An enormous amount of bigeyes were then  observed in the waters off Pearl Harbor. Since red was a color of Hawaii's  ali'i (royalty), events such as this one were considered to foretell the death  of a high chief. </p>
