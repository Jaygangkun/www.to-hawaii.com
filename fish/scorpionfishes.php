              <h2 class="small">Scorpionfishes (Scorpaenidae) Overview</h2>
              <p>Scorpionfishes don't like to move around much. They are sedentary carnivores and some of them have venomous spines. Since many of them are masters of camouflage and blend in with their surroundings, it is often difficult to spot them. They usually hunt for prey at night. </p>
              <p>Their sting is very painful and causes cramps and nausea. Since they live in the reef and are difficult to see, it's always a possibility to accidentally touch or step on one. A known first-aid treatment for scorpionfish stings is hot water. The affected area should be soaked in it for at least 30 minutes as heat destroys the venom. The victim should also be brought to a hospital for further care.</p>
              <p>The Hawaiian name for scorpionfish is nohu. There are 28 species of scorpionfishes in Hawaiian waters, of which ten are endemic.</p>
