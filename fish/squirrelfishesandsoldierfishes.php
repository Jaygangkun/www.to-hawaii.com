              <h2 class="small">Squirrelfishes and Soldierfishes (Holocentridae) Overview</h2>
              <p>Most squirrelfishes and soldierfishes are red in color. They are nighttime predators with large black eyes and a forked tail fin.</p>
              <p>Squirrelfishes have one more backward pointing spines on their gill covers. Most of them have horizontal, silver-colored stripes. When approached, squirrelfish often times make clicking sounds while nervously swimming around. The Hawaiian name for squirrelfish is 'ala'ihi. </p>
              <p>Soldierfishes don't have stripes. They have more rounded snouts and deeper bodies. Most of them also don&rsquo;t have backward pointing spines on their gill covers. They also make grunting sounds when they are disturbed and nervous. Their Hawaiian name is 'u'u.</p>
              <p>About 19 holocentridae species inhabit Hawaiian waters, some of which are can be found only in deeper waters. The name holocentridae means &ldquo;all spiny,&rdquo; which describes these fishes well.</p>
