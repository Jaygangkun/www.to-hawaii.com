              <h2 class="small">Dartfishes (Ptereleotridae) Overview</h2>
              <p>Dartfishes have two dorsal fins and feed on plankton. They are related to gobies and can be observed in sand- and rubble-bottomed areas. They are shy and if approached, they hide in burrows. Two species of dartfishes can be found in Hawaii, the Fire Dartfish and the Indigo Dartfish.</p>
