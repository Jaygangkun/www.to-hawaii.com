              <h2 class="small">Eels (Congridae, Muraenidae, Ophichthidae) Overview</h2>
              <p>Eels look like snakes and like to hide in reef crevices and  sand. There are hundreds species of eels, but in Hawaii,  only moray eels, conger eels and snake eels can be spotted by divers and  snorkelers.</p>
              <p>Their collective Hawaiian name is puhi, and some of them  were revered as a food source. Other species were 'aumakua, physical  embodiments of family gods and protectors of the family.</p>
              <p>There are about 12 species of conger eels in Hawaii.  Most of them live in deeper waters. They have a nocturnal lifestyle and stay  hidden during the day. They have small teeth, and small fish are their primary  food source. </p>
              <p>In Hawaii,  there is also one species of Garden Eels (Hawaiian Garden Eel). They are active  during the day and can be seen in sandy areas.</p>
              <p>Moray eels are common in Hawaii's  reefs. They can be seen looking out between rocks, part of their body hidden.  It is easy to mistake them for a rock. They often times keep their mouths  partially open, displaying their teeth. Most of them feed mainly at night.  Their main food source is small fish and crustaceans.</p>
              <p>Snake eels, as their name implies, are slim and resemble  snakes. They have a pointed tail and along dorsal fin. About 17 snake eel species  can be found in Hawaii.</p>
