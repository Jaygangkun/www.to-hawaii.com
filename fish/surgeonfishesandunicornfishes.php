              <h2 class="small">Surgeonfishes and Unicornfishes (Acanthuridae) Overview</h2>
              <p>Surgeonfishes and unicornfishes are some of the most common fishes on Hawaii's reefs. They usually have oval or oblong bodies with eyes located high on their heads. Most surgeonfishes are herbivores. They scrape algae from rocks and corals with their small mouths. Some surgeonfish species feed on organic matter on the ocean bottom. Unicornfishes are typically plankton eaters. </p>
              <p>Surgeonfishes are characterized by their two forward pointing spines at the base of their tales. These &ldquo;scalpels&rdquo; are razor sharp and cause a painful cut when touched. But they pose the most danger to fishermen trying to remove surgeonfish from a net or spear. They don&rsquo;t pose as much danger to snorkelers and divers. </p>
              <p>Unicornfishes on the other hand have rigid bony keels instead of scalpels, which are also very sharp. Some unicornfish species also have a horn on the forehead.</p>
              <p>Unicornfishes are known as kala (meaning &ldquo;thorn&rdquo;) in the Hawaiian language. There is no general name for surgeonfishes. There are 23 acanthurid species in Hawaiian waters, of which one is endemic.</p>
