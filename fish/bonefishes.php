              <h2 class="small">Bonefishes (Albulidae) Overview</h2>
              <p>Bonefishes have slim and silvery bodies with a deeply forked tail. They can be seen in shallow, sand-bottomed waters. Two species of bonefish can be found in Hawaiian waters, the Short Jaw Bonefish and the Longjaw Bonefish. Their flesh is flavorful, but has many small bones, hence their name. Their Hawaiian name is 'o'io. </p>
