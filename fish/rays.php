              <h2 class="small">Rays (Dasyatidae, Mobulidae, Myliobatidae) Overview</h2>
              <p>Even though they hardly look like sharks, rays are closely related to them. Rays&rsquo; skeletons are composed of cartilage and they don&rsquo;t have swim bladders. They are carnivores and most of them are bottom dwellers, except for manta rays, eagle rays, devil rays and cownose rays. </p>
              <p>They swim with their pectoral fins, slowly gliding through the endless ocean abyss. Their mouth is on the bottom of their bodies (except in manta rays), and their gill slits are under their pectoral fins. The Hawaiian name for rays is lupe (meaning &ldquo;kite&rdquo;) and hihimanu (meaning &ldquo;magnificent&rdquo;).</p>
