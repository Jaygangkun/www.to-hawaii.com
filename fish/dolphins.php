              <h2 class="small">Dolphins (Delphinidae) Overview</h2>
              <p>These fascinating mammals are intriguing, graceful and fun to watch. They rely on several forms of communication, including whistling and body language and use echolocation (sonar) to find food. As many as 15 species of dolphin, including bottlenose and spinner dolphins, can be found in Hawaiian waters. If you come across dolphins while swimming in the ocean, the keyword to remember is <em>respect</em>: </p>
              <ul>
                <li>Do not attempt to touch wild dolphins - it is illegal. There are several programs in the islands that work with domesticated dolphins that may allow you to make contact. </li>
                <li>Do not make aggressive, sudden or jerky movements. </li>
                <li>Swim parallel with the dolphins.</li>
                <li>Remember you are in their home. Allow the dolphins to make any "first moves" toward interaction. </li>
              </ul>
