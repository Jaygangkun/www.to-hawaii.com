              <h2 class="small">Sea Cucumbers (Holothuroidea) Overview</h2>
              <p>Sea cucumbers have leathery skin and an elongated body with a single gonad. They feed on debris on the seafloor or in midwater. They move around using their tube feet and usually measure 3-12 inches (10-30 cm) in length. The smallest species of sea cucumbers is just 0.12 inch (3 mm) long and the largest can grow up to 3.3 feet (1 m). They communicate with each other by sending hormone signals through the water. Their Hawaiian name is loli.</p>
