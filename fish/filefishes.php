              <h2 class="small">Filefishes (Monacanthidae) Overview</h2>
              <p>Filefishes have narrow bodies, small mouths with sharp teeth and a stout dorsal spine with often times small, thornlike spikes which they raise when they feel aroused or threatened. </p>
              <p>They swim by waving their dorsal and anal fins and can swim forward and backward. Filefishes are generally omnivorous and some of them can change their color quickly to match their surroundings.</p>
              <p>Their Hawaiian name is 'o'ili, which means &ldquo;sprout&rdquo; or &ldquo;come up.&rdquo; This probably refers to their frequently raised dorsal spine. There are seven species of filefishes in Hawaii, of which three are endemic.</p>
