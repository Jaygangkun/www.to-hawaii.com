              <h2 class="small">Wrasses (Labridae) Overview</h2>
              <p>Wrasses are common coral reef fish. Most species have elongated bodies with a continuous dorsal fin. But they come in all sorts of colors and sizes. All of them are either carnivores or planktivores and have sharp teeth.</p>
              <p>Most wrasses begin their life as a female and some of them later on change into males, who are usually larger and more colored and more active than females. The males are the leader of a group of females. Should a male die, the dominant female of that group transforms itself into a male to take his place. Their Hawaiian name is hinalea.</p>
