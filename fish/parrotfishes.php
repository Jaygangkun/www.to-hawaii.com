              <h2 class="small">Parrotfishes (Scaridae) Overview</h2>
              <p>Parrotfishes are herbivorous and feed mainly on algae. They have beaklike dental plates that make it easy for them to scrape off algae from rocks and coral heads. </p>
              <p>They usually have a pretty blue-green body and swim mainly with their pectoral fins. They can be difficult to spot in the water since they move fast. </p>
              <p>In Hawaii, parrotfishes are also known as uhu, the juveniles as &lsquo;ohua and mid-sized fish as panuhu. As they grow older they undergo color and sex changes.</p>
