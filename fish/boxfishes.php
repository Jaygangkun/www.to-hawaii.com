              <h2 class="small">Boxfishes (Ostraciidae) Overview</h2>
              <p>Boxfishes are closely related to the pufferfishes and filefishes. Five species can be seen in Hawaiian waters. Their bodies are encased in a rough shell and only their eyes, fins and mouths are movable. Some of them have a box-like shape, other are triangular, hexagonal or round. They usually stay by themselves, except when they are mating, and can be seen feeding on algae, worms, small crustaceans and sponges on the reef. Their Hawaiian name is pahu, meaning "box." </p>
