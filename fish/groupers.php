              <h2 class="small">Groupers (Serranidae) Overview</h2>
              <p>Groupers have large mouths, heavy bodies (they can grow to an enormous size), protruding lower jaw and are usually solitary bottom-dwellers. They are also called sea basses and can be found in shallow waters as well as depths of many hundreds of feet. Their tail fin usually has a straight or rounded back edge. Most groupers&rsquo; skin is blotched and dull looking, while a few others are brightly colored.</p>
              <p>They feed by carefully stalking their prey and strike when close enough.  Instead of chasing their prey, they engulf their prey and when close enough open their mouths. This sucks water into their mouths as well as their food, which can be anything from stingrays, lobsters, small fish and even sea turtles.</p>
              <p>The Giant Grouper has such a large mouth and body that a human could fit into it. It can grow to nine feet in length and weigh 900 pounds. However, this species is very rare in Hawaii. The endemic Hawaiian Black Grouper is another large species that can be found in the Northwestern Hawaiian Islands. Its Hawaiian name is hapu'u.</p>
