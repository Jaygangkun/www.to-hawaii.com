              <h2 class="small">Snappers (Lutjanidae) Overview</h2>
              <p>Snappers are carnivores. Most species of snappers that live in Hawaiian waters inhabit the deeper offshore waters, including the onaga, 'opakapaka and 'ula'ula, all prized food fishes.</p>
              <p>They vary in length from about 10 inches (25 cm) to 3 feet (90 cm). Fourteen species inhabit Hawaiian waters. The four species that divers and snorkelers in Hawaii are most likely to see are the forktail snapper (wahanui), gray snapper (uku), bluestripe snapper (ta'ape) and blacktail snapper (to'au).</p>
