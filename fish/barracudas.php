              <h2 class="small">Barracudas (Sphyraenidae) Overview</h2>
              <p>Barracudas can be found in tropical and subtropical oceans  worldwide. They have a long body that's covered with small and smooth scales.  Some species, such as the Great Barracuda, can grow up to 6 feet (1.8 m) in  length and move at almost 40 feet per second. In Hawaii, they are usually about  half that size. </p>
              <p>Barracudas have very sharp teeth and have a reputation  similar to sharks. They have been known to attack swimmers, but this is a rare  occurrence in Hawaii. Two such attacks have been recorded in Hawaii; both of  them happened in the 1960s to Maui fishermen. The men survived, but needed  surgery to close their wounds. </p>
              <p>Barracudas can be seen swimming alone or in groups and  usually wait motionless in shallow waters near the shoreline, and as soon as  they spot food, they strike. In the Hawaiian language, the Great Barracuda is  called kaku.</p>
