              <h2 class="small">Chubs (Kyphosidae) Overview</h2>
              <p>Chubs are typically heavy-looking fishes with oval,  medium-sized bodies and small, pointed mouths and large tails. In Hawaii,  they can be observed in shallow rocky areas, feeding on plants, including  seaweed. Of the 47 species of chubs worldwide, six can be found in Hawaii.  Their Hawaiian name is nenue. </p>
              <p>If fed, chubs are known to be greedy and swarm in an almost  aggressive way around the food source. They are known to nib hands to get to  the food quicker. During the times when the popular snorkeling beach Hanauma   Bay on the island   of Oahu still allowed fish feeding,  the chubs were known as "Hanauma Bay's  pirhanas."</p>
