              <h2 class="small">Jacks (Carangidae) Overview</h2>
              <p>Several fish species belong in the family of jacks, including trevallies, leatherbacks, rainbow runners, pompanos, scads and others. They usually stick to open waters near the edge of the reef. Occasionally they have been observed in shallow waters as well. </p>
              <p>Jacks are strong fighting fish. Most feed on fish, which they usually chase with great speed, often making swift moves and changing directions to confuse their prey.</p>
              <p>Jacks usually have silvery sides and undersides with greenish or bluish backs. This makes them difficult to spot. </p>
              <p>Their Hawaiian name is ulua, and juveniles are papio. Uluas were admired in the old Hawaii for their vigor and strength.</p>
