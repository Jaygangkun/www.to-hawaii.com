              <h2 class="small">Crabs (Decapod crustaceans) Overview</h2>
              <p>There are almost 200 species of crabs in Hawaii. The scientific name of crabs is decapod crustaceans, a name that applies to true crabs, which typically have a very short projecting tail or a short abdomen that&rsquo;s hidden under the thorax. Other animals with similar names, such as hermit crabs, are not true crabs.</p>
              <p>Many crab species walk sideways, but not all. They can communicate among each other by waving or drumming their pincers. They are often times aggressive toward each other and male crabs sometimes fight over female crabs or hiding holes. </p>
              <p>Crabs are omnivores and primarily eat algae, but also other foods that they find, including bacteria, fungi, worms and other crustaceans.</p>
