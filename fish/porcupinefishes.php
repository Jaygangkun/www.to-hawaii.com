              <h2 class="small">Porcupinefishes (Diodontidae) Overview</h2>
              <p>Porcupinefishes are covered with sharp spines and can inflate into a large ball too big to swallow, which makes it difficult for predators to attack and eat them. When in peace the sharp spines lie flat against their bodies, but stand up when they start to swell. </p>
              <p>A large porcupinefish in its inflated state can be the size of a basketball and with its additional 2-3 inch (5-7 cm) spines it can choke a large shark to death.</p>
              <p>In addition to their sharp spines as a defense mechanism, porcupinefishes are also poisonous. In spite of this, tiger sharks sometimes prey on porcupinefishes, which really justifies their name as &ldquo;nature's most feared predator.&rdquo;</p>
              <p>If stung by a porcupinefish, the victim should be brought to a hospital immediately. Their poison tetrodotoxin causes tingling, numbness and eventual paralysis and can lead to death, usually within 24 hours or less. One milligram of  tetrodotoxin is enough for humans to cause death. There is no remedy, but artificial respiration can keep the victim alive until the symptoms wear off.</p>
              <p>In Hawaii, porcupinefish are also called kokala. In the old Hawaii they were revered by some families as 'aumakua, the family god and protector.</p>
