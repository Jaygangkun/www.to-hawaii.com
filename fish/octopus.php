              <h2 class="small">Octopuses (Octopoda) Overview</h2>
              <p>The most common octopus in Hawaii is the Hawaiin Day Octopus (Octopus cyanea), which is known as he'e mauli in the Hawaiian language. It is a very intelligent creature with a large brain (when compared to its body size), a talent for problem solving and skilled in a variety of defense mechanisms. </p>
              <p>All octopuses have a funnel with which they propel themselves forward, eight arms with 1,920 muscular suction cups, three hearts, a hard beak, an inc sac and a tongue with teeth on it. They don't have bones or a shell. </p>
              <p>The he'e is active during the day and at night retreats into holes in the reef. It can be seen in shallow water up to depths of around 150 feet. It can grow to have an arm span of 3 feet (90 cm) and weigh 4 to 5 pounds.</p>
