              <h2 class="small">Monk Seals (Monachus Tropicalis, Monachus Monachus, Monachus Schauinslandi) Overview</h2>
              <p>There are three species of monk seals on Earth - the Caribbean,  the Mediterranean and the Hawaiian monk seal, also known  as monachus schauinslandi. It is considered endemic to the Hawaiian   Islands, meaning that this monk seal species exists nowhere else  in the world.</p>
              <p> The Hawaiian monk seal is protected by the Endangered Species Act and the  Marine Mammal Protection Act. Currently, Hawaii's  monk seal population is estimated to be around 1,500. Because it is an  endangered species, it is illegal to harass, capture or kill monk seals in Hawaii.</p>
              <p> The Hawaiian monk seal breeds mainly in the uninhabited Northwestern   Hawaiian Islands, which are located to the northeast of the island   of Kauai, the northernmost of the  seven populated Main Hawaiian Islands. Sometimes, monk seals can be seen  resting on beaches of the Main Hawaiian Islands, particularly on Kauai  and Oahu. However, monk seals don't stay there very long  as they prefer to live in solitude. </p>
              <p> Adult monk seals are about seven feet (2.13 m) in length and weigh about 400  to 500 pounds (181 to 226 kg). Females are often times larger than males. They  feed on octopuses, eels, lobsters and reef fish. Hawaiian monk seals can dive  up to 600 feet (182 m) deep and can stay underwater for up to 20 minutes. </p>
