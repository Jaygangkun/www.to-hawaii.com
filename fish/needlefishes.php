              <h2 class="small">Needlefishes (Belonidae) Overview</h2>
              <p>Needlefishes have long and slim bodies with pointed, needlelike beaks with sharp teeth. They can be seen just below the surface and feed on small schooling fish, which they swallow whole. They are also able to leap out of the water and are strong swimmers. Juvenile needlefish can easily be mistaken for small twigs floating on the water's surface. </p>
              <p>At night they can be attracted by lights. When diving at night in an area where needlefish are common it is therefore recommended to leave the dive lights off until one is fully in the water and submerged. If one gets wounded by a needlefish the wound should be disinfected as small beak pieces in the wound can cause an infection. Large needlefishes can cause serious injuries.</p>
              <p>There are at least four needlefish species in Hawaii, which are known as 'aha, meaning &ldquo;cord.&rdquo;</p>
