              <h2 class="small">Goatfishes (Mullidae) Overview</h2>
              <p>Goatfishes can easily be spotted while snorkeling over a sandy ocean bottom. They can be recognized by their barbels, which they use to brush over the sand to find worms, molluscs and other invertebrates. Some of them also use their barbels to get to shrimps and crabs that are hiding in the reef. </p>
              <p>All goatfish have a forked tail and many can change their color very quickly. Most of the time they stick to the ocean bottom, but several Hawaii species can be observed in midwater during daylight hours. </p>
              <p>There is no Hawaiian name for goatfishes that includes all species. The ones that have one or more body-length stripes are known as weke (meaning &ldquo;to open&rdquo;) and weke that are under seven inches are called 'oama. Other goatfishes are known as moano.</p>
