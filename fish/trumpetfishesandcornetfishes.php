              <h2 class="small">Trumpetfishes and Cornetfishes (Aulostomidae, Fistulariidae) Overview</h2>
              <p>Trumpetfishes and cornetfishes are the most common fish predators on Hawaii's reefs. Trumpetfishes have stiff and long bodies and hardly bend as they swim. Cornetfishes on the other hand flex as they swim. </p>
              <p>Both species are daytime hunters, usually in the early morning or late afternoon hours. They follow small fish around and when close, they suck them into their tubelike mouths. </p>
              <p>Their mouths can expand quite a bit, so they are able to swallow fish that are as wide as their own bodies are. The Hawaiian name for both trumpetfishes and cornetfishes is nunu or nuhu.</p>
