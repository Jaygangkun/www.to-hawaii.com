              <h2 class="small">Remoras (Echeneidae) Overview</h2>
              <p>Remoras are also known as suckerfishes. With their sucking disk that's on the top of their heads they attach themselves to larger animals, such as turtles, whales, rays, dolphins and sharks. Other remoras have been found to attach themselves to other objects, including boats, divers and even other remoras. By doing this they conserve energy but don't harm the other animal. </p>
              <p>The Slender Remora can grow up to 3 feet (90 cm) in length. They are the largest remora species. Their Hawaiian name is omo.</p>
