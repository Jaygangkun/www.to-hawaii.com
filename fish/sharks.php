              <h2 class="small">Sharks (Carcharhinidae, Rhincodontidae, Sphyrnidae) Overview</h2>
              <p>Sharks are known to have a fantastic sense of smell. They can detect blood in the water from as far as a quarter mile away. They can also sense low frequency vibrations, for example of a wounded animal, a mile away. </p>
              <p>Their skins are rough like sandpaper and they generally have a full set of fins &ndash; caudal, anal, pectoral, pelvic and dorsal. Their mouths are on the underside and they have five to seven exposed gill slits on each side.</p>
              <p>Shark's teeth are constantly being replaced about once a month. New teeth grow on the inside edge of the jaw and then move outward, replacing the teeth that have done their job and are used up. All sharks are carnivores. </p>
              <p>There are 36 species of sharks in Hawaiian waters, but most of them live away from the shorelines and in deeper waters. The seven most likely to be seen near the shorelines are the gray reef shark, Galapagos shark, blacktip reef shark, whitetip reef shark, whale shark, hammerhead shark and tiger shark (responsible for most <a href="../sharks.php">Hawaii shark attacks</a>). The Hawaiian name for shark is mano.</p>
