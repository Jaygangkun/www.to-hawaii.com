              <h2 class="small">Blennies (Blenniidae) Overview</h2>
              <p>Blennies are small fish with long bodies (some almost like  eels) with large eyes and mouths. They usually hang out on the sea floor. Many  of them like to hide in the sand or in reef crevices. They can also be seen in  tide pools. </p>
              <p>There are more than 300 species in the blenny family, of  which 15 can be found in Hawaii. In the Hawaiian language, their name is pao'o.</p>
