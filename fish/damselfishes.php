              <h2 class="small">Damselfishes (Pomacentridae) Overview</h2>
              <p>Damselfishes are small (six inches or less) and usually  brightly-colored reef fish, usually occurring in abundance. They have a single  dorsal fin and a forked tail. In Hawaii,  they can easily be spotted swarming over coral heads. Damsels feed on plankton  or algae.</p>
              <p>There are about 345 species of damselfishes, of which 17  occur in Hawaii. Only the larger  species have Hawaiian names, probably because the smaller ones were not  considered food sources by the ancient Hawaiians. Common damselfish in Hawaii  include the Hawaiian Sergeant (mamo), Blackspot Sergeant (kupipi) and Hawaiian  Dascyllus ('alo'ilo'i).</p>
