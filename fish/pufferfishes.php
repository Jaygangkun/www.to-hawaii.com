              <h2 class="small">Pufferfishes (Tetraodontidae) Overview</h2>
              <p>Pufferfishes are closely related to <a href="../fish/porcupinefishes.php">porcupinefishes</a>. When threatened, they inhale water and expand into a balloon shape, which makes it difficult or impossible for a predator to swallow them. They differ from porcupinefishes in that their skin has bristles while porcupinefishes have sharp spines.</p>
              <p>Pufferfishes, like porcupinefishes, are also poisonous. Their bodies have tetrodotoxin in them, a powerful neurotoxin. The toxin is produced by bacteria within their bodies. </p>
              <p>In the Hawaiian language pufferfishes are known as makimaki.  There are twelve species of pufferfishes in Hawaiian waters, of which three are endemic. They range in size from a few inches up to 3 feet (90 cm) and have strong jaws, sharp beaks and scaleless bodies with a single dorsal fin. They feed mainly at night.</p>
