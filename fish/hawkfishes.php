              <h2 class="small">Hawkfishes (Cirrhitidae) Overview</h2>
              <p>Hawkfishes can usually be seen waiting motionless on an outcrop or coral head waiting for prey. As soon as a smaller fish or crustacean is seen, they strike quickly. They live in groups of usually one male and one or more females. </p>
              <p>Most species are able to change sex, males being sex-reversed females. If the male in a group dies, the dominant female of the group changes sex and takes on its new role as male group leader.</p>
              <p>There are six species of hawkfishes in Hawaii. Their Hawaiian name is piliko'a, meaning &ldquo;coral clinging.&rdquo;</p>
