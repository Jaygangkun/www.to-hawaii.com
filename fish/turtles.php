              <h2 class="small">Turtles (Testudines) Overview</h2>
              <p>The graceful sea turtle swims to the surface in intervals to take short breaths, then heads back down to explore. If you're lucky enough to catch sight of these large and enlightening creatures, you may feel a sense of wisdom in their presence, maybe because the green sea turtle can live as many as 80 years and weigh in excess of 300 lbs (136 kg). Four species of sea turtles inhabit Hawaiian waters: the Hawaiian green sea turtle, the hawksbill, the leatherback and the olive ridley. The <em>honu</em>, or green sea turtle, is the most common.</p>
