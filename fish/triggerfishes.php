              <h2 class="small">Triggerfishes (Balistidae) Overview</h2>
              <p>Hawaii's state fish, the wedgetail triggerfish (known as humuhumunukunukuapua'a in the Hawaiian language) is a common sight on Hawaii's reefs. Humuhumu means &ldquo;to stitch pieces together&rdquo; and nukunukuapua'a means &ldquo;nose like a pig.&rdquo; It is also commonly known as Picasso triggerfish. </p>
              <p>Triggerfishes are named for the uncommon arrangement of their first two dorsal spines. The first one is thick and strong and can be lifted up and locked into place by the shorter second spine, called the trigger. </p>
              <p>When a triggerfish feels threatened, it disappears into a hole or crevice and raises and locks this main dorsal spine. Like this it is difficult to extract the fish from its hiding place. They have small but strong jaws and mouths and a bite can be very painful. There are 10 species of triggerfish in Hawaiian waters.</p>
