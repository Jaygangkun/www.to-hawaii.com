               <p><strong class="subtitle">Fee Simple (FS): </strong>With a fee simple property, you own the land and  the structures on it. This is the typical form of ownership that you can find  everywhere. In Hawaii,  it is possible for foreigners to own land since the late 1800s. </p>
              <p>Advantage: You own the land that your home is built on and you don't have to  pay a monthly lease. </p>
              <p>Disadvantage: Higher purchase price. </p>
              <p><strong class="subtitle">Leasehold (LH): </strong>Buying a  leasehold property means that you own the improvements on the land, but not the  land itself. The ground leases are mostly  negotiated for a term of 55 years and when the time is up you (or your heir) will have to renegotiate with the property owners, who  are not obliged to extend or renegotiate the lease.</p>
              <p>Advantage: Initial purchase price mostly lower than with fee simple properties  and the lease can be a tax advantage.</p>
              <p>Disadvantage: Extra monthly costs through lease and insecurity about future of  lease after end of term. </p>
