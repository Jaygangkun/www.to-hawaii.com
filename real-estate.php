              <p><strong>What is Available to Buy?</strong></p>
              <p> Today, state, county and the federal government own 39 percent of all  Hawaiian land. About 37 percent of the land is up for private individual  ownership. </p>
              <p>Most of the islands offer a variety of Hawaii MLS options. These options range  in availability and cost from island to island. They include: single-family  homes, multi-family homes with an &quot;ohana dwelling,&quot; condos, vacant lots and  commercial properties and timeshare. Let's take a look at the most popular options:</p>
              <p><strong> <img border="0" src="images/familyhouse.jpg" width="148" height="125" align="right" alt="Single Family Home" /><span class="subtitle">Single-Family Homes:</span></strong> According to Hawaii mortgage brokers, single-family is  the most popular type of home ownership. Many new single-family homes  are being built in Hawaii. As the owner of a single-family home you are  responsible for all costs for the house and property.</p>
              <p> <strong> <img border="0" src="images/condo.jpg" width="148" height="125" align="left" alt="condo" /><span class="subtitle">Condominiums:</span></strong> Condos are gaining in popularity in Hawaii, especially since  single-family homes are expensive. With condos you own &quot;the paint in.&quot; They are  a group of separate homes or apartments that share common space and amenities  like parking lots, pools, BBQ areas, laundry facilities, recreational areas,  such as tennis courts, golf courses etc. Condos generally cost owners a  monthly maintenance fee that goes to the upkeep of the common areas. </p>
              <p> <strong><span class="subtitle">Timeshare:</span></strong> Timeshares have become popular in the U.S. over the  past thirty years. Basically, what happens is that you buy the ownership of a  piece of property (usually a resort-like accommodation with amenities) for a  certain period of time each year for a certain amount of years. Most timeshares  are set up so that you can trade time at different resorts with other timeshare  owners. They can also be bought and sold on the timeshare resale market to avoid the wholesale prices offered through the resorts. A timeshare may be the right option  if you are only looking to spend short periods of time in Hawaii every year. </p>
              <p><strong>What is an "Ohana Dwelling"?</strong></p>
              <p> An &quot;ohana dwelling&quot; is a cottage on the property of a main dwelling. Each  county in Hawaii  has its own rules and regulations concerning these units. Ohana dwellings come  in handy for homeowners when it's time to pay the mortgage as monthly rental prices for a  cottage can bring in an extra few hundred or thousand  dollars per month.</p>
              <p><strong>How much will it cost?</strong></p>
              <p>Real estate in Hawaii is expensive. According to the Honolulu Board of Realtors, the median sales price for a single-family home on  Oahu  was $790,000 in August 2019, a 2.5% decrease from the median price in August 2018 ($810,000). The median price of an Oahu condo was  $419,500 in August 2019, a 1.8% decrease from August 2018 ($427,000). </p>
              <p> The following is an estimate of what type of properties you can buy for a  certain dollar amount based on information from the Multiple Listing Service.</p>
              <p> <strong class="subtitle">Less than $200,000:</strong> There are very few homes in Hawaii that fall into this price range. Those  that do generally provide under 1,000 square feet of living space and are often  leasehold properties.</p>
              <p> <strong class="subtitle">$200,000- $500,000: </strong>This price range offers a wide-array of homes  that range from 600 square feet of living space up to about 1,300 square feet. The majority of condos in Hawaii also fall into  this price range.</p>
              <p> <strong class="subtitle">$500,000- $700,000: </strong>The median sales price for a single-family home in Hawaii falls into this price range. Homes are bigger and often newer in this  price range, although the size and condition of the properties varies greatly.  There are properties that offer less than 700 square feet of living space as  well as those that offer over 2,000 square feet in this category.</p>
