              <p>The mantra in real estate has always been &quot;location, location, location.&quot; Hawaii boasts some  serious location. All of the inhabitable islands of Hawaii have a great <a href="climate.php">climate</a>, great views and  beach access. However, a great location has its price. Hawaii has one of the most expensive housing markets in the nation. </p>
            
              <p><strong>Types of Ownership</strong></p>
              <p>There are two types of property ownership in Hawaii &ndash; Fee Simple (FS) and Leasehold (LH).  Before you buy a property in Hawaii,  you should know what the difference between FS and LH is. On this page, we  provide you with some useful information about the two types of ownership. We  also give you some tips, where to look for your future Hawaii home. </p>
              
