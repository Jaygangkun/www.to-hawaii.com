              <p><strong>Horseback riding tours on Kauai</strong></p>
              <p>Kauai's beauty is endless. There are numerous ways of exploring it. And riding a horse is just one of them. You'll ride off the road and into the green to explore lush tropical rainforests, cascading waterfalls and hills. You can find great horseback riding tours in <a href="kauai/cities/princeville.php">Princeville</a> on the north shore. The natural scenery there is breathtaking and you'll enjoy both mountain and ocean views. Some of the highlights in the area are Anini Reef, which is the largest 
                reef in Hawaii and <a href="kauai/attractions/kilauea-lighthouse.php">Kilauea Lighthouse</a>, a bird sanctuary. </p>
                             <p><strong>Horseback riding tours on the Big Island</strong></p>
              <p>On the Big Island, the best horseback riding locations are in northern part of the island. <a href="big-island/attractions/waipio-valley.php">Waipio Valley</a> is the most popular and favorite horseback-riding site. The views are truly stunning - lush tropical valleys, cascading waterfalls, cliff edges and taro patches. About 72 miles (115 km) from Waipio Valley is
                Kona. There are a few horseback riding companies that can take you for a horseback adventure to <a href="big-island/beaches/kealakekua-bay.php">Kealakekua Bay</a>. </p>
