              <h3>Horseback riding tours on Oahu</h3>
              <p>On Oahu, one of the most spectacular horseback riding sites is <a href="oahu/attractions/kualoaranch.php">Kualoa Ranch</a>, 
                located on the windward coast. This is a place where besides beautiful ocean and valley views, you will come across famous movie locations you have seen in <em>Jurassic Park</em>, <em>Godzilla</em>, <em>Windtalkers</em> and <em>50 First Dates</em>.</p>
              <p>Other companies will take you for a horseback adventure on the North Shore of Oahu. Some of the highlights here include views of <a href="oahu/beaches/kaenapoint.php">Ka'ena Point </a>(northwestern tip of Oahu) and <a href="oahu/beaches/turtlebaybeach.php">Turtle Bay</a>.</p>
              <p><strong>Horseback riding tours on Maui</strong></p>
              <p>Maui's most spectacular horseback riding location is <a href="maui/attractions/haleakala-national-park.php"> Mount Haleakala</a>. There are many companies on the island that will be happy to take you for a horseback ride there. Haleakala is one of the largest dormant volcanoes on Earth and  a must-see if you are on Maui. While riding on the foothills of Haleakala, you will see amazing flora you can't see anywhere else in the world, such as the endemic <a href="flowers/silversword.php">silversword</a> flower. Besides the unique views, your guide will tell you about the island's history, flora, fauna and the geology of the volcano.</p>
              <p>Another remarkable horseback riding location on Maui is <a href="maui/cities/kipahulu.php">Kipahulu</a> - an ancient 
                land filled with hidden stories and historical events. You will see legendary sites and you will have the chance to learn about the culture and the history of the ancient Hawaiians.</p>
