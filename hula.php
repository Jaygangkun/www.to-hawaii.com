?> <p>Men and women were participants in the hula as they are today, but the chanters, or singers, were and are male. The chant, or mele, recounts the story, while the hula or dance acts out or emphasizes parts of the story with choreographed hand movements and repetitive footwork. The hula that men dance is more vigorous than the languid dances the women perform.</p>
              <p>The hula performance is accompanied by instruments. The music helps to keep the graceful rhythm for the dancers as well as the chanters. Traditionally, gourds, bamboo pipes, sticks and rattles were used to create music. A single gourd drum called an ipu was later followed by a double-gourd drum called an ipu heke. The anklets of dogteeth worn around the legs were also considered to be an instrument of music. In the modern era, the ukulele is used to keep rhythm, and today, guitars and a bass may also be used to add depth to the sound and enhance the rhythm.</p>
              <p><strong>The Hula's Denunciation and Rebirth</strong></p>
              <p>With the arrival of the missionaries in the early 1800s, the role of the hula started to change. The Protestant missionaries denounced the hula as pagan, and soon its performance was banned. The missionaries decried the gyrations, the scantily clad dancers and the worship to multiple gods. The
                Hawaiian royalty were encouraged to denounce the hula also, which they soon did. But 
                the hula did not die. It was danced in secret and quietly passed down through families.</p>
                               <? include ("theme/text-2.html") ?>
              <p>With the advent of King David Kalakaua, the hula was resurrected and slowly began to take on a change. King Kalakaua was a great proponent of all of the traditional Hawaiian arts and crafts. The hula that soon evolved was called Hula Kui, or old and new, because it combined the ancient traditional style with newer interpretations of older dances.</p>
              <p>With the advent of tourism as a business to Hawaii in the early twentieth century, the hula again underwent a change. Hula was depicted in films, as graceful swaying to soft melodic music so very unlike the real hula. Songs were written for this newest kind of hula, and many beautiful and fanciful costumes were created for the tourist trade. Most popular were cellophane skirts and sweet romantic songs were sung for the tourists' benefit. Halau Hulas were still teaching the more traditional style, which was practiced by older members of families. Traditional hula garb is more modest and the Hollywood version of the hula and the costumes does not depict the true hula. </p>
              <p><strong>The Hula Today</strong></p>
              <p>Today there are two major hula events held every year. One is called the Merrie Monarch Festival, where hula teams compete; male and female teams compete for prizes. Also, a Miss Aloha Hula is crowned every year. The other is the King Kamehameha Traditional Hula Event and Chant Competition, which is held every June.</p>
              <p>No matter how many times the hula has undergone change, it remains an important part of local culture and can be enjoyed as a participant or viewer.</p>
