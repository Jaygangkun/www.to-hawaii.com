              <h2>The Hula - Hawaii's Indigenous Dance</h2>
              <p>Hawaiians as a culture and as a people like to talk story. Kapunas, or chiefs, told stories to their peoples as a way of teaching about life and custom. These stories were part of the Hawaiian oral tradition. The hula, an indigenous dance, is a means of telling a story. The dance is accompanied by a chant, or mele. There are two very distinct styles of hula; the style created by the original Polynesian inhabitants, called Kahiko Hula, and the newer style, which was created in the 19th and 20th centuries after western 
                influence, which is called the Auana style.</p>
                              <? include ("theme/text.html") ?>
              <p>The Polynesians came to the Hawaiian Islands from the Southeast Pacific by handcrafted canoes. They settled on the lush fertile islands and worshipped their gods by dancing the hula. Laka is the patron goddess of the hula, and hula was danced for the benefit of another goddess, known as Pele. For many generations, those training to dance the hula underwent a very strict and rigorous course of study. </p>
              <p>The school for hula, or Halau Hula, enforced student rules, such as no haircuts and no fingernail cutting. Graduating students underwent purification rituals and were cleansed in the seawater. Afterwards, there was a large luau, or feast, to celebrate their accomplishment. All family members were invited to celebrate along with the hula celebrants. Today Halau Hula still exists and serves to continue the traditional dance heritage. And as it has always been, graduation is celebrated with food and dance.</p>
              <h3>The Hula's Origins</h3>
              <p>The hula began as a way to worship the gods. In olden days, the hula was danced on a platform, which had an altar to the goddess Pele on it. The traditional costumes were comprised of leis (flower necklaces), a pau (skirt) and ankle bracelets of whalebone or dogteeth. Today costumes are more modest and Halau Hula schools require long skirts and a top, or graceful muumuu for women, while men can choose between pants and a malo, a wrapped cloth.</p> <?php
