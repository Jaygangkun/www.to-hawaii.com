<p><em>The Haunted Hospital of Hilo.</em> In an old hospital in Kaumana, Hilo, the hallways sometimes resound with distant, surreal cries. Years ago, a fire razed the hospital nursery and burned the babies to death, along with their nurses. The hospital still stands, but so do the restless souls of the poor victims who haunt it to this day.</p>
<p><em>The Eerie Dorm. </em>The residence hall at the University of Hawaii at Hilo sits at the site of an old house. Much of the house has been rebuilt into the modern-day dorm, but its long-dead residents are keen on maintaining their presence. Today, students report hearing sounds at night, switches turning on by themselves and feeling eyes on them as they pass the halls.</p>
