            <h1>Haunted Places on Oahu</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
<p><strong>Aiea</strong></p>
<p><em>Aiea High. </em>Aiea High School is located on an old burial ground and like many of its kind, it is home to many strange sightings and unusual noises. The R building is said to be particularly surreal.</p>
<p><strong>Hauula</strong></p>
<p><em>Hauula, Pounder's Beach.</em> On the Hauula side of Pounder's Beach, one can sometimes hear a child's lost cry or see a woman wandering the waters. These are the ghosts of a mother and her child, who drowned on a rough night on the beach.</p>
<p><strong>Honolulu </strong></p>
<p><em>The Dole Cannery Signature Theaters. </em>Like many Hawaiian buildings, this theater is built on an ancient temple that met a tragic end. Some decades ago, a bus full of children crashed into the temple, killing most of its passengers. Today, people still hear children's voices in the theater, particularly in the bathrooms.</p>
<p><em>Highway 1 (H1).</em> This highway cuts through the Koolau Mountains, where many ancient warriors met their deaths. While the tunnel was being constructed, the diggers found old bones and weapons and many passers have reported ghostly sightings in the tunnel itself.</p>
<p><em>Hilton Hawaiian Village.</em> Staff and visitors have reported seeing a woman in a red dress wandering the hallways of this resort hotel. Believed to be the volcano goddess Pele, she appears throughout the premises and seems to show and hide herself as she pleases.</p>
<? include ("../theme/text.html") ?>
<p><em>Iolani Palace. </em>Guards have repeatedly claimed to hear footsteps and see strange shadows at this historic landmark. </p>
<p><em>Kakaako Fire Station.</em> An old man with a dog, probably an ancient firefighter, is said to haunt this fire station on South Street.</p>
<p>Kapiolani Park. Hundreds of years ago, this park was a battlefield and a site for human sacrifices. As you might expect, it's teeming with unexplained sights, sounds and smells. The Honolulu police have gotten used to calls from travelers reporting strange happenings in this area.</p>
<p><em>Kamehameha Schools. </em>The Night Marchers are a group of ancient warriors who wreak havoc on anyone or anything that stands in their way, and the school is located right in their path. When the moon is full, one can allegedly hear drum beats and knocks on the doors.</p>
<p><em>Kamehameha Schools Dormitories.</em> A ghostly lady dressed in 19th century garb is said to haunt the boys' rooms in this dormitory. She had been haunting the building for a while before residents recognized her from one of the paintings as Princess Bernice Pauahi Bishop, who was responsible for the school's establishment.</p>
<p><em>Manoa.</em> On the way to the Manoa Falls, just past Paradise Park, one can hear strange war cries seemingly muffled by the rainforest beyond it.</p>
<p><em>Nuuanu. </em>Just across the street from the cemetery is a haunted condominium, where the ghost of an old Chinese woman has been seen wandering the halls.</p>
<p><em>The Pali Lookout.</em> This place brings an eerie feeling even from the staunchest of skeptics. The Pali Lookout is the site of King Kamehameha's battle to unify the Hawaiian islands, where hundreds of warriors died. Their lost souls have been seen wandering around this area at night.</p>
<p><em>Pearl Harbor. </em>Pearl Harbor hardly needs explanation as to why it's haunted. Ford Island, located at the center of the harbor, is home to many strange sightings and footsteps.</p>
<p><strong>Laie </strong></p>
<p><em>Polynesian Cultural Center. </em>In the Fiji village of the Marquesas region, you can sometimes hear drums playing or babies crying in the distance.</p>
<p><strong>Makaha </strong></p>
<p><em>Makaha Sheraton Golf Club. </em>Several children drowned in a pool accident at the club some years ago. Their presence can still be felt around the premises, and some people claim to have seen them playing around the pool. </p>
 <? include ("../theme/text-2.html") ?>
<p><strong>North Shore</strong></p>
<p><em>Waimea Falls Park.</em> At the center of this park is a small, shallow pond said to be haunted by a spirit. Once in a while, someone mysteriously drowns in the pond and although there's not much space in the pond, the body doesn't turn up until a few days later.</p>
<p><em>Puu o Mahuka Heiau.</em> The largest heiau (Hawaiian temple) on the island, Puu o Mahuka also lies in the path of the Night Marchers and has seen its share of spooky occurances. Even today, people still hear ancient conch shells and the sound of the marchers' footsteps.</p>
<p><strong>Wahiawa </strong></p>
<p><em>Wahiawa Elementary School.</em> The Green Lady is a strange sighting in the wooded area surrounding this town. First seen in the 1940s, the woman is said to have scaly green skin and has made a few reappearances in recent years.</p>
<p><em>Sacred Birthstones.</em> A small site just outside Wahiawa used to be the birthing place for Hawaiian royalty. Marked by a group of round stones, the site brims with positive energy that is felt by every traveler. The stones always go back to their place by themselves when you move them, although no one knows why.</p>
<p><strong>Waialae </strong></p>
<p><em>Chaminade University.</em> Perhaps the most haunted school on the island, Chaminade was once the site of a children's hospital that lived through World War II. In Room 208, the outline of a crucifix can be seen on one wall, although the cross itself has been removed years ago. Room 319 was a former soldiers' morgue and the ghosts of the warriors can sometimes be seen inside.</p>
<p><strong>Waipahu</strong></p>
<p><em>Waipahu Street.</em> When King Kamehameha I invaded Oahu, he took so many lives that the dead had to be lined up from Waipahu gulch to  Mililani valley, a distance spanning several miles. The Night Marchers can sometimes be heard on this street, and the sound of drums in the distance is a common event.</p>

