<p>Effective November 8, 2021, the state of Hawaii will align with   federal international requirements and welcome all vaccinated   international travelers. Non-U.S. citizens traveling directly to Hawaii   must present BOTH a vaccination record AND a negative COVID-19 test   result (NAAT or antigen) within three days of boarding a flight to the   U.S.<br />
  <br />
  U.S. citizens flying directly to Hawaii from an   international destination have two options: (1) fully vaccinated U.S.   citizens must provide proof of vaccination AND a negative COVID-19 test   result within 72 hours of boarding a flight to Hawaii OR (2) if not   vaccinated, a negative COVID-19 test result within one day of boarding a   flight to Hawaii.<br />
  <br />
  The <a target="_blank" href="https://travel.hawaii.gov/#/">Hawaii Safe Travels Program</a>, located at <a target="_blank" href="https://travel.hawaii.gov/#/">https://travel.hawaii.gov/#/</a> remains in place for domestic travelers. To avoid the mandatory 10-day   self-quarantine, domestic travelers must upload proof of COVID-19   vaccination OR have a negative COVID-19 result from a trusted testing   partner, taken within 72 hours. All vaccines approved or authorized by   the U.S. Food and Drug Administration and vaccines listed for emergency   use by the World Health Organization will be accepted. <br />
  <br />
  Travelers   not participating in the Hawaii Safe Travels Program must quarantine   for 10 days or the length of their stay, whichever is shorter. From   November 8, 2021, this will apply only to passengers arriving from a   U.S. state or territory.Â </p>
<p> All incoming Hawaii  travelers will receive temperature checks upon   arrival. Temperature scans will  be taken via thermal temperature   screening. Photos will be temporarily retained  only for persons with   elevated temperatures of 100.4 degrees F and above for  identification   and additional assessments by airport representatives. Face  masks are   required in all Hawaii  airports and when entering places of business   (allowances are given for young  children and those with health   conditions). More information is available on <a href="https://hawaiicovid19.com/travel/travel-overview/" target="_blank">https://hawaiicovid19.com/travel/travel-overview/</a><br />
</p>
<p><strong>Trusted Testing and Travel  Partners<br />
  <br />
  </strong>The state of Hawaii  will ONLY accept Nucleic Acid Amplification Test   (NAAT) from a certified  Clinical Laboratory Improvement Amendment   (CLIA) lab test results from TRUSTED  TESTING AND TRAVEL PARTNERS. Detailed information is available on <a href="https://hawaiicovid19.com/travel-partners/" target="_blank">https://hawaiicovid19.com/travel-partners/</a><strong><br />
