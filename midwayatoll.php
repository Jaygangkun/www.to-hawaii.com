              <p>Midway Atoll became even more popular after the Battle of Midway, fought in World War II, from June 4 to 6 in 1942, when a small U.S. task force surprised and defeated the Japanese invasion fleet that was bound for the atoll. Midway Atoll was designated as the National Memorial to the Battle of Midway in 2000.</p>
              <p>Over the years, Midway Atoll has been altered extensively by humans. The atoll has 20 miles (32 km) of roads, 4.8 miles (7.8 km) of pipelines, a port on Sand Island and a runway, about 8,000 feet (2400 m) long.</p>
              <? include ("theme/text-desktop-only.html") ?>
              <p>Almost two million birds of 19 species nest on Midway Atoll. Species include Laysan albatrosses (the atoll is home to the largest Laysan albatross colony in the world), black-footed albatrosses, white terns, red-tailed tropicbirds, shearwaters, black and brown noddies and Bonin petrels.</p>
              <p>Seventy-five percent of Midway's plant species were introductions, including exotic vegetables, weeds, ornamental shrubs, coconut palms and ironwood. The marine life is abundant. More than 250 fish species can be found here, including hapuupuu (the Hawaiian grouper), kumu (goatfish), ulua (jack) and sharks. There are also many dolphins, turtles and monk seals. Moreover, 16 stony coral species have been reported.</p>
