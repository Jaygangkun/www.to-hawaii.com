              <p>As its name implies, Midway Atoll lies about half way between North America and Asia, like a stepping stone mid-way. Midway Atoll is also known as Midway Island or Pihemanu 
                in the Hawaiian language. Encompassing 2.4 square miles (6.2 km&sup2;), the circular-shaped atoll consists of a ring-shaped barrier reef and a few sand islets. The three main ones are Sand Island, Eastern Island and Spit Island, located on the southern end 
                of a lagoon and home to almost two million birds.</p>
                <? include ("theme/text.html") ?>
              <p>Midway is probably the best known island in the Northwestern Hawaiian Island chain. While it is part of the Hawaiian archipelago, Midway is legally not part of the state of Hawaii. The Midway Islands are administered by the U.S. Fish and Wildlife Service. The atoll has a small population of researchers and administrators (less than 100) and there is a visitor's program, which allows small groups of visitors to learn about and experience Midway's unique ecosystems.</p>
              <p>In 1859, Captain Brooks sailed to Midway Atoll and after that, the U.S. annexed it. The atoll was the country's first insular possession outside the continental U.S. In 1870, the U.S. recognized the strategic importance of Midway and began clearing a southern channel into the lagoon to create a port on Sand Island. Midway was later (in 1903) an important link in cable communications and a transpacific Panamerican seaplane stop (in 1935). In 1940, the work to build a military facility on the atoll began. </p> <?php
