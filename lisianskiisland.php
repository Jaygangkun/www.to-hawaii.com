              <p>Situated 1,041 miles (1,676 km) northwest of Honolulu, Lisianski Island (Papa'&#257;poho) has a maximum elevation of 40 feet (12 m) above sea level. The highest peak is a 
                sand dune. Encompassing 0.601 square miles (1.556 km&sup2;), Lisianski is a small island, but it has an extensive reef to the southeast called Neva Shoals. It covers 378 square miles (979 km&sup2;), which is almost the size of the island of Oahu. Lisianski is one of the Northwestern Hawaiian Islands.</p>
              <p>Lisianski Island is volcanic and undergoing a slow erosive process. A depression is located between two tall sand dunes. It is believed that it once was a lagoon. Because of that, the island is known as Papa'&#257;poho in Hawaiian. The name Lisianski comes from an officer in the Imperial Russian Navy, Yuri Feodorovich Lisyansky, whose ship <em>Neva</em> ran aground on the island in 1805 after spotting a Hawaiian gourd calabash on the beach.</p>
