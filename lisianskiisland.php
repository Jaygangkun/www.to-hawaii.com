              <p>In 1890, the North Pacific Phosphate and Fertilizer Company obtained a 26-year lease on the island. Feather collecting began on Lisianski around the year 1904. But because of the public outcry about the feather trade, Lisianski became part of the Hawaiian Islands Bird Reservation in 1909, which was established by U.S. President Roosevelt. A year later, an armed party came to Lisianski and arrested feather poachers. They confiscated and destroyed about 1.4 tons of feathers.</p>
              <p>Today, over three-quarters of the bonin petrels that nest in Hawaii live here. Also, Hawaiian green sea turtles and monk seals frequent Lisianski's white-sand beaches. Migratory shorebirds that come here include the ulili (wandering tattler), kolea (golden plover) and kioea (bristle-thighed curlew). More than a million sooty terns fly to Lisianski in some years.</p>
              <? include ("theme/text-desktop-only.html") ?>
              <p>The Neva Shoals surrounding Lisianski Island has been described as a "coral garden" because of the great variety of corals found here (24 types were identified). They look very beautiful and feature a variety of growth forms, including structures that look like castles and spires. The reef fish that live here are abundant and diverse as well. Researchers have found the sharks that live here to be very aggressive, and even the ulua (trevally jacks) have been found to harass divers and small boats. Algae are also plentiful here.</p>
