              <p>Mice were introduced to Lisianski in 1844, when ship picking up survivors of a shipwreck brought the animals with it. Rabbits were introduced later and along with the mice, they destroyed the island's ecology. Lisianski was claimed by King Kamehameha IV for the Hawaiian Kingdom in 1857. </p> <?php
