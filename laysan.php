?> <p>Schlemmer continued to allow the Japanese to export illegal bird wings and because of that was later removed from Laysan. Because the many rabbits had eaten so much of the island's vegetation, much of the soil became loose and blew around in dust storms. By 1918, the island could sustain no more than 100 rabbits and 26 plant species had been eliminated. Also, the Laysan Millerbird had become extinct.</p>
          
              <p>The Tanager Expedition, which arrived in 1923, exterminated the remaining rabbits. By then, the bird population had been reduced to about one tenth of its former size and many plant species had been erased. The Laysan Duck and the Laysan Finch, two other endemic species, survive to this day but are endangered.</p>
              <p>Although Laysan Island experienced so much abuse and an extinction period in its past, it has the most biodiversity and is considered the "gem" of the Northwestern Hawaiian Islands. Besides being home to the Laysan duck and the Laysan finch, 18 other bird species nest here. Native plants which had been eradicated during the extinction period, 
                such as the eragrostis variabilis, were later reintroduced from other neighboring islands by scientists. Other seabirds on Laysan include the black-footed and Laysan albatross, Christmas and wedge-tailed shearwaters and bristle-thighed curlews. Like most other Northwestern Hawaiian Islands, many Hawaiian monk seals and green sea turtles live here.</p>
                <? include ("theme/text-2.html") ?>
              <p>Laysan Island has beautiful white-sand beaches and dry coastal grasses. Much of Laysan's shoreline is composed of old coral reef and coral sand, which as time went by has become cemented together to form rock. Although the reef at Laysan is the smallest of all the Northwestern Hawaiian Islands (145,334 acres / 588,146 km&sup2;), the island's rocky intertidal area is home to numerous invertebrate species, algae and fish. Furthermore, 28 species of stony coral can be found here, and branching corals are common. The island is protected by the Hawaiian Natural Life Act of 1961.</p>
            
              <p>Like most of the other Northwestern Hawaiian Islands, Laysan is uninhabited. After years of restoration efforts by the U.S. Fish and Wildlife Service, pests, rats, rabbits and weeds have been eliminated and the some imperiled species and the native vegetation has been widely restored. Today, many consider Laysan one of the "crown jewels" of the Northwestern Hawaiian Islands. However, some perils 
                continue to threaten this vulnerable island. </p>
              <p>Even though Laysan is a remote place, all sorts of garbage that is thrown off of ships gets washed ashore. This poses a threat to the birds because they swallow the smaller pieces, which remain undigested in their stomachs. Also, a container with poisonous carbofuran had floated to shore and burst open in the 1990s, creating a "dead zone," which killed any living animal that went near it. This area is still off-limits.</p>
