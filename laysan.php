              <p>Laysan's history has been troubled because in the late 1800s and early 1900s, guano and feather harvesters killed thousands of birds, which caused dramatic changes in the island's ecosystem. Also, rabbits were introduced to the island, but soon the multiplied and the vegetation on Laysan wasn't enough to feed them. According to Professor William Alanson Byron of the Bishop Museum, there were about 10 million seabirds on Laysan in 1903, but just eight years later there were just a little more than a million. In these eight years, also the Pritchardia palms that were endemic to Laysan and the island's sandalwood trees became extinct. </p>
              <p>Another of the island's inhabitants, a German immigrant named Max Schlemmer, who arrived in 1894, brought European Hares and guinea pigs to Laysan to prepare for a future meat canning business. All these events led to the rapid decline of the ecosystem on Laysan. These events caused a public outcry, which led President Theodore Roosevelt to declare the Northwestern Hawaiian Island chain a bird sanctuary in 1909.</p> <?php
