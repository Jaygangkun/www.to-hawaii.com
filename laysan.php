              <p>The Hawaiian name of Laysan Island is  Kau&#333;, meaning egg, which refers to the island's shape and how much life springs from the island. It is located 930 miles (1,496 km) northwest of Honolulu. Its land area encompasses 1,588 square miles (4,114 km&sup2;). At about 1 mile in width and 1.5 miles in length, it is the second largest land mass in the Northwestern Hawaiian 
                Islands. Laysan Island has fringing reefs and a shallow and very salty 100-acre lake in its interior, about eight feet (2.4 m) above sea level. Its salinity content is about three times more than the ocean. It is the only lake in the Northwestern Hawaiian Island chain and one of only five natural lakes in the entire state of Hawaii. Laysan's tallest point is about 40 feet (12 m) above sea level, located on a large dune on the northern 
                point of the island.</p>
