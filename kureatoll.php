              <p>A U.S. Coast Guard LORAN station was located on Green Island and a short coral runway still remains on the island. However, it is not being used or maintained any longer.</p>
              <p>Even though Kure Atoll lies far to the north of the Main Hawaiian Islands and in relatively cool waters, there is still a lot of coral growth. In fact, 28 coral species have been documented. Large invertebrates such as crustaceans and mollusks can be found here as well. The nearshore reefs are also home to sharks, dolphins, jacks, monk seals, goatfish, chub, dragon morays, masked angelfish, knifejaws and the rare native grouper. </p>
              <? include ("theme/text-desktop-only.html") ?>
              <p>A constant threat to Kure Atoll is marine debris, since the atoll lies in the path of a major Pacific current. This brings tons of debris to the atoll, washing up on the reefs and beaches posing the threat of entanglement for turtles, monk seals, fish, lobsters and birds. Debris includes fishing nets and cigarette lighters, which 
                are frequently swallowed by the birds, whose skeletons are often found with plastic in their stomachs. In 2003, 2,700 pounds of debris was removed from the atoll.</p>
