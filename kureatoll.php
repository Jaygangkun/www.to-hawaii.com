              <p>Kure Atoll is the northwesternmost of the Northwestern Hawaiian Islands and also the northernmost coral atoll in the world. Also known as K&#257;nemiloha'i in the Hawaiian language, the oval-shaped atoll measures 6 miles (10 km) in diameter and features a circular barrier reef and a shallow lagoon in its center with a few sand islets. 
                To the neighboring Midway Atoll it is about 55 miles (89 km). Green Island is the atoll's only significant land area and is home to thousands of seabirds, including petrels, tropicbirds, boobies, shearwaters, frigatebirds, terns, albatrosses and noddies. The atoll's highest point is 20 feet (6 m) above sea level.</p>
              <? include ("theme/text.html") ?>
              <p> Before 1827, about half a dozen ships visited Kure Atoll and each time gave the atoll a different name. Starting in 1837, several crews were shipwrecked on the atoll after their ships ran aground on the reefs surrounding the atoll. They were stranded there for sometimes up to nine months, eating turtles, monk seals and birds to survive while constructing a smaller craft to make the way back to the Main Hawaiian Islands. Up until today, the shipwrecks remain on the atoll's reefs, such as some of Hawaii's oldest known shipwrecks, the <em>USS Saginaw</em> (1870) and the whaleship <em>Parker</em> (1842). </p>
              <p>After the shipwreck incidents, King Kalakaua sent Colonel James Boyd as his Special Commissioner to Kure and in 1886, he took possession of the atoll. In 1894, Kure Atoll was leased for guano mining; however, no mining was ever done here. The U.S. acquired Kure Atoll as part of the Territory of Hawaii in 1989. Twenty years later, in 1909, President Theodore Roosevelt made Kure part of the Hawaiian Islands Bird 
                Reservation. </p> <?php
