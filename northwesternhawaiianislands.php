?> <p>In the late 1800s to the early 1900s, the Northwestern Hawaiian Islands experienced abuse when fishermen, feather hunters and guano miners killed most of the birds and sea life there. Also, rabbits were brought to Laysan and Lisianski where they multiplied and consumed all the vegetation, extinguishing several species forever. But luckily, the islands were later restored and are now almost in the same condition as they were before their exploitation.</p>
<p>Today's endemic species in the Northwestern Hawaiian Islands include the Laysan duck, the Laysan albatross, the Nihoa and Laysan finch, the Hawaiian monk seal and the Hawaiian green sea turtle. Seventy percent of all coral reefs in the United States are located here. These coral reefs are some of the healthiest and most undisturbed reefs in the world. More than 7,000 species, including fish, turtles, birds, marine mammals and invertebrates can be found here (about a quarter are endemic).</p>
<p>President George W. Bush issued a proclamation on June 15, 2006 that created the Papahanaumokuakea Marine National Monument under the Antiquities Act of 1906. The monument encompasses the Northwestern Hawaiian Islands and surrounding waters and is the largest marine wildlife reserve in the world. It is about the size of the U.S. state of California, 139,000 square miles (360,000 km&sup2;).</p>
