               <h2>The Northwestern Hawaiian Islands - Leeward Islands</h2>
<p>When most people think of Hawaii, they think about Honolulu, Waikiki Beach and Pearl Harbor (which are all on the island of Oahu); others are maybe even familiar with the neighbor islands of Kauai, Maui, Lanai, Molokai or the Big Island, the lesser visited Hawaii tourism destinations. But who has ever heard of Nihoa, Laysan, Necker, Lisianski, French Frigate Shoals, Kure Atoll or the Gardner Pinnacles? These and many other small islands, reefs, shoals and atolls are called the Northwestern Hawaiian Islands, or the Leeward Islands. </p>
<p>Spanning more than 1,200 miles (1,931 km) in the Pacific Ocean, all of them are uninhabited, little known and rarely visited (except for a few researchers who do their work there). If you were to lay these islands atop the continental United States, they would cover a distance from Boston, Massachusetts, to the Florida Everglades. The Northwestern Hawaiian Islands encompass a land area of 3,100 square miles (8.030 km&sup2;).</p>
<p>The largest of the islands are: Nihoa, Necker (Mokumanamana), French Frigate Shoals (Mokupapapa), Gardner Pinnacles (Puhahonu), Maro Reef (Nalukakala), Laysan (Kauo), Lisianski (Papaapoho), Pearl and Hermes Atoll (Holoikauaua), Midway (Pihemanu) and Kure (Kanemilohai). The Midway Islands are part of the Hawaiian archipelago, but are not legally part of the state of Hawaii. While most of the abovementioned islands are administered by the state of Hawaii, Midway is administered by the U.S. Fish and Wildlife Service and has temporary residential facilities. </p>
<? include ("theme/text.html") ?>
<p>All of the Northwestern Hawaiian Islands were formed over the same volcanic hotspot that formed the eight Main Hawaiian Islands (Kauai, Niihau, Oahu, Maui, Molokai, Lanai, Kahoolawe and the Big Island) to the south. As time went by, the islands gradually eroded and subsided, evolving from high islands (in the south) to atolls or seamounts (in the north). Each one of the islands is in a different stage of erosion. Because the northwesternmost islands are the oldest, they have eroded the most. Necker, Nihoa and the Gardner Pinnacles are rocky basalt islands that haven't eroded enough to form an atoll, while Laysan and Lisianski are low, sandy islands that have eroded longer. The French Frigate Shoals, Pearl and Hermes, Midway and 
  Kure are all atolls. As the Pacific Plate moves northwest, the islands become seamounts and will eventually sink beneath the ocean. Kure Atoll will be the first. </p>
<p>Because of the isolation of the Northwestern Hawaiian Islands (they are about 2,500 miles from North America and 3,800 miles from Asia), they are home to a large number of endemic or unique species. While the ecosystem of the Main Hawaiian Islands has changed over the years (mainly because humans introduced foreign species), the ecosystem in the Northwestern Hawaiian Islands remains largely intact. More than 1,700 species in the Northwestern Hawaiian Islands are endemic, which means that they can't be found anywhere else in the world. That's why this region has also 
been called "America's Galapagos."</p> <?php
