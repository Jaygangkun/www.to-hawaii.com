<p>A strict permit system is in place that must be followed by anyone who wishes to come here. This is to prevent any stray species from entering and disrupting the ecosystem. The permit system is administered by the state of Hawaii, NOAA and FWS. All clothes must be new and one must keep them wrapped just before arrival. "Soft" items like blankets or camera straps must also be new, and all "hard" items like cameras or binoculars must be thoroughly cleaned and frozen for 48 hours. To prevent inter-island species introduction, one must prepare a new set of equipment for each island one is going to. This rule applies for all islands, except for the French Frigate Shoals and Midway Atoll because they are already too altered by humans.</p>
