<h2 class="small">Sugarcane Overview</h2>
<p>Sugarcane is a large grass and the source of sugar. When the early Polynesians traveled to Hawaii in their canoes, they brought sugarcane with them. Their general name for this plant was &ldquo;ko,&rdquo; but they had names for 40 different varieties. </p>
<p>The first commercial sugarcane plantations in Hawaii were started in 1835 on the island of Kauai. By the end of the 19th century, sugar was &ldquo;king&rdquo; in Hawaii. A century later, the sugar industry had fallen due to increasing land and labor costs.</p>
<p>Sugarcane is grown for two years and then harvested just before it blooms. The fields are first burned and then cut down. The burned stems are crushed in a sugar mill and the juice is then cooked.</p>
