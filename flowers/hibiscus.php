                      <h2 class="small">Hibiscus Overview</h2>
                      <p>In 1923, the Territory of Hawaii passed a law that made Hibiscus Hawaii's official state flower. This native hibiscus species is known as <em>ma'o hau hele</em> in the Hawaiian language. Its scientific name is Hibiscus brackenridgei. There are 5 endemic Hibiscus species in Hawaii and over 30 introduced ones. The ones that have been introduced have been crossed with Hawaiian species, producing a large variety of this species. </p>
                      <p>Hibiscus is native to southern China. In China, the flower is named Fu Sang. In India, it is called Sapattnu-mal. The juices from the flower are used as food coloring. The flower can grow three to 20 feet (6 m) in height. The petals grow 2-8 inches (5-20 cm) across.</p>
