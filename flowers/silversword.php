<h2 class="small">Silversword Overview</h2>
<p>The silversword (argyroxiphium sandwicense) (<a href="../flowers/more_silversword_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a unique Hawaiian plant. It is endemic to Maui's Haleakala and the Big Island's Mauna Kea regions and grows nowhere else in the world. It grows at a high altitude on these volcanic mountains. </p>
<p>The plant's leaves have a heat-reflective fur of white hairs that protects it from the sun. The silversword blooms just once in its life, just before its life's end. On average silversword plants grow for 12 years. Blooming season is between June and November. </p>
<p>The blossom is a single spike that can be up to 6 feet (1.8 m ) tall and contain 600 individual purple flowers. The scientific name Argyroxiphium looks difficult to pronounce. &ldquo;Argyro&rdquo; means silver and &ldquo;xiphium&rdquo; means sword. The Hawaiian name of the silversword plant is 'ahinahina, which literally means gray gray.</p>
