<h2 class="small">Plumeria Overview</h2>
<p>Plumeria is one of the most popular flowers in Hawaii. It is commercially grown for the flower lei industry. They have a sweet scent and their blossoms can easily be put on a string, making them perfect lei flowers. Plumerias are also a favorite in home gardens and are also a common sight in cemeteries. Because of that, the flower is also called &ldquo;make-man flower,&rdquo; meaning &ldquo;dead man flower.&rdquo;</p>
