<h2 class="small">Firecracker Plant Overview</h2>
<p>This shrub grows up to a height of 4 feet (1.2 m) and rarely has leaves. Instead, it grows many tubular, inch-long flowers that look like red firecrackers. The plant blooms year-round.<br />
  <br />
In Hawaii, gardeners use this plant as a low hedge or groundcover. It is easy to maintain as it requires little water. It can handle full sunlight, but prefers some shade. The firecracker plant is native to Mexico.</p>
