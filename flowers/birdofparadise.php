<h2 class="small">Bird of Paradise Overview</h2>
<p>The Bird of Paradise is native to South Africa. People who speak English in Europe and Africa call it Cranes Bill or the Crane Flower. It can grow 3-4 feet (0.9-1.2 m) in height and is a close relative of the banana and the heliconia. The &ldquo;bird&rdquo; is the top portion of this plant, with orange feathers and a blue tongue. The plant is easy to grow if it is watered well. It is a favorite in the cut-flower business.</p>
