<h2 class="small">Banana Blossom Overview</h2>
<p>Most people are familiar with the banana fruit, but not so much with the plant's blossom. It is a pretty purple flower. There are hundreds of varieties of this plant. The main part of the banana is underground. The stalk has many tight stalk of leaves fanning out in all directions. </p>
<p>The early Hawaiians developed about 70 varieties of banana, of which about 20 remain today. They saw a resemblance between banana trunks and men. In the old Hawaii only men were allowed to eat this fruit. Women who broke this kapu (taboo) were killed.</p>
<p>In addition to being a valuable source of food, the banana plant was also used for use in underground oven linings and lei-making. The plant's sap was used as a dye, and banana stalks were used for rolling canoes on land.</p>
