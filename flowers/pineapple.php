<h2 class="small">Pineapple Overview</h2>
<p>Pineapple is a related to the bromeliad. In the Hawaiian language, it is known as hala kahiki. Pineapple was first introduced to Hawaii in the early 19th century. They were first grown on the Big Island in the 1850s. The later much superior variety called Smooth Cayenne was introduced around 1885. By the mid-20th century Hawaii was producing about 80 percent of the world's pineapple. </p>
<p>Pineapple prefers higher altitudes and a drier climate. The plant produces the first fruit after two years of being planted, usually in late summer. Each plant produces a single fruit. After cutting off the top green part of the grown pineapple and planting it again, a new pineapple grows in two years. Read more about <a href="../agriculture/pineapple.php">Hawaii pineapples</a>.</p>
