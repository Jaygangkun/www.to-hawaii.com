              <h2 class="small">Red Ginger Overview</h2>
              <p>Red Ginger is sometimes called Pink Cone Ginger or Ostrich Plume. It is a native Malaysian plant with bright red bracts. These look like the bloom, but the actual flower is the small white flower at the top. Ginger grows thick underground stems and its flowers emit a subtle fragrance. It prefers partial shade and humid conditions and needs to be well-watered. It also makes for a good houseplant.</p>
