              <h2>A brief Hawaiian dictionary</h2>
              <p>Here is a compilation of various Hawaiian words. They are grouped by subject matter for your browsing convenience. Included are common Hawaiian words and names that you may encounter during your vacation. We know that some of the words may seem difficult to pronounce right away. Give yourself some time while you familiarize yourself with the <a href="hawaiian-language.php">Hawaiian language</a>. </p>
              <p>Traditionally in the Hawaiian language, the &ldquo;W&rdquo; is pronounced like a &ldquo;V.&rdquo; </p>
              <p>For example: The word &ldquo;Hawai'i&rdquo; is pronounced &ldquo;hah-vy-ee&rdquo; instead of &ldquo;hah-wy-ee.&rdquo;</p>
              <p>Other pronunciation tips:</p>
              <p>The Hawaiian language has many so-called glottal stops (&lsquo;). These are called 'okina. They indicate a very slight pause similar to when you say &ldquo;uh-oh&rdquo; to show concern. Vowels may also have a bar over them, like this: &#257;. These bars are called kahak&#333; and are pronounced by stressing the vowel sound.</p>
