            <div class="new-custom">
              <div class="midpanel_row">
                <p>One of the most unique golf courses in Hawaii, Hapuna Golf Course is set in rugged desert  terrain and stretches along the scenic Kohala coastline, above the western  shore of the Big Island. The course is incredibly picturesque with panoramic  views of the Pacific Ocean at every stage of play. A favorite with locals,  Hapuna Golf Course has earned itself something of a reputation as a hidden gem.  Its uniqueness is in part due to its dramatic elevation changes; the course  starts at sea level and rises to some 700 feet above.</p>
                 
                <p>An Arnold Palmer and Ed Seay collaboration, and  opened in 1992, this 18-hole, par-72 course has a links-style layout. Measuring  a little under 6,900 yards, the natural contours of the locality are seamlessly  incorporated into the design with due care afforded to the sensitive nature of  the environment in which the course is set. With a scenic backdrop that  includes the ocean and the snow-capped Mauna Kea volcano, players of all  ability levels should enjoy a challenging round of golf at Hapuna. Tee off  takes place at an elevation of just less than 700 feet with the remaining holes  appearing incrementally. Play continues on the silky fescue grasses with several risk-reward opportunities all the way  throughout this spectacularly scenic course, most notably at the par-5 holes,  of which the 14th is a perfect example. This hole features a  sizeable ravine to the right and a wide, shallow green, which presents players  with something of a strategy conundrum in terms of how best to play it. The 12th  hole, a par-4, is the course&rsquo;s signature hole, offering one of the most amazing  views on the Big Island &ndash; even Maui can be seen in the horizon from here.</p>
                <p>Those who use Hapuna Golf Course will enjoy a comprehensive range of facilities. The  club provides a cart, clubs, and shoe rental service, complimentary bottled  water, locker rooms, and shower facilities. There is a practice bunker, driving  range, and putting and chipping greens. Golf lessons and clinics are available  from professional PGA staff upon request. The full-service pro-shop is open  daily and offers a good range of merchandise. Essentially, golfers can look  forward to a challenging game and every necessary amenity at Hapuna Golf  Course. </p>
