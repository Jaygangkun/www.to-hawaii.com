                <p>Makalei Golf Club is a beautiful golf venue in  the Kona region on the western side of Hawaii Island. A short drive from Kona  International Airport, the course is very accessible from all of the island&rsquo;s  main resorts. While the region itself is renowned for its cool climate and  famous coffee-producing farms, the design of the golf course epitomizes the  pure natural beauty of the island. From its setting in the western slopes of  Hualalai Mountain, the layout includes native forest, cinder cones, and lava  tubes. This lush natural environment is home to a plethora of the region&rsquo;s  indigenous flora and fauna, including several native plant species and a host  of wildlife such as pheasants, peacocks, wild turkeys, and other animals, for  whom the golf course surroundings has become something of a sanctuary. Added to  all this, visitors to Makalei Golf Club will enjoy panoramic views of the  Kona-Kohala coastline, stretching all the way across to Maui. </p>
                 
                <p>Designed by Dick Nugent, this 18-hole, par-72  course offers a challenge to players of all skills levels, and is that unique  combination of golf course and nature ramble. With its many unique features,  this is a course that requires concentration and time to become accustomed to  the terrain. The approximate 1,000 feet elevation change of the course ranges  from 1,800 feet to 2,850 feet. The tees and greens are laid with Bentgrass and,  with multiple tees, the course can be extended from 5,242 to a championship  7,091 yards. While there are a number of notable holes at Makalei, the eight -  one of the par-3s where wind and elevation change are certainly factors - is  perhaps the real signature hole, in part because of its stunning aesthetics,  which includes a spectacular view spanning 90 miles over the ocean to Maui and  the 10,000 foot high Mount Haleakala. This is definitely a unique course, an  amazing blend of challenge and serenity.</p>
                <p>Visitors to Makalei Golf Club are offered every  possible amenity. In addition to a cart and club rental service, players can  enroll for lessons, practice their swing at the driving range, or hone their  skills at the chipping area and/or at any one of the two practice putting  greens. The golf shop offers a good selection of golf wear and equipment, as  well as logoed items, accessories, and other merchandise. For those in need of  refreshments, the Peacock Grille is the ideal place to relax and enjoy the  serene surroundings over a choice of salads, pupus, or one of the  specially-featured dishes, and a beverage from the fully-stocked bar. Makalei  Golf Club also caters for weddings, parties, corporate functions, and other  events.</p>
