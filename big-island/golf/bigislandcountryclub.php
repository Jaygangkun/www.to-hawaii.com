                <p>The Big  Island Country Club is situated above Kona, on the lower slopes of the 14,000  feet Mauna Kea Mountain. At an elevated level of more than 2,000 feet, the  course provides excellent views of the mountain above and the Hawaiian  coastline beneath. This is one of the Big Island&rsquo;s most distinctive golf  courses where the air is cool and the winds relatively light. Such a beautiful  setting is the perfect destination and sure to inspire those who play the  course.</p>
                 
                <p>An  18-hole, par-72 course, the Big Island Country Club golf course was designed by  renowned golf course architect Perry Dye and opened in 1997. The greens are  moderate in size and speed, and the fairways are wide and beautifully  landscaped. Indeed, with its wonderful mature trees and superb water features,  including waterfalls, the course has become something of a bird sanctuary. The  gently rolling fairways are laid with Bermuda grass, the undulating greens with  Brent grass, and water comes into play at nine holes. With all the attributes  generally expected of a world-class course, an array of interesting features,  panoramic scenery, and an excellent location, the course is both challenging  and enjoyable. The fact there is five sets of tee boxes at each hole make the  course sufficiently challenging for seasoned players yet also suitable for  novices. The signature hole at the Big Island Country Club is the 17th, a par-3 where water comes most prominently into play. Superb  views, moderate trade winds, refreshingly cool air, and well-maintained  surfaces are all trademarks of this 7,034-yard course. </p>
                <p>The  amenities at the Big Island Country Club are excellent and entirely befitting a  golf club of its caliber. In addition to cart and club rental services, the  practice facility includes a driving range. The Golf Academy offers lessons to  players of all ages and abilities, and helps people to improve their game, become  more competitive, or get started from scratch. The pro shop at the club offers  a wide range of merchandise, food, beverages, gift cards, and more.  Refreshments can be obtained from the club&rsquo;s lounge and snack bar.</p>
