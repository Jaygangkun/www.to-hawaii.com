                <h2 class="small">Waikoloa Village Golf Club, Big Island</h2>
                <p>Waikola Village Golf Club stretches along the base  of Mauna Kea, and looks out over the scenic Kohala Coast. At an elevation level  of about 1,000 feet players are treated to views of four other volcanoes (in  addition to Mauna Kea) from this course - Kohala, Mauna Loa, and Maui&rsquo;s Mt Hualalai. The locality in the vicinity  of this golf club is arid, with  an average annual rainfall of around twelve inches, which more or less  guarantees good playing conditions all year around.</p>
                 
                <p>The  18-hole, par-72 course at Waikoloa Village was designed by Robert Trent  Jones Jr who endeavored to preserve the landscape&rsquo;s natural qualities while building  in new features that would enhance play and the local surroundings. The second  golf course built on the Big Island, and  measuring 6,791-yards, the fairways are generously-proportioned and the greens well  protected. The landing areas are spacious and forgiving, which entices bold shots from the tees; the undulating greens  are large and heavily-bunkered, providing a challenge for golfers of all  abilities. With stunning views of the ocean, lakes have been incorporated into  the layout and young trees dot the lush terrain. Strong prevailing winds  sometimes invade the greens, and even though the course is sufficiently  challenging for seasoned golfers, its scenic and relaxed nature is suited to  players of all abilities. A truly beautiful golf course, this is a real  Hawaiian gem. </p>
                <p>Facilities  at Waikoloa Village Golf Club include a brand new fleet of golf carts, club  rental service, practice range, large putting area, and chipping green. The  club also offers private and group lessons, taught by fully-qualified PGA  instructors. The newly-renovated golf shop offers the best in golf apparel,  equipment, accessories, gifts, and logoed merchandise. The restaurant at  Waikoloa Village has also been newly renovated, is open seven days a week, and  features club favorites, local specialties, full bar, a banquet/catering  service, and indoor and outdoor seating.</p>
