                <p>A  comprehensive range of facilities awaits those who choose to use the North  Course at Mauna Lani Resort. As  well as golf carts and a club rental service, there are practice facilities  with driving range and putting green. Lessons and clinics are available from  the resort&rsquo;s professionals for beginners and existing players in all aspects of  the game. The pro shop offers a wide selection of golf-related merchandise,  catering for almost every requirement. The Golf Clubhouse is the place to go  for anyone in need of refreshments after a challenging round. Aside from the  Clubhouse, Mauna Lani resort offers several dining options, with the CanoeHouse  being the resort&rsquo;s signature restaurant. The resort also caters for meetings,  weddings, and just about every type of event.</p>
