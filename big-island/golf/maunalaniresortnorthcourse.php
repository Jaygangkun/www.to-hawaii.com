                <h2 class="small">Mauna Lani Resort - North Course, Big Island</h2>
                <p>The  North Course at the Mauna Lani Resort is one of the resort&rsquo;s two championship  golf courses, the other being the South Course. While both courses are design  masterpieces and present players with considerable challenge, each course is  strikingly different from the other, both geographically and in terms of player  experience. The kiawe trees lining the fairways of the North Course and the  markedly different terrain make this course appear significantly dissimilar to  the South. The North Course has rapidly become a hugely popular tournament  venue on the Big
                <p>Island and is bordered on its northern side by a protected  archaeological district of some 230 acres. Feral goats can be found roaming the  length of the course during the day.</p>
                 
                <p>Built  on a lava bed, the rolling terrain of the 18-hole, par-72 North Course is  interspersed at various intervals by Kiawe (mesquite) forests that come into  play at several holes. The course provides plenty challenge for players of all  abilities. However, most holes have up to five different tee options, so this  gives players plenty choice to counteract the course&rsquo;s many challenges. North  Course has its share of water hazards, with several adjoining the ocean. The  course&rsquo;s signature hole is the 140-yard 17th. This par-3 plays from  an elevated tee downwards to a lava-surrounded green. Although the playing  distance is short, it can be difficult to gauge because of the elevation. But  this position does provide players with a good photo opportunity! </p>
