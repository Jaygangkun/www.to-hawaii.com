                <p>One of the most popular golf courses in  Hawaii, Mauna Kea offers superb views of the  beautiful Kohala Coast. Since it opened in 1964, this course has set the  standards in terms of beauty and design excellence, cementing its place as one  of the most scenery-filled and favorite courses in Hawaii&rsquo;s golfing history. </p>
                <p>Originally designed by Robert Trent Jones Senior from a topography of barren lava rock, and later updated  by his son Rees Jones, this award-winning 18-hole, par-72 course has played  host to some of the greatest names in the game. With an eye on conserving  environmental resources, the greens are laid with highly efficient ultra-dwarf Tifeagle  Bermuda grass and the tees, fairways, and roughs with Tifway 419 hybrid Bermuda  grass. Other upgrades saw the  latest irrigation systems installed, draining improved, and liners placed in  bunkers, the latter having been extended to 99 in number. Length was also extended by approximately 200 yards to a total of over 7,100  yards from the tips, and with multiple tees at each hole, Mauna Kea should suit players of  all skills levels. The  elevated greens and prevailing trade winds put a premium on approach shots if  they are to land accurately on the greens. There are many memorable holes at Mauna Kea. The par-3 third hole is the  signature hole and is located, like the eleventh, at sea level. With plenty  nooks and crannies to negotiate, not to mention a rocky shoreline, this  requires some crafty play. This course embodies all the qualities that  make a golf course great &ndash; clever design, a naturally beautiful setting, epic  views, and an overall quality that will be remembered by those who use the  course. </p>
                <p>Like the golf course, all other amenities at Mauna Kea are  state-of-the-art. As well as complimentary bottled water, club rentals, and a  shoe cleaning service, the club has world-class practice facilities that  include practice balls, a driving range, and a putting green where players can  fine-tune their skills. Private lessons and group clinics are also available  for those who want to raise their game. The newly-designed pro shop is stocked  with the best golf equipment, the latest in designer wear and accessories, logo  merchandise, gift items, and more. Those seeking refreshments can retire to the  clubhouse&rsquo;s new restaurant &ldquo;Number 3&rdquo; where they will find flavorsome cuisine,  pupus, a chilled drink, or a Mauna Kea cocktail, the club&rsquo;s signature beverage.</p>
