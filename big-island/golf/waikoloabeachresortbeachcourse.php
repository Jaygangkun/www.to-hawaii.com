                <h2 class="small">Waikoloa Beach Resort - Beach Course, Big Island</h2>
                <p>Waikoloa Beach  Resort offers the choice of two golf courses &ndash; the Beach Course and the Kings&rsquo;  Course. Each course presents the player with a unique challenge, but both  provide a fun and enjoyable experience that encourages a return visit. Like its  counterpart, the Beach Course was formed out of an ancient lava field. The  result is a golf course full of vivid green fairways, sparkling blue water,  stark black lava, and colorful flowers dotting the landscape, a stunning  contrast.  Other features that add to the Beach Course experience are the second biggest petroglyph  field in Hawaii, age-old burial grounds, and remnants an ancient fishing  village. </p>
                 
                <p>The classic  18-hole, par-70 Beach Course was designed by Robert Trent Jones Jr. At  around 5,100 and 6,600 yards from the forward tees and the tips respectively,  the course winds its way through an avenue of coconut palm trees and lava rock,  down to the breathtaking Pacific Ocean. This course is designed to arouse every  player&rsquo;s sense of adventure, aided by the stark, windswept lava coastline as it  is washed by the powerful waves from the mighty Pacific. On a technical level,  this course is suitable for every player, whatever their skills level. It is  considered one of the most enjoyable and playable courses on the Big Island,  challenging enough for professionals but easy enough for the less experienced  golfer. Beach Course has no less than 75 large bunkers and an abundance of  water features. There is, for instance, a large pond much wider than the  putting surface protecting the generous green at the memorable par-4 second  hole. The 502-yard, par-5 twelfth hole, which doglegs to the left, is a real  gem. Here, Anaehoomalu Bay guards the fairway on its far right side and  stretches around two sides of the green. The signature hole at Beach Course is the seventh, which is located on  a lava peninsula and surrounded by a ring of coconut palms. The waves crashing  below, causing sea spray to shoot up into the air, just adds to the dramatic  vista. Depending on the season, players may even spot a few humpback whales or  spinner dolphins at work or play. A camera is a must here!</p>
                <p>Facilities  at Waikoloa Beach Resort  include golf carts, club rentals, driving range, pro shop, a full-service bar,  and a choice of eateries. The pro shop offers a wide range of fashion clothing,  shoes, accessories, logo merchandise, and golf equipment. Home to the King&rsquo;s  Grille and The Tropics Ale House, visitors to Waikoloa Beach Resort can  avail of a varied and innovative choice of food, a selection of local and  imported beers, and other beverages. </p>
