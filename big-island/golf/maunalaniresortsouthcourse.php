                <h2 class="small">Mauna Lani Resort - South Course, Big Island</h2>
                <p>Located  on Hawaii&rsquo;s Kohala Coast, the South Course is one of two championship golf  courses at Mauna Lani Resort. Like its North Course counterpart, the South  Course is also a masterpiece in design, but is notably different in character  and in terms of player experience. Now home to the prestigious Hawaii State  Open, the South Course was in the past renowned for its role in hosting the annual  Senior Skins Game for the ten years from 1990 to 2000.</p>
                 
                <p>Designed  by Homer Flint, R F Cain and Robin Nelson, and opened in 1981, the 18-hole,  par-72 South Course winds its way through a'a lava of the prehistoric Kaniku  lava flow. The rugged terrain offers superb views of the nearby mountains and  ocean, all the while providing players with a reasonable amount of challenge.  The fairways are relatively wide, and the greens quite approachable, which  makes this course more forgiving and easier to play than the North Course. And,  with three sets of tees to choose from, players of different ability levels are  adequately catered for. One of the most renowned features of the South Course  is the 15th hole. With its sumptuous ocean surroundings, this spot  is one of the world&rsquo;s most photographed. Yet, if the wind rises to more  than a mere breeze, players are likely to discover the treachery that lurks  beneath the stunning beauty &hellip; at least in terms of their golf score! </p>
