                <h2 class="small">Waikoloa Beach Resort - Kings' Course, Big Island</h2>
                <p>The King&rsquo;s Course is one of two golf  courses at Waikoloa Beach Resort. Its counterpart is called Beach Course. Both  courses are unique and enjoyable and each offers its own inimitable challenges,  making those who play want to come back. The King&rsquo;s Course is set against a  coastal backdrop where blackened lava rock and other geological remnants from  an ancient volcanic eruption remain and create a surreal and historical  landscape.</p>
                 
                <p>The 18-hole, par-72 King&rsquo;s Course was designed by Tom Weiskopf and Jay Morrish  with a mind to the prevailing trade winds. While the course is fun to play, it  is not without challenge. On days when the opposite winds, those from Kona, are  in force, golfers will find that play is shorter than anticipated on ten holes,  and the other eight will play much longer than expected. So, respect is  required if rewards are to be reaped. This is a course that offers many  interesting variations. Some fairways feature large, deep bunkers,  numbers 3 and 16 share a very large green, and water hazards come into play at  six holes. The scenic  fifth is the signature hole at King&rsquo;s Course. Hugely picturesque, this short  par-4 is quite a deceitful hole when the wind comes into the equation,  irrespective of whether that wind favours the player or not. Those who hit for  the green and the possibility of a coveted eagle could find their ball straying  into the lava field in the center should an unpredictable gust choose to carry  it there. However, when players reach the elevated green at the this hole, they  will be treated to a superb, sweeping lava &ldquo;moonscape&rdquo; vista. At other times  they may even see feral goats roaming the course. </p>
                <p>Waikoloa  Beach Resort provides most of the facilities associated with a highly-rated golf  resort, including golf carts, club rentals and a driving range. The pro shop  stocks a good range of fashionable clothing, shoes, accessories, equipment,  logo items, and more. The resort is home to the King&rsquo;s Grille and The Tropics  Ale House where visitors can  enjoy a varied and imaginative selection of food, a choice of local and  imported beers, and other beverages.</p>
