                <h2 class="small">Papakolea Green Sand Beach, Big Island</h2>
                <p>Papakolea Beach, located near the Big Island's southern tip, is known for its unique olive green sand. Olivine is a semi-precious stone. Here in this area these small green volcanic stones originate from the littoral cone that surrounds the small bay. The ocean erodes these stones and crushes them into fine sand. The sand is too fine to be considered gem stones, but beaches like this are rare. So it's almost like walking on jewels. </p> 
                <p>However, getting to this beach isn't easy. Papakolea is located at the base of Pu'u O Mahana, an old littoral cone in a wild, undeveloped terrain. Access involves a three-mile (4.8 km) hike through lava fields. If you're going on a sunny day, keep in mind that it is usually quite windy in this area, which blows up the dust. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>It is a good idea to prepare yourself for what some call a long and arduous walk. So wear hiking boots, a T-Shirt, comfortable pants or shorts and a hat and bring plenty of water and sunscreen. It is a common sight to see tourists arrive in sandals and bathing suits who turn around half way because they can't make it. </p>
                <p>Also, there aren't any signs, markers or maps to follow. There isn't even a specified trail. A good tip is to stay a little bit more to the left on the way there. Even though the trails near the ocean are nice, they are more rugged with sharp lava rocks and in the end will take longer. </p>
                <p>Once you make it to the beach, you'll be rewarded with one of the best views on the Big Island. The green beach appears almost unreal against the backdrop of steep cliffs and boulders. At sunset, the play of colors against the sand is simply breathtaking. To get down to the beach, you'll have to hike down a low sea cliff. The best spot is the area where the cliff is less steep. The steep face of the cliff is dangerous because the ground is loose and can slide away.</p>
                <p>The sparkling blue water appears calm, but beware that currents are often times difficult to spot, so it is best to stay close to the shore if you plan to swim here. If the surf is up, don't go into the ocean because if you get in trouble here, you're miles away from any help. There are no lifeguards on this beach.</p>
                <p>A visit to Papakolea Beach is a full-day trip, so plan ahead. The hike to and from the beach alone takes about 2-3 hours, depending on how fast you can walk, and then you still need to calculate for some time on the beach and the drive time it takes to get to South Point. It is best to leave the beach well before sunset because as mentioned above, the area is undeveloped, there are no lights and the terrain is rough. It is not a good idea to walk here in the dark. </p>
