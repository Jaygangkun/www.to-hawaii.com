                <h2 class="small">Keaukaha Beach Park, Big Island</h2>
                <p>Keaukaha Beach Park (<a href="../../big-island/beaches/more_keaukahabeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is one of the Hilo area beaches. There's actually no beach here, but only a lava rock shoreline. Several freshwater springs bubble up from the ocean bottom, which makes the water temperature in some areas a bit chilly. Because of the rocky shoreline, the swimming conditions are rather poor here, but the snorkeling is good when the ocean is calm. There is a large grassy area that&rsquo;s ideal for a picnic. </p>
