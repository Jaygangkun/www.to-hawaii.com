                <h2 class="small">Old Kona Airport State Park, Big Island</h2>
                <p>The old Kona Airport closed down in 1970, but developers have certainly put the site to good use. In its place now lies a one-mile-long beach and state park, the old runway conveniently turned into a parking lot. In fact, parking is one of the best facets of the Old Kona Airport Beach (<a href="../../big-island/beaches/more_oldkonabeachpark_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>) &ndash; it's free, the slots are wide and the lot can function as a picnic ground by itself. And the airport's old terminal building now serves as a pavilion.</p>
                <p>Many people come to Old Airport (as local residents refer to this state park) for the sunset views. The beach is long and wide, and there is a sandy stretch between the old runway and the rocks at the water's edge. Even though the beach is rather rocky, swimming is possible here. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>The best spot is located at the southern end, where you'll find an inlet that offers easy access to the water. There are a few more inlets and sandy channels scattered on the length of the beach, but the southern end is the best one. This southern area around the old terminal building is leased by the county, and sports fields and tennis courts for community events are located here as well.</p>
                <p>The beach is exposed to high surf, so during storms it is best to stay out of the water because of strong currents. </p>
                <h2 class="big">Old Kona Airport State Park Overview</h2>
                <ul type="disc" class="mylist">
                  <li>An old airport turned into a beach park - the runway serves as the parking lot and the old terminal building is now a public use pavilion</li>
                  <li>Beach is long and wide, but rocky - fair swimming conditions</li>
                  <li>A few sandy channels allow for entry into the ocean</li>
                  <li>A few palm and heliotrope trees provide limited shade</li>
                  <li>Best place for swimming is at the southern end of the beach where there is a small sheltered sandy inlet</li>
                  <li>Snorkelers and divers usually enter the ocean at a small cove at the northern end of the beach, called Pawai Bay, where a channel leads to deeper offshore waters</li>
                  <li>A shallow reef offshore sometimes creates a surf break called Old Airport that's popular among Kona surfers</li>
                </ul><div style="clear:both"></div>
