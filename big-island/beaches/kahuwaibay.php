                <h2 class="small">Kahuwai Bay, Big Island</h2>
                <p>Kahuwai Bay is a well-guarded secret of the Big Island diving community. The sandy beach is home to about 40 different diving sites, most of them within easy walking distance. The underwater lava rocks form beautiful archways and canyons that look almost surreal in the sunlight.</p>
                <p>There's also some interesting marine life here, most notably the green sea turtles that sometimes swim close to shore. The water is very shallow, so you don't even have to wade in to see them up close.</p>
                <p>One thing, however, that you can't get here is a leisurely swim. The only access to the water is via a sharp, slippery lava shelf, and the current can get out of control at times. There are also no lifeguards, so go diving only if you are very experienced and know your way around the bay.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>The beach itself is a nice place to relax. There are palm trees lining the area, which provide shade for picnics and parties. The beach also offers nice views of the late afternoon sunset. It is located next to the Kona Village Resort (which is closed indefinitely after sustaining extensive damage in the tsunami on March 11, 2011, which was generated by an earthquake in Japan). </p>
