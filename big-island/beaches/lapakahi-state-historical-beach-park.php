                <h2 class="small">Lapakahi State Historical Park, Big Island</h2>
                <p>Nature and history come together in this 265-acre (1 sq. km) park which is located a short drive north of Kawaihae on the Big Island's northwestern shoreline. Lapakahi State Historical Park was once an ancient Hawaiian fishing community, Koai'e, which dates back to the 14th century. The site features a wide array of historical artifacts, natural wonders and archaeological remains (such as old ruins, huts and temples), all of which stand as mute testaments to the island's fascinating history.</p> 
                <p>Visitors can take a self-guided tour through the park. There is a mile-long trail that leads to several points of interest, which are all explained by markers. The tour ends at the Koai'e Cove, home to a marine preserve (Lapakahi Marine Life Conservation District) and two small but beautiful beaches. </p>
                <p>Beautiful coral reefs and interesting wildlife make for excellent underwater views, although snorkelers are advised to keep a safe distance from the coral. This is also a good spot for whale watching during whale season in the winter months. A short walk to the north lies Mahukona State Park, which is equipped with restrooms, showers and barbecue pits. Camping is allowed here with a permit. </p>
