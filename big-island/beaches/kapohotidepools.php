                <h2 class="small">Kapoho Tide Pools, Big Island</h2>
                 <p><em style="color:#C30"><strong>Editor's Note:</strong> Due to a recent lava flow that occured on June 5, 2018, this beautiful destination was unfortunately destroyed. We are leaving this page up for all of us who have made memories here and would like to remember it as it used to be.</em></p>
                <p>Located on the Big Island's east shore at Kapoho Bay, the Kapoho Tide Pools (<a href="../../big-island/beaches/more_kapohotidepools_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) offer a unique outdoor bathing experience. There are numerous and interconnecting tide pools that extend up to 200 yards (183 m) offshore. Many of them are large and deep enough to provide enough room for swimmers and snorkelers. Some of the pools are volcanically heated and quite warm, up to about 90 degrees F (32.2 C). </p> 
                <p>In the outer pools near the surf break, a wide variety of sea life can be seen, such as fish, crabs, shellfish and sea cucumbers. However, it is best to stay away from the surf line and further inshore because strong rip currents are common further out.</p>
                <p>The inner pools are almost always calm (except during periods of strong surf and storms) and are a good area for snorkeling, since the water here is clear and protected. During calm days, the fishing conditions are good at the edge of the reef. This is also the time when scuba divers explore the outer edges of the reef, which they access via boat from offshore.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>The entire shoreline consists of black lava rocks, which is rough and sharp. There is no sand at all. So it is best to wear protective shoes to access the area and also when swimming in the tide pools. Reef shoes are ideal.</p>
                <p>A few beach homes line the shore and some residents have incorporated some of the smaller pools at the outer edges into their property to use as swimming pools and &quot;hot&quot; tubs, so make sure you don't go dipping in a pool that is located on private property.</p>
                <p>Kapoho is an old village that was destroyed by a lava flow in 1960. All that was spared was a lighthouse at Cape Kumukahi (accessible by driving straight ahead on Highway 132, a dirt road, where Highway 137 intersects Highway 132). A new community has been rebuilt on Kapoho Bay. The community that's closest to the lighthouse is called Kapoho Beach Lots (a gated community). </p>
                <p>To the south is the public access community called Vacationland. Fronting Vacationland are many public access tide pools. This oceanfront area has been designated the Wai'opae Tidepools Maring Life Conservation District in 2003. This means that commercial fishing or guided tours are not allowed here.</p>
                <p>The name &quot;Kapoho&quot; means &quot;the depression&quot; in the Hawaiian language, referring to the many salt-water-filled depressions (tidal pools) in the area.</p>
                <h3 class="big">Kapoho Tide Pools Overview</h3>
