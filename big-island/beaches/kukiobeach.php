                <h2 class="small">Kuki'o Beach, Big Island</h2>
                <p>Located north of Kikaua Point on the Big Island's Kohala coast, Kuki'o Beach (<a href="../../big-island/beaches/p6.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a pretty white-sand beach, which can most conveniently be accessed at the Hualalai Four Seasons Resort. Entering the water is a little tricky here because shoreline rocks line almost the entire length of the beach. The best entry and exit point to the water is at the south end of the beach, where there is a small opening in the rocks, near neighboring <a href="../../big-island/beaches/kikauabeach.php">Kikaua Beach</a>. Because of this, Kuki'o Beach is not regarded as a good swimming beach. </p>
<p>However, the fishing conditions are good. During the winter months, the waves are sometimes good enough for surfing and bodyboarding as well. The beach is surrounded by coconut palms, ironwood and kiawe trees. A few small brackish-water ponds are located near the beach. </p>
