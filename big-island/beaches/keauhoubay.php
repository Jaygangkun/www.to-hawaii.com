                <h2 class="small">Keauhou Bay, Big Island</h2>
                <p>Keauhou Bay (<a href="../../big-island/beaches/p19.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  is a historic area on the Big Island's Kona Coast. It is the birthplace of King Kamehameha III (born in 1814). It is said that he was stillborn, but brought back to life by a visiting kahuna (priest). He was the longest reigning monarch in the Kingdom of Hawaii, from 1825  until his death in 1854. </p>
                <p>There is a boat launch ramp and a small boat harbor at Keauhou Bay. Local residents like to fish here. The bay is not suitable for swimming since the ocean bottom is rocky and because of the harbor traffic. Several boat tour companies depart from here to take people snorkeling at nearby Kealakekua Bay and Red Hill. Next to the harbor is a small park with a grassy area, picnic tables, showers and restrooms. </p>
