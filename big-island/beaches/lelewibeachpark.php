                <h2 class="small">Leleiwi Beach Park, Big Island</h2>
                <p>Leleiwi Beach Park (<a href="../../big-island/beaches/more_leleiwibeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is located next to the Richardson's Ocean Center at the eastern end of Hilo. There is a small black-sand beach and the rocky shoreline is made up of a few natural ponds, inlets and small rock islets. Some of the ocean pools are fed by freshwater springs, so the surface water is a bit chilly. But nevertheless, many tropical fish and other marine life inhabit the bay. And because the beach is shallow and protected, it is a popular snorkeling destination.</p> 
                                 <? include ("../../theme/text.html") ?>
                <p>A sea wall lines almost the entire length of the beach, but there is a small pocket beach at the base of the wall which serves as an entry and exit point to the water. As you enter the water, don&rsquo;t be surprised if you&rsquo;re joined by some green sea turtles who inhabit the area and are particularly friendly to swimmers. Keep in mind though that they are protected by law and it is not allowed to touch or swim after them. Leleiwi is also a good spot for picnics. There are palm trees that provide shade, as well as picnic tables and pavilions. </p>
