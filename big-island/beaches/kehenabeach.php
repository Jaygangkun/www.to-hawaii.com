                <h2 class="small">Kehena Beach, Big Island</h2>
                <p>Located in the Big Island's Puna district, Kehena Beach is one of the few unofficial &quot;clothing optional&quot; beaches in Hawaii. It is a long, but narrow black-sand beach, which local residents also call Dolphin Beach because of the frequent appearances of spinner dolphins in this area.</p>
                <p>The beach was formed in 1955 when lava flowed down the sea cliffs and into the ocean here. At the eastern end of the beach, one can still see the tip of the lava flow (which is now a rocky point of land) that created the sand. </p> 
                <p>In 1979, when a strong earthquake shook the Puna district, Kehena Beach dropped almost 3 feet (91 cm). The concrete stairs that led to the beach broke off and now hang more than 10 feet (3 m) above sea level.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>The beach is well-shaded by coconut palms and ironwood trees, which make up the backshore. Swimming is possible here when the ocean is calm only because the beach is exposed to the open ocean and during times of high surf, strong rip currents and undertows can occur. That's why there have been quite a few near-drownings at Kehena in the past.</p>
                <p>Also, since this beach has many pebbles on the ocean bottom, they can easily get stirred up in the shorebreak during times of heavy surf, which can cause painful skin abrasions to people entering the water.</p>
                <p>So when the surf is up, it is best to stay out of the water and maybe have a picnic on the beach, which is accessible from the Kehena lookout. </p>
