                <h2 class="small">Ke'ei Beach, Big Island</h2>
                <p>Just beside the well-known Kealakekua Bay is a small beach unknown to most visitors. Ke'ei Beach is one of the best-kept secrets on the Big Island's Kona Coast, visited mostly by local fishermen, surfers and the occasional sightseer. </p>
                <p>Ke'ei Beach has white sand speckled with black lava fragments and is a good spot for a picnic. When it come to swimming though, the conditions aren't that good because the nearshore ocean bottom is very shallow and rocky. </p>
                <p>There is a narrow sandy channel at the northern end of the beach that's suitable for swimming. Other water activities that are possible here are snorkeling, surfing and fishing. The reef stretches offshore for almost 300 yards. On some days the surfing conditions are quite good here, with some of the biggest waves and longest rides in the area.</p>
                <? include ("../../theme/text.html") ?>
                <p>Besides the attractive scenery at Ke'ei Beach, it is also an important historical location. It was here where the Battle of Moku'ohai took place, King Kamehameha the Great's first battle in his effort to gain control of the Big Island of Hawaii. </p>
                <p>He achieved rule over half of the island, which he interpreted that the gods favored him and wanted him to rule not just the Big Island of Hawaii, but all of the Hawaiian islands (which he achieved in 1810 and then established the Kingdom of Hawaii). </p>
