                <h2 class="small">Mahai'ula Beach, Big Island</h2>
                <p>Mahai'ula Beach is part of the Kekaha Kai State Park on the Big Island's west coast. It is located about 2 miles (3.2 km) north of the Kona airport and is accessible via a rugged dirt road. This white-sand beach is backed by trees, which reach down almost to the water's edge, so there are some shady spots. The swimming conditions are usually good when the ocean is calm. From the beach, a coastal lava trail leads north to neighboring Makalawena Beach, about a 15 to 20-minute walk. </p>
