                <h2 class="small">Kekaha Kai (Kona Coast) State Park, Big Island</h2>
                <p>Kekaha Kai State Park on the Big Island's west coast encompasses a handful of secluded bays and sandy beaches. The most beautiful ones are <a href="../beaches/mahaiulabeach.php">Mahai'ula Beach</a>, <a href="../beaches/makalawenabeach.php">Makalawena Beach</a> and <a href="../beaches/kuabay.php">Kua Bay</a> (also known as Manini'owali). </p>
                <p>The entire shoreline in this area is gorgeous. The dark lava rocks, white sandy coves, green vegetation and deep blue water make and offshore coral reef for a nice contrast in colors. All three beaches offer great opportunities for swimming, snorkeling and other beach activities. In the winter, there are also some good waves for surfing and bodyboarding. </p> 
                <p>Coming from the south, the first beach is Mahai'ula Beach. It is accessible via a rugged dirt road (which begins between mile markers 91 and 90). A four-wheel drive is recommended, but regular cars can usually handle the road as well. There is a gate that is open from 9 am to 7 pm daily, except on Wednesdays when it is closed. You'll reach the beach after a 1.5-mile (2.4 km) drive and a 5-minute walk from the parking area.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>Mahai'ula Beach is sandy and has a picnic area with tables. The ocean bottom drops off gently here and is composed of a mixture of sand and rocks. Besides swimming and snorkeling, many divers come here to explore the underwater arches, tunnels and caves, as well as a sunken ship. During times of high surf, many surfers come here as well. There is a good surf spot called Kawili Point at the northern end of the bay.</p>
                <p>Also, many ancient archaeological sites are located near Mahai'ula Beach, such as Pohaku o Lama, a stone fish goddess standing near the water's edge. In ancient Hawaii, fishermen brought their offerings to this rock and asked for their good luck at sea. In the spring time (May to June), the water around this rock sometimes turns red. The ancient Hawaiians believed that this meant that the stone goddess is menstruating. Nowadays, the scientific explanation is that the red water is caused by small organisms called dinoflagellates. The phenomenon can still be observed today.</p>
                <p>From Mahai'ula Beach, follow the coastal Ala Kahakai Trail that leads to neighboring Makalawena Beach, about a 15 to 20-minute walk. (It's a lava trail, so wear sturdy shoes). This white-sand beach is popular among local boys who like to bodysurf here in the winter. At the northern end of the beach is a small brackish water pond, which is inhabited by brine shrimp and nanini fish.</p>
                <p>From Makalawena Beach, the coastal trail leads north for about another 4 miles (6.4 km) to Kua Bay. Along the way, there are sand dunes, ponds and mini forests. About midway, hikers can walk to the summit of Pu'u Ku'ili, a 342-foot (104 m) high cinder cone. </p>
                <p>Kua Bay is a white-sand beach that also offers great conditions for a variety of water activities, such as swimming, snorkeling and diving. However, it can get quite hot here, especially in the summer months because there is no shade. If you don't plan to hike here from Mahai'ula Beach, Kua Bay is also accessible via a road that starts between mile markers 88 and 89. At the end of the road is a parking lot. But you'll have to walk over rough lava rocks to get to the beach. </p>
