                <h2 class="small">Reeds Bay Beach Park, Big Island</h2>
                <p>Located on Banyan Drive (the road along which most of the hotels in Hilo are located), Reeds Bay Beach Park offers a calm and shallow swimming area. However, most people prefer another area called Ice Pond, which is located at the head of Reeds Bay. This natural pond got its name from the cold spring water that bubbles up from the ocean bottom and mixes with the salt water. <br />
                  <br />
                  The bay consists of white sand and coral rubble (the western edge of Reeds Bay is man-made). Between 1925 and 1930, coral material was dredged at Hilo Harbor and deposited here. Sailboats anchor in the waters offshore, and other smaller boats are launched from shore.<br />
                  <br />
                  The beach park is named after William H. Reed, a prominent businessman on the Big Island who arrived here in the 1840s and died in 1880. </p>
