                <h2 class="small">Mau'umae Beach, Big Island</h2>
                <p>Located in the Big Island's South Kohala district (northwestern shore), Mau'umae Beach (<a href="../../big-island/beaches/p14.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is tucked away behind shady trees and features protected waters. It is less popular than the neighboring beaches Mauna Kea Beach and Spencer Beach Park. The swimming conditions are good when the ocean is calm. Due to its seclusion, the beach is sometimes frequented by nudists who like to sunbathe in a smaller separate cove on the right (north) end of the beach.</p>
