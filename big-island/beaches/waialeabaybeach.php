                <h2 class="small">Waialea Bay (Beach 69), Big Island</h2>
                <p>Waialea Bay (<a href="../../big-island/beaches/p17.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  is one of the few beautiful white-sand beaches on the Big Island of Hawaii. Many local families come here with their children. The summer months are mostly calm and perfect for swimming and snorkeling. </p>
                <p>The ocean bottom drops off gradually and reaches depths of around 30 feet (9 m) just outside of the bay. Below the surface, you'll find several coral colonies around large underwater rocks. Lots of colorful fish live in the area. </p>
                <p>The water is less friendly in the winter and since there are no lifeguards on duty, it may be best to stay on the shore and watch the waves. When the surf is up, surfers and bodyboarders are drawn to a surf break at the northern point of the bay.</p>
                <? include ("../../theme/text.html") ?>
                <p>Waialea Beach is also known as Beach 69 among local residents. Visitors may chuckle at this name and wonder why it's called like that. The reason is because the public utility pole that's standing at the road that leads to the beach is number 69.</p>
                <p>The shore is tucked into a private neighborhood, but has a public access trail. There is plenty of shade on the beach, which makes it a perfect spot for a picnic and to spend an entire day here.</p>
