                <h2 class="small">Kua Bay (Manini'owali Beach), Big Island</h2>
                <p>Kua Bay (Manini'owali Beach) (<a href="../../big-island/beaches/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  is part of the Kekaha Kai State Park, which is located along the leeward coast of the Big Island of Hawaii. It marks the northern end of the state park and features several hiking trails and a number of lookout points from which you can enjoy great views of the salt-and-pepper coastline.</p>
                <p>Swimming and snorkeling are popular activities in the summer, when the water is calmer. But even then there are days when novice swimmers shouldn't go into the water because the shorebreak can be very powerful and dangerous. It's what makes Kua Bay popular among bodyboarders and surfers. </p>
                <p>Another major attraction is the marine wildlife that thrives in the bay. On calmer days, you can see turtles and dolphins not far from shore. Some turtles actually swim up to the shore. Dolphins are a less common sight, but they do make frequent appearances in the summer months.</p>
                <p>Access to Kua Bay is easy. From the main highway, take the 1.5 mile paved road that leads to a parking lot. The parking lot  fills up quickly during weekends, so people also park along the final stretch of the road. At the end of the road is a convenient turnaround.</p>
