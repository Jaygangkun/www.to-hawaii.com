                <h2 class="small">Punalu'u Black Sand Beach, Big Island</h2>
                <p>Black sand beaches can be found all over the Big Island of Hawaii, but Punalu'u Beach (<a href="../../big-island/beaches/more_punaluubeach_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>), located on the island's southeast shore, is the most popular and one of the most beautiful. This beach is easily accessible and boasts a shore of shiny, jet black sand, made up of ground lava rocks. It was created by a'a lava flowing into the ocean. The area is a well-known nesting place for hawksbill and green sea turtles, both endangered species and the target of many conservation efforts.</p> 
                <p>On a regular day, the shore is dotted with large sea turtles playing and catching some sun. A word of warning, however: these turtles are not to be touched, as they have no immune system to protect them against human-transmitted bacteria. Visitors are required to keep a 15-foot (4.5 m) distance from the turtles at all times.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>At one end of the beach is a natural freshwater pool perfect for wading and cooling down. Further offshore, there are numerous underground freshwater springs, which are very cold and mix with the ocean water. In the past, the people who lived in this area would dive underwater with bottles to obtain freshwater. They pressed their fingers on the water bottles, dove down, and when they reached the freshwater springs, they removed their fingers and filled the bottles. In the Hawaiian language, Punalu'u means &ldquo;diving spring.&rdquo;</p>
                <p>Legend has it that anyone who takes a stone from this beach will be cursed by the volcano goddess Pele and be forgiven only when the stone is returned. However, while this is supposedly an ancient Hawaiian legend, historians were only able to trace the origins of this legend to the mid-twentieth century. It is believed that park rangers invented it to prevent visitors from taking rocks and sand home. </p>
                <p>Nevertheless, if you visit the lobby of the Kilauea Military Camp, you'll see a cabinet where rocks are displayed that have been mailed back to Hawaii by people who had taken them and who were haunted by bad luck ever since. Their letters explaining their predicaments are on display as well. </p>
                <p>In the past, Punalu'u has suffered severe erosion by the ocean. In 1868, a huge tsunami that was caused by an earthquake struck the area. Waves as high as the coconut trees suddenly rolled in and leveled every shoreline village from South Point to Kumukahi. The residents later rebuilt the village at Punalu'u and in the 1880s, a pier was built to move the harvested sugar cane from the surrounding plantations via interisland steamships. But when more roads were built and automobiles were available to transport goods, the Punalu'u shipping point was abandoned and Hilo became the Big Island's main port. The area was hit by two other tsunamis in 1960 and 1975.</p>
                <p>The best part to enter the ocean at Punalu'u is at the northeastern end of the beach because there are fewer lava rocks at the water's edge. There is also a small boat ramp in this area, next to the ruins of the old pier. Swimmers should keep in mind that there is a strong rip current that runs out the boat channel into the open ocean. So it is best to stay away from the northeastern point of the bay. </p>
                <h3 class="big">Punalu'u Black Sand Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Most popular black-sand beach on the Big Island</li>
                  <li>Great place to see sea turtles swimming in the water and resting on the sand (remember to stay at least 15 feet away from them)</li>
                  <li>Remote beach, but easily accessible</li>
                  <li> There is a boat/kayak ramp that's open to the public</li>
                  <li>People with sensitive feet may want to keep their shoes on because the sand is composed of small fragments of volcanic rock that can hurt to walk on</li>
                  <li>Taking black sand or rocks from the beach is prohibited</li>
                  <li>Ocean bottom is rocky</li>
                  <li>Snorkeling conditions are fair due to low visibility</li>
                  <li>Beware of strong currents - swim close to shore and stay out of the water when the surf is up</li>
