                <h2 class="small">Kiholo Bay, Big Island</h2>
                <p>If you were to squeeze the Big Island into a single beach, you'll end up with something like Kiholo Bay (<a href="../../big-island/beaches/p23.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>). This Kohala Coast beach has a little of everything: beautiful sights, fascinating wildlife, tide pools, ancient ponds and rock formations. There's even a mini-island made of lava rock right at the heart of the bay. </p>
                <p>                  The bay itself consists of a mix of fresh and salt water, surrounded by dark lava rocks and a usually calm lagoon. The lagoon is a  great spot for swimming and snorkeling, since the water in it is usually calm compared to the rest of the bay. </p> 
                <p>Much of the bay was once a Hawaiian fishpond built in 1820 by King Kamehameha I. Its boundaries were 2 miles (3.2 km) long and its rock walls were 6 feet (1.8 m) tall and 20 feet (6 m) wide. In 1859, a lava flow from the Mauna Loa volcano, located 30 miles (48 km) away from here, reached Kiholo Bay and destroyed the southern portion of the ancient fishpond and created the lagoon you see today.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>                  At the northern end of the bay is Wainanali'i Pond, which is connected to the ocean via a man-made channel. It is an important feeding and sleeping site for green sea turtles. However, it is located on private property and therefore not accessible. Bordering Kiholo Bay to the south is Luahinewai Pond, a freshwater pool that's surrounded by coconut palms and lush vegetation. This area is also on private property and off-limits to visitors.</p>
                <p>                  On your way to Kiholo Bay, stop at Mile Marker #82 on Highway 19 at the Kiholo Bay Scenic Overlook, from where you can see most of the bay and its sprawling coastline (see below for directions on how to access the bay). </p>
                <h3 class="big">Kiholo Bay Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Good swimming in the bay during calm days</li>
                  <li>Water is relatively cold due to freshwater springs in the bay</li>
                  <li>Turtles can often times be seen here, they feed on limu (seaweed) that grows in the bay</li>
                  <li>Location of an ancient fishpond built by King Kamehameha the Great, which was destroyed by lava flows in 1859</li>
