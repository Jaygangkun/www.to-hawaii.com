                <h2 class="small">MacKenzie State Park, Big Island</h2>
                <p>This sprawling state park has one of the most dramatic oceanfronts, but people don't come here for the swimming. MacKenzie State Park is mostly popular among sunbathers, picnickers and fishing enthusiasts. Strong currents make it unsafe for swimmers and snorkelers, and many drownings and near-drownings have happened in this area. Most of them were fishermen who were swept off the rocky ledges. Nevertheless, the views at this 13-acre (52,609 sq. m) paradise certainly merit a full day of exploring.</p> 
                                 <? include ("../../theme/text.html") ?>
                <p>The park is located on the east rift zone of Kilauea, one of the world's most active volcanoes. Because of this, the park's landscape consists mostly of rocks and dried lava, with some lush greenery on the far sides. Low sea cliffs are located along the water's edge. Here, you'll find caves, sea arches and lava tube openings, most of them a result of decades of volcanic activity.</p>
                <p>The park is surrounded by many ironwood trees, so there is plenty of shade. Picnic tables and pavilions are available. If you want to stay overnight, you can get a camping permit from the park office.</p>
                <p>Be sure to check out King's Highway, an ancient Hawaiian trail that takes you through the park on a winding path. It was built back in the days when King Kamehameha the Great ruled the Hawaiian Islands. Along the way, there are several lookout points where you can enjoy great views of the ocean and surrounding landscape. The peak offers a bird's eye view of the coast, clearly defined against the water, making for a truly magnificent picture.</p>
                <p>MacKenzie State Park was named in 1939 after Albert J. W. MacKenzie, a forest ranger on the Big Island who passed away a year before. He was a dedicated forest ranger, replanting many burned areas in the Puna and Ka'u districts. He also planted the ironwood trees in this park.</p>
                <p>Another interesting fact is that MacKenzie State Park is known as a spooky place, especially at night. It is the nightmarchers' territory and eerie sounds and sights have been witnessed (Nightmarchers are the spirits of deceased ancient Hawaiians. Their processions, which include torches and drumming, have been witnessed by quite a few people, especially on full moon nights). </p>
