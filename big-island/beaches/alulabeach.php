                <h2 class="small">Alula Beach, Big Island</h2>
                <p>Alula Beach (<a href="../../big-island/beaches/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located at the entrance to Honokohau Harbor, is a secluded cove with a small white-sand beach dotted with pieces of black lava rock. During times of calm waters, the snorkeling conditions are excellent here. The beach is a popular take-off point for offshore divers and snorkelers. </p>
                <p>At the far side, you will find dark rock walls that plunge 45 feet (13.7 m) into the water, creating a natural shelter for the colorful fish that inhabit the area. These walls also shield the beach from the tides, making it calmer for swimmers and paddlers.</p>
                <p>Alula Beach is part of the Kalolo-Honokohau National Historic Park, a 1160-acre (4.7 sq. km) preserve dedicated to Hawaiian culture and history. On the way to the beach, you will come across old stone houses, a heiau (an ancient Hawaiian temple) and other small beaches. Most visitors actually explore the park before settling on the beach. </p>
