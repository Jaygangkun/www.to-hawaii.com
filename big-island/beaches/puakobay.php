                <h2 class="small">Puako Bay, Big Island</h2>
                <p>Puako Bay (<a href="../../big-island/beaches/p18.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>) on the Big Island's northwestern coast has a rocky shoreline with many tide pools, inlets and coves, which offer good snorkeling conditions when the ocean is calm. A long, but narrow white-sand beach spans almost the entire length of the bay. Due to the nearshore rocky ocean bottom, Puako Bay is not a good area for swimming. The bay is mainly frequented by scuba divers because of the bay's vast reef.</p>
                <p>Puako is also known for being home to the largest collection of petroglyphs in the state. The petroglyphs (ancient rock drawings) depicting life in prehistoric Hawaii hold a deep cultural value. Over 3,000 of these drawings can be found at the <a href="../../big-island/ancientsites/puakopetroglyphs.php">Puako Petroglyph Archaeological Preserve</a>, each one with a story to tell. A trail leads from <a href="../../big-island/beaches/holoholokaibeachpark.php">Holoholokai Beach</a> to the petroglyph site. </p>
