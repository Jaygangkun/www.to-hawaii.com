                <h2 class="small">Ai'opio Beach, Big Island</h2>
                <p>Ai'opio Beach (<a href="../../big-island/beaches/p4.html" onclick="NewWindow(this.href,'name','2550','550','yes');return false">view panorama</a>) on the Big Island of Hawaii is part of the Kaloko-Honokohau National Historic Park, a rich archaeological site just north of Honokohau Harbor. Here you can find artifacts showing how Hawaiians lived in the past, before the time of James Cook and the civilization that followed suit.</p>
                <p>Surrounding the beach is the Ai'opio Fish Trap, a primitive pond designed for catching fish. Built by ancient island villagers, it traps the fish that come in at high tide and get trapped at low tide, so it makes for an easy catch. All that remains today is the dark lava structure around the trap, but it continues to fascinate locals and visitors alike.</p>
                <p>The beach is safe for swimming, even for small children who can play in the shallow tide pools. Offshore reefs are scattered throughout the area, which acts as a buffer for incoming waves and currents.</p>
                <p>Occasionally, turtles will swim up to shore and feed on the limu (seaweed). However, if you see one stay at a distance since sea turtles are protected by law since they are an endangered species and it is not allowed to touch them.</p>
