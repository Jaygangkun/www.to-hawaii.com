                <h2 class="small">Hapuna Beach State Park, Big Island</h2>
                <p>Hapuna Beach Park (<a href="../../big-island/beaches/p13.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  is one of the real gems on the Big Island. <em>Conde Nast Traveller</em>, a prestigious travel magazine, has repeatedly voted it the best beach in the U.S. Golden white sands and clear, deep blue waters make this beach a popular spot for weddings, photo and video shoots and beach parties. </p>
                <p>The white-sand beach is 0.5 miles (800 m) long and about 200 feet (60 m) wide and is surrounded by a beautifully landscaped park, coconut palms and many public facilities. Beaches like Hapuna are rare on the Big Island, where most beaches are made up of lava rock, pebbles, black sand and sea cliffs, instead of soft white sand.</p>
                <p>The ocean bottom drops off gently here. At the northern end of the beach is a shallow, sand-bottomed cove with calm water where novice snorkelers like to mingle. At the southern end of the beach, there are some sea cliffs from where some people like to jump into the ocean. The snorkeling conditions here are also best because there is a small coral reef in this area. But generally speaking, there are better places for snorkeling on the Big Island. Both ends of Hapuna Beach are marked by large rocks that serve as lookout points during sunset. </p>
                <p>Always check the weather conditions before stepping into the water. While the area is generally calmer during the summer months, high surf is common during the winter months, which makes entering the ocean unsafe. Lifeguards are only on duty from time to time, so use the general rule of thumb: &quot;If in doubt, don't go out.&quot;</p>
