                <h2 class="small">Carlsmith Beach Park, Big Island</h2>
                <p>Carlsmith Beach Park (<a href="../../big-island/beaches/more_carlsmithbeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>)  is one of the nicer beaches in the Hilo area. This side of the Big Island is mainly made up of lava rocks, but Carlsmith Beach Park has a sandy ocean bottom.</p> 
                <p>There is no sandy beach, but it&rsquo;s a great place for swimming and snorkeling because the nearshore waters are protected by a reef. The entire area consists of several relatively large lagoons. Freshwater bubbles up from the bottom in several areas, so there are some cooler spots throughout the lagoons. </p>
                <p>Sea turtles can often times be seen here. They are tame and don&rsquo;t seem to mind the people in the water. They sometimes even swim up to snorkelers instead of away from them. The beach park has a spacious grassy area with trees, so there is plenty of room for a picnic. </p>
