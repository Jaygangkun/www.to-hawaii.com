                <h2 class="small">White Sands Beach Park (La'aloa Beach Park), Big Island</h2>
                <p>Even though this beach is on the small side, it's got four names: White Sands Beach, La'aloa Beach Park, Magic Sands and Disappearing Sands (<a href="../../big-island/beaches/p25.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>). During times of high surf (usually in the winter months), an interesting natural phenomenon occurs here. The shorebreak erodes the small pocket of white-sand beach very quickly, often times within 24 hours, washing away all sand and leaving only exposed lava rock. </p>
                <p>That's why this beach is also called Disappearing Sands because it quite literally vanishes. When the wave action ceases, the regular ocean currents slowly move the sand back. However, this can take a few months. But this periodic and complete flushing of the sand keeps it very white. </p>
                <p>Other than that, White Sands Beach is not particularly impressive. The Big Island is known for beaches with colors other than white, which makes a small white-sand beach like this one special. It is located next to a street in a busy area and surrounded by buildings. But it is easy to reach as it is located just south of Kailua-Kona. On the shore are a few coconut palms, and lava rocks frame both ends of the beach.</p>
                <? include ("../../theme/text.html") ?>
                <p>The bodyboarding and surfing conditions are often times great, so when the surf is up you&rsquo;ll see many people in the water here. In fact, this beach is one of the most popular bodyboarding locations in the area. The nearshore ocean bottom is mainly sandy and slopes gradually. The swimming conditions are good when the waves are not too high, but during rough ocean conditions, the strong shorebreak is dangerous and a strong rip current can pull swimmers into the open ocean. </p>
