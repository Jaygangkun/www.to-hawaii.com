                <h2 class="small">Kahalu'u Beach Park, Big Island</h2>
                <p>The Big Island has some outstanding underwater scenery, but Kahalu'u Beach Park (<a href="../../big-island/beaches/p24.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>) on the island's west coast just south of Kailua-Kona stands out in particular. The fish here aren't at all wary of the swimmers, as they tend to be in other areas. Indeed, if you hang around long enough, some of them will come up to you and swim by your side. </p> 
                <p>Over 100 species of fish can be observed here. The beach is one of the best snorkeling spots on the Big Island. In fact, many tourist maps refer to Kahalu'u as &ldquo;Snorkel Beach.&rdquo; Not only humans and colorful tropical fish love Kahalu'u Beach, but sea turtles as well. You&rsquo;ll probably be able to see some in the water here.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>The cove is ideal for novice swimmer and snorkelers as the water is almost always calm (except when there's a big storm or ocean swell coming in). That's because the beach is protected by a partially submerged rock wall, which keeps strong currents and larger waves out. </p>
                <p>Also, the water is very shallow in the cover, around neck deep in most spots and around 10 feet (3 m) in the deepest area. This is probably one of the reasons why the fish are so plentiful here. The sunlight can reach all the way to the bottom, which grows healthy corals and seaweed. The ocean bottom at Kahalu'u Beach Park has a mix of lava rock, corals and patches of sand. A little bit inland, many Hawaiian house ruins, heiaus and petroglyphs are located in the area, between the park and Keauhou Bay.</p>
                <p>The shoreline consists of white sand speckled with black lava fragments. It's best to get into the water via these sandy areas. During low tide, a few shallow tide pools surface, which can also be explored. During times of high surf, experienced bodyboarders and surfers venture out to the seaward edge of the reef in the outer area of the bay. A strong rip current may form during strong ocean swells in the area north from Kuemanu Heiau along the rugged shoreline rocks. </p>
