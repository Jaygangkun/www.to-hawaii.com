                <h2 class="small">Pine Trees Beach, Big Island</h2>
                <p>Unlike its name, you won't find any pines on this beach &ndash; it was named by clueless surfers who thought the nearby mangroves were pine trees. Pine Trees Beach is a popular place among local surfers and one of the best barbecue sites on the Big Island's Kona Coast. </p> 
                <p>The beach is mainly visited by local residents because it's not that easy to get to it. Access is via a rough, unpaved, 4-wheel drive road. It can be crowded on weekends, but during the week, you may have it almost to yourself. Locals often camp out here, but you&rsquo;ll need a permit to stay overnight. You can get one from the beach officials who usually set up a tent on the beach. </p>
                <p>The swimming conditions are poor because the ocean bottom is rocky. It is mainly surfers who go into the water. But the beach has white sand, so it's a good place for a barbecue and to relax. When the water is calm, the deeper offshore areas are sometimes visited by scuba divers, who come here on boat. The area offers good cave-diving opportunities.</p>
