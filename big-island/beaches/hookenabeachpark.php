                <h2 class="small">Ho'okena Beach Park, Big Island</h2>
                <p>Ho'okena Beach Park, located on the Big Island's west shore, was once an important inter-island steamship landing site. In the early 1880s, Ho'okena grew into a busy trading village. It had a wharf, a school, courthouse and even a jail. Eventually, cars and trucks replaced steamers and by the mid-1930s, storms and high surf had destroyed the landing at Ho'okena and most villagers moved away from the shoreline and closer to the highway further inland.</p> 
                <p>Ho'okena Beach is located at the northern end of Kauhako Bay, which is bordered by sea cliffs. The beach itself has fine, black detrital sand mixed with white sand, giving it a gray color. The shoreline is rocky, but there are a few sandy entry and exit points to get in and out of the water. The waters are generally calm here, but strong currents can occur during times of high surf. </p>
