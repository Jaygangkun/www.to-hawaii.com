                <h2 class="small">Makaiwa Bay, Big Island</h2>
                <p>Makaiwa Bay  (<a href="../../big-island/beaches/p22.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a beautiful sandy cove located south of the Mauna Lani Bay Hotel and Mauna Lani Terrace condos. The swimming and snorkeling conditions are good when the ocean is calm. You'll be able to see quite a few fish in the nearshore waters. The beach is surrounded by luxury accommodations. </p>
                <p>If you're not staying in the area, there is a public parking lot, but it's about a 10 to 15 minute walk on a paved path through a lava field and along the Mauna Lani fishponds (<a href="../../big-island/ancientsites/kalahuipuaa.php">Kalahuipua'a</a>) to get to the beach. It is a scenic trail. First you'll walk through an interesting lava landscape and then right along the ancient fishpond and along the shoreline, where you'll also see a fishtrap. </p>
