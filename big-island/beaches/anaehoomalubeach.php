                <h2 class="small">Anaeho'omalu Beach, Big Island</h2>
                <p>Anaeho'omalu Bay (also known as A-Bay)  (<a href="../../big-island/beaches/more_anaehoomalubeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../big-island/beaches/p16.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located on the Big Island of Hawaii's west shore near the Waikoloa Beach Marriott Resort Resort, is one of the prettiest beaches in this area. It features a long white-sand beach, several tide pools, fishponds and a large grove of coconut palms. You'll also find it better equipped than its neighbors, with restrooms, showers and a parking lot conveniently located nearby.</p>
                <p>Like many of the Big Island beaches, Anaeho'omalu is protected by an offshore reef. The ocean bottom drops off gently here, and the beach is popular for a variety of ocean sports, including swimming, snorkeling, windsurfing, scuba diving and surfing. Dangerous ocean conditions may occur during storms or high surf.</p>
                <? include ("../../theme/text.html") ?>
                <p>Located just behind the beach are  two ancient Hawaiian fishponds, named Ku'uali'i and Kahapapa (Ku'uali'i is the larger of the two). The ponds were once used by Hawaiian royalty to raise mullet and other small fish. They were maintained by the fishermen who lived in this area, and the fish that were raised here were only given to royal people. That's probably why this beach was named Anaeho'omalu, which means &quot;restricted mullet&quot; in the Hawaiian language. </p>
                <p>The area is surrounded by educational plaques offering interesting tidbits of Hawaiian history. Now a favorite among photographers, the ponds boast interesting colors and look simply spectacular during sunset. </p>
                <p>If you'd like to explore a bit, you can hike the nearby King's Trail, which leads through a 2-acre (8,094 sq. m) historical park featuring rock shelters, petroglyphs (rock carvings) and other ancient artifacts. Another trail will take you to the Hilton Waikoloa and other luxury hotels. </p>
                <p>In March 2011, Anaeho'omalu Bay was damaged by a tsunami wave that was caused by the devastating earthquake in Japan. The tsunami literally sliced Anaeho'omalu Bay into two pieces and broke the wall of the ancient Ku'uali'i Fishpond. Up until today, repair work is still in progress (<a href="../../big-island/beaches/more_anaehoomalubeach_photos-construction.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see photos</a>). The 100-foot gaping hole in the beach has been fixed, but the ancient rock wall has yet to be restored. This damage has also had a profound effect on the fishpond. The water isn't as clear as it used to be due to rampant algae growth, and there are fewer fish in it.</p>
