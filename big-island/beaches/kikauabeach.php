                <h2 class="small">Kikaua Beach, Big Island</h2>
                <p>This small man-made cove (<a href="../../big-island/beaches/p5.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) has a pretty white-sand beach. The sandy ocean bottom within the cove is ideal for small children. The cove is surrounded by lava rocks which protect the nearshore waters from waves and currents. There are some palm trees on the beach which provide shade, and the beach is easily accessible via a 5-minute walk on a paved path. The beach closes a half hour after sunset.</p>
