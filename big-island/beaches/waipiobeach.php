                <h2 class="small">Waipi'o Beach, Big Island</h2>
                <p><a href="../../big-island/attractions/waipio-valley.php">Waipi'o Valley</a> has been called the Valley of the Gods and one look at this secluded place is all it takes to see why. The lush valley is home to ancient sacred temples, tropical rainforests and tall sea cliffs rising 2,000 feet (610 m). Indeed, few places on the island can match the spiritual energy and mystical charm of Waipi'o. It is one of the most popular and beloved locations in all of Hawaii.</p> 
                <p>Access to Waipi'o Beach is somewhat difficult, as the road to the valley is narrow and very steep. It is one mile (1.6 km) long and descends 1,000 feet (305 m). It hugs the cliff and even though it is paved, one needs a four-wheel drive vehicle to navigate it. You may want to consider walking it instead of driving because once you're on your way in your car, it is impossible to turn around in case you change your mind. The road begins at the Waipi'o Valley Lookout.</p>
                                <? include ("../../theme/text.html") ?>
                <p>A black-sand beach lines almost the entire length of the shoreline of the valley. Even though the beach looks beautiful, entering the ocean here can be dangerous due to rip currents and high surf, especially during the winter months. </p>
                <p>There are a few accessible waterfalls to the south, such as Kaluahine Falls and Waiulili Falls. The valley itself is hardly populated, but signposts have been put up to mark the sacred and private areas. You may also come across some wild horses who live in the valley. </p>
                <p>The trail to the beach is public, but much of the valley is private property. Hikers who plan to explore the valley should keep this in mind and ask for permission. For this reason it is best to explore the valley with a guided tour.</p>
                <h2 class="big">Waipi'o Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Remote black-sand beach in beautiful Waipi'o Valley</li>
                  <li>Difficult to access, but the views are worth it</li>
                  <li>Many waterfalls are located in this lush, tropical valley</li>
                  <li>It is not recommended to enter the ocean here due to dangerous conditions</li>
                  <li>Horseback riding and wagon tours through the valley are being offered</li>
                  <li>It is a good idea to take a guided tour into Waipio Valley because like that, you are led to the best spots and don't have to worry about intruding in private and sacred areas</li>
                  <li>Hike from lookout to Waipi'o Valley takes about 30-45 minutes (1 mile, one-way)</li>
                  <li>25% grade road with no shade, so bring plenty of water</li>
