                <h2 class="small">Kolekole Beach Park, Big Island</h2>
                <p>Located about 10.5 miles (16.9 km) north of Hilo, Kolekole (<a href="../../big-island/beaches/more_kolekolebeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a popular beach park among residents in the Hilo area, mainly because of its beautiful scenery, spacious grassy areas and  facilities. On the weekends and on holidays, it is often times crowded with picnickers. </p> 
                <p>The ocean conditions are too rough and dangerous for swimming, but many people use the Kolekole Stream as a swimming pool. The stream is fed by Akaka Falls, a popular waterfall located about 4 miles (6.4 km) above Honomu, a nearby town. Another smaller waterfall flows into Kolekole Stream near the ocean, completing the tropical scenery of this beach park. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>Kolekole Beach is made up of black sand and rocks and is located below a 100 feet (30 m) tall highway bridge. The beach is small and during rough ocean conditions, high surf often times washes over the beach and into the stream. It is best to avoid swimming at the mouth of the stream due to strong currents in this area.</p>
                <p>The bridge was once used by the Hawaii Consolidated Railway. In 1946, a tsunami with waves of 37 feet (11 m) above sea level rushed onto the shoreline here and destroyed the girders from the middle part of the steel bridge, which destabilized it. Because many other parts of this part of the Big Island's shoreline were destroyed, the railroad went out of business. The county later purchased the bridge and rebuilt it.</p>
