                <h2 class="small">Onomea Bay, Big Island</h2>
                <p>Onomea Bay (<a href="../../big-island/beaches/more_onomeabay_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>)  is one of the lushest areas on the Big Island. The Hawaii Tropical Botanical Garden is located right on this bay and features waterfalls, streams and a raised wooden boardwalk leading to the ocean.</p> 
                <p>The bay is located just a short drive north of Hilo and is considered by many to be an area not to be missed. Past mile marker 7, the 4-mile (6.4 km) scenic drive on the Old Mamalahoa Highway begins. Make a stop at the botanical garden and do a self-guided tour. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>You&rsquo;ll come across over 2,000 exotic types of flora. The lush garden is also home to many birds, and you&rsquo;ll be breathing air that is filled with the fragrance of tropical flowers and fruits. Onomea Falls is located on the grounds as well. </p>
                <p>If you don't want to pay the entrance fee to the garden to hike down to the bay, you can see Onomea Bay from a lookout (located along Mamalahoa Highway).</p>
