                <h2 class="small">Hakalau Bay, Big Island</h2>
                <p>Located on the Big Island&rsquo;s eastern shore, about 13 miles (21 km) north of Hilo, Hakalau Bay is mainly a place to go to take pictures rather than go swimming or snorkeling. The small bay has deep offshore waters and dangerous water conditions develop during times of high surf. </p> 
                <p>The ocean bottom drops off quickly here and rip currents often times run from the bay into the open ocean. However, the bay is sometimes used by surfers. At the head of the bay, Hakalau Stream flows into the ocean, so the inshore waters are always murky. The beach is composed of black sand and fine pebbles.</p>
                <p>The town of Hakalau was once home to a thriving sugar industry and an important stop for local transport. Remnants of its prosperous past can still be seen in the old railroad bridge that leads to the bay and the long-defunct plantation towers you can see from the road. Also located on the shoreline are the ruins of Hakalau Mill, which was destroyed in the tsunami of 1946.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>If you&rsquo;d rather stay out of the water, you can check out the nearby Hakalau Forest National Wildlife Refuge. This 33,000-acre (134 sq. km) area is home to a large collection of native flora and fauna. Some of them are extremely rare and can only be found here. The park is also the first park built in the U.S. solely for the protection of forest birds. </p>
