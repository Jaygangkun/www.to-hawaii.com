                <h2 class="small">Laupahoehoe Point Beach Park, Big Island</h2>
                <p>Located on the Big Island&rsquo;s Hamakua Coast (east shore), Laupahoehoe Point (<a href="../../big-island/beaches/more_laupahoehoepoint_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a peninsula covered with coconut palms, grass and black lava rocks on the shoreline, which contrast beautifully with the deep blue ocean. On a sunny day, the ocean is particularly blue here. </p> 
                <p>Laupahoehoe is known for its scenic views, but it also holds a tragic past. It was here where a tsunami killed 19 schoolchildren and 5 adults on April Fool&rsquo;s Day in 1946. The names and ages of the victims were engraved on a rock, which serves as a memorial in the park. The village was later relocated further inland to avoid another tragedy.</p>
                <p>Laupahoehoe Park is a nice spot for a picnic and picture taking, but when it comes to water activities, the options are limited. Other than exploring the nearshore tidepools, the ocean is too furious and dangerous here for swimming. When you&rsquo;re out on the peninsula, you can often times see the waves crash against the  lava rocks. </p>
