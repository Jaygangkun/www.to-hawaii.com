                <h2 class="small">Kealakekua Bay, Big Island</h2>
                <p>Few other spots have as much historical meaning as Kealakekua Bay  (<a href="../../big-island/beaches/p9.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  on the Big Island's Kona Coast. This is where Captain James Cook (the British explorer who discovered the Hawaiian Islands in 1778) docked during his second voyage to the Hawaiian Islands in January 1779, and where he was tragically killed a month later in a conflict between his crew and the Hawaiians. In 1878, a 27-foot white obelisk was erected in his honor near the location where he was killed.</p>
                <p>But for most visitors, the real treasures are the coral reefs that slope beautifully from the shore into the water. The bay was declared a Marine Life Conservation District in 1969 as the coral reef is biologically significant. The reef is home to a large variety of fish and other sea creatures, many of which swarm close to shore. Needless to say, Kealakekua Bay is a favorite among snorkelers and scuba divers.</p>
<? include ("../../theme/text.html") ?>
                <p>At the northern coastline is a sheer cliff (Pali-kapu-o-Keoua). On the cliff's face, many lava tube openings are visible, some of which are ancient Hawaiian burial caves. Within the bay, the Napo'opo'o area is shallow and has little coral, but the diving conditions improve farther out. The area between the Napo'opo'o landing and the southern tip of Manini Beach Point is the most beautiful in this area, featuring caves, crevices, ledges and spectacular corals in waters up to about 30 feet (9 m) deep.</p>
                <p>On the bay's northeastern side below the cliffs, the snorkeling conditions are good on calm days. There water here is about 5 feet (1.5 m) deep and is home to many corals and a variety of marine animals. </p>
                <p>The best diving can be done in Ka'awaloa Cove (near the Cook Monument) where depths range from about 5 to 120 feet (1.5 to 36.5 m). Here, the diversity of coral and fish is exceptional, and the fish are quite tame.</p>
                <p>Kayaking is also a popular activity. The bay stretches out for miles, and the water is calm and clear for most of the year. If you're lucky, you may even spot the curious spinner dolphins that visit the bay occasionally. A more common sight is the green sea turtle, a regular visitor at many Big Island beaches.</p>
                <p>To add to its historic charm, Kealakekua Bay maintains a mystic atmosphere that leaves visitors enthralled. Access to the beach is only via water or a long, rough footpath, which keeps it quiet and uncrowded on most days. Kealakekua Bay is about 1 mile (1.6 km) wide.</p>
                <p>The bay was settled over a thousand years ago, and there are many archaeological and historical sites in the surrounding area, such as heiaus (Hawaiian temples). The bay was listed in the National Register of Historic Places in 1973 as the Kealakekua Bay Historical District.</p>
                <p>Since this is a Marine Life Conservation District, the following activities are prohibited:</p>
                <p>To fish for, take, or injure marine life (including eggs).<br />
                  To take or alter any sand, coral, or other geological feature or specimen.<br />
                  To engage or attempt to engage in fish feeding.<br />
                  Note: anchoring of boats is prohibited in Subzone A. In Subzone B, anchors may only be dropped onto sand or in such a way as to avoid damage to coral.</p>
