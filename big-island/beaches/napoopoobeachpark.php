                <h2 class="small">Napo'opo'o Beach Park, Big Island</h2>
                <p>A quaint historical charm surrounds the rugged landscape of Napo'opo'o Beach Park, a cultural gem on the Big Island's west shore, located at the southern end of Kealakekua Bay. This is where Captain James Cook first set foot on the Big Island of Hawaii on January 17, 1779, and where the first Christian service was held by his crew. </p> 
                <p>Just across the bay is the exact spot where he was killed less than a month later (today a 27-foot white obelisk is standing near the spot where he was killed, which can be seen from Napo'opo'o Beach Park). Every step on this beach is a walk through time, and every stone has a story to tell.</p>
                <p>One of the highlights of this beach is the Hiki'au Heiau (ancient Hawaiian temple site), a temple dedicated to Lono, the god of agriculture and fertility of the land. When Cook arrived here in 1779, the natives initially thought he was Lono and that the large ships were newly formed islands. The heiau is accessible via a trail located behind the beach.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>The shoreline is rocky, but walkable. What used to be a sandy beach has eroded over time. Now, mainly boulders make up the beach. Snorkeling at Napo'opo'o is good when the water is calm and clear. Like most beaches in the area, there is some interesting marine wildlife to observe, including green sea turtles. If you're lucky, you might spot some of them resting on the shore.</p>
                <p>Napo'opo'o is also a popular spot for picnics and barbecues. There's plenty of shade in this 6-acre (24,281 sq. m) beach park, and you can find grills and tables throughout the park. </p>
                <p>Update: The entire Napo'opo'o area were hit hard on March 11, 2011, when a tsunami (which was generated by the magnitude 9.0 earthquake in Japan) washed ashore here and caused extensive damage to many of the beach homes. One of the homes a little further south from here was lifted up and sucked out to sea.</p>
