                <h2 class="small">Road to the Sea Black Sand Beach, Big Island</h2>
                <p>The Road to the Sea, located on the Big Island's southwestern coast, is one that's hardly traveled, and it's easy to see why. The six-mile (9.7 km) trail is dotted with sharp lava rocks, surrounded by steep ledges and riddled with cracks and potholes. But those who make it here are well-rewarded, for at the end of this path lie a few black and green sand beaches that are surrounded by nothing but black lava rocks. On a sunny day, the coast looks picture-perfect with its greenish sand, deep blue waters and the surrounding black cliffs.</p> 
                <p>But getting here is definitely an adventure in itself. The first 3 miles (4.8 km) of the road are doable even in a regular car, but after that the road is rough and steep in places, so a four-wheel drive vehicle is necessary to navigate it (or you can park here and hike the final 3 miles to the beach).</p>
                <? include ("../../theme/text.html") ?>
                <p>It's commonly called the Road to the Sea Black Sand Beach, but there are actually two beaches at the end of the road. Neither has an official name, but both are known for their black and green sands. The green comes from crushed olivine crystals, a less-polished variety of peridot. It is a semi-precious gemstone, brought to the surface by past volcanic activity and the erosive force of the ocean. The ancient Hawaiians considered olivine (green sand) to be the tears of the volcano goddess Pele, and they used the green sand in healing ceremonies. The sand pieces are too small to be considered gemstones, but occasionally a lucky beachgoer finds a bigger piece.</p>
                <p>The two beaches are fairly small, between 50 and 100 feet (15-30 m) long. The green sand has a slightly darker hue here than at <a href="../beaches/papakoleagreensandbeach.php">Papakolea Beach</a> (near South Point), which is the most popular green-sand beach on the Big Island. There are no lifeguards in this area, so if you want to swim, snorkel or surf, keep in mind that the ocean bottom drops off quickly and strong currents can be present, especially during times of high surf. Fishing is a popular activity here, especially in the spring and summer when the fish swim closer to shore. The main catch is ulua. </p>
