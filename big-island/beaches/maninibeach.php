                <h2 class="small">Manini Beach, Big Island</h2>
                <p>Located on the Big Island's west shore just south of popular Kealakekua Bay, Manini Beach has a rocky shoreline consisting of white coral rubble and black lava rocks. The beach wraps around the north point of Kahauloa Bay, and the area behind it belongs to Napo'opo'o Park.</p> 
                <p>The surfing conditions are sometimes good here, especially if the waves break far enough out so that surfers can safely navigate away from the nearby rocks. Other water activities that are possible include snorkeling and scuba diving. There is a small sandy channel that cuts through the reef and allows for easy access into the ocean. </p>
                <p>The best underwater visibility can be found to the right of the channel. During rough ocean conditions, it is best to stay out of the water because it can get quite turbulent and this is when underwater visibility is diminished as well.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>For those who prefer to stay out of the water, Manini Beach offers nice views of the Kealakekua cliffs and the Captain Cook monument. The beach is a nice picnic spot as well and picnic tables are available. The park has grassy areas and trees that provide shade. </p>
                <p>Manini Beach and the entire Napo'opo'o area were hit hard on March 11, 2011, when a tsunami (which was generated by the magnitude 9.0 earthquake in Japan) washed ashore here and caused extensive damage to many of the beach homes. One of the homes next to Manini Beach was lifted up and sucked out to sea.</p>
