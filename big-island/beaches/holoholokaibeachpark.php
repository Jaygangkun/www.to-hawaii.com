                <h2 class="small">Holoholokai Beach Park, Big Island</h2>
                <p>Holoholokai Beach Park (<a href="../../big-island/beaches/p7.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on the Big Island is not your typical picture-perfect white-sand beach. This rocky strip of coastline boasts something completely different, but just as fascinating: a walk through prehistoric Hawaii, told in the words of ancient art. The beach is next to the Malama Petroglyph Trail leading to the <a href="../../big-island/ancientsites/puakopetroglyphs.php">Puako Petroglyphs</a>, the largest collection of petroglyphs in Hawaii. With a good eye, you can spot more than 3,000 of these age-old drawings etched onto larger-than-life boulders.</p>
                <p>Of course, the beach itself is not to be missed. Many  visitors to the trail also visit this beach to relax, picnic or take pictures. There is more rock than sand on Holoholokai Beach, but the black lava rocks mixed with white coral rubble make for interesting beach scenery. It's not a good beach for swimming because the nearshore ocean bottom is rocky, which makes entering the ocean difficult. Besides the rock outcroppings, the occasionally high surf can make it dangerous even for experienced swimmers. </p>
                <p>In the park area you&rsquo;ll find picnic tables, BBQ grills, lots of  trees and grassy areas for ball games. Restrooms and showers are also conveniently located in the park.</p>
