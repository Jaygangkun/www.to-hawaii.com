                <h2 class="small">Ahalanui Beach Park, Big Island</h2>
                <p><em style="color:#C30"><strong>Editor's Note:</strong> Due to a recent   lava flow that occured on June 5, 2018, this beautiful destination was   unfortunately destroyed. We are leaving this page up for all of us who   have made memories here and would like to remember it as it used to be.</em></p>
                <p>Located on the Big Island&rsquo;s east shore near Pahoa, Ahalanui Park (<a href="../../big-island/beaches/more_ahalanuipark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) (also known as Pualaa County Park) offers a bathing experience that is unique in the Hawaiian Islands. Here you can have a bath in a spring-fed, walled-in pool, which is volcanically heated to a comfortable 90&deg;F (32&deg;C). On some days though the water is a bit cooler than that.</p> 
<p>The part natural part man-made pool is located right next to the ocean. It is connected to it by a small inlet, which also allows small tropical fish to enter the pool. The pool water is brackish, but crystal clear, and the bottom of the pool is a combination of sand and mud, with a slight sulfur smell. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>Ahalanui Park is a good place for novice swimmers since the water is shallow and there is even a lifeguard. A few access ladders make getting into the pool easy. It is a beautiful spot, surrounded by palm trees, grass and shady areas, ideal for picnics. </p>
                <p><strong>Note:</strong> Avoid swimming at Ahalanui Pond with an open wound or cut. The pond was closed to the public on September 26, 2013, after a man contracted an infection after swimming in the pond with an open wound. The County reopened Ahalanui Park in early October after water testing by the state Department of Health showed bacterial levels safe for swimming. According to the Health Department, there was no evidence to suggest the pond water being contaminated. The Hawaii Department of Health recommends keeping open wounds clean, dry and covered to prevent infection, which can come from anywhere, including from bacteria that live naturally on the human body.</p>
