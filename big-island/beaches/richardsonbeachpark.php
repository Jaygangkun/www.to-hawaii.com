                <h2 class="small">Richardson Beach Park, Big Island</h2>
                <p>Also known as Richardson Ocean Center, this park has calm waters, excellent snorkeling conditions, lots of shade and several tide pools where children can play away from the surf. The water is accessible through a small black-sand beach at the edge of the park. </p> 
                <p>From here, you can go swimming, surfing or kayaking, depending on the weather. Natural seawalls made out of lava create natural pools and coves, which keeps the water calm. There&rsquo;s usually some good surf further offshore, although beginners are not advised to go too far. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>Snorkeling is one of the most popular activities here, especially in the summer season. When you get in, you&rsquo;ll be able to observe an abundance of tropical fish that live in and around the coral reef. If you&rsquo;re lucky, you may even spot a green sea turtle. </p>
                <p>Richardson Beach got its name from the home that stands on this beach. It was built by Elsa and George Richardson. The beach is a public park and the building is operated as the Richardson Ocean Center. </p>
