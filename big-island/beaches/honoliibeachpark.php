                <h2 class="small">Honoli'i Beach Park, Big Island</h2>
                <p>Located on the Big Island's east coast just north of Hilo, Honoli'i Beach Park is popular among the local surf crowd. Unlike most surfing beaches on the Big Island, Honoli'i sees action all year round. In the summer, the waves are relatively calm and ideal for beginning surfers. Winter is the season for the pros, where the surf gets higher and more challenging. A county lifeguard is on duty to keep an eye on the surfers. </p> 
                <p>Rip currents are common on this beach, and the nearshore ocean bottom drops off quickly, so Honoli'i is mainly frequented by surfers, rather than swimmers. The beach itself is a mix of black sand, coral rubble, rocks and beach glass. Fishermen also frequent this beach, as do spectators, who often stand on the road above to watch the surfers. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>The shore is a nice spot for picnics and relaxation. A dense grove of native trees grows along the area, providing lots of shade and privacy. The trees are lined up beside a small stream, a lesser-known but equally beautiful attraction. </p>
                <p>At the northern end of Honoli'i Beach, there is a pond at the mouth of the stream. The pond is a better area to swim than the rest of the beach itself, however, it is quite deep and drops off abruptly. </p>
