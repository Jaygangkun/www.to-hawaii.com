                <h2 class="small">Honoka'ope Beach, Big Island</h2>
                <p>Located at the south end of the Mauna Lani Resort area, Honoka'ope Beach (<a href="../../big-island/beaches/p21.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a small salt-and-pepper beach that's only sparsely visited. The beach is backed by several luxury homes, and access to Honoka'ope requires getting a beach access pass at the gate (there are 20 parking spaces).</p>
                <p>When the ocean is calm, snorkeling is possible here, and since the other resort beaches in this area are sandy, Honoka'ope makes for an interesting change. From Honoka'ope Beach, a trail leads north along the lava cliffs to 49 Black Sand Beach.  </p>
