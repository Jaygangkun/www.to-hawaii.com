                <h2 class="small">Pauoa Bay, Big Island</h2>
                <p>From the shore, Pauoa Bay (<a href="../../big-island/beaches/p8.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) looks  like a small pond with the crescent-shaped lava walls protecting the calm blue waters. Palm trees line the coast and rugged greenery grows on the large rocks that surround the bay.</p>
                <p>The natural seawalls keep out high surf, making the bay calm and perfect for swimming and snorkeling. Experienced divers and surfers sometimes venture beyond the walls to get some wave action, but most people prefer to stay in the safe zone. On calm days, you can also go rafting or boating around the bay.</p>
                <p>Pauoa Bay is home to one of the healthiest coral reefs in Hawaii, thanks mostly to conservation efforts by the University of Hawaii. Here you'll find the beloved honu (Hawaiian green sea turtle) and a large population of reef fish, including eels, rays, puffer fish and butterflyfish. In the winter, you may even spot a few dolphins and whales.</p>
                <p>Pauoa Beach is located in front of the Fairmont Orchid at Mauna Lani Resort. The resort unofficially doubles as a parking lot for its beachgoing patrons.  </p>
