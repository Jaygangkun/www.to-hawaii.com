                <h2 class="small">Honokohau Beach, Big Island</h2>
                <p>Honokohau Beach, located on the Big Island&rsquo;s west shore, is a long salt-and-pepper sand beach, with fragments of ground-up seashells, coral and lava rock. There&rsquo;s a low lava shelf at the water&rsquo;s edge that lines most of the shore. The nearshore ocean bottom is shallow and rocky, and an offshore fringing reef shelters the beach from strong surf and currents. This is a good place for snorkeling.</p> 
                <p>A little inland near the northern end of the beach, there is a trail that leads across a lava field to a brackish-water pool, known as Queen&rsquo;s Bath. It&rsquo;s a spring-fed lava pool that some people use to rinse off after a swim in the ocean. To get here from the northern end of Honokohau Beach, walk approximately 600 feet (180 m) north until you reach a rock wall. From here, walk toward the mountains (toward the rock mounds) until you reach the pool, which is located right behind the rock mounds.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>Honokohau is one of the many beaches that form the Kalolo-Honokohau National Historic Park. This 1160-acre (4.7 sq. km) park boasts some of the best archaeological sites and artifacts in the state, including ancient petroglyphs (rock carvings), temples, burial sites, trails, house platforms and three fishponds &ndash; Aiopio, Aimakapa and Kaloko &ndash; all of which is evidence that this area once supported a Hawaiian settlement of several hundred people.</p>
