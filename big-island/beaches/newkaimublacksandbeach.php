                <h2 class="small">New Kaimu Black Sand Beach, Big Island</h2>
                <p>As the name of this beach implies, it is new. In fact, the land that this beach encompasses and the area surrounding it didn't exist a mere 20 years ago. In 1990, a lava flow from nearby Kilauea volcano reached the shoreline at Kalapana and destroyed this entire community along with the neighboring subdivisions of Kaimu and Royal Gardens. </p>
                <p>Also buried under 50-75 feet (15-23 m) of lava was Kaimu Black Sand Beach, a beach that was a favorite among local residents and visitors alike because of its fine, jet black sand and stately coconut palms lining the shoreline. In an effort to bring Kaimu Black Sand Beach back to its former glory, new palm trees have been planted on this brand new shoreline land. A small new black sand beach is located near where the old Kaimu Black Sand Beach used to be. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>To reach it, drive Kapoho-Kalapana Road or Pahoa-Kalapana Road (Hwy 137 or Hwy 130) all the way to the end. There is a small parking lot near the Kalapana Village Caf&eacute;. From here, walk up the small hill where you'll see a sign that says &ldquo;Kaimu Beach Eco-Path.&rdquo; Follow the dirt trail that leads through the lava field. It's about a 5-10 minute walk to the ocean and the beach.</p>
                <p>There are no facilities out here, and water activities, including swimming, are not possible due to strong currents and hazardous surf. The beach is just nice to look at and imagine how it will change over the years as all the newly planted coconut palms reach full height. </p>
                <h2 class="big">New Kaimu Black Sand Beach Overview</h2>
