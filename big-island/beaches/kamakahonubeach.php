                <h2 class="small">Kamakahonu Beach, Big Island</h2>
                <p>For a beach once inhabited by a king, Kamakahonu Beach (<a href="../../big-island/beaches/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on the Big Island of Hawaii is surprisingly calm and unflustered. Locals have nicknamed it &quot;Children&rsquo;s Beach&quot; because of the gentle waves and shallow water. It is one of the safest swimming beaches in Kona. Nearby is the King Kamehameha Kona Beach Hotel, a popular place among vacationing families. </p>
                <p>Kamakahonu has a lot to offer for adults as well. It is an important historical site, having served as the final home of King Kamehameha the Great, who lived here from 1812 until his death in 1819. You can still find many of his old projects around; most of them are buildings and temples where he held office. His personal temple, the <a href="../../big-island/ancientsites/ahuena-heiau.php">Ahuena Heiau</a>, was built in the water and now sits a few yards off the shore.</p>
                <? include ("../../theme/text.html") ?>
                <p>Besides swimming, boating, snorkeling and fishing are also very popular activities here. Off the Kailua Pier, there are some good fishing spots. Also, you can rent canoes, paddle boats and fishing and snorkeling gear from the many beach shops on the shore.</p>
                <p>Kamakahonu has many beach facilities, such as restrooms, showers and BBQ areas. It&rsquo;s a good beach for a picnic since there are shaded picnic spots along the shore. There are also a few fancy restaurants and concessions in the area, offering a nice mix of local and international cuisine.</p>
                <p>The name Kamakahonu means &quot;the turtle eye&quot; in the Hawaiian language. It is believed that the name originated from a rock, which was shaped like a turtle and which is now located under Kailua Pier. </p>
