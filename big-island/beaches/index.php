              <p><a href="../../map-bigisland-beaches.html" target="_blank"><img src="../../theme/images/bigislandbeaches.jpg" alt="Big Island Beach Map" align="right" border="0" style="margin-left:10px;" /></a>The Big Island of Hawaii has a coastline of about 266 miles (428 km). Big Island beaches are the most diverse in all of Hawaii. Here you can find black sand beaches, red sand beaches, golden sand beaches and even green sand beaches. Most beaches, however, are rocky (with lava rocks and pebbles) and have an offshore reef, which makes for poor swimming conditions. Read more about Hawaiian <a href="../../hawaiibeachsand.php">beach sand composition</a>.</p>
              <p>The best sandy swimming beaches are located on the Kona Coast and Kohala Coast. This is where many hotels are located and where most visitors like to stay for their Big Island vacation. Snorkeling and diving locations on the Big Island are plentiful. One prime spot for example is Kealakekua Bay. And then there are several tide pool locations, such as the Kapoho Tide Pools, which are like an outdoor Jacuzzi - with the only difference that they are volcanically heated.</p>
