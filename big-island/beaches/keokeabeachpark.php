                <h2 class="small">Keokea Beach Park, Big Island</h2>
                <p>Located on the Big Island's rocky North Kohala shoreline, Keokea Beach is a boulder beach backed by sea cliffs. The swimming conditions are rather poor here because the bay is not protected by offshore reefs, so the surf is strong and the water rough on most days. Local residents have built a breakwater of boulders to improve the cove.</p>
                <p>There is a small stream in the park area where children can play, but make sure to keep an eye out so they don't stray off to the rocks. What it lacks in the water, Keokea makes up for on the coast. This beach has some of the best facilities in the area, including picnic tables, pavilions, fire pits, barbecue grills, restrooms and showers. The beach is a popular picnicking spot and gets crowded during the weekends and holidays. </p>
