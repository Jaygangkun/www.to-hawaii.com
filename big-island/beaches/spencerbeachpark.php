                <h2 class="small">Spencer Beach Park, Big Island</h2>
                <p>Located south of Kawaihae Harbor on the Big Island's northwestern coast, Spencer Beach Park (<a href="../../big-island/beaches/more_spencerbeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../big-island/beaches/p12.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) offers excellent swimming and snorkeling conditions, ample shade under large native trees and a calm, friendly atmosphere. The park is named after Samuel M. Spencer, former chairman of the Hawaii County Board of Supervisors who was responsible for developing the north Kohala Coast.</p>
                <p>Spencer Beach  is one of the few white-sand beaches on the Big Island. The soft, smooth shore offers a comfortable change from the usually rocky beaches on the Big Island, making it a great picnicking spot. A long, shallow reef offshore keeps out high waves and strong currents. The water near the shore is shallow and an excellent playground for kids. Surfing is not very popular, as the water is usually calm.</p>
                <? include ("../../theme/text.html") ?>
                <p>The park is ideal for weekend picnics as there are lots of facilities, including a large picnic pavilion complete with tables and electrical outlets. Camping is allowed on the grounds, but you&rsquo;ll need a permit to stay overnight (see <a href="../../big-island/camping/">Big Island camping</a> for info on how to get a permit). If you're up for a walk, consider going to the adjacent <a href="../../big-island/ancientsites/puukoholaheiau.php">Pu'ukohola Heiau</a>, which was built by King Kamehameha I in 1790-91. Another ancient temple site is situated a short walk to the north &ndash; Mailekini Heiau &ndash; located on <a href="../../big-island/beaches/pelekanebeach.php">Pelekane Beach</a>, which is considered a sacred area. </p>
