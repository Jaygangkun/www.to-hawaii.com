                <h2 class="small">Pololu Valley Beach, Big Island</h2>
                <p>You might feel a sense of d&eacute;j&agrave;-vu when visiting Pololu Valley Beach (<a href="../../big-island/beaches/more_pololubeach_photos.html" onclick="NewWindow(this.href,'name','700','600','yes');return false">see more photos</a>). Your hunches are probably right &ndash; this beach is one of the most photographed spots on the Big Island. You&rsquo;ve probably seen it on postcards, book covers and in magazines and brochures. </p> 
                <p>The small black-sand beach is surrounded by high sea cliffs, which look like giant walls. Some visitors just come to the Pololu Valley Lookout. The valley itself is a steep 400 feet (122 m) down, accessible on foot, but quite a challenge for inexperienced hikers. It takes about 25 minutes to get to the beach and you&rsquo;ll have to walk down steep steps on a zigzagging dirt path.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>However, the view is well worth it. You&rsquo;ll come across a lazily winding stream, emerald-green landscapes and a few little waterfalls. It&rsquo;s no surprise that people have fondly called this valley an enchanted forest &ndash; you can almost feel the magic in the air!</p>
                <p>The swimming conditions are poor here because of strong currents and the rocky ocean bottom. There are no lifeguards or facilities nearby, so make sure to plan ahead and bring everything you need. You&rsquo;ll need to hike all the way back up to load up on supplies! Also, make sure to bring insect repellent, as the area tends to attract mosquitoes. </p>
