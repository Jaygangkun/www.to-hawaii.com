                <h2 class="small">Hilo Bay Beachfront Park, Big Island</h2>
                <p>With close to 3,000 feet (914 m) of coastline, Hilo Bay Beachfront Park is one of the longest beaches on the Big Island of Hawaii. But it&rsquo;s surprisingly uncrowded. The culprit? A heavy industrial boom in the mid-1900s that has left the waters murky and the shores polluted due to sewage and industrial waste that was dumped into the ocean. </p> 
                <p>The past two decades have seen tremendous efforts by the government to restore the area. It&rsquo;s still a work in progress, but on a good day, you can already glimpse some of Hilo Bay&rsquo;s former glory. It was once considered to be one of the most beautiful beaches on the island. Nevertheless, it is still a nice place for a picnic and a popular surfing, fishing and canoeing beach.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>Swimming is not very popular, as the water hasn&rsquo;t quite gained back its clear blue shade and is colder than on other beaches due to the discharge of freshwater from the nearby rivers. Nevertheless, the shore makes for a nice viewing spot, especially during sunset. It is a black sand beach and the unique color adds a lot of interest to the scene.</p>
                <p>Much of Hilo Bay is a protected area, as there is some interesting marine wildlife around. It takes some luck and patience to spot some of the fish, but it&rsquo;s certainly worth it. Most of them hang around far from shore, where there&rsquo;s quite a bit of leftover sewage. Try paddling past the first few meters and they just might make an appearance.</p>
                <p>Hilo Bay Beachfront Park is well-furnished despite its current condition. On the shore, you&rsquo;ll find a number of picnic tables and shaded areas. Restrooms and showers can be found further back, as well as a few beach shops. </p>
