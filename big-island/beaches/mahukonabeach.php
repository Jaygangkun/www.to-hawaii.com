                <h2 class="small">Mahukona Beach, Big Island</h2>
                <p>Mahukona Beach is unusual in more ways than one. For one, it is not a real beach, but an abandoned commercial harbor run by the Kohala Sugar Company (the harbor was closed in 1956). Remnants of the area's enterprising past can still be found under water and are in fact one of its main attractions. The beach is not your typical Hawaiian paradise, but it's definitely got charm. </p> 
                <p>The calm, clear waters make Mahukona a popular snorkeling spot. The underwater scene here is unique, featuring old mill equipment and machinery and even a shipwreck nestled inbetween beautiful coral reefs. Together, they make a most peculiar playground for the local marine life. Be sure to bring an underwater camera as this is one scene you certainly wouldn't want to miss!</p>
                                 <? include ("../../theme/text.html") ?>
                <p>There's also a lot to see on land, including several artifacts from the old sugar mill. The harbor edge is marked by a rock wall, and some rusted machinery has been here for years, adding to the rugged appeal of the place. </p>
                <p>The water is easily accessible from the harbor, but the shore is very rocky and can be hard to navigate. Bring a pair of sturdy sandals and save yourself a few scratches. The entrance is marked by gentle sloping steps, so there's no steep drop-off. The underwater visibility is usually excellent, except during times of rough ocean conditions (mainly during the winter months). During times of heavy surf, it is best to stay out of the water. </p>
