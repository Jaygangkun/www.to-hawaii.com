                <h2 class="small">Pahoehoe Beach Park, Big Island</h2>
                <p>Pahoehoe Beach Park (<a href="../../big-island/beaches/p20.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is mainly frequented by fishermen and picnickers as it doesn't provide easy access to the ocean. The beach is rocky and there is a low seawall at the water's edge, which acts as a barrier against high waves. There is only a small pocket of sand mixed with coral rubble, so it is not a good beach for swimmers.</p>
                <p>However, Pahoehoe makes up for it by being a nice spot for a picnic or ball games. The park is shaded by kiawe trees and has grassy areas and picnic tables. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>When the ocean is calm experienced snorkelers and scuba divers frequent Pahoehoe because it offers some interesting underwater sights, such as ancient lava formations, colorful marine life and a beautiful coral reef. Beginners are not advised to dive in because of the rocky nearshore ocean bottom and occasional rough currents. </p>
