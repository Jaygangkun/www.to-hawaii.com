                <h2 class="small">Kapa'a Beach Park, Big Island</h2>
                <p>If you like your beaches small and private, you'll love this quiet rocky beach on the Big Island's northwestern shore. However, the shoreline here is rocky and the beach is not good for swimming. Kapa'a is mainly visited by picnickers, fishermen and divers. </p> 
                <p>In the summer, the nearshore fishing conditions are good, but in the winter, heavy surf and strong currents make entering the ocean dangerous. The water is exceptionally clear here when the ocean is calm, and even though there is no coral reef to marvel at, the many colorful fish make up for it.</p>
                <p>In the old Hawaii, this remote area was well-populated and up until today, many habitation ruins surround the beach, such as yard walls, platforms and enclosures. Many of them are hidden under thick vegetation.</p>
                <p>Camping is allowed on the beach (but a permit is required) and is popular among backpackers. The park has fairly good facilities, including restrooms, showers, picnic tables, barbecue grills and a pavilion. </p>
