                <h2 class="small">Kauna'oa Bay (Mauna Kea Beach), Big Island</h2>
                <p>&quot;Perfect&quot; has been used to describe Kauna'oa Bay (<a href="../../big-island/beaches/p10.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  on several occasions, and one look is enough to see why. Fringed with leafy palm trees and fine, golden sand, this beach is considered one of the island's best. White-sand beaches like this one are rare on the Big Island. It is no wonder that Kauna'oa Bay has consistently ranked among the top five beaches in the U.S. </p> 
                <p>Stretching for about half a mile (800 m), the beach has two coral reefs and generally calm waters. The gently sloping shore at this cove is a good spot for sunbathing, but more adventurous visitors can try snorkeling along the rocky, southern side of the beach, where sea turtles, parrotfish, butterflyfish and other tropical fish can be spotted. However, during times of high surf, which is common in the winter months, a pounding shorebreak and strong rip currents can make entering the ocean hazardous. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>Kauna'oa Bay fronts the Mauna Kea Beach Hotel and is therefore also known as Mauna Kea Beach. It is a public beach, but if you're not staying at the hotel, you'll have to get a beach access pass at the resort entrance gate. Tip: arrive early to ensure you're not turned away, as there are only 40 public parking spaces for beach visitors. </p>
                <p>Kauna'oa is one of the well-maintained beaches on the island. Most of the facilities can be found in or close to the hotel, and equipment rentals are available at several beachfront shops. </p>
                <p>For visitors who stick around after the sun has gone down, the hotel turns on floodlights over the water. These lights attract plankton, which in turn attract hungry manta rays. There's even a lookout called &quot;Manta Ray Point&quot; where one can get a good view of these interesting evening visitors. </p>
                <p>Two other smaller beaches can be accessed by walking for about 1.5 miles (2.4 km) north of Kauna'oa - Wai'ula'ula Beach and Mau'umae Beach. Both are two small pockets of white sand. </p>
