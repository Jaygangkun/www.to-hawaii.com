                <h2 class="small">Pelekane Beach, Big Island</h2>
                <p>Pelekane Beach (<a href="../../big-island/beaches/more_pelekanebeach_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>, <a href="../../big-island/beaches/p11.html" onclick="NewWindow(this.href,'name','2550','500','yes');return false">view panorama</a>) on the Big Island's South Kohala Coast isn't your typical beach park. In fact, swimming, picnicking, camping and even sunbathing isn't allowed on this beach because many ancient sacred sites are located here. But a visit is definitely worth it because you'll learn about Hawaii's past and can see traditional heiaus (Hawaiian temple sites), some of which were used by King Kamehameha the Great. One such heiau, located near Pelekane Beach, is the <a href="../../big-island/ancientsites/puukoholaheiau.php">Pu'ukohola Heiau</a> dedicated to the war god Ku and built by the king himself in 1790-91. </p>
                <p>Pelekane Beach has also witnessed a large number of human sacrifices, which were part of ancient Hawaiian rituals. The first such sacrifice was made during the dedication ceremony for the newly erected temple and was followed by several others over the course of several years. The king's cousin and rival, Keoua, also met his end on this beach, marking the start of Kamehameha's reign. Mailekini Heiau, which is directly located on Pelekane Beach, is much smaller than the great Pu'ukohola, but is one of the oldest and bears several ancient religious images. </p>
                <? include ("../../theme/text.html") ?>
                <p>Then there is another smaller heiau believed to be completely submerged about 100 feet (30 m) offshore. It's a shark heiau called Hale o Kapuni, dedicated to the shark god Kauhuhu. It is said that human remains were placed here for the sharks. Up until this day, a large shark population frequents this area. Local residents consider the area a breeding ground for sharks, mainly black tips and grays.</p>
                <p>The beach itself is small and consists of white sand and coral rubble. The ocean bottom is very shallow here because of the coral rubble, soil runoff from the shore and rocks from the nearby harbor landfill. The water is almost stagnant and appears dirty. Because of this and the sharks it doesn't really matter that swimming is prohibited here. </p>
                <p>Two signs near Pelekane Beach read:                </p>
                <p><em>&quot;Who would suspect that the area at the base of this hill is one of the most significant cultural sites in Hawaii? This plain-looking patch of earth was the site of Pelekane, one of several known royal compounds traditionally used by Hawaiian kings. The compound contained the royal residence, probably some housing for other members of the royal court, and a royal fish pond. It was at Pelekane where Keoua, rival to Kamehameha, was killed in 1791, securing Hawaii for Kamehameha and initiating his takeover of all the Hawaiian Islands. Here, too, Kamehameha II prepared for his role as king following his father's death in 1819. Pelekane also became a favorite landing spot for foreign ships, where trade negotiations with the king were often hosted. After 1819, it is probable that, like Pu'ukohola Heiau and the ancient religious beliefs, the royal compound was abandoned with the abolition of the kapu (sacred laws).&quot;</em></p>
                <p><em>&quot;The submerged remains of Hale o Kapuni Heiau, a temple dedicated to shark akua (deities), are believed to be just offshore. The heiau was built by a chief whose family revered sharks, believing that the family's 'aumakua (deified ancestors) could become physically present within sharks. A Hawaiian family often revered a specific animal species, in the belief that the spirit of a helpful ancestor might appear to them as one of that species during times of distress. The exact construction date and exact location of Hale o Kapuni Heiau are unknown. It is hoped that underwater archaeology will tell us more. The National Park Service strives to protect all cultural resources, whether on land or submerged.</em>&quot;</p>
