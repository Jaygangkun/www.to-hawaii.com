                <h2 class="small">Onekahakaha Beach Park, Big Island</h2>
                <p>Located just south of downtown Hilo on the Big Island&rsquo;s east shore, Onekahakaha Beach Park is the ideal beach for families with small children and for tidepooling. There is a sand-bottomed cove that is separated from the open ocean by a boulder break, so the nearshore waters are shallow and free of currents or wave action. Also, lifeguards are on duty year-round.</p> 
                <p>Surrounding the cove are a few tide pools, which are sometimes visited by sea turtles. The largest one of them is home to a large population of sea urchins, as well as sea cucumbers, brittle stars, cone shells, sea urchins and sea anemones. However, this pool is not as children-friendly as it has a rockier and deeper bottom and is not protected by the breakwater. Also, it is subject to currents.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>Behind the breakwater, the currents are strong especially when the surf it up, so in the past, when there weren&rsquo;t any lifeguards yet, many drownings and near-drownings have happened in the turbulent waters behind the protected area.</p>
                <p>When tidepooling, keep the following safety tips in mind: Always wear water shoes to protect your feet from the sharp lava rocks and reef. Don&rsquo;t touch the sea urchins or other ocean creatures. </p>
