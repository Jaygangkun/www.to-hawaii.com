                <h2 class="small">Isaac Hale Beach Park, Big Island</h2>
                 <p><em style="color:#C30"><strong>Editor's Note:</strong> Due to a recent lava flow that occured on June 5, 2018, part of this beautiful beach was unfortunately destroyed. This page contains information prior to this event. </em></p>
                <p>Located on the eastern edge of Pohoiki Bay on the Big Island&rsquo;s east shore, Isaac Hale Beach Park (<a href="../../big-island/beaches/more_isaachalebeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a small two-acre (8,094 sq. m) park and is often times quite crowded. It is popular among picnickers, shoreline fishermen, surfers, campers and boaters. However, the swimming conditions are poor here due to strong currents and waves. </p> 
                <p>There is a boat ramp which fishermen use to launch their boats (this is the only boat launch area in the entire Puna district). Local children can also be seen here using the boat ramp as a jumping point into the water. The Pohoiki Warm Springs, a small hot pond, is another attraction in this beach park and is surrounded by lush vegetation. To get there, walk for about two minutes (about 150 yards / 140 m) along the shoreline trail that begins to the right of the boat ramp. The pool is volcanically-heated and is located in a lava sink.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>Water activities that are possible at Isaac Hale, besides fishing, include surfing and bodyboarding. The waves are best in the center of the bay, but a rip current is often times present here. Swimmers usually stick to the area around the boat ramp (and disregard the no swimming sign that stands here). The beach itself is composed of black sand and pebbles, but most surfers and swimmers use the boat ramp as en entry/exit point to the ocean.</p>
                <h3 class="big">Isaac Hale Beach Park Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Small, but well-visited beach park on Big Island's each shore, south of Hilo</li>
                  <li>Popular fishing, boating, surfing and picnicking location</li>
                  <li>Warm water pool located inland</li>
