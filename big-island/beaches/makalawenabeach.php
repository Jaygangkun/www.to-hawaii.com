                <h2 class="small">Makalawena Beach, Big Island</h2>
                <p>The Kona Coast has its share of &quot;secret&quot; beaches &ndash; little strips of paradise hidden by larger, better-known spots. Makalawena Beach is one such place, accessible by a 20-minute hike on a rough lava trail (or with a 4-wheel drive vehicle that can handle the bumps in the road). But anyone who has been here will agree that it is certainly worth the challenge.</p>
                <p>Makalawena's salt-and-pepper beach is a mix of white coral chunks and black lava, strewn with larger lava rocks that create an unusual, dramatic landscape. The beach is also unique in that some of the most extensive sand dunes in the Kona district are located here. The area immediately offshore is the best for swimming because the ocean bottom is the less rocky here than in the other coves and inlets of this beach. Make sure to check the water conditions before getting in, as the surf tends to be rough at times.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>The Makalawena shore is lined by palm trees, and the beach has several coves and inlets, which are backed by a long, curving sand beach. Inland of the beach is the 12-acre (48,562 sq. m) Opae'ula Pond, which is one of the Big Island's most important shoreline bird sanctuaries. The pond is home to many opae'ula, or red shrimp, which are a delicacy for the birds. Some of the birds who can be seen here include the endangered Hawaiian stilt (a'eo), ducks, black-crowned night herons, coots, doves, sparrows, wandering tattlers and cardinals. </p>
                <h2 class="big">Makalawena Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Best place for swimming is in the largest inlet, where the sand dunes are highest (the other coves and inlets have rockier ocean bottoms)</li>
                  <li>Snorkeling and scuba diving conditions are also good in this area, which is known for octopus and shells</li>
                  <li>Occasionally, the waves are good for surfing, especially in the winter months</li>
                  <li>Opae'ula Pond, an ancient Hawaiian fishpond, is located inland - it is an important shoreline bird sanctuary</li>
                  <li>An ancient Hawaiian fishing village used to be located here, but all houses were destroyed in the 1946 tsunami</li>
                  <li>Beach is accessible via 4-wheel drive or 20-minute hike</li>
