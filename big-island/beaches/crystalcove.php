                <p>Crystal Cove is often times referred to as the Big Island's giant aquarium. There are some steep cliffs here and the shoreline is rocky. The cove is a popular dive spot. The area inside the cove is a good spot for beginners since the water is between 20-40 feet (6-12 m) deep and home to some nice coral gardens. Further out, the water averages 40-90 feet (12-27 m) in depth and one can see tall pinnacles, more coral heads and the occasional pod of spinner dolphins or manta rays. </p>
                <p>The surf can be high at times, so divers should check the weather and ocean conditions before venturing out. Besides diving, the cove is great for picture-taking since the crystal clear, deep blue water contrasts nicely with the dark lava cliffs.</p>
