                <h2 class="small">Honaunau Bay, Big Island</h2>
                <p>A former stronghold of Hawaiian chiefs, Honaunau Bay on the Big Island of Hawaii has some of the best underwater sights on the island, if not the entire state. Rare fish and sea turtles can be seen playing amidst colorful coral reefs.</p> 
                <p>On a clear summer day, you don&rsquo;t even need to swim too far to get a good view. The water is especially inviting to snorkelers and scuba divers, as it is clear and calm throughout the year. The beach is also known as &ldquo;Two Step&rdquo; because it has two natural steps in the lava flow to enter and get out of the water.</p>
                <p>Next to the beach is the Puuhonua O Honaunau Park, an important cultural and historical site. It once served as a refuge for prisoners of war and a place of judgment for people who went against the law. Today, this notable past is celebrated in a 180-acre (0.7 sq. km) historical park frequented by locals and visitors alike. </p>
                <p>Facilities are scarce on the beach itself, but you&rsquo;ll find most of the basics in the park area. Lifeguards aren&rsquo;t always on duty. Restrooms, showers and beach shops are located further back along the shore. </p>
