              <p class="subtitle">The Big Island's volcanoes</p>
              <p>The Big Island of Hawaii is built from five volcanoes: Mauna Kea, Mauna Loa, Hualalai, Kohala and Kilauea. Mauna Loa, which comprises over half of the island, is the largest shield volcano on Earth in terms of volume and area. It measures 13,679 feet (4,169 m) from sea level to its summit and another 16,400 feet (5,000 m) from sea level to the sea floor. Mauna Kea is slightly higher than Mauna Loa and when measured from the sea floor to its summit, it is the tallest mountain in the world, topping even Mount Everest. </p>
              <p>Kohala in the northernmost part of the Big Island is the oldest of the island's five volcanoes. It is now extinct. Mauna Kea and Hualalai are the next oldest volcanoes, and both of them are considered dormant. Mauna Loa and Kilauea are the youngest ones, and they are the ones that are still very much alive and active.</p>
     
              <p>Mauna Loa's most recent eruption occurred between March and April of 1984. Kilauea's Pu'u O'o vent spits lava on an almost daily basis. It is considered the most active volcano in the world. Over a million people visit the Hawaii Volcanoes National Park each year to see the volcano&rsquo;s lava flows. The total volume of Kilauea's erupted material is enough to pave a road three times around the world. </p>
              <p>Below is a map showing the location of the Big Island's five volcanoes.</p>
              <p align="center"><img src="../images/hawaii_volcanoes.png" alt="Hawaii volcanoes" style="width:100%; max-width: 279px;" /></p>
              <p class="subtitle">Current lava eruptions</p>
              <p>The Pu'u O'o cinder cone located in the eastern rift zone of the Kilauea volcano has been erupting continuously since January 1983. By 2007, the lava that erupted from Pu'u O'o covered more than 45 square miles (117 km2) and added 500 acres (2 km2) of land to the Big Island's south shore. So far the eruptions have destroyed 189 buildings. The coastal highway has been closed since 1987. It lies buried below 115 feet (35 m) of hardened lava rock. Pu'u O'o again made headlines in March 2011 when its crater floor collapsed, creating lava fountains that shot lava up to 65 feet (20 m) into the air. A few webcams are located within Hawaii Volcanoes National Park showing what is going on there at any given moment: http://volcanoes.usgs.gov/hvo/cams </p>
