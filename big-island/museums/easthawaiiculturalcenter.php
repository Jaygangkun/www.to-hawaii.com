                <h2 class="small">East Hawaii Cultural Center, Big Island</h2>
                <p>Unknown to many beachgoers, the Big Island is home to a thriving arts and culture scene. The East Hawaii Cultural Center in <a href="../cities/hilo.php">Hilo</a> is a lively hub showcasing local talent and presenting Hawaiian culture at its finest. Many cultural festivals, performances, exhibits, shows and workshops take place here.</p>
                 
                <p>The Center is run by the East Hawaii Cultural Council, a group of small organizations dedicated to presenting Hawaiian culture to the world. In 2002, they staged the first worldwide digital arts exhibition, accompanied by a global tour. A variety of shows and exhibits is shown here throughout the year, and classes and workshops are offered. Some of them are open to tourists and make great summer activities for kids.</p>
                <p>The building that houses the Center used to be a police station and the former courthouse of Hilo. Much of the original structure remains and adds an interesting air to the place. It was built in 1932 and was placed on the National and State Registers of Historic Places in 1979. The ground floor now holds three galleries dedicated to modern exhibits by both local and international artists. Local art is sold at the gift shop located in the main gallery. Upstairs is a small theater where performances are held from time to time.</p>
                <p>Across the Center is Kalakaua Park, a large open space featuring giant trees, monuments, educational plaques and a beautiful man-made pond. </p>
                <p><strong>Hours:</strong><br />
                  <time itemprop="OpeningHoursSpecification" datetime="Tu,We,Th,Fr,Sa 10:00-16:00">Tuesday through  Saturday: 10 am to 4 pm</time>
                </p>
                <p><strong>Admission:</strong><br />
                  Free, but you can give donations or help out by buying local art.</p>
