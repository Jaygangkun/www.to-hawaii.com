                <h2 class="small">Hulihe'e Palace, Big Island</h2>
                <p>Relive the life of the Hawaiian mornarchs in this stylish mansion, located in downtown <a href="../cities/kailua-kona.php">Kailua-Kona</a>. The Victorian-style Hulihe'e Palace (<a href="../../big-island/museums/more_huliheepalace_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../big-island/museums/p1.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>) was commissioned in 1838 by John Adams Kuakini, Hawaii's second governor. It served as a summer home to Hawaii's royal families, including King Kalakaua and Queen Kapiolani.</p>
                <p>Today, the palace offers a glimpse into the lives of royals in the 19th century. Various relics and artifacts are displayed. Make sure to check out the koa furniture, intricately crafted from the acacia koa tree. Especially impressive is the big formal dining table, which measures 70 inches (1,78 m) in diameter. It was constructed from one solid koa log. </p>
                <? include ("../../theme/text.html") ?>
                <p>Upstairs are two remarkable cabinets, which were built by a Chinese prisoner serving a life sentence for smuggling opium. He was instructed to build these pieces of furniture by King Kalakaua, who was told of his talents. When he was finished, the cabinets made such a big impression on the King that he pardoned the craftsman.</p>
                <p>An earthquake partially destroyed the palace in 2006, but repairs were immediately put in place and the palace is again open to the public. It is now run by the Daughters of Hawaii, an organization dedicated to preserving Hawaiian history and culture.</p>
                <p>Many also consider Hulihe'e Palace a hub for folk entertainment. A music and hula concert is held here every month, each one honoring one of the Hawaiian monarchs. The concerts are free of charge and begin at 4 pm.</p>
                <p>The palace lies across the street from Moku'aikaua Church, the oldest Christian church in the state. Like Hulihe'e Palace, it is made from coral mortar and lava rock and features New England-style architecture. Its most prominent feature is a steeple rising 112 feet (34 m), making it the tallest man-made structure in Kailua-Kona. </p>
