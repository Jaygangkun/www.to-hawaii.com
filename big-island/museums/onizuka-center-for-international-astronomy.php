                    <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 18:00-22:00">daily from 6 pm to 10 pm </time>
                    and a summit tour every Saturday and Sunday from 1 pm to 5 pm (weather permitting)</li>
                  <li>For the summit tour, you must provide your own 4 wheel drive transportation (or join a guided tour group)</li>
                  <li>Drinking water, coffee, and other snacks are available</li>
                  <li>Restrooms, bookstore and gift shop located on the premises</li>
                  <li>At night, they offer a popular stargazing program</li>
                  <li>Located about one hour from Hilo and Waimea and about two hours from Kailua-Kona</li>
                  <li>Bring warm clothing (long pants and a jacket). Temperatures can dip below freezing, especially during the winter.</li>
                  <li>Make sure you have enough gas in your vehicle. There is no gas station on Mauna Kea. The nearest station is in Hilo or Waimea.</li>
