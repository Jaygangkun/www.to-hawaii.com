                <h2 class="small">Onizuka Center for International Astronomy, Big Island</h2>
                <p>The Onizuka Center for International Astronomy is, simply put, a stargazer's dream. Located along the slopes of Mauna Kea at 9,200 feet (2,800 m) above sea level, you're much closer to the stars here, and the amazing night sky views provide spectacular proof. If you're coming from the bright lights of a big city, this place will remind you why our ancestors were so fascinated with the skies.</p>
                                 <p>The Center was named after Big Island resident Ellison Onizuka, who was one of the astronauts in the <em>Challenger</em>, the space shuttle that broke apart in 1986 right after take-off, resulting in the death of all seven crew members.</p>
                                                 <? include ("../../theme/text.html") ?>
                <p>From Saddle Road, the Center is located about 6 miles (9.7 km) up the road. Inside the Center you'll find informational displays about the mountain's geology and history, handouts, photos and computer-driven astronomy programs with links to the observatories on top of Mauna Kea. You can also watch a video about the <a href="../attractions/maunakeaobservatoriesandsummit.php">Mauna Kea observatories</a>. </p>
                <p>One of the best things to do here is to take part in their popular stargazing program, which takes place from 6-10 pm daily (admission is free). The stargazing here is excellent because the sky at Mauna Kea is among the clearest, darkest and driest in the world. </p>
                <p>Since this is the only place on Mauna Kea where you get a chance to peek through a telescope yourself, it's definitely worth it to come here in the evening (there are no public telescopes at the summit). During the day, there is a solar telescope that visitors can peek through to observe the sun and sunspots. The telescope is equipped with protective filters. In the evening, telescopes for star-viewing are available.<br />
                  <br />
                  Due to the hazards of altitude sickness, children under 16 should not venture above the Onizuka Visitor Information Center.</p>
                <p><strong>Hours:</strong><br />
                  Open daily from 8 am to 3:30 pm, 365 days a year <br />
                  <br />
                  <strong>Admission:</strong><br />
                  Free </p>
