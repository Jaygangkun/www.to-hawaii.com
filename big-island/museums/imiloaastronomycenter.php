                <h2 class="small">'Imiloa Astronomy Center, Big Island</h2>
                <p>Ever wondered how people from the Hawaiian Islands, surrounded by nothing but the vast Pacific Ocean, have for centuries been able to navigate around the archipelago? A visit to the magnificent 'Imiloa Astronomy Center of Hawaii can provide the answer. Hawaii's remote location in the Pacific has made astronomy and navigation via the position of stars an important part of local culture through the years &ndash; something that is reflected at the 'Imiloa Astronomy Center. Even the center's name hints at this tradition, the word <em>'Imiloa</em> meaning &quot;exploration driven by a sense of wonder and imagination.&quot; And that is what a visit to the center will provide.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>                  Key to the history of Hawaiian astronomy is <a href="../attractions/maunakeaobservatoriesandsummit.php">Mauna Kea</a>, the tallest mountain in the Pacific and even in the world (if measured from its base at the ocean bottom). Today, the summit of Mauna Kea is the site of numerous astronomical observatories which gaze out at the stars. Back down closer to earth, the 'Imiloa Center combines a history of ocean navigation via the stars, with the story of the modern-day star gazers who sit atop of Mauna Kea. As such, one of 'Imiloa's key features is the fantastic 3D planetarium, built by the National Space Center with input from astronomers from the UK. A choice from one of three daily shows is included in the center's standard entrance fee.</p>
                <p>                  Apart from the planetarium, 'Imiloa also houses over 100 hands-on exhibits to aid the explanation of the significance of astronomy in Hawaii's history. These are divided broadly between 'Hawaiian beliefs and theories' and 'astronomy beliefs and theories.' Perhaps one of the highlights is the scaled down replica of a <em>wa'a</em>, a traditional Hawaiian voyaging canoe. The ethnobotanical gardens are another major attraction. All exhibits are shown in both in English and Hawaiian languages, with headsets available for self-guided tours.</p>
