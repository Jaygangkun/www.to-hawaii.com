                <h2 class="small">Lyman House Museum, Big Island</h2>
                <p>The Lyman House Museum in <a href="../cities/hilo.php">Hilo</a> features a collection of galleries showcasing Hawaiian culture, history and natural wonders in a historic mission house. The house was built in 1839 for missionaries David and Sarah Lyman, and today, almost a century later, is one of the most esteemed museums in Hawaii.</p>
                 
                <p>The two main galleries are the Earth Heritage Gallery, which showcases Hawaii's natural wonders, and the Island Heritage Gallery, where you'll find relics from all over the Big Island. Among the highlights in the Earth Heritage Gallery are mineral and shell collections, a 10-foot (3 m) tiger shark and remains of the only two flightless birds native to Hawaii &ndash; the flightless goose and the Hawaiian rail. The Island Heritage Gallery takes you back through time with samples of native clothing, fishing and hunting tools, and models of old Hawaiian huts.</p>
                <p>Also, a popular stop is the Shipman Gallery of Chinese Art, named after the family whose civic works were instrumental in the development of East Hawaii. The gallery houses the family's private collection, which includes pieces dating back 3,000 years.</p>
