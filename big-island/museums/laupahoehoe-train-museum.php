                <h2 class="small">Laupahoehoe Train Museum, Big Island</h2>
                <p>A sign at the Laupahoehoe Train Museum invites you to &quot;take a trip back in time,&quot; and it is definitely a trip worth taking. Built by community residents in 1998, the museum celebrates the culture of the Hamakua Coast and its fascinating railroad history. It is located right where the old Laupahoehoe Train Station was located at the turn of the 20th century.</p>
                 
                <p>Years of neglect have damaged much of the main building, but the residents have worked long hours to bring back its old charm. Today, the old employee home is filled with relics, photos and other memorabilia. Even the old narrow-gauge diesel engine has been expertly restored and is now fully functional. The old box car is being reconstructed and will soon run along the tracks again.</p>
                <p>To the far side is a small gift shop where visitors can buy various hand-crafted logo items, including shirts, pins, hats and patches. History buffs can also pick up history books and educational videos about the Hamakua community. One of the bestsellers is called &quot;April Fools&quot; and depicts the aftermath of the tsunami that hit the island on April 1, 1946. </p>
