                <h2 class="small">Anna Ranch Heritage Center, Big Island</h2>
                <p>Take a step back in time by visiting the Anna Ranch Heritage Center. This historic ranch was established in 1848 and has been in the same family for five generations now. In the late 1800s James Fay and his Hawaiian wife, Ka'ipukai, bought land in the green hills near <a href="../cities/kamuela.php">Kamuela</a> (also known as Waimea). Their only child, Mary Ka'ala Fay, married a Lindsey and their descendants have since then made path breaking contributions to the concept of ranching in Hawaii.</p>
                 
                <p>Anna Leialoha Lindsey Perry-Fiske, after whom the ranch is named, was born in 1900. She is the great-great granddaughter of James and Ka'ipukai Fay. Anna lived on the ranch and learned to mend fences, ride, rope and tend cattle at an early age. After her father's death in 1939, Anna took over the reins at the ranch, an unprecedented move for that time period.</p>
                                <? include ("../../theme/text.html") ?>
                <p>Over time she came to be known as the &quot;The First Lady of Ranching&quot; in Hawaii and lived to the ripe old age of 95. Today, the ranch has been renovated to retain its historical character. It is a treasure trove with a collection of old photographs, records, maps, household items and artifacts that tell the story of Hawaii's ranching history, as well as of Waimea's past.</p>
                <p>At the ranch you can see different kinds of ranching gear, including saddles and tack, trophies and awards won by Anna for horse racing, pa'u riding and live stock enhancing, koa furniture almost a century old, fine china and porcelain that Anna used to entertain her guests, paintings by Lionel Walden, D. Howard Hitchcock, Martha Greenwell and Herb Kane, regarded as some of Hawaii's greatest painters, and go through photographs of the family. </p>
                <p>The various buildings on site are a barn, a garage, a ranch house and a slaughterhouse, all constructed between 1910 and 1930. The gift shop located on the grounds of the Anna Ranch Heritage Center is a veritable treasure trove of artifacts made by local artisans and craftsmen and includes items by an on-site blacksmith and saddle maker.</p>
