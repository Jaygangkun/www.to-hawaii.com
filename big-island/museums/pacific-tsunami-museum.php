                <h2 class="small">Pacific Tsunami Museum, Big Island</h2>
                <p>There aren't many museums dedicated to natural disasters, but here's one right in downtown <a href="../cities/hilo.php">Hilo</a>. The Pacific Tsunami Museum is an informational site about the science and history of tsunamis, particularly the last Pacific-wide waves that hit the Big Island in 1946 and 1960. Located in a tsunami zone itself, the museum aims to educate people about tsunamis and how they have and continue to affect Hawaiian culture.</p>
                                 <p>The museum is fairly modest, occupying a single floor with various presentations and exhibits. However, it makes good use of its space and packs every wall with information, from basic tsunami facts to stories of the waves that have impacted Hawaii. There's also a section on tsunami science and the technologies used to detect and minimize the damage of present-day tsunamis.</p>
                                                 <? include ("../../theme/text.html") ?>
                <p>Most of the exhibits are a mix of educational plaques, text, photos and videos. Some of the videos are long, so if you're really interested, be sure to set aside a few hours for the trip. The most watched clip is the one detailing the destruction wrought on Hilo by the 1960 disaster. The screen is placed near the window, so you can look out at the very spot where it happened. </p>
                <p><strong>Hours:</strong> <br />
