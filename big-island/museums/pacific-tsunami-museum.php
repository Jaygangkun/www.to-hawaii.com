                <h2 class="big">Pacific Tsunami Museum Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Small museum that offers a crash course on tsunamis</li>
                  <li>Teaches about the impact of tsunamis in the Pacific, focusing on the two tsunamis that hit the Big Island of Hawaii in 1946 and 1960, destroying most of the Hilo waterfront area and other parts on the island</li>
                  <li>Dedicated to those who lost their lives in the destructive tidal waves that hit the Big Island</li>
                  <li>Multimedia exhibits, such as computer simulations, visual displays, filmed oral accounts from survivors, photos and more</li>
