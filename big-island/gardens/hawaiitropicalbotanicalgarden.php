                <h2 class="small">Hawaii Tropical Botanical Garden, Big Island</h2>
                <p>This lush botanical garden just north of <a href="../cities/hilo.php">Hilo</a> is a veritable museum of living plants and a fascinating place to explore. Botanists, photographers, scientists and tourists from around the world come to the Hawaii Tropical Botanical Garden (<a href="../../big-island/gardens/more_hawaiitropicalbotanicalgarden_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>) to observe its rich flora representing over 2,000 species. The site offers visitors a chance to explore an authentic Hawaiian rainforest that's just chock full of life. </p>
                  <? include ("../../theme/text.html") ?>               
                <p>                  A raised wooden boardwalk leads you through a lush rainforest. You'll be able to see a variety of tropical flowers and plants, a waterfall and as you reach the shoreline, a picturesque bay. Due to its fertile volcanic soil and moist climate, the garden is able to support an exceptional bounty of species from around the world. More than 125 families and 750 genera are represented. </p>
                <p>                  The garden is also home to a wide variety of fauna, including many colorful birds and reptiles. Here you'll find the gold dust day gecko, a native of Madagascar with striking yellow speckles along its neon green back. The Indian mongoose, a small, agile rodent introduced to the Islands in the 1880s, also makes its home here. </p>
                <p>                  Resident bird species include the native 'auku'u (blue-crowned night heron) and the common mynah, which was introduced many years ago to control pests in the sugarcane plantations. Look closely and you'll also find the 'a'ama crab, a tree-climbing crab that moves faster than you can say &quot;aloha.&quot; Common insects include the Sphinx moth and the yellow and black garden spider.</p>
                <p><strong>Hours:</strong><br />
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 09:00-17:00">Open daily from 9 am to 5 pm.</time>
                  <br />
                  Closed on Thanksgiving, Christmas and New Year's Day.<br />
                  <br />
                  <strong>Admission:</strong><br />
                  Adults: $20<br />
                  Children (6-16 years): $5<br />
                  Children under 6 are free.<br />
                </p>
