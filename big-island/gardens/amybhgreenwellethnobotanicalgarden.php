              <div class="midpanel_row">
              <strong style="color:green">As of January 31, 2016, the garden is closed to the public. Bishop Museum is looking for a new owner and steward of the Garden.</strong>
                <p>Operated by the Bernice P. Bishop Museum, this 15-acre botanical garden near Captain Cook is situated on the on the western slope of Mauna Loa volcano. Named after a renowned local botanist, the Amy B.H. Greenwell Ethnobotanical Garden introduces visitors to native Hawaiian plants and their connection to the indigenous people of the Islands.</p>
                 
                <p>                  Over 200 species are on display here, including many plants that were once cultivated on Hawaiian farms and plantations before the arrival of Westerners. Indigenous, endemic and Polynesian-introduced plants inhabit the Greenwell gardens, which are divided into four zones: dry forest, coastal, upland forest and agricultural. Here you'll find some of the most important plants in Hawaiian culture, such as taro and kukui. Many rare and endangered species, including the kokio, can also be found. </p>
                <p>                  Curators at Amy B.H. Greenwell Ethnobotanical Gardens strive to maintain many traditional cultivation methods and have organized the gardens as if frozen in time. The site features a traditional Kona field system of stonework and divided land that occupies a 50 square mile network of fields. The upper five acres of the garden are actually part of an archaeological site that dates back to pre-colonial times. Visitors can also take a peek into the garden's native insect house, which includes the amazing Kamehameha butterflies. Plants are clearly labeled by their scientific and Hawaiian name, and most labels include some information about the traditional uses of the plants. </p>

