              <div class="midpanel_row">
                <p>Established in 1974 by botanist Dr. Virgil Place, the Pua Mau Place Arboretum and Botanical Gardens occupy 45 acres on the slopes of the Kohala Mountains. The facility focuses on the preservation of native pua mau woody plants as well as many other important native species. Here botanists, ecologists and amateur plant lovers can discover the richness and diversity of Hawaiian flora in a spectacular setting.</p>
                <p>                  Pua Mau Place may be a plant and tree sanctuary primarily, but there's more here than meets the eye. In addition to the garden's outstanding collection of continuous flowering plants, you'll find original sculptures, exposed red lava rock boulders and a magic circle of large stones based on Celtic ritual sites of the British Isles. These stones are arranged in such a way that they indicate the solstices and equinoxes throughout the calendar year. </p>
                 
                <p>                  Pua Mau Place also features an impressive hibiscus maze with over 200 varieties bursting with colors like lavender, pink and gold. The site's Pikake Gallery exhibits artwork by local artists, and there's also a snack bar selling bottled water, sodas and snacks. All plants are clearly labeled with their common name, scientific name and country of origin, and Pua Mau Palace provides an informative booklet to guide you on your walk. </p>
                <p>                  At certain times of the year you may even be able to spot whales in the waters off of Pua Mau's wraparound deck. Many exotic birds can also be observed roaming the grounds, and there's an aviary that houses 150 guineafowl and peafowl. </p>
                <p>                  The Pua Mau Place Arboretum and Botanical Gardens visitor center is available for wedding receptions, workshops, meetings, seminars and other private and public functions. For more information call <span itemprop="telephone">(808) 882-0888</span>. </p>
