                <h2 class="small">Botanical World Adventures, Big Island</h2>
                <p>Set on hundreds of acres of old sugarcane fields, <span class="big">Botanical World Adventures</span> is a spectacular natural site that features both a  botanical garden, as well as a maze and two waterfalls. Founded in 1995, <span class="big">Botanical World Adventures</span> is home to an astounding array of plant life, both native and non-native. Here you'll find plants such as agave, climbing ylang ylang, costa inflorescence, giant calaythia, ginger, giant heliconia, gout plant, milk cactus, ponytail palm, snownbush, yellow shrimp plant and many others. </p>
                 
                <p>                  The garden also features an outstanding collection of fruit trees, such as banana, breadfruit, papaya, guava, mango and pineapple. Don't miss the vibrant Rainbow Walk featuring hundreds of colorful bromeliad flowers. A 262-foot (80 m) tall wall covered in over a hundred different wild orchid species never fails to impress. Jaw-dropping views of Mauna Kea, paniola-style cattle ranches and the surrounding coastline ensure a Kodak moment or two.</p>
