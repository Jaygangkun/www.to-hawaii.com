            <h1><span itemprop="name">Sadie Seymour Botanical Gardens, Big Island</span></h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <p>Both beautiful gardens and archaeological wonders await you at these botanical gardens in the <a href="../cities/kailua-kona.php">Kailua-Kona</a> area. Located on the grounds of the Kona Outdoor Circle Educational Center, the Sadie Seymour Botanical Gardens were designed by landscape architect Scott Seymour in honor of his mother. Today the gardens feature common Hawaiian plants organized by geographic origin, many of which are native to the Islands.</p>
                 
                <p>                  Established in 1986, these lush gardens exhibit an impressive range of trees and plants from Hawaii, the South Pacific and beyond. Here you'll find plants from Australia, New Zealand and Indonesia, including eucalyptus, ixora and cuphea; African plants such as bismarckia nobilis; calabash from Central America; and South Asian spice plants such as clove trees, cinnamon, turmeric and lemon grass. </p>
                <p>Spanning 1.5 acres, the Sadie Seymour Botanical Gardens provides an  overview of the native and non-native plants that inhabit the Hawaiian Islands. A guided lunch tour explains the natural history of Hawaii and origins of different plants, including those that inhabited the island before the arrival of Polynesian explorers. The site also contains a horticultural library and educational center. </p>
                <p>                Visitors can also check out Kealakowa'a Heiau, an important archaeological site located on the grounds of the Kona Outdoor Circle Educational Center. Hawaiian for &quot;temple on the way for dragging canoes,&quot; this ritual site built in the 1600s during the age of the Umi people was once used to construct and bless canoes. The site contains the ruins of a priest's house, an astrological temple, a ceremonial platform and a meetinghouse. Kealakowa'a Heiau is located along an important sacred trail that leads from the upland Koa forests down to Holualoa Bay, where the Umi launched their canoes. </p>
