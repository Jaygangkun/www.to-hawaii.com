                <h2 class="small">Lili'uokalani Gardens, Big Island</h2>
                <p>Named after Hawaii's last reining queen, Lili'uokalani Gardens (<a href="../../big-island/gardens/more_liliuokalanigardens_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a 30-acre park containing expansive Japanese gardens that were built in the early 1900s. The park is located on Banyan Drive, southeast of downtown <a href="../cities/hilo.php">Hilo</a> on the Waiakea Peninsula in Hilo Bay. It mainly consists of Edo-style Japanese gardens and is said to be the largest outside of Japan. Located within the gardens is Waihonu Pond, as well as red bridges over koi ponds, statues, pagodas, rock gardens, stone lanterns and even a Japanese teahouse.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>                  One of the park's most popular uses with local residents is for an early morning workout. The hours before lunch see a plethora of fitness-minded locals don their running shoes to go for a jog whilst taking in the pleasant scenery along the trails that run through the gardens. </p>
                <p>                  There is a footbridge across to the nearby Moku Ola Island (also known as <a href="../ancientsites/coconutisland.php">Coconut Island</a>). Moku Ola means &quot;island of life&quot; in the Hawaiian language. A temple dedicated to healing used to be located here, hence the island's name. Moku Ola Island is a great place for a picnic with its expansive views to the Hilo bayfront and downtown Hilo. Off to the east is a large breakwater that protects Hilo Bay from tsunamis (which have hit Hilo in the past).</p>
                <h2 class="big">Lili'uokalani Gardens Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Largest Edo-style garden outside of Japan</li>
                  <li>Dedicated in 1917 as a tribute to the first Japanese immigrants who worked in the Big Island's sugarcane fields</li>
                  <li>Nice place for a picnic and to enjoy the views of downtown Hilo and the bayfront</li>
                  <li>No admission fee, open city park</li>
                </ul>
