                <h2 class="small">Body Glove Cruises, Big Island</h2>
               <p>Based in Kailua-Kona on Hawaii's Big Island, Body Glove Cruises offers several shoreline excursions along the Kona Coast, including a Kona snorkel and dolphin adventure, a Kona whale watch cruise and a Captain Cook historical lunch and dinner cruise to Kealakekua Bay.</p>
               <p>The company operates the Kanoa II, a 65-foot double-deck catamaran that features a 15-foot high jump, a 20-foot waterslide and two large double swim platforms. The vessel is also wheelchair friendly. Guests enjoy plenty of shade on board, cushioned seating and a full service premium bar. The Kanoa II is also equipped with flat screen televisions, a full sound system for live entertainment, three restrooms and two freshwater showers.</p>
               <p>The Body Glove Cruises crew includes a U.S. Coast Guard licensed Captain, First Mate, a licensed bartender and deck hands. All of their crew is also trained in first aid and C.P.R. Their main office is located in the Ilima Court near the Kailua-Kona pier.</p>
               <p>Their snorkel &amp; dolphin adventure takes guests to a good snorkel spot, usually Red Hill, which is about 10 miles south of Kailua-Kona. This Kona snorkel cruise includes breakfast and a BBQ lunch. Both of their Captain Cook cruises take guests to the Captain Cook Monument at Kealakekua Bay, about 11 miles south of Kailua-Kona. Both cruises include either lunch or dinner and feature live entertainment. Their Kona whale watch cruise is available during Hawaii's winter whale season, which runs from December through March. </p>
