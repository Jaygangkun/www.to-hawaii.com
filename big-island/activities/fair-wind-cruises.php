                <h2 class="small">Fair Wind Cruises, Big Island</h2>
               <p>Located on Hawaii's Big Island, Fair Wind Cruises has been offering Kona snorkel cruises since 1971. They specialize in taking guests snorkeling at Captain Cook Monument, which is located at <a href="../../big-island/beaches/kealakekua-bay.php">Kealakekua Bay</a>, a marine life conservation district.</p>
               <p>The company operates two vessels for their Kona snorkel cruises, the Fair Wind II and the Hula Kai. The Fair Wind II is a 60-foot catamaran that features two 15-foot waterslides, a high-jump platform and two custom staircases. Guests can book a morning or an afternoon snorkel cruise aboard the Fair Wind II, which departs from the harbor at <a href="../../big-island/beaches/keauhoubay.php">Keauhou Bay</a> and cruises south to Kealakekua Bay. The morning snorkel cruise includes breakfast and a BBQ lunch that is freshly grilled on board. The afternoon cruise is a shorter version of the morning cruise and without meals.</p>
               <p>The Hula Kai is a 55-foot catamaran that offers first-class comforts like individual theater-style seating. Guests can book a deluxe Kona snorkel cruise aboard the Hula Kai and snorkel at a prime South Kona destination, such as the Red Hill area, located just north of Kealakekua Bay, or Pali Kaholo, which is 10 miles south of Kealakekua Bay. This deluxe snorkel cruise also includes breakfast and a grilled BBQ lunch.</p>
               <p>The company also offers an evening manta snorkel adventure aboard the Hula Kai, which the Travel Channel calls &quot;One Of The Top-Ten Things To Do In Your Lifetime.&quot; Bread rolls, a warm soup and hot drinks are included in this evening adventure at sea.</p>
               <p>Fairwind Cruises also offers a private snorkel or fishing charter aboard Capt. Jack, a spacious 43-foot Powerboat Flybridge sportfishing vessel that has room for up to six guests. It features upholstered booth-style seating and a centrally located big game fishing chair. </p>
