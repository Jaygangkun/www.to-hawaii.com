                <h2 class="small">Lava Tree State Park, Big Island</h2>
                <p><em style="color:#C30"><strong>Editor's Note:</strong> Due to a recent lava flow that occured on June 5, 2018, this beautiful destination was unfortunately destroyed. We are leaving this page up for all of us who have made memories here and would like to remember it as it used to be.</em></p>
                
                <p>Located near Pahoa in the Big Island's Puna District, the Lava Tree State Park (<a href="../../big-island/attractions/more_lavatreestatepark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) makes for an interesting stop. Because when else do you get a chance to see lava trees? Here on the Big Island, lava trees can be seen in places where in the past, fast-flowing pahoehoe lava streams hit wet ohi'a trees. This is what happened here in 1790, when a lava flow surged through this once lush rainforest. </p> 
                <p>The lava covered the trees from bottom to top. As the lava drained away and the moisture inside the trees cooled the lava, a hard exterior shell was formed. The wood inside burned away and only the outside lava shell remained. Each tree has a different shape, and it takes little imagination to see craggy faces staring back at you.</p>
                <p>Many of the trees are covered in moss; others have fallen to the ground, exposing their interior pipe-like structure. It is a truly otherworldly sight. Also, there are some large monkeypod trees here with huge roots, which are equally amazing. </p>
