                <h2 class="small">Ala Kahakai National Historic Trail, Big Island</h2>
                <p>The Ala Kahakai Trail  (<a href="../../big-island/attractions/more_alakahakaitrail_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../big-island/attractions/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a 175-mile (282 km) long trail that runs along much of the Big Island of Hawaii's west and south shores. It begins at Upolo Point in the North Kohala District and continues south along the Kohala and Kona Coast, rounds the island's southern tip and ends at Hawaii Volcanoes National Park. It is not yet a continuous trail, but it can be accessed at various broken segments along this vast stretch of shoreline. In 2000, the trail was established as a National Historic Trail as a way to access and preserve Hawaiian culture (the trail follows the coastline over ancient fishermen's trails). In the Hawaiian language, <em>ala kahakai</em> means &quot;shoreline trail.&quot; </p>
