                <h2 class="small">Big Island Candies, Big Island</h2>
                <p>Established in 1977, Big Island Candies is the oldest, biggest and probably best-known sweets shop on the island, located in Hilo. The most popular items are their chocolate-dipped shortbread cookies and chocolate-covered macadamia nuts. Seasonal offerings such as the Mika Mints, truffles and crunch bars are also among the favorites. There&rsquo;s also an assortment of brownies, each of them given a distinct Hawaiian twist: coconut, peanut butter, and not surprisingly, macadamia nut.</p>
                <p>The recipes are of course a well-guarded secret, but workers will gladly give you a tour of the factory. Here you will see how each ingredient is picked, each nut carefully selected and each finished product wrapped in the signature BIC packaging. You&rsquo;ll even get free samples of their shortbreads and the famous Kona coffee, one of the most expensive coffees in the world.</p>
                <p>The store is open Mondays to Sundays from 8:30 am to 5:00 pm. Factory tours are offered on a walk-in basis, although large groups are advised to book in advance. The store is a tourist mecca, so it can be quite crowded at times because many local bus tour companies stop here on the way to other island attractions. </p>
