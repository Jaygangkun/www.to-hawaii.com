                <h2 class="small">Mauna Loa Macadamia Nut Company, Big Island</h2>
                <p>Here's where you can literally go nuts! Located near Hilo, the Mauna Loa Macadamia Nut Company is a good place to visit if you like macadamia nuts and would like to learn more about the process of harvesting and processing them.</p>
                <p>The grounds are home to around 800,000 trees, which are regularly maintained and harvested throughout the year. Visitors can do a self-guided tour. There is a walkway that leads along the factory building with large windows from where the various stages of the production and packaging process can be observed while videos explain the  nut processing. </p>
                                <? include ("../../theme/text.html") ?>
                <p>Also, there is a large gift shop where you can buy and sample the famous macadamia nuts, as well as popular varieties like chocolate and candied nuts. They also have other items, including postcards, jewelry, magnets and more.</p>
                <p>The Mauna Loa Macadamia Nut Company was started in 1946 and made its first harvest in 1956, and in 2004 became a subsidiary of the Hersheys Corporation. Their processing plant and headquarters are located in Hilo, Hawaii. </p>
                <p><strong>Hours:</strong><br />
                  Open daily from 8:30 am to 5:00 pm<br />
                  Factory closed Sundays &amp; holidays </p>
