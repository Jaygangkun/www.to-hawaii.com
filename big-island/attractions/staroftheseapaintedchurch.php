                <h2 class="small">Star of the Sea Painted Church, Big Island</h2>
                <p>With frescos and stained glass adorning the interior, the Star of the Sea Painted Church boasts a history as colorful as its walls. The paintings tell the story of Father Damien Devester, a Belgian priest who helped Hansen's disease (also known as leprosy) patients on the island of Molokai and later died from the disease himself. </p>
                <p>The upper section features paintings by another Belgian missionary, Father Everist Gielen, who began constructing the church in 1928. The paintings cover the arched ceiling across the length of the church. In 1964, artist George Heidler from Athens, Georgia, completed the interior by painting the lower panels and altar section. </p>
                                <? include ("../../theme/text.html") ?>
                <p>The church used to be located in Kalapana, but after a lava flow came dangerously close to it in 1990, they church was moved. Otherwise, it would have been destroyed. The Star of the Sea Painted Church is on the National Register of Historic Places, a list of places whose preservation is actively supported by the government. </p>
                <p><strong>Hours:</strong> Open daily from 9 am to 4 pm (free admission) </p>
                <h2 class="big">Star of the Sea Painted Church Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Known for its trompe l'oeil murals</li>
                  <li>Small Catholic church was built in 1928</li>
                  <li>First priest of this church was Father Damien, known for his services on Molokai</li>
                  <li>Church was originally located near Kalapana, but was in danger of being overrun by a lava flow, so in 1990 it was moved to its present location along Highway 130 (above Kaimu)</li>
