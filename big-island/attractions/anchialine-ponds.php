                <h2 class="small">Waikoloa Anchialine Pond Preservation Area, Big Island</h2>
                <p> Located within the Waikoloa Beach Resort, the Waikoloa Anchialine Ponds (<a href="../../big-island/attractions/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) are an easily accessible natural attraction. This series of  brackish water lava pools are home to various small fishes, tiny red shrimp and crustacean mollusks. A trail leads from the roadside parking area to the ocean. It is a nice 5-minute stroll with up-close views of the ponds. The Waikoloa anchialine ponds are managed for research and educational uses by the University of Hawaii. </p>
                <p>A sign at the entrance to the Waikoloa Anchialine Pond Preservation Area reads:</p>
                <p>&quot;Anchialine ponds are brackish water ponds formed by depressions, crevices or lava tubes that extend into the groundwater table. These pools were termed anchialine pools after the Greek word <em>anchialos</em>, meaning near the sea. Although landlocked and occurring some distance from the sea, the pool exhibit tidal fluctuation because of the subterranean connection with the sea and the groundwater movement through the highly porous lava.&quot;<br />
                </p>
