                <h2 class="small">Volcano Art Center, Big Island</h2>
                <p>Located within Hawaii Volcanoes National Park near the Kilauea Visitor Center, the Volcano Art Center Gallery features works from more than 300 local artists, offering a glimpse of Hawaiian culture and heritage. The impressive collection of Hawaii-made arts and crafts (all for sale and reasonably priced) includes island pottery, woodwork, paintings, photographs, jewelry, Hawaiian quilts and more. Also, classes and workshops are offered year-round, covering everything from arts and crafts to music, dance and various performing arts. </p>
                <p>The building was once a backpackers' inn that last operated some 100 years ago. This explains why the inside is divided into small rooms, now designated as mini-galleries. Remnants of the old inn can still be seen around the place, most notably the lobby fireplace which is still functional to this day. From the rooms, you can look out the windows at the dynamic environment that inspired the artworks inside.</p>
