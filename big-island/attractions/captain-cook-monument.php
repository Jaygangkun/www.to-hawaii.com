                <h2 class="small">Captain Cook Monument, Big Island</h2>
                <p>On January 17, 1779, Captain James Cook (the British explorer who discovered the Hawaiian Islands in 1778) and his crew sailed into <a href="../../big-island/beaches/kealakekua-bay.php">Kealakekua Bay</a> on the Big Island of Hawaii. The Hawaiians, thinking they were returning gods, gave them a warm welcome and held a feast in their honor. However, a month later he was killed in a conflict between his crew and the Hawaiians. The natives had realized that Cook and his men were mere mortals. In 1878, a 27-foot white obelisk was erected to honor this well-known seafarer.</p>
                <p>The Captain Cook Monument (<a href="../../big-island/attractions/more_captaincookmonument_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../big-island/attractions/p1.html" onclick="NewWindow(this.href,'name','2550','550','yes');return false">view panorama</a>) is a major landmark on the Kona Coast. Rising up against the sky, the tall white spire strikes a beautiful contrast against its natural surroundings &ndash; the rugged ground, the high cliffs and the lush surrounding vegetation. A plaque rests on the base of the structure, telling the story of his arrival and death in Hawaii.</p>
                <p>The plaque reads the following: &ldquo;In memory of the great circumnavigator, Captain James Cook, R. N., who discovered these islands on the 10th of January, A.D. 1770, and fell near this spot on the 14th of February, A.D. 1779. This monument was erected in November A.D. 1874 by some of his fellow countrymen.&rdquo;</p>
                <? include ("../../theme/text.html") ?>
                <p>The monument can only be reached by hiking to it or by kayaking to Kealakekua Bay or by taking a boat tour. The boat tour is the easiest of these three options because you don't have to paddle yourself and don&rsquo;t have to worry about ocean currents and high surf. Open ocean kayaking can be tricky if there are waves and you're inexperienced. Kayaks can easily flip over if you don't know what you're doing, so if you decide to kayak here, it is best to join a kayak tour group with an experienced guide.</p>
                <p>If you plan to kayak here on your own, the best time is early in the morning because this is when the ocean is usually calmer. Later in the day the water is often times choppier, so your way back may not be as smooth. Once you reach the monument, it is important to beach the kayak to the left of the monument because there are fewer sea urchins and coral here compared to the right side of it. </p>
                <p>Getting into and out of the water can be difficult since there is a strong surge. Many people end up slipping on the rocks, stepping on sea urchins and sharp coral and cutting their feet and legs. You may want to bring reef shoes to avoid injuries. </p>
                <p>If you decide to take a boat tour here, it is easier to get into the water to snorkel because that way, you can just climb off the boat in deeper waters and avoid stepping onto the rocky ocean bottom.</p>
                <p>The third option to get to the monument is by hiking here. However, the trail is uneven, steep, rock-strewn and may feel long, especially if you're not an avid hiker or on a hot day. Bring plenty of water and sunscreen and wear a hat and sturdy shoes. The hike takes about 2-4 hours round-trip (4.25 miles - 6.8 km). </p>
