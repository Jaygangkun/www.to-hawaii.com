                <h2 class="small">Kaimu Beach Eco-Path, Big Island</h2>
                <p>This scenic hike leads through a lava field that was created by the 1990 eruption of Kilauea volcano, which destroyed the ancient Hawaiian village of Kalapana, Kaimu and the Royal Gardens subdivision. The lava has now hardened and what remains is brand new shoreline land. There used to be a scenic black sand beach here that was framed by stately coconut palms, but it was buried under the lava, along with the entire neighborhood. Local residents have re-planted many palm trees in this area. </p>
                <p>A short 5-10 minute hiking trail was created that leads across this lava field to the ocean and a new black sand beach, appropriately named New Kaimu Black Sand Beach. It is a scenic hike, and it is amazing to see plants and palms growing inbetween the lava rocks and black sand. This hike is well worth it, and the good thing is that it is easily accessible and doesn't require you to be in top shape. It's an easy trail that even children can do.</p>
