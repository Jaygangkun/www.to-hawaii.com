                <h2 class="small">Waipio Valley, Big Island</h2>
                <p>Waipio Valley (<a href="../../big-island/attractions/more_waipiovalley_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on the Big Island's Hamakua Coast is the last and largest of the seven valleys east of the Kohala Mountains. Giant cliffs rising over 2,000 feet (610 m) surround the lush valley and give it an air of serenity, seclusion and mystery.</p>
                <p>Like many places on the Big Island, getting to the valley is somewhat difficult. If you plan to drive yourself, a 4-wheel drive vehicle is strongly recommended. The one-mile long road is paved, but steep and narrow. Alternatively, you can park at the Waipio Valley Lookout and from there hike down into the valley. </p>                <p>Once you reach the valley, there is a black-sand beach towards the right (after a 10-minute walk), which lines almost the entire shoreline of the valley. Even though the beach looks beautiful, swimming can be dangerous here due to rip currents and high surf, especially during the winter months. </p>
                                 <? include ("../../theme/text.html") ?>
                <p>After a 5-minute hike into the valley, you can see Hi'ilawe Falls in the distance, which is one of the tallest waterfalls in Hawaii, dropping 1,450 feet (442 m) with over 1,000 feet (305 m) of free-fall. Hiking here is possible, but difficult as there is no trail (it's a 2-mile, 4-hour roundtrip hike). Also, you'll have to walk through streams, bushwhack and ask residents for permission to cross their property. Sometimes the falls dries up completely, so trying to go there may not be worth the effort.</p>
                <p>There are a few accessible waterfalls to the south, such as Kaluahine Falls and Waiulili Falls. Legend has it that <a href="../../legends/night-marchers.php">nightmarchers</a> sometimes descend from the upper valley to the beach. They march to Lua o Milu, a secret entrance to the netherworld.</p>
                <p>The valley itself is hardly populated, but signposts have been put up to mark the sacred and private areas. You may also come across some wild horses who live here. The trail to the beach is public, but much of the valley is private property. If you plan to explore the valley, keep this in mind and ask for permission. For this reason it is best to explore the valley with a guided tour. </p>
