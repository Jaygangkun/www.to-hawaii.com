                <h2 class="small">Volcano Winery, Big Island</h2>
                <p>Hawaii isn't known as a wine destination, but the Big Island is home to the Volcano Winery, where visitors are treated to free wine tastings and can purchase unique Hawaiian fruit-blended wines with exotic names, such as Volcano Red and Volcano Blush.</p> 
                <p>The winery's specialties are two award-winning white wines made from the Symphony grape. The resulting wine is a cross between the Grenache Gris and Muscat, with a slight spiciness and a fruity flavor. Also a hit among visitors is the Macadamia Nut Honey Wine, perhaps because of its distinctly Hawaiian flavor. The sweet, tangy taste makes it a popular after-dinner wine. There are also fruit-blended wines flavored with local fruits, such as yellow guava and jaboticaba. </p>
                                <? include ("../../theme/text.html") ?>
                <p>Many of the wines are only available in Hawaii, so often times visitors take home a bottle or two. FAA regulations don&rsquo;t allow liquids on the plane (wines included), so you can have the wine packed to fit in your check-in luggage. Packaging is available for boxes of 3, 6, and 12 bottles. They only ship to a limited number of states, so check for shipping policies before ordering.</p>
                <p>The Volcano Winery is the southernmost winery in the United States. Located in Volcano, it makes for a great side trip if you&rsquo;re visiting other attractions in the area, such as the Hawaii Volcanoes National Park. </p>
                <p><strong>Hours</strong>: Open daily from 10 am to 5:30 pm </p>
