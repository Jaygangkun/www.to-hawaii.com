                <h2 class="small">King Kamehameha Statue, Hilo</h2>
                <p>The King Kamehameha Statue stands proudly near downtown Hilo in the Wailoa River State Park. It is one of four statues of King Kamehameha I. The other three are located in Kohala on the Big Island of Hawaii, in downtown Honolulu on the island of Oahu, and in Statuary Hall in Washington, D.C. </p>
                <p>                  King Kamehameha I, also known as Kamehameha the Great, who was born 1758 or 1737 or 1738 (there is some debate as to the precise year of his birth) and who died in 1819, was one of the most beloved of Hawaii's heroes. Kamehameha I was the one who conquered the Hawaiian Islands, unified them and established the Kingdom of Hawaii in 1810. Even during times of intense Western influence, his government managed to maintain peace and the traditional values. He developed alliances with the major Pacific colonial powers and by doing so preserved Hawaii's independence.</p> 
                                <? include ("../../theme/text.html") ?>
                <p>                  Until today, King Kamehameha is remembered for the Mamalahoe Kanawai, the Law of the Splintered Paddle, which, in times of battle, protects the human rights of non-combatants. Kamehameha's full Hawaiian name is Kalani Paiea Wohi o Kaleikini Keali'ikui Kamehameha o 'Iolani i Kaiwikapu kaui Ka Liholiho Kunuiakea.</p>
                <p>                  This statue in Hilo measures 14 feet (4.3 m) tall and is the tallest of the four statues of King Kamehameha. It was sculpted by R. Sandrin at the Fracaro Foundry in Vicenza, Italy, in 1963. However, the statue was placed here years later, in 1997. It had originally been commissioned for $125,000 by the Princeville Corporation for their Kauai resort. But because Kauai residents didn't want to see the statue erected on Kauai (Kauai was never conquered by Kamehameha I), the statue was donated to Hilo instead since the town was one of the king's political centers. </p>
