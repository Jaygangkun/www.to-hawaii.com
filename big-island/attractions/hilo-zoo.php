                <h2 class="small">Pana'ewa Rainforest Zoo, Big Island</h2>
                <p>Located just outside of Hilo, the Pana'ewa Rainforest Zoo is unique in that the animals live in a natural setting as they would in nature and are much less fenced in. The zoo prides itself as being &quot;the only natural tropical rainforest zoo in the United States.&quot; The grounds are lush and the entire zoo is set in a tropical rainforest, encompassing 12 acres (48.562 sq. m).</p> 
                <p>More than 80 animal species live here, such as a water buffalo, monkeys, tropical birds, pygmy hippos, Asian forest tortoises, iguanas, mongooses, lemurs, the Hawaiian nene, Laysan ducks, a feral pig and other animals. Peacocks roam the grounds freely, often walking among the visitors. </p>
                <p>The Pane'awa Rainforest Zoo also doubles as a botanical reserve. Volunteers from various local organizations have built a growing plant collection, which includes orchids, ferns, clumping bamboos and rhododendrons. Many are native to Hawaii and other Pacific islands, such as the crown flower and snow bush. There are also over 100 varieties of palm, which provide shade for picnics. </p>
