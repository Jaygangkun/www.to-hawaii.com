                <h2 class="small">Pololu Valley, Big Island</h2>
                <p>Pololu Valley (<a href="../../big-island/attractions/more_pololuvalley_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is one of the most remote locations on the Big Island, marking the dead end of Highway 270. Most visitors just come to the Pololu Valley Lookout, a scenic spot overlooking the lush valley and the surrounding sea cliffs. </p>
                <p>The valley itself is a steep 400 feet (122 m) down, accessible on foot, but the trail can be challenging for inexperienced hikers. It takes about 20 minutes to get to the beach and you&rsquo;ll have to walk down steep steps on a zigzagging dirt path that can be slippery when wet.</p>
                                <? include ("../../theme/text.html") ?>
                <p>At the water&rsquo;s edge lies a black-sand beach, which is backed by trees. Swimming here isn&rsquo;t recommended due to a strong rip current, especially in the winter months. But it&rsquo;s a nice place for a picnic. </p>
                <p>Pololu Valley used to be inhabited in the past and was among the richest wetland taro plantations in Hawaii. The water was carried here by the Pololu Stream, but when the Kohalo Ditch was built in 1906, much of the water was diverted and the taro production in the valley ended. The last residents of the valley left in the 1940s. </p>
                <h2 class="big">Pololu Valley Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Remote valley on the Big Island's northeastern shore</li>
                  <li>Most visitors come to the Pololu Valley Lookout to enjoy the scenic views of the valley below and the surrounding sea cliffs and ocean</li>
                  <li>Steep trail leads to the bottom of the valley (about a 20-minute hike)</li>
