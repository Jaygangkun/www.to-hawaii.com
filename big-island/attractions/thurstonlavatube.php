                <h2 class="small">Thurston Lava Tube, Big Island</h2>
                <p>The Thurston Lava Tube, also called Nahuku, is one of the magnificent sights located within the <a href="../attractions/hawaiivolcanoesnationalpark.php">Hawaii Volcanoes National Park</a>. Created over hundreds of years by volcanic activity, lava tubes are not uncommon in Hawaii. Molten lava flows beneath the ground's surface, insulated by the tube's roof of earth which keeps the lava flowing and molten for a long period of time. An eruption drains the lava from its cavernous tube and creates a vacant chamber once the earth cools.</p> 
                <p>Lorrin Thurston, the publisher of a local newspaper, discovered Nahuku in 1913. The several hundred year old tunnel's roof was once covered with stalactites when he discovered it, however, the tube rapidly lost them over the years at the hands of cave visitors and &lsquo;souvenir&rsquo; collectors. </p>
                <p>The trail to Nahuku, hidden beneath the boughs of the Hawaiian rainforest's canopy trees is only a 1/3-mile walk to the crater's pit. The entrance to the lava tube blends in well with its jungle-like surroundings, as it is disguised by the foliage that grows around it. Once inside, the lava tube is easy enough to navigate. The tunnel is lit by electric lights along the cavernous walls, and the ceiling is high enough to comfortably accommodate adults who wish to explore the tube. </p>
                <p>The exit is about halfway through the tube (this is the lit part). Adventurous explorers with a strong flashlight may want to see the unlit part as well, which continues for another 1,000 feet (305 m).</p>
