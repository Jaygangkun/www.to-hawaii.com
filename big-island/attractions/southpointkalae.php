                <h2 class="small">South Point - Ka Lae, Big Island</h2>
                <p>South Point, known as Ka Lae in the Hawaiian language (meaning &ldquo;the point&rdquo;) is the southernmost tip of the Big Island of Hawaii, as well as the United States. Only few people make it here because this undeveloped area is only accessible via a dirt road. A visit is well worth it though. Even though desolate, the landscape is beautiful in its own way. </p>
                <p>Drive south on South Point Road and you'll enjoy views of grassy plains speckled with lava rocks and once the road dead ends the deep blue ocean. Once you reach the sea cliffs at South Point and look south there's no other landmass between you and Antarctica, 7,500 miles (12,000 km) away. </p> 
                <p>It is believed that the first Polynesian voyagers who came to Hawaii arrived  at South Point some time between 400 and 800 A.D. Ruins of heiaus (temple sites) and fishing shrines have been found here. The entire area is registered as a National Historical Landmark, called the South Point Complex.</p>
                <p>Ka Lae is known for its strong winds and ocean currents. The wind is so consistent and strong here that some trees grew almost horizontally with their branches, all growing in the same direction. Because of these consistent winds, wind farms have been built in this area.</p>
                <p>The fishing is also good around South Point. Both ulua, red snapper, mahi mahi and marlin are plentiful here. Local residents come here and fish from the edge of the sea cliff or use toy boats to get their lines further offshore. Even the ancient Hawaiians have known about the good fishing here. They drilled holes in the rock ledges (which are still there today) to moor their canoes with long ropes. Like that they could paddle out without fear of being carried away by the strong currents and winds. (Anchors don't work here because the ocean bottom drops off quickly to great depths).</p>
                <p>Swimming anywhere around South Point is not a good idea because of the strong current, which even has a name. It's known as the &ldquo;Halaea current,&rdquo; named after a Hawaiian chief who was carried away by it and drowned. Also, beware of sudden large waves that clash against the cliffs here. </p>
