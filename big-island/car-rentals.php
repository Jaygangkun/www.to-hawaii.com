              <h2>Big Island Transportation Tips</h2>
<p>The best way to explore Hawaii's Big Island is with a rental car. There are rental car booths at the two major airports in Hilo and Kailua-Kona. You'll find all the major car rental companies - Alamo, Budget, Dollar, Enterprise, Hertz, National, Thrifty, etc.
<p>The Big Island also has a public bus system called Hele-On. The bus covers areas around Hilo, Kailua-Kona, Pahoa, Pahala, Ocean View, Waimea and Honokaa. The fare is $1 per trip. A Shared Ride Taxi program is also available, which provides door to door transportation for $2 within Hilo. For more information about public transportation on the Big Island, visit heleonbus.org.</p>
<p>Airport shuttles as well as taxis are also available. For airport shuttles it is best to make a reservation in advance.</p>
