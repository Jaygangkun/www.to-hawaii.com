                <h2 class="small">Hi'ilawe Falls, Big Island</h2>
                <p>Hi'ilawe Falls is the Big Island's tallest waterfall &ndash; a 1,450-foot (442 m) cascade with a free fall of 1,200 feet (366 m). It is located inside <a href="../attractions/waipio-valley.php">Waipi'o Valley</a> and can best be seen from the air. Hiking to the waterfall is possible, but because much of the land within the valley is private property it is best to view the waterfall from a distance to avoid any conflict with the valley residents. Once you reach the valley, follow the road to the left. The waterfall will be visible after a 5-10-minute walk.</p>
                
                <p>In the past, Hi'ilawe Falls (<a href="../../big-island/waterfalls/more_hiilawefalls_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) used to be a very powerful waterfall, but because Lalakea Stream above the waterfall was partially dammed (plus there are a few smaller ditches up the valley), the waterfall was reduced to a much smaller cascade and is often times a mere trickle.</p>
                <p>Hi'ilawe Falls also has a smaller twin waterfall called Hakalaoa Falls. But this one only flows during or right after rainfall.</p>
                <p>Note: The road down to Waipi'o Valley is very steep (25% grade). Regular cars are not able to drive down there, only SUVs with four-wheel drive are allowed. However, the road is tricky, so it is best to hike down. Alternatively, there are tour companies (such as Waipi'o Valley Shuttle or Waipi'o Valley Wagon Tours) that take guests into the valley.</p>
