                <h2 class="small">Onomea Falls, Big Island</h2>
                <p>Onomea Falls is a multi-tiered waterfall located within the <a href="../gardens/hawaiitropicalbotanicalgarden.php">Hawaii Tropical Botanical Garden</a>. To get to the waterfall, one has to enter the botanical garden (admission fee is $15). While this may sound steep for a botanical garden, it is well worth a visit. This garden is one of Hawaii's best. Besides the beautiful waterfall, you'll see a wide variety of tropical flowers and plants. The garden is very lush and colorful. </p>
                <p>The waterfall is nestled within thick vegetation of palm trees and ferns, and mosses are growing on the surrounding rocks and trees. There is a small wooden viewing bridge that leads across the stream. It is a serene spot that emits a rejuvenating energy.</p>
