                <h2 class="small">Pe'epe'e Falls and Boiling Pots, Big Island</h2>
                <p>If you're visiting popular <a href="../waterfalls/rainbowfalls.php">Rainbow Falls</a>, located in Hilo, you might as well continue up the road for another mile and see Pe'epe'e (pronounced Peh-eh-peh-eh) Falls as well. This lesser-known waterfall can be seen from a lookout point on the road (it is  distant view). Boiling Pots is closer to the lookout point, a series of small pools and cascades that can be quite turbulent if it has recently rained and there is lots of water coming down the stream. During heavy rain, the pots appear to be boiling (due to underground lava tubes), hence the name Boiling Pots.</p>
                <p>Pe'epe'e Falls is surrounded by lava rocks and lush vegetation and plunges about 80 feet (24 m) into a pool below. A small trail to the right of the viewing area leads to the pots below. However, it is not a good idea to hike down there when the stream flow is heavy because you could be swept away and drown. The hike to the waterfall will require some rock-skipping and you may get wet.</p>
