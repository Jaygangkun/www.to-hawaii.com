                <h2 class="small">Rainbow Falls, Big Island</h2>
                <p>Rainbows are a rare sight for most of us, but not when you're on the Hilo side of the Big Island of Hawaii. Just a short drive from Hilo is the aptly named Rainbow Falls, so named because of the morning rainbows that appear as the sun shines through the mist. The best thing about it is its accessibility. Unlike many other waterfalls on the Big Island, Rainbow Falls doesn't take a long hike or a four-wheel drive vehicle to access.</p>
                 
                <p>The source of the waterfall is Wailuku River, the longest in the Hawaiian archipelago at 26 miles (42 km) long. The river used to be impassable during heavy rain, until locals built three large bridges that traverse the water and offer excellent views. These bridges also provide a direct route to the falls for both drivers and hikers.</p>
                                <? include ("../../theme/text.html") ?>
                <p>The area around the falls is surrounded by large mango trees, which make it feel secluded despite being close to the city. Visitors can hear the full force of the water as it falls 80 feet (24 m) to the rushing pool below, a turquoise river surrounded by beautiful ginger shrubs. The Hawaiian name for Rainbow Falls is Waianuenue, meaning &ldquo;rainbow seen in water.&rdquo;</p>
                <p>Behind the waterfall is a deep cave believed to be the home of Hina, an ancient Hawaiian goddess. This is where the famed rainbow is commonly seen, as the sun reflects off the mist created by the water. The walk to the cave is rough and slippery, so tread carefully and make sure to wear sturdy shoes. The cave is worth exploring in the early morning, when there's enough light to see the rock formations. Beyond the Rainbow Falls lookout there is a short, unpaved path that leads from the left side of the parking lot to top of the waterfall. </p>
