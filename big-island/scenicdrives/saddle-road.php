                        <h2 class="small">Saddle Road, Big Island</h2>
                        <p>Buckle up! Slicing across the Big Island from east to west, through a high valley or &ldquo;saddle&rdquo; between the mountains of Mauna Kea and Mauna Loa, Saddle Road (also known as Hawaii Route 200) is 54 miles (87 km) of pure adventure. Along the way, you'll pass dried lava flows, rolling pastureland, desert- and moonlike fields, lush rainforests and trailheads that lead to several hiking trails. Also along the route are roads leading to the mountaintops of Mauna Kea and Mauna Loa, as well as the largest military training reserve in Hawaii and the Bradshaw Army Airfield. </p>
                              <? include ("../../theme/text.html") ?>           
                        <p>Saddle Road cuts through some of the best scenery on the Big Island. In the past, many car rental companies used to forbid use of their cars on Saddle Road, but now most allow it. For a long time, the road was considered the most dangerous in the state because it had many rough areas and marginally maintained pavement. Today, many sections of the road have been improved, but some rather dangerous spots remain. However, it is no worse than many other roads on the island and it is recommended to drive slowly and with caution. The area is remote and there are no facilities along the way, so if your car breaks down or you run out of gas, you have to walk a long way for help. </p>
                        <p>Saddle Road got its name from the stretch of road that passes between the Mauna Loa and Mauna Kea volcanoes. This &ldquo;saddle&rdquo; area is marked by small crosses to commemorate the people who have died while traversing the road and perhaps to remind drivers to take extra caution. Heavy mist is common in this area, so before setting out, make sure there's enough visibility. Also, there are many long, flat stretches and it's easy to become &ldquo;road hypnotized&rdquo; or get &ldquo;lead foot,&rdquo; so be careful. </p>
                        <p>The highest point on the road is at 6,632 feet (2,021 m). People have described this area as resembling a moonlike landscape, with long stretches of bare lava and very little vegetation. </p>
                        <h2 class="big">Saddle Road Overview</h2>
                        <ul type="disc" class="mylist">
                          <li>Might be the only stretch of road that takes you from rainforest to desert within a timeframe of 45 minutes</li>
                          <li>Starts in Hilo and leads all the way to Highway 190 just outside Waimea</li>
                          <li>Provides access to the slopes of Mauna Loa and the observatories atop Mauna Kea</li>
                          <li>Road was built in 1942 and used to be a gravel road, but was later paved</li>
                          <li>Western part of the road is a bit rough, but for only about 4-7 miles (rest of the road has been improved)</li>
                          <li>Subject to fog and low visibility (avoid driving here at night)</li>
                          <li>Shortest route from Hilo to Kailua-Kona</li>
