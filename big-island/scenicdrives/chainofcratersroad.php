                        <h2 class="small">Chain of Craters Road, Big Island</h2>
                        <p>There are two scenic drives within the <a href="../attractions/hawaiivolcanoesnationalpark.php">Hawaii Volcanoes National Park</a>. Most people explore the attractions and scenic lookout points along Crater Rim Drive, which leads around Kilauea Caldera. The second road, the Chain of Craters Road, leads from Crater Rim Drive (elevation 4,000 feet) all the way down to the ocean, a distance of 19 miles (it takes about 45 minutes one-way because the road is curvy).</p>
                         
                        <p>Along the way there are several scenic lookout points and the changing landscape is amazing. At the beginning of the road the scenery is lush and as you descend down the landscape becomes more and more desolate with the ocean on one side and black hardened lava fields on the other. </p>
                        <p>Chain of Craters Road has two lanes and is paved, so a four-wheel drive vehicle is not required. The road passes several craters on its way to the ocean, hence its name Chain of Craters. </p>
                                        <? include ("../../theme/text.html") ?>
                        <p>Past mile marker 16 there is a trail that leads to the Pu'u Loa Petroglyphs. It's an easy 20-minute walk (1.5-mile roundtrip hike) over an old pahoehoe lava flow leading to thousands of ancient Hawaiian petroglyphs (rock carvings). It's the largest petroglyph field in Hawaii. A boardwalk allows easy access to some of them, and visitors are asked to stay on this boardwalk and not step on the ancient carvings.</p>
                        <p>Another attraction is the Holei Sea Arch, located just before mile marker 19. It's easily accessible from the road (a one-minute walk). Just past the sea arch the Chain of Craters Road deadends. Further ahead the road was buried by lava from different flows from the Pu'u O'o vent that occurred between 1986 until recently.</p>
                        <p>Since the road is close to volcanic activity, conditions can change daily. Occasionally the road is closed. Check the daily conditions report before heading out. </p>
                        <h2 class="big">Chain of Craters Road Overview</h2>
                        <ul type="disc" class="mylist">
                          <li> 19-mile scenic drive from Kilauea Caldera down to the ocean</li>
                          <li>Scenic lookout points and trails along the way</li>
                          <li>Changing landscape, views of tropical forests to desolate lava fields</li>
                          <li>No gas station along Chain of Craters Road</li>
                          <li>Without stops the drive down and back up takes about 1.5 hours</li>
