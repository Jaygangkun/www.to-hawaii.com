                <h2 class="small">Queen Ka'ahumanu Highway, Big Island</h2>
                <p> Queen Ka'ahumanu Highway (Hwy 19) (<a href="../../big-island/scenicdrives/p1.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>)  leads from Kailua-Kona 33 miles (53 km) north along the coast and ends just south of Kawaihae, where it intersects Mamalahoa Highway  (Hwy 19) and Akoni Pule Highway (Hwy 270). It is a scenic highway and the only one that provides access to numerous North Kona and Kohala beaches and attractions, such as Kekaha Kai State Park, Kiholo Bay Lookout, Kua Bay, Mauna Kea Beach and Hapuna Beach, as well as the Waikoloa resort area. </p>
                <p>The Kona International Airport is also located off Queen Ka'ahumanu Highway. The views from Queen Ka'ahumanu Highway are beautiful. One can see the ocean almost the entire stretch of the road, which cuts through vast volcanic lava fields. Here is an interesting story about two ghostlike figures that were seen while driving north on Queen Ka'ahumanu Highway (<a href="../../mysterious-ghostlike-figures-big-island-hawaii.php">read story</a>).</p>
