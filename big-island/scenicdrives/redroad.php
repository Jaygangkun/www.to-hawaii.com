                        <h2 class="small">Red Road (Kapoho-Kalapana Road), Big Island</h2>
                        <p>The Red Road (also known as Kapoho-Kalapana Road 137) (<a href="../../big-island/scenicdrives/more_kapohokalapana_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>) is a scenic coastline drive in the Puna district on the Big Island's southeastern shore. The road was called Red Road because of its unique red cinder pavement. However, this pavement only remains at the northern stretch of the road (the Kapoho end). The rest of the road was resurfaced with traditional black asphalt in 2000. Nevertheless most people still call it the Red Road. </p>
                                     <? include ("../../theme/text.html") ?>    
                        <p>For the most part it is a narrow (about 8 feet wide) and winding road right along the ocean, over small hills, through lava fields and lush vegetation. This is what makes this drive unique in Hawaii &ndash; the great variety in scenery and great views. You will have to slow down because there are many blind hills and curves, but you will want to take your time anyway. </p>
                        <p>Along the way are a few points of interest &ndash; <a href="../beaches/ahalanuipark.php">Ahalanui Pond</a> (a volcanically heated pond where you can enjoy a dip), <a href="../beaches/mackenzie-state-park.php">MacKenzie State Park</a> (a large park with many trees from where you can watch the waves crash into steep sea cliffs) and <a href="../beaches/kehenabeach.php">Kehana Beach</a> (a remote black sand beach and one of the few unofficial nude beaches in Hawaii). Once you arrive in Kalapana (a village that doesn't exist anymore after it was destroyed by a lava flow in 1990), there is another unique attraction, the <a href="../attractions/kaimubeachecopath.php">Kaimu Beach Eco-Path</a>, an easy trail across a lava field that will take you to a small black sand beach). </p>
