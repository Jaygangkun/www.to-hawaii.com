                        <h2 class="small">Old Mamalahoa Highway, Big Island</h2>
                        <p>The Old Mamalahoa Highway on the Big Island's Hamakua Coast is a narrow and winding coastline road that begins about 5 miles (8 km) north of Hilo. The 4-mile (6.4 km) stretch until the <a href="../gardens/hawaiitropicalbotanicalgarden.php">Hawaii Tropical Botanical Garden</a> are breathtaking. The vegetation is very lush and there is a great lookout point along the way, right before you reach the botanical garden. It overlooks <a href="../beaches/onomeabay.php">Onomea Bay</a> and the surrounding rainforest. </p>
