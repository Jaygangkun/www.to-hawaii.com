          <h1>Big Island of Hawaii Events</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <div class="start"></div>
              <p><strong>January</strong></p>
              <p>No scheduled major events. </p>
              <p><strong>February</strong></p>
              <p>No scheduled major events. <br />
                <br />
                <strong>March</strong><br />
                <br />
                Kona Brewer's Festival: This annual event features microbreweries from all over the world. It includes beer tastings, food and entertainment. King Kamehameha's Kona Beach Hotel Luau Grounds, Kailua-Kona, Big Island. Second Saturday in March. <br />
                <br />
                Prince Kuhio Celebrations: A state holiday celebrating the birth of Jonah Kuhio Kalanianaole, who was born on March 26, 1871 and elected to Congress in 1902. All islands. March 26.<br />
                <br />
                <strong>April</strong><br />
                <br />
                Merrie Monarch Hula Festival: This is Hawaii's biggest and most prestigious hula festival. The week-long event features modern and ancient dance competitions honoring King David Kalakaua, the &quot;Merrie Monarch.&quot; He was the one who revived the dance. Tickets sell out at the end of January, so make your reservation early. Edith Kanakaole Stadium, Hilo. The week after Easter. <br />
                <br />
                <strong>May</strong><br />
                <br />
                Annual Lei Day Celebrations: Annual Lei Day Celebrations: May Day is Lei Day in Hawaii. It is celebrated with lei-making contests and arts and crafts on all the islands. <br />
                June<br />
                <br />
                King Kamehameha Celebration: King Kamehameha Celebration: A state holiday with a big flower parade and more. Takes place on all islands. First weekend in June. <br />
                Great Waikoloa Food, Wine &amp; Music Festival: Features Hawaii's top chefs (and a few mainland chefs) showing off their culinary talents. You can also sample wines from around the world and enjoy a jazz concert with fireworks. Hilton Waikoloa Village. Mid-June.<br />
                <br />
                Annual Hawaiian Cultural Festival: This event features demonstrations of traditional Hawaiian arts and crafts, island foods, hula lessons and reenactments of the Hawaiian royal court. Puuhonua o Honaunau National Historical Park, South Kona, Big Island. Late June or early July.</p>
              <p> 
               <? include ("../../theme/text.html") ?>
               
               <p> <strong>July</strong><br />
                <br />
                Parker Ranch Rodeo and Horse Races: A great rodeo competition. It takes place in Waimea, the heart of the Big Island's cattle country. Watch real-life paniolos (Hawaiian cowboys) in action. July 4. <br />
                <br />
                Hawaiian Slack-Key Guitar Festival: Hear the best of Hawaiian slack-key guitar music performed by the best musicians of the islands. This five-hour long event is free. King Kamehameha's Kona Beach Hotel, Kona, Big Island. <br />
                <br />
                Turtle Independence Day: Each year, green sea turtles, which have been raised in captivity, are released from the historic fish ponds at Mauna Lani. Watch how they race down to the ocean. Mauna Lani Resort and Bungalows, Kohala Coast, Big Island. July 4.<br />
                <br />
                Great Waikoloa Food, Wine &amp; Music Festival: This one of the Big Island's best food and wine festivals. It features Hawaii's greatest chefs, who present food and wines from all over the world. The event includes a jazz concert and fireworks. Hilton Waikoloa Village, Big Island. Weekend before July 4.<br />
                <br />
                Crater Rim Run and Marathon: A gathering of around 1,000 runners from all over the world to compete in 5-, 10- and 26-mile races over uneven lava terrain, through rainforests and up the walls of volcanic craters. Hawaii Volcanoes National Park, Big Island. Late July.<br />
                <br />
                <strong>August</strong><br />
                <br />
                Annual Hawaiian International Billfish Tournament: This is one of the most prestigious billfish tournaments in the world attracting teams from around the world. Kailua-Kona, Big Island. Mid-August.<br />
                <br />
                Puukohola Heiau National Historic Site Anniversary Celebration: A weekend of Hawaiian crafts, games and workshops. Kawaihae, Big Island. Mid-August.<br />
                <br />
                <strong>September</strong><br />
                <br />
                Aloha Festivals: Aloha Festivals: Parades, street festivals and other events in celebration of Hawaiian culture. Takes place on all islands. Each week from mid-September through October is Aloha week on a different island. <br />
                <br />
                Sam Choy Poke Recipe Contest: A poke-making competition. Top chefs from across the state and the U.S. mainland, as well as amateurs, compete against each other in making this traditional Hawaiian dish, which consists of chopped raw fish mixed with seaweed and spices. Hapuna Beach Prince Hotel and Mauna Kea Beach Resort, Kohala Coast, Big Island. <br />
                <br />
                Queen Liliuokalani Outrigger Canoe Race: This is the world's longest canoe event in which about 2,500 paddlers from across Hawaii, the U.S. mainland, Canada and the Pacific compete against each other. Kailua Pier to Honaunau and back. Labor Day weekend. <br />
                <br />
                <strong>October</strong><br />
                <br />
                Hamakua Music Festival: This event features famous musicians ranging from Hawaiian, blues, jazz, rock 'n' roll to classical. Hamakua, Big Island. Early October. <br />
                <br />
                Ironman Triathlon World Championship: This competition features about 1,500 world-class athletes. They run a full marathon, swim 2.5 miles and bike 112 miles on the Kona-Kohala Coast. You can watch them along the route for free. The best place to see the 7 am start is along the seawall on Alii Drive. The best place to see the bike-and-run portion is along Alii Drive (which is closed during the race; spectators have to park on a side street and walk to it). The finish is at Palani Road/Alii Drive. <br />
                Kailua-Kona, Big Island. Mid-October. </p>
              <p><strong>November</strong><br />
                <br />
                Hawaii International Film Festival: Hawaii International Film Festival: A statewide movie festival featuring film makers from Asia, the Pacific Islands and the United States. Most screenings take place on Oahu, but on the final weekend, events take place on all the islands. First two weeks in November. <br />
                <br />
                Annual Kona Coffee Cultural Festival: This celebration of the coffee harvest features a bean-picking contest, songs, dances and the Miss Kona Coffee pageant. Kailua-Kona, Big Island. <br />
                <br />
                Big Island Festival: This five-day-long event is one of Hawaii's largest festivals. It celebrates the island's cultural diversity with foods, Hawaiian music, exhibits, cultural activities and agricultural products. Kona-Kohala coast, Big Island. <br />
                <br />
                <strong>December</strong><br />
                <br />
                First Night: This is Hawaii's largest festival of arts and entertainment. It takes place on Maui and on the Big Island. Musicians, actors, dancers and magicians perform for 12 hours. Fireworks at midnight welcome the New Year. On the Big Island it takes place in Kailua-Kona. December 31. </p>
