                <h2 class="small">Kona Coffee Living History Farm, Big Island</h2>
                <p>The Kona Coffee Living History Farm is one of the cultural gems of Hawaii&rsquo;s Big Island. History truly lives on in the ancient artifacts, authentic farmhouses and the vivid storytelling of experienced farmhands. No visitor leaves the farm without a newfound appreciation of farm life and the way Hawaiians lived in the past.</p>
                <p>The site is officially known as the<strong> D. Uchida Coffee Farm</strong>, named after the farm&rsquo;s owner and developer. For the most part, it depicts the lives of the farmers as they planted, picked and harvested for long hours to keep the trade afloat. It is also a tribute to the Japanese immigrants who pioneered the coffee and macadamia nut industry on the island from 1925 to 1945.</p>
                                 <? include ("../../theme/text.html") ?>
                <p>The farm encompasses 5.5 acres (22.258 sq. m), so you won&rsquo;t run out of things to see. Some sites worth checking out include the farmhouse, the macadamia nut orchard and the nut processing mill. Along the way, costumed interpreters will provide interesting accounts of day-to-day farm life. Of course, you&rsquo;ll also get to sample Kona coffee, cultivated on the Hualalai and Mauna Loa volcanoes and considered one of the world&rsquo;s most luxurious coffees.</p>
                <p><strong>Admission:</strong><br />
                  Adults-$15; Children (5-12 years)-$5; Children under 5 are free.</p>
                <p><strong>Tours: </strong><br />
                  Self-guided tours include the orchard, farmhouse and coffee-processing mill. <br />
                  Guided tour available Monday-Friday 10 am to 2 pm <br />
