                <h2 class="small">Hilo Coffee Mill, Big Island</h2>
                <p>Contrary to its name, this coffee farm is not located in Hilo, but in Mountain View, a town about a half hour drive south of Hilo. It is on the way to the Hawaii Volcanoes National Park, so if you plan to go there or are coming from there, this coffee farm is worth a visit. </p>
                <p>They grow their coffees right here on their 24-acre farm. There is a viewing area where you can watch them roast and package their coffees. Tours and coffee samples are being offered as well. </p>
                <p>There is a small coffee shop where you can purchase coffee by the cup or get an entire package to take home. They also sell other gift items, including chocolates, soaps, teas and more. And besides coffee, they also offer smoothies, milk shakes, ice cream and breakfast and lunch foods. You can eat it inside or outside on their lanai. <br />
                  <br />
                  <strong>Hours:</strong><br />
                  Monday through Saturday: 8 am to 4:30 pm<br />
                  Closed on Sundays.</p>
