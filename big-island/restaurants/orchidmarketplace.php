                <h2 class="small">Orchid Marketplace, Big Island</h2>
                <p>The Orchid Marketplace Restaurant at the Hilton Waikoloa Village is a casual outdoor dining  environment, primarily focused on ensuring everyone has fun. Situated by the  Kona Pool, the atmosphere is charming and casual and the cuisine  family-friendly. The poolside setting is designed to be fun and relaxing, with  the chance to catch a quick bite to eat or a refreshing drink on the move. The  menu offers a range of tasty food to suit every palate - kids and adults.</p>
                 
                <p>The selection of Grab 'n Go Snacks at the Orchid Marketplace is all about  being able to grab something quick and filling before getting back to the fun.  To this end, there are plenty choices on the menu such as paninis, deli-type  sandwiches and much more. Other quick snacks that can be enjoyed poolside are  poke bowls and pizzas, all of which are good at keeping keiki satisfied and  providing them with nourishing food. The keiki menu includes a choice of  grilled cheese paninis, mini pizzas or a bento box that includes a sandwich and  fruit juice. The range of poolside pizzas is quite comprehensive with a good  selection of toppings. All pizzas are freshly baked in the restaurant's special  brick oven. These are perfect as a quick snack or as a flavorful lunch for all  the family. The pizzas at Orchid Marketplace are both delicious and very  popular. And, of course, refreshing beverages are a necessity, whether as an  accompaniment to food or as a thirst quencher, and there is an ample choice at  this lovely poolside restaurant.</p>
                <p>The Orchid Marketplace is open seven days a week - from Sunday through to  Monday. Opening hours are generally from 11.00am until 4.00pm but these can vary according to  seasonal demands. Ultimately, though, family-friendliness, fun, informality and  tasty food are the aims and trademark of the Orchid Marketplace. </p>
