                <h2 class="small">Imari, Big Island</h2>
                <p><strong>Permanently closed</strong></p>
                <p>Featuring distinctive Japanese cuisine, Imari at the Hilton Waikoloa Village is a captivating place where  diners can sample an array of Japanese food, both classic and contemporary  dishes. Imari is the only restaurant on the Big Island that offers Teppanyaki dining,  where a chef prepares exotic and artistic dishes right in front of the  customer. The type of Teppanyaki delicacies diners can savor at Imari include  jumbo shrimp, filet mignon, scallops and tender chicken with stir-fried  vegetables and steamed rice. The preparation is an experience all diners are  likely to remember for a long time to come.</p>
                 
                <p>In fact, Imari offers three distinct styles of Japanese dining. In  addition to the Teppanyaki style already described, the restaurant offers an a  la carte menu with a selection of Japanese dishes that are prepared with a  Hawaiian twist. The a la carte dining area overlooks a Japanese garden, which  provides a sense of peace and serenity. Here, diners can avail of a family-type  menu that combines Japanese cuisine with a hint of Hawaiian fare using as many  locally produced ingredients as is possible. These "Izakaya" type meals are  designed to be shared by diners. Then there is the Sushi  Bar and Open Kitchen where the restaurant's chefs display their culinary  artistry by preparing an array of exquisite delicacies. With this option,  diners can sample a wide range of exciting sushi and sashimi rolls, all  innovatively prepared to showcase the chef's unique skill and flair. </p>
                <p>Offering an unforgettable dining experience, opening days at Imara are  Monday, Wednesday, Thursday and Saturday where the three different styles of  dining are available from 5.30pm until 9.00pm. The restaurant is closed on  Sundays, Tuesdays and Fridays. The bar is open from 5.00pm until 9.00pm on the days the restaurant is  open for food. It is also worth noting that opening days and hours of service  may vary according to the season.</p>
