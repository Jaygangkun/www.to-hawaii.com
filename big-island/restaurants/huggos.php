            <h1><span itemprop="name">Huggo's, Big Island</span></h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <p>With its central location in Kailua-Kona, Huggo's is a well-known and  popular dining spot. Located on the oceanfront, the atmosphere is casual yet  elegant. With an endless horizon to gaze out upon, diners can enjoy the sight  of the surf foaming and swirling just a few feet below them at high tide.  Indeed, it is commonplace to hear loud applause from patrons at Huggo's when  the anglers who fish virtually every night nearby make a catch. Perhaps, with  its ocean location, it is not surprising that fish is a highlight on the menu  at Huggo's, although the restaurant is also renowned for its steaks, salad bar  and other culinary creations.</p>
                 
                <p>Opened since 1969, Huggo's was initially a casual meeting place where  fishermen would gather and swap stories. And even though fish does feature  heavily on the menu, the restaurant is constantly updating the menu to make the  most of local produce, whether that is harvested from the land, sea or shore.  While the Teriyaki Steak and Barbecued Beef Ribs never cease to be popular, new  menu items are continually appearing. The fish offered on the menu is all  recently caught and is prepared using Pacific Rim style recipes. Other menu  favorites at Huggo's include the Kona Poke appetizer, the Maui Onion Crusted  Ahi and the Crab-Crusted Ono. There are also a number of innovatively-prepared  chicken dishes and an extensive array of salads, all obtained from the  bountiful supply provided by the local land. With regards the dessert menu, one  popular offering is the Hualalai Ice Cream Pie while the Ginger Creme Brulee is  fast becoming a favorite. This is particularly good when it is served with  fully French-pressed Kona Coffee. The  extensive wine list features 80 types, 18 of which are available by the  glass. </p>
                <p>With the aim of providing much more than just a meal, Huggo's has been  the recipient of many awards. The restaurant has a separate lounge that  overflows onto a dining deck outside. Here, guests can choose from a wide array  of tropical drinks such as the Kilauea-a-flaming, which served in a bowl to be  shared by any two or four guests if they are courageous enough! Huggo's is open  seven days a week from 11.30am until midnight.</p>
                <p>A bistro-style diner, Island Lava Java is a favorite coffee house and restaurant  on the Big Island. With two locations, one at Waikoloa and one  at Kona, the restaurant prides itself on using the highest quality local  produce and ingredients. Although its breakfasts have been voted the &ldquo;Best  Breakfast in Kona&rdquo; by <em>West Hawaii Today</em> readers, Island Lava Java also serves lunch, dinner, and Sunday brunch. </p>
                <p>With a commitment to using the best local ingredients available, the beef  used by Island Lava Java is grass-fed and reared on local farms and the goat&rsquo;s  cheese is also organic and locally sourced. The bread is made in-house and the  desserts are home-made. The breakfast menu consists of an array of benedicts,  omelets, bacon, fish, waffles and pancakes. Lighter breakfasts, such as  croissants, bagels and yogurt are also available and there is always a  plentiful supply of fresh 100% Kona Coffee on offer. The lunch menu is  comprised of a selection of Fresh Fish, Burgers, Pizzas and Salads. The dinner  menu is divided into a choice of pupus, salads and entr&eacute;es. The selection of  entr&eacute;es includes Fresh Fish, Fish Tacos, Burgers, Ribs, Pork Chops and Chicken.  The Sunday brunch menu is also extensive, featuring many old breakfast and  lunch favorites. With mouth-watering treats like Chocolate Cappuccino  Cheesecake and Chocolate Decadence Cake, there are plenty sweet options to  round off every meal in the most satisfying way. As well as Kona Coffee, drinks  on offer at Island Lava Java include tea, fresh fruit smoothies, shakes,  speciality beverages and more. </p>
                <p>With a bakery on-site to freshly make the restaurant&rsquo;s own bread,  pastries and pizza dough each day, Island Java Lava is open seven days a week  every day of the year. Opening hours are from 6.30am until 9.00pm and a new online ordering service  has recently been introduced.&nbsp;&nbsp; </p>
