                <p>The flagship restaurant of chef Peter Merriman - one of the founding  members of Hawaii Regional Cuisine - Merriman's Market Caf&eacute; has long been one  of the Big Island's best culinary treasures. Located at the  Waikoloa Beach Resort in scenic upcountry Waimea, Merriman's offers its patrons  a selection of Italian/Mediterranean cuisine, most of it inspired by the famed  chef. </p>
                 
                <p>Having been named "Best Big Island Restaurant" by&nbsp;<em>Honolulu  Magazine</em>&nbsp;for 15 years in a row and having received the Wine  Spectator's Award of Excellence for more than ten years running, Merriman's Market  Caf&eacute; has also received praise from <em>The New York Times</em>&nbsp;and&nbsp;<em>The  Los Angeles Times</em>&nbsp;for its locally sourced food and exciting menu. This  restaurant really is where Hawaii Regional Cuisine originated and it does  showcase many of Hawaii's authentic flavors, which are  available for one and all to sample. The menu at Merriman's features a range of  produce this is either locally-grown or reared; the freshly-caught fish comes  from the ocean around the Kohala Coast, the meat from grass-fed cattle,  and as much other fresh produce as possible from local farms. Each ingredient  is specially selected and represents the myriad of flavors and bountiful  harvest that Hawaii has to offer. The fish and meat are prepared in quite distinctive ways  so that the cuisine is entirely representative of Hawaii's multicultural nature. The  result is a menu that contains a number of signature dishes, all of which  showcase authentic Hawaii Regional Cuisine at its finest. Kahua Naturally  Raised Lamb and Classic Wok Charred Ahi are just two examples from Merriman's  menu.</p>
                <p>With both indoor and outdoor seating and a fully-stocked bar available,  Merriman's Market Caf&eacute; is open seven days a week. Lunch is served from 11.30am until 1.30pm on Mondays through to Fridays and  dinner every night (Monday through to Sunday) from 5.30pm until 9.00pm. Brunch is available on Saturdays  and Sundays from 10.00am until 1.00pm. In addition to the eat-in  facilities, a take-out menu, featuring steaks, fresh fish and other fare, is  available.</p>
