                <p>The Big Island Grill is located in a small strip mall in Kailua-Kona.  This restaurant serves American cuisine with Hawaiian Island influences. The portions are  generous, the prices affordable and the atmosphere unassuming. Being a popular  eating spot for Big Island residents, the Grill is often  very full so there may be a bit of a wait before you find a seat. Therefore, it  might be wise not to arrive too hungry!</p>
                 
                <p>The Big Island Grill offers a real feel of island life, catering mostly  for local residents as well as for visitors looking for some respite from the  higher prices at other eateries. There is a bright and clean feel to the  restaurant and the tables are uncluttered. Most food items from the menu are  served in huge portions. Life at the Big Island Grill starts early each day  with breakfast, followed later in the day by lunch and dinner. Several types of  loco moco, which is a combination of meat, egg, gravy and rice, are available  from the breakfast menu, as well as gigantic helpings of French toast  accompanied by Portuguese sausage. There is also a selection of sandwiches on  the menu as well as fresh broiled fish served with coleslaw. Other offerings  include chicken katsu, fried chicken, home-made burgers with rich gravy and  topped with two eggs, tender kalua pork (roasted) and served with cabbage, lau-lau and a selection of pan-seared or grilled fish dishes.</p>
                <p>The dessert menu has a number of tempting sweet treats that include sweet  potato haupia cheesecake and banana pancakes. While delicious, the desserts are  also very large so these may be best shared. Serving hearty portions at reasonable prices, the Big Island Grill is  open seven days a week. Breakfast is served from 7.00am until 10.30am on Monday through to Saturday,  lunch from 10.45am until 5.00pm and dinner from 5.00pm until 9.00pm. Opening hours on Sunday are from  7.00am until noon, with breakfast served continually during  these hours.</p>
