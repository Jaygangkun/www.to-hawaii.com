                <h2 class="small">Puako, Big Island</h2>
                <p>Pukao is a small oceanfront community on the Big Island's Kohala Coast. The town is one of Hawaii's best kept secrets, known for its crystal clear, cobalt blue waters, and  top notch snorkeling and diving conditions. Hapuna Beach, one of the nicest white-sand beaches on the island, is a stone's throw away from Puako. This area is one of the driest on the Big Island, receiving only about 10 inches of rain per year.<br />
                  <br />
                  Pukao's main attraction though is its large collection of intricate petroglyphs carved into volcanic rocks.  At the Puako petroglyph field, visitors can see over 3,000 breathtaking vestiges of these ancient rock drawings. <br />
                  <br />
                  The community has a mixture of working-class homes, as well as a few upscale mansions and vacation rentals. When it comes to shopping, there aren't many options here as there's just one general store. <br />
                  <br />
                  Hokuloa Church is located right on Puako Road. It was built in 1859 by Rev. Lyons, who was very knowledgeable in the Hawaiian language. He composed many ballads, including &quot;Hawaii Aloha,&quot; which has become the unofficial anthem of Hawaii.<br />
                  <br />
                  The best way to enjoy the charm of Pukao is to stroll along its shore and explore the many tide pools, which are home to a wide variety of marine life. Offshore is one of the best nearshore scuba diving locations on the Big Island, with many caverns, lava tubes and a fantastic reef to explore.</p>
