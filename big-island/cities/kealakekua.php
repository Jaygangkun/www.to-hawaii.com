                <h2 class="small">Kealakekua, Big Island</h2>
                <p>The historic town of Kealakekua is most famous for being the landing spot of Captain Cook, who sailed into <a href="../beaches/kealakekua-bay.php">Kealakekua Bay</a> in January 1779 and was killed here a month later. A monument on the shore memorializes his death. <br />
                  <br />
                  The town is surrounded by the 600-foot (183 m) high Pali Kapu o Keoua sea cliffs, where the royal ali'i (high chiefs) were buried in ancient Hawaii. At the base of the cliffs is Kealakekua Bay, one of the best snorkeling and diving spots in all of Hawaii. </p>
                <p>A declared Marine Life Conservation District, the bay is home to dolphins, sea turtles and a wide variety of other tropical fish. Kayaking and snorkeling to explore the bay are the two most popular activities here.</p>
