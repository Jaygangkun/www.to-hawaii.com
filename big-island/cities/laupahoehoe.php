                <h2 class="small">Laupahoehoe, Big Island</h2>
                <p>Laupahoehoe, meaning &quot;leaf of lava,&quot; is a small village on Big Island's Hamakua Coast offering breathtaking ocean vistas &ndash; breaking waves, craggy lava rocks and lush vegetation. With this scenic beauty, one would never imagine that the town keeps one tragic past. <br />
                  <br />
                  On April Fool's Day, 1946, a tsunami hit the state of Hawaii, and the Big Island's windward coast was hit the hardest. A total of 159 people were killed, including 21 schoolchildren and three teachers in Laupahoehoe. A memorial has been erected in their memory at the Laupahoehoe Point Park. In the following years, the village was relocated to higher ground. </p>
                                 <? include ("../../theme/text.html") ?>
                  <p>Laupahoehoe used to be a sprawling sugar plantation town in the past. But even government subsidies couldn't keep the sugar industry afloat. The last mill in this area was forced to shut down in 1996. Today, a new crop is being planted here &ndash; eucalyptus trees.<br />
                  <br />
                  If you are interested in more history, visit the <a href="../museums/laupahoehoe-train-museum.php">Laupahoehoe Train Museum</a>, which offers mementos and photographs documenting the history of the sugar era and the Hawaii Consolidated Railroad. The museum is located near the Laupahoehoe scenic overlook on Mamalahoa Highway. It is open from 9 am to 4:30 pm on weekdays, and from 10 am to 2 pm on weekends.</p>
