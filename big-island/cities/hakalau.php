                <h2 class="small">Hakalau, Big Island</h2>
                <p>Hakalau, located on the Big Island's Hamakua Coast, is a small village which was once home to a large sugar plantation up until the early 1960s. Remnants of the village's prosperous past can still be seen in the old railroad bridge that leads to Hakalau Bay and the long-defunct plantation towers you can see from the road. Also located on the shoreline where the Hakalau stream meets the bay are the ruins of the Hakalau Mill, which was destroyed in the tsunami of 1946.<br />
                  <br />
                  Today, there are a few family farms growing tropical fruits, coffee, flowers and doing cattle ranching. The bay is mainly used by surfers and local fishermen. At the bottom of the Hakalau Gulch, there is a small park with picnic tables and ocean access.<br />
                  <br />
                  Located nearby is the Hakalau Forest National Wildlife Refuge, a 32,733-acre (134 sq. km) wilderness preserve that his home to endangered species, including the Hawaiian creeper, Hawaiian hoary bat, nene, &lsquo;io, &lsquo;akepa, i'iwi and pueo. The Upper Maulau Tract, located in the northernmost section of the wildlife refuge, is open for hiking (with prior permission) on weekends and holidays (call Refuge Office a week prior to your visit to get a permit: 808-933-6915). However, it's a two-hour drive from Hilo to get here and half of the drive is on a dirt road, accessible only with a four-wheel drive vehicle. </p>
