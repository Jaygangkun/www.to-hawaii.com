                <p>Holualoa is a small coffee-farming village situated 1,400 feet (427 m) above Kailua-Kona. The town is primarily a single road called Mamalahoa Highway and along it are several galleries exhibiting the works of local artists, as well as coffee shops brewing Kona coffee that has been grown right here. <br />
                  <br />
                  Holualoa got its name from the ancient Hawaiian practice of sledding the long &quot;loa&quot; mountain slopes using &quot;holua&quot; or wooden sleds. This sledding site is now known as Mamalahoa Highway. <br />
                  <br />
                  In 1778, when Europeans started coming to Hawaii for trade, coffee cuttings from Brazil were brought in to Holualoa by Samuel Reverend Ruggles. Later in the century, coffee became the prime product of Holualoa until its market value dropped. <br />
                  <br />
                  Landowners leased their coffee lands to their mostly Japanese tenants who grew quality coffee in small quantities for local consumption. Planting coffee as family crops was continued with the arrival of Filipino, European and American migrants. Until today, one can smell the coffee aroma as one drives down the slope of Mount Hualalai. </p>
