                <h2 class="small">Kapa'au, Big Island</h2>
                <p>Located on the northern tip of the Big Island near <a href="../beaches/keokeabeachpark.php">Keokea Beach Park</a> is the  small historical village of Kapa'au. It is best known for being home to the statue of King Kamehameha the Great (and near his birthplace as well), who unified the Hawaiian  islands under his rule. Each spring, Kapa'au honors the king with a parade,  games and festivities. The celebration ends with  a lei-draping ceremony of the King Kamehameha statue, which stands on the grounds of the former courthouse (today this building is the Kohala Information Center). <br />
                  <br />
                  The statue has an interesting history. Built in Paris in 1878,  the statue was put on a ship en route to Hawaii. However, the vessel sank at  sea near the Falkland Islands in the South Atlantic. Since the shipment was  insured, the Hawaiian Legislature ordered a new one. In the meantime, the  captain of the wrecked ship later found the statue that was believed to be  lost. It stood in Port Stanley. He purchased it for $500 and shipped it to  Hawaii, where it was erected in Kapa'au. The second statue of Kamehameha now  stands in front of Ali'iolani Hale (Judiciary Building) in downtown Honolulu. </p>
