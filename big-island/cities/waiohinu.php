                <h2 class="small">Wai'ohinu, Big Island</h2>
                <p>The small town of Wai'ohinu, which means &quot;shiny water&quot; in the Hawaiian language, is a small community located in the Ka'u district near <a href="../attractions/southpointkalae.php">South Point</a>. There is not much going on in this small town. <br />
                  <br />
                  Author Mark Twain visited Wai'ohinu in 1866 and planted a monkeypod tree here. The large tree called the Mark Twain Monkeypod Tree became the town's main attraction until it was hit by a windstorm in 1957 and destroyed. But a shoot from the tree was replanted and so the tree grew back. Today, it has turned into a sizable tree once more. Another part of the tree's original trunk, which has been carved into a bust of Twain, is on display at the <a href="../museums/lyman-house-museum.php">Lyman House Museum</a> in Hilo.</p>
                <p>Wai'ohinu has just one store, the Wong Yuen Store and Service Station, which was built by the Chinese immigrant Wong Yuen in 1914. There is also a small park with a pavilion. The county water spigot is also located here, where local residents go to get drinking water.</p>
