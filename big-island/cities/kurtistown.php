                <h2 class="small">Kurtistown, Big Island</h2>
                <p>Located at an elevation of 620 feet (190 m), Kurtistown it is one of the most agriculturally productive communities on the Big Island of Hawaii. There are many banana plantations and flower gardens here. The town's botanical gardens even serve as some of the prime tourist stops in the area. Three of these gardens are the Fuku Bonsai Cultural Center, Linda&rsquo;s Place of Hawaii and the Royal Palm Enterprises. </p>
                <p>In the 18th and 19th century, while other Big Island towns were engaged in sugarcane and coffee production, Kurtistown was planting bananas. It was banana that played a major role in the town's economy. Native Hawaiians in Kurtistown even considered bananas sacred, revering the fruit for possessing the same characteristics as that of the Hawaiian god Kanaola. Today, Kurtistown remains a top producer of banana on the Big Island. </p>
