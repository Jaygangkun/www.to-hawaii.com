                <h2 class="small">Kea'au, Big Island</h2>
                <p>Kea'au, formerly known as Ola'a, is located in the eastern part of the Big Island in the Puna District. The town is surrounded by an Ohia forest, and nearby are black-sand beaches and a plethora of botanical gardens. The town's 20-hectar greenhouse Pacific Floral Exchange and Hi'iaka Healing Herb Garden are the most visited. <br />
                  <br />
                  Kea'au was once connected to the neighboring towns of Hilo and Pahoa by the Hawaiian Consolidated Railway. The railroad was used to transport agricultural products and passengers in the 19th century until it was destroyed by the 1946 tsunami. <br />
                  <br />
                  Today, the town is a bedroom community for Hilo's workforce. On weekdays in the mornings and late afternoons, the traffic is bad in this area as everybody commutes to and from work in Hilo.</p>
