                <h2 class="small">Pahala, Big Island</h2>
                <p>This former sugar town is located in the Big Island's Ka'u District. Sugar was grown here up until the 1990s and the local townspeople believed that this would always be that way because &quot;sugar is king.&quot; However, the marketplace changed and sugar production in Hawaii became too expensive, so Pahala's large sugar mill is closed today. </p>
                <p>Today, there are a few coffee farms in the hills above Pahala, producing Ka'u coffee, which is not as known yet as the popular Kona coffee, but gaining a reputation on its own.</p>
                <p>Pahala has a few eateries, schools, a gas station, a small shopping center, a post office, a hospital and a few churches. With a population of 1,356 (2010 Census), it is the largest town in Ka'u.</p>
