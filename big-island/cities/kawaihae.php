                <h2 class="small">Kawaihae, Big Island</h2>
                <p>Kawaihae (<a href="../../big-island/cities/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a small port town along the Big Island of Hawaii's northwestern Kohala Coast. During the 19th century, Kawaihae was a busy whaling and trading port. Historically, Kawaihae Harbor was also the place where the ship that brought the first horses and cattle to Hawaii docked. During the late 1950s, Kawaihae Harbor was improved into a deep draft harbor to accommodate more ships. Today, it is one of the largest harbors in Hawaii. Large cargo ships arrive here, which give it more of an industrial atmosphere. Some scenes of the Hollywood movie <em>Waterworld</em> were shot here. </p>
                <p>There are a few small shops, restaurants and a gas station, but not much else. The main attraction in this area is the 83-acre <a href="../ancientsites/puukoholaheiau.php">Pu'ukohola Heiau</a>, an ancient war temple built by King Kamehameha the Great (located south of Kawaihae). Another attraction near Kawaihae (north of town) is Pua Mau Place, a botanical garden on the western slope of the Kohala Mountain. The garden showcases unique sculptures, everblooming plants, a hibiscus maze (planted with more than 200 varieties of hibiscus), large lava rocks on ravines and the Magic Circle (a circular arrangement of stones patterned after standing stone circles of the Celtics in the British Isles). </p>
