                <h2 class="small">Waikoloa Village, Big Island</h2>
                <p>Located about 6 miles (9.7 km) inland from the Waikoloa resort area is the small community of Waikoloa Village. Many new condo complexes have been built here in the recent past and the town is steadily growing. Local residents often times call the town &quot;Waiko-blow-a&quot; because it is often times quite windy here. The town is located below the saddle between the mountains of Mauna Kea and Kohala. Since the tradewinds usually blow from the northeast, they funnel right across this saddle and blow down the slope. This also creates a nice challenge for golfers at the nearby Waikoloa Village Golf Club, which  is open to the public. <br />
                  <br />
                  Further down toward the ocean along the coastline are Waikoloa's anchialine ponds, which are an interesting part of Waikoloa's natural history and have been maintained by the University of Hawaii as the Waikola Anchialine Pond Preservation Area.  So-called anchialine ponds are coastal ponds that have no above the surface connection to the ocean. But the water in them still rises and falls with the tides. That's because they are connected to the ocean below the ground.  Many of them are also connected to lava tubes, which in turn are connected to inland springs, so the water in these anchialine ponds is often brackish water (a mixture of saltwater and freshwater).<br />
                  <br />
                  Many such anchialine ponds in Hawaii are home to organisms that are endemic to the Islands (found nowhere else in the world). The Waikoloa anchialine ponds are home to three shrimp species that can only be found here on the Big Island.<br />
                  <br />
                  Waikoloa also has an ancient petroglyph field, rock drawings that were carved by the early Hawaiians. These drawings in a way were their written language. The petroglyphs are located along the King's Trail, also known as Ala Kahakai, meaning &quot;trail by the sea&quot; in the Hawaiian language. This ancient trail runs along the Waikoloa coastline. In the old Hawaii, it linked communities, heiaus (temple sites) and fishing areas. The petroglyph field is surrounded by the Beach Course golf course and is accessible from the King's Shops.</p>
