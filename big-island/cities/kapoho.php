                <h2 class="small">Kapoho, Big Island</h2>
             <p><em style="color:#C30"><strong>Editor's Note:</strong> Due to a recent lava flow that occured on June 5, 2018, this beautiful destination was unfortunately destroyed. We are leaving this page up for all of us who have made memories here and would like to remember it as it used to be.</em></p>
                <p>                  Kapoho has the distinction of being the sunniest spot in the eastern side of the Big Island, which is why it is often referred to as &quot;Sunny Kapoho.&quot; The area is also a snorkeler's delight. It may have no white-sand beaches, yet its coastline boasts numerous tide pools and lagoons rich with marine life. <br />
                  <br />
                  At Kapoho Bay, there's Champagne Pond, a tide pool with geothermally heated freshwater. This ocean-fed inlet is also called Turtle Pond because sea turtles like to hang out here. However, it has suffered from overuse in the past, which has created sanitation problems. An alternative volcanically heated tide pool is located at nearby <a href="../beaches/ahalanuipark.php">Ahalanui Beach Park</a>.</p>
                                  <? include ("../../theme/text.html") ?>
                <p>Nearby is Green Lake and Green Mountain, one of the most scenic spots in Hawaii. Surrounded by rainforest, the spring-fed Green Lake is one of only two natural freshwater lakes on the Big Island. Green Mountain, the crater that holds the lake, is 400 years old. <br />
                  <br />
                  Despite its idyllic beauty, however, Kapoho is a ghost town. In the past, Kapoho was an agricultural community with schools, churches and homes. The town was destroyed by a lava flow when Kilauea erupted in 1960. When the lava approached the ocean at Cape Kumukahi, it parted and spared the lighthouse that stands there. It's the only building that wasn&rsquo;t buried under molten rock. Kapoho was never resettled.<br />
                  <br />
                  Kapoho Bay is only accessible with a four-wheel drive vehicle. If you have a regular car, you'll have to walk a little over a mile to reach the bay. A little further south is the area called <a href="../beaches/kapohotidepools.php">Kapoho Tide Pools</a>. Vacation rentals are available here (called Vacationland). Next door is a gated community called Kapoho Beach Lots.</p>
