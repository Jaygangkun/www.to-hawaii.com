                <h2 class="small">Kailua-Kona, Big Island</h2>
                <p>Kailua-Kona (<a href="../../big-island/cities/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located along the shoreline of Kailua Bay on the west shore of the Big Island of Hawaii, enjoys a combination of sunny and dry weather all year round. This is where King Kamehameha the Great, the king who conquered the Hawaiian Islands and established the Kingdom of Hawaii, spent the last years of his life, worshipping at <a href="../ancientsites/ahuena-heiau.php">Ahuena Heiau</a>, the temple he built in 1813.</p>
                <p>In the 19th century, Kailua-Kona was a leisure retreat for Hawaii's ali'i (royalty). <a href="../museums/huliheepalace.php">Hulihe'e Palace</a> was a favorite getaway among them, especially King Kalakaua. Since the 1970s, Kailua-Kona has been the Big Island's economic center, fueled by tourism, real estate and retail (even though Hilo is the governmental seat of the island). </p>
                <p>Kailua-Kona is located along Queen Ka'ahumanu Highway (Hwy 19). Ali'i Drive is the main road that runs through the heart of the city and is where all the main tourist attractions are located, especially at the north end of it. </p>
