                <h2 class="small">Hawi, Big Island</h2>
                <p>Hawi, together with the closest town of Kapa'au, covers a great portion of the population in the Big Island's North Kohala District. It is the largest town on the island's north shore and has several art galleries, shops, a few restaurants and a hotel. Hawi was once a bustling sugar plantation town and contributed to the sugar production in Hawaii. Back then, Hawi had not just one, but four movie theaters. </p>
                <p>In the early 1970s, the Kohala Sugar Company stopped operating and the local economy was hit hard, since almost everyone worked in sugar production. However, many residents stayed in Hawi and toughed it out, and today, the town is making a comeback, this time as a tourist attraction. Hawi is a cozy little town, known for its picturesque charm. </p>
