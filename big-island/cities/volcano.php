                <h2 class="small">Volcano, Big Island</h2>
                <p>Volcano is located at the entrance to the <a href="../attractions/hawaiivolcanoesnationalpark.php">Hawaii Volcanoes National Park</a>. The Park is one of the most popular visitor attractions in the entire state of Hawaii, so a visit to the charming little town of Volcano is on the to-do list of many Big Island visitors. Because the National Park is big and it takes multiple days to see it all, some visitors stay in Volcano for a few days because of its convenient location.<br />
                  <br />
                  Volcano is also referred to as Volcano Village to avoid any confusion, since the entire area here is basically one big volcano. The village literally sits on the slopes of one of the most active volcanoes in the world &ndash; Kilauea. One may think that the surrounding landscape is barren and rocky from the dried lava, but in addition to the nearby dark lava fields and rocks, Volcano&rsquo;s scenery is breathtakingly lush. Flowers, ferns and trees are everywhere. The small town is very picturesque with charming cottages and plantation style homes. <br />
                  <br />
                  Even though Volcano is a small settlement, there are a few restaurants, two general stores, a hardware store, a post office, a farmer's market, a winery, as well as a few artists' studios and bed &amp; breakfasts. Because it is located at an elevation of about 3,750 feet (1,143 m), the temperature dips significantly at night to a chilly 55 F (13 C) in summer and 40 F (4.5 C) in winter. Daytime temperatures hover around 75 F (24 C) in summer and 65 F (18 C) in winter. <br />
                  <br />
                  Volcano receives over 150 inches of rain per year, but it is as beautiful when it rains as it is on a bright and sunny day.  Local residents even refer to the rains as &quot;liquid sunshine.&quot; Thanks to the rain, the landscape looks so beautiful and green. The lush environs are a draw for many hikers and nature enthusiasts. There are many walking trails, snaking through miles and miles of greenery.</p>
