                <h2 class="small">Hilo, Big Island</h2>
                <p>Situated on the rainy windward side of the Big Island, Hilo (<a href="../../big-island/cities/more_hilo_photos.html" onclick="NewWindow(this.href,'name','550','630','yes');return false">see more photos</a>) is the wettest city in the United States with an average rainfall of 128 inches (325 cm) annually. The University of Hawaii and Hilo International Airport serve the education and transportation needs of the community. The city is the largest on this side of the Big Island, as well as the most tropical in Hawaii. Because of the abundance of rain, everything flourishes and exotic flowers and lush greenery are a normal part of the landscape.</p>
                <p>Polynesians who lived along the shores of Hilo Bay first inhabited the area in 1100 A.C. As farmers and fishermen, they traded goods along the Wailuku River. In 1700, under King Kamehameha's rule, Hilo became the center of political activity and social growth. In the early 19th century, missionaries brought not only Christianity, but also Western education which further changed the lifestyle of the native Hawaiians, and Hilo grew into a commercial center. </p>
                                <? include ("../../theme/text.html") ?>
                <p>The booming sugar industry led to the construction of wharfs, a breakwater and a railroad that connected Hilo to other parts of the island. On April 1, 1946, a tsunami hit the island and killed 96 people in Hilo alone (and 159 people in total). It also destroyed much of Hilo&rsquo;s waterfront, as well as train trestles and bridges. Hilo then rebuilt its waterfront, but in 1960, another tsunami hit, killing 61 people and again destroyed most coastal buildings. After that, no one wanted to live near the ocean anymore, so today, Hilo's bayfront is mainly shops, parks and open space. </p>
                <h3 class="big">Hilo Overview</h3>
                <ul type="disc" class="mylist">
                  <li>One of the oldest settled towns in Hawaii</li>
                  <li>Hilo hosts the week-long celebration of ancient and modern hula, the Merrie Monarch Festival (takes place annually after Easter)</li>
                  <li>Nearby attractions include Rainbow Falls and Boiling Pots (naturally bubbling whirlpool tubs), located in Wailuku River State Park </li>
                  <li>It rains more here than in any other city in the U.S., but the rains create a natural greenhouse with exotic flowers and vegetation everywhere</li>
                  <li>City boasts several tropical gardens, flower farms and lush, tropical scenery</li>
                  <li>Population: 43,263 (2010 Census)</li>
                  <li>Zip code: 96720</li>
