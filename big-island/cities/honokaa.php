                <h2 class="small">Honoka'a, Big Island</h2>
                <p>Honoka'a (<a href="../../big-island/cities/more_honokaa_photos.html" onclick="NewWindow(this.href,'name','500','630','yes');return false">see more photos</a>) is the largest town on the Hamakua Coast (and once was the third largest in all of Hawaii, after Honolulu and Hilo) with a rich history due to the dozens of sugar plantations and cattle industry it had more than a hundred years ago. The last sugar plantation in Honoka'a (Hamakua Sugar Company) stopped its operation in 1994, but the townspeople and their rich heritage have lived on up to this day. <br />
                  <br />
                  The heart of the town features a few timber-framed stores along the wooden boardwalk of Mamane Street. New and innovative ways to revive the town's economy have made local and foreign entrepreneurs invest in ranching, tourism and farming. <br />
                  <br />
                  One of Honoka'a attractions is the Art Deco Honoka'a People's Theater, which was built in 1930 and still features occasional movie screenings and musical performances, as well as the Hamakua Music Festival each year in October. Then there's the Hawaiian Shop, also located on Mamane Street, a nice place to find a unique souvenir. <br />
                  <br />
                  A 60 megawatt power plant is also operational in the area, which sells power to the rest of the island. Although there are no nearby beaches in Honoka'a, there is a wide range of activities to do, such as hiking, cycling and hunting. A public sport gymnasium where you can play baseball, tennis and run on a tracking field is also accessible in the area. </p>
