                <h2 class="small">Mountain View, Big Island</h2>
                <p>True to its name, this town provides ample mountain views. In fact, one of the world's tallest mountains and volcanoes &ndash; Mauna Kea &ndash; looms next door. Located inland at an elevation of 1,440 feet (439 m), Mountain View has no lava shoreline or glimmering Pacific Ocean views. Nonetheless, there's no lack of scenic spots or activities. Biking and hiking  are some of the activities that abound in the area. <br />
                  <br />
                  Mountain View is also home to many nurseries specializing in anthuriums. The Hawaiian Flower Gardens Nursery, for example, is open daily and visitors are welcome. <br />
                  <br />
                  Owing to the town's rich and fertile land, Mountain View was also historically involved in the sugar trade. It was once connected to the city of Hilo by the Hawaii Consolidated Road. Since the decline of the sugar industry in Hawaii, Mountain View has been involved primarily in coffee production. Today, the town is home to several coffee mills, including the most renowned of them all, the Hilo Coffee Mill.</p>
