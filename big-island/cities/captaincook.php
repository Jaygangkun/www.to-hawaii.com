                <h2 class="small">Captain Cook, Big Island</h2>
                <p>The small town of Captain Cook (<a href="../../big-island/cities/p1.html" onclick="NewWindow(this.href,'name','2550','720','yes');return false">view panorama</a>) is located in the land division of Kealakekua on the Big Island's west shore. It is named after Captain Cook, the circumnavigator who landed here in January 1779 and was killed a month later.</p>
                <p>Captain Cook has a few cozy B&amp;Bs and small shops, including kayak rentals, a gas station and grocery store. It is the last of the &quot;big&quot; towns in this area (further south are just small villages and scattered houses) and the gateway to attractions in this part of the island. Snorkeling, fishing, sailing, sightseeing and scuba diving are the main activities in this area. </p>
                <p>If you are in here in March, visit the yearly Kona Stampede Rodeo at the Honaunau Rodeo Grounds, located at the road leading down to Pu'uhonua O Honaunau. It's a nice event where you can watch local cowboys in action.</p>
