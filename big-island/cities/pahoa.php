                <h2 class="small">Pahoa, Big Island</h2>
                <p>Pahoa is located in the Big Island's Puna District. It is also known as the Big Island's hippie capital, a place where New Agers, hippies and others living an alternative lifestyle reside. The pride of &quot;downtown&quot; Pahoa is its raised wooden sidewalk, the false-front stores and the numerous old buildings, which give it a &quot;Wild West&quot; atmosphere. In fact, Pahoa has the highest concentration of old buildings in Hawaii.</p>
                <p>There are a few small restaurants with a variety of cuisines, craft stores, galleries, boutiques and the island's oldest movie theater. The Pahoa Marketplace, which is located at the edge of town, has more shops, but is not as scenic as the main street. Pahoa also has schools, a church, supermarkets, police and fire departments, a large public swimming pool and a post office.</p>
                                <? include ("../../theme/text.html") ?>
                <p>In 1955, Pahoa was almost destroyed by a fire. Right in the middle of town was a tofu factory that had a wood-fired furnace. The owner usually banked his fires before he went home, but that night, the fire got out of control. It burned all the way to the main alley. Luckily, a papaya farmer who had water loaded on the back of his truck saw what happened and extinguished the fire, saving Pahoa from destruction. <br />
                  <br />
                  In the past, the area around Pahoa was timber and later sugarcane land, but today there are many nurseries located here growing anthuriums. Papayas are also grown here in large numbers.<br />
                  <br />
                  An attraction just outside of Pahoa is the <a href="../attractions/lavatreestatepark.php">Lava Tree State Park</a>. This area was once a lush rainforest, but lava flows encased the trees and burned the wood inside. What remains are hollow stone skeletons that are covered with moss &ndash; an otherworldly rock forest. </p>
