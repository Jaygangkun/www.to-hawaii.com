                <h2 class="small">Waimea (Kamuela), Big Island</h2>
                <p>Waimea is also known as Kamuela. The name  is used to distinguish this Big Island town from Kauai and Oahu towns that are also named Waimea. It is located in the interior of the Big Island in the Kohala District at an elevation of 2,670 feet (814 m). Because of this, the air is nice and crisp here, unlike in other areas that are at sea level. <br />
                  <br />
                  Though a small town, Waimea has many modern amenities and historical places. It offers numerous restaurants, a large shopping center, theater, museums, art galleries, boutiques and shops. There's also the historic, New-England style Imioloa Church. Built in 1857, the church features a memorial dedicated to Lorenzo Lyons, the priest who translated English hymns into Hawaiian and Hawaiian songs into English. If you're interested in more history of the town, drop by Parker Ranch Museum, the Thelma Parker Theater, or the Parker Ranch Historic Homes and Garden. <br />
                  <br />
                  Kamuela is the Hawaiian name for Samuel. It is not clear whether the town was named after the postmaster named Samuel Spencer or after Samuel Parker, the son of John Parker Palmer who founded Parker Ranch. Parker Ranch played a very important role in the economic development and even cultural evolution of the town. During its prime, Parker Ranch helped Waimea (Kamuela) become the center of cattle rearing in the entire state of Hawaii. Cattle ranching also influenced the development of the town&rsquo;s distinctive paniolo (or Hawaiian cowboy) personality. Today, Waimea remains the home of many paniolo cowboys as well as newcomers.</p>
