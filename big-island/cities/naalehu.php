                <h2 class="small">Na'alehu, Big Island</h2>
                <p>Located near South Point in the Big Island's Ka'u District, Na'alehu bills itself as the southernmost town in the United States. There are only a few small shops and eateries in this quaint little town, but it's definitely worth a visit, if not for the charming monkeypod trees that frame some of the town's old plantation homes. <br />
                  <br />
                  Two popular festivals take place here. Each year on July 4, the annual Na'alehu Rodeo is a fun event for amateur riders who show off their bull-riding skills. And on Labor Day, the Hawaiian Hula Festival is celebrated. On this day, people gather at Na'alehu Park to enjoy food, music, crafts, and of course, hula. <br />
                  <br />
                  Na'alehu means &quot;volcanic ashes&quot; in the Hawaiian language. But the lava flows of the past have cooled and dried and today, Na'alehu is far from being an unsightly remnant of a volcanic eruption. Lush vegetation, green valleys and hills surround the town, and black sand beaches are located nearby. <br />
                  <br />
                  The Ka'u District is the warmest area on the Big Island. In 1931, Na'alehu hit 100 degrees Fahrenheit (37.8 C), the highest recorded temperature in the entire state of Hawaii. </p>
