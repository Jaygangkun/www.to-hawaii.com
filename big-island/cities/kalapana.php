                <h2 class="small">Kalapana, Big Island</h2>
                <p>Kalapana (<a href="../../big-island/cities/more_kalapana_photos.html" onclick="NewWindow(this.href,'name','500','630','yes');return false">see more photos</a>), located in the Big Island's Puna District, was once a thriving fishing village. Located near it was Kaimu Beach, one of the largest and most popular black-sand beaches on the Big Island, as well as a famed spring-fed pool on lava rock known as &quot;Queen's Bath.&quot; Today, Kalapana is one dark ocean of solidified lava. <br />
                  <br />
                  It all began in 1983, when Kilauea erupted and the lava started to flow towards this area. This time, however, the lava didn't come close to Kalapana yet, but flowed downslope to the west of it. But in 1990, from April to December, the flow changed direction and swallowed up much of Kalapana. Only a few residential properties survived. Residents watched as the lava destroyed their homes, burying everything under 30-75 feet (9-23 m) of molten rock. <br />
                  <br />
                  When the lava flow threatened to destroy the <a href="../attractions/staroftheseapaintedchurch.php">Star of the Sea Painted Church</a>, residents moved it away to safety. Just an hour later, the ground where the church once stood was buried. The lava destroyed almost the entire village, as well as Kaimu Beach. Many local residents took it with relative ease and instead of being upset about their loss said that Pele (Hawaii's volcano goddess) is reclaiming the land. <br />
                  <br />
                  Today, a few families still reside in this barren ghost town in buildings that were spared. They have no water and no access to electricity and move in and out of the town using four-wheel drives. Although access to the town has been cut off, there is a bed and breakfast, as well as makeshift tents of rangers who monitor the traffic of visitors who tour the site. The dead end of the highway, which has been cut off by the lava flow, is now a tourist attraction. There are a few booths selling photos, gifts and ice cream. <br />
                  <br />
                  Though the lava destroyed Kalapana's beautiful black-sand beach, it created a new one. There is a short path that leads to it. When exploring the area, be careful and observe the warning signs. The land is still unstable and may cave in. </p>
