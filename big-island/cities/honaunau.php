                <h2 class="small">Honaunau, Big Island</h2>
                <p>Honaunau is a small community located at Honaunau Bay in South Kona. The area is known for its national historic park, which was once a sanctuary for ancient lawbreakers. 
                  In the 12th century, Pu'uhonua o Honaunau, also known as the Place of Refuge, was built in Honaunau to be used as a religious haven. In those times the Hawaiians had to abide by the kapu system, which defined societal rules and laws. (Kapu translated means &ldquo;forbidden&rdquo; or &ldquo;sacred&rdquo;). Breaking a kapu, even unintentionally, often times meant immediate death. <br />
                  <br />
                  To avoid this fate, a lawbreaker could try to get to a pu'uhonua, a place of refuge. However, it was difficult to get to one. But once a lawbreaker made it to a pu&rsquo;uhonua, he was spared. Inside the place of refuge, kahunas (priests) performed absolution ceremonies.<br />
                  <br />
                  The Pu'ohonua o Honaunau had 10-foot (3 m) high walls and next to it were the Royal Grounds, the sacred home of the ali'i (chiefs). Fierce ki'i, or wooden images of gods, guard the Hale o Keawe heiau, the sacred temple which once used to house the bones of deceased ali'i. <br />
                  <br />
                  During the rule of King Kamehameha II, traditional religious practices ended. In 1961, the site became a national historical park. <br />
                  <br />
                  Another attraction in Honaunau is the St. Benedict's Painted Church. It was founded by Belgian priest John Berchmans Velghe, who came to Hawaii in 1899. He wanted to convert the illiterate natives, so he painted the church walls with biblical scenes as visual aids. It is a picturesque place to explore. Services in Hawaiian take place every second Sunday of the month at 7:15 am. </p>
