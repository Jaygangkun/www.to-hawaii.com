          <h1>Big Island Camping at Campgrounds / Campsites</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p><img src="../../images/bigislandcamping.jpg" alt="Big Island of Hawaii Camping" style="width:100%; max-width: 478px;" /></p>
              <p>The Big Island's terrain is the most diverse of any Hawaiian island. From camping on a tropical beach to camping in an alpine environment and climate, it's all possible here. There are a variety of camping areas &ndash; state campsites, county campsites and national park campsites. If you plan to camp on the Big Island, you will need to obtain a camping permit in advance. <br />
                <br />
                <strong>Camping at a Hawaii state park on the Big Island</strong><br />
                <br />
                All state camping areas are administered by the Hawaii DLNR, State Parks. They prefer that all reservations are made on their online portal, which is located at: www.hawaiistateparks.org/camping/hawaii.cfm (see the button &ldquo;Online Reservations&rdquo; in the top left corner on their website). You can make your reservation up to one year in advance with the exception of the Mauna Kea State Recreation Area, where permits can be applied for no more than 30 days in advance.<br />
                <br />
                <strong>Here is a list of all state campsites on the Big Island:</strong></p>
              <p><a href="../beaches/hapunabeachstatepark.php">Hapuna Beach State Recreation Area</a> (A-frame shelters)<br />
                Kalopa State Recreation Area (tent camping and cabins)<br />
                <a href="../beaches/mackenzie-state-park.php">Mackenzie State Recreation Area</a> (tent camping only)<br />
                <a href="../attractions/manukastatewaysidepark.php">Manuka State Wayside</a> (tent camping only)<br />
                <a href="../attractions/maunakeastatepark.php">Mauna Kea State Recreation Area</a> (cabins)<br />
                <br />
                <strong>Here is an overview of the state campsite fees:</strong><br />
                <br />
                Hawaii residents: $12 per campsite per night for up to 6 persons; $2 per night for each additional person*. Maximum fee per site: $20/night.<br />
                Non-residents: $18 per campsite per night for up to 6 persons; $3 per night for each additional person*. Maximum fee per site: $30/night.<br />
                *Children 2 and under free.<br />
                <br />
                The Hapuna Beach State Recreation Area has 4-person A-frame shelters that feature a single room with wooden sleeping platforms and a table. Nearby is a pavilion with a refrigerator, a range and tables. Cold showers and restrooms are shared by all campers. The rates for a shelter are $30 per shelter per night for Hawaii residents, and $50 per shelter per night for non-residents.<br />
                <br />
                The Kalopa State Recreation Area has duplex cabins that sleep up to 8 people. They are equipped with bunk beds, hot showers and toilets. There is a central dining hall which is shared by all campers. It features a range, refrigerator, water heater, dishes and cooking/eating utensils. All campers must bring their own towels and linens. The rates for a cabin are $60 per cabin per night for Hawaii residents, and $90 per cabin per night for non-residents.</p>
              <? include ("../../theme/text.html") ?>
              <p>The Mauna Kea State Recreation Area has cabins that can accommodate up to 6 people. The cabins are only available on weekends. They are equipped with a range, refrigerator and toilet. They don't have running water or cookware. All campers also have to bring their own bedding and towels. The rates are $50 per cabin per night for Hawaii residents, and $80 per cabin per night for non-residents.</p>
              <br />
              <strong>Camping at Hawaii County campsites</strong><br />
              <p> There are 10 county campsites on the Big Island. To get a camping permit for one of the county campsites, contact the County of Hawaii Parks &amp; Recreation. On their website you can download and print a camping permit request form and mail it in. The form is located here: www.co.hawaii.hi.us/parks/Camping.htm  There is also a link on this website where you can pay and obtain your camping permit online.</p>
              <p> <strong>Here is a list of all Hawaii County campsites:</strong></p>
              <p><a href="../beaches/hookenabeachpark.php">Hookena Beach Park</a><br />
                <a href="../beaches/isaachalebeachpark.php">Isaac Hale Beach Park</a><br />
                <a href="../beaches/kapaabeachpark.php">Kapaa Beach Park</a><br />
                <a href="../beaches/kolekolebeachpark.php">Kolekole Beach Park</a><br />
                <a href="../beaches/laupahoehoepointbeachpark.php">Laupahoehoe Beach Park</a><br />
                <a href="../beaches/mahukonabeach.php">Mahukona Beach Park</a><br />
                Milolii Beach Park<br />
                <a href="../beaches/punaluublacksandbeach.php">Punaluu Beach Park</a><br />
                <a href="../beaches/spencerbeachpark.php">Spencer Beach Park</a><br />
                <a href="../beaches/whittingtonbeachpark.php">Whittington Beach Park</a></p>
              <p><strong>Here is an overview of the county campsite fees:</strong><br />
                <br />
                Adults (18+ years): $5 per day (plus $1 transaction fee if booked online)<br />
                Juniors (13-17 years): $2 per day (plus $0.25 transaction fee if booked online)<br />
                Children (0-12 years): $1 per day </p>
              <p><strong>Camping within Hawaii Volcanoes National Park<br />
                </strong><br />
                There are two drive-in campgrounds within the Hawaii Volcanoes National Park, Namakanipaio and Kulanaokuaiki. At  neither one of them are permits or reservations needed. You just arrive and pitch your tent. There is no fee for camping, but since the campgrounds are located within the national park, you already pay an admission fee to get into the park. Stays are limited to 7 days in a month and cannot exceed 30 days per year.<br />
                <br />
                The Namakanipaio Campground is located 31.5 miles (50.7 km) south of Hilo on Highway 11 at an elevation of 4,000 feet (1,219 m). It is a large, open grassy area with eucalyptus and ohia trees. This campground has restrooms, picnic tables, barbecue pits and water. <br />
              </p>
               
              <p>The Kulanaokuaiki Campground is located 5 miles (8 km) down the Hilina Pali Road at an elevation of 2,700 feet (823 m). Two of the eight camp sites are wheelchair accessible. There is a vault-type toilet (no running water) and picnic tables, but no water.</p>
              <p>There are also several other shelters within the park that are only accessible via trail and often times involve a long, strenuous hike. Two of them are the Pu'u Ula'ula (Red Hill) cabin, located at 10,035 feet (3,059 m), and the Mauna Loa summit cabin, located at 13,250 feet (4,039m). Both cabins have bunk beds. Hikers should have adequate equipment with them and be experienced in high altitude trecking and physically fit.</p>
              <p>Four other campgrounds that are only accessible via trail are located near the ocean. They are at Ka'aha, Halape, Keauhou and Apua Point.<br />
              </p>
