              <p>Generally speaking and from a visitor's point of view, the weather on the Big Island divides the island in half.  The Hilo side (east side of the  island) is much wetter than the Kona side (west side of the island). Most  people associate Hilo with rain. In  fact, Hilo is the wettest city in  the United States  (no, it's really not Seattle!). On  average, the city receives about 130 inches (3302 mm) of rain per year. Most of  the rain falls at night and early morning, and daytime showers are usually  intense, but short-lived. </p>
              <p> But the advantage of all the rain on the Big   Island's east shore is that it's so  green. Hilo can be described as one  big natural greenhouse, with blooming flowers, waterfalls and tropical foliage  everywhere. The green surroundings, dark lava rocks on the shore and deep blue  ocean make for a picture-perfect sight (when the sun is shining). Kona, on the  other hand, is so dry that there are no waterfalls or streams here and the  scenery is rather barren and by far not as lush as the Hilo  side. </p>
                     <? include ("../theme/text.html") ?>
              <p> However, since not all rain gets absorbed into the ground and also flows  into the ocean, this so-called runoff makes the ocean look darker and murkier  on the Hilo side. That's why the  snorkeling and diving conditions on the Kona side of the island are far  superior to the Hilo side. The Kona  side also has more sandy beaches than the Hilo  side, which has a predominantly rocky shoreline.</p>
       
