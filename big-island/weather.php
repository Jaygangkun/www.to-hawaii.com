              <p><strong><br />
                Volcanic smog (vog) on the Big Island</strong></p>
              <p>A common natural phenomenon on the Big   Island is vog (volcanic smog).  Since the island is home to Kilauea, an active volcano,  the eruptions produce vog. Kilauea's Pu'u O'o vent spews out about 200 tons of sulfur dioxide and other toxic fumes  each day. Thanks to the tradewinds a lot of these fumes are quickly blown out to sea. Areas around the Hawaii Volcanoes National Park and the Big Island's Kona Coast are the most affected by this vog. </p>
              <p>Vog makes the air and sky look hazy and can cause  problems for people who suffer from asthma or other breathing problems. For those people it is best to avoid vog-prone areas or to stay indoors when the vog is thick. Since  the tradewinds generally blow from the northeast, the vog is thicker on the  Kona side of the island.</p>
