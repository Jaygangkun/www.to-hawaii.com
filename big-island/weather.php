              <p>The Kona side of the Big Island  is dry and sunny on most days of the year. That's why most hotels and resorts  on the island are located here because visitors prefer sunshine. The Kohala  area (located north of Kailua-Kona) is the driest area on the island, receiving  just about 10 inches (254 mm) of rain per year. When it rains here, it usually  rains in the late afternoons and evenings.</p>
              <p align="center"><img src="../images/kailuakona_temp.jpg" alt="Average temperatures in Kailua Kona" style="width:100%; max-width: 449px;" /></p>
              <p align="center"><img src="../images/kailuakona_temp2.jpg" alt="Kailua Kona temperatures and rainfall" style="width:100%; max-width: 476px;" /></p>
              <p>Even though the main difference between Hilo  and Kona is the amount of rain, when it comes to the temperatures there isn't  much difference. The year-round average temperature in both cities is around 74&deg;F (23&deg;C). It's cooler in Waimea (Kamuela), which is located at an elevation  of about 2,670 feet (814 m). Here the average year-round temperature is around  70&deg;F (21&deg;C). The temperature in the town Volcano, which is at the entrance to  the Hawaii Volcanoes National Park at about 3,800 feet (1,158 m), hovers around  60&deg;F (15.5&deg;C), while the summits of the Big Island's tallest mountains, Mauna  Kea and Mauna Loa, are usually about 30&deg;F cooler than the coastlines.</p>
              <p>The Big Island's <strong>average ocean temperature</strong> is around 75&deg;F (24&deg;C) in February and 82&deg;F (28&deg;C) in September. The water can be a bit colder in areas where freshwater springs bubble up from the ocean bottom, such as for example at Carlsmith Beach Park or Ice Pond in Hilo.</p>
              <p><strong>Big Island of Hawaii climate </strong></p>
              <p> No other island in the state of Hawaii  experiences such a variety in weather and climate zones as the Big   Island. In fact, according to the  widely used K&ouml;ppen Climate Classification System, the Big   Island has 10 of the 15 climate  zones in the world, which is quite amazing. It is difficult to think about  another place in the world of equal size that is home to that many different  climates. </p>
                          <p> On the Big Island,  you'll find everything, from tropical and desert, to monsoonal, periglacial and  other climates. On no other Hawaiian island (and in only very few other places  in the world) can you walk in snow in the morning on top of a tall mountain and  in the afternoon go swimming in the ocean and sunbathe on a tropical beach.  This is what makes the Big Island of Hawaii a truly unique place.</p>
