                <h2 class="small">Ahu'ena Heiau, Big Island</h2>
                <p>Located on a small artificial island across from Kamakahonu Beach and King Kamehameha's Kona Beach Hotel in Kailua, the Ahu'ena Heiau (<a href="../../big-island/ancientsites/more_ahuenaheiau_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see photos</a>, <a href="../../big-island/ancientsites/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) &ndash; a temple of peace and prosperity &ndash; was built by King Kamehameha I between 1812 and 1813 to honor Lono, the god of fertility. It served as the king's personal refuge and was his home for the last years of his life. Many ritual prayers took place here, but it was not a place of human sacrifice.</p>
                <p>For the rituals at Ahu'ena Heiau, Kamehameha gathered the kahuna and the focus of the rituals were humanity's higher nature. The oracle tower (anu'u) is the tallest structure of the heiau. It is where the chief kahuna went into deep trance and received messages from the gods. </p>
                <p>It is believed that Kamehameha I himself died here. According to belief, his bones were prepared soon after he died according to an ancient ritual and taken to a secret burial place, which is believed to be Wawahiwaa Point, located north of Kailua. The temple is so revered today that no one can set foot inside or on the grounds surrounding it.</p>
                <? include ("../../theme/text.html") ?>
                <p>Shortly after the king's death, his son, Kamehameha II, came into power and set about destroying the artifacts of old religion. It was during this time that the people's spirituality declined and the temple's sanctity came close to its end. It was only decades later, with the efforts of the <a href="../../oahu/museums/bishopmuseum.php">Bishop Museum</a> in Honolulu, that the place underwent some restoration. However, the temple has only been restored to one-third of its original size. </p>
