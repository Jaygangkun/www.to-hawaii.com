                <h2 class="small">Mookini Luakini Heiau, Big Island</h2>
                <p>Mookini Luakini Heiau in North Kohala is one of the oldest and most significant in all of Hawaii. A dramatic (and somewhat gruesome) history surrounds the heiau, which was built around AD 480. </p>
                <p>About 500 years later, a Tahitian high priest named Pa'ao arrived and brought order and structure to the community. Unfortunately, this order came with a new tradition: human sacrifice. The heiau still bears the large, flat stone where actual human sacrifices were made, and some visitors swear they can feel the spirits roaming the temple.</p>
                <p>However, the heiau wasn't purely a place of suffering. When there were no sacrifices, the priests allegedly used the temple to communicate with the gods and their long-dead ancestors. </p>
                <p>Despite the air of dark mystery surrounding the place, Mookini is one of the most significant historic sites in Hawaii. After Pa'ao arrived, he wanted to enlarge the heiau. According to an oral legend, the stones for the temple were placed in a single night. About 18,000 warriors carried them here by hand, all the way from Pololu Valley 14 miles (22.5 km) away.</p>
                <p>The heiau was designated as Hawaii's first registered National Historic Landmark in 1963. The stone structure is much smaller than it used to be in ancient times, but its remains still measure 250 feet (76 m) by 125 feet (38 m), with 6-foot (1.8 m) high walls.</p>
