                <h2 class="big">Mookini Luakini Heiau Overview</h2>
                <ul type="disc" class="mylist">
                  <li>One of the first heiaus to be built on the island</li>
                  <li>Located in an isolated and difficult to access area in North Kohala (road to it is rough and may not be passable &ndash; so a short hike may be necessary)</li>
                  <li>In the past, only alii nui (kings and chiefs) were allowed at the heiau, who used to pray, fast and offer human sacrifices</li>
                  <li>Legend says that the heiau was built as early as AD 480 &ndash; upon the arrival of the first Polynesian seafarers</li>
                  <li>Heiau was enlarged by Tahitian priest Pa'ao, who arrived in the 12th century and dedicated the heiau to war god Ku</li>
                  <li>Used to be a closed heiau, but the kapu of restriction was lifted in 1978 so that now all can enter &ndash; be respectful and stay on the designated paths</li>
                  <li>A stone wall was built around the heiau in 1981 to protect it</li>
                  <li>Located near the heiau (a few minutes walk down a dirt road) is the site of Kamehameha's birthplace</li>
