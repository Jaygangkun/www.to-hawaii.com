                <h2 class="small">Puako Petroglyphs, Big Island</h2>
                <p>Located on the Big Island's Kohala Coast near <a href="../beaches/holoholokaibeachpark.php">Holoholokai Beach Park</a>, the Puako Petroglyph Archaeological Preserve (<a href="../../big-island/ancientsites/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) features more than 3,000 ancient Hawaiian rock carvings. The carvings show figures of humans, paddlers, marchers, as well as turtles, dogs, chickens and deity symbols. They are believed to have been carved between 1000 and 1800 A.D. The ancient Hawaiians called their stone art <em>ki'i pohaku</em>, which literally means &quot;images in stone.&quot;</p>
                <p>Many of the carvings are <em>puka</em>, which are cup marks and dots. These played a role in postbirth rituals. The <em>piko</em>, or umbilical cord stump, of the newborn was placed in a hole in the rock and a stone was put on top of it. It was believed that this would be beneficial for the child since this created helpful <em>mana</em> (spiritual energy).</p>
