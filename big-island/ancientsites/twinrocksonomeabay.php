                <h2 class="small">The Legend of Twin Rocks, Big Island</h2>
                <p>These two rocks are located in the offshore waters at Onomea Bay, about 6 miles (10 km) north of Hilo. They can be seen from a nice viewing area that is located inside <a href="../gardens/hawaiitropicalbotanicalgarden.php">Hawaii Tropical Botanical Garden</a> (admission fee is $15). This may sound steep for a botanical garden, but this is one of Hawaii's best. It's well worth a visit. The viewing area is located at the mouth of the bay. Along the way, you'll see a large variety of tropical plants and a waterfall.</p> 
                <p>An info sign at Onomea Bay  reads:</p>
                <p><em>The Legend of Twin Rocks</em></p>
                <p><em>The village of Kahali'i was located on this large point of land which extends into Onomea Bay. Though the village is gone, the descendants of Kahali'i still remember some of the legends concerning the area's landmarks. One story tells of the origin of two rock formations at the head of Onomea Bay that are said to be a young man and woman, known as the lovers of Kahali'i.</em></p>
                <p><em>Legend has it that one day, a chief of the village spotted many canoes with sails heading shoreward in their direction. Fearing an attack, the chiefs and village elders held a council to determine a course of action. They decided to build a reef to prevent a landing on their beaches. Not having the means to complete the task quickly enough, they asked for two young lovers to be the guides and protectors of the village by giving their lives. Two willing individuals were found.</em></p>
                <p><em>That night a decree was sent to all who lived at Kahali'i to remain indoors from sunset to sunrise without making any light or sound, on penalty of death. In the light of the new day, everyone went down to the shoreline where they were amazed to find the lovers gone, and in their place two gigantic rock formations at the entrance of the Bay, attached to each other, as if on guard.</em></p>
                <p><em>The chief informed the people that no canoe could now pass the treacherous currents swirling around the rocks unless allowed to do so by the guardians. The lovers and their offspring still stand today, sentinels at the head of the Bay.</em></p>
