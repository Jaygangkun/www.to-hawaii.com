                <h2 class="small">Hapaiali'i and Ke'eku Heiau, Big Island</h2>
                <p>Located in Keauhou near <a href="../beaches/kahaluubeach.php">Kahalu'u Beach Park</a> and the Outrigger Keauhou Beach Resort, these two heiaus have been reconstructed by Kamehameha Schools in 2007. Hapaiali'i Heiau is believed to date back to the 1400s (carbon dating indicates that it was built between 1411 and 1465). </p>
                <p>                  During the restoration process, archaeologists discovered that Hapaiali'i Heiau served as a solar calendar. One can accurately mark the passing of the seasons when standing behind the center stone on the heiau's top platform and aligning it with various other points on the heiau. On the winter solstice the sun sets directly over the southwest corner of the platform-like structure. And at the summer solstice, it sets over the northwest corner of the structure. The platform measures 150 feet (46 m) by 100 feet (30 m), and during high tide, it is surrounded by water.</p> 
                <? include ("../../theme/text.html") ?>
                <p>                  Historians believe it took thousands of commoners about a decade to maneuver the rocks into place and build the platform. When Hapaiali'i was reconstructed in 2007, with the help of modern machinery, it took just four month to recreate the heiau.</p>
                <p>At the adjacent Ke'eku Heiau, it is believed that it is the place where invading Chief Kamalalawalu of Maui was sacrificed after being defeated by Chief Lonoikamakahiki in the 16th century.</p>
<p>A plaque in front of <span class="small">Hapaiali'i</span> Heiau reads:</p>
                <p><em>Literally translated means &quot;elevated chiefs.&quot; A heiau, or temple, is a pre-christian place of worship. Its age has been recorded as prehistoric. It is said that this heiau was built by a Ma'a, a kahuna (priest) of Maui, who left for Kaua'i later. Others say that it was built by Kamehameha about 1782 after the battle of Moku'ohai, and that Hewahewa was its priest. It was also said that the ali'i women would hanau or give birth at this heiau to instill the great mana or spiritual power within their child.</em><em></em></p>

