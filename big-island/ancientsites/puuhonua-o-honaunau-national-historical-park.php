                <h2 class="small">Pu'uhonua o Honaunau National Historical Park, Big Island</h2>
                <p>The Big Island has no shortage of historical wonders, but few can match the significance, or the local charm, of the Pu'uhonua o Honaunau National Historical Park. Located on the island's west coast, the park consists of two sections &ndash; the royal grounds and the pu'uhonua (place of refuge).</p> 
                <p>The ali'i (royals) of Kona and their warriors lived in the royal grounds. The <strong>Hale o Keawe Heiau </strong>(ancient temple), was built in 1650 and holds the remains of 23 ali'i. A few thatched huts and a fishpond are located in this area as well. Located nearby is a long stone slab that might look pretty normal at first glance. This is the<strong> Keoua Stone</strong>, named after the high chief Keoua of Kona, who used it as a resting place. Leading up to the heiau is the <strong>Great Wall</strong>, which separates the royal grounds from the pu'uhonua, an ancient place of refuge for Hawaiians who broke the law. </p>
                                <? include ("../../theme/text.html") ?>
                <p>In ancient Hawaii, the <em>kapu</em> (taboo) system was the law all people had to live by. A commoner was not allowed to look at the ali'i, touch them, or even walk in their footsteps. Women were not allowed to cook for men or eat together with them. There were many rules. If a commoner did something that was kapu, the penalty was death because it was believed that breaking a law made the gods angry, who could easily send a lava flow or tsunami to kill all. So to appease the gods, violators of the kapu system were killed. </p>
                <p>However, there was one way to escape death and save your life. If you were able to get to a pu'uhonua, then you would be given an absolution ceremony and be spared. A pu'uhonua was a sanctuary, not only for kapu breakers, but also for defeated warriors. However, it was difficult to get to a pu'uhonua because the royals and warriors lived in the surrounding area. </p>
                <p>So you either had to run faster than the angry warriors chasing you on land, or swim to it and beat rough ocean currents, waves, and hungry sharks on the way. But once inside the sanctuary, kahuna (priests) performed ceremonies to please the gods and kapu breakers were then able to be released into the community.</p>
                <p>The park is also home to a large population of honu, the revered green sea turtles. Most of them take refuge in the<strong> Keone'ele</strong>, which was once the royal canoe landing and forbidden to commoners. </p>
