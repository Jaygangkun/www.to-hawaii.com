                <h2 class="small">Pu'ukohola Heiau, Big Island</h2>
                <p>Pu'ukohola Heiau  (<a href="../../big-island/ancientsites/more_puukoholaheiau_photos.html" onclick="NewWindow(this.href,'name','700','900','yes');return false">see more photos</a>, <a href="../../big-island/ancientsites/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is where King Kamehameha the Great brought down his last opponent, Keoua, and united the Hawaiian Islands under his reign. By 1790, the king had conquered the islands of Maui, Molokai and Lanai, but control over his home island, the Big Island of Hawaii, was a challenge. He was told by a prophet that if he built a heiau (temple) atop Pu'ukohola and dedicated it to the war god Kuka'ilomoku, he would rule all the Hawaiian Islands. </p>
                <p>Oral stories that have been passed down from generation to generation tell that the king and his men carried rocks all the way from <a href="../../big-island/attractions/pololuvalleylookout.php">Pololu Valley</a> to this location by forming a human chain 20 miles (32 km) long. The heiau was completed in 1791, and the king held a ceremony, to which he invited his cousin and rival Keoua, the chief of Ka'u. When he came ashore on <a href="../../big-island/beaches/pelekanebeach.php">Pelekane Beach</a> fronting the heiau, Kamehameha killed him and took him to the heiau as a first offering to the gods. After Keoua's death, Kamehameha gained control of the Big Island and all other islands by 1810.</p>
                <? include ("../../theme/text.html") ?>
                <p>There used to be an altar, wooden images of gods, a drum house, a tower and additional thatched huts, but when Kamehameha died in 1819, the heiau was abandoned. Today, only the rock foundation remains, which measures 224 feet (68 m) by 100 feet (30 m), with 16-20 foot (5-6 m) walls.</p>
                <p>The temple is part of an official national historic site and is maintained by the National Park Service. The park features an interpretive trail leading from the visitor center to the ruins of the heiau, with descriptions and educational plaques along the way. </p>
                <p>Every August on the weekend closest to Establishment Day, the Pu'ukohola Heiau hosts a free, two-day Hawaiian Cultural Festival, which celebrates local customs and traditions, from food and clothing to various forms of art. Visitors can join a wide range of activities, such as Hawaiian handicrafts, native dances, music performances and educational games for children. </p>
                <p>A sign near Pu'ukohola Heiau reads:</p>
                <p><em>&quot;Build a heiau on Pu'ukohola, the &quot;hill of the whale,&quot; dedicate it to your war god, and you will achieve your dream - you will rule the islands. Responding to this prophecy told by Kapoukahi, a famous seer, Kamehameha built the heiau (temple) that stands before you. Work began in 1790. Workers carefully set tons of waterworn lava rock in place without mortar. Kamehameha labored with them. Only his brother was excused, because he would preside over dedication rituals upon Pu'ukohola Heiau and had to remain ceremonially clean. To please the war god, the heiau had to be ritually perfect. By the summer of 1791, the heiau was complete. Soon Kamehameha ruled Hawaii, then Maui, Lanai, Molokai, and Oahu. When he added Kauai through peaceful negotiations in 1810, the prophecy was fulfilled - Kamehameha the Great ruled all of the Hawaiian Islands. Pu'ukohala Heiau still holds great significance to many native Hawaiians. To respect their traditions, and to protect the structure, the heiau is closed to the general public. Only native Hawaiians exercising traditional religious practices may enter the temple.&quot;                </em></p>
