                <h2 class="small">Kalahuipua'a (Mauna Lani Fishponds), Big Island</h2>
                <p>These ancient Hawaiian fishponds (<a href="../../big-island/ancientsites/p4.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located on the grounds of the Mauna Lani Resort on the Big Island's Kohala Coast, are producing fish up until today. There are seven fishponds: Kalahuipua'a, Waipuhi, Waipuhi Iki, Kahinawao, Hope'ala, Manoku and Milokukahi, with Lahuipua'a and Ka'aiopio as divisions of Kalahuipua'a. </p>
                <p>Kalahuipua'a is the largest pond encompassing 4.6 acres and measuring up to 18 feet (5.5 m) in depth. In the old Hawaii, most fishponds were managed by the ali'i (chiefs), and most of the fish were consumed by them.<br />
                  <br />
                  An educational plaque at the Mauna Lani fishponds reads:</p>
                <p><em>&quot;Since men first found them, the fishponds at Kalahuipa'a [name of the ahupua'a on which Mauna Lani sits] have been a delightful oasis along this arid coast. These brackish ponds are fed and cleansed by fresh water springs seeping into them and the tidal action of the sea. By modifying them and managing them wisely, the prehistoric Hawaiians were able to raise a variety of fish in these ponds. 'Ama'ama (mullet) and awa (milkfish) were the most commonly raised fish, but others such as papio (jack) kaku (barracuda) and puhi (eels) as well as 'opae (shrimp) lived here also. These ponds are among the few anywhere that are still being managed in much the same way as they were in ancient times. They are still producing fish.&quot;</em></p>
