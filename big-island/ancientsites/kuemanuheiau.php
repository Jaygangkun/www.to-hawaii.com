                <h2 class="small">Ku'emanu Heiau, Big Island</h2>
                <p>Located on the Big Island's western shore, about 5 miles (8 km) south of Kailua-Kona, Ku'emanu Heiau is believed to have been devoted to surfing. It was used to pray for good surfing conditions and to observe surfers offshore. It stands opposite of an excellent surfing break, which is popular up until today.</p>
                <p>Its stone platform is about 100 feet (30 m) long and 50 feet (15 m) wide. On top of the foundation sits an upper stone terrace. There is a stone water pool on one side of it which could have been used for bathing or rinsing off saltwater after coming out of the ocean.</p> 
                <p>A plaque in front of the heiau reads:</p>
                <p><em>In the past, Hawaiian religious practices included the worship of many gods, both through individual and family rituals at small shrines and through larger community ceremonies at heiau (temples) such as this one. In 1819, King Kamehameha II renounced the traditional Hawaiian religion and the wooden god images, thatched houses and other perishable structures that once stood on the stone heiau foundations were destroyed or allowed to fall into ruin and decay. What we call heiau today are the foundations of the temples themselves.</em></p>
                <p><em>Prehistorically, the Kahalu'u area supported a large population, including high ranking chiefs. Many of the once numerous heiau and other archaeological sites left behind by the ancient Hawaiians are being lost to the rapid growth and development that Hawaii has seen during this century. Kuemanu Heiau, said to have been used to pray for good surfing conditions, has been preserved by the County of Hawaii. Kuemany was recently repaired and portions of the walls you see have been reconstructed.</em></p>
                <p><em>For your safety, please don't walk along the edge of the walls.</em></p>
                <p><em>Department of Parks and Recreation, County of Hawaii, 1982</em></p>
