                <h2 class="small">Hikiau Heiau, Big Island</h2>
                <p>Located at <a href="../beaches/napoopoobeachpark.php">Napo'opo'o Beach</a> (Kealakekua Bay), the Hikiau Heiau is an ancient Hawaiian temple site that was built by King Kalani'opu'u. It is a luakini (human sacrifice) heiau. </p>
                <p>The original heiau used to be more than 250 feet (76 m) long and 100 feet (30 m) wide. A smaller stone platform is built on top of the main platform and is believed to have been the location of the lele (altar).</p> 
                <p>Hikiau Heiau was visited by Captain James Cook in 1778-1779. When the natives first saw his huge ship sail into <a href="../beaches/kealakekua-bay.php">Kealakekua Bay</a>, they thought he was the returning god Lono, the god of agriculture and prosperity. The time when Cook arrived was during the months of the makahiki, which was a festival honoring the god Lono with hula performances, competitive games, feasting and special offerings. </p>
                                <? include ("../../theme/text.html") ?>
                <p>Cook was treated as a divine guest by the Hawaiians. He attended a special ceremony at another nearby heiau that was held in his honor. And at Hikiau Heiau, he performed the first Christian ceremony in Hawaii, a funeral service for one of his crew members. </p>
                <p>Shortly after Cook left Hawaii, he was forced to return due to a broken ship mast. By then the makahiki festivities had ended and the natives&rsquo; attitude toward Cook and his crew had changed. If they were truly divine, why would they have to return to land just because of a broken mast? This is what a mere mortal would do, not a god. Not to mention that one of the crew members had died a few days earlier.</p>
                <p>Cook was killed in a conflict at Kealakekua Bay shortly after returning to land. Cook had tried to take the chief hostage in return for a small rowboat that a few natives had stolen from his ship.</p>

