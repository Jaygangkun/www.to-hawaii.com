                <h2 class="small">Coconut Island (Mokuola), Big Island</h2>
                <p>This little island is located in Hilo Bay and can be accessed from <a href="../gardens/liliuokalanigardens.php">Liliuokalani Gardens</a> via a pedestrian footbridge. Its Hawaiian name is Mokuola (meaning &ldquo;island of life&rdquo; or &ldquo;healing&rdquo;), but today it is more commonly known as Coconut Island. </p>
                <p>In the old Hawaii the island was known for its curative spring waters. Also, it was believed that if a sick person swam around one of the rocks at the eastern inlet of the island he or she would get healed from the illness. </p>
                <p>The umbilical cords of newborns were also brought here and placed under a stone called Papa a Hina. This assured that the child would live a strong and healthy life. There is a small sea pool called Pua'akaheka on the island that was known as a special healing location.</p>
