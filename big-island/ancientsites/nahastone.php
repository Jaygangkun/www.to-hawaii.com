                <h2 class="small">Naha Stone, Hilo</h2>
                <p>Located in front of the Hilo Public Library are two stones, one of them larger than the other. This larger one is called the Nana Stone. In ancient Hawaii, it was used to find out who belongs to the ruling Naha clan. After a baby was born, it was placed on top of this stone. If it remained calm, the baby was Naha. If it cried, it wasn&rsquo;t. </p>
                <p>It is believed that this 7,000 pound rock was brought here from the island of Kauai via canoe. The stone fulfilled another important role in the past. Legend had it that whoever was able to move it would be the first king of all the Hawaiian islands. </p>
                <p>At the age of 14, Kamehameha decided to try to move this stone (and risked death if he couldn&rsquo;t). He tried several times and succeeded in the end. And indeed, he was the Hawaiian king who unified all the islands under his rule. </p>
