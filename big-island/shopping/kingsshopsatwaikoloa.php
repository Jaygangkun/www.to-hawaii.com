                <h2 class="small">Kings' Shops at Waikoloa, Big Island</h2>
                <p>Kings' Shops (<a href="../../big-island/shopping/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) at Waikoloa Beach Resort is located 20 minutes north of the Kona International Airport and has the biggest selection on the coast. There is a wide variety of shops, restaurants and services available. Not only will you find everything you need in one place, you'll also find hula performances, live Hawaiian music and tours of petroglyph fields and native Hawaiian plants, arts and crafts displays, and historic murals of ancient Hawaii. </p>
                <p>For fine arts gifts, jewelry and accessories check out Under the Koa Tree, Indochine, Ukulele House, Island Shells and Pacific Rim Collections, and Walking in Paradise for resort shoe wear. For high-end apparel outlets, you'll find DFS Galleria, Louis Vuitton, Blue Ginger and Macy's. For island apparel, check out Making Waves, (women's swimwear for all ages), Crazy Shirts, Kane Malia, Honolua Surf Co. and Noa Noa, specializing in batiks. At the other end of the spectrum, there are some convenience stores.</p>
                <p>When you are hungry from all that shopping you have many options, such as Big Island Steak House, Island Fish &amp; Chips, Merriman's Market Caf&eacute;, Hama Yu Japanese and Roy's Waikoloa Bar &amp; Grill. For your convenience, there is also a Starbucks and Dairy Queen/Orange Julius. </p>
                <p>Another interesting feature of the King's Shops is the observation tower with descriptions of Mauna Kea, Mauna Loa and the Kohala Mountains with an adjacent telescope.</p>
                <p>A shuttle service is available. The Waikoloa Shopping Trolley operates 7 days a week and provides transportation between the Waikoloa Beach Marriott Resort &amp; Spa, Hilton Waikoloa Village and Kings&rsquo; Shops and Queens&rsquo; MarketPlace. The cost is $1 per person, each way. Hours: 10 am to 10 pm. </p>
                <p><strong>Hours</strong>: Monday to Sunday from 9:30 am to 9:30 pm<br />
                  Select store, restaurant and holiday hours may vary.</p>
