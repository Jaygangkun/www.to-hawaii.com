            <h1>Fairway Villas Condo Vacation Rentals</h1>
                   <p>On the shores of the  King's lake and the Waikoloa Beach Golf Course sits Waikoloa Fairway Villas.  This quiet, convenient location places guests along the Kohala Coast, close to  the beach, KOA International Airport and historic sites and towns. Kings' Shopping Center is also close by.</p> 
                <p> The accommodations  themselves consist of villas with one or two guestrooms all of which are open  to private balconies. All include are spacious enough and include standard  amenities for a 3.5 star resort. These include cable televisions, air  conditioning, kitchens and coffee/tea makers. Bathrooms include separate  bathtubs and showers. Daily housekeeping is provided.</p>
                <p> Away from the bedrooms  the condo accommodation includes various recreational options. A swimming pool  (no toys allowed) with barbeque area is provided for relaxation and outside  dining. Spa tubs and fitness center are also provided for those wishing to sit  amongst the bubbles or keep trim respectively! The nearby golf course provides  discount rates and preferential tee times to guests at the Villas.</p>
                <p> Although there is no  restaurant linked to the Waikoloa Fairway Villas, there are many options in the  surrounding area. These include two in the Waikoloa Beach Resort, and many  close to Kings' shopping.</p>
