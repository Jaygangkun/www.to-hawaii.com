?> <p>It was this second group of settlers who established the kapu system. Kapu means &ldquo;taboo&rdquo; or &ldquo;forbidden&rdquo; in the Hawaiian language. It was a law system that prohibited many things and was designed to keep order. So it was forbidden for commoners to merely walk in the shadow of an ali'i (chief). Also, women were not allowed to eat together with men, or to eat bananas or pork. One wasn't allowed to interrupt a chief if he was speaking, and the list goes on. <br />
                  <br />
                  The punishment for breaking a kapu was usually death. If the offense was very serious, the entire family of the offender was killed as well. During this time, human sacrifices were common.</p>
                <p>The first Europeans arrived much later in Hawaii, beginning with English explorer Captain James Cook in 1778, who named the islands he discovered the Sandwich Islands after the English Earl of Sandwich.</p>
                <p>The natives greeted Cook with bewilderment and joy, believing that he was Lono, the god of fertility of the land. Cook was eventually killed after a dispute in Kealakekua Bay on the Big Island of Hawaii. At the time of Cook's visit, the archipelago was divided into three kingdoms: Hawaii, Oahu and Maui, and Lanai and Molokai. </p>
                <p><strong>Unifying the Islands</strong></p>
                <p>King Kamehameha the Great (1758-1819) was Hawaii's most powerful king. Born and raised on the Big Island of Hawaii, Kamehameha participated in numerous battles in his early years. After he became ruler of the Big Island, his next goal was to rule all the other islands as well. In 1795 his warriors arrived on Maui, Lanai, Molokai and Oahu and later Kauai, defeating the local rulers of these islands. In that year Kamehameha conquered and unified the Hawaiian Islands. Shortly after his death, the kapu system was abolished.<br />
                </p>
