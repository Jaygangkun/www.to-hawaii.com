                <p><strong>The Beginning</strong></p>
                <p>The Hawaiian Islands are of volcanic origin. They were created by a so-called hot spot on the ocean floor about 70 million years ago. The oldest Hawaiian island is Kure Atoll and the youngest one is the Big Island of Hawaii, which rose to the surface about one million years ago.</p>
                <p><strong>The First Settlers</strong></p>
                <p>It is believed that the first settlers, who were originally from the Marquesas Islands, arrived in Hawaii some time around the 4th or 5th century AD. They crossed 2,500 miles of ocean in double-hulled voyaging canoes and used the stars to guide them. With them they brought some crops, such as taro and breadfruit, as well as animals. Archaeologists believe that a second wave of Polynesian voyagers arrived around 1000 AD, who were from Tahiti. </p> <?php
