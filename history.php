                <p>A constitution for the Republic of Hawaii was framed in 1894, with Sanford B. Dole as its first president. A plot was formed to overthrow the republic and to restore the monarchy. But that plan was broken up when a police squad alarmed the town in advance. </p>
                <p>Queen Lili'uokalani was arrested and imprisoned for nine months in the former palace after weapons and ammunition and incriminating documents were found on her premises. She renounced all claim to the throne in January 1895 and took an oath of allegiance to the new republic.</p>
                <p><strong>The &ldquo;Modern Era&rdquo; </strong></p>
                <p>In March 1897, President McKinley was inaugurated and negotiations with the U.S. resumed. Shortly after, Hawaii was annexed by a joint resolution of Congress. The U.S. flag was raised over the executive building in August 1898, and in 1900, Hawaii became an official territory of the United States.</p>
                <p>Sugar and pineapple were the main industries in the early 20th century. On December 7, 1941, the Japanese attacked Pearl Harbor on Oahu, which led the U.S. to enter World War II. In World War II, Hawaii served as an important base for operations in the Pacific. </p>
                <p>In 1958, Hawaii's citizens voted in favor of becoming a U.S. state. In March 1959, President Dwight Eisenhower signed the bill which led to statehood. Hawaii became the 50th state on August 21, 1959. Tourism is one of Hawaii's main industries today.</p>
