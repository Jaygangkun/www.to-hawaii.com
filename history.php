                <p><strong>The Missionaries and the Monarchy</strong></p>
                <p>In 1820, the missionaries arrived in Hawaii. King Kamehameha III ruled the Islands from 1825 until his death in 1854. He listened to advice from the missionaries and allowed them to establish schools and preach Christianity. The missionaries developed the Hawaiian alphabet and taught it to the people. They also used it to translate the Bible into the Hawaiian language. Kamehameha III instituted religious freedom in 1839, and a year later established a constitutional monarchy.</p>
                <p><strong>The Sugar Industry </strong></p>
                <p>The sugar industry was introduced in Hawaii in the 1830s. Also, businesspeople from all over the world arrived in the Islands to exploit Hawaii's sandalwood and whales. During the same time, plantation workers were brought to Hawaii from foreign countries. Hawaii's feudal land system was abolished in 1848, which made private ownership legal. <br />
                  <br />
                  From this point on, capital investment in the land was possible. The leaders of Hawaii themselves participated in these ventures and became more affluent. The Hawaiian government sold large chunks of land to foreigners, government officials and royalty. The ones who suffered were the Hawaiian commoners because they were stripped of their land where they had lived on for generations.</p>
                <p>Threatened by European nations wanting to add Hawaii to their empires, American businessmen began to seek annexation by the United States. In 1875, a treaty of reciprocity was negotiated and renewed in 1884, but not ratified. It was ratified in 1887 when an amendment was added that gave the U.S.A. the exclusive right to establish a naval base at Pearl Harbor. </p>
                <p><strong>Lili'uokalani's Rise to Power</strong></p>
                <p>Queen Lili'uokalani took the oath to maintain the constitution of 1887. The legislative session of 1892 was extended to eight months because of Lili'uokalani's determination to carry through the opium and lottery bills and to have a workable cabinet. </p>
                <p>Lili'uokalani had a new constitution drawn up, which resulted in an absolute monarchy and disfranchised a large group of citizens who had voted since 1887. At a public meeting a Committee of Safety was appointed, which issued a proclamation declaring the monarchy to be abolished and establishing a provisional government. </p>
                <p>In the meantime, volunteer troops arrived and occupied the grounds. By the advice of her ministers, the Lili'uokalani surrendered under protest, appealing to the United States to reinstate her authority. </p>
                                <p><strong>The Republic</strong></p>
                <p>A treaty of annexation was negotiated with the United States during the next month, just before the close of President Benjamin Harrison's administration, but it was withdrawn in March 1893 by President Harrison's successor, President Cleveland.</p>
