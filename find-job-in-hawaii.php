?> <h3>How to find a job in Hawaii for foreign citizens</h3>
              <p>Hawaii  is a U.S.  state and U.S. working regulations are very   strict. People who are not U.S.  citizens or green card holders need a   valid work visa to work in Hawaii, such as an H-1B visa or investor visa. </p>
                                           <p><strong>H-1B visa to work in Hawaii</strong></p>
              <p>According to the U.S. government: <a href="http://travel.state.gov/visa/temp/types/types_1271.html" target="_blank" rel="nofollow">H-1B</a> classification applies  to persons in a specialty occupation which   requires the theoretical and  practical application of a body of highly   specialized knowledge requiring  completion of a specific course of   higher education. This classification  requires a labor attestation   issued by the Secretary of Labor. This  classification also   applies to Government-to-Government research and  development, or   co-production projects administered by the Department of  Defense.</p>
              <p>In other words, to get an H-1B visa you need to have a   specialty  occupation which is in high demand in the U.S. Then you must   have an employer  who is willing to sponsor you and pay you the prevailing   wage (which is required  for all H-1B workers). An H-1B visa is valid  for 3   years, after which it can be renewed for another 3 years. </p>
              <p><strong>Important note:</strong> If you lose your job while you   are in H-1B status, you need to find a new  employer within 10 business   days who is willing to sponsor you and pay you  the prevailing wage.   If you are unable to find a new employer, you will have to  leave the   U.S.</p>
               <? include ("theme/text-desktop-only.html") ?>
              <p><strong>What other options  are available?</strong></p>
              <p>If you can't get an H-1B visa, there is another way how you    can work in Hawaii.  Foreign students with an   F-1 visa are allowed to work  up to 20 hours a week in Hawaii  after 9   months of their arrival in the U.S. This can be arranged through  the   school. If you need a job immediately you don't need to wait for 9   months. You can get a job on campus (up to 20 hours a week),   but keep in  mind that usually campus jobs pay very little - about $7-8   per hour, which is  not enough to cover basic expenses. </p>
