              <h2>Swim with dolphins in Hawaii</h2>
              <p>The two most common dolphin species  in Hawaii's nearshore waters are spinner and bottlenose dolphins. Many people dream of watching dolphins or even swim with them in the open ocean. There are several companies in Hawaii that offer dolphin watching cruises that sometimes include snorkeling.</p>
              <h3>Swim with dolphins in the ocean</h3>
              <p>Before going on a dolphin watching cruise or swim out to deeper offshore waters in pursuit of dolphins, it is good to know the rules. Dolphins in Hawaii are protected by the Marine Mammal Protection Act, which prohibits any taking of marine mammals meaning to &quot;harass, hunt, capture, or kill&quot; and &quot;any act of pursuit, torment, or annoyance which ... has the potential to disturb a marine mammal or marine mammal stock in the wild by causing disruption of behavioral patterns.&quot; However, this does not mean the dolphins cannot approach you. Some dolphins are playful and they may choose to come close to boats or swimmers. But if they prefer to keep a distance, boats and swimmers should not chase them. </p>
              <? include ("theme/text.html") ?>
              <p>The National Oceanic and Atmospheric Administration (NOAA) has put out guidelines for responsible marine mammal watching &ndash; a marine mammal and sea turtle viewing code of conduct, which advises people to stay at least 50 yards away from wild dolphins and turtles (see guidelines here: http://www.nmfs.noaa.gov/pr/education/hawaii).</p>
              <p>If you're planning to go on a dolphin watching cruise in Hawaii, you might want to bring binoculars to get a better view of the dolphins, known as nai'a in the Hawaiian language. Besides dolphins, you might also spot green sea turtles (honu), flying fish (malolo) and whales (kohola) in the winter season (November through March). Sometimes these cruises that include snorkeling take their guests to what they call &quot;turtle towns&quot; or &quot;turtle cleaning stations.&quot; These are areas where turtles like to hang out and enjoy having their shells cleaned by surgeonfish and other algae eaters (who feed on algae that grows on turtle shells).</p> <?php
