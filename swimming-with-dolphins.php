              <p><strong>Bottlenose dolphins</strong></p>
              <p>Bottlenose dolphins are larger than spinner and spotted dolphins. Adult bottlenose dolphins are 6 to 13 feet (2 to 4 m) long and weigh between 440 to 600 pounds (200 to 300 kg). They usually swim in smaller pods of up to 10 individuals. The bottlenose dolphin's name comes from its elongated upper and lower jaws.</p>
