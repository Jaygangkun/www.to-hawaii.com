?> <p><strong>Swim with dolphins in a park</strong></p>
              <p>In Hawaii you also have the option to come close to dolphins in a marine park and participate in interactive programs. This is possible at <a href="oahu/attractions/sealifepark.php">Sea Life Park Hawaii</a> on Oahu or at the marine research and educational center called DolphinQuest on Oahu or the Big Island. Both offer daily education programs, where you can learn about dolphins, get close to them in the water or simply watch them.</p>
              <p><strong>Marine life educational exhibits and programs</strong></p>
              <p>If you want to learn more about dolphins and other marine life in Hawaii, you may also want to visit the <a href="oahu/attractions/waikikiaquarium.php">Waikiki Aquarium</a> on Oahu or the <a href="maui/attractions/maui-ocean-center.php">Maui Ocean Center</a> on Maui. Both have many interesting marine life exhibits and aquariums.</p>
              <p><strong>Spinner dolphins</strong></p>
              <p>Spinner dolphins are most commonly seen along Hawaii's shorelines. At night they swim to their feedings grounds in the deeper waters offshore and return to the shallow nearshore waters in the morning to rest. This rest behavior is characterized by swimming close together in groups, surfacing synchronously to breathe and gliding along the ocean bottom in a &ldquo;carpet&rdquo; formation. They swim in pods of between 10 to 100 dolphins. Spinner dolphins are known for their aerial antics and acrobatics (leaping out of the water and spinning around its longitudinal axis, head and tail slaps, etc.). A grown spinner dolphin is about 6 feet (2 m) long and weights 175 pounds (80 kg).</p>
