                <h2 class="small">Opaekaa Falls, Kauai</h2>
                <p>This is one of Kauai&rsquo;s best-known and main waterfalls. Most people view it from a lookout off of Kuamoo Road, as it is a difficult and tricky hike around its top and pool. If you want to see Opaekaa Falls up close, find the trailhead at the top of the falls, close to the 2-mile marker that is past the fall&rsquo;s lookout on Highway 580. </p>
                <p>Going all the way to the waterfall can be well worth the trip, but the trail is muddy. It takes about a half hour, but the view of Opaekaa Falls and the lush, tropical rainforest surrounding it makes for a great photo opportunity. </p>
                <p>Opaekaa Falls is a solitary location on most days because most people do their observations from the highway lookout. The name &ldquo;Opaekaa&rdquo; means &ldquo;rolling shrimp&rdquo; in the Hawaiian language. The waterfall was named after the shrimp that were once in abundance in its stream and pool.</p>
