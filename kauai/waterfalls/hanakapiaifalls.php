                <h2 class="small">Hanakapi'ai Falls, Kauai</h2>
                <p>Hanakapi'ai Falls is a waterfall on Kauai's Na Pali Coast. To see it requires some effort because it's an 8-mile (13 km) hike roundtrip. To get to it, you'll first have to hike to Hanakapi'ai Beach on the popular Kalalau Trail. It is 2 miles (3.2 km) to get to the beach. From here, follow the trail inland for another 2 miles (3.2 km) and you'll reach the waterfall. </p>
                                <p>The trail is quite strenuous. It goes up and down, can be slippery (especially if it has recently rained) and involves several stream crossings. That's why it is dangerous to hike this trail during rain because of flash floods. It takes about 1 to 2 hours to get to the waterfall from the beach.</p>
                <p>The 300-foot (91 m) cascade is a beautiful sight with an inviting pool at its bottom where it plunges into. Many people enjoy a refreshing dip in the pool. If you do this, step into it slowly (no head-diving into it) and be aware of rockfall (which is always possible near waterfalls and around cliffs). </p>
                <p>So if you plan to hike from Ke'e Beach all the way to Hanakaip'ai Falls, it can easily take an entire day, especially if you plan to rest on the way.</p>
