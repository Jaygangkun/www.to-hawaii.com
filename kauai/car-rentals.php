              <h2>Kauai Transportation Tips</h2>
<p>The best way to explore Kauai is with a rental car. That way, you can sightsee at your own pace. There are a number of car rental companies on Kauai, such as Alamo, Avis, Budget, Dollar, Hertz, National and Thrifty. Their rental booths are located just across the street from Lihue Airport. It is a good idea to make your rental reservation in advance as cars may be limited, particularly during peak travel seasons and holidays.</p>
<p>Driving on Kauai is fairly easy as there is only one major highway that goes around almost the entire island (with the exception of the northwestern shore, which is inaccessible by car). So it's difficult to get lost. There are a few narrow one-land bridges, particularly on Kauai's north shore, where drivers are expected to follow the &quot;bridge etiquette,&quot; meaning to yield to oncoming vehicles if you are the eighth car coming from your direction wanting to drive across the bridge.</p>
<p>Kauai also has a public bus system, but unlike on Oahu, buses here don't drive as frequently and don't cover the entire island. The Kauai Bus has routes going between Hanalei and Kekaha. It also provides service to Lihue Airport and limited service to Koloa and Poipu. The fare is $2 per trip. Visit www.kauai.gov/default.aspx?tabid=58 for more information about Kauai's public bus system.</p>
<p>Another way to explore the island is by motorcycle. You can rent one at Pacific Island Rentals in Kapaa. Taxis are also available, for example through Akiki's Taxi, Kauai's Taxi Company, Poipu Taxi and Scotty Taxi. Bicycle rentals are available at Kauai Cycle and Tour or at Outfitters Kauai.<br>
</p>
