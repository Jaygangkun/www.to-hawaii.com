                <p>Take the Plantation Lifestyles Tour through an authentic  sugar plantation and the ruins of an old sugar mill. Stroll down the original  dirt roads and see the workers' actual homes and personal gardens. This small  museum in <a href="../cities/waimea.php">Waimea</a> on Kauai's south shore immerses you in Hawaii's sugar  plantation history and the communities that lived and worked here. Most of the workers came from foreign countries, and the  plantation played an important role in their socialization. It was both work  and a community.</p>
                
                <p>The museum also features the historic Engine 7162, built in  1928. It was purchased by the sugar mill sometime after 1945 and used to power  the pumps in the irrigation ditches. The massive engine has suffered little  corrosion and remains in remarkably good condition.</p>
