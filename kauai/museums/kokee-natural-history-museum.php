                <h2 class="small">Koke'e Natural History Museum, Kauai</h2>
                <p>The Koke'e Natural History Museum is a good place to visit if you're interested in learning more about Kauai's geology, ecology and climatology. The museum also provides information about trail conditions in the <a href="../attractions/waimeacanyon.php">Waimea Canyon</a> and <a href="../attractions/kokeestatepark.php">Koke'e</a> state parks. </p>
                                <p>Events at the museum are offered on many days of the month, which include guided hikes and forest workdays. However, the daily schedule of events is heavier from June through September.</p>
                <p>Popular exhibits include Hawaiian &amp; Pacific Weather during Hurricane Iniki of September 1992, Woods of the Hawaiian Forest, Native Hawaiian Birds &amp; Introduced Birds of Kauai and Shells of Kauai. </p>
                <p>There's a gift shop featuring an extensive collection of Hawaiian books, gift items and local art and craft. For more information, contact the museum at <span itemprop="telephone">(808) 335-9975</span>.<br />
                  <br />
                  <strong>Hours:</strong><br />
                  Open
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 09:00-16:30">daily from 9 am to 4:30 pm</time>
                  .</p>
                <p><strong>Admission:</strong><br />
                  Free, but donations are welcome. Suggested donation is at least $1 per person. The museum receives no government funding for its operations.</p>
