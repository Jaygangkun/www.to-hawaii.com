                <h2 class="small">Wai'oli Mission House, Kauai</h2>
                <p>Located in the heart  of <a href="../cities/hanalei.php">Hanalei</a>, this mission house dates back to 1837 and offers a  chance to see how the early Christian missionaries lived. The missionaries  first arrived in Hanalei on the island of Kauai in 1832 to  convert the natives to Christianity. </p>
                <p> This used to be the home of early  Christian missionaries Abner and Lucy Wilcox who moved here in 1847. The home  was originally constructed by Reverend William Alexander prior to the Wilcox' arrival.</p>
                                <? include ("../../theme/text.html") ?>
                <p>The architecture of this home is what makes it different  from others in Hawaii. Most mission homes from the same time were built in the  traditional New England style architecture, appearing at odds with their  tropical landscape. </p>
                <p>Since Reverend Alexander originally came from Kentucky, this  home has influences from his southern roots, as well as Hawaii. There's a lava  rock chimney, lanais on both stories and ohia wood floors. The home is  furnished with traditional Hawaiian koa furniture. There's even a clock inside  that was installed in 1866 and still keeps perfect time. </p>
                <p>Next to the missionary home is the Wai'oli Hui'ia Church,  which was founded it 1834. Its stained glass windows and green shingles make it  a popular landmark in Hanalei. </p>
