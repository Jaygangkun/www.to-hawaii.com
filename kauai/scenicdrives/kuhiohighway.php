                <h2 class="small">Kuhio Highway (Route 560), Kauai</h2>
                <p>The stretch of Kuhio Highway from <a href="../cities/hanalei.php">Hanalei</a> to its dead end at <a href="../beaches/kee-beach-park.php">Ke'e Beach</a> along Kauai's north shore is a scenic drive. It is the main and only road leading to the sleepy little community of <a href="../cities/haena.php">Ha'ena</a>. </p>
                <p>In a way this stretch of Kuhio Highway is similar to Maui's Hana Highway in that it also has many one-lane bridges. The first one-lane bridge is the Hanalei Bridge right before arriving in Hanalei. During heavy rains the area around the bridge (which leads across the Hanalei Stream) often floods and the bridge is closed. Since it has a 15-ton weight limit you won't see any large tour buses past this point. </p>
                                <? include ("../../theme/text.html") ?>
                <p>After Hanalei there are many scenic lookout points and beaches along the way, but finding a place to park can often be challenging. The &ldquo;highway&rdquo; is  more like a country road and parts of it are narrow and curvy. During peak tourist season this is a really busy road. You may think traffic on Kauai isn't as bad as on Oahu, but it can get really busy here too because Kauai's north shore is such a popular and scenic destination. </p>
                <p>The first nice lookout point past Hanalei is at <a href="../beaches/lumahai-beach.php">Lumaha'i Beach</a>, but there's only a small turnout. The next beach coming up is <a href="../beaches/wainihabeach.php">Wainiha Beach Park</a>. There is a double bridge here that leads across the Wainiha River. Of all the one-lane bridges along this highway, this is the trickiest because you really have to look into the distance and around a curve to see if there is oncoming traffic before you drive across (if you're the first car to drive across). There are no traffic lights at any of the one-lane bridges on Kauai's north shore, but instead there's a one-lane bridge etiquette. The rule is that 7-8 cars from one direction can drive across a one-lane bridge and then it's the other side's turn. </p>
                <p>One of the most popular beaches along the way is <a href="../beaches/tunnelsbeach.php">Tunnels Beach</a>, which has a very small public parking area. So local residents have created a parking area across the beach on a meadow adjacent to the highway. However, it's quite pricey to park here ($10 per car).</p>
                <p>Right before reaching <a href="../beaches/haena-beach-park.php">Ha'ena Beach Park</a>, there is a dip in the road and you'll have to drive through Manoa Stream (a small stream during dry weather). Drive carefully here and watch for potholes. On your right is the beach park (which has a relatively large parking area) and on your left is <a href="../ancientsites/maniniholodrycave.php">Maniniholo Dry Cave</a>. From here it's just another mile until the highway dead ends at Ke'e Beach, another very beautiful Kauai north shore beach. If you want to venture further, you can only do it on foot on the popular <a href="../attractions/kalalautrail.php">Kalalau Trail</a>, which leads for another 11 miles (18 km) along the <a href="../attractions/napalicoaststatepark.php">Na Pali Coast</a>. </p>

