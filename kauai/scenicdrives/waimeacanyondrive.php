                <h2 class="small">Waimea Canyon Drive, Kauai</h2>
                <p>On the to-do list of many visitors to Kauai is <a href="../attractions/waimeacanyon.php">Waimea Canyon</a> and <a href="../attractions/kokeestatepark.php">Koke'e State Park</a>. There are two roads you can take to get to the canyon &ndash; Waimea Canyon Drive (which begins in Waimea) and Koke'e Road (which begins in Kekaha). Waimea Canyon Drive merges into Koke'e Road around mile marker 6. </p>
                                <p>Waimea Canyon Drive is a little more scenic. It has quite a few curves (just like Koke'e Road), but it is relatively easy to drive. There are some great lookout points along the way. Some overlook the coastline, others the mountain landscape. One popular stop overlooks a small waterfall surrounded by Kauai's popular red dirt. After Waimea Canyon Drive and Koke'e Road merge (around mile marker 6), it is another 4 miles (6.4 km) to the Waimea Canyon lookout.<br />
                  <br />
                  Since the Waimea Canyon lookout is located at an elevation of about 4,000 feet (1.219 m), the temperature can be a bit cooler up there, especially in the winter months when it may be around 10&deg;F (5&deg;C) cooler. If you plan to stay overnight at the Koke'e Cabins which is further up, bring some warm clothes because it can get chilly at night. It is best to visit Waimea Canyon on a sunny day. If it is rainy, the scenery won't be as pretty and the different color shades of the canyon may not be so visible. </p>
