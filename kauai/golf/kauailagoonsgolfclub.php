                <h2 class="small">Kauai Lagoons Golf Club, Kauai</h2>
                <p>The Kauai Lagoons Golf Club is located in a secluded area in the southwest  corner of Hawaii&rsquo;s Kauai Island. The golf course is part of a lush, 750-acre  resort and its fairways wend their way along majestic ocean cliffs and over  promontories that rise high above the harbor. The golf course is set in some  forty acres of fresh water lagoons that weave their way through the resort.  Indeed, the beauty of this course is described as unbeatable.</p>
                                <? include ("../../theme/text.html") ?>
                <p>The Kiele course at Kauai Lagoons, which opened in 1988, was created by  Jack Nicklaus and it is said that the terrain here lent itself so well to the  renowned golfer&rsquo;s vision that he needed to make only a single change to his  original design. This excellent resort course has been the recipient of  numerous accolades as one of the Pacific&rsquo;s best golfing destinations. Nicklaus  was called on again in 2010 to lead the redesign of his famous Kiele ocean  nine. This effort resulted in a stunning course, which Golf Magazine ranked at  No 6 in their list of the 2012 Best Courses: State by State. When the newly  redesigned Kiele Moana (ocean) nine, previously the Kiele back nine, was  reopened it boasted new Tif Eagle putting surfaces, new bunkers, and three new  holes, one of which is a dramatic ocean hole that combines with three others to  create the longest continuous stretch of ocean holes of any Hawaiian golf  course.&nbsp; </p>
                <p>The 18-hole signature layout at Kauai Lagoons is created by the pairing of  the Kiele Moana nine and the Kiele Mauka nine. The par-3 fifth hole, measuring  219 yards from the back, features in the Kiele Mauka nine, which Nicklaus  described as one of the best holes &ldquo;anywhere.&quot; Playing this hole is a real  test of a player&rsquo;s nerve and skill, in part because it requires a tee shot over  a forest of guava and mango trees. The fifth is also the third in a four-hole  span that staff at the resort call the &quot;amen corner&quot; &ndash; because once a  player gets past it, they utter the word &quot;amen&quot; and continue.</p>
                <p>As might be expected from a first-rate golf resort, the amenities at Kauai  Lagoons are excellent. The practice facility is open to all players of all  abilities and all golfing requirements are adequately catered for in the well-stocked  pro shop. While there is no bar or restaurant at the clubhouse, there are  conference and banquet facilities available for anyone wishing to host a  function. </p>
