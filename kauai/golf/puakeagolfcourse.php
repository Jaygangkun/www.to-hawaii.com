            <div class="new-custom">
              <div class="midpanel_row">
                <p>Puakea Golf Course located in the western side of Lihue at the base  of the scenic Haupu Ridge, where much of the film Jurassic Park was made. It is  just about a ten minute drive from Kauai&rsquo;s main airport and can be found opposite Lihue&rsquo;s Kukui Grove Shopping  Center. The course was designed by Robin Nelson and is built on over 200 acres  of land that was once a sugar cane plantation, so it has historic links as well  as beauty. Puakea Golf Course has won many accolades. In 2008, it ranked 7th  in Kaua&lsquo;i Golf Courses Earn &lsquo;Best-Of&rsquo; Acclaim from Golfweek Magazine and, in  2010, it was ranked 15th Best in State &ndash;by Golfweek Magazine. When Puakea Golf  Course was initially being constructed work came to a halt when Hurricane Iniki.  The course opened as a 10-hole facility in 1997 and earned the distinction of  being one of the &ldquo;Top 10&rdquo; 9-hole courses in the US by Sports Illustrated in  1997. However, in 2002, Nelson was invited back to complete the final eight  holes.</p>
                                <p>The architect incorporated the new  holes and built in spectacular views wherever feasible. When Puakea Golf Course reopened in July 2003, the new  layout caused considerable excitement. Nelson had designed the holes for  optimized play in the prevailing trade winds. The result is that each  hole at Puakea is distinctly  different to play and only one &ndash; the par-3 fourth hole, plays directly into the  trade winds. Much of the course gets its character from the rich foliage in  which there are six ponds strategically located. Additionally, the course  slopes up and down, and weaves around deep ravines, making it a really fun  course to play. While around a quarter of the course offers panoramic ocean  views, all of this golf course is hugely scenic. </p>
                <p>Puakea Golf Course offers a full range of amenities. As well as club rental  services and pro shop, the reopened golf club has a new state-of-the-art  practice facility with a new driving range, putting green, and short-game  practice area. The Ho'okipa Caf&eacute; is open from 7.00am to sunset and serves  breakfast, lunch, and pupus all day long from a menu of fresh ingredients and  island favorites. Meetings, parties, and other events can be catered for &ndash; just  ask the staff at Puakea for details. </p>
