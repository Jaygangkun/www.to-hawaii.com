                <h2 class="small">Kiahuna Golf Club, Kauai</h2>
                <p>The Kiahuna Golf Club can be  found in sunny Poipu Beach amid lush Kauai surroundings, in an area of great  natural beauty and ancient  Polynesian&nbsp;archaeology. The golf course was designed by Robert Trent  Jones Jr who deftly incorporated many authentic remnants of an ancient Hawaiian  village into the design. As they negotiate this course, players will experience  its exceptional beauty and they will get a real sense of Kauai, of Hawaii, and  of the islands&rsquo; enduring history. The Kiahuna  Golf Club also acts as a sanctuary for many of the island&rsquo;s endangered species  including the Nene &ndash; the state bird of Hawaii. If they are lucky, players may  glimpse the rare Hawaiian Stilt or even a Moorhen when they are in the vicinity  of Waikomo Stream. It is also possible they will see a Black-Crowned Night Heron  fishing in any of the water features along the course.</p>
                                <? include ("../../theme/text.html") ?>
                <p>The  golf course at Kiahuna is an 18-hole, par-70 course that offers considerable  challenge along its entire length of 6,925 yards. The layout includes many doglegs including six on the inside  nine, and there are water hazards at seven  holes. These include three ponds and a stream that cuts across the 12th  fairway. The fairways are  relatively narrow and players will encounter fascinating obstacles such as  stone structures and lava walls. For example, there is a small outcrop of lava  tubes to the left of the second fairway and these contain a rare type of spider  - the Kauai Cave wolf spider, which has no eyes. Discovered in 1973, this harmless spider variety is  thought to only exist in six caves in Kauai&rsquo;s Koloa-Poipu region. The remnants  of a house that was inhabited by a Portuguese immigrant in the early 1800s can  be found to the left of the 15th fairway. The man&rsquo;s occupation was  controlling irrigation of the local agland, which was achieved by diverting the  Waikomo Stream, and he and his family are interred in a nearby crypt.</p>
                <p>The  second hole is the most picturesque but the most difficult. This is a par-4  measuring 440 yards where a pond and bunkers impose on the dogleg to the left.  The signature hole at Kiahuna is the 12th, a par-3 measuring 212  yards. Here, players have to contend with crossing trade winds, treetops, and  Waikomo Stream.</p>
                <p>The  comprehensive range of facilities at Kiahuna Golf Club includes a club rental  service and a driving range. The pro shop stocks a good selection of  high-quality merchandise where visitors can purchase anything from a pack of  golf balls to a full set of clubs. The club&rsquo;s restaurant &ndash; Joe&rsquo;s On the Green &ndash;  offers a tasty menu and a nice relaxed environment. Weddings, meetings, and  other events are also catered for at Kiahuna Golf Club. </p>
