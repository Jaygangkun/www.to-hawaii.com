                <h2 class="small">Poipu Bay Golf Course, Kauai</h2>
                <p>Located in the gentle environs of Poipu Bay on  Kauai&rsquo;s sunny south shore, and carved from a rolling plateau eight stories  above the Pacific Ocean, Poipu Bay Golf Course nestles comfortably against the  scenic background of a lush emerald-green mountain range. This is a place where  visitors will find ancient stone walls and what remains of old-world Hawaiian  heiau (places of worship). Indeed, there is one beautiful natural relic after  another here. The recipient of numerous accolades, Poipu Bay Resort Golf Course  has consistently been ranked as one of the top golfing destinations in Hawaii  and the USA. From 1994 to 2006 the course played host to the PGA Grand Slam of  Golf and is described as a &ldquo;course unlike any other. </p>
                                <? include ("../../theme/text.html") ?>
                <p>This is an 18-hole, par-72 course measuring 7,123  yards. Designed by Robert Trent Jones Jr, players get a stunning head-on view of Mount Haupu at the  twelfth hole, while the ocean comes into play on holes 15 through to 17. However, with such  spectacular ocean views, players will need to stay focused on their game and  not get distracted by the beautiful scenery. It is possible that native Nene  geese, green sea turtles, rare Hawaiian monk seals, or humpback whales will put  in an appearance to distract even the most dedicated of golfers. From a  technical perspective, players start off with the trade winds directly behind  them and slightly to their right to play the first hole. The second plays  uphill and into the wind, but the bunker to the right is misleading because it  plays longer than it would appear. The third plays longest of all the par-3s  with the wind crossing from left to right. And so the challenge goes on with  thrills and variety at each stage of play. The sixth hole is the longest at  Poipu Bay Golf Course, but is considered the easiest. The 16th hole,  a par-4 measuring 501 yards, is considered  the course&rsquo;s signature hole because of its mixture of beauty and challenge.  But, whatever the score, playing at Poipu Bay Golf Course is a unique, refreshing,  and memorable experience. </p>
                <p>Situated  next to Grand Hyatt Kauai Resort and Spa, Poipu Bay Golf Course has every amenity you  would expect from a first-class golf resort. Refreshing cold towels and  complimentary bottled water is provided. Additionally, the club offers an <strong>i</strong>n-cart satellite navigation system  that tells players the exact distance to the hole and pin placement. Golf  clinics and private lessons are available from professional tutors, and there  are practice facilities for those who want to fine-tune their game. The golf  shop offers a range of fashionable clothing and accessories with the club&rsquo;s  logo as well as a selection of casual wear, contemporary wear, souvenirs, and giftware.  There is also a restaurant where visitors and players can avail of  refreshments. </p>
