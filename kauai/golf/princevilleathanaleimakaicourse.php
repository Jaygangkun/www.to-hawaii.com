            <div class="new-custom">
              <div class="midpanel_row">
                <p>The Makai Golf Club at the St. Regis  Princeville Resort sits on a rolling plateau high above the Pacific Ocean and  Hanalei Bay. This course was the first one on Kauai to be designed by Robert  Trent Jones Jr and it is one of only two courses on the island to have a  27-hole layout. The course, which was originally opened in 1971 and redesigned  in 2009, snakes its way around native woodland, calm lakes, and a stunning  stretch of coastline that offers majestic views of Hanalei Bay and&nbsp; Bali Hai.</p>
                
                <p>The renovated 18-hole, par-72 Makai course is  made up of what was previously two nine-hole courses - the Ocean and Lakes  nines. The Woods Course has become a separate 9-hole course. There are four  different sets of tees on the Makai course and the length can be stretched to  over 7,200 yards from the tips. The new design uses Seashore Paspalum grass on  all greens and fairways and this provides a wonderfully smooth playing surface  for golf enthusiasts of all abilities. The renovations also included a  reconfiguration of the greens, a reshaping of existing bunkers, and the  addition of new bunkers. The back nine holes are the most diverse. The seventh  hole, which plays to 213 yards from the back tees, is considered the signature  hole offering spectacular views of the mountains and ocean. This requires a  strong tee shot over stunning cliffs onto a heavily-bunkered green.</p>
                <p> In addition to this stunning 18-hole  course, Makai Golf Club offers the 9-hole Woods Course to those who want a  relaxed and fun game with friends and family. The layout at the Woods Course  has more of a leisurely and informal feel as it winds its way through the  native woodlands of the island&rsquo;s north shore. </p>
                <p>As well as a newly-built clubhouse,  two new practice tees, a practice fairway bunker, a teaching tee, seven  bunkered target greens, and a short game practice complex have been added to  Makai Golf Club. Facilities include a club rental service and GPS-enabled golf  carts. The golf shop offers a good range of leading brand names in both men&rsquo;s  and women&rsquo;s leisure wear as well as golf equipment and other paraphernalia.  Breakfast and lunch are served daily at the Makai Grill, which is located on  the large lanai of the new clubhouse. Surrounding by stunning scenery and  breathtaking views, this golf club also caters for weddings and other  functions.</p>
