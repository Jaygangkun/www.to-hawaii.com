                <p>The  St Regis Princeville Resort is situated on a rolling plateau on Kauai&rsquo;s northern  shoreline, high above the Pacific Ocean and Hanalei Bay. As well as the Makai  Golf Club, the resort is home to the Prince Golf Course, a magnificent facility  that also enjoys stunning  ocean views from its 300 feet vantage point in 390 acres of tableland above the  Pacific. This excellent course was rated as &quot;Hawaii's No. 1 Golf  Course&quot; by Golf Digest, but it was its amazing design rather than its  scenic setting that earned it this accolade. Like Makai, Prince Golf Course was  designed by Robert Trent Jones Jr who used all the natural elements available  to him &ndash; tropical jungle, deep ravines, streams, and a waterfall &ndash; and turned  the rolling terrain into a challenging links-type golfing experience that has  no equal. </p>
                                <p> The Prince Golf Course recently re-opened after  extensive renovations where its bunkers and various other amenities were  upgraded. It is an 18-hole,  par-72 golfing challenge that climbs upwards over sloping hills and careers  downwards. The tough layout requires players to plan their shots carefully and  deliver them with precision. A unique aspect of this course is that each hole  has five different tees, which allows players to customize their game to match  their own skills and experience. The different tees at Prince Golf Course are a  good way for players to track and improve their game because this design allows  individuals to choose a more difficult tee-off option on return visits.</p>
                 <p> On this  course the back nine begin with the creative 'Burma Road' hole, which is  actually the tenth hole &ndash; a par-5. This turns at an angle of over 90&deg; to the  left and has a deep ravine all the way along the full length of its left side  protecting the green. The par-3 seventh hole, which overlooks Anini Beach some  165 feet below, is the signature hole at Prince Golf Course. All in all, there  are some pretty challenging obstacles on this most scenic of golf courses.</p>
                <p>Amenities  at the Prince Golf Club are excellent.&nbsp;  As well as a driving range and putting green, visitors can avail of club  rental services, GPS, golf lessons, and expert advice from professional staff.  The pro shop offers a range of golf-related merchandise &ndash; clothing, equipment,  and just about anything players might require. Food and beverages are available  on a daily basis from the on-site restaurant.</p>
