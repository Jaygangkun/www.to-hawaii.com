                <h2 class="small">Po'ipu, Kauai</h2>
                <p>Po'ipu is the most popular tourist area on Kauai, located 13 miles (21 km) south of Lihu'e. It is a resort community  featuring numerous fine accommodation options facing the ocean. Some of the  most popular Kauai beaches are located here: <a href="../../kauai/beaches/poipu-beach-park.php">Poipu Beach Park</a>, <a href="../../kauai/beaches/kiahunabeach.php">Kiahuna Beach</a> and <a href="../../kauai/beaches/shipwreckbeach.php">Shipwreck Beach</a>.</p>
                <p>There are two golf courses in Poipu - <a href="../golf/kiahunagolfclub.php">Kiahuna Golf Club</a> and <a href="../golf/poipubaygolfcourse.php">Poipu Bay Golf Course</a>, a shopping village, as well as a few good  restaurants. Other nearby popular attractions are <a href="../../kauai/attractions/spouting-horn.php">Spouting   Horn Park</a> and <a href="../gardens/nationaltropicalbotanicalgarden.php">Allerton Garden</a>. </p>
