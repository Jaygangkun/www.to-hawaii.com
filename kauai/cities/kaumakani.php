                <h2 class="small">Kaumakani, Kauai</h2>
                <p>In the Hawaiian language, Kaumakani means &quot;place in the wind.&quot;  For more than 40 years, however, the town was called Makaweli, which literally  means &quot;fearful features.&quot; It was only in 1956 that the town name was officially  changed to Kaumakani. <br />
                  <br />
                  Kaumakani has no white-sand beach, resorts or museums  seen in other Kauai towns. It is a rather quiet  and rural town surrounded by several sugar plantations. Tours around the  plantations are being offered. Kaumakani is located 20 miles (32 km) from Lihue, between  Hanapepe and Waimea on the southwestern shore of Kauai.</p>
