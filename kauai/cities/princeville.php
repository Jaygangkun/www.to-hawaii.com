                <h2 class="small">Princeville, Kauai</h2>
                <p>Princeville (<a href="../../kauai/cities/more_princeville_photos.html" onclick="NewWindow(this.href,'name','830','600','yes');return false">see more photos</a>) is a master-planned luxury resort community  overlooking <a href="../beaches/hanaleibay.php">Hanalei Bay</a> on Kauai's north shore. It is  set among a large golf course with breathtaking views to the blue ocean,  green mountains with cascading waterfalls, pine trees and palm trees. There are many  accommodation options in Princeville, from vacation condos and villas to large resorts, such as the 5-star St. Regis Princeville Resort.</p>
                                <? include ("../../theme/text.html") ?>
                                <p>At the entrance of the community is the <a href="../../kauai/shopping/princevilleshoppingcenter.php">Princeville Shopping    Center</a> with a grocery store, gas station, post office, gift shops  and a bank. There aren't many restaurants in Princeville. Princeville is also a home of several popular Kauai  beaches: <a href="../../kauai/beaches/puu-poa-beach.php">Pu'u Poa   Beach</a>, <a href="../../kauai/beaches/hideaways-beach.php">Hideaways   Beach</a>, <a href="../../kauai/beaches/sealodgebeach.php">SeaLodge Beach</a> and <a href="../../kauai/beaches/queensbath.php">Queen's Bath</a>.  &nbsp;</p>
                <p>Public parking in Princeville is very limited, so if you  don't stay in this resort community one of the few places where you can park is at the Princeville   Shopping Center. There is also a  small public parking area near Queen's Bath and  Hideaways Beach.</p>
