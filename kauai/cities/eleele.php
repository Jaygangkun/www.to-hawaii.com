                <h2 class="small">Ele'ele, Kauai</h2>
                <p>The small town of Ele'ele is located on Kauai's south shore. There used to be many sugarcane fields in this area in the past, but today, it is coffee that is produced here. In fact, Ele'ele is home to the largest coffee producer in the state of Hawaii &ndash; the <a href="../coffeefarms/kauaicoffee.php">Kauai Coffee Company</a>. There is a visitor center located just outside of town, along the so-called Coffee Highway (Highway 540/Halewili Road), where visitors can sample a variety of coffees and tour the coffee plantation (coffee samplings and the self-guided tour are free of charge).</p>
                                <? include ("../../theme/text.html") ?>
                                <p>Port Allen is a nearby separate community from where many snorkeling and sailing tours to the Na Pali Coast and the neighbor island of Ni'ihau depart daily. From Port Allen you can also get a good overview of Hanapepe Bay. Even though this is an industrial area and there isn't much to do and see, there are a few shopping and dining opportunities (at Port Allen Marina and Ele'ele Shopping Center). </p>
                <p>Next to Port Allen Harbor (to the east of it) is a small dark sand beach called <a href="../beaches/glassbeach.php">Glass Beach</a>. It is not a swimming beach or particularly pretty, but if you want to take a peak at shiny glass pieces mixed with dark sand, it is worth a visit.</p>
                <p>Another nearby attraction is the Red Dirt Shirt store, located right across from the Port Allen Marina. Here, the popular and uniquely Hawaiian Red Dirt Shirts are produced. How they came into existence is an interesting story. In 1992, Hurricane Iniki hit Kauai and caused massive damage. The small screen print shop that was located right there was damaged as well. The white shirts that were stored there, waiting to be printed, were covered in Kauai's ubiquitous iron-rich red dirt that blew in with the storm. Instead of throwing all the shirts away, the company decided to dry them and print them as they were. The shirts became a hit with local residents and visitors alike.</p>
