<h2 class="small">Ha'ena, Kauai</h2>
<p>Ha'ena is a small community on the north shore of Kauai, approximately 7 miles (11 km) from Hanalei and 37 miles (60 km)  from Lihue. Backed by green mountains and cascading waterfalls, Ha'ena is at the beginning of the 11-mile (18 km) hike along the remote and scenic <a href="../attractions/napalicoaststatepark.php">Na Pali Cost</a>. Near Ha'ena is <a href="../beaches/tunnelsbeach.php">Tunnels Beach</a>, one of the best snorkeling and diving beaches in the entire state of Hawaii. Other scenic locations nearby are <a href="../beaches/kee-beach-park.php">Ke'e Beach</a>, <a href="../beaches/haena-beach-park.php">Ha'ena Beach Park</a> and <a href="../beaches/lumahai-beach.php">Lumaha'i Beach</a>.</p>
<p>Even though Ha'ena is only 7 miles (11 km) from Hanalei, getting to this small town and its attractions takes time and requires driving on a narrow, winding road, crossing several one-lane bridges and keeping an eye open for warning signs, such as falling rocks from the nearby cliffs. The drive from Hanalei to Ha'ena can easily take half an hour, and since the area is very popular among visitors to the island, the road and parking lots can be very busy. </p>
