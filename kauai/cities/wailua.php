                <h2 class="small">Wailua, Kauai</h2>
                <p>Wailua, located on Kauai's east shore, is a major tourist destination on the island. The main attractions here are <a href="../../kauai/attractions/wailua-river.php">Wailua River</a> and <a href="../../kauai/attractions/fern-grotto.php">Fern Grotto</a> (accessible via boat or kayak). Also, there are two popular waterfalls in the Wailua   River State Park  &ndash; <a href="../../kauai/waterfalls/wailuafalls.php">Wailua Falls</a> and <a href="../../kauai/waterfalls/opaekaa-falls.php">Opaeka'a Falls</a>, as well as a few good beaches and ancient heiau sites.</p>
                <p>This side of the island is also called the Coconut   Coast, and you will see why as you  approach Wailua. You can see numerous coconut trees all over. And just like nearby Kapa'a, Wailua offers plenty of shopping and dining  options.</p>
