                <h2 class="small">Hanalei, Kauai</h2>
                <p>Hanalei, meaning &quot;crescent bay&quot; in the Hawaiian language, is  located about 32 miles (52 km) from  Lihue. This small north  shore town is home to one of the finest and most scenic Kauai beaches &ndash; <a href="../beaches/hanaleibay.php">Hanalei Bay</a> &ndash; popular for swimming, relaxation and other ocean activities. </p>
                                <p>Hanalei has two main shopping centers located in the middle  of town &ndash; <a href="../shopping/hanaleicenter.php">Hanalei Center</a> and <a href="../shopping/chingyoungvillageshoppingcenter.php">Ching Young Village</a> &ndash; where you can find several  shopping, dining and entertainment venues. </p>
                <p>Right before Hanalei (coming from Princeville), there is a one-lane bridge. From here to the end of the highway at <a href="../beaches/kee-beach-park.php">Ke'e Beach</a> it is an approximately 7-mile (11 km) scenic drive on a narrow and winding road. During heavy rains the road is closed and you can't go beyond this first one-lane bridge.</p>
                                <? include ("../../theme/text.html") ?>
                <p>There are two scenic lookout points on the road from Princeville  to Hanalei (before reaching the bridge). One is located on the left side of the  road and overlooks Hanalei Valley. The other one is on the right side and overlooks Hanalei Valley and Hanalei Bay.</p>
                <p>There are no hotels in Hanalei (except one inn and some  vacation rental homes). The majority of accommodation options near  Hanalei is in the resort town of Princeville. </p>
