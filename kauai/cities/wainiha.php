                <p>Wainiha is a small community located on the north shore of Kauai, between Hanalei and Ha'ena on Kuhio Highway 560.</p>
                <p>There is a hotel, the Hanalei Colony Resort, and a  restaurant, the Mediterranean Gourmet, as well as a spa, the Hanalei Day Spa<em>. </em>There  are also two more accommodation options here - Lauralani Retreat and Hale  Ho'o Maha B &amp; B. There is as mall general store with convenience items  as well.</p>
                <p>Wainiha is close to some of the most pristine Kauai  beaches &ndash; <a href="../../kauai/beaches/hanaleibay.php">Hanalei Bay</a>, <a href="../../kauai/beaches/tunnelsbeach.php">Tunnels Beach</a>, <a href="../../kauai/beaches/haena-beach-park.php">Ha'ena Beach Park</a> and <a href="../../kauai/beaches/kee-beach-park.php">Ke'e Beach</a>.</p>
