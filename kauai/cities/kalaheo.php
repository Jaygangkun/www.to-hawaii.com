                <h2 class="small">Kalaheo, Kauai</h2>
                <p>One of Kalaheo's main attractions is the Kukuiolono Park  &amp; Golf Course, which is a good place to learn to play golf at an affordable daily rate. There is a small Japanese garden located on the golf  course, which was a gift to the people of Kauai donated  by Walter McBryde, who is also buried here.</p>
                <p>Located in southwestern Kauai, Kalaheo is a growing community with many new housing subdivisions. The town has   gas stations, a liquor store, post office, medical center, church,  restaurants, a mini-mart and a shopping plaza.</p>
