                <h2 class="small">Anahola, Kauai</h2>
                <p>Anahola is a small village located on the east shore of Kauai, approximately 14 miles (22.5 km) from Lihue. The old part of the village is located along the bay, and the new commercial part is located along the highway. The area is designated  Hawaiian Homelands, a residential area available to  Native Hawaiian people.</p>
                <p>There is a general store, a burger place and a post office. A nearby attraction is the Na &lsquo;Aina Kai Botanical Garden &ndash; a 240-acre well-maintained tropical garden. Guided tours are being offered for a fee. The jagged mountain that you can see from Anahola (on the mountain side of town) is called Kalalea Mountain, also known as King Kong's Profile.</p>
                <p>If you love secluded beaches, there are some near Anahola, including <a href="../../kauai/beaches/anaholabeach.php">Anahola Beach Park</a>, a very large sandy beach that is usually empty.</p>
