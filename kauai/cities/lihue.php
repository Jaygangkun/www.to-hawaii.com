                <h2 class="small">Lihu'e, Kauai</h2>
                <p>Lihu'e is Kauai's capital and mainly   a residential town with limited accommodation options (one Marriot hotel  and two inns). There are a few shops and restaurants at the <a href="../../kauai/shopping/kukuigrovecenter.php">Kukui   Grove Shopping Center</a> (the biggest shopping centers on Kauai), as well as  at the <a href="../shopping/harbormall.php">Harbor Mall</a> and <a href="../shopping/anchorcoveshoppingcenter.php">Anchor Cove   Shopping Center</a>. </p>
                <p>Local places of interest are the Nawiliwili   Harbor (where cruise ships dock), <a href="../../kauai/ancientsites/menehunealekokofishpond.php">Menehune Fishpond</a>, <a href="../beaches/kalapakibeach.php">Kalapaki Beach</a> and the Kauai Museum. </p>
