                <h2 class="small">Kekaha, Kauai</h2>
                <p>Kekaha is the last town along Highway 50 on Kauai's west shore. The highway runs right along the ocean. The town's main road (Kekaha Road) veers off near the Kikiaola Harbor and leads toward Kekaha's shopping plaza. 
                  Most visitors come to Kekaha because it is on the way to <a href="../attractions/waimeacanyon.php">Waimea Canyon</a>. This is the last town where you can get some food before you take a ride up the road to the state park. </p>
                <p>Kekaha itself doesn't have major tourist attractions. There is a two-mile long beach, <a href="../../kauai/beaches/kekahabeach.php">Kekaha Beach Park</a>, which is not a good swimming beach, but it is still worth stopping by because you can enjoy nice views to the island of Ni'ihau on the horizon.</p>
                <p>Kekaha  is a former sugar plantation town. Up until 1996, large cane trucks could be seen here daily as they brought their load to the Kekaha Sugar Company mill.</p>
