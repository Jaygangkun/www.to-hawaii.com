                <h2 class="small">Hanapepe, Kauai</h2>
                <p>Hanapepe, meaning &quot;crushed   bay&quot; in the Hawaiian language, is located  about 18 miles (29 km) west of Lihue. Hanapepe bills itself as &quot;Kauai's Biggest Little Town&quot; (look for the  welcome sign with this slogan on it as you enter the town).</p>
                                <p>In the 1800s, Chinese farmers came here to grow rice, and by the early 1900s until after World War II, Hanapepe was a bustling economic  center and one of the main population centers on Kauai. Hanapepe had many bars and shops, two movie theaters and three skating rinks. In 1982, the Kukui Grove Shopping Center opened in Lihue and many businesses in Hanapepe shut down. </p>
                                                <? include ("../../theme/text.html") ?>
                <p>Today, Hanapepe is mainly known for its art scene with the greatest  concentration of artists on the island. Hanapepe becomes alive every Friday night from 5-9 pm for a popular event called Friday Art Night. This is when local art studios  keep their doors open longer. Some artists  sit right on the sidewalk to paint and display their most recent work, and local performers play Hawaiian music.</p>
                <p>Another visitor attraction in Hanapepe is the Hanapepe   Swinging Bridge, which leads across the Hanapepe River.  The original bridge was built in 1911, but was destroyed by Hurricane Iniki in 1992. Located near Hanapepe is <a href="../../kauai/beaches/saltpondbeachpark.php">Salt   Pond Beach Park</a>, which is a good beach for swimming and picnicking (it has facilities).</p>
