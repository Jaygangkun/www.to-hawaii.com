                <h2 class="small">Waimea, Kauai</h2>
                <p>Waimea is located on Kauai's southwest shore. Even though this side of the island is  not as lush as the rest of Kauai, Waimea has other attractions that  are worth visiting. There are old buildings, churches, sugar mill ruins, as  well as the Captain Cook landing site, located on <a href="../../kauai/beaches/lucywrightbeachpark.php">Lucy   Wright Beach Park</a>.</p>
                <p>Waimea also has a few good restaurants and shops,  two supermarkets, two gas stations (your last chance to get gas before  you drive up to Waimea Canyon), as well as a few accommodation options.</p>
                <p>If you plan to drive to either the <a href="../../kauai/attractions/waimeacanyon.php">Waimea   Canyon</a> or the <a href="../../kauai/attractions/kokeestatepark.php">Koke'e   State Park</a>, Waimea is the last main town on your way there. From Waimea, the Waimea Canyon Drive leads to the canyon. As you drive up the road, stop for a moment at one of the lookout points to enjoy the nice view of Kauai's west shore and the island   of Niihau on the horizon.</p>
