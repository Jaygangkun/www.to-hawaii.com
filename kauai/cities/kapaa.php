                <h2 class="small">Kapa'a, Kauai</h2>
                <p>With a population of around 10,700, Kapa'a is the largest town on Kauai. It offers a variety of shopping opportunities, from small boutique stores to a large shopping center.  There are plenty of restaurants and accommodation options here as well, which make Kapa'a the preferred  choice for Kauai vacationers who want to enjoy dining  out and shopping. Accommodations in Kapa'a are relatively affordable compared to other resorts and hotels in  the Poipu and Prineville areas.<br />
                </p>
