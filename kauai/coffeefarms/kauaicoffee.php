                <h2 class="small">Kauai Coffee Company</h2>
                <p>The Kauai Coffee Company is a great place to visit if you like coffee. It is located in Ele'ele on Kauai's south shore, right on Highway 540 (also known as Kauai's Coffee Highway), which makes for a convenient stop on your way to Waimea Canyon (or on your way back). This is the largest coffee grower in Hawaii, as well as in the entire United States, with over four million coffee trees grown on 3,100 acres. </p>
                                <? include ("../../theme/text.html") ?>
                <p>At the visitor center you can sample their entire line of 100% estate-grown coffees. And best of all, the coffee tasting is free. There is a lanai with tables where you can sit down and enjoy the large variety of coffees, get a cookie from their snack bar and watch a short video that explains how they grow and harvest their coffee. Or you can take your cup of coffee and walk around the building to do a self-guided garden tour. It's about a one-mile (1.6 km) walk through a coffee field and along the way are signs that tell you more about how coffee is grown, the different varieties, harvesting, etc. </p>
                <p>After your coffee tasting, you can visit their gift shop, where you can purchase coffee to take home with you, as well as other souvenir items, such as grinders, press pots, insulated mugs, island-style pancake mixes, T-shirts, baseball caps, sweatshirts and more.</p>
