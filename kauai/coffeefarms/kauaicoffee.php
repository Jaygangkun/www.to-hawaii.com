                <h2 class="big">Kauai Coffee Company Overview</h2>
                <ul type="disc" class="mylist">
                  <li>All coffee beans are grown, harvested, roasted and packaged right here on their plantation (no foreign or imported beans are used)</li>
                  <li>Large variety of coffees are produced, including Peaberry, Yellow Catuai and Typica, as well as flavored varieties (vanilla, chocolate, macadamia nut, hazelnut and more)</li>
                  <li>Taste their estate-grown coffee right here at the plantation (coffee tasting is free)</li>
                  <li>Take a self-guided tour through the plantation (free of charge)</li>
                  <li>Nice views of the plantation, which overlooks the ocean</li>
                  <li>Sole producer of over half of the coffee grown in the United States</li>
                  <li>Committed to following environmentally friendly agricultural practices - largest drip irrigation coffee estate in the world with 2,500 miles (4,023 km) of drip tubing </li>
                  <li>Efficient drip irrigation system applies water and fertilizers directly to the roots of the trees, so there is no spraying or dusting of fertilizer </li>
                  <li>Coffee beans are GMO free</li></ul>
