                <h2 class="small">Fern Grotto, Kauai</h2>
                <p>Fern Grotto, an overgrown, fern-fringed lava cave, located on the Wailua River on the eastern side of Kauai near the town Wailua. Once an exclusive destination for Hawaiian royalty, the only way you can reach the grotto is via boat up the Wailua River, which is the only navigable river in the state of Hawaii. It takes about forty minutes to get to it. The scenery along Wailua River is beautiful, with a heavy overlay of plants along the bank. </p>
                              <? include ("../../theme/text.html") ?>
                <p>Fern Grotto is like an amphitheater of nature, and music played in it sounds amazing and unique. Taking advantage of the incredible natural acoustics of Fern Grotto, visitors are treated to musicians playing beautiful Hawaiian music. It's no wonder why this is a popular destination for wedding ceremonies. A paved path takes you up to the grotto itself, which is behind a large rock. </p>
                <p>Boats depart from the south side of Wailua River from the Wailua Marina every 30 minutes. You can also rent a kayak and paddle up the river yourself. However, the main boat dock at Fern Grotto is reserved for the tour boats. The surrounding land is owned by the Smith family, so make sure you have permission to dock your kayak there.</p>
