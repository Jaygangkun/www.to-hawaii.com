                <h2 class="small">Spouting Horn, Kauai</h2>
                <p>Spouting Horn is one of Kauai&rsquo;s natural wonders. Located near <a href="../../kauai/beaches/poipu-beach-park.php">Poipu Beach</a> on the island&rsquo;s south coast, this blowhole can shoot water up to 50 feet (15 m) into the air when waves crash into it. The water is forced up when the ocean flows under the lava shelf and is shot up through a small opening on the surface. The best times to see this natural phenomenon is during high tide and high surf.</p>
                <p>Another hole nearby only makes an eerie groan instead of blowing water. An ancient Hawaiian legend tells that the coastline was once guarded by a giant moo (lizard). This moo ate people who swam or fished here. One day, a man called Liko went swimming here and the moo tried to attack him. Liko managed to swim under the lava shelf and escaped through the blowhole, but the giant moo got stuck in the small opening. The story goes that the moo is still there today, and its cries are of hunger and pain.</p>
