                <h2 class="small">Bali Hai, Kauai</h2>
                <p>Bali Hai, also known as the Makana Mountain, is a part of a National Tropical Botanical Garden at the beginning of the Na Pali Coast on the north shore of Kauai. The Hawaiian name for this pyramid-shaped mountain peak is Makana or Mount Makana, which means a reward or &ldquo;gift from heaven&rdquo; in the Hawaiian language. The movie <em>South Pacific</em> released in 1960 featured this mountain as a forbidden but exotic island called Bali Hai. From then onwards, the name &ldquo;Bali Hai&rdquo; has stuck on and most people forgot its original name. </p>
                              <? include ("../../theme/text.html") ?>
                <p>Makana Mountain is the place where the renowned ceremony related to fire-throwing &ldquo;ohai&rdquo; took place in ancient Hawaii. This was a special ceremony performed to celebrate special occasions like students&rsquo; graduations or a high chief&rsquo;s visit. The skilled fire throwers used to climb the peak of the mountain, which had dry, light logs of hau (Hibiscus tiliaceus) and papala (Charpentiera spp.). </p>
                <p>At night time, the logs were set on fire and hurled into the ocean. The ridge lifts, which were created by the tradewinds that hit the Makana cliff, were successful in keeping the firebands aloft, rising to one mile (1.6 km) out on the sea. As a result, the night sky was illuminated with fiery torches and sparks that traced long arc lights.</p>
