                <h2 class="small">Waimea Canyon State Park, Kauai</h2>
                <p>Waimea Canyon (<a href="../../kauai/attractions/more_waimeacanyon_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>) is one of Kauai&rsquo;s most outstanding natural features. It is ten miles (16 km) long and over a half-mile (800 m) deep, making it the largest canyon in the Pacific. The canyon&rsquo;s rugged, craggy outface and its deep valley gorges have already made an impression on popular author Mark Twain, who called Waimea the &ldquo;Grand Canyon of the Pacific.&rdquo; </p>
                              <? include ("../../theme/text.html") ?>
                <p>Many tours are being offered in and around this amazing chasm, which has been carved by centuries of rains cascading down from Mount Waialeale&rsquo;s summit. Colorful rock layers reveal Waimea&rsquo;s geological history. </p>
                <p>The canyon is located within the Kokee State Park, which covers an area of over 4,300 acres (17.4 sq. km) and includes 45 miles (72.4 km) of hiking trails. Some of these trails are for experts only, while others are for hikers of all skill levels. Trail maps are available at the Ranger&rsquo;s Station, located at the Kokee Museum. </p>
                <p>The Walmea Canyon Drive is the main access road. It has several scenic lookout points. Bring your camera, a light jacket for the mountain climate and a full tank of gas because there is no service station in this area. </p>
