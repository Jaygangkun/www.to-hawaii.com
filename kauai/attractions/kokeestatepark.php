                <h2 class="small">Kokee State Park, Kauai</h2>
                <p>The Kokee State Park is located inward from the <a href="napalicoaststatepark.php">Na Pali Coast</a> on Kauai&rsquo;s west side. The park&rsquo;s main attractions are the native plants, birds and beautiful scenic views. Situated on a plateau of 3,200 to 4,200 feet (975 to 1,280 m), it is quite a bit colder here than on the beaches. The temperature can get as low as 45 degrees F (7 C) in January and is around 68 degrees F (20 C) in July. The annual rainfall is 70 inches (1778 mm). It rains the most from October to May.</p>
                              <? include ("../../theme/text.html") ?>
                <p>There are 19 hiking trails in the Kokee State Park. Tent camping is allowed and there are a few approved camp sites in the Kokee region, but overnighters must have a permit to camp. The cost is $5. The most accessible is at the head of Kanaloahuluhulu Meadow. It has grills and a comfort station with cold showers. The fee is $5 per night. The other sites, Sugi Grove and Kawaikoi, are on opposite sides of Kawaikoi Stream. They can only be accessed with a four-wheel drive vehicle. There is currently no charge for the permit, but this will change soon.</p>
                <p>If you&rsquo;d like more comforts, there are modern cabins at the Kokee Lodge. The cabins sleep up to six people. The maximum stay is five days and the minimum stay is two days. The cabins are popular and should be booked three to four months in advance. The cost per person per night is $92.80 and there is a two-person minimum. For more information, contact the Kokee State Park at (808) 335-6061.</p>
                <h2 class="big">Hiking trails at Kokee State Park:</h2>
                <ul type="disc" class="mylist">
                  <li>Alakai Swamp Trail &ndash; Length: 3.5 miles (5.6 km). It leads through a native wet forest to the rim of Wainiha Pali with sweeping view of Kauai&rsquo;s north shore. It&rsquo;s a good trail for bird watching. It is often wet, slippery and muddy. </li>
                  <li> Awaawapuhi Trail &ndash; Length: 3.25 miles (5.2 km). The trail features mesic and dryland native plants and great views into the Nualolo and Awaawapuhi valleys. </li>
                  <li> Berry Flat Trail &ndash; Length: 0.6 miles (0.97 km). This trail leads through a mix of planted alien and native forests, including redwood and sugi groves and a koaohia forest.</li>
                  <li> Black Pipe Trail &ndash; Length: 0.5 miles (0.8 km). This trail is an alternate access to the Canyon Trail. Native iliau and hibiscus can be seen here.</li>
                  <li> Canyon Trail &ndash; Length: 1.8 miles (2.9 km). This is a popular scenic trail with great views of the Waimea and Poomau canyons. The trail follows the rim of Waimea Canyon and crosses Kokee Stream.</li>
                  <li> Cliff Trail &ndash; Length: 0.1 miles (0.16 km). This is a spur trail leading to a viewpoint of Waimea Canyon. Feral goats can often be seen on the canyon walls.</li>
                  <li> Ditch Trail &ndash; Length: 1.7 miles (2.7 km). The trail was developed to construct and maintain the Kokee Ditch. It features views of the surrounding forest and Poomau Stream.</li>
                  <li> Faye Trail &ndash; Length: 0.1 miles (0.16 km). This is a short trail that accesses other trails in the Halemanu area.</li>
                  <li> Halemanu-Kokee Trail &ndash; Length: 1.2 miles (1.9 km). This trail leads through a somewhat disturbed koaohia forest. </li>
                  <li> Iliau Nature Loop &ndash; Length: 0.25 miles (0.4 km). This is an easy roadside trail that leads through dry shrubland. It features sweeping views of Waimea Canyon and Waialae Canyon.</li>
                  <li> Kaluapuhi Trail &ndash; Length: 1.6 miles (2.6 km). This trail leads through an ohia montane mesic forest. It is a good bird watching trail.</li>
                  <li> Kukui Trail &ndash; Length: 2.5 miles (4.0 km). This is a scenic, but steep trail into Waimea Canyon. It has an elevation drop of 2,000 feet (610 m).</li>
                  <li> Kumuwela Trail &ndash; Length: 1 mile (1.6 km). This trail goes through a mix of ohia and koaohia montane mesic forests with alien weed problems and some hurricane damage. It is a good bird watching trail.</li>
                  <li> Nature Trail &ndash; Length: 0.1 miles (0.16 km). This is a good beginner&rsquo;s trail to learn about native forest vegetation.</li>
                  <li> Nualolo Trail &ndash; Length: 3.75 miles (6.04 km). This trail goes through a koaohia montane mesic forest and an aalii lowland dry shrubland with a nice view of Nualolo Valley. </li>
                  <li> Pihea Trail &ndash; Length: 3.7 miles (6.0 km). This is a scenic nature trail along the rim of Kalalau Valley, leading through an ohia montane wet forest. It&rsquo;s a great bird watching trail.</li>
                  <li> Puu ka Ohelo Trail &ndash; Length: 0.5 miles (0.8 km). This trail shows the impact that invasive alien weeds can have on native forest vegetation.</li>
                  <li> Waininiua Trail &ndash; Length: 0.6 miles (0.97 km). This is a good trail to see a relatively intact koaohia montane mesic forest. </li>
                  <li> Water Tank Trail &ndash; Length: 1.0 mile (1.6 km). This trail leads through a native koaohia forest. </li>
                </ul>
