                <h2 class="small">Kilauea Lighthouse, Kauai</h2>
                <p>Located at Kilauea Point on the north shore of Kauai, the Kilauea Lighthouse (<a href="../../kauai/attractions/more_kilauealighthouse_photos.html" onclick="NewWindow(this.href,'name','1100','600','yes');return false">see more photos</a>) stands at the northernmost point of the eight main Hawaiian Islands. It was built in 1913 by the U.S. government and served for many years to keep ships traveling to and from Asia safe from running aground and provide direction. Back then, the lighthouse&rsquo;s beam could be seen 90 miles (145 km) out at sea. It had the largest lens of its type when it was built. The lighthouse was decommissioned in 1976. </p>
                              <? include ("../../theme/text.html") ?>
                <p>The Kilauea Lighthouse is now part a wildlife refuge of 203 acres (0.8 sq. km) that is managed by the U.S. Fish and Wildlife Service. Here you can observe various species of native and transient birds and study the native vegetation in the visitor center. Over 500,000 visitors a year visit the Kilauea Lighthouse. </p>
                <p>Besides the lighthouse, there are three keepers' quarters, made from field stone, a fuel oil shed, old cisterns and a landing platform. The Kilauea Lighthouse is one of the most intact light stations left in the United States.</p>
                <p>After so many years of being exposed to the elements and harsh salt conditions, the landmark needs help to stay in good shape. To help restore this piece of Hawaiian history, the Kilauea Point Natural History Association has established a fund to raise money. (Volunteers are needed as well.) Fans of the Kilauea Light Station can buy a personalized brick to help towards this end.</p>
