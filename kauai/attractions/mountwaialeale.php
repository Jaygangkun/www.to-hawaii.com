                <h2 class="small">Mount Wai'ale'ale, Kauai</h2>
                <p>Taking sunny pictures of Mount Wai'ale'ale (<a href="../../kauai/attractions/more_mtwaialeale_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>) proves to be difficult. This mountain and especially its summit is almost always concealed in moisture-laden clouds. In fact, it is one of the wettest locations on Earth, receiving about 450 inches (11,430 mm) of rain each year. The rainiest year on record so far was 1982 with 683 inches (17,300 mm). </p>
                <p>Many sources (including the local tourist industry) say that Mt. Wai'ale'ale is <em>the</em> wettest spot on Earth, however, the 38-year average at Mawsynram (India) is higher at 467.4 inches (11,870 mm), according to the <em>Guinness Book of World Records</em>.</p>
                              <? include ("../../theme/text.html") ?>
                <p>Located in the center of Kauai, Mt. Wai'ale'ale rises 5,148 feet (1,569 m), making it the second highest peak on the island, after Kawaikini at 5,243 feet (1,598 m). In the Hawaiian language, Wai'ale'ale means &ldquo;rippling water&rdquo; or &ldquo;overflowing water.&rdquo;</p>
                <p>There are several reasons why Wai'ale'ale receives so much rain. First, Kauai is the northernmost of the main Hawaiian Islands, so it has more exposure to frontal systems that bring rain in the winter. Second, Wai'ale'ale has a round and conical shape, which exposes all sides of its summit to moisture-laden winds. Third, its summit lies just below the trade wind inversion layer of 6,000 feet (1,829 m), above which trade-wind-generated clouds cannot rise. And finally, the mountain's steep cliffs cause the humid air to rise quickly over 3,000 feet (910 m) in less than a half mile (800 m) and drop a large portion of its rain on one spot.</p>
                <p>The summit itself is rather barren, despite all the water it receives. One of the reasons for that is that few plants and trees can handle that much rain. Also, since the summit is shrouded in clouds on most days of the year, little sunshine reaches the ground to foster plant growth. However, fungi and lichen flourish here.</p>
                <p>Surrounding Wai'ale'ale is the Alakai Wilderness Preserve (also known as the Alakai Swamp), which is home to many rare plants, birds and other animals. What the summit lacks in plant growth, this area makes up for it, and big time. The entire area couldn't be any lusher, with ferns, trees and plants about everywhere. The ground is very saturated and there are a few hiking trails in the area. However, there is no access to the summit of Wai'ale'ale. The only way to see it is by helicopter.</p>
