                <h2 class="small">Wailua River, Kauai</h2>
                <p>The Wailua River (<a href="../../kauai/attractions/more_wailuariver_photos.html" onclick="NewWindow(this.href,'name','1100','600','yes');return false">see more photos</a>) on Kauai is the only navigable river in the entire state of Hawaii. It is fed by two main waterfalls. Rains from Mount Waialeale, the wettest location on Earth, with an abundant 480 inches (12,192 mm) of rain per year, also help to feed the river.</p>
                              <? include ("../../theme/text.html") ?>
                <p>For those wanting a bit of a challenge on their Kauai vacation, a trip down the river to the legendary Fern Grotto is a wonderful way to see the beauty of the natural habitat of the &ldquo;Garden Isle&rdquo; up close. You can travel on the river by kayak or canoe. </p>
                <p>Boat tours are also available and will take you to <a href="fern-grotto.php">Fern Grotto</a> as well. The boats are open-air and guarantee a 360-degree view of all the beautiful scenery. The luxuriant river valley once served as the capital of ancient Kauai. It was held to be a sacred place for the island&rsquo;s &ldquo;alii,&rdquo; or royalty.</p>
                <h2 class="big">Wailua River Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Only navigable river in Hawaii</li>
                  <li>Boat tours are available or you can go on your own in a kayak or canoe</li>
                  <li>River flows through lush river valleys that offer scenic views</li>
                  <li>Leads to Fern Grotto, a popular Kauai visitor attraction</li></ul><div style="clear:both"></div>
