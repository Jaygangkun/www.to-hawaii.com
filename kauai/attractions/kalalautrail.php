                <h2 class="small">Kalalau Trail, Kauai</h2>
                <p>The Kalalau Trail is the most popular hike on Kauai. The trail leads along the majestic <a href="../attractions/napalicoaststatepark.php">Na Pali Coast</a>, which is considered by many to be one of the most beautiful coastlines in the world. The views from the trail are amazing, and the hike is a once-in-a-lifetime adventure for many visitors.</p>
                <p>However, the 11-mile (18 km) trail is not suitable for everybody. It is very strenuous with steep inclines and declines. If you have a fear of heights, you may not like the fact that the trail hugs high sea cliffs and is very narrow in several areas, not more than a mere two feet (60 cm) wide. Furthermore, the trail can be very slippery after recent rainfall and also because of loose crumbly rocks underfoot. In the past, high surf has eroded some parts of the trail, and flash floods and strong currents can pose a danger at river crossings.</p>
                              <? include ("../../theme/text.html") ?>
                <p>The trail begins at <a href="../beaches/kee-beach-park.php">Ke'e Beach</a> and leads all the way to Kalalau Valley. A physically fit and experienced hiker can do the hike to Kalalau in a day, but most people need two days and camp at Hanakoa (for which a permit is required). Access to Kalalau is controlled and only a limited number of permits are issued in the cause of conservation. So it's best to apply early, about 6-12 months in advance.</p>
                <p>The first 3 miles (5 km) of the trail until <a href="../beaches/hanakapiaibeach.php">Hanakapi'ai Beach</a> are moderately strenuous (the second mile is a steep downhill and tough on the way back). Most people hike up until here, which  takes about 1.5 to 2 hours each way, so it's a good day trip. From Hanakapi'ai Beach, a side trail leads to Hanakapi'ai Falls (another 2-mile hike inland). </p>
                <p>From Hanakapi'ai Beach, the trail continues for about 4 miles (6.4 km) to Hanakoa Valley, which doesn't have beach access. From here, there is another half-mile (800 m) side trail to Hanakoa Falls. Hiking beyond Hanakoa requires a camping permit, even if no camping is planned. The last 5-mile (8 km) stretch of the Kalalau Trail is the toughest, with many switchbacks and narrow spots (especially between miles 6.5 and 8). At the end, you'll reach Kalalau Valley, which measures almost a mile (1.6 km) across. This is one of the most remote and inaccessible spots in Hawaii. The gorgeous views of the valley, the beach and the fact that it is so isolated make this a magical place to explore. From here, a 2-mile (3.2 km) side trail leads inland to the &quot;Big Pool,&quot; a large pool in the stream.</p>
                <p>From <a href="../beaches/kalalaubeach.php">Kalalau Beach</a>, some hardcore adventurers swim over to the neighboring <a href="../beaches/honopubeach.php">Honopu Beach</a>, which is the ultimate remote beach. Getting here requires getting wet because the only legal way to access it is by swimming to it (no boats, kayaks and even surfboards are allowed on the beach). However, the currents can be very strong along the entire Na Pali Coast and many drownings have occurred along this stretch of coastline. It is recommended to stay out of the water at all beaches along the Kalalau Trail. If you get into trouble, there is no lifeguard to rescue you.<br />
                </p>
                <p><strong>Camping fee: </strong>$20 per person per night ($15 for Hawaii residents). Hanakoa and Kalalau are the only two authorized areas for camping along the trail. A maximum stay of 5 nights is allowed in the Na Pali Coast State Park. Within the 5-night maximum, no 2 consecutive nights are allowed at Hanakoa.</p>
                <p>A camping permit is required when hiking beyond Hanakoa, even if overnight camping is not planned.</p>
                <p>To get a permit, visit http://www.hawaiistateparks.org/camping/permit_napali.cfm (click on the button &quot;Online Reservations&quot; in the top left corner).</p>
                <h2 class="big">Kalalau Trail Overview</h2>
                <ul type="disc" class="mylist">
                  <li>11-mile (18 km) hike along Kauai's Na Pali Coast (north shore)</li>
                  <li>Day hiking without a permit is allowed until Hanakoa, which is 6 miles (9.6 km) from trailhead</li>
                  <li> Beautiful views of tropical scenery, tall sea cliffs and the crystal clear, azure blue ocean</li>
                  <li> Strenuous and difficult hike, steep and narrow trail, strong sun</li>
                  <li> Beware of dangers (narrow trail along high sea cliffs, dangerous shorebreak and rip tides at Hanakapi'ai and Kalalau beaches, flash floods and strong currents at river crossings, rock falls, etc.)</li>
                  <li> No water fountains along the way, bring bottled water or water treatment tablets to drink from streams and waterfalls along the way (water may be contaminated by bacteria)</li>
                  <li> Bring a small first aid kit and sunscreen, wear sturdy shoes</li>
                  <li> Camping allowed with permit only at Hanakoa (6 miles) and Kalalau (11 miles)</li>
                  <li> The only facilities along the way (at Hanakapiai, Hanakoa and Kalalau) are composting toilets<br />
