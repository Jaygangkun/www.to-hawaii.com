                <h2 class="small">Na Pali Coast State Park, Kauai</h2>
                <p>The Na Pali Coast State Park (<a href="../../kauai/attractions/more_napalicoast_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>) is located on the north shore of Kauai. It is a great place to &ldquo;rough it&rdquo; and go on an adventurous hike. The area has a dramatic, with 4,000-foot (1,219 m) cliffs, known as &ldquo;pali&rdquo; in Hawaiian, towering above crystal clear waters. Below, a coral reef is home to an amazing array of colorful tropical fish.</p>
                              <? include ("../../theme/text.html") ?>
                <p>This stretch of coastline is a wilderness area of 6,175 acres (25 sq. km), consisting of cliffs that define an untamed region of deep and narrow valleys that end at the sea. Waterfalls and swift streams cut through the valleys as they have for eons. Remains of the ancient Hawaiians can still be found in places.</p>
                <p>The park is inaccessible to automobiles, but exploring can be done by hiking, kayaking, boating and even by helicopter. Snorkeling in the secluded coves is a popular activity, and if you travel between January and April, you&rsquo;re likely to catch sight of the Pacific humpback whales. The Kalalau Trail provides the only land access. It&rsquo;s an eleven-mile (17.7 km) hike through five major valleys before reaching Kalalau Beach.</p>
                <p>The &ldquo;Forbidden Island&rdquo; of Niihau is across the channel, a primitive place of no modern technology. Niihau is inhabited by Native Hawaiians only, but visitors are allowed to snorkel along the pristine coastline of the island. It is one of the best snorkeling locations in the world, as the water is unpolluted and clear. </p>
                <p>Camping on the Napali Coast is allowed with permits. Hiking permits are required to hike beyond Hanakapiai Valley. No drinking water is available. Water from streams must be treated. Visitors must take all their trash with them when they leave. There is no entrance fee to the Na Pali State Park; the fee for camping permits is $10 per person. </p>
