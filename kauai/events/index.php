          <h1>Kauai Events</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p><strong>January</strong><br />
                <br />
                No major scheduled events.<br />
                <br />
                <strong>February</strong><br />
                <br />
                Waimea Town Celebration: This 2-day event takes place in Waimea on the west coast of Kauai, the place where Captain Cook first landed when he discovered Hawaii. It is a celebration of the town's multiethnic history featuring great Hawaiian entertainers, rodeo and food booths. Waimea, Kauai. Second or third week of February.<br />
                <br />
                <strong>March</strong><br />
                <br />
                Prince Kuhio Celebrations: A state holiday celebrating the birth of Jonah Kuhio Kalanianaole, who was born on March 26, 1871, and elected to Congress in 1902. Takes place on all islands. Kauai, his birthplace, stages a huge week-long celebration in Lihue. Second half of March.<br />
                <br />
                <strong>April</strong><br />
                <br />
                No major scheduled events.<br />
                <br />
                <strong>May</strong><br />
                <br />
                Annual Lei Day Celebrations: May Day is Lei Day in Hawaii. It is celebrated with lei-making contests and arts and crafts on all the islands. May 1.<br />
                <br />
                <strong>June</strong><br />
                <br />
                King Kamehameha Celebration: A state holiday with a big flower parade and more. Takes place on all islands. First weekend in June. <br />
                <br />
                
                <strong>July</strong><br />
                <br />
                Koloa Plantation Days: Koloa is a little town on Kauai's southern coast. This week-long community event offers local-style fun celebrating Koloa's plantation past with a rodeo, craft fair, block party, historic walks, a traditional luau, cooking demos and more. End of July.<br />
                </p>
                    <? include ("../../theme/text.html") ?>
             <p>   <strong>August</strong><br />
                <br />
                No major scheduled events.<br />
                <br />
                <strong>September</strong><br />
                <br />
                Aloha Festivals: Parades, street festivals and other events in celebration of Hawaiian culture. Takes place on all islands. Each week from mid-September through October is Aloha week on a different island. <br />
                <br />
                <strong>October</strong><br />
                <br />
                Emalani Festival: A celebration in honors of Queen Emma, who was a gardener and Hawaii's first environmental queen. She built a forest trek to Kokee State Park together with 100 friends in 1871. Kokee State Park, Kauai. Second Saturday in October.</p>
              <p> <br />
                <strong>November</strong><br />
                <br />
                Hawaii International Film Festival: A statewide movie festival featuring film makers from Asia, the Pacific Islands and the United States. Most screenings take place on Oahu, but on the final weekend, events take place on all the islands. First two weeks in November. <br />
                <br />
                Hawaiian Slack-Key Guitar Festival: A great opportunity to listen to Hawaii's top slack-key guitarists. This event is five hours long and free. Kauai Marriott Resort, Lihue, Kauai. Mid-November.<br />
                <br />
                <strong>December</strong><br />
                <br />
                Festival of Lights: A lighting ceremony that takes place in front of the former county building. Rice Street, Lihue, Kauai. Early December.</p>
