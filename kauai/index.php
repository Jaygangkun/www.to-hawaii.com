              <p>Aloha and welcome to the beautiful island of Kauai. Nicknamed &#8220;The Garden Isle,&#8221; Kauai is considered by many to have the lushest greenery and most distinctive natural beauty. Ideal ingredients for a perfect Kauai vacation.  The outdoor activities here are incomparable. Kayak the <a href="../kauai/attractions/napalicoaststatepark.php"> Na Pali Coast</a> below majestic volcanic cliffs, travel through state parks on horseback, or view Kauai&#8217;s stunning landscape by helicopter. Whichever way you choose to tour Kauai, you&#8217;re in for exhilarating views and unique native plant and bird sightings.</p>
              <p> Kauai is a curious mix of ranchlands, sugarcane fields, rainforests, pristine beaches and sea cliffs. The oldest of the main Hawaiian Islands, Kauai&#8217;s 553 square miles exude a distinct timelessness not present on the neighboring islands.</p>
