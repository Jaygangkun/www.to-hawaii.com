              <p> <strong>Golf</strong></p>
              <p> Golfers rejoice! Kauai is a prized golfing destination, home to five top-rated golf courses. Challenging obstacles and uniquely shaped terrain are enhanced by magnificent backdrops that surround each hole. Reserve your spot at  one of these popular golfing destinations: Kiahuna Golf Club, Poipu Bay Golf Course, Princeville Golf Club, Puakea Golf Course or Kauai Lagoons Golf Club.</p>
              <p><strong>Spouting Horn</strong></p>
              <p> <a href="../kauai/attractions/spouting-horn.php"> This natural delight on Kauai</a>&#8217;s south shore consists  of a lava formation that spouts out a towering geyser of water every time the water swells in during high tide. Water spout heights can reach up to 60 feet (18 m)!</p>
              <p> <strong>Waimea Canyon</strong></p>
              <p> At the famous Na Pali Coast of West Kauai, visitors will find <a href="../kauai/attractions/waimeacanyon.php"> Waimea Canyon</a>, also known as &#8220;The Grand Canyon of the Pacific,&#8221; a definite must-see for Kauai visitors. Waimea Canyon is 10 miles (16 km) long, 2 miles (3.2 km) wide and a whopping 3,600 feet (1,097 m) deep. </p>
              <p> <strong>Waialeale Crater (West Kauai) </strong></p>
              <p> Although rainfall is minimal at about 34 inches (86 cm) per year in Poipu, Mt. Waialeale Crater is the wettest spot in the world with an average of 460 inches (1,168 cm) of rain per year. Hike around the crater or take a helicopter tour for a breathtaking aerial perspective of the resulting waterfalls. </p>
              
              <p> <strong>Alakai Wilderness Preserve</strong></p>
              <p> Kauai&#8217;s Alakai Wilderness Preserve is 939 acres of pure country, including the 10-mile (16 km) Alakai Swamp and many rare plant and animal species.&nbsp;</p>
              <p><strong>Botanical Gardens</strong></p>
              <p> Kauai is home to the picturesque <em>Lawai</em> Garden, the only tropical botanical garden chartered by the U.S. Congress. Here you can view a unique and extensive collection of tropical plant life from Hawaii and around the world. </p>
              <p> <em>Allerton</em> Garden, adjacent to Lawai Garden, offers 100 acres of brilliant flowers and diverse trees. Stop by Kauai&#8217;s north shore prize, the 17-acre <em>Limahuli </em>Garden, a garden noted for its incorporated historical 
                and archaeological sites. </p>
              <p> <strong>What makes Kauai unique?</strong></p>
              <p>Vacationers who choose Hawaii don&#8217;t take long to settle on Kauai, the &#8220;must-see&#8221; island in the chain. Remember the gorgeous scenery in the movies <em>South Pacific, Fantasy Island</em> and <em>Blue Hawaii</em>? There is a reason Kauai is the location of choice for Hollywood directors, honeymooners and visitors of all ages. Kauai is small enough to get that quaint island feel, yet so diverse in activities, landscape and geography that one would need to return several times to see and do everything here.</p>
