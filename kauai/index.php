              <p><strong>Geography and Climate</strong></p>
              <p> As the fourth largest Hawaiian island, Kauai&#8217;s climate is  semitropical. Kauai is home to several different micro-climates thanks to the varying landscape and elevation differences. From sand dunes to bare desert plateaus, fertile river valleys, foothills, tropical rainforests and awe-inspiring mountain tops, Kauai is an exceptional geographical subject. </p>
              <p> Temperatures range from the lower 60s to the upper 80s (15.5-26.6 C) depending on elevation and season. Adventurers can explore any one of eight state parks, or picnic in one of the 67 county parks. Mountain lovers can hike up to the island&#8217;s highest spot, Kawaikini Peak at an elevation of 5,243 feet (1,598 m), or the slightly shorter Mt. Waialeale at 5,052 feet (1,540 m). </p>
              <p> A resident population of just over 58,000 leaves plenty of room to lose yourself in this uncrowded paradise.</p>
              
              <p> <strong>Beaches</strong></p>
              <p> Visitors can play on Kauai&#8217;s 69 spectacular sandy beaches scattered along the island&#8217;s 113 miles of shoreline. That&#8217;s more beach per coastline mile than any other Hawaiian Island! The majority of beaches here are pristine white-sand beaches, and each offers unique views and activities. If you love to swim, visit one of the many south shore beaches. For scuba and <a href="../snorkeling.php">snorkeling</a>,  visitors will find breathtaking spots on Kauai&#8217;s east side. <a href="../kauai/beaches/tunnelsbeach.php"> Tunnels Beach</a>, <a href="../kauai/beaches/lumahai-beach.php"> Lumahai Beach</a>, <a href="../kauai/beaches/poipu-beach-park.php"> Poipu Beach</a> and <a href="../kauai/beaches/hanaleibay.php"> Hanalei Bay Beach</a> are some of the most popular and most scenic spots.</p>
