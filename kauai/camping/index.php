          <h1>Kauai Camping at Campgrounds / Campsites</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p><img src="../../images/kauai_camping.jpg" alt="Kauai Camping" style="width:100%; max-width: 478px;" /></p>
              <p>Camping is a popular activity on Kauai and this is not surprising considering the island's spectacular beaches, rainforests, mountains and breathtaking vistas. There are 13 campgrounds on Kauai, ranging from beach campsites to mountain cabins. Six of them are state campsites and the remaining seven are county campsites. If you plan to camp on Kauai, no matter if at a state or county site, you will need to obtain a camping permit in advance. </p>
              <p><strong>Camping at a Hawaii state park on Kauai</strong></p>
              <p>All state camping areas are administered by the Hawaii DLNR, State Parks. They prefer that all reservations are made on their online portal, which is located at: www.hawaiistateparks.org/camping/kauai.cfm (see the button &ldquo;Online Reservations&rdquo; in the top left corner on their website). You can make your reservation up to one year in advance. </p>
              <? include ("../../theme/text.html") ?>
              <p><strong>Here is a list of all state campsites on Kauai:</strong></p>
              <p><a href="../attractions/napalicoaststatepark.php">Na Pali Coast Wilderness Park</a> (has four camp sites: Hanakapiai, Hanakoa, Kalalau, Milolii)<br />
                <a href="../attractions/kokeestatepark.php">Kokee State Park</a><br />
                <a href="../beaches/polihale-state-beach-park.php">Polihale State Park</a></p>
              <p><strong>Here is an overview of the state campsite fees:</strong></p>
              <span class="subtitle"><strong>All state parks (except Na Pali Coast):</strong></span>
              <p>Hawaii residents:  $12 per campsite per night for up to 6 persons; $2 per night for each additional person*.  Maximum fee per site: $20/night.<br />
                Non-residents:  $18 per campsite per night for up to 6 persons; $3 per night for each additional person*.  Maximum fee per site: $30/night.<br />
                *Children 2 and under free.</p>
              <span class="subtitle"><strong>Na Pali Coast State Wilderness Park:</strong></span>
              <p>Hawaii Residents:  $15 per person per night.<br />
                Non-residents:  $20 per person per night.</p>
              <p>It is a good idea to make your reservation well in advance, at least a few months, because space is limited. If you plan to hike the Na Pali Coast and would like to camp there, make your reservation at least six months in advance. Camping permits for the Na Pali Coast are very much in demand.</p>
              <p><strong>Camping at Kauai County campsites</strong></p>
              <p>To get a camping permit for one of the county campsites, contact the County of Kauai Parks &amp; Recreation. On their website you can download and print a camping permit request form and mail it in. The form is located here: http://www.kauai.gov/government/departments/<br />
                parksrecreation/campinginformation/tabid/176/default.aspx</p>
              <p><strong>Here is a list of all Kauai County campsites:</strong></p>
              <p><a href="../beaches/anaholabeach.php">Anahola Beach Park</a>: closed every Thursday from 10 am to 12 pm on Friday for maintenance<br />
                <a href="../beaches/anini-beach-park.php">Anini Beach Park</a>: closed every Tuesday from 10 am to 12 pm on Wednesday for maintenance<br />
                <a href="../beaches/haena-beach-park.php">Haena Beach Park</a>: closed every Monday from 10 am to 12 noon on Tuesday for maintenance)<br />
                <a href="../beaches/hanaleibay.php">Hanalei Black Pot Beach Park</a>: open Friday &amp; Saturday only, closed Sunday thru Thursday<br />
                <a href="../beaches/hanamaulubay.php">Hanamaulu Beach Park</a>: open Thursday, Friday &amp; Saturdays only &amp; certain school holidays, closed Sunday thru Wednesday<br />
                <a href="../beaches/lucywrightbeachpark.php">Lucy Wright Beach Park</a>: closed every Monday from 10 am to 12 pm Tuesday for maintenance<br />
                <a href="../beaches/saltpondbeachpark.php">Salt Pond Beach Park</a>: closed every Tuesday from 10 am to 12 pm Wednesday for maintenance</p>
                              
              <p><strong>Here is an overview of the county campsite fees:</strong></p>
              <p>County camping permit fees are $3 per adult per night for non-residents and free for Hawaii residents (Hawaii ID required).  Children under the age of 18 years are also free and must camp with at least one adult 18 years of age or older. For more information about county camping permits, call the County of Kauai, Department of Parks &amp; Recreation, Parks Permit Section at (808) 241-4463.</p>
