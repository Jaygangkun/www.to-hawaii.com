                <h2 class="small">Na 'Aina Kai Botanical Gardens, Kauai</h2>
                <p>If there were ever a botanical garden to visit with the whole family, Na 'Aina Kai on the north shore of Kauai is it. Whereas many botanical gardens educate but often bore, the Na 'Aina Kai Botanical Gardens breaks the mold by offering an Eden-like world of cascading waterfalls, colorful plants, fish-filled ponds, gazebos, fountains and lagoons. Set on 240 acres of lush, tropical coastal land that was once used for sugarcane farming, these gardens are now an exceptional natural attraction that should not be missed. </p>
                                <? include ("../../theme/text.html") ?>
                <p>                  The Na 'Aina Kai Botanical Gardens were originally private gardens founded by Joyce and Ed Doty in 1982, who later opened their doors to the public in 1999 and established a nonprofit organization. The site features an impressive diversity of land, including 13 gardens, meadows, farmland, a hardwood plantation, a canyon and beachfront. Over 90 bronze sculptures can be found throughout the estate, comprising one of the largest private collections in the country. Don't miss the 16-foot bronze &quot;Jack and the Beanstalk&quot; fountain located in the Under The Rainbow Children's Garden, which also features a tree house, trains, bridges, tunnels, slides and log cabins. </p>
                <p>                  Other highlights include the International Desert Garden, with its massive collection of cacti, aloe, agave, tamarind and baobab plants; the Bog House, containing carnivorous plant species, such as pitcher plants and butterworts; the impressive Poinciana Maze; the Shower Tree Park &amp; Ka'ula Lagoon featuring a traditional Japanese teahouse and plants, such as firecracker flowers, fiddlewood trees, hibiscus and ixora; and the Wild Forst Garden featuring many spice trees like cinnamon, nutmeg, vanilla, cardomom, ginger, heliconia, noni and ylang ylang, as well as ornamental banana trees. Dozens of bird, mammal and reptile species inhabit the grounds as well.</p>
