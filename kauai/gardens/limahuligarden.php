                <h2 class="small">Limahuli Garden, Kauai</h2>
                <p>Located in <a href="../cities/haena.php">Haena</a> on the north shore of Kauai, the Limahuli Garden is set in a lush tropical valley, overlooking the Pacific Ocean and the majestic Makana Mountain. It extends for over 1,000 acres (4 km&sup2;) of tropical rainforest and covers three distinct ecological zones. Seventeen acres are available to the public. The remaining acres are a protected nature preserve. A 3/4 mile (1.2 km) long loop trail leads through the garden. It has some steep areas.</p>                <? include ("../../theme/text.html") ?>
                
                <p>Guided or self-guided tours examine the lava rock terraces built by ancient Hawaiians to cultivate taro, an important food crop originally introduced by the Polynesians. The Limahuli Garden is dedicated to the preservation of native Hawaiian culture and plants. It is also the location of ongoing research and educational programs. </p>
                <p>Taro and other plants make up the garden's collection. There is a plantation-era garden and examples of invasive species of plants introduced by modern man. The Limahuli Garden was chosen by the American Horticultural Society as the best natural botanical garden in the United States.</p>
