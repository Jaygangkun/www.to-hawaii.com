              <div class="midpanel_row">
                <p>Situated in the lush Lawai Valley, this garden contains the world's largest ex situ collection of native Hawaiian plants. Many threatened, rare and endangered tropical plants from around the globe can be viewed here. The McBryde Tropical Botanical Garden was the first of five gardens that make up Hawaii's National Tropical Botanical Garden, a site containing nearly 2,000 acres of protected land where plants can flourish. </p>
                
                <p>                  Once occupied by a sugar plantation, this land was purchased in the early 1970s and subsequently transformed into a botanical garden. The site contains many distinct microclimates, including hot, cool, wet, dry, meadow and cliff areas. Hundreds upon hundreds of plant species can be seen here, including many flowering trees, orchids, heliconias, rubiaceae, palms and others that have been collected from tropical regions around the world. The Canoe Garden showcases a fascinating collection of ancient Hawaiian plants and Maidenhair Falls, which is surrounded by ginger, allspice and chocolate plants. </p>
                <p>                  The garden provides a space to not only enjoy the plants, but also study and learn about them. Here you'll find a state-of-the-art horticulture and micropropagation facility. McBryde's knowledgeable staff is available to answer any questions you may have about the plant life, conservation efforts and the history of the garden. Wandering the gardens, you'll see the lovely Lawai Stream and can cross it on a bamboo bridge.</p>
