            <h1><span itemprop="name">Moir Gardens, Kauai</span></h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <p>Developed during the 1930s, this well-known botanical garden is located on the grounds of Kiahuna Plantation in Poipu. Alexandra &quot;Sandie&quot; Moir, the wife of longtime sugar plantation manager Hector Moir, created the garden, which were first opened up to the public in the mid-1950s. Since then the garden has become a popular visitor attraction home to many rare plants and native species.</p>
                
                <p>                  What's unique about Moir Gardens, also known as Pa'u a Laka (meaning &quot;skirt of laka&quot; and named after the Hawaiian goddess of hula), is that due to the particularly dry climate in Poipu, many popular Hawaiian tropical plants such as heliconia, ginger and hibiscus don't grow well here. Alexandra Moir realized this early on and filled her garden instead with cactus and succulents that flourish in such a dry climate. </p>
                <p>                  Over the years the garden has become one of the premier cactus and succulent gardens in the world, alongside Huntington Gardens and the Royal Gardens of Monaco. Moir Gardens has even been featured in many worldwide publications, such as the book Great Gardens of America and both Sunset and Life magazines. </p>
                <p>                  The garden is also home to other plants that require little rain, such as orchids, bromeliads and many mature trees. Traditional Hawaiian plants including wiliwili, hau, coconut and plumeria firmly ground the gardens in the local traditions, though you'll also find dozens of foreign species collected by Moir's brother-in-law, William Whitmore Goodall Moir, during his world travels. Several lava rock ponds filled with Japanese koi fish and water lilies further enhance the Moir Gardens experience.</p>
