                <h2 class="small">Allerton Garden, Kauai</h2>
                <p>Located on the south shore of Kauai, the Allerton Garden lies between the ocean and the McBryde Garden in Lawai Valley, which was once a retreat of Hawaii's Queen Emma. It offers scenic views of towering rainforest trees, a grove of swaying golden bamboo, tropical fruit trees and a cut-flower garden. Among the plantings that can be seen are botanically important species of the tropics, such as varieties of palms, heliconias, ki (ti) and gingers. Allerton Garden also features miniature waterfalls, small pools and fountains. It is a masterpiece of landscape design and a natural showcase for tropical plants. </p>
