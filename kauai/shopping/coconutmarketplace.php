                <h2 class="small">Coconut Marketplace, Kauai</h2>
                <p>The place for distinctly Hawaiian gifts, arts, and clothes. With over 40 stores, you are sure to find what you are looking for in this open-air market, plus a bit of fun, such as free hula show performances every Wednesday. There are many casual, island-style clothing shops like Crazy Shirts Hawaii, Del Sol Hawaii, Kaua'i Casuals, Kilohana Clothing Co., Nakoa Surf Co., Overboard Clothing, and Sgt. Leisure Hawaii. </p>
                                <? include ("../../theme/text.html") ?>
                                <p>You are also bound to find gifts for everyone with the variety of gifts and specialty shops such as Auntie Lynda's Treasures, By The Sea, Hawaiian Music Store, Island Fever, Kaua'i Shades and Hats, and Teak &amp; Treasure. Art and jewelry shops include Grande's Gems &amp; Gifts, Hot Flashes!, Island Jewels, Pearls, Perles, Purles, and Ship Store Gallery.</p>
                <p>When you get hungry visit one of the many restaurants: Aloha Kaua'i Pizza, Harley's Tropical Barbecue, Kaua'i Hula Girl Bar &amp; Grill,TC's Island Grill or The Fish Hut. Try Lappert's Hawaii for ice cream and coffee. You will find free entertainment every night as well as Hollywood Theaters Cinema running first-run films. </p>
            
