                <h2 class="small">Hanalei Center, Kauai</h2>
                <p>The Hanalei Center on Kauai's north shore is not like the usual modern malls or shopping centers. It is spread across a few historical buildings (including the Old Hanalei School) that have been refurbished and turned into boutiques and restaurants. This architectural design gives Hanalei Center its quaint and unique appeal. You'll find  an eclectic combination of boutiques, art galleries, specialty shops, apparel stores and restaurants. Most shops here are locally owned and sell Hawaiian items. </p>
                                <? include ("../../theme/text.html") ?>
                                <p> For snorkeling gear, surfboards, surf wear and boogie boards visit the Hanalei Surf Shop. It has all the gear you need for the beach. Some gear, such as surfing apparel, can even be rented. The Hanalei Center also houses the Yellowfish Trading Company. This shop sells 1940s and 1950s memorabilia as well as antiques and home furnishings. If vintage doesn't appeal to you, check out Sand People's home furnishings. For casual island wear, go to Hula Beach Clothing, but if you're looking for clothes specifically fitted for keikis (children), head to Rainbow Ducks Toys and Clothing. Tropical Clothing sells clothes exclusively for women. </p>
                <p>If you're a fitness buff, head to the newly added two-story modern building of the Hanalei Center and join a fitness class at the Hanalei Yoga studio. Shop for your health foods at Papayas, which is also located in the new building. </p>
                <p>Shoppers have a wide choice of snack bars and restaurants at the Hanalei Center - from coffee (Java Kai) to sandwiches (Hanalei Gourmet) to burgers (Bubba&rsquo;s Burgers). What is Hawaii without shaved ice? Get it at Shave Ice Paradise.</p>
