                <h2 class="small">Hauola City of Refuge, Kauai</h2>
                <p>Located at the mouth of the Wailua River, right next to the Hikinaakala Heiau, Hauola City of Refuge (<a href="../../kauai/ancientsites/more_hauolacity_photos.html" onclick="NewWindow(this.href,'name','1050','600','yes');return false">see more photos</a>) was a pu'uhonua (place of refuge) in ancient Hawaii. It was a sanctuary for kapu (taboo) breakers. It was forbidden, for example, to walk in the shadow of an ali'i (royalty). Breaking a kapu was punishable by death. But if one made it to a pu'uhonua such as this one, one could find refuge and was ultimately able to escape prosecution and resume one's life. A place of refuge also served as a sanctuary during times of war.</p>
