                <h2 class="small">Menehune (Alekoko) Fishpond, Kauai</h2>
                <p>This fishpond is said to have been built in just one night by the menehune, the mythical little people of Kauai. The menehune were master craftsmen who could accomplish amazing deeds in very little time. They used to live in the island's forests and hid from humans, so during one night they came out and built the fishpond. They did this by lining up from the village of Makaweli, 25 miles (40 km) away, passing stones hand-by-hand. </p>
                              <? include ("../../theme/text.html") ?>
                <p>The fishpond is located next to the Hulei'a Stream. A lava rock wall between the pond and the stream is 900 feet (274 m) long and 5 feet (1.5 m) high, which is amazing considering the fact that archaeologists estimate that the fishpond is around 1,000 years old. It was placed on the National Register of Historic Places in 1973.<br />
                  <br />
                  In ancient Hawaii, fishponds like this one were built to catch fish. The Alekoko Fishpond illustrates this type of ancient Hawaiian aquaculture very well. It is located just a short drive from downtown Lihu'e. Today, the fishpond is no longer in use. It is situated on private land, but there is a lookout point on Hulemalu Road.</p>
