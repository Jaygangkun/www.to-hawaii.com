              <h2 class="small">Holoholoku Heiau, Kauai</h2>
              <p>It is believed that Holoholoku Heiau was used for human  sacrifice in ancient Hawaii, mainly prisoners of war. Others who were killed  here were those who had broken a kapu (who had done something that was  forbidden, such as walking in the shadow of a chief). Holo-holo-ku means (run  run stand fast) in the Hawaiian language. Curiously enough, the heiau is  located right next to the Royal Birthstone (Pohaku Ho'ohanau), the sacred site of  royal births on the island.</p>
                            <? include ("../../theme/text.html") ?>
              <p>In later years the heiau was vandalized. The wife of  Kaumuali'i, the last king of Kauai, then used it as an enclosure to raise pigs.  She chose the heiau as a sign that the old religious Hawaiian traditions should  be forsaken in favor of Christianity. Today, only the stone foundation remains  of the original construction.</p>
