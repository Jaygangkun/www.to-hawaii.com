                <h2 class="small">Hikinaakala Heiau, Kauai</h2>
                <p>Hikinaakala means the "rising of the sun" in the Hawaiian language. Since it is located on Kauai's east shore, it offers a nice view of the sunrise. In ancient Hawaii, the dawn was celebrated here with chants and prayers. </p>
                
                <p>Located at the northern end of Lydgate Beach Park next to the mouth of the Wailua River, Hikinaakala Heiau encompasses about one acre of land. The heiau's walls are believed to once have been 6 feet (1.8 m) high and up to 11 feet (3.4 m) wide. However, in the late 1800s and early 1900, after the traditional Hawaiian religion was abolished, many of the rocks were taken away or used in roadbeds. Today, only the heiau's foundation stones remain.</p>
                                <? include ("../../theme/text.html") ?>
                <p>The exact date when the heiau was built is unknown, but it is believed that it was as early as the 1300s. The heiau's large size suggests that it used to be an important site. A row of wooden ki'i (images), known as ka pae ki'i o Wailua, used to stand outside the heiau's stone walls facing the rivermouth. These ki'i statues watched over the site and were said to sway and tilt by the waters of the ocean.</p>
