                <h2 class="small">Pohaku Ho'ohanau (Royal Birthstones), Kauai</h2>
                <p>This is an ancient and sacred Hawaiian site. In the old times it was important that all of Kauai&rsquo;s kings were born here. There is a stone where the pregnant woman was leaning against with her back and another stone where she put her legs while giving birth. Within the stone enclosure once stood a grass shack, where the mother-to-be stayed prior to giving birth. </p>
                <p>The flat stone in the front was used to cover a sacrificed dog, signifying to commoners that for them the place was kapu (forbidden). After birth, the baby&rsquo;s umbilical cord was placed in a crack in the rock wall. If the cord was taken away by a rat, then that was a bad sign, indicating that the child would later become a thief. If no rat showed up, it was a good sign.</p>
