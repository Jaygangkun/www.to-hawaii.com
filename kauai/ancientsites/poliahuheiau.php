                <h2 class="small">Poli'ahu Heiau, Kauai</h2>
                <p>Poli'ahu Heiau is located within the Wailua River State Park, near the Opaeka'a Falls lookout point. It is one of seven heiau along the Wailua River. The age of the heiau is unknown, though it is believed that it was built by the menehune, the legendary little people of Hawaii. It is believed that the heiau was in use during the 1600s and 1700s, but it may be older than that. </p>
                              <? include ("../../theme/text.html") ?>
                <p>The heiau encompasses a little over an acre. Its rock wall was constructed by interlocking the stacked rocks without use of mortar. The outside wall is 5 feet (1.5 m) high and 5 feet (1.5 m) wide. The rocks had to be brought up here from the river valley below. A heiau of this size was usually built under the direction of an ali'i nui (high ruling chief), who would first consult with his kahuna (priests). The actual building of the heiau was done by the maka'ainana (commoners).</p>
                <p>Since Poli'ahu is a rather large and complex heiau, it has been suggested that it was a luakini heiau, a heiau dedicated to the war god Ku. Luakini heiaus could involve human sacrifice as well as offerings of fish and other animals.</p>
