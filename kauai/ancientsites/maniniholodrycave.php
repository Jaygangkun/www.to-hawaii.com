                <h2 class="small">Maniniholo Dry Cave, Kauai</h2>
                <p>Located across from Ha'ena Beach Park on Kauai's north shore, this dry cave is easily accessible as it is located right on the main highway. It is at the bottom of a steep cliff and is about 300 yards (274 m) deep. Its high roof lowers as you walk further to the back of it, where you'll also see a small opening. The interior of the cave used to be larger in the past, but when a tsunami hit the island in 1957, the waves carried sand into the cave and partially filled it.</p>
                <p>The cave is named after Maniniholo, the legendary head fisherman of the menehune, Kauai's mythical little people. The menehune fishermen used to catch many fish at Ha'ena. They left some of their catch right here under the cliff and the rest they carried to the menehune in the island's interior. However, when they returned, they noticed that an akua (evil spirit) took the fish they had left there. So to catch the akua, they started digging the cave that you see today. </p>
