                <h2 class="small">Papa'a Bay, Kauai</h2>
                <p>If you have seen the action adventure movie <em>Six Days, Seven Nights</em> starring Harrison Ford and Anne Heche, you may recognize this beach, since it was the setting for the plane crash scene. Movie producer Peter Guber found Papa'a Bay so picturesque that he purchased all 174 acres surrounding the bay and built a large luxury oceanfront estate, which he called <em>Tara</em>.</p>
                              <? include ("../../theme/text.html") ?>
                <p>In November 2009, the property was sold for a record sum of $28 million, the highest price ever paid for an existing home on Kauai. The estate had been listed for sale since 2004 with an asking price of $46 million.</p>
                <p>There have been access issues to Papa'a Bay in the past, and even a few arrests were made because people were allegedly trespassing through private property. The existing public access trail to the bay involves a 5-10 minute hike across a somewhat steep hill and through the brush and about 100 yards of boulder-hopping. </p>
                <h2 class="big">Papa'a Bay Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Picturesque and secluded bay with a beautiful lagoon - accessible via a somewhat steep trail (5-10 minute hike)</li>
                  <li>Shallow reefs fringes the crescent-shaped beach</li>
                  <li>Plane crash scene of movie <em>Six days, Seven Nights</em> was filmed here</li></ul>
