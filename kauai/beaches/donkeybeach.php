                <h2 class="small">Donkey Beach, Kauai</h2>
                <p>Some years back, Donkey Beach on Kauai's east shore was considered one of the best nudist or clothing optional beaches. But even then, the beach wasn't great for swimming, but it was popular among sunbathers who wanted it au naturel. The secluded location of this beach made it a perfect spot for nudist  visitors. To get to it, one had to take a hike across a sugarcane field. Earlier owners permitted people to cut through the field to the beach. </p>
                              <? include ("../../theme/text.html") ?>
                <p>As Donkey Beach's fame went far and wide and the sugarcane field got new owners who happened to be land developers and who didn't want to displease their high-end clients, anti-nudity laws were upped. </p>
                <p>Today, Donkey Beach continues to be visited as a good beach for sunbathing, and a convenient access walkway has been built. It is not a good beach for swimming or snorkeling since the water is choppy and rough, but when the conditions are good, the beach attracts local surfers and bodyboarders. Donkey Beach is also no longer as secluded as it had been years ago. </p>
                <p>The beach has been referred to as Donkey Beach for a long time. Most people don't know that its official name is Paliku. It was nicknamed Donkey Beach after the donkeys and mules that were herded in a pasture located next to the beach many years ago. The donkeys were owned by the Lihue Plantation Company and were used to tow bundles of seed cane. </p>
