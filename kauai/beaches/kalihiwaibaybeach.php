                <h2 class="small">Kalihiwai Bay Beach, Kauai</h2>
                <p>Kalihiwai Beach  is located at the head of Kalihiwai Bay about 1.5 miles (2.4 km) from the town of Kilauea on Kauai's north shore. The beach is enclosed by lava cliffs on each side and backed by  ironwood trees that provide shade. The only time you can safely swim here is in summer if the ocean is calm. An alternative option is to take a dip instead in the river that crosses the beach and flows into the ocean. If you want to snorkel, try the far eastern end of the bay. However, don't expect to see as many corals and fish as you would in prime snorkeling spots such as <a href="../beaches/tunnelsbeach.php">Tunnels</a> and <a href="../beaches/kee-beach-park.php">Ke'e Beach</a>. </p>
                              <? include ("../../theme/text.html") ?>
                <p>The bay has a shallow, underwater rock shelf that extends from the cliffs, which provides one of the north shore's most popular surf breaks. Surfers gather along the rocky base of Kapukaamoi Point, the eastern point of the bay and ride to shore very close to the sea cliff. The waves in the winter months are very steep and break fast and are only something for experts. This is also when the water conditions are very dangerous due to the pounding shorebreak, powerful backwash and strong rip currents. During the calmer summer months, the beach is a good spot for bodyboarding, beginner surfing and shoreline fishing. </p>
                <h2 class="big">Kalihiwai Bay Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Swimming only possible in summer if the ocean is very calm, otherwise too dangerous</li>
                  <li>Good for bodyboarding and beginning surfers in the summer</li>
                  <li>Popular surfing spot in the winter (high and dangerous waves)</li>
                  <li>Beach is backed by ironwood trees</li></ul>
