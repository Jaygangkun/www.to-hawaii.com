                <h2 class="small">Kalalau Beach, Kauai</h2>
                <p>Kalalau Beach (<a href="../../kauai/beaches/more_kalalaubeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) lies at the end of the 11-mile (17.7 km) <a href="../attractions/kalalautrail.php">Kalalau Trail</a> on Kauai's north shore. The trail is one of the most difficult in the Hawaiian Islands, which limits visitors to the beach. This remote wilderness area is accessible on foot (or by boat) only. The trail begins right after <a href="../beaches/kee-beach-park.php">Ke'e Beach</a> and has extreme inclines and declines and narrow footpaths, especially between the dreaded 6.5 to 8 miles (10.5 to 12.9 km) into it. Not only is the trail narrow in some places, but it can also be slippery because of loose rocks or if it has recently rained. So if you're afraid of heights, you may not like this trail, especially around the middle of it.</p>
                              <? include ("../../theme/text.html") ?>
                <p>Kalalau Beach is about 1 mile (1.6 km) long. From here, trails lead through the rest of the valley. Camping is allowed on the beach, but permits are required. Since the trail is quite long, most people who visit Kalalau Beach plan to stay overnight. There are no facilities on the beach, except for composting toilets. You need to bring all food and equipment with you. A waterfall at the western end of the beach (Ho'ole'a Falls) provides freshwater, but before drinking it, the water must be purified (bring a water filter or water treatment tablets) due to the threat of <a href="../../hikingtips.php">leptospirosis</a>, a bacterial disease.</p>
                <p>Swimming is possible on Kalalau Beach, but not without dangers. Because the beach lacks a protective offshore reef to block incoming waves, high surf is common, especially during the winter months. Also, there are strong rip and alongshore currents, which may be difficult to spot, even if the ocean appears calm. The sandbar is quite shallow here and in some spots the ocean bottom drops off quickly to overhead depths. Since there are no lifeguards here and drownings have happened in the past, it is recommended to avoid swimming at Kalalau.</p>
                <p>A few people have made Kalalau Valley their permanent home, even though it is not legal. They live here because the valley is difficult to access and offers solitude and isolation. Some of these year-round residents hike out of the valley every couple of months to get supplies. Other inhabitants of the valley include feral goats. To prevent overpopulation, the Department of Land and Natural Resources (DLNR) allows hunting on weekends in August and September.</p>
                <p>For some people who come to Kalalau Beach, the long and difficult trail is not adventurous enough. Equipped with fins, they enter the water at Kalalau and swim over to the even more isolated <a href="honopubeach.php">Honopu Beach</a>, which can only be accessed this way. </p>
                <p>To get a camping permit, visit http://www.hawaiistateparks.org/camping/permit_napali.cfm . 
                  Apply for your permit about 6-12 months in advance. Permits are very popular and sell out well in advance, especially during summer.</p>
