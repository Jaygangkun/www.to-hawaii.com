                  <li>Secluded and difficult to access beach on Kauai's Na Pali Coast</li>
                  <li> You should be in good physical condition to hike to the beach and not afraid of heights (the trail is narrow in some areas and involves several stream crossings)</li>
                  <li> Beware of flash floods when crossing streams (don't cross if the water is too high)</li>
