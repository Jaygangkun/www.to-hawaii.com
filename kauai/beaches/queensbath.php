                <h2 class="small">Queen's Bath, Kauai</h2>
                <p>This is not your typical sandy beach. In fact, there is no sand here at all - just rocks. Located in Princeville on Kauai's north shore, Queen's Bath (<a href="../../kauai/beaches/more_queensbath_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a natural pool surrounded by a lava shelf. There is an inlet where fresh saltwater comes in from the ocean, together with all kinds of fish. </p>
                              <? include ("../../theme/text.html") ?>
                <p>When the surf is up, which is often times the case, the lava pool and surrounding area are extremely  dangerous because the waves wash over the lava shelf. The surf doesn't even have to be high to make this area dangerous. In the past, people have been swept into the ocean who didn't even enter the water, but who were merely walking on the boulders near the water's edge.  Sudden large waves  can pose a danger even on calm ocean days. Even if you observe the ocean for a while and it appears calm, a sudden large wave  can wash over the cliff and drag you into the ocean. </p>
                <p>For this reason, numerous warning signs have been posted in this area, which all say the same thing: &quot;Waves break on ledge. Stand back! You could be swept out to sea, injured and drown.&quot; Many people don't heed the warning sings and walk near the edge of the cliff to enjoy the views. Unfortunately, more than 30 drownings have occurred here as of today, the most recent one in February 2016.</p>
                <h3 class="big">Queen's Bath Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Natural pool located on a lava bench</li>
                  <li>Area is exposed to strong currents, high surf and waves that wash over the cliffs</li>
                  <li>Numerous warning signs have been posted in the area, but nevertheless many people have gotten hurt or killed here</li></ul>
