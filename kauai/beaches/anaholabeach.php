                <h2 class="small">Anahola Beach Park, Kauai</h2>
                <p>Anahola Beach Park, located within Anahola Bay on Kauai's east shore, has a nice and long white-sand beach, perfect for a beach stroll. Swimming is most protected at the right (east) end of the bay. The center of the beach at the river mouth has occasional rip currents, so swimming is not recommended here. The beach is backed by ironwood trees, so there's plenty of shade. At the south end of the bay camping is allowed, but a permit is required. Read more about camping on Kauai <a href="../../kauai/camping">here</a>. </p>
