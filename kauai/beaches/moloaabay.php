                <h2 class="small">Moloa'a Bay, Kauai</h2>
                <p>Moloa'a Bay (<a href="../../kauai/beaches/more_moloaabay_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is one of the lesser-known beaches on Kauai, located on the island's northeastern shore. It is a pretty beach, framed by hilly dunes and green vegetation. When the ocean is calm, swimming is possible and best on the right (southeast) side of the bay. </p>
                <p>The currents in this area can be strong and since there is a lot of coral reef on the ocean bottom of the bay, it is best to enter the water with reef shoes. From the bay, a hiking trail leads along the coast toward nearby <a href="../beaches/larsensbeach.php">Larsen's Beach</a> further north. </p>
                <p>Parking is very limited at Moloa'a Bay, especially on weekends. You may have to park a little further away from the beach and walk the rest to it.</p>
