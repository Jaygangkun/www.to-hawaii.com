                <h2 class="small">Anini Beach Park, Kauai</h2>
                <p>Anini Beach (<a href="../../kauai/beaches/more_aninibeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>)  is sheltered by one of the longest and widest reefs in Hawaii measuring 2 miles (3.2 km) in length and 1,600 feet (488 m) at its widest point. The fringing reef protects the beach from high surf and currents, so the waters here are the calmest you'll find on Kauai's north shore. </p>
                              <? include ("../../theme/text.html") ?>
                <p>The shallow nearshore ocean bottom makes Anini a great beach to visit if you have kids. Anini Beach Park is also a snorkeler's delight - here you may spot Hawaii's state fish, the humuhumunukunukuapua'a (triggerfish) and uhu (parrotfish), kala (unicornfish) and other tropical fish. On the downside, there are some sharp corals and rocks on the nearshore ocean bottom, so take extra care when wading in. </p>
                <p>The area's consistent breeze makes for good windsurfing conditions, and it's a good spot for beginners. At the western end of the beach (near where the road ends) there is a channel where the currents can be strong, especially when the surf is up, so it's not a good idea to swim here.</p>
                <p>Camping is allowed at this beach park with a permit. Read more about camping on Kauai <a href="../../kauai/camping">here</a>.</p>
                <h3 class="big">Anini Beach Park Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Calmest waters on Kauai's north shore, ideal for novice swimmers</li>
                  <li>Shallow nearshore ocean bottom - protected by a long fringing reef</li>
                  <li>Good snorkeling conditions</li>
                  <li>Kite and windsurfing schools nearby</li>
                  <li>Many luxury homes and vacation rentals in the area</li>
                  <li>Camping allowed with a permit</li></ul>
