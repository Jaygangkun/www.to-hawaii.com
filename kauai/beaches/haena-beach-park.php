                <h2 class="small">Ha'ena Beach Park, Kauai</h2>
                <p>This beach on Kauai's north shore is also known as Maniniholo Beach, named after the large schools of convict fish (manini fish) that feed on the coral here. Vegetated sand dunes make up the backshore of Ha'ena Beach. </p>
                
                <p>Offshore are two large reefs, the Makua Reef to the east and the Hauwa Reef to the west. But the beach itself is not protected by a reef; it is fully exposed to the open ocean and high surf, so swimming is hazardous except when the ocean is very calm. When the surf is up (even if just a little)  there are strong currents and the waves are quite powerful here. This is when entering the ocean is not a good idea as  drownings and near drownings have happened here.</p>
                              <? include ("../../theme/text.html") ?>
                <p>Camping is allowed at Ha'ena Beach Park, but a permit is required. More info about how to get a camping permit is <a href="../../kauai/camping">here</a>. Right across from Ha'ena Beach Park is the <a href="../ancientsites/maniniholodrycave.php">Maniniholo Dry Cave</a>.</p>
