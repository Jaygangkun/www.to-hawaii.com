                <h2 class="small">Tunnels Beach (Makua Beach), Kauai</h2>
                <p>Tunnels Beach (aka Makua Beach) located at Ha'ena Point on Kauai's north shore is a postcard-perfect stretch of golden sand backed by ironwood trees, tropical palms and lush mountains. Even though much of the shoreline is lined by beachrock, which is usually not favored by swimmers, Tunnels Beach is nevertheless one of the north shore's most popular beaches. </p>
                                <? include ("../../theme/text.html") ?>
                <p>One of the main attractions is the large reef offshore. The beach is unique for having an inner and an outer reef. About an eighth of a mile offshore is a half-moon shaped reef. This is the inner reef and it is shallow. This area is ideal for novice snorkelers, while advanced divers and snorkelers explore the outer reef, which has more lava tubes, arches and coral formations. During the winter months or times of high surf, the water conditions are hazardous at Tunnels Beach and swimming and snorkeling are  dangerous.</p>
                <p>A popular surf break called Tunnels is located at the outer edge of the reef, which is visited by both surfers and windsurfers. It is for experts only. A wide channel lies between the two reefs. Tunnels Beach got its name because parts of the reef here have many arches and tunnels. </p>
                <h3 class="big">Tunnels Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Good beach for snorkeling, diving, surfing and windsurfing</li>
                  <li>Has an inner reef and an outer reef</li>
                  <li>Dangerous swimming  conditions in the winter months due to strong currents and high surf</li></ul>
