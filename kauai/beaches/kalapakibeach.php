                <h2 class="small">Kalapaki Beach, Kauai</h2>
                <p>Kalapaki Beach (<a href="../../kauai/beaches/more_kalapakibeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>), located in Lihue at the entrance of Nawiliwili Harbor, is one of Kauai's most popular beaches. It offers great swimming conditions as the ocean bottom is sandy and gently sloping. When the conditions are right, Kalapaki is also a good spot for beginning bodyboarders and surfers. The waves here are mostly gentle and roll across a shallow sandbar. When the surf is up (which occurs most often in winter), strong rip currents can form. </p>
                <p>The beach is backed by shops and restaurants as well as the Kauai Marriott Resort and a paved walkway with a grassy strip that is a beautiful contrast against the golden sand. Located east of Kalapaki Beach is an area with 40 acres of freshwater lagoons (known as Kauai Lagoon) and the Kauai Lagoon Golf Club. The Kauai Lagoon area is a wildlife sanctuary. </p>
