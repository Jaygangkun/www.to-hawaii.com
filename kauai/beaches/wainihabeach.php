                <h2 class="small">Wainiha Beach Park, Kauai</h2>
                <p>Wainiha means &quot;unfriendly water&quot; in the Hawaiian language. Wainiha Beach is  one of the few Kauai beaches that is off limits to swimming at all times of the year. The beach has no reef to shelter it from large ocean swells. Anyone who dives into the ocean here has to deal with dangerous rip currents, a powerful backwash and strong shorebreak. A few drownings and near-drownings have happened here in the past.</p>
                <p>Wainiha River flows into the ocean at the  eastern end of the beach. The nearshore water tends to be murky  from the silt deposited by the river. When the conditions are right, surfers can be seen on the river side of the bay riding a break.</p>
