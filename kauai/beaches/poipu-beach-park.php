                <h2 class="small">Po'ipu Beach Park, Kauai</h2>
                <p>In 2001, the renowned beachologist Dr. Stephen &ldquo;Dr. Beach&rdquo; Leatherman, picked Po'ipu Beach Park (<a href="../../kauai/beaches/more_poipubeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) as the best beach in the United States. Travel Channel also named Po'ipu Beach Park as one of America's and the World's Best Beaches. As Kauai's most popular beach, it attracts many beachgoers, yet it is never crowded in the way popular Waikiki Beach on the neighboring island of Oahu is. </p>
                              <? include ("../../theme/text.html") ?>
                <p>Po'ipu Beach Park is a family beach and accommodates nearly every type of beach activity. Two beaches actually make up Po'ipu Beach. The eastern side is called Baby Beach because it's where keikis (kids) can swim free from dangers of big waves and strong currents. The water here is just a few inches deep and is sheltered by a jetty. On the western side of the beach, the waves break on the reef. This is the spot frequented by surfers and is also good for fishing, snorkeling, pole fishing and windsurfing. </p>
                <p>Nukumoi Point, a  tombolo, separates the two bays. A tombolo is a narrow piece of land, such as a spit or a bar, extending from a shore to an island. Po'ipu Beach Park's tombolo is a favorite spot of Hawaiian monk seals; they are often found napping here in the afternoon. The area just before the tombolo, on the western side of the beach, is frequented by beginning bodyboarders. Waves here break gently on a shallow sandbar.</p>
