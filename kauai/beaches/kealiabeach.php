                <h2 class="small">Kealia Beach, Kauai</h2>
                <p>Kealia Beach (<a href="../../kauai/beaches/more_kealiabeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a 150-foot (46 m) wide and 0.5-mile (805 m) long sandy beach located on Kauai's Coconut Coast (east shore). Kealia means &quot;salt bed&quot; or &quot;salt-encrusted area,&quot; a name referring to the traditional Hawaiian method of collecting and drying salt. Kealia Beach has been a site of salt mining for centuries. It is a former plantation town with a sugar mill and a train depot.</p>
                              <? include ("../../theme/text.html") ?>
                <p>As with most Kauai beaches, the ocean conditions at Kealia Beach depend on the season. In winter, dangerous waves and strong currents characterize the beach. When the surf is up, swimming is not possible here. Kealia Beach has no reef to protect it from high swells. The beach's long sandbar often creates waves that attract surfers and bodyboarders. Surfers  usually gather at the northern end of the beach where the waves are better. </p>
                <p>The northern part of the beach was once used by interisland steam ships that brought supplies for plantations. The remains of the jetty, formerly called Kealia Landing, is still visible. This area is  best for swimmers or novice bodyboarders because a breakwater makes for slightly more protected waters. </p>
                <h3 class="big">Kealia Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Good bodyboarding and surfing conditions</li>
                  <li>Limited swimming, especially during times of high surf</li>
                  <li>Paved walkway runs along the beach - nice for a beach stroll</li>
                  <li>Popular among local surfers and bodyboarders</li>
                  <li>Easy beach access, spacious parking lot right next to highway</li>
                  <li>Former plantation town and salt mining location</li></ul>
