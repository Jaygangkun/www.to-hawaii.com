                <h2 class="small">Kapa'a Beach Park, Kauai</h2>
                <p>This beach park is located in Kapa'a on Kauai's east shore.  The scenery here is not as picturesque as on many other Kauai beaches. The  beach park is mainly used by local residents as well as guests who stay at the  nearby condo hotels. The nearshore ocean bottom has a few sandy areas where  swimming is possible when the ocean is calm. Snorkeling is best at the northern part of the beach. On a windy day the beach is frequented by kite surfers.</p>
