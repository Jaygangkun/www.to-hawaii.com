              <h2 class="small">Kahili Quarry Beach, Kauai</h2>
              <p>Kahili Quarry Beach is located where Kilauea Stream empties into the Pacific Ocean on Kauai's north shore. This small beach is bordered by a densely vegetated hill and a rock quarry, which is why this beach is known as Quarry Beach or Rock Quarry Beach. The rocky quarry, which overlooks the bay, was used during the plantation days and has long been abandoned. </p>
              <p>Kahili Quarry Beach has a large pool where the Kilauea River flows into the ocean. When the ocean is calm, the pool is great for swimming. Most days, though, the nearshore water turns murky from the silt and mud carried by the Kilauea River. The beach doesn't have a reef barrier to protect it from high ocean waves. When the surf is up, it's not a good idea to swim or snorkel here. During high surf season the beach attracts surfers and bodyboarders.</p>
