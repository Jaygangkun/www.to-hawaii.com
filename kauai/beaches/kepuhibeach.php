                <h2 class="small">Kepuhi Beach, Kauai</h2>
                <p>Kepuhi Beach is a lesser-known beach on Kauai's north shore. On most days you will hardly find any people here. The reason is that the beach doesn't offer good swimming conditions, and snorkeling is only possible when the ocean is calm and even then it's not as good here as at neighboring <a href="../beaches/tunnelsbeach.php">Tunnels Beach</a>. But the views are pretty and it's a good spot for a picnic with not many other people around. Beach walks are also nice here. </p>
