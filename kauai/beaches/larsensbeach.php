                <h2 class="small">Larsen's Beach, Kauai</h2>
                <p>Larsen's Beach is located in a secluded area on Kauai's northeastern shore. There is no road that leads all the way to the beach, and the one that leads near it is a dirt road, which limits visitors. The beach has a nice long stretch of sand and is backed by brush and trees. </p>
                              <? include ("../../theme/text.html") ?>
                <p>Near the middle of the beach is a lava rock that protrudes into the ocean, known as Pakala Point, which cuts the beach in half. Right before this point is Pakala Channel, an opening in the reef through which the nearshore water flows back into the ocean. Don't go swimming in or near this channel. The rip current here is very strong and you don't want to find yourself caught in it. </p>
                <p>Swimming in general on Larsen's Beach is less than ideal. The nearshore ocean bottom is rocky and too shallow. All that's possible here is snorkeling, but not anywhere near Pakala Channel and only during calm ocean conditions and if you're an experienced snorkeler. The currents here are too unpredictable, and there are no lifeguards.</p>
                <p>Larsen's Beach is also known as Ka'aka'aniu Beach. It was named Larsen after a former manager at Kilauea Plantation named L. David Larsen. There have been issues with access to Larsen's Beach in the past. The relatively easy, gently sloping trail to the beach cuts through private property that is owned by Waioli Corp. and leased by Paradise Ranch. A fence was put up to prevent people from trespassing. An alternative route is a much steeper trail, which some people regard as challenging and unsafe. </p>
                <h2 class="big">Larsen's Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Secluded beach - accessible via an unpaved road and a short hike</li>
                  <li>Long stretch of sand, divided by a lava rock outcropping known as Pakala Point</li>
                  <li>Swimming here is unsafe due to strong currents, especially in or near Pakala Channel</li>
                  <li>Recent controversy about access to the beach - traditional trail cuts through private property</li>
                  <li>Some local families come here to harvest limu (edible seaweed) on the reef</li></ul>
