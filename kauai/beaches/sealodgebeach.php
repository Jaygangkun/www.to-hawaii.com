                <h2 class="small">SeaLodge Beach, Kauai</h2>
                <p>Located in Princeville on Kauai's north shore, SeaLodge Beach is a hidden gem. It is a small pocket of sand, surrounded by lush vegetation. Most of the time, the beach is empty because not many people know it's there. The reason is mainly access issues. If you're not a guest at SeaLodge Resort, there is basically no way that you can park anywhere near the trail to the beach. Even in the adjacent neighborhood, street parking is not allowed. They will tow your car.</p>
                <p>                  All parking is reserved for hotel guests. No public parking is available (we specifically called SeaLodge Resort and they confirmed this). So if you really want to visit this beach, your only options are to either take a taxi here, to park at the shopping center in Princeville and walk here (about an hour walk), or to stay at the SeaLodge Resort.</p>
                                <? include ("../../theme/text.html") ?>
                <p>There is a reef on the nearshore ocean bottom, so it's more of a snorkeling beach rather than a swimming beach. Turtles also like this area. When the surf is up, currents often times form and the waves can be too dangerous and reach all the way to the base of the cliff. To get to the beach, you'll have to hike to it (takes about 10-15 minutes) from Building A at the SeaLodge Resort. The trail itself is scenic too and well worth it.</p>
                <h3 class="big">SeaLodge Beach Overview</h3>
                <ul type="disc" class="mylist">
