                <h2 class="small">Hideaways Beach (Pali Ke Kua Beach), Kauai</h2>
                <p>Kauai has a number of secret gems and the appropriately named Hideaways Beach (<a href="../../kauai/beaches/more_hideawaysbeach_photos.html" onclick="NewWindow(this.href,'name','850','500','yes');return false">see  more photos</a>) is one of them. It can be accessed only after a difficult climb down a very steep path. You'll have to hold on to the metal poles and ropes that have been put there as a security measure. The trail is not suitable for people who are afraid of heights (next to the trail the cliff drops off sharply) or for small children or for people with physical limitations. </p>
                              <? include ("../../theme/text.html") ?>
                <p>There are actually two access trails to the hidden beach - the concrete pathway that leads down the cliff, which is reserved for guests of the Pali Ke Kua condominiums and which leads to the east section of the beach, and the public right-of-way trail, which is much more difficult and leads to the west section of the beach. The first section of the public trail  has steep steps with a railing and after that the trail is a dirt path, which can be slippery and dangerous when wet. It takes about 10 minutes to walk (or better said, to climb) down to the beach. Also, watch out for mosquitoes. There are plenty of them on the trail.</p>
                <p>Hideaways Beach is crescent shaped and the large, grainy sand is a golden yellow where sea turtles can often be seen. Trees provide plenty of shade. Just 300 feet (90 m) long, the beach provides an intimate and secret feel. For those who know its beauty, the trek down to the beach is definitely worth it. </p>
                <p>Swimming isn't the best here because of the rocky ocean bottom, but the snorkeling conditions are good when the waters are calm. When the surf is up, the water can be murky and rip currents  can form. Winter swells also tend to swallow up the narrow shoreline. There are no facilities at the beach. </p>
                <h2 class="big">Hideaways Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Secluded sandy and narrow beach on the north shore of Kauai, near the Pali Ke Kua Condominiums in Princeville</li>
                  <li>Not recommended for small kids and people with mobility problems (very steep trail down the cliff)</li>
                  <li>Two beaches separated by a rocky outcropping</li>
                  <li>Great for snorkeling when the ocean is calm</li>
                  <li>Hazardous currents and shorebreak when the surf is up</li>
                  <li>Slippery rocks and sharp coral on nearshore ocean bottom</li>
                  <li>False kamani trees provide plenty of shade on the beach</li>
                  <li>Small public parking lot is located at the trailhead that leads to the beach (in the beginning, the trail is a narrow walkway with a metal fence on both sides of it, which is located next to tennis courts)</li></ul>
