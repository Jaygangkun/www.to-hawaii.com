                <h2 class="small">Lucy Wright Beach Park, Kauai</h2>
                <p>Lucy Wright Beach Park is a historic beach. It was here where Captain James Cook first set foot in the Hawaiian Islands in 1778. Curiously enough, the beach isn't called Captain Cook beach, but named after Lucy Wright, who was an active member in the Waimea community and the first native Hawaiian schoolteacher. She passed away in 1931. The beach was named in her honor.</p>
                             <? include ("../../theme/text.html") ?> 
                <p>                  Because the beach is located near the mouth of the Waimea River, the water is not as clear as on many other Kauai beaches. Instead of swimming, the best thing to do here is watch the river water flow into the ocean. </p>
                <p>                  The beach sand is a mixture of white, black and green sand. The green comes from a semi-precious gemstone called olivine, which originates from the river's lava bed. The black is lava rock from Waimea Canyon.</p>
                <p>Camping is allowed in the park, but you need to get a county permit first. More info about how to get a permit is <a href="../../kauai/camping">here</a>. However, the beach park also has a small homeless encampment and the beach doesn't have much shade. When it comes to camping and picnicking, there are nicer beach parks on Kauai.</p>
