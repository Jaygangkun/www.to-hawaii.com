                <h2 class="small">Glass Beach, Kauai</h2>
                <p>Sea glass is glass (e.g. from bottles, flasks, windshields, windows) that has been broken up and smoothened by moving ocean water and time. It takes the ocean about 10 to 30 years to create it, and the resulting fragment is a frosted, jelly bean-like pebble. </p>
                <p>Sea glass is found all over the world. Kauai's Glass Beach is located in the middle of an industrial area near Port Allen Harbor in Ele'ele. The shoreline at Glass Beach is covered with millions of sea glass pieces - brown, aqua-colored, clear and blue. </p>
                                              <? include ("../../theme/text.html") ?>
                <p>The glass originates from  broken bottles and auto glass dumped here years ago. It's amazing to see what the ocean did to all this junk. The one thing that could mar the ethereal effect of Glass Beach is the gas tanks overlooking one side of the beach. </p>
                <p>Glass Beach is best for beachcombing only. Swimming is not recommended because the beach is rocky. </p>
                   <h3 class="big">Glass Beach Overview</h3>
