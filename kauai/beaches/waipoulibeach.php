                <h2 class="small">Waipouli Beach, Kauai</h2>
                <p>Waipouli Beach is located near the Coconut Marketplace in Kapa'a on Kauai's east shore. It fronts the Kauai Sands Hotel, Islander on the Beach and Aston Kauai Beach at Maka'iwa. Swimming here is only possible when the ocean is calm and even then it is not that good because the ocean bottom is rocky in most areas. The surroundings are beautiful and on the southern end of the beach is a small cove where sea turtles can often be seen near the rocky outcropping. A public access trail leads from the road to this cove. There is also a paved trail along much of the rest of the beach.</p>
                                <? include ("../../theme/text.html") ?>
                <p>Further north is Waipouli Beach Park, which is not as scenic as Waipouli Beach. It is a gathering spot among local residents. There is a boat ramp and a pedestrian bridge that leads over a stream to the neighboring <a href="../beaches/kapaabeachpark.php">Kapa'a Beach Park</a>. The swimming at Waipouli Beach Park is good when the ocean is calm. A natural breakwater offshore acts as a barrier to incoming waves in one section of the beach (between Panihi Road and Makana Road). This area is a good place for novice swimmers and children because the nearshore waters are shallow. </p>
                <h3 class="big">Waipouli Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Waipouli Beach is fronted by a few hotels, is scenic, but has limited swimming options</li>
                  <li>Waipouli Beach Park (located a little further north) has an area that is proteced by a natural breakwater, where even novice swimmers can enjoy a dip if the ocean is calm (if the surf is up, water splashes over the breakwater and swimming is not recommended)</li></ul>
