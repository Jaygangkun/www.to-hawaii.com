                <h2 class="small">Shipwreck Beach (Keoneloa Beach), Kauai</h2>
                <p>Shipwreck Beach on Kauai's south shore got its name from the small wooden boat that used to lie at the water's edge for many years. It deteriorated badly over the years, was hit by storms and people stripped it for firewood. When hurricane &lsquo;Iwa hit Kauai in November 1982, it completely destroyed what little remained of the already dismantled boat. </p>
                                <? include ("../../theme/text.html") ?>
                <p>The Hawaiian name of this beach is Keoneloa, meaning &quot;the long sands.&quot; There are some ancient petroglyphs that have been carved into the beachrock shelves here. However, they are covered under sand and are only exposed during very strong southwesterly winter storms, which strip away enough sand to uncover these carvings.</p>
                <p>The most prominent feature of Shipwreck Beach is Makawehi Point, a sandstone cliff at the eastern end of the beach. Shipwreck Beach is about 0.5 miles (800 m) long and and known for its good bodyboarding and windsurfing conditions. Surfers and windsurfers enter the water at the eastern end of the beach near the cliff. During times of high surf, strong rip currents and a powerful shorebreak make swimming dangerous. </p>
                <h3 class="big">Shipwreck Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Good bodyboarding, surfing and windsurfing beach</li>
                  <li>Location of ancient rock carvings (however, petroglyphs are covered by sand)</li>
                  <li>Beach is backed by the Grand Hyatt Kauai Resort</li></ul>
