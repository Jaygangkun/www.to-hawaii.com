                <h2 class="small">Lumaha'i Beach, Kauai</h2>
                <p>Lumaha'i Beach was made famous in the 1957 movie <em>South Pacific</em>. The beach is where Mitzi Gaynor &quot;washed that man right out of her hair.&quot; Partly because of this history and partly because of its picturesque scenery, Lumahai has become one of the most photographed beaches on Kauai. Located just past Hanalei, this scenic beach  is nearly a mile long and several hundred feet wide. Vegetated lava cliffs provide a vibrant  backdrop. </p>
                          <? include ("../../theme/text.html") ?>    
                <p>While Lumaha'i Beach appears serene, there are dangers that lurk in its depths. The beach has no protective reef, making the coast totally exposed to the open ocean. Rip currents, powerful backwash, strong shorebreaks and high surf make it one of the most dangerous beaches on Kauai. On calm summer days, the Kahalahala (eastern) end of the beach may be calm enough to allow swimming. </p>
                <p>Throughout the year, the western end attracts surfers and bodyboarders, but if you're not an expert at riding Hawaiian waves, the waves are certainly not friendly. Drownings and near-drownings have occurred here in the past. The beach is more suitable for a picnic or beach stroll.</p>
