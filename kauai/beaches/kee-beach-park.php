                <h2 class="small">Ke'e Beach Park, Kauai</h2>
                <p>Ke'e (pronounced Keh-eh) means &quot;avoidance&quot; in Hawaiian. However, this beach is far from being avoided by people. In fact, Ke'e Beach Park (<a href="../../kauai/beaches/more_keebeach_photos.html" onclick="NewWindow(this.href,'name','530','600','yes');return false">see more photos</a>) is one of the most popular beaches on Kauai. It has beautiful golden sand and marks the end of Highway 560 and the beginning of the enchanting <a href="../attractions/napalicoaststatepark.php">Na Pali Coast</a>. Coconut palms, ironwood and guava trees make for a nice backdrop.</p>
                              <? include ("../../theme/text.html") ?>
                <p>This is also where the popular 11-mile (17.7 km) <a href="../attractions/kalalautrail.php">Kalalau Trail</a> begins, so many hikers and backpackers come here (the two beach parking lots are usually quite busy). Another short trail leads to the Ke Ahu o Laka hula platform and the Kaulu Paoa Heiau, a stone temple dedicated to the goddess of hula, named Laka. </p>
                <p>But those attractions are not the  only reason why Ke'e Beach is usually crowded. Nor is the reason because the beach has been featured in the popular 1983 TV series <em>The Thorn Birds</em>. People come here because of its clear, sandy and shallow lagoon and the  reef that offers great snorkeling conditions in the summer months when the ocean is usually calm. Protected by a reef running parallel to the shore the lagoon is usually calm. Below the surface, wrasses, goatfish, convictfish, butterflyfish, damselfish, surgeonfish and other reef fish swim in abundance.</p>
                <p>Swimmers should avoid the reef channel located at the west end of the beach. The channel connects the lagoon to the open ocean. The currents can be very strong here as this is where the lagoon water flows back into the ocean. Especially when the surf is up, it is dangerous to swim in this channel.</p>
                <p>Summer months are the best time to snorkel or swim at Ke'e Beach. In winter, high surf can reach right over the reef and into the lagoon, which makes the water not only murkier, but also dangerous. </p>
