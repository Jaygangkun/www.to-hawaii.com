                <h2 class="small">Hanalei Bay, Kauai</h2>
                <p>At 2 miles (3.2 km) long, Hanalei Bay (<a href="../../kauai/beaches/more_hanaleibay_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>, <a href="../../kauai/beaches/p1.html" onclick="NewWindow(this.href,'name','2550','500','yes');return false">view panorama</a>) is the largest bay on Kauai. Hanalei means &ldquo;crescent bay&rdquo; in Hawaiian. It is a long half moon of golden sand backed by  4,000-foot-high (1,219 m) green mountains. Look closely and you may be able to spot several waterfalls. The bay is so beautiful that the enamored Japanese author Haruki Murakami wrote a short story about it titled <em>Hanalei Bay</em>.</p>
                              <? include ("../../theme/text.html") ?>
                <p>There are actually four beach parks or sections within Hanalei Bay, starting from Waikoko Beach at the bay's western end to Black Pot Beach at the bay's eastern end (near the pier). Wai'oli Beach Park and Hanalei Pavilion Beach Park are in the middle of the bay.</p>
                <p>Coral reefs grow at both ends of the bay &ndash; Pu'u Poa Reef at the left end and Waikoko Reef at the right end &ndash;	while the middle part is scattered with shallow sandbars and tiny patches of coral. In summer, boats and yachts anchor in the bay. </p>
                <p>Though Hanalei Bay is generally good for a wide range of activities, from swimming to kayaking to windsurfing to yachting, it is subject to high surf and strong currents in the winter months. High surf from mid-October through March creates strong rip currents and a pounding shorebreak. Waves of 20 feet (6 m) and more are not uncommon. For surfers, the biggest waves are at a spot called Pinetrees, which is near the center of the bay.</p>
                <p>Camping is allowed at Black Pot Beach (near the pier), but a permit is required. More info about how to get a permit is <a href="../../kauai/camping">here</a>.</p>
                <h3 class="big">Hanalei Bay Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Largest bay on Kauai </li>
                  <li>Swimming only possible in summer when ocean is generally calmer</li>
                  <li>High surf and strong shorebreak in winter</li>
                  <li>Camping is allowed with a  permit at Black Pot Beach, located at the east end of Hanalei Bay near the pier</li></ul>
