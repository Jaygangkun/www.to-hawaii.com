                <h2 class="small">Lydgate Beach Park, Kauai</h2>
                <p>Lydgate Beach Park  (<a href="../../kauai/beaches/more_lydgatebeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is one of the most popular beach parks on Kauai's  east shore. The  park's main attraction is the large, double-sectioned pool on the northern end of the beach that is protected from ocean swells by a man-made rock barrier, which was  built  in 1964 using huge boulders (see history of the Lydgate pools below). </p>
                              <? include ("../../theme/text.html") ?>
                <p>The pool is divided into an inner and an outer section by two man-made rock walls. The inner pool is shallow and ideal for children, while the outer pool is deeper and ideal for swimming and snorkeling. A lifeguard tower overlooks both pools. While water activities inside the pools are generally safe, any activity outside of this protected swimming area is not. The ocean is unpredictable, so venturing outside the pools is not safe.</p>
                <p>Lydgate Beach Park has a grass field where local residensts like to fly kites or play frisbee. South of the pools is the 16,000-foot Kalamani Playground, which was built by 7000 local volunteers in 1994. Further south is a second playground called Kalamani Kai Bridge. The bridge was designed by local keikis (children) themselves. To reach the playground, you can bike or walk down the 2.5 mile (4 km) pedestrian and bicycle path running along the coast. </p>
                <p>North of the pools are several historical sites including the remains of the <a href="../ancientsites/hikinaakalaheiau.php">Hikinaakala Heiau</a>, a Hawaiian temple that dates as far back as 800 A.D. Next to the heiau are the remains of <a href="../ancientsites/hauolacityofrefuge.php">Hauola</a>, a sanctuary (place of refuge) for Hawaiians who broke a taboo (who did something that was forbidden). </p>
                <p>Lydgate Beach Park was named after Rev. John Lydgate, a civic leader and former church pastor of Lihue Union Church and Koloa Church. This long and wide beach has been used as the setting for a number of films such as <em>Diamond Head</em> and <em>Blue Hawaii</em>. <br />
                  <br />
                  A plaque has been placed at the Lydgate pools, which is a tribute to Mr. Morgan, who had the idea to build the sheltered swimming area. Here is what the plaque says: <br />
                  <br />
                  Albert S. Morgan Sr.'s Lydgate Pools<br />
                  Albert Smith Morgan Sr.<br />
                  March 9, 1908 - May 22, 2001</p>
                <p>In 1942, Albert S. Morgan, Sr. a native Hawaiian, married Helen Farias and raised five children. The family lived in the community of Wailua where Mr. Morgan served a term as president of the Wailua House Lots Community Association.</p>
                <p>As the children grew outdoor recreation became increasingly important. Mrs. Morgan frequently took the children to Lydgate Park. There was no sheltered swimming area there, but they enjoyed the beach. </p>
                <p>On a 1958 European trip Albert &amp; Helen visited Sorrento, a town on the southwestern Italian coast. In Sorrento while viewing a beautiful bay with protected swimming areas for the people, Mr. Morgan got in inspiration.</p>
                <p>Returning to Kauai with pictures he contacted Senator Billy Fernandes. Together they prepared and presented to the State of Hawaii a proposal to create a 2.6 acre rock barrier and impoundment along Lydgate beach.</p>
                <p>In 1964, $18,000 was appropriated to build the children's wading pool and the adult's swimming pool. A crane was floated into Lydgate beach to lift and place the boulders donated by Lihu'e Plantation. The entire project was completed in nine months.</p>
                <p>Mahalo Mr. Morgan</p>
