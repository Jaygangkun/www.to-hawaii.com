              <p><a href="../../map-kauai-beaches.html" target="_blank"><img src="../../theme/images/kauaibeaches.jpg" alt="Kauai Beach Map" align="right" border="0" style="margin-left:10px;" /></a>The island of Kauai has a coastline of about 90 miles (145 km). Kauai beaches are known for their powdery white sand and uncrowded atmosphere &ndash; some are hardly visited at all, which means that you can easily find your tropical paradise beach on this island. Many beaches on Kauai offer great swimming, snorkeling, scuba diving, surfing and kayaking conditions. Most Kauai beaches have white sand. A few others are rocky.  Read more about <a href="../../hawaiibeachsand.php">beach sand composition</a>.</p>
