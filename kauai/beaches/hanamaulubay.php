                <h2 class="small">Hanama'ulu Bay, Kauai</h2>
                <p>Hanama'ulu Bay is located just north of Lihue Airport on Kauai's east shore. The nearshore waters of the bay are usually murky thanks to silt that enters the bay from the Hanama'ulu Stream. The beach park is therefore mainly visited by picnickers and campers. Camping is allowed here, but a permit is required. More info about how to get a permit is <a href="../../kauai/camping">here</a>. However, there have been reports of campers who found this campground unsafe in terms of suspicious activity going on at night. Facilities in the park include picnic tables, BBQ grills, a pavilion, restrooms, showers and a playground.</p>
