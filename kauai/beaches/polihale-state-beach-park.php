                <h2 class="small">Polihale State Beach Park, Kauai</h2>
                <p>Polihale Beach is one of the longest continuous sand beaches in all of Hawaii, stretching 15 miles (24 km) along Kauai's west shore. The beach is also one of the widest in Hawaii, averaging 300 feet (91 m) during the summer months. It is backed by sand dunes, some of them are 100 feet (30 m) above sea level. Because of its remoteness and because of the eerie quality of its beauty, this beach has been named Polihale, which literally means &ldquo;the home of the underworld (Po)&rdquo; in Hawaiian. It is believed that it is here where the souls of the dead depart for Po, the underworld.</p>
                              <? include ("../../theme/text.html") ?>
                <p>Polihale State Park encompasses 140 acres (0.5 km&sup2;) and is located at the first 2.25 miles (3.6 km) at the northern end of the beach. North of the state park are the mountain ridges of the <a href="../attractions/napalicoaststatepark.php">Na Pali Coast</a>. And to the south  is the Pacific Missile Range Facility. The shoreline fronting the Pacific Missile Range Facility all the way up to Barking Sands Beach (a distance of 5 miles) has been off-limits to the public as part of a security policy since the 9/11 tragedy. Access is only allowed to people with a military ID card or a special pass that has to be applied for in advance and involves a criminal background check and costs $25. Access to Polihale State Park (the northernmost section of this coastline) is open to the public.</p>
                <p>Polihale  is a wild beach characterized by rough waves and rip currents  along the shore. There is no reef barrier that protects the beach from the open ocean. The only relatively safe place for a swim is at Queen's Pond, a shallow pond with a sandy bottom surrounded by a protecting reef. It is located at the southern end of the state park near where the large monkeypod tree can be seen from the dirt road (about 3.2 miles north of where Hwy 50 ends).</p>
                <p>South of Queen's Pond is Barking Sands Beach, also known as Keonekani o Nohili (The Sounding Sands of Nohili) in the Hawaiian language. If one walks over the sand here or takes two handfuls of sand in each hand and slaps it together, a sound is produced. This phenomenon occurs due to the formation of the individual sand grains. Each of them has small holes in it and the sound is produced within these small cavities through the vibrations that occur when the sand is pushed together. As mentioned above, access to Barking Sands is limited and not open to the general public as it is in a military restricted area.</p>
                <p>Since swimming is not possible at Polihale Beach on most days of the year due to rough ocean conditions, most people come here to relax, picnic, camp and enjoy the views. Camping is allowed at Polihale State Park, but a permit is required. More info about how to get a permit is <a href="../../kauai/camping">here</a>. There are no food stores in this area, so bring food and water before driving all the way out here. Local residents enjoy driving their ATVs or trucks on this beach. And since the beach is so wide and it takes a while to walk across it to get near the ocean, many simply drive their trucks onto the beach and set up camp next to their vehicle. It's a good idea to bring a beach umbrella or tent because there is no shade here and it gets hot on Kauai's leeward coast in the summer months.</p>
                <p>To reach the beach, one has to drive through a 5-mile (8 km) long somewhat bumpy dirt road. The road is graded, but is most drivable if it's dry. If it's raining or has recently rained, the road can be a muddy mess and have even more potholes. Driving to Polihale with a four-wheel drive is recommended, but it's not  necessary. A regular car will be fine if the road is dry, but be careful and avoid the sandy patches and don't drive near the sand dunes or on the beach to avoid getting stuck. Also keep in mind that if you come here with a rental car and you get stuck, you'll have to pay for the tow truck. Rental car contracts usually don't allow off-road or dirt-road driving. So if anything happens to the car, the rental car insurance won't cover it.</p>
