                <h2 class="small">Maha'ulepu Beaches, Kauai</h2>
                <p>Even though it is just about 10 minutes from popular Poipu, the Maha'ulepu area on Kauai's south shore is pristine, beautiful and undeveloped and few visitors come here, partly because it takes a bit more effort to get here than to other more popular beaches on Kauai. </p>
                <p>Maha'ulepu actually consists of three separate beaches, known as Gillin's Beach, Kawailoa Bay and Ha'ula Beach. To get to the first beach, <strong>Gillin's</strong>, drive past the Hyatt on Poipu Road. After the road becomes dirt, there is an old guard shack. Drive past it and park in the area where the road continues to the left. From here, take the short trail to the beach. Gillin's is named after the late Elbert Gillin who built a house on this beach, which is now a vacation rental. </p>
                              <? include ("../../theme/text.html") ?>
                <p>Near Gillin's Beach is a site known as the Maha'ulepu cave, which is actually a sandstone sinkhole and an ongoing archaeological project. It is sometimes gated off. On Sundays, there is an interpreter on site who explains what it is and how it was used as an ancient habitation site. Ancient artifacts have been found here. To reach the cave, walk past the vacation rental until you see a stream. Take the trail that follows it on its far southern side. After a two-minute walk, you'll see the sinkhole on your left. (Alternatively, it is accessible from the trail above CJM Stables).</p>
                <p>To get to the next beach, <strong>Kawailoa Bay</strong>, continue on the dirt road to the east. Windsurfing is a popular activity here. If you want to continue further to <strong>Ha'ula Beach</strong>, you need to walk along a sandy trail, which starts near the edge of the cliff. It's about a 10-15 minute walk. Because it is the most difficult to access, it is also the least visited. So on most days, you're likely to have it to yourself. The area is mainly frequented by local fishermen and occasionally by some horseback riders. Even further to the northeast, over the ridge, is <strong>Kipu Kai Beach</strong>. It is best to see this beach from a helicopter or boat because the trail over the ridge is difficult.</p>
                <p>In 1796 when King Kamehameha launched the first of his two invasion attempts on Kauai, more than 1,200 of his canoes carrying 10,000 warriors left Oahu at midnight, hoping to get to Wailua on Kauai early the next day. However, many of the canoes sank during a storm in the Kauai Channel. A few of the warriors managed to get to shore at Maha'ulepu, where most of them were killed. The ones who escaped paddled all the way back to the Big Island instead of to Oahu because they feared facing the King's wrath.</p>
                <h3 class="big">Maha'ulepu Beaches Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Two miles (3.2 km) of reddish-golden sand on Kauai's southeastern shore, surrounded by lithified cliffs and sand dunes</li>
                  <li>Beautiful and unspoiled area consisting of three separate beaches</li>
                  <li>Good area for exploring, hiking and beachcombing</li>
                  <li>Swimming conditions are fair and can be risky, ocean entry is difficult in many places along shoreline and the water is often times rough</li>
                  <li>Take out all belongings and trash that you bring here to keep the area clean and beautiful</li></ul>
