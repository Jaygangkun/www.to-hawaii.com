                <h2 class="small">Salt Pond Beach Park, Kauai</h2>
                <p>Salt Pond Beach Park is the only place in Hawaii where salt is still produced  in natural salt ponds. First, the salt ponds at the southern end of the beach are filled with ocean water and are then left to evaporate. What's left are rock crystals that are then bagged and used for cooking and medicinal purposes. Salt is produced during the dry and  hot summer months. </p>
                <? include ("../../theme/text.html") ?>                
                <p>Salt Pond Beach Park is a wide, reddish-golden sand pocket beach on Kauai's south shore that is generally safe for swimming year-round. At both ends of the beach are rocky outcrops; a natural rock ridge connects the outcrops, creating a small lagoon. This pool-like area is where families with children like to swim. The beach is usually safe for swimming, except during times of high surf, when rip currents form in the channels between the rock barriers.</p>
                <p>The area in the middle of the beach is unprotected. If you choose to swim in here, stay close to the shore. For snorkeling, the area near the lava rock outcrops - particularly the one near the salt ponds - provides excellent snorkeling opportunities. Moorish idols, goatfish, butterflyfish and unicornfish are some of the underwater creatures that can be seen here. </p>
                <p>Camping is allowed here, but you need to get a permit from the county before heading to the beach if you plan to camp. More info about camping permits is <a href="../../kauai/camping">here</a>.</p>
