                <h2 class="small">Pu'u Poa Beach, Kauai</h2>
                <p>Pu'u Poa Beach, located on the north shore of Kauai, is mainly used by guests of the St. Regis Princeville Resort and the Hanalei Bay Resort  (<a href="../../kauai/beaches/more_hanaleibayresort_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>).   If you're a guest at the Hanalei Bay Resort, you can take the steep, paved path that leads to the beach or use the resort shuttle. St. Regis Princeville Resort guests  can just take the elevator to the ground level. If you're not a guest at either one of the two resorts, there is a small public parking lot near the St. Regis and a beach access path (see directions below).</p>
                              <? include ("../../theme/text.html") ?>
                <p>The beach has reddish-golden sand and extends all the way to the Hanalei River. When the tide is low, a sandbar appears extending from the river to neighboring Black Pot Beach at Hanalei Bay. The width of the shoreline is subject to changing surf, narrowing in winter and widening during calm summer months. </p>
                <p>An offshore reef protects the beach, making it generally safe for swimming and snorkeling. The reef, however, doesn't protect the beach from large winter swells that often occur between November and March. Swimming and snorkeling are discouraged during winter months. There are no lifeguards at Pu'u Poa Beach. The waves that  break against the outer edge of the reef  attracts surfers.</p>
                <h2 class="big">Pu'u Poa Beach Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Wide, white-sand beach fronting the St. Regis Princeville Resort and the Hanalei Bay Resort</li>
                  <li>Plenty of trees and shade</li>
                  <li>Good swimming conditions in summer</li>
                  <li>Good surfing conditions in winter</li>
                  <li>Protected by a fringing reef</li></ul>
