                <h2 class="small">Wailua Beach, Kauai</h2>
                <p>Wailua Beach is a wind-swept, half-mile long white-sand beach located at the mouth of the <a href="../attractions/wailua-river.php">Wailua River</a> on Kauai's Coconut Coast (east shore). With no  reef to protect the beach against open ocean swells, the surf is usually up &ndash; all the reason why surfers and bodyboarders love Wailua Beach. This is despite the fact that the water tends to be murky because of the river water entering the ocean here. </p>
                                <? include ("../../theme/text.html") ?>
                <p>Due to its poor water quality and exposure to the open ocean, the swimming conditions are poor. Also, the river and the ocean are always clashing, creating strong rip currents and powerful shorebreaks. What the beach is lacking in terms of water activities, Wailua River makes up for it. It's a popular visitor  destination. Kayak and boat tours and hiking  expeditions up the Wailua River are  offered by nearby businesses.</p>
                <p>Located at the river's mouth (on the southern end of the beach) are two historical sites, the <a href="../ancientsites/hikinaakalaheiau.php">Hikinaakala Heiau</a> and <a href="../ancientsites/hauolacityofrefuge.php">Hauola City of Refuge</a>. </p>
                <h3 class="big">Wailua Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Good surfing beach when the conditions are right</li>
                  <li>Not a good beach for swimming or snorkeling - murky water</li>
                  <li>Kayak and boat tours up Wailua River are popular</li>
                  <li>Outrigger canoe races are held here in summer</li>
                  <li>Historical sites nearby</li></ul>
