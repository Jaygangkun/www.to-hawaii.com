                <h2 class="small">Aliomanu Beach, Kauai</h2>
                <p>Composed of two sections of beach, Aliomanu is a beautiful stretch of sand and one of the lesser-known beaches on Kauai. The south beach is mainly used by local families for pole-, torch- and throw-net fishing, octopus hunting and limu (seaweed) harvesting on the offshore fringing reef. The south beach also has a lagoon where one can swim in. The north beach is more picturesque and a nice place to enjoy the serene surroundings. However, the nearshore waters are rocky, so swimming is limited. Aliomanu is a rather remote area and not many tourists come here just for the beach. The ones who come here are likely staying at a vacation home along this stretch of shoreline.</p>
