                <h2 class="small">Secret Beach (Kauapea Beach), Kauai</h2>
                <p>Also known as Kauapea Beach, Secret Beach (<a href="../../kauai/beaches/more_secretbeach_photos.html" onclick="NewWindow(this.href,'name','550','650','yes');return false">see more photos</a>) is not exactly secret, but it takes a bit more effort to get to it. This fact keeps visitors limited. No road leads to it, but a 10-15-minute hike gets you here. But it is definitely worth a visit because it is one of Kauai's most scenic beaches. </p>
                                <? include ("../../theme/text.html") ?>
                <p>Secret Beach is backed by red and black sea cliffs and lush vegetation. It stretches for 3,000 feet (915 m) and to the east offers nice views of Kilauea Lighthouse and Moku'ae'ae Island, a bird sanctuary. To the west, about a quarter mile beyond the west end of the beach, is a sizable waterfall that plunges about 15 feet (4.5 m) into the ocean. Also, there are a few other smaller waterfalls along the beach.</p>
                <p>The ocean bottom is mostly sandy and moderately steep. However, the swimming conditions vary. In the summer months when the ocean is usually calmer, swimming is possible here, but can be dangerous even then. Strong currents can be present even if the ocean appears calm, and there are no lifeguards stationed here, so be careful. In the winter months, don't think about swimming here. </p>
                <p>The trail to the beach is a bit steep and winds through brush. It is slippery on rainy days. Some people prefer to hike it barefooted because of the reddish clay that is difficult to remove from shoes and clothing. Alternatively, you can wear neoprene boots or other water shoes that are easy to wash off.</p>
