                <h2 class="small">Nukoli'i Beach Park, Kauai</h2>
                <p>Nukoli'i Beach Park, with its 2 miles (3.2 km) of white-sand shoreline, is the longest beach on Kauai's east shore. It starts from where <a href="../beaches/lydgatebeach.php">Lydgate Beach Park</a> ends and spans the area fronting the Wailua Golf Course and the Hilton Kauai Beach Resort. The beach provides poor swimming conditions because of surf and currents and because the bottom is rocky and shallow. </p>
                              <? include ("../../theme/text.html") ?>
                <p>However, Nukoli'i Beach offers good snorkeling, scuba diving and fishing opportunities when the ocean is calm. Offshore, a reef provides shelter from waves, but not during the high surf season. Always monitor surf conditions before wading in. There is no lifeguard, so better stay out of the water particularly during the winter months. </p>
                <p>Nukoli'i Beach is sometimes referred to as Kauai Beach and sometimes as Kawailoa Beach. This is one of those Kauai beaches that never gets crowded. Most of the time it is rather deserted. Surfers can sometimes be seen here to ride the surf break called Graveyards, while beachcombers find plenty of room and shady spots for a picnic. </p>
                <p>In the 1970s, Nukoli'i Beach Park was the last beach between Lihue and Kapa'a to be developed. After years of controversies and political disputes between the county and Committee to Save Nukoli'i, a citizen's group, Nukoli'i was finally developed in 1986 as a beach park complete with facilities. That same year, the Hilton Kauai Beach Resort fronting the beach also opened. </p>
                <h2 class="big">Nukoli'i Beach Park Overview</h2>
                <ul type="disc" class="mylist">
                  <li>2-mile (3.2 km) long sandy beach, longest on Kauai's east coast</li>
                  <li>Protected by a wide fringing reef&nbsp; </li>
                  <li>Sand and rock ocean bottom</li>
                  <li>Never crowded, usually almost deserted</li>
                  <li>Not a good swimming beach (the area fronting the golf course is the best for swimming, if at all)</li>
                  <li>Fronts the Wailua Golf Course and Hilton Kauai Beach Resort</li></ul>
