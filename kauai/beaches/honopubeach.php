                <h2 class="small">Honopu Beach, Kauai</h2>
                <p>It doesn't get anymore secluded and remote than Honopu Beach on Kauai's <a href="../attractions/napalicoaststatepark.php">Na Pali Coast</a>. No road or even a hiking trail leads to it. In fact, the only legal way to access it is to swim to it. </p>
                <p>Honopu actually consists of two beaches, which are backed by majestic 1200-foot (365 m) high sea cliffs and separated by the Honopu arch. Around the corner is a large waterfall, cascading down the sea cliff, through the arch and into the ocean.</p>
                              <? include ("../../theme/text.html") ?>
                <p>Honopu means "conch shell" in Hawaiian. It got this name because of the conch shell-like sound that the arch makes when northerly winds blow into the valley. Another nickname of Honopu is Cathedral Beach. </p>
                <p>Trying to reach Honopu Beach is an adventure that is certainly not for everyone. Since swimming to the beach is necessary to set foot on it, one must be a very strong swimmer to reach it (and wear fins). When the surf is up, it is dangerous to enter the ocean along the Na Pali Coast because of strong rip currents and pounding surf. </p>
                <p>Also, currents can be present even if the ocean appears calm. Keep in mind that the currents usually flow in the same direction, so if you swim over to Honopu and it was easy, it may not be as easy on the way back if the current comes from that direction (which you may not have noticed on your way there). During summer when the ocean is calm, some people walk from <a href="../beaches/kalalaubeach.php">Kalalau Beach</a> on the exposed rocks and sand along the cliffs to Honopu Beach for most of the way.</p>
                <p> Since Honopu Valley is so isolated, it is believed to be spiritual. Many Hawaiian myths and legends tell of this area, and it is also a place of ancient burial grounds for local chiefs. </p>
                <h3 class="big">Honopu Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Isolated beach on Kauai's Na Pali Coast</li>
                  <li>Most prominent landmark on beach is the Honopu arch, which separates two small sandy coves</li>
                  <li>No landing of any aircraft, boat, kayak or even surfboard is allowed at Honopu Beach</li>
                  <li>The only legal way to access it is to swim here (either from neighboring Kalalau Beach or from an offshore boat)<br />
