                <h2 class="small">Lawa'i Beach, Kauai</h2>
                <p>Lawa'i Beach  is a tiny strip of land that sometimes disappears during high surf conditions. The beach is a top snorkeling destination on Kauai's south shore, located at the western end of Poipu Road next to the Beach House Restaurant. Because of that Lawa'i Beach is sometimes referred to as Beach House. </p>
                
                <p>The beach is sheltered by a fringing reef barrier. With little wave disturbance, snorkelers enjoy clear views particularly during calm summer months. The entry to the snorkeling spot is an easy gentle slope of sand. It has a rocky bottom that makes it great for snorkeling. Raccoon butterflies, tangs, parrotfish and sea turtles are some of the marine creatures that have made the bay their home. </p>
                              <? include ("../../theme/text.html") ?>
                <p>Lawa'i Beach also attracts surfers and bodyboarders when the waves break against the reef. Surfers frequent three surf spots here: Acid Drop, PK's and Centers. Lawa'i is also conveniently located near popular island attractions, including Spouting Horn (a blowhole), heiaus, historical sites and botanical gardens. Shops, restaurants, B&amp;Bs and vacation rentals are also in the area. </p>
