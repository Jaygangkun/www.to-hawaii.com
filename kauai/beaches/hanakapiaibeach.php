                <h2 class="small">Hanakapi'ai Beach, Kauai</h2>
                <p>Hanakapiai Beach is the first main destination on the <a href="../attractions/kalalautrail.php">Kalalau Trail</a> on Kauai's <a href="../attractions/napalicoaststatepark.php">Na Pali Coast</a>. The beach is about 2 miles (3.2 km) into the hike and looks beautiful, offering great photo opportunities. The scenic surroundings stand in stark contrast to the beach sign with a body count on it. On a calm day, you wouldn't think that Hanakapiai is the deadliest beach on the island. Dozens of drownings have happened here. The current is so strong that some victims have never been found. </p>
                              <? include ("../../theme/text.html") ?>
                <p>During the summer months, Hanakapiai's shore is sandy, but in the winter, the waves wash ashore and remove most of the sand from the beach. The hike to Hanakapiai is the busiest stretch on the Kalalau Trail. Most people hike up to here, since the trail continues for another long and arduous 9 miles (14.5 km) and one needs a permit to hike it all the way. Up until Hanakapiai, one doesn't need a permit. However, the hike to Hanakapiai is not easy. Especially the second mile of it is very steep. After a recent rainfall, it can be very muddy and slippery. The hike takes about 1.5 to 2 hours each way. </p>
                <p>From here, a side trail leads to <a href="../waterfalls/hanakapiaifalls.php">Hanakapiai Falls</a>. The side trail to the 100-foot (30 m) Hanakapiai Falls is another 2 miles (3.2 km) and is quite tough as well with numerous stream crossings. Some people reward themselves by having a refreshing dip in the pool. If you do this, step into it slowly (no head-diving into it) and be aware of rockfall (which are always possible near waterfalls and around cliffs). So if you plan to hike from <a href="../beaches/kee-beach-park.php">Ke'e Beach</a> all the way to Hanakaipai Falls, it's a total of 8 miles (13 km) and can easily take an entire day, especially if you plan to rest on the way. </p>
                <p>Camping is allowed at Hanakapiai Beach, but a permit is required. More info about how to get a permit is <a href="../../kauai/camping">here</a>.</p>
                <h3 class="big">Hanakapi'ai Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Remote beach on Kauai's Na Pali Coast with no road access</li>
                  <li> Beach is sandy in the summer, but boulders are exposed in winter</li>
                  <li> Has no protective reef, so it is fully exposed to strong ocean currents and waves</li>
                  <li> Dangerous to swim here, even if the ocean appears calm (rip currents are difficult to spot from land)</li>
                  <li> Nice views of the Na Pali Coast </li>
                  <li>Camping allowed with permit<br />
