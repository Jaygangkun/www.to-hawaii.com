                <h2 class="small">Kekaha Beach Park, Kauai</h2>
                <p>Located along Kaumualii Highway on the southwestern shore of Kauai, Kekaha Beach Park is a good place for a long beach stroll. Starting from here and going north, it's 15 miles of uninterrupted sandy beach. Kekaha Beach is mainly visited by local surfers, bodyboarders and fishers. Surfers have a wide choice of surf spots: Rifle Range, Targets, First Ditch, Second Ditch, Whispering Sands, Davidson and Inters.</p>
                <p>Kekaha Beach is unprotected from the open ocean that produces powerful waves, strong rip currents and  shorebreaks, making the beach unsuitable for swimming and snorkeling, especially during times of high surf.	The	pounding	shorebreak	is so powerful it has snapped surfboards in half. So before jumping into the water, ask a lifeguard about the ocean conditions. Avoid swimming when the surf is up. </p>
