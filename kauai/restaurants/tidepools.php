<p>Tidepools Restaurant is set in the grounds of Poipu's Grand Hyatt Resort.  The open-air setting is unique with a number of hale pili, a type of thatched  roof hut, floating above a lagoon filled with koi. The romantic ambience is  further enhanced with tropical flora, waterfalls and the refreshing ocean  breeze. The restaurant offers contemporary Hawaiian cuisine according to the  islands' rich traditions with an emphasis on steak and seafood. </p>

<p>As well as being the recipient of a Certificate of Excellence from  TripAdvisor and named Kauai's best restaurant by Zagat Surveys, Tidepools has also been voted the  best resort restaurant by readers of <em>Hawaii  Magazine</em> and one of the most romantic restaurants in the US by users of Open Table. The menu  at Tidepool, which is based on local ingredients insofar as this is possible,  has something for every taste. With a good choice of appetizers, soups, salads  and seafood, as well as a selection of fare from the land, some of the most  popular dishes include Grilled Hawaiian Ahi, Hawaiian Opah and Macadamia Nut  Mahi Mahi. The restaurant serves the freshest fish and steak, all prepared in  unique Hawaiian style. There are, of course, some other excellent culinary  creations on the menu as well as options for vegetarians and a keiki menu. The  dessert menu has the right mix of sweet treats to suitably round off any meal  while the wine list is quite extensive with sufficient choice to satisfy every  palate. </p>
<p>The multi-award winning Tidepools is open seven days a week - from Monday  through to Sunday. Dinner is served from 5.30 until 10.00pm every night and the lounge is  open during the same hours. The Grand Hyatt Resort in which Tidepools is  located offers many amenities such as spa facilities and golf. The resort  caters for all types of functions, including wedding receptions. Casual resort  attire is acceptable for dining at Tidepools restaurant. Reservations are  highly recommended. </p>
