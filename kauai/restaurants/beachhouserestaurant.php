                <p>The Beach House Restaurant is situated around a mile to the west of Poipu Beach, close to Lawai Beach Resort.  Offering a beautiful beachfront location, great ocean views and an unwavering  focus on providing its patrons with innovative local cuisine, this restaurant  has won multiple awards. The owners of the Beach House have a simple  philosophy, which is to make the most of local resources and blend them with  the old-fashioned hospitality for which the Hawaiian Islands are famous in order to provide a  dining experience that stimulates all the senses.</p>
                <p>Against the beautiful natural backdrop, owners Roy Roy Dunn and Mike  Hooks work closely with their kitchen team to ensure every aspect of their  culinary vision is represented in what the Beach House has to offer. Local  ingredients are used to the full whenever possible. Local fishermen provide the  island-caught fish and the restaurant cooperates with Kauai ranches to source the best beef,  coffee and other produce. Locally-grown fruits and vegetables are used whenever  these are available and the Beach House collaborates with local artisan bakers,  folk experts and other knowledgeable sources to find the most innovative ways  to use the island's resources. The lunch menu offers a selection of starters,  salads, sandwiches, wraps, entr&eacute;es and sides. For dinner, as well as a choice  of starters, soups and salads, diners are offered a number of saut&eacute;ed and  fire-grilled entr&eacute;es such as Oven Roasted Breast of Chicken, Macadamia  Nut&nbsp;Butter&nbsp;Saut&eacute;ed&nbsp;Fresh&nbsp;Hawaiian&nbsp;Catch, Fire Grilled  Center Cut Pork Loin, Angus Filet Mignon and Chinese 5-Spice Crusted Lamb  Sirloin. Dessert and wine menus are available to complete any repast at the  Beach House. Other beverages available include a selection of signature drinks,  martinis and beers.</p>
                <p> The multiple-award winning Beach House restaurant is open daily. Lunch is  available from 11.00am until 3.00pm. Dinner is served from 5.30pm until 8.30pm (September 17 to March 10) and  from 6.00pm until 9.00pm in peak season (March 11 until September  16). Light meals are available daily from 3.00pm until 5.30pm. While reservations are not  required for lunch, it is highly advisable to make reservations for dinner.  Pupus and drinks are served daily in the bar and lounge from 11.00am until 9.00pm. Facilities are also available to  cater for parties, weddings and other functions.</p>
