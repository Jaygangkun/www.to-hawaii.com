                <p>Looking out onto Kalapaki Bay from its location on the grounds  of the Kauai Marriot Resort, Duke's is the perfect place for anyone looking  casual oceanfront dining. The restaurant is named after Duke Kahanamoku, the  famous Waikiki-born surfing legend and Olympic gold medallist. The cuisine at  Duke's is distinctly Hawaiian. Serving lunch, dinner and Sunday brunch, many  dishes based on age-old and well-loved island recipes. </p>
                
                <p>Although he represented the US in the Olympics for two decades  and won many medals, Duke Kahanamoku was equally well known for his  sportsmanship and humor as he was for his exceptional speed and grace as a  swimmer and surfer. That spirt is embodied in Duke's restaurant. With a  welcoming atmosphere, the menu at this oceanfront oasis reflects the sense of  fellowship that makes Hawaii famous all over the world. The  dinner menu is comprised of a selection of pupus, salads, fresh fish and  seafood, a number of island favorites, grilled steaks, sides and desserts.  There is also a special keiki menu, offering a range of entr&eacute;es and desserts to  tempt young palates. The bar at the Kauai Marriott - Barefoot Bar - also offers  a selection of beverage to suit virtually every palate - wines, beers,  cocktails, beach drinks and two different types of Duke's famous mai tais. As  well as the drinks, visitors are offered  a selection of food from the bar - pupus, salads, flatbreads, sandwiches,  burgers, tacos, fresh fish and desserts. The bar also features an Aloha Hour  every day from 4.00pm until 6.00pm with live music on Mondays,  Wednesdays, Thursdays, Fridays and Sundays to coincide with the Aloha Hour.  Live music is provided on Fridays and Saturdays from 8.30pm until 10.30pm.</p>
                <p>Duke's is a great place to get together with friends, family or someone  special. The ocean view provides a relaxing and scenic backdrop for any  occasion. All types of events are catered for too, whether it is a birthday  celebration, an anniversary party or a business function. The restaurant/bar is  open from 11.00am until 11.00pm every day - Monday through to  Sunday.</p>
