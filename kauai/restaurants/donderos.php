                <p>Located at the Grand Hayatt Resort in Poipu with both indoor and outdoor  seating, the atmosphere in Dondero's Italian restaurant is all at once  intimate, romantic and casual. The d&eacute;cor inside the restaurant is reminiscent  of a Tuscan villa with windows that reach from the ceiling to the floor,  providing diners with excellent views of the ocean and the elegant resort. The  detailed murals and exquisite tiles are further tributes to Itlay. The outdoor  seating area is surrounded by the tropical beauty of the resort with superb  views of the Pacific Ocean, interrupted only by the gentle, cooling breeze. While the cuisine is  classic Italian, visitors to Dondero's are sure to find several speciality  dishes and some new twists to old favorites.</p>
                
                <p>The recipient of several awards, Dondero's offers a bistro-style dining  experience. There are many specialty dishes on the dinner menu, including  Capesante, Conchiglie con Aragosta and Ossobuco. The dinner menus are broadly  divided into selections of antipastis, soups, salads, pastas, risottos, pizzas  and signature dishes that included saut&eacute;ed local catch, pan-roasted rib eye and  poached lobster. With everything home-made and magnificently presented, the  food is superb. The pastas and seafood are delicately dressed with ingredients  that are entirely fresh. The wine list is quite extensive, offering a good  selection from the vineyards of Italy, California, Argentina and other destinations. And while  the interior is intimate, a drink on the outdoor terrace is the best place to  soak up the ambience and the beauty of the surroundings.</p>
