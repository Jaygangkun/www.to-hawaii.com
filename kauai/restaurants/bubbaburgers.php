                <p>Said to serve some of the best burgers available on Kaui, Bubba Burgers  can be found in the heart of Hanalei on the island's North Shore, just a few minutes away from Hanalei Bay. Whether you want to meet with  friends for lunch or an evening meal, have worked up an appetite after hours of  surfing, snorkelling, rafting, hiking the Napali or exploring the island, Bubba  Burgers is the place to look for if you want a good burger.</p>
                
                <p>Part of a franchise with three outlets spread around Kaui, it is Bubba's  belief that integrity, hard work and being able to make the best of any  situation is a winning formula. The restaurant sources the best ingredients  available and uses local produce where possible. Using only beef that has been grass-fed beef  and ground to Bubba's specification, the extensive menu includes burgers of all  sizes - from the Bubba Burger, which is suitable for youngsters, those with  smaller appetites and the not-so-hungry, to the Big Bubba, the Slopper and the  Double Slopper, there is something to suit all tastes and hunger levels.  Burgers can be served relatively unadorned or with all the trimmings.  Typically, burgers are served on a toasted bun with diced onion, mustard  (usually French), an old-fashioned relish with a ketchup base and a  good-quality mayonnaise. A selection of local favorites includes the Teriyaki  and Double Teri Burgers (with onion, mayonnaise and teriyaki glaze), the Big  Teri and the Hubba Bubba (a combination of burger, split grilled hot dog and  rice, spread with Bubba's Chili). Other menu offerings include chicken  sandwiches, hot dogs, salads and rice. Side orders of fries, onion rings,  frings and other favorites are also available. A range of soft drinks - coke,  iced tea, lemonade, root beer and so on - as well as variously-flavored milk  shakes and floats are available to complete all meals. </p>
