                <h2 class="small">Brennecke's Beach Broiler, Kauai</h2>
                <p>Located no more than a few steps from the renowned Poipu Beach, Brennecke's Beach Broiler serves  a wide selection of food to suit all appetites. From its second floor location  overlooking the beach, the open-air dining room provides its patrons with  superb views of the ocean. The environment at Brennecke's is casual, the food  is good and the service is friendly - all the ingredients that make for a great  day out at the beach.</p>
                
                <p>Open since 1983, the wide-ranging menu at Brennecke's Beach Broiler  includes a selection of appetizers, salads, sandwiches, pasta, seafood, steak,  drinks and much more. All salads are made from fresh, organic ingredients, and the  prices are affordable. Visitors can follow their favorite games from the bar  where drinks - beer, rum, signature cocktails and a number of non-alcoholic  options - are also available. The keiki are catered for too with a special  pupus menu to whet young appetites. As well as the restaurant and bar  facilities, Brennecke's Beach Broiler provides a beach deli, which is located  across from the beach parking lot. Here, beach-goers will find a selection of  differently-flavored shaved ice and fruit smoothies. The deli will even provide  a picnic of breakfast burritos, deli-style sandwiches and other fare for those  heading out for a day at the beach or a hiking expedition - you just need to  call in advance to discuss your requirements and place an order. </p>
                <p>Brennecke's Beach Broiler is open seven days a week - from Monday through  to Sunday. The restaurant is open for lunch and dinner from 11.00am until 10.00pm while the beach deli is open from  7.00am until 9.00pm. The bar is open from 10.00am until closing time, with happy  hours daily from 3.00pm until 5.00pm and again from 8.30pm until closing time. Catering  services are also available, details of which are available on enquiry. </p>
