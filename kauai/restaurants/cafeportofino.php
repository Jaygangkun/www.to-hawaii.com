                <p>Overlooking Kalapaki Bay and set in the grounds of the  Kauai Marriott Resort, Caf&eacute; Portofino is a classic Italian restaurant,  resplendent with candle-adorned tables and marble floors. The restaurant is  likened to something akin to tropical Italy or a Hawaiian Riviera. The bay is  visible from the villa-style dining room and terrace. With the d&eacute;cor and vista  providing an intimate and romantic backdrop, the menu is influenced by the  cuisine of northern Italy - with fresh seafood, veal and a  selection of pasta on offer. The extensive and authentic wine list is the  perfect accompaniment to such a setting.</p>
                
                <p>Caf&eacute; Portofino is owned and run by Italian-born and Giuseppe Avocadi who  says Kauai reminds him of the Italian  Riviera. There is a great sense of style and elegance to this restaurant, which  offers the choice of both salon and outdoor dining. As well as a number of specialty pastas, the  menu includes such culinary treats as Bistecca di Vitellone, Fettuccini Alfredo  and veal Osso Bucco. All dishes are accompanied by special Italian sauces, of  which the fresh Marinara is particularly good. Every day, gelato and fruit  sorbets are carefully prepared by hand. After dinner, patrons can linger over a  glass of fine Italian wine from the extensive drinks menu or sit back over an  exotic coffee from the espresso and cappuccino bar. The Caf&eacute; has its own  accomplished harpist who performs every night, another factor that greatly  enhances the Riviera-style atmosphere and excellent food. </p>
