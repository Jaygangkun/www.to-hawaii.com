                <h2 class="small">JJ's Broiler, Kauai</h2>
                <p>Just three minutes' drive from Lihue Airport and right beside the Kauai Marriott  Resort, JJ's Broiler is a family-owned restaurant that prides itself on  offering good quality food, an outstanding service and a warm welcome. The  cuisine is primarily American and local Pacific Rim fare, with considerable emphasis  on island-inspired steak and seafood. This restaurant is known for its large  and hearty portions, all served in the Aloha spirt of friendliness and  hospitality. There is an outdoor terrace area overlooking Kalapaki Bay from whence diners can enjoy  great views of Kalapaki Bay and the ocean.</p>
                                <p>Initially opened by Jim and Mary Jasper in 1967, JJ's Broiler is now run  by the couple's children and grandchildren. The menu offers a wide variety of  salads and grilled entr&eacute;es with a view to offering diners healthy eating  options. The island fish is freshly caught, the green salads are freshly  prepared, and the prime rib is top quality. In addition, the menu offers a  selection of pastas and is famous for its award-winning Slavonic steak, which  the restaurant has given permission to be featured at other eateries around Hawaii. The drinks menu includes the  restaurant's famous Mai Tais, which can be enjoyed on the terrace overlooking  the ocean or in the sports pub at the full-service bar where visitors can  follow their favorite sports events with friends and loved ones. The bar has  nine flat-screen televisions strategically placed throughout for this  purpose. </p>
                <p>The winner of many awards and accolades, JJ's Broiler is open seven days  a week for lunch and dinner. Opening hours are from 10.30am until 10.00pm. Lunch is served from 11.00am until 5.00pm and dinner from 5.00pm until 9.00pm. There is a daily happy hour in  the bar from 3.00pm until 5.00pm. The restaurant has the  facilities - a dining room on the second floor - to cater for large parties of  over 100 guests and, as well as the eat-in option, large take-out orders to  accommodate parties and business meetings are welcome.</p>
