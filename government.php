                <h2>Who Governs the Hawaiian Islands?</h2>
                <p>Government in Hawaii is similar to other U.S. states but also has several differences. The state constitution was modeled from the constitution of the Kingdom of Hawaii, modified to not conflict with the U.S. Constitution.</p>
                <p>English and Hawaiian are official languages, though in practice, it is not required that all official documents be printed in each language, or that government officials be fluent Hawaiian speakers. Admission Day is a state holiday in Hawaii, observed on the third Friday in August. </p>
                <p><strong><img border="0" src="images/iolani.jpg" width="148" height="125" align="right" alt="Iolani Palace" />Pre-Statehood Period<font size="2"></font></strong></p>
                <p>On August 12, 1898, sovereignty of Hawaii was formally transferred to the USA. A ceremony took place at <a href="oahu/attractions/iolanipalace.php">Iolani Palace</a> in Honolulu. Sanford Dole gave a speech as the newly appointed governor of the Territory of Hawaii. </p>
                <p>The Hawaiian anthem, <em>Hawaii Pono'i</em>, was played as the Hawaiian flag was lowered and replaced by the American flag and the <em>Star-Spangled Banner.</em></p>
