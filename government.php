                <p><strong><img border="0" src="images/hawaiistateseal.gif" width="231" height="228" align="right" alt="Hawaii State Seal" />Interesting 
                  Facts about Hawaii's State Seal</strong></p>
                <p>Hawaii's state motto, as displayed on the state seal, is "Ua mau ke ea o ka aina i ka pono" - "The life (sovereignty) of the land is perpetuated in (by) righteousness." King Kamehameha III made the phrase "Ua mau ke ea o ka aina i ka pono" a motto of the Hawaiian Kingdom on July 31, 1843. After that Queen Victoria made a public announcement that Hawaii's sovereignty was finally restored after the British occupation of Hawaii that lasted five months. On May 1, 1959,  "Ua mau ke ea o ka aina i ka pono" became the official motto of the state of Hawaii. 
                                
                  Here are some interesting facts about the state seal:</p>
                <ul>
                  <li>1959 is the date of statehood.</li>
                  <li>The rising sun replaces the royal crown of the original 
                    coat of arms, which symbolizes the birth of a new state.</li>
                  <li>King Kamehameha the Great and the Goddess of Liberty are holding the Hawaiian flag. They 
                    replace the two warriors on the royal coat of arms.</li>
                  <li>The four stripes of the Hawaiian flag in each of the first and fourth quarters of the shield
                    represent the eight main Hawaiian islands.</li>
                  <li>Pulo'ulo'u, or tabu ball and stick, in the second and third quarters was carried 
                    before the king and placed before the door of his home, signifying his authority 
                    and power. In the seal it symbolizes  the authority and power of the government.</li>
                  <li>The phoenix, which is a symbol of death and resurrection, symbolizes the change from 
                    the monarchy to a freer democratic form of government.</li>
                  <li>The eight taro leaves, flanked by banana foliage and maidenhair fern, are 
                    typical Hawaiian flora. Taro also had great religious significance.</li>
                </ul>
