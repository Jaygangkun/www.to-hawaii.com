                <p>A prominent political issue in recent years has been the achievement of some form of sovereignty by Native Hawaiians. In 1996, a majority of the Hawaii's roughly 200,000 descendants of indigenous Hawaiians voted to establish some form of self-government. In August 1998, the 100th anniversary of the U.S. annexation of Hawaii, protesters marched in Washington, D.C., demanding their full sovereignty from the federal government. </p>
