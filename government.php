                <p><strong>The Push for Statehood</strong></p>
                <p>Starting from around the turn of the century until after World War II there was a push for statehood, but  Congress was reluctant at first because of social as well as political concerns. In 1947, the U.S. House of Representatives passed a statehood bill, but the U.S. Senate refused. Not until 1959, after Alaska became the 49th state, did Congress vote to let Hawaii enter the Union. Following Alaska's admission in January 1959, a new flag with 49 stars was designed and first flew over Independence Hall in Philadelphia on July 4, 1959. It would be the official
                  flag for only a year. The 50-star flag became the 27th flag on July 4, 1960, and remains the official flag of the United States until today. President Dwight Eisenhower signed the bill on March 18, 1959, and the question was then proposed to the Hawaiian electorate, who voted for statehood on June 27, 1959, by a margin of 17-1. Hawaii became the 50th state on August 21, 1959.</p>
                <p><strong><img border="0" src="images/hawaiistate.jpg" width="148" height="125" align="left" alt="Hawaii State Capitol" />Post-Statehood 
                  Period</strong></p>
                <p>Hawaii's constitution was drafted in 1950 and became effective in 1959 upon attainment of statehood. In 1969, the construction of a new <a href="oahu/attractions/statecapitol.php">state capitol</a> was completed.</p>
                <p>Hawaii's government is organized into three branches: Executive, Legislative and Judicial. A governor elected every four years heads the executive branch. The current governor of Hawaii is Neal Abercrombie. The Hawaii State Legislature has a Senate with 25 members elected for four-year terms and a House of Representatives with 51 members elected for two-year terms. The state of Hawaii elects two representatives and two senators to the U.S. Congress and has four electoral votes. The Supreme Court of Hawaii is the state's highest court. It consists of five justices.</p>
