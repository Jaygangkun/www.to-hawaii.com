              <h2>Hawaiian Art - Diverse and Complex</h2>
              <p>The beauty of the Hawaiian Islands has inspired countless artists in the creation of some of the world's most treasured collectables. Art lovers visiting the islands may find the art scene here to be nothing short of heaven and like nowhere else in the world. As a Pacific melting pot, a broad range of cultural artisans work with many media to create unique works of art. </p>
              <p>The art of Hawaii is diverse and complex, with a broad range of styles sure to please connoisseurs of all ages. From beautiful and intricate koa wood carvings to surf photography, fine art, coconut weaving, lei making, printmaking, heirloom jewelry and painting, Hawaiian art will delight the eyes and inspire the minds and hearts of viewers. </p>
                           <p><strong>Na Hana Noeau - Functional Beauty</strong></p>
              <p>Much of the artwork created in the islands today has roots in the ancient cultural practices of Hawaii. The ancient Hawaiian artisans were skillful craftsmen with extremely high standards of work and a unique perspective on artistic nature. They believed that each undertaking was a test of self and dedication to Hawaiian culture and religion. Through careful study and focused attention, a craftsman could harness the mana (power or spirit) of the gods and in turn, thereby appeasing the entities and creating pono (righteousness).</p> <?php
