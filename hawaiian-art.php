?> <p>Na hana noeau refers to "wise and skillful works" and represents works of ancient Hawaiian art that were functional as well as beautiful. The Hawaiians applied aesthetic principles in their creation of useful crafts, for every creation was to be originated with a reflection of the mana they might possess. Na hana noeau of ancient Hawaii includes<strong class="subtitle"> kapa</strong>, a versatile material pounded and stamped, <strong class="subtitle">lauhala</strong>, the weaving of hala leaves for functional purposes, <strong class="subtitle">koa wood</strong>, carved to suit a variety of purposes, and <strong class="subtitle">hulu manu</strong>, or 
                intricate featherwork, used for adornment and distinguishment. </p>
                 <? include ("theme/text.html") ?>
              <p><strong class="subtitle">Kapa</strong> is a fine material created by beating the wood of the wauke (paper mulberry) plant. The trunk of a carefully chosen wauke 
                was stripped to remove bark by the layer. The inner bark layers were soaked in seawater, fermented and then beaten with rocks and dried in the sun. When enough of the bark was collected, the pieces were again soaked and pounded. The bark was then left alone to ferment and soften under banana leaves. The softened bark was then kneaded, beaten with a mallet, soaked yet again and taken through the process once or twice more. The unmistakable "tap, tap, tap" of the kapa pounder could be heard from miles away. </p>
              <p>The strips of cloth were then sewn together to create the area needed. If the material was to become a blanket, lava lava or muumuu cloth, bamboo or ohia wood stamps dipped in natural dye were then used to create intricate geometric patterns repeated over the material. Or, the material might have been twisted into cordage to use as fishing nets or carrying nets. Blankets, lava lavas, canoe sails and muumuus were just some of the uses for kapa. Although the practice of kapa making has virtually vanished, a few dedicated artists carry on the tradition today. Renowned kapa artist 
                Puanani Van Dorpe continues to practice this amazing craft day in and day out, dutifully beating kapa to replicate designs of old for eight or more hours a day.</p>
                               <p><strong class="subtitle">Lauhala</strong> (hala leaves) are still used today for creation of many products from bowls to hats to floor mats. The leaves of the 
                hala (pandanus) tree are dried, stripped of their sharp thorns and soaked to create a texture that is intricately woven into a variety of functional shapes. Lauhala could be tightly woven to create a stiff surface, or loosely woven for a more flexible shape. </p>
              <p><strong class="subtitle">Hulu manu</strong>, or featherwork, was used to create beautiful and colorful adornments designed specifically for high-ranking Hawaiian 
                chiefs (alii). Professional bird catchers, called kia manu, carefully studied the habits of the native winged species, including the iiwi, oo, mamo, apapane, nae and ahuula and tracked down the birds with the colored feathers necessary to complete a certain project. Feather capes called ahuula, feather standards called kahili, gourd rattles called uli uli and anklets and bracelets called kupee were a few of the featherwork products. Longer capes requiring more artistry were reserved for the highest ranking chiefs, as were the most brightly colored and rarer bird feathers.</p>
              <p><strong class="subtitle">Koa</strong>, the King of the Hawaiian forest trees, was used in ancient times to create a wide variety of products from giant seafaring canoes, the oo (digging stick), tikis and other godlike images and calabashes. Carvings were made using a koi or adze, and the koa craftsmen studied for years to learn the intricate and focused practice. Koa is still used today, although the wood is in shorter supply. </p>
              <p><strong>Hawaiian Quilts</strong></p>
              <p><img src="images/quilt_3.jpg" width="148" height="125" alt="Hawaiian Quilts"  align="right"/>Hawaiian quilting, although not practiced in ancient Hawaii, has its roots in the early art of kapa making. Influenced by the sewing practices of Hawaii's missionaries, native quilting took on a life of its own, incorporating patterns and designs inspired by the beauty of the land and the mana of ancestors. <em>Kapa apana</em> refers to the Hawaiian method of quilting in which three layers are stitched together, consisting of a decorated top layer, a middle layer of fibers and an undecorated back layer. You can find a wide variety of natural designs incorporated into Hawaiian quilts, 
                from leaf patterns to flowers and other symbolic representations. </p>
                             <p>Images may also represent special locations, royal lineage, historical events, abstract thoughts and familial ties. Each stitch held much meaning and intention. A quilt crafted with the ulu, or breadfruit design, symbolizes nourishment and is said to bless the quilt maker with prosperity. Hawaiian quilts are believed by many to pass on the spirit of the quilt maker along with blessings and aloha. In earlier tradition, a Hawaiian quilt might be buried with the original artist to symbolize the wholeness of the spirit. Although many of the earliest Hawaiian quilt patterns have been lost, the quilt makers of today are infusing traditional patterns with modern designs to keep this cultural practice alive.</p>
              <p><strong>Hawaii State Foundation on Culture and Art (HSFCA)</strong></p>
              <p>Instituted by the Hawaii State Legislature in 1965, the HSFCA is dedicated to preserving, promoting and perpetuating cultural arts in the Hawaiian Islands. Federal grants from the National Endowment for the Arts help the HSFCA work towards their mission of increasing art awareness. Educational milestones have been reached, including the Art in State Buildings Law that established the Art in Public Places Program, the adoption of the first percent-for-art law. Another one was the 1970 Artists in the Schools Program, the first direct instructional statewide program between 
                students and professional artists in schools.</p>
                 <? include ("theme/text-2.html") ?>
              <p>One need not visit a museum or art gallery to find priceless native Hawaiian art. When exploring the Hawaiian Islands, it is nearly impossible to miss the ubiquitous artistic expressions that adorn historical landmarks, state buildings, libraries, shopping centers, airports, parks and beachfronts. From giant bronze statues to shell or feather lei, artwork of Hawaii is deeply spiritual and infused with symbolism. </p>
