              <p>Located 155 miles (249 km) northwest of Nihoa and 430 miles (690 km) northwest of Honolulu, Necker Island (Mokumanamana) is a small (barely 41 acres), 
                steep-sided, hook-shaped and barren volcanic island, quite unlike the tropical ideal of white-sand beaches and swaying coconut palms. Rainfall is less a little less than 25 inches a year and the only vegetation one can find here are low-growing shrubs and grasses. Necker Island is rocky and has very little soil. Its highest peak is 276 feet (84 m) and it encompasses 0.07 square miles (0.18 km&sup2;). It is one of the Northwestern Hawaiian Islands.</p>
