                  <h2>Animals in Hawaii</h2>
                  <p>Since Hawaii's native fauna (<a href="more_fauna_photos.html"  onclick="NewWindow(this.href,'name','450','600','yes');return false">see more photos</a>) evolved in the absence of predators or competitors, they did not develop natural defenses such as thorns, poisons, or camouflage. The first Polynesian settlers arrived in Hawaii around 500 A.D. who brought pigs, horses, sheep, goats and dogs with them. Scientists believe that at that time, the Hawaiian Islands were inhabited by some 67 species of birds, of which a third are extinct today. </p>
                  <p>The first settlers did not find any other animals, such as amphibians, mosquitoes, reptiles or bugs. The Islands were home to only two endemic mammals, the Hawaiian monk seal and the hoary bat. It is believed that the hoary bat at some time was accidentally blown to Hawaii from North or South America.</p>
                  <p>In the 19th century, the Hawaiian monk seal almost became extinct because it was being caught for its oil and skin. Today, they are on the endangered species list and have made a comeback.</p>
                  <? include ("theme/text.html") ?>
                  <p>Much of the fauna in Hawaii developed special adaptations to their new home and evolved into new species. Today, nearly 90 percent of the fauna in Hawaii are endemic, meaning that they exist nowhere else in the world.</p>
                  <p>Agricultural inspections at airports and harbors work at keeping out unwanted species. One example of an unwanted species is snakes. Hawaii has no endemic snakes. However, a few have been brought here, but they are under continuous eradication programs.</p>
                  <p>Kauai is home to the largest number of native bird species in Hawaii. It is the only island in Hawaii free of mongoose, which prey upon the eggs of ground-nesting birds. </p>
                  <p>Of Hawaii's two native mammals, the hoary bat lives at the Koke'e State Park, while the Hawaiian monk seal can occasionally be spotted napping on a remote beach. Wild goats, pics and black-tailed deer are non-native mammals that are hunted. </p>
                  <p>On the Big Island, wild horses can be found in Waipi'o Valley, feral cattle graze on the slopes of Mauna Kea, while wild goats, pigs and sheep live in the island's interior. On the island of Oahu, goats and wild pigs live in the mountain valleys. In Kalihi Valley, brush-tailed rock-wallabies can be observed. Although they are difficult to spot, the wallabies are interesting for zoologists because they may be an extinct subspecies in their native Australia.</p>
                  <p>Within the last 200 years, more bird species have become extinct in the Hawaiian Islands than anywhere else in the world. Of the 67 initial species, 23 are now extinct and 30 are endangered. Even the alala (Hawaiian crow) is threatened.</p>
                  <p>Among the native bird species in Hawaii are the aeo (Hawaiian stilt). It can be observed in protected wetlands, such as the Hanalei National Wildlife Refuge on Kauai, the Kealia Pond on Maui and Kanaha Wildlife Sanctuary on Maui.</p> <?php
