?> <p>Hawaii&rsquo;s state bird is the nene, a duck. At one point it was almost extinct, but because of strict laws, about 500 nene are in existence today. They thrive in three places in the Islands: Haleakala National Park on Maui, the slopes of Mauna Kea on the Big Island and at Mauna Kea State Park bird sanctuary. They can also be observed in a few other places.</p>
                  <p>From the reptile family, it is worthy to mention the gecko. This small lizard is about 4 inches long. It lives comfortably in people's houses and actually it is good to have one because it is harmless and it likes to eat cockroaches. So it's like having a natural roach remover. </p>
                  <p>Underwater there are about 680 fish species that inhabit the Hawaiian waters. There are about 450 types of reef fish in Hawaii, of which 27 percent are endemic to Hawaii.</p>
                  <p>Conger and moray eels are the most common eels in Hawaii. Moray eels are usually harmless if one leaves them alone. In the past, some divers fed them, which made the animals associate divers with food. So they occasionally can be aggressive if they are hungry.</p>
                  <p>Humpback whales migrate to Hawaii each year from Alaska. They arrive in the warm Hawaiian waters around the end of November and stay here until spring (end of March). Maui is the best island for viewing the whales on their annual migration route to their birthing grounds. Whale watching cruises are a popular activity in Hawaii. Humpback whales can be up to 45 feet long, so they can easily be spotted even from land. Other whales, such as sperm, false killer, pilot, pygmy killer and melon-headed whales can be observed year-round. </p>
