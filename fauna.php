                  <p>Hawaii is also home to about 40 species of sharks, including the harmless whale shark to the not so harmless tiger shark. Occasionally great white sharks have been seen as well. The most common shark species in Hawaii are the gray reef, white-rip and black-tip reef sharks. </p>
