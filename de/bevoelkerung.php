            <h2>Die Menschen auf Hawaii</h2>
            <p> Hawaii steht f&uuml;r Vielfalt und das kennzeichnet auch die Bev&ouml;lkerung Hawaiis. Hawaii ist ein wahrer Schmelztopf der Kulturen. Christliche Missionare kamen hier im Jahre 1820 aus Neu England von der US-Ostk&uuml;ste an. Dadurch wurde die einheimische hawaiianische Bev&ouml;lkerung stark beeinflusst. Krankheiten die auf den Inseln fremd waren wurden eingef&uuml;hrt an denen viele Einheimische starben. Gleichzeitig wuchs die wei&szlig;e Bev&ouml;lkerung, 
              die auch "haole" genannt wird, an. </p>
                                       <p> Zu der heutigen kulturellen Vielfalt kam es dank der Zuwanderung von Zuckerrohr und Ananas Plantagenarbeitern aus Portugal, Japan, China und anderen &ouml;stlichen Nationen in mittleren bis sp&auml;ten 19. Jahrhundert. Andere Einwanderer die folgten waren Puerto Ricaner, Filipinen und Koreaner. Ungef&auml;hr 43.5% der heutigen Bewohner Hawaii's wurden woanders geboren. Und es ist leicht zu sehen, warum Menschen aus aller Welt hierher kommen um sich hier niederzulassen. </p>
            <p> Die Insel Oahu, die mehr Einwohner und Besucher hat als alle anderen Hawaii-Inseln zusammen, wird auch "The Gathering Place" ("Der Versammlungsplatz") genannt. Samoaner, Tonganer, Hawaiianer, Chinesen, Japaner, Maori, Kanadier, Tahitianer, Vietnamesen, Spanier, Schotten, Iren, Italiener und andere Ethnizit&auml;ten haben hier ihre neue Heimat gefunden. Auf welcher Insel auch immer Sie sich aufhalten, Sie werden hier 
              eine Menge Menschen aus aller Welt treffen, Einwohner und Besucher zugleich.</p>
                       <p>Durch die kulturelle Vielfalt bekommt man hier auch eine Menge Sprachen zu h&ouml;ren, unter anderem Samoanisch, Portugiesisch, Japanisch, Hawaiianisch, Chinesisch und Pidgin, ein &ouml;rtlicher Dialekt, der ein Mix aus Hawaiianisch und Englisch ist.</p>
            <h3>Tourismus und das Milit&auml;r auf Hawaii</h3>
            <p>Das sp&auml;te 19. Jahrhundert ist auch gekennzeichnet durch den Beginn des lukrativen Tourismus auf Hawaii, eine Industrie die mehr anwuchs als Hawaii im Jahre 1959 zum 50. US-Bundesstaat ernannt wurde und die ersten gro&szlig;en Flugzeuge hier eintrafen. Im Jahr 2000 gab es einen Besucherrekord von 6.9 Millionen Besuchern. Obwohl die Terrorattacken vom September 2001 die Tourismusindustrie negative beeinflusst haben, hat sie sich nur drei Jahre sp&auml;ter, in 2004, wieder erholt. </p>
            <p> Als ein Tor zum Pazifik ist auf Hawaii, insbesondere auf Oahu, auch das Milit&auml;r ans&auml;ssig. Deshalb gibt es eine hohe milit&auml;rische Pr&auml;senz auf Hawaii: Marine Corp Base (MCB) Hawaii, Wheeler Army Airfield, Schofield Barracks und Fort Shafter Army Bases, Barbers Point Coast Guard Air Station, MCBH Kaneohe Bay, Hickam Air Force  Base und Naval Station Pearl Harbor. </p>
