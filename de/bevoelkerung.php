            <p><strong>Stetes Bev&ouml;lkerungswachstum</strong></p>
            <p> In 2003 hat das US Zensusb&uuml;ro Hawaii an 10. Stelle f&uuml;r Bev&ouml;lkerungswachstum in den USA eingeordnet. Als Gr&uuml;nde hierf&uuml;r werden die konsistente Zuwanderung, eine stabile Wirtschaft und die niedrige Arbeitslosenrate gesehen. Hawaii's derzeitige Bev&ouml;lkerungsanzahl von etwas mehr als 1.3 Millionen liegt haupts&auml;chlich an der hohen milit&auml;rischen Pr&auml;senz und des Tourismus. Ungef&auml;hr 72% aller Einwohner wohnen in gr&ouml;&szlig;eren St&auml;dten, 
              wie Honolulu. Die Bev&ouml;lkerung Hawaii's kann zu Zeiten von kulturellen oder sportlichen Veranstaltungen noch weiter anschwellen, wie zum Beispiel zu den Aloha Festivals von August bis Oktober, dem Merrie Monarch Festival in Hilo im Februar, dem Honolulu Marathon im Dezember oder dem Triple Crown of Surfing, der jedes Jahr im November und Dezember Tausende Surfer und Surffans von der ganzen Welt an die Nordk&uuml;ste Oahu's 
              lockt.</p>
            <p> Jedes Jahr reisen Millionen von Besuchern zu den wundersch&ouml;nen Str&auml;nden Hawaii's und um die kulturelle und landschaftliche Vielfalt der Inseln zu entdecken. Hawaii ist der perfekte Ort um das beste von verschiedenen Welten zu genie&szlig;en - vom sprudelnden Stadt- und Nachtleben zu ruhigen Strandspazierg&auml;ngen, Hawaii bietet einfach alles. Besuchen Sie w&auml;hrend Ihres Aufenthaltes das Polynesian Cultural Center (Polynesisches  Kulturzentrum) an der Nordk&uuml;ste Oahu's, entdecken Sie das komplexe Kunsthandwerk eines buddhistischen Tempels, oder probieren Sie einfach mal die pazifische K&uuml;che 
              um auf den Geschmack der kulturellen Vielfalt Hawaii's zu kommen.</p>
