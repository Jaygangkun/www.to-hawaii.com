          <h1>Regierung Hawaiis</h1>
          <div class="midpanel_row">
            <h2>Wer regiert die Hawaii-Inseln?</h2>
            <p>Die Regierung Hawaiis ist &auml;hnlich die der anderen US-Bundesstaaten, aber es gibt auch einige Unterschiede. Die Konstitution des Staates Hawaii wurde von der Konstitution des K&ouml;nigreiches Hawaii abgeleitet und etwas modifiziert, sodass sie nicht mit der 
              US-Konstitution in Konflikt steht.</p>
            <p> Die offiziellen Sprachen sind englisch und hawaiianisch, jedoch ist es in der Praxis so, dass offizielle Dokumente nicht in beiden Sprachen gedruckt werden m&uuml;ssen, ebensowenig wie Regierungsbeamte beide Sprachen sprechen m&uuml;ssen. Der Tag an dem Hawaii in die Union der Vereinigten Staaten eintrat hei&szlig;t Admission Day. Er wird j&auml;hrlich am dritten Freitag im August gefeiert.</p>
            <p><strong> <img border="0" src="../images/iolani.jpg" width="148" height="125" align="right" alt="Iolani Palast" />Die Zeit vor dem Eintritt in die Union</strong></p>
            <p> Die Souver&auml;nit&auml;t Hawaiis wurde am 12. August 1898 formell mit Zeremonien, die am Iolani Palast stattfanden, auf die USA &uuml;bertragen. Sanford Dole hielt eine Rede als der neubenannte Gouverneur des hawaiianischen Territoriums.</p>
            <p> Die hawaiianische Staatshymne, <em>Hawaii Pono&#39;i</em> - die von K&ouml;nig Kalakaua geschrieben wurde - wurde gespielt w&auml;hrend die hawaiianische Flagge gesenkt und von der amerikanischen Flagge, dem Sternenbanner, ersetzt wurde. </p>
            <p><strong>Die Bem&uuml;hungen um Unionsbeitritt</strong></p>
            <p> Einige Hawaiianer dr&auml;ngten auf den Beitritt Hawaiis in die Vereinigten Staaten nach dem Zweiten Weltkrieg, aber der Kongress war dem vorerst abgeneigt, teils wegen sozialen sowie auch politischen Bedenken. Das US-Repr&auml;sentantenhaus verabschiedete einen Gesetzesentwurf &uuml;ber einen Beitritt Hawaiis in 1947, der US-Senat jedoch lehnte ihn ab.</p>
            <p> Es dauerte bis zum Jahre 1959, nachdem Alaska zum 49. US-Bundesstaat ernannt wurde, dass der Kongress auch Hawaii in die Union eintreten lie&szlig;. Nachdem im Januar 1959 Alaska der Union beitrat, wurde eine neue Flagge mit 49 Sternen kreiert, die dann &uuml;ber der Independence Hall (Unabh&auml;ngigkeitshalle) in Philadelphia am 4. Juli 1959 wehte. Sie war die offizielle Flagge der USA f&uuml;r jediglich ein Jahr lang. Am 4. Juli 1960 wurde die Flagge mit den 50 Sternen die 27. Flagge der USA, die bis zum heutigen Tage die offizielle Flagge ist. </p>
            <? include ("../theme/languages/de-text.html") ?>
            <p> President Dwight Eisenhower unterschrieb das Gesetz am 18. M&auml;rz und die Frage &uuml;ber den Beitritt Hawaiis wurde dann dem hawaiianischen Elektorat vorgelegt, die am 27. Juni 1959 mit einer Mehrheit von 17-1 f&uuml;r den Beitritt Hawaiis stimmte. Am 21. August 1959 wurde Hawaii zum 50. US-Bundesstaat. </p>
            <p><strong> <img border="0" alt="" src="../images/hawaiistate.jpg" width="148" height="125" align="left" />Die Zeit nach dem Eintritt in die Union</strong></p>
            <p> Im Jahre 1969 wurde der Bau eines neuen Staatskapitols abgeschlossen. Hawaiis Konstitution wurde im Jahre 1950 entworfen und trat im Jahre 1959 nach der Aufnahme in die Union in kraft. Ein Gouverneur, der alle vier Jahre gew&auml;hlt wird, ist der Kopf des exekutiven Regierungszweiges. </p>
            <p> Die gesetzgebende Gewalt (Legislative) hat einen Senat mit 25 Mitgliedern, die auf vier Jahre gew&auml;hlt werden, und ein Repr&auml;sentantenhaus mit 51 Mitgliedern, die auf zwei Jahre gew&auml;hlt werden. Der Staat w&auml;hlt zwei Repr&auml;sentanten und zwei Senatoren in den US-Kongress und hat vier Wahlstimmen.</p>
            <p> Hawaii ist seit langem als demokratischer Staat bekannt. John A. Burns, ein Demokrat, wurde im Jahre 1962 zum Gouverneur gew&auml;hlt und in den Jahren 1966 und 1970 wieder gew&auml;hlt. Daniel Inouye, ein demokratischer Senator, wurde im Jahre 1962 gew&auml;hlt und war der Vorsitzende des Iran-Kontra Kommittees von 1987 bis 1988.</p>
            <p> Verteidigung, Tourismus und Lebensmittelverarbeitung sind die wichtigsten Hauptumsatztr&auml;ger der Wirtschaft Hawaiis. Der Staat Hawaii wird immer wichtiger als wirtschaftliche, bildende und kulturelle Br&uuml;cke zwischen den USA und den asiatischen und pazifischen Nationen. </p>
                        <p> In den 1990er Jahren stie&szlig;en die Hawaiianer auf die Herausforderung, die nat&uuml;rliche  Sch&ouml;nheit ihrer Inseln zu bewahren und gleichzeitig gen&uuml;gend Lebensraum f&uuml;r die wachsende Inselbev&ouml;lkerung, insbesondere auf Oahu, zu haben. Im Mai 2000 erteilte der damalige Pr&auml;sident Bill Clinton den Auftrag an bundesstaatliche Agenturen, ihre K&uuml;stenschutzprogramme zu erweitern, einschlie&szlig;lich der die Hawaiis Korallenriffe sch&uuml;tzen.</p>
            <p><strong>Hawaiianische Ureinwohner dr&auml;ngen auf Souver&auml;nit&auml;t</strong></p>
            <p> Ein andauerndes politisches Thema in den letzten Jahren war das Erreichen von Souver&auml;nit&auml;t der hawaiianischen Ureinwohner. Die Kontrolle von gesch&auml;tzten 8.093.713 Quadratkilometern steht auch auf dem Spiel.</p>
            <p> Im Jahre 1996 stimmten die Mehrheit der cirka 200.000 Nachkommen der hawaiianischen Ureinwohner f&uuml;r die Gr&uuml;ndung einer Form souver&auml;ner Selbstregierung. Im August 1998, dem 100-j&auml;hrigen Jahrestag der Annexion Hawaiis, marschierten Protestierende in der Hauptstadt Washington und forderten volle Souver&auml;nit&auml;t von der bundesstaatlichen Regierung. </p>
            <p> Im Juli 2000 erhielt die Bewegung Unterst&uuml;tzung durch einen dem Kongress vorgelegten Gesetzesentwurf vom hawaiianischen Senator Daniel Akaka. Dem Gesetzesentwurf zugrunde w&uuml;rde es hawaiianischen Ureinwohnern erlaubt sein, ihre eigene Regierung zu gr&uuml;nden und einen Status &auml;hnlich wie der der American Indians (Indianer) zu haben.</p>
            <p><strong>Die Zeit nach dem 11. September 2001</strong></p>
            <p> Hawaiis Tourismusindustrie wurde durch die Terroranschl&auml;ge vom 11. September 2001 und den Beginn des Irakkrieges im Jahre 2003 negativ beeinflusst. Bis zum April 2003 ging die Tourismusindustrie auf Hawaii um cirka ein Drittel zur&uuml;ck. </p>
            <p> In diesem Jahr debatierte die Hawaii Legislatur &uuml;ber eine nicht bindende Resolution, die Teile des USA PATRIOT Acts (Abk&uuml;rzung f&uuml;r <em>Uniting and Strengthening America  by Providing Appropriate Tools Required to Intercept and Obstruct Terrorism Act  of 2001</em>, auf deutsch in etwa: Gesetz zur Einigung und St&auml;rkung der USA durch Bereitstellung geeigneter Werkzeuge, um Terrorismus aufzuhalten und zu blockieren.) und Teile des 2002 Homeland Security Acts (Gesetz zur Sicherheit des Heimatlandes) verbannen w&uuml;rde. Zur eigentlichen Durchf&uuml;hrung kam es jedoch nie. Hawaii w&auml;re der 
              erste Staat gewesen, der gegen diese beiden Gesetze vorgegangen w&auml;re.</p>
            <p><strong> <img border="0" src="../images/hawaiistateseal.gif" width="231" height="228" align="right" alt="Hawaii?s Staatswappen" />Interessante 
              Fakten &uuml;ber Hawaiis Staatswappen</strong></p>
            <p> Hawaiis Staatsmotto, wie es auf dem Staatswappen steht, lautet &quot;Ua mau ke ea o ka aina i ka pono&quot; - &quot;Das Leben (Souver&auml;nit&auml;t) des Landes wird bewahrt durch Rechtschaffenheit.&quot; Dieser Satz wurde zum Motto des hawaiianischen K&ouml;nigreichs nachdem er von K&ouml;nig Kamehameha III am 31. Juli 1843 gesprochen wurde. An diesem Tag, gefolgt von der Proklamation von K&ouml;nigin Victoria, wurde die Sourer&auml;nit&auml;t Hawaiis nach einer f&uuml;nfmonatigen 
              britischen Inbesitznahme wiederhergestellt. Am 1. Mai 1959 wurde dieser Satz zum offiziellen Staatsmotto ernannt. Hier sind ein paar interessante Fakten &uuml;ber das Staatswappen:</p>
            <p>1959 steht f&uuml;r das Datum der Eigenstaatlichkeit.</p>
            <ul>
              <li>Die aufgehende Sonne ersetzt die k&ouml;nigliche Krone und das Malteserkreuz 
                und deutet die Geburt eines neuen Staates an. </li>
              <li>K&ouml;nig Kamehameha der Gro&szlig;e und die Freiheitsg&ouml;ttin, die die hawaiianische 
                Flagge h&auml;lt, ersetzen die zwei Krieger.</li>
              <li>Die vier Streifen der hawaiianischen Flagge in jedem der ersten und vierten 
                Viertel repr&auml;sentieren die acht Hauptinseln Hawaiis. </li>
              <li>Puloulou, oder tabu Ball, im zweiten und dritten Viertel wurde vor den K&ouml;nig 
                getragen und vor seine Haust&uuml;r gelegt um seine Autorit&auml;t und Kraft zu w&uuml;rdigen. </li>
              <li>Hier ist es ein Symbol der Autorit&auml;t und Kraft der Regierung.</li>
              <li>Der Ph&ouml;nix, ein Symbol von Tod und Auferstehung, symbolisiert den Wechsel 
                von der Monarchie zu einer freieren demokratischen Form der Regierung. </li>
              <li>Die acht Tarobl&auml;tter, umrandet von Bananenstauden und Frauenhaar Fernen, 
                sind typische hawaiianische Pflanzen. Taro war ein Haupterzeugnis der Inseln 
                und hatte gro&szlig;e religi&ouml;se Bedeutung. </li>
            </ul>
