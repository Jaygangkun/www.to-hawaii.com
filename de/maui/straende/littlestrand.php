              <h2 class="small">Little Beach Info</h2>
              <p>Little Beach ist der einzige unoffizielle Nacktbadestrand auf Maui. Er liegt direkt neben dem bekannteren und viel gr&ouml;&szlig;eren Big Beach. Beide Str&auml;nde befinden sich im Makena State Park. Nacktbaden ist im gesamten Staat Hawaii zwar verboten, es wird aber hier am Little Beach meist nicht kontrolliert. Die kleine Bucht liegt versteckt vor dem Lavah&uuml;gel namens Pu'u Ola'i und ist von der Stra&szlig;e aus nicht zu sehen. Man erreicht den Strand nur &uuml;ber einen schmalen Trampelpfad. Die Bedingungen zum Schwimmen und Schnorcheln sind hier gut wenn das Meer ruhig ist. Allerdings sind hier keine Rettungsschwimmer stationiert, sodass man auf sich allein gestellt ist, sollte man in Schwierigkeiten geraten.</p>
