              <h2 class="small">Big Beach (Oneloa/Makena Beach) Info</h2>
              <p>Wie der Name Big Beach (Gro&szlig;er Strand) schon sagt, gibt es  hier genug Sand f&uuml;r alle. Big Beach ist ca. 1 km lang und &uuml;ber 30 m breit und  ist einer der beliebtesten Str&auml;nde auf Maui. Big Beach  ist auch unter den Namen Oneloa   Beach oder Makena   Beach bekannt. </p>
              
              <p>Hier gibt es alles, was man f&uuml;r einen sch&ouml;nen Tag am Strand  ben&ouml;tigt - weichen Sand, Toiletten, einen Parkplatz und einen wundersch&ouml;nen  Blick auf das azurblaue Meer. Wenn das Meer ruhig ist, ist Schwimmen und  Schnorcheln hier m&ouml;glich. Wenn das Wasser turbulent ist, entstehen hier  gef&auml;hrliche Rei&szlig;str&ouml;mungen, und dann es ist besser das Schwimmen zu meiden.</p>
              <p> N&ouml;rdlich von Big Beach liegt Little Beach. Diesen Strand kann man &uuml;ber einen  Fu&szlig;weg vom Big Beach  aus erreichen. Little Beach ist ein unoffizieller Nacktbadestrand (&ouml;fftenliche  Nacktheit ist in Hawaii  gesetzlich verboten). </p>
              <p> Big Beach und Little Beach geh&ouml;ren zum Makena   State Park. Der H&uuml;gel den man von  beiden Str&auml;nden aus sehen kann hei&szlig;t Pu'u Ola'i (oder auch Red Hill oder  Earthquake Hill). Er ist vulkanischen Ursprungs und 110 m hoch.</p>
