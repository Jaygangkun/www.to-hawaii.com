              <h2 class="small">Waianapanapa Black Sand Beach Info</h2>
              <p>Dieser schwarze Sandstrand mit dem Zungenbrechernamen ist bildh&uuml;bsch. Viele  Tourbusse halten hier auf dem Weg zu anderen Sehensw&uuml;rdigkeiten in der Region.  Waianapanapa (auch unter dem Namen Pailoa   Beach bekannt) liegt an Mauis  Nordk&uuml;ste entlang des bekannten Hana Highways. Der rabenschwarze Sand ist  vulkanischen Ursprungs und bietet einen intensiven Kontrast zu dem &uuml;ppigen Gr&uuml;n  der Landschaft und dem tiefblauen Meer. </p>
              <p> Das Schwimmen ist hier nur m&ouml;glich, wenn das Meer ruhig ist.  Rettungsschwimmer sind hier keine stationiert, sodass man auf sich allein  gestellt ist. Wenn die Wellen hoch sind, k&ouml;nnen Rei&szlig;str&ouml;mungen entstehen. In  der N&auml;he des Strandes befinden sich zwei H&ouml;hlen, die von einer S&uuml;&szlig;wasserquelle  gespeist werden. </p>
