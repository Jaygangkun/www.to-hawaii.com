              <h2 class="small">Hamoa Beach Info</h2>
              <p>Hamoa Beach  ist einer der entlegendsten Str&auml;nde auf Maui. Dieser  wundersch&ouml;ne Strand liegt s&uuml;dlich des Dorfes Hana an Mauis Ostk&uuml;ste, am Ende  der bekannten Road to Hana (Hana Highway).  Dieser Strand machte selbst einen Eindruck auf James Michener, Autor des  bekannten Romans <em>Hawaii</em>.  Michener nannte Hamoa Beach  einen der sch&ouml;nsten Str&auml;nde im Pazifik. </p>
              <p>Dieser sog. "Salz- und Pfeffer-Strand" (eine Mischung aus  wei&szlig;em und schwarzen Sand) ist umrahmt von tropischer Vegetation und gr&uuml;nen  H&uuml;geln. Wenn das Wasser ruhig ist, kann man hier schwimmen und bodysurfen.  Allerdings sollte man nicht zu weit rausschwimmen, da hier keine  Rettungsschwimmer stationiert sind und Rei&szlig;str&ouml;mungen jederzeit auftreten  k&ouml;nnen. </p>
