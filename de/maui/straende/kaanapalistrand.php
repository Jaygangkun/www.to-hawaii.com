              <h2 class="small">Kaanapali Beach Info</h2>
              <p>Kaanapali Beach  ist einer der bekanntesten Str&auml;nde auf Maui. Viele  Hotels s&auml;umen den Strand. Am n&ouml;rdlichen Ende ist Pu'u  Keka'a, auch Black Rock (Schwarzer Fels) genannt. Um diesen Felsen herum sind  die Bedingungen zum Schnorcheln gut, wenn das Meer ruhig ist. Desto weiter  s&uuml;dlich man l&auml;uft, desto mehr Steine befinden sich im Wasser. Die besten  Bedingungen zum Schwimmen sind somit nahe des Black Rocks. </p>
              <p> Ein gepflasterter Weg f&uuml;hrt am Strand entlang, somit  kann man hier sch&ouml;n schlendern. Vor allem abends im Sonnenuntergangslicht ist  dies hier besonders sch&ouml;n, und es gibt auch eine Reihe an Restaurants, die  direkt am Strand liegen.</p>
