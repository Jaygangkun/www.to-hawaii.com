              <h2 class="small">La Perouse Bay Info</h2>
              <p>La Perouse Bay befindet sich an Mauis S&uuml;dk&uuml;ste im Ahihi Kinau Natural Area  Reserve, einem Naturschutzgebiet. Die gesamte Gegend ist ein Lavafeld und daher  gibt es hier keinen Sandstrand. La Perouse   Bay hat einen Ruf als guten Schnorchelstrand,  allerdings nicht die Bucht selbst, sondern die vielen kleinen Buchten weiter  n&ouml;rdlich. Allerdings ist der meiste Bereich des Naturschutzgebietes bis zum 31.  Juli 2014 f&uuml;r die &Ouml;ffentlichkeit geschlossen (hier ist eine <a href="http://hawaii.gov/dlnr/dofaw/nars/reserves/maui/Ahihi%20Closure%20Sign.jpg" rel="nofollow" target="_blank">Karte</a> des geschlossenen Bereichs). Man will somit dem &Ouml;kosystem Zeit geben, sich von  der jahrelangen &Uuml;berbenutzung zu erholen.</p>
              
              <p>Eine alternative Schnorchelstelle die noch offen ist, ist Moanakala  (auch unter dem Namen Dumps bekannt). Dumps befindet sich am n&ouml;rdlichen Ende  des Naturschutzgebietes, s&uuml;dlich von Ahihi Cove. Die Schnorcheln ist hier  allerdings nichts f&uuml;r Anf&auml;nger, da der Wind sehr stark sein kann und die  Str&ouml;mungen stark. Zugang zu dem n&ouml;rdlichen Ende des Naturschutzgebietes ist  erlaubt zwischen 5:30 Uhr und 19:30 Uhr. </p>
              <p> La Perouse wurde nach dem franz&ouml;sischen Marineoffizier Jean-François de La  Pérouse benannt, der der erste Europ&auml;er war, der im Jahre 1786 einen Fu&szlig; auf  Maui setzte. Der hawaiianische Name der Bucht ist Keone'o'io.</p>
