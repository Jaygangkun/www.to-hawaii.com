              <h2 class="small">Honomanu Bay Info</h2>
              <p>Diese sch&ouml;ne Bucht liegt an der bekannten und vielbefahrenen Road to Hana (Hana    Highway) an Mauis Nordk&uuml;ste, zwischen Kahului und  Hana (zwischen Meilenmarker 13 und 14). Viele Leute halten hier an, um Fotos zu  machen. Allerdings gibt es keinen Parkplatz, sondern lediglich kleine  Haltebuchten entlang der Stra&szlig;e. </p>
              <p> Mit dem Auto kann man nicht runter zum Strand fahren.  Um zum Strand zu gelangen (kein Sandstrand, sondern  schwarze Steine), muss man den ungeteerten Zugangsweg herunterlaufen, der ca.  800 m hinter dem Meilenmarker 13 beginnt.</p>
              <p> Den besten Ausblick auf den Strand hat man allerdings  direkt vom Hana Highway.  Die Bucht eignet sich nicht zum Schwimmen, da das Wasser hier oft turbulent und  der Meeresboden steinig ist. </p>
