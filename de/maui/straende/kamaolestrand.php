              <h2 class="small">Kamaole Beach Park Info</h2>
              <p>Kamaole Beach  hat drei Abschnitte, Kamaole I, II und III. Kamaole I ist der gr&ouml;&szlig;te Abschnitt.  Es ist ein langer Sandstrand, an dem man gut Baden kann  wenn das Meer ruhig ist. Am s&uuml;dlichen Ende des Strandes ist eine Rasenfl&auml;che,  wo man gut picknicken kann. Das n&ouml;rdliche Ende von Kamaole I ist auch unter dem  Namen Charley   Young Beach  bekannt. </p>
              
              <p> Weiter s&uuml;dlich ist Kamaole II, ein etwas kleinerer Sandstrand, wo man auch  gut schwimmen kann. Der Meeresgrund f&auml;llt hier schnell ab und kleine Wellen  schaffen an den meisten Tagen gute Bedingungen f&uuml;r Bodysurfer. Wenn der Wind  kr&auml;ftig aus dem S&uuml;den blast und die Wellen h&ouml;her sind und den Sand von diesem Strand  abtragen, werden hier oft Steine freigelegt. Wenn die Wellen &uuml;ber diese Steine  waschen, entsteht ein brausendes Get&ouml;se, was die alten Hawaiianer <em>ili ili</em> nannten.  Daher ist der Strand auch unter dem Namen Iliiliholo bekannt, was wortw&ouml;rtlich  "laufende Steine" bedeutet.</p>
              <p> Kamaole III hat einen kurzen, aber breiten Sandstrand. Es ist der kleinste  Strandabschnitt, aber er hat eine gro&szlig;e Wiese und ist meist sehr gut besucht,  da sich hier auch ein Kinderspielplatz befindet und die meisten Parkpl&auml;tze.</p>
