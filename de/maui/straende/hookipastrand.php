              <h2 class="small">Hookipa Beach Park Info</h2>
              <p>Hookipa ist einer der weltbekanntesten Str&auml;nde zum Windsurfen. Der Strand  befindet sich an der Road to Hana (Hana Highway)  an Mauis Nordk&uuml;ste. Zwei professionelle Windsurf-Wettbewerbe finden hier jedes  Jahr statt: Aloha Classic und Red Bull. </p>
              <p> Die Wellen k&ouml;nnen hier vor allem in den Wintermonaten sehr hoch werden,  daher ist das Surfen hier nur etwas f&uuml;r Profis. Den besten Blick auf das  Geschehen hat man von einem nahegelegenen Aussichtspunkt auf den Klippen.</p>
              <p> Der Strand hat nur wenig Sand und der Meeresgrund ist von Steinen und einem  Riff &uuml;berzogen. Die Str&ouml;mungen k&ouml;nnen hier sehr stark sein, daher gibt es  bessere Str&auml;nde zum Schwimmen.</p>
