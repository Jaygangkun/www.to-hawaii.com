            <p><strong>G&ouml;tter, Geister und Kobolde</strong></p>
            <p> Hawaii ist voll von Mythen und Legenden - Geschichten die voll von Leidenschaft,   Betrug, Loyalit&auml;t, Liebe, Geburt und Tod sind. Laut W.D. Westervelt, einem der bekanntesten Geschichtenerz&auml;hlern &uuml;ber die Mythen und Legenden Hawaiis in den fr&uuml;hen 1900er Jahren, sind einige dieser Mythen und Legenden sehr &auml;hnlich der Geschichten, die auf Tahiti, Samoa, Fiji, Neuseeland und anderen Inseln im Pazifik erz&auml;hlt wurden.</p>
