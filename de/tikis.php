              <p><strong>Was 
                sind Tikis?</strong></p>
              <p>Was sind diese mythischen Symbole und was haben Sie mit der Kultur Hawaiis zu tun? Falls Sie schon mal diese h&ouml;lzernen handgeschnitzten Figuren gesehen haben, werden Sie wahrscheinlich deren ernste Gesichtsausdr&uuml;cke bemerkt haben. Diese Tikis stehen f&uuml;r sehr wichtige, respektierte Autorit&auml;ten, n&auml;mlich Hauptg&ouml;tter, H&uuml;ter und spirituelle Kr&auml;fte.</p>
              <p>Nur sehr wenige Tiki-Liebhaber sind sich der einmaligen Geschichte und Spiritualit&auml;t dieser Tiki-Figuren bewusst. Aber auch eine nur kurze Beschreibung dieser alten Herkunft der Figuren wird Ihnen eine Ahnung geben &uuml;ber die essentielle symbolische Rolle, die Tikis in der traditionellen hawaiianischen Gesellschaft spielen.</p>
