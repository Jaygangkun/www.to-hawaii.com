              <p><strong>Die 
                Herkunft der Tikis</strong></p>
              <p>Die ersten Bewohner Hawaiis kamen vor &uuml;ber eintausend Jahren von Polynesien nach Hawaii und brachten ihre religi&ouml;sen und spirituellen &Uuml;berzeugungen mit. Die vielen G&ouml;tter von Hawaii und Polynesien wurden durch Tikis repr&auml;sentiert. Der Name Tiki kann f&uuml;r viele Typen von Bildern im ganzen polynesischen Raum verwendet werden, von Bildern die zeremoniell von den Maori St&auml;mmen in Neuseeland verwendet werden, bis hin zu den Moa Schnitzereien auf der Osterinsel bis hin zu den modernen Bildern, die es auf Hawaii gibt.</p>
              <p>In der polynesischen Mythologie repr&auml;sentiert der Tiki oft den ersten Menschen auf der Erde. Diese Bilder werden noch heute in manchen polynesischen Kulturen bei der spirituellen Praxis benutzt. Es ist nicht selten dass kleine Tiki-Figuren zum Schutz vor Unfruchtbarkeit in Neuseeland getragen werden.</p>
              <p>In der alten hawaiianischen Kultur hatten die G&ouml;tter, das aina, oder Land, und die kanaka, oder Menschen, eine symbiotische Existenz. Wenn die Menschen das Land "pono" (richtig) behandelten, waren die G&ouml;tter bes&auml;nftigt. Wenn die G&ouml;tter zufrieden waren, erlaubten sie es dem Land f&uuml;r eine reiche Ernte zu sorgen. Jeder Gott hat viele "kinolau", oder Formen, einschlie&szlig;lich menschliche und tierische.</p>
              <p>Tiki-Figuren wurden geschnitzt, um das Bild eines bestimmten Gottes darzustellen und als eine Verk&ouml;rperlichung der speziellen Kraft, oder "mana", dieses Gottes. Mit wohlgeformten Tikis konnten sich die Menschen vor Schaden sch&uuml;tzen, ihre Kr&auml;fte zu Kriegszeiten st&auml;rken und mit einer reichen Ernte gesegnet werden.</p>
              
              <p><strong>Die hawaiianischen Hauptg&ouml;tter, die durch Tikis dargestellt wurden, sind:</strong></p>
              <p><strong class="subtitle">Ku </strong>- der Kriegsgott</p>
              <p><strong class="subtitle">Lono </strong>- der Gott der Landwirtschaft und des Friedens</p>
              <p><strong class="subtitle">Kane </strong>- der Gott der Sch&ouml;pfung, des Sonnenlichts, der W&auml;lder und des Trinkwassers</p>
              <p><strong class="subtitle">Kanaloa</strong> - der Gott des Meeres </p>
              <p>Heutzutage kann man gro&szlig;e Holzstatuen an verschiedenen Orten auf den Hawaii-Inseln finden, einschlie&szlig;lich des Polynesischen Kulturzentrums an der Nordk&uuml;ste der Insel Oahu. Auf der Sand Island in Honolulu ist die La Marianas Tiki Bar, wahrscheinlich die bekannteste Tiki Bar auf Hawaii, wo Besucher einen tropischen Cocktail genie&szlig;en k&ouml;nnen und gleichzeitig eine gro&szlig;e Anzahl an Tiki-Sammlungsst&uuml;cken bestaunen k&ouml;nnen. Besucher auf Hawaii k&ouml;nnen kleine h&ouml;lzerne Tiki-Figuren buchst&auml;blich &uuml;berall finden. Aber wenn Sie durch diese touristischen, massengefertigten Tiki-Artikel st&ouml;bern 
                bedenken Sie, dass diese Figuren einst die geliebten und hochgeachteten hawaiianischen G&ouml;tter repr&auml;sentierten.</p>
