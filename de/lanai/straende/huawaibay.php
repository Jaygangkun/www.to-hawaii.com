              <h2 class="small">Huawai Bay Info</h2>
              <p>Huawai Bay ist sehr einfach &uuml;ber einen Trampelpfad (Fisherman's Trail) vom Nachbarstrand Hulopoe Bay zu erreichen, ein ca. 10-min&uuml;tiger Fu&szlig;weg. Die kleine steinige Bucht ist von Felsen umrandet, und am besten &uuml;berblickt man sie direkt vom Fu&szlig;weg aus. Herunter bis zum Strand zu klettern braucht man also nicht. Ein Badestrand ist Huawai Bay auch nicht, da der Meeresgrund steinig ist und die Wellen und Str&ouml;mungen auch stark sein k&ouml;nnen. Oberhalb der Bucht befindet sich der par-72 Championship Challenge at Manele Golfplatz, der von Jack Nicklaus entworfen worden ist.</p>
