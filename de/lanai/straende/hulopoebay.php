              <h2 class="small">Hulopoe Bay Info</h2>
              <p>Dies ist der bekannteste und am meisten besuchte Strand  auf Lanai. Er befindet sich im S&uuml;den der Insel in der  N&auml;he des Four Seasons Resort Lanai at Manele   Bay. Wenn man nach einem Badestrand  auf Lanai sucht, dann ist Hulopoe   Bay der am besten geeignete.  Allerdings sind auch hier keine Rettungsschwimmer stationiert (wie nirgends  sonst auf der Insel) und man ist somit auf sich allein gestellt, falls man in  Schwierigkeiten ger&auml;t. Die Wellen und vor allem die Brandung sind hier oftmals  ziemlich kr&auml;ftig und somit f&uuml;r unerfahrene Schwimmer nicht ratsam. </p>
              <p>Der Strand an sich ist wundersch&ouml;n und hat Picknicktische  und eine Rasenfl&auml;che. Die Bucht ist ein Naturschutzgebiet. Da das Angeln  verboten ist, sind die Fische hier besonders zahlreich. Auch Delfine und  Schildkr&ouml;ten kann man hier oft beobachten. Hulopoe   Bay liegt neben der Manele Bay und  dem dortigen kleinen Hafen, wo die F&auml;hre von Maui ankommt.</p>
