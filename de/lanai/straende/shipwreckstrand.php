              <h2 class="small">Shipwreck Beach Info</h2>
              <p>Shipwreck Beach (Schiffbruch-Strand) hei&szlig;t so, weil sich hier schon so  einige Schiffe auf dem flachen Riff festgefahren haben. Die <em>Alderman Wood</em>, ein  britisches Schiff, verungl&uuml;ckte und sank hier im Jahre 1824 (das erste  dokumentierte Schiffsungl&uuml;ck). Zwei Jahre sp&auml;ter, sank hier die <em>London</em>,  ein amerikanisches Schiff. Man hat damals angenommen, dass die <em>London</em> viel Gold und Silber an Bord hatte, aber niemand wei&szlig;, wie viel davon geborgen  werden konnte. </p>
              
              <p> Bis heute kann man noch die &Uuml;berreste des Liberty Schiffes aus dem Zweiten  Weltkrieg sehen. Allerdings wurde dieses Schiff absichtlich hier festgefahren.  Nachdem der Krieg zu Ende war, war dies der &ouml;konomischste Weg, das Schiff  loszuwerden.</p>
              <p> Der Strand befindet sich an Lanais Nordk&uuml;ste, und abgesehen von ein paar  sich sonnenden Schildkr&ouml;ten und einsamen Fischern findet man hier nur Ruhe und  einen sch&ouml;nen Ausblick. Die Bedingungen zum Baden sind  hier nicht gut wegen des flachen Riffs. </p>
