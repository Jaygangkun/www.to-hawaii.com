              <h2 class="small">Awahua Beach Info</h2>
              <p>Dieser Strand befindet sich an Molokais Nordk&uuml;ste auf der  entlegenen Kalaupapa Peninsula.  Diese Halbinsel ist auf drei Seiten vom Meer umgeben und an der vierten von den  gr&ouml;&szlig;ten Meeresklippen auf der Welt. Dadurch dass die Halbinsel so schwer zu  erreichen ist, wurden dort zwischen den Jahren 1866 und 1969 Menschen  hinverbannt, die an der Hansen-Erkrankung (Lepra) litten. Noch heute leben hier  ein paar dieser fr&uuml;heren Patienten. Grunds&auml;tzlich gilt Lepra heutzutage als  heilbar.</p>
              <p>Ein schmaler, kurviger und steiler Trampelpfad f&uuml;hrt zu der  Halbinsel herunter. Allerdings darf man die Halbinsel nicht auf eigene Faust  besuchen, sondern nur mit einer gef&uuml;hrten Gruppe. Die Halbinsel und Awahua   Beach kann man auch von einem 500 m  hohen Aussichtspunkt aus sehen. Dieser befindet sich im Palaau   State Park und ist vom Parkplatz  aus &uuml;ber einen geteerten Weg zu erreichen (ein 2-min&uuml;tiger Fu&szlig;weg).</p>
