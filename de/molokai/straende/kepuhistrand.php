              <h2 class="small">Kepuhi Beach Info</h2>
              <p>Kepuhi Beach  ist ein sch&ouml;ner langer Sandstrand an Molokais Westk&uuml;ste. Der Strand hat zwei  Abschnitte, die durch Lavafelsen getrennt sind. Die Ferienh&auml;user des Kepuhi  Beach Resorts befinden sich am kleineren n&ouml;rdlichen Abschnitt. </p>
              
              <p> Obwohl das azurblaue Meer einladend aussieht, kann das Schwimmen am Kepuhi   Beach gef&auml;hrlich sein wegen der  starken Str&ouml;mungen. Die Wellen k&ouml;nnen auch ziemlich kr&auml;ftig sein, vor allem in  den Wintermonaten zwischen Oktober und M&auml;rz. Haupts&auml;chlich erfahrene und  einheimische Surfer gehen hier ins Wasser. </p>
              <p> Aber alles in allem ist Kepuhi Beach  trotz des kleinen Resorts nur sp&auml;rlich besucht, wie so ziemlich alle Str&auml;nde  auf der ruhigen Insel Molokai. </p>
