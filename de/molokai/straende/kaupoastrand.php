              <h2 class="small">Kaupoa Beach Info</h2>
              <p>Kaupoa Beach  an Molokais Westk&uuml;ste ist durch Lavasteine in zwei H&auml;lften geteilt. Der  Meeresgrund in Strandn&auml;he ist meist sandig, aber er f&auml;llt steil ab. Wenn das  Meer ruhig ist (was meist in den Sommermonaten der Fall ist), sind die  Bedingungen zum Schwimmen hier am besten. Allerdings sind hier keine  Rettungsschwimmer stationiert (wie nirgends sonst auf der Insel).</p>
              
              <p> In dieser Gegend hat es schon so einige Schiffsungl&uuml;cke gegeben. Eines war  dass der <em>Carrier Dove</em>,  ein amerikanisches 800-Tonnen schweres, viermastiges Cargo-Schiff, dass auf dem  Weg von Tonga nach Kanada war. Am 4. November 1921 fuhr das Schiff mitten in  der Nacht auf einen Felsen in der N&auml;he von Kaupoa Beach. Es zerbrach binnen  weniger Stunden. Die <em>Carrier  Dove</em> hatte eine interessante Geschichte. Vor ihrem letzten Tag hatte  sie heftige St&uuml;rme, Piratenattacken und zwei fr&uuml;here Festfahrungen &uuml;berstanden.</p>
              <p> Am Kaupoa Beach  gab es fr&uuml;her 40 Tentalows (kleine Zelt-H&uuml;tten), wo man campen konnte. Das   Kaupoa Beach Village  (Kaupoa Stranddorf) wurde in 2008 geschlossen, als der Eigent&uuml;mer, Molokai  Ranch, geschlossen wurde.</p>
