              <h2 class="small">Poolau Beach Info</h2>
              <p>Poolau Beach ist eine kleine Bucht an Molokais Westk&uuml;ste, s&uuml;dlich vom  bekannteren Papohaku Beach  gelegen. Der Meeresboden in Strandn&auml;he ist eine Mischung aus Sand und Steinen.  Daher ist die Bucht eher zum Picknicken als zum Schwimmen geeignet. Wenn das  Meer ruhig ist (was eher in den Sommermonaten der Fall ist), kommen manchmal  Leute zum Schnorcheln hierher.</p>
              <p> Die Zugangsstra&szlig;e kann man leicht &uuml;bersehen. Sie beginnt an der    Pohakuloa Road. Normalerweise sind alle  &ouml;ffentlichen Zugangsstra&szlig;en in dieser Gegend mit einem Schild "public  right-of-way" gekennzeichnet, aber das Schild f&uuml;r diese Zugangsstra&szlig;e fehlt zur  Zeit.</p>
