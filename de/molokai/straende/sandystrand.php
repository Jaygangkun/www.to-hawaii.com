              <h2 class="small">Sandy Beach Info</h2>
              <p>Sandy Beach an Molokais Ostk&uuml;ste hat einen kleinen Sandstrand. Der  Meeresboden in Strandn&auml;he ist tiefer hier als an den anderen Str&auml;nden in dieser  Gegend (wo meist ein flaches Riff den Zugang zum Wasser erschwert), daher ist  es am Sandy Beach einfacher, ins Wasser zu gelangen. </p>
              
              <p> Das Wasser ist hier auch klarer als an den anderen Str&auml;nden in dieser Region  (mit der Ausnahme von Murphy's Beach). Allerdings muss man sich auch hier vor  starken Str&ouml;mungen in Acht nehmen. Rettungsschwimmer sind hier keine stationiert,  wie nirgends sonst auf Molokai.</p>
              <p> Sandy Beach liegt noch weiter hinter Murphy's Beach, in der N&auml;he des 22.  Mile Markers. Die Stra&szlig;e hierher ist zum Teil kurvig und einspurig (mit vielen  blinden Kurven), sodass schon die Anreise ein Abenteuer ist.</p>
