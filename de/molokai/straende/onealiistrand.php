              <h2 class="small">One Alii Beach Park Info</h2>
              <p>One Alii Beach   Park an Molokais S&uuml;dk&uuml;ste hat zwei  Abschnitte (I und II), die direkt nebeneinander am Kamehameha    Highway liegen. Wenn man von Kaunakakai aus kommt,  ist One Alii II der erste Abschnitt. Der Strand ist in beiden Abschnitten  schmal, und der Meeresboden in Strandn&auml;he ist von einem Riff &uuml;berzogen. Daher  ist dies kein guter Badestrand. Der Strandpark wird haupts&auml;chlich von  einheimischen Familien genutzt, die hier zum picknicken herkommen. Der Park hat  eine gro&szlig;e freie Fl&auml;che, ideal f&uuml;r Ballspiele.</p>
              <p> Zwei Steindenkm&auml;ler markieren den 75. und 100. Jahrestag der ersten  Einwanderer aus Japan  nach Hawaii. In der  hawaiianischen Sprache bedeutet <em>oneali'i</em> "k&ouml;niglicher Sand." Der Strandpark ist nach dem  nahegelegenen Alii Fischteich benannt, der sich am westlichen Ende des 2.  Abschnitts befindet. Allerdings ist er vom Strand aus nicht zu erreichen, da er  von &uuml;ppiger Vegetation umrandet ist.</p>
