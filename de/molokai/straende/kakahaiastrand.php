              <p>Kakahaia ist ein kleiner Strandpark direkt am Highway 450 an Molokais S&uuml;dk&uuml;ste gelegen. Der Strand ist schmal, der Meeresgrund steinig und das Wasser meist tr&uuml;b, sodass es kein Badestrand ist. Haupts&auml;chlich Fischer kommen hierher. Was man hier auch gut machen kann ist V&ouml;gel beobachten, da sich hier ein Vogelschutzgebiet befindet. Oftmals ist es hier sehr windig und man bekommt den Sand um die Ohren geblasen.</p>
