              <h2 class="small">Moomomi Beach Info</h2>
              <p>Moomomi Beach  an Molokais Nordwestk&uuml;ste ist einer der entlegendsten Str&auml;nde auf der Insel.  Diese Gegend hat die gr&ouml;&szlig;ten Sandd&uuml;nen in Hawaii.  Sie sind an manchen Stellen &uuml;ber 6 km breit. Darum nennt man diese unbesiedelte  Gegend auch den Desert Strip (W&uuml;stenstreifen), oder Keonelele, was "vom Winde  verwehter Sand" bedeutet.</p>
              
              <p> Die Gegend ist Teil des 3,7 Quadratkilometer gro&szlig;en Moomomi  Naturschutzgebietes und wird von der Nature Conservancy of Hawaii verwaltet. Diese  Naturschutzorganisation bietet gef&uuml;hrte Wanderungen in diese Gegend an, aber  man muss sich vorher anmelden (was kostenfrei ist). Unter der Nummer  808-553-5236 bekommt man mehr Informationen.</p>
              <p> Falls man selbst in diese Region fahren m&ouml;chte, braucht man ein Auto mit  Allradantrieb. Man muss jedoch bedenken, dass Mietautos nicht auf ungeteerten  Stra&szlig;en zugelassen sind. Daher ist es besser, sich einer gef&uuml;hrten Wandergruppe  anzuschlie&szlig;en.</p>
