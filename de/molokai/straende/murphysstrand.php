              <h2 class="small">Murphy's Beach Info</h2>
              <p>Murphy's Beach an Molokais S&uuml;dostk&uuml;ste ist einer der bekanntesten Str&auml;nde  auf der Insel. Dieser Strand ziert viele Postkarten und Wandkalender. Die  meisten Str&auml;nde an Molokais S&uuml;dostk&uuml;ste haben keine guten Bedingungen zum  Schwimmen, das das Wasser zu tr&uuml;b und der Meeresboden von einem flachen Riff  &uuml;berzogen ist, aber Murphy's Beach ist die Ausnahme. Hier kann man baden gehen  und wenn das Wasser ruhig ist, sogar schnorcheln. Allerdings sollte man nicht  zu weit rausschwimmen, da die Str&ouml;mungen stark sein k&ouml;nnen.</p>
              <p> Etwa 3 km bevor man am Murphy's Beach ankommt wird der Highway einspurig.  Der Strand hat einen kleinen Parkplatz. Da Murphy's Beach genau 20 Meilen (32  km) &ouml;stlich von der Hauptstadt Kaunakakai entfernt ist, wird der Strand  auch Twenty Mile   Beach genannt.</p>
