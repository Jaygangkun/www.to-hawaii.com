              <h2 class="small">Pohakumauliuli Beach Info</h2>
              <p>Pohakumauliuli Beach  an Molokais Westk&uuml;ste besteht aus zwei Buchten, die man nur zu Fu&szlig; vom  nahegelegenen Kepuhi Beach Resort erreichen kann. Da diese Buchten sehr  abgelegen sind, kommen nur wenige Leute hierher. Die Buchten eignen sich zum Sonnenbaden  oder Picknicken, aber das Schwimmen ist zu gef&auml;hrlich, weil die Str&ouml;mung in  Strandn&auml;he stark sein kann. </p>
              
              <p> Der H&uuml;gel im Hintergrund der Buchten hei&szlig;t Pohaku Mauliuli, was "schwarzer  Fels" bedeutet. Ab und zu brechen hier lose Steine ab, daher ist es ratsam,  sich nicht direkt unter diesen Felsen zu sonnen. </p>
              <p> Um zum Pohakumauliuli Beach  zu gelangen, l&auml;uft man vom Nachbarstrand   Kepuhi Beach  in Richtung Norden. Von hier aus f&uuml;hrt ein Pfad an einem alten Goflplatz vorbei.  Nach 5-10 Minuten erreicht man die erste Bucht. Die beiden Buchten sind durch  Felsen getrennt, &uuml;ber den man klettern muss, um die zweite Bucht zu erreichen.</p>
