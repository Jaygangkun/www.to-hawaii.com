              <h2 class="small">Dixie Maru Cove Info</h2>
              <p>Diese Bucht befindet sich am Ende der Stra&szlig;e (Pohakuloa    Road) an Molokais Westk&uuml;ste. Die kurvige Bucht ist  der beste Badestrand in dieser Gegend. Allerdings sind auch hier, wie nirgends  sonst auf der Insel, keine Rettungsschwimmer stationiert, sodass man auf sich  allein gestellt ist, sollte man in Schwierigkeiten geraten. In den Sommermonaten  ist das Meer meist ruhig, im Winter oft sehr turbulent.</p>
              
              <p> Der Name Dixie Maru stammt von einem japanischen Fischerboot, dass hier in  den 1920er Jahren sank. Der hawaiianische Name dieser Bucht ist Kapukahehu.</p>
              <p> Die Bucht ist ges&Auml;umt von dornigen Kiawe-B&auml;umen. Da die Dornen &uuml;ber den Sand  gestreut sind, ist es hier ratsam, die Schuhe anzulassen, oder zumindest mit  Schlappen bis zum Wasser zu laufen. Auch beim Hinsetzen in den Sand ist es eine  gute Idee, vorher nach den Dornen zu schauen und eine Matte unterzulegen.</p>
              <p><strong>Tipp:</strong> Das letzte Mal, als wir diesen Strand  besuchten, ist einer von uns in einen gro&szlig;en Fischhaken (<a href="../../../molokai/beaches/n2.html" onclick="NewWindow(this.href,'name','650','450','yes');return false">see here</a>),  getreten. Zum Gl&uuml;ck hatten wir beide keine Flip Flops, sondern Turnschuhe an,  die eine dickere Sohle haben. Es war sehr schwer, diesen <em>Widerhaken</em> von dem Schuh zu entfernen. Es  kann also sein, dass manche Fischer hier einfach ihre alten Haken an den Strand  werfen.</p>
