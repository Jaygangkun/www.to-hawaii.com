              <h2 class="small">Kiowea Beach Info</h2>
              <p>Dieser Strandpark befindet sich inmitten des gro&szlig;en Kapuaiwa Kokusnusshains  an Molokais S&uuml;dk&uuml;ste. Der Kokusnusswald wurde zu Ehren des H&auml;uptlings Kapuaiwa  benannt, der im Jahre 1863 King Kamehameha V wurde. Man nimmt an, dass entweder  er selbst, oder sein Agent und Ranchmanager Rudolph W. Meyer diese  Kokusnusspalmen gepflanzt hat.</p>
              
              <p> Der Strand ist schmal und hat nur wenig Sand. Der Meeresgrund in Strandn&auml;he  ist steinig und das Wasser flach und tr&uuml;b. Deshalb ist Kiowea   Beach kein Badestrand. In Ufern&auml;he  sind eine Reihe an S&uuml;&szlig;wasserquellen, die hier ins Meer flie&szlig;en.</p>
              <p> Wenn man an den Strand gelangen will, muss man durch den Kokusnusshain  durchlaufen. Dies ist jedoch nicht ungef&auml;hrlich, da die Kokusn&uuml;sse hier einfach  von den Palmen fallen, wenn sie reif sind. Eine schwere Kokusnuss auf den Kopf  zu bekommen ist der Gesundheit nicht zutr&auml;glich. </p>
