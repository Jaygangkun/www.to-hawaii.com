              <h2 class="small">Papohaku Beach Info</h2>
              <p>Mit seinen 3.2 km L&auml;nge ist Papohaku Beach an Molokais Westk&uuml;ste einer der  l&auml;ngesten ununterbrochenen Sandstr&auml;nde in Hawaii.  Es ist der beste Strand auf der Insel f&uuml;r eine lange  Strandwanderung. Der Strand ist ges&auml;umt von niedrigen Sandd&uuml;nen und dorningen  Kiawe-B&auml;umen.</p>
              
              <p> Obwohl das Wasser einladend aussieht, ist das Schwimmen hier gef&auml;hrlich wegen  der starken Brandung und Str&ouml;mungen, die man von Land aus oft nicht sehen kann.  Der Strand hat auch kein sch&uuml;tzendes Riff, sodass die Wellen mit voller Wucht  ankommen. Au&szlig;erdem sind hier keine Rettungsschwimmer stationiert, wie nirgends  sonst auf Molokai.</p>
              <p> Oftmals ist es hier ziemlich windig und es kommen Sandst&uuml;rme auf, sodass das  Picknicken oder Sonnenbaden hier unangenehm sein kann. Am besten sucht man sich  in diesem Fall ein Pl&auml;tzchen in dem gro&szlig;en Papohaku   Beach Park,  wo auch Picknicktische stehen und B&auml;ume Schatten spenden.</p>
              <p><strong>Hinweis:</strong> In dieser Gegend gibt es viele Kiawe-B&auml;ume. Diese  B&auml;ume haben bis zu 2,5 cm lange Dornen (<a href="../../../molokai/beaches/n1.html" onclick="NewWindow(this.href,'name','650','650','yes');return false">Bild ist hier</a>), deshalb ist es ratsam,  die Schuhe anzulassen und beim Hinsetzen den Boden vorher nach Dornen  abzusuchen. </p>
