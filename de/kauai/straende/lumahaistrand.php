              <h2 class="small">Lumahai Beach Info</h2>
              <p>Lumahai Beach liegt hinter dem kleinen Ort Hanalei an Kauais Nordk&uuml;ste. Es ist ein sch&ouml;ner, langer und breiter Strand mit zwei Abschnitten. Der &ouml;stliche kleinere Strandabschnitt hei&szlig;t auch Kahalahala. Er ist etwas gesch&uuml;tzter, und an ruhigen Sommertagen gehen hier manche Leute ins Wasser. An den meisten Tagen ist das Schwimmen am Lumahai Beach jedoch zu gef&auml;hrlich, weil das Wasser zu turbulent ist und die Str&ouml;mungen zu stark. Besonders in den Wintermonaten zwischen Oktober und M&auml;rz k&ouml;nnen die Wellen hier sehr hoch sein. Daher ist Lumahai Beach eher ein Ort zum Sonnenbaden und Picknicken.</p>
