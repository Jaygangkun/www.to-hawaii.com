              <h2 class="small">Hanakapiai Beach Info</h2>
              <p>Ein bekannter Wanderweg an Kauais Nordk&uuml;ste ist der 18 km lange Kalalau  Trail. Die meisten Leute gehen allerdings nur die ersten 3.2 km bis zum  Hanakapiai Beach, da der Weg anstrengend ist und dieser k&uuml;rzere Abschnitt schon  einen Tagesausflug f&uuml;r viele Leute ist. Somit ist Hanakapiai   Beach ein beliebtes Ausflugsziel  f&uuml;r Wanderer. Ein Badestrand ist es nicht, da hier die Str&ouml;mungen zu gef&auml;hrlich  sind. Viele Leute sind hier schon ertrunken, was diesen Strand  zu einem der gef&auml;hrlichsten auf Kauai macht. Hier steht  sogar ein Schild, was mit Strichen die Zahl der Opfer markiert. </p>
              <p> In den Sommermonaten ist das Meer ruhiger, aber im Winter waschen die Wellen  einen gro&szlig;en Teil des Sandes von diesem Strand weg. Um  bis zum Ende des Kalalau Trails wandern zu d&uuml;rfen braucht man eine Erlaubnis,  die man sich vorher besorgen muss. Aber bis nach Hanakapiai darf man ohne diese  Erlaubnis laufen. Das letzte St&uuml;ck des Weges hierher ist ziemlich steil, und  wenn es geregnet hat, kann der Weg rutschig sein. Bis nach Hanakapiai braucht  man ungef&auml;hr 1.5 bis 2 Stunden.</p>
