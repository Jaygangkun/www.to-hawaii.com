              <h2 class="small">Anini Beach Park Info</h2>
              <p>Anini Beach  ist durch eines der l&auml;ngsten und breitesten Riffe in Hawaii  gesch&uuml;tzt, welches 3.2 km lang und bis zu 488 m breit ist. Dieses Riff sch&uuml;tzt  die K&uuml;ste vor Erosion. Aus diesem Grund ist sind die Wellen hier meist flach  und das Meer am ruhigsten an Kauais Nordk&uuml;ste. </p>
              <p> Der Meeresgrund in Strandn&auml;he ist flach, und die Bedingungen zum Schnorcheln  sind oftmals gut. Ein Nachteil ist, dass der Meeresgrund an manchen Stellen  steinig ist und das Riff scharf sein kann (am besten tr&auml;gt man hier  Wasserschuhe).</p>
              <p> Der Wind bl&auml;st hier auch meist best&auml;ndig und somit ist Anini auch ein  beliebter Strand f&uuml;r Windsurfer. Am westlichen Ende des Strandes (wo die Stra&szlig;e  endet) ist ein Kanal in dem die Str&ouml;mung stark sein kann, daher ist es ratsam,  hier nicht schwimmen zu gehen.</p>
