              <h2 class="small">Kee Beach Park Info</h2>
              <p>Dieser Strand ist einer der beliebtesten auf der Insel. Besonders an den  Wochenenden und in der Hauptsaison im Sommer und um Weihnachten findet man hier  kaum einen Parkplatz. Kee Beach ist der letzte Strand an  Kauais Nordk&uuml;ste. Er liegt dort, wo der Highway 560 endet und die Na Pali K&uuml;ste  beginnt, die man nur zu Fu&szlig; erkunden kann. </p>
                            <p> Der Sandstrand ist umrandet von &uuml;ppiger Vegetation, und die Bedingungen zum  Schnorcheln sind gut wenn das Meer ruhig ist (haupts&auml;chlich im Sommer). Im  strandnahen Bereich ist der Meeresboden sandig und ein St&uuml;ck weiter drau&szlig;en  beginnt das Riff. </p>
              <p> In den Wintermonaten (zwischen Oktober und M&auml;rz) ist das Schnorcheln hier  oft zu gef&auml;hrlich. Zu dieser Zeit sind die Wellen meist zu hoch und die  Str&ouml;mungen zu stark. Aber auch in den ruhigeren Sommermonaten sollten Schwimmer  den Kanal am westlichen Ende des Strandes meiden, da dieser Kanal zum Meer  offen ist und die Str&ouml;mung hier stark sein kann.</p>
