              <h2 class="small">Hideaways Beach Info</h2>
              <p>Den versteckten Hideaways Beach findet man nur, wenn man genau wei&szlig;, wo er ist.  Man kann ihn nur &uuml;ber einen sehr steilen und holprigen Fu&szlig;weg erreichen, der  die Klippen herunterf&uuml;hrt und somit eher eine Kletterpartie ist. Man muss sich  hierbei am Gel&auml;nder und an Seilen festhalten. Somit ist Hideaways   Beach kein Ausflugsziel f&uuml;r kleine  Kinder oder Menschen mit Mobilit&auml;tseinschr&auml;nkungen oder H&ouml;henangst.</p>
                            <p> Genauer gesagt gibt es zwei Zugangswege zum Strand  (der zwei getrennte Buchten hat), allerdings ist der einfache Weg reserviert  f&uuml;r Hotelg&auml;ste des Pali Ke Kua Condominiums. Dieser Weg f&uuml;hrt zu der &ouml;stlichen  Bucht. Der schwierigere, &ouml;ffentliche Zugangsweg f&uuml;hrt an die westliche Bucht.  Vom Parkplatz braucht man etwa 5-10 Minuten bis zum Strand.</p>
              <p> Der Strand ist klein, aber sch&ouml;n mit B&auml;umen die Schatten spenden. Wegen des  Riffs ist es haupts&auml;chlich ein Schnorchelstrand, aber nur in den Sommermonaten,  wenn das Meer ruhiger ist. Im Winter ist das Schwimmen an Kauais Nordk&uuml;ste  wegen der hohen Wellen und starken Str&ouml;mungen meist zu gef&auml;hrlich.</p>
