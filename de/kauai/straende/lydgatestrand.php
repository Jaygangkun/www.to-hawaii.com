              <h2 class="small">Lydgate Beach Park Info</h2>
              <p>Lydgate Beach ist einer der beliebtesten Str&auml;nde an Kauais Ostk&uuml;ste. Es ist  ein langer Strand, aber der beste Schwimmbereich ist am n&ouml;rdlichen Ende des  Strandes, wo sich die zwei Lydgate Pools befinden. Hier wurden zwei  Steinbarrieren ins Meer gebaut und in diesen zwei gesch&uuml;tzten Bereichen kann  man schwimmen und schnorcheln. Au&szlig;erhalb dieser zwei Pools ist das Schwimmen  nicht zu empfehlen, weil das Meer oft zu turbulent ist und die Str&ouml;mungen zu  stark.</p>
              
              <p> Der Lydgate Strandpark hat auch einen Spielplatz und eine gro&szlig;e Rasenfl&auml;che,  wo Anwohner manchmal ihre Drachen fliegen lassen oder Frisbee spielen. Lydgate  eignet sich auch f&uuml;r einen langen Strandspaziergang. Ein 4 km Fu&szlig;g&auml;ngerweg  f&uuml;hrt die K&uuml;ste entlang, ideal f&uuml;r eine Wanderung oder Fahrradtour. Im  n&ouml;rdlichen Bereich des Strandparks (nahe der zwei Pools) befindet sich auch ein  alter hawaiianischer Heiau (Tempel) namens Hikinaakala Heiau, der um 800 AD  errichtet wurde.</p>
