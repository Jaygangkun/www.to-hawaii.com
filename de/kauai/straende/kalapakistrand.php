              <h2 class="small">Kalapaki Beach Info</h2>
              <p>Kalapaki Beach ist wegen seiner zentralen Lage in Lihue ein beliebter Strand. Im Hintergrund ist der Kauai Marriott Resort und eine Reihe an Restaurants. Die gesch&uuml;tzte Bucht liegt direkt neben dem Nawiliwili Harbor, wo oftmals Kreuzfahrtschiffe halten. Die Bedingungen zum Schwimmen sind gut, wenn das Meer ruhig ist. Hier finden auch oft Kanuwettbewerbe statt. Wenn die Bedingungen zum Surfen gut sind, kommen hier auch viele Surfer und Stand-Up-Paddleboarder her.</p>
