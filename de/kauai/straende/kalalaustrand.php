              <h2 class="small">Kalalau Beach Info</h2>
              <p>Der Kalalau Beach liegt am Ende des 18 km langen Kalalau Trails an Kauais  Nordk&uuml;ste. Dieser Wanderweg ist einer der schwierigsten in Hawaii.  Er beginnt am Kee Beach,  f&uuml;hrt an den Klippen entlang und ist an vielen Stellen sehr schmal und steil  (besonders im mittleren Abschnitt, nach 10,5 km). Die Strecke ist nichts f&uuml;r  Kinder und Menschen mit H&ouml;henangst. </p>
                            <p> Da man oft nur langsam vorankommt, erfordert ein Ausflug zum Kalalau   Beach eine &Uuml;bernachtung dort (man  muss also alles Equipment, einschlie&szlig;lich Essen  und Trinken) mitbringen. Au&szlig;erdem muss man sich vorher eine Wandererlaubnis  besorgen. Auf dieser Seite gibt es mehr Informationen: http://www.hawaiistateparks.org/camping/permit_napali.cfm </p>
              <p>Da diese Wandererlaubnis sehr begehrt ist, muss man sich schon Monate im voraus  darum k&uuml;mmern (am besten 6 Monate vor der geplanten Wanderung). Kalalau   Beach ist kein Badestrand, da die  Wellen hier oft zu hoch sind (vor allem im Winter) und die Str&ouml;mungen zu stark.</p>
