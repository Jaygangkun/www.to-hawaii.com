              <h2 class="small">Haena Beach Park Info</h2>
              <p>Haena Beach Park ist einer der bekanntesten Str&auml;nde an Kauais Nordk&uuml;ste. Vor  allem an den Wochenenden ist der relativ gro&szlig;e Parkplatz oftmals bis auf den  letzten Platz voll. Der Strand ist lang und von hier aus kann man an den Nachbarstrand   Tunnels Beach  laufen. </p>
              <p> Ein Badestrand ist Haena Beach  nur bedingt, d.h. nur an Tagen wo das Meer ruhig ist (was eher in den  Sommermonaten) der Fall ist. Und auch dann k&ouml;nnen hier starke Str&ouml;mungen  auftreten, da Haena Beach kein sch&uuml;tzendes Riff hat und die Wellen hier  ungebremst an den Strand rollen. Die meisten Leute kommen zum Sonnenbaden oder  Picknicken hierher.</p>
