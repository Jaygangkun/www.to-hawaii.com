              <h2 class="small">Honopu Beach Info</h2>
              <p>Honopu Beach  kann man weder per Auto noch zu Fu&szlig; erreichen. Dieser Strand  ist so abgelegen und trotzdem so bekannt, da er auf vielen Postkarten und  Wandkalendern abgebildet ist wegen seiner Sch&ouml;nheit. Die meisten Besucher Kauais  und auch die Einheimischen bekommen diesen Strand dennoch nie zu Gesicht, denn  man muss entweder einen Hubschrauberflug oder eine Bootsfahrt buchen, um ihn  von der Ferne aus sehen zu k&ouml;nnen. </p>
              <p> Honopu Beach besteht aus zwei Buchten an Kauais Na Pali K&uuml;ste im Norden der  Insel. Die Buchten liegen noch ein St&uuml;ck hinter dem Ende des bekannten Kalalau  Trail, einem 18 km langen Wanderpfad, der an den Klippen entlangf&uuml;hrt und am Nachbarstrand <a href="../../../de/kauai/straende/kalalaustrand.php">Kalalau Beach</a> endet. Bis hierhin kann man zu Fu&szlig; laufen. Zum   Honopu Beach  m&uuml;sste man dann schwimmen, was zu gef&auml;hrlich ist wegen der starken Str&ouml;mungen  in dieser Gegend. Es ist verboten, mit einem Kayak zum Honopu   Beach zu fahren.</p>
