              <h2 class="small">Puu Poa Beach Info</h2>
              <p>Puu Poa Beach ist ein kleiner versteckter Strand an Kauais  Nordk&uuml;ste. Er wird haupts&auml;chlich von G&auml;sten des St. Regis Princeville Resorts  und des Hanalei Bay Resorts genutzt. Diese beiden Resorts befinden sich direkt  hier an diesem Strand. </p>
              <p> Der Strand hat r&ouml;tlich-goldenen Sand. In den Wintermonaten ist das Meer hier  oft sehr turbulent (wie an den meisten Str&auml;nden an der Nordk&uuml;ste), sodass man  nicht baden gehen kann. In den Sommermonaten ist das Wasser ruhiger.</p>
