              <h2 class="small">Donkey Beach Info</h2>
              <p>Vor einigen Jahren war Donkey   Beach als Nacktbadestrand bekannt,  da er etwas abgelegen und nur &uuml;ber einen 10-min&uuml;tigen Fu&szlig;weg zu erreichen ist.  Da &ouml;ffentliche Nacktheit in Hawaii  offiziell verboten ist und die dortigen Landbesitzer auch dagegen waren, wurde  hier mehr kontrolliert und nun ist es ein Strand wie  jeder andere. Die Leute kommen zum Sonnenbaden und Picknicken hierher. </p>
              <p>Ein guter Badestrand war Donkey   Beach noch nie, da das Wasser hier  meist zu turbulent ist und gef&auml;hrliche Str&ouml;mungen keine Seltenheit sind.  Haupts&auml;chlich Surfer und Bodyboarder gehen hier ins Wasser.</p>
              <p>Der hawaiianische Name dieses Strandes ist Paliku, aber fast  jeder nennt ihn Donkey Beach (Esel-Strand), da hier in der Vergangenheit Esel  auf der nahegelegen Weide gehalten wurden.</p>
