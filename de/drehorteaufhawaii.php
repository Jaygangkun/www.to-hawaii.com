          <h1>Drehorte auf Hawaii</h1>
          <div class="midpanel_row">
            <p><strong>Hawaii - Ein malerischer Drehort</strong></p>
            <p> Die Hawaii-Inseln - ein malerisches Paradies, das mit seinen majest&auml;tischen Bergen, seiner &uuml;ppigen Vegetation und seinem himmelblauen Meer eine perfekte Kulisse f&uuml;r  Filme bietet. Viele unvergessliche Hollywood-Streifen wurden auf diesen Inseln gedreht, ohne dass die Zuschauer sich jemals dessen bewusst waren.</p>
                        <p> Hawaii war stets ein erstklassiger Ort zum Drehen von unabh&auml;ngigen Filmen oder auch   gro&szlig;en Hollywoodproduktionen sowie Surfvideos, Reise- und Lifestyle-Shows und unz&auml;hligen   anderen Medienformaten. Und ebenso wie die tropische Landschaft Hawaiis, die jeden   Tag an Vielfalt und Sch&ouml;nheit gewinnt, floriert auf den Inseln auch zunehmend die   Filmindustrie. Ob Vulkane, schneebedeckte Berggipfel oder Riffe an der Meeresk&uuml;ste 
              - die Inseln verf&uuml;gen &uuml;ber eine Vielzahl unterschiedlicher Schaupl&auml;tze wie man sie nirgends sonst finden kann.</p>
            <p> Hawaii ist sehr wandlungsf&auml;hig und kann den Zuschauer deshalb leicht glauben machen,   dass es ein ganz anderer Ort ist - sei es ein Strand im S&uuml;dpazifik, ein Dorf in   S&uuml;dafrika oder ein exotischer Regenwald in Peru. Und da sich Hawaii durch viele   atemberaubende Landschaftsgegens&auml;tze auszeichnet- wie etwa der dichte Dschungel   einerseits und malerische Aussichten andererseits oder etwa gro&szlig;e Metropolen auf   der einen und unber&uuml;hrte Landschaften auf der anderen Seite - ist es gut geeignet, mittels der Filmkunst Geschichten zu erz&auml;hlen.</p>
            
            <p> Ob Teenie-Surffilm oder Hollywood-Thriller, Hawaii schafft es stets, die Zuschauer   zu fesseln. Filmlegenden wie Steven Spielberg, George Lucas, Tim Burton und George   Armitage haben die unglaublichen M&ouml;glichkeiten, die Hawaii als Drehort bietet, erkannt   und genutzt. Und Hawaii besitzt nicht nur ein h&uuml;bsches Gesicht - auch die kreativen   Talente, die dort schlummern, sind einmalig. In den Talent- und Produktionsagenturen   wimmelt es nur so von unvergleichlichen Schauspielern und Fachleuten, die selbst das schwierigste Filmprojekt umsetzen k&ouml;nnen.</p>
            <p> Stunts, Pyrotechnik, Spezialeffekte, Graphik, Szenenbild, Kamera, Lichtf&uuml;hrung,   Klangeffekte und Schnitt sind nur einige der Aufgabenfelder einer Filmproduktion,   die man in Hawaii finden kann. Einheimische Drehort-Sp&auml;her locken Produzenten aus aller Welt mit einmaligen und atemberaubenden Drehorten an.</p>
