            <p> Einige der am meisten gefeierten Filme der Geschichte sind schon im Rahmen der j&auml;hrlich   in Hawaii abgehaltenen Filmfestivals - wie dem Hawaii International Filmfestival,   dem Maui Filmfestival und dem Honolulu Rainbow Filmfestival - zur Erstauff&uuml;hrung   gekommen. Filme, die auf den Hawaii-Inseln gedreht wurden, haben schon mehrere Millionen   an den Kinokassen eingespielt. Und eins ist sicher - ein hawaiianischer Drehort 
