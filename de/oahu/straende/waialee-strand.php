              <h2 class="small">Waialee Beach Park Info</h2>
              <p>Dieser kleine Strandpark an Oahus Nordk&uuml;ste wird oft  &uuml;bersehen da die meisten Leute zu bekannteren Surfstr&auml;nden fahren, wie z.B. Sunset   Beach und Waimea   Bay. Waialee   Beach wird haupts&auml;chlich von  Fischern besucht und einheimischen Familien die hier picknicken und im flachem  Wasser waden. </p>
              <p>Der Strandpark hat einen Parkplatz, aber es gibt ansonsten  keine &ouml;ffentlichen Einrichtungen. Jedoch lohnt sich ein Besuch, denn der  Ausblick ist sch&ouml;n. In K&uuml;stenn&auml;he hat man einen sch&ouml;nen Blick auf die kleine  Insel Kuka'imanini. Eisenb&auml;ume spenden Schatten am Strand.</p>
