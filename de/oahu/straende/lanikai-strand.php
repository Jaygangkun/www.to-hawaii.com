              <h2 class="small">Lanikai Beach Info</h2>
              <p>Lanikai bedeutet "himmlisches Meer" im Hawaiianischen und dieser Name beschreibt diesen Strand sehr gut. Lanikai Beach an Oahus Ostk&uuml;ste hat feinen wei&szlig;en Sand, sanfte Wellen, azurblaues Wasser und bietet einen tr&auml;umerischen Blick auf die zwei kleinen Mokulua Inseln. Aus diesem Grund wurde Lanikai Beach schon auf zahlreichen Wandkalendern, Postkarten und Reisef&uuml;hrern abgebildet. Es ist einer der sch&ouml;nsten Str&auml;nde auf Oahu.</p>
              <p>Der Ort Lanikai ist eine gehobene Wohngegend und die H&auml;user hier haben alle einen Wert von 1 Millionen und aufw&auml;rts, vor allem die H&auml;user die direkt am Strand stehen. Man erreicht Lanikai Beach durch Zugangswege von der Hauptstra&szlig;e. In den letzten Jahren wurde Lanikai Beach immer beliebter, sodass er an den Wochenenden ziemlich voll sein kann. Deshalb empfiehlt sich ein Besuch an Wochentagen.</p>
              <p>Au&szlig;er schwimmen kann man hier auch sehr gut schnorcheln. Im Wasser befinden sich ein paar Felsen woran Korallen wachsen. Viele Kayaker starten von hier in Richtung der beiden Inseln - Moku Nui und Moku Iki. F&uuml;r Unge&uuml;bte ist dies jedoch nicht zu empfehlen, da die Inseln doch recht weit drau&szlig;en sind und die Str&ouml;mungen hier stark sein k&ouml;nnen.</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Die Wellen sehen vom Strand aus auch meist flach aus. Ist man jedoch erst einmal auf's offene Meer herausgepaddelt, sind sie pl&ouml;tzlich h&ouml;her als angenommen. Die Wellen k&ouml;nnen einen unerfahrenen Kanufahrer leicht umrei&szlig;en. Rettungsschwimmer sind am Strand keine vorhanden.</p>
