              <h2 class="small">Pokai Bay Beach Park Info</h2>
              <p>Durch die k&uuml;nstlich gebaute Steinbarriere hat die Pokai Bay den ruhigsten Strand an Oahus Westk&uuml;ste. Die Wellen sind hier stets flach und die Bedingungen zum Schwimmen und Kayaken sind gut. Die Bucht eignet sich somit auch f&uuml;r Kinder.</p>
              <p>Der Strand wurde nach dem hawaiianischen H&auml;uptling Pokai genannt, der die erste Kokusnusspalme nach Hawaii gebracht haben soll. "Ka Uluniu o Pokai" ist der bekannte Kokusnusshain, der einst an diesem Strand stand. </p>
