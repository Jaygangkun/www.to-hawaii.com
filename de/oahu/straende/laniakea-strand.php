              <h2 class="small">Laniakea Beach Info</h2>
              <p>Laniakea Beach an der Nordk&uuml;ste von Oahu ist auch unter dem Namen Turtle Beach (Schildkr&ouml;tenstrand) bekannt. Die Hauptattraktion hier sind die Schildkr&ouml;ten, die sich hier am Strand sonnen. An anderen Str&auml;nden hat man Gl&uuml;ck, wenn man eine Schildkr&ouml;te im Wasser sieht, die ihren Hals herausstreckt. Hier kommen die Tiere aus dem Wasser heraus um am Strand ein Nickerchen zu machen. </p>
              <p>In den letzten Jahren als mehr und mehr Leute von den Schildkr&ouml;ten erfuhren, wurde der Strand immer voller. Viele Tourbusse halten hier an auf dem Weg zu anderen Attraktionen. Deshalb arbeiten hier freiwillige Helfer zum Schutz der Schildkr&ouml;ten. Es wurden Schilder errichtet, die den Besuchern erkl&auml;ren, warum man die Tiere nicht anfassen oder f&uuml;ttern soll. Denn das schadet ihnen. Die Helfer legen, sowie sich eine Schildkr&ouml;te an den Strand legt, eine rote Leine um die Schildkr&ouml;te herum, sodass die Menschen sich aus diesem Bereich fern halten (in der Vergangenheit haben sich Leute auf die Schildkr&ouml;ten draufgesetzt oder sie angefasst, was per Gesetz verboten ist). </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Im Juli 2008 wurde hier eines morgens eine Schildkr&ouml;te tot aufgefunden. Sie wurde absichtlich get&ouml;tet. Ihr Name war "Honey Girl" und sie war sehr alt, zwischen 35 und 40 Jahren. Sie wurde zuletzt am Vorabend hier am Strand gesehen, wie sie bei Vollmond am Strand lag und schlief. Es wurde eine Webseite errichtet, die eine Belohnung verspricht f&uuml;r Hinweise, die zur Erfassung des T&auml;ters f&uuml;hren (honeygirlrewardfund.com). </p>
