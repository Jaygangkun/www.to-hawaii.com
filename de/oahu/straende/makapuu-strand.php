              <h2 class="small">Makapuu Beach Park Info</h2>
              <p>Dieser Strand hat fast immer Wellen, die sich in Strandn&auml;he brechen. Deshalb ist Makapuu ein beliebter Strand zum Bodyboarden. Allerdings sind die Wellen hier gef&auml;hrlich, da sie sehr stark sind und Schwimmer mit voller Gewalt umherwirbeln k&ouml;nnen. Aus diesem Grund gab es hier schon oft Unf&auml;lle, u.a. Halswirbelverletzungen. Rote Warnflaggen stehen ganzj&auml;hrig am Strand. Lediglich erfahrene Surfer sollten hier ins Wasser gehen.</p>
              <p>Ein Aussichtspunkt auf den Klippen bietet einen sch&ouml;nen Blick auf den Strand und die K&uuml;ste. Ein Leuchtturm (Makapuu Lighthouse) befindet sich auf der anderen Seite der Klippen. Die Insel die man vom Makapuu Beach aus sehen kann hei&szlig;t Rabbit Island. <br />
              </p>
