              <h2 class="small">Waimanalo Beach Park Info</h2>
              <p>Waimanalo Beach  an Oahus Ostk&uuml;ste ist der ideale Ort f&uuml;r eine lange Strandwanderung. Mit seinen  4,8 km L&auml;nge ist er der l&auml;ngste Sandstrand auf Oahu. Der  Sand ist fein, das Wasser azurblau und Eisenb&auml;ume spenden Schatten, perfekt f&uuml;r  ein Picknick am Strand. </p>
              <p>Es gibt zwei Hauptzugangswege zu diesem Strand  (zwei Strandparks). Der eine ist in der N&auml;he des s&uuml;dlichen Ende des Strandes  und der andere am n&ouml;rdlichen Ende. Der s&uuml;dliche Strandpark hei&szlig;t Waimanalo   Beach Park  und befindet sich gegen&uuml;ber der Nakini Street.  Dieser Strandpark hat Duschen, Toiletten, ein Pavilion, Picknicktische und  Rettungsschwimmer. </p>
              <p>Die n&ouml;rdliche Zugangsstra&szlig;e (Zugang zwischen der    Aloiloi Street und Tinker    Road) f&uuml;hrt durch einen kleinen Wald zum  Strandpark, der Waimanalo Bay State Recreation Area. Dieser Strandpark hat  dieselben &ouml;ffentlichen Einrichtungen und auch hier sind Rettungsschwimmer  stationiert. </p>
              <p>Ein Nachteil in Waimanalo ist, dass es oft ziemlich windig  sein kann. Falls Sie hier ein Picknick planen, bringen Sie eine leichte Jacke  mit, vor allem in den Wintermonaten (November bis M&auml;rz).</p>
