              <h2 class="small">Kaena Point State Park Info</h2>
              <p>Kaena, was "rot hei&szlig;" oder "gl&uuml;hend" im Hawaiianischen bedeutet, ist der Name des nordwestlichen Zipfels der Insel Oahu. Der Kaena Point State Park ist so abgelegen, dass keine Strasse hierher f&uuml;hrt. Man kann Kaena Point nur zu Fu&szlig; erreichen. Es gibt zwei Wanderwege. Der eine beginnt am Ende der Stra&szlig;e n&ouml;rdlich von Makaha, und der andere beginnt am Ende der Stra&szlig;e westlich von Waialua (hinter dem Dillingham Fluggel&auml;nde) an der Nordk&uuml;ste.</p>
              <p>Der Park beherbergt zahlreiche Seev&ouml;gel, einschlie&szlig;lich Albatrosse, sowie seltene Pflanzenarten. Die K&uuml;ste ist hier felsig und schwimmen ist nicht m&ouml;glich und auch zu gef&auml;hrlich wegen starker Str&ouml;mungen. Schatten ist hier kaum vorhanden, sodass es ratsam ist, gen&uuml;gend Wasser dabei zu haben. Man l&auml;uft gute 90 Minuten bis hierher.</p>
