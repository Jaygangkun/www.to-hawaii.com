              <h2 class="small">Malaekahana Bay Info</h2>
              <p>Die Malaekahana Bay an Oahus Nordostk&uuml;ste hat einen langen wei&szlig;en Sandstrand, an dem man prima eine Strandwanderung machen kann. Der Sand ist hier sch&ouml;n fest, sodass man gut laufen kann, ohne zu schnell zu erm&uuml;den.</p>
              <p>Die Bedingungen zum Schwimmen sind am n&ouml;rdlichen Ende des Strandes am besten, da der Meeresboden in Strandn&auml;he hier sandig ist. Weiter s&uuml;dlich erschwert ein Korallenriff den Zugang zum Meer. Am s&uuml;dlichen Ende der Bucht liegt die kleine Insel mit Namen Goat Island (Esel Insel), im Hawaiianischen auch unter dem Namen Mokuauia Island bekannt. Esel wohnen hier allerdings keine, lediglich nistende Seev&ouml;gel.</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Bei Ebbe ist es m&ouml;glich, zu der Insel hin zu laufen, da das Wasser dann nur h&uuml;fthoch ist. Allerdings ist dies nicht ohne Riffschuhe zu empfehlen, da man sich daran schnell die F&uuml;&szlig;e aufschneiden kann. Am besten paddelt man mit einem Boot, Kanu oder Surfbrett zu der Insel. Sind die Wellen hoch, ist dies jedoch nicht ratsam denn die Str&ouml;mungen k&ouml;nnen ziemlich stark sein und Rettungsschwimmer sind hier keine stationiert.</p>
              <p>Da Goat Island ein Seevogel-Naturschutzgebiet ist, darf man sich lediglich an dem kleinen Strand auf der Insel aufhalten. Von der Inselmitte muss man sich fernhalten, da hier die Eier der Seev&ouml;gel liegen. </p>
