              <h2 class="small">Ala Moana Beach Park Info</h2>
              <p>Der Ala Moana Beach Park befindet sich im Herzen von Honolulu und grenzt an Waikiki an. Es ist einer der beliebtesten Str&auml;nde auf Oahu. Hier kann man gut schwimmen, picknicken und an der Promenade spazieren gehen.</p>
              <p>Der Stand ist fast einen Kilometer lang und die Wellen sind immer flach, was besonders gute Bedingungen f&uuml;r Langestreckenschwimmer schafft. Der Strand ist durch ein Korallenriff gesch&uuml;tzt und eignet sich somit f&uuml;r kleine Kinder und Nichtschwimmer.</p>
              <p>Der beste Bereich des Strandes ist das &ouml;stliche Ende (in Richtung Waikiki), da hier der Meeresboden sandig ist und keine Steine oder Riff im Wasser sind (das westliche Ende hat Steine und Riff).</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Im angrenzenden Park befinden sich Picknicktische, Duschen, Toiletten und Tennispl&auml;tze. Magic Island, eine k&uuml;nstlich geschaffene Halbinsel, lieft am &ouml;stlichen Ende des Parks. Die Halbinsel hat auch einen kleinen Strand, der durch eine Steinbarriere gesch&uuml;tzt ist. Rettungsschwimmer sind hier und am Haupstrand das ganze Jahr &uuml;ber stationiert. </p>
