              <h2 class="small">Diamond Head Beach Park Info</h2>
              <p>Der Diamond Head Beach Park befindet sich am Fu&szlig;e des Diamond Heads im Osten von Waikiki. Der Diamond Head sieht aus wie ein Berg. Er ist allerdings ist ein erloschener Vulkankrater, der ein beliebtes Ausflugsziel ist (man kann zu einem Aussichtspunkt hochwandern, von wo aus man einen sch&ouml;nen Ausblick auf die Stadt Honolulu und Waikiki hat).</p>
              <p>Der Strandpark befindet sich unterhalb der Klippen. Der Strand ist schmal und eignet sich nicht zum Schwimmen, da sich an der gesamten Breite ein flaches Korallenriff befindet. Viele Surfer kommen hier jeden Tag her, da sich die Wellen an dem Riff brechen und somit gute Bedingungen zum Surfen schaffen. Schnorchlen ist m&ouml;glich wenn die Wellen nicht zu hoch sind.</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>An den Klippen steht ein alter Leuchtturm (Diamond Head Lighthouse). Den besten Blick auf den Leuchtturm hat man von dem Wanderweg aus, der zum Diamond Head Aussichtspunkt f&uuml;hrt. Der Eingang zum Wanderweg befindet sich auf der anderen Seite des Strandparks.</p>
