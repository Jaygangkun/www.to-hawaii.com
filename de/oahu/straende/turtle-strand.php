              <h2 class="small">Turtle Beach Info</h2>
              <p>Der hawaiianische Name dieses Strandes ist Panaoneone   Beach, aber die meisten Leute  nennen ihn Turtle Beach,  da man hier oftmals gr&uuml;ne Meeresschildkr&ouml;ten sehen kann, die an den Algen an  dem nahegelegenen Riff nagen. Am besten kann man die Schildkr&ouml;ten in den fr&uuml;hen  Morgenstunden beobachten. </p>
              <p>Diesen Strand kann man von der Stra&szlig;e aus nicht sehen, daher  ist er etwas versteckt und geh&ouml;rt somit nicht zu den meistbesuchten Str&auml;nden an  Oahus Westk&uuml;ste. Das hei&szlig;t allerdings nicht, dass es kein sch&ouml;ner Strand  ist. Ganz im Gegenteil, der feine wei&szlig;e Sand und das azurblaue Meer ist ein  Traum, vor allem in den Sommermonaten wenn die Wellen nicht so hoch sind und  das Wasser nicht so sehr aufwirbeln. Der einzige Nachteil hier ist, dass der Strand  nicht viel Schatten hat. </p>
              <p>In den Wintermonaten k&ouml;nnen die Wellen hier sehr hoch  werden. Dann ist Schwimmen nicht m&ouml;glich. Allerdings haben die Wellen in  Strandn&auml;he auch im Sommer oftmals ziemlich viel Power. Am s&uuml;dlichen Ende des  Strandes ist Mauna Lahilahi, ein kleiner H&uuml;gel. In diesem Bereich des Strandes  befinden sich zwei kleine Hotels mit Ferienwohnungen.</p>
