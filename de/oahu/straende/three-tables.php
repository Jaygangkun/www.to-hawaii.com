              <h2 class="small">Three Tables Beach Info</h2>
              <p>Three Tables (auf deutsch "Drei Tische") Beach ist nach den  drei flachen Steinbrocken benannt, die bei Ebbe besonders gut zu sehen sind.  Wenn die Wellen flach sind, was oft in den Sommermonaten (Mai bis September)  der Fall ist, kann man am Three Tables   Beach gut schnorcheln und tauchen.  In den Wintermonaten k&ouml;nnen die Wellen in dieser Gegend bis zu 6 m hoch werden.  Der Strand ist dann f&uuml;r Schwimmer gesperrt und nur professionelle Surfer d&uuml;rfen  ins Wasser gehen.</p>
              <p>chwimmen, picknicken und an der Promenade spazieren gehen.</p>
              <p>Der Stand ist fast einen Kilometer lang und die Wellen sind immer flach, was besonders gute Bedingungen f&uuml;r Langestreckenschwimmer schafft. Der Strand ist durch ein Korallenriff gesch&uuml;tzt und eignet sich somit f&uuml;r kleine Kinder und Nichtschwimmer.</p>
