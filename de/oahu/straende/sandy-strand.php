              <h2 class="small">Sandy Beach Park Info</h2>
              <p> Der Sandy Beach Park an Oahus S&uuml;dostk&uuml;ste ist einer der besten Str&auml;nde zum Bodyboarden, allerdings nur f&uuml;r Experten. Die Wellen sind hier das ganze Jahr &uuml;ber stark und brechen sich in Strandn&auml;he in flachem Wasser mit voller Wucht. Aus diesem Grund ist dieser Strand einer der gef&auml;hrlichsten Str&auml;nde in Hawaii. Er wird auch "der Strand der gebrochenen H&auml;lse genannt." Viele Schwimmer und Bodyboarder haben sich hier schon verletzt. Rote Warnflaggen und -schilder sind &uuml;berall am Strand positioniert. F&uuml;r Besucher die keine Surfexperten sind, bieten sich andere Aktivit&auml;ten an, wie z.B. ein Picknick oder die Wellen und Surfer im Wasser fotografieren.</p>
