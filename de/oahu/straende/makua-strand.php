              <h2 class="small">Makua Beach Info</h2>
              <p>Der Makua Beach ist einer der am entlegendsten Str&auml;nde auf Oahu. Er ist &uuml;ber 1.5 km lang und liegt an der Westk&uuml;ste der Insel, kurz vor dem Ende der Stra&szlig;e. Im Hintergrund liegen die Waianae Berge. Die Landschaft hier ist so, wie sie schon immer gewesen ist - vollkommen unbebaut. Der Strand ist meist leer, abgesehen von ein paar Fischern. Touristen sieht man hier nur vereinzelt, vor allem weil Makua mit dem Auto von Waikiki aus gute eineinhalb bis zwei Stunden entfernt ist, je nach Verkehrslage.</p>
              <p>Die Bedingungen zum Schwimmen sind hier nicht besonders gut, da vor allem in den Wintermonaten (November bis M&auml;rz) die Wellen hier sehr hoch werden und die Str&ouml;mungen zu stark sind. Der Strand hat kein sch&uuml;tzendes Riff, was herankommende Wellen abbremsen w&uuml;rde. Somit schlagen die Wellen mit aller Wucht auf den Strand. Dazu kommt noch, dass der Meeresboden hier sofort steil abf&auml;llt. </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Der Makua Beach ist der zweitletzte Strand vor dem Kaena State Park (der letzte Strand ist Yokohama Bay). Falls Sie Lust auf eine Wanderung haben, k&ouml;nnen Sie mit dem Auto bis ans Ende der Stra&szlig;e fahren und an die nordwestliche Spitze der Insel laufen (eine zirka drei- bis vierst&uuml;ndige Wanderung hin und zur&uuml;ck). Der Wanderweg ist sonnig. Nehmen Sie deshalb Wasser mit. Viele Albatrosse br&uuml;ten in dieser einsamen Gegend. Halten Sie sich von den Brutstellen fern, um die Tiere nicht zu st&ouml;ren. </p>
              <p>Hinweis: In der Vergangenheit kamen Einbr&uuml;che in Mietautos in dieser Gegend vor. Lassen Sie deshalb keine Wertsachen im Auto. </p>
