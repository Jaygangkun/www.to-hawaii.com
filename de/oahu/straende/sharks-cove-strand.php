              <h2 class="small">Shark's Cove Info</h2>
              <p>Diese Bucht hat zwar einen furchterregenden Namen (Hai-Bucht), allerdings braucht man vor Haien hier keine Angst zu haben. Die Bucht eignet sich gut zum Schnorcheln und Tauchen, besonders in den Sommermonaten wenn das Meer an der Nordk&uuml;ste Oahus ruhig ist (im Winter sind die Wellen zu hoch). Viele bunte tropische Fische leben in der felsigen Bucht. </p>
              <p>Shark's Cove bekam seinen Namen durch das Korallenriff au&szlig;erhalb der Bucht, dass die Form eines Haies hat wenn man es von der Luft aus betrachtet.</p>
              <p>In der Bucht befinden sich viele Unterwasser-Lavar&ouml;hren und -Tunnel (ca. 4.5 m bis 13.7 m unterhalb der Wasseroberfl&auml;che). F&uuml;r erfahrene Taucher sind diese Tunnel interessant.</p>
