              <h2 class="small">Waikiki Beach Info</h2>
              <p>Waikiki Beach ist der bekannteste und am meisten besuchte Strand auf Hawaii. Er liegt an der S&uuml;dk&uuml;ste der Insel Oahu, angrenzend an den Berg Diamond Head. Der Strand ist dreieinhalb Kilometer lang und ges&auml;umt von vielen Hotels, Restaurants, Souvenirl&auml;den und Strandbars. Am &ouml;stlichen Ende des Strandes, an den Ausl&auml;ufern des Diamond Head, ist ein wundersch&ouml;ner gro&szlig;er Park, der Kapiolani Park. </p>
              <p>Wenn Sie Wassersport m&ouml;gen, k&ouml;nnen Sie hier alle m&ouml;glichen Ausr&uuml;stungen mieten oder kaufen. Zu den beliebtesten Wassersportarten in Waikiki geh&ouml;ren surfen, bodyboarden, parasailen, schnorcheln und Kanu fahren. Duschen, Toiletten und &ouml;ffentliche Telefone sind &uuml;berall am Strand vorhanden. In den Sommermonaten sind die Wellen oftmals hoch. Wenn Sie sich nicht sicher sind &uuml;ber die Bedingungen im Wasser fragen Sie einen 
                der hier stationierten Rettungsschwimmer. </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Der Strand von Waikiki ist gew&ouml;hnlich sehr voll, aber wenn Ihnen Menschenmassen nichts ausmachen, ist dies hier ein wahres Paradies. In den Abendstunden gibt es in den zahlreichen Strandbars und Nachtclubs ein gro&szlig;es musikalisches Angebot. An den Wochenenden finden Filmvorstellungen direkt am Strand statt. Die Filme werden auf einer riesigen Leinwand in der N&auml;he vom Kapiolani Park gezeigt. Wenn Sie gerne picknicken, k&ouml;nnen Sie dies auch wunderbar im Kapiolani Park machen. Hier gibt es Picknicktische und viele gro&szlig;e B&auml;ume, die Schatten spenden.</p>
