              <h2 class="small">Waialae Beach Park Info</h2>
              <p>Dieser Strand liegt neben dem luxuri&ouml;sen Kahala Hotel &amp;  Resort in Kahala an Oahus S&uuml;dk&uuml;ste, zirka 5 km &ouml;stlich von Waikiki.  In der Mitte des Strandparks flie&szlig;t der Waialae Stream ins Meer. Der Fluss hat  einen breiten Kanal durch das Riff geformt. Eine Steinbr&uuml;cke verbindet die  beiden H&auml;lften des Parks.</p>
              <p>Die Bedingungen zum Schwimmen sind hier nicht allzu gut, da  der Meeresgrund in Strandn&auml;he von einem Riff &uuml;berzogen ist. Am &ouml;stlichen Ende  des Strandes ist eine kleine k&uuml;nstlich angelegte Insel, die in 1963 errichtet  wurde als das Hotel gebaut wurde. </p>
              <p>In dem Strandpark kann man gut picknicken. Es gibt ein  Pavilion das Schatten spendet, sowie einige Picknicktische auf der gro&szlig;en  Rasenfl&auml;che. Der Parkplatz ist ziemlich klein und wird an den Wochenenden  schnell voll. </p>
