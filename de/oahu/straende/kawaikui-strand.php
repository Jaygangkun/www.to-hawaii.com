              <h2 class="small">Kawaikui Beach Park Info</h2>
              <p>Dieser Strandpark ist leicht zu &uuml;bersehen. Er liegt direkt neben dem Kalanianaole Highway vor dem Ort Hawaii Kai (wenn man von Waikiki aus kommt). Der Park ist ein sch&ouml;ner Ort f&uuml;r ein Picknick oder Ballspiele. Er hat eine gro&szlig;e Rasenfl&auml;che und ein paar B&auml;ume, die Schatten spenden.</p>
              <p>Der Strand ist schmal und hat nur wenig Sand. Oftmals riecht das Wasser hier nach Fisch und es ist in Strandn&auml;he flach und tr&uuml;b, sodass die Bedingungen zum Schwimmen nicht besonders gut sind. Allerdings kann man von hier aus den sch&ouml;nen Ausblick genie&szlig;en.</p>
