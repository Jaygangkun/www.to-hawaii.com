              <h2 class="small">Kaiona Beach Park Info</h2>
              <p>Der Kaiona Beach ist von der Stra&szlig;e aus leicht zu &uuml;bersehen. Viele Touristen fahren hier unwissend vorbei. Deshalb sind die meisten Besucher &ouml;rtliche Anwohner mit ihren Familien und Kindern. Der Strandpark ist klein und hat einen schmalen Sandstrand, der bei Flut vom Meer fast ganz verschluckt wird.</p>
              <p>Die alten Hawaiianer errichteten hier vor vielen Jahren Steinbarriere zum Fische fangen. Die Steinbarriere steht immer noch. In diesem gesch&uuml;tzten Bereich ist das Wasser ganz ruhig und wie in einem Schwimmbecken. Kaiona ist ein idealer Strand f&uuml;r kleine Kinder und Nichtschwimmer. </p>
