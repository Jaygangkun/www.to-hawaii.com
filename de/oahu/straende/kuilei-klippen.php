              <h2 class="small">Kuilei Cliffs Beach Park Info</h2>
              <p>Der Kuilei Cliffs Beach Park befindet sich am Fu&szlig;e des Diamond Heads im Osten von Waikiki. Der Strand ist hier schmal und im Hintergrund befinden sich Klippen, von wo aus man einen sch&ouml;nen Ausblick genie&szlig;en kann. Man kann direkt am Stra&szlig;enrand parken und erreicht von dort aus den schmalen Park, der sich entlang der Klippen befindet. </p>
              <p>Um an den Strand zu gelangen folgt man dem geteerten Weg, der an der Diamond Head Road beginnt. Die Bedingungen zum Schwimmen sind hier allerdings nicht gut, da sich am Meeresboden ein Korallenriff befindet, was den Zugang zum Meer erschwert. Am besten l&auml;sst es sich hier Picknicken. </p>
