              <h2 class="small">Paiko Lagoon Info</h2>
              <p>Der Paiko Beach an Oahus S&uuml;dk&uuml;ste ist ein schmaler Strand, der bei Flut fast ganz verschwindet. Das Wasser in Strandn&auml;he ist hier flach und steinig, sodass die Bedingungen zum Schwimmen nicht gut sind. Kayaken kann man hier allerdings gut, weil die Wellen meist flach sind. Surfer benutzen diesen Strand, um die Wellen die sich weiter drau&szlig;en brechen zu erreichen.</p>
              <p>Die unbebaute Paiko Halbinsel befindet sich am &ouml;stlichen Ende des Strandes. &Ouml;rtliche Anwohner nennen sie Sand Point. Die Halbinsel umrandet die Paiko Salzwasserlagune, die ein Naturschutzgebiet ist. Viele bedrohte Seevogelarten leben hier, einschlie&szlig;lich der Hawaiianischen Stelze (a'eo). </p>
