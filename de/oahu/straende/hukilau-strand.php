              <h2 class="small">Hukilau Beach Park Info</h2>
              <p>Der Hukilau Beach Park liegt in dem   kleinen Ort Laie an  Oahus Nordostk&uuml;ste. Die Bedingungen zum Schwimmen sind gut hier in den  Sommermonaten. Im Winter entstehen hier oft starke Str&ouml;mungen. </p>
              <p>Es ist ein sehr sch&ouml;ner wei&szlig;er Sandstrand mit B&auml;umen im Hintergrund,   die  Schatten spenden. Im Sand w&auml;chst die auf Hawaii typische Naupaka   Pflanze, die  an vielen Str&auml;nden zu finden ist. </p>
              <p>Ein "Hukilau" ist eine traditionell hawaiianische Fischmethode, in   der eine  Gruppe an Leuten teilnehmen. "Hukilau" wortw&ouml;rtlich &uuml;bersetzt bedeutet   "ein Lau  ziehen." Ein "Lau" ist ein Seil, an dem Bl&auml;tter der Ti-Pflanze befestigt   sind.   </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Im Jahre 1947 organisierte die Church of Jesus Christ of Latter-Day   Saints  (Mormonenkirche) einen Hukilau in Laie, um Geld f&uuml;r eine neue Holzkirche   zu  sammeln. Die alte war abgebrannt. Da der Hukilau so beliebt war, bat die  Besucherindustrie die Anwohner Laies, diese Fischmethode als eine  Besucherattraktion weiter zu f&uuml;hren. Somit wurde die Hukilau   Fischmethode  jahrelang an diesem Strand praktiziert.  </p>
              <p>Im Jahre 1970 entschied der Staat, dass die Zeremonie eine   kommerzielle  Veranstaltung ist, die besteuert werden muss. Daraufhin befanden die   Anwohner  die Zeremonie als unprofitabel und die Veranstaltung fand ihr Ende. Der   Name  "Hukilau Beach" wurde jedoch bis heute beibehalten.</p>
