              <h2 class="small">Pounders Beach Info</h2>
              <p>Der Laie Beach Park an Oahus Nordostk&uuml;ste hat einen wei&szlig;en Sandstrand mit Wellen das ganze Jahr &uuml;ber, vor allem in den Wintermonaten, wenn sie ziemlich kr&auml;ftig auf den Strand prallen. Der Strand vor dem Park ist auch unter dem Namen Pounders Beach bekannt.</p>
              <p>Am linken (n&ouml;rdlichen) Ende des Strandes ist ein ruhigerer Bereich, der sich besser zum Schwimmen eignet. Ein Korallenriff ist hier der K&uuml;ste vorgelagert, was den Strand vor allzu hohen Wellen sch&uuml;tzt. &Ouml;rtliche Anwohner nennen diesen ruhigeren Bereich "Badewannenstrand". </p>
              <p>Das Kalksteinfels am s&uuml;dlichen Ende des Strandes hei&szlig;t Pali Kiloi'a, was im Hawaiianischen "Fischsichterfels" bedeutet. Dies ist ein beliebter Ort zum Angeln.</p>
