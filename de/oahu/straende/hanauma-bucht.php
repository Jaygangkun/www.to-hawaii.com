              <h2 class="small">Hanauma Bay Info</h2>
              <p>Haben Sie schon einmal davon getr&auml;umt, in einem riesigen Aquarium zu schwimmen, inmitten von vielen bunten Fischen? Die Hanauma Bay an Oahus S&uuml;dk&uuml;ste ist wie ein riesiges Aquarium. Die Bucht ist der bekannteste Schnorchelstrand auf der Insel. Viele Schnorchler und Taucher kommen hierher, um die vielen bunten und zahmen Fische und andere Meerestiere wie z.B. Schildkr&ouml;ten zu beobachten.</p>
              <p>In der Bucht leben &uuml;ber 450 Fischarten, von denen viele lediglich in Hawaii zu finden sind. Auch der Humuhumunukunukuapuaâ€˜a, ein Dr&uuml;ckerfish (der hawaiianische Staatsfisch), ist hier zu Hause. Die Bucht ist ein Naturschutzgebiet, in dem Fischen nicht erlaubt ist. Auch ist es verboten, die Fische zu f&uuml;ttern. </p>
              <p>Ein Korallenriff sch&uuml;tzt die Bucht vor starken Wellen. An der gesamten Breite des Strandes befinden sich Steine und Riff im Wasser. Deshalb sind Schwimmschuhe oder Schwimmflossen ratsam. Am linken Ende der Bucht ist ein sandiger Bereich, der sich f&uuml;r Kinder eignet. </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Da das Korallenriff sehr empfindlich ist, sollte man vermeiden, mit den F&uuml;&szlig;en auf das Riff zu treten, da es dadurch besch&auml;digt wird. Am besten schwimmt man &uuml;ber das Riff hin&uuml;ber, ohne es zu ber&uuml;hren, da es au&szlig;erdem scharfe Ecken hat. Falls man sich an einem Korallenriff schneidet, was leicht passieren kann, sollte man den Schnitt desinfizieren, da das Riff lebendig ist und die Bakterien und Mikroorganismen unter der Haut eine Infektion verursachen k&ouml;nnen. </p>
              <p>Die Bucht ist von Klippen umgeben. Eine steile Stra&szlig;e f&uuml;hrt hinunter zum Strand. Man kann zu Fu&szlig; gehen (ca. 3-4 Minuten) oder mit einem Shuttlebus hinunterfahren (Kosten f&uuml;r den Bus betragen 50 cents auf dem Weg nach unten und einen Dollar nach oben).</p>
              <p>Bevor man an den Strand gehen kann muss man sich erst einen 9-min&uuml;tigen informativen Film ansehen. Das Mini-Kino befindet sich im Besucherinfozentrum. Die Eintrittsgeb&uuml;hr zur Hanauma Bay betr&auml;gt $7 (es ist der einzige Strand auf Oahu der eine Eintrittsgeb&uuml;hr hat). Die Erl&ouml;se dienen zum zuk&uuml;nftigen Schutz der Bucht.</p>
              <p>Falls man kein eigenes Schnorchelequipment dabei hat, kann man hier eine Ausr&uuml;stung mieten. Hinweis: Die Hanauma Bay ist dienstags geschlossen. </p>
