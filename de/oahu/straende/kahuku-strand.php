              <h2 class="small">Kahuku Beach Info</h2>
              <p>Dieser abgelegene Strand an Oahus Nordostk&uuml;ste sieht kaum Besucher, au&szlig;er vielleicht ein paar Fischern. Der Strand befindet sich vor einem &ouml;ffentlichen Golfplatz (Kahuku Golf Course). Der Kahuku Beach ist lang und hat keinerlei Vegetation (keine Palmen oder B&auml;ume). Lediglich die Naupaka Pflanze, die man auf Hawaii h&auml;ufig an Str&auml;nden sieht, w&auml;chst hier.</p>
              <p>Somit ist der Strand an sonnigen Tagen sehr hei&szlig;, weil kein Schatten vorhanden ist. Abgesehen von einem Strandspaziergang und der Ruhe, die man hier genie&szlig;en kann, bieten sich keine Wasseraktivit&auml;ten an, da sich am Ufer ein langes flaches Riff befindet. Dadurch ist der Zugang ins Wasser nicht m&ouml;glich.</p>
