              <h2 class="small">Ehukai Beach Park Info</h2>
              <p>Der Ehukai Beach Park an Oahus Nordk&uuml;ste ist ein Schauplatz f&uuml;r professionelle Surfer, die hier in den Wintermonaten (November bis Februar) die riesigen Wellen reiten. Dies ist die Zeit wo hier Surfwettbewerbe stattfinden.</p>
              <p>Ehukai ist somit einer der bekanntesten Str&auml;nde in der Surferwelt. F&uuml;nf bis zehn Meter hohe Wellen sind hier keine Seltenheit im Winter. Im Sommer allerdings sind die Wellen so flach wie auf einem See. Dann kann man hier schwimmen und schnorcheln.</p>
              <p>In der hawaiianischen Sprache bedeutet Ehukai "rot gef&auml;rbtes Wasser." Die roten Sonnenunterg&auml;nge die man von hier aus genie&szlig;en kann, f&auml;rben die sich brechenden Wellen rot, dadurch der Name. </p>
