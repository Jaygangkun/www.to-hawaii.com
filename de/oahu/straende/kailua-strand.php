              <h2 class="small">Kailua Beach Park Info</h2>
              <p>Der Kailua Beach ist mit seinem feinen Sand, azurblauem Wasser und Pinienb&auml;umen einer der sch&ouml;nsten Str&auml;nde auf Oahu. An den Wochenenden ist er meist gut besucht. Viele Familien kommen hierher um zu Picknicken, Kayakan, Surfen, Windsurfen und Schwimmen. Die flache Insel die man von hier aus sehen kann ist Flat Island, die man leicht mit einem Kayak erreichen kann. Ein St&uuml;ck weiter s&uuml;dlich liegen zwei weitere Inseln, Moku Nui und Moku Iki, auch die Mokulua Islands genannt. </p>
              <p>Die Wellen sind hier meist flach, sodass die Bedingungen zum Schwimmen gut sind. Da der Strand an der Ostk&uuml;ste der Inseln liegt, bl&auml;st hier der Wind meist st&auml;rker als in Waikiki an der S&uuml;dk&uuml;ste (der Wind kommt meist aus dem Osten). Dadurch ist es manchmal am Strand etwas frisch. </p>
