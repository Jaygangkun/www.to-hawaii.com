              <h2 class="small">Sand Island Beach Park Info</h2>
              <p>Sand Island mag zwar kein   Bilderbuchstrand sein, aber er  bietet andere Sehensw&uuml;rdigkeiten und ist somit einen Besuch wert. Die   kleine  Insel liegt vor dem Hafen von Honolulu und ist &uuml;ber eine Zufahrtsstra&szlig;e  erreichbar. </p>
              <p>Von hier aus hat man einen sch&ouml;nen   Blick auf Honolulu,  Waikiki und den Berg Diamond Head. Da der Strand in der N&auml;he des   gesch&auml;ftigen  Hafens liegt, ist es interessant den Schiffverkehr zu beobachten.   Oftmals  kommen gro&szlig;e Containerschiffe, die neue Waren nach Oahu bringen. Gro&szlig;e   Kreuzfahrtschiffe  kommen meist an den Wochenenden an. </p>
              <p>Neben dem Schiffverkehr kann man auch   Flugzeuge beim Start  oder der Landung beobachten, denn der Flughafen von Honolulu befindet   sich in  der Nachbarschaft. </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Die Sand Island war im Zweiten   Weltkrieg ein Abwehrst&uuml;tzpunkt.  Die verlassenen Aussichtst&uuml;rme und Bunker stehen noch und erinnern an diese  Zeit. </p>
              <p>Auf der Insel befindet sich ein gro&szlig;er   Park mit Palmen, wo  man gut Picknicken kann. Der Strand an sich ist nicht zum Schwimmen   geeignet,  da sich Steine im Wasser befinden. Au&szlig;erdem ist das Schwimmen in der   Einfahrt  zum Hafen verboten.   </p>
