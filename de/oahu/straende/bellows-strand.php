              <h2 class="small">Bellows Beach Park Info</h2>
              <p>Der Bellows Beach Park an Oahus Ostk&uuml;ste ist   ein beliebter Strand  f&uuml;r Surfer und Bodyboarder. Es ist einer der sch&ouml;nsten Straende auf   Oahu, mit  feinem wei&szlig;en Sand und B&auml;umen im Hintergrund. Das Wasser ist hier   azurblau und  die Wellen brechen sich in Strandn&auml;he, was gute Bedingungen zum   Bodyboarden  schafft. </p>
              <p>Hier kann man gut Picknicken, da die   B&auml;ume Schatten spenden.  Duschen und Toiletten sind ebenso vorhanden. Von hier hat man auch einen   guten  Blick auf ein paar kleinere Inseln. Die beiden Mokulua Inseln befinden   sich in  Richtung Norden und Rabbit Island im S&uuml;den. </p>
