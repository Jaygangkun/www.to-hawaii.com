              <h2 class="small">Haleiwa Beach Park Info</h2>
              <p>Der Haleiwa Beach Park befindet sich in dem Ort Haleiwa an Oahus Nordk&uuml;ste. Der Strand ist schmal und die Bedingungen zum Schwimmen sind hier nicht besonders gut, da sich am Boden Steine und ein Korallenriff befinden. Der Park eignet sich zum Picknicken.</p>
              <p>Surfer m&ouml;gen diesen Strand da sich die Wellen an dem Riff brechen. Kanu-Regattas finden hier auch statt. Der Name Haleiwa bedeutet im Hawaiianischen das "Haus des Fregattvogels."</p>
