              <h2 class="small">Bayview Beach Info</h2>
              <p>Der Bayview Beach befindet sich direkt neben dem Turtle Bay Resort an Oahus Nordk&uuml;ste. Er liegt in einer gesch&uuml;tzten Bucht zwischen zwei Felspunkten, Kalaeokauna (was "der Punkt des Altars" bedeutet) im Westen und Kalaeokamanu (was "der Punkt der V&ouml;gel" bedeutet) im Osten. Der erste wurde nach einem Altar benannt, der sich einst hier befand und von hawaiianischen Fischern benutzt wurde.</p>
              <p>Die Bedingungen zum Schwimmen sind gut hier, aber wenn die Wellen hoch sind (besonders in den Wintermonaten) k&ouml;nnen starke Rei&szlig;str&ouml;mungen auftreten, insbesondere entlang des Turtle Bay Points (der Halbinsel auf der sich der Turtle Bay Resort befindet). Den Strand erreichen Sie &uuml;ber den Parkplatz des Resorts. </p>
