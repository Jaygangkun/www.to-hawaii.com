              <h2 class="small">Queen's Beach Info</h2>
              <p>Dieser Strandabschnitt in Waikiki wurde nach der K&ouml;nigin Lili'uokalani genannt, deren Haus hier einst stand. Der Strand liegt inmitten von Waikiki und grenzt an das Waikiki Aquarium an.</p>
              <p>Die Mauer die hier in das Meer gebaut wurde hei&szlig;t Kapahulu Groin. Sie ist auch unter dem Namen The Wall oder Walls bekannt. Der Strandabschnitt neben der Mauer ist f&uuml;r Bodyboarder reserviert (Surfen ist hier nicht erlaubt). Es ist somit ein Paradies f&uuml;r Bodyboarder denn sie haben diesen Strandabschnitt ganz f&uuml;r sich allein.</p>
