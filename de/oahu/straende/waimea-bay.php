              <h2 class="small">Waimea Bay Info</h2>
              <p>Waimea Bay  ist einer der bekanntesten Surfstr&auml;nde in der Welt. In den Wintermonaten  (November bis Februar) k&ouml;nnen die Wellen hier um die 5 m hoch werden. Dann  kommen hier viele professionelle Surfer her um zu trainieren und an diversen  Wettbewerben teilzunehmen. Zu dieser Zeit kann es schwer sein, einen Parkplatz  zu ergattern.</p>
              <p> Surfwettbewerbe die hier stattfinden sind z.B. <em>The Eddie</em>. Dieser  Surfevent findet allerdings nur unter strikten Bedingungen statt. Die Wellen  auf dem offenen Meer m&uuml;ssen mindestens 6 m hoch sein. Daher fand der Eddie seit  1984 bis jetzt nur acht Mal statt. Dieser Surfevent findet zu Ehren von Eddie  Aikau statt, ein legend&auml;rer Surfer und Rettungsschwimmer an der Waimea Bay der  in 1978 starb als das Kanu Hokule'a sank und er versucht hatte Hilfe zu holen.</p>
              <p> Wenn die Wellen an der Waimea Bay hoch sind, ist der Strand  oft mit einer Leine abgespannt. Die Rettungsschwimmer machen dann oft  Durchsagen, um Menschen vom Wasser fernzuhalten. Lediglich professionelle  Surfer d&uuml;rfen dann ins Wasser. Und selbst f&uuml;r die Surfer ist Waimea   Bay eine Herausforderung.</p>
              <p> In den Sommermonaten sieht die Bucht ganz anders aus. Das Wasser ist dann  oft so ruhig wie auf einem See oder in einem Swimming Pool und man kann hier  wunderbar schwimmen und schnorcheln. Aber am besten schwimmt man nicht zu weit  raus denn die Str&ouml;mung kann trotzdem stark sein. </p>
