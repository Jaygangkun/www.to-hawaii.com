              <h2 class="small">Yokohama Bay Info</h2>
              <p>Der hawaiianische Name dieses Strandes ist Keawaula Beach. Es ist der am n&ouml;rdlichsten  gelegene Strand an Oahus Westk&uuml;ste. Kurz hinter dem Strand  endet der Farrington Highway  und man kann nur zu Fu&szlig; weiterlaufen auf dem Wanderweg zum Ka'ena Point, der  nordwestlichen Spitze der Insel. </p>
              <p>Yokohama Bay  ist einer der entlegendsten Str&auml;nde Oahus. Nur wenige Besucher kommen hierher.  In den Wintermonaten (November bis Februar) sind die Wellen oft sehr hoch und  die Str&ouml;mungen start, sodass schwimmen hier nicht m&ouml;glich ist. Und selbst in  den Sommermonaten k&ouml;nnen die Str&ouml;mungen stark sein. Dar&uuml;berhinaus ist der  Meeresgrund von einem Riff &uuml;berzogen. </p>
              <p> Von der Yokohama Bay hat man einen sch&ouml;nen Blick auf die Waianae   Mountains. Der hawaiianische Name "keawaula" bedeutet "der rote  Hafen," benannt nach der gro&szlig;en Anzahl der r&ouml;tlichen Tintenfische die hier  einst zu finden waren. Der Name Yokohama stammt von japanischen Fischern die  hier im fr&uuml;hen 20. Jahrhundert herkamen und den Strand so nannten.</p>
