            <div class="images_tabbox_toptext">
              <p> Die Insel Oahu hat eine K&uuml;stenl&auml;nge von circa 180 km. Die Str&auml;nde auf Oahu sind sehr sch&ouml;n und einzigartig - manche eignen sich zum Schwimmen und Schnorcheln, und andere bieten gute Bedingungen zum Surfen und Windsurfen. Die meisten Str&auml;nde auf Oahu sind Sandstr&auml;nde. Ein paar andere haben nur wenig oder gar keinen Sand und haben anstatt dessen Lavagestein und Felsen, wie z.B. Shark's Cove an Oahus Nordk&uuml;ste. Dieser Strand ist dennoch sehr beliebt, vor allem wenn das Meer ruhig ist, denn dann kann man hier gut schnorcheln. </p>
