              <h2 class="small">Makaha Beach Park Info</h2>
              <p>Der Makaha Beach Park liegt an der Westk&uuml;ste Oahus. Es ist einer der besseren Str&auml;nde, der sich gut zum Schwimmen, Bodyboarden und Surfen eignet. Seien Sie jedoch vorsichtig in den Wintermonaten, wenn hier oftmals sehr hohe Wellen und starke Str&ouml;mungen sind. Im Sommer ist das Meer hier ruhiger. Bevor Sie ins Wasser gehen beachten Sie eventuelle Warnschilder am Strand und fragen Sie die Rettungsschwimmer &uuml;ber die 
                Bedingungen im Wasser. </p>
              <p>Wenn Sie schwimmen wollen, gehen Sie in die Mitte des Strandes, weil hier kein Korallenriff ist. Surfer gehen meist an die rechte Seite des Strandes (nahe des Hotels), wo sich die Wellen an dem Riff brechen. Wenn Sie ein Anf&auml;nger des Surfsports sind, sollten Sie sich von diesem Bereich fernhalten. </p>
              <p>Der Makaha Beach Park liegt an der trockenen Seite der Insel, was bedeutet, dass die Luft hier sehr hei&szlig; und trocken ist. Es ist daher zu empfehlen, genug Wasser und Sonnencreme mitzubringen. Da der Sand hier oft sehr hei&szlig; ist, l&auml;uft es sich am besten mit Strandschuhen. Sie k&ouml;nnen auch einen Sonnenschirm mitbringen, da hier nicht viel Schatten vorhanden ist. </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Dichtgedr&auml;ngtes Strandleben gibt es hier nicht. An den Wochenenden treffen sich hier oft Einheimische um zu grillen, zu surfen und zu entspannen. Ein paar Picknicktische, Duschen und Toiletten sind vorhanden.</p>
