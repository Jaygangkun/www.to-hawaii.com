              <h2 class="small">Kawela Bay Info</h2>
              <p>Kawela Bay ist einer der am abgeschiedensten gelegenen Str&auml;nde auf Oahu. Er liegt in der N&auml;he der Turtle Bay (Schildkr&ouml;tenbucht) an der Nordk&uuml;ste der Insel. Dieser Strand liegt so versteckt, dass dort kaum andere Leute sind, was diesen Ort zu einem perfekten Bilderbuchstrand macht, ideal wenn Sie genug von &uuml;berf&uuml;llten Str&auml;nden haben. Hier k&ouml;nnen Sie sich entspannen, in dem warmen sanften Wasser baden oder einen gem&uuml;tlichen Strandspaziergang machen. </p>
              <p>Der Sandstrand ist schmal und umrandet von einem tropischen Regenwald. Halten Sie nach fallenden Kokosn&uuml;ssen Ausschau! Wenn Sie schwimmen, schnorcheln oder tauchen wollen ist Kawela Bay ideal. Der westliche Teil des Strandes wo ein paar H&auml;user stehen ist besser zum Schwimmen geeignet weil da weniger Korallenriffe sind. Aber gehen Sie nicht zu nah an die H&auml;user heran, weil dies eine private Wohngegend ist. </p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Das Meer ist hier im Sommer ruhiger. Wenn Sie hier in den Wintermonaten herkommen, beachten Sie, dass die Wellen an der Nordk&uuml;ste dann hoch und gef&auml;hrlich sein k&ouml;nnen. An diesem Strand gibt es keine Einrichtungen (Toiletten, Duschen, etc.) und auch keine Rettungsschwimmer. Es ist ein naturbelassener Strand der es wert ist zu sehen.</p>
