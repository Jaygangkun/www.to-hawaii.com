              <h2 class="small">Waiahole Beach Park Info</h2>
              <p>Waiahole Beach   Park sieht nicht allzu viele  Besucher, haupts&auml;chlich weil der schmale Strand dunklen  Sand hat, das Wasser hier meist braun ist und das Wasser in Strandn&auml;he zu flach  ist und ein Riff hat. Daher ist Schwimmen hier nicht m&ouml;glich. Ein Besuch lohnt  sich dennoch, da man von hier aus einen sch&ouml;nen Blick auf die kleine Insel  Chinaman's Hat (auch Mokoli'i Island genannt) hat. </p>
              <p>Vom Waiahole   Beach Park  kann man auch den Waikane Pier sehen, der in zahlreichen Hollywoodfilmen und TV  Serien gefeatured wurde, wie z.B. 50 Erste Dates, Fantasy   Island und LOST. Allerdings kann  man zu dem Pier nicht hinlaufen, da er sich auf einem Privatgrundst&uuml;ck  befindet.</p>
              <p> Der Strandpark hat eine gro&szlig;e Rasenfl&auml;che mit B&auml;umen die Schatten spenden,  allerdings sind keine Picknicktische vorhanden. Dieser Strand wird  haupts&auml;chlich von Fischern besucht. In der N&auml;he des Strandes befindet sich das Waiahole   Valley. In diesem Tal wurde fr&uuml;her  Taro angebaut. Die alte Waiahole Poi Factory (die in 1971 schlie&szlig;), war einst  eine der gr&ouml;&szlig;ten Poi-Fabriken auf der Insel.</p>
