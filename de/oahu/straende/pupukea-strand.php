              <h2 class="small">Pupukea Beach Park Info</h2>
              <p>Pupukea, was im Hawaiianischen "wei&szlig;e Muschel" bedeutet, ist ein langer, schmaler und steiniger Strandabschnitt an Oahus Nordk&uuml;ste. An beiden Enden des Strandparks liegen zwei Buchten - Shark's Cove und Three Tables Beach. Beides sind beliebte Schnorchelstr&auml;nde. In den Sommermonaten kann man hier gut schnorcheln und tauchen und das Korallenriff, die Lavar&ouml;hren und tropischen Fische bestaunen. Im Winter jedoch sind die Wellen hier zu hoch. Zu dieser Zeit finden an Oahus Nordk&uuml;ste viele Surfwettbewerbe statt. Schwimmen ist dann entlang dieser K&uuml;ste zu gef&auml;hrlich. </p>
