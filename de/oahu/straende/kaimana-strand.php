              <h2 class="small">Kaimana Beach Info</h2>
              <p>Der Kaimana Beach liegt im Osten von Waikiki am Ende der Strandpromenade in der N&auml;he des Kapiolani Parks, direkt vor dem gleichnamigen Hotel und neben dem War Memorial Natatorium, einem alten Denkmal.</p>
              <p>Der Strand ist relative weit, im Gegensatz zu Waikiki Beach, der an vielen Stellen eher schmal ist. Kaimana ist ein beliebter Strand, der meist gut besucht ist. Der Meeresboden in Strandn&auml;he ist relativ flach und sandig. Viele Surfer paddeln von hier aus zu einem beliebten Surfspot namens Old Man's.</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>An diesem Strand fand am 28. Dezember 1902 ein wichtiges Ereignis statt. Das Dampfschiff <em>Silvertown</em> aus San Francisco kam hier an. Auf seinem Weg von Kalifornien legte es ein Unterwasserkabel, was die Westk&uuml;ste der USA mit Hawaii verband. Das Kabel war die erste telegrafische Verbindung zwischen Hawaii und dem Festland.</p>
              <p>Die erste Nachricht wurde am 1. Januar 1903 an Pr&auml;sident Theodore Roosevelt &uuml;bermittelt. Bis zum heutigen Tage kann man am Meeresboden &Uuml;berreste des Kabels sehen. Das Kabel wurde im Jahre 1951 durch neuere Kommunikationssysteme ersetzt.</p>
