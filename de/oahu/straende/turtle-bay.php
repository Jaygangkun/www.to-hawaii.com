              <h2 class="small">Turtle Bay Info</h2>
              <p>Die Turtle Bay liegt an der Nordostk&uuml;ste  Oahus. Die Bucht tr&auml;gt den Namen Turtle   Bay (Schildkr&ouml;tenbucht) da hier vor  Jahren die Schildkr&ouml;ten ihre Eier an diesen Strand  gelegt haben (zur Zeit tun sie dies hier nicht mehr). Die meisten Menschen die  hierher kommen sind G&auml;ste des angrenzenden Turtle Bay Resorts. Allerdings  bevorzugen die Hotelg&auml;ste den nahegelegenden Bayview   Beach zum Schwimmen, da der  Meeresgrund der Turtle Bay zu viele Steine und ein Riff hat. </p>
              <p>Turtle Bay ist attraktiver f&uuml;r Surfer, die hier vor allem in  den Wintermonaten (November bis Februar) die hohen Wellen surfen. Die Str&ouml;mungen  k&ouml;nnen sehr stark sein, daher ist das Schwimmen in dieser Bucht nicht geeignet.  Rettungsschwimmer sind hier nicht stationiert. </p>
              <p>Obwohl die Turtle Bay  kein Ausflugsziel f&uuml;r Schwimmer ist, kann man hier wunderbar picknicken. Der  Strand ist ges&auml;umt von Eisenb&auml;umen und er ist sehr ruhig und oftmals leer. Am  westlichen Ende der Turtle Bay liegt eine kleine Insel mit Namen Papa'amoi. </p>
