              <h2 class="small">Kokololio Beach Park Info</h2>
              <p>Kokololio bedeutet "windig" in der hawaiianischen Sprache. Es ist der Name eines Tals und eines kleinen Flusses n&ouml;rdlich von Hau'ula an Oahus Nordostk&uuml;ste. Der Strandpark liegt am Fu&szlig;e des Tals. Es ist ein sch&ouml;ner langer Sandstrand und in den Sommermonaten ist das Meer meist ruhig und man kann hier schwimmen.</p>
              <p>In den Wintermonaten (November bis Februar) sind die Wellen oftmals ziemlich hoch und zu gef&auml;hrlich zum Schwimmen. Dies ist die Zeit wo Surfer hier die Wellen reiten.</p>
              <p>Der Felsen der am n&ouml;rdlichen Ende des Strandes am Ufer liegt hei&szlig;t Pali Kiloi'a, was im Hawaiianischen "Kliff der Fischsichter" bedeutet. Von diesem Kliff hat man eine gute Aussicht. Die alten Hawaiianer nutzten diesen Aussichtspunkt, um Fische zu sichten und zu fangen. Von hier oben aus gaben Sie den Fischern in Kanus Anweisungen, die dann mit Netzen die gesichteten Fische fingen.</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p><strong>Hinweis: </strong>Am n&ouml;rdlichen Ende des Strandes stehen ein paar H&auml;user. Als wir an diesem Strand einen Spaziergang machten, kamen pl&ouml;tzlich von einem der H&auml;user zwei gro&szlig;e Hunde auf uns zugelaufen und bellten uns an. Der Hundebesitzer war nirgends in Sicht und die Hunde hatten freien Zugang auf den &ouml;ffentlichen Strand. Die Hunde waren aggressiv und lie&szlig;en uns erst in Ruhe, nachdem wir sie laut anschrien. Falls Sie Kokololio Beach besuchen, vermeiden Sie diesen Bereich des Strandes, da die &ouml;rtlichen Anwohner ihre Tiere nicht zu &uuml;berwachen scheinen und kein Zaun zu deren Grundst&uuml;ck vorhanden ist.</p>
