              <h2 class="small">Halona Beach Info</h2>
              <p>Die Halona Beach Cove ist eine kleine Strandbucht an Oahus S&uuml;dostk&uuml;ste. Die kleine Bucht wurde ber&uuml;hmt durch den Film "From Here to Eternity" (Von Hier bis zur Ewigkeit) aus dem Jahr 1953, in dem die Bucht als Hintergrundkulisse diente, um eine Liebesszene eines Paares zu filmen. Deshalb tr&auml;gt die Bucht den Spitznamen "Eternity Beach."</p>
              <p>Die Bucht befindet sich in der N&auml;he eines beliebten Aussichtspunktes auf das Halona Blowhole (eine Lavar&ouml;hre, die, wenn die Wellen in das Loch flie&szlig;en, das Wasser in die H&ouml;he schie&szlig;t).</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Die Bucht ist &uuml;ber einen kurzen, steilen und steinigen Weg erreichbar, der die Klippen herunterf&uuml;hrt. Feste Schuhe sind hier ratsam. Schwimmen ist hier nur m&ouml;glich, wenn das Wasser ruhig ist und dann auch nur in Strandn&auml;he. Weiter drau&szlig;en am Eingang der Bucht treten starke Str&ouml;mungen auf, gegen die man nicht anschwimmen kann. Rettungsschwimmer sind hier keine stationiert. </p>
