              <p>Diese Bucht ist an drei Seiten von den gr&uuml;nen Koolau Bergen umrandet. Es ist eine sehr sch&ouml;ne Bucht, mit Picknicktischen und einem herrlichen Blick. Am Strand stehen Pinienb&auml;ume, die angenehmen Schatten spenden. An einer Seite des Strandes ist ein Fischteich, in den der Kahana Fluss flie&szlig;t. Das Wasser ist hier meist etwas tr&uuml;b und die Bedingungen zum Schwimmen sind nicht allzu gut, aber die Bucht ist trotzdem ein sch&ouml;nes Ausflugsziel.</p>
