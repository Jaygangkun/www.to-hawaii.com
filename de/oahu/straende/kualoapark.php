              <h2 class="small">Kualoa Park Info</h2>
              <p>Dieser sch&ouml;ne gro&szlig;e Park liegt an Oahus Ostk&uuml;ste. Hier kann man sich wunderbar entspannen, denn der Park hat eine gro&szlig;e Rasenfl&auml;che, B&auml;ume die Schatten spenden, Picknicktische und einen sch&ouml;nen Blick auf die kleine Insel Chinaman's Hat (die auch Mokoli'i Island genannt wird). Der Highway ist weit genug entfernt vom Park, sodass man keinerlei Verkehrsl&auml;rm h&ouml;rt, sondern lediglich die Wellen, wie sie an den Strand rollen.</p>
              <p>In der Mitte und am s&uuml;dlichen Ende des Strandes wurde eine Steinbarriere gebaut, sodass man vor hohen Wellen und Str&ouml;mungen gesch&uuml;tzt ist. Dieser Bereich eignet sich somit f&uuml;r Kinder und Nichtschwimmer. Rettungsschwimmer sind hier stationiert.</p>
              <p>Der Park, der auf einer Halbinsel liegt, ist so gro&szlig; dass man ausreichend Platz f&uuml;r Ballspiele hat. Am s&uuml;dlichen Ende grenzt der Moli'i Fischteich an den Park an.</p>
              <p>Kualoa war einst eine heilige St&auml;tte im alten Hawaii. Der Park ist deshalb im National Register of Historical Places (Nationales Register der Historischen St&auml;tten) gelistet.</p>
