              <h2 class="small">Kaneohe Bay Info</h2>
              <p>Diese "lebendige" Postkarte an Oahus Ostk&uuml;ste diente schon als Kulisse in vielen bekannten Hollywoodfilmen, wie z.B. <em>Pearl Harbor, Gilligans Insel, Und dann kam Polly </em>und <em>50 Erste Dates.</em></p>
              <p>Die Bucht erstreckt sich von der Mokapu Halbinsel (ein St&uuml;tzpunkt der U.S. Marine) bis hin zu der kleinen Insel namens Chinaman's Hat (Mokoli'i Island). Innerhalb dieses Bereichs liegen f&uuml;nf kleinere Inseln, einschlie&szlig;lich der ber&uuml;hmten Coconut Island (aus <em>Gilligans Insel</em>).</p>
              <p>Inmitten der Bucht sind ein paar Sandb&auml;nke, wo man t&auml;glich Boote vor Anker liegen sehen kann. Das Wasser ist hier bei Ebbe sehr flach, und viele Leute paddeln mit Kayaks raus um hier zu Picknicken.</p>
              <p>Die Bucht ist auch ein Brutplatz f&uuml;r Hammerhaie. Hunderte von Hammerhaien werden hier jedes Jahr geboren und verbringen ihre ersten Monate in den flachen Gew&auml;ssern der gesch&uuml;tzten Bucht. </p>
