              <h2 class="small">Kuhio Beach Park Info</h2>
              <p>Der Kuhio Beach Park ist Teil des Waikiki Beach. Er liegt inmitten von Waikiki. Der Strandpark wurde nach Prinz Jonah Kuhio Kalanianaole genannt, der im Jahre 1871 geboren wurde. Sein Haus namens Pualeilani stand einst an diesem Strand und er wohnte hier mit seiner Frau, der Prinzessin Elizabeth Kahanu.</p>
              <p>Drei Sehensw&uuml;rdigkeiten befinden sich im Kuhio Strandpark: die Prinz Kuhio Statue, die Duke Kahanamoku Statue und die Stones of Kapaemahu. Die Prinz Kuhio Statue wurde von Skulptor Sean Browne kreiert und wurde im Jahr 2002 eingeweiht. Prinz Kuhio (1871-1922) war ein Prinz zu Zeiten des hawaiianischen K&ouml;nigreichs.</p>
              <? include ("../../../theme/text-desktop-only.html") ?>
              <p>Jan-Michelle Sawyer kreierte die Duke Kahanamoku Statue, die im Jahre 1990 zum einhundertj&auml;hrigen Jubil&auml;um von Duke's Geburtstag eingeweiht wurde. Duke (1890-1968) wurde der "Vater des modernen Surfsports" genannt. Er war ein exzellenter Surfer, Olympischer Schwimmer und Kanufahrer.</p>
              <p>Die Stones of Kapaemahu repr&auml;sentieren vier legend&auml;re M&auml;nner: Kapaemahu, Kapuni, Kahaloa und Kinohi, die von einem weit entfernten Land nach Hawaii kamen. In Hawaii wurden sie durch ihre heilerischen Kr&auml;fte und Weisheit bekannt.</p>
