                Show werden traditionelle T&auml;nze aufgef&uuml;hrt. Jeder Luau ist anders, aber alle haben die traditionelle Imu Zeremonie, ein Festmahl und eine anschlie&szlig;ende Show mit T&auml;nzen und traditioneller Inselmusik. </p>
