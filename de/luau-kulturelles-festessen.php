              <h2>Was ist ein Luau?</h2>
              <p>Einen Luau zu besuchen steht bei vielen Hawaiireisenden ganz oben auf der Liste. Aber was genau ist ein Luau? F&uuml;r die Menschen Hawaiis ist es eine Art, verschiedenerlei Anl&auml;sse, wie zum Beispiel Geburtstage, eine reiche Ernte, einen Schulabschluss, etc. zu feiern. Es ist ein traditionell hawaiianisches Festessen mit kulturellen Aktivit&auml;ten, Ges&auml;ngen und Musik - ein Fest des Lebens. Ein Luau kann zu Hause oder in einem Park stattfinden. Wenn Sie zu Besuch auf Hawaii sind und hier keine Familie oder Freunde haben mit denen sie feiern k&ouml;nnen, besuchen Sie einfach einen der zahlreichen 
                Luauveranstalter. Diese Luaus finden speziell f&uuml;r die Besucher Hawaiis als auch f&uuml;r Einheimische statt.</p>
                <? include ("../theme/text-desktop-only.html") ?>
