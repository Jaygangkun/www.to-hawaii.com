              <p>&quot;Aloha&quot; ist mehr als nur ein Gru&szlig;wort. </p>
              <p>&quot;Aloha&quot; bedeutet R&uuml;cksicht aufeinander haben und warmherzig zu handeln.</p>
              <p>&quot;Aloha&quot; ist die Essenz von Beziehungen, in denen jeder Mensch f&uuml;r jede andere Person wichtig ist f&uuml;r eine kollektive Existenz. </p>
              <p>&quot;Aloha&quot; bedeutet zu h&ouml;ren, was nicht gesagt wird, zu sehen, was nicht gesehen werden kann und das Unbekannte zu kennen. </p>
              <p> (b) Bei der Ausf&uuml;hrung ihrer Staatsmacht im Namen der Bev&ouml;lkerung und in der Erf&uuml;llung ihrer Verantwortlichkeit, Verpflichtungen und ihres Dienstes f&uuml;r die Menschen, werden sich die Legislatur, der Gouvern&ouml;r, Staatsbeamten jeder Abteilung und die Richter 
                an den "Aloha Spirit" halten. [L 1986, c 202, 1]</p>
              <p><strong>Aloha Freitag</strong></p>
              <p> Viele Firmen erlauben es ihren Angestellten, sich am letzten Tag der Arbeitswoche, am Freitag, etwas lockerer zu kleiden - am sogenannten Aloha Friday (dt. Aloha Freitag). Freitags kann man also Aloha Hemden oder Blusen zur Arbeit anziehen. Allerdings war dies nicht immer so. Dieser Brauch hat eine interessante Geschichte, die sehr viel mit der Kleidungsindustrie auf Hawaii zusammenh&auml;ngt.</p>
              <p> Bis zu den 1940er Jahren wurden die meisten &ouml;rtlich hergestellten Artikel an Touristen verkauft. Die Angestellten bei gro&szlig;en hawaiianischen Firmen mussten sich formell kleiden - Anz&uuml;ge und Krawatten f&uuml;r M&auml;nner und Kleider, R&ouml;cke und Blusen f&uuml;r Frauen. Im Jahre 1947 wurde die Aloha Week (dt. Aloha Woche) gegr&uuml;ndet und die Menschen realisierten, dass es f&uuml;r die Wirtschaft Hawaiis gut w&auml;re, wenn auch die Einwohner Hawaiis mehr Kleidung tragen w&uuml;rden, die hier hergestellt wurde.</p>
              <p> Deshalb erlaubte es die Stadt Honolulu, dass M&auml;nner freitags sportliche Hemden (allerdings nicht zu bunt) an der Arbeit tragen d&uuml;rfen. In den 1960er Jahren lockerte sich die Mode noch mehr. Die Bank of Hawaii (dt. Bank von Hawaii) war die erste Kapitalgesellschaft,   die Aloha Kleidung an Freitagen erlaubte. Heutzutage gibt es kaum ein Gesch&auml;ft, dass den Aloha Freitag nicht befolgt.</p>
              <h3>Aloha Hemd</h3>
              <p> Der Aloha Spirit hat seinen Namen dem bekannten und bunten hawaiianischen Hemd gegeben, dass als Aloha Hemd bekannt ist. Es gibt unz&auml;hlig viele Muster und Designs und wird in frohen Farben hergestellt.</p>
              <p> Hawaii produziert zwei Arten von Hemden - Aloha Hemden die f&uuml;r die Einwohner Hawaiis hergestellt werden, und Hemden, die f&uuml;r die Touristen und f&uuml;r den Export in andere L&auml;nder hergestellt werden. Der Unterschied dieser Hemden ist der, dass die Hemden f&uuml;r die Einwohner meist Blumen- oder generische polynesische Motive haben, wobei die Hemden f&uuml;r die Touristen meist viel bunter sind und Motive wie z.B. einen sonnigen Tag auf Hawaii, Palmen, tropische Cocktails, Blumenkr&auml;nze, Str&auml;nde, blaues Meer und Blumen haben. </p>
              <p> Ein weiter Unterschied diese beiden Hemdentypen ist, dass Touristen diese Hemden  meist als Freizeitkleidung tragen, wobei die Einwohner Hawaiis diese Hemden an die Arbeit anziehen.</p>
