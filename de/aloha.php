              <p><strong>Was bedeutet Aloha?</strong></p>
              <p>Aloha ist das am besten bekannte hawaiianische Wort. In der hawaiianischen Sprache kann es "hallo" oder "tsch&uuml;&szlig;" bedeuten. Es bedeutet auch Liebe und Zuneigung. Das Wort Aloha wird auch in Kombination mit anderen Worten verwendet, wie in "Aloha Kakahiaka", was "Guten Morgen" bedeutet, "Aloha Auinala", was "Guten Tag" bedeutet, und "Aloha Ahiahi", was "Guten Abend" hei&szlig;t. Da das Wort Aloha auf Hawaii so bekannt und beliebt ist, wird Hawaii auch der "Aloha State" (dt. Aloha Staat) genannt.</p>
             
              <p> Aloha ist ein hawaiianisches Symbol. Seine Bedeutung geht weit &uuml;ber seine Definition   hinaus, die man in einem W&ouml;rterbuch findet. Auf Hawaii k&ouml;nnen Sie Aloha die ganze Zeit h&ouml;ren und Sie werden &uuml;berall mit Aloha behandelt.</p>
              <h2>Der Aloha Spirit</h2>
              <p> Die wortw&ouml;rtliche Bedeutung von Aloha ist "die Pr&auml;senz des Atems" oder "des Atemzugs   des Lebens". Es kommt von "Alo" was Pr&auml;senz bedeutet und "ha", was Atem bedeutet.   Aloha ist eine Art und Weise zu leben und sich gegenseitig mit Liebe und Respekt   zu behandeln. Dies beginnt, indem wir zuerst unser eigenes Wesen lieben lernen und dann diese Liebe auf andere &uuml;bertragen.</p>
              <p> Nach den alten Kahunas (dt. Priestern) ist die F&auml;higkeit, nach dem Aloha Spirit   zu leben eine Art und Weise, Selbstrealisation f&uuml;r den eigenen K&ouml;rper und die eigene   Seele zu erreichen. Aloha ist eine positive Energie zu senden und zu empfangen. Aloha ist in Harmonie zu leben. Wenn man den Aloha Spirit lebt, kreiert man positive   Gef&uuml;hle und Gedanken, die immer da sein werden. Sie existieren im Universum, multiplizieren 
                sich und gehen auf andere &uuml;ber.</p>
              <? include ("../theme/languages/de-text.html") ?>
              <p> Inspiriert von der Philosophie und der Weisheit des Aloha Spirits tragen heutzutage viele Institutionen und Gesch&auml;fte auf Hawaii diesen Namen, wie z.B. Aloha Tower, Aloha Stadium und Aloha Airlines. Viele hawaiianische S&auml;nger schreiben und singen Lieder &uuml;ber Aloha.</p>
              <p><strong>Der Aloha Spirit als Gesetz</strong></p>
              <p> Der Aloha Spirit wird als "Staatsgesetz" betrachtet. Auch wenn das Wort "Gesetz" zu streng wirkt, ist dieses Gesetz kein Gesetz was Ihnen &Auml;rger bringen wird wenn Sie es brechen. Es dient haupts&auml;chlich als Erinnerung f&uuml;r Regierungsbeamte, damit sie die Menschen mit F&uuml;rsorge und Respekt behandeln, genauso wie es ihre Vorfahren getan haben. Der Aloha Spirit ist eher eine Idee als ein Gesetz. Indem Regierungsbeamte diese Idee tagt&auml;glich anwenden, k&ouml;nnen sie zur Schaffung einer besseren Welt beitragen, einer Welt, die mit dem Aloha Spirit gef&uuml;llt ist.</p>
