              <p><strong>Hawaii - der entlegendste besiedelte Ort auf der Welt</strong></p>
              <p>Hawaii ist die entlegendste bev&ouml;lkerte Inselgruppe auf der Welt. Sie liegt mitten im Pazifischen Ozean, 3,850 km von Kalifornien und 6,195 km von Japan entfernt.</p>
              <p> Fr&uuml;her bekannt als die Sandwichinseln, das Hawaii Archipelago erstreckt sich auf einer L&auml;nge von 2,451 km von der Grossen Insel von Hawaii im S&uuml;dosten bis zum Kure Atoll im Nordwesten. Das macht Hawaii auch zu der weltweit l&auml;ngsten Inselgruppe.</p>
              <p>Der Staat Hawaii wurde nach der gleichnamigen gr&ouml;&szlig;ten Insel der gesamten Inselgruppe benannt, die mehr als zweimal so gro&szlig; ist wie alle anderen Hauptinseln zusammen. Die acht Hauptinseln des Staates Hawaii sind Oahu, wo auch die Hauptstadt Honolulu liegt, Big Island von Hawaii (auch Hawaii genannt), Maui, Kauai, Molokai, Lanai, Niihau und Kahoolawe. Diese acht liegen am S&uuml;dostende der gesamten Inselkette. In Richtung Nordwesten liegen noch 124 weitere unbewohnte Inseln, Riffe und Atolle, die zusammen die Nordwestlichen Hawaii-Inseln genannt werden.</p>
              <? include ("../theme/languages/de-text.html") ?>
              <p><strong>Von Vulkanen geformt</strong></p>
              <p> Alle der Hawaii Inseln wurden durch Vulkane geformt. Die Inseln sind die Spitze von im Meer liegenden hohen Bergen, die durch gigantische Eruptionen entstanden sind. Manche der Vulkane sind heute immer noch aktiv und der Bildung neuen Landes kann auf der Big Island zugeschaut werden. </p>
              <p><strong>Ein paar Daten</strong></p>
              <p> Die gr&ouml;&szlig;ten Berge sind auf der Big Island zu finden. Mauna Kea erstreckt sich &uuml;ber 4,205 m und Mauna Loa &uuml;ber 4,169 m &uuml;ber dem Meeresspiegel. Die durchschnittliche Landerhebung im gesamten Staat von Hawaii liegt bei 923 m.</p>
              <p> Hawaii ist der 43. gr&ouml;&szlig;te Bundesstaat in den USA, und kommt noch vor Rhode Island, Delaware, Connecticut, New Jersey, New Hampshire, Vermont und Massachusetts.</p>
              <p> Hawaii hat insgesamt 16,635 km2 Landfl&auml;che und 4,508 km2 sind von Wasser bedeckt. Die gesamte K&uuml;stenl&auml;nge betr&auml;gt 1,207 km. Der l&auml;ngste Fluss ist der Kaukonahua auf der Insel Oahu, der 53 km lang ist. Der h&ouml;chste Wasserfall ist der Kahiwa auf der Insel Molokai, der stolze 533 m misst. </p>
              <p> Hawaii's geographische Lage: 21?18&#8242;41&#8243;N, 157?47&#8242;47&#8243;W </p>
              
