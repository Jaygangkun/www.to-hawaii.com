              <h2>Hawaii Heiratslizens</h2>
              <p>Das gl&uuml;ckliche Paar muss eine Bescheinigung vom Staat einholen um die Heirat zu legitimisieren. Diese Heiratserlaubnis kostet eine geringe Geb&uuml;hr. Es ist zu empfehlen, sich um diese Lizens cirka einen Monat vor der geplanten Hochzeit zu k&uuml;mmern. Sowie die Lizens ausgestellt wurde kann die Zeremonie stattfinden. Sie ben&ouml;tigen hierf&uuml;r die Geburtsurkunde oder andere Identifikationspapiere und eventuell auch ihre Steuerinformationen. Falls Sie oder Ihr(e) Partner(in) vorher schon einmal verheiratet waren, m&uuml;ssen Sie dies dokumentieren. Sie werden die Bescheinigung in der Anwesenheit eines Zeugen unterschreiben. Die offizielle Lizens werden Sie per Post zugeschickt bekommen. Am Hochzeitstag muss diese dann von demjenigen, der Sie 
                traut unterschrieben werden. </p>
              <p> Auf dieser Webseite des Staates Hawaii finden Sie heraus, wie Sie die Lizens beantragen k&ouml;nnen. </p>
              <p><strong> <a class="content" target="_blank" href="http://www.hawaii.gov/health/vital-records/vital-records/marriage/index.html#howto"> Wie Sie eine Heiratslizens beantragen k&ouml;nnen</a></strong></p>
              <p><strong>Hawaii Heiratslizens</strong></p>
              <p> Nachdem die Hochzeitszeremonie stattgefunden hat, muss die Heiratsurkunde unterschrieben werden damit die Heirat offiziell wird. Ihr Pastor oder derjenige, der die Trauung durchf&uuml;hrt, wird das Dokument vorbereiten und beim Department of Health (Gesundheitsamt) einreichen. Neuverheiratete werden eine Kopie dieses Dokuments erhalten. </p>
              
              <p> Sie werden jemanden w&auml;hlen m&uuml;ssen, der die Trauung ausf&uuml;hrt, der im Staat Hawaii lizensiert ist. Dies muss kein kirchlicher Pastor/Pfarrer sein. F&uuml;r weitere Informationen, wie Sie jemanden finden der Sie trauen kann, besuchen Sie die Webseite des Hawaii Visitors and Convention Bureau (Hawaii Besucher und Kongress B&uuml;ro) auf <strong> <a class="content" target="_blank" href="http://www.hvcb.org">www.hvcb.org</a></strong>.</p>
              <h3>Wer kann eine hawaiianische Heiratsurkunde beantragen?</h3>
              <p>(mit freundlicher Genehmigung des Hawaii State Department of Health)</p>
              <ul>
                <li>Man muss kein Einwohner des Staates Hawaii oder US-Staatsb&uuml;rger sein, um 
                  eine hawaiianische Heiratsurkunde beantragen zu d&uuml;rfen.</li>
                <li>Bluttests sind nicht notwendig.</li>
                <li>Das legale Heiratsalter f&uuml;r M&auml;nner und Frauen ist 18 Jahre. </li>
                <li>M&auml;nner oder Frauen, die 16 oder 17 Jahre alt sind und heiraten wollen, m&uuml;ssen 
                  eine schriftliche Einverst&auml;ndniserkl&auml;rung beider Eltern, des rechtlichen Vormundes 
                  oder des Familiengerichts vorlegen. Die Eltern oder der Vormund m&uuml;ssen nicht 
                  Einwohner des Staates Hawaii sein. </li>
                <li>Einverst&auml;ndniserkl&auml;rungen sind bei lizensierten Trauungsberechtigten (marriage 
                  license agent), wie zum Beispiel einem Pastor, erh&auml;ltlich. </li>
                <li>M&auml;nner oder Frauen, die 15 Jahre alt sind und heiraten wollen, m&uuml;ssen eine 
                  schriftliche Einverst&auml;ndniserkl&auml;rung beider Eltern oder des Vormundes und eine 
                  schriftliche Bewilligung eines Richters des Familiengerichts vorlegen. Die Eltern 
                  oder der Vormund m&uuml;ssen nicht Einwohner des Staates Hawaii sein. </li>
                <li>Das Alter muss nachgewiesen werden. Eine beglaubigte Kopie der Geburtsurkunde 
                  m&uuml;ssen Heiratswillige, die 18 Jahre alt oder j&uuml;nger sind, vorlegen. Alle die 
                  19 Jahre oder &auml;lter sind, m&uuml;ssen eine g&uuml;ltige ID Karte oder F&uuml;hrerschein vorlegen. </li>
                <li>Cousins und Cousinen d&uuml;rfen heiraten. Jedoch darf die Blutsverwandtschaft 
                  zwischen dem zuk&uuml;nftigen Paar nicht n&auml;her als erste Cousins und Cousinen sein. </li>
              </ul>
              <p><strong>Wie beantragt man eine hawaiianische Heiratsurkunde? </strong><br />
                (mit freundlicher Genehmigung des Hawaii State Department of Health)</p>
              <ul>
                <li>Das heiratswillige Paar muss zusammen bei einem lizensierten Trauungsberechtigten 
                  (marriage license agent) erscheinen um eine Heiratslizens (Heiratserlaubnis) 
                  zu beantragen. Bevollm&auml;chtigte sind nicht erlaubt. </li>
                <li>Das heiratswillige Paar muss die n&ouml;tigen Papiere (Altersnachweise, eventuell 
                  ben&ouml;tigte Einverst&auml;ndniserkl&auml;rungen, ID Karten, etc.) vorlegen. Alle diese ben&ouml;tigten 
                  Papiere m&uuml;ssen im Voraus besorgt werden. </li>
                <li>Das heiratswillige Paar muss einen offiziellen Antrag ausf&uuml;llen und diesen 
                  pers&ouml;nlich bei einem lizensierten Trauungsberechtigten abgeben. Antr&auml;ge sind 
                  beim Trauungsbeamten erh&auml;ltlich oder k&ouml;nnen im Internet auf der Webseite des 
                  Hawaii State Department of Health (Hawaii Gesundheitsamt) heruntergeladen werden. 
                  Antr&auml;ge die per Post oder E-mail gesendet werden k&ouml;nnen nicht akzeptiert werden. </li>
                <li>Nach der Bewilligung wird die Heiratslizens von dem Trauungsberechtigten 
                  noch am selben Tage vor Ort ausgestellt.</li>
                <li>Die Heiratslizens kostet $60.00 (ca. 46 Euro). Barzahlung vor Ort ist m&ouml;glich. </li>
                <li>Die Heiratslizens ist nur im Staat Hawaii g&uuml;ltig. </li>
                <li>Die Heiratslizens verf&auml;llt 30 Tage nach Ausstellung. </li>
                <li>Wenn Sie nicht innerhalb von 30 Tagen heiraten, schicken Sie die unbenutzte 
                  Heiratslizens in dem vorgedruckten Briefumschlag, den Sie bei Ausstellung erhalten 
                  haben, zur&uuml;ck. Die Lizens wird dann ung&uuml;ltig gemacht. </li>
              </ul>
              <p><strong> Gleichgeschlechtliche Hochzeiten</strong></p>
              <p> Im Jahre 1997 hat der Staat Hawaii ein Gesetz unter dem Reciprocal Beneficiary Act (Gegenseitiges Beg&uuml;nstigungsgesetz) erlassen, dass viele eheliche Rechte und Vorteile von heterosexuellen Paaren auf homosexuelle Paare ausgeweitet hat. Obwohl homosexuelle Unionen im Staat Hawaii noch nicht anerkannt werden, gestatten sogenannte Reciprocal Benefit Gesetze gleichgeschlechtlichen Paaren gewisse Rechte, die einst nur verheirateteten 
                heterosexuellen Paaren zugestanden wurden. </p>
              <p> In einer monumentalen Gerichtsverhandlung in dem kontroversen Baehr gegen Miike Fall beschloss der Bezirksrichter Kevin Chang, dass die Ablehnung von Heiratslizensen f&uuml;r homosexuelle Paare gegen die Konstitution des Staates Hawaii verst&ouml;&szlig;t. Jedoch hat Richter Chang den Staat Hawaii noch nicht aufgefordert, Heiratslizensen auch f&uuml;r homosexuelle Paare auszustellen, da er noch auf die Einwilligung des Supreme Courts wartet. Obwohle homosexuelle Hochzeiten noch nicht legalisiert sind, k&ouml;nnen Paare dennoch ihr B&uuml;ndnis in einer privaten Zeremonie feiern. </p>
              <p> Gleichgeschlechtliche Paare k&ouml;nnen ein Reciprocal Beneficiary Zertifikat des State Department of Health (Hawaii Gesundheitsamt) beantragen. Sie m&uuml;ssen:</p>
              <ul>
                <li>Mindestens 18 Jahre alt sein.</li>
                <li>Nicht verheiratet sein oder eine andere Reciprocal Beneficiary Beziehung 
                  haben.</li>
                <li>Es muss dem Paar unter dem Hawaii Heiratzgesetz verboten sein zu heiraten 
                  (nur Paare die nicht traditionell heiraten d&uuml;rfen k&ouml;nnen ein Reciprocal Beneficiary 
                  Zertifikat erhalten). </li>
                <li>Die Reciprocal Beneficiary Beziehung nicht unter Zwang, N&ouml;tigung oder Betrug 
                  eingehen. </li>
                <li>Eine notarisierte Erkl&auml;rung der Beziehung unterschreiben und beim State 
                  Department of Health (Hawaii Gesundheitsamt) einreichen. </li>
                <li>Eine geringe Registrationsgeb&uuml;hr bezahlen. </li>
                <li>Sie m&uuml;ssen kein US-Staatsb&uuml;rger oder Einwohner des Staates Hawaii sein.</li>
              </ul>
