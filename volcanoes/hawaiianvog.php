              <p>Vog is air pollution caused by active volcanoes  (the name comes from &lsquo;volcanic smog&rsquo;). The volcanoes in Hawaii emit sulfur dioxide into the  atmosphere, which reacts with other gases, moisture, dust and sunlight to form  vog. </p>
              <p>The most severely affected region is the  western part of the Big Island of Hawaii because the tradewinds (which blow  from the northeast on most days of the year) carry the vog around the  mountains, where it then collects in the western regions on the island. In Hawaii, the gas plumes  of Kilauea rise up from three locations:  Halema'uma'u Crater, Pu'u 'O'o vent and from along the coastline where lava  flows from the east rift zone enter the ocean. &nbsp;</p>
                           <? include ("../theme/text.html") ?>
                         <p>In general, the Big Island  has higher levels than elsewhere, and the area around Kailua-Kona is often the  most heavily affected. Areas from Volcano   Village to Hilo can also experience  high levels of vog.</p>
              <p>Many people don&rsquo;t notice vog in low levels.  The volcanic fog is most noticeable when driving through the Hawaii Volcanoes   National Park. The sky  appears hazy and the horizon is obscured.</p>
              <p>Vog levels range from light to extreme  (where objects less than a mile away appear to have a blue/grey tint). Some  people may have more sensitivity to vog than others &ndash; reactions can include a  build-up of mucus, difficulty breathing, headache and sore throat. Vog is  mostly a concern for people who already suffer from asthma and other  respiratory conditions.</p>
              <? include ("../theme/text-desktop-only.html") ?>
               <p>Vog exposure generally increases with  altitude, so the amount of exposure at sea level is the lowest. Vog is most  present from 300 &ndash; 6,000 ft. (91 &ndash; 1,829 m), with the area around 1,000 ft. (305  m) experiencing the most. Above 6,000 ft., vog emissions decrease rapidly.</p>
                            <p>It is impossible to forecast a raise in vog  levels, as emissions can increase dramatically without warning, and levels are  dependent on variable winds and weather patterns. For people who may be  subjected to high levels of vog, the government recommends drinking plenty of  bottled water, staying indoors, and if using an air-conditioner, to keep the  vent closed and use re-circulated air if possible.</p>
