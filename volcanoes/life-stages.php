            <h1>Hawaiian Volcanoes Life Stages</h1>
            <div class="new-custom">
              <div class="midpanel_row">
                <div class="clear"></div>
                <p><img src="../images/volcano_3.jpg" width="148" height="125" alt="Volcano" align="right" />It is a law of nature that when something begins, it ends one day, sooner or later. Just like everything else, the Hawaiian volcanoes don't make an exception. Their existence passed through three development stages - birth, maturity and old age.</p>
                <h2>The Birth of Hawaii's Volcanoes</h2>
                <p>The Hawaiian volcanoes' birthplace is known to be 2.5 to 3 miles (4 to 5 km) under the ocean's surface. The outpouring of the lava created underwater mounds that were coming closer and closer to the surface as years went by. This process of outpouring was slow and lasted for thousands of years. </p>
                <p>When the volcano is within at about 330 feet (100 m) of the surface, it produces steam explosions that throw ash in the air. The volcano continues its eruption activities and it grows as a mountain above the surface. It is known that the oldest of Hawaii's volcanoes, Kohala Volcano, has broken the surface of the ocean exactly in this manner million of years ago. </p>
                <p>Scientists have found that about 15.5 miles (25 km) south of the Big Island of Hawaii, another volcano has started to form. Its seamount is slowly coming closer to the surface. However, the newly forming Hawaiian volcano it is not expected to break the ocean's surface within the next few thousands of years.</p>
                <? include ("../theme/text.html") ?>
                <p>Although it is difficult to determine the length of the Hawaiian volcanoes youth stages, there are specific signs that can tell when they are about to end. The tops of most Hawaiian volcanoes collapse and form large craters, known as calderas.</p>
                <p><strong>Maturity - the Caldera's Stage</strong></p>
                <p>The volcanoes' maturity, also known as the caldera stage, is characterized by many volcanic activities, such as repeated collapses and refilling of the volcanoes' calderas. The length of this age lasts for thousands of years. Mauna Loa and Kilauea on the Big Island are now in this stage.</p>
                <p>The process of caldera forming is not known in detail, and there are not many explanations about why the activity of this stage comes gradually to its end. However, it is assumed that it is related to the cooling of the magma, which leads to a decrease of volcanic activity. Toward the end of this stage, the caldera gradually fills, and at the end it completely vanishes.</p>
                <? include ("../theme/text-desktop-only.html") ?>
                               <h3>Volcanoes Old Age</h3>
                <p>The volcano is in its old stage when the caldera is filled and when the top of the shield is covered with a sleepy cod. As the age of the volcano increases, the lava's chemical components start to change and contain different substances, rich in sodium and potassium. </p>
                <p>Mauna Kea and Haleakala have gone through this stage and the different types of rocks appearing along the old ones attest that. Usually, old-age eruptions form steep cinder cones and are more explosive than eruptions in the earlier stages. For other volcanoes, such as Kahala, the eruption activities stop for thousands of years and then start again and create completely different types of rocks.</p>
