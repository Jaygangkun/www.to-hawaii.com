              <p>The abundance of active volcanoes on the Big Island of Hawaii means that lava is a prominent feature of the island&rsquo;s landscape. The Big Island is the youngest island in the Hawaiian archipelago and the only island that&rsquo;s still growing in size and new land is being formed. A series of earthquakes trigger eruptions, where fissures allow magma to reach the earth&rsquo;s surface. One of the unique features of Hawaiian lava flows is that they are considered relatively quiet, because the lava is, respective to other regions, quite fluid, and the gases escape readily. </p>
                   <p>Given its liquid nature, the lava in Hawaii is less likely to turn into ash or cinders as it escapes from deep within the earth, which has resulted in impressive floods of molten lava down the mountainsides. Eruptions here have lasted from a few days to ten months, frequently followed in two or three years by a flank eruption. The temperature of lava when it flows from an erupting volcano is between 1300 and 2400F (700 to 1300C)</p>
              <p>Hawaii has two types of typical lava flows &ndash; A'a and Pahoehoe. An a'a lava flow is typically blocky, can be 3-20 meters (9.8-65.6 ft) thick and rolls over itself across the ground like a tank track. The jagged front or the flow typically creeps forward and gets higher and higher until a section becomes unstable and breaks off, revealing the glowing central core. This lava flow has pointed and jagged blocks with dangerously sharp edges and spiny projections.</p>
                            <? include ("../theme/text.html") ?>
                          <p>Pahoehoe in Hawaiian means &lsquo;ropy,&rsquo; and this term is used to refer to basaltic lava that has a smooth, hummocky, or ropy surface. Pahoehoe is formed as a layer of skin covers the underlying liquid lava and the movement of the continued lava flow below begins to wrinkle the surface creating a ropy or billowy appearance. The bizarre shapes on the surface of pahoehoe lava flows are often referred to as lava sculpture. Pahoehoe lava flows are thinner than a'a flows and are sometimes only 30-50 centimeters (11.8-19.7 inches) thick.</p>
                                     <p>Some lava flows emerge from the vent as pahoehoe and change to a'a (never the other way around) as they are mixed, cooled, lose gas and increase in crystallization. As soon as a crust has formed on a pahoehoe flow, lava tubes below it continue to move the remaining liquid for long periods of time. The size of the tubes range from only a few inches to many feet in diameter. </p>
              <p>When the source of lava stops, the liquid keeps on draining, leaving lava tubes or caves. These lava tubes can extend for several kilometers &ndash; as they do in Hawaii &ndash;as this is anefficient way for the lava to travel long distances without significant cooling. </p>
              <? include ("../theme/text-desktop-only.html") ?>
              <p>Lava flows can have many features depending on the rate at which they cool. One of the simplest formations is &lsquo;spatter cones&rsquo; &ndash; steep, conical hills that are formed of molten lava ejected from a vent which splashes down and over the sides of the developing mound. Spatter cones are often found in Hawaii because of the particularly fluid magma.</p>
                        <p>Another  feature of the Hawaiian landscape formed by lava is the islands&rsquo; lava lakes. These are large volumes of molten lava contained in a vent, crater, or broad depression. Scientists use the term to describe both lava lakes that are molten and those that are partly or completely solidified.</p>
