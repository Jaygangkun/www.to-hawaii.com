                <p>Named after the two pinnacles of volcanic rock that are a distinctive landmark for mariners, the Gardner Pinnacles (P&#363;h&#257;honu in Hawaiian, meaning "turtle surfacing for air") 
                  are the smallest island in the Northwestern Hawaiian Island chain. While the two islands are tiny at just 5.759 acres (0.024 km&sup2;), the surrounding reef is expansive, encompassing an area of 600,000 acres (2,428 km&sup2;). The highest peak measures 190 feet (58 m). </p> <?php
show_widgets('','https://www.to-hawaii.com/vacation-rentals.php','rating_desc');
?> <p>First discovered on June 2, 1820 by the American whaler Maro commanded by Captain Joseph Allen, the Gardner Pinnacles are 588 miles (946 km) northwest of Honolulu. While the islets' landmass is home to only one plant, the luscious sea purslane, the surrounding underwater life is much more diverse. The underwater shelves are diverse and home to the highest recorded numbers of fish species in the Northwestern Hawaiian Islands, including many species not found anywhere else in the Northwestern Hawaiian island chain. </p>
                <? include ("theme/text.html") ?>
                <p>The coral reef area is home to an abundance of giant opihi, the endemic Hawaiian limpet. The islets' rocky inter-tidal areas are an ideal habitat for them. Besides the large numbers of fish species, the Gardner Pinnacles also have almost twice the number of coral species than the two similar rocky neighbor islands to the south, Nihoa and Necker Island. In fact, 27 documented species of stony coral can be found there.</p>
                <p>Despite the islands' small size, the Gardner Pinnacles is also home to numerous insects, such as spiders, moths, mites, centipedes, flies, isopods, beetles and earwings, and 19 species of seabirds, including the rare blue gray noddy.</p>
                                 <p>The Gardner Pinnacles are part of the Northwestern Hawaiian Islands wildlife refuge monument. A special permit is required to visit this area. The islands are off-limits even to the military, who once made an unauthorized 
                  entrance to blow off the tip of an outcrop to build an emergency helicopter landing spot. </p>
