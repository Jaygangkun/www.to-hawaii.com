?> <h3>Inventor of the bodyboard</h3>
                <p>The modern bodyboard, as we know it today, was invented by Tom Morey from California in 1971 while he was on the island of Hawaii. Morey trademarked the name Morey Boogie in 1973. In 1977, Morey was already producing about 80,000 Boogie Boards per year.</p>
                <p><strong>The equipment you will need</strong></p>
                <p>Bodyboarding doesn't require a lot of equipment. All one needs is a board and fins. The size of the board isn't crucial. However, smaller boards fit smaller people best and vice versa. It is optimal if the board stands up to the belly button. The fins should have an open heel, because closed-heel fins tend to get pulled off in stronger waves. </p>
                <p>For people who easily get blisters on their feet, neoprene foot socks are available. It is also good to have a wrist leash attached to the board so that it doesn't get lost in the water. Some women also find it more convenient to wear a one piece swimsuit or a surf top because a bikini top can slip away.</p>
                                <p><strong>How to bodyboard</strong></p>
                <p>The basic technique of bodyboarding is easy. While paddling out, head and chest shouldn't touch the board. The hips should touch the back edge of the board. It is most efficient to keep the fins underwater when kicking. When catching a wave, the elbows should be kept on the board and not drag in the water. </p>
             <? include ("theme/text-desktop-only.html") ?>
                <p>Beginners often find it exhausting to paddle out when the waves are big. They get carried back when the waves wash over them. An effective method of avoiding the thrust of the waves is called duckdiving, which means diving under the wave. It is best done when the whitewash of the wave is about 6 feet away. To execute this method, one has to grab the board between the nose and the midpoint of the board and push all the upper body weight onto the hands until the nose of the board sinks. Then one has to use one knee to push the tail of the board under the water. Body and board should be as much under the water as possible. </p>
                <p>These are very basic techniques for beginners. Although some consider bodyboarding much easier than surfing, it takes about the same time to become proficient in this sport. That's because bodyboarding is more trick-oriented than surfing and there are many variations of maneuvers possible. For experts it is therefore rather boring to ride a wave in a straight line. </p>
                <p><strong>Bodyboarding etiquette</strong></p>
                <p>Bodyboarding is not only fun, it is also a great exercise. It strengthens the arms and legs and also works the hips and stomach. For beginners it is best to avoid practicing too close to surfers, as they might run you over. Be sure you are familiar with some basic surfing rules. Dropping in a wave another surfer is surfing, for example, can be dangerous, because you might collide with each other. Generally, the surfer who is nearest the breaking part or peak of a wave has the right of way. </p>
                <p>Beginners should also pay attention to the tides. During low tide, bodyboarding can be dangerous because the reef is closer to the surface and its sharp rocks can cut careless bodyboarders to pieces. If one gets cut, no matter how small, the first thing to do is get out of the water before you attract sharks. Then disinfect the wound because the coral reef is a living animal and bacteria or coral cells in the wound can easily cause an infection. </p>
                <p>If you are a cautious beginner, bodyboarding is a fun sport that can be enjoyed every day when the surf is good. It's a great workout, clears the mind from stressful thoughts and can be enjoyed by anyone who loves to be in the water.</p>
