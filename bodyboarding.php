                <p><strong>A fun sport for beginning surfers</strong></p>
                <p>Some people say one can't spend time or live in Hawaii without having tried surfing at least once. While it takes time and effort to learn the art of
                  surfing on a surfboard, bodyboarding allows beginners to enjoy the sport immediately without having to take lessons.</p>
                <p><strong>Bodyboard design</strong></p>
                <p>A bodyboard, in surfing slang also known as a sponge, is a small, rectangular piece of foam and usually between 39 and 42 inches long. Most bodyboarders ride the wave lying down, called prone, but it can also be ridden standing up.</p>
                <p>The board's core is made from polypropylene, dow, or arcel, which are all types of plastic. Each one of them gives the board a different amount of flexibility. The core is glued to a thick plastic bottom, also known as the slick. The top of the board is made from softer plastic to give the rider a firm grip.</p>
                <? include ("theme/text.html") ?>
                <p><strong>Bodyboards are handy</strong><strong><img border="0" src="images/bodyboarding.jpg" width="148" height="125" align="right" alt="Bodyboarding" /></strong></p>
                <p>One of the advantages of bodyboards is that they are smaller and lighter than surfboards, so the risk of injury by the board is greatly reduced. Furthermore, bodyboards fit easily into a car and are also allowed on the bus. A backpack style cover for the board makes walking and bike riding convenient. Bodyboards are also affordable. A medium-priced board for beginners costs between $20-80. In Hawaii, they are available 
                  in surf shops and in some supermarkets and drugstores.</p>
