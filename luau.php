?> <p><img src="images/luau/p-poi.jpg" width="148" height="125" alt="Poi" align="left"/>In the ancient Hawaii, a luau was celebrated for special occasions, much like today. What was different back then was that it was taboo for men and women to dine together. Also, the dining took place on the ground on lauhala mats (leaves of the hala tree weaved together). There was no silverware or other utensils. People ate with their hands. One of the traditional foods was poi, made from taro roots that were cooked or baked, pounded to a paste and then fermented (much like it is made today). Poi can either be three-finger, two-finger, or one-finger poi, depending on the amount of fingers needed to eat it (depending on its consistency). One-finger poi has the thickest consistency. Other traditional luau foods were dried fish, pork (cooked in the <em>imu</em> or underground oven), sweet potatoes and fruits.</p>
