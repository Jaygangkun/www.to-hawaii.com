                <h2>What is a Luau?</h2>
                <p>Attending a luau (Hawaiian: l&#363;'au) is on the to-do-list of many Hawaii visitors. But what exactly is a luau? For Hawaii's people, it is a way to celebrate a variety of occasions, such as a birth, birthday, successful harvest, graduation and many other occasions. A luau is a feast of life. It can be held at a family's home or a park in form of a huge family gathering, with lots of traditional luau foods and games. </p>
                <p><img src="images/luau/p-luaudance.jpg" width="148" height="125" alt="Luau dance" align="left"/>If you're visiting Hawaii and don't have a big ohana (family) here, you can visit one of the organized luaus, 
                  which are held for visitors and locals alike. This  celebration features traditional Polynesian foods, music, dances and games. You will also have the chance to learn about the culture of Hawaii and  other Polynesian island nations. </p>
                <? include ("theme/text.html") ?>
                <p>Typical events that take place at organized luaus include the <em>imu</em> ceremony (roasting of the kalua pig in a traditional underground oven), hula and other Polynesian dances, lei making lessons, Hawaiian storytelling, a royal court procession, hukilau on the beach demonstration (a traditional fishing method) and other games, as well as a show after dinner. Each organized luau is different and features unique events. Some luaus may not include games at all and instead focus on the food and musical performance only. All luaus include a luau dinner and drinks. Organized Hawaiian luaus are offered on Oahu, Maui, Kauai and the Big Island.</p> <?php
