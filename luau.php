                                <p> In the old Hawaii, this important feast was called 'aha'aina ('aha &ndash; gathering and 'aina &ndash; meal). In 1819, King Kamehameha II ended long-time traditional religious practices. He also started feasting together with women for the first time, thereby ending this Hawaiian religious taboo. Shortly after, the name l&#363;'au replaced 'aha'aina. The new name luau actually means &ldquo;young taro tops&rdquo; in the Hawaiian language. The new name comes from a traditional dish that is usually served at a luau, which includes taro leaves with coconut cream and chicken or octopus.</p>
