            <h1>Blue Hawaii Hawaiian Cocktail Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>The Blue Hawaii cocktail was invented by Harry Yee, a legendary bartender at the Hilton Hawaiian Village in Waikiki. It was the year 1957 when Yee was asked by a sales representative of Dutch distiller Bols to create a drink that featured their blue color of Cura&ccedil;ao liqueur. There are several variations of this popular drink. Some people prefer vodka over rum. Others add sweet and sour to the mixture. Also, the cocktail can either be served on the rocks or blended as a tropical frozen treat.</p>
              <strong>Ingredients:</strong>
              <ul>
                <li>1 oz light rum</li>
                <li> 1 cherry</li>
                <li> 2 oz pineapple juice</li>
                <li> 1 oz Blue Curacao liqueur</li>
                <li> 1 oz cream of coconut</li>
                <li> 1 slice pineapple</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Blend the blue curacao, light rum, pineapple juice and cream of coconut with one cup ice in a blender at high speed. Pour contents into a glass and decorate the cocktail with a slice of pineapple and a cherry. And last but certainly not least, enjoy...responsibly! </p>
