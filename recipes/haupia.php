            <h1>Haupia Hawaiian Dessert Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Haupia is a traditional Hawaiian coconut-flavored tropical treat that can be found at any and every Hawaiian luau. This dessert takes less than twenty minutes to prepare, then another two hours to set up properly.  In this recipe you will immediately notice the creamy texture as opposed to a gelatin texture so commonly found at most restaurants and frowned upon at all luaus.</p><? include ("../theme/text-desktop-only.html") ?>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>2 cups coconut milk<br />
                </li>
                <li> 1 cup whole milk</li>
                <li> 6 tbsp granulated sugar</li>
                <li> 5 tbsp cornstarch</li>
                <li> 1/4 tbsp vanilla extract</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Pour 1 cup coconut milk into a medium saucepan. Combine sugar, cornstarch and vanilla into coconut milk.  Stir constantly over low heat until contents begin to become bubbly and thicken. At this point, add the other cup of coconut milk and 1 cup of whole milk and continue to stir over low heat until smooth and once again thick. Remove from heat, pour into an 8&quot; square pan or onto a graham cracker crust and let it cool to room temperature and then put it in the refrigerater for 2 hours or until completely chilled. Remove from refrigerater, cut into 2&quot; square pieces and serve on ti leaf lined trays. *Unless of course, you choose to pour it onto a graham cracker crust. </p>
