            <h1>Papaya with Chicken Curry Salad Hawaiian Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>This is a simple recipe and makes for a great lunch. Sweet, savory and protein  packed. </p>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>3 fresh ripe papayas</li>
                <li> 3 chicken breasts or skinless boneless thighs</li>
                <li> 1 cup mayonnaise</li>
                <li>&frac14; cup white wine</li>
                <li> 4 tablespoons curry powder</li>
                <li> 2 stalks celery (diced)</li>
                <li> 1/3 cup chopped scallions</li>
                <li> 1/3 cup raisins</li>
                <li>&frac34; cup whole roasted unsalted cashews</li>
                <li> Olive oil</li>
                <li> Sea salt</li>
                <li> Black pepper</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Cut papayas in half and scoop out seeds. Set aside in the  refrigerator. Preheat oven to 350&deg;F. Sprinkle chicken with a little salt and  pepper and rub in olive oil. Roast for 35-40 minutes until chicken is   cooked. Set aside to cool until easy to handle. Dice chicken into bite size  pieces. For the dressing combine the mayo, wine and curry powder.  Blend mixture well. </p>
             <? include ("../theme/text-desktop-only.html") ?>
              <p>In a large bowl add dressing to chicken. Make sure you have  enough dressing to moisten chicken well. Add celery, scallions and raisins.  Refrigerate a few hours to allow flavors to blend. Add cashews and serve in a  half of papaya.</p>
