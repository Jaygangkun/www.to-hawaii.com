            <h1>Kalua Pig Hawaiian Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Kalua is a traditional Hawaiian method of cooking. In the Hawaiian language, kalua literally means &quot;to cook in an underground oven.&quot; So kalua describes the food that is cooked in this manner, such as kalua pig or kalua turkey. In the old Hawaii, an imu (underground oven) was built the following way: A pit was dug in the ground and lined with rocks. Then a fire was built and when the rocks were hot, the meat and other foods were wrapped in banana, ginger or ti leaves and placed into the pit. Well, since most people today don't have an imu in their backyard, this recipe is for the rest of us and although it surely will not be as tasty as the real deal it will provide a similar flavor.</p><? include ("../theme/text-desktop-only.html") ?>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>3 pound pork butt (well marbled)</li>
                <li> 1 1/2 tbsp liquid smoke</li>
                <li> 2-4 tbsp (to taste) Hawaiian salt</li>
                <li> 6-8 ti or banana leaves</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Preheat oven to 350 F.</p>
              <p> Trim all visible fat from the pork butt and then score the meat all over. Rub down with the Hawaiian salt, then with one tablespoon of liquid smoke. Wrap the pork butt with ti leaves or banana leaves, then tie with cooking twine to secure.</p>
              <p>Now, place the wrapped pork into a roasting pan and add two cups of water. Next, add a pinch of  Hawaiian salt and 1/2 tablespoon of liquid smoke to the water, then cover the pan with aluminum foil and bake for 1 hour at 350&deg;F. After an hour, reduce heat to 325&deg;F and cook for an additional 2 1/2 to 3 hours.<br />
                <br />
                Once taken out of the oven, carefully remove leaves, then shred the pork, allowing it to sit in the pan juices, soaking for 15 to 20 minutes before serving.</p>
              <p>The pork meat is very tender and moist, with a slightly salty, smoky flavor that is absolutely delicious.</p>
              <p>For a traditional Hawaiian meal or a luau at home, serve with rice, macaroni salad, lomi lomi salmon, sweet potatoes and poi. This recipe will make 6-8 servings, so if you have any leftovers try it on Hawaiian sweet bread for the best pork sandwich you will ever taste!</p>
