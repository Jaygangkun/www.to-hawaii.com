            <h1>Lomi Lomi Salmon Hawaiian Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Does lomi lomi make you think of a spa? That's because lomi lomi is the Hawaiian word for massage. Lomi lomi is also a traditional side dish in Hawaiian cuisine that was introduced to the Hawaiian people by the early Western sailors. It is a fresh tomato and salmon salad that is considered  a classic and integral part of most Hawaiian parties and gatherings.</p>
              <? include ("../theme/text-desktop-only.html") ?>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>8 oz. salted salmon filet</li>
                <li> Fresh tomatoes, peeled and diced</li>
                <li> 1 medium Maui onion or any mild onion</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Cut the salmon into small pieces. Add onions and tomatoes and massage (or lomi lomi) this mixture with your hands until all ingredients and their flavors are broken up and mixed. Lomi lomi salmon is best served chilled. If you only have unsalted salmon available, rub the salmon with Hawaiian salt (or rock or kosher salt) and let stand overnight. Rinse the salmon the next day and soak it in cold water for an hour, changing the water a few times. This dish is tasty on its own, however, if you're preparing a traditional Hawaiian feast, it is great together with poi. The saltiness of the lomi lomi salmon is the perfect complement to the sweet and sour smoothness of poi.</p>
