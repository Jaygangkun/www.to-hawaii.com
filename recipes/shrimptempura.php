            <h1>Rock Shrimp Tempura Hawaiian Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>2 lb rock shrimp</li>
                <li>&frac12; cup flour</li>
                <li>&frac12; cup cornstarch</li>
                <li> 1 teaspoon baking powder</li>
                <li>&frac12; teaspoon salt</li>
                <li>1 egg</li>
                <li> 2/3 cup milk</li>
                <li>6 cups canola oil</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>In a small bowl, sift together the flour, cornstarch, baking  powder and salt. In a medium bowl using a wire whisk, beat the egg for 30  seconds. Slowly whisk the milk into the egg. Add the dry ingredients and stir  until just mixed. The mixture will have lumps, but don't&nbsp;overmix it. Refrigerate the batter until  ready to use.</p><? include ("../theme/text-desktop-only.html") ?>
              <p>To prepare the shrimp, pat the rock shrimp dry with paper  towels and place on a plate. Place oil in a large saucepan over high heat and heat up to  375&deg;F.</p>
              <p>Line a platter with paper towels. Add the shrimp to the  batter in batches. Transfer the battered shrimp to a strainer on a plate to  catch the extra batter drippings. Using tongs, place the shrimp into the hot  oil. Fry until the shrimps turn golden brown. When the shrimps are cooked,  transfer them to a platter to drain. Repeat until all the shrimps are fried.</p>
              <p>Serve with mango chile sauce and/or soy-sesame aioli (see  below).</p>
              <p><strong>Soy-Sesame Aioli</strong></p>
              <p><strong>Ingredients</strong></p>
              <ul>
                <li>2 teaspoons dry mustard</li>
                <li> 2 teaspoons water</li>
                <li> 1 chopped small onion</li>
                <li> 2 tablespoons soy sauce</li>
                <li> 2 tablespoons sesame oil</li>
                <li>&frac14; cup oil</li>
                <li> 2 teaspoons dashi (Japanese soup stock)</li>
                <li> 1 tablespoon sugar</li>
                <li> 1 teaspoon fresh ground black pepper</li>
                <li> 2 teaspoons salt</li>
                <li> 1 cup mayonnaise</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Mix the dry mustard and water to form a thick paste.  Transfer to a blender and add all other ingredients except the mayonnaise.  Blend well, then add the mayonnaise and blend again.</p>
