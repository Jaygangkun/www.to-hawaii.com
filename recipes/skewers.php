            <h1>Coconut Flavored Chicken Skewers</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>2 lbs chicken breasts</li>
                <li> 3 cups shredded sweetened coconut</li>
                <li> 1 cups shredded unsweetened coconut</li>
                <li> 2 cups panko (Japanese breadcrumbs)</li>
                <li> 1/3 cup toasted sesame seeds</li>
                <li> 1 teaspoon salt</li>
                <li>&frac12; teaspoon fresh ground black pepper</li>
                <li> 2 cups flour</li>
                <li> 6 eggs, lightly beaten</li>
                <li> Canola or coconut oil for frying</li>
                <li> Mango chile dipping sauce</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Cut the chicken breasts into long thin strips and thread  onto bamboo skewers. In a shallow bowl mix both types of shredded coconut, panko,  sesame seeds, salt and pepper. Dip the skewers in flour, then dip into egg to  coat well. Cover with the the breading mixture and set aside on a tray until  you have all the skewers coated. You may refrigerate the skewers until you are  ready to fry them.</p>
              <? include ("../theme/text-desktop-only.html") ?>
              <p>To cook the chicken, use a deep skillet or deep fryer. Heat  3-4 inches of oil to 375&deg;F. Fry the skewers, a few at a time, until  golden brown and crisp, approximately 3-4 minutes. Remove from the oil and  drain on a paper towel. Serve with warm mango chile sauce.</p>  
