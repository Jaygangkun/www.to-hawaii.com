            <h1>Mai Tai Hawaiian Cocktail Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Maita'i is the Tahitian word for &quot;good&quot;. It is also the name of a popular drink (though spelled as two words). It was  invented by Victor J. Bergeron, Jr. from San Francisco. In 1934, he opened a small restaurant in Oakland, California, named Hinky Dink's. Its  name was soon changed to Trader Vic's and became a very popular Polynesian-themed restaurant chain. </p>
              <p>The Mai Tai was created in his restaurant in 1944  and introduced to the Hawaiian Islands in 1953. The story how the Mai Tai got its name goes like this: One day Victor created the cocktail  for some friends who were visiting from Tahiti. One of them tasted it and cried out: &quot;Maita'i roa ae!&quot; (meaning &quot;very good!&quot; or &quot;the very best!&quot;), hence the name. The following recipe is the original, and though today there are many variations, this will remain &quot;the very best&quot;.</p><? include ("../theme/text-desktop-only.html") ?>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>1oz Jamaican rum (17-year-old J. Wray &amp; Nephew  rum if you have it)</li>
                <li> Juice from one fresh lime</li>
                <li> 1/2 oz Holland DeKuyper Orange Cura&ccedil;ao</li>
                <li> 1/4 oz Trader Vic's Rock Candy Syrup</li>
                <li> 1/2 oz French Garnier Orgeat Syrup</li>
                <li> Sprig of fresh mint</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Put ice in a shaker. Combine all ingredients and as with any cocktail, always add your non-alcoholics first as to not water down the beverage. Shake 5-6 times and pour into a rocks glass. Garnish with a mint sprig (or one pineapple spear and a cocktail cherry).</p>
              <p>Now as mentioned before there are many ways to make a Mai Tai, however, just to clear up several misconceptions...There is no float of dark rum, nor grenadine or 5 different tropical fruit juices. No umbrella, unless you are on a cruise ship and it is raining. And the original recipe did not call for sour mix. </p>
              <p>If you're curious, here is also the recipe for today's Trader Vic Mai Tai.</p>
              <p><strong>Ingredients:</strong></p>
              <p> 1/4 Lime squeeze (drop in glass as garnish)<br />
                1/2 oz lemon juice<br />
                3/4 oz Trader Vic's Mai Tai mix<br />
                2 oz Royal Amber Rum</p>
              <p> Garnish: cocktail cherry, pineapple, mint sprig</p>
