            <h1>Blackened Ahi Hawaiian Recipe</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Ahi is Hawaii's  prized fish - yellow fin tuna caught in deep Pacific waters. People in the Islands love it raw as  sashimi, seared on the edges, blackened, or cut into bite size pieces and  seasoned in a famous local dish known as poke. Fresh ahi   should have a deep red color, firm texture and a glistening surface that  indicates high fat content. </p><? include ("../theme/text-desktop-only.html") ?>
              <p><strong>Ingredients:</strong></p>
              <ul>
                <li>Cilantro-ginger oil (see recipe below)</li>
                <li> 2 tablespoons oil</li>
                <li>&frac12; cup blackening mix (see recipe below)</li>
                <li> 2 lb shashimi grade ahi, cut into 8 steaks</li>
                <li>&frac14; cup scallions, cut diagonally for garnish</li>
              </ul>
              <p><strong>Preparation:</strong></p>
              <p>Place blackening spice mix in a shallow plate. Coat the ahi  well with the spices. In a cast iron skillet over high heat, add 2 tablespoons  of oil. Sear the ahi very quickly on all sides. The spices will turn black as  you do this. Do not overcook the fish. Transfer the fish to a cutting board and slice into 1/2  inch slices.</p>
              <? include ("../theme/text.html") ?>
              <p>To serve, place the ahi slices on a platter, drizzle with  cilantro-ginger oil and garnish with green onions.</p>
              <p><strong>Blackening spices</strong></p>
              <p>Ingredients:</p>
              <ul>
                <li>2 cups paprika</li>
                <li> 8 tablespoons chili powder</li>
                <li> 1 tablespoon salt</li>
                <li> 2 teaspoons fennel seeds</li>
                <li> 2 teaspoons cumin seeds</li>
                <li> 2 teaspoons onion powder</li>
                <li> 2 teaspoons cayenne pepper</li>
                <li> 4 teaspoons black pepper</li>
                <li> 2 teaspoons garlic powder</li>
                <li> 2 teaspoons red pepper flakes</li>
                <li> 2 tablespoons dry basil</li>
                <li> 2 tablespoons dry oregano</li>
                <li> 2 tablespoons dry dill weed</li>
              </ul>
              <p>Mix all ingredients in a large mixing bowl and mix very  well. Store in an airtight container. This should make approximately 3 cups of  spice mix.</p>
              <p><strong>Cilantro-Ginger Oil</strong></p>
              <ul>
                <li>&frac12; cup olive oil</li>
                <li> 3 tablespoons chopped cilantro</li>
                <li> 2 tablespoons fresh chopped ginger</li>
                <li> 2 fresh garlic cloves (minced)</li>
              </ul>
              <p>Place oil in a small saucepan over medium heat until warm.  Add the cilantro, ginger and garlic. Cook for 2 minutes. Remove from heat and  set aside to cool. Cover and let steep 24 hours. Strain the oil through a fine mesh strainer into an airtight container. Cover and refrigerate until ready to use.</p>
