?> <p>Of the 50,000 Hawaiian endemic species, only 2,600 remain today. Of these 2,600 remaining endemic species, roughly 30% are endangered according to the U.S. Fish and Wildlife Service (USFWS). In 2003, the USFWS listed 273 endangered native plants. Of the world's endangered plant species, half of them are found in Hawaii according to the U.S. list. </p>
              <p><strong>Hawaiian Trees</strong></p>
              <p>Trees grow in rich diversity in the Islands. Two endemic Hawaiian trees are koa and 'ohi'a. The koa tree (Acacia koa) is the oldest known tree in the Islands. It is one of the tallest trees in Hawaii, reaching heights of up to 70 feet (21 m) and measuring around 10 feet (3 m) in circumference. The tree has a very sturdy wood and has many uses. The Hawaiians used the trunk of the tree for boat- or canoe-building. Because koa is resistant to saltwater, it can also be used to make surfboards. Also known as Hawaiian mahogany, this tree is referred to as the king of the forest. Up until today, koa is considered a superb furniture wood. Hawaii used to be copiously populated with koa trees, but today they are mainly found in reserves. To protect the remaining koa trees in Hawaii, there are some koa plantations that are used for commercial purposes, including the making of souvenirs.</p>
              <p>The 'ohi'a (Metrosideros polymorpha) is the most abundant endemic tree species in Hawaii. This tree species grows in a variety of sizes. The tallest ones can reach heights of up to 100 feet (30 m) and are typically found at the higher and cooler elevations. The main characteristic of the 'ohi'a tree is its orange-red flower (occasionally it can also be yellow or white). According to an ancient Hawaiian legend about the fire goddess Pele, the 'ohi'a flower is sacred. Hawaiians used 'ohi'a wood to build canoes and dishes, such as poi bowls. </p>
