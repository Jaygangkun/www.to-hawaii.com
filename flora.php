              <h2>Flowers and plants in Hawaii</h2>
              <p>Hawaiian flora is beautiful and diverse. A subtropical region, Hawaii has a year-round warm climate and soil heavy with volcanic ash - two elements that play a role in the healthy growing of a wide variety of flowers and plants. There are three categories of Hawaiian flora &ndash; endemic, native and introduced. Endemic refers to those flowers, plants and trees found only in Hawaii. These are the flowering and non-flowering plants and trees that were brought to Hawaii by natural means, such as wind, tides and birds. Native refers to those species occurring in the Hawaiian Islands, but found elsewhere in the world. The third category includes foreign or introduced plant species. These are plants brought to Hawaii by people. </p>
                             <? include ("theme/text.html") ?>
                <p>Long before the human settlement of the Hawaiian island chain, the Islands were devoid of plant or animal life. Over thousands of years seeds, insects and birds made their way to the Hawaiian archipelago. Populating the Islands this way was slow - it is estimated that only one plant every 90,000 years was added to the Hawaiian landscape. </p>
              <p>The early Polynesian voyagers who arrived in Hawaii around AD 500-800 brought plants with them that they needed for food, such as breadfruit, taro, banana, sweet potato and sugarcane. Other plants they brought were needed as building materials, such as the ti plant to make clothing. Later settlers brought mangoes, papayas, pineapples, passion fruit and a variety of vegetables, as well as flowers, including plumeria, orchids, protea, heliconia, ginger, jasmine (pikake) and hibiscus.</p>
              <p>The arrival of early settlers in Hawaii with their plants and animals affected the Hawaiian flora in two ways. On the one hand, it led to a more diverse Hawaiian flora. New plant species were introduced and grew on the Islands. On the other hand, it led to the disappearance of many endemic varieties. Some introduced plant species are fast growing and lead to a crowding out of native and endemic species. These introduced species are also called invasive species. Unable to adapt to the changes in the surroundings, many endemic plants gradually died and disappeared over time. From the onset of human settlement in the Islands, it is estimated that one endemic plant vanished every nine months. </p> <?php
