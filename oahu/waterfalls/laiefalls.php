                <h2 class="small">Laie Falls, Oahu</h2>
                <p>La'ie Falls is located near the town of La'ie on the northeastern shore of Oahu. It is accessible via the 7-mile (11.3 km) long (out and back) La'ie Ridge Trail. The waterfall drops 15 feet (4.5 m) into a pool at its base. Along the way, you can enjoy views of lush tropical foliage, the mountains and ocean.<br />
                  <br />
                  The hike takes about 4 hours, depending on your pace. On the trail to the falls, you will go through pine forests, exposed ridges and bamboo forest-lined cliffs. There are also areas that are quite steep.</p>
                <p>CAUTION: 
                  As with all freshwater streams and waterfalls in Hawaii, Leptospirosis bacteria may be present in the water. Do not drink the water and keep water out of open wounds (including small scratches) and eyes. </p>
