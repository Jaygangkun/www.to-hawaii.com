                <h2 class="small">Manoa Falls, Oahu</h2>
                <p>The Manoa Falls trail (<a href="../../oahu/waterfalls/more_manoafalls_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on the island of Oahu, Hawaii, is one of the most popular hikes in the Honolulu area, mainly because it is easily accessible from Waikiki, either via car or city bus, and it is classified as an easy hike. A short drive out of Waikiki towards Manoa Valley and you can be surrounded by a tropical bamboo and rainforest. Many Oahu tour hiking tour operators take their guests here. </p>
                <? include ("../../theme/text.html") ?>
                <p>Another factor that makes the Manoa Falls trail is a favorite hiking destination for many is that its highlight, the 150-foot (46 m) waterfall awaits after a relatively easy 45-minute walk up the mountain. The waterfall is bigger if it has recently rained. During the hot and dry summer months, it is hardly more than a small dribble. </p>
                
                <p>The water cascades down from the mountain into a small pool. Even though it looks inviting, it is not recommended to swim in it because of the risk of rock slides, which have occurred in this area. Also, it isn't safe to drink the water because of leptospirosis contamination. But it is a nice spot for taking pictures. </p>
                <p>The entire trail is about 1.6 miles (2.6 km) out and back. It can be slippery if it is raining. It rains more often here than in Waikiki and along the coastline. The trail is in a rainforest, so even if it's dry and sunny in Waikiki, it often times is raining here. Check the conditions before you go (a quick glimpse towards the mountains will give you the answer &ndash; if you see dark clouds, it is probably raining in the valley).</p>
                <p>Bring a water bottle and insect repellant with you and wear comfortable hiking shoes. You will have to climb over some rocks along the way. Even though the hike is classified as easy, it may be challenging for people with knee problems or other conditions.</p>
                <p>CAUTION: As with all freshwater streams and waterfalls in Hawaii, Leptospirosis bacteria may be present in the water. Do not drink the water and keep water out of open wounds (including small scratches) and eyes.</p>
                
                <h2 class="big">Manoa Falls Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Just minutes away from Honolulu and Waikiki</li>
                  <li>Easy 1.5-hour hike (but you should be able to climb over some small rocks)</li>
                  <li>Trail has an elevation gain of 600 feet (183 m)</li>
                  <li>Trail can get quite muddy on rainy days</li>
                  <li>Take bus number 5 (Manoa), which leaves from Ala Moana Shopping Center, all the way to the last station and then just walk towards the mountains, you'll then reach the Manoa Falls Trail</li></ul> <div style="clear:both"></div>
