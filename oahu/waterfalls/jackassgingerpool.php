                <h2 class="small">Jackass Ginger Pool, Oahu</h2>
                <p>Jackass Ginger Pool is a small, charming-looking 10-foot (3 m) waterfall in the Honolulu neighborhood of Nu'uanu. It has a swimming hole around eight feet (2.4 m) in depth, so it is also great for jumping into. In fact, it even has a rope tied to a tree above the falls for exactly that purpose. Even Hawaiian royalty used to sit on ti leaves around this pool and swim here.</p>
                 <? include ("../../theme/text.html") ?>
                  <p>The waterfall is accessible via the Judd Memorial Trail. The hike, which travels along the Nu'uanu Stream, is quite short - only about a half mile (800 m) from the trailhead to the falls. The trailhead begins on Nu'uanu Pali Road just south of the Nu'uanu Reservoir. Scenery on this hike includes Cook pines, eucalyptus, swamp mahogany, guava and bamboo. Remember to wear proper footwear, as the trail can be muddy, especially right after rainfall, and keep insect repellent handy as well.</p>
                <p>CAUTION: 
                  As with all freshwater streams and waterfalls in Hawaii, Leptospirosis bacteria may be present in the water. Do not drink the water and keep water out of open wounds (including small scratches) and eyes. <br />
                </p>
                
