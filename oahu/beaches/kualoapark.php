                <h2 class="small">Kualoa Regional Park, Oahu</h2>
                <p>Oahu may be Hawaii's party island, but it also got its share of calm, peaceful beaches. If you're looking to just sit back and relax, a trip to Kualoa Regional Park (<a href="../../oahu/beaches/more_kualoabeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p50.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) may be just the break you need. It is located just off of Kamehameha Highway, but since the park has a large grassy area, one can't hear the noise from the highway at the beach. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>Kualoa means &ldquo;long ancestral background&rdquo; in the Hawaiian language, a fitting tribute to the area's interesting history. In the old Hawaii, chiefs brought their children here to learn learn about their heritage and traditions. The park itself was once a sacred town. Today, the park is  listed on the National Register of Historic Places.</p>
                	                <p>One of Kualoa Park's main attractions is the view to nearby Mokoli'i Island. According to an ancient legend, the island is a fluke from the tail of a large dragon. Hi'iaka, sister of the goddess Pele, slew the creature and threw its tail into the bay. Mokoli'i, which means &ldquo;little lizard,&rdquo; is said to be part of the tail. From the shore, the island looks a bit like a coolie's hat, which has earned it the nickname &ldquo;<a href="../../oahu/attractions/chinamanshat.php">Chinaman's Hat</a>.&rdquo; </p>
                <p>The island (located about 600 yards offshore) is a kayaking destination (others paddle here with a surfboard). Some people even try to swim to the island during low tide because this is when the water is very shallow. However, swimming here is not a good idea because the current can be unpredictable and high tide can come in sooner than you think. People have drowned here in the past while trying to swim back to shore from the island.</p>
                <p>The beach at Kualoa Park is narrow and the nearshore ocean bottom is shallow with some rocky patches. So the swimming isn't that great here. At the south end of the park is a stone barrier, so this is a nice area for a dip. This is also where two ancient Hawaiian fishponds are located - <a href="../../oahu/ancientsites/moliifishpond.php">Moli'i and Apua Fishpond</a>. Kualoa Park is open to campers and has around 30 small campsites. More info about how to get a camping permit is <a href="../../oahu/camping/">here</a>.</p>
                
