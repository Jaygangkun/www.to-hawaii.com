                <h2 class="small">Kailua Beach Park, Oahu</h2>
                <p>If you could squeeze Hawaii into a single mile, you'd probably get something like Kailua Beach Park (<a href="../../oahu/beaches/more_kailuabeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p17.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>). Located on Oahu's windward coast, Kailua gives you the whole package: soft white sand, towering ironwood trees with a few coconut palms inbetween and crystal clear, turquoise waters.Even the locals never get tired of this place - you see them working out here first thing in the morning and barbecuing until sundown.</p>
                <? include ("../../theme/text.html") ?>
                <p>The beach has just the right size surf for an exciting ride, but is calm enough to be suitable for beginners. Kailua is also a world-class kite and windsurfing destination. Kite and windsurfers usually enter the ocean and surf around the center and north end of the Kailua shore. The best area for swimming and for families with children is near the boat ramp and around the area where the Ka'elepulu Stream enters the ocean (near the south end of the beach). Lifeguards are also stationed in this area.</p>
                                
                <p>If you're into kayaking, a popular destination is nearby Popoi'a Island (also known as Flat Island), located about a quarter of a mile (400 m) offshore. The island has a small sandy beach, but is otherwise barren. In the center of the island used to be an ancient Hawaiian fishing shrine, but it was destroyed by a tsunami in 1946. In the Hawaiian language, &quot;popoi'a&quot; means &quot;rotten fish.&quot; The name probably refers to offerings that were left behind at the shrine. The area is also great for snorkeling, especially in the summer. Here you'll find a good population of green sea turtles and other rare creatures. Flat Island is also a seabird sanctuary.</p>
                <p>One thing to watch out for is the occasional appearance of Portuguese man-of-war and box jellyfish. They usually invade the beaches 9-12 days after a full moon, but may appear on other days as well. Their sting can cause a painful rash with even the slightest touch. Watch out for warning signs posted on the beach.</p>
                <p>Kailua Beach has suffered from erosion for many years, but recently the situation has worsened after several days of high surf in mid-January 2013 when even the Kailua boat ramp partially collapsed. 
                  High surf took a chunk out of the shoreline at the south end of the beach, leaving exposed roots of trees. Some of the trees that were close to the shoreline were leaning dangerously or fell down, so that city crews were forced to cut them. <br />
                  <br />
                  Back in March 2012 the city had tried to repair the beach erosion by spreading 1,500 cubic yards of sand from Ka'elepulu Stream along the beach, but that sand is now gone. Here are photos taken in February 2013 (<a href="../../oahu/beaches/more_kailuabeach_erosion_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see  photos</a>) that show the damage to the beach after the week of high surf in January. The section of beach at the south end of Kailua Bay is very narrow  and there are many exposed roots and tree stumps near the water's edge.</p>
                
