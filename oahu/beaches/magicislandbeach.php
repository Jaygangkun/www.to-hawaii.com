                <h2 class="small">Magic Island Beach, Oahu</h2>
                <p>Magic Island Beach (<a href="../../oahu/beaches/p14.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is part of <a href="../beaches/ala-moana-beach-park.php">Ala Moana Beach Park</a>, a popular Honolulu city beach, located between Waikiki and downtown Honolulu. Many local residents and visitors alike come to Ala Moana Beach Park and adjacent Magic Island to swim, bodyboard, surf, picnic or exercise (jogging, walking, etc.). </p>
                	<? include ("../../theme/text.html") ?>		
                <p>Even though it's called Magic Island, this is actually a man-made peninsula. The small curved beach is located at the end of the peninsula. Most people prefer to swim at Ala Moana Beach instead of at Magic Island Beach. The reason is that the ocean bottom at Magic Island Beach is a bit rocky in some areas and the sand is not as smooth. Also, the swimming area here is a lot smaller. </p>
                <p>Magic Island Beach has a rock barrier, so it is protected from incoming waves. However, when the surf is up, the waves can still wash over the rocks, so then it's best stay away from the barrier. Also, it's not a good idea to swim in or through the openings of the rock barrier because the current can be strong in this area. Magic Island Beach is backed by a large grassy area with trees and picnic tables. </p>
                <p>The Magic Island peninsula (made of dredged coral) was built onto the reef off Ala Moana Beach Park in the 1960s (completed in 1964). It was supposed to be the location of a new resort hotel, but the hotel was never built. So the peninsula was instead turned into a public park. The peninsula was later renamed to 'Aina Moana (meaning &ldquo;land from the sea&rdquo;), but people seem to prefer the name Magic Island and still call it that way today.</p>
                
