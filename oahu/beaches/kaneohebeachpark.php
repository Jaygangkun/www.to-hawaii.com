                <h2 class="small">Kane'ohe Bay, Oahu</h2>
                <p>It's no stretch to say that Kane'ohe Bay is the stuff of Hollywood. This so-called &ldquo;living postcard&rdquo; has provided a scenic backdrop for dozens of popular movies and TV series, the most notable being <em>Pearl Harbor, Gilligan&rsquo;s Island </em>and<em> 50 First Dates</em>. Hawaiian chiefs reportedly chose this place to build their sacred fishponds, and its historical vibe can be felt up to this day.</p>
                <? include ("../../theme/text.html") ?>
                <p>Kane'ohe Bay stretches from the Mokapu Peninsula, where the U.S. Marine Corps is based, all the way to Mokoli'i Island, also known as the <a href="../../oahu/attractions/chinamanshat.php">Chinaman's Hat</a>.This vast stretch of water contains five small islands, including Coconut Island (Moku o Lo'e Island), made popular in the TV series <em>Gilligan's Island</em>. Near the middle of the bay is a  sandbar (<a href="../attractions/kaneohesandbar.php">Ahu o Laka</a>), a popular barbecue spot among local residents who have boats.</p>
                <p>The bay is also a breeding ground for hammerhead sharks. Thousands of them are born here every year and spend their first few months in the shallow water. Marine scientists flock to the bay to study these oddly-shaped creatures. Eventually, the hammerhead sharks swim out to the deeper waters where they grow up to 13 feet in length.</p>
                <p>At the north end of Kane'ohe Bay  is <a href="../beaches/kualoapark.php">Kualoa Regional Park</a>, a spacious beach park with nice views of Chinaman's Hat, Apua Fishpond and Kane'ohe Bay.</p>
                
                <h3 class="big">Kane'ohe Bay Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Large bay that stretches from Chinaman's Hat to the Mokapu Peninsula on Oahu's windward (east) coast</li>
                  <li>Bay is 8 miles (12.8 km) long and 2.7 miles (4.3 km) wide</li>
                  <li>Popular attraction in the bay is the Kane'ohe Sandbar (Ahu o Laka)</li>
                  <li>No good swimming beach along the bay's shoreline - bay is mainly visited for the views - great destination for kayaking and boating</li>
                  <li>Five small islands are located in the bay, the two most popular are Chinaman's Hat and Coconut Island</li>
                  <li>Marine scientists do research here (hammerhead shark breeding ground)</li></ul>                <div style="clear:both"></div>
