                <h2 class="small">Ma'ili Beach Park, Oahu</h2>
                <p>This is a long white-sand beach located along Farrington Highway on Oahu's west shore (<a href="../../oahu/beaches/more_mailibeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p47.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>) .The 40-acre park is named after the small town of Ma'ili. Much of the shoreline is lined by rock shelves. In the winter months, the beach gets pounded by high surf and strong currents can be present on any day. There is a spacious park with plenty of room for a picnic. A popular surf site called Green Lanterns is just offshore near the mouth of Ma'ili'ili Stream, and another one called Tumbleland is near the center of the beach. Camping is allowed at Ma'ili Beach Park, but a permit is required. For more info on how to get a permit, see <a href="../../oahu/camping/">here</a>.</p>
            	<? include ("../../theme/text.html") ?>		
                <p>Inland from Ma'ili Beach Park is the Naval Radio Transmitter Facility Lualualei (NRTF Lualualei). The two tallest masts are 1,503 feet (458 m) tall. When they were built in 1972, they were the tallest man-made structure in the Western Hemisphere at the time. The facility is used to transmit high and low frequency radio signals to communicate with Navy vessels including submarines throughout the Pacific. Both the Navy and the Coast Guard use this facility.</p>
                
