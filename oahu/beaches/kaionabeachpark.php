                <h2 class="small">Kaiona Beach Park, Oahu</h2>
                <p>Kaiona Beach Park is a small 4-acre park at the base of the Ko'olau Mountains at the south end of Waimanalo Bay on Oahu's windward (east) coast. It's a great beach for kids and novice swimmers because the water is usually calm. The best area for swimming is the area in front of the seawall, to the right (south) of the grassy area. This is where  an ancient Hawaiian fishpond called <a href="../ancientsites/pahonupond.php">Pahonu Pond</a> is located. During low tide its walls are visible. </p>
                <? include ("../../theme/text.html") ?>
                <p>Pahonu means &ldquo;turtle fence&rdquo; in the Hawaiian language. Legend has it that a former chief of the district liked turtle meat so much that he had the pond built in a way so that it maintained a constant supply of live turtles. The ocean bottom in the fishpond is sandy and is a nice place for swimming. Outside of the pond it is a mixture of rocks and sandy patches.</p>
                <p>On a sunny day, the water at Kaiona has an amazing turquoise color and is so clear that you get the feeling you're swimming in a pool. It's for a reason that this beach is named Kaiona, which means &ldquo;attractive sea&rdquo; in the Hawaiian language. The beach has also been used for many years as a community boat anchorage. In 1999, they community built a boat ramp at the south end of the park for small boats. </p>
                <p>Two popular landmarks on the beach are the Pahonu and the Shriners Club estates. The latter is a private clubhouse that opened in 1931. It is used for events like weddings, birthday parties and other gatherings.</p>
                
