                <h2 class="small">Pua'ena Point Beach Park, Oahu</h2>
                <p>Located on Oahu's North Shore next to <a href="../beaches/haleiwabeachpark.php">Haleiwa Beach Park</a>, Pua'ena Point (<a href="../../oahu/beaches/more_puaenapoint_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p63.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is mainly known among the local surf crowd for being a good surf spot.It can be a good spot for beginners when the waves aren't too high. Surfing lessons take place here. But when the surf is way up, it's for experts only. Most surfers enter the ocean from a small sandy beach before reaching Pua'ena Point. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The beach is a nice place for a picnic (there is some shade), and on the weekends local families come here with their children. The nearshore ocean bottom is a mixture of rocks and small sand patches. If you walk further along the shoreline (about three minutes), you'll reach Pua'ena Point (a rocky outcropping). Around the corner is <a href="../beaches/policebeach.php">Police Beach</a>, where episodes of the popular TV series <em>LOST</em> were filmed. </p>
                <p>The backshore of this entire area is private property. There are many signs that tell people to keep out. According to Hawaii state law all beaches are public property and owned by the state. Private property however can begin right at the natural vegetation line. So if you plan to visit this shoreline, stay on the beach and don't venture inland. The private property is guarded by a security guard and you don't want to be prosecuted for trespassing.</p>
                
