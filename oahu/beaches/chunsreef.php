                <h2 class="small">Chun's Reef, Oahu</h2>
                <p>Chun's Reef is a popular surf site on Oahu's North Shore. There is a long stretch of sandy beach that begins here and ends at <a href="../beaches/kawailoabeach.php">Kawailoa Beach</a> to the east. The beach is located right next to Kamehameha Highway. The surf site is named after John Chun, a former resident in this area, who owned a house in nearby Kawailoa. His children liked to surf, but preferred this surf site over the site in front of their house, called Leftovers. A family friend eventually named the surf site where the Chun kids liked to surf Chun's Reef and since that time (the 1960s) it is known as Chun's.</p>
