                <h2 class="small">Wailupe Beach Park, Oahu</h2>
                <p>Wailupe Beach Park is a small neighborhood park in east Honolulu, located in Aina Haina.The beach is very narrow and the water looks brown and murky. It's not a good beach for swimming because the nearshore ocean bottom is too shallow and there is a coral reef. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The beach park is mainly frequented by fishermen and local residents who come here for a picnic. It is easily accessible since it is located along Kalanianaole Highway. Some surfers also enter the ocean here to get to the surf spots Wailupe, Kim's, Suicides and Bones offshore.</p>
                <p>Even though it's a small park and water activities (except for fishing, surfing and kayaking) are not really possible, restrooms, showers and picnic tables are available.</p>
                <p>The adjacent Wailupe Peninsula is man-made. In the early 20th century, a fishpond was located here. In the 1930s, the company Hawaiian Dredging filled it up with coral and rocks, which came from building the adjacent boat channel. The peninsula is now a residential community.</p>
                
