                <h2 class="small">Sunset Beach Park, Oahu</h2>
                <p>Located on the North Shore of the island, Sunset Beach Park (<a href="../../oahu/beaches/more_sunsetbeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p9.html" onclick="NewWindow(this.href,'name','2550','500','yes');return false">view panorama</a>) is one of Oahu's finest beaches for swimming and snorkeling during the summer when the water is usually calm. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>During the winter the beach is a world-famous surfing spot, home to internationally renowned surfing contests. This is when the waves can reach 30+ feet (9m), which makes Sunset Beach a real surfer's paradise.Sunset Beach is at the east end of this 2-mile long sandy shore. To the west are <a href="../beaches/ehukaibeachpark.php">Ehukai Beach</a> and <a href="../beaches/banzaipipeline.php">Banzai Pipeline</a>, two other popular surfing beaches.</p>
                <p>Sunset Beach is also known for its powerful rip current, called the Sunset Rip, that runs on every big swell. But even when the ocean appears calm, you'll notice the strong alongshore current in the nearshore waters. You enter the ocean in one area, but a few minutes later you find yourself a little bit further down the beach. Guessing from its name, Sunset Beach has nice sunset views, especially in the summer months.</p>
                
                <h3 class="big">Sunset Beach Park Overview</h3>
                <ul type="disc" class="mylist">
                  <li>One of Oahu&#39;s finest beaches for swimming and snorkeling during the summer</li>
                  <li>One of world&#39;s most popular surfing beaches during the winter -several internationally renowned contests take place here (such as the Vans Triple Crown of Surfing)</li>
                  <li>Be aware of strong currents</li>
                  <li>Wonderful sunset views</li>
                  <li>Facilities (showers, restrooms) located across the street from the beach</li></ul>                <div style="clear:both"></div>
