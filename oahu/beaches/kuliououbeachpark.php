              <h2 class="small">Kuliouou Beach Park, Oahu</h2>
              <p>Kuliouou Beach Park (<a href="../../oahu/beaches/p37.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located on Oahu's south shore near Hawaii Kai. This park is mainly visited by local residents who like to picnic here, play basketball, or wade in the shallow nearshore waters. The water is usually a murky brown, so it's not a good beach for swimming. However, there is a large grassy area with a few picnic tables and trees that provide some shade, so it's a nice place for a picnic. To the right you can see Paiko Lagoon, a wildlife sanctuary, and to the left is Koko Head. </p>
