                <h2 class="small">Kahana Bay Beach Park, Oahu</h2>
                <p>Surrounded on three sides by the majestic Ko'olau Mountains, Kahana Bay (<a href="../../oahu/beaches/more_kahanabay_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p49.html" onclick="NewWindow(this.href,'name','2550','670','yes');return false">view panorama</a>) is one of the best-kept secrets of windward Oahu. The crescent shore is set against a beautiful backdrop of ironwood trees and the steep and lush Ko'olau Mountain Range. The sandy beach is a great spot for sunbathing, and the ironwood trees provide plenty of shade for a picnic. On the south end of the bay where Kahana Stream enters the ocean is an ancient Hawaiian fishpond called Huilua.</p>
                <? include ("../../theme/text.html") ?>
                <p>The waters are generally calm, but few people go swimming here because the water is murky from the runoff of Kahana Stream and the nearshore ocean bottom is rocky. Local residents like to camp here during weekends, so this is when the beach can get busy. A camping permit is required. For more info on how to get a permit, see <a href="../../oahu/camping/">here</a>.</p>
                <h3 class="big">Kahana Bay Beach Park Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Located on Oahu's windward coast </li>
