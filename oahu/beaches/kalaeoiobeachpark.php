                <h2 class="small">Kalae'o'io Beach Park, Oahu</h2>
                <p>Kalae'o'io Beach Park (<a href="../../oahu/beaches/more_kalaeoiobeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p66.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located right along Kamehameha Highway on Oahu's windward coast. Even though it is just about one acre in size, it is a nice beach park with a sandy beach and a grassy area with picnic tables and trees. In the southern part of the park is a bridge that leads across a small stream. If you go close enough you can spot small fish in it. The nearshore ocean bottom is a mixture of rocks and sandy patches. A channel cuts through the reef here.</p>
