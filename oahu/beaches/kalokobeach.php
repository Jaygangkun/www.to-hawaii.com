                <h2 class="small">Kaloko Beach, Oahu</h2>
                <p>Kaloko Beach on Oahu's southeastern shore is easy to overlook. Most people visit the neighboring <a href="../beaches/sandy-beach-park.php">Sandy Beach Park</a> and pass by this one. The main reason is because Kaloko Beach isn't a swimming beach. The nearshore ocean bottom is rocky.  Also, there is no shade on the beach, so it's not a good picnicking spot either. One thing to do here is to enjoy the view and take pictures. The <a href="../attractions/kaiwistatescenicshoreline.php">Ka Iwi State Scenic Shoreline</a> state park begins here.</p>
