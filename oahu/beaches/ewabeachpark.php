                <h2 class="small">Ewa Beach Park, Oahu</h2>
                <p>Ewa Beach Park (<a href="../../oahu/beaches/more_ewabeachpark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>) is the main beach in the residential community of the same name &ndash; Ewa Beach. The five-acre park is fronted by a narrow sandy beach. It is a popular swimming and picnicking location among local residents.The beach is also a good spot to gather edible seaweed, called limu in Hawaiian.</p>
                <? include ("../../theme/text.html") ?>
                <p>Ewa means &ldquo;strayed&rdquo; in the Hawaiian language. The name comes from an ancient Hawaiian legend, which tells of two gods, Kane and Kanaloa. As they traveled around Oahu they decided to throw a stone to mark the boundaries of the different land divisions. </p>
                <p>Wherever a stone fell would be the border of two land divisions. They were unable to find the stone that they threw on the island's wide plain west of Pearl Harbor, so they named this area Ewa (&ldquo;strayed&rdquo;) for the stone that strayed.</p>
