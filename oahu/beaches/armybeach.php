                <h2 class="small">Army Beach, Oahu</h2>
                <p>Now you may wonder why a beach in Hawaii  would be called Army Beach (<a href="../../oahu/beaches/p54.html" onclick="NewWindow(this.href,'name','2550','550','yes');return false">view panorama</a>) .  The name doesn't exactly sound Hawaiian. Well, this beach was named for a  military recreation center that was located here from 1970 to 1989. A surf site  offshore bears the same name.</p>
                <? include ("../../theme/text.html") ?>
                <p>Army Beach  is located on Oahu's northwestern shore, across from the entrance to the <a href="../attractions/dillinghamairfield.php">Dillingham Airfield</a>. It is near the end of Farrington    Highway and at the beginning of the <a href="../beaches/kaenapoint.php">Ka'ena Point  State Park</a>. The beach is backed by the Waianae Mountains  and is quite scenic. It is a long, sandy beach with azure blue waters.</p>
                <p>The ocean bottom is mainly rocky with a coral reef, but  there is a small reef-free area that's ideal for a dip into the ocean if you would like to swim. Surfing is also possible here. In the  winter months (November to March), the waves can reach heights of 15+ feet (4.5+ m), so  this is no time for swimming. When the surf is up, strong currents make  entering the ocean too dangerous.</p>
                <h3 class="big">Army Beach Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Located near the end of the road on Oahu's northwestern shore</li>
                  <li>Sandy beach with a small reef-free area (located straight down from the parking lot)</li>
                  <li>Named after a military recreation center</li>
                </ul>
