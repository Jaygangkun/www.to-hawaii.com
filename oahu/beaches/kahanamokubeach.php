              <h2 class="small">Kahanamoku Beach, Waikiki</h2>
              <p>Waikiki Beach is known around the world and since it is 2 miles (3.2 km) long it has several sections. The western end is known as Kahanamoku Beach. It fronts the Hilton Hawaiian Village and the Duke Kahanamoku Lagoon. The beach is named after Duke Paoa Kahanamoku (1890-1968), who is perhaps the most remembered and respected Hawaiian surfer, a champion Olympic swimmer and a master of canoeing. He epitomized Hawaiian athleticism and helped the surf sport reach unbelievable popularity worldwide. That's why he is also known as &quot;The Father of Modern Surfing.&quot;</p>
              <? include ("../../theme/text.html") ?>
              <p>Kahanamoku Beach is the widest stretch of Waikiki Beach. In 2012, the beach was named the #2 beach in America according to the annual list of &quot;Dr. Beach.&quot; Dr. Stephen Leatherman, an internationally known coastal scientist, has selected the annual Top 10 Beaches since 1991. Fifty criteria are used to evaluate beaches, which include water and sand quality as well as safety and environmental management.</p>
              <p>The man-made 5-acre Duke Kahanamoku Lagoon is also a fun place to explore. Here you can swim in calm waters or picnic on the grassy area under one of the palm trees. Located in the center of Kahanamoku Beach is the Hilton Pier from where Atlantis Submarine tours depart.</p>
              
