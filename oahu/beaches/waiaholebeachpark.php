                <h2 class="small">Waiahole Beach Park, Oahu</h2>
                <p>Even though this is a beach park, there are no facilities here. Waiahole Beach Park (<a href="../../oahu/beaches/more_waiaholebeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p48.html" onclick="NewWindow(this.href,'name','2550','610','yes');return false">view panorama</a>) doesn't see many visitors, mainly because the narrow beach has dark sand and the ocean is murky and brown.Swimming isn't possible here because the nearshore ocean bottom is shallow and has a reef. What this beach park does have though is nice views. In the distance you can see <a href="../attractions/chinamanshat.php">Chinaman's Hat Island</a> (also known as Mokoli'i Island), and the lush Ko'olau Mountains provide a scenic backdrop. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The Waikane Pier is also visible from Waiahole Beach. The pier has been featured in numerous Hollywood movies and TV shows, including <em>50 First Dates</em>, <em>Fantasy Island</em> and <em>LOST</em>. However, it is located on private property.</p>
                <p>The beach park has a large grassy area with a few trees that provide shade, but there are no picnic tables. Waiahole Beach is mainly visited by fishermen. The beach park is at the foot of Waiahole Valley, where taro was grown in the past. The historic Waiahole Poi Factory (which closed in 1971) used to be one of the largest poi factories on the island.</p>
                
