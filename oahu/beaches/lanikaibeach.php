                <h2 class="small">Lanikai Beach, Oahu</h2>
                <p>With a name meaning &ldquo;heavenly sea,&rdquo; Lanikai Beach (<a href="../../oahu/beaches/p28.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) has gained international fame as America's number one beach for several years running. The dreamlike shores of Lanikai have been immortalized on many pictures, postcards, wall calendars and travel guides.</p>
                	<? include ("../../theme/text.html") ?>		
                <p>Lanikai Beach is only about half a mile (800 m) long, but it's one of the most scenic stretches of coastline on Oahu.The only way to reach the beach is through small public access paths in this upscale neighborhood of <a href="../cities/lanikai.php">Lanikai</a>. During weekdays and off-season, Lanikai Beach is almost empty, making it the perfect spot if you're looking for some peace and quiet. The beach can get quite crowded during weekends.</p>
                  				
                  <p>There are some beautiful reef patches on Lanikai Beach. These underwater gardens are popular among snorkelers and divers. Kayaking is also a popular watersport here. Some people kayak all the way out to the two <a href="../attractions/mokuluaislands.php">Mokulua Islands</a> (Moku Nui and Moku Iki). This can be quite challenging, especially if you've never done ocean kayaking before. The waves may look small and calm from shore, but by the time you're half way there they can turn out to be much larger. </p>
                <p>Lanikai Beach has no showers, restrooms or other facilities. Plan your trip ahead of time so you can stock up on food and supplies. Also, finding a parking spot can be challenging on weekends and holidays. There is no public parking lot that's designated for the beach. Parking is only possible on the side of the street. </p>
                <p>Between 9-12 days after a full moon (but on other days as well), jellyfish often times get washed ashore. Get out of the water as soon as you spot these translucent creatures, as their sting can leave you off the beach for a good part of the day. Also watch out for <em>limu</em> (stinging seaweed), which can appear after a storm.</p>
                
