                <h2 class="small">Hanauma Bay, Oahu</h2>
                <p>Ever fancied swimming amidst some of the world's rarest marine wildlife? It can't get much better than at Hanauma Bay (<a href="../../oahu/beaches/p27.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>, <a href="../../oahu/beaches/more_hanaumabay_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>), located on Oahu's southeastern shore near Hawaii Kai. Named America's best beach in 2004, Hanauma Bay is a longtime favorite among snorkelers and marine enthusiasts. It is home to over 450 kinds of tropical fish, many of which can only be found in Hawaii. Here you can observe marine life up close, including the humuhumunukunukuapua&lsquo;a, the Hawaiian state fish. Feeding, chasing or touching the fish and turtles is not allowed, but you can swim alongside them and take pictures. You can rent snorkeling gear from the nearby shops, and lifeguards are more than willing to assist beginners.</p>
                <? include ("../../theme/text.html") ?>
                <p>Although often called a beach park, Hanauma Bay is actually a nature preserve established to protect the delicate ecosystems in the bay. Visitors have to watch a short educational video about the bay's history and wildlife before entering the beach. A steep, but paved road leads down to it. If you don't feel like walking or have a physical disability, there's a shuttle bus that takes people to the beach (it costs $1 per person each way).</p>
                <p>If you forgot to bring your own food, there's a concession stand at the entrance of the bay. There's also a gift shop and shower facilities. Parking can be difficult because of the crowds, so try to come early - preferably before 9:30 a.m. There is a parking fee of $1 per vehicle. Non-residents of the state of Hawaii also have to pay an admission fee of $7.50 to enter Hanauma Bay. This fee is waved for Hawaii residents, active military and children under 13. The beach opens at 6 a.m. every day, except Tuesdays when it is closed.</p>
                <p><strong>Special Note:</strong> Hanauma Bay is the top site on Oahu for drownings. Although the bay is protected by a reef and the nearshore waters are usually calm, Hanauma Bay is considered one of the deadliest beaches in Hawaii. According to the State Department of Health, 15 people died here between 2009 and 2014. A total of 47 people have drowned at Oahu's most popular snorkeling spot since 1995. Keep this in mind when planning a trip to Hanauma Bay. Always go snorkeling together with a buddy and stay together at all times. If you are not an experienced open ocean swimmer, stay close to the shore and don't swim out of the channel at Hanauma Bay (the channel that leads into deeper waters behind the reef).</p>
