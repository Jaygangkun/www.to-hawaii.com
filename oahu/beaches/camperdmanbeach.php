                <h2 class="small">Camp Erdman Beach, Oahu</h2>
                <p>Located on Oahu's northwestern shore, Camp Erdman (<a href="../../oahu/beaches/p11.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a long white-sand beach that marks the end of Farrington Highway.Just beyond this beach, the highway turns into a dirt road. The beach is named after Harold R. Erdman (1905-1931). The camp is mainly used as a summer camp for children and year-round for retreats and workshops. </p>
                <? include ("../../theme/text.html") ?>
                <p>In 1926, the YMCA leased these 10 beachfront acres from Walter Dillingham and built this camp on it. Walter Dillingham was the uncle of Harold R. Erdman, who died in a horseback riding accident in 1931. After his death, his mother Marion Dillingham Erdman requested that the camp be named in memory of her son.</p>
