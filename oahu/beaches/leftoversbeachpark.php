                <h2 class="small">Leftovers Beach Park, Oahu</h2>
                <p>Located on Oahu's North Shore, Leftovers Beach Park (<a href="../../oahu/beaches/p4.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>) is one of the island's lesser-known surf sites.The small beach park can easily  be missed as there is only a small pullout parking area along Kamehameha    Highway. From here, a hidden trail leads down to  the small beach, which is framed by rocks. </p>
                <p>In the 1960s when big wave surfing first became popular,  surfers considered this beach a second choice, or "leftover" surf site, paling  in comparison to its neighboring beaches of Hale'iwa and Waimea   Bay. Coincidentally, this beach  also has a "left" surf break, meaning a wave that surfers ride to their left.</p>
