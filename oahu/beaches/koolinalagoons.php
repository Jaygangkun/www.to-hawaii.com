                <h2 class="small">Ko Olina Lagoons, Oahu</h2>
                <p>Four sandy beaches make up the picturesque Ko Olina Lagoons (<a href="../../oahu/beaches/more_koolinalagoons_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>), one of the few man-made water bodies in the Hawaiian Islands, located on Oahu's southwestern shore. From the manicured lawns to the fancy nearby resorts, the modern vibe can be felt the minute you arrive.</p>
                <? include ("../../theme/text.html") ?>
                <p>The four lagoons &ndash; Hanu, Naia, Kolola and Ulua &ndash; are shielded from high surf by large rock barriers. This makes the water safe to swim in year-round, although some find it too calm for most water sports, such as surfing or bodyboarding. A paved walkway connects the four lagoons, so the entire area is also a nice place for a shoreline stroll.</p>
                <p>One area to stay away from are the lagoons' rock barriers. These rocks can be dangerous to climb because they are slippery. Also, the currents can be strong between the barriers, and you could be dragged out into the open ocean if you swim in this area, especially when the surf is up. So make sure to stay inside of the lagoons.</p>
                
