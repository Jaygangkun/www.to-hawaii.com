                <h2 class="small">Shark's Cove, Oahu</h2>
                <p>Don't worry, you will most likely not run into any sharks when swimming at Shark's Cove (<a href="../../oahu/beaches/p57.html" onclick="NewWindow(this.href,'name','2550','750','yes');return false">view panorama</a>). This cove got its name from a popular story that says that the outline of a reef outside the cove looks like a shark when seen from above. </p>
                  	<? include ("../../theme/text.html") ?>		
                  <p>Shark's Cove is a lava-rock beach on the North Shore of Oahu and is part of <a href="../beaches/pupukeabeachpark.php">Pupukea Beach Park</a>. It is unique not only because of its spectacular underwater rock formations, but also because of its diverse marine life. Tropical colorful fish and sea turtles are the cove's constant inhabitants. Harmless white-tipped reef sharks may also be spotted just outside the cove.</p>
                <p>Because of its amazing underwater world, Shark's Cove is a favorite spot among snorkelers and scuba divers. The lava has formed underwater caves and tunnels about 15 to 45 feet (4.5 to 13.7 m) below the surface. These caves are a real thrill for experienced scuba divers. </p>
                <p>No lifeguards are stationed here. If you want to go swimming at Shark's Cove, it is a good idea to wear reef shoes because of the sharp coral reef that is everywhere in the cove. The best time to explore the cove is in the summer months when the ocean is usually calm. During the winter, getting into the water at Shark's Cove is not recommended because this is when the surf is up on Oahu's North Shore. </p>
                <p>Shark's Cove is part of an 80-acre Marine Life Conservation District. People need to respect the cove's marine inhabitants and keep the area clean.</p>
                
