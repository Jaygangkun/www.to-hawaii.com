                <h2 class="small">Ka'alawai Beach, Oahu</h2>
                <p>Ka'alawai Beach is located at the foothills of Diamond Head on Oahu's south shore.The beach fronts the upscale neighborhood of Ka'alawai. It is a narrow, white-sand beach with a shallow reef offshore, which makes for poor swimming conditions. There are only a few scattered pockets of sand on the nearshore ocean bottom. Freshwater bubbles up between the rocks of the reef. That's probably why the beach is called &ldquo;Ka'alawai,&rdquo; which means &ldquo;the watery rock&rdquo; in the Hawaiian language. The beach is mainly used by surfers, who paddle out to the surf spot called Brown's, which is located just behind the reef.</p>
                <? include ("../../theme/text.html") ?>
                <p>A famous museum lies at the eastern end of the beach &ndash; Shangri La (also known as Doris Duke Foundation). This mansion is the former home of Doris Duke, who was the daughter and only child of James Duke, the founder of the American Tobacco Company. After her father's death, she became one of the wealthiest women in the world. The mansion is now an art museum, featuring Islamic art. Another landmark at the eastern end of Ka'alawai Beach is Black Point, a lava rock peninsula. </p>
                
