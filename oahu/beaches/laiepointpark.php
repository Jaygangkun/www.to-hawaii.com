                <h2 class="small">La'ie Point State Wayside Park, Oahu</h2>
                <p>La'ie Point State Wayside Park (<a href="../../oahu/beaches/more_laiepoint_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p62.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) lies on a peninsula in the town <a href="../cities/laie.php">La'ie</a> on Oahu's northeastern shore. This place is steeped in Hawaiian legend, one of which according to William Rice in his <em>Hawaiian Legends</em> goes like this: </p>
                	<? include ("../../theme/text.html") ?>		
                <p>&quot;Laniloa is the name given to a point of land which extends into the ocean from La'ie. In ancient times this point was mo&lsquo;o, standing upright, ready to kill the passerby. After Kana and his brother had rescued their mother from Molokai and had taken her back to Hawaii, Kana set out on a journey around the islands to kill all the mo&lsquo;o. In due time he reached La'ie, where the mo&lsquo;o was killing many people. Kana had no difficulty in destroying this monster. Taking its head, he cut it into five pieces and threw them into the sea, where they can be seen today as the five small islands lying off Malaekahana.&quot;</p>
                <p>This legend of the Laniloa mo&lsquo;o can be read on a granite plaque attached to a boulder in the park. The five small islands along this shoreline are called Kihewamoku, Moku'auia, Pulemoku, Kukuiho'olua and Mokualai. Tour buses with visitors often times stop here to enjoy the scenic view. The peninsula is also frequented by fishermen, who primarily fish for ulua (trevally).</p>
                
