                <h2 class="small">Makaleha Beach Park, Oahu</h2>
                <p>Makaleha Beach Park (<a href="../../oahu/beaches/p10.html" onclick="NewWindow(this.href,'name','2550','560','yes');return false">view panorama</a>) is one of the lesser-known beaches on Oahu's North Shore, located about 13 miles (21 km) west from popular Sunset Beach and Waimea Bay.It is beautiful with a long and curved sandy beach, azure blue waters and trees that provide ample shade. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The beach park is easy to miss from Farrington Highway, so many people drive right by it. It is accessible via a narrow beach access trail (look for a blue sign that says &ldquo;public right-of-way to beach&quot; which is hidden behind bushes and sometimes parked cars), located right across from the driveway to the Dillingham Lodge. There is no designated beach parking lot, only roadside parking. </p>
                <p>After a 5-minute walk on the beach access trail you'll reach Makaleha Beach. It is backed by fenced-off private property where horses are being kept. The nearshore waters right where the beach access trail ends are rocky. But if you walk a little bit to the west (left), there is a sandy channel in the reef.</p>
                <p>When the ocean is calm, the snorkeling can be good here. In the winter months (November to March), Oahu's North Shore often gets pounded by high surf. There are no lifeguards stationed here. The beach park also has no facilities.</p>
                
