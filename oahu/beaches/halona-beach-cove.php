                <h2 class="small">Halona Beach Cove, Oahu</h2>
                <p>Halona Beach Cove (<a href="../../oahu/beaches/more_halonabeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p38.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a small pocket of sand located on the southeastern shore of Oahu.It was made popular in the movie <em>From here to Eternity</em>, filmed here in 1953, where it was a love scene of a couple (Burt Lancester and Deborah Kerr). Because of the movie, the beach was nicknamed <em><span itemprop="alternateName">Eternity Beach</span></em>. </p>
                <? include ("../../theme/text.html") ?>
                <p>It is located  on the right (west) side of the Halona Blowhole overlook. You have to hike down the rocks from there to get to it. Be careful and always wear shoes because the rocks are slippery, steep and sharp. </p>
                <p>The best time to swim in this small cove is when the water is calm. However, don't swim too far out because strong, dangerous currents are often times present and the water is very turbulent further out. Since there are no lifeguards stationed here, it is not recommended to get into the water if the surf is up. Also, stay away from the rocky areas. </p>
                <p>The rocky point to the west of the beach cove is called Bamboo Ridge. It has been a popular fishing spot since the 1920s. The location was named after the fishermen's bamboo fishing poles that could be seen along the edge of the cliff in the past.</p>
                <p>There are no facilities at Halona Beach Cove. Free parking is available at the Halona Blowhole lookout point. </p>
