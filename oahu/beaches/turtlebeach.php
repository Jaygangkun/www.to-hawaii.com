                <h2 class="small">Turtle Beach (Papaoneone Beach), Oahu</h2>
                <p>The Hawaiian name of this beach is Papaoneone Beach, but it is more commonly known as Turtle Beach (<a href="../../oahu/beaches/more_turtlebeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>), named after the green sea turtles who like to feed on the seaweed that grows on the reef here. They can sometimes be seen in the early morning hours just offshore. </p>
                <? include ("../../theme/text.html") ?>
                <p>Turtle Beach is a hidden gem on Oahu's west shore. It is not among the most popular beaches on this side of the island, but that doesn't mean that it's a bad beach. On the contrary, the almost white sand contrasts amazingly with the azure blue water. The ocean looks especially turquoise here for some reason, more than on many other Oahu beaches. On the south end of the beach is <a href="../ancientsites/maunalahilahi.php">Mauna Lahilahi</a>, a small mountain which provides a scenic backdrop. Two condominiums are also located on this side of the beach. </p>
                <p>Turtle Beach can easily be missed when driving along Oahu's west shore because it is not visible from the road. There is a small dirt parking area along the road and three narrow public access walkways lead to the ocean. The only thing Turtle Beach lacks is shade. So if you plan to come here, bring a beach umbrella.</p>
                <p>In the winter months, the surf can get very high here and this is when swimming is too dangerous. Even in the summer months there can be a strong shorebreak and currents on this beach. A sea cave, called Ulua cave, is located offshore and is a popular scuba diving location in the summer months, with a depth of around 80 feet (24 m).</p>
                <h3 class="big"><span class="small">Turtle Beach</span> Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Wide pocket of sand</li>
                  <li>Mauna Lahilahi provides a scenic backdrop on south end of the beach</li>
                  <li>Snorkeling can be good when the ocean is calm (usually in the summer months)</li>
                  <li>High surf and strong currents in the winter (November to February)</li>
                  <li>Steep shorebreak and strong undertow in nearshore waters (can occur year-round)</li>
                  <li>A few beach homes and two condominiums located along the beach</li>
                  <li>This beach is not to be confused with another beach of the same name, which is located on Oahu's North Shore (local residents call that beach Turtle Beach as well, but its Hawaiian name is <a href="../beaches/laniakeabeach.php">Laniakea Beach</a>)</li>
                </ul>
