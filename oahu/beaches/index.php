                <p><a href="../../map-oahu-beaches.html" target="_blank"><img src="../../theme/images/oahubeaches.jpg" alt="Oahu Beach Map" align="right" border="0" style="margin-left:10px;" /></a>The island of Oahu has a coastline of about 112 miles (180 km). Oahu beaches are diverse and there is something for everyone &ndash; there are many great beaches for swimming, snorkeling, surfing, scuba diving, windsurfing, kayaking and relaxing. Most beaches on Oahu are sandy, and some have an offshore coral reef. A few beaches have little or no sand but are nevertheless popular, such as Shark's Cove, an excellent snorkeling and diving spot in the summer months. Beach sand in Hawaii comes in all kinds of shapes and colors. Read more about Hawaiian <a href="../../hawaiibeachsand.php">beach sand composition</a>.</p>
                <p>Oahu is home to popular Waikiki Beach, which is busy year-round. Oahu North Shore beaches such as Waimea Bay, Sunset Beach and Banzai Pipeline are some of the most popular surfing beaches in the world, where waves can get as high as 40 feet (12 m) in the winter months. And Hanauma Bay on the southeastern shore is one of the best snorkeling beaches on the island.</p>
