                <h2 class="small">Diamond Head Beach Park, Oahu</h2>
                <p>Diamond Head Beach Park (<a href="../../oahu/beaches/p31.html" onclick="NewWindow(this.href,'name','2550','620','yes');return false">view panorama</a>) is located on the foothills of <a href="../../oahu/attractions/diamondhead.php">Diamond Head</a>, an extinct volcanic crater  at the eastern end of <a href="../../oahu/cities/waikiki.php">Waikiki</a>.  The park includes two acres of sea cliffs on the south slope of the crater between Beach Road and the Diamond Head Lighthouse. </p>
                <? include ("../../theme/text.html") ?>
                <p>The beach here is narrow and fronted by a rocky shelf. The swimming conditions are poor because of the coral reef, but surfers find an uncrowded paradise here compared to <a href="../beaches/waikiki-beach.php">Waikiki Beach</a>. Snorkeling is also good if the waves aren't too high. Diamond Head Beach is also a popular tidepool excursion site.</p>
