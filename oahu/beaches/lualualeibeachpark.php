                <h2 class="small">Lualualei Beach Park, Oahu</h2>
                <p>This is a narrow 18-acre park located along Farrington Highway in Waianae. The beach is sandy, but there is a rocky limestone shelf along the shoreline, which makes entering the ocean difficult. For this reason the beach is empty most of the time as most people prefer swimming at neighboring <a href="../beaches/pokaibeachpark.php">Pokai Bay</a> or at other beaches in this area. The beach park is mainly used by local fishermen. A surf site called Sewers is located offshore. It is named after the Waianae sewage treatment plant, which is located across from the beach park.</p>
