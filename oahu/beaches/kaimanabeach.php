                <h2 class="small">Kaimana Beach, Oahu</h2>
                <p>If you're staying in Waikiki, Kaimana Beach (<a href="../../oahu/beaches/p25.html" onclick="NewWindow(this.href,'name','2550','740','yes');return false">view panorama</a>), named after the nearby hotel, is just a stroll away and a nice beach to go to. Walk toward Diamond Head, along <a href="../../oahu/attractions/kapiolanipark.php">Kapiolani Park</a> on your left, and you'll get to Kaimana Beach. It is located between the <a href="../../oahu/attractions/warmemorial.php">War Memorial Natatorium</a> and the New Otani Kaimana Beach Hotel. </p>
                <? include ("../../theme/text.html") ?>
                <p>Kaimana Beach is relatively wide, unlike Waikiki Beach, which is narrow in many areas. It is a nice family beach and many locals like to go here because it is protected by a wide reef and because the nearshore ocean bottom is shallow and sandy. The beach is also an access point for surfers, who paddle out to surf at the popular surf spot Old Man's. </p>
                <p>Kaimana Beach is also the place where an important historic event took place on December 28, 1902. The steamship <em>Silvertown</em>, which came from San Francisco and had laid a submarine cable from California to Hawaii, anchored here after a 12-day crossing of the ocean. The end of the cable was brought ashore through the Kapua Channel at Kaimana Beach, in a canoe steered by David Pi'ikoi Kahanamoku, an uncle of <a href="../attractions/dukestatue.php">Duke Kahanamoku</a> (who is perhaps the most remembered and respected Hawaiian surfer). </p>
                <p>This cable was the first telegraphic link between Hawaii and the U.S. mainland. The  first message was sent to President Theodore Roosevelt in Washington D.C. on January 1, 1903. Up until today, remnants of the cable, which was abandoned in 1951 for newer communication systems, still lie on the ocean floor in the channel at Kaimana Beach.</p>
                <p>If you're swimming in this channel, be aware that the current can be quite strong, especially after a high tide when the water is flowing out. Ask the lifeguard on the beach if you're not sure about the tides and wear fins if you plan to swim further out.</p>
                
