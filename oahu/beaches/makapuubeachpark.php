                <h2 class="small">Makapu'u Beach Park, Oahu</h2>
                <p>Backed by sea cliffs, Makapu'u Beach Park (<a href="../../oahu/beaches/p41.html" onclick="NewWindow(this.href,'name','2550','560','yes');return false">view panorama</a>) is popular among bodyboarders and bodysurfers. Rip currents and a powerful shorebreak make for a very rough ride, perfect for adventure seekers. During the summer months the ocean is usually calmer than in the winter months (November to February), but even in the summer there are waves here almost every day. In other words, Makapu'u is not suitable for a leisurely swim. The width of the beach varies throughout the year. In the winter, high surf erodes the beach and exposes large boulders in the shorebreak.</p>
                	<? include ("../../theme/text.html") ?>		
                <p>Just around the corner from Makapu'u Beach on the sea cliff is Makapu'u Lighthouse. A paved trail leads to it that can be accessed from a parking lot along Kalanianaole Highway. This area is known as the <a href="../attractions/kaiwistatescenicshoreline.php">Ka Iwi State Scenic Shoreline</a>. Also located just south of Makapu'u Beach is the <a href="../attractions/makapuulookout.php">Makapu'u Lookout</a> (also has a parking lot). From up here you can enjoy a nice view of Makapu'u Beach and two islands - <a href="../attractions/rabbitisland.php">Rabbit Island</a> (the larger one, also known as Manana Island) and Black Rock (the smaller one, also known as Kaohikaipu Island).</p>
                
