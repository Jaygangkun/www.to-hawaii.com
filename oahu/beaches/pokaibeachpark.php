              <h2 class="small">Poka'i Bay Beach Park, Oahu</h2>
              <p>Because of its breakwater offshore that blocks high surf, Poka'i Bay (<a href="../../oahu/beaches/more_pokaibay_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p45.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is the most protected beach on the leeward (west) coast of Oahu. It is calm year-round, while other beaches on the leeward coast get strong surf in the winter months. So this is a good place to take the kids or to swim if you're a novice swimmer. The calmest area is at the southern end of the bay, next to the breakwater. There's a wide reef in the middle of the bay and the waves that break on it are usually small, which makes it a good spot for beginning surfers. </p>				
              <p>Poka'i Bay lies between the small boat harbor in Wai'anae and Kane'ilio Point, a peninsula which is home to the <a href="../ancientsites/kuilioloaheiau.php">Ku'ilioloa Heiau</a>, an ancient Hawaiian temple site.The peninsula's shoreline has a few memorials for people whose ashes have been scattered offshore. It is also a popular fishing location. In the center of the bay is the Wai'anae Army Recreation Center, which was acquired by the federal government in 1918. </p>
              <p>The bay is named after the Hawaiian chief Pokai, who is said to have brought the first coconut palm tree to Hawaii. &ldquo;Ka Uluniu o Pokai&rdquo; is the famous coconut grove that once lined the backshore. There is a sign with a plaque at the beginning of the peninsula that tells of the history of this area. It says:</p>
              <p><em>Malaea</em></p>
              <p><em>Pokai Bay was originally named Malaea, meaning &quot;calm&quot; or &quot;serene.&quot; Malaea shelters the sandy beach of Nene'u and the site of the temple called Ku'ilioloa Heiau at Kane'ilio Point. Dedicated to a dog kupua (spirit), rites performed at the heiau protected those traveling by land and sea. At the north end of the bay is Kaupuni Stream, the spawning ground of anae (mullet) which gave Wai'anae (mullet waters) its name. The village Poka'i, famous for its coconut grove, once lay near the stream.</em></p>
