                <h2 class="small">Hau'ula Beach Park, Oahu</h2>
                <p>Hau'ula Beach Park on Oahu's windward coast is a popular camping and picnicking site among local residents. There is only a very narrow beach, and because of a shallow nearshore reef, the swimming conditions are poor. However, snorkeling is possible when the ocean is calm. Also, a beginners' surf spot is located offshore at the northern end of the park. </p>
                <p>Hau'ula, meaning "red hau" in the Hawaiian language, was named for the hau flowers, the native Hibiscus tiliaceus, which bloom in the summer months.</p>
