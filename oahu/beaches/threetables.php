                <h2 class="small">Three Tables Beach, Oahu</h2>
                <p>Three Tables Beach (<a href="../../oahu/beaches/more_threetablesbeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p59.html" onclick="NewWindow(this.href,'name','2550','740','yes');return false">view panorama</a>) got its name from the three sections of flat reef that appear on the ocean's surface during low tide. These formations make an interesting backdrop for one of the most attractive beaches on Oahu's North Shore. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The snorkeling and diving conditions are good if the ocean is calm, usually between May and September. The water is only about 15 feet (4.5 m) deep and you'll be able to see all kinds of colorful fish and underwater lava tubes, ledges and arches. Sometimes you can also see sea turtles here. </p>
                <p>In the winter, the waves can reach heights of over 20 feet (6 m) in this area. This makes for a very rough and dangerous ride and is only something for experienced surfers. The surf can be unpredictable during this season, so never turn your back to the ocean when you're in the water.</p>
                
