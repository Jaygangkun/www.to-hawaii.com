              <h2 class="small">Mokule'ia Beach Park, Oahu</h2>
              <p>Mokule'ia Beach Park (<a href="../../oahu/beaches/p56.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>, <a href="../../oahu/beaches/more_mokuleiabeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on Oahu's North Shore has a long sandy beach and a shallow reef that attracts swimmers, surfers, kite- and windsurfers year-round.This is  the only beach park with facilities along this shore, between Waialua and <a href="../beaches/kaenapoint.php">Ka'ena Point</a>.</p>
              	<? include ("../../theme/text.html") ?>		
              <p>From the beach one has access to several popular surf spots, including Park Rights and Day Star, a little further to the west. When the winds are blowing from the south (Kona winds), it means good conditions for kite- and windsurfers. And when the water is calm and the surf low, the shallow reef provides good conditions for snorkeling and spearfishing. </p>
              <p>Watch out for strong swells that are common in the winter months. Then it is best to stay out of the water because this beach has dangerous rip currents that can sweep you out to sea.</p>
              
              <h3 class="big">Mokule'ia Beach Park Overview</h3>
              <ul type="disc" class="mylist">
                <li>Has a long sandy beach and a shallow reef that provides good conditions for several ocean activities, including  surfing, fishing, windsurfing and snorkeling (all are seasonal)</li>
                <li>Usually calm waters in the summer months and strong swells in the winter</li>
                <li>38.5 acre beach park with many picnic tables and a large parking area</li>
                <li>Camp Mokule'ia located here (rent a cabin or bring your own tent)</li>
                <li>In the Hawaiian language, <em>mokule'ia</em> means &quot;district of abundance&quot;</li></ul>                <div style="clear:both"></div>
