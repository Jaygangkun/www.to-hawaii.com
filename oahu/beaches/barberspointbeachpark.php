                <h2 class="small">Barber's Point Beach Park (Kalaeloa Beach), Oahu</h2>
                <p>Barber's Point Beach Park is most known for its lighthouse. The small sandy beach is mainly used by fishermen and picnickers because the swimming conditions are poor.There is a low limestone shelf along most of the shoreline which makes entering the ocean difficult. This, as well as strong currents, make this a no-swimming beach. The beach park is located on Oahu's leeward coast (at the southwestern tip) near Campbell Industrial Park. It is not the most scenic area on the island. </p>
                <? include ("../../theme/text.html") ?>
                <p>Barber's Point is named after Captain Henry Barber. In 1796, Barber arrived on Oahu in the <em>Arthur</em>. After loading supplies in Honolulu, he wanted to sail to Kauai and then on to China. But he ran aground at Oahu's southwestern tip, known as Kalaeloa, which means &ldquo;the long point&rdquo; in the Hawaiian language. After this incident, this area became known as Barber's Point.</p>
