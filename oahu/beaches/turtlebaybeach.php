                <h2 class="small">Turtle Bay, Oahu</h2>
                <p>Turtle Bay (<a href="../../oahu/beaches/p19.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) marks the entrance to the legendary North Shore, one of the most scenic stretches of Oahu's coastline. The bay is named after the large number of green sea turtles that inhabit the area and who used to lay their eggs in the sand on the beach years ago (however, they don't do this anymore). </p>
                <? include ("../../theme/text.html") ?>
                <p>Most people come to Turtle Bay for surfing, diving and fishing. There is a reef in the nearshore waters, and beyond this reef is where the surfing area begins. This is where you get some serious waves, especially in the winter months (November to February). The current can be very strong at times, so make sure to check weather conditions before venturing beyond the reef. No lifeguards are stationed here. </p>
                <p>The beach itself is sandy, but the swimming condition are poor at Turtle Bay because the nearshore ocean bottom is rocky and there is a large limestone shelf that spans almost the entire length of the bay. At the west end of the bay is a small island called Papa'amoi. </p>
                <p>The upscale Turtle Bay Resort (<a href="../../oahu/beaches/more_turtlebayresort_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p68.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located next to this bay. Most resort guests prefer to swim at neighboring <a href="../beaches/bayviewbeach.php">Kuilima Cove</a> because it offers easier access to the water (it has a sandy patch in the nearshore waters) and the beach is more protected from high surf due to an offshore reef. So overall, the beach at Turtley Bay is only sparsely visited. It is perfect if you a looking for a relaxing spot for a picnic. Ironwood trees line the backshore and provide plenty of shade.</p>
                <p>Fronting the Stables (where guided horseback riding tours are offered), there is a small Hawaiian stone altar (<a href="../beaches/more_hawaiian_stone_altar_2.html" onclick="NewWindow(this.href,'name','650','650','yes');return false">view photos</a>). It is marked by a plaque that says: &quot;This Hawaiian altar is an 'ahupua'a demarcation between the 'ahupua'a land division boundaries of Opana-Kawela to the west and Hanaka'oe to the east. 'Ahupua'a - literally the altar ('ahu) of the pig (pua'a), it is the name for both a land division and the stone altar that serves as a marker. The 'ahupua'a system of land management was a cornerstone of traditional Hawaiian life and helped Native Hawaiians to develop one of the most sustainable methods of land use in the world. Extending from the forested mountain tops mauka (inland) or the wao akua (region of the gods), through the kula (open plains used for farming) and extending out into makai (ocean), each 'ahupua'a contained everything its inhabitants needed to sustain life.&quot;</p>
