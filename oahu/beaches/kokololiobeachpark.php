                <h2 class="small">Kokololio Beach Park, Oahu</h2>
                <p>Kokololio, meaning &ldquo;gusty&rdquo; in Hawaiian, is the name of a valley and a stream, located north of the town Hau'ula on Oahu's northeastern shore. The beach park (<a href="../../oahu/beaches/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) lies at the foot of the valley and next to the stream. Kokololio Beach is a nice stretch of sand and during the summer months, swimming is possible here. During weekends, especially in the summer months, local residents like to camp at Kokololio.The beach park is a county camp site, but permits are required. More info about how to get a permit is <a href="../../oahu/camping/">here</a>.</p>
                <p>In the winter time, it's a nice spot to take some great pictures of the pounding shorebreak or to go surfing or bodyboarding. Offshore is a popular surf spot named CYO's, which stands for Catholic Youth Organization. The CYO used the house that stood once there (from the Cooke family) as a retreat before it was bought by the City and taken down to develop the park in 1991. </p>
                				
                <p>The limestone sea cliff that reaches into the ocean on the northern end of the beach is called Pali Kiloi'a, meaning &ldquo;fish-spotter cliff&rdquo; in the Hawaiian language. The ancient Hawaiians used this cliff to locate fish in the bay. From up here they gave directions to net fishermen in their canoes. The area is still a popular site for fishermen today, who fish for o'io, moi, papio and ulua (giant trevally).</p>
                <p><strong>Editor's note:</strong> There are a few homes at the north end of the beach. When we walked along the beach there, suddenly two large dogs came running towards us from one of the beach houses. Their owner wasn't in sight. The dogs were very territorial and only left us alone after we yelled at them. We felt threatened and left immediately. If you're visiting Kokololio Beach, you may want to avoid the beach area near the homes because the home owners don't seem to attend their dogs and there is no fence, so the dogs have access to the public beach.</p>
                
