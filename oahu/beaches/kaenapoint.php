                <p>Ka'ena Point has a rocky shore and is backed by sand dunes. The best thing to do here is fishing and hiking. Stay out of the water because dangerous rip currents and very large waves are often times present. There are also a lot of rocks in the water. So swimming, snorkeling or other water activities are not recommended and there are no lifeguards.</p>
                <p>Ka'ena Point State Park includes the nearby <a href="../beaches/makuabeach.php">Makua Beach</a> and <a href="../beaches/yokohama-bay.php">Yokohama Bay</a> (also known as Keawa'ula Beach). These are the only sand beaches where swimming is possible when the ocean is calm. If you plan to hike to Ka'ena Point from the leeward (west) coast, you can drive all the way to Yokohama Beach, up to where the road ends, park there and walk north. </p>
                <p>There is a narrow trail that will lead you to Ka'ena Point. Take a water bottle with you because the area is often times hot and dry (there is no shade). It will take you about 1 to 1.5 hours one-way. Keep in mind that the albatrosses there are protected by law, so don't try to go close to them or to find their nest.</p>
                <p>On the way to Ka'ena Point (on the trail that leads there from Yokohama Beach), you may come across some dumped or abandoned junk cars that have been thrown over the cliffs or parked on the side of the trail. Sometimes just the motor parts or tires are left &ndash; a not so pretty sight in paradise and very unfortunate that this happens.</p>
                <p>Alternatively, you may also hike to Ka'ena Point from the trail that begins at the end of Farrington Highway on the north shore, just past <a href="../attractions/dillinghamairfield.php">Dillingham Airfield</a> and <a href="../beaches/camperdmanbeach.php">Camp Erdman Beach</a>.</p>
