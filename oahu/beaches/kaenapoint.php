                <h2 class="small">Ka'ena Point State Park, Oahu</h2>
                <p>Ka'ena, which means &quot;red hot&quot; or &quot;glowing&quot; in the Hawaiian language, is the name of the northwestern tip of the island of Oahu. Ka'ena Point State Park  (<a href="../../oahu/beaches/p6.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is so remote that no road leads here. Access is only possible by hiking here, either from the road that ends north of Makaha on the west shore, or from the road that ends behind the Dillingham Airfield on the north shore.</p>
                <p>The 853-acre (3.45 sq. km) park stretches for 9 miles (14.5 km) around the northwestern tip and is home to numerous seabirds, including albatrosses and rare native plants. The whole area is steeped in legend, such as stories of Pohaku o Kauai and Ka Leina a ka Uhane.</p>
                <p>Pohaku o Kauai, meaning &quot;rock of Kauai,&quot; is a rock in the ocean just off Ka'ena Point that was named after the demigod Maui tried to join Oahu and the neighbor island of Kauai. He took his magic hook &quot;mana i ka lani,&quot; meaning &quot;divine power of heaven,&quot; and threw it across the Ka'ie'ie Waho Channel to snag Kauai. After tugging on the tagline he could only loosen a large boulder. The rock fell into the ocean at Ka'ena Point where it remains up until today as Pohaku o Kauai.</p>
                <p>According to Hawaiian folklore, Ka'ena Point was also known as the place where souls depart from the earth, as a &quot;jumping off&quot; place, a &quot;leina a ka uhane.&quot; After a person died, the soul would find its way to a leina a ka uhane, such as the one at Ka'ena Point. The exact point was called &quot;ka leina a ka uhane,&quot; meaning &quot;the leap of the soul.&quot; It is also known as White Rock because of a large limestone formation on the north side of the point.</p>
