                <h2 class="small">Waikiki Beach, Oahu</h2>
                <p>Waikiki Beach (<a href="../../oahu/beaches/p23.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>, <a href="../../oahu/beaches/more_waikikibeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on the island of Oahu is Hawaii's most popular and visited beach. It is located on the south shore of the island next to <a href="../../oahu/attractions/diamondhead.php">Diamond Head</a>. The beach is about 2 miles (3.2 km) long and across from it on the other side of the street are many hotels, restaurants, gift shops and beach bars.</p>
                <? include ("../../theme/text.html") ?>
                <p>At the east end of the beach, on  the foothills of Diamond Head, is a large and beautiful oceanfront park called <a href="../attractions/kapiolanipark.php">Kapiolani Park</a>. It is a great place for a picnic, ball games or running. At the west end of Waikiki Beach is the Duke Kahanamoku Lagoon (also called Hilton Lagoon), which is a nice place for a relaxing swim.</p>
                <p>If you like watersports you will be able to rent all kinds of equipment on the beach, such as bodyboards, surfboards and snorkeling gear. Some of the most popular things to do at Waikiki Beach are swimming, surfing, bodyboarding, snorkeling and kayaking.</p>
                <p>Waikiki Beach is almost always crowded, and during the peak tourist seasons it can almost be difficult to find a spot where to put your towel. In the evening hours, there is music entertainment at the numerous beach bars and restaurants. </p>
                <p>Since Waikiki Beach is 2 miles long, it has several sections that have their own name. Located at the west end of Waikiki is <a href="../beaches/kahanamokubeach.php">Kahanamoku Beach</a>, which is the widest stretch of Waikiki Beach. The area near the Kapahulu Groin (also known as The Wall) is called <a href="../beaches/kuhiobeach.php">Kuhio Beach</a>. This area has a breakwater, which creates a shallow and calm swimming lagoon that's ideal for families with children. To the east of Kuhio Beach is <a href="../beaches/queensbeach.php">Queen's Surf Beach</a>, a popular place among bodyboarders. Further east is <a href="../beaches/kaimanabeach.php">Kaimana Beach</a>, a nice swimming beach because of its sandy nearshore ocean bottom.</p>
