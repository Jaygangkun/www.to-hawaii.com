                <h2 class="small">Kalaniana'ole Beach Park (Nanakuli Beach Park), Oahu</h2>
                <p>Nanakuli Beach Park (<a href="../../oahu/beaches/more_nanakulibeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>), located in <a href="../cities/nanakuli.php">Nanakuli</a> on Oahu's leeward (west) coast, is a 40-acre park with a long and wide sand beach (about 500 feet long and 125 feet wide &ndash; 152 m x 38 m). It is situated between two limestone points and right along Farrington Highway. In March 2016 it was announced that the original name of Nanakuli Beach Park will be restored. So this beach park is now called Kalaniana'ole Beach Park. Restoration of the park's original name was done on behalf of residents who 
                expressed a desire to honor the late Prince Jonah Kuhio Kalaniana'ole Pi'ikoi. </p>
                	<? include ("../../theme/text.html") ?>		
<p>There isn't a lot of shade here and because the west coast of Oahu is hot and dry, it is a good idea to bring plenty of water and sunscreen. On the right (north) end of the beach park is a grassy area with some trees, picnic tables and a ballfield. The beach gets high surf and strong currents during the winter months and this is when swimming is not recommended here. And even during the summer, you can feel the power of the ocean - this beach has a strong shorebreak.</p>
<p>Camping is allowed with a permit. More info about how to get a permit is <a href="../../oahu/camping/">here</a>.</p>
                
