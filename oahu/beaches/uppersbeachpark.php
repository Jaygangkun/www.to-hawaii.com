                <h2 class="small">Uppers Beach Park, Oahu</h2>
                <p>Even though it's called a beach park, there is no park here and no facilities either. The shoreline is rocky and there is no sandy beach. Located right next to Kamehameha Highway, Uppers (<a href="../../oahu/beaches/p13.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is mainly visited by surfers (a surf spot called Uppers is located offshore). But the surf site isn't that good or popular. Access to the ocean is only possible after climbing over big lava rocks. The beach park was called Uppers because it is the last surf spot before Waimea Bay as one drives up from Haleiwa.</p>
