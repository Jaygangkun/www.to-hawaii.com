                <h2 class="small">Makaha Beach Park, Oahu</h2>
                <p>Makaha Beach Park (<a href="../../oahu/beaches/more_makaha_beach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p44.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>) is located on Oahu's leeward (west) coast and offers good swimming, bodyboarding and surfing conditions. The swimming is  better in the summer months when the ocean is generally calmer here (but exceptions are always possible). In the winter months, the surf is often times up and there is a strong shorebreak and rip currents. This is when Makaha Beach is only suitable for experienced surfers. Before going into the water, pay attention to all signs posted at the beach and ask the lifeguards about the current ocean conditions. </p>
              	<? include ("../../theme/text.html") ?>		  				
                <p>If you want to swim, enter the ocean in the middle section of the beach where there is no reef. Surfers usually prefer the northern end of the beach because this is where the waves break on the reef (near the hotel). However, it's only recommended for experienced surfers. Especially during low tide the reef is close to the water's surface and it is sharp.</p>
                <p>Makaha Beach Park is located on the driest side of the island, so especially in the summer months it is hot and dry here. Bring enough water and sunscreen and maybe even a beach umbrella because there is not much shade on the beach.</p>
                <p>Makaha Beach Park is one of the more popular beach parks on this side of the island, but nevertheless the beach is hardly ever  crowded. The busiest times are the weekends when local residents come here with their families to picnic and relax. There are a few picnic tables, restrooms and showers.</p>
                
                <h3 class="big">Makaha Beach Park Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Good for swimming, surfing, bodyboarding, snorkeling and scuba diving (depending on the season)</li>
                  <li>Strong shorebreak and rip currents during the winter - no swimming possible</li>
                  <li>Calmer water during the summer - this is when swimming conditions are best</li>
                  <li>Rip currents possible any time</li>
                  <li>Middle part of the beach is best for swimming</li>
                  <li>Sharp reef on the right side of the beach (near the hotel)</li>
                  <li>Dive site called Makaha Caverns located offshore</li></ul>                <div style="clear:both"></div>
