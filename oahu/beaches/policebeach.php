                <h2 class="small">Police Beach, Oahu</h2>
                <p>Police Beach is a remote beach on Oahu's North Shore, even though it lies between other popular surfing beaches. It is not remote in terms of distance, but rather in terms of accessibility. The beach encompasses a large stretch of shoreline, all the way from Papa'iloa Beach to Pua'ena Point. To get to Police Beach, one either has to walk along the shoreline from <a href="../beaches/papailoabeach.php">Papa'iloa Beach</a> or <a href="../beaches/puaenapointbeachpark.php">Pua'ena Point Beach Park</a> because the backshore of Police Beach is private property. Episodes of the popular TV series <em>LOST</em> were filmed on Police Beach.</p>
                <p>The beach is sandy, but many areas on the water's edge are covered by a lava rock shelf. The ocean bottom offshore is rocky too, so swimming isn't possible here (also because of strong rip currents, especially when the surf is up). Several surf spots are located offshore. Also, local fishermen come here for shoreline fishing.</p>
                <? include ("../../theme/text.html") ?>
                <p>This stretch of coastline was leased by the Honolulu Police Relief Association (HPRA) in 1975 and was used as a campsite for members of the Honolulu Police Department. That's how this beach got its rather strange and un-Hawaiian name.</p>
                
