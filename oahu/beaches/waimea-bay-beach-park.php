                <h2 class="small">Waimea Bay, Oahu</h2>
                <p>Waimea Bay (<a href="../../oahu/beaches/more_waimeabay_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p53.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>) on Oahu's North Shore can be very different depending on the season. During the winter months (November to February) the waves in the bay can reach heights of 20 feet (6 m) and more. If they do, Waimea Bay is a very busy place and it is difficult to find a parking spot. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>This is when surfers from Hawaii and around the world come here to surf and take part in world-famous surfing competitions, such as <em>The Eddie</em> (which only takes place when open-ocean swells reach a minimum of 20 feet, so it's a very special event that since its inception in 1984 has only taken place eight times). This surfing contest honors Eddie Aikau, a legendary big wave surfer and lifeguard at Waimea Bay who died in 1978 after the voyaging canoe Hokule'a capsized and he swam away to get help.</p>
                
                <p>When the surf is up at Waimea Bay, you won't even be able to get anywhere near the water. Lifeguards will put a yellow line from one end of the beach to the other to keep people away from the shorebreak. This is when only experienced surfers are allowed to enter the ocean. And even for them the winter waves and powerful currents at Waimea Bay are a challenge.</p>
                <p>During the summer months the ocean can be as calm as a lake with no waves at all. This is when swimming and snorkeling can be fun here because the water is so azure blue and clear, it's like swimming in a pool. But even then there can be strong currents that can't be seen from the beach, so obey all warning signs and stick to the center of the beach and away from the popular jumping rock. </p>
                <p>During the inbetween seasons when the surf is up but not way up it's a fun place for experienced bodysurfers and bodyboarders. The waves break very close to shore so you don't have to venture into the ocean very far to play in the waves. </p>
                <p>Overlooking Waimea Bay is one of the North Shore's most popular landmarks, the Saints Peter and Paul Church. It is located at the bay's east end and can easily be seen from the beach. The building that houses the church today used to be a rock quarry in the early 1930s, built to produce gravel for the construction of the stretch of Kamehameha Highway between Haleiwa and Kahuku. In 1953, the former machine shed and storage facility was transformed into the church you see today.</p>
                <p>Waimea Bay Beach Park has showers, restrooms and picnic tables, so even if the waves are too high for swimming it's still a nice place for a relaxing picnic coupled with exciting photo opportunities. </p>
                
                <h3 class="big">Waimea Bay Overview</h3>
                <ul type="disc" class="mylist">
                  <li>One of the most famous big wave surfing beaches in the world</li>
                  <li>High surf in the winter (November to February)</li>
                  <li>Usually calm waters during the summer</li>
                  <li>Strong rip currents when the surf is up</li>
                  <li>Challenging surfbreak even for experienced surfers</li>
                  <li>Swimming and snorkeling when the ocean is calm</li>
