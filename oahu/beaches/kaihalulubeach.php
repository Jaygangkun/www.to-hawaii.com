                <h2 class="small">Kaihalulu Beach, Oahu</h2>
                <p>Kaihalulu Beach (<a href="../../oahu/beaches/more_kaihalulubeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p18.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located east of the Turtle Bay Resort  (<a href="../../oahu/beaches/more_turtlebayresort_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p68.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on Oahu's North Shore. The beach doesn't see many visitors mainly because it's not a good beach for water activities such as swimming. The nearshore ocean bottom is rocky and a limestone shelf lines much of the bay. Some visitors have scratched their names and other symbols into the limestone shelf, which looks interesting &ndash; like modern-day petroglyphs (rock carvings). </p>
                <? include ("../../theme/text.html") ?>
                <p>Even though the swimming conditions are poor here, Kaihalulu Beach is a nice beach for a long walk in peaceful surroundings. The coastline is mainly undeveloped (the beach is backed by a small forest as well as the Fazio and Palmer golf courses). To access the beach, park at the Turtle Bay Resort public parking lot and walk towards the right (behind Ola's Restaurant). You can either walk in the sand or along a path that leads along the entire length of Kaihalulu Beach.</p>
                <p>Near the center of the beach is a small protected wading area called Keiki Pool (<a href="../../oahu/beaches/p_keiki_pool.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view photo</a>). &quot;Keiki&quot; means child in the Hawaiian language. A plaque marks the pool and says: &quot;This natural pool was formed during the 1946 tsunami that pushed up two ocean bedrock formations creating a protected enclosure, making it much calmer than the surrounding waters.&quot; </p>
                <p>Located right next to the walking path along the beach, there is a small Hawaiian stone altar (<a href="../../oahu/beaches/more_hawaiian_stone_altar.html" onclick="NewWindow(this.href,'name','650','650','yes');return false">view photos</a>). It is marked by a plaque that says: &quot;This Hawaiian altar is an 'ahupua'a demarcation between the 'ahupua'a land division boundaries of Hanaka'oe to the west and Kahuku to the east. 'Ahupua'a - literally the altar ('ahu) of the pig (pua'a), it is the name for both a land division and the stone altar that serves as a marker. The 'ahupua'a system of land management was a cornerstone of traditional Hawaiian life and helped Native Hawaiians to develop one of the most sustainable methods of land use in the world. Extending from the forested mountain tops mauka (inland) or the wao akua (region of the gods), through the kula (open plains used for farming) and extending out into makai (ocean), each 'ahupua'a contained everything its inhabitants needed to sustain life.&quot;</p>
