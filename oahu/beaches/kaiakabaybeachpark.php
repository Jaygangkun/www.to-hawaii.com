                <h2 class="small">Kaiaka Bay Beach Park, Oahu</h2>
                <p>Located on Oahu's North Shore on the Kaiaka Point peninsula in Haleiwa (<a href="../../oahu/beaches/more_kaiakabaybeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p65.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), this spacious (53 acre) beach park is a  great spot to relax, have a picnic or play ball games.Because it is located on  a peninsula and no major street is nearby, it is quiet here. </p>
                <? include ("../../theme/text.html") ?>
                <p>Kaiaka means "shadowy sea" in the Hawaiian language. The  ocean in this area looks brown and murky and indeed as if a shadow has fallen  on it. The swimming conditions at the west end of the peninsula are poor  because of soil runoff from the nearby Paukauila and Kiki'i Streams. Also,  there are many sharp rocks at the water's edge on that side, as well as a dark,  detrital sand beach. The best area for swimming is at the east end of the peninsula  where there is a sandy beach. But it's more like a dip into the water rather  than a swim because of the rocks in the water.</p>
                <p>From the park one can enjoy nice views of the Waianae Mountains and the neighboring Haleiwa beaches and coastline.  Located in the park is what looks like a giant stone mushroom. It is a  so-called balancing rock, and legend has it that it floated ashore from the  distant land of Kahiki.  In Hawaiian the name of this large limestone is <a href="../ancientsites/pohakulanai.php">Pohaku Lana'i</a>. </p>
                <p>There are many picnic areas in the park, and several  designated campsites are at the northern end of the peninsula. A permit is  required to camp here. More info how to get a permit is <a href="../../oahu/camping/">here</a>. However, there is no shade in this area. It's a large  open grassy area. </p>
                <p>Mostly local families camp here. At the time of writing, a  large tent stood here that displayed an upside down Hawaiian flag. Upside down  flags symbolize a nation in distress. Some islanders (some of whom are Native  Hawaiian), disapprove the U.S.  annexation of Hawaii. They want Hawaii  to be a sovereign nation. Some people in the Islands  show their discontent by flying the Hawaiian flag upside down, either in front  of their homes, or in parks, or as bumper stickers. </p>
                <h3 class="big">Kaiaka Bay Beach Park Overview</h3>
