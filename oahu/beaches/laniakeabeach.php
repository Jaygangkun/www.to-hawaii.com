                <h2 class="small">Laniakea Beach, Oahu</h2>
                <p>Laniakea Beach (<a href="../../oahu/beaches/p55.html" onclick="NewWindow(this.href,'name','2550','500','yes');return false">view panorama</a>) on Oahu's North Shore is also known as Turtle Beach. The main attraction here is the green sea turtles who often times can be seen on the beach basking in the sun. On other Oahu beaches, you're lucky if you can spot one of these amazing animals in the ocean, sticking her head out of the water. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>But on Laniakea Beach, the turtles come out of the water and don't seem to mind the people who are snapping pictures of them. In recent years the beach has become more crowded. Many circle island tour buses stop here on their way to other attractions. So  it can get busy here. The parking area is across the beach on the other side of the highway and there is no traffic light, so this often times causes a traffic jam because many people are crossing the highway here.</p>
                				
                <p>The Hawaiian honu (green sea turtle) enjoys full protection under the federal Endangered Species Act and under Hawaii state law. It is illegal to chase, hunt, handle, injure or harass them in any way. To make sure that the turtles are not bothered, there are volunteers on Laniakea Beach who inform visitors about the turtles and use a red rope to mark the &ldquo;no-go&rdquo; zones. Also, the National Oceanic and Atmospheric Administration (NOAA) has put up informational signs.</p>
                <p>The beach itself is rather small and has a rocky shelf. The turtles enjoy eating the seaweed that grows on it and on the surrounding rocks. When the surf is up, a strong rip current forms in the wide channel on the west side of the beach. Surfers use this beach to get to the offshore surf spots named Laniakea, Himalayas and Hultin's.</p>
                <p>Unfortunately, Laniakea Beach made tragic headlines in July 2008 when a large turtle named &ldquo;Honey Girl&rdquo; was found slaughtered on the beach one morning. She had last been seen resting on the beach under a full moon the evening before. Honey Girl was one of Laniakea's 24 resident turtles and the largest adult female turtle, weighing about 250 pounds. She was about 35-40 years old.</p>
                
