                <h2 class="small">Sand Island Beach Park, Oahu</h2>
                <p>Sand Island Beach Park (<a href="../../oahu/beaches/more_sandisland_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p52.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>) may not be the typical picture-perfect tropical beach, but it has other charms that make it a good place to visit.The small island is located right offshore from Honolulu Harbor on Oahu. It is accessible via the Sand Island Access Road. </p>
                <? include ("../../theme/text.html") ?>
                <p>From here you can enjoy unique views of Waikiki Beach, Diamond Head and the Honolulu International Airport. Besides the many low-flying airplanes of which you'll get up-close views, you'll also see many incoming freight and passenger ships and often times even big cruise ships that anchor right there in the harbor.</p>
                <p>Sand Island used to be an important location for coastal defense in World War II. The deserted lookout towers and bunkers that are still here today are remnants of this time. They make for an almost surreal contrast to the grassy areas, swinging palm trees and the blue waters of the Pacific.</p>
                <p>The long, sandy beach is never crowded and the beach park is a good place for picnics. Camping is also possible here Fridays through Mondays, but only with a permit. More info on how to get a permit is <a href="../../oahu/camping/">here</a>. </p>
                <p>The swimming conditions are fair, due to rocks in the water and the fact that Sand Island is near a busy harbor and underground sewer canals. Swimming is also limited to certain areas and not allowed in the harbor shipping lanes. However, fishing is good and you'll see many local fishermen trying their luck. Surfing is also possible; the waves are best in the summer months.</p>
                <p>No lifeguards are stationed on Sand Island. As for amenities, picnic tables and restrooms are available. Keep in mind that it's not safe to go on top of the lookout towers or bunkers. They are closed to the public. Never swim in the harbor lanes and follow all warning signs. </p>
                
