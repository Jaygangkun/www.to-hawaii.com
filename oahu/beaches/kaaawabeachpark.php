                <h2 class="small">Ka'a'awa Beach Park, Oahu</h2>
                <p>Ka'a'awa Beach Park (<a href="../../oahu/beaches/more_kaaawabeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a> , <a href="../../oahu/beaches/p64.html" onclick="NewWindow(this.href,'name','2550','600','yes');return false">view panorama</a>) on Oahu's  windward coast has a narrow sandy beach. Because of a shallow reef on the  nearshore ocean bottom, the swimming conditions are rather poor. There are a few sandy patches on the nearshore ocean bottom where it's fun to splash around in. Otherwise snorkeling can be fun when the ocean is calm.</p>
                <p>The beach park  is mainly used by fishermen, campers and picnickers. Fishermen come here to  catch he'e (octopus). In the Hawaiian language, Ka'a'awa means "the yellow wrasse  fish," a reef fish that was once abundant on the reefs in this area. </p>
