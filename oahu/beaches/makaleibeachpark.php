                <h2 class="small">Makalei Beach Park, Oahu</h2>
                <p>Located at the base of Diamond Head at the eastern end of Waikiki, Makelei Beach Park (<a href="../../oahu/beaches/p32.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) has a small and narrow beach.The swimming conditions are limited here because there is a reef on the nearshore ocean bottom. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>It's a nice place to dip in the water and on most days (especially on weekdays) the beach is empty. It can't be seen from the road. To get to it you'll have to walk through the beachfront park all the way to the stone seawall that lines the park's oceanfront and then turn right. The beach is backed by a few luxury beachfront homes. </p>
                <p>The beach park itself has some large trees that provide plenty of shade, and there are picnic tables and benches. The park is also used by surfers to access a few surf sites located offshore, some of which have scary or strange sounding names, such as Graveyards, Suicides, Radicals, Sleepy Hollows and Ricebowls.</p>
                
