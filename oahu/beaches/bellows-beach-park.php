                <h3 class="big">Bellows Beach Park Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Beach is a favorite among bodyboarders</li>
                  <li>Beach park is located between Inoa'ole and Waimanalo Stream, in front of the Bellows Air Force Station</li>
                  <li>Part of the longest stretch of sandy shoreline on Oahu - to the south is Waimanalo Beach</li>
                  <li>Watch out for jellyfish and Portuguese man-of-wars on the shoreline, they often get washed up on the beach</li>
                  <li>Camping is allowed with a permit, more info <a href="../../oahu/camping/">here</a></li>
