                <h2 class="small">Bellows Beach Park, Oahu</h2>
                <p>Located on Oahu's windward coast,  Bellows Beach is noted for its clear, azure blue waters, soft white sand and waves ideal for bodyboarding. Many people would consider it one  of the prettiest beaches on the island. The beach is backed by rows  of ironwood trees, which provide plenty of shade in the afternoons.</p>
