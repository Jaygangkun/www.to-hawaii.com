                <h2 class="small">Maunalua Bay Beach Park, Oahu</h2>
                <p>Maunalua Bay (<a href="../../oahu/beaches/p29.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>, <a href="../../oahu/beaches/more_maunaluabay_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>), located in Hawaii Kai on Oahu's south shore, is a favorite spot among canoe paddlers, boaters, kayakers, fishers and picnickers. Swimming conditions are rather poor here because the ocean bottom is shallow and rocky, but the views are nice and other ocean activities possible. Maunalua means &ldquo;two mountains&rdquo; and refers to Koko Crater and Koko Head, the two peaks located east of Maunalua Bay.</p>
                	<? include ("../../theme/text.html") ?>		
                <p>The beach park, which borders Kalanianaole Highway, was developed by Henry J. Kaiser when the Hawaii Kai Marina was built. In the past, the beach consisted of 535,000 square feet (49,703 sq. m) of coral that was dredged from the bay and pumped into low places along the existing shore. A boat channel was also dredged and upon completion of the park, Kaiser donated it to the City and County of Honolulu in 1960. </p>
                <p>The Hawaii Kai Marina was once a huge fishpond named Kuapa. At one point it covered 523 acres (2.1 sq. km) and its wall was 5,000 feet (1.5 km) long. There is a canoe storage building, or halau, at the west end of the park.</p>
                
