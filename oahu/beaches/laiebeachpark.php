                <h2 class="small">La'ie Beach Park (Pounders Beach), Oahu</h2>
                <p>La'ie Beach Park (<a href="../../oahu/beaches/more_poundersbeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p51.html" onclick="NewWindow(this.href,'name','2550','560','yes');return false">view panorama</a>), located on Oahu's northeastern shore, has a white-sand beach with waves year-round, but the waves are usually more consistent and higher in the winter months. The beach fronting the beach park is known as <span itemprop="alternateName">Pounders Beach </span>because of its often times pounding shorebreak. A shallow sandbar is responsible for the pounding surf and strong currents. Be cautious because this  sandbar  drops off abruptly. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The limestone cliff at the south end of the beach is called Pali Kiloi'a, which means &quot;fish-spotter cliff&quot; in the Hawaiian language. It is a popular spot for shoreline fishermen who fish for o'io, moi, papio and ulua (giant trevally).</p>
                <p>A calmer area for swimming is at the north end of the beach near the old pier pilings.  And if you walk along the shore around the corner, there is another small beach which locals call &ldquo;bathtub beach.&rdquo; An offshore coral reef buffers incoming waves, making the water less turbulent here (except during high surf season when you may not even be able to walk to this area along the shore). </p>
                <p>If you prefer to stay dry, the beach is lined with large ironwood trees which provide plenty of shade for a picnic. There are no facilities on the beach, but some shops and a grocery store is located in <a href="../cities/laie.php">La'ie</a>. The beach park is close to Brigham Young University and the <a href="../attractions/polynesianculturalcenter.php">Polynesian Cultural Center</a>.</p>
                
