                <h2 class="small">Papa'iloa Beach, Oahu</h2>
                <p>Papa'iloa Beach (<a href="../../oahu/beaches/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is among the lesser-known beaches on Oahu's North Shore.It only became more popular in recent years because of the TV series<em> LOST</em>, which was partially filmed near here. <br />
                  <br />
                  Papa'iloa is a beautiful and long white-sand beach fronting the beachfront community of the same name. A shallow reef lines much of the coastline, so it is not a prime beach to go swimming. <br /></p>
                  	<? include ("../../theme/text.html") ?>		
            <p>Several surf spots are located offshore. One of the more popular spots is called Himalayas. The surf spot was named by two big-wave surfers in the late 1950s who after surfing here believed that they had ridden the highest surf in the world and named this place after the highest mountains in the world &ndash; the Himalayas. <br />
                  <br />
                  During the winter months, the waves can get very big and the ocean currents erode the parts of the beach that are not protected by a reef. In the summer months when the surf is generally calmer, spearfishers can be seen diving on the reefs offshore.</p>
                
