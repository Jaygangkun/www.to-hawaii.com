                <h2 class="small">Kaka'ako Waterfront Park (Point Panic Beach Park), Oahu</h2>
                <p>The history of Kaka'ako Park (<a href="../../oahu/beaches/p26.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>, <a href="../../oahu/beaches/more_kakaakopark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>) is an interesting one. While walking over the green, hilly meadows in the park one wouldn't think that this is a former landfill site. In 1948, the City and County of Honolulu constructed a large landfill right here on the shoreline's shallow reef to get rid of material that they couldn't burn. </p>
                <? include ("../../theme/text.html") ?>
                <p>They built a large seawall that measured 10 feet (3 m) high and 30 feet (9 m) wide at its base that reached all the way from Kewalo Basin at its west end to Pier 1 in Honolulu Harbor at its east end. The landfill was in operation until the 1960s, which added 29 new acres of land to the Kaka'ako shoreline. In 1992, years after the landfill was closed, the Kaka'ako Waterfront Park opened on this former landfill site. </p>
                <p>The park's shoreline doesn't have a sandy beach. There is a seawall along the water's edge and large boulders. A convenient access point to the ocean is located at the east end of the park, which is used by surfers to enter the water. </p>
                <p>The park's shaded and spacious grassy areas are the perfect place for family gatherings and picnics. Most people who visit the park come here to do just that &ndash; to picnic, relax or do some shoreline fishing. </p>
                <p>Kaka'ako Waterfront Park is sometimes also referred to as Point Panic Beach Park because of the popular bodysurfing site that's located offshore at the east end of the park, which is called Point Panic. This surf site is strictly for bodysurfers only. No surfboards or bodyboards are allowed. Two other surf sites at the park's west end where board surfing is allowed are called Flies and Incinerators. Kaka'ako is also a popular diving location. Especially on weekends one can see divers in the nearshore waters exploring the coral reef.</p>
                <p>Two memorials are located within the park, the Ehime Maru Memorial and the Victim Memorial. The <em>Ehime Maru</em> was a Japanese fisheries training vessel that was accidentally rammed by the U.S. Navy Submarine <em>Greenville</em> on February 9, 2001. The <em>Ehime Maru</em> sank and nine crewmembers and students of the <em>Ehime Maru</em> died. The accident occurred about 9 miles (14.5 km) south of Oahu. The Victim Memorial was sponsored by Mothers Against Drunk Drivers (MADD). It is dedicated to victims and survivors of drunken driving accidents.</p>
                
