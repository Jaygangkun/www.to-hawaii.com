                <h2 class="small">Swanzy Beach Park, Oahu</h2>
                <p>Located on Oahu's windward coast, Swanzy Beach Park (<a href="../../oahu/beaches/more_swanzybeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>) is a  good spot to camp, picnic and fish.A seawall stands at the water's edge and  the nearshore ocean bottom has a shallow reef, so swimming isn't possible here.  There is only a narrow strip of sand right behind the seawall. </p>		
                <p>Since the name Swanzy isn't Hawaiian, one may wonder why this beach park is named like this. In the 1920s, island resident Julie Judd Swanzy donated these five acres of beachfront land to the City and County  of Honolulu for a park. She had inherited the land from her father, Dr. Gerritt  P. Judd. The park was officially named after her to recognize her generosity. </p>
                <p>The park also has a playground for children and picnic  tables. Camping is allowed on the weekends (Friday through Sunday) and with a  permit only. More info how to get a permit is <a href="../../oahu/camping/">here</a>.</p>
