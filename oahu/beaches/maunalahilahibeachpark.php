                <h2 class="small">Mauna Lahilahi Beach Park, Oahu</h2>
                <p>This beach park is named after Mauna Lahilahi (<a href="../../oahu/beaches/more_maunalahilahi_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p46.html" onclick="NewWindow(this.href,'name','2550','450','yes');return false">view panorama</a>) , a small mountain located on the northern end of the beach. In the Hawaiian language, Mauna Lahilahi means &ldquo;thin mountain.&rdquo; It rises 230 feet (70 m) and provides a scenic backdrop. The beach itself is narrow and fronted by a reef. The swimming conditions are therefore rather poor. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The beach park is located right on Farrington Highway, so one can hear the traffic at all times. There are a few picnic tables in the park, but since they are not standing in the shade of a tree, they are almost always empty. There are a few scattered trees near the southern end of the beach that are a better spot for a picnic (if you don't have a beach umbrella). However, some of the trees have sharp thorns, so avoid picnicking under one of those. </p>
                <p>On the far southern side of the beach are three small coves. One of them has a protected swimming area (it is protected by a man-made rock wall). A few vacation condominiums are located here as well.</p>
                
