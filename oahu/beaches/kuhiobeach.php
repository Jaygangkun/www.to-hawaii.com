                <h2 class="small">Kuhio Beach, Oahu</h2>
                <p>Kuhio Beach Park (<a href="../../oahu/beaches/more_kuhiobeach2_photos.html" onclick="NewWindow(this.href,'name','650','650','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p22.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>) is part of Waikiki Beach, located between the Sheraton Moana Surfrider Hotel and the Kapahulu Groin (The Wall), which was built in 1951 as part of the Waikiki Beach Improvement Project. It is an extension of a storm drain that runs under Kapahulu Avenue. The project also included building a low wall on the Diamond Head side of the groin and importing sand to create the beaches on both sides. Today, this area right in front of the groin is a popular surf spot among bodyboarders. Surfboards are not allowed here. To the west of the beach park are two of Waikiki's popular surf sites, Queen's and Canoes. </p>
                				
                <p>Three popular landmarks are located in Kuhio Beach Park: the Prince Kuhio Statue, the Duke Kahanamoku Statue and the Stones of Kapaemahu.The Prince Kuhio Statue was created by sculptor Sean Browne and was dedicated in 2002. The <a href="../attractions/dukestatue.php">Duke Kahanamoku Statue</a> was created by sculptor Jan-Michelle Sawyer and dedicated on 1990, the hundredth anniversary of Duke's birth. He is considered the &ldquo;Father of Modern Surfing,&rdquo; a great Hawaiian watermen, Olympic swimmer, surfer and canoeist. The Stones of Kapaemahu, also known as the <a href="../ancientsites/wizardstones.php">Wizard Stones</a>, represent four legendary men: Kapaemahu, Kahaloa, Kapuni and Kinohi, who came to Hawaii from a faraway land. In Hawaii they were famous for their great wisdom and healing powers.</p>
                <p>If you were in  Waikiki between January and April of 2012, you may have wondered why there was this large barge offshore and construction going on right on popular Kuhio Beach. It's because the beach is literally disappearing due to the erosive force of the ocean. So the City decided that it was again time for a sand replenishment project. A barge collected about 24,000 cubic yards of sand offshore and pumped it through a pipe to the beach. Some local residents were concerned that the sand was  taken from the same area where ashes of the deceased have been scattered for generations, including the ashes of <a href="../../donho.php">Don Ho</a> and <a href="../../oahu/attractions/dukestatue.php">Duke Kahanamoku</a>. Prior to the start of the 4-month project the state held a blessing ceremony, and officials from the State Department of Land and Natural Resources told the people that work crews are sensitive to their concerns. The beach is all fixed up now and is wider than it was before. You can see some construction photos <a href="../../oahu/beaches/more_kuhiobeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">here</a>. </p>
                <p>Kuhio Beach Park was named after Prince Jonah Kuhio Kalanianaole, born in 1871. His home, Pualeilani (meaning &ldquo;flower from the wreath of heaven&rdquo;) used to stand here, and he lived here with his wife Princess Elizabeth Kahanu.</p>
                
