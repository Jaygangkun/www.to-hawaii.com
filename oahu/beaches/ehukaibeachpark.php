                <h2 class="small">Ehukai Beach Park, Oahu</h2>
                <p>Raging waters, towering surf and massive waves that shake the shores. Ehukai Beach Park (<a href="../../oahu/beaches/p8.html" onclick="NewWindow(this.href,'name','2550','580','yes');return false">view panorama</a>) on Oahu's North Shore is a daredevil's dream, with waves  reaching heights of over 30 feet (9 m) during the winter months (November to February).It's no wonder the beach is one of the most famous surf spots in the world - and as you might expect, also one of the most dangerous.</p>
                <? include ("../../theme/text.html") ?>
                <p>In the winter, adventurous surfers come to Ehukai to surf the  massive waves that form tubes as they break. From the shore, it makes for an undeniably splendid view. But the real spectacle is the surfers who dare to ride these waves. Not all of them succeed, of course - let's just say the Ehukai lifeguards have become well-trained at treating broken bones. Surfing contests take place here, for example the 2013 Volcom Pipe Pro, which was held in January/February 2013 (<a href="../../volcompipepro.html" onclick="NewWindow(this.href,'name','850','580','yes');return false">see photos</a>).</p>
                <p>The beach is a lot calmer from May to September, often times as calm as a lake. This is when swimming is possible here. The trees lining the shore also provide  shade for picnics. In the Hawaiian language, <em>ehukai</em> means  &ldquo;sea spray.&rdquo;</p>
