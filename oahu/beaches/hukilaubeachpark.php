                <h2 class="small">Hukilau Beach Park, Oahu</h2>
                <p>Hukilau Beach Park  (<a href="../../oahu/beaches/more_hukilaubeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p61.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located in the little town of La'ie on Oahu's northeastern shore.It is a section of the long sand beach that runs between La'ie and Kalani Points. Swimming is good here during the summer. In the winter time when the surf is up, watch out for strong currents. </p>
                <? include ("../../theme/text.html") ?>
                <p>A &ldquo;hukilau&rdquo; in Hawaiian is a traditional method of fishing, in which a group of people participate. &ldquo;Hukilau&rdquo; literally means &ldquo;pull a lau,&rdquo; which is a rope with ti leaves attached to it. </p>
                <p>In 1947, the Church of Jesus Christ of Latter-Day Saints organized a hukilau in La'ie to fundraise money to replace their wooden chapel, which had burned down. Because the hukilau was so popular, the visitor industry asked the La'ie community to continue this fishing method as a visitor attraction. So for years, it was held on this beach. </p>
                <p>Only when in 1970, the state determined that it was a commercial endeavor that needed to be taxed, the community found the hukilau to be unprofitable and discontinued this popular event. But up until today, the beach here is known as Hukilau Beach.</p>
                <h3 class="big">Hukilau Beach Park Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Located in La'ie on Oahu's northeastern coast</li>
                  <li>Sandy beach that is good for swimming when the surf is calm</li>
                  <li>Watch out for currents that usually occur in the winter months or when the surf is up</li>
                  <li>Beach park is closed on Sundays (the parking lot), but you may still walk in</li>
                </ul>
