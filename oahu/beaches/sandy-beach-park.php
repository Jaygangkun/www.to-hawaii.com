                <h2 class="small">Sandy Beach Park, Oahu</h2>
                <p>Sandy Beach Park (<a href="../../oahu/beaches/more_sandybeachpark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p39.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located on Oahu's southeastern shore. With its pounding waves and powerful shorebreak, it is popular among experienced bodyboarders and bodysurfers.  This beach is known as one of the most dangerous beaches in Hawaii, and people call it &quot;the beach of broken necks&quot; or &quot;Broke Neck Beach&quot; for a reason. It is sad that numerous people have become paralized after enjoying the waves at Sandy's. Others have drowned. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>On most days of the year, you will see red warning flags and signs posted on the beach that warn people about the dangers  (which are: strong currents, dangerous shorebreak, high surf and sharp coral). Even if the ocean looks calm, which is rare here as there are waves on almost every day of the year, be aware that there could be dangerous currents. </p>
                <p>Many people come to Sandy Beach Park to just watch the waves, take photos and relax on the beach. There is no shade on the beach though. The beach park has a large grassy area with picnic tables, restrooms and showers.</p>
                
