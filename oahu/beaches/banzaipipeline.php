                <h2 class="small">Banzai Pipeline, Oahu</h2>
                <p>Located on Oahu's North Shore, Banzai Pipeline (<a href="../../oahu/beaches/p7.html" onclick="NewWindow(this.href,'name','2550','550','yes');return false">view panorama</a>) is a popular surf site where waves can reach heights of 20+  feet (6+ m) in the winter months (November to February).Many big wave surfing contests take place here, and  people from all over the world - professional surfers as well as spectators and  photographers - come here to see the  waves.</p>
                <? include ("../../theme/text.html") ?>
                <p>The Pipeline was named in 1961 by a California  surfing movie producer, Bruce Brown. He visited Oahu's  North Shore and was scouting possible movie locations together with two California  surfers, Mike Diffenderfer and Phil Edwards. They stopped at this yet unnamed surf  site and Brown filmed the surfers catching several waves. They all agreed that  the surf site needed a name. </p>
                <p>On the same day, a construction project on the highway next  to the beach (Kamehameha Highway)  was going on. An underground pipeline needed to be fixed. Diffenderfer  suggested they name this surf spot "the Pipeline." In 1961, Brown made this  name popular in his new movie <em>Surfing Hollow Days</em>.  At that time, the beach fronting the surf site was called Banzai   Beach. But today, it is better  known as Banzai Pipeline.</p>
                <h3 class="big">Banzai Pipeline Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Popular surf site on Oahu's North Shore</li>
                  <li>In the winter months, waves often times reach heights of 20+ feet (6+ m)</li>
                  <li>You may want to bring binoculars as the waves break about 180 yards (165 m) offshore</li>
                  <li>Surfing contests take place here in the winter</li>
                  <li>Swimming only possible when the ocean is calm (generally in the summer months)</li>
                </ul>
