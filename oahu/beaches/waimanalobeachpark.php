                <h2 class="small">Waimanalo Beach Park, Oahu</h2>
                <p>What do you call 3 miles of sloping white sand, hundreds of towering ironwood trees and a boundless turquoise sea? For visitors, this is paradise. But for the locals of windward Oahu it is Waimanalo Beach  (<a href="../../oahu/beaches/more_waimanalobeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p43.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>). Located about a 45-minute drive  from famous Waikiki Beach, Waimanalo is the kind of place that makes you blink to make sure you're not dreaming. The main attraction here is the ocean, which looks azure blue even on a cloudy day. </p>
<? include ("../../theme/text.html") ?>
                <p>At around 3 miles (4.8 km) long, Waimanalo Bay is lined by the longest uninterrupted white-sand beach on Oahu. It's the perfect place for the ultimate beach stroll. Much of Waimanalo Beach is backed by ironwood trees, which provide plenty of shade. </p>
                
                <p>There are two main access points to the beach (two beach parks), one near the southern end of the beach and one near the northern end. The southern one is called Waimanalo Beach Park, located across from Nakini Street. This beach park has showers, restrooms, a pavilion, picnic tables and a lifeguard tower. The northern access (between Aloiloi Street and Tinker Road) leads through an ironwood forest to the Waimanalo Bay State Recreation Area. It has the same facilities and a lifeguard tower, plus a camping area where camping is allowed with a permit (more info on how to get a permit is <a href="../../oahu/camping/">here</a>).</p>
                <p>Since Waimanalo Beach is located on Windward Oahu, it faces the prevailing northeasterly trade winds, which can be quite breezy. If you plan a picnic here in the winter months (November to March), you may want to bring a light jacket as it can get a bit chilly, especially if it's windy.</p>
                <p>Waimanalo Beach is known for being a good bodyboarding beach since the waves are not too high and break close to shore. Occasionally the shorebreak is very powerful here, so be cautious when entering the ocean and stick to the areas where the lifeguards are located.</p>
                <p>Did you know that Waimanalo Beach was the filming location for <em>Magnum P.I.</em> and <em>Baywatch Hawaii</em>? And even famous author James Michener wrote his novel <em>Hawaii</em> in Waimanalo. Also, Waimanalo Beach is a favorite place for couples who just got married to have their pictures taken.</p>
                <p>Waimanalo Beach is a silent refuge on most weekdays when you can have a good stretch of the beach to yourself. But on weekends, the grills and picnic tables get a serious workout. Locals gather for all-day barbecues and visitors come to sunbathe, swim and have fun. </p>
                
