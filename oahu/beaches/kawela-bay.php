                        <h2 class="small">Kawela Bay, Oahu</h2>
                        <p>Kawela Bay  (<a href="../../oahu/beaches/p20.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>) is one of the most secluded beaches on Oahu. Located near the Turtle Bay Resort on the island's North Shore, it is a perfect spot to unwind. Bathe in the calm water or enjoy a peaceful stroll along the beach. The sandy beach is narrow and backed by coconut palms and ironwood trees. Watch out for falling coconuts! </p>
                        <p>This curved bay is one the most protected on Oahu's North Shore because of the reef that diffuses high waves during the winter months when other North Shore beaches get pounded by high surf. Kawela Bay's nearshore waters are calm year-round. </p>
                        <? include ("../../theme/text.html") ?>
                        <p>Swimming at Kawela Bay is ok, but not the best because the water is often times murky and some areas of the bay have rocks on the ocean bottom. So it is tricky to avoid the rocky patches and to watch out for your toes. For the same reason, the snorkeling isn't that good because you can't see much. A couple of surf sites are located on the outer reefs of the bay, but here the currents can be strong.</p>
                        <p>Located in the small forest behind Kawela Bay is a beautiful large banyan tree (<a href="../beaches/more_kawelabay_banyantree_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">view photos</a>). It is marked by a plaque that says: &quot;This single tree, an amazing expanse of multiple trunks and vines, is routinely used as a filming location for major motion pictures and television shows, including <em>LOST</em> and <em>Pirates of the Caribbean</em>. Older banyan trees have &quot;prop roots&quot; that eventually grow into trunks as large as the original, and many banyans live to be several hundred years old.&quot; <br />
                          <br />
                          In the past, houses stood along Kawela Bay's entire length. But in 1986, all residents from the bay's east point to the center were evicted to make way for a resort that was never built. So up until today, the property along this part of the bay remains undeveloped. There are only a few beach homes along the bay's west end. </p>
                        <p>There have been talks in the recent past that more vacation rentals were being planned at the east end of Kawela Bay. However, in May 2014, Turtle Bay Resort along with the State of Hawaii, City and County of Honolulu and The North Shore Community Land Trust, established a conservation easement on 665.8 acres of land. This agreement protects Kawela Bay from future development. </p>
                        <p>Even though no development will take place for now, Kawela Bay has seen some major changes in 2014. Kawela Bay's backshore used to be heavily forested (<a href="../beaches/more_kawelabay_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">view photos</a>), but many trees and shrubs have recently been removed. There are a few signs that explain the deforestation. The signs say: &quot;Turtle Bay Resort is working with the State of Hawaii and shoreline experts to prune overgrown non-native vegetation to clear the shoreline and improve beach access (<a href="../beaches/more_kawelabay_sign.html" onclick="NewWindow(this.href,'name','650','500','yes');return false">view photo</a>).&quot;</p>
                        <p>Because Kawela Bay is much easier accessible now, the bay is seeing more commercial activities take place. Surf schools are driving right up to the beach to take students for surf lessons in the bay. To reach the bay, walk along the shore from the Turtle Bay Resort. It takes about 20-30 minutes to get there. Alternatively, Kawela Bay is also accessible via a footpath from Kamehameha Highway.</p>
                        
