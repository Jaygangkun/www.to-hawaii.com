                <h2 class="small">Yokohama Bay (Keawa'ula Beach), Oahu</h2>
                <p><span itemprop="alternateName">Keawa'ula Beach</span>, better known as Yokohama Bay, is the northernmost beach on Oahu's west shore. It is located a short distance just before the road ends and the trail to <a href="../beaches/kaenapoint.php">Ka'ena Point</a>, the island's northwestern tip, begins. </p>
                <p>This beach is truly off the beaten path. It is one of the most remote beaches on the island and is never crowded. You need a car to get here as the City Bus only goes up to <a href="../beaches/makaha-beach-park.php">Makaha Beach</a>. In the winter months (November to February) the beach gets pounded by high surf. This is the time when many local and visiting surfers come here to experience some thrills in the churning white water. Lifeguards are present most of the time and for good reason. The rocky ocean bottom, strong currents and high waves can be dangerous especially for beginning surfers. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>The Hawaiian name for this beach is Keawa'ula, meaning &quot;the red harbor,&quot; named for the great number of squid that used to be found in the bay's nearshore waters. These squid have a bright red color when they reproduce. Most locals today refer to this beach as Yokohama Bay. Japanese fishermen in the early 1900s who came here to fish gave the beach this name, which later stuck in people's heads.</p>
                <p>Yokohama Bay has fine sand and is backed by the Wai'anae Mountain Range. The beautiful natural scenery makes for great photo opportunities. The bay is part of the Ka'ena Point State Park. From here a dirt trail leads north to Ka'ena Point, Oahu's northwestern tip.</p>
                
