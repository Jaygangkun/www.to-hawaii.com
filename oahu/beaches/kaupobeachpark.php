                <h2 class="small">Kaupo Beach Park, Oahu</h2>
                <p>Kaupo Beach Park (<a href="../../oahu/beaches/more_kaupobeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p42.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located across the street from Sea Life Park Hawaii on Oahu's southeastern shore. It is a narrow roadside beach park with a sandy beach.Swimming isn't really possible here because the nearshore ocean bottom is rocky, but the snorkeling and diving conditions next to the pier are good. Also, there are a few sand-bottomed pools, where  families  take their kids to play in. </p>
              <? include ("../../theme/text.html") ?>  				
                <p>In the Hawaiian language, Kaupo means &ldquo;night landing.&rdquo; It got this name from the former fishing village that used to be located here. Kaupo Beach is also known as Baby Makapu'u, Kumu Cove and Cockroach Bay. The youngest lava flow on Oahu is believed to have occurred here.</p>
                <p>From the beach one can see the Makai Research Pier. The pier also has a small boat harbor and submarine slip and is used by a few ocean engineering firms. </p>
                <p>Located offshore are two small islands, <a href="../attractions/rabbitisland.php">Rabbit Island</a> (the larger one, which is also known as Manana Island) and Kaohika'ipu (also known as Black Rock). Both are seabird sanctuaries and landing on them is only allowed with a permit.</p>
                <p> Another feature at Kaupo Beach can easily be overlooked because it is just a rock that's  mostly submerged at high tide. This rock is called <a href="../ancientsites/pohakupaakiki.php">Pohaku Pa'akiki</a>, and according to an ancient legend, it is a monument that reminds of the agreement between Hawaiians who lived in this area in the old Hawaii and a guardian shark that there would be no shark attacks here.</p>
                
