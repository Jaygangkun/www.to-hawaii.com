                <h2 class="small">Kuilima Cove, Oahu</h2>
                <p>Kuilima Cove (<a href="../../oahu/beaches/more_bayviewbeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p21.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), also known as Bayview Beach, is located right next to the Turtle Bay Resort (<a href="../../oahu/beaches/more_turtlebayresort_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p68.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on Oahu's North Shore. It is where resort guests go swimming, but the beach is also open to the public. The beach lies in a protected cove between two limestone points, Kuilima Point or Kalaeokaunu (which means &quot;the point of the altar&quot;) to the west and Kahuku Point or Kalaeokamanu (which means &quot;the point of the birds&quot;) to the east. The former was named for an altar that was once located on the point and used by Hawaiian fishermen. Residents have called the beach Kalokoiki, &quot;the little pool&quot; in the past; today, the cove is known as Kuilima Cove. </p>
                <? include ("../../theme/text.html") ?>
                <p>Swimming and snorkeling is good here, but during high surf, be aware of a strong rip current that runs out of the cove and into the open ocean along Turtle Bay Point (the peninsula on which the resort is located). In case of high surf, it is better to stay near the shore. You can access the beach through the resort's public parking lot.</p>
