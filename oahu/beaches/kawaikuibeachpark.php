                <h2 class="small">Kawaiku'i Beach Park, Oahu</h2>
                <p>Kawaiku'i Beach Park (<a href="../../oahu/beaches/p36.html" onclick="NewWindow(this.href,'name','2550','550','yes');return false">view panorama</a>) is easy to miss. It is located right off of Kalanianaole Highway right before Hawaii Kai when coming from Honolulu. For being a neighborhood park, this beach park is quite spacious. There is plenty of shade and a large grassy area with picnic tables. </p>
                <p>The beach is narrow and there is not a lot of sand. It also smells a bit fishy and the nearshore waters are too shallow for a swim. The water looks brown here instead of blue. But the views are nice and it&rsquo;s a great place for a picnic. Surfers enter the ocean here to reach the surf spots Manantan's, Tunas and Turtles, which are located at the outer edge of the reef.</p>
