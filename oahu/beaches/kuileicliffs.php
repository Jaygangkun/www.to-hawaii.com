                <h2 class="small">Kuilei Cliffs Beach Park, Oahu</h2>
                <p>Kuilei Cliffs Beach Park (<a href="../../oahu/beaches/p33.html" onclick="NewWindow(this.href,'name','2550','730','yes');return false">view panorama</a>) lies at the foothills of <a href="../attractions/diamondhead.php">Diamond Head</a>, between the Diamond Head Lighthouse and the residential community of Kaalawai.The 11-acre beach park has a narrow beach backed by sea cliffs. The beach sand has a greenish hue and glitters in the sun. The sand along this stretch of coastline has already made an impression to early sailors. In 1825, British sailors noticed small sparkling calcite crystals in the beach sand along the crater's foothills and sea cliffs. They named the crater Diamond Hill. This name was later changed to Diamond Head. This is how the crater got its English name.</p>
                		<? include ("../../theme/text.html") ?>					
                <p>There are three popular drive-in lookouts along <a href="../scenicdrives/diamondheadroad.php">Diamond Head Road</a> that offer sweeping views of this stretch of coastline. To get to the beach, walk down the paved trail, located between two of the lookouts. The swimming conditions aren't good here because the nearshore ocean bottom is shallow and has a  reef. However, this beach is a surfers'  paradise as the reef generates waves almost every day and it is a lot less crowded here than in Waikiki. When the tradewinds are strong, this area is also popular  among windsurfers.</p>
                <p>The middle lookout is the site of a memorial to Amelia Earhart (1898-1939), who was the first woman to fly across the Atlantic Ocean. In 1935, she was the first person to fly alone from Hawaii to North America. In 1939, she tried to fly from New Guinea to Howland Island, on her way to Hawaii, but she never reached Howland Island and was never seen again. The monument on this lookout was created by Kate Kelly, Honolulu's leading sculptress of the 1930s.</p>
                
