                <h2 class="small">Pupukea Beach Park, Oahu</h2>
                <p>Pupukea, which means &ldquo;white shell&rdquo; in the Hawaiian language, is a long and narrow stretch of beach with a rocky shoreline. On each end of Pupukea Beach Park (<a href="../../oahu/beaches/p58.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) lie two small pocket beaches within the rocks &ndash; Shark's Cove and Three Tables.Both are very popular and fantastic dive and snorkeling sites, but mainly in the summer months when the surf is calm. In the winter when the surf is up and dangerous, swimming, snorkeling and diving are often times not possible or recommended.</p>
                	<? include ("../../theme/text.html") ?>		
                  <p>                 <a href="../../oahu/beaches/sharks-cove.php">Shark's Cove</a> got its name from a popular story that says that the outline of a reef outside the cove looks like a shark when seen from above. In the summer months, Shark's Cove is a prime snorkeling spot, and even though the access to the water makes you wish you were a mountain goat, it is a very popular destination (finding a parking spot can be challenging on busy summer weekends). To get to the ocean, you have to climb down a steep dirt path. There is no sandy beach here, just a few sandy patches between the boulders that line the shore.&nbsp;</p>
                  
                <p><a href="../../oahu/beaches/threetables.php">Three Tables</a> is named like that because of the three flat reefs that lie off the beach. The &ldquo;tables&rdquo; emerge above the ocean's surface during low tide. It's another great snorkeling spot, also only in summer as the surf is too wild and too dangerous in winter. In between both beaches you find a beachcomber's paradise with tide pools, surreal lava formations and the occasional blowhole. </p>
                <p>In the middle section of the park is the Honolulu Fire Department's Sunset Beach Fire Station, an important base for the North Shore of Oahu. The firefighters stationed here also respond to many ocean rescues.</p>
                <p>Pupukea Beach Park (<a href="../../oahu/beaches/more_pupukeapark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>)  is a marine life conservation area and has several great spots to play in the water along its narrow 80 acres on the side of Kamehameha Highway. Across the street is a Foodland supermarket (only grocery store in this area), and you'll find many small surf-wear stores and roadside food trucks nearby. </p>
                
