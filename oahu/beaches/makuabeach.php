                <h2 class="small">Makua Beach, Oahu</h2>
                <p>Makua Beach is one of the most secluded and far away beaches on Oahu. This undeveloped and never crowded white-sand beach is backed by the Waianae Mountain Range and <a href="../attractions/makuavalley.php">Makua Valley</a> (after which the beach is named). </p>
                <p>Most Oahu visitors never get to see this beach. That's because no tour buses go here, and most people who make the effort to drive to the remote leeward coast of Oahu only drive up to <a href="../beaches/makaha-beach-park.php">Makaha Beach</a>, a popular big wave surfing beach in the winter months. After the small town of Makaha the development soon ends and there aren't any other buildings. Even the road narrows.</p>
                	<? include ("../../theme/text.html") ?>		
                <p>From November to April, there is some excellent surf on this side of the island, perfect for expert surfers. Unlike many Oahu beaches, Makua Beach is not protected by an offshore reef, so the waves can get really high and the currents strong on rough days. The ocean bottom drops off quickly here, so swimmers should stay close to the shore. Fishing and bodyboarding are popular activities on calmer days. There are no lifeguards or facilities here.</p>
                <p>Makua Beach is the second last beach where the road ends (the last one is Yokohama Bay). Both beaches are part of the <a href="../beaches/kaenapoint.php">Ka'ena Point State Park</a>. If you're up for a hike, there is a trail that leads to the northwestern tip of the island. Ka'ena Point is supposedly a &quot;leina a ka uhane,&quot; or a jumpoff point for departing souls. The area is sacred to Native Hawaiians. No paved roads lead to the park, so hiking is the only way to get there.</p>
                
