                <h2 class="small">Hale'iwa Beach Park, Oahu</h2>
                <p>Located in the small town of Hale'iwa on Oahu's North Shore, Hale'iwa Beach Park (<a href="../../oahu/beaches/more_haleiwabeachpark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p67.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is fronted by a narrow sand beach. Swimming isn't that good here because of the shallow and rocky ocean bottom, but the park is nice for picnics. </p>
                <? include ("../../theme/text.html") ?>
                <p>Also, Hale'iwa Beach Park is frequently used by canoe paddlers as a training and regatta site and by kayakers as an access point to the bay. Surfers also like this beach because from here they can get to the waves at the surf spot Puaena Point. The name Hale'iwa in the Hawaiian language means &ldquo;house (of) frigate bird.&quot;</p>
