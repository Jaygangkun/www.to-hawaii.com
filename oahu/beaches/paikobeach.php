                <h2 class="small">Paiko Beach, Oahu</h2>
                <p>Located on Oahu's south shore, Paiko Beach (<a href="../../oahu/beaches/p35.html" onclick="NewWindow(this.href,'name','2550','580','yes');return false">view panorama</a>) is a narrow strip of sand that almost disappears at high tide.It is fronted by a shallow coral reef, so the swimming conditions are poor here. It's a better spot to kayak or fish. Also, surfers paddle out here to reach the waves of Paikos and other surf spots that are located at the outer edge of the reef.</p>
                	<? include ("../../theme/text.html") ?>		
                <p>Paiko Beach is named after Joseph Paiko who died on June 1, 1942. His father Manuel Paiko came to Hawaii as a whaler in the 1840s. He came from Pico, one of the nine main islands of the Azores, located about 800 miles (1,287 km) off the coast of Portugal. He was known as &ldquo;Manuel do Pico&rdquo; and his name later became &ldquo;hawaiianized&rdquo; into Paiko. Paiko senior was one of the first Portuguese to settle in Hawaii, who came to Hawaii even before the first group of Portuguese contract laborers came in 1878.</p>
                <p>The undeveloped Paiko Peninsula is located at the east end of the beach. Local residents call it Sand Point. The peninsula encloses the saltwater Paiko Lagoon, which was designated as a wildlife sanctuary by the State Department of Land and Natural Resources in 1974. Many endangered Hawaiian shorebirds find a place of refuge here, including the Hawaiian stilt, also known as a&lsquo;eo. </p>
                
