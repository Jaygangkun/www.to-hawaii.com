                <h2 class="small">Ke'ehi Lagoon Beach Park, Oahu</h2>
                <p>This urban beach park is located next to the Honolulu International Airport. Ke'ehi Lagoon  (<a href="../../oahu/beaches/more_keehilagoonbeachpark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a great place for canoeing and boating since the waters are usually calm in the protected lagoon.Many canoe clubs do their practice session here and outrigger canoe regattas take place from time to time. However, the man-made beach is not suitable for swimming, snorkeling or surfing. </p>			
                <p>There is a large grassy area with soccer, baseball, softball and cricket fields, tennis courts, a pavilion and restrooms. In the weekends, the park is often times used for family gatherings since it's a good place to barbecue. When outrigger canoe races take place here, many paddlers as well as spectators come to the park. </p>
                <p>Even though the beach park is located on a major highway, the views over to neighboring Sand Island and the city are nice. And since the park is located right next to the airport, one can see many planes take off and land from up close. <br />
                  <br />
                  Located within Ke'ehi Lagoon are several tiny islands. One of them is <a href="../ancientsites/mokaueaisland.php">Mokauea Island</a>. Encompassing a mere 10 acres, this tiny spit of land is the site of Oahu's last Hawaiian fishing village (and one of two left in the entire state of Hawaii). The non-profit organisation Ho'ola Mokauea has been working to environmentally and culturally restore Mokauea Island.</p>
