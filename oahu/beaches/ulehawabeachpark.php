                <h2 class="small">Ulehawa Beach Park, Oahu</h2>
                <p>Ulehawa Beach Park (<a href="../../oahu/beaches/more_ulehawabeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a long and narrow sandy beach bordering Farrington Highway on Oahu's west shore.The park area encompasses 58 acres. Near the middle of the beach, the Ulehawa Stream crosses the beach and flows into the ocean. There are two concrete walls on either side of it that extend into the ocean. A surf site named Channels is located here. When the surf is up, strong currents form along the shoreline, which make swimming hazardous. Next to where Ulehawa Stream enters the ocean (north of it) is the <a href="../ancientsites/ulehawahistoricsite.php">Ulehawa Historic Site</a>.</p>
