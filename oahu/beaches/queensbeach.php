                <h2 class="small">Queen's Surf Beach, Oahu</h2>
                <p>Named after Queen Liliuokalani because her beach house and its pier used to stand here, Queen's Surf Beach (<a href="../../oahu/beaches/more_queensbeach_photos.html" onclick="NewWindow(this.href,'name','650','650','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p24.html" onclick="NewWindow(this.href,'name','2550','750','yes');return false">view panorama</a>) is located right in Waikiki and is actually a section of <a href="../beaches/waikiki-beach.php">Waikiki Beach</a>.It is located between the Kapahulu Groin on its west end and the <a href="../../oahu/attractions/waikikiaquarium.php">Waikiki Aquarium</a> on its east end.</p>
                 	<? include ("../../theme/text.html") ?>		
                 <p> The Kapahulu Groin was built in 1951 and surfers named it The Wall, or in short Walls. Walls is also the name of the nearby surf break, which is reserved exclusively for bodyboarders (no surfboards are allowed within 150 yards of it and the area is marked by buoys). Another popular big-wave surf spot, named Cunha's, is located straight out from The Wall.</p>
                  
                <p>So besides being a bodyboarder's paradise, Queen's Surf Beach is also a great spot for snorkelers. There is a reef in front of the Waikiki Aquarium where snorkeling is good when the water is calm.</p>
                <p>In the park near The Wall is a bronze sculpture, which was dedicated by the City and County of Honolulu in 2003. Designed by artist Robert Pashby, It's called &ldquo;Surfer on a Wave&rdquo; and it is a monument to surfing in Waikiki. </p>
                <p>The Kapahulu Groin also marks the western boundary of the Waikiki Marine Life Conservation District, which goes all the way from the Groin east to the <a href="../../oahu/attractions/warmemorial.php">War Memorial Natatorium</a>. In this area, fishing and removing marine life is prohibited.</p>
                <p>If you like movies, come here on weekend evenings when free outdoor movies are shown on a big movie screen (you can't miss it, just look out for the large construction right on the beach). The event, known as <em>Sunset on the Beach</em>, includes music entertainment prior to the movie and food booths and begins at around 4 p.m. The movie begins around sunset, or around 7 p.m.</p>
                
