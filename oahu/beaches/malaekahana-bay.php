                <h2 class="small">Malaekahana Bay, Oahu</h2>
                <p>Malaekahana Bay (<a href="../../oahu/beaches/more_malaekahanabay_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p1.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>), located on the northeastern shore of Oahu between La'ie and Kahuku, has a long white-sand beach, which makes it perfect for beach walks, ball games and relaxation. It offers good bodyboarding and surfing conditions, especially in the winter when the surf is up. However, the currents can be strong, and there are no lifeguards on this beach. </p>
                	<? include ("../../theme/text.html") ?>		
                <p>Located just offshore on the south end of the bay  is the small Moku'auia Island, also known as <a href="../../oahu/attractions/goatisland.php">Goat Island</a>. If you are eager to explore it and want to be on a secluded island all by yourself, you can swim over, take a body- or surfboard with you or when it's low tide, simply walk over because the water is shallow enough. Just put some reef shoes on to prevent cuts on your feet. </p>
                <p>Goat Island is a real hidden paradise. It has a white, sandy beach and green sea turtles can be spotted in the area. In the center of the island is a seabird nesting area. This area is off limits to people.</p>
                
                <h3 class="big">Malaekahana Bay Overview</h3>
                <ul type="disc" class="mylist">
                  <li>Over a mile long white-sand beach - perfect for a long beach stroll</li>
                  <li>Goat Island located offshore</li>
                  <li>Sea turtles in the area</li>
                  <li>Two campgrounds are located on Malaekahana Bay, as well as a few vacation rentals (more info about camping is <a href="../../oahu/camping/">here</a>)</li>
                  <li>Two entrances to the bay - southern one (closer to La'ie) has more facilities - Malaekahana State Recreation Area (Look for the yellow gate that's open during the day, this road leads to the parking area and campground. Second entrance is at the Malaekahana State Recreation Area - Kahuku Section, where you can also camp or rent cabins)</li></ul>                <div style="clear:both"></div>
