                <h2 class="small">Ala Moana Beach Park, Oahu</h2>
                <p>Ala Moana Beach Park (<a href="../../oahu/beaches/more_alamoanabeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p15.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located between <a href="../../oahu/cities/waikiki.php">Waikiki</a> and downtown <a href="../../oahu/cities/honolulu.php">Honolulu</a>, is a favorite beach among local residents. Often times less crowded than <a href="../../oahu/beaches/waikiki-beach.php"> Waikiki Beach</a>, Ala Moana is a great place to swim, picnic, stroll on the beach or to relax.The beach is over half a mile long and the water is calm because it's protected by a shallow outer coral reef. </p>
                <p>There are hardly ever any waves, which makes Ala Moana Beach a favorite spot for long-distance swimmers, stand-up paddleboarders, novice swimmers and families with children. Recently installed  buoys in the water mark the area for stand-up paddleboarders (it's the area closest to the offshore reef). The area closer to the beach is for swimmers.</p>
                <p>Back in the 1920s, Ala Moana Beach Park didn't exist. It was a wetland that the city used as a garbage dump. A boat channel, which was to connect the Ala Wai Boat Harbor with Kewalo Basin was dredged through the coral reef. </p>
                <p>The beach itself is also man-made. It was created by the owner of the Dillingham Dredging Company, who was looking for a place to deposit his dredged earth. In the 1950s, the company dumped sand at the park, which created the beach. The dredged channel is 20 to 30 feet (6 to 9 m) deep. Today, boats no longer pass through it. </p>
                <p>The nearshore ocean bottom at Ala Moana Beach drops quickly, so novice swimmers should be cautious. To avoid stepping on the sharp corals, most people prefer the east end of the beach (the one that's closer to Diamond Head) where the ocean bottom is sandy and has no reef or rocks. The middle section and west end of the beach has rocks on the nearshore ocean bottom, which makes entering the ocean more tricky.</p>
                <p>The 100-acre park has large grassy areas, banyan and palm trees, restrooms, showers, concession stands, a music pavilion, tennis courts and plenty of picnic tables. Ala Moana Beach Park is a good place to go jogging, picnicking, playing games, sunbathing, fishing, swimming, surfing, stand-up paddleboarding and relaxing. Some of the south shore's best surf spots are located just offshore, such as Big Rights, Big Lefts, Concessions, Bomboras and Courts. Lifeguards are stationed on the beach daily.</p>
