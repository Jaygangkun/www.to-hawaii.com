                <h2 class="small">Wai'alae Beach Park, Oahu</h2>
                <p>Wai'alae Beach Park (<a href="../../oahu/beaches/p34.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>, <a href="../../oahu/beaches/more_waialaebeach_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is located right next to the Kahala Hotel &amp; Resort (and near the Wai'alae Country Club) in Kahala on Oahu's south shore, about 3 miles (4.8 km) east of Waikiki.The Wai'alae Stream flows right through the beach park and into the ocean, where its waters have formed a wide channel through the reef. The stream divides the park into two sections and a white stone bridge connects them. </p>		
                <p>Wai'alae's beach sand is interspersed with coral rubble from the reef offshore. The swimming conditions aren't the best here because of the shallow reef. At the east end of the beach is  a small man-made island, which was built in 1963 during the construction of the Kahala Hotel &amp; Resort. A surf site named Razors is located on the west side of the channel. The beach park is frequented by picnickers, surfers and windsurfers. There is a small parking lot that fills up quickly during weekends when local families come here to picnic.</p>
