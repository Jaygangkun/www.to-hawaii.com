                <h2 class="small">Punalu'u Beach Park, Oahu</h2>
                <p>Punalu'u Beach Park (<a href="../../oahu/beaches/p60.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>) on Oahu's windward coast has a narrow sandy beach. The nearshore ocean bottom is shallow and has a reef, so the swimming conditions are poor. Snorkeling is possible when the ocean is calm.The beach park is a popular fishing location, mainly for he'e (octopus). </p>
                <p>On the northern end of the park, Punalu'u Stream flows into the ocean. Large rocks were placed near the water's edge here to prevent beach erosion. Punalu'u means "to dive for coral" in the Hawaiian language. The beach park is located along Kamehameha Highway. <br />
                </p>
