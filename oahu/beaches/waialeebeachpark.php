                <h2 class="small">Waiale'e Beach Park, Oahu</h2>
                <p>This 26-acre beach park on Oahu's North Shore is easy to miss or forget it's there because most people head for the more popular and nearby Sunset Beach.<br />
                  <br />
                  Waiale'e Beach Park (<a href="../../oahu/beaches/more_waialeebeach_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/beaches/p12.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is mainly visited by fishermen and picnickers. When the tide is low and the ocean calm, another fun thing to do here is tidepooling. The nearshore ocean bottom is covered by rocks with sandy patches, forming small pools during low tide. The east (right) end of the beach has a rocky shelf, so entering the ocean in this area is difficult.</p>
                 	<? include ("../../theme/text.html") ?>		
                  <p>The beach park has a small parking lot that's accessible from the highway, but there isn't much else here in terms of facilities. The views are nice though. Offshore lies a small limestone island called Kuka'imanini, meaning &ldquo;manini fish procession&rdquo; in the Hawaiian language, and the name of the beach park &ndash; Waiale'e &ndash; means &ldquo;rippling water.&rdquo; <br />
                  <br />
                  The park is backed by ironwood trees, which provide some shade. A surf spot called Bong's is located offshore. </p>
                
