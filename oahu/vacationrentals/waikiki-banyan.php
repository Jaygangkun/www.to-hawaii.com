   <p>Formerly the ResortQuest at the  Waikiki Banyan, this hotel has since been taken on by the Aston  group, hence the change of name.  Unfortunately the name change hasn't led to an update in furnishings or general  d&eacute;cor. Many of the rooms appear rather dated. However, what they do offer is  space in large one-bedroom condominium suites, making the  Waikiki Banyan a good choice for families. All rooms have  fully  equipped kitchens and lanais.  The hotel  offers a personal welcome to all children. This  includes a 'sand pai' gift pack filled with surprises. The family-friendly  nature of this hotel is further enhanced by the sixth-floor recreation deck.  Here guests will find a heated pool with two hot tubs and a sauna.  Of greater interest to  many families however will be the playground, barbecues, picnic area and snack  shop on offer in the same area as the pool. And for the more active, tennis and  basketball courts and a putting green are also located on the sixth floor.  Though the hotel  doesn't have an in-house restaurant, there is a coffee bar located in the lobby. Here guests find a variety of breakfast options as well as sandwiches,  wraps and salads for a lighter lunch or dinner. </p>
