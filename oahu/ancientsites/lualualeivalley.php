                <h2 class="small">Lualualei Valley, Oahu</h2>
                <p>This is the largest coastal valley on Oahu's leeward (west) coast. It is backed by the Wai'anae Mountain Range, which is one of two mountain ranges on Oahu (the Ko'olau Mountain Range is the other one). Both mountain ranges were formed by volcanic activity and the older one is the Wai'anae.</p>
                <p>The oldest lava at Lualualei dates back 3.9 million years. During the volcano's active years, its center of activity was right here, at Lualualei Valley. This is where Oahu originated.</p>
                <p>Inside the valley are a few government communication stations, as well as the U.S. Navy's weapons storage facility for Pearl Harbor. The small town of Ma'ili is located at the base of the valley. </p>
