                <h2 class="small">Kukaniloko Birthing Stones, Oahu</h2>
                <p>This ancient sacred site (<a href="../../oahu/ancientsites/p3.html" onclick="NewWindow(this.href,'name','2550','400','yes');return false">view panorama</a>) is located in the center of Oahu near Wahiawa. It was here where royal women were taken to give birth in the old Hawaii. Kukaniloko, which means &quot;to anchor the cry from within,&quot; was built in the 12th century by an Oahu chief whose son Kapawa was the first birth here. </p>
                <? include ("../../theme/text.html") ?> 
                <p>The ali'i (royals) gave birth here because it assured them high-ranking status. It was also believed that women who gave birth here suffered no pain during childbirth. They had to follow a strict diet and exercises in the weeks prior to giving birth that was recommended by the kahuna (priests). </p>
                
