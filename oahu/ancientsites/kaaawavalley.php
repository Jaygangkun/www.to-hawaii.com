                <h2 class="small">Ka'a'awa Valley, Oahu</h2>
                <p>Ka'a'awa Valley is one of the most beautiful locations on the island of Oahu. It is also a site that is mentioned in many ancient legends and a place where the <a href="../../legends/night-marchers.php">nightmarchers</a> are believed to roam on full moon nights. </p>
                <p>Located on Oahu's windward coast, Ka'a'awa Valley (also known as Kualoa Valley), was the residence of kings. In ancient Hawaii, newborns of the ali'i (Hawaiian chiefs) were brought here and trained in ancient traditions, history and the arts of war. As fishermen passed this valley, they had to lower their sails. If they broke this kapu (law), it meant death. </p>
                <? include ("../../theme/text.html") ?> 
                <p>The entire Kualoa are was considered very sacred and a place of refuge, a pu'uhonua, was located here as well. If a commoner broke a kapu, it usually meant death. But if he made it to a pu'uhonua, kahunas (priests) would pray for him and he could be forgiven. A pu'uhonua was also a place to rest for defeated warriors. It was basically a safe haven.</p>
                <p>The island&rsquo;s most famous burial cave for chiefs, Pohukaina, is located in the mountains above the valley. It is believed that ancient treasures have been stored deep in this cave in secret caverns.</p>
                <p>The valley is backed by the Ko'olau Mountains. The highest peak of the Kualoa ridge rises to 1,900 feet. It is called Kanehoalani, meaning &ldquo;Kane's heavenly companion.&rdquo; Kane was the god of procreation. He was one of the leading gods and was worshipped by the Hawaiians. </p>
                <p>There are many modern accounts of local residents who claim to have heard or seen nightmarchers in the valley, especially on full moon nights. People have reported hearing drums and seeing warriors and chiefs in ghostly processions marching from their burial grounds in the mountains to the sea.</p>
                <p>Between 1863 and 1870, the Kualoa Sugar Mill was built and operated here, but it wasn't successful because of a lack of rainfall, so it was closed. During World War II, the U.S. military had an airfield at Kualoa and built bunkers in the cliffs above the ocean. In 1974, this ahupua'a (land division) was listed on the National Register of Historic Places as the Kualoa Ahupua'a Historical District.</p>
                <p>Kaaawa Valley is part of <a href="../attractions/kualoaranch.php">Kualoa Ranch</a>, a 4,000-acre working cattle ranch that was established in 1850. In addition to raising cattle, the ranch offers a variety of activities for visitors, including horseback rides, ATV tours and guided tours of the valley and <a href="../ancientsites/moliifishpond.php">Moli'i fishpond</a>.</p>
                <p>The valley is beautiful and if it looks familiar, that&rsquo;s because it has been the filming location of many popular Hollywood movies and television shows, such as <em>Jurassic Park</em>, <em>Godzilla</em>, <em>Pearl Harbor</em>, <em>50 First Dates</em>, <em>Windtalkers</em>, <em>LOST</em>, <em>Hawaii Five-O</em>, <em>Magnum P.I.</em> and many others. </p>
                
