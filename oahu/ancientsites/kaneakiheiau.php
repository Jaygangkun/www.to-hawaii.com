                <h2 class="small">Kane'aki Heiau, Oahu</h2>
                <p>Please note: Kane'aki Heiau is currently closed until further notice.<br />
                  <br />
                Built in the 16th century, Kane'aki Heiau is considered the best preserved heiau on Oahu. It is located on the island's leeward (west) side in Makaha Valley. One reason why the heiau is in such a good shape is because it has been completely restored. It was reconstructed with traditional ohia tree logs and <em>pili</em>, a bunchgrass, and includes an altar, god images, two prayer towers, a drum house and a taboo house.</p>
                <? include ("../../theme/text.html") ?> 
                <p>The heiau is dedicated to Lono, the god of agriculture and fertility. A large stone at the heiau is &ldquo;Pohaku o Kane&rdquo; (Stone of Kane). Kane is one of the major gods and is regarded as the guardian over the heiau up until today. King Kamehameha used to worship here and the heiau was in use until his death in 1812.                </p>
                
