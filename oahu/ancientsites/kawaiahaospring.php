                <h2 class="small">Ka Wai a Ha'o, Oahu</h2>
                <p>This sacred spring is located in downtown Honolulu on the grounds of <a href="../attractions/kawaiahaochurch.php">Kawaiahao Church</a>. This is where in the old Hawaii, a spring was located that was reserved for ali'i (high chiefs and chiefesses). It was kapu (taboo) for a commoner to bathe in it. One of these ali'i who regularly came here for ceremonial bathing was chiefess Ha'o. The spring is named after her, Ka Wai a Ha'o, meaning &ldquo;the water of Ha'o.&rdquo;</p>
                <? include ("../../theme/text.html") ?> 
                <p>The first missionaries who settled in this area arrived in 1820. The ali'i granted them land near the sacred spring, on which the missionaries built homes. Up until today, some of these homes can be visited at the nearby <a href="../museums/missionhousesmuseum.php">Mission Houses Museum</a>.</p>
                <p>Kawaiahao Church, which is located here as well, was built between 1836 and 1842. Back then it was known as the &ldquo;Great Stone Church&rdquo; even though it is not built of stone, but coral blocks that were chiseled by hand out of an Oahu south shore reef. </p>
                <p>The actual spring of Ka Wai a Ha'o doesn't exist anymore in its natural state. It used to be located closer to the area where today South King Street and Kapiolani Boulevard intersect. The spring that can be seen today on the church grounds is man-made, built in 1926, but it does feature one of the original stones from the ancient spring of Ka Wai a Ha'o. </p>
                
