                <h2 class="small">Nu'upia Ponds, Oahu</h2>
                <p>The Nu'upia Ponds are located on the Mokapu Peninsula on Oahu's southwestern shore. Many ancient legends tell of this peninsula and the ponds. One of the legends says that it was here where the first man was created  out of the red and bluish-black soil by the Hawaiian gods Kane, Kaneloa, Ku and Lono. In the Hawaiian language, mokapu means &ldquo;sacred district.&rdquo; Many old Hawaiian sand burial sites have been excavated here.</p>
                                <p>Another legend tells of a boy named Puniakai'a (&ldquo;devoted to fish&rdquo;) who often times went fishing for parrotfish here. One of the fish he caught and tamed was Uhumaka'ika'i, meaning &ldquo;the parent of all fish.&rdquo; This fish was home here at the Nu'upia fishpond and it became the boy's companion. </p>
                                <? include ("../../theme/text.html") ?> 
                <p>In the old Hawaii, there were three ponds here that separated the peninsula from the island. They were Nu'upia, Halekou and Kaluapuhi. Some of their old dividing walls still remain their shape, but today, there are eight ponds: Nu'upia Ekahi, Nu'upia Elua, Nu'upia Ekolu, Nu'upia Eha, Halekou, Heleloa, Pa'akai and Kaluapuhi.</p>
                <p>The three original ponds are believed to date back to between 1300-1600 A.D. The ponds were later subdivided by Chinese fishermen who leased the ponds to raise mullet and milkfish here. Today, the ponds are part of the 482-acre Nu'upia Ponds Wildlife Management Area. Since the ponds are located within the Kaneohe Marine Corps Air Station, one has to get permission first to access the site.</p>
                
