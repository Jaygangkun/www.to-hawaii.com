                <h2 class="small">Pohaku Pa'akiki, Oahu</h2>
                <p>Pohaku Pa'akiki is a lava rock that lies in the nearshore waters of <a href="../beaches/kaupobeachpark.php">Kaupo Beach</a> on Oahu's windward coast. The rock looks ordinary and just like any other rock on the beach. But there is an ancient legend about this one that's quite interesting. In the old Hawaii this rock was sacred to sweet potato farmers in this area. Every day they offered &lsquo;awa root to the shark god named Kamohoali'i. </p>
                <? include ("../../theme/text.html") ?> 
                <p>A fishermen one day tried to provoke the farmers and threw shark tails into the ocean near this rock, angering the shark god who then one day decided to eat the fisherman. The shark god first ate the fisherman's feet, but couldn't eat his buttocks because of their smell. The shark god after that pledged to never eat human flesh again. He also didn't allow any other sharks to attack humans between Waimanalo and Makapu'u. </p>
                
