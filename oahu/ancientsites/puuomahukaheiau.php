                <h2 class="small">Puu o Mahuka Heiau, Oahu</h2>
                <p>Go to this ancient Hawaiian heiau (religious temple site) around sundown to feel the &ldquo;mana&rdquo; (sacred spirit) and a mysterious energy that will give you goose bumps all over. The Puu O Mahuka Heiau (<a href="../../oahu/ancientsites/more_puuomahukaheiau_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>, <a href="../../oahu/ancientsites/p4.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>) is the largest heiau on Oahu, covering almost two acres. It is a national historic landmark, its name meaning &ldquo;hill of escape.&rdquo; The heiau may have been constructed as early as the 1600s. It is situated on a mountain, 300 feet high, and overlooks <a href="../../oahu/beaches/waimea-bay-beach-park.php">Waimea Bay</a> on Oahu&rsquo;s North Shore. </p>
                <? include ("../../theme/text.html") ?> 
                <p>The heiau is well-preserved and consists of three adjoining enclosures measuring 575 feet by 170 feet, with an altar in front of it that is often covered by flowers and fruits left there as offerings by Native Hawaiians and local residents. Most tourists don&rsquo;t know about this place as many maps don&rsquo;t show it. The Puu O Mahuka Heiau was considered a sacred and powerful place for the kahuna (priests) and one of two places where the wives of the ancient chiefs gave birth. But the heiau was also a site of human sacrifice. In the 1770s, a time of political upheaval, the high priest Kaopulupulu supervised this heiau. He predicted that Oahu would be overrun by strangers from a faraway land. </p>
                
                <p>Captain George Vancouver anchored his ship, the Daedalus, off Waimea in 1792 and sent some people ashore to collect water. A conflict with the Hawaiians arose and three of Vancouver&rsquo;s men were killed. Some say that they were sacrificed here at this heiau.</p>
                <p>In 1795, when King Kamehameha I conquered Oahu, his high priest Hewahewa carried out religious ceremonies here, until the traditional religion was abolished in 1819. After that the heiau may have been used for other purposes. Some say that the middle enclosure was used for agriculture. Pineapple was grown around the heiau until the 1960s.</p>
                <p>In 1962, the Puu O Mahuka Heiau was declared a National Historic Landmark in recognition of its importance to the Hawaiian history and culture. In the same year, the four-acre property surrounding the heiau was placed under the jurisdiction of State Parks to preserve this site for future generations. A path through the heiau was created around this time.</p>
                <p>If you plan to come here for a visit, please don&rsquo;t climb over the walls to get to the center of the heiau as this may damage the walls and paving. Also, this is a sacred place that should only be viewed from behind the walls. The park is open daily from 7 a.m. to 6:45 p.m. There is no entrance fee.</p>
                
