                <h2 class="small">Mokauea Island, Oahu</h2>
                <p>Located next to the Honolulu International Airport and less than half a mile from downtown Honolulu, this tiny island is home to Oahu's last Hawaiian fishing village. Mokauea is one of the only two traditional fishing villages left in the entire state of Hawaii (the other one is Miloli'i on the Big Island of Hawaii).</p><? include ("../../theme/text.html") ?> 
                <p>The island of Oahu is the most populous in the Hawaiian archipelago and by far the most developed. So it is quite amazing that right on the doorstep of Hawaii's largest metropolis, this tiny fishing village survived and managed to retain old Hawaii. Many Oahu residents have never heard of it. </p>
                <p>In ancient Hawaii, fishing villages were wide-spread on all islands. Up until 1975, 14 fishing families lived permanently on Mokauea Island, which had its own aquaculture fishpond and was completely self-sufficient. The families even grew produce such as taro, tomatoes and eggplant on this tiny spit of land, which encompasses a mere 10 acres. Today, only five fishing families remain on the island.</p>
                
                <p>For the island residents, going back and forth from Mokauea to Oahu to get supplies is a daily trip between the past and the present. Mokauea islanders use propane tanks for cooking and have generators for electricity. Going shopping means hopping on their boats and going over to Oahu. Mokauea has an old fishpond that was a major food source in the past. But it needs restoration work before it can be used again. </p>
                <p>In the 1970s, this ancient fishing village was almost destroyed. The government evicted the families on Mokauea, arrested some of them for trespassing and burned down five homes. The fishermen, with the help of the non-profit organization Save our Surf, organized the Mokauea Fishermen's Association. <br />
                  <br />
                  Subsequently, the state conducted a study and later agreed to lease the land to the families and let them live there after it was determined that Mokauea was an important historical location. Up until today, the mission of the Mokauea islanders is to revive their little fishing village and continue to preserve it.</p>
                <p>Ho'ola Mokauea, a local non-profit organization, is giving them a helping hand. The organization's mission is to preserve this historic fishing village and to protect the island, mainly from marine debris and invasive species. Their goal is to recreate a living example of a Hawaiian subsistence fishing village. </p>
                <p>Ho'ola Mokauea is entirely run by volunteers. Many local high school students and others volunteers regularly visit Mokauea to help with the restoration efforts, which include fishpond repair, removal of marine debris and invasive species, and reintroduction of fish and native limu (seaweed). The island is visible from Oahu's shoreline (from Sand Island, which is accessible via Sand Island Access Road). </p>
                <p>To become a volunteer and help with the restoration efforts, contact Ho'ola Mokauea at Mokauea@gmail.com. </p>
                
