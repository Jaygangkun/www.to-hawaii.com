                <h2 class="small">Mauna Lahilahi, Oahu</h2>
                <p>Mauna Lahilahi is a prominent geographic feature on Oahu's leeward (west) coast, located just south of Makaha on an 11,145-acre peninsula. In the Hawaiian language, lahilahi means &ldquo;thin mountain.&rdquo; It is believed that this mountain was sacred to Kane, the Hawaiian god of procreation and the father of all living creatures. He is one of the four leading gods for the Hawaiians (the other three are Ku, Lono and Kanaloa). It is also believed that another god named Ai'ai marked the area around Mauna Lahilahi as a prime fishing location.</p>
                <? include ("../../theme/text.html") ?> 
                <p>Several petroglyphs have been carved at the mountain's eastern end, mainly human and dog figures. The mountain is also home to a few small stone platforms, shrines and a possible heiau (ancient temple) site, as well as burial sites.</p>
                <p>In the old Hawaii, Mauna Lahilahi served as a lookout point. Its 230-foot (70 m) summit gives a good overview of the western shoreline and any approaching boats. It is also believed that the mountain&rsquo;s peak has astronomical significance, mainly in recognition of the summer solstice.</p>
                <p>Located just offshore is Shark Island, a large rock, said to be the mother of the surrounding smaller rocks, which are the children. </p>
                
