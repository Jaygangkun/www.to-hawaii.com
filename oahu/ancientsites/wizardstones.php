                <h2 class="small">Wizard Stones, Oahu</h2>
                <p>This ancient site is located in the heart of Waikiki, right on the beach near the Duke Kahanamoku Statue. If these four giant stones could talk, they would have an interesting story to tell. Also called The Stones of Life or Na Pohaku Ola Kapaemahu a Kapuni, the Wizard Stones are said to contain the mana (power) of four wizards who came to Oahu from Tahiti a long time ago, possibly around 400 A.D. </p>
                <p>According to this ancient legend, the four wizards named Kahaloa, Kinohi, Kapuni and Kapaemahu possessed healing powers. They settled in Waikiki near where today the Moana Surfrider hotel is located. They helped the island residents by relieving their aches and pains and word about these healers spread quickly on the island. </p><? include ("../../theme/text.html") ?> 
                
                <p>The wizards told the people that they didn't intend to stay on the island. Their intention was to heal, teach and share their knowledge before returning home. Before they left, they gave the people instructions to build a monument in memory of their visit. </p>
                <p>On a full moon night thousands of islanders met on the hill behind the neighborhood of Kaimuki (about two miles away from Waikiki Beach) to move four large stones, each weighing a ton or more, to the place where the wizards lived, as they had been instructed. </p>
                <p>Before the wizards left Hawaii, they told the people that they would endow these stones with their healing powers. Today, these four stones are still there for people to see. They have been moved onto a platform and fenced in to protect and preserve them. Many visitors come to see the Wizards in Waikiki each day and bring a flower lei to honor these healing stones. </p>
                
