                <h2 class="small">Moli'i  and 'Apua Fishponds, Oahu</h2>
                <p>Moli'i Fishpond is one of the largest fishponds in all of Hawaii, encompassing 125 acres and ranging in depth from 4 to 30 feet (1-9m). Located on Oahu's windward (east) coast, the fishpond is  between 600 to 960 years old. </p>
                <p>In 1850, Dr. Gerritt P. Judd bought the land division of Kualoa, which includes the fishpond, from King Kamehameha III. Today, the Morgan family, who are Judd's descendents, still own the property and manage it as <a href="../attractions/kualoaranch.php">Kualoa Ranch</a>. Moli'i Fishpond is one of the few early royal fishponds in Hawaii that remain operational up until today. Moi (threadfish), 'ama'ama (mullet) and awa (milkfish) are raised in it.</p>
                <? include ("../../theme/text.html") ?> 
                <p>To see the fishpond from up-close, you can take a guided tour. The Ranch offers a garden and fishpond tour, as well as an ocean voyaging tour, where they ferry visitors across the fishpond to Secret Island (the seaward side of Moli'i Fishpond).</p>
                <p>Neighboring &lsquo;Apua Fishpond (<a href="../../oahu/ancientsites/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) (located to the east of Moli'i) is much smaller. It can easily be reached from Kualoa Regional Park by following the beach park road all the way to the end. The fishpond is located behind a low stone wall and surrounded by bushes and trees. &lsquo;Apua Fishpond fell into disuse in the early 20th century. </p>
                
                <h2 class="big">Moli'i <span class="small">and 'Apua Fishponds</span> Overview</h2>
