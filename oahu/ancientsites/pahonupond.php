                <h2 class="small">Pahonu Pond, Oahu</h2>
                <p>Located just south of <a href="../beaches/kaionabeachpark.php">Kaiona Beach Park</a>, Pahonu Pond is an ancient Hawaiian stone enclosure surrounded a turtle pond where fishermen put turtles that were to be consumed by the ali'i (Hawaiian chiefs and royalty). In the old Hawaii, only ali'i were allowed to eat turtle meat. It was kapu (taboo) for a commoner to eat this delicacy, and breaking this kapu was punishable by death.</p>
                <p>The pond's original size is believed to have measured 500 feet (152 m) in length and 60 feet (18 m) in width. The rocks are partially submerged at high tide. The offshore island that one can see from here is <a href="../attractions/rabbitisland.php">Rabbit Island</a> (also known as Manana Island). The island was used as a burial site in the past and two fishing shrines were located there as well. Today, it is a seabird sanctuary.</p>
