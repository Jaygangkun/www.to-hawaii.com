                <h2 class="small">Ulupo Heiau, Oahu</h2>
                <p>The Ulupo Heiau on Oahu is a large Hawaiian heiau, or temple site, located in Kailua, right next to the Kawainui Regional Park and off of Kailua Road. Its walls are up to 30 feet in height and it measures 140 by 180 feet. Some Hawaiian heiaus were used as a place for sacrifice, others to celebrate a good harvest or the birth of alii. It is assumed that the function of the Ulupo Heiau changed over time. It probably was an agricultural heiau in the beginning and in later times it may have become a heiau dedicated to success in war.</p>
                <? include ("../../theme/text.html") ?> 
                <p>The heiau is quite massive and required a large workforce, which is a hint that it must have been culturally important. The large individual rocks all had to be carried here. One stone is recorded to have been brought to the heiau all the way from Kualoa, which is more than 10 miles away. Today, the Ulupo Heiau is listed on the National and Hawaii Registers of Historic Places. It remains a sacred site for the Hawaiian people.</p>
                <p>If you come here, you&rsquo;ll enjoy nice views of Kailua Bay and the Kawai Nui Swamp. Many fruit trees grow at the heiau, such as bananas and noni. Taro is also being grown here. Show respect when you visit the heiau and stay on the trails. </p>
                
