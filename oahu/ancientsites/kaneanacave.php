                <h2 class="small">Kaneana Cave (Makua Cave), Oahu</h2>
                <p>Kaneana Cave is a mysterious site on Oahu's leeward (west) coast, almost as far away as you can get on the island near where the road ends. It sits at the base of a cliff outcropping and is about 100 feet (30 m) high and 450 feet (137 m) deep. The cave is estimated to be around 150,000 years old and was once underwater, carved out by the waves of the ocean. </p>
                <? include ("../../theme/text.html") ?> 
                <p>There are many ancient legends that tell about the cave's history. One legend tells that it was here where mankind was born, from the womb of the earth goddess. That's why the cave is called Kaneana (Cave of Kane), named after Kane, the god of creation.</p>
                <p>Another legend tells of the shapeshifter Kamohoali'i, who could transform himself from a shark into a man. He and his human wife had a son, Nanaue. When Nanaue discovered his taste for meat, just like all man-eating sharks, from then on he always had an appetite for human flesh. So whenever he was hungry, he disguised himself as a human to trick his victims. He then dragged them into the cave and ate them. But when residents later found out about Nanaue's true identity, that he was a shark man, they captured and killed him.</p>
                <p>The interior of the cave is dark and sometimes slippery. From the main cavern, a few smaller tunnels branch off to narrower areas. Stay away from these tunnels because they are not safe.</p>
                
