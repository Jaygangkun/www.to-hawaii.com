                <h2 class="small">Ku'ilioloa Heiau, Oahu</h2>
                <p>Located on Kane'ilio Point, a small peninsula in Waianae, the Ku'ilioloa Heiau commands a beautiful view of Oahu's west shore. To the north is <a href="../beaches/pokaibeachpark.php">Pokai Bay</a> and Lahilahi Point in the distance, and to the south are the <a href="../beaches/lualualeibeachpark.php">Lualualei</a> and <a href="../beaches/mailibeachpark.php">Ma'ili</a> beach parks. </p>
                <p>The heiau has three terraced platforms and is believed to have been a temple of learning and training, mainly in the arts of fishing, navigation and other ocean-related skills. </p>
                <p>In the Hawaiian language, Ku'ilioloa means &ldquo;the long dog Ku.&rdquo; It is believed that the heiau was named after a legendary dog who protected travelers.</p>
