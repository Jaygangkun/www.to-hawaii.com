                <h2 class="small">Pele's Chair (Kapaliokamoa), Oahu</h2>
                <p>Pele is Hawaii's volcano goddess. Located on Oahu's south shore is an interesting lava rock formation that overlooks the ocean. In the old Hawaii, this rock was called Kapaliokamoa, meaning &ldquo;the cliff of the chicken.&rdquo; When looking closely at this rock from a certain perspective, it looks a bit like a squatting chicken, hence this name. However, from another perspective it more looks like a chair and today it is commonly known as Pele's chair. </p>
                <? include ("../../theme/text.html") ?> 
                <p>Pele created Oahu and the other Hawaiian islands by her volcanic activity, and it is believed that she was here right before she left Oahu to continue her work on the other islands. After she was finished on Oahu, she left her chair to head to Molokai and Lanai to continue there. The area where Pele's chair is located is indeed the youngest on Oahu.</p>
                <p>The lava formation can be seen from the road to Makapuu or from the Hawaii Kai Golf Club. From afar it looks like a giant chair, but as one gets closer to it, it loses this appearance.</p>
                
