                <h2 class="small">Hale O Lono Heiau, Oahu</h2>
                <p>Located in Waimea Valley on Oahu&rsquo;s North Shore, the Hale o Lono Heiau is dedicated to Lono, the God of agriculture. The heiau was built between 1470 A.D. and 1700 A.D.</p>
                <p>&ldquo;Hale o Lono&rdquo; is Hawaiian and means &ldquo;House of Lono.&rdquo; Lono is one of the four main gods in ancient Hawaii. The other ones are Ku, Kane and Kanaloa. </p>
                <p>Human sacrifices didn&rsquo;t happen here regularly. However, sometimes fish and human offerings were made to ensure a good harvest and fishing season in the coming year.</p>
