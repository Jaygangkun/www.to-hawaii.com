                <h2 class="small">Hawaii Maritime Center, Oahu</h2>
                <p><strong>Note: The Hawaii Maritime Center was closed in 2009 due to economic constraints. It is unknown when or if it will re-open.</strong></p>
                <p>Hawaii's rich maritime history comes to life at this fascinating museum located at Honolulu Harbor's Pier 7. Located inside a replica of His Majesty King David Kalakaua's own canoe house, the two-level Hawaii Maritime Center houses an extensive collection of artifacts, old photos, models and more. Handy tape machines guide visitors through 30 exhibits covering everything from ancient Polynesian mariners through Captain Cook's arrival and the now defunct whaling industry. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  One of the big draws here is the museum's enormous Pacific humpback whale skeleton. The animal's carcass washed up on the shores of Kaho'olawe Island in 1986, and the 159-bone skeleton was later restored and put on display. The whale was named Lei'iwi, which means &quot;Lei of Cherished Bones&quot; in the Hawaiian language. </p>
                <p>                  Admission to the Hawaii Maritime Center used to include the chance to step inside the Falls of Clyde, an impressive full-rigged, four-masted schooner that dates back to 1878. The ship is parked in a prominent location at Honolulu Harbor, but it is badly deteriorated. In 2008, the nonprofit organization Friends of Falls of Clyde became the ship's new owner and they plan to restore the vessel. </p>
                <p>From 1899 to 1907, the Falls of Clyde transported sugar from Hawaii to the West Coast and general merchandise from the West Coast to Hawaii. Paying passengers could also be accommodated. In 1907, the ship was  converted into a bulk oil tanker with a capacity of 19,000 barrels (3,000 m&sup3;). It was then used to transport kerosene to Hawaii and molasses to the West Coast. Today, the Falls of Clyde is the only remaining sail-driven oil tanker. It became a National Historic Landmark in 1989. </p>
                <p><strong>Hours:</strong><br />
                  Currently closed.</p>
                
