                <h2 class="small">Pacific Aviation Museum, Oahu</h2>
                <p>On December 7th, 1941, the Japanese Imperial Navy did a surprise attack on the United States Naval base at Pearl Harbor during World War II. Since then the date and event have remained marked U.S. history. At the Pacific Aviation Museum (<a href="../../oahu/museums/more_pacificaviationmuseum_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on Ford Island, Oahu, visitors can experience first-hand the historic landmark, as well as take a trip back in time to Pearl Harbor in the 1940s.</p>
                <p>The tour of the museum starts at Hangar 37, a former seaplane hangar that survived the attack on Pearl Harbor. The 42,000 square foot hangar is the departure point for Ford Island via shuttles where visitors are then welcomed into the Pacific Aviation Museum itself.</p>
                <? include ("../../theme/text.html") ?>
                <p>Upon arrival at the museum, guests are welcome to view a short film in the museum's 200-seat theater featuring historic footage of the attack on the harbor. Through the corridor after the film, photos of the pre-1941 island give viewers a glimpse into the past.</p>
                <p>Authentic exhibits, such as the Japanese Zero, set in a diorama on the deck of a Japanese carrier named Hiryu, can be viewed in the hangar, which also contains a civilian plane &mdash; shot while airborne during the Pearl Harbor attack. Types of fighter planes like the P-40 can also be enjoyed by history buffs and aviation enthusiasts while taking in the exhibit, the story and the historic significance of the site in the 25,000 square foot exhibit area.</p>
                <p>Many other aircrafts, including a B-25 Mitchell used in the Doolittle Raid on Japan, or an SBD Dauntless dive bomber, are other interesting exhibits to see at the Pacific Aviation Museum.</p>
                <p>New to the museum, the Mig Alley Korean War exhibit displays a recently acquired MiG-15 and a restored F-86 Sabre aircraft, both of which were flown during the Korean War. Airplane lovers of all ages will be able to view, learn about, and enjoy the aircrafts used throughout the United States' history.</p>
                <p>Ready for take-off? While many of the exhibits are exciting to view, visitors also have the opportunity to sit in the pilot's seat and take part in the museum's interactive Combat Flight Simulator. Visitors can &quot;fly&quot; the planes for a unique experience off the runway. <br />
                  <br />
                  After exploring the museum, visitors can sit down for a cup of coffee at the museum's own Laniakea Caf&eacute; or visit the gift shop to take a memory of historic Pearl Harbor home. </p>
