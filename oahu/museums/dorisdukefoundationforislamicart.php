                <h2 class="small">Shangri La, Oahu</h2>
                <p>The Doris Duke Foundation for Islamic Art (Shangri La), the artistic Honolulu home of philanthropist Doris Duke, is a spectacular montage of Islamic artistic and architectural legacy.  Against the backdrop of the breathtaking Pacific Ocean, Shangri La serves as a significant hub for studying, preserving and appreciating Islamic culture and art. </p>
                <? include ("../../theme/text.html") ?>
                <p>The seed for this unique museum was sown in 1935 when Doris Duke was enamored by the beauty of Hawaii and decided to build a holiday habitat in Honolulu. The American philanthropist who made a trip to several Muslim nations on her honeymoon then decided to merge the artistic genius of Islamic culture with the natural splendor of Hawaii. The result was a fiercely private and sung architectural masterpiece designed by collaborating with famed architect Marion Sims Wyeth.</p>
                
                <p>The structure was primarily constructed as an artistic haven to support Doris Duke's unflinching quest for aesthetic expression and preservation. Shangri La subsequently acquired an individualist character silhouetted by Islamic artistic sensibilities flourishing in a serene environment.  Doris Duke's consistent endeavor to source rare artifacts from Islamic regions continued till her demise in the early 1990s, and today her brainchild houses about 3,500 pieces of objet d'art. The meticulously organized artistic estate features everything from elaborate mosaic tiles and vibrant textiles to elegantly carved doors and intricately designed ceilings.</p>
                <p>Encircled by dramatic views of Diamond Head, the Doris Duke Foundation for Islamic Art displays an assortment of art and architecture that has made its way to Honolulu from Turkey, Morocco, Iran and other Islamic artistic hotbeds. The sprawling artistic edifice overlooks a picturesque landscape comprising lush gardens, fountains and the ocean. </p>
                <p>Visitors can explore the museum's Mughal garden, the quaint courtyard and Mihrab library along with the grand living room, dining room and Turkish rooms. There are several other corners brimming with art pieces dating back to the 18th century.</p>
                <p>The museum hosts regular cultural programs and guided small group tours (by reservation only) to orient visitors to the main art house along with a section of the ground and adjoining garden. There are special events (organized through the Doris Duke Foundation for Islamic Art grant) that feature discussions, lectures, exhibitions and documentaries on the finer nuances of Islamic art. </p>
                <p>Since the house is constructed on multiple levels, visitors should be ready for a climb up several stairs. Wheelchair tours are only made available by special arrangement. </p>
                <p><strong>Hours:</strong><br />
                  Tours start Wednesday through Saturday at 9 am, 10:30 am and 1:30 pm. Prior reservation is required. </p>
                <p><strong>Admission:</strong><br />
                  General Admission: $25 <br />
                  Kama'aina (Hawaii residents): $20 (ID required)<br />
                  Children under the age of 8 are not permitted on the tour. <br />
                  There are no discounts on admission for senior citizens, students, military  and members of the Honolulu Museum of Art.<br />
                  The price includes admission to the Honolulu Museum of Art.<br />
                  To book a tour, call <span itemprop="telephone">(808) 532-3853</span>.</p>
                <p style="color:#C00"><strong>Important note:  All Shangri La tours originate at the Honolulu Museum of Art (see map below). Visitors first tour the Arts of the Islamic World Gallery at the <a href="../museums/honoluluacademyofarts.php">Honolulu Museum of Art</a> and then watch a ten-minute introductory video on the history of Shangri La. Visitors are then transported in small groups by shuttle bus to Shangri La for a tour which lasts about 1.5 hours. The entire tour experience lasts about 2.5 hours. Visitors who go directly to Shangri La on their own will not be permitted on the property.</strong> <br />
                </p>
                
