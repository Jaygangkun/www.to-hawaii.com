                <h2 class="small">The ARTS at Marks Garage, Oahu</h2>
                <p>With rooms showcasing visual arts and new and exciting performance arts in the adjacent rooms, The ARTS at Marks Garage has long been the geographical and spiritual center of downtown Honolulu's revived arts scene. These days flanked by many smaller galleries, Marks was the first to arrive in the area. Founded in 2001 with the aim of establishing an incubator arts program, Marks aims to demonstrate the central place culture and the arts can play in enhancing the vitality and quality of life within a community. Indeed, as a part of Hawaii Arts Alliance, this neighborhood based arts center has been a key part in the growth of Honolulu's Culture &amp; Arts district, all the while remaining economically viable, and thus highlighting the resurgence of downtown Honolulu.</p>
                <? include ("../../theme/text.html") ?>
                <p>                  Fast forward to the present day and visitors to Marks can tour the permanent exhibition space free of charge, whilst office space is available within the site for those wishing to place their business or non-profit organization within creative surroundings. Other rooms on the site offer a wide variety of often changing entertainment and cultural exhibits provided by various occupant partners; from local craftsmen to performing arts groups. In total, the gallery offers somewhere in the region of twelve exhibits and thirty or so performances or workshops each calendar year.</p>
                
                <p>                  Perhaps though, the most famous and popular element of the space's cultural program is &lsquo;First Friday.&rsquo; Incorporating not only The ARTS at Marks Garage, but many of the newer galleries which have followed in its wake in the area, this free, monthly, self-guided gallery tour attracts a multitude of observers who flock to see the rotating exhibitions, entertainment and, of course, the obligatory refreshments! An evening to celebrate art and artists, the event creates a real buzz on the streets of downtown Honolulu, between 5 pm and 9 pm on the first Friday of every month. </p>
                <p>                  A more recent addition to the calendar, once more taking advantage of that Friday feeling, is Slow Art Friday, which runs on the third Friday of each month. This street based Arts fair runs between Marks and the nearby Hawaii Theatre, along Pauahi Street. The evening offers up a fine selection of works from artists and performers of all disciplines and comes complete with culinary delights such as organic food and wine which adorn the huge 35-foot table, ideally situated in the street to allow you to enjoy the performances taking place around you. This cosmopolitan affair even includes valet parking for your bicycle! Indeed, Slow Art Friday is indicative of the vibrant cultural arts scene that Marks has been instrumental in helping to create in downtown Honolulu in recent years. </p>
                <p><strong>Hours:</strong><br />
                  Gallery is open
                  <time itemprop="OpeningHoursSpecification" datetime="Tu,We,Th,Fr,Sa 11:00-18:00">Tuesdays through Saturdays from 11 am to 6 pm</time>
                  . Performance and event hours vary.<br />
                  <br />
                  <strong>Admission: </strong><br />
                  Permanent gallery admission is free; prices vary for performances, films, concerts and workshops.</p>
                
