                <h2 class="small">Hawaii Plantation Village, Oahu</h2>
                <p>This outdoor village-style museum offers visitors a glimpse back to a time when the sugar industry dominated Hawaii's economy and culture. Sugar plantations and the labor required to operate them were largely responsible for the diversification of Hawaii long before it became a state. </p>
                <p>                  An estimated 400,000 laborers from Japan, China, Puerto Rico, Korea, Portugal and the Philippines came to work on Hawaii's sugar plantations between 1852 and 1947. Modeled on the humble settlements of these workers, Hawaii Plantation Village tells their fascinating story. </p>
                <? include ("../../theme/text.html") ?>
                
                <p>                  Here you'll find both restored buildings and replicas of various plantation structures, including Chinese and Japanese temples, 30 restored camp houses belonging to different ethnic groups, the community bathhouse, the infirmary and the plantation general store. </p>
                <p>                  The museum features a range of exhibits detailing the lives of plantation workers through artifacts, photographs and historical records.  Be sure to check out the original steam engine train that was once used to haul sugarcane around. </p>
                <p>                  Hawaii Plantation Village offers guided tours Mondays through Saturdays (the museum is closed on Sundays). Tours are available in English or Japanese and typically last 1.5 hours. Tours take place on the hour every hour between 10 am and 2 pm. If you're coming with a group of eight or more, call ahead to make a reservation. </p>
                <p><strong>Hours:</strong><br />
