                <h2 class="small">Honolulu Museum of Art, Oahu</h2>
                <p>Founded in 1927 by Anna Rice Cooke, the Honolulu Museum of Art (formerly the Honolulu Academy of Arts) is an art museum located near downtown Honolulu, featuring a collection of over 50,000 works. The museum's major strengths are European and American paintings, graphic and decorative arts and the arts of Asia. It also has a Hawaiian collection, an Oceania collection, an Africa collection and a textiles collection.</p>
                <p>                  The museum's mission is education and it also administers the Honolulu Museum of Art School (formerly the Academy Art Center at Linekona), the largest private art school in the Islands. Besides the exhibitions, there is a caf&eacute;, the Robert Allerton Art Library and a museum shop.</p>
