                <h2 class="small">Bishop Museum, Oahu</h2>
                <p>Here is a destination that's not only entertaining but also enlightening. The Bishop Museum (<a href="../../oahu/museums/more_bishopmuseum_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/museums/p4.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) in Honolulu on the island of Oahu is Hawaii's largest museum with an extensive collection of cultural artifacts, art works, documents, manuscripts, audio files and historical photographs of Hawaii and other Pacific island nations and cultures. The collection started with the royal family heirlooms of Princess Bernice Pauahi Bishop. Her husband Charles Reed Bishop founded the Bishop Museum as a tribute to her. The collection has since then ballooned to millions of items, which tell museum visitors the history of Hawaii and the Pacific.</p>
                <? include ("../../theme/text.html") ?>
                <p>The huge collection has also expanded to items that are of scientific nature: <a href="../../oahu/museums/more_shells_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">shells from the Hawaiian Islands</a>, insect specimens, birds and the skeleton of a 50-foot sperm whale. You'll also find displays of ceremonial spears, great feathered capes of kings and old photos of  hula dancers. There are interactive exhibits and presentations (such as a lava-melting demo) as well as  a spectacular planetarium, where visitors can learn about Polynesian navigation by the stars, the stars visible above the Hawaiian Islands and the cutting-edge astronomy that is being done with top-notch telescopes on the summit of Mauna Kea on Hawaii's Big Island. </p>
                <p>Some of the exhibits at the Bishop Museum change from time to time. For example, from December 2012 to March 2013 visitors could explore the world of insects at the <em>Xtreme Bugs!</em> exhibit, a display of more than 20 giant animatronic insects - from a 12-foot tarantula and an 18-foot honeybee to a 20-foot Japanese hornet (<a href="../../oahu/museums/more_xtremebugsexhibit_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see exhibit photos</a>). From February until September 2015,  over a dozen life-like animatronic dinosaurs were on display in an exciting and interactive family exhibit <em>Dinosaurs Unleashed</em> (<a href="../../oahu/museums/more_dinosaurs_unleashed_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see exhibit photos and video</a>).</p>
                <p>Current exhibit Journeys: Heritage of the Northwestern Hawaiian Islands (<a href="../../oahu/museums/more_heritage-northwestern-islands.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see exhibit photos</a>). </p>
                <p>Visitors to the Bishop Museum are permitted to photograph and videotape the exhibits. However, please note that in certain exhibit areas, videography and photography may be limited due to copyright and/or cultural sensitivities. Please comply with posted rules. </p>
                <p><strong>Bishop Museum Hours</strong></p>
                <p>The museum is open
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,We,Th,Fr,Sa,Su 09:00-17:00">daily from 9 am to 5 pm.</time>
                  The museum is closed on Thanksgiving and Christmas Day (December 25). Parking is $5 per vehicle.</p>
