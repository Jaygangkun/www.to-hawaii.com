                <h2 class="small">Hawaii State Art Museum, Oahu</h2>
                <p>Cultural enthusiasts can satisfy their appetite for art and history with a visit to Hawaii's newest museum, the Hawaii State Art Museum (HiSAM). Although the museum itself opened in November of 2002, the Spanish-Mission style building itself has a rich history of Hawaiian culture dating back to the 1800s.</p>
                <p>Serving as the site to Hawaii's first hotel, the Hawaiian Hotel, in 1872, the building was a celebrated social establishment for royal guests and other newcomers to Oahu.  In the early 1900s, it became the Army and Navy YMCA, an important facility for men stationed in Honolulu during World War I. Over the course of the 20th century, the building underwent a demolition, as well as a number of renovations, restorations and ownership changes, before being purchased by the state of Hawaii in 2000. It is now on the National Register of Historic Properties. </p>
                <? include ("../../theme/text.html") ?>
                <p>Museumgoers can see remainders of the original building as evidenced in floor tiles, wainscoting tiles, iron grillwork, light fixtures and cast stone detailing found on the first and second floors. In the courtyard, the colored tiles under the eaves, cast stone detailing and swimming pool are also features of the original building.</p>
                <p>Known as the &quot;People's Museum,&quot; HiSAM is committed to offering the most inspiring and extensive collection of work by native artisans. The museum offers three galleries: the Diamond Head Gallery, HiSAM's largest gallery, which features large-scale exhibitions, as well as an Exploration Station for hands-on learning through interactive exhibits; the Ewa Gallery, which chronicles Hawaii's visual art history leading up to the 1960s and features both 3D and 2D works; and the Sculpture Gallery, which displays incredible pieces from the Arts in Public Places Collection, comprised of commissioned and relocatable artworks that include approximately 5,000 pieces of art by 1,400 artists. This collection includes paintings, prints, drawings, photographs, textiles and smaller-scaled three-dimensional artworks that can be moved and displayed in different buildings throughout the island.</p>
                <p>Past exhibitions have included &quot;Ho'oulu: The Inspiration of Hula,&quot; which honored the art of hula and featured hula implements and objects; &quot;The 47th Annual Hawaii Regional Scholastic Art Awards 2010,&quot; which exhibited the finest art work from hundreds of Hawaiian students, in grades 7-12, throughout the Islands; and &quot;Palolo Kids and Friends,&quot; which featured black and white photographs taken in the mid-1970s of local children at play in Palolo, a residential neighborhood in Honolulu.</p>
                <p>Other features of the museum include the Front Lawn, the Visitor Center Area, the Downtown @ the HiSAM Restaurant and the Shop @ the HiSAM Gift Store.</p>
                <p>Free public programs are offered every month that include concerts and performances on the First Friday, 5 pm to 9 pm; family art activities on the second Saturday, 11 am to 3 pm; and a lecture series on the last Tuesday of each month, 12 pm to 1 pm. </p>
                <p>Visitors to the museum are allowed hand-held still and video photography in the galleries. Photography is not permitted in special exhibitions or areas designated as &quot;No photography,&quot; and flash photography is prohibited throughout the entire museum. Cell phones and pagers are not allowed inside the galleries.<br />
