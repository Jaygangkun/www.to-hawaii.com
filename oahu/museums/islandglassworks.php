                <p style="color:#F00"><strong>Permanently closed</strong></p>
                <p>Looking for a hand-blown art glass gallery, glassblowing classes, a hotshop rental, or seeking to commission a piece of blown glass? All this and more is to be found at Island Glassworks (<a href="../../oahu/museums/more_islandglassworks_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) in Kailua. </p>
                
                <p>Established in 2005, Island Glassworks is the brainchild of Geoff Lee, who has lived in Hawaii since 1979 and considers the island paradise home. Geoff has a degree in glass from the University of Wisconsin-Madison and has worked as a professional glassblower in Seattle, Ohio and Pennsylvania. A keen interest in the art saw him participating at workshops at The Haystack Mountain School of Crafts in Maine, the Pilchuck Glass School in Washington, and Centro Studio Vetro in Italy. </p>
                <p>Island Glassworks was set up to encourage glass arts in Hawaii. The facility provides a studio for rent to experienced glass blowers and conducts classes and workshops ranging from 4 hours to 4 weeks for those interested in learning the art. </p>
