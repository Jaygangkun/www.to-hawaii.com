                <h2 class="small">Battleship Missouri Memorial, Oahu</h2>
                <p>A place of national pride, of enormous historical significance, and a museum full of interest and intrigue. The Battleship Missouri Memorial, located at <a href="../attractions/pearlharbor.php">Pearl Harbor</a>, Hawaii, is a living, working, all-action monument. It provides the chance to discover not only a U.S. naval vessel, but also the personal experiences of crewmembers and other war veterans through oral history recordings and transcriptions. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  Built during World War II this Iowa class battleship was a part of the Pacific Third Fleet and was later made famous as the site of the Japanese surrender to the Allied Forces in September 1945, officially ending World War II. Going on to serve during the Korean War and the first Gulf War, the Missouri was officially transferred to the non-profit USS Missouri Memorial Association on May 4, 1998. </p>
                
                <p>                  Since opening its decks and hatches to the public as part of the Historic Pearl Harbor Site the following January, the &ldquo;Mighty Mo&rdquo; as the battleship lovingly called, has attracted millions of visitors eager to share in the memory of this historic vessel and those who so bravely manned it.</p>
                <p>                  As an international standard, modern, floating museum, there is a fantastic array of options for visitors to chose from. A variety of tours are available, accompanied either by specially trained tour guides, audio-visual equipment, or straightforward walking tours following set routes, at one's own leisurely pace. </p>
                <p>                  The tours take in a look at the most famous historical aspects of the ship, the chance to learn about life at sea for crew members, and the command and control areas. A new addition to these options is the Battlestations tour. This, the most comprehensive tour option, includes a look at what would happen when the alarm sounded, &quot;General Quarters, General Quarters, All hands man your battle station!&quot; This  tour gives an insight into one of the most fascinating elements of life on board, the roles and experiences of individual crew members during battle and the armaments they used, including nine enormous, 116-ton guns. </p>
                <p><strong>Hours:</strong><br />
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 08:00-16:00">Daily from 8 am to 4 pm</time>
                  (September - May)<br />
                  Daily from 8 am to 5 pm (June - August)<br />
                  Closed on Thanksgiving Day, Christmas Day and New Year's Day.<br />
                  The site can be reached by shuttle bus from the USS Bowfin Submarine Museum &amp; Park just outside Pearl Harbor, where plenty of parking is available.</p>
                <p><strong>Admission:</strong><br />
