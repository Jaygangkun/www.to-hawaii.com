                <h2 class="small">King Kamehameha V Judiciary History Center, Oahu</h2>
                <p>This small museum occupies Ali'iolani Hale in downtown Honolulu, a beautiful historic building constructed under the rule of King Kamehameha V in 1874. Popular with school field trips, the King Kamehameha V Judiciary History Center (<a href="../../oahu/museums/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) chronicles Hawaii's legal history from pre-contact to present day. </p>
                <p>                  Founded in 1989, the museum aims to educate the public about the development of Hawaii's judicial system through exhibits and educational activities for teachers, students and the general public. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  Here you'll find The Monarchy Gallery detailing the island state's transition from the traditional Hawaiian kapu to a Western legal system. The gallery utilizes artifacts, photos, models and artwork to tell this fascinating story. The Center also features a restored courtroom dating back to 1913 that's now used for lectures and mock trials. And at the History Center Theater you can see documentary films about Hawaii's legal system. </p>
                <p>                  World War II buffs will enjoy the Center's Hawaii Under Martial Law exhibit, which documents the military government that took control following the bombing of Pearl Harbor on December 7, 1941. </p>
                <p>                  Guided tours are available for school and community groups by reservation. Don&rsquo;t miss the annual National Historic Preservation Week in May when the Center hosts special events celebrating Hawaii's legal history. Metered parking is available out front. </p>
                <p><strong>Hours:</strong><br />
