                <h2 class="small">Washington Place, Oahu</h2>
                <p>This historic home and museum located in downtown Honolulu's capitol district is a must-see for those interested in the island state's unique history. Named in honor of the first president of the United States, Washington Place was built in 1847 by wealthy sea captain John Dominis, but later became the home of Lydia Kapa'akea, aka Queen Lili'uokalani.</p>
                <p>                  After the Hawaiian monarchy was overthrown in 1893, the queen inhabited the beautiful colonial house until her death in 1917. The house was subsequently purchased for $55,000 by the Territory of Hawaii and converted into a governor's mansion. Over the years thirteen Hawaiian governors occupied the house until 2001, when a new governor's mansion was constructed on the same property. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  Washington Place is now open to the public and offers tours to the public. The museum, which opened in 2001, offers a fascinating glimpse into an important transitional period in Hawaii's history. Washington Place has been listed on the United States National Register of Historic Places since June 18, 1973, and in 2007 the house became a National Historic Landmark. </p>
                <p>Tours, which usually take 30 to 45 minutes, have a maximum size of 12 visitors. All visitors must present a valid photo ID and may be subject to a bag inspection. Nothing bigger than a handbag is allowed in the house. Photography is prohibited everywhere except outside the building, and video photography is not allowed. </p>
                <p>                  Washington Place is located across the street from the Hawaii State Capitol building at 320 South Beretania Street in Honolulu. Parking is not allowed on the property, but visitors can park on the street, at the city garage in the 500 block of South Beretania, or in the Iolani Palace parking lot. </p>
                <p><strong>Hours: </strong><br />
                  By appointment only, at least 48 hours in advance. Call <span itemprop="telephone">(808) 586-0248</span>.<br />
