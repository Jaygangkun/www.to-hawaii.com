                <h2 class="small">Hawaii Children's Discovery Center, Oahu</h2>
                <p>This exiting children's museum teaches through interactive displays and games of make-believe. The Center is divided into four areas and geared towards children ten and under. </p>
                <p>In &quot;Fantastic You!&quot; children are introduced to the human body and can learn about digestion inside a larger-than-life stomach, visit a health clinic, ride a bike with a skeleton and experience life in a wheelchair. </p>
                <? include ("../../theme/text.html") ?>
                <p>In &quot;Your Town&quot; there's an entire kid-sized community where children can visit the grocery store, work on cars, put on a play, become a fireman, fight crime and much more. </p>
                
                <p>In &quot;Hawaiian Rainbows&quot; children learn about the history of Hawaii while working a pineapple plantation, captaining a fishing boat, blowing big bubbles and visiting an old Hawaiian store. </p>
                <p>Finally, in &quot;Your Rainbow World&quot; children can travel to other cultures and visit a Japanese home, cook in a Chinese restaurant and play native instruments. </p>
                <p>For children five and under there are toys that stimulate the senses in the Tot Spot. </p>
                <p>Food and drink, including gum, cellular phones and strollers are prohibited in the exhibits. Stroller parking is available in the lobby and snacks can be purchased in the gift shop. </p>
                <p>The Hawaii Children's Discovery Center was formerly the Hawaii Children's Museum. In 1998 the museum moved from Iwilei to its current larger location outside of downtown Honolulu and changed its name. The building now encompasses 38,000 square feet of interactive fun. <br />
                  <br />
                  <strong>Hours:</strong><br />
                  <time itemprop="OpeningHoursSpecification" datetime="Tu,We,Th,Fr 09:00-13:00">Tuesday to Friday: 9 am to 1 pm</time>
                  <br />
                  <time itemprop="OpeningHoursSpecification" datetime="Sa,Su 10:00-15:00">Saturday and Sunday: 10 am to 3 pm</time>
                  <br />
                  The Center is closed Mondays, Easter, Thanksgiving, Christmas Day, New Years Day, Labor Day and any holiday that falls on a Monday. There is ample free parking in the Kakaako Waterfront Park parking lot.</p>
                <p><strong>Admission:</strong><br />
                  Free admission to HCDC members<br />
                  General: $12.00 <br />
                  Seniors, 62+ (ID required): $7.00<br />
                  Children (under age 1): Free<br />
                  Kama'aina (local residents) and military (ID required): $10.00</p>
                
