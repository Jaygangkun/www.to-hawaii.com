                <h2 class="small">Honolulu Surfing Museum, Oahu</h2>
                <p>Located at the Jimmy Buffett's at the Beachcomber restaurant, the Honolulu Surfing Museum (<a href="../../oahu/museums/more_honolulusurfing_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) can't fail to educate and enthrall even the most hardcore of surfing enthusiasts. Perhaps owing to the fact that the museum's founders are just that! On a surf trip up the Californian coast, Jimmy Buffet himself stopped into what, at the time, was the tiny Santa Barbara Surfing Museum run by surfer and antique collector James O'Mahoney. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  Buffet realized the fantastic and varied collection O&rsquo;Mahoney had amassed could generate far greater interest and reach a more enthusiastic audience in Hawaii. Hence the collection was shipped across to Honolulu where it formed the basis for the Honolulu Surfing Museum. The result being that to this day surfers and historians (or even surf-historians!) can sample the intrigue of this surfing Mecca whilst recharging with a cheeseburger at Jimmy Buffet's before heading back out to surf!</p>
                
                <p>                  The museum itself houses a superb array of surfing and historical artifacts, all professionally presented, but with a definite, laid-back vibe. Visitors can take a look at the development of some significant surfboards from the last century of surfing, or gaze in wonderment at pieces made famous more by their owners than their significance in terms of development. For example, interactive exhibits include the original board used in Francis Ford Coppola's much celebrated <em>Apocalypse Now</em>, or the only board ever authorized by Marvel Comics as &lsquo;belonging&rsquo; to the legendary, speed-of-light-defying, <em>Silver Surfer</em>.</p>
                <p>                  For those visiting with a keener eye for human history, unique exhibits include a gold ring worn by Captain James Cook when he explored Hawaii aboard HMS <em>Resolution</em> in the latter half of the 18th Century, traditional ukuleles believed to be some of the oldest ever found, and even one of only two Rell Sunn Hawaiian print dresses thought to still be in existence. </p>
                <p>                  Whilst the exhibits on display can perhaps then be placed in two distinct categories, the atmosphere at the Honolulu Surf Museum retains the consistently chilled out ambience you might expect from a surf museum. Whilst remaining informative and educational about &lsquo;the royal sport of surfing,&rsquo; the emphasis is clearly on fun as visitors stroll around the exhibits in their boardies and flip-flops, with a cocktail bought from the museum bar in hand! </p>
