                <h2 class="small">Spalding House, Oahu</h2>
                <p>Located on a hill overlooking the city and the ocean in the Honolulu neighborhood of Makiki, the Spalding House (formerly known as The Contemporary Museum) is part of the <a href="../museums/honoluluacademyofarts.php">Honolulu Museum of Art</a>. The museum features six galleries of art and a permanent installation of David Hockney's L'Enfant et les sortil&egrave;ges. In the museum caf&eacute;, visitors can sit indoors in a gallery-like atmosphere amid changing displays of art or outdoors in a garden-like setting. </p>
                <? include ("../../theme/text.html") ?>
                <p>Besides the exhibitions, the museum also maintains a sculpture garden, which encompasses 3.5 acres. The garden, called Nu'umealani (heavenly terrace), is so beautiful that the museum won the American Society of Landscape Architects Millennium Award for preserving and maintaining it. </p>
                <p>The garden was completed between 1928 and 1941 by Reverend K.H. Inagaki, a local minister with a passion for landscape architecture. Designed to provide a place to retreat, meditate and experience the harmony of nature, the garden includes a sprawling lawn, a tropical terraced garden, walking paths and places to sit. It is open to the public during museum hours.</p>
