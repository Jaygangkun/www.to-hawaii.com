                <h2 class="small">Japanese Cultural Center of Hawaii, Oahu</h2>
                <p>Hawaii's Japanese population has been instrumental in shaping the islands' diverse cultural landscape ever since the arrival of the first Japanese sugar plantation laborers in the late 1800s. Incorporated in 1987, this non-profit cultural center in the Mo'ili'ili neighborhood of Honolulu celebrates and educates the public about the ever-evolving Japanese-American experience in Hawaii. </p>
                <p>                  For those interested in Japanese culture, the Japanese Cultural Center of Hawaii (JCCH) offers a wide range of programs, classes, festivals and exhibits throughout the year. One week you may hear a concert of traditional Japanese music, the next week it's a lecture on lessons learned from the bombing of Hiroshima. </p>
                <? include ("../../theme/text.html") ?>
                
                <p>                  The JCCH also hosts art exhibits by important Japanese artists past and present. The center features a number of excellent facilities, including a martial arts center dojo, an authentic Japanese teahouse and a 10,000 square foot ballroom with a 600-person seating capacity. 
                  The JCCH also offers classes about various topics, such as Japanese braiding, proper tea ceremony etiquette and Japanese calligraphy. </p>
                <p>                  If you're interested in taking home a souvenir, you can pick up some unique Japanese antiques and collectibles at the JCCH. Tea sets, woodblock prints, kimonos, Japanese dolls and more are available for purchase. </p>
