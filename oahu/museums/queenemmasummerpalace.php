                <h2 class="small">Queen Emma Summer Palace, Oahu</h2>
                <p>Born in 1836, Queen Emma was the wife of King Kamehameha IV. She was the descendant of Hawaiian chieftains and John Young from England, who was a friend and advisor to King Kamehameha I. Queen Emma became a symbol of Hawaii&rsquo;s cosmopolitan culture. Built in 1847 on the island of Oahu, the summer palace (<a href="../../oahu/museums/p5.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) and home of Queen Emma and King Kamehameha IV has been restored and offers a glimpse into the lifestyle of the Hawaiian monarchy. </p>
                <? include ("../../theme/text.html") ?>
                                <p>In the summer palace, one can view household furnishings, personal items of Queen Emma and her family, Hawaiian quilts and artifacts and there&rsquo;s a reference collection on Hulihee Palace, Queen Emma Palace and the Daughters of Hawaii. Guided group and school tours are offered. There are also special exhibits, arts and crafts demonstrations, audiovisual programs and Hawaiian quilting and language classes.</p>
                <p>After a visit to the Palace stop by at neighboring Nu'uanu Valley Park which has a few picnic tables and amazing banyan trees (<a href="../../oahu/museums/more_nuuanuvalleypark_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see park photos</a>, <a href="../../oahu/museums/p6.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>). The park is located right behind the Summer Palace gift shop.</p>
