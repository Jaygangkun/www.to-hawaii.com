                <h2 class="small">King's Guard Museum, Oahu</h2>
                  <p style="color:#F00"><strong>Permanently closed</strong></p>
                <p>Take a step back in time as you tour the King's Guard Museum and learn about the history of Hawaii when it was a territory of the United States. Located in the heart of Waikiki, the museum features interesting memorabilia, such as rifles, uniforms, photos, swords, banners and flags. <br />
                  <br />
                  Every evening at 6:15 pm, the &quot;Changing of the Guards&quot; ceremony takes place. This short and militarily precise ceremony harks back to the days when the King's Guard would retire the Hawaiian flag. The present day guards wear uniforms that are exact replicas of those worn by the Royal Palace Guards of King Kalakaua. </p>
