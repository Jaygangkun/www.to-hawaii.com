                <h2 class="small">John Young Museum of Art, Oahu</h2>
                <p><strong>Note: Since May 2013, the museum has been closed for roof and facility maintenance.</strong><br />
                  <br />
                  This small art museum affiliated with the University of Hawaii at Manoa occupies Krauss Hall, one the oldest buildings on the school's campus. Asian art lovers should not miss the John Young Museum, which focuses primarily on Japanese, Chinese, Korean and Southeast Asian artworks, the bulk of which were donated by local painter John Chin Young.</p>
                 <? include ("../../theme/text.html") ?>
                <p>                  The John Young Museum's outstanding collection includes ceramic sculptures, wood carvings and bronze pieces dating back to ancient times. The ancient China collection features Han Dynasty tomb figures (c. 200 B.C.), Neolithic pottery jars dating back to 3000 B.C. and dozens of artifacts from the Ming Dynasty (1300s).</p>
                
                <p>You'll also find pieces from the Three Kingdoms of Korea (5th to 6th centuries), a Mashiko stoneware plate by renowned Japanese artist Shimaoka Tatsuzo, Ban Chiang pottery dating from 2,000 to 1,000 B.C. and ceramic works by the Khmer people (11th to 13th centuries). The collection even has a few noteworthy African pieces from Mali, Ghana and Nigeria. </p>
                <p>                  Krauss Hall is a work of art in itself and is listed on the Hawaii Register of Historic Sites. The space features a peaceful water garden designed by Betsy Sakata, where you can spot a Thai Buddha that belonged to Young. Don't miss the 1976 portrait of John Chin Young by contemporary artist and Stanford University professor Nathan Oliveira located near the museum entrance. </p>
                <p><strong>Hours:</strong><br />
                  Sunday to Friday 12 - 4pm<br />
Closed holidays <br />
                  <br />
                  <strong>Admission:</strong> <br />
                  Free. Reservations required for group visits.<br />
                </p>
                
