                <h2 class="small">USS Bowfin Submarine Museum &amp; Park, Oahu</h2>
                <p>One of the key features of Pearl Harbor's Battleship Row alongside the USS Arizona and the Battleship Missouri memorials, the USS Bowfin Submarine Museum &amp; Park (<a href="../../oahu/museums/p7.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) sits as a gleaming reminder of a Nation's past. One of only fifteen surviving U.S. World War II submarines, the Bowfin was acquired by the non-profit Pacific Fleet Submarine Memorial Association on August 3, 1979, after a lengthy process begun seven years earlier by Pearl Harbor survivor ADM Bernard A. &quot;Chick&quot; Clarey. </p>
                
                <p>                  After a cleaning and restoration process, undertaken by a team of dedicated volunteers, the Bowfin was eventually moved to its current location at Pearl Harbor in December 1980. The first visitors to what is now one of the world's finest museum ships were welcomed on board the following April, and within five years the museum had earned the right to be named a National Historic Landmark by the U.S. Department of the Interior. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  With a plethora of artifacts from the history of not only the USS Bowfin but submarine warfare in general, the Bowfin submarine museum is an intriguing, educational and unique experience. Visitors can board the submarine itself and later browse an extensive collection of artifacts in the adjacent Submarine Museum building. Highlights include a look at the McCann rescue chamber used in the only successful rescue of men from a sunken U.S. submarine in 1939 and, on a more somber note, the Waterfront Memorial tells the story of each lost submarine, listing those brave submariners lost during battle. All this and more can be observed at your leisure or as part of a submarine and museum tour.</p>
                <p>                  Away from the typical personal visit, the staff at the USS Bowfin also offer visits for all types of educational groups from school parties, home study students, adult education groups, or elderly groups. Running alongside this, the museum's outreach program offers presentations on the history and science of submarines to educational groups based on Oahu and the neighbor islands. Space is also available onsite for groups wishing to organize lectures and seminars.</p>
                <p>                  So whether you are visiting as part of a day out at the wider Pearl Harbor site, or simply to witness this fantastic, genuine example of a WWII submarine, the museum's 4,000 plus artifacts and exhibits are sure to enthrall and amuse enthusiasts of all ages. </p>
