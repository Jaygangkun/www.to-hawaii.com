                <h2 class="small">Lucoral Museum, Oahu</h2>
                <p>The Lucoral Museum in Waikiki is a great place to learn about the ocean and its natural wonders. The museum has a vast collection of rocks, gemstones, fossils and sculptures from around the world, where visitors can see how coral and pearls are transformed into beautiful jewelry and works of art.</p>
                <p>The museum offers jewelry making workshops on Tuesday and Thursday afternoons. The beads and other necessities are supplied by the museum and visitors can learn to thread them together to design a personalised piece of jewelry. </p>
                <? include ("../../theme/text.html") ?>
                <p>Exhibits feature endangered plants and birds of the Islands, and there is an area devoted to Hawaii's volcanic past. Children are especially attracted to these displays, and since the museum opened more than 15,000 school children have visited. </p>
                <p>The museum was established in 1989 by Flora Lu and is the home of Flora Lu Collections. The company is a leading producer of coral and pearl jewelry and remains a well-known and trusted name in the industry. The gift shop at the museum has a sizeable stock of jewelry designs of Flora Lu Collections. </p>
                <p><strong>Hours:</strong><br />
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr 09:15-17:30"> Monday to Friday: 9:15 am to 5:30 pm</time>
                  . <br />
                  Jewelry workshops every Tuesday and Thursday from 2-4 pm. Genuine gemstone and pearl material cost starts at $10 for adults, kids tour and workshop $2. <br />
                  <br />
                  <strong>Admission:</strong><br />
                  Free.</p>
                
