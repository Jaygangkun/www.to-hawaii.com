                <h2 class="small">USS Arizona Memorial, Oahu</h2>
                <p>The finest example of and the greatest tribute to valor in the Pacific Ocean is the USS Arizona Memorial Visitor Center. It is located at the Pearl Harbor Naval Base, next to the sunken remains of the battleship USS Arizona and its crew. <a href="../attractions/pearlharbor.php">Pearl Harbor</a> was attacked by the Japanese on December 7, 1941, and the events of that day, commemorated by the Memorial, resulted in the United States entering World War II.</p>
                <? include ("../../theme/text.html") ?>
                <p>The 184 feet (56 m) long memorial was designed by Alfred Pries, an architect from Honolulu, and was dedicated in 1962. Accessible only by boat, the USS Arizona Memorial Center receives more than a million visitors each year. Visits to the Center and Memorial are free of charge, and free tickets are distributed on a first-come, first-served basis. </p>
                
                <p>Visitors are encouraged to watch a short film about the attack on Pearl Harbor prior to boarding the Navy shuttle boat that takes them to the Memorial. A tour of the Memorial is a self-guided venture. </p>
                <p>The Memorial has three main sections &ndash; entry, assembly room and a shrine. There is an opening on the Memorial's floor over the sunken decks. It is from here that visitors can pay their respects to the lost soldiers by tossing in flowers. One of the ship's three anchors and one bell is on display as well. </p>
                <p>At the shrine behind velvet ropes are the names of the 1,102 brave crewmen killed on board the ship. Every U.S. President since Franklin D. Roosevelt has visited the site to offer his respects. </p>
                <p><strong>Hours:</strong><br />
                  The USS Arizona Memorial Center is open
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 07:00-5:00">daily from 7 am to 5:00 pm.</time>
                  <br />
                  Tours to the USS Arizona Memorial begin at 8 am and run through 3 pm daily. <br />
                  The Center is closed on Thanksgiving, Christmas Day and New Year's Day.</p>
                <p><strong>Admission:</strong><br />
                  Visits to the USS Arizona Memorial Visitor Center are free.</p>
                
