                <h2 class="small">Hawaiian Mission Houses Historic Site and Archives, Oahu</h2>
                <p>Visit the Hawaiian Mission Houses Historic Site and Archives, formerly known as the Mission Houses Museum (<a href="../../oahu/museums/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), to get an idea how the missionaries lived on the island during Hawaii's missionary period from 1820 to 1863. The settlement was built in 1831 and three of the original buildings still remain on the site in downtown Honolulu. </p>
                <p>The missionaries had a profound impact on the people of Hawaii and their culture. All in all, the museum features a collection of over 3,000 Hawaiian, Pacific and Western artifacts and more than 12,000 books, original letters, manuscripts, diaries, journals, illustrations and Hawaiian church records. The museum is a National Historic Landmark.</p>
                <? include ("../../theme/text.html") ?>
                <p>The Hale Laau is the oldest building in the state of Hawaii. It was built in 1821 and served as a home for the missionaries. The Ka Hale Pai was the printing house. Here, Hawaiians and the missionaries created the Hawaiian alphabet. The Ka Hale Kamalani or the Chamberlain House was built completely from blocks of coral skeletons. It was the home of the Chamberlain family and additionally was used as a storehouse for mission goods.</p>
