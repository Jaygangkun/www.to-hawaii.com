                <h2 class="small">Iolani Palace, Oahu</h2>
                <p>The Iolani Palace (<a href="../../oahu/attractions/p9.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>) in Honolulu, Hawaii, was built as a seat of government by the &ldquo;Merry Monarch&rdquo; King David Kalakaua, Hawaii&rsquo;s final king and the first monarch to travel around the world visiting other sovereign states. Inspired by the grand palaces owned by other monarchs around the world, he constructed his own in a location that is now downtown Honolulu on the island of Oahu. Completed in 1882, it cost over $360,000. Not only is it an architectural masterpiece, but it had electricity and telephones four years before the White House did.</p>
                <? include ("../../theme/text.html") ?>
                <p>The Iolani Palace kept its splendor as a royal residence until the king&rsquo;s successor, his sister Queen Liliuokalani, was overthrown and imprisoned here. After eight months of imprisonment and to avoid further bloodshed of her people, the Queen surrendered to the U.S. and Hawaii became a U.S. territory. Iolani Palace was then used as a capitol for the new territory and then after 1959, when Hawaii became the 50th state, for the state of Hawaii. In 1969, a new state capitol was built and the neglected Iolani Palace was restored with the efforts of the &ldquo;Friends of Iolani Palace.&rdquo; For Native Hawaiians, the Iolani Palace is a reminder of the royal heritage of Hawaii and a symbol for the restoration of Hawaii&rsquo;s independence and sovereignty. </p>
                <p>Visitors can do a self-guided tour. However, a guided tour is best since there are areas that are off-limits to unguided guests.</p>
