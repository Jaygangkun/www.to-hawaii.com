                <h2 class="small">Kapiolani Park, Oahu</h2>
                <p>Kapiolani Park (<a href="../../oahu/attractions/p12.html" onclick="NewWindow(this.href,'name','2550','850','yes');return false">view panorama</a>, <a href="../../oahu/attractions/more_kapiolanipark_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on the island of Oahu was given to the people of Hawaii from King Kalakaua in 1887. He named the 130 acres of land after his beloved wife, Queen Kapiolani. Today, local families and visitors alike enjoy the park for many reasons. There are picnic sites, open grassy areas perfect for game playing, 100-year-old trees, tennis courts, soccer fields and a 3-mile jogger's course with aerobic exercise stations. </p>
                <? include ("../../theme/text.html") ?>
                <p>The park is also the site of historic <a href="../../oahu/attractions/diamondhead.php">Diamond Head</a>, the Sunday Art Shows and the Waikiki Shell, home to many outdoor concerts and shows, from traditional Hawaiian songs to rock and pop. In the summer, the Royal Hawaiian Band offers free concerts every Sunday afternoon in the Kapiolani Bandstand, just as they did during Kalakaua's reign. </p>
                <p>Kapiolani Park is located at the east end of Waikiki bordered by Monsarrat, Kalakaua and Paki Avenue.</p>
                
