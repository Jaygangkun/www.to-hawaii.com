                <h2 class="small">Waimea Valley, Oahu</h2>
                <p>Waimea Valley (<a href="../../oahu/attractions/more_waimeavalley_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/attractions/p4.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a beautiful 1,800-acre visitor attraction that is also a sanctuary for rare birds and plants endemic to Hawaii. A trip to this park is a combination of learning about Hawaiian history and culture and appreciation of nature.</p>
                <? include ("../../theme/text.html") ?>
                <p>                  Within the park there are 78 reconstructed archaeological sites that show the life of earlier inhabitants of the area. These include religious sites and shrines, sites of former houses and agricultural and fishing areas. Some of the sites have been excavated and restored with storyboard displays explaining their significance. Continuing with the cultural theme, several activities are being offered, including lei making, kapa demonstrations, history walks, hula lessons and Hawaiian games, as well as crafts, music and storytelling with kupuna.</p>
                
                <p>                  For those more interested in the beauty of nature than the history of man, the park is home to a spectacular botanical collection. This 150-acre site contains more than 5,000 species of tropical and subtropical plants, including native and endangered Hawaiian plants. Visitors can take a self-guided walking tour through the garden. </p>
                <p>Educational brochures and plaques provide information along these easy, paved trails. Throughout the park visitors also get the chance to see native and endangered bird species, including the 'alae'ula. Four of the five species of the native 'o'opu freshwater fish can also be spotted in Kamananui Stream. </p>
                <p>                  Perhaps the biggest attraction in Waimea Valley, and focal point of the park, is the 45-foot (13.7 m) Waimea Falls. Visitors can refresh themselves in the cool waters of the pool. Note: Look for the sign at the entrance gate to the park if swimming is allowed on the day you are visiting. On some days when the conditions are not good, swimming is not allowed.</p>
                <p>                  CAUTION: As with all freshwater streams and waterfalls in Hawaii, Leptospirosis bacteria may be present in the water. Do not drink the water and keep water out of open wounds (including small scratches) and eyes. <br />
                </p>
              
