                <h2 class="small">Makua Valley, Oahu</h2>
                <p>Makua Valley is located on Oahu's leeward (west) coast on the foothills of the Waianae Mountains. The valley is spoken about prominently in Hawaiian tales, myth and music. The word &ldquo;makua&rdquo; means parent in the Hawaiian language, and legend has it that Makua Valley was the place where man was first created. Conversely, Hawaiians consider the valley to be the point from where souls depart for an afterlife once the human form has been cast off. It is for this reason that one can see many handmade memorials to the dead along the rocky cliffs lining the shore.</p>
                <? include ("../../theme/text.html") ?>
                <p>Apart from a U.S. military base, large parts of Makua Valley remain devoid of residential and commercial development. The U.S. military set up a base on 4,000 acres of land in the valley during the 1930s and continues using the area for training. This has been a point of contention between the government and the local people as Makua is home to many endangered species. More than 40 endangered species of flora and fauna can be found in the valley, and the area is also sacred to many Native Hawaiians because it has a number of wahi pana (sacred places), cultural and archaeological sites. Many Native Hawaiians and other local residents have been objecting to the activities at the military base for long and the issue is under consideration at present.</p>
                
