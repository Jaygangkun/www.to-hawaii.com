                <h2 class="small">Waikiki Shell, Oahu</h2>
                <p>The Waikiki Shell, an outdoor amphitheater, is located in <a href="kapiolanipark.php">Kapiolani Park</a> at the east end of Waikiki. Live theatrical, entertainment and musical events take place here year-round. </p>
                <p>The shell-shaped building has an acoustically accommodating stage and is considered a prime location for many sunset and twilight concerts. The amphitheater seats 1,958 in stadium chairs and 6,000 on the lawns. Its front &ldquo;pool&rdquo; area has an additional 505 theater-style seats or room for 320 banquet table seats. </p>
                <p>The lush tree-lined grounds of the Waikiki Shell are called the Diamond Head Greens and can be rented for private parties, banquets, weddings and other events. Also on the grounds of the Shell is a smaller amphitheater, closer to the ocean, that hosts daytime hula shows. </p>
