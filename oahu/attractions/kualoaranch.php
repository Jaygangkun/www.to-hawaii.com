                <h2 class="small">Kualoa Ranch, Oahu</h2>
                <p>Encompassing 4,000 acres of lush valleys and mountains on Oahu&rsquo;s windward coast near the town of Ka'a'awa, Kualoa Ranch (<a href="../../oahu/attractions/more_kualoaranch_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) has some of the most beautiful scenery on the island. It is no wonder that it has served as a Hollywood filming location for many popular movies in the past, including <em>Jurassic Park</em>, <em>Godzilla</em> and <em>50 First Dates</em>.</p>
                <? include ("../../theme/text.html") ?>
                <p>Family-owned since 1850, visitors can take part in a wide variety of activities and educational programs here, such as horseback rides, ATV (all-terrain vehicle) tours, movie tours, a Jungle Expedition, a Hawaiian Fishpond &amp; Garden Tour, hula lessons and a snorkeling excursion to Kaneohe Bay with a picnic on a &ldquo;secret&rdquo; island. Kualoa Ranch also has a shooting range, a petting zoo and a visitor&rsquo;s center with a gift shop. They have individual half- and full-day tour packages.</p>
                <p>Kualoa is also known as one of the most sacred places on the island of Oahu. There are many legends and myths surrounding the Kualoa Valley. For example, it is believed that the ranch&rsquo;s Moli'i fishpond was built by the Menehune, the legendary little people of Hawaii, within just one night.</p>
                
