                <h2 class="small">Waikiki Aquarium, Oahu</h2>
                <p>With over 3,055 water denizens representing 464 species, the Waikiki Aquarium (<a href="../../oahu/attractions/more_waikikiaquarium_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on Oahu is not only a public attraction, but also the University of Hawaii's research and educational facility for aquatic life. It is also designated as a Coastal Ecosystem Learning Center of the Coastal America Partnership federal program.</p>
                <? include ("../../theme/text.html") ?>
                <p>The Waikiki Aquarium, which is the third oldest aquarium in the United States, averages more than 350,000 visitors per year who are drawn to its coral reef exhibits. Especially attractive for visitors is its collection of reef sharks, tropical fish,  jellyfish of all sizes and shapes, eels, living corals and other ocean creatures of Hawaii and the South Pacific region. Other main attractions include the endangered Hawaiian monk seals, rays and green sea turtles.</p>
                <p>One of the interesting features of the Waikiki Aquarium is its &ldquo;Aquarium After Dark&rdquo; activity that is offered once each month. With this presentation, visitors, with only flashlights to aid their vision, can explore the tanks and their swimming occupants to observe their nocturnal habits and movements.</p>
                <p>It is easy to find the Waikiki Aquarium at the east end of Waikiki, across from Queen Kapiolani Park. If you are staying at any of the Waikiki Beach hotels, you can just walk down the beach towards Diamond Head. You may also take bus number 2 or the Waikiki Trolley, which usually arrives every 15 minutes. </p>
               
