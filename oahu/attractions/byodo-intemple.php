                <h2 class="small">Byodo-In Temple, Oahu</h2>
                <p>The Byodo-in Temple sits in a peaceful location called the Valley of the Temples, with the 2,000-ft Koolau Mountains of Oahu providing a magnificent natural backdrop. Once considered a &ldquo;secret&rdquo; place for alternative tourists, the temple saw international fame as the location of the popular ABC TV series <em>Lost</em>. Nevertheless, the Byodo-In Temple has maintained its reclusive air and remains a place of refuge for many local residents.</p>
                <? include ("../../theme/text.html") ?>
                <p>The temple is a replica of the Byodo-In, a 950-year-old Buddhist temple located in Uji, Japan. It was built in the 1960s to celebrate the 100th year since the first Japanese workers came to Hawaii in search of work, first working the sugar plantations and later establishing one of the islands&rsquo; early industries. What&rsquo;s amazing about this full-scale replica is that it was built entirely without the use of nails - the strong concrete is all that holds it up!</p>
                
                <p>Inside the temple is a gigantic statue of Buddha, the largest wooden Buddha made in the last 900 years. Around it are 52 smaller sculptures depicting Boddhisattvas (enlightened beings) floating on clouds, dancing and playing musical instruments. The set is located in a large room called the Phoenix hall, arguably the most visited room in the temple.</p>
                <p>Another highlight of the Byodo-In Temple is the large Zen-style garden on which it stands. Wide green lawns and stone paths lead up to the temple and surround a large man-made pond that faces the temple entrance The pond is home to a small population of koi fish and a couple of beautiful black swans.</p>
