                <h2 class="small">Dole Plantation, Oahu</h2>
                <p>Take a walk through history, commune with nature and enjoy great outdoor views. You can do all these and more at the Dole Pineapple Plantation  (<a href="../../oahu/attractions/more_doleplantation_photos.html" onclick="NewWindow(this.href,'name','1050','600','yes');return false">see more photos</a>) in central Oahu, a site attracting close to a million visitors per year. The sprawling complex gives visitors a peek into the daily grind at the world&rsquo;s leading pineapple grower. The plantation has long played a significant role in Hawaiian history and livelihood.</p>
                <? include ("../../theme/text.html") ?>
                <p>Easily one of the most popular attractions of the plantation is the Pineapple Maze, listed in the <em>Guinness Book of World Records</em> as the world&rsquo;s largest maze. The maze spreads out over two acres and features over 14,000 varieties of Hawaiian plants. The 2.46-mile trail is a real puzzle - you&rsquo;ll have your share of loops, dead ends and wrong turns. But finish the maze and you get a coupon worth a 15% discount for any Dole merchandise.</p>
                
                <p>You can also take part in the Plantation Garden Tour, a walking tour that takes you to eight attractive mini-gardens. Each garden showcases a variety of crops and plants, many of them native to Hawaii. Of course, you&rsquo;ll also come across a vast number of pineapples and can see how they are picked, processed and packed every day. </p>
                <p>Don&rsquo;t feel like walking? Try the Pineapple Express - a two-mile ride aboard a miniature train around the famed Oahu pineapple fields. During the 20-minute ride, a guide talks about the history of the pineapple production and agriculture in Hawaii, as well as the fascinating story of James Dole and how he built his empire.</p>
                
<h2 class="big">Dole Plantation Overview</h2>
