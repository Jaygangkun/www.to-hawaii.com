                <h2 class="small">Stairway to Heaven, Oahu</h2>
                <strong style="color:#F00; font-size:20px">This hike is permanently closed.</strong>
                <p>The hike up the Stairway to Heaven (<a href="../../oahu/attractions/more_stairways_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>), also known as Haiku Stairs, is one of the most popular of Oahu's &quot;forbidden&quot; trails. In the past, people used to climb up here for the fantastic panoramic views, which await all those who made it to the top. The hike is difficult. First you have to make the 2,120-foot, 3,922-step ascent. But even though the trail is closed today, some avid hikers go anyway because of the spectacular view that awaits from the top. </p>
				<? include ("../../theme/text.html") ?>
                <p>The stairway was originally built for the U.S. Coast Guard to allow access to the LORAN radio antenna on the top of the mountain. But the later termination of Omega Station operations also led to the cessation of stair maintenance. Because of this, the stairs became terribly worn and dangerous. So the state later officially closed the stairs to the public in 1987.</p>
                <p>As of 2003, the Haiku Stairs have been completely repaired, which cost the city $875,000. However, legal access is currently still on hold. Up until today, there is no legal entrance to the Stairway to Heaven/Haiku Stairs. If you attempt to hike here, expect to be turned away or cited by Honolulu Police. Hikers have been parking in the residential area, which is limited and frowned upon by residents.</p>
                
