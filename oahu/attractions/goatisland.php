                <h2 class="small">Goat Island, Oahu</h2>
                <p>Goat Island is located on Oahu&rsquo;s northeastern coast and is also known as Mokuauia Island. Contrary to its name, there are no goats on Goat Island, only nesting seabirds. In fact, it's a seabird sanctuary, situated at the southern end of <a href="../../oahu/beaches/malaekahana-bay.php">Malaekahana Bay</a>. </p>
                <p>There are three beaches on Mokuauia Island; however, the best one for swimming and the easiest to access is located toward the left. It is already visible when paddling out there. This beach is protected from the surf by cliffs and one can just float in the calm water and enjoy the awesome view of Oahu, the mountains and the beaches. If you visit Goat Island, keep in mind that this is a protected seabird sanctuary. Stay away from the center of the island because the seabirds lay their eggs here. They should not be disturbed.&nbsp;</p>
                <? include ("../../theme/text.html") ?>
                <p>The island is close enough to the beach so it&rsquo;s possible to swim over, especially during low tide when the water is only about two to four feet deep. However, it is not recommended to walk over because there is a lot of reef and you may cut your feet if you walk. Besides that, stepping on the reef may damage it, so it is better to take a bodyboard or surfboard and paddle over. </p>
                <p>The current can be quite strong here, especially when the tide changes, and there are no lifeguards, so if you&rsquo;re not used to swimming in the ocean, better stay on land. In addition to strong currents, there may be dangerous rip tides and even though the waves may look small, they can be quite powerful in this area. The waves often times come from the left and right and in the middle, where you would swim over, they clash together, making it a bit difficult to hold your balance if you&rsquo;re on a board. </p>
                
