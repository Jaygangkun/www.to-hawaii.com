                <h2 class="small">He'eia State Park, Oahu</h2>
                <p>He'eia State Park (<a href="../../oahu/attractions/more_heeiastatepark_photos.html" onclick="NewWindow(this.href,'name','650','800','yes');return false">see more photos</a>, <a href="../../oahu/attractions/p3.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) on Oahu's windward coast just north of Kaneohe has a nice view of <a href="../beaches/kaneohebeachpark.php">Kane'ohe Bay</a> (with its many sandbars), Coconut Island, the Mokapu Peninsula, He'eia Boat Harbor and He'eia Fishpond, an ancient Hawaiian fishpond that remains largely intact. </p>
                <p>The state park, located on Kealohi Point, is surrounded on three sides by water, hence the sweeping views. There is a relatively large parking lot with many roaming chickens and feral cats. </p>
                <p>The state park has a large grassy area, trees that provide shade and a building that can be rented for special occasions, such as weddings. The park has restrooms and outdoor showers. It is open from 7 am to 6:45 pm. </p>
