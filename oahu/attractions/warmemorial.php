                <h2 class="small">War Memorial Natatorium, Oahu</h2>
                <p>Located across from Kapiolani Park, next to <a href="../../oahu/beaches/kaimanabeach.php">Kaimana Beach</a> on the island of Oahu, the War Memorial Natatorium (<a href="../../oahu/attractions/p13.html" onclick="NewWindow(this.href,'name','2550','730','yes');return false">view panorama</a>) was completed in 1927 as a memorial to the 102 soldiers from Hawaii who were killed in World War I. Built in Hawaiian beaux arts architecture, the memorial&rsquo;s centerpiece is the 20-foot tall entrance arch featuring four stone eagles. </p>
                <? include ("../../theme/text.html") ?>
                <p>City planners decided during the late 1920s that they wanted a &ldquo;living memorial&rdquo; and that an Olympic-sized swimming pool would be fitting. During that time, all of Hawaii&rsquo;s competitive swimming events took place in the open waters of Honolulu Harbor. So an enclosed pool was considered to be a worthy public facility. Inside the memorial, a 100 meter by 40 meter saltwater swimming pool was built that was used as a recreational gathering spot by local residents as well as a training pool by Olympian swimmers Duke Kahanamoku, Buster Crabbe and Johnny Weissmuller. </p>
                <p>In 1979, the natatorium was closed for safety reasons. In the years after, the site was neglected until in 2000, its exterior fa&ccedil;ade was renovated. The archway of the natatorium can be viewed, but the inside area where the pool is located remains closed. </p>
                
