                <h2 class="small">Mount Tantalus, Oahu</h2>
                <p>Tantalus is an extinct cinder cone, located at the foothills of the Ko'olau Mountain Range in Honolulu. The Tantalus mountain summit is a prime example of the climb to the top being the best part. Whether on foot or by bike or car, breathtaking views await after every turn on the twisting Round Top Drive to the summit.</p>
                <? include ("../../theme/text.html") ?>
                <p>This scenic route leading through lush and dense vegetation offers panoramic views of Diamond Head and Waikiki in the east to downtown Honolulu and Pearl Harbor in the west. Tantalus is also a popular destination to view the city lights at night, and the sunset views from up here are equally amazing. </p>
                <p><a href="../attractions/puuualakaastatepark.php">Pu'u Ualaka'a State Park</a>, located in a forested region of Tantalus is a popular, yet tranquil place to enjoy a picnic and the views. It is the most acclaimed spot to take in the view from the mountain. The park also features beautiful flora, such as tropical guava and jacaranda trees, as well as other flowers and plants.</p>
                
