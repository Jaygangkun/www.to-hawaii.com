                <h2 class="small">Makapu'u Lookout, Oahu</h2>
                <p>The Makapu'u Point Lookout is located just off Kalanianaole Highway on Oahu's southeastern tip. There is a parking lot and a paved walkway that leads to an area that overlooks Makapu'u Beach Park, the sea cliffs, as well as <a href="../attractions/rabbitisland.php">Rabbit Island</a> (also known as Manana Island) and Kaohikaipu Island. </p>
                <? include ("../../theme/text.html") ?>
                <p>The lookout point is located within the 38-acre Makapu'u Point State Wayside Park, which also encompasses Makapu'u Head and the Makapu'u Point lighthouse. The lighthouse can't be seen from the lookout. To get there, one has to walk up the paved road that begins just below the entry to the lookout (on the Hawaii Kai side of the lookout). It's a 1.75-mile (2.8 km) trail. </p>
                
