                <h2 class="small">Kaneohe Sandbar (Ahu O Laka), Oahu</h2>
                <p>Heading down to Kaneohe Bay? Why not spend the day at the bar instead? Admittedly you're obliged to bring your own chairs and even refreshments. In fact, it's recommended that you bring your own shade too, a hat or an umbrella. Despite these potential drawbacks, at this bar you at least have the benefit of being surrounded by the ocean. Why? Because at Kaneohe Bay one of the most popular spots is in fact the Kaneohe <em>Sand</em>bar!</p>
                <? include ("../../theme/text.html") ?>
                <p>                  The only one of its kind in all of Hawaii, the sandbar is located about fifteen minutes from Heeia Boat Harbor if you do the journey in a small sail or motorboat or kayak. The sandbar is &ldquo;open&rdquo; at low tide when this stretch of sand is partially exposed to the sun's dazzling rays. It attracts plenty of bathers and sailors alike, eager to get away from the &ldquo;mainland&rdquo; for a lounging spot here - complete with a picnic, or a game of Frisbee or volleyball. At high tide the sandbar is completely submerged, so check the tides before planning a visit.</p>
                <p>                  However, for some people  high tide at the sandbar brings an altogether different, but equally pleasurable experience. The shallow on the sandbar can be an ideal place for snorkeling. Marine life in this temporary mini eco-system include tiny cornet fish and flounders. These can be seen attempting to blend in with the sand beneath them by those snorkelers with eagle eyes! At the edges of the sandbar is a deep drop-off so those feeling a little more adventurous and with a little more experience can snorkel to greater depths. Just keep in mind that Kaneohe Bay is also a favorite breeding ground for hammerhead sharks (which are known to be usually harmless to humans, but may still look scary if you happen to be next to one on the water).</p>
                <p>                  The view of Kaneohe Bay from the sandbar is nice. Coconut Island, Kekepa Island and Chinaman's Hat Island can also all be seen from this vantage point, as of course, can the majestic Koolau Mountains. So for a great half day out (depending on tides!), head to Kaneohe Bay's sandbar. Just remember the sun screen and refreshments and of course a bag to take your trash with you when you leave. </p>
                <p>The sandbar is known as Ahu O Laka in the Hawaiian language, meaning Altar of Laka. It is about 1 mile (1.6 km) wide and 3 miles (4.8 km) long.</p>

