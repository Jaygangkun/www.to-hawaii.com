                <h2 class="small">Honolulu Harbor, Oahu</h2>
                <p>First used as a port over 200 years ago, Honolulu Harbor (<a href="../../oahu/attractions/more_honoluluharbor_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/attractions/p8.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) has grown from a small 12th century  landing point for the first North American and European visitors to Hawaii, into an expansive harbor operating as the gateway to the modern metropolis of Honolulu. </p>
                <p>                  The port itself at Honolulu Harbor was really born in 1825 after a sunken ship was used as a wharf! Eight years later a real dock was constructed and by 1840 efforts had begun to deepen the area, creating a more suitable port. By 1905 the harbor had grown to over 3,500 feet (1,067 m) long, with the modern day 45 foot (13.7 m) deep main channel and 30 berth facilities that allow for the huge amount of commerce and passengers that pass through Honolulu Harbor each year.</p>
                <? include ("../../theme/text.html") ?>
                <p>                  These days the port receives over 500,000 cruise ship passengers annually, handling as much as $3.3 billion in imports and $200 million in exports! This makes for a thriving port area within easy reach of the bright lights and excitement of downtown Honolulu. </p>
                <p>                  The Aloha Tower Marketplace complex with its wide ranging retail outlets and restaurants sits at the center of the harbor experience for many. The complex houses some 60 stores making it the ideal place to pick up gifts or simply browse around for a few hours, and if all the shopping gets your stomach rumbling, some of the restaurants in the complex are said to be amongst the best in Hawaii. Another famous landmark that is located here is the Aloha Tower. There is a viewing platform at the top, which is the best place for a spectacular view of the harbor.</p>
                <p>                  Unlike some other ports, Honolulu Harbor is so much more than a just gateway; it offers a veritable feast of entertainment, shopping and nightlife all with a wonderful, port-side atmosphere.</p>
                
