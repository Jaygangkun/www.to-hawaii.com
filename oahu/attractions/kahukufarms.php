                        <h2 class="small">Kahuku Farms, Oahu</h2>
                        <p>Kahuku Farms (<a href="../../oahu/attractions/p23.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located just north of Kahuku on Oahu's North Shore. They offer a variety of foods that are grown on the farm, as well as farm tours. Their farm caf&eacute; is situated right along Kamehameha Highway. There are many picnic tables under a shaded outdoor dining area. Right next to it one can see how they grow their lettuce. The farm caf&eacute; menu features a variety of smoothies, lemonade, iced tea, hot coffee, sandwiches, soups, salads and desserts, including ice creams and banana bread. One can also purchase a variety of culinary items (including jellies, bread spreads and a salad dressing) and body butters, soaps and lotions, as well as farm-grown chocolate. </p>
                        <p>Besides dining, a reason to visit Kahuku Farms is to do their farm tour - a tractor-pulled wagon ride through their farm where guests learn about the crops and history of this place.</p>
                        <p>Hours: Open daily
                          <time itemprop="OpeningHoursSpecification" datetime="Mo,We,Th,Fr,Sa,Su 08:30-16:30">from 11 am to 4 pm </time>
                          except Tuesdays when they are closed.</p>
