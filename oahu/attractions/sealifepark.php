              <h2 class="small">Sea Life Park Hawaii, Oahu</h2>
              <p>Located on Oahu&rsquo;s southeastern tip by Makapuu Point, just 15 miles east of Waikiki, Sea Life Park is Hawaii&rsquo;s premier marine attraction. Here you can enjoy up-close views of dolphins, sea lions, manta rays, penguins, stingrays and many other marine animals. Plus you&rsquo;ll have a chance to see the world&rsquo;s only &ldquo;Wolphin,&rdquo; a mix between a whale and a dolphin. </p>
              <p>Explore the park and say hello to the marine animals, sit back and watch the thrilling shows, or if you want to get wet, you can swim with dolphins in the park&rsquo;s large lagoon. They even offer an underwater trek that allows you to walk 18 feet down among many colorful tropical fish.</p>
              <? include ("../../theme/text.html") ?>
              <p><strong>Attractions at Sea Life Park include:</strong></p>
              <p class="subtitle">Dolphin Cove </p>
              <p>Watch a couple of dolphins perform stunning acrobatics in an ocean-side lagoon that even features its own private island. You&rsquo;ll sit in a large open-air theater and can also enjoy great coastline views. </p>
              <p class="subtitle">Hawaiian Reef</p>
              <p>This huge reef aquarium offers up-close views of Hawaii&rsquo;s underwater ecosystem. You&rsquo;ll get to see more than 2,000 reef animals, including turtles, colorful tropical fish, stingrays and even sharks.</p>
              <p class="subtitle">Stingray Lagoon</p>
              <p>Get face to face with these fascinating creatures of the ocean. This lagoon provides an authentic environment for these shy marine animals.</p>
              <p class="subtitle">Sea Turtle Lagoon</p>
              <p>Not only is this lagoon an interesting exhibit &ndash; it is also a breeding sanctuary for the endangered Hawaiian green sea turtles (honu). To date, more than 2,200 hatchlings have been produced here and many of them have been released to the open ocean. </p>
              <p class="subtitle">Hawaiian Ocean Theater</p>
              <p>In the center of this theater is a large glass tank and from the benches surrounding it you&rsquo;ll be able to watch dolphins perform from above and below the surface of the water. You&rsquo;ll also learn about the newest training techniques and Sea Life Park&rsquo;s conservation efforts.</p>
              <p class="subtitle">Penguin Habitat</p>
              <p>Here you can see 11 Humboldt penguins, which are native to South America and also an endangered species. This habitat at Sea Life Park serves as a comfortable breeding site.</p>
              <p class="subtitle">Rocky Shores</p>
              <p>This exhibit showcases Hawaii&rsquo;s intertidal wildlife. Intertidal is the area that is sometimes land and sometimes sea. You&rsquo;ll get to see animals that have made this intertidal zone their home.</p>
              <p>The general admission package at Sea Life Park gives you access to all of the abovementioned attractions. For the adventurous among you, the park also offers a couple of more hands-on packages, where you&rsquo;ll even have the chance to get in the water with the animals. Here is an overview of the other packages:</p>
              <p class="subtitle">Dolphin Encounter</p>
              <p>If you&rsquo;ve ever dreamed of interacting closely with dolphins, here&rsquo;s your chance! You&rsquo;ll stand in the pool on a waist-deep platform and be able to caress and kiss the dolphins and watch them perform their stunning tricks. If you feel comfortable, you may also leave the platform and venture into slightly deeper water to participate in some fun activities with the dolphins.</p>
              <p>Please note that the minimum age to participate in this activity is 1 year. Children under the age of 6 are free of charge and children from 6 to 12 pay the children fee (children younger than 8 must come with a paying adult per child). Pregnant women may not participate in this program.</p>
              <p>Program duration: 45 minutes<br />
                General park admission included in the price </p>
              <p class="subtitle">Dolphin Swim Adventure</p>
              <p>With this program, you&rsquo;ll enjoy an exciting belly-ride, a memorable kiss and some free time with these magnificent mammals. You&rsquo;ll be able to touch the dolphins, pose for a big kiss, perform several fun activities and you&rsquo;ll even have the chance to view them underwater. Your trainer will tell you lots of interesting facts and info about the dolphins.</p>
              <p>Please note that the minimum age to participate in this activity is 8 years. Children from 8 to 12 years of age must be accompanied by one paying adult per child. Pregnant women may not participate in this activity.</p>
              <p>Program duration: 45 minutes<br />
                General park admission included in the price </p>
              <p class="subtitle">Dolphin Royal Swim</p>
              <p>Ready for the royal swim? This dolphin package is a highlight of Sea Life Park Hawaii. After a loving handshake and a kiss on your cheek by the two dolphins, you are in for an exciting dorsal fin ride. After that get ready for the foot push &ndash; you&rsquo;ll feel all the dolphins&rsquo; energy and strength as they push you across the water from the bottom of your feet.</p>
              <p>Please not that the minimum age to participate in this activity is 8 years (minimum height of 4 feet). Children from 8 to 12 years of age must come with a paying adult (per child) as a companion. Pregnant women may not participate in this program.</p>
              <p>Program duration: 45 minutes<br />
                General park admission included in the price </p>
              <p class="subtitle">Hawaiian Ray Encounter</p>
              <p>Did you know that rays are actually the docile distant cousins of sharks? Come face to face with these amazing marine creatures right here at the park. You&rsquo;ll get to snorkel by their side and experience their mysterious personality. You may touch and even feed them. </p>
              <p>Please note that the minimum age to participate in this program is 4 years (minimum height of 40 inches). Children 4 to 6 years of age must be accompanied by one paying adult per child as a companion. Pregnant women may not participate in this activity.</p>
              <p>Program duration: 40 minutes (25 minutes under water)<br />
                General park admission included in the price</p>
              <p class="subtitle">Sea Lion Discovery Swim</p>
              <p>Enjoy some close moments with an adorable and loving family of sea lions. You may even pet, hug and kiss them. Watch them carry out their daring water acrobatics and stunning jumps. You can also help their trainer give out instructions using a special signal system.</p>
              <p>Please note that the minimum age to participate in this activity is 8 years. Children 8 to 12 years of age must come with one paying adult per child as a companion. Pregnant women may not participate in this program.</p>
              <p>Program duration: 30 minutes<br />
                General park admission included in the price </p>
              <p class="subtitle">Sea Trek Adventure</p>
              <p>This is a unique opportunity for an underwater stroll at about 18 feet below the surface in the Hawaiian Reef Tank. You&rsquo;ll encounter eels, which undoubtedly belong to some of the strangest species on Earth; stingrays, which will amaze you with their prehistoric body shape; enchanting sea turtles and a variety of colorful tropical fish. You&rsquo;ll even have the chance to feed these fascinating marine creatures of the deep.</p>
              <p>Please note that the minimum age to participate in this activity is 13 years. Pregnant women may not participate.</p>
              <p>Program duration: 40 minutes<br />
                General park admission included in the price.</p>
              <p><strong>Sea Life Park Hours</strong></p>
              <p>The park is open daily from
                <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 10:30-17:00">10:30 am to 5 pm.</time>
              </p>
              <p><strong>Sea Life Park Admission</strong></p>
              <p>The entrance fee is $29 per adult and $19 per child, age 3-11 years old. The prices for the individual packages vary. For more info, see their website at sealifeparkhawaii.com.</p>
              
