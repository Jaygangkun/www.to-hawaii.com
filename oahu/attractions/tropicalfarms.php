                <h2 class="small">Tropical Farms Macadamia Nut Farm, Oahu</h2>
                <p>Tropical Farms on Oahu&rsquo;s windward (east) coast is a destination that&rsquo;s off the beaten path (<a href="../../oahu/attractions/more_macadamianutfarm_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>). Most visitors to the island only get to see it if they take part in a greater circle island bus tour, which they usually book at a local tour operator. So if you don&rsquo;t know about this place, you wouldn&rsquo;t rent a car to get here. But it&rsquo;s definitely worth a visit, not only for the many delicious goodies you can buy, but also because you can learn a great deal about Hawaiian culture.</p>
                <? include ("../../theme/text.html") ?>
                <p>They grow their macadamia nuts and other fruits right here and you can enjoy flavors that aren&rsquo;t available in stores in Waikiki, such as cinnamon macadamias. They are freshly made on this farm. And the good thing is, you can try them for free. They even offer free samples of Kona coffee in their store. Besides macadamia nuts, they also sell jewelry, art, creams, aromatic oils and other souvenirs.</p>
                <p>Besides browsing through the farm&rsquo;s store, they also offer a tour of the farm, called the Alii Tour. The tour lasts about one hour and takes you to the plantation and fishpond. It also includes a demonstration on how the Hawaiians lived off the land. The tour comes complete with a super funny guide, aptly named &ldquo;The Chief.&rdquo; </p>
                <p>He&rsquo;ll first load everyone into an old bus and drive you through the farm, pointing out types of plants and what they are used for. Then you&rsquo;ll head to a little amphitheater for a show, where you&rsquo;ll learn more about Hawaiian foods and customs. Your guide will even show you how to make fire by rubbing sticks together and how to open a coconut. You can try yourself and get some of the good stuff out of the fruit and snack on it.</p>
                <p>After that, your guide will take you on a short boat ride around the fishpond. He&rsquo;ll tell you some more about the history of the area and include recent events, such as the filming of popular Hollywood movies. Quite a few movies and TV series have been filmed in this area, including <em>Gilligan's Island, Fantasy Island, 50 First Dates, Lost, Tears of the Sun </em>and<em> Along Came Polly</em>.</p>
                <p>Back on land, you&rsquo;ll enjoy a tour of the fruit trees. Now is your chance to try guavas and star fruits right off the tree. Nothing is better than a fruit that&rsquo;s picked fully ripe from the tree and eaten right away. This completes the tour and back in the store you can then browse around some more.</p>
                <p><strong>Tropical Farms Hours</strong></p>
                <p>Open 7 days a week,
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 9:05-17:00">9:30 a.m. to 5:00 p.m.</time>
                </p>
                <p><strong>Tropical Farms Admission</strong></p>
                <p>Entry into the shop is free. They also have many free coffee and free macadamia nut samples.</p>
                
