                <h2 class="small">Polynesian Cultural Center, Oahu</h2>
                <p>The Polynesian Cultural Center, located on Oahu&rsquo;s northeastern coast in Laie, is Oahu&rsquo;s number one paid visitor attraction. In a nutshell, it is a 42-acre (170,000 m&sup2;) open-air park, a living museum, which showcases six Polynesian island nations and cultures in traditional villages and exhibits. By walking through this park, you can learn about six Polynesian cultures in just one day, without having to fly across the Pacific.</p>
                <p>The featured Polynesian islands include: Fiji, Tonga, Samoa, Aotearoa (Maori New Zealand), Tahiti and Hawaii. There is also a special Rapa Nui (Easter Island) exhibit. Activities and demonstrations take place throughout the day. As you visit each village, you can take part in traditional arts and crafts, view dance performances, visit educational exhibits and displays, take a canoe ride through the park&rsquo;s lagoon, get a removable Polynesian tattoo, take a tour of the nearby Mormon temple in Laie, learn to dance the hula, and much more. </p>
                <? include ("../../theme/text.html") ?>
                <p>At 2:30 pm  you can watch the Canoe Pageant, a dance performance that takes place daily in the Center&rsquo;s lagoon. Watch how island natives perform  dances in traditional costumes on double-hulled canoes.</p>
                <p>The Polynesian Cultural Center also has a 600-seat IMAX&trade; Theater, currently featuring the movie <em>Hawaiian Journey</em>, shown twice daily  on a huge screen measuring 65 feet high (19.8 m) and 93 feet wide (28.4 m). The movie is an impressive, big-screen tribute to the splendors of the Hawaiian Islands.</p>
                <p>The Center is also known for its award-winning Ali'i Luau, a traditional Hawaiian feast, which starts at 5 pm. Great entertainment and great food - two things that make this place the best luau on Oahu. This is your chance to try traditional Polynesian fare, including lomi lomi salmon, kalua pig (roasted in an underground oven), mahi mahi (island fish), haupia (coconut pudding) and poi, an island staple made from taro roots. If you have less adventurous taste buds, continental American foods are available as well. Besides the luau, visitors can also opt to dine in the Center&rsquo;s restaurant instead of participating in the luau.</p>
                <p>The day is topped off with <em>H&#257;: Breath of Life</em>, a 90-minute award-winning night show, which takes place in the Center&rsquo;s 2,800-seat Pacific Theater. The Polynesian Cultural Center is the only place on the island that casts more than 100 performers nightly.</p>
                <p><strong>Polynesian Cultural Center Hours &amp; Admission</strong></p>
                <p>The Polynesian Cultural Center is open Monday to Saturday (closed Sundays, Thanksgiving and Christmas holidays). Other blackout dates may apply.</p>
                <p>The parking lot opens at 11:45 am. Island tours and cultural presentations begin at 12 pm with other island activities spread throughout the afternoon. The island villages close at 6 pm. The luau begins at 5 pm and the Horizons night show begins at 7:30 pm. </p>
                <p>Admission: Ticket prices range from $45 to $225 per person, depending on which package you choose. For details, see polynesia.com</p>
                
