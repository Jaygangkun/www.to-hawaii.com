                <h2 class="small">Lanikai Pillbox Hike (Ka'iwa Ridge Trail), Oahu</h2>
                <p>The Pillbox Hike in Lanikai (<a href="../../oahu/attractions/p17.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), also known as the Ka'iwa Ridge Trail, is a fun thing to do before or after visiting Lanikai Beach. The trailhead is easily accessible by car or if you come here with the bus (Route #70). It's a short hike to the top (about 20-30 minutes) and the views are amazing. </p>
                <? include ("../../theme/text.html") ?>
                <p>The trail overlooks the upscale neighborhood of Lanikai and its amazing white-sand beach, as well as Kailua and Waimanalo Beach. The two islands on the horizon are the Mokulua Islands, a popular kayaking destination. </p>
                <p>Along the way are two military bunkers (pillboxes) that people use as lookout points. There isn't much shade on this hike and the trail can be slippery, especially if it has recently rained. It also has a few areas that are narrow (about 4 feet wide) and with a steep drop-off, so be cautious in these areas.</p>
                
                <h2 class="big">Lanikai Pillbox Trail Overview</h2>
                <ul type="disc" class="mylist">
                  <li>One-hour roundtrip hike with great views of Lanikai Beach and Kailua</li>
                  <li>Steep trail, narrow in some areas with steep drop-off</li>
                  <li>Trail leads to two pillboxes (bunkers)</li>
                  <li>Popular hike, can get crowded especially on weekends and holidays</li>
                  <li>Street parking along Kaelepulu Drive</li>
                  <li>Not much shade - bring water and sunscreen</li>
                  <li>No facilities, no restrooms here</li></ul>
