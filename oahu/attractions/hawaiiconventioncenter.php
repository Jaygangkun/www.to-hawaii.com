                <h2 class="small">Hawaii Convention Center, Oahu</h2>
                <p>With more than 60% of the building open to the sky, the Hawaii Convention Center (<a href="../../oahu/attractions/more_conventioncenter_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>) in Honolulu gives a similar vibe to the culture of Hawaii, free and welcoming. The contrast between high beams and open space is reminiscent of palm trees, waves and Polynesian sailing canoes. With so much open space, the building is shaded naturally with real palm trees and given an authentic feel with fountains made to look like streams. Simply viewing the Hawaii Convention Center from the outside will tell a visitor a lot about the types of people that inhabit Hawaii as a whole. </p>
                <? include ("../../theme/text.html") ?>
                <p>The International Association of Exhibition Management voted the Hawaii Convention Center the most beautiful in the world, a declaration that many would easily agree upon. With a 36,000 square-foot (3,345 sq. m) ballroom, 12 executive conference rooms, two presentation theatres, press rooms and a telemarketing center, Honolulu&rsquo;s Convention Center is the location of many of the city&rsquo;s important happenings, as well as the location of many national and international conferences. </p>
                
                <p>After a unanimous vote, LMN Architects was chosen to build the Center in 1994. LMN was favored largely due to their sensitivity to maintaining Hawaiian culture. The project was not completed until 1997, not opened until 1998 and came to an overall cost of 200 million dollars. While the Center was an expensive structure, many would agree that it is a successful representation of Hawaiian culture and history. The location was chosen largely out of convenience and has over 30,000 hotel rooms within a one-mile radius of the Center. </p>
                <p>On account of its convenience and tropical location, the Hawaii Convention Center is a popular site for business meetings from all over the country with topics that range from geography bees and the World Association of Bencher&rsquo;s and Dead Lifters to The World Congress on Zero Emissions Initiatives. The Center has been awarded the 2010 Best in Business Travel award presented by <em>Business Travel Magazine</em> for the second year in a row. With such a wide range of different types of businesses and associations coming to the Convention Center, the catering and event planning staff stays busy year-round making preparations for upcoming events. </p>
                <p>With rooftop gardens and a two million dollar art collection, the Convention Center is not just a site for business meetings. If visiting Honolulu, the Convention Center is a place not to be missed whether you come in coat and tie or aloha attire. The mission of the Convention Center is to promote economic benefits to Hawaiian citizens, enhance the quality of life and to treat guests to the finest of convention and meeting experiences in the spirit of ho'okipa, meaning hospitality. As the slogan of the Hawaii Convention Center states, &ldquo;Bring us your people and we&rsquo;ll bring them to life.</p>
                
