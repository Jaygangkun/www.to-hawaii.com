                <h2 class="small">Lanai Lookout, Oahu</h2>
                <p>Lanai Lookout (<a href="../../oahu/attractions/p19.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located on Oahu's southeast shore right on Kalanianaole Highway. It is one of three main roadside lookout points on this scenic stretch of coastline (the other two are the <a href="../attractions/makapuulookout.php">Makapuu Lookout</a> and the <a href="../attractions/halonablowhole.php">Halona Blowhole</a> Lookout). The lookout point has a small parking lot carved into the sea cliff. On a clear day you can see three neighbor islands from here, not just the island of Lanai, but Molokai and Maui as well. Maui will be a bit tricky to make out because it is  behind Molokai. And the main feature of Maui that you'll be able to see is Mt. Haleakala (Maui's 10,023 foot tall mountain). </p>
