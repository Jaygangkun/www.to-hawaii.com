                <h2 class="small">Aloha Tower, Oahu</h2>
                <p>Just as hundreds of thousands of immigrants arriving to the United States by sea from Europe and elsewhere were greeted by the legendary Statue of Liberty in New York, new immigrants and visitors to Hawaii were welcomed by the wonderfully imposing Aloha Tower at Honolulu Harbor. Built between the wars and opened in 1926 at Honolulu Harbor's ninth pier, the tower has welcomed a great variety of vessels to the Hawaiian Islands ever since. The tower stands ten stories high, with a further 40 feet (12 m) provided by a flag pole on the roof. This height made it the tallest structure in Hawaii at the time of its construction and for four decades thereafter.</p><? include ("../../theme/text.html") ?>
                
                <p>                  These days, visitors to the tower are invited to enjoy its Hawaiian Gothic style architecture and contemplate its significance in the history of the United States, and indeed, the world! The tower gained strategic significance after the 1941 attack on Pearl Harbor. This led to the defensive positioning of Coast Guardsmen at its base to protect the tower from becoming occupied. Later the tower was painted in camouflage to prevent its identification, which of course would have given away the location of the harbor to enemy fighters, in the dark night sky.</p>
                <p>
                  Once inside the tower, modern-day visitors are treated to spectacular views of Honolulu Harbor and the surrounding areas from the tenth floor observation deck (open daily from
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 09:30-17:00">9:30 am to 5 pm</time>
                  , admission is free). To get to the observation deck one has to take a small, vintage elevator (if you're claustrophobic, you may want to avoid the ride up).</p>
                <p>                  The Aloha Tower's clock was installed in 1926 when then the tower was completed. Back then it was the biggest clock in Hawaii and even one of the largest in the United States. It was built with precise German movement by the E. Howard Clock Company of Boston. It weighs seven tons and up until today it is weight driven, with its pendulum keeping perfect time.</p>
                <p>                  Many events  take place at the tower's adjacent Aloha Tower Marketplace complex throughout the year. Also located here are a variety of retail outlets and restaurants. </p>
                
