                <h2 class="small">Halona Blowhole, Oahu</h2>
                <p>The Halona Blowhole (<a href="../../oahu/attractions/more_blowhole_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a popular attraction on Oahu's southeastern shore near <a href="../beaches/hanaumabaypreserve.php">Hanauma Bay</a>. It's a scenic area with steep sea cliffs and great views. The blowhole can be seen from the lookout point, which is located right on the highway (a parking lot is available). </p>
                <p>The blowhole is an underwater lava tube with an opening on both sides of it. The water from the ocean enters the tube with each wave and as the tube becomes narrower, the pressure in it increases and the water shoots up into the air above the lava terrace, sometimes up to 30 feet (9 m).</p>
                <? include ("../../theme/text.html") ?>
                <p>This natural phenomenon can best be observed when the surf is up. The higher the waves, the higher the blowhole shoots water into the air. Some people hike down to the blowhole to be closer to it. But this is not a good idea. The sea cliffs are steep, slippery and sharp. </p>
                <p>The area around the blowhole is dangerous and some people have died while trying to sit on the blowhole or holding parts of their bodies into the stream of water. So it is best to stay at the lookout point and enjoy the views from a safe distance.</p>
                <p>Halona means &quot;peering place&quot; in the Hawaiian language. The views from here are beautiful. On a clear day, the neighbor islands of Molokai, Maui and Lanai are visible on the horizon. In the winter months, between November and March, humpback whales can be spotted offshore. Many sea turtles frequent the area as well. The azure blue water is so clear that it is easy to spot them from the lookout point. </p>
                <p>To the right (west) of the blowhole is <a href="../beaches/halona-beach-cove.php">Halona Beach Cove</a>, also known as Eternity Beach (it was made popular in the movie <em>From Here to Eternity</em>).</p>
                
