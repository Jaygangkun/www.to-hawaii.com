                <h2 class="small">Helemano Plantation, Oahu</h2>
                <p>Located right next to another popular visitor attraction, the Dole Plantation, the Helemano Plantation makes another fun stop especially if you have kids. Helemano provides a &ldquo;real world&rdquo; environment for developmentally disabled people by offering training and employment opportunities. </p>
                <p>Visitors to the plantation can visit the gift shop, Country Inn Restaurant, bake shop and farm. A stroll through the garden is fun for kids. There are many colorful displays and small huts they can walk into.<br />
                  <br />
                  The restaurant is open daily for lunch and offers two all-you-can-eat buffets. At the bake shop you'll find bread puddings, homemade cookies, manapuas and other local treats. </p>
