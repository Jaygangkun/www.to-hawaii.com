                <h2 class="small">Hawaii Theatre, Oahu</h2>
                <p>Built in 1922 as a vaudeville and movie theatre, the Hawaii Theatre now stands as Honolulu's primary arts centre, showcasing the finest in local and international cinema, theatre and live music. Its original neo-classical design by Honolulu residents and architects Walter Emory and Marshall Webb, saw the theatre christened the &lsquo;Pride of the Pacific&rsquo; on its opening. The venue continued to thrive, largely as a movie theatre, until the 1950s when the advent of television sets saw numbers for moviegoers begin to fall. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  The following thirty years saw the building's usage decline, its doors eventually closing in 1984. Local residents refused to see this as the end however, forming the not-for-profit &lsquo;Hawaii Theatre Center&rsquo; to purchase the building in 1986 and continuing to run shows and events until closing it again in 1989, this time for renovations. Having raised the necessary funds, the $20.8 million renovation of the interior took place in 1994, with exterior works continuing until 2005.</p>
                <p>                  In 1996 the theatre reopened to the public, with the scintillating style it retains to this day. Varied programs of events are staged throughout the year. Whether you desire to soak up local culture with traditional Hawaiian performances, or sample some fantastic international acts, the Hawaii Theatre has returned to its rightful place as the &lsquo;Pride of the Pacific.&rsquo; Already 2011 has seen South Africa's Ladysmith Black Mambazo take to the stage, whilst &lsquo;25 years of Hulu&rsquo; saw Hawaii's most celebrated dance troop return to the Hawaii for an enthralling retrospective of the companies work over the last quarter century. </p>
                <p>                  These high caliber acts run alongside more grass-roots events such as the Hawaii Theatre Slam, which runs on selected nights throughout the 2010-11 season. Such programs continue to ensure the theatre provides a fantastic experience for the entire family. </p>
                <p>                  Apart from the performances and performers themselves, it is the architecture, which employs Byzantine, Corinthian and Moorish ornamentation to the neo-classical exterior and a veritable feast of Beaux-Arts inspired designs for the interior, that attracts visitors to the theatre. Guided tours take place weekly, every Tuesday from 11 am and include a demonstration of the splendid Orchestral Theatre pipe organ.<br />
                </p>

