                <h2 class="small">Restaurant Row, Oahu</h2>
                <p>Located near downtown Honolulu on Ala Moana Boulevard, Restaurant Row (<a href="../../oahu/attractions/p16.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>, <a href="../../oahu/attractions/more_restaurantrow_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a business as well as a restaurant and entertainment complex. With dozens of restaurants and various shops, Restaurant Row is popular mainly among local residents. </p>
                <p>While the area has been around for a long time, many restaurants have come and gone over the years. Some even refer to the Row as &ldquo;death row&rdquo; because it is difficult for many restaurants to stay afloat with all the surrounding competition at the large Ala Moana Center and the many shops in nearby Waikiki. </p>
