                <p>A few restaurants that are noteworthy are Ruth&rsquo;s Chris Steakhouse, featuring a variety of beef entrees and other dishes. Hiroshi Eurasian Tapas is a great place to have a little taste of everything, from carpaccio to bacon-wrapped jumbo shrimp. Ocean&rsquo;s is a popular night club that is open until 4 a.m. (but closed on Sundays and Mondays), featuring both drinks and a variety of foods. If you&rsquo;re just passing through the area or need a break from shopping, there are two caf&eacute;s &ndash; Kafe Europa and For the Love of Coffee.</p>
                
