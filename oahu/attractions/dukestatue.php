                <h2 class="small">Duke Kahanamoku Statue, Oahu</h2>
                <p>A popular statue in Waikiki and one of the most photographed spots is the Duke Kahanamoku Statue, which is standing right on <a href="../../oahu/beaches/kuhiobeach.php">Kuhio Beach</a>, a part of <a href="../../oahu/beaches/waikiki-beach.php">Waikiki Beach</a> on Oahu. Duke&rsquo;s bronze statue welcomes you with open arms and on most days, he is adorned with flower leis that people hang over his arms and lay in front of his feet.</p>
                
                <p> Duke is perhaps the most remembered and respected Hawaiian surfer, a champion Olympic swimmer and a master of canoeing &ndash; a true Hawaiian hero and waterman. He epitomized Hawaiian athleticism and helped the surf sport reach unbelievable popularity. The Duke is credited with bringing surfing to the mainland and possibly even to Australia. That&rsquo;s why is also known as &ldquo;The Father of Modern Surfing.&rdquo;</p>
                <? include ("../../theme/text.html") ?>
                <p>Duke Paoa Kahanamoku (1890-1968), was born and raised in Waikiki. In 1905, Duke and his surfer buddies started a surfing club that was to influence many, called Hui Nalu (Club of Waves). Duke and his comrades were known as the &quot;Beach Boys of Waikiki.&quot; His legend began when he broke the world record in the 100-yard freestyle during his first competition. He later won three gold, two silver and one bronze medal in four Olympic Games between 1912 and 1922. </p>
                <p>Besides surfing and swimming, Duke also acted in Hollywood movies and he was later elected Sheriff and official greeter of the City and County of Honolulu. He is also recognized as the &quot;Hawaiian Ambassador of Aloha.&quot; Duke died from a heart attack at the age of 77, on January 22, 1968. He was buried at sea. </p>
                <p>The Duke Statue is located right next to the Waikiki Police Station on Kalakaua Avenue, across the street from the Hyatt Regency hotel. To see authentic photos and memorabilia of Duke and the Waikiki Beach Boys, visit the Duke&rsquo;s Canoe Club (also a restaurant) in the Outrigger Waikiki hotel right on Waikiki Beach.</p>
                
