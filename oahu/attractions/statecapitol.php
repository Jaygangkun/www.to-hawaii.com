                <h2 class="small">Hawaii State Capitol, Oahu</h2>
                <p>Located in downtown Honolulu on the island of Oahu, the Hawaii State Capitol (<a href="../../oahu/attractions/p10.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) was commissioned and dedicated by John A. Burns, second Governor of Hawaii. It opened on March 15, 1969, replacing the former statehouse, the Iolani Palace. The Hawaii State Legislature convenes in the building. The main tenants are the Governor of Hawaii and Lieutenant Governor of Hawaii, as well as all legislative offices. </p>
                       <? include ("../../theme/text.html") ?>         
                <p>Called Hawaiian international architecture, the Hawaii State Capitol is an American adaptation of the Bauhaus style. Unlike other state capitols that are modeled after the U.S. Capitol, Hawaii&rsquo;s capitol architectural features symbolize various natural aspects of Hawaii. For example, the whole building is surrounded by a reflecting pool, which symbolizes the Pacific Ocean. Also, the two legislative chambers are cone-shaped, representing the volcanoes that formed the Hawaiian Islands. The columns around the building&rsquo;s perimeter have coconut tree shapes. And the building was constructed in an open-air design, allowing sun, rain and wind to enter. </p>
                <p>From the time the building was completed in 1969, the pool surrounding it has had an algae growth problem. The main reason is that brackish water from nearby wells feed the pool. The state has tried to remedy the problem by placing tilapia into the pool and installing an ozone treatment system. Some say that the algae growth has come to symbolize the pollution of the Pacific Ocean.</p>
                
