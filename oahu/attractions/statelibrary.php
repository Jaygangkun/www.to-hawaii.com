                <h2 class="small">Hawaii State Library, Oahu</h2>
                <p>Located in downtown Honolulu next to the <a href="../../oahu/attractions/iolanipalace.php">Iolani Palace</a>, the Hawaii State Library (<a href="../../oahu/attractions/p5.html" onclick="NewWindow(this.href,'name','2550','730','yes');return false">view panorama</a>) serves as the seat of the Hawaii State Public Library System, which is the only statewide library system and one of the largest in the United States. It is a historic building that was funded by Andrew Carnegie and designed by architect Henry D. Whitfield. The building was completed after two years of construction in 1913. In 1978, the Hawaii State Library building was added to the National Register of Historic Places. </p>
                <? include ("../../theme/text.html") ?>
                <p>The building holds more than 525,000 cataloged books, and the entire Hawaii State Public Library System has a collection of more than 3 million books. The nearby Hawaii State Archives has book collections of historical significance to Hawaii. The bronze sculptures at the lawn in front of the building were cast by Barbara Hepworth. They are called Parent I and Young Girl. </p>
