                <h2 class="small">Koko Crater, Oahu</h2>
                <p> Koko Crater (<a href="../../oahu/attractions/more_kokoheadtrail_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/attractions/p1.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>), a tuff cone rising 1,208 feet (368 m), is one of the most popular landmarks on Oahu's south shore. Even from a distance one can see the hiking trail that leads to its peak. A smaller tuff cone nearby called Koko Head  is 642 feet (196 m) in height.</p>
                <? include ("../../theme/text.html") ?>
                <p>To get to the Koko Crater lookout point, one has to climb 1,048 stairs/railroad ties, which were originally built over 60 years ago to get supplies to the military bunkers at the top. The stairs are very steep. There is short, bridge-like area that some people find scary because the railroad tracks where one walks on are above the ground. The alternative route to the right of it is much easier. Within the crater are horse stables and the Koko Crater Botanical Garden, home to many cacti and succulents. </p>
                <p>Koko Crater is also known as Kohelepelepe, which means &quot;labia minor&quot; in the Hawaiian language. It got this name from an ancient legend, which tells of one of the sisters of the volcano goddess Pele named Kapo, who had a magical &quot;flying vagina&quot; that she could send anywhere. When Kamapua'a, the pig god, tried to rape Pele, Kapo came to help her. She distracted Kamapua'a by throwing her vagina to Koko Head, where it made the crater.</p>
                
