                <h2 class="small">Ala Wai Canal, Oahu</h2>
                <p>It may be hard to believe, but where the bustling tourist center of Waikiki now stands, ninety years ago was swampland and rice paddies, with waterlogged fish and duck ponds, not to mention millions of mosquitoes over the stagnant waters. In the mid-1920s, the land was drained and reclaimed, and the Ala Wai Canal (<a href="../../oahu/attractions/p6.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) was built. </p>
                <p>Nowadays, the canal drains up to 150 inches of rainwater, which seeps down from the mountains in Waikiki every year, and it empties into the Pacific Ocean at the Ala Wai Yacht Harbor.</p>
                <? include ("../../theme/text.html") ?>
                <p>The canal marks the entrance to Waikiki, beginning northwest of Kapahulu Avenue (a few blocks from Kapiolani Park and Diamond Head) and running along the length of Waikiki, bordered by Ala Wai Boulevard on the south and west sides, before turning southwest towards the harbor.</p>
                <p>The canal, which runs for one and a half miles, has a long well-lit walkway parallel to the ocean which is often used for jogging and strolling. The waters of the canal are popular with kayakers and canoe paddlers.</p>
                <p>The Ala Wai Canal is home to stick fish, baby kaku, crabs and lost jellyfish and fishermen have taken advantage, but ongoing controversy over sewage and street water runoff have discouraged many from getting too intimate with the waterway. </p>
                <p>The <a href="../attractions/hawaiiconventioncenter.php">Hawaii Convention Center</a> is located on ten acres next to the canal, on Kalakaua Avenue.</p>
                
