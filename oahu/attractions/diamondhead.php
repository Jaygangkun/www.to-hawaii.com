                <h2 class="small">Diamond Head, Oahu</h2>
                <p>Diamond Head (<a href="../../oahu/attractions/more_diamondhead_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/attractions/p18.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is one of the most famous landmarks on Oahu and the entire state of Hawaii. It has once been an active volcano, but hasn&rsquo;t spewed any ashes for more than 150,000 years. The extinct crater and lookout point at the peak of it is a popular hiking destination for many. In fact, Diamond Head is one of the most photographed and painted spots on Oahu, adorning many wall calendars and postcards. </p>
                <? include ("../../theme/text.html") ?>
                <p>Located at the eastern end of Waikiki and right on the ocean, Diamond Head has already made an impression to early sailors. In 1825, British sailors noticed small sparkling calcite crystals in the beach sand nearby the crater and named the mountain Diamond Hill. This name was later changed to Diamond Head. This is how the crater got its English name. </p>
                <p>The Hawaiian name for Diamond Head is Le&lsquo;ahi, a contraction of lae, meaning point of land and &lsquo;ahi, meaning yellowfin tuna. So Le&lsquo;ahi means &ldquo;point (of the) ahi fish.&rdquo; From a distance, the mountain looks like the dorsal fin of the ahi fish, hence its Hawaiian name Le&lsquo;ahi. </p>
                <p>Diamond Head was purchased by the federal government in 1904 for $3,300. In the 1930s, a couple of gun emplacements for coast artillery defenses were built. After the attack on Pearl Harbor in 1941, further gun emplacements, pillboxes and foxholes were built on Diamond Head to be able to better defend Oahu in the future. Many of them still remain on the crater rim and one underground facility, the Birkheimer Tunnel, houses the headquarters for the State Civil Defense Agency. Today, the entire mountain and crater is a state park known as Diamond Head State Monument. </p>
                <p>Diamond Head Crater measures 3,520 feet (1,073 m) across and 760 feet (232 m) in height. A hiking trail leads from the crater up to a lookout point. But be prepared to climb some stairs. In fact, there are two stairs, one has 99 steps and a second one has 76 steps. There&rsquo;s also a 225-foot (69 m) hardly lit tunnel through which you&rsquo;ll have to go. The tunnel is a remnant of the former military use of the crater. So you should be in good physical condition to do the hike. But it&rsquo;s definitely worth the effort once you reach the top, after about 30-40 minutes if you&rsquo;re in good shape. The view is spectacular, especially on a clear and sunny day. </p>
                <p>Bring some water and sunscreen because it will get hot. In case you forget, there is a comfort station at the base of the otherwise undeveloped crater, where you can buy water (water fountains and restrooms are also there, as is a truck that sells hot dogs and hamburgers).</p>
                
