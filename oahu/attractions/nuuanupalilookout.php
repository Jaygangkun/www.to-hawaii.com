                <h2 class="small">Nu'uanu Pali Lookout, Oahu</h2>
                <p>Here's one spot that's a feast for the eyes - Nu'uanu Pali Lookout (<a href="../../oahu/attractions/more_palilookout_photos.html" onclick="NewWindow(this.href,'name','900','600','yes');return false">see more photos</a>, <a href="../../oahu/attractions/p20.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>) on the island of Oahu, Hawaii. The lookout is located on a 1,186-foot high (361 m) mountain pass surrounded by 3,000-foot (914 m) peaks. From up here you'll enjoy a nice bird's eye view of the towns of Kaneohe and Kailua, Kaneohe Bay, Chinaman's Hat Island, the magnificent Ko'olau Mountain Range and the vast expanse of the Pacific Ocean. Often times it's very windy here, so hold on to your hat, hairpieces, small dogs and children :)</p>
                <? include ("../../theme/text.html") ?>
                <p>Not only is Nu'uanu Pali a popular lookout point, but it is also a historic location. It was here where King Kamehameha I fought his last battle in his war to unite all the Hawaiian Islands with an army of 10,000 soldiers. In 1795, a few hundred warriors were driven off the cliff to their death 1,000 feet below by Kamehameha's men. Legend has it that on certain nights, one can still hear the screams of the warriors. According to reports, workers unearthed 80 skulls believed to have belonged to the warriors during a road construction in 1848.</p>
                <p>The Nu'uanu Pali Lookout is easily accessible from the <a href="../scenicdrives/palihighway.php">Pali Highway</a> (Route 61). Just look for the signs on the Honolulu side of the Pali tunnels.  The lookout is open to the public daily from 9 am to 4 pm. There is no admission fee, but it costs  to park.</p>
                
