                <h2 class="small">Rabbit Island, Oahu</h2>
                <p>Rabbit Island, also known as Manana Island, is located off the southeastern shore of Oahu, about &frac34; of a mile (1.2 km) off of Kaupo Beach, just northwest of Makapuu Point. It is a volcanic tuff cone island, encompassing 63 acres (0.25 sq. km) and rising to about 360 feet (110 m) at its highest point.</p>
                
                <p>It was not named after its shape, although many regard the island to be formed in the likeness of a rabbit, but because originally it was the location of a rabbit-raising farm. In 1994, the rabbits were removed because they started destroying the native ecosystem of the island, which is an important seabird breeding area.</p>
                <? include ("../../theme/text.html") ?>
                <p>The islet is now a state protected bird sanctuary and during the spring and summer months thousands of sooty terns, wedge-tailed shearwaters, bulwers petrels, brown noddies and red-tailed tropic birds come to breed here. </p>
                
                <p>Popular with photographers, the island can easily be seen from several beaches along Oahu&rsquo;s windward coast. By hiking to the top of Makapuu Point where the Makapuu Lighthouse is located, one can enjoy a great view of this island as well as a long stretch of Oahu&rsquo;s eastern coastline.</p>
                <p>Where this island really excels though, is as a dive site. With a surrounding water depth of 40-70 feet (12-21 m) and a fantastic visibility which can reach 80 feet (24 m), advanced divers are tempted here by exciting marine life including lobster, triton trumpets, helmet shells, whitetip reef sharks and tiger sharks, plus interesting coral formations. </p>
                <p>The best diving is done from a boat, rather than the small beach on the island. Visiting divers are warned, however, that the sharks here are extremely territorial creatures and have a history of attacks on humans at this site; therefore, divers are advised to leave the water if sharks are spotted.</p>
                <p>Visitors should also note that because Rabbit Island is a protected sanctuary, it is not legal to land there unless you have permission from the Hawaii Department of Land and Natural Resources. </p>
                
