                <h2 class="small">Mokulua Islands, Oahu</h2>
                <p>The Mokuluas (<a href="../../oahu/attractions/p15.html" onclick="NewWindow(this.href,'name','2550','520','yes');return false">view panorama</a>) are twin islands located offshore at Lanikai Beach on Oahu&rsquo;s windward coast. Both islands are seabird sanctuaries protected by the state of Hawaii, but the beach on North Mokulua is open to the public during the day. The islands can easily be reached by renting a kayak on nearby Kailua Beach and paddling about one mile (1.6 km) offshore (which takes about 45 min. to 2 hours depending on the experience of the kayaker and the ocean conditions).</p>
                <? include ("../../theme/text.html") ?>
                <p>North Mokulua Island &ndash; also called Mokulua Nui or Moku Nui &ndash; is a volcanic cone, with two peaks reaching to about 225 feet (69 m) at its highest point. The island itself is about 1,000 feet (305 m) by 800 feet (244 m) in size with steep cliffs on the north, east and southeast sides and a sandy beach on the west side.</p>
                
                <p>South Mokulua Island &ndash; also called Mokulua Iki or Moku Iki &ndash; has just one hump and a maximum elevation of about 150 feet (46 m). It is smaller than Moku Nui, about 875 feet (267 m) by 700 feet (213 m), and it has a rocky coral beach on the west side.</p>
                <p>For many adventurors, the journey out to the Mokulua Islands is as rewarding as the destination. Kailua Beach and Lanikai Beach are two picture-perfect white-sand beaches surrounded by tropical scenery. </p>
                <p>On calm ocean days, it is relatively easy (for paddlers with a little experience) to reach the Mokuluas with a kayak or paddle boat. However, when the surf is up (and it doesn&rsquo;t even have to be up a lot), it can be quite difficult. As the waves approach the islands from the open ocean, they flow around the islands on the right and left and then clash together in front of them (right in the path that one takes to reach the islands). </p>
                <p>Also, the current can be strong on some days and the waves may come from a side angle so that it can be difficult (especially for inexperienced paddlers) to prevent a kayak from flipping over. The tricky thing is also that from shore, the ocean may look calm and flat, but once on a way, the waves are suddenly larger. There are no lifeguards within sight, so inexperienced kayakers should not attempt the crossover to the islands or paddle together with an experienced guide.</p>
                <p>In the waters around Moku Nui, green sea turtles, wedge-tailed shearwaters, rock crabs and very occasionally, spinner dolphins and humpback whales (in winter season), can be seen. Snorkelling here is also popular because of the old underwater lava formations. For rock climbers, Moku Nui has a trail. Take the route on the right (south) side of the island and continue around to the back. </p>
                
