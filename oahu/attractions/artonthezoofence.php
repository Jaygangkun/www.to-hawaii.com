                <h2 class="small">Art on the Zoo Fence, Oahu</h2>
                <p>For more than 50 years, artists have been displaying their works on the east side of the fence of the Honolulu Zoo. The exhibition, known as Art on the Zoo Fence, has been one of Hawaii&rsquo;s lasting treasures. </p>
                <? include ("../../theme/text.html") ?>
                <p>Here, one can freely view amazing artwork and meet Hawaii&rsquo;s artists in person. Each artist presents his or her original works and prints. A wide variety of art styles can be seen, but the majority are oils, watercolors, acrylics and photography. </p>
                <p>Art on the Zoo Fence takes place every Saturday and Sunday from
                  <time itemprop="OpeningHoursSpecification" datetime="Sa,Su 09:00-16:00">9 am to 4 pm </time>
                  on the zoo fence on Monsarrat Avenue, right across from the bandstand at Kapiolani Park. It&rsquo;s a lush, beautiful setting under large banyan trees and within walking distance of <a href="../../oahu/beaches/waikiki-beach.php">Waikiki Beach</a> and hotels. </p>
                
