                <h2 class="small">Ka Iwi State Scenic Shoreline, Oahu</h2>
                <p>One of Hawaii's beautiful state parks, the Ka Iwi State Scenic Shoreline features stunning views. Located between Waimanalo and Hawaii Kai along Kalaniana'ole Highway (Hwy 72), the Ka Iwi State Scenic Shoreline attracts many visitors. Amongst them are those eager to catch a glimpse of the migratory humpback whales or Makapu'u Point Lighthouse.</p>
                <p>                  The whale watching season along the Ka Iwi State Scenic Shoreline runs from November to March. Over 10,000 whales can be observed close to this shoreline each year, with numbers peaking from January to March. Whales arrive in the waters of the Hawaiian Islands to mate, give birth and nurse their calves. Bring your binoculars to be able to spot the whales better.</p>
                <? include ("../../theme/text.html") ?>
                <p>                  The hike along the shoreline is a short one (1 mile up, 1 mile back) along the paved, former lighthouse access road. This takes you up to a lookout spot above the lighthouse (the lighthouse itself is not accessible) where you are treated to sweeping views of Koko Head and Koko Crater along Oahu's southeastern shoreline. Offshore islets can be seen from various points along the trail, as well of course, as the 1909 Makapu'u Lighthouse with its red roof burning bright against the deep blue of the sea behind it.</p>
                <p>                  There is no parking lot close to the Ka Iwi State Scenic Shoreline. Visitors park their cars along the side of the road at the trail&rsquo;s entrance. Once through the gate on foot the trail begins along a fairly flat, cactus lined path; the cacti grow beautiful yellow flowers in the right season.<br />
                  <br />
                  <strong>Hours:</strong> 7 am to 7:45 pm<br />
                  <strong>Admission:</strong> Free</p>
                
                <h2 class="big">Ka Iwi State Scenic Shoreline Overview</h2>
                <ul type="disc" class="mylist">
                  <li>Scenic shoreline on Oahu's southeastern tip featuring beautiful lookout points</li>
                  <li>2-mile (3.2 km) roundtrip trail involves an uphill climb to 500-foot elevation (hot trail without shade)</li>
                  <li> Trail is paved, being a former access road to the lighthouse</li>
                  <li>Good whale watching spot during the winter months (November to March)</li>
                  <li>No drinking water or restrooms available</li></ul>
