                <h2 class="small">Dillingham Airfield, Oahu</h2>
                <p>Dillingham Airfield (<a href="../../oahu/attractions/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located on Oahu&rsquo;s north shore, is a public as well as a military use airport. Encompassing an area of 134 acres (0.5 sq. km), it is primarily used for sky diving (parachuting) and gliding tours. Military night operations for night vision device training also take place here. The military started using the airfield in the 1920s, and it was used extensively during World War II.</p>
                <? include ("../../theme/text.html") ?>
                <p>The airfield is operated by the Hawaii Department of Transportation under a 25-year lease from the U.S. Army. There is one runway measuring 9,007 feet by 75 feet (2,745 m x 23 m).</p>
                <p>Dillingham Airfield has also served as a movie filming location in the past. The popular TV series <em>Lost</em> filmed several scenes here because of the airfield&rsquo;s remote location. Also, the TV series <em>Hawaii Five-0</em> used the airfield in its second episode, <em>Ohana</em>.<br />
