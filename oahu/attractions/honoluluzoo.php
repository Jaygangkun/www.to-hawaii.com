                <h2 class="small">Honolulu Zoo, Oahu</h2>
                <p>The Honolulu Zoo (<a href="../../oahu/attractions/more_honoluluzoo_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">see more photos</a>, <a href="../../oahu/attractions/p25.html" onclick="NewWindow(this.href,'name','2550','700','yes');return false">view panorama</a>), located right across from popular Waikiki Beach on the island of Oahu, is home to hundreds of animals from around the world. Not only is it the largest zoo within a 2,300-mile radius, but it is also the only zoo in the U.S. that had its origins in a grant given by a king. </p>
                <? include ("../../theme/text.html") ?>
                <p>Back in 1876, King Kalakaua made 300 acres of land available for lease. The Honolulu Zoo started out as a grand public park carved out of marshes and lagoons in this area, which was later named Queen Kapiolani Park. King Kalakaua did not only succeed in beautifying this park, but also used it to keep his personal collection of exotic birds and horses. The first animals, including a monkey, a honey bear and a few lion cubs, arrived in the early 1900s. </p>
                <p>Today, more than 750,000 people visit the 42-acre Honolulu Zoo each year to see the more than 1,230 animals that live here. Animal habitats in the zoo include an African Savannah, a Tropical Forest and Pacific Islands birds and reptiles.</p>
                <p>A popular activity is the Twilight Tour, where parents and kids get to observe these exotic animals at night and find out what they do after dark. This tour takes place Fridays and Saturdays from 5:30 pm to 7:30 pm. </p>
                <p>Another great activity is the &ldquo;Snooze in the Zoo&rdquo; adventure. Imagine the entire family on a camping trip in a jungle full of wild animals. The night begins with a buffet dinner and continues with a guided walking tour of the zoo after dark. Later you&rsquo;ll have s&rsquo;mores around a campfire. You may pitch your tent on one of the zoo&rsquo;s lawns or just bring a sleeping bag to snooze under the stars. Camp-outs begin at 5:30 pm and end the following morning at 9 am. Reservations are limited. Occasionally there are other unique events, such as <a href="../../oahu/attractions/vw_club_of_hawaii_photos.html" onclick="NewWindow(this.href,'name','850','600','yes');return false">VW Beetles Invade the Zoo</a>, showcasing unique oldtimers. Check the Honolulu Zoo website for the exact dates, honoluluzoo.org.</p>
