                <h2 class="small">Kawaiahao Church, Oahu</h2>
                <p>Located in downtown Honolulu on the island of Oahu, Kawaiahao Church (<a href="../../oahu/attractions/p14.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>)  was the first Christian church in Hawaii, built between 1836 and 1842. It features a New England style architecture, and for its construction, about 14,000 slabs of coral rock were digged out by hand from offshore reefs on Oahu's south shore. Divers had to dive 10 to 20 feet (3 to 6 m) under water and with hand tools chisel out each coral block, which were then transported from the reef to the shore. </p>
                <? include ("../../theme/text.html") ?>
                <p>Each coral slab weighs about 1,000 pounds. The church is also known as &ldquo;Hawaii's Westminster Abbey.&rdquo; Its Hawaiian name Ka wai a Ha'o means &ldquo;the water of Ha'o&rdquo; because its location was that of a freshwater pool and spring, which was taken care of by a woman chief named Ha'o. She would take ceremonial baths here for purification. However, the name Kawaiahao was not applied to the church until 1853.</p>
                <p>It was here at Kawaiahao Church where King Kamehameha III in 1843 spoke the phrase that would later become Hawaii's official motto: Ua mau ke ea o ka aina i ka pono (meaning &ldquo;The life of the land is perpetuated in righteousness&rdquo;). </p>
                <p>Kawaiahao Church at one time was the national church of the Hawaiian Kingdom and chapel of the royal family. Up until today, Kawaiahao uses the Hawaiian language for parts of its service. The church currently seats 4,500 people. It is designated as a National Historic Landmark of the National Register of Historic Places.</p>
                
