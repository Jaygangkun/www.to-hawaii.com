                <h2 class="small">Wet'n'Wild Hawaii, Oahu</h2>
                <p>One wouldn&rsquo;t think Hawaii, with its hundreds of islands scattered over 1,500 miles of water, would ever need a water park. But it&rsquo;s got one!&nbsp;Formerly known as the Hawaiian Waters Adventure Park, Wet'n'Wild Hawaii encompasses 25 acres of water rides and other wet attractions. It opened in May 1999 by Waters of Kapolei, LLC, and was sold 9 years later (in May 2008) to Australian theme park giant Village Roadshow Limited at a whooping $27 million.</p>
                <? include ("../../theme/text.html") ?>
                <p><strong>Main Attractions </strong></p>
                <p><em>The Cliffhanger, Volcano Express, Flyin&rsquo; Hawaiian, Big Kahuna</em>, &ndash; all great slides. The first one is not for the fainthearted. A two-lane slide that starts out innocently, making riders feel comfortable until they suddenly find themselves on a 72-foot tall freefall. </p>
                <p><em>Hurricane Bay</em> &ndash; great wavepool! The only one in Hawaii, featuring 2 to 4 foot waves where you can bodysurf or ride the wave in a tube.</p>
                <p><em>Kapolei Kooler, The Cliffhanger,</em>&nbsp;&ndash; perhaps the tamest of all the attractions inside Hawaiian Waters Adventure Park, you just lazily lay back on a tube, perhaps with a refreshing drink, and float down an 800-foot long meandering river. Good for the older ones and the romantics.</p>
                
                <p><em>Da Flowrider, Island Adventure Golf</em> &ndash; the new additions to Hawaiian Waters Adventure Park. You have to pay extra to enjoy a thrill ride: forty feet of consistent standing wave where you can show all your skills at riding a wave. The second is an 18-hole high-end miniature golf course that is truly a marvel for golf enthusiasts.</p>
                <p>Wet'n'Wild Hawaii is located in Kapolei, West Oahu, which is accessible by car, public transport or bus from Waikiki. The place is open daily except Tuesdays and Wednesdays. Please note that no personal floatation devices may be brought into the park.</p>
               
