                <h2 class="small">Chinaman's Hat, Oahu</h2>
                <p>Mokolii, also known among locals as Chinaman&rsquo;s Hat  (<a href="../../oahu/attractions/p21.html" onclick="NewWindow(this.href,'name','2550','500','yes');return false">view panorama</a>) because of its shape, is a popular spot for taking pictures on Oahu&rsquo;s windward coast. The small island is located at the north end of Kaneohe Bay, offshore of <a href="../../oahu/beaches/kualoapark.php">Kualoa Regional Beach Park</a>.</p>
                <p>Mokolii means &ldquo;little lizard&rdquo; in Hawaiian. It&rsquo;s also called Chinaman&rsquo;s Hat because of its cone shape, which resembles the peasant&rsquo;s hat worn in rural China. Besides a few lonely palm trees and two small beaches, there isn&rsquo;t anything else here. But it&rsquo;s still a great place to visit if you like adventures and would like to have an island to yourself once in your life. </p>
                <? include ("../../theme/text.html") ?>
                <p>While most people only know Chinaman&rsquo;s Hat from postcards, wall calendars, or from looking at it from a distance, its real charm is accessible only to those who actually go there to explore it. </p>
                
                <p>During low tide, it is possible to walk right over (but make sure you have a tide timetable with you so you know when to head back). The distance to Mokolii from Kualoa Park is about 1,477 feet (450 m). It can take up to 45 minutes to make it over there, due to the sometimes strong current and waves. Most people who go there come with a kayak, surfboard or small boat.</p>
                <p>Chinaman&rsquo;s Hat has sea caves and two small beaches, one is in a secluded cove facing the ocean. If you walk across to the island, make sure to wear some sort of reef shoes to protect your feet from cuts.</p>
                <p>Once you reach Mokolii, you can either walk around the island to the small beach facing the open ocean, or climb up to the top for a stunning view of the majestic Koolau Mountains and the windward coast. The approximately 20-minute climb is a bit strenuous and leads through thick brush, dirt and lava ledges. But if you&rsquo;re in decent condition, you can make it to the top. The island is also a seabird nesting ground, so if you visit here, be aware of that and don&rsquo;t disturb the animals. </p>
                <p>From the island&rsquo;s peak, you can also see the distinctive profile of Kamehameha&rsquo;s Turtle, also known as the Mokapu peninsula. Legend has it that the turtle safeguards a secret underwater tunnel, which was used by King Kamehameha I, Hawaii&rsquo;s warrior monarch, to reach the island of Molokai. Today the peninsula is the home of a marine base.</p>
                <p>The reef that is located around Chinaman&rsquo;s Hat supports a variety of marine life, most of which are harmless to humans. But keep in mind that you may see hammerhead sharks on your way over. They are usually harmless, but if you&rsquo;re scared by the idea to swim in waters that are a hammerhead shark breeding ground, you may want to stay on land. </p>
                <p>Besides its natural beauty, Chinaman&rsquo;s Hat is also a mysterious place. According to local legend, Hi&lsquo;aka, the volcano goddess Pele&rsquo;s sister, created the island by sleighing a freightening dragon, a no&lsquo;o, and setting his huge flukes in the water as a landmark. </p>
                <p>Today, Chinaman&rsquo;s Hat is part of the 157-acre Kualoa Regional Park. In the past, Kualoa used to be a training ground for Oahu&rsquo;s high chiefs, and Kualii, the first king of Oahu, lived there. The island was an adze extraction and finishing area. Adze blanks from the basalt on the eastern part of the island were quarried by the Hawaiians. They then shaped them using seawater and sand and poured them into one- by two-foot hollows in the rock. Up until today traces of a few of these work areas can be seen.</p>
                
