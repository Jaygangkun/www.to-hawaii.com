                <h2 class="small">Honolulu Hale, Oahu</h2>
                <p>Pronounced &ldquo;HAH-leh&rdquo; in the Hawaiian language and meaning house or home, Honolulu Hale (<a href="../../oahu/attractions/p7.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) means House of Honolulu. Honolulu Hale is the official seat of government of the city and county, site of the chambers of the Mayor of Honolulu and the Honolulu City Council. The building is located on King Street in downtown Honolulu on the island of Oahu. It is listed on the National Register of Historic Places with the United States Department of Interior. </p>
                <? include ("../../theme/text.html") ?>
                                <p>Honolulu Hale was built in a California Spanish colonial architectural style; its inner courtyard, staircase and ceiling were modeled after the Bargello in Florence. In 1951, two three-story wings were added to the original structure. The Hale is surrounded by other historic landmarks, such as the <a href="../../oahu/attractions/statecapitol.php">Hawaii State Capitol</a>, <a href="../../oahu/attractions/iolanipalace.php"> Iolani Palace</a>, the <a href="../../oahu/museums/missionhousesmuseum.php"> Mission Houses Museum</a> and <a href="../../oahu/attractions/kawaiahaochurch.php"> Kawaiahao Church</a>. </p>
                <p>The annual Honolulu City Lights celebration takes place on the grounds surrounding Honolulu Hale every year in the beginning of December. A large Christmas tree is put up in front of Honolulu Hale together with Shaka Santa and Tutu Mele and more Christmas displays inside (<a href="../../forums/Thread-Christmas-lights-at-Honolulu-Hale-2012">see photos</a>).</p>
                
