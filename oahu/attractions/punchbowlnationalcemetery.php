                <h2 class="small">Punchbowl National Cemetery, Oahu</h2>
                <p>The Punchbowl National Cemetery of the Pacific, located on Oahu, Hawaii, was built in 1948 to serve as a resting place for soldiers of the American Armed Forces who lost their lives during World War II, the Korean War and the Vietnam War. </p>
                <p>The 116-acre cemetery is situated in an extinct volcano, named Pouwaina, which means &ldquo;consecrated hill&rdquo; or &ldquo;hill of sacrifice&rdquo; in the Hawaiian language. In the past, Punchbowl was the site of &ldquo;alii&rdquo; (royal) burials. </p>
