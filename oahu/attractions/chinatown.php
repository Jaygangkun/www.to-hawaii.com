                <h2 class="small">Honolulu Chinatown, Oahu</h2>
                <p>You don't have to love anything Oriental to get drawn to Honolulu's Chinatown (<a href="../../oahu/attractions/more_chinatown_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>). There are scores of other reasons why this place could become your favorite haunt. Mostly described as one of Oahu's most exciting and mysterious neighborhoods, Honolulu's Chinatown showcases a unique and colorful part of local history that makes it an interesting stop on anyone's itinerary. Some of the island's oldest buildings are located in this neighborhood, so do a self-guided walking tour   to explore some of Honolulu's oldest buildings and historic landmarks (<a href="../../blog/historic-honolulu-chinatown-walking-tour" target="_blank">see more info and photos</a>).</p>
                <? include ("../../theme/text.html") ?>
                <p>The 25-acre marketplace has been home to Chinese families dating back to the 1800s. The place grew with waves of contract workers coming to Hawaii to work as laborers in the sugar plantations. With their earnings, some started putting up businesses, which grew to become what Honolulu's Chinatown is today.</p>
                <p>Honolulu's Chinatown is quite unlike many other mainland cities in the sense that many of the buildings are relatively new. The whole place completely burned several times, and at one time after several restorations, it became a red-light district with a high crime rate, drug dealers and punks. Back then local residents and visitors practically avoided this area, especially after dark.</p>
                <p>Today, the neighborhood has been cleaned up and is alive with a new generation of market-goers, diners and tourists. Home to many Chinese, Filipinos, Japanese, Koreans, Vietnamese, Hawaiians and people from other Southeast Asian countries, Honolulu's Chinatown is now one of the most vibrant cultural and economic centers of Hawaii. Many local residents come here to shop for fruits, vegetables and other groceries because the prices in Chinatown are lower than in other Oahu supermarkets.</p>
                <p>You can have your hands full just by gazing at cutting-edge art, rummaging through piles of herbal concoctions or ginseng tonic, gawk at live eels, hanging pigs&rsquo; heads, chicken feet and other exotic animals offered as delicacies. You may want to get a tattoo, or offer some prayers in a nearby Buddhist temple. And yes, you might end up loving anything Oriental after this visit.</p>
                
