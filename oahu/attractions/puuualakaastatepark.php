              <h2 class="small">Pu'u  Ualaka'a State Park, Oahu</h2>
              <p>Pu'u Ualaka'a is a forested area which sits on a cinder cone close to downtown Honolulu. Despite its proximity to the hustle and bustle of the city, it is a great place to escape. Most people come here for the views, which are breathtaking. From up here one can see all the way from Diamond Head to Pearl Harbor.<br />
                <br />
                The short Ualaka trail leads through the forest. It is more of a short walk than a hike, making it suitable for people of all ages. Picnic shelters and restrooms are located along the trail.<br />
                <br />
                Visitors to the park are asked to remain on the trail at all times and to keep dogs on a leash. At the uphill end of the trail you come to a 4-way intersection with Makiki Valley, Moleka and Maunalaha Trails. There is a system trail map at this intersection allowing those who wish to join other trails at this point.<br />
                <br />
                Pu'u Ualaka'a and its stunning views are a place of genuine natural beauty, something fragile and irreplaceable. That&rsquo;s why open fires, camping or mountain biking are not permitted in the park.<br />
                <br />
                <strong>Hours: </strong><br />
                7:00 am to 6:45 pm</p>
