                <h2 class="small">Pearl Harbor, Oahu</h2>
                <p>Pearl Harbor (<a href="../../oahu/attractions/more_pearlharbor_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on Oahu, Hawaii, is a significant historical location. It was here where the Japanese attacked the U.S. Pacific fleet on December 7, 1941, which led the United States to enter World War II. Today, Pearl Harbor (including the Arizona Memorial) is the most-visited destination on Oahu, with more than 1.5 million visitors per year. </p>
                <? include ("../../theme/text.html") ?>
                <p>A visit of Pearl Harbor and the Arizona Memorial is a solemn and a learning experience in the same time. At the Arizona Memorial, (a platform that was built atop the sunken battleship USS Arizona), you will be literally on top of the grave of 1,177 soldiers who perished aboard. From the platform you can see the battleship, which lies six feet below the water's surface. After more than 67 years after the attack, oil is still leaking out of the battleship, also called the &ldquo;Black Tears of the Arizona.&rdquo; You&rsquo;ll have to board a small Navy shuttle boat to reach the Memorial.</p>
                
                <p>The best time to visit Pearl Harbor is early in the morning, when it&rsquo;s not too crowded yet. You can either drive yourself, take a city bus or do the tour with one of the <a href="https://www.pearlharbortours.com/" target="_blank">local Pearl Harbor tour companies</a> (which also includes a guide). Once you arrive, you can stroll through the Pearl Harbor museum, featuring World War II memorabilia and photos of the attack. After that, watch a 23-minute documentary film featuring actual footage of the attack. After the video you&rsquo;ll board the Navy shuttle boat that will take you to the Arizona Memorial.</p>
                <p>Back on land, you can visit the battleship USS Missouri, also known as the &ldquo;Mighty Mo.&rdquo; This battleship, which is still intact, is the site of Japan&rsquo;s formal surrender, ending World War II for the United States.</p>
                <p>There are a few other things to do at Pearl Harbor if you still have time. A complete tour would also include a visit to the Pacific Aviation Museum and the Bowfin submarine. So you can definitely make an entire day out of your visit to Pearl Harbor.</p>
                <p><strong>Tip:</strong></p>
                <p>There&rsquo;s a strict no-bags policy at Pearl Harbor. No bags of any kind (including hand bags, backpacks, camera bags, purses, diaper bags, etc.) or luggage and/or other items that offer concealment are allowed. A storage container is available for items that are carry-on size or smaller (30&rdquo;H x 30&rdquo;W x 18&rdquo;D). The fee is $2.00 per item. You can purchase water and other snacks at a snack bar there. You may bring your camera only. All other bags must be left in the car/tour bus. This is a new regulation that was put into action following the September 11, 2001, attacks on the World Trade Center. </p>
                <p><strong>Pearl Harbor Hours</strong></p>
                <p>
                  <time itemprop="OpeningHoursSpecification" datetime="Mo,Tu,We,Th,Fr,Sa,Su 07:00-17:00">7 am - 5 pm daily</time>
                  , except Thanksgiving, Christmas and New Years Day.</p>
                <p>Tours to the USS Arizona Memorial begin every 15 minutes, with the last program starting at 3 pm. Visitors should plan to arrive by 1 pm. to ensure space availability. The entire program takes about one hour and 15 minutes. Tours are given on a first-come, first-serve basis. No reservations are accepted.</p>
                
