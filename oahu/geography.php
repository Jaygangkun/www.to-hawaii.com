          <h1>Oahu Geography</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="clear"></div>
              <p>Oahu is the third largest of the main Hawaiian islands (after Hawaii's Big Island and Maui) encompassing 596.7 square miles (1,545.4 km2). It measures 44 miles (71 km) from north to south and 30 miles (48 km) from east to west, with a total shoreline of 227 miles (365 km). The island is made up of two now extinct shield volcanoes, Wai&rsquo;anae in the western part of the island and Ko'olau in the east. Inbetween lies the central Oahu Plain, a broad valley. The highest mountain on Oahu is Mt. Ka'ala, located in the Wai'anae Mountain Range, rising to 4,003 feet (1,220 m) above sea level.</p>
              <p>The Wai'anae Mountain Range the oldest part of Oahu at about four million years old. It is believed that Oahu originated at Lualualei Valley. This is the dry side of the island. The Ko'olau Mountain Range, located on the windward coast, faces the tradewinds which bring moisture with them. This side of Oahu is green and lush with rainforests and waterfalls. Besides these two main mountain ranges, there are a few smaller volcanic mountains on the island, all of which are extinct as well. The most popular such volcanic cone is Diamond Head, located at the east end of Waikiki. Two other ones are Punchbowl and Koko Head. The Koko Head area (Oahu's southwest) is the youngest part of the island at about 32,000 years old.</p>
