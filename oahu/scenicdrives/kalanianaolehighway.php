                <h2 class="small">Kalanianaole Highway (near Makapu'u), Oahu</h2>
                <p>Kalanianaole Highway, between Hawaii Kai and Waimanalo, is a scenic road with many lookout points along the way. Coming from Hawaii Kai and driving towards Waimanalo, you will first see <a href="../attractions/kokoheadcrater.php">Koko Head Crater</a> on your left, an old tuff cone and one of the most popular landmarks on Oahu's south shore. <a href="../beaches/hanaumabaypreserve.php">Hanauma Bay</a>, Oahu's most popular snorkeling beach will be on your right (even though you won't be able to see the bay from the highway). </p>
                <? include ("../../theme/text.html") ?>
                <p>The &ldquo;highway&rdquo; (a two-lane road) is built into the sea cliffs, so while driving you will enjoy great views of the ocean below. The first lookout point is the Lanai Lookout, from where you can see three neighbor islands (Molokai, Lanai and Maui) on a clear day. The next lookout point (with a more spacious parking lot) is the <a href="../attractions/halonablowhole.php">Halona Blowhole</a> lookout, where you can see water from the ocean shoot up from a blowhole in the lava shelf below (the higher the waves, the higher the water will shoot up). <a href="../beaches/halona-beach-cove.php">Halona Beach Cove</a>, also known as Eternity Beach, is located here as well. It is a small sandy cove that was made popular in the 1953 movie <em>From Here to Eternity</em>. </p>
                <p>Drive a bit further and stop at <a href="../attractions/makapuulookout.php">Makapu'u Lookout</a>, where you can enjoy great views of <a href="../beaches/makapuubeachpark.php">Makapu'u Beach</a>, <a href="../beaches/sandy-beach-park.php">Sandy Beach</a>, as well as Kaohika'ipu and <a href="../attractions/rabbitisland.php">Rabbit Island</a>. Just before Makapu'u Lookout you will see a small road on the right (usually many cars park here). This is the trailhead to the scenic <a href="../attractions/kaiwistatescenicshoreline.php">Ka Iwi Scenic Shoreline</a> trail, which leads to yet another lookout point of the Makapu'u Lighthouse (a 2-mile hike). Continue driving along Kalanianaole Highway and you will reach the small town of Waimanalo and <a href="../beaches/waimanalobeachpark.php">Waimanalo Beach Park</a>, a beautiful and long white-sand beach. </p>
                
