                <h2 class="small">Pali Highway (Route 61), Oahu</h2>
                <p>The Pali Highway (Route 61) is one of the most important and scenic roads on Oahu. It connects Honolulu with the two main towns on Windward Oahu, Kailua and Kaneohe. From downtown Honolulu, it leads up through the residential neighborhood of Nu'uanu, then through the two Nu'uanu Pali tunnels and then down to Kailua and Kaneohe. Right after exiting the tunnels the views are amazing (this stretch of the highway is also quite curvy).</p>
                <p>The views make it almost difficult to focus on the road. Luckily there is a small pullout along the highway if you want to stop to take some pictures. But even better, plan a stop at the <a href="../attractions/nuuanupalilookout.php">Nu'uanau Pali Lookout</a>, which is located before the tunnels if you're coming from the Honolulu side. There is a spacious parking lot at this lookout. </p>
                <? include ("../../theme/text.html") ?>
                <p>Today's Pali Highway is actually the third road that was built here. The original road over the Nu'uanu Pali was built in 1845 and went along much of the same route. In 1898 it was developed into a &quot;highway&quot; (compared to today's standards this highway was still quite narrow and was known for its 22 hairpin turns). Some parts of this old road still exist. However, the Old Pali Highway is closed to vehicular traffic and can be accessed on foot only (one access point is at the Pali Lookout). Work on today's Pali Highway began in 1955. Two years later two of the four tunnels were completed and the highway was opened for Honolulu-bound traffic. It took another four years until the other two tunnels were finished and the highway completed in 1961.</p>
                <p>There is a popular urban legend about the Pali Highway. According to this legend, it is a bad idea to take pork over the Pali. If you do, don't be surprised if you suddenly have car problems. Spooky tales tell of people who have taken pork over the Pali Highway and had eerie experiences, especially late at night. </p>
                
