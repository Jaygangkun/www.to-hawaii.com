                  <h2 class="small">Tantalus - Round Top Drive, Oahu</h2>
                  <p>Round Top Drive is one of the best roads to enjoy panoramic bird's eye views of Honolulu and Waikiki. The winding, canopy-covered road leads in a loop to the top of Mount Tantalus and back down. It is quite narrow and curvy. There are a few scenic lookout points along the way. The best one is at <a href="../attractions/puuualakaastatepark.php">Pu'u Ualaka'a State Park</a>. From here one can see all the way to Diamond Head in the east to Honolulu International Airport and Pearl Harbor in the west. The sunset and night-time city light views are also amazing from up here.</p>
                  <? include ("../../theme/text.html") ?>
                  <p>Back in 1917, Round Top Drive was a gravel road, and in 1937 it was paved. A 7-mile (11.3 km) stretch of the road was put on the State Register of Historic Places in 2007 and on the National Register of Historic Places in 2009. It is the first road on Oahu to receive such designation. </p>
                  
