                <h2 class="small">Farrington Highway (North Shore), Oahu</h2>
                <p>There are two Farrington Highways on Oahu. One is one the north shore, the other one on the west shore. Both highways are dead end roads, interrupted by the inaccessible northwestern tip of the island, Kaena Point. The Waianae Mountain Range begins here and on the foothills of these mountains is a seabird sanctuary (albatrosses and other seabirds nest here). But no road leads here. The area is only accessible on foot. </p>
                <? include ("../../theme/text.html") ?>
                <p>The stretch of Farrington Highway on Oahu's north shore begins in the small town of Waialua. It ends just past Camp Erdman, right before <a href="../beaches/kaenapoint.php">Kaena Point State Park</a> begins. The two-lane road leads  along the coastline and a few scenic beach parks, including <a href="../beaches/mokuleiabeachpark.php">Mokuleia Beach Park</a> and <a href="../beaches/armybeach.php">Army Beach</a>. On the other side of the highway is Dillingham Airfield, so you may also see small planes start and land from up-close. The green Waianae Mountains provide a scenic backdrop. </p>
                
