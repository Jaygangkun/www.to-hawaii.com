                  <h2 class="small">Diamond Head Road, Oahu</h2>
                  <p>Diamond Head Road begins at the eastern end of Waikiki. Continue on Kalakaua Avenue past the <a href="../../oahu/attractions/waikikiaquarium.php">Waikiki Aquarium</a> and then continue along the foothills of <a href="../../oahu/attractions/diamondhead.php">Diamond Head</a>, south Oahu's popular landmark. Diamond Head Road encircles Diamond Head, but the most scenic stretch of the road is along the coastline. The road goes uphill along the cliff and down below is the ocean. </p>
                  <? include ("../../theme/text.html") ?>
                  <p>There is a popular lookout point called <a href="../../oahu/beaches/kuileicliffs.php">Kuilei Cliffs</a> (once the road levels out and before it goes downhill again). You can't miss it because there is a large but usually busy parking lot. From here you can enjoy nice views of Oahu's south shore and <a href="../../oahu/cities/kahala.php">Kahala</a>, an upscale neighborhood with many fancy oceanfront mansions. From the parking area you can also walk a bit along the road (in the direction where you came from - towards Waikiki) to get a glimpse of the Diamond Head Lighthouse. </p>
                  <p>Another point of interest is the Amelia Earhart memorial (located at the eastern end of the  parking lot). Amelia Earhart was the first person to fly alone from Hawaii to North America on January 11, 1935. Continue driving down Diamond Head Road and you will arrive in Kahala. Kahala Avenue is an expensive Honolulu neighborhood with most homes costing $1 million and up. </p>
                  
                  <h2 class="big">Diamond Head Road Overview</h2>
                  <ul type="disc" class="mylist">
                    <li>Scenic cliff road on the foothills of Diamond Head</li>
                    <li>On a clear day you can see the neighbor islands of Molokai and Lanai on the horizon</li>
                    <li>In the winter months (December to March), bring binoculars and watch whales offshore</li></ul>
