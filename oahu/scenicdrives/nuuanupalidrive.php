                  <h2 class="small">Nu'uanu Pali Drive (Morgan's Corner), Oahu</h2>
                  <p>Most people who drive from Honolulu to Kailua or Kaneohe on Oahu's windward coast take the Pali Highway (Route 61). Take a small detour and make a turn onto Nu'uanu Pali Drive and you will feel like you're in a jungle. This canopy-covered road has some interesting looking trees and is home to a popular Hawaiian spooky legend. In fact, many residents would consider the sharp curve known as Morgan's Corner one of the spookiest places on Oahu. Morgan's Corner is named after Dr. James Morgan who used to live at this sharp curve between the 1920s and 1940s. </p>
                  <? include ("../../theme/text.html") ?>
                  <p>On a moonless night it is literally pitch black in here and even during the day many people get chicken skin when stopping at Morgan's Corner. There are many variations of the spooky tale, and one of them goes like this: A girl from the Morgan's residence hanged herself on the tree at the end of the road. She was reported missing for a few days and when she was found, her head had severed itself from her body, leaving it handing by the rope alone. Her body and head were never reattached when she was buried, leaving her to forever haunt the Nu'uanu Pali Drive. </p>
                  <p>But spooky or not, Nu'uanu Pali Drive is one of the most scenic roads on Oahu. If you don't have time for a rainforest hike, drive along this road and enjoy the lush vegetation surrounding it. </p>
                  
