                <h2 class="small">Wahiawa Botanical Garden, Oahu</h2>
                <p>Located in central Oahu between the Wai'anae and Ko'olau mountain ranges, this 27-acre park makes for a nice stop on your way to Oahu's North Shore. Hawaiian sugar farmers owned this site from the 1920s until 1950, when they donated the land for public use. In 1957, the land was designated a garden, and today the Wahiawa Botanical Garden is a special place home to a wide range of tropical plant species from around the world. </p>
                <? include ("../../theme/text.html") ?>
                  <p>The Wahiawa Botanical Garden features mostly native and naturalized Hawaiian plants, though you'll also find many plants from other countries. Among the species on display are the elephant apple, rainbow eucalyptus, blue ginger, koa, shaving brush tree, candle tree, Mexican cedar, Queenland kauri, mamaki and camphor tree. </p>
                <p>The non-native plants come primarily from tropical regions of Africa, Asia, Australia and the South Sea islands. The garden's humid, cool climate provides excellent growing conditions for these species and the multitude of native Hawaiian specimens. Many of the older trees date back to the 1920s, when the sugar planters used the site for experimental tree planting. <br />
