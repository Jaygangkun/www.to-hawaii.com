                <h2 class="small">Ho'omaluhia Botanical Garden, Oahu</h2>
                <p>Situated on lush 400 acres (1.6 km&sup2;) in <a href="../cities/kaneohe.php">Kaneohe</a>, at the foot of the majestic Ko'olau Mountain Range, the Ho'omaluhia Botanical Garden (<a href="../../oahu/gardens/more_hoomaluhiabotanicalgardens_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a peaceful park that is home to hundreds of plant species, divided into geographical regions as diverse as the Philippines, Africa, Sri Lanka, India, Malaysia, Tropical America, Polynesia, Melanesia and Hawaii.</p>
                <? include ("../../theme/text.html") ?>
                <p>The garden was created by the U.S. Army Corps of Engineers to provide flood protection for Kaneohe, and the facilities now include a day use area, campground and a center with a lecture room, exhibition hall, workshop and botanical library.</p>
                <p>Head first to the visitors center, where you'll find information on guided tours (every weekend), special events and ethnic exhibits. Ho'omaluhia means &quot;to make a place of peace and tranquillity&quot; in the Hawaiian language. To get a real grasp of the incredible diversity of the flora at Ho'omaluhia, inquire about the nature walks, hiking, crafts and cloud-watching activities on offer, or just while away a quiet afternoon by the 32-acre (0.13 km&sup2;) lake (swimming is not permitted in the lake due to water impurities). </p>
                <p>When you feel like a stroll, take one of the meandering trails past the lake and streams, over footbridges and alongside verdant meadows, where an endless array of flowers, fruits, bushes, vines and trees await the photographer, naturalist or curious botanist.<br />
