                  <h2 class="small">Moanalua Gardens, Oahu</h2>
                  <p>Located about 5 miles (8 km) northwest of downtown Honolulu, Moanalua Gardens is a 24-acre privately-owned park managed by the non-profit Moanalua Gardens Foundation. The garden is known for the site where the annual Prince Lot Hula Festival takes place on the third weekend in July. It is the largest non-competitive hula festival in Hawaii. The festival honors Prince Lot Kapua'iwa who reprised the once banned hula in the district of Moanalua. Prince Lot Kapua'iwa would later become King Kamehameha V. </p>
                  <? include ("../../theme/text.html") ?>
                  <p>The garden is home to rare plants, trees, a koi pond, stream and historic structures, such as the Kamehameha V cottage. A large monkeypod tree is located in the middle of the garden. There are many monkeypod trees in Hawaii, but this one is very popular in Japan. It is known as the Hitachi tree. Hitachi Ltd., the Japanese electronics manufacturer, has used the tree as their corporate symbol since 1973. The company pays an annual fee that grants it exclusive worldwide rights to use the tree's image for promotional purposes. The most recent contract was done in 2006 when Hitachi agreed to pay $400,000 annually for promotional rights until 2016. While this sounds like a lot of money, this revenue only partially covers the annual expenses to operate the garden (which are $600,000). Since the Hitachi tree is a well-recognized corporate icon in Japan, Japanese tourists enjoy visiting Moanalua Gardens.</p>
                  <p>In the past, the Moanalua Gardens Foundation sponsored 3-mile (4.8 km) guided hikes into Kamananui Valley. However, they are currently not offering these hikes. If you would like to hike in this valley, contact the DLNR Division of State Parks (587-0300) for permission to enter the valley. A self-guided booklet from Moanalua Gardens Foundation is available at the garden office (booklet cost is $5).<br />
