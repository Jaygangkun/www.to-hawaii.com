<h2 class="small">Halawa Xeriscape Garden, Oahu</h2>
<p>This unique garden in Aiea features an extensive collection of xeric (dry) plants. Opened in September 1989, the Halawa Xeriscape Garden was designed by the Board of Water Supply to demonstrate for local residents and visitors the possibility of constructing an attractive garden that uses less water than most traditional gardens. Plants here require only minimal irrigation, but you probably wouldn't guess by the variety of species.</p>
<? include ("../../theme/text.html") ?>
<p>  In addition to featuring plants that require less water, the garden employs many techniques to limit the amount of irrigation. Here you'll find water-efficient irrigation systems that use automatic timers, dripper lines, micro-spray sprinkler heads, moisture sensors, emitters and other devices. Everything from the fertilization and weed control methods to turf mowing practices, mulching and soil alteration is designed to consume less water and to use that water more efficiently. </p>
<p>  The Halawa Xeriscape Garden is divided into several sections. The International Garden contains plants from around the world, mainly from the dry tropics. You'll also find the Bromeliad Garden containing pineapple relatives from different tropical areas of the Americas. The Tree Garden showcases different drought-tolerant trees from around the world. And the Hawaiian Garden showcases native species adapted to hot and dry conditions.</p>
<p>  Self-guided garden tours, water conservation workshops for kids and adults, and free garden mulch are all available at this unique site. For more information call <span itemprop="telephone">(808) 748-5041</span>.<br />
