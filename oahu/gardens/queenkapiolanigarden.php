                <h2 class="small">Queen Kapiolani Garden, Oahu</h2>
                <p>Hawaii is famous for its hibiscus, which was declared the state flower in 1988. This spectacular botanical garden located within Honolulu's historic <a href="../attractions/kapiolanipark.php">Kapiolani Park</a> features an outstanding collection of these sweet-smelling, colorful flowers, as well as a nice selection of roses. Whether you're a professional botanist or casual flower enthusiast, these gardens offer a wonderful place to relax and smell the flowers.</p>
                  <? include ("../../theme/text.html") ?>
                  <p>
                  Opened in 1972, the garden and adjacent park once belonged to King David Kalakaua who donated the land for public use in 1877. He named the 133-acre park in honor of his wife, Queen Kapiolani. At the garden, deep reds, vibrant oranges and delicate pink flowers await you at Her Majesty's garden, including many rare species from Hawaii and beyond. You'll also find a collection of rare Lokelani roses, one of Hawaii's most elusive plant species. The colorful, refreshing gardens offer a welcome escape from the surrounding urban environment in Waikiki.<br />
