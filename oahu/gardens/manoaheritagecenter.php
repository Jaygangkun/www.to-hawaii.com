                  (reservation to tour the grounds required)<br />
                  Limited parking is available (up to 5 cars)<br />
                  <br />
                  <strong>Admission: </strong><br />
                  Guided tours of the Kuka'o'o Heiau and native Hawaiian garden are available for groups of up to 20 people. Tour prices are $7 for adults, $4 for seniors (age 62+) and military, and free for children and students. Reserve your tour by calling <span itemprop="telephone">(808) 988-1287</span>. <strong>TOURS ARE BY ADVANCE RESERVATION ONLY</strong>.<br />
                </p>

