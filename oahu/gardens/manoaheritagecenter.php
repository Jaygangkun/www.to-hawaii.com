                <h2 class="small">Manoa Heritage Center, Oahu</h2>
                <p>Located in the lush Manoa Valley about 3 miles (5 km) east of downtown Honolulu, the Manoa Heritage Center is a small historic site that makes for a pleasant and educational afternoon. A non-profit organization founded in 1996, the Center promotes and preserves Hawaii's natural and cultural heritage, allowing visitors to experience a piece of living history. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  The Manoa Heritage Center consists of three main features. The  Kuka'o'o Heiau is a sacred agricultural temple built by the Menehune people nearly a thousand years ago. Once used in food productivity rites and rituals, the temple was restored in 1993. It is the only remaining Hawaiian temple in the greater <a href="../cities/waikiki.php">Waikiki</a> region and offers a fascinating glimpse into the spiritual practices of native people. </p>
                <p>At the native Hawaiian garden, visitors can see a range of Native Hawaiian plants such as kokio (native hibiscus) as well as plants introduced by the first Polynesian arrivals, such as kalo (taro) and noni. Many of these unique plant species are listed as endangered, threatened or at risk. Visitors can learn about the plants&rsquo; beneficial properties and importance of preserving this native flora.</p>
                <p>                  Last but not lease, Kuali'i is a historic Tudor-style home dating back to 1911 that was designed by well-known Honolulu architects Emory and Webb. A significant Manoa Valley landmark, the home is listed on the National Register of Historic Places, but is still a private residence and currently not open to tours. </p>
                <p><strong>Hours:<br />
                  </strong>
