                <h2 class="small">Lyon Arboretum, Oahu</h2>
                <p>Located at the northern end of Manoa Valley (at the start of the trail to <a href="../waterfalls/manoafalls.php">Manoa Falls</a>), the 194-acre Lyon Arboretum (<a href="../../oahu/gardens/more_lyonarboretum_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>  <a href="../../oahu/gardens/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) offers visitors a chance to experience the richness and diversity of the islands' native and non-native vegetation in a tranquil environment. More than 5,000 tropical plant species inhabit this area, owned and operated by the University of Hawaii. The Lyon Arboretum functions simultaneously as a research facility, preservation site and visitor attraction; it is a place not to be missed if you're a horticulture enthusiast. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  The Lyon Arboretum was established by the Hawaii Sugar Planters Association in 1918 and became part of the University of Hawaii in 1953. During the arboretum's early years, a young botanist named Dr. Harold L. Lyon planted upwards of 2,000 tree species on the grounds. Over the years he nurtured and developed the site until his death in 1957, the year the site adopted the name Lyon Arboretum. Here you'll encounter an impressive range of plants, including aroids, heliconia, ginger, ti and many tropical palm varieties. With an average annual rainfall of 165 inches, the Lyon Arboretum provides the perfect climate for an incredible diversity of tropical plant life. In recent years the arboretum's curators have dedicated their efforts toward preservation and conservation of Hawaii's native endangered plant species. </p>
                <p>                  Avid hikers and outdoor enthusiasts are encouraged to explore the arboretum on foot. Well-marked trails take visitors on a fascinating tour of plants and trees from different corners of the world, including Asia, Europe and the Americas. Hikers are rewarded with spectacular views of sites such as Aihualama Waterfall, Inspiration Point and the many gardens that occupy the grounds. These include the Beatrice H. Krauss Ethnobotanical Garden, the Hawaii Plants Section, the Economic Botany Section and the Tropical Palms Garden. In addition visitors are likely to spot colorful bird species such as cockatoos, white-rumped shama trhushes, mejiro, Brazilian cardinals, bulbuls and mynas.</p>
