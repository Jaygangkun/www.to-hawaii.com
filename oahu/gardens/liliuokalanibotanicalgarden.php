                <h2 class="small">Lili'uokalani Botanical Garden, Oahu</h2>
                <p>Lili'uokalani may be the smallest of the five Honolulu Botanical Gardens, but it is a wonderfully quiet and beautiful place to spend an afternoon. The garden, which once belonged to Hawaii's last reigning monarch, Queen Lili'uokalani, is devoted to native Hawaiian plants. Queen Lili'uokalani donated the 7 &frac12; acres for public use when the Honolulu Botanical Gardens were founded in 1958. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  Rumored to have been the Queen's favorite picnic spot on the island, the Lili'uokalani Botanical Garden (<a href="../../oahu/gardens/more_liliuokalanibotanicalgarden_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is the only botanical garden around Honolulu to feature native Hawaiian plants exclusively. The quiet, shaded grounds include the lovely Nu'uanu Stream, Waikahalulu Waterfall and pedestrian bridge, all of which add to the site's relaxing, natural atmosphere. </p>
                <p>Springtime in Lili'uokalani Botanical Garden welcomes the refreshing scent of white, yellow and shell ginger plants, as well as many other species native to the Islands. Whether you're jogging, taking photos or enjoying a picnic with the family, this tranquil site offers a memorable escape from the hustle and bustle of the city. </p>
                <p>Before entering the Lili'uokalani Botanical Garden, be sure to review the rules and regulations intended for your safety and the well-being of the flora and fauna located here. Visitors are prohibited from touching or picking the plants, fruits and flowers in the garden. Swimming in the Waikahalulu Falls pool is also not advised. Mosquitoes can be a problem at certain times in the year, so come prepared with insect repellent. Free parking is available near the garden. </p>
