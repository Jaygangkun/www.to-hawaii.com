                <h2 class="small">Koko Crater Botanical Garden, Oahu</h2>
                <p>Part of the Honolulu Botanical Gardens first established in 1958, this 60-acre botanical garden is located in Oahu's Koko Crater. The area's dry, hot climate provides ideal growing conditions for a wide range of dryland plants from Hawaii and elsewhere. Many of these are rare and endangered species.</p>
                <p>                  The Koko Crater Botanical Garden  (<a href="../../oahu/gardens/more_kokogarden_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) specializes in cacti, aloes, dryland palms, plumeria cultivars, sansevieria, euphorbias, bougainvillea, alluaudias, baobabs and adeniums. In addition to native and naturalized Hawaiian plants, the garden features species from Madagascar, Africa and the Americas. </p>
                <? include ("../../theme/text.html") ?>
                
                <p>                  A 2-mile (3.2 km) loop trail takes visitors on a 1 &frac12; hour self-guided walk through the garden. But the gardens aren't the only attraction here. You'll also get to experience the natural landscape of Koko Crater, which features many Hawaiian tree species, including the kiawe, wiliwili and koa haole. The wiliwili tree is particularly noteworthy and is one of the species listed under Hawaii's Exceptional Tree Act 105. </p>
                <p>                  Services are limited at Koko Crater, so it's important to come prepared and to be aware of potential dangers. Restrooms, drinking fountains and visitor accommodations are currently unavailable. Due to Koko Crater's dry, hot climate visitors are advised to bring potable water, sunglasses, sunscreen and other such items. Sturdy walking shoes or hiking boots are also recommended, as the trail can be rocky and unstable in parts. Picking any plants in the garden is strictly forbidden. Visitors should also avoid touching, eating or smelling any unfamiliar plants, as many contain allergens that can cause skin irritations, sneezing, inflammation and other discomforts.</p>
