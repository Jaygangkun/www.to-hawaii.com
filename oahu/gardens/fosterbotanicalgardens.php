                <h2 class="small">Foster Botanical Garden, Oahu</h2>
                <p>Encompassing almost 14 acres, the Foster Botanical Garden (<a href="../../oahu/gardens/more_fosterbotanicalgarden_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is a serene oasis and home to more than 10,000 species of rare tropical plants from all over the world. The garden is located just a few blocks from the business district in downtown Honolulu on the island of Oahu. In 1988, it was placed on the Hawaii Register of Historic Places and because of its beauty, it has served as the setting for several Hollywood films and TV shows in the past.</p>
                
                <p>The Foster Botanical Garden features rare and endangered trees, an orchid, herb and prehistoric garden and even an economic garden, which shows plants that are used for medicine, food, fabrics and dyes. </p>
                <? include ("../../theme/text.html") ?>
                <p>The garden was created in 1853, when Queen Kalama leased a small area of land to a young German doctor, William Hillebrand. Hillebrand, who was a physician as well as a botanist, built a home together with his wife in the upper terrace area of the garden. The beautiful trees that are still there today were planted by him.</p>
                
                <p>To preserve the garden's plants, visitors are not allowed to pick any part of the plants or remove any plant material from the garden. More than 75,000 visitors come here each year and guided tours are available Mondays through Saturdays at 1 p.m. (and by appointment), but reservations are recommended because the tours are based on availability of volunteer docents. </p>
                <p>The Foster Botanical Garden is also a favorite place for weddings and photo shootings. A permit is required for weddings and commercial photography (see below for contact info). </p>
                <p><strong>Foster Botanical Garden Areas of Interest</strong>:<br />
                  Lyon Orchid Garden: a collection of Old and New World orchid species<br />
                  Main Terrace: the oldest part of the garden dating back to 1853<br />
                  Middle Terraces: palms, aroids, heliconia and ginger plants<br />
                  Economic Garden: herbs, spices, dyes, poisons and beverage plants<br />
                  Prehistoric Glen: primitive plants from around the world<br />
                  Hybrid Orchids: blooming orchid display<br />
                  Butterfly Garden: colorful open-air habitat for Hawaii's butterflies <br />
                  Exceptional Trees: 24 trees designated &quot;exceptional&quot; in the garden</p>
                <p><strong>Hours:<br />
                  </strong>Open daily from
                  <time itemprop="OpeningHoursSpecification" datetime="Mo, Tu, We, Th, Fr, Sa, Su 09:00-16:00">
                  9 am to 4 pm<br />
                  Closed on Christmas Day and New Year's Day.<br />
                  Guided tours are offered at 10:30 am, Monday through Saturday.  Reservations are recommended.  Call <span itemprop="telephone">(808) 522-7066 </span>for more info on guided tours, weddings or commercial photo shoots.</p>
                <p><strong>Admission: </strong><br />
                  General (13 years and older): $5<br />
                  Kama'aina (Hawaii resident): $3 <br />
                  Children (ages 6-12): $1<br />
                  Children age 5 and under: Free<br />
                  Annual family pass: $25</p>
                
