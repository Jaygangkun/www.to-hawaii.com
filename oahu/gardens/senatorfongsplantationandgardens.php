                <h2 class="small">Senator Fong's Plantation & Gardens, Oahu</h2>
                <p>Rising from 80 feet to 2,600 feet (24 m to 792 m) above sea level in Oahu's Ko'olau Mountains, this family-owned tropical garden contains 700 acres of lush vegetation, an area larger than all of Waikiki. Senator Fong's Plantation &amp; Gardens were named after U.S. Senator Hiram Fong, who purchased the land in 1950. Fong, who served in the U.S. Senate for 17 years, divided the plantation into five sections, each one bearing the names of different presidents he served under during his career. </p><? include ("../../theme/text.html") ?>
                
                <p>                  Today visitors are welcome to explore this colorful collection of palms, flowers, tropical fruit and ferns with the help of an experienced guide. Here you'll encounter a wide range of exotic Hawaiian plant species, such as kukui, koa, ohia-'ai (mountain apple), hala, ti and pili grass. You can also view 15 early Polynesian introduced plants, such as bamboo, betel nut and taro. Fong's tropical palm garden features an impressive 80 varieties. </p>
                <p>                  The 1-mile (1.6 km) walking tour lasts about 90 minutes and takes visitors through Fong's lush valley, dense canopies and forested plateaus bursting with native plants. Knowledgeable guides let visitors sample kukui nuts, tropical fruits and whatever other tasty treats are currently in season. In fact, the plantation features over 70 edible nuts and fruits from around the world. Senator Fong's open-air visitor center hosts weddings, baby's first luau celebrations and other events amidst spectacular natural surroundings. Visitors can even learn the art of lei making with classes offered daily. </p>
                <p><strong>Hours:</strong><br />
                  <time itemprop="OpeningHoursSpecification" datetime="Su,Mo,Tu,We,Th,Fr 10:00-14:30">Daily: 9 am to 2 pm</time>
                  <br />
                  Closed on Saturdays, Christmas Day and New Year's Day.<br />
                  Guided 1.5-hour tours available at 10:30 am and 1 pm daily. No reservations needed.</p>
                <p><strong>Admission:</strong><br />
                  Adults: $14.50<br />
                  Seniors (65+): $13 <br />
                  Children (ages 5-12): $9<br />
                  Lei-making class: $6.50 per lei (no reservation needed to participate)</p>
                <p>Gift shop, snacks, refreshments and restroom facilities are available. Visitor Center is wheelchair accessible.</p>
                
