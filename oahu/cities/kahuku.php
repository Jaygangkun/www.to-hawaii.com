                <h2 class="small">Kahuku, Oahu</h2>
                <p>Kahuku (<a href="../../oahu/cities/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a small town on Oahu's northeastern coast, between Laie and Turtle Bay. Even though you can hardly find it on a map, there are still things to do here. Like shrimp? Then Kahuku is the place to go. Here you'll find numerous shrimp trucks parked alongside the main street (Kamehameha Highway) that runs through the town. There are several shrimp farms that raise the shrimp right there. One of the most popular is Giovanni's. You can't miss the old white truck right next to the road. Their shrimps taste great and you can choose from three different flavors &ndash; scampi (with garlic), with lemon and butter, or spicy. Each dish comes with two scoops or rice and currently costs $13. </p>
                <? include ("../../theme/text.html") ?>
                <p>Kahuku's high school students have made a name for themselves. Kahuku High School is ranked nationally in the U.S. for football and in 2007 was second in the nation to De La Salle High School of Concord, CA, for the most active NFL players from a single high school. Besides producing football champions, Kahuku High School students have also won titles in track and field, wrestling, basketball, water polo, volleyball and judo. They also do well in academics and the arts.</p>
                
                <p>Other students at Kahuku High who are not athletes have placed in the top ten spots in the U.S. in academic contests, such as History Day and We the People. The school's choir Vocal Motion constantly sweeps competitions at national venues. Other students have won the photography portfolio category in the Scholastic Arts competition, which is the state's most competitive art contest for high school students. All in all, students from Kahuku High have received recognition in 25 national contests, which is more than any other public school in Hawaii. </p>
                <p>The school has even produced somewhat famous people. The well-known musician Jack Johnson attended Kahuku High and graduated in 1993. Alexander Bonde, one of his classmates, is now a member of the German Parliament. Other well-known graduated include renowned Chef Sam Choy, award-winning surfer Pancho Sullivan and public figure Earl Anzai. So if you have kids and plan to move here, you'll be glad that this small town has a good school. </p>
                <p>Kahuku is has also been a filming location in the past. It first became famous in the mid 1970s when The Diamond Head Game, a popular TV game show was contested at the nearby Kuilima Hotel (today known as Turtle Bay Resort). At the end of each show the announcer would say: &quot;If you plan to be in Hawaii, send a self-addressed, stamped envelope to the Diamond Head Game, Kahuku, Oahu 96731.&quot; The Fox TV show North Shore was also filmed there recently. The Turtle Bay Resort is a nice place to go and have fun. There's a nice beach and after some swimming and snorkeling, one can have a snack at the restaurant there. </p>
                <p>In the Hawaiian language, Kahuku means &ldquo;the projection,&rdquo; which most probably refers to nearby Kahuku Point, the northernmost point of the island of Oahu. <a href="../beaches/kahukubeach.php">Kahuku Beach</a> is popular among fishermen, but not a good swimming beach because of a long limestone shelf and rocky ocean bottom that makes entering the ocean difficult. The beach is backed by the Kahuku Golf Course.</p>
                
