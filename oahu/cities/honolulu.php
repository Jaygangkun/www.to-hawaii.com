                <h2 class="small">Honolulu, Oahu</h2>
                <p>Honolulu (<a href="../../oahu/cities/more_honolulu_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">see more photos</a>, <a href="../../oahu/cities/p6.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is the capital of the U.S. state of Hawaii. It is located on the south shore of the island of Oahu, in the Central Pacific Ocean, 2,551 miles (4,105 km) west of Los Angeles. Honolulu's population is 337,256 (2010 Census), which makes it the largest city in the state of Hawaii. But the city is not just the largest in Hawaii, but the &quot;largest&quot; city in the whole world. That's because Hawaii's state constitution states that any Hawaiian island not named as belonging to a county belongs to Honolulu. </p>
                <? include ("../../theme/text.html") ?>
                <p>This makes the entire island of Oahu, where Honolulu is located, plus all the other small, uninhabited islands from the island of Nihoa to the Kure Atoll (except Midway), known as the Northwestern Hawaiian Islands, part of Honolulu. Honolulu is about 1,500 miles (2,414 km) long. Or in other words, it spans the distance from Los Angeles, California, to Denver, Colorado. </p>
                <p>The population of the island of Oahu, where Honolulu is located, makes up about 70 percent of the state's total population. In the Hawaiian language, <em>hono</em> means &quot;sheltered or protected&quot; and <em>lulu</em> means &quot;bay.&quot; So <em>Honolulu</em> means &quot;sheltered bay.&quot; Even though Honolulu is located near the Equator, its climate (temperature and humidity) is moderated by the cooling effect of the steady tradewinds and ocean currents.</p>
                <p>Honolulu has several districts, including the popular tourist destination <a href="../../oahu/cities/waikiki.php">Waikiki</a>, located about 3 miles (4.8 km) east of downtown Honolulu. <a href="../../oahu/attractions/pearlharbor.php">Pearl Harbor</a>, a military harbor that was attacked by the Japanese in 1941, which led to the involvement of the United States in World War II, is located about 8 miles (12.9 km) to the west. Kapiolani Park, Honolulus largest park, is located at the east end of Waikiki, on the foothills of popular <a href="../../oahu/attractions/diamondhead.php">Diamond Head</a>, an extinct volcanic crater. </p>
                <p>Notable institutions are the University of Hawaii at Manoa, the Bishop Museum (noted for its studies of Polynesia), the Honolulu Academy of Arts (known for its Hawaiian and Asian collections) and Kawaiahao Church (1841), where funerals of Hawaiian monarchs took place. Iolani Palace, located downtown right next to the Hawaii State Library, is the only royal palace in the United States. It was constructed as seat of the government in 1882 by the &quot;Merry Monarch&quot; King David Kalakaua and his wife Queen Kapiolani. </p>
                <p>Since World War II, tourism in Hawaii has increased a lot and many hotels, apartment buildings and private residences were built. This has made Honolulu the business and population center of Hawaii. Besides tourism, Honolulu's other industries include printing and publishing, jewelry, clothing, food and beverages, rubber products, construction materials and electronics and computer equipment.<br />
                </p>
                
