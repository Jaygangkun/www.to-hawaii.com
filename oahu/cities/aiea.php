                <h2 class="small">Aiea, Oahu</h2>
                <p>Aiea (<a href="../../oahu/cities/p4.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located on Oahu's south shore, about 8 miles (13 km) west of downtown Honolulu. It is home to the largest indoor shopping center in Hawaii &ndash; Pearlridge (the second largest shopping center after Ala Moana Center in Honolulu). Pearlridge features the island's only monorail system that transports shoppers between Uptown and Downtown Pearlridge. Customers enjoy a scenic ride with views of Pearl Harbor and the Arizona Memorial.</p>
                <? include ("../../theme/text.html") ?>
                <p>Pearlridge has more than 170 stores, restaurants and services. It also has two food courts, a miniature golf course, 16 theaters, an emergency clinic (Kapiolani Medical Center at Pali Momi) and an eight-story office complex. Between the two shopping areas Uptown and Downtown lies Sumida Farm, the largest supplier of watercress in Hawaii. </p>
                <p>Aiea also features the Aloha Stadium, where the National Football League's Pro Bowl takes place every year. The residential area named Aiea Heights extends up the ridgeline above the city.</p>
                
