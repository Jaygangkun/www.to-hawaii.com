                <h2 class="small">Waipahu, Oahu</h2>
                <p>Waipahu is located in the southern area of Oahu, just north of <a href="../../oahu/attractions/pearlharbor.php">Pearl Harbor</a>. It is a former sugar plantation town. Its name refers to an artesian spring. In the Hawaiian language, wai means water and pahu means &ldquo;burst of gush forth.&rdquo; </p>
                <p>The ancient Hawaiians enjoyed the cool and clear water that came out of the ground and named this spring Waipahu. They considered Waipahu to be the capital of the island. Hawaiian royalty used to gather here to enjoy the fresh spring water. </p>
                <p>In 1987, the Oahu Sugar Company was incorporated. The site of the sugar mill was in Waipahu. Nearby attractions include Pearl Harbor and <a href="../shopping/waikelefactoryoutlet.php">Waikele Outlets</a>, a popular outlet shopping center.</p>
