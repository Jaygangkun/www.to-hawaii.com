                <h2 class="small">Wai'anae, Oahu</h2>
                <p>Wai'anae (<a href="../../oahu/cities/p11.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is the main  town on Oahu's leeward coast. That's why this side of the island is also often times referred to as the Wai'anae Coast. This is where leeward residents stock up on food and other necessities. Wai'anae Mall has a drugstore, a bank and a few fast food eateries, food and clothing stores. Wai'anae also has a post office, public library and high school.</p>
                <p>At the Wai'anae Small Boat Harbor, tour operators launch their boats to take people out to snorkel with wild dolphins or to cruise along the coastline. It is a small working harbor where you can also see fishing boats and small yachts. The best swimming beach in Wai'anae is <a href="../beaches/pokaibeachpark.php">Poka'i Bay</a>.</p>
