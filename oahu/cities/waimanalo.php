                <h2 class="small">Waimanalo, Oahu</h2>
                <p>Waimanalo is a small town on Oahu's windward coast, near the southeastern tip of the island. It is a Hawaiian homelands community, meaning that mainly people of Hawaiian descent live here. Waimanalo is backed by the Ko'olau Mountain Range. The mountains are very pretty in this area - steep, jagged and covered with every shade of green.</p>
                <p>Agricultural lots in the valley extend all the way towards the mountains, and bananas, papayas, anthuriums and a variety of organic greens are being grown here. Waimanalo's main attraction for visitors and residents alike is its  beautiful, long, white-sand beach (the longest sandy shoreline on Oahu -<a href="../../oahu/beaches/waimanalobeachpark.php">Waimanalo Beach</a>).</p>
                <? include ("../../theme/text.html") ?>
                <p>The town has a small commercial center. Located just to the north is  Bellows Air Force Station and to the south near Makapu'u Point is Sea Life Park Hawaii. In the Hawaiian language, Waimanalo means &ldquo;potable water.&rdquo; The name refers to the many brackish ponds in the area that were used for irrigation.</p>
                
