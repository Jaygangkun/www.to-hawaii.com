                <h2 class="small">Manoa, Oahu</h2>
                <p>Manoa (<a href="../../oahu/cities/more_manoa_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a> , <a href="../../oahu/cities/p13.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is a residential neighborhood of Honolulu on the island of Oahu, about 3 miles (5 km) east and inland from downtown Honolulu. Manoa encompasses an entire valley and runs from <a href="../../oahu/waterfalls/manoafalls.php">Manoa Falls</a> (located in the mountain) all the way to King Street. Because Manoa receives ample amounts of rainfall (almost daily, even if just a few sprinkles), its vegetation is lush.</p>
                <? include ("../../theme/text.html") ?>
                <p>Manoa is an affluent neighborhood, composed of private houses (many of them built before the 1960s) and low-rise condos. There is a central shopping area, the Manoa Marketplace featuring a farmer's market and other stores and restaurants.</p>
                <p>Manoa is also home to the University of Hawaii at Manoa, the flagship campus of the University of Hawaii system. It is a large campus and surrounding it are several faculty and student resident buildings. </p>
                <p>In the past, Manoa was the site of the first coffee and sugarcane plantations in the Hawaiian Islands. In 1825, John Wilkinson planted the first crops. In the Hawaiian language, the term Manoa means solid, thick, vast, depth or thickness.</p>
                <p>The Manoa Stream, which begins at the Manoa Falls, runs through the valley. It has caused floods after heavy rainfalls, especially for residents that live near it. In October 2004, after heavy downpours, the stream overflowed and caused millions of dollars in damages to the Hamilton Library of the University of Hawaii at Manoa and also to residential homes.</p>
