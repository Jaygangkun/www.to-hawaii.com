                <h2 class="small">Makaha, Oahu</h2>
                <p>Makaha is a small town located on Oahu's west shore, about 34 miles (55 km) northwest of Honolulu. It is a residential community and besides a supermarket and some small restaurants and convenience stores, there isn't much else here. </p>
                <? include ("../../theme/text.html") ?>
                <p>The town's main attraction is its beach, mainly among surfers. In fact, <a href="../beaches/makaha-beach-park.php">Makaha Beach</a> is among the most popular beaches in the world. Every year surf events take place here, such as the Town and Country Pro, the Buffalo's Big Board Classic and the Rell Sunn's Menehune Surfing Championships. It's an ideal spot for surfers of all skill levels. Wave heights reach anywhere from 2 feet just behind the reef to 30+ feet at the so-called Makaha Bowl. Some of the biggest waves ever have been ridden here, including Greg Noll's famous ride during the huge swell of 1969 when waves were between 40 to 50 feet high.</p>
                <p>The beach is wide and is also a nice spot for sunbathing and swimming. The best area for going into the water to just play and swim is the center of the beach, where there is no reef. The waves are calmest during the summer when there may be no surf at all. The high waves usually occur in the winter months. When the surf is up, the beach is known for its strong rip currents. Then it is recommended to stay out of the water.</p>
                <p>Another popular activity at Makaha Beach is diving. The Makaha Caverns is a great dive spot featuring a series of arches, caverns and overhanging ledges, located just outside of the surfing area. But usually diving is only possible during calm waters.</p>
                
