                <h2 class="small">Nanakuli, Oahu</h2>
                <p>Nanakuli is a small town and valley on Oahu's west shore, about 30 miles (48 km) northwest of Honolulu. It is the first town as you drive up Oahu's leeward coast. Nanakuli has a nice beach, known as <a href="../../oahu/beaches/nanakulibeachpark.php">Nanakuli Beach Park</a>. The waves and currents are strong in the winter months, but in the summer it's usually calm and a favorite gathering spot among local residents, who barbecue and picnic here. Most of Nanakuli's residents live in the valley across from Farrington Highway, the town's main main road. </p>
                <? include ("../../theme/text.html") ?>
                <p>In the Hawaiian language, &quot;nana kuli&quot; means to &quot;pretend to be deaf.&quot; It is an interesting story how the town got its name. Oahu's leeward coast is hot and dry, and the ancient Hawaiians knew it is tough to grow food here. Hawaiian custom dictates that one must offer food to passing travelers. So the ancient Hawaiians who lived here felt ashamed that they couldn't offer passing travelers any food since they hardly had any. Instead, they hid from passerbys to avoid having to be hospitable. In case they were seen anyway, they pretended to be deaf. So Nanakuli was known as the town full of deaf people.</p>
                
