                <h2 class="small">Hale'iwa, Oahu</h2>
                <p>Hale'iwa (<a href="../../oahu/cities/more_haleiwa_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) is located on the North Shore of Oahu, about 30 miles (48 km) north of Honolulu. The little town was built by Rev. and Mrs. John Emerson in 1832. In 1898, businessman Benjamin Dillingham opened a hotel here and named it Haleiwa (meaning &quot;home of the frigate birds&quot; in the Hawaiian language). With the building of the hotel and the Oahu Railroad, the town began to grow and flourish. </p>
                <? include ("../../theme/text.html") ?>
                <p>Today, Hale'iwa is the largest town and commercial center on the North Shore of Oahu with a population of 3,970 (2010 Census). It features an old plantation town atmosphere and is a popular destination among tourists. Here you can find unconventional and unique shops, art galleries and surf stores, such as the Strong Current shop where you can buy surfboards designed by North Shore surf legends Mike Diffenderfer and Dick Brewer. </p>
                <p>If you like ice cream, you have to try the famous Hawaiian shave ice. The two local stores Matsumoto's and Aoki's sell this delicious treat, and often times there are many people standing in line &ndash; that's how good it is. There is a small boat harbor in Hale'iwa, as well as three beach parks: <a href="../beaches/haleiwabeachpark.php">Haleiwa Beach Park</a>, <a href="../beaches/haleiwaaliibeachpark.php">Haleiwa Alii Beach Park</a> and <a href="../beaches/kaiakabaybeachpark.php">Kaiaka Bay Beach Park</a>.</p>
                
