<h2 class="small">Lanikai, Oahu</h2>
<p>Lanikai (<a href="../../oahu/cities/p8.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is an upper-class residential community on Oahu's windward coast. It is part of <a href="../cities/kailua.php">Kailua</a>. Lanikai is most known for its great beach. Even though it is just about a half mile long, its powdery soft white sand is a draw to residents and visitors alike. Just offshore are the two <a href="../attractions/mokuluaislands.php">Mokulua Islands</a>, a popular kayaking destination. <a href="../beaches/lanikaibeach.php">Lanikai Beach</a> has been photographed the world over and graces many Hawaiian postcards and wall calendars. </p>
<? include ("../../theme/text.html") ?>
<p>The community is small and doesn't have any supermarkets or other stores. The homes here are expensive and some of them are vacation rentals.</p>
<p>A popular hiking trail in Lanikai is located at the end of Kaelepulu Drive. It is the Ka'iwa Ridge Trail, also known as Lanikai Pillboxes. It leads up the small mountain and from up there one has a nice view of Lanikai, Kailua and the Mokulua Islands. It takes about one hour to hike all the way up. The trail is wide enough to keep away from some steep dropoffs, but nevertheless be cautious. </p>

