                <h2 class="small">La'ie, Oahu</h2>
                <p>La'ie is a small town  on Oahu's northeastern shore, between Hau'ula and Kahuku, about 38 miles (61 km) north of Honolulu. It is a predominantly Mormon community. The town's cultural attractions include the Mormon Temple, the Hawaii campus of Brigham Young University and the Polynesian Cultural Center. </p>
                <p>The temple is the fifth oldest Mormon temple in the world. And the <a href="../../oahu/attractions/polynesianculturalcenter.php">Polynesian Cultural Center</a> is Hawaii's number one paid visitor attraction. It's a large open-air museum and park featuring the cultures of seven Polynesian island nations.</p>
                <? include ("../../theme/text.html") ?>
                <p>Another natural sight of interest is <a href="../../oahu/beaches/laiepointpark.php">Laie Point</a> (also known as Laniloa Point), a prominent lithified dune jutting out into the ocean. To reach it, go toward the ocean from Kamehameha Highway onto Anemoku Street. Then turn right and follow Napaka Street to the end. Two other lithified dunes (Kukuihoolua and Mokualai) lie just offshore as scenic islets. <a href="../beaches/laiebeachpark.php">La'ie Beach Park</a> (Pounders Beach) and <a href="../beaches/hukilaubeachpark.php">Hukilau Beach</a> are located nearby.</p>
                <p>La'ie has a small shopping square with a Foodland grocery store and a Subway, Pizza Hut and Taco Bell restaurant, among others. It is mainly a residential community. In the Hawaiian language, laie means &quot;ie leaf&quot; (&quot;ie&quot; is a climbing pandanus: Freycinetia arborea).</p>
                
