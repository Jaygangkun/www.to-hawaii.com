                <h2 class="small">Kapolei, Oahu</h2>
                <p>Kapolei is located on Oahu's south shore, about 25 miles (40 km) west of downtown Honolulu. In the past, the land where Kapolei is now located was agricultural land, used for sugarcane and pineapple production. Today, Kapolei is rapidly growing and already has  a population of  15,186. The town is being developed as an urban center, second to Honolulu. The projected population for the year 2025 is 173,170. In fact, much of the island's population growth is occurring in the Ewa Plain and the area around Kapolei.</p>
                <? include ("../../theme/text.html") ?>
                <p>One attraction near Kapolei is <a href="../../oahu/attractions/hawaiianwatersadventurepark.php">Wet'n'Wild Hawaii</a>, an 25-acre open-air water park featuring many water slides, pools and hot tubs. Located near Kapolei is Ko Olina, an upscale resort community. The man-made <a href="../beaches/koolinalagoons.php">Ko Olina Lagoons</a> feature nice beach coves and a sheltered area for swimming. </p>
                <p>Kapolei has a public library, post office and schools (a private one, pre-K through 12 opened in 2004). Located near Kapolei is also an industrial area, the Campbell Industrial Park featuring a deepwater port, and Barbers Point Naval Air Station, with its Kalaeloa Airport. </p>
                <p>In the Hawaiian language, the name Kapolei comes from the name for a volcanic cone, Pu'u o Kapolei. &ldquo;Pu'u&rdquo; means hill and &ldquo;Kapo lei&rdquo; means beloved Kapo. According to Hawaiian legend, Kapo was the sister of Pele.</p>
                <p>Kapolei's zip code is 96707, and in 2002, the Pacific Business News magazine reported that this zip code had the second highest income on the island of Oahu, with a median income of $62,303. Almost one in five households has an income of more than $100,000. Also, according to the City and County of Honolulu, Kapolei has a home ownership rate of 70 percent. Furthermore, according to Sterling's Best Places, Kapolei has a median household income of $70,129 compared to the national average of $42,350.</p>
                
