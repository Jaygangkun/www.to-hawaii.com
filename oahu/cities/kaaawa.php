                <h2 class="small">Ka'a'awa, Oahu</h2>
                <p>Ka'a'awa is a small town on Oahu's windward (eastern) coast in the district of Koolauloa. Foreigners often times stumble over the town's name and think it may be misspelt because it has three vowels in a row. In the Hawaiian language, it is written Ka'a'awa. Each one of the &quot;a&quot; vowels is pronounced separately and distinctly. That's why it is set apart by the two okina in the middle, the little hooks that look like apostrophes. They are actually a letter in the Hawaiian language, meaning that the vowels have to be emphasized while pronouncing the word. &quot;Ka'a'awa&quot; means &quot;the yellow wrasse fish&quot; in Hawaiian. These yellow wrasse fish, 'a'awa, are common reef fish in Hawaii.</p>
                <? include ("../../theme/text.html") ?>
                <p>Ka'a'awa is north of Kaneohe Bay, located right on the ocean. Its shore is fronted by a broad fringing reef with a narrow beach (<a href="../beaches/kaaawabeachpark.php">Ka'a'awa Beach Park</a>) that's perfect for long strolls. <a href="../beaches/swanzybeachpark.php">Swanzy Beach Park</a> is located just to the north. Kamehameha Highway (Route 83), the highway that goes around the island, passes right through the town. A long valley, the Ka'a'awa Valley, extends inland and is part of Kualoa Ranch. Here, various tourist activities, such as horseback rides, ATV rides and movie tours are being offered. Many popular Hollywood movies have been filmed in this valley, including <em>Jurassic Park</em>, <em>Godzilla</em>, <em>50 First Dates</em> and <em>Pearl Harbor</em>.</p>
                
