                <h2 class="small">Hawaii Kai, Oahu</h2>
                <p>Hawaii Kai (<a href="../../oahu/cities/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>), located on Oahu's south shore, is the largest of several residential communities at the southeastern end of the island. The town was the brainchild of the billionaire industrialist Henry J. Kaiser, who developed an area of kiawe flatland and a shallow fishpond into an affluent suburb. Starting around 1959, the Hawaii Kai or Koko Marina was dredged from the pond. Several neighborhoods now extend back into several valleys and up the mountain ridges.</p>
                <? include ("../../theme/text.html") ?>
                <p>Attractions include the Koko Marina Center, located on the corner of Kalanianaole Highway and Lunalilo Home Road. Here you will find many shops, restaurants, watersports activities and a large movie theater. It is located directly on the marina and features a nice atmosphere. You can dine right next to anchored boats and enjoy views of the water and the green mountains in the background.</p>
                <p>Hawaii Kai is located along <a href="../beaches/maunaluabaybeach.php">Maunalua Bay</a>. The bay is popular among jet skiers, parasailers and boaters, but not among swimmers since the nearshore ocean bottom is shallow and has a reef.</p>
