                <h2 class="small">Waikiki, Oahu</h2>
                <p>Most visitors who say they are going to Hawaii spend their time here, in Waikiki (<a href="../../oahu/cities/p5.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>). Waikiki is located on the island of Oahu and is a neighborhood of the city Honolulu. It is in Waikiki where most of the hotels, restaurants and entertainment venues are located. </p>
                <? include ("../../theme/text.html") ?>
                <p>Waikiki is also known for its world-famous beach, and no matter what day it is, you'll find hordes of people sunbathing, surfing and playing in the water. Waikiki is Hawaii's center of the tourist industry and many high-rise hotels line the shore, including historic hotels that date back to the early 20th century, such as the Royal Hawaiian Hotel, also known as &quot;The Pink Palace of the Pacific&quot; and the Moana Surfrider Hotel, also known as &quot;The First Lady of Waikiki.&quot; It was the first hotel, built in 1901. Other luxury hotels are the Sheraton Waikiki, the Halekulani Hotel, the Hyatt Regency Waikiki and the Hilton Hawaiian Village.</p>
                
                <p>Waikiki means &quot;spouting waters&quot; in the Hawaiian language. Up until the beginning of the 20th century, Waikiki was separated from the island's interior by springs and streams. It was a wetland, full of fishponds and swamps. Then the <a href="../attractions/alawaicanal.php">Ala Wai Canal</a> was built to drain the area, which turned Waikiki into a prime real estate location. Waikiki stretches from the Ala Wai Canal to <a href="../../oahu/attractions/diamondhead.php">Diamond Head</a>, another world-famous Hawaiian landmark. The extinct volcanic crater is visible from anywhere on <a href="../beaches/waikiki-beach.php">Waikiki Beach</a>. </p>
                <p>Waikiki Beach itself is rather narrow in some areas and had its problems with erosion in the past. This led to construction of groins and beach replenishment projects. The latest sand replenishment project was just completed in May 2012, see more <a href="../../forums/Thread-Parts-of-Waikiki-Beach-Under-Construction">here</a>. At one time in the 1920s and 1930s, sand from California was shipped here to replenish Waikiki Beach. Also, there are rocks and a coral reef on the nearshore ocean bottom, so swimmers should watch their feet. The reef is ideal for bodyboarders and surfers because it creates nice waves, especially during the summer months. Waikiki Beach has several sections, each of which has its own name, such as <a href="../beaches/kahanamokubeach.php">Kahanamoku Beach</a>, <a href="../beaches/kuhiobeach.php">Kuhio Beach</a> and <a href="../beaches/queensbeach.php">Queen's Surf Beach</a> and <a href="../beaches/kaimanabeach.php">Kaimana Beach</a>.</p>
                
