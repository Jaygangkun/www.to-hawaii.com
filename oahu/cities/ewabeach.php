                <h2 class="small">Ewa Beach, Oahu</h2>
                <p>Ewa Beach is located on Oahu's leeward coast (near the southwestern tip of the island), about 20 miles (32 km) west of downtown Honolulu. The word &ldquo;ewa&rdquo; means &ldquo;stray&rdquo; in the Hawaiian language, referring to a myth that says that one day the Gods were playing a game of ulu maika. While playing, one of the &ldquo;rolling stones&rdquo; went off course and landed in an area the Gods called Ewa, for the stray ulu maika.</p>
                <? include ("../../theme/text.html") ?>
                <p>Ewa is often used on Oahu's south shore to give directions. When someone says, go one mile Ewa, they refer to Ewa Beach, which means that the person has to go one mile west. If they want to say east, they usually say Diamond Head, which is east of downtown Honolulu and Waikiki. Other common words are mauka (which means toward the mountains) and makai (which means toward the ocean). </p>
                
