                <h2 class="small">Waialua, Oahu</h2>
                <p>Waialua is a small community on the North Shore of Oahu. In the town's center is the Waialua Sugar Mill, one last remnant of the town's plantation history. It is not in operation anymore since sugar isn't being grown here anymore, but some coffee, bananas and other diversified crops are being grown in this area on a small scale. </p>
                <p>Nearby are a couple of shops, such as the North Shore Soap Factory (located inside the Sugar Mill), Farmer's Market, Waialua Coffee and Island X, selling island products. <a href="../beaches/mokuleiabeachpark.php">Mokuleia Beach Park</a>, <a href="../beaches/kaiakabaybeachpark.php">Kaiaka Bay Beach Park</a> and <a href="../beaches/haleiwaaliibeachpark.php">Haleiwa Alii Beach Park</a> are both located in the Waialua area.</p>
