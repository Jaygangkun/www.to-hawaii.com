                        <h2 class="small">Kahala, Oahu</h2>
                        <p>Kahala (<a href="../../oahu/cities/p9.html" onclick="NewWindow(this.href,'name','2550','750','yes');return false">view panorama</a>) is a Honolulu neighborhood, located on the eastern end of <a href="../../oahu/attractions/diamondhead.php">Diamond Head</a>, along Oahu's south shore. Here you'll find some of the most expensive real estate in Hawaii &ndash; huge and beautiful estates that cost millions of dollars. In 2011 for example, the typical price for a house in Kahala was about $2 million, with beachfront homes going for a lot more than that.</p>
                        <? include ("../../theme/text.html") ?>
                        <p>There are many mansion-like houses, with beautiful lawns and decorated with palm trees and tropical flowers. Many celebrities and business moguls have their vacation home in this area. Kahala is also a favorite location for investors, who first buy and repair an old house and then sell it for a record price.</p>
                        <p>Located in Kahala is also the luxurious Kahala Resort and Hotel, which was formerly known as the Kahala Mandarin Oriental. It lies on beautiful grounds and there's even a small man-made island offshore. Also, the hotel has a dolphin lagoon, where a handful of dolphins live. They offer daily shows for their guests. </p>
                        <p>The Kahala shoreline is backed by upscale beachfront homes. There are a few public access paths to the narrow beach. However, swimming here isn't that good because the nearshore ocean bottom is shallow and rocky. At the east end of Kahala is <a href="../beaches/waialaebeachpark.php">Waialae Beach Park</a>, which is a little more suitable for swimming. The beach park also has a large grassy area and picnic tables.</p>
                        <p>Kahala also has a large mall - Kahala Mall. It features many stores and restaurants.</p>
                        
