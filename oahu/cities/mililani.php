                <h2 class="small">Mililani, Oahu</h2>
                <p>Mililani is located in the center of the island of Oahu, in the central valley between the island's two volcanic mountain ranges, about 16 miles (26 km) north of Honolulu. Although the town is mainly a bedroom community for Honolulu, it has its own shopping centers, parks, schools and a public golf course. Mililani resembles a typical American suburban town. </p>
                <? include ("../../theme/text.html") ?>
                <p>When entering Mililani the first thing you see is a sign that says &quot;Mililani - 1986 All-America City.&quot; There is a story behind this distinction. In 1983, the agricultural pesticides dibromochloropropane (DBCP) and ethylene dibromide (EDB) were found to be contaminating Mililani's drinking water. Mililani residents were worried about his, but met resistence when talking to developers and city officials. </p>
                <p>After a grassroots campaign by Mililani residents, the developer agreed to install a $2.5 million carbon-filter system for the town's existing wells. The state of Hawaii also drilled new wells to provide clean water to the community. This large-scale effort by Mililani residents brought national attention to the issue of agricultural contamination through pesticides. Recognizing Mililani's civic spirit, the National Civic League named Mililani an All-America City in 1986. Until today Mililani is the only community in Hawaii to have received this honor.<br />
                </p>
                <p>In the past, the area was all plantation fields. The first houses in Mililani went on sale in 1968. Today, the town is the third wealthiest zip code in the state of Hawaii, according to the 2006 ranking by Pacific Business News. In 2005, Money Magazine named Mililani as one of the best places to live in the United States. Mililani is made up of an older area, named Mililani Town, and a newer area, named Mililani Mauka. The majority of Mililani's retail and commercial centers are in Mililani Town. In 2010, the population of Mililani Town was 27,629, and the population of Mililani Mauka was 21,039 (total population of Mililani was 48,668).</p>
                
