                <h2 class="small">Wahiawa, Oahu</h2>
                <p>Wahiawa is located in the center of Oahu, 4 miles (6.4 km) north of Mililani and about 20 miles (32 km) north of downtown Honolulu. It is situated in the island's central valley between the two mountain ranges that make up the island (Wai'anae Mountains and Ko'olau Mountains). What's unique about Wahiawa is that it is surrounded on three sides by Lake Wilson, also known as the Wahiawa Reservoir or Kaukonahua. Lakes and reservoirs are rare in Hawaii. To reach Wahiawa, one must drive over one of two bridges on Kamehameha Highway. </p>
                <? include ("../../theme/text.html") ?>
                <p>Wahiawa is surrounded by agricultural fields and military bases, including Schofield Barracks, Wheeler Army Airfield and East Range. Located in Wahiawa is also the U.S. Navy's Naval Computer and Telecommunications Area Master Station Pacific. The southern portion of Wahiawa is increasingly becoming more urbanized as more and more houses are being developed. &quot;Downtown&quot; Wahiawa is not really a tourist town. Mostly people affiliated with the military live here, and Wahiawa also has a large Filipino population. The main road that runs through town is lined with burger joints, cheap bars, used car dealers and tattoo parlors. </p>
                <p>One popular visitor attraction just outside of Wahiawa is the <a href="../../oahu/attractions/doleplantation.php">Dole Plantation</a>. Here visitors can see how pineapples grow and other tropical fruits, such as papaya, mango and lychee. They have a nice botanical garden that also features many colorful flowers. Visitors can also hop on the Pineapple Express, a small train that takes people on a narrated ride through the pineapple fields. They also have a huge maze where people can walk through. In fact, it is the world's largest, according to the Guinness Book or World Records 2001. Two other places that warrant a visit in and around Wahiawa are the <a href="../gardens/wahiawabotanicalgarden.php">Wahiawa Botanical Garden</a> featuring many native and naturalized Hawaiian plants and the <a href="../ancientsites/kukanilokobirthingstones.php">Kukaniloko Birthing Stones</a>, an ancient Hawaiian site.</p>
                <p>In the Hawaiian language, &quot;wahi a wa&quot; means &quot;place of noise.&quot; The name may refer to the heavy surf on the distant coast that could be heard here in the past, when the island wasn't developed yet.</p>
                
