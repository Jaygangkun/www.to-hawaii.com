<h2 class="small">Kane'ohe, Oahu</h2>
<p>Kane'ohe is one of the largest residential communities on the windward coast of Oahu (the other one is neighboring Kailua), located about 12 miles (19 km) from downtown Honolulu. The town's commercial center lies along the highway that runs through it (Kamehameha Hwy). There are several restaurants and a shopping mall complex, <a href="../../oahu/shopping/windwardmall.php">Windward Mall</a>.</p>
<? include ("../../theme/text.html") ?>
<p>Attractions in Kane'ohe include the <a href="../../oahu/gardens/hoomaluhiabotanicalgardens.php">Ho'omaluhia Botanical Garden</a> and three golf courses, Pali Golf Course (public), Ko'olau Golf Club (privately owned but open to the public) and Bayview Golf Park (privately owned but open to the public). </p>

<p>Beautiful Kane'ohe Bay is one of the most photographed spots on the island. A popular gathering place just offshore is the <a href="../../oahu/attractions/kaneohesandbar.php">Kane'one Sandbar</a>, which partially emerges during low tide. It is a favorite hangout spot for local residents who have boats. They like to barbecue out here, play ball games and snorkel. Kane'ohe Bay is also a breeding ground for hammerhead sharks. Across the south end of Kane'ohe Bay on the Mokapu Peninsula lies the Marine Corps Air Station.</p>
<p>Another Kaneohe attraction is the <a href="../../oahu/attractions/byodo-intemple.php">Byody-In Temple</a> at the Valley of the Temples Memorial Park. The temple was built in the 1960s to celebrate the 100th anniversary of the arrival of the first Japanese immigrant workers in Hawaii who  worked in the sugar plantation fields. The temple features a large Buddha statue and is a replica of the 950-year-old Byodo-In Temple located in Uji, Japan.</p>
<p>In the Hawaiian language, &quot;Kane'ohe&quot; means &quot;bamboo man.&quot; There is an ancient Hawaiian legend that tells of a local woman, who compared her husband's cruelty to the sharp edge of cutting bamboo. So the place was named Kane'ohe.</p>

