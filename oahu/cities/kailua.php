                <h2 class="small">Kailua, Oahu</h2>
                <p>Kailua (<a href="../../oahu/cities/p7.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is located on Oahu's windward coast, about 12 miles (19 km) northeast of downtown Honolulu. It is one of the largest residential communities on this side of the island (after neighboring Kaneohe). Kailua has a central commercial district, located along Kailua Road. It is surrounded by mainly single-family homes and a few low-rise apartment buildings.</p>
                <? include ("../../theme/text.html") ?>
                <p>Kailua's main attraction is its beautiful white-sand beach. <a href="../beaches/kailuabeachpark.php">Kailua Beach</a> has  white, soft and powdery fine sand. It's a beachlover's dream, a typical tropical beach we all know from postcards. In fact, Kailua Beach has been repeatedly ranked among the best beaches in the world. In 1998, it was ranked as the #1 beach in the United States by coastal expert Dr. Stephen Leatherman. </p>
                
                <p>The beach is about 2 miles (3.2 km) long and perfect for beach strolls. The ocean bottom slopes gently to overhead depths. It's a good beach for swimming, bodyboarding and kayaking. Because of the steady tradewinds year-round, Kailua Beach is also an ideal location for kite and windsurfers.</p>
                <p>Kailua is also home to the largest wetland in the Hawaiian Islands &ndash; Kawai Nui Marsh. It encompasses over 800 acres (3.2&nbsp;km2). Located just south of Kailua Beach is the small community of Lanikai. It also has a wonderful beach (<a href="../beaches/lanikaibeach.php">Lanikai Beach</a>) that was rated as one of the top ten beaches in the world by Sherman's Travel Magazine.</p>
                <p>In the Hawaiian language, Kailua means &quot;two seas,&quot; or &quot;two currents.&quot; The word &quot;kai&quot; means sea or sea water, and the word &quot;elua&quot; means two. So Kailua is a contraction of these two words. The town was named Kailua because of the two lagoons in the district or the two currents that run through Kailua Bay.</p>
                
