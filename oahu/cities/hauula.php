                <h2 class="small">Hau'ula, Oahu</h2>
                <p>Hau'ula is a small town located on Oahu's northeastern shore, about 30 miles (48 km) north of Honolulu. There is a small commercial center, with a few shops and a supermarket in the town's center. <a href="../beaches/hauulabeachpark.php">Hauula Beach Park</a> is a popular gathering and camping place among local residents, especially during weekends. A fringing reef extends off the shoreline.</p>
                <p>In the <a href="../../hawaiian-language.php">Hawaiian language</a>, hau'ula means &ldquo;red hau&rdquo; (hau is a type of tree, the Hibiscus tiliaceus). In the past, sugarcane was grown in the area around Hau'ula, along the narrow coastal plain inland from the highway.</p>
