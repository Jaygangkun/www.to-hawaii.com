                <h2 class="small">Waialua Estate</h2>
                <p>Waialua Estate is the only coffee farm on Oahu. It is owned and operated by Dole Food Company Hawaii. It started out in the early 1990s the variety &lsquo;Kona Typica.&rsquo; Today, the farm encompasses 155 acres. Trees are planted in hedge rows so that the coffee can be mechanically harvested.</p>
                <p>The farm overlooks Oahu's North Shore and is situated on former sugarcane lands at an elevation of 700 feet (300 m). It is bordered on two sides by Oahu's two main mountain ranges, the Ko'olau Mountains to the east and the Wai'anae Mountains to the west. In addition to producing coffee, Waialua Estate also has a 20-acre cacao orchard. </p>
