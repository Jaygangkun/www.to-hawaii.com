                <h2 class="small">Star of Honolulu, Oahu</h2>
               <p>With its 1,500 passenger capacity, the Star of Honolulu is Hawaii's largest shoreline cruise vessel. Docked at Pier 8 at <a href="../../oahu/attractions/honoluluharbor.php">Honolulu Harbor</a>, adjacent to <a href="../../oahu/attractions/alohatower.php">Aloha Tower</a> and the <a href="../../oahu/shopping/alohatowermarketplace.php">Aloha Tower Marketplace</a>, the Star of Honolulu takes guests on lunch and dinner cruises along the Honolulu and <a href="../../oahu/beaches/waikiki-beach.php">Waikiki Beach</a> shoreline.</p>
               <p>The Star of Honolulu features four spacious decks (six dining rooms and private lounges) located in the Star Ballroom (1st deck), the Galaxy Ballroom (2nd deck) and the Constellation Ballroom (3rd deck). The Observation deck (4th deck) is 60 feet above the ocean and features lounge chairs and binoculars and provides panoramic views. </p>
               <p>The Star of Honolulu is also Hawaii's only A.D.A. compliant vessel with two elevators. Other features include bars and restrooms on all decks, onboard culinary facilities and beautiful interiors inspired by old Hawaii &quot;Boat Days&quot; and Art Deco design.</p>
               <p>Guests can choose between various lunch and dinner cruise packages, as well as whale watch cruises that are offered during Hawaii's winter whale watch season, which runs from December through April. The Star of Honolulu also offers several holiday cruises year-round, including a Valentine's Day Sunset Dinner Cruise, an Easter Brunch Cruise, an Independence Day Cruise and a New Year's Eve Dinner Cruise.</p>
               <p>The company also operates a smaller sister ship, the Dolphin Star, out of Waianae Boat Harbor on Oahu's leeward coast. This vessel, a two-deck catamaran, has a capacity of 149 passengers. The Dolphin Star offers snorkel and dolphin watching cruises along Oahu's west shore.</p>
