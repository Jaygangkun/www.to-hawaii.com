              <h2>Oahu Transportation Tips</h2>
              <p>Unlike some of the other areas in the Hawaiian Islands, the island of Oahu offers the most transportation options. You can either rent a car to explore at your own pace, take public transportation for convenience and affordability, rent a moped or motorcycle, take the Waikiki Trolley or get a taxi.</p>
              <p>All the major car rental companies have stations on Oahu. There is Alamo, Avis, Budget, Dollar, Enterprise, Hertz, National and Thrifty. You can usually find their offices right at the airport or around Waikiki. Just remember to book your car in advance as there are a lot of visitors looking for their own ride.</p>
              <p>If you are planning to drive around Oahu, it is a good idea to know that the streets can get pretty busy. With around 941,000 registered motor vehicles, it can get crowded particularly during the rush hours.</p>
              <p>Another way to explore Oahu is by taking advantage of its public bus system. Aptly named TheBus, the Oahu public bus system has been named America's Best Transit System twice in the past. With 93 routes and around 4,200 bus stops all over Oahu, this fact is not surprising. The fare is pegged at $2.50, which includes one free transfer. If you are planning to be on the move a lot, you can get a visitor's pass for $25, which allows four days of unlimited travel. A monthly bus pass costs $50. You can get a bus pass at any ABC store in Waikiki or at the Ala Moana Shopping Center. To get information on routes, rates and timetables, visit thebus.org.</p>
              <p>Then there's also the Waikiki Trolley, a 34-seat, open-air vehicle that travels through Waikiki and downtown Honolulu. It stops at 12 of the most popular attractions, such as the Aloha Tower, Chinatown and the King Kamehameha Statue. The driver also provides useful commentary throughout the trip, so taking the Trolley can be both educational and entertaining.</p>
              <p>Oahu's newest addition is the Aloha Bus. You can easily recognize these bright red double decker buses with open-air seating on the upper deck. This bus stops at several locations throughout Waikiki, downtown Honolulu and Pearl Harbor, giving you a unique vantage point of major attractions. The Aloha Bus is also equipped with GPS-activated narration in English, Japanese, Chinese (Mandarin), Korean and Spanish. You can purchase your ticket on board.</p>
              <p>Taxis are also a popular way of getting around on Oahu. The island's major taxi companies often times have multilingual drivers. There are also different vehicles to choose from, which range from limousines to cars equipped with wheelchair lifts. At about $35-40 per trip from the Honolulu airport to Waikiki, it is a reasonable way to travel.</p>
              <p>For those looking for a thrill, you can choose to explore the island on two wheels. There are plenty of rental companies that offer mopeds, motorcycles and bicycles for rent. Remember to bring a valid license to be able to enjoy your ride.<br>
              </p>
