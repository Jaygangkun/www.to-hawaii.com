                <h2 class="small">Gordon Biersch Brewery Restaurant, Oahu</h2>
                <p>The Gordon Biersch Brewery restaurant can be found in the Aloha Tower Marketplace at <a href="../../oahu/attractions/honoluluharbor.php">Honolulu Harbor</a>. Hawaii's first brewpub, this restaurant  offers indoor and outdoor seating from its location close to downtown Honolulu. The outdoor seating overlooks  the harbour, providing diners with pleasant views. Offering an informal style,  diners can seat themselves wherever they wish, which is quite unusual in Honolulu and in many other places. The view  of the harbour allows diners to watch the ships as they come and go with their  passengers or cargo. </p>
                <? include ("../../theme/text.html") ?>
                <p>The Gordon Biersch Restaurant has a varied menu with the main focus  tending to be on steak, burgers, seafood, pastas, tacos and salads. A selection  of pupus and desserts is also offered. The food portions, whether salads or  mains, tend to be generous, with meat perfectly cooked and juicy. Not surprisingly, perhaps, for a brewery  restaurant, there is a good selection of beverages including German beer, island-inspired  cocktails, wine and soft drinks. The atmosphere, helped by the waterfront  setting, is considered awesome, and with facilities in the bar for watching  various sports, this tends to draw sports lovers. </p>
                <p>With beer to suit every taste, the Gordon Biersch restaurant is open  seven days a week - from 10.00am until midnight on Sundays through to Thursdays  and from 11.00am until 1.00am on Fridays and Saturdays. </p>
