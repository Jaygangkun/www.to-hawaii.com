                                <p>The Old Spaghetti Factory near Ala Moana Beach in Honolulu is one of a chain of restaurants  started by the Dussin family, with others located in various parts of the United States. The aim of the founders was to  share their love of spaghetti and ensure every visitor to their eateries has a  memorable experience. To this end, the d&eacute;cor in every restaurant, including the  one near Ala Moana, is meticulously attended to. The dining halls are uniquely  furnished to reflect Italian traditions - antique lighting, elaborate stained  glass displays and other old-world items that add to the ambience.</p>
