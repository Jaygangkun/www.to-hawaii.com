                <p>From the moment they opened the first Old Spaghetti Factory in Portland, Oregon in 1969, the Dussins have  believed that honesty, integrity, hard work, respect and satisfied customers  are a recipe for success. Hence, they  strive to ensure every meal at their restaurants is an experience. Virtually  every item on the menu is created from scratch and in-house. Every entr&eacute;e is  served as a full meal, complete with soup, salad or bread and dessert. This  restaurant even serves a free appetizer - salad or soup - with every meal. Some  popular choices on the menu include Spinach and Cheese Ravioli, Baked Lasagna,  Chicken Marsala, Chicken Parmigiana, Sicilian Meatballs, and there are many  more. Being a family-friendly establishment, there is a special kid's menu at  the Old Spaghetti Factory - one that includes favorite kiddie's food, treats  and fun.</p>
