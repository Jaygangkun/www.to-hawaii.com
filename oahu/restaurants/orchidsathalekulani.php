                <h2 class="small">Orchids at Halekulani, Oahu</h2>
                <p>Offering both indoor and outdoor seating, the Orchids restaurant is  located at the Halekulani Hotel looking out onto Waikiki Beach. The dining area is surrounded by  a plentiful supply of bright, multi-colored tropical orchids. The oceanfront  setting is casually elegant and all the menus (breakfast, lunch and dinner)  offer a good mix of local favorites and speciality dishes. The Orchids also  serves afternoon tea and Sunday Brunch, with the latter having been credited by <em>Honolulu Magazine</em> as "Oahu's Best Brunch." </p>
                <? include ("../../theme/text.html") ?>
                <p>Originally the site of a two-story house built by Robert Lewers in 1883,  the Halekulani Hotel - home to the Orchids - has a rich history. In Lewers'  day, local fisherman would come to the front of the house with their canoes to  rest. So welcoming were Lewers' family that the location was named Halekulani,  meaning "house befitting heaven." Today's owners of the Halekulani endeavour to  maintain that Hawaiian spirt of welcome. The Orchids aims to provide a dining experience  that celebrates all the region's flavors - a rare mix of taste, local  hospitality, and beautiful surroundings. Specialty dishes on the menu include  island-raised moi with hearts of palm, ahi-seared sashimi with Cajun spices and  oriental-style steamed onaga. Some of the seating at the Orchids overlook the  yard of the House Without A Key - a beachfront venue that hosts live Hawaiian  music every night from 5.30pm to 9.00pm, the sound of which further  enhances the ambience for patrons of the Orchids.</p>
                <p>One of Oahu's top restaurants, the Orchids is open all day, every day. An &Agrave; La Carte  Breakfast Menu is served from 7.30am to 11.00am (Monday through to Saturday),  lunch is available from 11.30am to 2.00pm (Monday to Saturday), afternoon tea  from 3.00pm to 4.30pm (Monday to Saturday), dinner from 5.30pm to 9.30pm (every  night Monday through to Sunday), and brunch is available from 9.00am to 2.30pm  on Sundays. Proper attire is required for dinner while resort attire is  expected during the day. Reservation is recommended to avoid disappointment.</p>
