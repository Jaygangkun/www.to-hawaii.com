                <h2 class="small">Longhi's</h2>
                <p>One in a family of three restaurants, this Longhi's is located at the Ala Moana Shopping Center on Ala Moana Boulevard in Honolulu. Overlooking Ala Moana Beach, this award-winning restaurant is  situated on the third level and features Mediterranean-style food, with a bias  towards seafood, steak and Italian cuisine. With an open-air aspect, Longhi's  is open from morning until late in the evening, serving breakfast, lunch and  dinner throughout the day.</p>
                <? include ("../../theme/text.html") ?>
                <p>In business since their first restaurant opened in 1975, Longhi's is said  to have been created by a man who loved to eat! The Oahu restaurant was opened in 2008 and  has been serving both local residents and visitors from all over the world  since then. Breakfast at Longhi's starts at the weekends at 7.30am and on  weekdays at 8.00am with a menu that is comprised of a selection of fruits,  pastries, benedicts, omelets, pasta, Italian frittata, and bacon/steak/fish  with eggs and/or sausage. A selection of waffles and pancakes with a variety of  fruit, nut and whipped cream toppings are also offered for breakfast. The lunch  menu is made up of a choice of appetizers, brick oven pizza, risotto, fresh  fish/seafood, steaks, sandwiches, pastas, salads, fresh vegetables, side dishes  and desserts. For dinner, patrons are offered a choice of appetizers followed  by a selection of fresh fish and seafood dishes, steaks, lamb, veal chops,  fresh lobster, chicken, veal scaloppini, pasta, risotto, vegetables, sides,  salads and desserts. An assortment of beverages to suit each menu - coffee,  juices, iced teas, wines, spirts and cocktails - are also available, with a  happy hour every day from 3.00pm to 7.00pm at the bar. </p>
                <p>While the restaurant at the Ala Moana Center was the last Longhi's restaurant  to open, the first was opened in 1976 and is located on Front Street in Lahaina, Maui. The second restaurant "Shops of  Wailea" (located in Wailea) was opened in 2002. The Ala Moana restaurant is  open seven days a week - from 8.00am to 10.00pm on Monday through to Friday and  from 7.30am until 10.00pm on Saturdays and Sundays. It is advisable to  book in advance for dinner.</p>
