                <h2 class="small">Singha Thai Cuisine, Oahu</h2>
                <p>Located close to the Ala Moana Center in Waikiki, Singha is a Thai restaurant with  a choice of indoor and outdoor seating. The award-winning restaurant is owned  by Chai  and Joy Chaowasaree  whose devotion to their homeland is clearly demonstrated in the d&eacute;cor. The  indoor dining area is presented in classic Thai style with an array of bronze  and wood Buddha statues, and an assorted collection of orchids and torch ginger  prominently on display. The brightly-colored flora and small waterfall adorning  the outdoor space provides an intimate and romantic setting for diners.</p>
                <? include ("../../theme/text.html") ?>
                <p>Singha specializes in creating delicious food and  taking good care of its customers. The menu is quite comprehensive and is laden with classic Thai dishes to  tempt virtually every palate. Thai curries and a selection of  creatively-prepared chicken dishes compete with a range of delicious-sounding  seafood platters. Indeed, Singha seem to have a special knack with seafood. One  dish that exemplifies this is the Pad Nam-Prik Pad. Served with steamed rice, this  culinary delight is made of stir-fried scallop, shrimp, calamari, fish fillet,  carrots, green onions, yellow onions, bell peppers and sweet basil in a special  sauce called &quot;Nam Prik Pad.&quot; A selection of appetizers, daily  specials, desserts and drinks complete the restaurant's offerings.</p>
                <p>With enough capacity to cater for  up to 80 guests or over, Singha is a suitable venue for family celebrations and  parties. The restaurant is open daily from Monday through to Saturday from 11.00am to 9.00pm. Each night, from 7.00pm onwards, diners are treated to  performances from Thai dancers, an authentic touch that further enhances the  oriental atmosphere. A take-out service is also available with the option to  order online before pickup. Singha prides itself on the quality of its customer  service and actively invites patrons to provide feedback about their  experience.</p>
