                <h2 class="small">Beachhouse at the Moana, Oahu</h2>
                <p>Surrounded by beautiful tropical views, the  Beachhouse at the Moana can be found at the Moana Surfrider - one of Waikiki's  most iconic hotels. With its striking Victorian architecture and elegant  oceanfront setting, the restaurant is focused on offering contemporary classic  cuisine in an atmosphere that is both relaxing and romantic. </p>
                <? include ("../../theme/text.html") ?>
                <p>The Beachhouse at the Moana aspires to offer its  customers vibrant island-inspired cuisine using the finest quality  locally-sourced ingredients. The menu features steak, a variety of fresh  seafood, and a range of other specially-prepared culinary classics to whet the  appetite. Additionally, with a unique and award-winning selection of wines from  around the world and a list of signature hand-made cocktails to choose from,  diners should find the perfect accompaniment to accentuate the enjoyment of their  choice of food. The Beachhouse offers two styles of dinner, a set dinner  option, which is comprised of three or four courses, and a family-style dinner  where customers can choose individual items from the day's menu. Favorites  include the Filet Mignon, the Kona Lobster Bisque and, undoubtedly, the Kona  Coffee Cheesecake - with macadamia nut mascarpone cream and caramel brittle.  And, of course, the backdrop is perfect - the timeless beauty and tranquility  of the Pacific Ocean within sight at every turn. </p>
                <p>Open daily  from Monday through to Sunday, the Beachhouse at the Moana serves breakfast  from 6.00am to 11.00am, lunch and afternoon tea from 12.00pm to 3.00pm, and  dinner from 5.30pm to 9.00pm. Private functions and seasonal events are catered  for too. From intimate celebrations to gala events, Beachhouse at the Moana  endeavors to make every event a memorable experience. While the restaurant does  its best to cater for walk-ins, it is highly recommended that customers make an  advance reservation to avoid disappointment. </p>
