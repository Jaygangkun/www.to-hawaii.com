                <p>A well-established fine dining fixture in Honolulu and set in unique surroundings,  Hy's Steak House endeavors to offer its patrons the finest USDA-quality prime  beef, a tranquil atmosphere and an all-round five star dining experience. The  restaurant is especially famed for its kiawe broiled steaks. Native to Hawaii, kiawe is a type of fragrant  hardwood that burns at very intense, even temperatures, creating a cooking  process that is believed to retain existing tenderness and add perfect flavour  to beef. As well as steak, Hy's serve a selection of delicious seafood and a  host of other favorites. The restaurant is also well-known for its table-side  food preparation. Caesar Salad, Warm Spinach Salad and a number of flamb&eacute; dishes, such  as Cherries Jubilee and Bananas Foster, are all prepared table-side. And, of  course, a selection of fine wines is available to accompany every meal. </p>
