                <h2 class="small">Kani Ka Pila Grille, Oahu</h2>
                <p>The phrase Kani Ka Pila means "let's make music" and, as well as serving  food and drinks, that is what the Kani Ka Pila Grille does. The restaurant is  located beside the pool at the newly-renovated Outrigger Reef on Waikiki Beach and, nightly, it hosts some of  the best local musicians (including those of international renown) and rising  local stars. While Kani Ka Pila Grille serves lunch and dinner daily, it is the  nightly entertainment that makes this venue exceptional. </p>
                <? include ("../../theme/text.html") ?>
                <p>The combination of food, drinks and the chance to hear some of the best  musicians from around Hawaii ensures that the Kani Pa Pila  Grille lives up to its name. Accompanying a great line-up of entertainment  every night, the restaurant serves casual island cuisine. As well as serving  breakfast and dinner, Kani Pa Pali offers an all-day menu where guests can  partake of fresh seafood and tasty pupu appetizers, such as ahi poke and kalua pork quesadillas, or  just relax over a casual drink. As far as the entertainment is concerned, Luana  Maitland (events manager at the Kani Pa Pila Grille) explains that the concept  upon which the restaurant was founded is not solely to offer good music but to  give "real Hawaiian  music a place it could call home." She further explains that Outrigger has long  been a supporter of the sharing and perpetuation of Hawaii's artistic heritage and that the  format at Kani Pa Pila is a reflection of that support. </p>
                <p>Some of the names that have performed or regularly perform at Kani Ka  Pila Grille include Kaukahi (talented, award-winning four-part group who blend  modern and traditional Hawaiian sounds), Kawika Kahiapo and Martin Pahinui  (award-winning artist and talented vocalist respectively), Weldon Kekauoha  (talented, award-winning Hawaiian musician), Manoa DNA (multi-generational  family of musicians rapidly gaining international recognition), Sean Naauao  (talented composer, signer and musician) and Cyril Pahinui (legendary  award-winning slack key artist). Kani Ka Pila is open every day - from Monday  through to Sunday - from 11.00am to 9.00pm with nightly entertainment on  offer from 6.00pm to 9.00pm. </p>
