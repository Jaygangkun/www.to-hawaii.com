                <h2 class="small">Eggs'n Things, Oahu</h2>
                <p>With three outlets now in operation, Eggs'n Things runs its flagship  restaurant from 343 Saratoga Road - opposite Waikiki post office. Since opening, this  has become one of the most popular breakfast venues in Waikiki. Indeed, one reason why Eggs'n  Things is loved so much is its custom of serving breakfast into the evening. As  a result, it is quite usual to see lines of people, from early morning onwards,  waiting to sample the array of omeletes, pancakes and crepes that are on offer  all day long.</p>
                <? include ("../../theme/text.html") ?>
                 <p> The first Eggs'n Things restaurant was opened by Jerry and Jan Fukunaga  in Ena Road, Waikiki in 1974. The aim of the founders was to use  fresh ingredients, maintain reasonable prices, and treat everyone - locals and  tourists - like family (Ohana). Now, just over forty years later, those goals  still apply and the spirit of Aloha lives on. The menu at Eggs'n Things can be  roughly divided into the savory and the sweet. The savory menu is made up of a  wide choice of meat and egg dishes (sausage and eggs, ham and eggs, bacon and  eggs, ribeye steak and eggs, and so on) and a large selection of omeletes, all  of which are made with three fresh eggs. The pancakes, crepes and waffles come  into the sweet category. These are all laden with generous amounts of fruit,  nuts and (if desired) whipped cream.</p>
                  
                <p>As well as the restaurant in Saratoga Road, an outlet called Eggspress  opened in 2011 in the lobby of the Aston Waikiki Circle Hotel (at 2464 Kalakaua Avenue at Waikiki Beach). This location caters for  takeouts, casual dining, and people on the move. The newest branch of the  restaurant is the first venue outside Waikiki and is located on the corner of Piikoi and Kona Street (near the <a href="../../oahu/shopping/alamoanashoppingcenter.php">Ala Moana Shopping Center</a>). This venue was opened in  response to requests from local devotees who wanted an Eggs'n Things outlet  with parking space. All restaurants are open daily - from Monday through to  Sunday - from 6.00am until 2.00pm for breakfast and from 5.00pm until 10.00pm for dinner. While Eggs'n Things  does not generally serve alcohol, the newest branch (corner of Piikoi and Kona Street) does offer an expanded dinner  menu, with wine and beer served after 4.00pm.</p>
