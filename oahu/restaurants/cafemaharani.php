                <h2 class="small">Cafe Maharani, Oahu</h2>
                <p>Caf&eacute; Maharani is an Indian restaurant located on South King Street in Honolulu. The restaurant is open every  evening, offers an informal dining environment, and the prices are affordable.  Cafe Maharani offers its patrons a full range of delicious and authentic Indian  cuisine using an array of herbs and spices that are selected as much for their  nutritional and digestive benefit as their flavour. While the restaurant does  not serve alcohol, patrons are permitted to bring their own.</p><? include ("../../theme/text.html") ?>
                <p>Opened in 2000, Caf&eacute; Maharani takes pride in offering authentic Indian  food to both local residents and visitors to Oahu. Indeed, it endeavors to make  every meal a feast. The chefs take great care to recreate a myriad of dishes  that reflect the different culinary styles, cooking techniques and ingredients  of the various regions of India's vast nation. The distinct  flavors of the different herbs and spices are evident in the many culinary  creations offered on the menu. A blend of as many as 20, 30 and 40 different  herbs and spices can appear in some dishes. The menu is comprised of a  selection of appetizers (served with home-made mint sauce and tamarind  chutney), chaats, and a wide variety of chicken, lamb, seafood, and vegetarian  speciality dishes. The menu includes a number of hugely popular curries, which  are based on an old family recipe. All dishes are served with a selection of  rice and Indian breads. The breads are home-made and baked in a tandoor, which  is a special type of clay oven native to India. The condiments on the menu  include raita, mango chutney and achar, which is a pickle. A choice of desserts  and non-alcoholic beverages are available to complete each meal.</p>
                <p>Cafe Maharani is considered to be one of Hawaii's best Indian restaurants. It is  open every day - Monday through to Sunday - from 5.00pm to 10.00pm. The restaurant operates on a  first-come first-serve basis. However, it is possible to call 45 minutes before  arrival and have your name added to a waiting list. This cuts waiting time when  you get there.</p>
