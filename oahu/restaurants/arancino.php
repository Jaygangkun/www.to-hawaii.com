                <h2 class="small">Arancino, Oahu</h2>
                <p>Located in the heart of world-famous Waikiki on the south shore of Oahu,  Arancino is an award-winning Italian restaurant chain that serves classic  Italian cuisine in an atmosphere that is warm and welcoming. The restaurant&rsquo;s  dishes are served with an innovative Japanese twist, which makes dining at  Arancino a memorable experience. Each day&rsquo;s menu is crafted with great thought  and care by the restaurant&rsquo;s Executive Chef using authentically-sourced  ingredients.</p>
                <p>Ranked as Honolulu&rsquo;s top Italian restaurant by both Yelp! and Trip  Advisor, Arancino Restaurants have long been serving authentic Italian cuisine  to Hawaiian residents and visitors from all over the world. Indeed, Arancino&rsquo;s  has played host to several high-profile US and Japanese celebrity guests. The  restaurant&rsquo;s recipes reflect Italy&rsquo;s Napoli style and dishes are mainly created  with pastas, cheeses and meats imported all the way from the homeland. The menu  at Arancino also includes fresh locally-sourced produce, such as seafood, fruit  and vegetables, ensuring that the essence of Hawaiian is also present in almost  every culinary creation. Examples of some of the hot favorites on the  restaurant&rsquo;s menu include Panna Cotta, Mista vinaigrette, and Tiramisu.</p>
                <? include ("../../theme/text.html") ?>
                <p>Arancino now operates three restaurants in Honolulu, with the most recent  having opened at the Kahala Hotel &amp; Resort - Arancino at The Kahala - in  June 2013. The other two restaurants are located in Waikiki and are: 1)  Arancino on Beachwalk (Honolulu) and 2) Arancino di Mare (at the Waikiki Beach  Marriott Resort). Not only is the cuisine at Arancino authentic, but the  atmosphere is upbeat and the d&eacute;cor cozy and distinctly Italian, giving a real  feeling of being in Italy. With checked table cloths, Italian music playing,  great food and friendly waiting staff, few seem to find fault with Arancino.  All Arancino restaurants are open seven days &ndash; Monday through to Sunday &ndash;  serving lunch and dinner daily. Although reservations are recommended, especially  during peak periods, it is usually possible just to walk in and be seated  within 15 to 20 minutes.&nbsp;&nbsp; </p>
