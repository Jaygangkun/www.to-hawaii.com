                <h2 class="small">Bali Steak & Seafood, Oahu</h2>
                <p>Located in the Rainbow Tower of the Hilton Hawaiian Village, visitors to the Bali Steak &amp;  Seafood restaurant will enjoy stunning views of Waikiki Beach. The restaurant&rsquo;s open-air  setting is fresh and bright, and its aim is to provide diners with a culinary  experience that incorporates the flavors of the Hawaiian Islands, Bali and the Philippines. </p>
                <? include ("../../theme/text.html") ?>
                <p>Intent on providing patrons with cuisine that gives full expression to  the islands&rsquo; very own harvest, the chef&rsquo;s at the Bali Steak &amp; Seafood  restaurant work closely with local fisherman, farmers and ranchers. These  partnerships allow the restaurant to secure fresh seafood, meat and other  locally harvested produce, which the culinary experts then artistically prepare  using a range of exotic spices and tastes from around the islands. The aim is  to present diners with wholesome dishes that are both elegant and simple, and  to sweep all who visit away on a delicious culinary journey of the islands. As  the restaurant&rsquo;s name suggests, prime steak (USDA-quality) and seafood feature  prominently on the menu. Chicken, pork and succulent lamb is also available,  with every dish seasoned to perfection using the restaurant&rsquo;s own fusion of  local herbs, spices and Kona sea salt. All dishes are accompanied by a choice  of mouth-watering butters and sauces. As well as offering a medley of island  seafood &ndash; locally-caught fish, lobster, scallops and shrimp, served with a  garlic ragout and local vegetables in a variety of recipes, other popular  oceanic dishes include Roasted Mahi Mahi and Seared Ahi Tuna. </p>
                <p>Offering a picturesque oceanfront setting, the Bali Steak &amp; Seafood  restaurant is open in the evenings - Tuesday through to Saturday &ndash; from 5.00pm to 9.00pm. Intimate celebrations are  catered for with private dining rooms available upon enquiry. Visitors are  requested to wear proper aloha attire. While dress shorts are permitted, casual  wear, t-shirts, tank tops, swim shorts and/or slippers are not acceptable.</p>
