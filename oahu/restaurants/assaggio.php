                <h2 class="small">Assaggio, Oahu</h2>
                <p>Facing Ala Moana Beach Park, the Assaggio is situated on the  ground floor of the Ala Moana Center in Honolulu. Offering an elegant dining  environment, this Italian restaurant provides the ideal setting for family  get-togethers, birthday parties, anniversary celebrations and romantic evenings  out.</p>
                <p>After selling his share of a restaurant he co-owned with his business  partner in 1989, Vietnam-born Thomas Ky opened his first restaurant in Kailua. He now  has six restaurants at various locations throughout Oahu, one of  which is Assaggio at the Ala Moana Center. Open  daily for both lunch and dinner, some of the highlights on the Assaggio menu  include Chicken Assaggio, Cheese Ravioli, Eggplant Alla Parmigiana, Fettuccini Alfredo, Shrimp  Assaggio and Calamari. Typical lunch and dinner menus are comprised of cold  appetizers, hot appetizers, soups, salads, vegetable dishes, sandwiches,  pastas, side dishes, grills, house specialties, chef specials, desserts and  coffee. The majority of the entrees are available in a choice of sizes - medium  or large portions. In addition to the extensive menu, Assaggio uses a number of  cooking styles, from which patrons are given the option to select their  preferred method. A choice of wines, beers, cocktails, soft drinks and water is  available from the bar. The predominant atmosphere in Assaggio is warm,  intimate and welcoming, and the restaurant has been the recipient of numerous  awards for its food, style and presentation. </p>
