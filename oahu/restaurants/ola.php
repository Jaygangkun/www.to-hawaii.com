                <h2 class="small">Ola, Oahu</h2>
                <p><strong>This restaurant is permanently closed.</strong><br />
                  <br />
                Located at the Turtle Bay Resort on Oahu's north shore, Ola restaurant overlooks the  ocean from its spot in the grounds of the resort. It is the only restaurant in Oahu that is literally sited on the  beach. Ola serves contemporary American and Hawaiian cuisine encapsulated in a  menu that includes steaks, fresh fish and vegetarian options. The impressive  wine list features over 50 wines from various parts of the world or guests can  choose from a selection of fresh juices, beers and cocktails.</p>
                <? include ("../../theme/text.html") ?>
                <p>The word "Ola" means "life, health, living and alive," and the founders  of this restaurant wanted its patrons to enjoy an experience that stimulates  all the senses. The sand beneath one's feet, contemporary island food, the  romantic setting, and the gentle ocean breeze... it's all here at this open-air  restaurant. Ola aims to provide innovative cuisine. The menu is based on  ingredients and produce that has been locally-grown or reared within close  proximity of the resort. The fish and different seafood is caught from local  waters and served fresh from the ocean. Beef is sourced from the North Shore  Cattle Company and the restaurant partners with local farmers to secure the  finest produce the island has to offer. This Hawaiian fare is further blended  with flavors from around the world to bring delicious cuisine to every table.  Open for lunch and dinner, the menu offers a selection of starters, soups,  salads, entr&eacute;es and entr&eacute; combinations. And, of course, there is the extensive  selection of wines to complement each menu choice.</p>
                <p>Whether it is a romantic dinner for two, a birthday party, a company  outing for 100 guests or some other type of function, Ola has the facilities to  cater for it. The winner of several awards, this restaurant is open seven days  a week - from Monday through to Sunday. Lunch is served from 11.00am to 3.00pm, dinner from 5.30pm until 10.00pm, and the beach bar is open from 3.00pm until 10.00pm. An online reservation service is  available for those who want to book a table in advance. </p>
