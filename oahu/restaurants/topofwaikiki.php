                <h2 class="small">Top Of Waikiki, Oahu</h2>
                <p>With panoramic views of the Island, the Top Of Waikiki on the top floor of the Waikiki Business Plaza is Hawaii's only revolving restaurant. With  its elegant retro design, diners are afforded breathtaking views of Waikiki, Honolulu, Diamond Head and the beautiful mountains of Oahu when the restaurant makes a full  360-degree rotation every hour. The Top of Waikiki serves a wide choice of  Pacific fusion cuisine. The combination of award-winning food and the stunning  views attract both local residents and visitors from all over the world in  large numbers.</p>
                <? include ("../../theme/text.html") ?>
                <p>The Top of Waikiki can be reached by elevator to the 18th  floor of the plaza and escalator the rest of the way to the top. The restaurant  is three-tier in design and can seat close to 200 diners. An open sit-down bar  forms the centrepiece on the top tier, and here guests can relax over drinks,  appetizers and stunning views. With a daily happy hour, this is an ideal place  to catch up with friends. On the lower tiers, diners can enjoy a selection of  carefully-prepared food from the full dinner menu. Favorites include Chicken  Cordon Blue, Tenderloin of Beef on a Hot Rock, Seared Scallops with Braised  Pancetta, and more. Essentially, whether it's a romantic night out for dinner,  a family reunion, a group celebration, a corporate event or an evening's fun at  the bar, the Top of Waikiki has it covered.</p>
                <p>Having hosted over a million visitors, some from all over the world,  since opening in 1965, Top of Waikiki has attracted a lot of positive media  coverage and won many accolades for its cuisine, ambience and spectacular  setting. The bar and restaurant are open seven days a week, from Monday through  to Sunday. Dinner is served from 4.30pm until 9.30pm daily, with Sunset Specials on  offer from 4.30pm until 6.00pm. Complimentary parking is offered  to patrons, and proper attire - no beachwear or sleeveless shirts - is  requested. A reservation service is available, and this is particularly  advisable for larger parties.</p>
