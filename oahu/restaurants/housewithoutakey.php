                <h2 class="small">House Without A Key, Oahu</h2>
                <p>One of the best-known venues in Waikiki for meeting and dining - House Without A Key  - offers nightly entertainment in a memorable oceanfront setting. The venue  faithfully maintains one of the original Halekulani's favorite traditions  - Hawaiian music and cocktails under a century-old Kiawe tree. The restaurant  with its beachfront bar is a popular meeting spot - people gather here  regularly to enjoy informal breakfasts, lunches, dinners and drinks, to watch  the sunset, or to take in the superb views of Waikiki Beach and Diamond Head. </p>
                <? include ("../../theme/text.html") ?>
                <p> Made famous in a 1925 novel by Charlie Chan, House Without A Key is  considered one of the best restaurants in Waikiki and is renowned for its signature cocktail -  the mai tai. As well as its wide array of cocktails, the restaurant serves  breakfast, lunch, afternoon tea and dinner throughout the day. Appetizers,  salads, main courses and desserts are all available. Tuna &amp; Foie Gras Croquette, Seafood Curry or  Organic Chicken Tagine are just some of the lunchtime options, while the dinner  menu features such culinary creations as Nairagi &amp; Asian Pear Tartare,  Seared Scallops, Lobster &amp; Beef Cheeks and Beef Ribeye or Tenderloin, all  carefully prepared and beautifully presented. Another great treat at House  Without A Key is the time-honored Hawaiian tradition of afternoon tea. This is  an opportunity to sit back with friends or family over a selection of  international teas and coffees served with freshly-baked pastries and cakes.</p>
                <p>Nights at House Without A Key are occasions to enjoy  traditional Hawaiian music and dance. Each night, from 5.30pm to 830pm, five  former Miss Hawaii winners - Radasha Ho'ohuli, Skyler Kamaka, Lauren Matsumoto  Kanoe Miller and Debbie Nakanelua complement the music with graceful hula dance  performances. House Without A Key is open daily - from Monday through to Sunday  - from 7.00am to 9.00pm. It is a great place to meet with friends, partake of  good food, enjoy a few drinks, watch the sunset, and take in the excellent  view.</p>
