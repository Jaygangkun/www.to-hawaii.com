                <h2 class="small">Azure Restaurant, Oahu</h2>
                <p>Looking out onto Waikiki Beach and Diamond Head, the Azure restaurant is located  at the Royal Hawaiian Resort. The award-winning seafood restaurant takes its  name from the &lsquo;blue&rsquo; of its surroundings. Azure is a shade of blue that is  regularly described as the color of the sky on a fine sunny day or the color of  blue at 210 degrees. In this case, it is intended to reflect the clear blue  skies over Hawaii and the beautiful blue hues of the Pacific Ocean. The  sweeping ocean views to the front of the restaurant, the mellow candle light,  the plush banquettes and the inviting pillows serve to create an inviting  ambience and romantic dinner setting.</p>
                <? include ("../../theme/text.html") ?>
                <p>The Azure restaurant aims to bring its customers the island&rsquo;s tastiest  seafood and an exquisite, memorable dining experience. Each morning at 5:30 am  a brass bell rings out to announce the opening of the Honolulu fish auction.  The sound of the bell provides the prompt for the chefs from the Auzure to  begin their hunt to find the freshest catch of the morning.&nbsp; Driven by a passion for quality and excellence,  they hand select the finest locally caught fish available &ndash; ahi, moi, onaga,  opah, opakapaka, and more. Once the chefs have made their selection, the fish  are expertly prepared to create a variety of dishes to please virtually every  palate. Diners can choose between innovative local regional dishes created with  bright tropical flavors or classic offerings such as a high-heat roasted  preparation infused with aromatic herbs. Knowledgeable servers and sommeliers  are on hand to help diners choose the perfect wine to complement their  particular choice of food.</p>
                <p>Set under the canopy of the Royal Hawaiian hotel's impressive  architecture with superb views of the Waikiki beachfront, the Auzure restaurant  is open every evening &ndash; Monday through to Sunday &ndash; from 5.30pm to around  9.00/9.30pm. The restaurant also offers private dining facilities to cater for  groups of anything from 30 to 65 people. This is ideal for group outings,  celebratory events or any type of special occasion.&nbsp;&nbsp; </p>
