                <h2 class="small">Wailana Coffee House</h2>
                <p>Situated opposite the Hilton Hawaiian Village in Waikiki, the Wailana Coffee House is open  all day (24 hours a day) every day. The fact that it is always open makes this  a popular spot for anyone in need of sustenance, especially late at night when  other eateries have finished serving. With affordable prices, a laid-back  ambience and friendly service, the Wailana Coffee House is a multiple winner of  the 'Ilima Award.' </p>
