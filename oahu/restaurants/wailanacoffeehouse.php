                <p>Offering an all-day breakfast, the delicious aroma  of bacon from the Wailana Coffee House tends to draw people in at all times of  the day. Appealing in equal measures to local residents and visitors, this  coffee house attracts late night revelers looking for a tasty snack as well as  those seeking a substantial morning meal to start the day. The wide variety of  waffles and pancakes also come highly recommended. In addition to all-day  breakfasts, the Wailana Coffee House serves lunch and dinner. These menus,  which feature a number of favorites such as shrimp and catch of the day, are  accompanied by a salad bar that is well-stocked with an array of salad items  and fresh fruit. The atmosphere is a family-friendly one and the attractive  prices are also a draw for families. Come night, a selection of alcoholic drinks is available in the cocktail  lounge, which also hosts karaoke and live music from 9.00pm until midnight.</p>
