                <h2 class="small">Tiki's Grill & Bar, Oahu</h2>
                <p>With spectacular views of the ocean, Tiki's Grill &amp; Bar at the Aston  Waikiki Beach Hotel is a popular meeting spot for both local residents and  visitors to the island. The d&eacute;cor is distinctly South Pacific retro and  features open-air seating on the lanai. With distinct American and Pacific Rim influences, the cuisine is based  on fresh local produce. Offering a happy hour every day and live music at  night, Tiki's Grill &amp; Bar aims to provide its patrons with delicious food,  a chance to relax, and an all-round wonderful experience.</p>
                <? include ("../../theme/text.html") ?>
                <p>Open since 2001, Tiki's has built its reputation on providing a fun  environment without compromising quality, by treating everyone like family, and  by extending a warm welcome to all who visit. Serving a range of  locally-inspired dishes, the cuisine is designed to incorporate the flavors of  the islands while simultaneously supporting local farmers and businesses. Some of the more popular menu  offerings include charred Ahi Tuna slices, Thai Chicken, and crusted Ono with pancetta  bacon and lemongrass. The  restaurant's commitment to serve fresh island produce, especially its endeavors  to serve locally-caught seafood, earned an award from TripAdvisor. The beef  used is  island-reared and grass-fed to ensure the highest quality standards. A wide  selection of drinks is available to accompany the carefully-prepared food. This  includes a selection of wines, beers and mouth-watering cocktails. With a happy  hour every day from 2.00pm until 5.00pm, special drinks offers most nights from  5.00pm until closing, and live Hawaiian entertainment every evening, Tiki's  Grill &amp; Bar does everything possible to create a memorable experience for  its patrons.<br>
                  <br>
                  Offering the perfect ambience to relax after work or get the most from a  vacation, Tiki's Grill &amp; Bar is only 150 steps from the idyllic blue water  of the Pacific. Combining some best-loved local classics with the highly  exciting, this restaurant is all about serving gourmet cuisine. Lunch is served  daily from Saturday through to Sunday from 10.30am to 4.00pm and dinner is available from 4.00pm to 10.30pm. Reservations are advisable, especially for those  who would like an outdoor table with ocean views. </p>
