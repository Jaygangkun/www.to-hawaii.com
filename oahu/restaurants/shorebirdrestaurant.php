                <h2 class="small">Shore Bird Restaurant & Beach Bar, Oahu</h2>
                <p>The Shore Bird Restaurant &amp; Beach Bar at the Outrigger Reef Hotel on Waikiki Beach is a dining experience with a  difference. The unique aspect of dining at this restaurant is that patrons are  not served their food ready-cooked, rather they cook their own on a large  community grill - just the way they want it. With a fun atmosphere and pleasant  views of Diamond   Head, to  the eastern side of the Waikiki coastline, this beachfront venue is a popular meeting place for both  locals and tourists.</p>
                <? include ("../../theme/text.html") ?>
                <p>Opened over 30 years ago, the Shore Bird serves breakfast, lunch and  dinner, and it even has its own bakery. However, it is at night that things  really come alive when diners try out their own cooking skills. This is how it  works. A server brings whatever entr&eacute;e a diner orders, whether that is seafood,  chicken or steak, to their table with instructions on how to prepare that  particular food. It is then up to each individual to spice and season the food  to their own taste and grill it to their liking. Each entr&eacute;e is accompanied by  access to the all-you-can-eat salad bar. The salad bar is comprised of over 25  items, including bread, various fruits, garnishes, greens, haupia, pasta, poi,  tako poke and rice. Experienced helpers (or Grill Masters) are on hand to  assist. While all this might sound like cooking at home, the great aspect is  that all the preparatory work and clearing away is done by someone else! In  addition to the food, the Shore Bird Beachfront Bar offers a large selection of  tropical drinks, the coldest beer on tap in Waikiki, and free karaoke  sessions after 9.30pm.</p>
                <p>Dinner at the Shore Bird Restaurant &amp; Beach Bar is available between 4.00pm and 10.00pm from Monday through  to Saturday. The restaurant also serves an all-you-can-eat buffet breakfast,  which includes, juices, coffee, roast turkey, ham, fish, hash browns and more,  between 7.00am and 11.00am from Monday through to Sunday. Lunch is served from  11.00am to 3.00pm (Monday to Sunday)  with a choice of buffet or items from a menu. And, to ensure a lively night  out, the Shore Bird provides live entertainment every night from 5.00pm onwards.</p>
