                <h2 class="small">The Edge of Waikiki, Oahu</h2>
                <p>An adult-only bar, the Edge of Waikiki is to be found at the Sheraton  Waikiki Hotel. It is situated beside the hotel's infinity edge pool and  provides superb views of the Pacific Ocean and Diamond Head. From this beachfront location  guests can enjoy a "Vint-Edge" cocktail. The bar also offers a selection of  seafood appetizers, artisan sandwiches and brick oven pizzas. Evening  entertainment is provided by some of Hawaii's most celebrated musicians.</p>
                <? include ("../../theme/text.html") ?>
                <p>The Sheraton Wakiki is a towering structure on a prime stretch of the  world-famous Waikiki beach. The resort is trendy, stylish and sophisticated, offering patrons  a high level of comfort. In the midst of this sits the Edge of Waikiki - the  resort's signature pool bar - notable for its lively atmosphere. The bar was  inspired by glorious bygone times, mainly the 1920s to 1960s era, when famous  and influential Hollywood stars would visit Waikiki. The cocktail menu is extensive and includes classic Mai Tai, Over the  Edge, Coco Splash, A&ccedil;aipirinha and Mango Margari-to as well as a vast choice of  Hawaiian tropical combinations. For example, the Fruit Daiquiris &amp; Margaritas is a  concoction of cruzan aged light rum or sauza silver tequila blended with a  choice of strawberry, guava, mango, pineapple, lime, or banana while the Blood  Orange Sangria is made up of skyy blood orange vodka, pinot grigio wine, malibu  passion fruit rum, passion fruit and a splash of cranberry and pineapple juice.  Food options include pupus, salads, sandwiches, burgers and desserts. The live  nightly entertainment completes the ambience, making for an enjoyable night  out.</p>
                <p>Its location means there are breath-taking ocean views from the Edge of  Waikiki. In addition to the great variety of cocktails, the unique drinks menu includes  wine, beer and non-alcoholic beverages. The Caf&eacute;/bar is open daily from Monday  through to Sunday. Drinks are served from 10.00am to 10.00pm with last orders at 9.30pm. Food service begins at 11.30am each day and finishes at 9.00pm.</p>
