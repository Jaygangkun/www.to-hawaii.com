                <h2 class="small">Ocean House, Oahu</h2>
                <p>The Outrigger Reef Hotel at Waikiki Beach is home to the Ocean  House restaurant, from where there are spectacular views of the beach, Diamond Head, and the beautiful  Hawaiian sunset. While only a few tables are oceanfront, the Pacific is visible  from most tables and there are also some oceanview booth tables available. The  Ocean House, known for its seafood, prime rib and other favorites, endeavors to  avoid products that are chemically treated with hormones, pesticides and/or  antibiotics.</p>
                <? include ("../../theme/text.html") ?>
                <p>Open since 2002, the Ocean House is locally owned  and operated, and its cuisine is popular with both local residents and  visitors. Although the restaurant is known for its delicious slow-roasted prime  rib, its menu includes a number of dishes that are a fusion of Asian and  pacific rim cuisine. There are also gluten-free options available. Some of the  most popular dishes at the Ocean House include Sizzling Ahi Carpaccio, Palehu  Prime Rib, Seafood Lau Lau and Macadamia Nut Crusted Mahi Mahi. Given the  restaurant's support for green practices, seasonal harvesting and  sustainability, it uses fresh, natural ingredients, and serves organic produce  insofar as this is feasible. Whenever possible and when conditions permit, all  fishing is done by line. </p>
                <p>Similar to an old Hawaiian plantation home in design, the Ocean House  evokes memories of a simpler time. The relaxed atmosphere and superb ocean  views make it a very pleasant place indeed to spend time with friends and  family. The restaurant is open every day - from Monday through to Sunday - with  breakfast served from 7.00am to 11.00am and dinner from 5.00pm to 10.00pm. The Ocean House is also available for private  lunch functions, whether it is a business meeting, family party, bridal shower  or other special occasion. While walk-ins are welcome, it is recommended that  diners make an advance reservation because demand in the evening is quite high.  This is especially the case for anyone who would like an oceanfront or booth  table. </p>
