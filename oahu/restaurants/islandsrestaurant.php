                <h2 class="small">Islands Restaurant, Oahu</h2>
                <p>The Islands Restaurant at the <a href="../../oahu/shopping/alamoanashoppingcenter.php">Ala Moana Shopping Center</a> near Waikiki is just one of over 50 in this  family of restaurants spread throughout the United States. The Islands is built on the philosophy of  providing good-quality fresh food, a fun atmosphere and a friendly service.  With a choice of indoor and outdoor seating, the Islands is open for lunch and dinner, and  serves contemporary American cuisine to its many patrons.</p>
               <? include ("../../theme/text.html") ?> 
                <p>The Islands was founded in 1982 by Tony  DeGrazier after he had sampled some of Oahu's cuisine following a day's surfing. Tony  opened the first Islands restaurant in Southern California, and with it, he set out his vision and the philosophy upon which his  restaurants would be based. He wanted to create a sense of place and a sense of  satisfaction he wanted his restaurants to be a place that people would want  to come after a hard day's work or play. To this day, the Islands remains true to those original  ideals. Upon entering the Islands in Oahu, the first notable aspect is the  d&eacute;cor - an abundance of Polynesian bamboo adorns the interior, giving the  eatery a true tropical feel. The menu, which also has a number of gluten-free  options and a special section for kids under 12, is primarily based on all-time  American favorites such as soups, burgers, fish, soft tacos, sandwiches and  salads. In particular, there is a wide choice of gourmet burgers, all served with  fries and other side dishes. A selection of drinks is also available, with a  bar located near the entrance.</p>
                <p>As well as its focus on offering good food, the Islands  is very community-orientated and is always seeking to give something back to  the community in which it operates. To this end, the Islands  sponsors a number of community initiatives each year, especially youth-related  programs, contributing over half a million dollars annually. The restaurant is  open seven days a week - from 11.00am  to 10.00pm on Mondays through to Thursdays, from 11.00am to 11.00pm  on Fridays and Saturdays, and from 11.00am  to 9.00pm on Sundays. A take-out service is also  available.</p>
