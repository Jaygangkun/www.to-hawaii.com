                <h2 class="small">Tropics Bar & Grill, Oahu</h2>
                <p>The Tropics Bar &amp; Grill is located in the Hilton Hawaiian Village and occupies a prime spot on Waikiki Beach. Open for breakfast, lunch and  dinner, this is the ideal place to enjoy Hawaiian beach cuisine. The food at  this restaurant is based on fresh locally-sourced ingredients, with nightly  entertainment provided for guests. The Tropics Bar &amp; Grill is an especially  popular place on Friday night, when a clear view of the Aloha Friday fireworks  show is an added bonus.</p>
                <? include ("../../theme/text.html") ?>
                <p>With both indoor and outdoor seating available, the menu at the Tropics  Bar &amp; Grill features a selection of comfort food, with many dishes enhanced  with an innovative twist. This restaurant boasts of offering a menu that is  based on the finest locally-produced ingredients. The steaks are described as  succulent, the fish mostly line caught, and greens that are the freshest the  island has to offer. Each day at the Tropics Bar &amp; Grill starts with an  abundant breakfast menu that includes wholesome steel-cut oats, farm-fresh  omelets, griddled crepes adorned with island fruits, and any seasonal fare that  is available. Eggs Benedict or the Japanese-style Fried Rice &amp; Eggs come  highly recommended, especially with a helping of fragrantly-fresh Paradise Kona  Blend Coffee. The children's breakfast menu offers favorites like maple  pancakes and mini waffles. The lunch and dinner menus are equally focused on  serving the finest island produce. With an array of entrees that includes Coconut-Crusted  Shrimp, diners can choose from a selection of all-year-round favorites and  seasonal fare. Seafood fresh from the ocean and tender chargrilled steaks can  be rounded off with a delicious dessert and washed down with fresh Kona Coffee.  The children's lunch and dinner menu offers classics such as fish and chips,  cheeseburgers and three cheese pizzas.</p>
                <p>The Tropics Bar &amp; Grill is open seven days a week from Monday through  to Sunday. Breakfast is served daily from 7.00am to 11.00am, lunch from 11.00am to 5.00pm, and dinner from 5.00pm to 10.00pm. Anyone wanting a good outdoor  seat is advised to arrive early because the Tropics gets quite busy, especially  in the evenings. </p>
