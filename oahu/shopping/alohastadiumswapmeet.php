                <h2 class="small">Aloha Stadium Swap Meet, Oahu</h2>
                <p>The Aloha Stadium Swap Meet (<a href="../../oahu/shopping/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is the largest flea market in the state of Hawaii, open every Wednesday, Saturday and Sunday. Tons of goods are being offered, ranging from pre-packaged delicacies, coconut syrup, T-shirts, luggage, local antique collectibles, Hawaiian items, sarongs, jewelry, electronics, music, fruits and so much more. </p>
