                <h2 class="small">King's Village, Oahu</h2>
                <p>King&rsquo;s Village is a very unique place &ndash; a shopping center unlike any other &ndash; located in the heart of Waikiki on the island of Oahu. The old and narrow cobblestone paths, colorful small houses and unique shops have a European atmosphere. Yet King&rsquo;s Village also depicts Honolulu as it might have looked like at the turn of the century. There are a few restaurants and more than 45 shops to discover featuring thousands of items from Hawaii, as well as imports from all over the world. </p>
                <? include ("../../theme/text.html") ?>
                <p>                  At the front gate of King&rsquo;s Village, there is a daily event at 6:15 pm, the &ldquo;Changing of the King&rsquo;s Guard.&rdquo; The King&rsquo;s Guard wears uniforms that are the exact replica of those worn by the Hawaiian Royal Palace Guard for King Kalakaua in 1875. Watch as they do a spectacular rifle drill demonstration after retiring the Hawaiian flag.</p>
                <p>Also visit Waikiki&rsquo;s version of Hollywood&rsquo;s &ldquo;Walk of Fame.&rdquo; The King&rsquo;s Village &ldquo;Celebrity Circle&rdquo; features the handprints of some of Hawaii&rsquo;s best-loved celebrities as well as a few nationally known ones.</p>
                <p>King&rsquo;s Village is open from 10:00 am to 11:00 pm daily and is located at 131 Kaiulani Avenue, next to the Hyatt Regency Waikiki.</p>

