                <h2 class="small">Royal Hawaiian Shopping Center, Oahu</h2>
                <p>Are you in the mood for a classy shopping experience? The Royal Hawaiian Shopping Center is Waikiki's largest shopping spot with more than 110 shops and restaurants, plus a theater, nightclub and coconut grove with an entertainment area. This large, open-air mall spans four levels. Hilo Hattie is one of the largest stores and is accompanied by upscale European boutiques, such as Chanel, Cartier, Fendi, Juicy Couture, Kate Spade, Salvatore Ferragamo and many others. There are also casual shops, including Allure Swimwear and The Walking Company. </p>
                <? include ("../../theme/text.html") ?>
                <p>There are plenty of options to chose from for food and snacks. You'll find 10 sit-down restaurants and more than a dozen spots to grab a quick bite, an ice cream or an iced coffee. You can enjoy traditional Hawaiian food or sushi at the Paina Lanai, a comfortable dining space that overlooks the garden and coconut grove.</p>
                <p>Each day, the Royal Hawaiian Shopping Center hosts free entertainment from hula shows to lei-making to ukulele lessons. You might also be able to catch Waikiki Nei, a live performance featuring a conflagration of songs, chants and language of Hawaiian hosts. It is performed in a state-of-the-art theater.</p>
                
