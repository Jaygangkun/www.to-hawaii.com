                <h2 class="small">Victoria Ward Center, Oahu</h2>
                <h2 style="color:#F00">Permanently Closed</h2>
                <p>Victoria Ward Center in Honolulu on Oahu, Hawaii, offers unique shops, outdoor cafes and entertainment, in addition to being a nice place to stroll in a historic complex. Comprised of four shopping areas, the Ward Center is joined by an enormous movie theater complex. Anchor stores include Nordstrom's, Borders Books, Kua Aina Sandwich, Pier 1 Imports and Starbucks.</p>
                <? include ("../../theme/text.html") ?>
                <p>Ward Center is the place for island-style shopping, from sports wear, men's and women's fashion, jewelry, Hawaiian arts and gifts and home furnishings. There is also a farmers market for fresh local produce.</p>
                <p>The Ward Stadium 16 holds a 16-plex movie theater and is the largest of its kind in Hawaii. Next door, Dave and Busters is a sports bar and restaurant, but best of all, has a row of interactive games known as &ldquo;The Million Dollar Midway.&rdquo; It's basically Las Vegas in Hawaii with the difference that you can't win money while playing the slot machines. Instead, you get coupons that you can exchange for merchandise. Besides playing, Dave and Busters is a great place to watch a game, enjoy happy hour, attend a theme party (like a costumed '70s party), listen to live music and just have a good time.</p>
                <p>Recommended restaurants include Goma Tei Ramen for authentic Japanese noodles, assorted pupus and ice cold beer, Old Spaghetti Factory, known as Hawaii's favorite kamaaina Italian restaurant and Chowder House for their classic clam chowders and oysters.</p>
                
