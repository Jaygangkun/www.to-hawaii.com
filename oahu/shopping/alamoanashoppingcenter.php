<h2 class="small">Ala Moana Shopping Center, Oahu</h2>
<p>Time for some retail therapy? At the Ala Moana Shopping Center (<a href="../../oahu/shopping/more_alamoanashoppingcenter_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) in Honolulu on the island of Oahu, Hawaii, the largest open-air shopping center in the world, you will surely find what you are looking for, in a lush, tree-lined setting. Anchored by favorites like Neiman Marcus, Macy's, Nordstrom's, Sears and Old Navy, you will also find boutiques by international designers, including Juicy Couture, Prada, Armani, Louis Vuitton and Chanel. Easy to find and enhanced by free local performances, such as concert bands and hula shows, this is a key destination for many Oahu visitors.</p>
<? include ("../../theme/text.html") ?>
<p>At Ala Moana Shopping Center, you can pick up the latest &ldquo;great read&rdquo; from Barnes and Noble's and the necessary computer gadgets at the Apple Store. And for the shoe shoppers, you will find everything from Nine West to Foot Locker.</p>
<p>An international food court offers casual and low-priced options, such as Sbarros, Quizno's Subs and Mama's Spaghetti House. Restaraurants are located mainly on the Upper Level 4, including The Pacific Place Tea Garden, Tsukiji Fish Market and Restaurant, Mai Tai Bar, California's Pizza Kitchen, Maccaroni Grill and Bubba Gump Shrimp.</p>
<p>With over 290 stores and restaurants, you can spend a whole day at the Ala Moana Center. Some of the newest stores include: J Crew-on-the-Island, Design Within Reach, Blue Hawaii Lifestyle, Williams-Sonoma, Ann Taylor, and Bare Escentuals. </p>

