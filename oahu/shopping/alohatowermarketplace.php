                <h2 class="small">Aloha Tower Marketplace, Oahu</h2>
                <p>Shopping along the Honolulu Harbor oceanfront with a cool breeze... The Aloha Tower Marketplace (<a href="../../oahu/shopping/more_alohatowermarketplace_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>) on Oahu, Hawaii, could not be located in a better spot. Here you can shop while cruise and container ships dock right next door, making for a unique atmosphere and shopping experience. There are many interesting stores and restaurants here, such as casual Hawaiian stores like Hibiscus Collection, Himawari Boutique, Island Style Attitudes, Pacific Cabana and At Hula (hula dance costumes). At the Aloha Tower Marketplace you'll also find an abundance of gift shops, boutiques, salons and jewelry &amp; accessory shops. You can pick up coupons valid at the retailers from the guest services kiosk.</p>
                <? include ("../../theme/text.html") ?>
                <p>When you get hungry, there are a few choices available, such as Chai's Island Bistro, offering Thai and Pacific Rim food with live Hawaiian music, or Makino Chaya for unique Japanese food like fresh-caught uni (sea urchin). For a quick bite, places like Aloha Tower BBQ and Casa Mia offer convenient take-out meals.</p>
                <p>The Aloha Tower Marketplace is also a coffee lover's heaven. You will find coffee shops with creative names, such as Bad Ass Coffee, coffee shops with island flavors, such as Island Vintage Coffee and well-known names, such as Starbucks. Sweet Memory Chocolate in Hawaii will satisfy chocolate lovers with their freshly made gourmet chocolates. Special events take place at the marketplace year-round, including on July 4 (Independence Day) and New Year's Eve. </p>
                <p>You may also visit the Aloha Tower itself. There's an elevator that takes you to a 360-degree viewing platform on the 11th floor. It's a great place for picture-taking. You'll be able to see Honolulu downtown, the airport, the harbor and Waikiki from a bird's eye view.</p>
                
