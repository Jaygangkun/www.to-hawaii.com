                <h2 class="small">International Marketplace, Oahu</h2>
                <p>Located in the heart of Waikiki, the International Marketplace is an open-air shopping center featuring about 90 stores and 10 restaurants. The Marketplace first opened in 1956 and for many years it was a popular place among visitors and local residents alike to shop for island souvenirs. On the ground floor were many small booths where you could find island apparel, jewelry, flower leis, arts &amp; crafts, souvenirs, luggage and local foods. There was even a place where you could get married. </p>
                <p>After 56 years in operation, the International Marketplace in Waikiki closed its doors on December 31, 2013 (<a href="../../oahu/shopping/more_internationalmarketplace_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see photos of old International Marketplace</a>). The property owner, The Queen's Health Systems, announced that the Marketplace would be redeveloped and replaced by an upscale retail, dining and entertainment complex (at a cost of about half a billion dollars). In August 2016, the International Marketplace reopened and now offers 345,000 square feet of retail and restaurant space. The anchor tenant is a three-level Saks Fifth Avenue. The iconic banyan tree that stood in the center of the old Marketplace and was a key element of its legacy was preserved. The new shopping center was planned around that tree.</p>
