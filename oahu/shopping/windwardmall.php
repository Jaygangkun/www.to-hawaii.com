                <p>So you want a Hawaii shopping experience with a few reliable old favorites? At Windward Mall in Kaneohe on Oahu, Hawaii, you'll find brands you recognize, plus there are unique Hawaiian stores. National stores like Macy's, Sears and Borders are right next to local stores like Ainokea, Skin Industries Hawaii, Get Wet! and Local Motion, with 120 stores in total.</p>
