              <p>The Royal Kunia Country Club is situated high above  the Ewa plains in Oahu, about a thirty minute drive from Waikiki on the north  shore. As well as panoramic views of Oahu&rsquo;s entire south shore, there are  spectacular views of Pearl Harbor, the Ko&rsquo;olau and Waianae mountain ranges,  Pearl Harbor, Diamond Head, and the city of Honolulu from every part of the  golf course. With a host of native tropical flora and palm trees lining the  fairways, the Royal Kunia golf course is beautifully picturesque and has been  described as Oahu&rsquo;s best and most scenic golf course. </p>
              
              <p>Designed by Robin Nelson, Royal Kunia is an 18-hole,  par-72 golfing challenge with 99 bunkers and six water features. This course is  said to offer the ultimate playing experience to both beginners and those with  a low handicap. The course rolls gently over the landscape and is  primarily made up of short par-4s and challenging par 5s. The numerous bunkers  are strategically placed throughout the layout. Because of its coastal  position, the prevailing trade winds are certainly a dominating factor at Royal  Kunia. At the ninth hole, the half-way mark exactly, players will find the  course&rsquo;s signature hole, a par-5 measuring 621 yards from the back tees.  Players will need three good shots from tee to green if they are to avoid the  many obstacles that come in the form of bunkers, trees, a large pond, and the  wind. </p>
              <p>Built initially in 1994 and reopened in 2003, there  is a full range of player and visitor facilities at the Royal Kunia Country  Club. In addition to cart and club rental facilities, there is a driving range  where players can practice or warm up. The pro shop has a good range of golf  attire and equipment. And last but not least, there is a restaurant where  players or members of the public can partake of delicious entrees and excellent  pupu&rsquo;s and/or sate their thirst with beverages from the full-service bar. </p>
