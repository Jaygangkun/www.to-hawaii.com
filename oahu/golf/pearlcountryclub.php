                <h2 class="small">Pearl Country Club, Oahu</h2>
                <p>Open and operational  since 1967, Pearl Country Club offers one of the finest golfing experiences on  Oahu. Overlooking Pearl Harbor, the USS Missouri, and the USS Arizona Memorial,  and with the majestic Waianae mountain range to the west, this is a beautiful  golf course. Located in Aiea, a mere twenty minute drive from Waikiki,  Pearl Country Club is within easy access of the airport. Open to the public all  year round and offering all the amenities of a private golf course, the Hawaii  Pearl Open has been held here every year since 1979 where half the participants  come from Japan's professional golf tour, as well as the country's top  amateurs. The remaining half of the field is made up each year of top  professional and amateur players from Hawaii, the US mainland, and other parts  of the world.</p>
                  <? include ("../../theme/text.html") ?>
                <p>Designed by Akira Sato, Pearl Country Club is an 18-hole,  par-72 course measuring 6,787 yards from the longest tees. The course offers three different sets  of tees to accommodate various skills levels, which makes it suitable for  players of all abilities. With a slope rating of 136 and a USGA rating of 72.7,  the course at Pearl Country Club is made up of rugged, rolling terrain.  Although there are few fairway bunkers or water hazards, the course is lined  with a mix of coconut palms, flowering monkey pod trees, and Northfolk pine,  which, along with the fast greens and uneven terrain, make Pearl Harbor a  challenging and fun golfing experience.</p>
                <p>The amenities at Pearl Harbor Country Club are superb. As well as a  challenging golf course, the club also offers a driving range, a  practice putting green, a short game area, and a golf academy where private,  semi-private, and group lessons are available. The banquet facilities, with stunning view  of the surrounding area, are suitable for both private and business functions  and can accommodate any number of guests from 35 to 325. The  restaurant serves a selection of Hawaii's favorite &quot;pupu's&quot; to sate  players before or after their game. The pro shop is well stocked and caters for  all golfing requirements, whether visitors need apparel and/or equipment.  Rental carts and rental clubs are available for anyone who requires these, and  the club is also available for shotgun tournaments, fundraising events, or  other goodwill functions. </p>
                
