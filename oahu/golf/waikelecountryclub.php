              <h2 class="small">Waikele Country Club, Oahu</h2>
              <p>The  Waikele Country Club is located in the exceptionally beautiful area of Waipio, a  picturesque landscape just about  a half an hour&rsquo;s drive from Waikiki. The golf course provides great views of  Diamond Head, as well as the Ko&rsquo;olau and Waianae mountain ranges, and it  overlooks Pearl Harbor and the sparkling blue waters of the Pacific Ocean.</p>
                <? include ("../../theme/text.html") ?>
              <p>Visitors  to Waikele Country Club can look forward to an enjoyable and challenging  18-hole golfing experience to suit all abilities, from beginner level through  to professional. Designed by Ted Robinson, the most notable features of this  course are its rolling fairways, undulating Tiff Eagle Greens, an abundance of  strategically-located bunkers, and panoramic vistas at every hole. Typical of  this renowned designer&rsquo;s style, an array of ponds and waterfalls make up the  beautiful water features along the course. One of the biggest  challenges on this course is the short par-4 fourth hole, which plays downwind.  It requires a skillful lay-up shot off the tee in order to line up a short-iron  approach to the elevated pond-protected, two-tiered green. The 17th par-3  hole on the back nine, which begins on an elevated tee, is another favorite  with players. This hole plays to a narrow, deep green, which is protected at  the front with water and a sand bunker. It is important to keep the ball in  play off tee if players are to score well at Waikele, but big hitters may be tempted to take aim at virtually every  hole on this course. </p>
              <p>Facilities  at the Waikele Country Club include locker rooms, club rental, shoe rental, and  lessons. As well as offering accessories and souvenirs, the pro shop is well  stocked with the best names in golf attire and equipment. The full-service  driving range and putting areas are an ideal amenity for warming up or getting  some practice in. With superb views of the golf course, the dining room at the  Waikele Country Club Restaurant is the perfect place to relax over lunch with  family or friends. The menu features some great Continental and Pacific-Asian  Cuisine, as well as fresh Sushi and Sashimi, freshly prepared each day by  professional chefs. </p>
              
