              <h2 class="small">Mililani Golf Club, Oahu</h2>
              <p>The Mililani Golf Club is located in Oahu&rsquo;s  central plain, a plateau a mere half hour&rsquo;s drive from Waikiki, in the quite  district of Mililani. In Hawaiian, &ldquo;Mililani&rdquo; means &ldquo;beloved heaven,&rdquo; and  visitors to this club will understand why.&nbsp;  With two mountain ranges &ndash; the Ko'olau and Waianae mountains &ndash;  stretching scenically in the background to the east and west respectively, a  small forest of trees &ndash; African  Tulip, Eucalyptus, Norfolk Pine  - and a host of lush native foliage blooming all year round, this club has a  real sense of remoteness. It is perfect for those who want to feel as if they  are getting away from the hustle and bustle of everyday life.</p>
                <? include ("../../theme/text.html") ?>
              <p>An  18-hole, par-72 championship course, the Mililani Golf Club was designed by Bob  Baldock and is 6,455 yards in  length.&nbsp; The course has been carefully  designed &ndash; the fairways are lined with trees and the greens are amenably fast.  Throughout the layout there are a number of strategically-located bunkers,  crafty doglegs, and several water features to ensure players have a pleasant  yet challenging experience. Although the Mililani Golf Course is friendly enough for beginners,  players will need to be prudent about their club selection and they will need  to apply shrewd management skills if they are to get a good score on this  course. The three finishing holes on the course&rsquo;s front are the main tone-setters  for this particular golf course. The seventh hole is one of Mililani&rsquo;s most  memorable. This is a par-5 challenge that is located close to the renowned one  hundred year-old Waiahole ditch, which cuts through the landing area and forces  players to hit past the canal - a nice feature for anyone who wants to try for  an eagle. The eight hole, a par-4, veers very slightly to the left and  brings the prevailing trade winds into play on the approach. The par-5 ninth  hole, located on a small, narrow green, doglegs to the right and is  heavily-bunkered. </p>
              <p>The twelfth hole is the  primary challenge in the back nine. This is a short par-3 that plays over a  jungle gulch and into the left-to-right crosswind. However, after that, players  will enjoy a solid run on the back. <br />
                The  Mililani Golf Club has a full range of amenities. As well as club rental  facilities and a pro shop, there is a driving range, chipping green, and two  putting greens located close to the clubhouse for anyone who wants to improve their golf skills. As far as food  and drink is concerned, visitors can refresh themselves with breakfast, lunch,  or dinner at Alonzo&rsquo;s Restaurant before or after their game. </p>
              
