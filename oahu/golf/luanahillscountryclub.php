                <p>The Royal Hawaiian Golf Club is nestled at the base of  the Ko' olau mountain range on the east side of Oahu. Framed by ancient 100ft  jungle trees, Mt Olomana can be seen in the background from the course and  clubhouse. Previously known as Luana Hills Country Club, the Royal Hawaiian is  carved through a tropical rain forest in a manner that is reminiscent of  Jurassic Park, and there are wonderful views throughout the course. Indeed, the  one-and-a-half mile journey to the golf club, through the beautiful rain  forest, is a marvel in itself but, upon arrival, the sight of such a  paradise-like golf course is truly amazing. </p>
                
                <p> Designed by  father and son, Pete and Perry Dye, the Royal Hawaiian is an 18-hole, par-72  course. It is 6,595 yards in length, with a slope of 138 from the back tees,  and is designed with contrasting nines, and numerous signature holes. Other features of this course include a  number of forced carries off the tee, par-3 holes with water over to manicured  greens, bunkers bordered by railroad ties, and fairways that drop sharply into  bunkers and ravines. The fairways on the front nine are narrower with more  trees and a more rugged terrain, a nice contrast with the back nine, which are  on smoother terrain and wide open. The layout has numerous bunkers  throughout in various styles and the elevation changes quite frequently. The  course begins on a downward slope into the Maunawili Valley. The back of the course plays  towards the mouth of the valley, which provides players with an excellent view  of the mountain and the waters of the Pacific sparkling away in the horizon. In  all, while the Royal Hawaiian Golf Course is not overly-difficult, is an  intriguing fusion of challenging golf and great natural beauty. </p>
