              <h2 class="small">Hawaii Prince Golf Club, Oahu</h2>
              <p>The  Hawaii Prince Golf Club is located on the Ewa Plain, about forty minutes from  Waikiki, and is comprised of 270 acres of long, lush fairways. The course is  lined with ten strategically-located lakes, 90 well-placed white sand bunkers,  and beautifully-manicured greens. The course was created from land that once  produced sugar cane and was designed by Arnold Palmer and Ed Seay who were  given the land. The entire course, which opened in 1992, is one of great  beauty, character, and variety. It provides players with magnificent views of  the Waianae Mountains from this sunny part of Oahu &ndash; an oasis that enjoys  minimal rain and fresh, cooling trade winds. </p>
              <? include ("../../theme/text.html") ?>
              <p>The  only course of its kind in Hawaii, there are 27 dramatic holes to negotiate at  the Hawaii Prince Golf Club. The course is unique in that it offers three  nine-hole interchangeable layouts that can be played in different combinations.  Each combination provides a variety of conditions and a memorable golf  experience for all players whatever their skills level. The signature holes on  the course are 1) the 398-yard, par-4 A7 hole; 2) the 403-yard, par-4 B9 hole;  the 446-yard, par-4 C2 hole; and the 183-yard par-3 C7 hole. Although related,  each of the three nine-hole courses &ndash; A, B, and C &ndash; has its own identity with  water coming into play at six holes on the A course, at three holes on the B  course, and at seven holes on the C course. The A-C combination is considered  the most difficult while the A-B combination is thought the least challenging.  The putting greens on all three courses are amongst the fastest in Hawaii.</p>
              <p>Visitors  to the Hawaii Prince Golf Club can avail of a great range of services including  shoe and club hire options and a complimentary shuttle service from selected  locations. There is also an excellent practice range, locker rooms, and shower  facilities. Those wishing to take up golf or improve their game can invest in  lessons, which are provided by experienced and friendly professionals. The pro  shop has a good range of apparel and logoed merchandise. The full-service  clubhouse includes a superb restaurant called The Bird of Paradise where hungry  and/or thirsty golfers can enjoy lunch or a selection of cocktails before or  after their game.</p>
              
