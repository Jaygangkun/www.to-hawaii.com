              <h2 class="small">Ewa Beach Golf Club, Oahu</h2>
              <p>The Ewa  Beach Golf Club is located on the Ewa Plain, a mere forty minutes from Waikiki.  The course was designed by Robin Nelson and is reminiscent of a traditional  Scottish Links course in many aspects of its design, most notably the double  green that bears a strong similarity to the Old Course at St Andrews in  Scotland. This striking course, with its lush fairways and rolling greens, is  set in an impressive and beautifully natural Hawaiian surrounding with a  curtain of Kiawe trees providing an idyllic backdrop.</p>
              <? include ("../../theme/text.html") ?>
              <p>An  18-hole, par-72 course that measures 6,711 yards, Ewa Beach is considered  suitable for players of all abilities. The layout is not without its share of  challenge, thanks in great part to the various water hazards and the numerous  bunkers on the tree-lined fairways. The signature hole on this course is the 13th,  a par-3 hole, which requires players to take a tee shot into the wind and over  water. However, most holes are short par-4s with just one par-5. The par-4 No 2  and par-3 No 16 holes share a double green - the only one of its type on Oahu.  The course is designed in a way that takes players away from the clubhouse on  the outward nine holes, and back to it on the inward nine across sandy terrain  that is mostly level. This course was the first in Hawaii to use the  environmentally-friendly Seashore Paspalum grass throughout. </p>
              <p>Ewa  Beach Golf Club offers a wide range of facilities. The banqueting hall, which  overlooks an outside garden and the clubhouse courtyard, can be hired for  private use. It is entirely suitable for corporate events, weddings, meetings,  and other special occasions. There is a family-friendly restaurant with a warm  and welcoming atmosphere and a varied menu where visitors can enjoy local and  clubhouse fare in a setting with superb views of the Pacific Ocean and the  tree-lined golf course. The pro shop is well stocked with a large selection of  golf equipment, golf apparel, golf accessories, gifts, and other goodies &ndash; a  treasure trove where visitors are sure to find something to suit their taste. </p>
              
