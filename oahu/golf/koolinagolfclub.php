              <h2 class="small">Ko Olina Golf Club, Oahu</h2>
              <p>The  award-winning Ko Olina Golf Club can be found on Oahu&rsquo;s sunny west coast at the  Ko Olina Resort, a relatively short drive from Honolulu and Waikiki. The 6,867  yard course is located in a beautiful ocean setting and boasts multi-tiered  greens, wide fairways, spacious landing areas, and picuturesque waterfalls.  From 1990 to 1996, Ko Olina played host to the LPGA Tour's Hawaiian  Ladies Open, in 1992 the Senior PGA Tour's Ko Olina Senior Invitational  tournament was held here, and from 2006 to 2008 the course hosted the Fields  Open in Hawaii tournament. In April 2012, women&rsquo;s professional golf returned to  Ko Olina. The course has won multiple awards and accolades, and has ranked in  the Top 75 Resort Courses in the  US by Golf Digest. </p>
              <? include ("../../theme/text.html") ?>
              <p>The  championship course at Ko Olina is an 18-hole, par-72 challenge for people of  all ages, for experienced players, and for beginners. The course was designed  by Ted Robinson, a well-known golf course architect renowned for his use of  water, and this course is no exception because there are 16 water features  built into eight separate holes. With mild trade winds, well-bunkered greens, and exacting  par-3s, players will be challenged to use every type of club as they negotiate  this course.</p>
              <p>The golf academy at Ko Olina is another noteworthy  aspect of this resort&rsquo;s state-of-the-art facilities. Here, visitors can partake  of personalized instruction should they wish to improve their golfing skills  and general enjoyment of the game. The academy offers a putting green, grass  driving range, and one of the island&rsquo;s best short game practice areas &ndash; with  the help of some of Hawaii&rsquo;s best PGA instructors. The academy takes part in  several Play Golf America programs, which helps players of all ages and skills  level to grow their game and have fun. </p>
              <p>As well as the academy and a challenging golf  course, Ko Olina Golf Club  offers a full range of amenities, such as an excellent golf shop with a good  selection of apparel, headware, top of the range sports footwear, giftware, and  accessories for both seasoned golf players and non-golf players. There is also  a rental service offering full sets of clubs and other golfing equipment. Other  facilities include locker rooms with saunas, showers, and Jacuzzis, all of  which are free-of-charge to guests. Last but not least, visitors to Ko Olina  can enjoy a fine dining experience with plenty innovative cuisine made from  fresh local ingredients at Roy&rsquo;s Restaurant. </p>
              
