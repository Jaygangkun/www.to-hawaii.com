              <h2 class="small">Ko'olau Golf Club, Oahu</h2>
              <p>The  Ko'olau Golf Club is carved into a mountainside in the picturesque eastern side  of Oahu. Situated in an area with three different climate zones, each one  distinctive, the course is created from a tropical rainforest and surrounded by  magnificent mountains. This golf course features drastic changes in elevation,  beautiful winding ravines, and stunning views of cascading waterfalls. Ko`olau  golf course is considered one of the most beautiful courses in Hawaii, and its  stunning setting should inspire players right through their game. </p>
              <? include ("../../theme/text.html") ?>
              <p>Designed by renowned architect Dick Nugent in 1992,  the 18-hole Ko'olau golf course  is considered one of the world&rsquo;s toughest golf courses. Golf Digest placed  Ko'olau at No. 25 on its 2012 list of the most difficult golf courses in the  nation, although this rating means it has dropped from its place at No 3 five  years earlier. The spectacular natural beauty of the course with the tropical  rainforest and the Ko`olau Ridge in the background provides the perfect setting  for all types of golfing and social events. Indeed, it is easy to see why  visitors may be tempted to come here for the scenery rather than a high golf  score. Some people just come to walk around the course, view the waterfalls,  and admire the tropical flowers. The course has four sets of tees, making it  suitable for players of all abilities. The tournament course is 7,310 yards,  has a 75.7 rating, and a challenging slope of 152. However, to accommodate  those who find this setup too tough, there is an easier course layout of 6, 406  yards with more suitable challenges. Another hazard is that of losing balls,  which can disappear into the ravines and the dense jungle along many of the  fairways. Players, especially novices, may find this frustrating. The wet climate  in this area can also make the conditions soggy, another factor that adds to  the course&rsquo;s difficulty.</p>
              <p>The  amenities at Ko'olau Golf Club are excellent. It is an ideal place for hosting  a tournament, a corporate outing, or a fundraising event; it also provides a  memorable setting for client meetings, weddings, birthday parties, and other  family functions. The club is home to a PGA Approved Golf School, an  educational establishment dedicated to training and developing the skills of  aspiring and talented junior golfers. Individual instruction and/or tailor-made  courses, delivered by PGA-qualified instructors, are also available for men,  women or groups who want to learn the game or just fine-tune their existing  skills. Other facilities at the club include a practice range and  restaurant.; </p>
              
