              <h2 class="small">Olomana Golf Links, Oahu</h2>
              <p>Olomana Golf Links is located to the east side of  the Ko'olau mountain range in the Waimanalo community, around a half an hour&rsquo;s  drive from Waikiki. The area can be relied on for its mostly sunny weather and  there are great views of the Ko'olau mountain range from the course. Situated  nearer to the windward shore of Oahu than to the mountains where showers can  appear at short notice, this course is a very enjoyable place to play and a  favorite with locals.</p>
                <? include ("../../theme/text.html") ?>
              <p>                A links-style course, Olomana is an 18-hole, par-72  golfing challenge made up of two very different nines, each of which provides  players with a unique challenge. The  front nine takes players along relatively level fairways, but with significant  water hazards at each hole. By contrast, the back nine are located along  irregular terrain where numerous cavernous and strategically-placed sand bunkers are dotted in rolling hills. Another  constant factor on this golf course are the prevailing trade winds. The eight  hole is the signature hole at Olomana. This par-3 hole is located on a  half-island green, heavily shielded by sand bunkers and surrounded by coconut  trees. While Olomana Golf Links is relatively short for a par-72 course, it is  not without its share of formidable obstacles, all designed to keep players on  their guard and make their game as interesting as possible. In all, it can be  said that Olomana will test the skills of both novice and experienced players  with its many distinct and challenging characteristics. </p>
              <p>Open since 1968, designed by Bob E Baldock, and available for play  all year round, Olomana offers a full range of facilities. Rental carts and  rental clubs are available for anyone requiring them. There is a pro shop on  site with a selection of golf-related merchandise and gifts. There is also a  double-decker driving range with forty tees, which may prove useful for those  who want to get in a little practice in, whether it is to brush up on existing  skills or warm up before a game. Anyone looking for food and/or drinks will  find a selection of local fare at Larry&rsquo;s Grill, and the club has the  facilities to cater for all types of functions &ndash; golf events, business  meetings, weddings, or any type of special occasion. </p>

