                <h2 class="small">Coral Creek Golf Course, Oahu</h2>
                <p>Opened  in 1999, Coral Creek Golf Course is located in Ewa Beach, along the Ewa Plain  to the west of Waikiki. The course winds its way along a unique route that is  surrounded by natural coral rock formations, providing golfers with a most  pleasurable experience. The design incorporates a deep coral base, from which  four lakes spring forth. The largest of these is named Lake Nelson and the  course also boasts a 30 foot waterfall. Although this golf course was the last one  to have opened in Hawaii in the twentieth century, it is the first in the state  to use the luxurious and beautifully verdant Seashore Paspalum grass,  a type of turf that provides high levels of durability as well as a plush  appearance.</p>
                <? include ("../../theme/text.html") ?>
                <p>Designed  by Robin Nelson, Coral Creek Golf Course is considered comprehensive, challenging, fair,  and suitable for all golfers regardless of their skills or handicap level. The  entire par-72 manicured course is 6,808 yards in length and has 18 holes. With  short rough and no grain on the putting surfaces, a feature of Paspalum, the  course is designed to be extremely fair. The holes meander wonderfully through  terrain that is relatively level and with winds that are generally reflective  of typical conditions on Oahu. The par-3 tenth hole is located by the  well-appointed waterfall while the short par-4 twelfth hole is pre-buffered by  Lake Nelson. While short par-4s are a trademark of this course&rsquo;s designer,  Coral Creek has one of the longer and more difficult par-5 holes in the state.  In fact, measuring 636 yards into the prevailing trade winds, Hole 14 should  challenge all hitters, however big and whatever their experience. </p>
                <p>The facilities at Coral Creek Golf Course are  comprehensive. The club can facilitate functions; there is a restaurant, and  visitors can purchase memorabilia, gift certificates, and a range of varied  logoed merchandise from the on-site shop. The covered driving range offers two  practice putting greens, a practice bunker, two chipping areas, and 35 hitting  stations (off mats). All levels of golf instruction are available and provided  by PGA professionals on request.</p>
                
