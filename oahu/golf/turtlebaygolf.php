                <h2 class="small">Turtle Bay Golf, Oahu</h2>
                <p>The Turtle Bay Resort is located on a stretch of  stunning north shore coastline on Oahu and it offers two spectacular golf  courses &ndash; one course designed by Arnold Palmer and the other by George Fazio (<a href="../../oahu/golf/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>),  two renowned names in the fields of golf and architecture. With stunning ocean  views and incredible coastal panoramas, the two 18-hole championship courses at  Turtle Bay have played host to the Champions Tour, the Hawaii State Open, the  LPGA Tour, and Golf Channel&rsquo;s The Big Break.</p>
                  <? include ("../../theme/text.html") ?>
                <p>The Fazio Course at Turtle Bay is the only golf  course in Hawaii to have been designed by George Fazio. With its classic  design, it was originally built in the 1970s and, having proved its mettle over  time, it remains equally popular amongst visitors and professionals. This  course is 6,535 yards long, has a slope of 131, and holes that wind their way  through acres of lush fairways, deep bunkers, and well-maintained greens, all  the while offering amazing views of Hawaii&rsquo;s famous North Shore.&nbsp; <br />
                  &nbsp; <br />
                  While the Fazio course is a superb facility, the  famous Palmer Course is considered the gem of Turtle Bay and has achieved many  accolades, not least being included in Golf Digest&rsquo;s &quot;Top 100 Golf Courses  in the US.&quot; The design of this championship course combines a Scottish  Links-style layout with dense wetlands, contoured fairways that are lined with  trees, and stunning Pacific views from the penultimate hole &ndash; the signature 17th.  This course is comprised of two distinctly different experiences. The front  nine features rolling terrain, sand, water, and wind with few shrubs or trees.  By contrast, the back nine takes the player through a jungle of forest pines  and provides an altogether different feel.&nbsp;  This course comes to an end on a plateau one hundred feet from the  ocean. </p>
                <p>There are two practice facilities near the clubhouse  at Turtle Bay with a driving range, a chipping green with sand-filled bunkers,  and a sizeable putting green. The Arnold Palmer Practice Area is one of the  best in Hawaii and includes a large driving range, a short-game area, and a  pitching green. There is also a Golf Academy where players can obtain  instruction to improve their existing game or completely overhaul their  techniques.</p>
                <p>Other facilities include a complimentary shuttle  service from Waikiki, a fully-stocked pro shop, and a restaurant &ndash; the North  Shore Kula Grille &ndash; where freshly-sourced seasonal produce features heavily on  the menu. Turtle Bay Resort also offers a variety of accommodation for those  who want to stay, and there are facilities to cater for business meetings,  weddings, and all types of social events.</p>
                
