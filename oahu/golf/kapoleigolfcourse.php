                <p>Located in Oahu&rsquo;s second city, to the west of Pearl  Harbor and around 24 miles west of Waikiki, the Kapolei Golf Course in sunny Kapolei is beautifully designed and  magnificently landscaped. The 7001-yard course is dotted with countless coconut  palms, beautiful floral gardens, and no less than five large lakes. The course  was designed by Ted Robinson Senior, a golf course architect who has designed  more than 160 courses during his career and is renowned for his innovative use  of water. Indeed, he was one of the first architects to advocate the use of  water features on golf courses and, in this case, removed huge amounts of  wasteland to create the picturesque lakes and build elevation into the course. &nbsp;As well as their aesthetic value, the lakes  play an important environmental r&ocirc;le by capturing storm water from the  surrounding ground. From 1996 to 2001, this course was the setting for the  LPGA's Hawaiian Ladies Open Tournament, and hosted the Champions Tour Pacific  Links open for a number of years running. </p>
                              <p>The award-winning Kapolei Golf Course is an 18-hole,  par-72 public amenity with four sets of tees to suit all skills levels. The  fairways use Bermuda grass while the greens are laid with Paspalum turf. With gently contoured and  rolling fairways, elevated greens, and over eighty bunkers, the course is not  without challenge.&nbsp; The beautiful water  features come into play at twelve holes, providing an enjoyable golfing  experience for both men and women. Some of the world&rsquo;s greats have played at Kapolei - Greg Norman and Annika Sorenstam, to name  just two. </p>
                <p> Stretching  over 190 acres of rolling terrain that was once a sugar plantation, this course  is renowned for its strategically-placed holes, great beauty, and floral  displays. It requires considerable tactical planning and shrewd play. </p>
