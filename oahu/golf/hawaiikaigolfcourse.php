              <h2 class="small">Hawaii Kai Golf Course, Oahu</h2>
              <p>With  views of Maui and Molokai on clear days and the Pacific Ocean from every hole,  the Hawaii Kai Golf Course can be found in East Honolulu, a distance of about  13 miles from Waikiki. This golf course is comprised of a championship course  and an executive course. Players should be able to see whales playing in the Pacific  during the summer or spouting water as they make their way to waters off Maui  to give birth during the winter months. The tropical winds from the ocean make  play both enjoyable and challenging on both courses.</p>
              <? include ("../../theme/text.html") ?>
              <p>The  18-hole Hawaii Kai championship course, designed by William Bell, is a mere  6,500 yards in length, but it is challenging and the windy conditions are a  significant factor. These can affect the spin of downward shots, which can make  selecting the right clubs something of a problem where players are hitting into  the wind. Many players tend to under club, particularly those who are not  accustomed to the course or the conditions.&nbsp;  However, with a number of well-placed bunkers around the course, the  large greens and wide fairways give all players a fair chance. Along the way,  golfers will enjoy great scenery because the coastline along this course is  possibly the most beautiful on Oahu. <br />
                &nbsp;<br />
                Designed  by Robert Trent Jones Senior, the executive 18-hole course is also relatively  short at 2,223-yards but its small, sloping greens make it quite challenging.  The design is such that the course snakes upwards and downwards along the base  of the Makapuu Cliffs with panoramic views of the coastline and a view of the  Koko Head Crater from every hole. While trade winds always have a bearing, the  greens are always in excellent condition with a tendency to be fast,  particularly in the summer season.</p>
              <p>The  comfortable restaurant/banquet facilities at Hawaill Kai Golf Course are newly  refurbished, and visitors are offered a choice of two climate-controlled, full  bar service rooms in which to enjoy meals or host functions. The Koko Head Room  is the larger of the two and overlooks the Koko Head Crater, the picturesque  Koolau Mountains, and both golf courses. The Molokai Room provides a more  intimate setting and overlooks the beautiful shoreline of East Oahu and the  Ka`iwi Channel.&nbsp;The two rooms can be combined to seat up to 250 people if  or when a special occasion requires it. </p>
              
