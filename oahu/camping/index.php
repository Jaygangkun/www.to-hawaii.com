          <h1>Oahu Camping at Campgrounds / Campsites</h1>
          <div class="new-custom">
            <div class="midpanel_row">
              <div class="start"></div>
              <p><img src="../../images/oahucamping.jpg" alt="Oahu Camping" style="width:100%; max-width: 478px;"  /></p>
              <p>Oahu is the most populous island in the Hawaiian archipelago and most visitors choose Oahu as their vacation destination. There are many hotels and vacation homes and condos all over Oahu, but equally, there are many areas where you can simply pitch your tent and enjoy sleeping under the stars.<br />
                <br />
                If you plan to camp on Oahu, no matter if at a state or county site, you will need to obtain a camping permit in advance. <br />
                <br />
                <strong>Camping at a Hawaii state park on Oahu</strong><br />
                <br />
                All state campsites on Oahu are administered by the Hawaii DLNR, State Parks. They prefer that all reservations are made on their online portal, which is located at: www.hawaiistateparks.org/camping/oahu.cfm (see the button &ldquo;Online Reservations&rdquo; in the top left corner on their website). You can make your reservation no more than 30 days in advance (unlike for campsites on the other Hawaiian islands where you can reserve up to one year in advance). There are other special rules that apply for Oahu only. Camping in state parks is not available on Wednesday and Thursday nights, and Sand Island State Park is only open to camping on weekends (Friday through Sunday nights).</p>
              <? include ("../../theme/text.html") ?>
              <p><strong  class="subtitle">Here is a list of all state campsites on Oahu:</strong></p>
              <p><a href="../beaches/kahanabay.php">Ahupuaa o Kahana State Park</a>: A popular campground is the Ahupuaa O Kahana State Park. It is the only Ahupuaa or land division that connects the ocean to the mountains that is owned by the state. It boasts hiking trails, archaeological sites and a prehistoric fishpond. 10 campsites are available.<br />
                <br />
                Keaiwa Heiau State Recreation Area: Covering an area of 384 acres, the Keaiwa Heaiau State Recreation Area has a camping area, picnic tables and the popular Aiea Loop hiking trail. There are also covered pavilions and barbecue grills. 4 campsites are available.<br />
                <br />
                <a href="../beaches/malaekahana-bay.php">Malaekahana State Recreation Area</a>: (<a href="../../oahu/camping/malaekahana_state_recreation_area.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see  photos</a>) Located on 110 acres with shady ironwood trees and a sandy beach. The campground has picnic tables, outdoor showers and phones. 37 campsites are available.<br />
                <br />
                <a href="malaekahana-campground.php">Malaekahana Beach Campground</a>: Privately managed. This the safest campground on Oahu. It has staff on site and a gated entrance. The campground has 26 tent camp sites, 5 vehicle camp sites, 40 plantation huts and 4 plantation suites. Fees vary for each type of accommodation. There is also a group camping section that can accommodate up to 20 campers. In addition to the camp sites, there are  outdoor showers, fire pits at each camp site and a food truck where one can purchase breakfast and lunch. <br />
                <br />
                <a href="../beaches/sandislandbeachpark.php">Sand Island State Recreation Area</a>: The 102-acre Sand Island is the only camp site in urban Honolulu, overlooking Honolulu Harbor. 35 campsites are available.</p>
              <p><strong  class="subtitle">Here is an overview of the state campsite fees:</strong></p>
              <p>Hawaii residents: $12 per campsite per night for up to 6 persons; $2 per night for each additional person*. Maximum fee per site: $20/night.<br />
                Non-residents: $18 per campsite per night for up to 6 persons; $3 per night for each additional person*. Maximum fee per site: $30/night.<br />
                *Children 2 and under free.</p>
             
             <p>              <strong>Camping at City and County of Honolulu campsites</strong><br />
              <p> To get a camping permit for one of the City and County of Honolulu campsites online, visit https://camping.honolulu.gov<br />
                Camping permits may also be obtained in person at the DPR Permits office on the ground floor of the Fasi Municipal building at 650 South King Street in Honolulu.<br />
