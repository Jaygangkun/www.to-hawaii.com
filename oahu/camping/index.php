              <p><strong  class="subtitle">Here is an overview of the City and County of Honolulu campsite fees:</strong></p>
              <p>If you plan to stay at a 3-day campground, the fee is $32. The fee for a 5-day campground is $52. These are flat fees. You pay this no matter if you plan to stay for just 1 night or for the entire time (3 or 5 days). The fee is per campsite. Each campsite is for up to 10 people. Camping always begins on Friday starting at 5 pm and extends through the weekend ending on either Monday at 8 am (3 days) or Wednesday at 8 am (5 days).</p>
              <p><strong>Some rules and tips for your camping trip</strong></p>
              <p>Keep in mind that campfires and bonfires are illegal on all public beaches and parks in Hawaii. For cooking you may only use above-ground barbecue grills, but they have to be at least 12 inches off the ground. If you are planning to camp during a holiday weekend, it is a good idea to make your reservation early. You can obtain a permit two Fridays prior to the Friday you want to camp at 5 pm (HST).</p>
