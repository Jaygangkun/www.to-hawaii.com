                <h2 class="small">Malaekahana Beach Campground, Oahu</h2>
                
                <p>The Malaekahana Beach Campground (formerly known as Malaekahana State Recreation Area - Kahuku Section - <a href="../../oahu/camping/more_malaekahanacamping_photos.html" onclick="NewWindow(this.href,'name','650','600','yes');return false">photos before the renovation</a>, <a href="../../oahu/camping/p1.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">panorama before the renovation</a>)  is located on Oahu's windward coast, between Kahuku and Laie, about 55 yards (50 m) off Kamehameha Highway, directly on the beach. The 37-acre campground (<a href="../../oahu/camping/more_malaekahana-beach-campground_photos.html" onclick="NewWindow(this.href,'name','550','600','yes');return false">see more photos</a>, <a href="../../oahu/camping/p2.html" onclick="NewWindow(this.href,'name','2550','800','yes');return false">view panorama</a>) is surrounded by ironwood trees and has picnic tables, restrooms, a food truck, activity rentals and hot outdoor showers. Every campsite also has a fireplace and picnic table. </p>
                <? include ("../../theme/text.html") ?>
                <p>The campground has 26 campsites for tent camping, 5 vehicle campsites, 40 plantation huts with beds that sleep up to 4 people (bring your own mattress), 4 plantation suites that sleep up to 4 people but are significantly larger than the huts and include a private bathroom and shower and larger accommodations for groups. With the plantation huts and suites, you have the option to add additional tent campers in your area to accommodate groups. If you don't want to bring food, the campground has a new food truck with plate lunches and other island favorites. There are also supermarkets in nearby Kahuku and Laie. It's about a mile to Kahuku and two miles to Laie. If you don't have a car, the campground is accessible by bus (route #55).</p>
                <p>Malaekahana Bay is a great spot to go swimming and bodysurfing. The waves aren't as big as on the North Shore, but it can still be a lot of fun. The beach is a bit busy on weekends, but usually calmer during the week, especially further south. But the best area for swimming is the northern part of the bay because the nearshore ocean bottom is sandy here. Further south where the beach homes are, there is a coral reef.</p>
                <p>Most campgrounds on Oahu require permits, which have to be obtained at the State Park Office in advance. But to make a reservation at this campground, all you have to do is either go to their website, malaekahana.net and book online, or call their reservation line at 808-262-3628. It is recommended to make your reservation as early as possible. During peak seasons (June to August and December to January and on long holiday weekends), they are often times booked out. Currently, the fee for a tent campsite is $9.41 + tax per person per night. Kama'aina rates (for Hawaii residents) are available.</p>
