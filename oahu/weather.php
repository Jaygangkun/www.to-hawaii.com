              <p>Just like on the other main Hawaiian islands, the weather on Oahu's south and west shore is usually drier and sunnier than on the island's east shore. That's because the predominant tradewinds are blowing from the northeast about 90% of the time and these winds bring moisture with them. As these moisture-laden clouds approach the islands, they get stuck on the mountains, where the moisture comes down as rain. The weather on Oahu's windward (east) shore is therefore usually a lot wetter than on the leeward (west) shore. But thanks to the rain, the windward coast is green and lush while the leeward coast is dry and has no rainforests.</p>
