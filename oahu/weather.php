              <p>Waikiki, a Honolulu neighborhood, is where most visitors to Oahu are headed since this is where most of the accommodation options (hotels and resorts) are located. Waikiki is on Oahu's sunny south shore. It is sheltered from most rain clouds by the Ko'olau Mountain Range. These mountains are visible from Waikiki. On most days of the year one can see grey or dark clouds hanging over the tops of the mountains. But not to worry, the clouds usually stay right there and with them the rain. And when it does rain on Oahu's south shore, it is usually short-lived. Here is an overview of Honolulu's year-round temperatures.</p>
              <p>Oahu's <strong>average ocean temperature</strong> is around 75&deg;F (24&deg;C) in February and 80&deg;F (27&deg;C) in September.</p>
