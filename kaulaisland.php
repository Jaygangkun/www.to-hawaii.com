<p>Ka'ula is a small crescent-shaped islet, located about 23 miles (37 km) southwest of the island of Ni'ihau. The small island is uninhabited, and it is believed that no one ever lived here permanently, even though the island was known to the Native Hawaiians who visited it. Two stone structures have been found near the island's summit, which might have been heiaus (ancient temple sites). The highest point of the island is 548 feet (167 m) above sea level and the total land area is 158.2 acres (0.64 km&sup2;). </p>
