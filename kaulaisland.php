<p>The island is slowly shrinking due to erosion. There is a large cave on the northwestern side of Ka'ula, which is known as Kahalauaola (Shark Cave). The former United States Lighthouse Service built a lighthouse on Ka'ula in 1932, which was in operation until 1947. The U.S. Navy has been using the island as a bombing target since 1952, and unexploded ordnance might still be found on the island. That's why one needs permission from the U.S. Navy to land on Ka'ula. The waters near Ka'ula are visited by fishermen and scuba divers.
