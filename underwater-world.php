              <br />
              <h3>Protect Hawaii's Ocean Ecosystem</h3>
              <ul>
                <li>Do not step on the coral with feet or fins.</li>
                <li>Do not grab onto coral for balance or out of curiosity.</li>
                <li>Even the slightest touch can kill a coral colony.</li>
                <li>Do not attempt to touch or hold any kind of marine life.</li>
                <li>Support reef friendly businesses.</li>
                <li>Use fewer chemicals in your home - even chemical use thousands of miles 
                  away from the islands can end up in the Pacific Ocean.</li>
                <li>Wear floatation devices while snorkeling to help keep you off the reef.</li>
                <li>When diving, use a mooring buoy or sand anchor to keep you away from reef 
                  formations.</li>
                <li>Support organizations that protect coral reefs.</li>
                <li>Don't use chemically enhanced pesticides and fertilizers.</li>
                <li>Educate yourself on the importance of the reef and share your knowledge.</li>
                <li>Buy only sustainably harvested reef fish.</li>
                <li>Recycle</li>
                <li>Conserve water</li>
                <li>Report dumping or illegal fishing.</li>
              </ul>
              <p>Hawaii's underwater world is truly unique and beautiful, and with help from all who visit these Pacific communities, our ocean ecosystem will continue to thrive and grow for generations to come. Wherever you are exploring along the Hawaiian shores, remember the cardinal rule of ocean respect: <em class="red"><strong>look but don't touch</strong></em>.</p>
