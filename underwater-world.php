              <h2>Discover the underwater paradise of Hawaii</h2>
              <p>Many argue that the beauty of Hawaii is unsurpassed by any other location on Earth. And what continues to draw millions of visitors a year is often below sea level. Humpback whales, bottlenose dolphins, stingrays, sea lions, manta rays, colorful reef fish and giant sea turtles - these are just a few of the amazing and diverse sea creatures that frequent the Pacific coastlines of the Hawaiian Islands. Snorkeling, scuba diving and snuba, a combination of the two, are popular activities that allow us land dwellers to peek into the environment that these marine animals call home. </p>
              <? include ("theme/text.html") ?>
              <p><strong><img border="0" src="images/underwater-hawaii.jpg" width="148" height="125" align="right" alt="Underwater world of Hawaii" />Malama 
                I Ke Kai </strong></p>
              <p><em>Malama i ke kai </em>means to "care for or protect the ocean," an integral Hawaiian value that dates back over centuries. The Hawaiian chant of creation, the kumulipo, explains that all life began in the sea - that humanity is tied to all living things, especially marine life. The Hawaiian archipelago is not described as islands in a sea, but rather, a sea of islands, thereby acknowledging the ocean as being equally important with the aina, or land. The ancient Hawaiians shared an intimate knowledge of the sea and showed ultimate respect to its inhabitants. The unique ecosystem of Hawaii has existed in isolation for years and relies on continuous conservation for survival. Visitors to Hawaii can "malama i ke kai" in 
                several ways:</p>
              <ul>
                <li>Learn about Hawaii's delicate ecosystem before visiting.</li>
                <li>Respect all warning signs near the ocean.</li>
                <li>Help keep Hawaiian beaches clear of litter and debris.</li>
                <li>Pick up anything you brought with you, and if you spot rubbish, take a minute 
                  to help it into a trash can.</li>
                <li>Report any knowledge of poaching or dumping.</li>
                <li>Do not desecrate important cultural sites, rock formations, or coral reef.</li>
                <li>Don't feed or harm marine life.</li>
                <li>Leave coral, sand and rocks in their place.</li>
              </ul>
              <p><strong><img border="0" src="images/tropical-fish.jpg" width="148" height="125" align="left" alt="Tropical Fish" />Diving and Snorkeling</strong></p>
              <p>The topographical layout of the Hawaiian coasts presents an underwater world that draws divers from around the world. The volcanic activity that formed the Hawaiian Islands continues to expand the island layout with the help of the active volcano Kilauea on the Big Island, constantly adding to the long lava tubes, ornate arches and coral gardens on the sea floor. Accessing these underwater cities also allows divers to see rare and unique indigenous aquatic species found only in Hawaii. </p>
              <p><strong><img border="0" src="images/coral2.jpg" width="148" height="125" align="right" alt="Coral Reef" />The Coral Reef</strong></p>
              <p>The ubiquitous coral reef ecosystem that surrounds the Hawaiian coastlines is a complex structure that supports countless life forms and nourishes Hawaii's interdependent marine community. From filtering water and providing nourishment to protecting the shores from sediment and storms, the coral reef is the lifeblood of the symbiotic underwater community. Divers will find literally thousands of life forms present in the coral reef, including hard and soft corals, colorful fish of all sizes, octopus, eels, algae, sea anemones, sponges, crustaceans, worms, turtles and many others. Although the massive coral reef is in constant expansion, it also faces difficult challenges, such as pollution, damage from chemicals and other wastes, damage from human destruction, disease and bleaching. Coral reef branches are incredibly delicate and can be destroyed with even minor contact. </p> <?php
