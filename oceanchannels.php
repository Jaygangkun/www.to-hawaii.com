              <strong>Alenuihaha Channel</strong>
              <p> The Alenuihaha Channel lies between the Big Island of Hawaii and Maui. Its maximum depth is 6,810 feet (2,076 m) and it spans 29.6 miles (47.6 km). In the Hawaiian language, alenuihaha means &quot;great billows smashing.</p>
