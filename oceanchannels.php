              <strong> Kealaikahiki Channel</strong>
              <p> The 17.8-mile (27 km) wide Kealaikahiki Channel separates the islands of Lanai and Kahoolawe. The channel's maximum depth is 1,086 feet (331 m). Kealaikahiki  means &ldquo;the road to Tahiti.&rdquo; In the old Hawaii, voyages to foreign lands traveled through this channel. A boat heading directly straight down south from here would arrive in Tahiti, hence its name. </p>
              <strong>Auau Channel</strong>
              <p> The 9.5-mile (15.3 km) wide Auau Channel is the most shallow and protected one in the Hawaiian Islands. It is no wonder that the Pacific humpback whales, which migrate to Hawaii from Alaska each year in the winter, prefer this channel to birth and raise their calves. The channel lies between Maui to the east and Lanai to the west. It is also protected by Kahoolawe to the south and Molokai to the north. Its depth is a mere 252 feet (77 m). In the winter months, many whale watching boats frequent this channel to view the whales. In the Hawaiian language, 'Au'au means &ldquo;to take a bath,&rdquo; referring to the channels bathtub-like conditions. The center of the Auau Channel is known as the Lahaina Roads. It used to be filled with whaling ships when the town of Lahaina on Maui's west shore was the capital of the whaling industry. </p>
            
              <strong>Pailolo Channel</strong>
              <p>The Pailolo Channel lies between Maui and Molokai. Its maximum depth is 846 feet (258 m). Even though it spans just 8.8 miles (14.2 km), it is usually very windy and rough. Pailolo in Hawaiian means &ldquo;crazy fisherman,&rdquo; referring to any fisherman who would be crazy enough to try to navigate this tough channel.</p>
              <strong>Kalohi Channel</strong>
              <p>The 9.2-mile (14.8 km) Kalohi Channel separates Lanai and Molokai. Its maximum depth is 540 feet (165 m). Strong winds and choppy seas are common in this channel. A British vessel, the <em>Alderman Wood</em>, got into trouble off Lanai's north shore in 1824 and sank. Two years later, the <em>London</em>, an American ship was purposely grounded here. After the end of World War II, grounding the ship here was seen as the most economical way to dispose of it. Up until today, the ship's hull can be seen resting on Lanai's aptly named Shipwreck Beach. Even though this channel has a rather turbulent history, its name means &quot;the slowness&quot; in the Hawaiian language. </p>
