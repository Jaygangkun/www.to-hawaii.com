              <p>Completely devoid of vegetation except for some species of grasses, the Pearl and Hermes Atoll (Holoikauaua) is primarily underwater and consists of only a few small sandy islets (seven of them are above sea level) that are contained within a lagoon and surrounded by a reef. While the total land area is just 0.139 square miles (0.359 km&sup2;), the surrounding reef is extensive, covering 450 square miles (1,165 km&sup2;). The atoll's highest point is just 10 feet (3 m) above sea level.</p>
              <p>The Pearl and Hermes Atoll was discovered in 1822 by two English whaling ships, the <em>Pearl</em> and <em>Hermes</em>, which wrecked on the reef during a storm. Because the atoll has such a small land area, it was for the most part spared from the impact that miners and feather hunters had on some of the other Northwestern Hawaiian Islands. In 1854, King Kamehameha III made the atoll part of the Hawaiian Kingdom.</p> <?php
