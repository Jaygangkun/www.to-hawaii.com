?> <p>In  1927, as Captain Anderson was fishing for tuna, he discovered pearl beds within the lagoon. So for two years in the late 1920s, black-lipped pearl oysters were harvested to make buttons from their shells. However, the oysters were over-harvested and nearly eliminated, so in 1929, the harvesting of oysters was prohibited. The atoll was later declared a bird sanctuary. In 2006, the atoll  was declared part of the Northwestern Hawaiian Islands National Monument.</p>
              <p>At present, about 160,000 birds from 22 species have made the atoll their home. Species include black-footed albatrosses, Tristram&#39;s storm petrels, little terns and the Laysan finch.</p>
