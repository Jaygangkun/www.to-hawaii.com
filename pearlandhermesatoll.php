              <p>Dry grasses, herbal plants and vines grow here. These plants are salt-tolerant and recover after the frequent floods on the islands. As for marine animals, the fish are abundant 
                here. In fact, the Pearl and Hermes Atoll has the highest number of fish species and the highest standing fish stock in the Northwestern Hawaiian Islands. Species include Galapagos sharks, sandbar sharks, saber squirrelfish, eels, ulua (big jacks), aweoweo (bigeyes), uhu (parrotfish), angelfish and many lobsters. Also, Hawaiian monk seals and sea turtles breed and feed at the atoll, and the atoll is also a mating area for spinner dolphins. </p>
              <p>Several sponge species have been found in the reef. Some of these may even be new to science. Also, 33 species of corals have been identified here. Even though the Pearl and Hermes atoll experienced less human impact than some of its neighbor islands, one problem remains up until today - marine debris. In 2003, over 90 tons of washed ashore marine debris was removed from the atoll.</p>
