                                 <p><strong>Admissions Day</strong></p>
                <p>Admissions Day is also called "Statehood Day," although it is rarely called that by residents. Admissions Day is observed annually on the third Friday of August to celebrate Hawaii becoming the 50th state of America.</p>
                <p>Hawaii became recognized as a state of the U.S. in 1959. Representatives for Hawaii as a U.S. territory had been trying to get the Hawaii Statehood Bill passed for fifty years. It took five failed attempts (1919, 1931, 1935, 1947, 1950) before the bill was finally passed and signed by President Eisenhower on <strong class="subtitle">August 21</strong>, 1959.</p>
                <p><strong>May Day</strong></p>
                <p>May Day is also known as "Lei Day" in Hawaii. It is celebrated annually usually during the <strong class="subtitle">first week of May</strong>. The holiday was established back in 1928 to celebrate the making and wearing of the flower lei. It seemed appropriate to have this "Lei Day" fall on May Day. </p>
                <p>May Day is celebrated statewide by the wearing of leis, parades and school programs depicting the traditional Hawaiian court. School children usually participate in festivities during May Day.</p>
                <p><strong>Boy's Day</strong></p>
                <p>Boy's Day is also referred to as Tango no Sekku in Hawaii. It is a Japanese holiday that originated back between the years of 593 A.D. to 629 A.D. It was brought to Hawaii by Japanese immigrants.</p>
                               <p>Boy's Day is celebrated annually on <strong class="subtitle">May 5th</strong>. Local families hang large kite-like carps (koi fish) that are suspended on thin bamboo poles from their homes. Families will hang one carp for every boy in their household. The carps represent strength and power. They are thought to bring courage and the ability to meet goals to the boy that they represent.</p>
                <p>Boy's Day is also celebrated in Hawaii with festivals where children can create their own carp kites, have their faces painted and eat Japanese food.</p>
