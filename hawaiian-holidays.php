                <p><strong>Girl's Day</strong></p>
                <p>Girl's Day is also referred to as Hina Matsuri in Hawaii. It is a Japanese holiday that originated in Japan as a Shinto purification ceremony during spring. It was brought to Hawaii by Japanese immigrants.</p>
                <p>Girl's Day is celebrated annually on <strong class="subtitle">March 3rd</strong>. Local families display collections of beautiful dolls that represent the Japanese court. Often times, girls will receive additional dolls for their collection on this day.</p>
                <p>Girl's Day represents peace, composure and beauty. It is a celebration of all things feminine and is a day to wish happiness and marriage onto young women.</p>
                <p>Girl's Day is often celebrated by eating diamond shaped mochi (sweetened pounded rice cakes), parades, wearing and displaying of traditional Japanese attire (kimonos) and dancing.</p>
                <p><strong>Chinese New Year</strong></p>
                <p>Chinese New Year is celebrated annually <strong class="subtitle">between late January and mid-February </strong>in Hawaii. During this time, many local people greet each other by saying, "Gung Hee Fat Choy" or "Happy New Year."</p>
                <p>Chinese New Year has been celebrated in China for millennia. It was most likely brought to Hawaii around 1855, when a surge of Chinese immigrants entered Hawaii to work on the plantations. In Hawaii, the holiday is celebrated with tea ceremonies, parades, firecrackers, the annual Narcissus Festival Pageant and lion dances.</p>
                <p>The Lion Dance is the most popular way to celebrate the Chinese New Year. During the dance, two people crouch in an elaborate lion costume. The lion dances to the beat of a drum. Local people feed the lion money for good luck, happiness and prosperity. The lion is said to ward off evil spirits.</p>
