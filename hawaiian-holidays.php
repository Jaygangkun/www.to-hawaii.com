                <h2>Holidays Observed in Hawaii</h2>
                <p>Besides recognizing U.S. national holidays (besides Columbus Day), Hawaii also observes three unique state holidays. These days are Prince Jonah Kuhio Kalanianaole Day, King Kamehameha I Day and Admissions Day. On these observed holidays, most banks and state agencies are closed.</p>
                <p>Because Hawaii is such a melting pot of cultures, there are a handful of other holidays that are celebrated "unofficially" by residents of Hawaii. The most popular of these holidays are: May Day or "Lei Day," Boy's Day, Girl's Day and Chinese New Year. Here's a sneak peak at the background of these Hawaiian holidays.</p>
                <p><strong>Prince Jonah Kuhio Kalanianaole Day</strong></p>
                <p>This day is more commonly referred to as "Kuhio Day." It takes place annually on <strong class="subtitle">March 26th</strong>. The holiday was established back in 1949 to celebrate the birthday of Prince Jonah Kuhio Kalanianaole.</p>
                                                <? include ("theme/text.html") ?>
                <p>Prince Kuhio was born on Kauai in 1871 to High Chief David Kahalepouli and Princess Kinoiki Kekaulike. He went on to serve as the second delegate to U.S. Congress from 1903-1921. He is most remembered for getting Congress to designate public land for Native Hawaiian families to homestead on.</p>
                <p>Prince Kuhio Day is celebrated by a parade on the island of Oahu and with statewide canoe races and luaus.</p>
                <h3>King Kamehameha I Day</h3>
                <p>This day is more commonly referred to as "Kamehameha Day." It takes place annually on <strong class="subtitle">June 11th</strong>. The holiday was established back in 1871 by King Kamehameha V in honor of his great-grandfather Kamehameha the First. Kamehameha Day is the only holiday that was established by royal proclamation that is still officially observed by the state of Hawaii.</p>
                <p>King Kamehameha was born on the Big Island of Hawaii between the years 1740-1758. He was the son of Chief Keoua Kupuapaikalani and Kekuiapoiwa. He was raised by foster parents and trained in the art of battle. Kamehameha is remembered for fearlessly unifying the Hawaiian islands and becoming the first unified ruler of the islands in 1795.</p> <?php
