              <p><strong>The Origins of Shaka</strong></p>
              <p>There is some debate about the origin of the shaka, although most agree that its roots lie with surf and beach culture. One version claims that the shaka sign was first used in the 1940s as a symbol of blessing by a local Hawaiian folk hero named Hamana Kalili from Laie who happened to be missing the three middle fingers on his right hand due to a sugar mill incident. </p>
              <p>Others debate the symbol was born when Kalili waved his malformed hand to shoo away children from jumping trains. A third version claims that the symbol was born when one of the first surfers in Hawaii raised a shaking pinky and thumb out of the water after having his middle fingers bitten off by a hungry shark. Yet another version says that the origin lies with Spanish immigrants, who folded the middle fingers and brought the thumb to the lips as a friendly gesture to symbolize sharing a drink with the natives they encountered in Hawaii. </p>
                          <? include ("theme/text-desktop-only.html") ?>
              <p>Whatever the genesis of this powerful and enjoyable symbol, it remains a strong reminder of the spirit of aloha that makes Hawaii so special. The shaka has transcended many generations and continues to unite island lovers with a unique tradition not found in any dictionary.</p>
              <p>The naming of the symbol as "shaka" came later in the 1960s when used as a signoff by local Honolulu TV movie host Lippy Espinda, although some believe the word "shaka" was derived from an ancient Buddha named Shakyamuni, who prayed with his hands in the shape of two shakas pressed together. Another story tells that the word "shaka" was derived from the term "shark eye," a traditional compliment given to respected friends and family members. In 1976, campaigning mayoral candidate Frank Fasi quickly popularized the symbol throughout the islands after designing a campaign around 
                the shaka. </p>
              <p>The shaka is a simple yet powerful way to remind locals and visitors of the way people look out for each other on the islands and strive to spread aloha day in and day out, in keeping with the Hawaiian principle of malama i kekahi i kekahi, - take care of one, take care of all. If you're new to the islands, don't be shy about throwing up shakas, just make sure you've got the hand gesture down first! Aloha in the Hawaiian language can mean love, hello, goodbye, affection, regards, fondness and compassion. 
                The amazing "shaka" communicates all this and more with just a simple wave of the hand.</p>
