              <p>Many coffee varieties are grown throughout Hawaii, but the six most common ones are:</p>
              <p><strong>Catuai:</strong> Sweet aroma with earthy notes.<br />
                <strong>Caturra:</strong> Silky texture with fruity, berry aromas.<br />
                <strong>Kauai Blue Mountain:</strong> Chocolaty flavor with hints of caramel.<br />
                <strong>Mokka:</strong> Cocoa and fruity flavors with hints of wood and black cherry.<br />
                <strong>Mundo Novo:</strong> Spicy aroma with hints of clove.<br />
                <strong>Typica:</strong> Chocolaty as well as spicy flavors with hints of caramel, wood and nuts.</p>
              <p>When it comes to the final product, many different varieties and blends are available on the market. Some coffees are purely from a single farm or region, others are a blend of different Hawaii regions, and others contain coffee from around with world with a certain percentage of Hawaii-grown coffee mixed in. </p>
              <p>In Hawaii, one can find many blends in stores that contain 10 percent Kona coffee and the rest is imported coffee from elsewhere. Since Kona coffee is pricey, a Kona coffee blend captures the unique Kona flavor while making the final product more affordable.</p>
