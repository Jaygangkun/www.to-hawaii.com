              <p>Hawaii is the only state in the United States that grows coffee. Gourmet Kona coffee is what first comes to mind when thinking about Hawaii and coffee, but there are many other varieties that have made names of their own in recent years. </p>
              <p>The first coffee plant was introduced to Hawaii through King Kamehameha the First's Spanish advisor, Don Francisco de Paula Marin, in 1817. However, the coffee he planted didn't succeed. In 1825, the governor of Oahu, Chief Boki, brought coffee plants from Brazil and successfully planted them in Oahu's Manoa Valley. From that time on people started planting coffee in different regions in the Islands. </p>
